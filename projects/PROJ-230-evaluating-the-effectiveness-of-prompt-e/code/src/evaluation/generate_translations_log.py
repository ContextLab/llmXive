"""
Aggregate all prompts, seeds, and raw outputs into a version-controlled CSV.

This script scans the raw translation outputs generated by run_inference.py,
extracts the associated metadata (prompt condition, seed, raw output, timestamp),
and compiles them into a single CSV file for version control and analysis.

Output: data/evaluation/raw_translations_log.csv
"""
import os
import sys
import json
import csv
import logging
from pathlib import Path
from datetime import datetime
from typing import List, Dict, Any

# Add project root to path if running as script
if __name__ == "__main__":
    project_root = Path(__file__).resolve().parent.parent.parent
    if str(project_root) not in sys.path:
        sys.path.insert(0, str(project_root))

from src.utils.logging import get_logger

logger = get_logger(__name__)

# Configuration
PROJECT_ID = "PROJ-230-evaluating-the-effectiveness-of-prompt-e"
BASE_OUTPUT_DIR = Path("data/evaluation/raw_translations")
LOG_OUTPUT_FILE = Path("data/evaluation/raw_translations_log.csv")

def scan_translation_dirs(base_dir: Path) -> List[Path]:
    """
    Recursively scan for JSON files containing translation results.
    Expected structure: base_dir/<condition>/<entry_id>.json
    """
    json_files = []
    if not base_dir.exists():
        logger.warning(f"Base output directory does not exist: {base_dir}")
        return json_files

    for condition_dir in base_dir.iterdir():
        if condition_dir.is_dir():
            condition_name = condition_dir.name
            for json_file in condition_dir.glob("*.json"):
                json_files.append((condition_name, json_file))
    return json_files

def extract_translation_data(condition: str, file_path: Path) -> Dict[str, Any]:
    """
    Read a single translation JSON file and extract required fields.
    Expected JSON schema:
    {
        "seed": int,
        "raw_output": str,
        "timestamp": str (ISO format),
        "prompt_condition": str (optional, fallback to dir name)
    }
    """
    try:
        with open(file_path, 'r', encoding='utf-8') as f:
            data = json.load(f)

        # Extract fields with fallbacks
        seed = data.get("seed", 0)
        raw_output = data.get("raw_output", "")
        
        # Timestamp handling: prefer stored timestamp, else file mtime
        timestamp_str = data.get("timestamp")
        if not timestamp_str:
            # Fallback to file modification time if not in JSON
            mtime = os.path.getmtime(file_path)
            timestamp_str = datetime.fromtimestamp(mtime).isoformat()
        
        # Prompt condition: prefer JSON field, else directory name
        prompt_condition = data.get("prompt_condition", condition)

        return {
            "prompt_condition": prompt_condition,
            "seed": seed,
            "raw_output": raw_output,
            "timestamp": timestamp_str
        }
    except json.JSONDecodeError as e:
        logger.error(f"Failed to decode JSON in {file_path}: {e}")
        return None
    except Exception as e:
        logger.error(f"Unexpected error processing {file_path}: {e}")
        return None

def aggregate_translations():
    """
    Main entry point: scan all translation outputs and write aggregated CSV.
    """
    logger.info(f"Starting aggregation of translations from {BASE_OUTPUT_DIR}")
    
    translation_files = scan_translation_dirs(BASE_OUTPUT_DIR)
    
    if not translation_files:
        logger.warning("No translation JSON files found. CSV will be empty.")
    
    rows = []
    for condition, file_path in translation_files:
        data = extract_translation_data(condition, file_path)
        if data:
            rows.append(data)
            logger.debug(f"Processed: {file_path.name} -> {data['prompt_condition']}")

    # Ensure output directory exists
    LOG_OUTPUT_FILE.parent.mkdir(parents=True, exist_ok=True)

    # Write CSV
    fieldnames = ["prompt_condition", "seed", "raw_output", "timestamp"]
    with open(LOG_OUTPUT_FILE, 'w', newline='', encoding='utf-8') as csvfile:
        writer = csv.DictWriter(csvfile, fieldnames=fieldnames)
        writer.writeheader()
        for row in rows:
            writer.writerow(row)

    logger.info(f"Successfully wrote {len(rows)} entries to {LOG_OUTPUT_FILE}")
    return len(rows)

def main():
    """
    CLI entry point.
    """
    logging.basicConfig(
        level=logging.INFO,
        format='%(asctime)s - %(name)s - %(levelname)s - %(message)s'
    )
    count = aggregate_translations()
    print(f"Log generated with {count} entries.")

if __name__ == "__main__":
    main()
