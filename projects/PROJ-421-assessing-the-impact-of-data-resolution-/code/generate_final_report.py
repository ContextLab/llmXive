    # Run the sweep to ensure data is fresh (or read existing if we want to avoid re-run)
    # For this task, we generate the report content to embed.
    sensitivity_results = run_sensitivity_sweep(str(RESULTS_DIR / "power_results.csv"))
    
    # Write the detailed sensitivity report if not already done, 
