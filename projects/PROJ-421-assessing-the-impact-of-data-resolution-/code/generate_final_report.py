# Ensure we are running from the project root context
# The script is expected to be run as: python code/generate_final_report.py
# But paths are relative to the project root defined in config or hardcoded relative to code/
PROJECT_ROOT = Path(__file__).parent.parent
RESULTS_DIR = PROJECT_ROOT / "data" / "results"
REPORT_PATH = RESULTS_DIR / "final_report.md"

# Ensure results directory exists
RESULTS_DIR.mkdir(parents=True, exist_ok=True)

logger = get_logger("T033_ReportGenerator")

def load_power_results():
    """Load the power curve CSV generated by T025."""
    csv_path = RESULTS_DIR / "power_results.csv"
    if not csv_path.exists():
        raise FileNotFoundError(f"Required power results not found at {csv_path}. "
                                "Ensure T025 has been completed.")
    return pd.read_csv(csv_path)

