"""
Sensitivity Analysis for Resolution Threshold Stability (Task T031).

This module implements a sensitivity sweep around the identified inflection point
(threshold resolution) to verify that the statistical power threshold (0.80)
does not vary by more than one resolution step.

The sweep adjusts the aggregation factor by ±10% around the inflection point.
Since the dataset only contains discrete geometric resolution steps (30, 60, 120...),
we map the swept factors back to the nearest valid resolution step and verify
stability.
"""

import os
import logging
import pandas as pd
import numpy as np
from pathlib import Path
from typing import List, Tuple, Dict, Any, Optional

# Import local utilities and config
import sys
# Ensure code directory is in path if running as script
if 'code' not in sys.path:
    sys.path.insert(0, str(Path(__file__).parent))

import config
from utils import get_logger

logger = get_logger(__name__)

# Define the valid resolution steps as per config
# These correspond to aggregation factors [1, 2, 4, 8, 16] for base 30m
VALID_RESOLUTIONS = [30, 60, 120, 240, 480]
VALID_FACTORS = [1, 2, 4, 8, 16]

def load_power_results(power_csv_path: str) -> pd.DataFrame:
    """
    Load the power results CSV generated by previous analysis steps.
    
    Expected columns: 'resolution', 'moran_i', 'p_value', 'power'
    """
    if not os.path.exists(power_csv_path):
        raise FileNotFoundError(f"Power results file not found: {power_csv_path}")
    
    df = pd.read_csv(power_csv_path)
    logger.info(f"Loaded power results from {power_csv_path} with {len(df)} rows.")
    return df

def find_inflection_point(df: pd.DataFrame, threshold: float = 0.80) -> int:
    """
    Identify the resolution (in meters) where power drops below the threshold.
    
    This corresponds to the 'inflection point' or the threshold resolution.
    Returns the resolution value (e.g., 240) where power < 0.80.
    """
    # Sort by resolution to ensure order
    df_sorted = df.sort_values('resolution')
    
    # Find the first row where power < threshold
    below_threshold = df_sorted[df_sorted['power'] < threshold]
    
    if below_threshold.empty:
        # If power never drops below threshold, return the max resolution + next step
        # or simply the max resolution if we assume stability
        max_res = df_sorted['resolution'].max()
        logger.warning(f"Power never dropped below {threshold}. Using max resolution {max_res} as inflection.")
        return max_res
    
    inflection_res = below_threshold['resolution'].iloc[0]
    logger.info(f"Inflection point identified at resolution: {inflection_res}m (Power < {threshold})")
    return int(inflection_res)

def get_factor_for_resolution(resolution: int) -> int:
    """
    Map a resolution (e.g., 240) to its aggregation factor (e.g., 8).
    Base resolution is 30m.
    """
    if resolution < 30 or resolution % 30 != 0:
        raise ValueError(f"Invalid resolution {resolution}. Must be multiple of 30.")
    return resolution // 30

def get_nearest_valid_resolution(swept_resolution: float) -> int:
    """
    Map a swept (continuous) resolution back to the nearest valid discrete step.
    """
    # Find the valid resolution closest to the swept value
    diffs = [abs(r - swept_resolution) for r in VALID_RESOLUTIONS]
    min_idx = diffs.index(min(diffs))
    return VALID_RESOLUTIONS[min_idx]

def run_sensitivity_sweep(power_csv_path: str, threshold: float = 0.80, delta_pct: float = 0.10) -> Dict[str, Any]:
    """
    Perform the sensitivity analysis sweep.
    
    1. Load results.
    2. Identify inflection point (resolution where power < 0.80).
    3. Calculate aggregation factor for inflection point.
    4. Sweep factor by ±10%.
    5. Map swept factors back to valid resolution steps.
    6. Verify that the threshold resolution (the step where power < 0.80)
       does not shift by more than one resolution step.
       
    Returns a dictionary with the analysis results.
    """
    df = load_power_results(power_csv_path)
    inflection_res = find_inflection_point(df, threshold)
    
    if inflection_res not in VALID_RESOLUTIONS:
        # Should not happen if data is correct, but handle gracefully
        inflection_res = get_nearest_valid_resolution(inflection_res)
        
    inflection_factor = get_factor_for_resolution(inflection_res)
    logger.info(f"Sweeping around inflection factor: {inflection_factor} (Resolution: {inflection_res}m)")
    
    # Calculate sweep range
    lower_factor = inflection_factor * (1 - delta_pct)
    upper_factor = inflection_factor * (1 + delta_pct)
    
    logger.info(f"Sweep range for factor: [{lower_factor:.2f}, {upper_factor:.2f}]")
    
    # Map swept factors to valid resolutions
    # Since we are sweeping a small percentage (10%), and resolution steps are geometric (x2),
    # the swept range will almost certainly fall within the same step or the immediate neighbor.
    # We need to check if the "threshold resolution" shifts.
    
    # The "threshold resolution" is defined as the specific resolution step where power < 0.80.
    # In our discrete dataset, this is a single point (e.g., 240m).
    # The sensitivity analysis asks: if the true inflection was slightly off (±10%),
    # would we still identify the same resolution step as the threshold?
    
    # We simulate this by mapping the swept bounds to the nearest valid steps.
    # If the lower bound maps to a step < inflection_res and upper bound maps to a step > inflection_res,
    # the threshold is unstable (shifts by more than 0 steps, potentially 1 step in either direction).
    # The requirement is: "threshold does not vary by more than one resolution step".
    # This implies the identified threshold step should remain the same or move at most 1 step.
    
    lower_res = get_nearest_valid_resolution(lower_factor * 30)
    upper_res = get_nearest_valid_resolution(upper_factor * 30)
    
    logger.info(f"Swept bounds mapped to resolutions: [{lower_res}m, {upper_res}m]")
    
    # Determine the set of resolutions that could be considered the threshold
    # given the sweep.
    # If the sweep is entirely within one resolution step, the threshold is stable (0 shift).
    # If the sweep crosses a boundary, the threshold might shift by 1 step.
    
    # Calculate steps index
    steps_map = {r: i for i, r in enumerate(VALID_RESOLUTIONS)}
    inflection_idx = steps_map[inflection_res]
    
    lower_idx = steps_map[lower_res]
    upper_idx = steps_map[upper_res]
    
    # The range of potential threshold indices
    potential_indices = set(range(min(lower_idx, upper_idx), max(lower_idx, upper_idx) + 1))
    
    # Calculate max shift
    max_shift = 0
    for idx in potential_indices:
        shift = abs(idx - inflection_idx)
        if shift > max_shift:
            max_shift = shift
    
    # Check condition: "does not vary by more than one resolution step"
    # i.e., max_shift <= 1
    is_stable = max_shift <= 1
    
    result = {
        "inflection_resolution": inflection_res,
        "inflection_factor": inflection_factor,
        "sweep_range_factor": (lower_factor, upper_factor),
        "sweep_range_resolution": (lower_res, upper_res),
        "potential_threshold_resolutions": sorted(list(potential_indices)),
        "max_shift_steps": max_shift,
        "is_stable": is_stable,
        "threshold_step_definition": "Transition between adjacent levels (e.g., 30->60, 60->120)"
    }
    
    logger.info(f"Sensitivity analysis complete. Max shift: {max_shift} steps. Stable: {is_stable}")
    return result

def write_sensitivity_report(result: Dict[str, Any], output_path: str):
    """
    Write the sensitivity analysis results to a text file.
    """
    os.makedirs(os.path.dirname(output_path), exist_ok=True)
    
    with open(output_path, 'w') as f:
        f.write("Sensitivity Analysis Report: Threshold Stability\n")
        f.write("=" * 50 + "\n\n")
        
        f.write(f"Inflection Point (Threshold Resolution): {result['inflection_resolution']}m\n")
        f.write(f"Aggregation Factor: {result['inflection_factor']}\n\n")
        
        f.write(f"Sweep Range (Factor ±10%): [{result['sweep_range_factor'][0]:.4f}, {result['sweep_range_factor'][1]:.4f}]\n")
        f.write(f"Sweep Range (Mapped Resolution): [{result['sweep_range_resolution'][0]}m, {result['sweep_range_resolution'][1]}m]\n\n")
        
        f.write(f"Potential Threshold Resolutions (Indices): {result['potential_threshold_resolutions']}\n")
        f.write(f"Maximum Shift (Steps): {result['max_shift_steps']}\n")
        f.write(f"Threshold Step Definition: {result['threshold_step_definition']}\n\n")
        
        status = "STABLE" if result['is_stable'] else "UNSTABLE"
        f.write(f"VERDICT: The threshold varies by {result['max_shift_steps']} step(s). "
                f"Requirement (<= 1 step): {status}\n")
        
        if not result['is_stable']:
            f.write("\nWARNING: The threshold stability criterion was NOT met.\n")
            f.write("The identified threshold resolution may vary by more than one step under ±10% uncertainty.\n")
        else:
            f.write("\nCONFIRMED: The threshold is stable within the ±10% sweep.\n")

def main():
    """
    Main entry point for the sensitivity analysis script.
    """
    # Paths from config
    power_csv_path = config.RESULTS_DIR / "power_analysis_results.csv"
    report_path = config.RESULTS_DIR / "sensitivity_analysis_report.txt"
    
    if not os.path.exists(power_csv_path):
        logger.error(f"Cannot run sensitivity analysis. Missing input: {power_csv_path}")
        logger.error("Please ensure T025 (Power Analysis) has been completed first.")
        sys.exit(1)
    
    try:
        result = run_sensitivity_sweep(str(power_csv_path))
        write_sensitivity_report(result, str(report_path))
        logger.info(f"Sensitivity analysis report written to {report_path}")
        
        if not result['is_stable']:
            logger.warning("Sensitivity analysis failed stability check.")
            sys.exit(1)
            
    except Exception as e:
        logger.error(f"Error during sensitivity analysis: {e}", exc_info=True)
        sys.exit(1)

if __name__ == "__main__":
    main()