import os
import logging
import pandas as pd
from pathlib import Path
from typing import Optional

# Import shared utilities
from utils import get_logger

logger = get_logger(__name__)

def find_threshold(power_csv_path: str) -> Optional[str]:
    """
    Reads the statistical power results from a CSV file and identifies
    the resolution where statistical power first drops below 0.80.

    Args:
        power_csv_path (str): Path to the CSV file containing power analysis results.
                              Expected columns: 'resolution', 'power' (and others).

    Returns:
        Optional[str]: The resolution string (e.g., '240m') where power < 0.80.
                       Returns None if no such resolution is found or file is missing.

    Writes:
        data/results/threshold_report.txt: A text file containing the identified threshold.
    """
    path = Path(power_csv_path)
    if not path.exists():
        logger.error(f"Power CSV file not found: {power_csv_path}")
        return None

    try:
        df = pd.read_csv(path)
    except Exception as e:
        logger.error(f"Failed to read power CSV: {e}")
        return None

    required_cols = {'resolution', 'power'}
    if not required_cols.issubset(df.columns):
        logger.error(f"CSV missing required columns: {required_cols - set(df.columns)}")
        return None

    # Sort by resolution magnitude to ensure we find the *first* drop
    # Assuming resolution strings like '30m', '60m', etc.
    # We need to parse the numeric part to sort correctly.
    def parse_res(res_str):
        try:
            return int(''.join(filter(str.isdigit, str(res_str))))
        except ValueError:
            return 999999

    df_sorted = df.sort_values(by='resolution', key=lambda x: x.apply(parse_res))

    # Find the first row where power < 0.80
    threshold_row = df_sorted[df_sorted['power'] < 0.80]

    if threshold_row.empty:
        logger.warning("No resolution found with power < 0.80. Power remains high across all resolutions.")
        return None

    # Get the first such resolution
    threshold_resolution = str(threshold_row.iloc[0]['resolution'])
    threshold_power = threshold_row.iloc[0]['power']

    logger.info(f"Threshold identified: Resolution {threshold_resolution} has power {threshold_power:.4f} (< 0.80)")

    # Write report
    results_dir = Path("data/results")
    results_dir.mkdir(parents=True, exist_ok=True)
    report_path = results_dir / "threshold_report.txt"

    with open(report_path, 'w') as f:
        f.write(f"Statistical Power Threshold Report\n")
        f.write(f"===================================\n")
        f.write(f"Threshold Resolution: {threshold_resolution}\n")
        f.write(f"Power at Threshold: {threshold_power:.4f}\n")
        f.write(f"Criterion: Power < 0.80\n")
        f.write(f"\n")
        f.write(f"Analysis: This is the coarsest resolution (lowest spatial detail)\n")
        f.write(f"where the statistical power to detect spatial autocorrelation\n")
        f.write(f"falls below the standard 0.80 threshold.\n")

    logger.info(f"Threshold report written to: {report_path}")
    return threshold_resolution

def main():
    """CLI entry point for finding the power threshold."""
    # Default path based on project structure
    power_csv = Path("data/results/power_analysis_results.csv")
    
    if not power_csv.exists():
        # Try to find it if generated by T025
        logger.error(f"Expected power results not found at {power_csv}.")
        return 1

    threshold = find_threshold(str(power_csv))
    
    if threshold:
        print(f"Threshold found: {threshold}")
        return 0
    else:
        print("No threshold found (power >= 0.80 for all resolutions).")
        return 0

if __name__ == "__main__":
    logging.basicConfig(level=logging.INFO)
    main()