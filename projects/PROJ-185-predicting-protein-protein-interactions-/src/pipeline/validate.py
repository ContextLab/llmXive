"""
Validation script for the PPI prediction pipeline.

This script performs two categories of checks:

1. **Existence & parsability** – Ensures that all required output files
   generated by the Makefile targets exist and can be read without error.

2. **Schema compliance** – Validates each output file against its
   corresponding contract (YAML schema) located in the ``contracts/`` directory.

The script is intentionally strict: any failure results in a non‑zero exit
code and a clear error message printed to ``stderr``.  It is meant to be
invoked from the Makefile ``validate`` target (e.g. ``python -m src.pipeline.validate``)
and also usable as a stand‑alone command‑line utility.

The implementation relies only on the public API surface already present
in the repository (standard library, ``pandas``, ``PyYAML`` and ``jsonschema``).
"""

import sys
import json
import logging
from pathlib import Path
from typing import List, Dict

import pandas as pd
import yaml
from jsonschema import Draft7Validator, ValidationError

# ----------------------------------------------------------------------
# Helper utilities
# ----------------------------------------------------------------------
def _load_yaml(path: Path) -> Dict:
    """Load a YAML file and return its contents as a Python dict."""
    with path.open("r", encoding="utf-8") as f:
        return yaml.safe_load(f)

def _load_json(path: Path) -> Dict:
    """Load a JSON file and return its contents as a Python dict."""
    with path.open("r", encoding="utf-8") as f:
        return json.load(f)

def _load_tsv(path: Path) -> pd.DataFrame:
    """Load a TSV file with pandas, enforcing a header row."""
    return pd.read_csv(path, sep="\t", dtype=str)

# ----------------------------------------------------------------------
# Validation functions
# ----------------------------------------------------------------------
def _validate_file_exists(path: Path) -> None:
    if not path.is_file():
        raise FileNotFoundError(f"Required file not found: {path}")

def _validate_predicted_ppi(path: Path, schema: Dict) -> None:
    df = _load_tsv(path)
    # Basic sanity: at least two columns (e.g., protein_a, protein_b)
    if df.shape[1] < 2:
        raise ValueError(f"Predicted PPI file {path} has insufficient columns")
    # Convert to list‑of‑dicts for JSON‑Schema validation
    records = df.to_dict(orient="records")
    validator = Draft7Validator(schema)
    errors = sorted(validator.iter_errors(records), key=lambda e: e.path)
    if errors:
        msgs = "\n".join(str(e) for e in errors)
        raise ValidationError(f"Schema validation failed for {path}:\n{msgs}")

def _validate_evaluation_metrics(path: Path, schema: Dict) -> None:
    data = _load_json(path)
    validator = Draft7Validator(schema)
    errors = list(validator.iter_errors(data))
    if errors:
        msgs = "\n".join(str(e) for e in errors)
        raise ValidationError(f"Schema validation failed for {path}:\n{msgs}")

def _validate_go_enrichment(path: Path, schema: Dict) -> None:
    df = _load_tsv(path)
    # Require typical GO enrichment columns
    required_cols = {"GO_term", "p_value", "adjusted_p_value"}
    if not required_cols.issubset(set(df.columns)):
        missing = required_cols - set(df.columns)
        raise ValueError(f"GO enrichment file {path} missing columns: {missing}")
    # At least one term with adjusted p‑value < 0.05
    if not (df["adjusted_p_value"].astype(float) < 0.05).any():
        raise ValueError(
            f"GO enrichment file {path} contains no significant terms (adj p < 0.05)"
        )
    # Schema validation (treat the whole table as a list of records)
    records = df.to_dict(orient="records")
    validator = Draft7Validator(schema)
    errors = sorted(validator.iter_errors(records), key=lambda e: e.path)
    if errors:
        msgs = "\n".join(str(e) for e in errors)
        raise ValidationError(f"Schema validation failed for {path}:\n{msgs}")

def _validate_pipeline_log(path: Path) -> None:
    # Ensure the log file is readable and non‑empty
    with path.open("r", encoding="utf-8") as f:
        content = f.read()
        if not content.strip():
            raise ValueError(f"Pipeline log {path} is empty")

# ----------------------------------------------------------------------
# Main orchestration
# ----------------------------------------------------------------------
def main() -> None:
    """
    Entry point for the validation script.

    The function performs the following steps:

    1. Resolve the project root (two directories up from this file).
    2. Define the set of required output files and their matching schema files.
    3. For each required file:
        * Verify existence.
        * Verify parsability (read into memory).
        * Validate against the corresponding schema.
    4. Exit with status 0 on success; otherwise print errors and exit 1.
    """
    logger = logging.getLogger(__name__)
    logger.setLevel(logging.INFO)
    handler = logging.StreamHandler(sys.stderr)
    handler.setFormatter(
        logging.Formatter("%(asctime)s %(levelname)s %(message)s")
    )
    logger.addHandler(handler)

    # Resolve directories
    this_file = Path(__file__).resolve()
    project_root = this_file.parent.parent.parent  # src/pipeline/validate.py -> project root
    results_dir = project_root / "results"
    contracts_dir = project_root / "contracts"
    # The pipeline log is written to the project root (see utils/logger)
    pipeline_log_path = project_root / "pipeline.log"

    # Mapping of logical name -> (file path, schema path, validator function)
    checks: List[Dict] = []

    # 1. Predicted PPI edge list(s)
    for ppi_path in results_dir.glob("predicted_ppi_*.tsv"):
        schema_path = contracts_dir / "predicted_ppi.schema.yaml"
        checks.append(
            {
                "name": "predicted_ppi",
                "file": ppi_path,
                "schema": schema_path,
                "validator": _validate_predicted_ppi,
            }
        )

    # 2. Evaluation metrics (single JSON file)
    eval_path = results_dir / "evaluation_metrics.json"
    if eval_path.exists():
        checks.append(
            {
                "name": "evaluation_metrics",
                "file": eval_path,
                "schema": contracts_dir / "evaluation.schema.yaml",
                "validator": _validate_evaluation_metrics,
            }
        )

    # 3. GO enrichment files (may be multiple species)
    for go_path in results_dir.glob("go_enrichment_*.tsv"):
        schema_path = contracts_dir / "go_enrichment.schema.yaml"
        checks.append(
            {
                "name": "go_enrichment",
                "file": go_path,
                "schema": schema_path,
                "validator": _validate_go_enrichment,
            }
        )

    # 4. Pipeline log
    checks.append(
        {
            "name": "pipeline_log",
            "file": pipeline_log_path,
            "schema": None,
            "validator": lambda p, s: _validate_pipeline_log(p),
        }
    )

    # Execute checks
    errors_occurred = False
    for chk in checks:
        name = chk["name"]
        file_path: Path = chk["file"]
        schema_path: Path | None = chk["schema"]
        validator = chk["validator"]

        try:
            logger.info(f"Validating {name}: {file_path}")
            _validate_file_exists(file_path)

            # Load schema if it exists
            schema = None
            if schema_path:
                _validate_file_exists(schema_path)
                schema = _load_yaml(schema_path)

            validator(file_path, schema)  # type: ignore[arg-type]

        except (FileNotFoundError, ValueError, ValidationError) as exc:
            errors_occurred = True
            logger.error(f"Validation failed for {name} ({file_path}): {exc}")

    if errors_occurred:
        logger.error("One or more validation checks failed.")
        sys.exit(1)

    logger.info("All validation checks passed.")
    sys.exit(0)

if __name__ == "__main__":
    main()