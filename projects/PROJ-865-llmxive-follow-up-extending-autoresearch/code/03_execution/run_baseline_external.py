"""
T021: Orchestrate baseline agent execution on external resources.

This script orchestrates the execution of the baseline agent on the tasks
specified in the experiment manifest. It ensures strict task ID alignment
between the manifest and the baseline results.

The script:
1. Loads the experiment manifest (CSV) generated by T019a.
2. Invokes the baseline agent CLI: `python code/03_execution/run_baseline.py --manifest <path> --output <path>`.
3. Validates that the output contains exactly the task IDs from the manifest.
4. Writes the results to `data/derived/baseline_results.json`.
"""

import json
import csv
import subprocess
import sys
import os
from pathlib import Path
from typing import List, Dict, Any, Set

# Add project root to path to allow relative imports if needed,
# though we are using subprocess for CLI.
PROJECT_ROOT = Path(__file__).parent.parent.parent
CODE_DIR = PROJECT_ROOT / "code"
DATA_DIR = PROJECT_ROOT / "data"
DERIVED_DIR = DATA_DIR / "derived"

# Input/Output paths
MANIFEST_PATH = DERIVED_DIR / "experiment_manifest.csv"
BASELINE_SCRIPT = CODE_DIR / "03_execution" / "run_baseline.py"
OUTPUT_PATH = DERIVED_DIR / "baseline_results.json"

# Resource limits (from T005)
MAX_MEMORY_GB = 7
MAX_CPU_CORES = 2


def load_manifest(manifest_path: Path) -> List[Dict[str, Any]]:
    """Load the experiment manifest CSV."""
    if not manifest_path.exists():
        raise FileNotFoundError(f"Manifest not found: {manifest_path}")

    tasks = []
    with open(manifest_path, 'r', encoding='utf-8') as f:
        reader = csv.DictReader(f)
        for row in reader:
            tasks.append(row)
    return tasks


def run_baseline_agent(manifest_path: Path, output_path: Path) -> None:
    """
    Invoke the baseline agent CLI.

    Command: python code/03_execution/run_baseline.py --manifest <path> --output <path>
    """
    if not BASELINE_SCRIPT.exists():
        raise FileNotFoundError(f"Baseline script not found: {BASELINE_SCRIPT}")

    cmd = [
        sys.executable,
        str(BASELINE_SCRIPT),
        "--manifest", str(manifest_path),
        "--output", str(output_path)
    ]

    print(f"Executing baseline agent: {' '.join(cmd)}")
    try:
        result = subprocess.run(
            cmd,
            cwd=PROJECT_ROOT,
            check=True,
            capture_output=True,
            text=True
        )
        print("Baseline execution stdout:")
        print(result.stdout)
        if result.stderr:
            print("Baseline execution stderr:")
            print(result.stderr)
    except subprocess.CalledProcessError as e:
        print(f"Baseline execution failed with return code {e.returncode}")
        print(f"Stdout: {e.stdout}")
        print(f"Stderr: {e.stderr}")
        raise RuntimeError(f"Baseline agent execution failed: {e}")


def validate_results(manifest_tasks: List[Dict[str, Any]], results_path: Path) -> None:
    """
    Validate that the baseline results contain exactly the task IDs from the manifest.
    """
    if not results_path.exists():
        raise FileNotFoundError(f"Baseline results not found: {results_path}")

    with open(results_path, 'r', encoding='utf-8') as f:
        results = json.load(f)

    # Handle both list of dicts and dict of results
    if isinstance(results, list):
        result_ids = {item.get('task_id') for item in results if 'task_id' in item}
    elif isinstance(results, dict):
        # Assuming keys are task_ids or values have task_id
        if all(isinstance(k, str) and not isinstance(v, dict) for k, v in results.items()):
            result_ids = set(results.keys())
        else:
            result_ids = {item.get('task_id') for item in results.values() if isinstance(item, dict) and 'task_id' in item}
    else:
        raise ValueError(f"Unexpected results format: {type(results)}")

    manifest_ids = {task.get('task_id') for task in manifest_tasks if 'task_id' in task}

    missing_in_results = manifest_ids - result_ids
    extra_in_results = result_ids - manifest_ids

    if missing_in_results:
        raise ValueError(f"Missing task IDs in baseline results: {missing_in_results}")
    if extra_in_results:
        raise ValueError(f"Extra task IDs in baseline results: {extra_in_results}")

    print(f"Validation passed: {len(result_ids)} tasks matched.")


def main():
    """Main entry point for T021."""
    print(f"Starting T021: Baseline External Execution")
    print(f"Project Root: {PROJECT_ROOT}")

    # Ensure output directory exists
    DERIVED_DIR.mkdir(parents=True, exist_ok=True)

    # 1. Load Manifest
    print(f"Loading manifest from: {MANIFEST_PATH}")
    try:
        manifest_tasks = load_manifest(MANIFEST_PATH)
        print(f"Loaded {len(manifest_tasks)} tasks from manifest.")
    except Exception as e:
        print(f"Error loading manifest: {e}")
        sys.exit(1)

    if not manifest_tasks:
        print("Manifest is empty. Cannot run baseline.")
        sys.exit(0)

    # 2. Run Baseline Agent
    print(f"Running baseline agent...")
    try:
        run_baseline_agent(MANIFEST_PATH, OUTPUT_PATH)
    except Exception as e:
        print(f"Error running baseline agent: {e}")
        sys.exit(1)

    # 3. Validate Results
    print(f"Validating baseline results...")
    try:
        validate_results(manifest_tasks, OUTPUT_PATH)
    except Exception as e:
        print(f"Validation failed: {e}")
        sys.exit(1)

    print("T021 completed successfully. Output: {}".format(OUTPUT_PATH))


if __name__ == "__main__":
    main()