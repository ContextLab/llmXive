"""
T013d: Retry Loop for Rule Distillation

This script implements the retry logic for User Story 1.
It checks if T014 (coverage validation) failed and if T013b (regex fallback) was triggered.
If so, it re-runs `distill_rules.py` with a quantized model or stricter regex settings
until coverage >= 90% or all fallback options are exhausted.
"""
import json
import sys
import subprocess
from pathlib import Path
from typing import List, Dict, Any, Optional

# Project root handling
PROJECT_ROOT = Path(__file__).resolve().parents[2]
DATA_DERIVED = PROJECT_ROOT / "data" / "derived"
COVERAGE_REPORT_PATH = DATA_DERIVED / "coverage_report.json"
SCOPE_CHANGES_PATH = DATA_DERIVED / "scope_changes.md"
RULES_LIBRARY_PATH = DATA_DERIVED / "rules_library.json"

# Constants
TARGET_COVERAGE = 0.90
MAX_RETRY_ATTEMPTS = 3
FALLBACK_ATTEMPTS = 2  # 1. Quantized Model, 2. Strict Regex

def load_coverage_report() -> Optional[Dict[str, Any]]:
    """Load the coverage report generated by T014."""
    if not COVERAGE_REPORT_PATH.exists():
        return None
    try:
        with open(COVERAGE_REPORT_PATH, 'r', encoding='utf-8') as f:
            return json.load(f)
    except (json.JSONDecodeError, IOError) as e:
        print(f"Error reading coverage report: {e}")
        return None

def load_scope_changes() -> List[Dict[str, Any]]:
    """Load existing scope changes to check if T013b triggered."""
    if not SCOPE_CHANGES_PATH.exists():
        return []
    try:
        with open(SCOPE_CHANGES_PATH, 'r', encoding='utf-8') as f:
            content = f.read()
            # Simple parsing for markdown list items if JSON not strictly used,
            # but assuming we store JSON in a specific block or just check for the marker.
            # For robustness, we look for the specific marker text.
            return [{"triggered": "regex_fallback" in content.lower()}]
    except IOError:
        return []

def check_regex_fallback_triggered() -> bool:
    """
    Check if the regex fallback was triggered in the previous run.
    We look for a specific marker in scope_changes.md or check the rules_library metadata.
    """
    if not SCOPE_CHANGES_PATH.exists():
        return False
    
    content = SCOPE_CHANGES_PATH.read_text(encoding='utf-8')
    # T013b documentation requirement
    if "regex fallback" in content.lower() or "fallback triggered" in content.lower():
        return True
    
    # Fallback: check if rules_library has a flag indicating fallback (if T013 wrote it)
    if RULES_LIBRARY_PATH.exists():
        try:
            with open(RULES_LIBRARY_PATH, 'r', encoding='utf-8') as f:
                rules_data = json.load(f)
                if rules_data.get("generation_method") == "regex_fallback":
                    return True
        except (json.JSONDecodeError, IOError):
            pass
    
    return False

def run_distill_rules(attempt: int, method: str) -> bool:
    """
    Re-run distill_rules.py with specific configuration.
    Returns True if the script completes successfully.
    """
    cmd = [
        sys.executable,
        str(PROJECT_ROOT / "code" / "02_annotation_distillation" / "distill_rules.py")
    ]
    
    # Pass configuration arguments based on the attempt
    if method == "quantized":
        # Attempt to use a quantized model (e.g., 4-bit) to reduce RAM
        cmd.extend(["--model_quantize", "4bit"])
        print(f"[Retry {attempt}] Running distill_rules.py with 4-bit quantized model...")
    elif method == "strict_regex":
        # Force strict regex extraction without LLM
        cmd.extend(["--force_regex", "--regex_strictness", "high"])
        print(f"[Retry {attempt}] Running distill_rules.py with strict regex extraction...")
    else:
        print(f"[Retry {attempt}] Running distill_rules.py with default settings...")

    try:
        result = subprocess.run(
            cmd,
            cwd=PROJECT_ROOT,
            check=True,
            capture_output=False, # Stream output to parent
            timeout=600 # 10 minutes timeout for model loading/inference
        )
        return result.returncode == 0
    except subprocess.CalledProcessError as e:
        print(f"Error: distill_rules.py failed with return code {e.returncode}")
        return False
    except subprocess.TimeoutExpired:
        print("Error: distill_rules.py timed out")
        return False

def main():
    print("Starting T013d: Retry Loop for Rule Distillation")
    
    # 1. Check if T014 failed (coverage < 90%)
    coverage_data = load_coverage_report()
    if not coverage_data:
        print("No coverage report found. T014 may not have run or failed to write output.")
        print("Nothing to retry.")
        return

    current_coverage = coverage_data.get("coverage_score", 0.0)
    print(f"Current coverage: {current_coverage:.2%}")

    if current_coverage >= TARGET_COVERAGE:
        print(f"Coverage ({current_coverage:.2%}) meets target ({TARGET_COVERAGE:.2%}). No retry needed.")
        return

    # 2. Check if T013b (regex fallback) was triggered
    fallback_triggered = check_regex_fallback_triggered()
    
    # The task description says: "IF T014 fails AND T013b triggered"
    # However, if T013b was NOT triggered, it means we likely used the LLM and it failed coverage.
    # In that case, we should try the LLM again (maybe with different params) OR fallback now.
    # The prompt implies: "IF T014 fails (coverage < 90%) AND T013b triggered (regex fallback), 
    # explicitly re-run... with a smaller model...".
    # This implies we are in a state where we already tried regex and it failed, or we are 
    # reacting to the failure of the fallback.
    # Let's interpret the logic as: "We are below 90%. We need to try harder."
    
    if not fallback_triggered:
        print("Regex fallback was NOT triggered in the previous run. Attempting to fallback now.")
        # We haven't tried regex yet, so try regex first as a fallback strategy
        # But the task specifically asks to re-run with smaller model if fallback triggered.
        # Let's assume the standard flow: LLM -> Fail -> Regex (T013b) -> Fail (T014).
        # If we are here and fallback wasn't triggered, maybe LLM failed but didn't switch?
        # We will proceed to try the retry strategies regardless of the previous state 
        # to ensure we hit 90%.
    
    attempts = 0
    strategies = []
    
    # Define strategies: 
    # 1. If we haven't tried strict regex, try it.
    # 2. If we have, try quantized model (if not already used).
    # 3. Try a combination or simpler regex.
    
    if not fallback_triggered:
        strategies.append("strict_regex")
    
    strategies.extend(["quantized", "strict_regex_high"]) # Try quantized, then stricter regex

    while attempts < MAX_RETRY_ATTEMPTS and attempts < len(strategies):
        strategy = strategies[attempts]
        print(f"\n--- Attempt {attempts + 1}/{MAX_RETRY_ATTEMPTS} ---")
        print(f"Strategy: {strategy}")
        
        success = run_distill_rules(attempts + 1, strategy)
        
        if not success:
            print(f"Retry {attempts + 1} failed to execute successfully.")
            attempts += 1
            continue
        
        # Re-check coverage
        coverage_data = load_coverage_report()
        if not coverage_data:
            print("Retry succeeded but no new coverage report generated.")
            attempts += 1
            continue
        
        new_coverage = coverage_data.get("coverage_score", 0.0)
        print(f"New coverage: {new_coverage:.2%}")
        
        if new_coverage >= TARGET_COVERAGE:
            print(f"SUCCESS: Coverage ({new_coverage:.2%}) >= {TARGET_COVERAGE:.2%}")
            print("Retry loop terminated successfully.")
            return
        
        print(f"Coverage still below target. Proceeding to next strategy...")
        attempts += 1

    print(f"\n--- Retry Loop Exhausted ---")
    print(f"Final coverage: {coverage_data.get('coverage_score', 0.0):.2%}")
    print(f"Target: {TARGET_COVERAGE:.2%}")
    print("All fallback options exhausted. Build may fail as per T014 gatekeeper.")
    
    # Record this failure in scope changes if not already done
    if not SCOPE_CHANGES_PATH.exists() or not check_regex_fallback_triggered():
        with open(SCOPE_CHANGES_PATH, 'a', encoding='utf-8') as f:
            f.write(f"\n### Retry Loop Failure (T013d)\n")
            f.write(f"- Date: {subprocess.run(['date'], capture_output=True, text=True).stdout.strip()}\n")
            f.write(f"- Final Coverage: {coverage_data.get('coverage_score', 0.0):.2%}\n")
            f.write(f"- Status: Exhausted all retry strategies without reaching 90% coverage.\n")

if __name__ == "__main__":
    main()