"""
Validate the schema of data/derived/results.csv against the pivot_attempt.schema.yaml.

This script ensures that the results generated by run_experiments.py (T022)
strictly adhere to the defined schema before proceeding to statistical analysis.
"""
import csv
import json
import sys
import yaml
from pathlib import Path
from typing import List, Dict, Any, Set, Optional

# Add parent directory to path for imports if running as script
if __name__ == "__main__":
    sys.path.insert(0, str(Path(__file__).parent.parent))

from utils.logging import get_logger, log_stage_start, log_stage_end

logger = get_logger(__name__)

SCHEMA_PATH = Path("specs/001-llmxive-followup/contracts/pivot_attempt.schema.yaml")
RESULTS_PATH = Path("data/derived/results.csv")

def load_schema(schema_path: Path) -> Dict[str, Any]:
    """Load and return the YAML schema."""
    if not schema_path.exists():
        raise FileNotFoundError(f"Schema file not found: {schema_path}")
    with open(schema_path, 'r', encoding='utf-8') as f:
        return yaml.safe_load(f)

def validate_csv_schema(csv_path: Path, schema: Dict[str, Any]) -> bool:
    """
    Validate that the CSV file matches the expected schema.
    
    Checks:
    1. Required columns exist.
    2. Column types match (string, number, enum).
    3. Enum values are valid.
    
    Returns True if valid, raises ValueError if invalid.
    """
    required_columns = schema.get('required', [])
    properties = schema.get('properties', {})
    
    if not csv_path.exists():
        raise FileNotFoundError(f"Results file not found: {csv_path}")
    
    with open(csv_path, 'r', encoding='utf-8') as f:
        reader = csv.DictReader(f)
        headers = reader.fieldnames
        
        if not headers:
            raise ValueError("CSV file is empty or has no headers.")
        
        # Check required columns
        missing_cols = set(required_columns) - set(headers)
        if missing_cols:
            raise ValueError(f"Missing required columns: {missing_cols}")
        
        extra_cols = set(headers) - set(required_columns)
        if extra_cols:
            logger.warning(f"Extra columns found (not in schema): {extra_cols}")
        
        row_count = 0
        errors: List[str] = []
        
        for row in reader:
            row_count += 1
            current_errors = []
            
            # Validate each column
            for col in required_columns:
                value = row.get(col)
                prop_def = properties.get(col, {})
                col_type = prop_def.get('type')
                
                if value is None or value == '':
                    if col_type != 'null':
                        current_errors.append(f"Row {row_count}: Column '{col}' is empty.")
                        continue
                
                # Type validation
                if col_type == 'string':
                    if not isinstance(value, str):
                        current_errors.append(f"Row {row_count}: Column '{col}' must be string, got {type(value)}.")
                    
                    # Enum validation
                    if 'enum' in prop_def:
                        if value not in prop_def['enum']:
                            current_errors.append(
                                f"Row {row_count}: Column '{col}' value '{value}' not in allowed values: {prop_def['enum']}"
                            )
                
                elif col_type == 'number':
                    try:
                        float_val = float(value)
                        if 'minimum' in prop_def and float_val < prop_def['minimum']:
                            current_errors.append(
                                f"Row {row_count}: Column '{col}' value {float_val} < minimum {prop_def['minimum']}"
                            )
                    except ValueError:
                        current_errors.append(f"Row {row_count}: Column '{col}' must be a number, got '{value}'.")
                
                elif col_type == 'integer':
                    try:
                        int_val = int(value)
                        if 'enum' in prop_def and int_val not in prop_def['enum']:
                            current_errors.append(
                                f"Row {row_count}: Column '{col}' value {int_val} not in allowed values: {prop_def['enum']}"
                            )
                    except ValueError:
                        current_errors.append(f"Row {row_count}: Column '{col}' must be an integer, got '{value}'.")
            
            if current_errors:
                errors.extend(current_errors)
                # Stop after first 10 errors to avoid log spam
                if len(errors) >= 10:
                    errors.append("... (truncated further errors)")
                    break
            
            if errors:
                break # Stop on first row error for immediate feedback
        
        if errors:
            raise ValueError(f"Schema validation failed for {csv_path}:\n" + "\n".join(errors))
        
        logger.info(f"Validation passed for {row_count} rows in {csv_path.name}")
        return True

def main():
    log_stage_start("validate_results_schema")
    try:
        logger.info(f"Loading schema from {SCHEMA_PATH}")
        schema = load_schema(SCHEMA_PATH)
        
        logger.info(f"Validating results at {RESULTS_PATH}")
        is_valid = validate_csv_schema(RESULTS_PATH, schema)
        
        if is_valid:
            logger.info("SUCCESS: results.csv schema is valid.")
            return 0
    except FileNotFoundError as e:
        logger.error(f"File not found: {e}")
        return 1
    except ValueError as e:
        logger.error(f"Validation Error: {e}")
        return 1
    except Exception as e:
        logger.exception(f"Unexpected error during validation: {e}")
        return 1
    finally:
        log_stage_end("validate_results_schema")

if __name__ == "__main__":
    sys.exit(main())
