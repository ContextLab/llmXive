#!/bin/bash
set -e

# Project Root
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
PROJECT_ROOT="$(dirname "$SCRIPT_DIR")"
cd "$PROJECT_ROOT"

echo "=== llmXive Quickstart Validation Script ==="
echo "Project Root: $PROJECT_ROOT"
echo "Working Directory: $(pwd)"

# 1. Ensure dependencies are installed (using requirements.txt from T002)
echo ">>> Installing dependencies..."
if [ -f "requirements.txt" ]; then
    pip install -q -r requirements.txt
else
    echo "ERROR: requirements.txt not found in project root."
    exit 1
fi

# 2. Verify directory structure (from T001)
echo ">>> Verifying directory structure..."
required_dirs=(
    "code"
    "code/data"
    "code/analysis"
    "data"
    "data/raw"
    "data/processed"
    "tests"
    "scripts"
)
for dir in "${required_dirs[@]}"; do
    if [ ! -d "$dir" ]; then
        echo "ERROR: Missing directory $dir"
        exit 1
    fi
done

# 3. Generate Target List (T006)
# This script fetches real data from GitHub API.
echo ">>> Running T006: Generate Target List..."
python code/data/generate_target_list.py
if [ ! -f "data/raw/target_list.csv" ]; then
    echo "ERROR: data/raw/target_list.csv was not generated by T006."
    exit 1
fi
echo "    Target list generated successfully."

# 4. Download NVD Data (T007)
# This script downloads real NVD feeds.
echo ">>> Running T007: Download NVD Data..."
python code/data/download_nvd.py
if [ ! -f "data/raw/nvd_cve_merged.json.gz" ]; then
    echo "ERROR: data/raw/nvd_cve_merged.json.gz was not generated by T007."
    exit 1
fi
if [ ! -f "data/raw/nvd_cve_merged.json.gz.sha256" ]; then
    echo "ERROR: Checksum file missing."
    exit 1
fi
echo "    NVD data downloaded and verified."

# 5. Run Unit Tests (T027, T028, T029, T030)
echo ">>> Running Unit Tests..."
# We run a subset of tests to validate the pipeline logic without needing the full 500-repo dataset
# specifically testing the config, utils, and schema validation logic.
python -m pytest tests/unit/ -v --tb=short || {
    echo "ERROR: Unit tests failed."
    exit 1
}
echo "    Unit tests passed."

# 6. Validate Schema Contracts (T011 logic - simplified for CI)
echo ">>> Validating Data Schemas..."
python -c "
import pandas as pd
from data.schemas import get_schema, validate_dataframe
from pathlib import Path

# Validate target_list.csv
target_df = pd.read_csv('data/raw/target_list.csv')
target_schema = get_schema('target_list')
if not validate_dataframe(target_df, target_schema):
    raise ValueError('target_list.csv schema validation failed')

# Validate nvd_cve_merged.json.gz structure (basic check)
import gzip
import json
with gzip.open('data/raw/nvd_cve_merged.json.gz', 'rt') as f:
    data = json.load(f)
    if 'CVE_Items' not in data:
  raise ValueError('NVD JSON missing CVE_Items key')

print('Schema validation passed.')
"

echo "=== Quickstart Validation Successful ==="
exit 0