def generate_test_data():
    """
    Ensures synthetic data exists for the test.
    This mimics the output of T024.
    """
    if not os.path.exists("data/synthetic"):
        os.makedirs("data/synthetic", exist_ok=True)

    # Generate Inflation dataset as ground truth
    data_dict = generate_inflation_dataset(Nside=Nside, seed=42)
    save_dataset(data_dict, "data/synthetic/inflation_ground_truth")
    
    # Create a dummy mask file (Planck Common Mask)
    # In reality, this would be a FITS file with 0/1 values.
    # Here we generate a mask that covers > 70% of the sky.
    mask_data = np.ones(Npix, dtype=np.float32)
    # Mask out a small region (e.g., galactic plane)
    mask_data[:int(Npix * 0.25)] = 0.0 
    
    np.save("data/derived/planck_common_mask_nside64.npy", mask_data)
    return data_dict

def test_masking_pipeline():
    """
    Integration test: Load synthetic map, apply mask, verify coverage.
    """
    # 1. Setup: Generate or load data
    data_dict = generate_test_data()
    b_mode_map = data_dict['maps']['B'] # Extract B-mode component

    # Ensure output directory exists
    os.makedirs(OUTPUT_DIR, exist_ok=True)

    # 2. Load Mask
    # Simulate loading the mask (in real code: hp.read_map(MASK_FILE))
    mask_path = "data/derived/planck_common_mask_nside64.npy"
    if not os.path.exists(mask_path):
        # Fallback if file not created by setup in this specific run
        mask_data = np.ones(Npix, dtype=np.float32)
        mask_data[:int(Npix * 0.25)] = 0.0
        np.save(mask_path, mask_data)
    
    mask = np.load(mask_path)

    # 3. Apply Mask
    # Logic: masked_map = b_mode_map * mask
    # We expect the B-mode map to be a vector of size Npix in this mock context
    masked_map = b_mode_map * mask

    # 4. Verify Sky Coverage (SC-001)
    non_masked_pixels = np.sum(mask > 0.5)
    total_pixels = Npix
    coverage = non_masked_pixels / total_pixels

    assert coverage >= MASK_THRESHOLD, (
        f"Sky coverage {coverage:.2f} is below threshold {MASK_THRESHOLD}. "
        f"Masking operation failed to preserve sufficient sky area."
    )

    # 5. Verify Data Integrity (Checksum)
    # Save the masked map to verify it can be written and read back
    output_path = MASKED_MAP_FILE.replace('.fits', '.npy')
    np.save(output_path, masked_map)

    # Verify the file exists
    assert os.path.exists(output_path), "Masked map file was not created."

    # 6. Verify Content Consistency
    loaded_masked = np.load(output_path)
    assert np.allclose(loaded_masked, masked_map), "Masked map content mismatch after save/load."

    print(f"Integration Test T011 PASSED. Coverage: {coverage:.2f}")

if __name__ == "__main__":
    test_masking_pipeline()
    print("All integration tests for masking passed.")