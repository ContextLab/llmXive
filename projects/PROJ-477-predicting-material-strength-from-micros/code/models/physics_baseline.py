"""
Physics-based baseline predictor using the Hall-Petch relationship.

This module implements a naive baseline that predicts yield strength
based solely on grain size using the Hall-Petch equation:
sigma_y = sigma_0 + k * d^(-0.5)

Dependencies:
- T022: Requires data/processed/grain_features.csv
- T004: Requires utils.config for path management
- T006: Uses data.models.YieldStrengthValue for validation
"""

import os
import csv
import logging
import math
from pathlib import Path
from typing import Dict, List, Optional, Tuple, Any

import numpy as np

# Import project utilities
from utils.config import get_project_root, get_processed_dir
from data.models import YieldStrengthValue

# Constants for Hall-Petch relationship (Steel approximation)
# These values are typical for mild steel and serve as the baseline physics model
HALL_PETCH_SIGMA_0 = 60.0  # MPa (friction stress)
HALL_PETCH_K = 0.7  # MPa * mm^0.5
# Note: Grain size in CSV is in micrometers (um), need to convert to mm for standard HP equation
# d_mm = d_um / 1000.0
# sigma = sigma_0 + k * (d_mm)^(-0.5)

logger = logging.getLogger(__name__)


def load_grain_features_csv() -> Dict[str, float]:
    """
    Loads grain size features from the processed CSV generated by T022.

    Returns:
        Dict mapping image_id to grain_size in micrometers.
    """
    processed_dir = get_processed_dir()
    features_path = processed_dir / "grain_features.csv"

    if not features_path.exists():
        raise FileNotFoundError(
            f"Grain features file not found at {features_path}. "
            "Ensure T022 (extract_features.py) has been run successfully."
        )

    features = {}
    with open(features_path, mode='r', newline='', encoding='utf-8') as f:
        reader = csv.DictReader(f)
        for row in reader:
            image_id = row['image_id']
            try:
                grain_size_um = float(row['grain_size_um'])
                if grain_size_um <= 0:
                    logger.warning(f"Non-positive grain size {grain_size_um} for {image_id}, skipping.")
                    continue
                features[image_id] = grain_size_um
            except ValueError:
                logger.warning(f"Invalid grain size value for {image_id}, skipping.")
                continue

    logger.info(f"Loaded {len(features)} grain size features from {features_path}")
    return features


def calculate_hall_petch_prediction(grain_size_um: float) -> float:
    """
    Calculates yield strength prediction using the Hall-Petch equation.

    Args:
        grain_size_um: Grain size in micrometers.

    Returns:
        Predicted yield strength in MPa.
    """
    # Convert micrometers to millimeters
    grain_size_mm = grain_size_um / 1000.0

    # Hall-Petch equation: sigma_y = sigma_0 + k * d^(-0.5)
    # Avoid division by zero or negative roots
    if grain_size_mm <= 0:
        raise ValueError(f"Grain size must be positive, got {grain_size_mm}")

    d_inv_half = 1.0 / math.sqrt(grain_size_mm)
    predicted_strength = HALL_PETCH_SIGMA_0 + HALL_PETCH_K * d_inv_half

    return float(predicted_strength)


def generate_baseline_predictions(
    image_ids: Optional[List[str]] = None,
    grain_features: Optional[Dict[str, float]] = None
) -> List[Dict[str, Any]]:
    """
    Generates physics-based predictions for a list of images.

    Args:
        image_ids: Optional list of image IDs. If None, uses all IDs from the features file.
        grain_features: Optional pre-loaded features dict. If None, loads from disk.

    Returns:
        List of dicts containing image_id, grain_size_um, and predicted_strength_mpa.
    """
    if grain_features is None:
        grain_features = load_grain_features_csv()

    if image_ids is None:
        image_ids = list(grain_features.keys())

    predictions = []
    for img_id in image_ids:
        if img_id not in grain_features:
            logger.warning(f"Image ID {img_id} not found in grain features, skipping.")
            continue

        grain_size = grain_features[img_id]
        try:
            strength = calculate_hall_petch_prediction(grain_size)
            # Validate using the project's Pydantic model structure if needed
            # Here we just ensure it's a valid float
            if not np.isfinite(strength):
                logger.error(f"Generated non-finite strength for {img_id}")
                continue

            predictions.append({
                'image_id': img_id,
                'grain_size_um': grain_size,
                'predicted_strength_mpa': strength,
                'model_type': 'hall_petch_baseline'
            })
        except ValueError as e:
            logger.error(f"Error calculating prediction for {img_id}: {e}")
            continue

    logger.info(f"Generated {len(predictions)} physics-based baseline predictions.")
    return predictions


def save_predictions_csv(predictions: List[Dict[str, Any]], output_path: Optional[Path] = None) -> Path:
    """
    Saves the baseline predictions to a CSV file.

    Args:
        predictions: List of prediction dictionaries.
        output_path: Optional path to write to. Defaults to data/processed/baseline_predictions.csv.

    Returns:
        Path to the written file.
    """
    if output_path is None:
        processed_dir = get_processed_dir()
        output_path = processed_dir / "baseline_predictions.csv"
    else:
        output_path = Path(output_path)

    output_path.parent.mkdir(parents=True, exist_ok=True)

    fieldnames = ['image_id', 'grain_size_um', 'predicted_strength_mpa', 'model_type']

    with open(output_path, mode='w', newline='', encoding='utf-8') as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(predictions)

    logger.info(f"Saved {len(predictions)} predictions to {output_path}")
    return output_path


def main():
    """
    Main entry point to run the physics baseline prediction.
    Generates predictions for all images in the processed dataset and saves them.
    """
    setup_logger = logging.getLogger(__name__)
    setup_logger.setLevel(logging.INFO)
    if not setup_logger.handlers:
        handler = logging.StreamHandler()
        formatter = logging.Formatter('%(asctime)s - %(name)s - %(levelname)s - %(message)s')
        handler.setFormatter(formatter)
        setup_logger.addHandler(handler)

    try:
        logger.info("Starting Physics-Based Baseline Prediction (Hall-Petch)...")

        # Load features
        features = load_grain_features_csv()

        # Generate predictions
        predictions = generate_baseline_predictions(grain_features=features)

        # Save results
        output_file = save_predictions_csv(predictions)

        logger.info(f"Physics baseline complete. Output: {output_file}")
        return 0

    except Exception as e:
        logger.exception(f"Physics baseline failed: {e}")
        return 1


if __name__ == "__main__":
    import sys
    sys.exit(main())