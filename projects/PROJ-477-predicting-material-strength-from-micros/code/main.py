"""
Main training orchestration script for Material Strength Prediction.

Supports standard training with augmentation and an ablation study mode
via the --no-augmentation flag.

Workflow:
1. Setup logging and seed.
2. Load dataset (train/val splits).
3. Instantiate CNN model.
4. Configure transforms (with or without augmentation).
5. Train with early stopping.
6. Evaluate on test set (MSE, R2, t-test against baseline).
7. Check Null Hypothesis (R2 >= 0.2) or exit with failure.
"""
import os
import sys
import argparse
import logging
import json
from pathlib import Path
from typing import Optional, Dict, Any

import torch
from torch.utils.data import DataLoader
import torchvision.transforms as transforms

# Local imports based on API surface
from utils.config import get_project_root, get_data_dir, get_results_dir, set_seed, get_seed
from utils.logging_config import get_logger
from data.loader import MicrostructureDataset, OOMSafeDataLoader, setup_logging as setup_loader_logging
from data.split import load_processed_manifest
from models.cnn import MaterialStrengthCNN, get_model
from train.augment import get_train_augmentations, get_val_augmentations
from train.trainer import train_with_early_stopping
from eval.metrics import evaluate_model_performance, load_predictions_from_csv
from eval.evaluator import evaluate_and_check_null_hypothesis
from models.physics_baseline import generate_baseline_predictions, save_predictions_csv

def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Orchestrate Material Strength CNN Training")
    parser.add_argument(
        "--no-augmentation",
        action="store_true",
        help="Disable data augmentation for ablation study (uses only basic resize/normalize)"
    )
    parser.add_argument(
        "--epochs",
        type=int,
        default=50,
        help="Maximum number of training epochs (default: 50)"
    )
    parser.add_argument(
        "--patience",
        type=int,
        default=5,
        help="Early stopping patience (default: 5)"
    )
    parser.add_argument(
        "--lr",
        type=float,
        default=1e-4,
        help="Learning rate (default: 1e-4)"
    )
    parser.add_argument(
        "--batch-size",
        type=int,
        default=None,
        help="Override auto batch size detection (optional)"
    )
    parser.add_argument(
        "--seed",
        type=int,
        default=42,
        help="Random seed for reproducibility (default: 42)"
    )
    parser.add_argument(
        "--results-dir",
        type=str,
        default=None,
        help="Custom results directory (optional)"
    )
    return parser.parse_args()

def setup_directories(args: argparse.Namespace) -> Dict[str, Path]:
    project_root = get_project_root()
    data_dir = get_data_dir()
    results_dir = Path(args.results_dir) if args.results_dir else get_results_dir()
    
    # Ensure directories exist
    results_dir.mkdir(parents=True, exist_ok=True)
    (results_dir / "models").mkdir(exist_ok=True)
    (results_dir / "logs").mkdir(exist_ok=True)
    
    return {
        "project_root": project_root,
        "data_dir": data_dir,
        "results_dir": results_dir
    }

def load_dataset_manifests(dirs: Dict[str, Path]) -> Dict[str, Path]:
    """Load paths to train/val/test manifests generated by split.py"""
    processed_dir = dirs["data_dir"] / "processed"
    manifests = {}
    for split in ["train", "val", "test"]:
        manifest_path = processed_dir / f"{split}_manifest.csv"
        if not manifest_path.exists():
            raise FileNotFoundError(f"Split manifest not found: {manifest_path}. "
                                    "Run data/process_all.py first.")
        manifests[split] = manifest_path
    return manifests

def get_transforms(use_augmentation: bool) -> Dict[str, Any]:
    """Get training and validation transforms."""
    if use_augmentation:
        train_transform = get_train_augmentations()
    else:
        # Basic normalization only, no random crops/flips/rotations
        train_transform = transforms.Compose([
            transforms.Resize((224, 224)),
            transforms.ToTensor(),
            transforms.Normalize(mean=[0.485, 0.456, 0.406], std=[0.229, 0.224, 0.225])
        ])
    
    val_transform = get_val_augmentations()
    
    return {
        "train": train_transform,
        "val": val_transform
    }

def run_training(args: argparse.Namespace, dirs: Dict[str, Path], manifests: Dict[str, Path], transforms: Dict[str, Any]) -> Dict[str, Any]:
    logger = logging.getLogger("main_training")
    logger.info("Initializing Dataset and DataLoader...")
    
    # Setup Dataset
    train_ds = MicrostructureDataset(manifest_path=manifests["train"], transform=transforms["train"])
    val_ds = MicrostructureDataset(manifest_path=manifests["val"], transform=transforms["val"])
    
    # Setup DataLoader with OOM safety
    batch_size = args.batch_size
    if batch_size is None:
        # Attempt to auto-detect safe batch size if loader supports it
        # For now, default to a safe small value if not overridden
        batch_size = 8 
    
    train_loader = OOMSafeDataLoader(
        dataset=train_ds,
        batch_size=batch_size,
        shuffle=True,
        num_workers=0, # Keep 0 for robustness in single-script run
        pin_memory=False
    )
    
    val_loader = OOMSafeDataLoader(
        dataset=val_ds,
        batch_size=batch_size,
        shuffle=False,
        num_workers=0,
        pin_memory=False
    )
    
    logger.info(f"Train size: {len(train_ds)}, Val size: {len(val_ds)}")
    
    # Initialize Model
    logger.info("Initializing CNN Model...")
    model = get_model(pretrained=True) # Uses MobileNetV2 backbone per spec
    device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
    model = model.to(device)
    
    # Training
    logger.info("Starting Training Loop...")
    best_model_state, history = train_with_early_stopping(
        model=model,
        train_loader=train_loader,
        val_loader=val_loader,
        device=device,
        epochs=args.epochs,
        patience=args.patience,
        lr=args.lr,
        results_dir=dirs["results_dir"]
    )
    
    # Save final model
    model_path = dirs["results_dir"] / "models" / "best_material_strength_model.pt"
    torch.save(best_model_state, model_path)
    logger.info(f"Best model saved to {model_path}")
    
    return {
        "history": history,
        "model_path": str(model_path),
        "device": str(device)
    }

def run_evaluation(args: argparse.Namespace, dirs: Dict[str, Path], manifests: Dict[str, Path], model_path: str) -> Dict[str, Any]:
    logger = logging.getLogger("main_evaluation")
    logger.info("Loading trained model for evaluation...")
    
    device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
    model = get_model(pretrained=False)
    model.load_state_dict(torch.load(model_path, map_location=device))
    model = model.to(device)
    model.eval()
    
    # Load Test Set
    test_ds = MicrostructureDataset(manifest_path=manifests["test"], transform=get_val_augmentations())
    test_loader = OOMSafeDataLoader(
        dataset=test_ds,
        batch_size=8,
        shuffle=False,
        num_workers=0
    )
    
    # Run Evaluation (Metrics + Baseline Comparison)
    logger.info("Running evaluation on test set...")
    eval_results = evaluate_model_performance(
        model=model,
        loader=test_loader,
        device=device,
        results_dir=dirs["results_dir"],
        baseline_type="physics" # Uses Hall-Petch baseline as per T023
    )
    
    # Null Hypothesis Check
    logger.info("Checking Null Hypothesis...")
    null_hypothesis_status = evaluate_and_check_null_hypothesis(
        results_dir=dirs["results_dir"]
    )
    
    return {
        "metrics": eval_results,
        "null_hypothesis": null_hypothesis_status
    }

def main():
    args = parse_args()
    set_seed(args.seed)
    
    # Setup Logging
    logger = setup_loader_logging(level=logging.INFO)
    logger.info(f"Starting Main Orchestration (Seed: {args.seed}, Augmentation: {not args.no_augmentation})")
    
    try:
        # 1. Setup
        dirs = setup_directories(args)
        manifests = load_dataset_manifests(dirs)
        transforms = get_transforms(not args.no_augmentation)
        
        # 2. Train
        training_info = run_training(args, dirs, manifests, transforms)
        
        # 3. Evaluate
        evaluation_info = run_evaluation(args, dirs, manifests, training_info["model_path"])
        
        # 4. Final Report
        final_report = {
            "config": {
                "seed": args.seed,
                "augmentation": not args.no_augmentation,
                "epochs": args.epochs,
                "patience": args.patience,
                "lr": args.lr
            },
            "training": {
                "model_path": training_info["model_path"],
                "device": training_info["device"]
            },
            "evaluation": evaluation_info["metrics"],
            "null_hypothesis": evaluation_info["null_hypothesis"]
        }
        
        report_path = dirs["results_dir"] / "training_report.json"
        with open(report_path, "w") as f:
            json.dump(final_report, f, indent=2)
        
        logger.info(f"Training complete. Report saved to {report_path}")
        
        # If null hypothesis check failed (R2 < 0.2), exit with error code
        if evaluation_info["null_hypothesis"].get("status") == "rejected":
            logger.error("Null Hypothesis Rejected: R2 < 0.2. Model performance insufficient.")
            sys.exit(1)
            
    except Exception as e:
        logger.error(f"Pipeline failed: {e}", exc_info=True)
        sys.exit(1)

if __name__ == "__main__":
    main()