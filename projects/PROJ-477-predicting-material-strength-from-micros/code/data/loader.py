# --- Dataset Implementation ---
class MicrostructureDataset(Dataset):
    """
    PyTorch Dataset for loading Microstructure images and Yield Strength labels.
    Reads from the split manifests generated by code/data/split.py.
    """
    def __init__(self, manifest_path: Path, transform=None, target_size: int = 224):
        self.manifest_path = Path(manifest_path)
        self.transform = transform
        self.target_size = target_size
        self.data_dir = get_data_dir() / "processed"
        
        if not self.manifest_path.exists():
            raise FileNotFoundError(f"Manifest not found: {self.manifest_path}")

        self.samples: List[Dict[str, Any]] = []
        self._load_manifest()

    def _load_manifest(self):
        """Loads the CSV manifest into memory."""
        with open(self.manifest_path, 'r', encoding='utf-8') as f:
            # Assuming header: image_path, label_value, split (or similar)
            # Adjust based on actual split.py output schema
            import csv
            reader = csv.DictReader(f)
            for row in reader:
                self.samples.append(row)
        
        if not self.samples:
            logger.warning(f"Manifest {self.manifest_path} is empty.")

    def __len__(self) -> int:
        return len(self.samples)

    def __getitem__(self, idx: int) -> Tuple[torch.Tensor, float]:
        sample = self.samples[idx]
        img_path = self.data_dir / sample['image_path']
        
        if not img_path.exists():
            raise FileNotFoundError(f"Image not found: {img_path}")

        # Load image
        image = cv2.imread(str(img_path))
        if image is None:
            raise ValueError(f"Failed to read image: {img_path}")
        
        # Convert BGR to RGB
        image = cv2.cvtColor(image, cv2.COLOR_BGR2RGB)
        
        # Resize
        image = cv2.resize(image, (self.target_size, self.target_size))
        
        # Normalize to [0, 1]
        image = image.astype(np.float32) / 255.0
        
        # Convert to Tensor (C, H, W)
        image = torch.from_numpy(image).permute(2, 0, 1)
        
        # Normalize with ImageNet stats if needed, or simple mean/std
        # Standardizing to simple normalization for this task
        mean = torch.tensor([0.485, 0.456, 0.406]).view(3, 1, 1)
        std = torch.tensor([0.229, 0.224, 0.225]).view(3, 1, 1)
        image = (image - mean) / std

        label = float(sample['label_value'])

        return image, label

# --- Safe DataLoader Wrapper ---
class OOMSafeDataLoader:
    """
    Wrapper around PyTorch DataLoader that monitors memory usage.
    If memory usage exceeds a threshold, it attempts to clear cache and reduce batch size.
    """
    def __init__(self, dataset: Dataset, initial_batch_size: int = 16, max_memory_gb: float = 6.0):
        self.dataset = dataset
        self.batch_size = initial_batch_size
        self.max_memory_gb = max_memory_gb
        self.base_loader: Optional[DataLoader] = None
        self.logger = logging.getLogger("data.loader")

    def _get_memory_usage_gb(self) -> float:
        """Estimates current memory usage in GB."""
        if torch.cuda.is_available():
            return torch.cuda.memory_allocated() / (1024 ** 3)
        else:
            # Fallback for CPU: try to get process memory (approximate)
            try:
                import resource
                usage = resource.getrusage(resource.RUSAGE_SELF)
                # ru_maxrss is in KB on Linux/macOS
                return usage.ru_maxrss / (1024 * 1024) # Convert KB to GB
            except Exception:
                return 0.0

    def _clear_cache(self):
        """Forces garbage collection and GPU cache clearing."""
        gc.collect()
        if torch.cuda.is_available():
            torch.cuda.empty_cache()
        self.logger.warning("Memory pressure detected. Clearing cache and reducing batch size.")

    def get_loader(self, batch_size: Optional[int] = None) -> DataLoader:
        """Returns a DataLoader with the current safe batch size."""
        bs = batch_size if batch_size else self.batch_size
        # Ensure deterministic behavior if seed is set
        worker_init_fn = None
        seed = get_seed()
        if seed is not None:
            def worker_init_fn(worker_id):
                np.random.seed(seed + worker_id)
                torch.manual_seed(seed + worker_id)
        
        return DataLoader(
            self.dataset,
            batch_size=bs,
            shuffle=False, # Usually handled by training loop
            num_workers=0, # Set to 0 to avoid fork issues in stress test context
            pin_memory=False,
            worker_init_fn=worker_init_fn
        )

    def iterate_with_monitor(self) -> Iterator[Tuple[torch.Tensor, torch.Tensor]]:
        """
        Iterates through the dataset, monitoring memory.
        If OOM is approached, reduces batch size dynamically.
        """
        loader = self.get_loader()
        
        for batch_idx, (data, target) in enumerate(loader):
            mem_gb = self._get_memory_usage_gb()
            if mem_gb > self.max_memory_gb:
                self._clear_cache()
                if self.batch_size > 1:
                    self.batch_size = max(1, self.batch_size // 2)
                    loader = self.get_loader()
                    # Restart iteration with smaller batch
                    self.logger.info(f"Restarting iteration with batch size {self.batch_size}")
                    for data, target in loader:
                        yield data, target
                    return
                else:
                    raise MemoryError(f"Memory usage ({mem_gb:.2f}GB) exceeded limit even with batch size 1.")
            
            yield data, target

# --- Stress Test Logic ---
def get_optimal_batch_size(
    dataset: Dataset, 
    target_memory_gb: float = 5.0, 
    max_trials: int = 5
) -> int:
    """
    Finds the largest batch size that fits within target_memory_gb.
    """
    logger.info("Starting batch size search...")
    batch_sizes = [32, 16, 8, 4, 2, 1]
    
    for bs in batch_sizes:
        try:
            loader = DataLoader(dataset, batch_size=bs, num_workers=0)
            # Consume one batch to trigger memory allocation
            for _ in loader:
                break
            
            mem_gb = torch.cuda.memory_allocated() / (1024 ** 3) if torch.cuda.is_available() else 0.0
            if torch.cuda.is_available() and mem_gb > target_memory_gb:
                logger.warning(f"Batch size {bs} used {mem_gb:.2f}GB, exceeding target.")
                continue
            
            logger.info(f"Optimal batch size found: {bs} (Mem: {mem_gb:.2f}GB)")
            return bs
        except MemoryError:
            logger.warning(f"Batch size {bs} caused OOM.")
            continue
        
    return 1

def run_stress_test(
    manifest_path: Path, 
    max_memory_limit_gb: float = 7.0,
    output_path: Optional[Path] = None
) -> Dict[str, Any]:
    """
    Runs a stress test on the loader:
    1. Loads the dataset.
    2. Iterates through the entire dataset.
    3. Monitors peak memory usage.
    4. Records results.
    
    Fails if peak memory > max_memory_limit_gb.
    """
    logger.info(f"Starting stress test with manifest: {manifest_path}")
    logger.info(f"Memory limit set to: {max_memory_limit_gb} GB")
    
    if not manifest_path.exists():
        raise FileNotFoundError(f"Manifest for stress test not found: {manifest_path}")

    # 1. Setup Dataset
    dataset = MicrostructureDataset(manifest_path)
    logger.info(f"Dataset loaded. Size: {len(dataset)}")

    # 2. Determine Batch Size
    # We try a reasonable start, but the OOMSafeDataLoader will adjust
    safe_loader = OOMSafeDataLoader(dataset, initial_batch_size=16, max_memory_gb=max_memory_limit_gb)
    
    peak_memory_gb = 0.0
    start_time = time.time()
    total_samples = 0
    
    try:
        for data, target in safe_loader.iterate_with_monitor():
            # Simulate processing (e.g., forward pass) to ensure memory is actually used
            # In a real scenario, this would be model inference
            _ = data.sum() 
            
            current_mem = safe_loader._get_memory_usage_gb()
            if current_mem > peak_memory_gb:
                peak_memory_gb = current_mem
            
            total_samples += data.shape[0]
            
            if total_samples % 100 == 0:
                logger.info(f"Processed {total_samples} samples. Current Mem: {current_mem:.2f}GB")
        
        elapsed_time = time.time() - start_time
      
        # 3. Compile Results
        results = {
            "status": "success",
            "total_samples_processed": total_samples,
            "peak_memory_gb": round(peak_memory_gb, 3),
            "limit_gb": max_memory_limit_gb,
            "passed": peak_memory_gb <= max_memory_limit_gb,
            "elapsed_seconds": round(elapsed_time, 2),
            "final_batch_size": safe_loader.batch_size
        }
        
        logger.info(f"Stress test completed. Peak Memory: {peak_memory_gb:.3f} GB. Passed: {results['passed']}")
        
        # 4. Write Output
        if output_path is None:
            output_path = get_results_dir() / "memory_profile.json"
        
        output_path.parent.mkdir(parents=True, exist_ok=True)
        with open(output_path, 'w', encoding='utf-8') as f:
            json.dump(results, f, indent=2)
        
        logger.info(f"Results written to {output_path}")
        
        if not results['passed']:
            logger.error(f"STRESS TEST FAILED: Peak memory ({peak_memory_gb:.3f}GB) exceeded limit ({max_memory_limit_gb}GB).")
            raise MemoryError(f"Stress test failed: Peak memory {peak_memory_gb:.3f}GB > {max_memory_limit_gb}GB")
            
        return results

    except MemoryError as e:
        logger.error(f"Stress test failed due to memory error: {e}")
        results = {
            "status": "failed",
            "error": str(e),
            "peak_memory_gb": round(safe_loader._get_memory_usage_gb(), 3),
            "limit_gb": max_memory_limit_gb,
            "passed": False
        }
        if output_path:
            output_path.parent.mkdir(parents=True, exist_ok=True)
            with open(output_path, 'w') as f:
                json.dump(results, f, indent=2)
        raise

# --- CLI Entry Point ---
def main():
    parser = argparse.ArgumentParser(description="Data Loader Stress Test")
    parser.add_argument(
        "--manifest", 
        type=str, 
        default=None,
        help="Path to the split manifest CSV (e.g., data/processed/splits/test_manifest.csv)"
    )
    parser.add_argument(
        "--split",
        type=str,
        default="test",
        choices=["train", "val", "test"],
        help="Which split to test if manifest is not provided"
    )
    parser.add_argument(
        "--stress-test",
        action="store_true",
        help="Run the full stress test with memory monitoring"
    )
    parser.add_argument(
        "--limit",
        type=float,
        default=7.0,
        help="Maximum allowed memory in GB (default: 7.0)"
    )
    
    args = parser.parse_args()
    
    set_seed(get_seed())
    
    if args.stress_test:
        if args.manifest is None:
            # Construct default path
            data_dir = get_data_dir()
            manifest_path = data_dir / "processed" / "splits" / f"{args.split}_manifest.csv"
        else:
            manifest_path = Path(args.manifest)
        
        try:
            run_stress_test(manifest_path, max_memory_limit_gb=args.limit)
            print("Stress test PASSED.")
            sys.exit(0)
        except MemoryError as e:
            print(f"Stress test FAILED: {e}")
            sys.exit(1)
        except FileNotFoundError as e:
            print(f"Error: {e}")
            sys.exit(1)
        except Exception as e:
            print(f"Unexpected error: {e}")
            traceback.print_exc()
            sys.exit(1)
    else:
        parser.print_help()

if __name__ == "__main__":
    main()