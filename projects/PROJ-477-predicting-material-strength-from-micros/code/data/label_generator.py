"""
Hall-Petch Label Generation Logic

This module implements the physics-based label generation for yield strength
based on the Hall-Petch relationship: σ_y = σ_0 + k_y * d^(-0.5)

It depends on the grain size features extracted by T022 (extract_features.py)
and reads from data/processed/grain_features.csv.
"""

import os
import csv
import logging
from pathlib import Path
from typing import Dict, List, Optional, Tuple

# Import configuration from utils
# Assuming config.py provides paths and constants as per T004
# We will implement a fallback if not imported directly to avoid circular issues
# but the primary source of truth is the config file.

# Constants for Hall-Petch (Steel/Mild Steel typical values as per Plan)
# σ_0 (friction stress) in MPa
HALL_PETCH_SIGMA_0 = 60.0
# k_y (Hall-Petch slope) in MPa * um^0.5
HALL_PETCH_KY = 0.7
# Reference grain size in um (for normalization if needed, though formula uses absolute)
REFERENCE_GRAIN_SIZE = 1.0

logger = logging.getLogger(__name__)

def load_grain_features(input_path: Path) -> List[Dict]:
    """
    Loads grain size features from the CSV generated by T022.
    
    Expected schema: image_id, grain_size_um
    """
    if not input_path.exists():
        raise FileNotFoundError(f"Input file not found: {input_path}")
    
    rows = []
    with open(input_path, 'r', newline='', encoding='utf-8') as f:
        reader = csv.DictReader(f)
        for row in reader:
            try:
                rows.append({
                    'image_id': row['image_id'].strip(),
                    'grain_size_um': float(row['grain_size_um'])
                })
            except (ValueError, KeyError) as e:
                logger.warning(f"Skipping invalid row in {input_path}: {row} - Error: {e}")
    return rows

def calculate_hall_petch_strength(grain_size_um: float) -> float:
    """
    Calculates yield strength (MPa) using the Hall-Petch equation.
    
    Formula: σ_y = σ_0 + k_y * d^(-0.5)
    Where:
      σ_0 = 60.0 MPa (friction stress)
      k_y = 0.7 MPa * um^0.5 (Hall-Petch slope)
      d = grain size in micrometers
    
    Args:
        grain_size_um: Average grain diameter in micrometers.
        
    Returns:
        Calculated yield strength in MPa.
    """
    if grain_size_um <= 0:
        raise ValueError(f"Grain size must be positive, got {grain_size_um}")
    
    # Hall-Petch calculation
    # d^(-0.5) is 1 / sqrt(d)
    d_inverse_sqrt = 1.0 / (grain_size_um ** 0.5)
    yield_strength = HALL_PETCH_SIGMA_0 + (HALL_PETCH_KY * d_inverse_sqrt)
    
    return yield_strength

def generate_labels(
    input_csv: Path,
    output_csv: Path,
    sigma_0: Optional[float] = None,
    ky: Optional[float] = None
) -> Tuple[int, int]:
    """
    Generates yield strength labels for all images based on grain size.
    
    Args:
        input_csv: Path to the grain features CSV (output of T022).
        output_csv: Path where the labeled CSV will be written.
        sigma_0: Optional override for friction stress constant.
        ky: Optional override for Hall-Petch slope constant.
        
    Returns:
        Tuple of (success_count, failure_count)
    """
    # Use constants if overrides not provided
    current_sigma_0 = sigma_0 if sigma_0 is not None else HALL_PETCH_SIGMA_0
    current_ky = ky if ky is not None else HALL_PETCH_KY
    
    logger.info(f"Loading grain features from {input_csv}")
    features = load_grain_features(input_csv)
    
    if not features:
        logger.warning("No valid features found in input file.")
        # Ensure output directory exists even if empty
        output_csv.parent.mkdir(parents=True, exist_ok=True)
        with open(output_csv, 'w', newline='', encoding='utf-8') as f:
            writer = csv.DictWriter(f, fieldnames=['image_id', 'grain_size_um', 'yield_strength_mpah'])
            writer.writeheader()
        return 0, 0

    logger.info(f"Processing {len(features)} images...")
    output_csv.parent.mkdir(parents=True, exist_ok=True)
    
    success_count = 0
    failure_count = 0
    
    with open(output_csv, 'w', newline='', encoding='utf-8') as f:
        fieldnames = ['image_id', 'grain_size_um', 'yield_strength_mpah']
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        
        for item in features:
            try:
                strength = calculate_hall_petch_strength(item['grain_size_um'])
                writer.writerow({
                    'image_id': item['image_id'],
                    'grain_size_um': item['grain_size_um'],
                    'yield_strength_mpah': round(strength, 4)
                })
                success_count += 1
            except ValueError as e:
                logger.error(f"Failed to calculate label for {item['image_id']}: {e}")
                failure_count += 1
            except Exception as e:
                logger.error(f"Unexpected error for {item['image_id']}: {e}")
                failure_count += 1
    
    logger.info(f"Label generation complete. Success: {success_count}, Failed: {failure_count}")
    return success_count, failure_count

def main():
    """
    Entry point for the label generation script.
    Reads from data/processed/grain_features.csv and writes to data/processed/labels.csv
    """
    # Setup logging
    logging.basicConfig(
        level=logging.INFO,
        format='%(asctime)s - %(name)s - %(levelname)s - %(message)s'
    )
    
    # Determine paths relative to project root
    # Assuming this script runs from the project root or code/ directory
    # We try to resolve relative to the script location first, then project root
    script_dir = Path(__file__).resolve().parent
    project_root = script_dir.parent.parent # code -> root
    
    input_path = project_root / 'data' / 'processed' / 'grain_features.csv'
    output_path = project_root / 'data' / 'processed' / 'labels.csv'
    
    # If running from code/data/, adjust
    if not input_path.exists():
        # Fallback if run from code/data/
        input_path = script_dir.parent / 'data' / 'processed' / 'grain_features.csv'
        output_path = script_dir.parent / 'data' / 'processed' / 'labels.csv'
    
    if not input_path.exists():
        logger.error(f"Input file not found at {input_path}. Ensure T022 has been run.")
        raise FileNotFoundError(f"Input file not found: {input_path}")
    
    logger.info(f"Starting Hall-Petch label generation.")
    logger.info(f"Input: {input_path}")
    logger.info(f"Output: {output_path}")
    logger.info(f"Constants: σ_0={HALL_PETCH_SIGMA_0} MPa, k_y={HALL_PETCH_KY} MPa·μm^0.5")
    
    try:
        success, failure = generate_labels(input_path, output_path)
        if failure > 0:
            logger.warning(f"Completed with {failure} failures.")
            # Depending on strictness, we might exit with error code
            # but usually partial success is acceptable in pipelines
        else:
            logger.info("Successfully generated all labels.")
    except Exception as e:
        logger.critical(f"Label generation failed: {e}")
        raise

if __name__ == "__main__":
    main()