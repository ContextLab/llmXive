"""
Data Validation Script for User Story 1.

Validates the split datasets (train/val/test) generated by split.py.
Checks for:
1. Existence of image files referenced in manifests.
2. Consistency of labels in manifests.
3. Presence of required columns.

Outputs: results/validation_report.json
Exits with code 1 if invalid ratio > 1%.
"""
import os
import sys
import json
import logging
from pathlib import Path
from typing import Dict, List, Any, Tuple
from collections import defaultdict

# Import project utilities
from utils.config import get_project_root, get_results_dir, get_processed_dir
from utils.logging_config import get_logger

# Constants
INVALID_RATIO_THRESHOLD = 0.01  # 1%
REQUIRED_MANIFEST_COLUMNS = {'filename', 'yield_strength'}
SPLIT_DIRS = ['train', 'val', 'test']

def setup_logging():
    """Initialize logger for validation script."""
    logger = get_logger("validate", level=logging.INFO)
    # Ensure handler doesn't duplicate if called multiple times in same session
    if not logger.handlers:
        handler = logging.StreamHandler(sys.stdout)
        handler.setFormatter(logging.Formatter('%(asctime)s - %(name)s - %(levelname)s - %(message)s'))
        logger.addHandler(handler)
    return logger

def load_manifest(manifest_path: Path) -> Tuple[List[Dict[str, Any]], List[str]]:
    """
    Load a manifest CSV file.
    Returns: (list of rows, list of error messages)
    """
    errors = []
    rows = []
    
    if not manifest_path.exists():
        errors.append(f"Manifest file not found: {manifest_path}")
        return rows, errors

    try:
        import csv
        with open(manifest_path, 'r', newline='', encoding='utf-8') as f:
            reader = csv.DictReader(f)
            
            # Check required columns
            if reader.fieldnames is None:
                errors.append(f"Empty or malformed CSV: {manifest_path}")
                return rows, errors
            
            missing_cols = REQUIRED_MANIFEST_COLUMNS - set(reader.fieldnames)
            if missing_cols:
                errors.append(f"Missing required columns in {manifest_path}: {missing_cols}")
                return rows, errors
            
            for row_num, row in enumerate(reader, start=2):
                # Basic validation of row content
                if not row.get('filename'):
                    errors.append(f"Row {row_num} missing filename in {manifest_path}")
                    continue
                if row.get('yield_strength') is None or row.get('yield_strength') == '':
                    errors.append(f"Row {row_num} missing yield_strength in {manifest_path}")
                    continue
                try:
                    float(row['yield_strength'])
                except ValueError:
                    errors.append(f"Row {row_num} has non-numeric yield_strength in {manifest_path}")
                    continue
                
                rows.append(row)
                
    except Exception as e:
        errors.append(f"Failed to read manifest {manifest_path}: {str(e)}")
        
    return rows, errors

def validate_image_exists(image_path: Path, manifest_path: Path, row_idx: int) -> bool:
    """Check if the image file exists at the specified path."""
    if not image_path.exists():
        return False
    return True

def run_validation(logger: logging.Logger) -> Dict[str, Any]:
    """
    Run validation checks on all split manifests.
    Returns a report dictionary.
    """
    project_root = get_project_root()
    processed_dir = get_processed_dir()
    results_dir = get_results_dir()
    
    # Ensure results directory exists
    results_dir.mkdir(parents=True, exist_ok=True)
    
    total_pairs = 0
    invalid_pairs = 0
    split_details = {}
    all_errors = []
    
    logger.info(f"Starting validation in project: {project_root}")
    logger.info(f"Looking for splits in: {processed_dir}")
    
    for split_name in SPLIT_DIRS:
        manifest_path = processed_dir / f"{split_name}_manifest.csv"
        split_report = {
            "total_rows": 0,
            "invalid_rows": 0,
            "missing_files": 0,
            "malformed_rows": 0,
            "errors": []
        }
        
        logger.info(f"Validating split: {split_name}")
        
        if not manifest_path.exists():
            msg = f"Manifest not found for split {split_name}: {manifest_path}"
            logger.error(msg)
            split_report["errors"].append(msg)
            split_details[split_name] = split_report
            all_errors.append(msg)
            continue
        
        rows, load_errors = load_manifest(manifest_path)
        split_report["total_rows"] = len(rows)
        split_report["errors"].extend(load_errors)
        all_errors.extend(load_errors)
        
        if not rows:
            logger.warning(f"No valid rows found in {split_name} manifest")
            split_details[split_name] = split_report
            continue
        
        # Validate each row
        for row_idx, row in enumerate(rows):
            total_pairs += 1
            is_valid = True
            
            # Check image existence
            image_filename = row['filename']
            # Assuming images are stored in processed_dir/{split_name}/ or similar structure
            # Based on standard pipeline, images usually reside in data/processed/{split_name}/
            # But the manifest might just have the filename. Let's assume the structure:
            # data/processed/images/{split_name}/{filename} OR data/processed/{split_name}/{filename}
            # Given T013 generates splits, it likely creates subdirs.
            # We'll check common locations.
            
            possible_paths = [
                processed_dir / split_name / image_filename,
                processed_dir / "images" / split_name / image_filename,
                project_root / "data" / "processed" / split_name / image_filename
            ]
            
            image_found = False
            for p in possible_paths:
                if p.exists():
                    image_found = True
                    break
            
            if not image_found:
                split_report["missing_files"] += 1
                invalid_pairs += 1
                is_valid = False
                if len(split_report["errors"]) < 10: # Limit log noise
                    split_report["errors"].append(f"Missing image: {image_filename}")
            
            if not is_valid:
                split_report["invalid_rows"] += 1
        
        split_details[split_name] = split_report
        
        logger.info(f"  Total: {split_report['total_rows']}, Invalid: {split_report['invalid_rows']}")
    
    # Calculate ratio
    invalid_ratio = invalid_pairs / total_pairs if total_pairs > 0 else 0.0
    
    report = {
        "status": "failed" if invalid_ratio > INVALID_RATIO_THRESHOLD else "passed",
        "invalid_ratio": invalid_ratio,
        "threshold": INVALID_RATIO_THRESHOLD,
        "total_pairs": total_pairs,
        "invalid_pairs": invalid_pairs,
        "splits": split_details,
        "execution_summary": {
            "total_errors_logged": len(all_errors),
            "critical_failure": invalid_ratio > INVALID_RATIO_THRESHOLD
        }
    }
    
    return report

def main():
    """Main entry point."""
    logger = setup_logging()
    
    try:
        report = run_validation(logger)
        
        # Write report to results
        results_dir = get_results_dir()
        output_path = results_dir / "validation_report.json"
        
        with open(output_path, 'w', encoding='utf-8') as f:
            json.dump(report, f, indent=2)
        
        logger.info(f"Validation report written to: {output_path}")
        logger.info(f"Status: {report['status']}")
        logger.info(f"Invalid Ratio: {report['invalid_ratio']:.4f} (Threshold: {report['threshold']})")
        
        if report['status'] == 'failed':
            logger.error(f"Validation FAILED: Invalid ratio {report['invalid_ratio']} exceeds threshold {report['threshold']}")
            sys.exit(1)
        else:
            logger.info("Validation PASSED")
            sys.exit(0)
            
    except Exception as e:
        logger.exception(f"Validation process crashed: {e}")
        sys.exit(1)

if __name__ == "__main__":
    main()
