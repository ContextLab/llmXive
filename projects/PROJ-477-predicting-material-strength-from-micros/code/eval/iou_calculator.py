"""
T030: IoU Calculation for Grad-CAM vs Grain Boundaries.

Calculates Intersection over Union (IoU) between Grad-CAM heatmaps
and manually annotated grain boundaries (if available).
If no annotations are available, generates an expert review report.

Input:
    - data/processed/grain_features.csv (for image list)
    - data/processed/grain_boundaries.json (optional, manual annotations)
    - results/grad_cam_heatmaps/ (generated by T029)

Output:
    - results/interpretability_iou.json
"""

import os
import json
import csv
import logging
import numpy as np
from pathlib import Path
from typing import Dict, List, Optional, Tuple, Any

# Import from project utilities
from utils.config import get_project_root, get_data_dir, get_processed_dir, get_results_dir
from utils.logging_config import get_logger


def calculate_iou(mask1: np.ndarray, mask2: np.ndarray) -> float:
    """
    Calculate Intersection over Union between two binary masks.

    Args:
        mask1: First binary mask (0 or 1)
        mask2: Second binary mask (0 or 1)

    Returns:
        IoU score (0.0 to 1.0)
    """
    intersection = np.logical_and(mask1, mask2)
    union = np.logical_or(mask1, mask2)

    if np.sum(union) == 0:
        return 0.0

    return float(np.sum(intersection) / np.sum(union))


def load_grain_boundaries(annotations_path: Path) -> Optional[Dict[str, np.ndarray]]:
    """
    Load manual grain boundary annotations from JSON file.

    Expected format:
    {
      "image_id_1": {
        "width": 224,
        "height": 224,
        "boundaries": [[x1, y1], [x2, y2], ...]  # polygon points
      },
      ...
    }

    Returns:
        Dictionary mapping image_id to binary mask, or None if file doesn't exist.
    """
    if not annotations_path.exists():
        logging.info(f"No grain boundary annotations found at {annotations_path}")
        return None

    try:
        with open(annotations_path, 'r') as f:
            data = json.load(f)

        masks = {}
        for image_id, info in data.items():
            height, width = info['height'], info['width']
            mask = np.zeros((height, width), dtype=np.uint8)

            # Draw boundaries (simplified: fill polygon)
            for polygon in info.get('boundaries', []):
                pts = np.array(polygon, dtype=np.int32)
                cv2.fillPoly(mask, [pts], 1)

            masks[image_id] = mask

        return masks
    except Exception as e:
        logging.warning(f"Failed to load grain boundary annotations: {e}")
        return None


def load_grad_cam_heatmaps(heatmaps_dir: Path) -> Dict[str, np.ndarray]:
    """
    Load Grad-CAM heatmaps from directory.

    Expected format: PNG or numpy files in results/grad_cam_heatmaps/
    Returns dictionary mapping image_id to normalized heatmap.
    """
    heatmaps = {}

    if not heatmaps_dir.exists():
        logging.warning(f"Grad-CAM heatmaps directory not found: {heatmaps_dir}")
        return heatmaps

    for file_path in heatmaps_dir.iterdir():
        if file_path.suffix.lower() in ['.png', '.jpg', '.npy']:
            image_id = file_path.stem
            try:
                if file_path.suffix.lower() == '.npy':
                    heatmap = np.load(file_path)
                else:
                    import cv2
                    heatmap = cv2.imread(str(file_path), cv2.IMREAD_GRAYSCALE)
                    if heatmap is not None:
                        heatmap = heatmap.astype(np.float32) / 255.0

                if heatmap is not None and heatmap.size > 0:
                    heatmaps[image_id] = heatmap
            except Exception as e:
                logging.warning(f"Failed to load heatmap {file_path}: {e}")

    return heatmaps


def load_grain_features(processed_dir: Path) -> List[str]:
    """
    Load list of image IDs from grain_features.csv.

    Returns:
        List of image IDs.
    """
    features_path = processed_dir / "grain_features.csv"

    if not features_path.exists():
        logging.error(f"Grain features file not found: {features_path}")
        return []

    image_ids = []
    with open(features_path, 'r') as f:
        reader = csv.DictReader(f)
        for row in reader:
            image_ids.append(row['image_id'])

    return image_ids


def generate_expert_review_report(
    image_ids: List[str],
    annotations_available: bool,
    heatmaps_available: bool,
    output_path: Path
) -> Dict[str, Any]:
    """
    Generate an expert review report when IoU cannot be calculated.

    Returns:
        Report dictionary.
    """
    report = {
        "status": "expert_review_required",
        "annotations_available": annotations_available,
        "heatmaps_available": heatmaps_available,
        "reason": "",
        "recommendation": "Manual inspection required by domain expert",
        "sample_images": image_ids[:5] if image_ids else [],
        "threshold_target": 0.4,
        "notes": [
            "No manual grain boundary annotations found for IoU calculation",
            "Grad-CAM heatmaps should be reviewed visually for alignment with microstructure features",
            "Expert should verify if high-activation regions correspond to grain boundaries"
        ]
    }

    if not heatmaps_available:
        report["reason"] = "Grad-CAM heatmaps not generated. Run T029 first."
    elif not annotations_available:
        report["reason"] = "No manual grain boundary annotations available."

    with open(output_path, 'w') as f:
        json.dump(report, f, indent=2)

    return report


def calculate_iou_report(
    image_ids: List[str],
    annotations: Optional[Dict[str, np.ndarray]],
    heatmaps: Dict[str, np.ndarray],
    threshold: float = 0.4
) -> Dict[str, Any]:
    """
    Calculate IoU for each image and generate summary report.

    Args:
        image_ids: List of image IDs to process
        annotations: Dictionary of manual boundary masks
        heatmaps: Dictionary of Grad-CAM heatmaps
        threshold: Minimum IoU threshold for "good" alignment

    Returns:
        Report dictionary with per-image IoU and summary statistics.
    """
    if annotations is None:
        return {
            "status": "no_annotations",
            "message": "Cannot calculate IoU without manual annotations",
            "threshold": threshold
        }

    iou_scores = []
    per_image_results = []

    for image_id in image_ids:
        if image_id not in annotations or image_id not in heatmaps:
            continue

        gt_mask = annotations[image_id]
        pred_mask = heatmaps[image_id]

        # Binarize heatmap at median threshold
        pred_binary = (pred_mask > np.median(pred_mask)).astype(np.uint8)

        iou = calculate_iou(gt_mask, pred_binary)
        iou_scores.append(iou)

        per_image_results.append({
            "image_id": image_id,
            "iou": iou,
            "passes_threshold": iou >= threshold
        })

    if not iou_scores:
        return {
            "status": "no_overlap",
            "message": "No images with both annotations and heatmaps found",
            "threshold": threshold
        }

    mean_iou = float(np.mean(iou_scores))
    median_iou = float(np.median(iou_scores))
    min_iou = float(np.min(iou_scores))
    max_iou = float(np.max(iou_scores))

    passes_count = sum(1 for iou in iou_scores if iou >= threshold)

    report = {
        "status": "completed",
        "threshold": threshold,
        "summary": {
            "mean_iou": mean_iou,
            "median_iou": median_iou,
            "min_iou": min_iou,
            "max_iou": max_iou,
            "total_images": len(iou_scores),
            "images_passing_threshold": passes_count,
            "pass_rate": passes_count / len(iou_scores) if iou_scores else 0.0
        },
        "per_image": per_image_results,
        "conclusion": "PASS" if mean_iou >= threshold else "NEEDS_IMPROVEMENT"
    }

    return report


def main():
    """Main entry point for IoU calculation task."""
    import argparse

    parser = argparse.ArgumentParser(description="Calculate IoU between Grad-CAM and grain boundaries")
    parser.add_argument("--threshold", type=float, default=0.4, help="IoU threshold for pass/fail")
    args = parser.parse_args()

    # Setup logging
    logger = get_logger(__name__)

    # Get paths
    project_root = get_project_root()
    processed_dir = get_processed_dir()
    results_dir = get_results_dir()

    # Ensure results directory exists
    results_dir.mkdir(parents=True, exist_ok=True)

    output_path = results_dir / "interpretability_iou.json"
    annotations_path = processed_dir / "grain_boundaries.json"
    heatmaps_dir = results_dir / "grad_cam_heatmaps"

    logger.info(f"Starting IoU calculation task")
    logger.info(f"Output will be written to: {output_path}")

    # Load image IDs
    image_ids = load_grain_features(processed_dir)
    if not image_ids:
        logger.error("No image IDs found in grain_features.csv")
        return 1

    logger.info(f"Found {len(image_ids)} images to process")

    # Load annotations (optional)
    annotations = load_grain_boundaries(annotations_path)
    annotations_available = annotations is not None

    # Load heatmaps
    heatmaps = load_grad_cam_heatmaps(heatmaps_dir)
    heatmaps_available = len(heatmaps) > 0

    logger.info(f"Annotations available: {annotations_available}")
    logger.info(f"Heatmaps available: {heatmaps_available}")

    # If no annotations, generate expert review report
    if not annotations_available:
        logger.warning("No manual annotations found. Generating expert review report.")
        report = generate_expert_review_report(
            image_ids,
            annotations_available=False,
            heatmaps_available=heatmaps_available,
            output_path=output_path
        )
    else:
        # Calculate IoU
        logger.info("Calculating IoU scores...")
        report = calculate_iou_report(
            image_ids,
            annotations,
            heatmaps,
            threshold=args.threshold
        )

    # Write report
    with open(output_path, 'w') as f:
        json.dump(report, f, indent=2)

    logger.info(f"IoU report written to: {output_path}")

    # Print summary
    if report.get("status") == "completed":
        summary = report.get("summary", {})
        logger.info(f"Mean IoU: {summary.get('mean_iou', 0):.4f}")
        logger.info(f"Pass Rate: {summary.get('pass_rate', 0):.4f}")
        logger.info(f"Conclusion: {report.get('conclusion', 'UNKNOWN')}")
    else:
        logger.info(f"Report Status: {report.get('status', 'UNKNOWN')}")
        logger.info(f"Message: {report.get('message', 'No message')}")

    return 0


if __name__ == "__main__":
    exit(main())
