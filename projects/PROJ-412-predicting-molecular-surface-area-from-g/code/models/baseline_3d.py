"""
Geometry-Based Baseline Implementation.

This module implements the Geometry-Based Baseline (FR-004) which uses
ground-truth 3D geometry (conformers) generated by RDKit to predict
molecular surface area (SASA).

Unlike the GCN model which predicts from 2D graphs, this baseline
calculates SASA directly from the 3D structure, serving as the
"oracle" comparator to evaluate how much information is lost when
moving from 3D to 2D representations.

The "training" step is simply the execution of the RDKit pipeline
on the test set to generate predictions.
"""
import os
import sys
import logging
import json
from pathlib import Path
from typing import List, Dict, Any, Optional, Tuple

import numpy as np
import pandas as pd
from rdkit import Chem
from rdkit.Chem import AllChem, rdMolDescriptors

# Project imports
from utils.logging import get_logger
from utils.config import get_project_root, get_data_dir
from utils.conformer_config import load_conformer_config

logger = get_logger(__name__)

def calculate_sasa_from_smiles(smiles: str, max_conformer_attempts: int = 5) -> Optional[float]:
    """
    Generate a 3D conformer from a SMILES string and calculate SASA.

    This function:
    1. Parses the SMILES into an RDKit molecule.
    2. Generates 3D coordinates using ETKDG.
    3. Optimizes the geometry.
    4. Calculates the Solvent Accessible Surface Area (SASA).

    Args:
        smiles: The SMILES string of the molecule.
        max_conformer_attempts: Number of attempts to generate a valid conformer.

    Returns:
        The calculated SASA value (float) or None if generation fails.
    """
    mol = Chem.MolFromSmiles(smiles)
    if mol is None:
        logger.warning(f"Invalid SMILES: {smiles}")
        return None

    # Add hydrogens for accurate SASA calculation
    mol_h = Chem.AddHs(mol)

    # Generate 3D conformer
    params = AllChem.ETKDGv3()
    params.randomSeed = 42  # Reproducibility
    params.maxAttempts = max_conformer_attempts

    try:
        res = AllChem.EmbedMolecule(mol_h, params)
        if res == -1:
            logger.debug(f"Conformer generation failed for: {smiles}")
            return None

        # Optimize geometry with MMFF94
        mmff_props = AllChem.MMFFOptimizeMolecule(mol_h, maxIters=200)
        if mmff_props != 0:
            logger.debug(f"MMFF optimization did not converge for: {smiles}")
            # Continue anyway, as SASA might still be calculable

        # Calculate SASA
        # rdMolDescriptors.CalcSASA uses the probe radius (default 1.4 Angstrom for water)
        sasa = rdMolDescriptors.CalcSASA(mol_h)
        return float(sasa)

    except Exception as e:
        logger.warning(f"Error calculating SASA for {smiles}: {e}")
        return None

def predict_baseline_sasa(
    input_path: str,
    output_path: str,
    smiles_column: str = "smiles",
    target_column: str = "sasa"
) -> Dict[str, Any]:
    """
    Run the Geometry-Based Baseline on a dataset.

    This function reads a CSV/Parquet file containing SMILES, generates
    3D conformers, calculates SASA, and writes the results to a new file.
    It acts as the "inference" step for the baseline.

    Args:
        input_path: Path to the input data file (CSV or Parquet).
        output_path: Path to save the predictions.
        smiles_column: Name of the column containing SMILES.
        target_column: Name of the column to use for the ground truth (for comparison).

    Returns:
        A dictionary containing statistics about the run.
    """
    logger.info(f"Loading data from {input_path}")
    df = pd.read_csv(input_path)

    if smiles_column not in df.columns:
        raise ValueError(f"Column '{smiles_column}' not found in input file.")

    logger.info(f"Processing {len(df)} molecules for baseline SASA prediction...")

    sasa_predictions = []
    failures = 0

    for idx, row in df.iterrows():
        smiles = row[smiles_column]
        sasa = calculate_sasa_from_smiles(smiles)

        if sasa is not None:
            sasa_predictions.append(sasa)
        else:
            sasa_predictions.append(np.nan)
            failures += 1

        if (idx + 1) % 100 == 0:
            logger.debug(f"Processed {idx + 1} molecules...")

    failure_rate = failures / len(df)
    logger.info(f"Baseline processing complete. Failure rate: {failure_rate:.2%}")

    if failure_rate > 0.10:
        logger.error(f"Critical: Baseline failure rate ({failure_rate:.2%}) exceeds 10% threshold.")
        # We do not halt here, but log the error. The evaluation step will handle the NaNs.

    # Create output dataframe
    df_out = df.copy()
    df_out["baseline_sasa"] = sasa_predictions

    # Calculate error metrics against ground truth if available
    if target_column in df.columns:
        valid_mask = df_out[target_column].notna() & df_out["baseline_sasa"].notna()
        y_true = df_out.loc[valid_mask, target_column]
        y_pred = df_out.loc[valid_mask, "baseline_sasa"]

        mae = np.mean(np.abs(y_true - y_pred))
        rmse = np.sqrt(np.mean((y_true - y_pred) ** 2))
        r2 = 1 - (np.sum((y_true - y_pred) ** 2) / np.sum((y_true - y_true.mean()) ** 2))

        stats = {
            "total_molecules": len(df),
            "successful_predictions": len(df) - failures,
            "failure_rate": failure_rate,
            "mae": float(mae),
            "rmse": float(rmse),
            "r2": float(r2)
        }
        logger.info(f"Baseline Statistics (vs Ground Truth): MAE={mae:.4f}, RMSE={rmse:.4f}, R2={r2:.4f}")
    else:
        stats = {
            "total_molecules": len(df),
            "successful_predictions": len(df) - failures,
            "failure_rate": failure_rate,
            "mae": None,
            "rmse": None,
            "r2": None
        }

    # Save output
    output_path_obj = Path(output_path)
    output_path_obj.parent.mkdir(parents=True, exist_ok=True)
    df_out.to_csv(output_path, index=False)
    logger.info(f"Predictions saved to {output_path}")

    return stats

def main():
    """
    Entry point for the baseline script.
    Expects input data in data/processed/ and saves results to data/processed/.
    """
    project_root = get_project_root()
    data_dir = get_data_dir()

    # Default paths
    input_file = data_dir / "processed" / "processed_molecules.csv"
    output_file = data_dir / "processed" / "baseline_predictions.csv"

    # Allow override via command line
    if len(sys.argv) > 1:
        input_file = Path(sys.argv[1])
    if len(sys.argv) > 2:
        output_file = Path(sys.argv[2])

    if not input_file.exists():
        logger.error(f"Input file not found: {input_file}")
        sys.exit(1)

    stats = predict_baseline_sasa(
        input_path=str(input_file),
        output_path=str(output_file),
        smiles_column="smiles",
        target_column="sasa"
    )

    # Save stats to a JSON file for downstream evaluation
    stats_path = output_file.replace(".csv", "_stats.json")
    with open(stats_path, "w") as f:
        json.dump(stats, f, indent=2)

    logger.info(f"Baseline run complete. Stats saved to {stats_path}")

if __name__ == "__main__":
    main()
