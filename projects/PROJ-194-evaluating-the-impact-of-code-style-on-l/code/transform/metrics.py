import json
import os
from typing import List, Dict, Any, Optional
from datetime import datetime
from transform.validator import validate_code_variants, filter_valid_variants

RESULTS_DIR = "results"
TRANSFORMATION_METRICS_FILE = os.path.join(RESULTS_DIR, "transformation_success_rate.json")

class TransformationMetrics:
    """
    Aggregates validation results and calculates transformation success metrics.
    Implements T012a, T012b, and T012c.
    """

    def __init__(self):
        self.metrics: Dict[str, Any] = {
            "total_variants": 0,
            "valid_variants": 0,
            "invalid_variants": 0,
            "success_rate": 0.0,
            "timestamp": None,
            "details": {}
        }

    def aggregate(self, variants: List[Dict[str, Any]], validation_results: Optional[Dict[str, bool]] = None) -> Dict[str, Any]:
        """
        Aggregates validation results from T008 (validator) and T012a logic.
        Calculates the proportion of successfully generated and syntactically valid style variants (T012b).
        
        Args:
            variants: List of variant dictionaries generated by generator.py.
            validation_results: Optional pre-computed dict mapping variant_id -> is_valid.
                                If None, runs validate_code_variants internally.
        
        Returns:
            Dict containing the aggregated metrics.
        """
        if not variants:
            self.metrics["total_variants"] = 0
            self.metrics["valid_variants"] = 0
            self.metrics["invalid_variants"] = 0
            self.metrics["success_rate"] = 0.0
            return self.metrics

        self.metrics["timestamp"] = datetime.now().isoformat()
        self.metrics["total_variants"] = len(variants)

        if validation_results is None:
            # Run validation if not provided
            validation_results = validate_code_variants(variants)

        valid_count = 0
        invalid_count = 0
        details = {}

        for variant in variants:
          variant_id = variant.get("id", "unknown")
          is_valid = validation_results.get(variant_id, False)
          
          if is_valid:
              valid_count += 1
              details[variant_id] = {"status": "valid"}
          else:
              invalid_count += 1
              details[variant_id] = {"status": "invalid"}

        self.metrics["valid_variants"] = valid_count
        self.metrics["invalid_variants"] = invalid_count
        
        if self.metrics["total_variants"] > 0:
            self.metrics["success_rate"] = round(valid_count / self.metrics["total_variants"], 4)
        else:
            self.metrics["success_rate"] = 0.0

        self.metrics["details"] = details
        return self.metrics

    def save_to_json(self, output_path: Optional[str] = None) -> str:
        """
        Implements T012c: Records the metric to results/transformation_success_rate.json.
        
        Args:
            output_path: Optional path to save the file. Defaults to results/transformation_success_rate.json.
        
        Returns:
            The path where the file was saved.
        
        Raises:
            FileNotFoundError: If the results directory does not exist.
        """
        if output_path is None:
            output_path = TRANSFORMATION_METRICS_FILE

        output_dir = os.path.dirname(output_path)
        if not os.path.exists(output_dir):
            raise FileNotFoundError(f"Results directory '{output_dir}' does not exist. Please run setup first.")

        with open(output_path, 'w', encoding='utf-8') as f:
            json.dump(self.metrics, f, indent=2)

        return output_path


def run_transformation_metrics(variants: List[Dict[str, Any]], output_path: Optional[str] = None) -> Dict[str, Any]:
    """
    Convenience function to aggregate metrics and save to JSON in one step.
    Corresponds to T012a, T012b, and T012c execution flow.
    """
    metric_collector = TransformationMetrics()
    metric_collector.aggregate(variants)
    saved_path = metric_collector.save_to_json(output_path)
    return metric_collector.metrics


def main():
    """
    Entry point for running metrics aggregation if called as a script.
    Expects variants to be passed via a JSON file or stdin, or uses a mock for testing.
    For T012c, this demonstrates the saving logic to results/transformation_success_rate.json.
    """
    # Check if results directory exists, create if missing (defensive)
    os.makedirs(RESULTS_DIR, exist_ok=True)

    # Mock data for demonstration if no arguments provided
    # In a real pipeline, this would receive the output from generator.py
    mock_variants = [
        {"id": "func_1_style_1", "code": "def x(): pass", "is_valid": True},
        {"id": "func_1_style_2", "code": "def y(): pass", "is_valid": True},
        {"id": "func_1_style_3", "code": "invalid syntax here", "is_valid": False},
    ]

    print("Running transformation metrics aggregation...")
    results = run_transformation_metrics(mock_variants)
    
    print(f"Total variants: {results['total_variants']}")
    print(f"Valid variants: {results['valid_variants']}")
    print(f"Success rate: {results['success_rate']}")
    print(f"Saved to: {TRANSFORMATION_METRICS_FILE}")

    # Verify file exists
    if os.path.exists(TRANSFORMATION_METRICS_FILE):
        print("Verification: Output file successfully written.")
    else:
        print("Error: Output file was not written.")

if __name__ == "__main__":
    main()