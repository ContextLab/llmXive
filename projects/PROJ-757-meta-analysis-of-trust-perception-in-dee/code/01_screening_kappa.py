            # It does not explicitly say what to do if Kappa >= 0.6 but disputes exist.
            # Usually, high Kappa implies few disputes. We will not generate the file 
            # if Kappa is acceptable, assuming disputes are resolved or negligible.
            # If the logic requires listing ALL disputes regardless of Kappa, 
            # the condition would be different. Based on strict reading:
            # "if Kappa < 0.6, log..., generate..., and exit"
            # implies if >= 0.6, we do not do these specific actions.
            
            # Clean up any previous adjudication file if we are proceeding?
