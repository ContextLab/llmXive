"""
Contract test for model output schema (US2).

This test validates that the model output files generated by the analysis pipeline
conform to the schema defined in `contracts/model-output.schema.yaml`.

It ensures:
1. The output file exists at the expected path.
2. The file is valid JSON.
3. All required top-level keys are present.
4. The structure of the 'results' list matches the schema (fixed effects, SE, p-values).
5. Data types are correct (floats for estimates, strings for labels).
6. No causal language is used in metadata (associational framing).
"""
import json
import os
import pytest
import yaml
from pathlib import Path
from typing import Any, Dict, List

# Path configuration relative to project root
PROJECT_ROOT = Path(__file__).parent.parent.parent
CONTRACTS_DIR = PROJECT_ROOT / "contracts"
SCHEMA_PATH = CONTRACTS_DIR / "model-output.schema.yaml"
OUTPUT_DIR = PROJECT_ROOT / "results" / "models"

# Expected output filenames based on T025
EXPECTED_FILES = [
    "recall_logistic_results.json",
    "bizarreness_linear_results.json",
    "ordinal_approx_results.json"
]

@pytest.fixture
def schema() -> Dict[str, Any]:
    """Load the model output schema definition."""
    if not SCHEMA_PATH.exists():
        pytest.fail(f"Schema file not found: {SCHEMA_PATH}")
    with open(SCHEMA_PATH, "r") as f:
        return yaml.safe_load(f)

@pytest.fixture
def model_outputs() -> List[Dict[str, Any]]:
    """Load all model output JSON files."""
    outputs = []
    if not OUTPUT_DIR.exists():
        pytest.fail(f"Output directory not found: {OUTPUT_DIR}")
    
    for filename in EXPECTED_FILES:
        filepath = OUTPUT_DIR / filename
        if not filepath.exists():
            pytest.fail(f"Expected model output file not found: {filepath}")
        
        with open(filepath, "r") as f:
            try:
                data = json.load(f)
                outputs.append(data)
            except json.JSONDecodeError as e:
                pytest.fail(f"Invalid JSON in {filepath}: {e}")
    
    return outputs

def test_schema_exists(schema: Dict[str, Any]):
    """Verify the schema file is valid and contains required definitions."""
    assert "type" in schema, "Schema must define a type"
    assert "properties" in schema, "Schema must define properties"
    assert "required" in schema, "Schema must define required fields"

def test_output_files_exist():
    """Verify all expected output files are present."""
    missing = [f for f in EXPECTED_FILES if not (OUTPUT_DIR / f).exists()]
    assert not missing, f"Missing expected output files: {missing}"

def test_json_validity(model_outputs: List[Dict[str, Any]]):
    """Verify all outputs are valid JSON objects."""
    assert len(model_outputs) > 0, "No model outputs were loaded"
    for i, output in enumerate(model_outputs):
        assert isinstance(output, dict), f"Output {i} must be a dictionary"

def test_top_level_keys(model_outputs: List[Dict[str, Any]], schema: Dict[str, Any]):
    """Verify required top-level keys exist in all outputs."""
    required_keys = set(schema.get("required", []))
    schema_props = set(schema.get("properties", {}).keys())
    
    for i, output in enumerate(model_outputs):
        missing_keys = required_keys - set(output.keys())
        assert not missing_keys, f"Output {i} missing required keys: {missing_keys}"
        
        # Verify keys match schema definition
        extra_keys = set(output.keys()) - schema_props
        # Allow extra keys but warn if they are unexpected (optional strict check)
        if extra_keys:
            pytest.fail(f"Output {i} contains unexpected keys: {extra_keys}")

def test_results_structure(model_outputs: List[Dict[str, Any]], schema: Dict[str, Any]):
    """Verify the 'results' list contains valid model parameter objects."""
    results_schema = schema.get("properties", {}).get("results", {})
    item_schema = results_schema.get("items", {})
    
    for i, output in enumerate(model_outputs):
        results = output.get("results", [])
        assert isinstance(results, list), f"Output {i} 'results' must be a list"
        assert len(results) > 0, f"Output {i} 'results' list cannot be empty"
        
        for j, item in enumerate(results):
            assert isinstance(item, dict), f"Output {i}, result {j} must be a dict"
            
            # Check required fields within a result item
            item_required = item_schema.get("required", [])
            missing_item_keys = set(item_required) - set(item.keys())
            assert not missing_item_keys, f"Result {i}-{j} missing keys: {missing_item_keys}"

def test_data_types(model_outputs: List[Dict[str, Any]], schema: Dict[str, Any]):
    """Verify data types match the schema (floats for stats, strings for labels)."""
    results_schema = schema.get("properties", {}).get("results", {})
    item_props = results_schema.get("items", {}).get("properties", {})
    
    for i, output in enumerate(model_outputs):
        for j, item in enumerate(output.get("results", [])):
            for key, prop in item_props.items():
                if key not in item:
                    continue
                
                val = item[key]
                expected_type = prop.get("type")
                
                if expected_type == "number":
                    assert isinstance(val, (int, float)), f"Output {i}, result {j}, key '{key}' must be a number, got {type(val)}"
                elif expected_type == "string":
                    assert isinstance(val, str), f"Output {i}, result {j}, key '{key}' must be a string, got {type(val)}"
                elif expected_type == "integer":
                    assert isinstance(val, int), f"Output {i}, result {j}, key '{key}' must be an integer, got {type(val)}"

def test_associational_framing(model_outputs: List[Dict[str, Any]]):
    """Verify metadata explicitly frames findings as associational."""
    for i, output in enumerate(model_outputs):
        metadata = output.get("metadata", {})
        description = metadata.get("description", "")
        notes = metadata.get("notes", "")
        
        # Check for forbidden causal language and required associational language
        forbidden = ["causes", "causal", "proves", "demonstrates that"]
        required = ["associational", "correlation", "association", "simulation-based"]
        
        text_to_check = (description + " " + notes).lower()
        
        for word in forbidden:
            assert word not in text_to_check, f"Output {i} contains forbidden causal language: '{word}'"
        
        # Ensure at least one associational marker is present
        has_associational = any(req in text_to_check for req in required)
        assert has_associational, f"Output {i} metadata must explicitly frame findings as associational."

def test_simulation_flag(model_outputs: List[Dict[str, Any]]):
    """Verify simulation-based results are flagged."""
    for i, output in enumerate(model_outputs):
        metadata = output.get("metadata", {})
        is_simulation = metadata.get("is_simulation", False)
        
        assert is_simulation is True, f"Output {i} must have 'is_simulation' set to True for synthetic data runs."

def test_schema_compliance_with_yaml(model_outputs: List[Dict[str, Any]], schema: Dict[str, Any]):
    """
    High-level check ensuring the structure of the JSON matches the YAML schema definition.
    This is a sanity check to ensure the contract is actually being enforced.
    """
    # This test re-iterates the structure check but ensures the schema itself is being used correctly
    # If the schema says 'results' is a list of objects with 'estimate', we check that.
    results_def = schema["properties"]["results"]
    assert results_def["type"] == "array"
    
    item_def = results_def["items"]
    assert "properties" in item_def
    assert "estimate" in item_def["properties"]
    
    for output in model_outputs:
        for item in output["results"]:
            assert "estimate" in item
            assert isinstance(item["estimate"], (int, float))