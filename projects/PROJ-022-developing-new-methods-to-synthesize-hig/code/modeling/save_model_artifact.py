"""
T024: Save trained model artifact to artifacts/model.pkl

This script loads the trained Random Forest model generated by train_model.py,
adds required metadata (including the FR-007 associational disclaimer), and
serializes it to artifacts/model.pkl.
"""
import os
import sys
import json
import pickle
import logging
from pathlib import Path
from datetime import datetime

# Add project root to path for imports
project_root = Path(__file__).resolve().parents[2]
sys.path.insert(0, str(project_root))

from utils.logging_config import get_logger
from utils.errors import PipelineError

# Configure logger
logger = get_logger(__name__)

# Constants based on project structure
MODEL_INPUT_PATH = project_root / "artifacts" / "model_temp.pkl"
MODEL_OUTPUT_PATH = project_root / "artifacts" / "model.pkl"
METADATA_PATH = project_root / "artifacts" / "model_metadata.json"

# FR-007: Explicit disclaimer that findings are associational, not causal
MODEL_DISCLAIMER = (
    "WARNING: This model predicts performance based on statistical associations "
    "derived from literature data. The relationships identified are correlational, "
    "not causal. Experimental validation is required before any synthesis decisions."
)

def load_trained_model(input_path: Path) -> dict:
    """
    Load the temporary model artifact generated by train_model.py.
    
    Args:
        input_path: Path to the temporary model pickle file.
        
    Returns:
        Dictionary containing the model and training metadata.
        
    Raises:
        PipelineError: If the model file does not exist or cannot be loaded.
    """
    if not input_path.exists():
        raise PipelineError(f"Trained model not found at {input_path}. "
                            "Please run train_model.py first.")
    
    try:
        with open(input_path, 'rb') as f:
            model_data = pickle.load(f)
        logger.info(f"Successfully loaded model from {input_path}")
        return model_data
    except Exception as e:
        raise PipelineError(f"Failed to load model from {input_path}: {e}")

def add_metadata(model_data: dict) -> dict:
    """
    Add metadata to the model dictionary, including the FR-007 disclaimer.
    
    Args:
        model_data: Dictionary containing the model and existing metadata.
        
    Returns:
        Updated dictionary with added metadata.
    """
    if "metadata" not in model_data:
        model_data["metadata"] = {}
    
    model_data["metadata"]["disclaimer"] = MODEL_DISCLAIMER
    model_data["metadata"]["saved_timestamp"] = datetime.now().isoformat()
    model_data["metadata"]["artifact_version"] = "1.0"
    model_data["metadata"]["task_id"] = "T024"
    
    logger.info("Added metadata including FR-007 disclaimer")
    return model_data

def save_final_model(model_data: dict, output_path: Path, metadata_path: Path) -> None:
    """
    Save the final model artifact and metadata to disk.
    
    Args:
        model_data: Dictionary containing the model and metadata.
        output_path: Path where the final model.pkl will be saved.
        metadata_path: Path where the metadata JSON will be saved.
    """
    # Ensure output directory exists
    output_path.parent.mkdir(parents=True, exist_ok=True)
    
    # Save the main model artifact
    with open(output_path, 'wb') as f:
        pickle.dump(model_data, f)
    logger.info(f"Saved final model artifact to {output_path}")
    
    # Save metadata separately for easy inspection
    with open(metadata_path, 'w') as f:
        json.dump(model_data.get("metadata", {}), f, indent=2)
    logger.info(f"Saved metadata to {metadata_path}")

def main():
    """Main entry point for saving the model artifact."""
    try:
        logger.info("Starting model artifact save process (T024)")
        
        # Load the trained model
        model_data = load_trained_model(MODEL_INPUT_PATH)
        
        # Add metadata including disclaimer
        model_data = add_metadata(model_data)
        
        # Save final artifacts
        save_final_model(model_data, MODEL_OUTPUT_PATH, METADATA_PATH)
        
        logger.info("Model artifact save completed successfully")
        print(f"Model saved to: {MODEL_OUTPUT_PATH}")
        print(f"Metadata saved to: {METADATA_PATH}")
        
    except PipelineError as e:
        logger.error(f"Pipeline error: {e}")
        sys.exit(1)
    except Exception as e:
        logger.error(f"Unexpected error: {e}")
        sys.exit(1)

if __name__ == "__main__":
    main()
