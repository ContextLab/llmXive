"""
Integration test for T038: FR-004 Verification.

Verifies that the validator correctly flags inconsistencies based on:
1. Absolute p-value difference > 0.05
2. Relative effect size difference > 5%
3. Sample size mismatch (exclusion from prevalence)

This test uses the real synthetic dataset generated by T026 to ensure
the validator thresholds are correctly applied to real data artifacts.
"""
import json
import csv
import os
import sys
import logging
from pathlib import Path
from typing import List, Dict, Any

# Add project root to path for imports
project_root = Path(__file__).parent.parent.parent
sys.path.insert(0, str(project_root / "code"))

from src.audit.validator import (
    calculate_absolute_p_difference,
    calculate_relative_effect_size_difference,
    detect_sample_size_mismatch,
    check_p_value_consistency,
    check_effect_size_consistency,
    create_audit_record,
    validate_summary,
    validate_all_summaries,
    write_audit_report,
    filter_for_prevalence
)
from src.models.data_models import ABTestSummary, AuditRecord
from src.utils.logger import get_default_logger

logger = get_default_logger(__name__)

# Thresholds defined in FR-004
THRESHOLD_ABSOLUTE_P = 0.05
THRESHOLD_RELATIVE_EFFECT = 0.05  # 5%

def load_synthetic_summaries(path: Path) -> List[Dict[str, Any]]:
    """Load the synthetic dataset generated by T026."""
    if not path.exists():
        raise FileNotFoundError(f"Synthetic dataset not found at {path}. "
                                "Ensure T026 has been executed successfully.")
    records = []
    with open(path, 'r', encoding='utf-8') as f:
        reader = csv.DictReader(f)
        for row in reader:
            # Convert numeric fields
            for key in ['n_control', 'n_treatment', 'successes_control', 'successes_treatment']:
                if key in row and row[key]:
                    row[key] = int(row[key])
            for key in ['p_value_reported', 'effect_size_reported']:
                if key in row and row[key]:
                    row[key] = float(row[key])
            records.append(row)
    return records

def test_absolute_p_difference_calculation():
    """Test that absolute p-difference is calculated correctly."""
    # Case 1: Consistent p-values
    p_reported = 0.04
    p_reconstructed = 0.042
    diff = calculate_absolute_p_difference(p_reported, p_reconstructed)
    assert abs(diff - 0.002) < 1e-9, f"Expected 0.002, got {diff}"
    
    # Case 2: Inconsistent p-values (threshold violation)
    p_reported = 0.04
    p_reconstructed = 0.10
    diff = calculate_absolute_p_difference(p_reported, p_reconstructed)
    assert abs(diff - 0.06) < 1e-9, f"Expected 0.06, got {diff}"
    
    logger.info("Absolute p-difference calculation test passed.")

def test_relative_effect_size_calculation():
    """Test that relative effect size difference is calculated correctly."""
    # Case 1: Consistent effect sizes
    eff_reported = 0.10
    eff_reconstructed = 0.105
    diff = calculate_relative_effect_size_difference(eff_reported, eff_reconstructed)
    # Relative diff = |10 - 10.5| / 10 = 0.05
    assert abs(diff - 0.05) < 1e-9, f"Expected 0.05, got {diff}"
    
    # Case 2: Inconsistent effect sizes (threshold violation)
    eff_reported = 0.10
    eff_reconstructed = 0.16
    diff = calculate_relative_effect_size_difference(eff_reported, eff_reconstructed)
    # Relative diff = |10 - 16| / 10 = 0.60
    assert abs(diff - 0.60) < 1e-9, f"Expected 0.60, got {diff}"
    
    logger.info("Relative effect size calculation test passed.")

def test_sample_size_mismatch_detection():
    """Test that sample size mismatches are detected."""
    # Case 1: Matching sample sizes
    n_control = 1000
    n_treatment = 1000
    n_control_reported = 1000
    n_treatment_reported = 1000
    assert not detect_sample_size_mismatch(n_control, n_treatment, n_control_reported, n_treatment_reported)
    
    # Case 2: Mismatch in control
    n_control_reported = 950
    assert detect_sample_size_mismatch(n_control, n_treatment, n_control_reported, n_treatment_reported)
    
    # Case 3: Mismatch in treatment
    n_control_reported = 1000
    n_treatment_reported = 1050
    assert detect_sample_size_mismatch(n_control, n_treatment, n_control_reported, n_treatment_reported)
    
    logger.info("Sample size mismatch detection test passed.")

def test_validator_thresholds_on_synthetic_data():
    """
    Main integration test: Run the validator on the synthetic dataset and verify
    that flags correspond to the defined thresholds.
    """
    synthetic_path = project_root / "data" / "synthetic" / "synthetic_validation.csv"
    
    logger.info(f"Loading synthetic data from {synthetic_path}")
    summaries_raw = load_synthetic_summaries(synthetic_path)
    
    if not summaries_raw:
        raise ValueError("No synthetic data loaded. Cannot proceed with validation.")
    
    logger.info(f"Loaded {len(summaries_raw)} synthetic summaries.")
    
    audit_records = []
    inconsistent_count = 0
    sample_size_mismatch_count = 0
    
    for i, summary_raw in enumerate(summaries_raw):
        # Convert to ABTestSummary object
        summary = ABTestSummary(
            url=summary_raw.get('url', f'test_url_{i}'),
            domain=summary_raw.get('domain', 'test_domain'),
            n_control=summary_raw['n_control'],
            n_treatment=summary_raw['n_treatment'],
            successes_control=summary_raw['successes_control'],
            successes_treatment=summary_raw['successes_treatment'],
            p_value_reported=summary_raw['p_value_reported'],
            effect_size_reported=summary_raw['effect_size_reported'],
            n_control_reported=summary_raw.get('n_control_reported', summary_raw['n_control']),
            n_treatment_reported=summary_raw.get('n_treatment_reported', summary_raw['n_treatment'])
        )
        
        # Run validation
        audit_record = validate_summary(summary)
        audit_records.append(audit_record)
        
        if audit_record.is_inconsistent:
            inconsistent_count += 1
            # Verify that the flag is due to a specific threshold
            if audit_record.data_quality_warning and "sample-size" in audit_record.data_quality_warning.lower():
                sample_size_mismatch_count += 1
            else:
                # Must be p-value or effect size inconsistency
                assert audit_record.absolute_p_difference > THRESHOLD_ABSOLUTE_P or \
                       audit_record.relative_effect_size_difference > THRESHOLD_RELATIVE_EFFECT, \
                       f"Inconsistent record {i} has no specific threshold violation. " \
                       f"p_diff={audit_record.absolute_p_difference}, eff_diff={audit_record.relative_effect_size_difference}"
        
        # If there's a sample size mismatch, it should be flagged
        if audit_record.data_quality_warning and "sample-size" in audit_record.data_quality_warning.lower():
            assert detect_sample_size_mismatch(
                summary.n_control, summary.n_treatment,
                summary.n_control_reported, summary.n_treatment_reported
            ), "Flagged for sample size mismatch but detection function returned False"
    
    logger.info(f"Validation complete. Inconsistent: {inconsistent_count}, Sample size mismatches: {sample_size_mismatch_count}")
    
    # Write the audit report to disk (required for T038 verification)
    output_path = project_root / "output" / "audit_report.json"
    output_path.parent.mkdir(parents=True, exist_ok=True)
    write_audit_report(audit_records, output_path)
    logger.info(f"Audit report written to {output_path}")
    
    # Verify that the output file exists and is valid JSON
    assert output_path.exists(), "Audit report file was not created."
    with open(output_path, 'r') as f:
        loaded_records = json.load(f)
    assert len(loaded_records) == len(audit_records), "Audit report record count mismatch."
    
    # Final assertion: We expect some inconsistencies in the synthetic data
    # (unless the synthetic generator was perfectly consistent, which is unlikely)
    # The key is that the thresholds are respected.
    logger.info("Integration test passed: Validator thresholds correctly applied.")

def main():
    """Entry point for the integration test."""
    logging.basicConfig(level=logging.INFO, format='%(asctime)s - %(name)s - %(levelname)s - %(message)s')
    
    try:
        test_absolute_p_difference_calculation()
        test_relative_effect_size_calculation()
        test_sample_size_mismatch_detection()
        test_validator_thresholds_on_synthetic_data()
        
        print("\n✅ T038 VERIFICATION PASSED: All validator thresholds are correctly implemented and applied.")
        return 0
    except Exception as e:
        logger.error(f"❌ T038 VERIFICATION FAILED: {str(e)}")
        raise

if __name__ == "__main__":
    sys.exit(main())