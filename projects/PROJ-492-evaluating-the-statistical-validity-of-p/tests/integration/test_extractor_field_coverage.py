"""
Integration test for FR-002 Verification: Extracted fields exist for > 95% of valid pages.

This test verifies that the extraction module successfully populates required fields
in the ABTestSummary model for at least 95% of valid pages in the dataset.

Requirements:
- Loads extracted summaries from data/extracted_summaries.json (generated by T020)
- Defines the set of required fields based on ABTestSummary schema
- Calculates the percentage of summaries where all required fields are present and non-null
- Asserts coverage >= 95%
- Logs detailed failure information if threshold is not met
"""
import json
import logging
import sys
from pathlib import Path
from typing import Dict, Any, Set, List, Tuple
from pydantic import BaseModel, ValidationError

# Import the data model to get field definitions
# Assuming the model is defined in src/models/data_models.py as per T006
try:
    from code.src.models.data_models import ABTestSummary
except ImportError:
    # Fallback if path structure differs in execution context
    sys.path.insert(0, str(Path(__file__).parent.parent.parent))
    from code.src.models.data_models import ABTestSummary

from code.src.utils.logger import get_default_logger

# Configure logger
logger = get_default_logger("T036_EXTRACTOR_COVERAGE")

REQUIRED_FIELDS: Set[str] = {
    "url",
    "domain",
    "n_control",
    "n_treatment",
    "p_control",
    "p_treatment",
    "p_value_reported",
    "outcome_type",
    "test_type"
}

def load_extracted_summaries(path: Path) -> List[Dict[str, Any]]:
    """Load the extracted summaries JSON file."""
    if not path.exists():
        raise FileNotFoundError(f"Extracted summaries file not found: {path}")
    
    with open(path, 'r', encoding='utf-8') as f:
        data = json.load(f)
    
    # Handle both list format and dict with 'summaries' key
    if isinstance(data, list):
        return data
    elif isinstance(data, dict) and 'summaries' in data:
        return data['summaries']
    else:
        raise ValueError("Unexpected JSON structure in extracted summaries")

def get_required_fields(model_class: type) -> Set[str]:
    """Extract field names from Pydantic model."""
    return set(model_class.model_fields.keys())

def calculate_field_coverage(
    summaries: List[Dict[str, Any]], 
    required_fields: Set[str]
) -> Tuple[float, List[Dict[str, Any]]]:
    """
    Calculate the percentage of summaries with all required fields present.
    
    Returns:
        Tuple of (coverage_percentage, list of failed records)
    """
    total_count = len(summaries)
    if total_count == 0:
        return 0.0, []

    valid_count = 0
    failed_records = []

    for idx, record in enumerate(summaries):
        missing_fields = []
        
        for field in required_fields:
            value = record.get(field)
            # Check if field exists and is not None/empty
            if value is None or (isinstance(value, str) and value.strip() == ""):
                missing_fields.append(field)
        
        if not missing_fields:
            valid_count += 1
        else:
            failed_records.append({
                "index": idx,
                "url": record.get("url", "UNKNOWN"),
                "missing_fields": missing_fields
            })

    coverage = (valid_count / total_count) * 100.0
    return coverage, failed_records

def run_verification() -> bool:
    """
    Main verification logic for FR-002.
    
    Returns:
        True if coverage >= 95%, False otherwise
    """
    # Determine paths relative to project root
    project_root = Path(__file__).parent.parent.parent
    summaries_path = project_root / "data" / "extracted_summaries.json"
    
    logger.info("Starting FR-002 Field Coverage Verification")
    logger.info(f"Loading summaries from: {summaries_path}")
    
    try:
        summaries = load_extracted_summaries(summaries_path)
        logger.info(f"Loaded {len(summaries)} summaries")
    except (FileNotFoundError, json.JSONDecodeError, ValueError) as e:
        logger.error(f"Failed to load summaries: {e}")
        # If no data exists, we cannot verify. 
        # In a real CI, this might be a failure or skip depending on pipeline stage.
        # For this task, we assume the data pipeline (T020) has run.
        raise RuntimeError("Extraction data not found. Ensure T020 has completed.") from e

    # Get required fields from the model
    required_fields = get_required_fields(ABTestSummary)
    logger.info(f"Checking coverage for {len(required_fields)} required fields: {required_fields}")
    
    coverage, failed_records = calculate_field_coverage(summaries, required_fields)
    
    logger.info(f"Field Coverage Result: {coverage:.2f}%")
    logger.info(f"Valid records: {len(summaries) - len(failed_records)} / {len(summaries)}")
    
    if failed_records:
        logger.warning(f"Found {len(failed_records)} records with missing fields:")
        # Log first 5 failures for debugging
        for record in failed_records[:5]:
            logger.warning(f"  Record {record['index']} ({record['url']}): missing {record['missing_fields']}")
        if len(failed_records) > 5:
            logger.warning(f"  ... and {len(failed_records) - 5} more")

    threshold = 95.0
    passed = coverage >= threshold
    
    if passed:
        logger.info(f"SUCCESS: Coverage {coverage:.2f}% meets threshold of {threshold}%")
    else:
        logger.error(f"FAILURE: Coverage {coverage:.2f}% is below threshold of {threshold}%")
    
    return passed

def main() -> int:
    """Entry point for the script."""
    try:
        if run_verification():
            return 0
        else:
            return 1
    except Exception as e:
        logger.error(f"Verification failed with exception: {e}")
        return 2

if __name__ == "__main__":
    sys.exit(main())
