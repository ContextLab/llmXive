"""
Integration test for T029: Evaluate inconsistency-detection component on synthetic validation dataset.

This test runs the evaluation pipeline on the synthetic dataset generated by T026.
It computes precision, recall, and F1 score for the inconsistency detection component.
It asserts that precision >= 90% and recall >= 80% as per FR-031.
If thresholds are not met, it raises ERR-800.
"""
import json
import logging
import sys
from pathlib import Path
from typing import Dict, Any, Tuple

# Add project root to path if running as script
if str(Path(__file__).parent.parent.parent) not in sys.path:
    sys.path.insert(0, str(Path(__file__).parent.parent.parent))

from code.src.audit.evaluation import (
    load_synthetic_summaries,
    load_ground_truth,
    load_audit_records,
    evaluate_detection,
    write_evaluation_results
)
from code.src.utils.logger import get_default_logger, get_error_message

# Constants
PRECISION_THRESHOLD = 0.90
RECALL_THRESHOLD = 0.80
SYNTHETIC_DATA_DIR = Path("data/synthetic")
OUTPUT_DIR = Path("output")
EVALUATION_RESULTS_FILE = OUTPUT_DIR / "synthetic_evaluation_results.json"

logger = get_default_logger(__name__)

def run_synthetic_validation() -> Tuple[bool, Dict[str, Any]]:
    """
    Run the synthetic validation evaluation.
    
    Returns:
        Tuple of (success_flag, metrics_dict)
        success_flag is False if precision < 90% or recall < 80% (raises ERR-800)
    """
    try:
        # Ensure output directory exists
        OUTPUT_DIR.mkdir(parents=True, exist_ok=True)
        
        # Load synthetic data
        logger.info("Loading synthetic summaries...")
        synthetic_summaries = load_synthetic_summaries(
            SYNTHETIC_DATA_DIR / "synthetic_summaries.json"
        )
        
        logger.info("Loading ground truth...")
        ground_truth = load_ground_truth(
            SYNTHETIC_DATA_DIR / "ground_truth.json"
        )
        
        # Run the audit pipeline on synthetic data to generate audit records
        # This simulates running the full pipeline on the synthetic dataset
        logger.info("Running audit pipeline on synthetic data...")
        
        # We need to reconstruct the audit process to get audit records
        # Since we have synthetic summaries and ground truth, we can simulate
        # the reconstruction and validation steps
        
        from code.src.audit.reconstructor import reconstruct_all
        from code.src.audit.validator import validate_all
        
        # Reconstruct statistical tests for all synthetic summaries
        reconstructed_records = reconstruct_all(synthetic_summaries)
        
        # Validate inconsistencies
        audit_records = validate_all(reconstructed_records)
        
        # Evaluate detection performance
        logger.info("Evaluating detection performance...")
        metrics = evaluate_detection(audit_records, ground_truth)
        
        # Write results
        write_evaluation_results(metrics, EVALUATION_RESULTS_FILE)
        
        logger.info(f"Evaluation results written to {EVALUATION_RESULTS_FILE}")
        
        # Check thresholds
        precision = metrics.get('precision', 0.0)
        recall = metrics.get('recall', 0.0)
        f1 = metrics.get('f1_score', 0.0)
        
        logger.info(f"Precision: {precision:.4f}, Recall: {recall:.4f}, F1: {f1:.4f}")
        
        if precision < PRECISION_THRESHOLD:
            error_code = "ERR-800"
            error_msg = get_error_message(error_code)
            logger.error(f"{error_code}: Precision {precision:.4f} is below threshold {PRECISION_THRESHOLD}")
            raise RuntimeError(f"{error_code}: {error_msg} - Precision {precision:.4f} < {PRECISION_THRESHOLD}")
        
        if recall < RECALL_THRESHOLD:
            error_code = "ERR-800"
            error_msg = get_error_message(error_code)
            logger.error(f"{error_code}: Recall {recall:.4f} is below threshold {RECALL_THRESHOLD}")
            raise RuntimeError(f"{error_code}: {error_msg} - Recall {recall:.4f} < {RECALL_THRESHOLD}")
        
        logger.info("Synthetic validation passed: precision >= 90% and recall >= 80%")
        return True, metrics
        
    except Exception as e:
        logger.error(f"Synthetic validation failed: {str(e)}")
        raise

def main():
    """Main entry point for the synthetic validation test."""
    try:
        success, metrics = run_synthetic_validation()
        if success:
            print("✓ Synthetic validation test PASSED")
            print(f"  Precision: {metrics['precision']:.4f} (threshold: >= {PRECISION_THRESHOLD})")
            print(f"  Recall: {metrics['recall']:.4f} (threshold: >= {RECALL_THRESHOLD})")
            print(f"  F1 Score: {metrics['f1_score']:.4f}")
            sys.exit(0)
        else:
            print("✗ Synthetic validation test FAILED")
            sys.exit(1)
    except Exception as e:
        print(f"✗ Synthetic validation test FAILED with exception: {str(e)}")
        sys.exit(1)

if __name__ == "__main__":
    main()