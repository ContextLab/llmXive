"""
Test for T042b: Verify that prevalence.json does not contain entries flagged for sample-size mismatch.

This test loads the audit report generated by T025/T025c, identifies records
flagged with data_quality_warning for sample-size mismatch, and verifies that
these records are NOT present in the prevalence.json output.
"""
import json
import csv
import logging
from pathlib import Path
from typing import List, Dict, Any, Set

# Configure logging for the test
logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def load_audit_records(path: Path) -> List[Dict[str, Any]]:
    """Load audit records from the audit report JSON."""
    if not path.exists():
        raise FileNotFoundError(f"Audit report not found at {path}")
    with open(path, 'r', encoding='utf-8') as f:
        return json.load(f)

def load_prevalence_records(path: Path) -> List[Dict[str, Any]]:
    """Load prevalence records from the prevalence JSON."""
    if not path.exists():
        raise FileNotFoundError(f"Prevalence report not found at {path}")
    with open(path, 'r', encoding='utf-8') as f:
        return json.load(f)

def get_sample_size_mismatch_ids(records: List[Dict[str, Any]]) -> Set[str]:
    """Extract IDs of records flagged for sample-size mismatch."""
    mismatch_ids = set()
    for record in records:
        # Check for data_quality_warning containing sample-size mismatch
        warnings = record.get('data_quality_warning', [])
        if isinstance(warnings, str):
            warnings = [warnings]
        
        for warning in warnings:
            if 'sample_size_mismatch' in warning.lower() or 'sample size mismatch' in warning.lower():
                # Use a unique identifier if available, otherwise index
                  record_id = record.get('id', record.get('url', f"record_{records.index(record)}"))
                  mismatch_ids.add(record_id)
                  break
    return mismatch_ids

def get_prevalence_record_ids(records: List[Dict[str, Any]]) -> Set[str]:
    """Extract IDs of records included in prevalence analysis."""
    prevalence_ids = set()
    for record in records:
        record_id = record.get('id', record.get('url', f"record_{records.index(record)}"))
        prevalence_ids.add(record_id)
    return prevalence_ids

def test_prevalence_excludes_sample_size_mismatch():
    """
    T042b Verification: Ensure prevalence.json excludes entries flagged for sample-size mismatch.
    
    This test:
    1. Loads output/audit_report.json (generated by T025)
    2. Identifies records with data_quality_warning for sample-size mismatch
    3. Loads output/prevalence.json (generated by T042)
    4. Verifies no mismatched records appear in prevalence data
    """
    project_root = Path(__file__).parent.parent.parent.parent
    audit_report_path = project_root / 'output' / 'audit_report.json'
    prevalence_path = project_root / 'output' / 'prevalence.json'

    # Load data
    logger.info(f"Loading audit report from {audit_report_path}")
    audit_records = load_audit_records(audit_report_path)
    
    logger.info(f"Loading prevalence report from {prevalence_path}")
    prevalence_records = load_prevalence_records(prevalence_path)

    # Identify mismatched records
    mismatch_ids = get_sample_size_mismatch_ids(audit_records)
    logger.info(f"Found {len(mismatch_ids)} records flagged for sample-size mismatch")

    # Get prevalence record IDs
    prevalence_ids = get_prevalence_record_ids(prevalence_records)
    logger.info(f"Found {len(prevalence_ids)} records in prevalence analysis")

    # Check for intersection
    violations = mismatch_ids.intersection(prevalence_ids)

    if violations:
        error_msg = (
            f"FAILURE: Prevalence report contains {len(violations)} records "
            f"flagged for sample-size mismatch: {violations}"
        )
        logger.error(error_msg)
        raise AssertionError(error_msg)

    logger.info("SUCCESS: No sample-size mismatch records found in prevalence.json")
    print("✓ T042b Verification Passed: Prevalence.json correctly excludes sample-size mismatch entries")

if __name__ == '__main__':
    test_prevalence_excludes_sample_size_mismatch()
