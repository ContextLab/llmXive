def test_csv_values_match_json_derived_aggregates(
    temp_directory, sample_audit_report, expected_prevalence_data
):
    """Test that CSV values exactly match JSON-derived aggregates."""
    audit_report_path = temp_directory / "audit_report.json"
    summary_csv_path = temp_directory / "summary_report.csv"

    # Write sample audit report
    with open(audit_report_path, "w") as f:
        json.dump(sample_audit_report, f)

    # Generate summary report
    generate_summary_report(
        audit_report_path=audit_report_path,
        summary_csv_path=summary_csv_path,
    )

    # Read CSV
    with open(summary_csv_path, "r", newline="") as f:
        reader = csv.DictReader(f)
        csv_row = next(reader)

    # Calculate expected values from JSON
    total_summaries = len(sample_audit_report)
    inconsistent_count = sum(
        1 for record in sample_audit_report if record["is_inconsistent"]
    )
    inconsistent_rate = inconsistent_count / total_summaries

    # Verify CSV values match JSON-derived aggregates
    assert int(csv_row["total_summaries"]) == total_summaries, (
        f"total_summaries mismatch: CSV={csv_row['total_summaries']}, "
        f"expected={total_summaries}"
    )
    assert int(csv_row["inconsistent_count"]) == inconsistent_count, (
        f"inconsistent_count mismatch: CSV={csv_row['inconsistent_count']}, "
        f"expected={inconsistent_count}"
    )

    # Check rates with tolerance for floating point
    csv_inconsistent_rate = float(csv_row["inconsistent_rate"])
    assert abs(csv_inconsistent_rate - inconsistent_rate) < 1e-6, (
        f"inconsistent_rate mismatch: CSV={csv_inconsistent_rate}, "
        f"expected={inconsistent_rate}"
    )

    # Verify bias_adjusted_rate is present and numeric
    bias_adjusted = float(csv_row["bias_adjusted_rate"])
    assert 0 <= bias_adjusted <= 1, (
        f"bias_adjusted_rate out of range: {bias_adjusted}"
    )

    # Verify Wilson CI bounds
    ci_lower = float(csv_row["wilson_ci_lower"])
    ci_upper = float(csv_row["wilson_ci_upper"])
    assert 0 <= ci_lower <= 1, f"wilson_ci_lower out of range: {ci_lower}"
    assert 0 <= ci_upper <= 1, f"wilson_ci_upper out of range: {ci_upper}"
    assert ci_lower <= ci_upper, (
        f"CI bounds invalid: lower={ci_lower}, upper={ci_upper}"
    )


