"""
Integration test for T029: Evaluate inconsistency-detection component on synthetic validation dataset.

This script runs the evaluation module on the synthetic dataset generated by T026,
computes precision, recall, and F1 score, and asserts the thresholds defined in FR-031:
- Precision >= 90%
- Recall >= 80%

If thresholds are not met, it raises ERR-800.
"""
import sys
import logging
from pathlib import Path
from code.src.utils.logger import get_default_logger, get_error_message
from code.src.audit.evaluation import main as run_evaluation

# Setup logging
logger = get_default_logger(__name__)

def main():
    """
    Entry point for the synthetic validation test.
    Runs the evaluation and asserts success criteria.
    """
    logger.info("Starting T029: Synthetic Validation Evaluation")
    
    # Define paths relative to project root
    # T026 generates synthetic data in data/synthetic/
    # The evaluation script expects these paths or we pass them as args
    # We assume the standard project structure:
    # data/synthetic/summaries.csv (generated by T026)
    # data/synthetic/ground_truth.csv (generated by T026)
    
    synthetic_dir = Path("data/synthetic")
    summaries_file = synthetic_dir / "summaries.csv"
    ground_truth_file = synthetic_dir / "ground_truth.csv"
    output_file = Path("output/evaluation_results.json")
    
    if not summaries_file.exists():
        logger.error(f"Synthetic summaries file not found: {summaries_file}")
        logger.error(get_error_message("ERR-800"))
        sys.exit(1)
        
    if not ground_truth_file.exists():
        logger.error(f"Ground truth file not found: {ground_truth_file}")
        logger.error(get_error_message("ERR-800"))
        sys.exit(1)
    
    logger.info(f"Loading synthetic summaries from {summaries_file}")
    logger.info(f"Loading ground truth from {ground_truth_file}")
    
    # Run the evaluation logic
    # The evaluation module's main function handles loading and computing metrics
    try:
        # We call the evaluation main with explicit paths to ensure we use the correct files
        # Note: The existing evaluation.py main might expect args or defaults.
        # We'll invoke it via the internal functions to be precise.
        from code.src.audit.evaluation import (
            load_synthetic_summaries,
            load_ground_truth,
            load_audit_records,
            evaluate_detection,
            write_evaluation_results
        )
        
        summaries = load_synthetic_summaries(summaries_file)
        ground_truth = load_ground_truth(ground_truth_file)
        
        # We need the audit records to compare against.
        # In a real pipeline, these would come from running the reconstructor/validator.
        # For this task, we assume the audit records are generated and stored.
        # The task description implies we evaluate the *component* on the dataset.
        # We need to simulate running the validator on the synthetic summaries to get "predicted" labels.
        # However, T026 generates the synthetic data with ground truth.
        # The evaluation module likely expects the audit records to be pre-generated.
        # Let's assume the pipeline (T032) has run and produced output/audit_report.json
        # which contains the "predicted" inconsistency flags.
        
        audit_records_path = Path("output/audit_report.json")
        if not audit_records_path.exists():
            # If the full pipeline hasn't run, we might need to run the reconstructor/validator
            # on the synthetic summaries. For now, let's assume the test environment
            # ensures the audit report exists or we run the necessary steps.
            # Given the dependencies, T032 (driver) should have run.
            logger.warning("Audit report not found. Attempting to run validation on synthetic data...")
            # Fallback: run the reconstructor and validator on the synthetic summaries
            from code.src.audit.reconstructor import reconstruct_all
            from code.src.audit.validator import validate_all_summaries, write_audit_report
            
            reconstructed = reconstruct_all(summaries)
            audit_records = validate_all_summaries(reconstructed)
            write_audit_report(audit_records, audit_records_path)
        
        audit_records = load_audit_records(audit_records_path)
        
        # Perform evaluation
        metrics = evaluate_detection(summaries, ground_truth, audit_records)
        
        precision = metrics.get('precision', 0.0)
        recall = metrics.get('recall', 0.0)
        f1 = metrics.get('f1', 0.0)
        
        logger.info(f"Evaluation Results: Precision={precision:.4f}, Recall={recall:.4f}, F1={f1:.4f}")
        
        # Write results
        write_evaluation_results(metrics, output_file)
        
        # Assert thresholds
        precision_threshold = 0.90
        recall_threshold = 0.80
        
        if precision < precision_threshold:
            logger.error(f"Precision {precision:.4f} is below threshold {precision_threshold}.")
            logger.error(get_error_message("ERR-800"))
            sys.exit(1)
            
        if recall < recall_threshold:
            logger.error(f"Recall {recall:.4f} is below threshold {recall_threshold}.")
            logger.error(get_error_message("ERR-800"))
            sys.exit(1)
        
        logger.info("T029: All success criteria met (Precision >= 90%, Recall >= 80%).")
        sys.exit(0)
        
    except Exception as e:
        logger.error(f"Error during evaluation: {str(e)}")
        logger.error(get_error_message("ERR-800"))
        sys.exit(1)

if __name__ == "__main__":
    main()