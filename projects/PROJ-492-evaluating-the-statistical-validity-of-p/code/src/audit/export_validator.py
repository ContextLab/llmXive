"""
Export Validator Module (T059)

Implements consistency checking between the JSON audit report and the CSV summary report.
Verifies that counts of consistent vs inconsistent entries match exactly.
Logs ERR-201 if a mismatch is detected.
"""
import json
import csv
import logging
from pathlib import Path
from typing import Tuple, Optional, List, Dict, Any

from code.src.utils.logger import get_default_logger, AuditLogger, get_error_message
from code.src.models.data_models import AuditRecord

logger = get_default_logger(__name__)


def load_audit_records_from_json(json_path: Path) -> List[AuditRecord]:
    """
    Load audit records from the JSON report file.
    """
    if not json_path.exists():
        raise FileNotFoundError(f"Audit report JSON not found: {json_path}")

    with open(json_path, 'r', encoding='utf-8') as f:
        data = json.load(f)

    # Handle both list format and dict with 'records' key
    records_data = data if isinstance(data, list) else data.get('records', [])

    records = []
    for item in records_data:
        # Convert dict to AuditRecord if needed, or assume already dict-like
        # Based on T056, output/audit_report.json is generated by validate_all_summaries
        # which writes a list of dicts or a wrapped object.
        # We treat it as a list of dicts for counting.
        records.append(item)

    return records


def load_summary_counts_from_csv(csv_path: Path) -> Dict[str, int]:
    """
    Load the summary report CSV and count entries by consistency status.
    Expects columns: 'total_summaries', 'inconsistent_count', etc.
    We verify the 'inconsistent_count' against the JSON count.
    """
    if not csv_path.exists():
        raise FileNotFoundError(f"Summary report CSV not found: {csv_path}")

    counts = {
        'total': 0,
        'inconsistent': 0,
        'consistent': 0
    }

    with open(csv_path, 'r', encoding='utf-8') as f:
        reader = csv.DictReader(f)
        # Expecting exactly one row based on T047
        row = next(reader, None)
        if row:
            # Parse the CSV values
            counts['total'] = int(row.get('total_summaries', 0))
            counts['inconsistent'] = int(row.get('inconsistent_count', 0))
            counts['consistent'] = counts['total'] - counts['inconsistent']

    return counts


def validate_export_consistency(
    json_path: Path,
    csv_path: Path,
    logger_instance: Optional[AuditLogger] = None
) -> Tuple[bool, Dict[str, Any]]:
    """
    Compare counts from JSON audit report and CSV summary report.

    Returns:
        Tuple of (is_consistent: bool, details: dict)
    """
    if logger_instance is None:
        logger_instance = logger

    results = {
        'json_total': 0,
        'json_inconsistent': 0,
        'csv_total': 0,
        'csv_inconsistent': 0,
        'json_consistent': 0,
        'csv_consistent': 0,
        'mismatch_type': None
    }

    try:
        # Load JSON
        json_records = load_audit_records_from_json(json_path)
        results['json_total'] = len(json_records)
        results['json_inconsistent'] = sum(
            1 for r in json_records
            if r.get('is_inconsistent', False) or r.get('data_quality_warning', False)
        )
        # Note: T025c says sample-size mismatch entries are excluded from prevalence,
        # but for this consistency check, we compare the raw audit report counts
        # against the summary report counts. The summary report (T047) is generated
        # from the audit report. We need to ensure the counts match exactly.
        # Let's count based on the 'is_inconsistent' flag which is the primary indicator.
        results['json_consistent'] = results['json_total'] - results['json_inconsistent']

        # Load CSV
        csv_counts = load_summary_counts_from_csv(csv_path)
        results['csv_total'] = csv_counts['total']
        results['csv_inconsistent'] = csv_counts['inconsistent']
        results['csv_consistent'] = csv_counts['consistent']

        # Check consistency
        is_consistent = True
        mismatch_reasons = []

        if results['json_total'] != results['csv_total']:
            is_consistent = False
            mismatch_reasons.append(f"Total count mismatch: JSON={results['json_total']}, CSV={results['csv_total']}")
            results['mismatch_type'] = 'total'

        if results['json_inconsistent'] != results['csv_inconsistent']:
            is_consistent = False
            mismatch_reasons.append(f"Inconsistent count mismatch: JSON={results['json_inconsistent']}, CSV={results['csv_inconsistent']}")
            results['mismatch_type'] = 'inconsistent'

        if not is_consistent:
            error_msg = get_error_message('ERR-201')
            logger_instance.error(f"{error_msg}: {'; '.join(mismatch_reasons)}")
        else:
            logger_instance.info("Export consistency check passed. JSON and CSV counts match.")

        return is_consistent, results

    except Exception as e:
        error_msg = get_error_message('ERR-201')
        logger_instance.error(f"{error_msg}: Exception during validation: {str(e)}")
        return False, {'error': str(e)}


def run_export_validation(
    json_path: Path,
    csv_path: Path,
    raise_on_mismatch: bool = False
) -> bool:
    """
    Run the export validation and optionally raise an error if mismatched.

    Args:
        json_path: Path to audit_report.json
        csv_path: Path to summary_report.csv
        raise_on_mismatch: If True, raise ValueError on mismatch

    Returns:
        True if consistent, False otherwise
    """
    is_consistent, details = validate_export_consistency(json_path, csv_path)

    if not is_consistent and raise_on_mismatch:
        raise ValueError(f"Export consistency check failed: {details.get('mismatch_type', 'unknown')}")

    return is_consistent


def main():
    """
    CLI entry point for export validation.
    """
    import argparse

    parser = argparse.ArgumentParser(description="Validate consistency between audit JSON and summary CSV.")
    parser.add_argument(
        "--json-path",
        type=Path,
        default=Path("output/audit_report.json"),
        help="Path to audit_report.json"
    )
    parser.add_argument(
        "--csv-path",
        type=Path,
        default=Path("output/summary_report.csv"),
        help="Path to summary_report.csv"
    )
    parser.add_argument(
        "--strict",
        action="store_true",
        help="Exit with code 1 if mismatch detected"
    )

    args = parser.parse_args()

    logger_instance = get_default_logger(__name__)
    logger_instance.info(f"Validating export consistency: JSON={args.json_path}, CSV={args.csv_path}")

    try:
        is_consistent = run_export_validation(
            args.json_path,
            args.csv_path,
            raise_on_mismatch=args.strict
        )

        if is_consistent:
            logger_instance.info("Validation PASSED: No ERR-201 logged.")
            return 0
        else:
            logger_instance.error("Validation FAILED: ERR-201 logged due to mismatch.")
            return 1 if args.strict else 0

    except Exception as e:
        logger_instance.error(f"Validation failed with exception: {e}")
        return 1


if __name__ == "__main__":
    import sys
    sys.exit(main())