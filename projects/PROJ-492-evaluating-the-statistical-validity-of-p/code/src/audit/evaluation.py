def evaluate_detection(summaries: List[Dict[str, Any]], audit_records: List[Dict[str, Any]]) -> Dict[str, float]:
    """
    Compare audit records against ground truth to compute Precision, Recall, F1.
    """
    # Map audit records to indices. The synthetic data and audit records should be in the same order
    # if the validator processed them sequentially without filtering out non-inconsistent ones entirely.
    # However, T025 says: "generate AuditRecord objects ... writing output/audit_report.json".
    # If the validator only outputs inconsistent records, we need to handle that.
    # Let's assume the validator outputs ALL records (consistent and inconsistent) or we can infer.
    # T025 description: "writing output/audit_report.json".
    # Let's check if the audit record contains the original index or we assume order.
    # To be safe, we'll assume the order is preserved and the list length matches.

    if len(audit_records) != len(summaries):
        # If lengths differ, we might have filtered.
        # Let's try to match by some ID if available, otherwise assume order and log warning.
        logger.warning(f"Length mismatch: summaries={len(summaries)}, audit_records={len(audit_records)}. "
                       "Assuming order preservation or missing records.")

    predicted_inconsistent = set()
    true_inconsistent = set()

    for idx, summary in enumerate(summaries):
        if summary.get('is_inconsistent', False):
            true_inconsistent.add(idx)

        # Check if the corresponding audit record flags it as inconsistent
        # We need to find the record for this index.
        # Assuming audit_records is ordered same as summaries.
        if idx < len(audit_records):
            record = audit_records[idx]
            # Check if the record indicates inconsistency.
            # T025: "flags inconsistencies".
            # We look for a field like 'is_inconsistent' or 'inconsistency_detected' or 'flags'.
            is_flagged = False
            if 'is_inconsistent' in record:
                is_flagged = record['is_inconsistent']
            elif 'flags' in record and len(record['flags']) > 0:
                is_flagged = True
            elif 'data_quality_warning' in record and record['data_quality_warning']:
                # T025 mentions data_quality_warning for sample size mismatch, but inconsistency is separate.
                # We focus on statistical inconsistency.
                # Let's assume 'is_inconsistent' is the primary flag.
                pass

            # If we can't find a clear flag, we might need to inspect the structure.
            # For now, let's assume if 'inconsistency_score' > 0 or similar.
            # Re-reading T025: "generate AuditRecord objects with data_quality_warning messages for sample-size discrepancies".
            # The main inconsistency flag is likely 'is_inconsistent'.
            # If the validator doesn't output consistent records, we have a problem.
            # Let's assume the validator outputs a record for every input.

            if is_flagged:
                predicted_inconsistent.add(idx)
        else:
            # No record found for this index -> treated as consistent (False Negative)
            pass

    # Calculate metrics
    # Precision = TP / (TP + FP)
    # Recall = TP / (TP + FN)
    # F1 = 2 * (Precision * Recall) / (Precision + Recall)

    tp = len(predicted_inconsistent & true_inconsistent)
    fp = len(predicted_inconsistent - true_inconsistent)
    fn = len(true_inconsistent - predicted_inconsistent)

    precision = tp / (tp + fp) if (tp + fp) > 0 else 0.0
    recall = tp / (tp + fn) if (tp + fn) > 0 else 0.0
    f1 = 2 * (precision * recall) / (precision + recall) if (precision + recall) > 0 else 0.0

    return {
        "precision": precision,
        "recall": recall,
        "f1": f1,
        "tp": tp,
        "fp": fp,
        "fn": fn,
        "total_summaries": len(summaries),
        "true_inconsistent_count": len(true_inconsistent),
        "predicted_inconsistent_count": len(predicted_inconsistent)
    }


def write_evaluation_results(results: Dict[str, Any], filepath: Path):
    """
    Write evaluation results to JSON.
    """
    filepath.parent.mkdir(parents=True, exist_ok=True)
    with open(filepath, 'w', encoding='utf-8') as f:
        json.dump(results, f, indent=2)
    logger.info(f"Evaluation results written to {filepath}")


