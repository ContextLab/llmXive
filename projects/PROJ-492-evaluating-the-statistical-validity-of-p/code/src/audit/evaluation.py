def load_synthetic_summaries(filepath: Path) -> List[Dict[str, Any]]:
    """
    Load synthetic summaries from CSV.
    Expects columns: ..., 'is_inconsistent' (ground truth boolean), ...
    """
    if not filepath.exists():
        raise FileNotFoundError(f"Synthetic dataset not found at {filepath}. "
                                "Please ensure T026 (synthetic.py) has been run first.")

    summaries = []
    with open(filepath, 'r', newline='', encoding='utf-8') as f:
        reader = csv.DictReader(f)
        for row in reader:
            # Convert ground truth flag to boolean
            row['is_inconsistent'] = row.get('is_inconsistent', 'False').lower() == 'true'
            # Convert numeric strings to floats for the validator
            for key in ['baseline_n', 'baseline_successes', 'treatment_n', 'treatment_successes',
                        'baseline_rate', 'treatment_rate', 'reported_p_value', 'reported_effect_size']:
                if key in row and row[key] is not None and row[key] != '':
                    try:
                        row[key] = float(row[key])
                    except (ValueError, TypeError):
                        row[key] = None
            summaries.append(row)

    logger.info(f"Loaded {len(summaries)} synthetic summaries from {filepath}")
    return summaries


def load_ground_truth(summaries: List[Dict[str, Any]]) -> Set[int]:
    """
    Extract indices of summaries that are actually inconsistent (ground truth).
    """
    inconsistent_indices = set()
    for idx, summary in enumerate(summaries):
        if summary.get('is_inconsistent', False):
            inconsistent_indices.add(idx)
    return inconsistent_indices


