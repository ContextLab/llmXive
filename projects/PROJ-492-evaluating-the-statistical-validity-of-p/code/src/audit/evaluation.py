"""
Evaluation module for T029: Evaluate inconsistency-detection component on synthetic validation dataset.

This module computes precision, recall, and F1 score for the inconsistency detection
component using the synthetic dataset generated by T026. It validates that the
pipeline meets the success criteria (precision >= 90%, recall >= 80%).
"""

import csv
import json
import logging
import sys
from datetime import datetime
from pathlib import Path
from typing import Dict, Any, List, Optional, Tuple

from code.src.utils.logger import get_default_logger
from code.src.utils.helpers import safe_float
from code.src.config import SEED, set_rng_seed

logger = get_default_logger(__name__)

def load_synthetic_summaries(path: Path) -> List[Dict[str, Any]]:
    """Load synthetic summaries from JSON file generated by T026."""
    if not path.exists():
        raise FileNotFoundError(f"Synthetic summaries file not found: {path}")
    
    with open(path, 'r', encoding='utf-8') as f:
        data = json.load(f)
    
    # Handle both list and dict with 'summaries' key
    if isinstance(data, dict) and 'summaries' in data:
        return data['summaries']
    elif isinstance(data, list):
        return data
    else:
        raise ValueError(f"Unexpected format in synthetic summaries: {type(data)}")

def load_ground_truth(path: Path) -> Dict[str, bool]:
    """
    Load ground truth labels from the synthetic dataset metadata.
    
    The synthetic generator (T026) creates a metadata file with ground truth
    labels indicating which summaries have intentionally injected inconsistencies.
    """
    if not path.exists():
        raise FileNotFoundError(f"Ground truth file not found: {path}")
    
    with open(path, 'r', encoding='utf-8') as f:
        data = json.load(f)
    
    # Expected format: {"inconsistent_ids": [...], "consistent_ids": [...]}
    if 'inconsistent_ids' not in data or 'consistent_ids' not in data:
        raise ValueError(f"Ground truth missing required fields: {list(data.keys())}")
    
    ground_truth = {}
    for summary_id in data['inconsistent_ids']:
        ground_truth[summary_id] = True
    for summary_id in data['consistent_ids']:
        ground_truth[summary_id] = False
    
    return ground_truth

def load_audit_records(path: Path) -> Dict[str, Dict[str, Any]]:
    """Load audit records from the validator output (T025)."""
    if not path.exists():
        raise FileNotFoundError(f"Audit report file not found: {path}")
    
    with open(path, 'r', encoding='utf-8') as f:
        data = json.load(f)
    
    # Handle both list and dict with 'records' key
    if isinstance(data, dict) and 'records' in data:
        records = data['records']
    elif isinstance(data, list):
        records = data
    else:
        raise ValueError(f"Unexpected format in audit report: {type(data)}")
    
    # Index by summary_id
    return {record['summary_id']: record for record in records}

def evaluate_detection(
    audit_records: Dict[str, Dict[str, Any]],
    ground_truth: Dict[str, bool]
) -> Tuple[float, float, float, Dict[str, int]]:
    """
    Compute precision, recall, and F1 score for inconsistency detection.
    
    Returns:
        Tuple of (precision, recall, f1, counts_dict)
        counts_dict contains: tp, fp, tn, fn
    """
    tp = 0  # True Positives: flagged as inconsistent, actually inconsistent
    fp = 0  # False Positives: flagged as inconsistent, actually consistent
    tn = 0  # True Negatives: flagged as consistent, actually consistent
    fn = 0  # False Negatives: flagged as consistent, actually inconsistent
    
    for summary_id, is_actually_inconsistent in ground_truth.items():
        if summary_id not in audit_records:
            # Missing record - treat as not flagged (consistent)
            flagged = False
        else:
            record = audit_records[summary_id]
            # Check if flagged as inconsistent
            flagged = record.get('is_inconsistent', False)
        
        if flagged and is_actually_inconsistent:
            tp += 1
        elif flagged and not is_actually_inconsistent:
            fp += 1
        elif not flagged and is_actually_inconsistent:
            fn += 1
        else:  # not flagged and not inconsistent
            tn += 1
    
    # Calculate metrics
    precision = tp / (tp + fp) if (tp + fp) > 0 else 0.0
    recall = tp / (tp + fn) if (tp + fn) > 0 else 0.0
    f1 = 2 * (precision * recall) / (precision + recall) if (precision + recall) > 0 else 0.0
    
    counts = {'tp': tp, 'fp': fp, 'tn': tn, 'fn': fn}
    return precision, recall, f1, counts

def validate_summary(
    precision: float,
    recall: float,
    f1: float,
    precision_threshold: float = 0.90,
    recall_threshold: float = 0.80
) -> Tuple[bool, str]:
    """
    Validate that metrics meet success criteria.
    
    Returns:
        Tuple of (passed, message)
    """
    errors = []
    
    if precision < precision_threshold:
        errors.append(f"Precision {precision:.4f} < threshold {precision_threshold}")
    
    if recall < recall_threshold:
        errors.append(f"Recall {recall:.4f} < threshold {recall_threshold}")
    
    if errors:
        return False, "; ".join(errors)
    
    return True, f"Metrics meet criteria: Precision={precision:.4f}, Recall={recall:.4f}, F1={f1:.4f}"

def write_evaluation_results(
    path: Path,
    precision: float,
    recall: float,
    f1: float,
    counts: Dict[str, int],
    passed: bool,
    message: str
) -> None:
    """Write evaluation results to JSON file."""
    results = {
        'timestamp': datetime.utcnow().isoformat(),
        'task_id': 'T029',
        'metrics': {
            'precision': precision,
            'recall': recall,
            'f1_score': f1
        },
        'counts': counts,
        'validation': {
            'passed': passed,
            'message': message
        },
        'thresholds': {
            'precision': 0.90,
            'recall': 0.80
        }
    }
    
    path.parent.mkdir(parents=True, exist_ok=True)
    with open(path, 'w', encoding='utf-8') as f:
        json.dump(results, f, indent=2)
    
    logger.info(f"Evaluation results written to {path}")

def main() -> int:
    """
    Main entry point for T029 evaluation.
    
    Loads synthetic dataset, compares audit results with ground truth,
    computes precision/recall/F1, and validates against thresholds.
    
    Returns:
        0 on success, 1 on failure (metrics below threshold)
    """
    set_rng_seed(SEED)
    
    # Define paths
    project_root = Path(__file__).parent.parent.parent.parent
    synthetic_data_path = project_root / 'data' / 'synthetic' / 'synthetic_summaries.json'
    ground_truth_path = project_root / 'data' / 'synthetic' / 'synthetic_metadata.json'
    audit_report_path = project_root / 'output' / 'audit_report.json'
    results_path = project_root / 'output' / 'evaluation_results.json'
    
    logger.info(f"Starting T029 evaluation on synthetic dataset")
    logger.info(f"Synthetic data: {synthetic_data_path}")
    logger.info(f"Ground truth: {ground_truth_path}")
    logger.info(f"Audit report: {audit_report_path}")
    
    try:
        # Load data
        logger.info("Loading synthetic summaries...")
        summaries = load_synthetic_summaries(synthetic_data_path)
        logger.info(f"Loaded {len(summaries)} synthetic summaries")
        
        logger.info("Loading ground truth labels...")
        ground_truth = load_ground_truth(ground_truth_path)
        logger.info(f"Loaded ground truth for {len(ground_truth)} summaries")
        
        logger.info("Loading audit records...")
        audit_records = load_audit_records(audit_report_path)
        logger.info(f"Loaded {len(audit_records)} audit records")
        
        # Evaluate
        logger.info("Evaluating detection performance...")
        precision, recall, f1, counts = evaluate_detection(audit_records, ground_truth)
        
        logger.info(f"Results: TP={counts['tp']}, FP={counts['fp']}, TN={counts['tn']}, FN={counts['fn']}")
        logger.info(f"Precision: {precision:.4f}, Recall: {recall:.4f}, F1: {f1:.4f}")
        
        # Validate
        passed, message = validate_summary(precision, recall, f1)
        logger.info(f"Validation: {message}")
        
        # Write results
        write_evaluation_results(results_path, precision, recall, f1, counts, passed, message)
        
        if not passed:
            logger.error(f"ERR-800: Evaluation failed - {message}")
            return 1
        
        logger.info("T029 evaluation completed successfully")
        return 0
        
    except FileNotFoundError as e:
        logger.error(f"ERR-800: Required data file not found - {e}")
        return 1
    except ValueError as e:
        logger.error(f"ERR-800: Data validation error - {e}")
        return 1
    except Exception as e:
        logger.error(f"ERR-800: Unexpected error during evaluation - {e}")
        return 1

if __name__ == '__main__':
    sys.exit(main())
