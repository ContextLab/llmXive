"""
Evaluation module for T029: Evaluate inconsistency-detection component on synthetic validation dataset.

This module loads the synthetic dataset generated by T026 and the audit records
produced by the validation pipeline (T025). It computes precision, recall, and F1
for the inconsistency detection component.

Requirements:
- Precision >= 90%
- Recall >= 80%

If thresholds are not met, the script exits with status code 1 and logs ERR-800.
"""
import csv
import json
import logging
import sys
from datetime import datetime
from pathlib import Path
from typing import Dict, Any, List, Set, Tuple, Optional

from code.src.utils.logger import get_default_logger, AuditLogger, get_error_message

logger = get_default_logger(__name__)

# Error codes
ERR_800 = "ERR-800"

def load_synthetic_summaries(path: Path) -> Dict[str, Dict[str, Any]]:
    """
    Load synthetic summaries generated by T026.
    Expected format: JSON list of ABTestSummary objects with 'ground_truth_inconsistent' flag.
    """
    if not path.exists():
        raise FileNotFoundError(f"Synthetic summaries file not found: {path}")
    
    with open(path, 'r', encoding='utf-8') as f:
        data = json.load(f)
    
    # Index by a unique identifier (e.g., 'id' or 'url' if present)
    # Assuming the synthetic generator adds a unique 'id' field
    return {item['id']: item for item in data}

def load_ground_truth(path: Path) -> Dict[str, bool]:
    """
    Load ground truth labels for inconsistency.
    Expected format: JSON dict mapping record_id -> is_inconsistent (bool)
    """
    if not path.exists():
        raise FileNotFoundError(f"Ground truth file not found: {path}")
    
    with open(path, 'r', encoding='utf-8') as f:
        data = json.load(f)
    
    return {k: v['is_inconsistent'] for k, v in data.items()}

def load_audit_records(path: Path) -> Dict[str, Dict[str, Any]]:
    """
    Load audit records produced by the validator (T025).
    Expected format: JSON list of AuditRecord objects.
    """
    if not path.exists():
        raise FileNotFoundError(f"Audit records file not found: {path}")
    
    with open(path, 'r', encoding='utf-8') as f:
        data = json.load(f)
    
    # Index by 'summary_id' or similar unique identifier
    # Assuming the validator adds a 'summary_id' field matching the synthetic ID
    return {record['summary_id']: record for record in data}

def validate_summary(summary_id: str, ground_truth: Dict[str, bool], audit_records: Dict[str, Any]) -> Tuple[bool, bool]:
    """
    Compare a single summary's predicted inconsistency against ground truth.
    
    Returns:
        (predicted_inconsistent, actual_inconsistent)
    """
    actual = ground_truth.get(summary_id, False)
    record = audit_records.get(summary_id)
    
    if record is None:
        # If no audit record exists, we assume it wasn't flagged as inconsistent
        # This might be a false negative if ground_truth is True
        predicted = False
    else:
        # Check if the record was flagged as inconsistent
        # The validator sets 'is_inconsistent' or 'flag' based on thresholds
        predicted = record.get('is_inconsistent', False) or record.get('flag', False)
    
    return predicted, actual

def evaluate_detection(
    synthetic_path: Path,
    ground_truth_path: Path,
    audit_records_path: Path
) -> Dict[str, float]:
    """
    Compute precision, recall, and F1 for inconsistency detection.
    
    Precision = TP / (TP + FP)
    Recall = TP / (TP + FN)
    F1 = 2 * (Precision * Recall) / (Precision + Recall)
    """
    summaries = load_synthetic_summaries(synthetic_path)
    ground_truth = load_ground_truth(ground_truth_path)
    audit_records = load_audit_records(audit_records_path)
    
    tp, fp, fn, tn = 0, 0, 0, 0
    
    # Evaluate on all records that have ground truth
    for summary_id, is_actual_inconsistent in ground_truth.items():
        predicted, actual = validate_summary(summary_id, ground_truth, audit_records)
        
        if predicted and actual:
            tp += 1
        elif predicted and not actual:
            fp += 1
        elif not predicted and actual:
            fn += 1
        else:
            tn += 1
    
    precision = tp / (tp + fp) if (tp + fp) > 0 else 0.0
    recall = tp / (tp + fn) if (tp + fn) > 0 else 0.0
    f1 = 2 * (precision * recall) / (precision + recall) if (precision + recall) > 0 else 0.0
    
    return {
        'precision': precision,
        'recall': recall,
        'f1': f1,
        'true_positives': tp,
        'false_positives': fp,
        'false_negatives': fn,
        'true_negatives': tn
    }

def write_evaluation_results(results: Dict[str, Any], output_path: Path) -> None:
    """Write evaluation metrics to a JSON file."""
    output_path.parent.mkdir(parents=True, exist_ok=True)
    
    results['timestamp'] = datetime.now().isoformat()
    results['task_id'] = 'T029'
    
    with open(output_path, 'w', encoding='utf-8') as f:
        json.dump(results, f, indent=2)
    
    logger.info(f"Evaluation results written to {output_path}")

def main() -> int:
    """
    Main entry point for T029.
    
    Reads synthetic data and audit records, computes metrics, and asserts thresholds.
    Returns 0 on success, 1 on failure (thresholds not met).
    """
    # Define paths relative to project root
    project_root = Path(__file__).resolve().parent.parent.parent.parent
    synthetic_path = project_root / "data" / "synthetic" / "synthetic_summaries.json"
    ground_truth_path = project_root / "data" / "synthetic" / "ground_truth_labels.json"
    audit_records_path = project_root / "output" / "audit_report.json"
    output_path = project_root / "output" / "evaluation_results.json"
    
    # Ensure paths exist
    if not synthetic_path.exists():
        logger.error(f"Synthetic summaries not found at {synthetic_path}")
        logger.error("Run T026 to generate synthetic dataset first.")
        return 1
    
    if not ground_truth_path.exists():
        logger.error(f"Ground truth not found at {ground_truth_path}")
        logger.error("Run T026 to generate ground truth labels first.")
        return 1
    
    if not audit_records_path.exists():
        logger.error(f"Audit records not found at {audit_records_path}")
        logger.error("Run the validator pipeline (T025) first.")
        return 1
    
    try:
        logger.info("Starting inconsistency detection evaluation (T029)...")
        metrics = evaluate_detection(synthetic_path, ground_truth_path, audit_records_path)
        
        logger.info(f"Precision: {metrics['precision']:.4f}")
        logger.info(f"Recall: {metrics['recall']:.4f}")
        logger.info(f"F1 Score: {metrics['f1']:.4f}")
        
        # Write results
        write_evaluation_results(metrics, output_path)
        
        # Assert thresholds
        precision_threshold = 0.90
        recall_threshold = 0.80
        
        precision_ok = metrics['precision'] >= precision_threshold
        recall_ok = metrics['recall'] >= recall_threshold
        
        if not precision_ok:
            logger.error(f"{ERR_800}: Precision {metrics['precision']:.4f} is below threshold {precision_threshold}")
            return 1
        
        if not recall_ok:
            logger.error(f"{ERR_800}: Recall {metrics['recall']:.4f} is below threshold {recall_threshold}")
            return 1
        
        logger.info(f"SUCCESS: All thresholds met. Precision >= {precision_threshold}, Recall >= {recall_threshold}")
        return 0
        
    except Exception as e:
        logger.error(f"Error during evaluation: {e}", exc_info=True)
        return 1

if __name__ == "__main__":
    sys.exit(main())
