"""
Evaluation module for T029: Evaluate inconsistency-detection component on synthetic validation dataset.

Computes precision, recall, and F1 score for the inconsistency detection component
using the synthetic validation dataset generated by T026.

Requirements (FR-031):
- Precision >= 90%
- Recall >= 80%
- F1 >= 0.85

If thresholds are not met, raises ERR-800.
"""
import csv
import json
import logging
import sys
from datetime import datetime
from pathlib import Path
from typing import Dict, Any, List, Optional, Tuple

from code.src.utils.logger import get_default_logger, AuditLogger, get_error_message
from code.src.audit.validator import validate_all_summaries, create_audit_record
from code.src.models.data_models import ABTestSummary, AuditRecord
from code.src.config import set_rng_seed

# Constants for evaluation thresholds
PRECISION_THRESHOLD = 0.90
RECALL_THRESHOLD = 0.80
F1_THRESHOLD = 0.85
ERROR_CODE_EVAL_THRESHOLD = "ERR-800"


def load_synthetic_summaries(path: Path) -> List[Dict[str, Any]]:
    """Load synthetic summaries from CSV file."""
    summaries = []
    with open(path, 'r', newline='', encoding='utf-8') as f:
        reader = csv.DictReader(f)
        for row in reader:
            # Convert numeric fields
            for key in ['n_control', 'n_treatment', 'success_control', 'success_treatment', 
                        'mean_control', 'mean_treatment', 'std_control', 'std_treatment']:
                if key in row and row[key]:
                    try:
                        row[key] = float(row[key])
                    except (ValueError, TypeError):
                        row[key] = None
            
            # Convert boolean fields
            for key in ['is_inconsistent_ground_truth']:
                if key in row:
                    val = row[key]
                    if isinstance(val, str):
                        row[key] = val.lower() in ('true', '1', 'yes')
                    else:
                        row[key] = bool(val)
            
            summaries.append(row)
    return summaries


def load_ground_truth(path: Path) -> Dict[str, bool]:
    """
    Load ground truth from JSON file.
    Returns a dict mapping summary_id to is_inconsistent_ground_truth.
    """
    with open(path, 'r', encoding='utf-8') as f:
        data = json.load(f)
    
    # The ground truth JSON may have a different structure; adapt as needed
    # Expected structure from T026: {"summaries": [{"id": "...", "is_inconsistent": true/false}, ...]}
    if isinstance(data, dict) and 'summaries' in data:
        return {item['id']: item['is_inconsistent'] for item in data['summaries']}
    elif isinstance(data, list):
        return {item['id']: item['is_inconsistent'] for item in data}
    else:
        raise ValueError(f"Unexpected ground truth format: {type(data)}")


def evaluate_detection(summaries: List[Dict[str, Any]], ground_truth: Dict[str, bool]) -> Dict[str, Any]:
    """
    Evaluate the inconsistency detection component.
    
    For each synthetic summary:
    1. Validate it using the pipeline's validator (T025)
    2. Compare the detected inconsistency flag with ground truth
    3. Compute precision, recall, and F1
    
    Returns a dict with metrics and detailed results.
    """
    tp = 0  # True Positives: correctly flagged as inconsistent
    fp = 0  # False Positives: flagged inconsistent but actually consistent
    tn = 0  # True Negatives: correctly flagged as consistent
    fn = 0  # False Negatives: missed inconsistency (actually inconsistent but not flagged)
    
    logger = get_default_logger(__name__)
    results = []
    
    for summary_data in summaries:
        summary_id = summary_data.get('id')
        expected_inconsistent = ground_truth.get(summary_id, False)
        
        # Convert to ABTestSummary model
        try:
            ab_summary = ABTestSummary(
                id=summary_id,
                url=summary_data.get('url', ''),
                domain=summary_data.get('domain', ''),
                outcome_type=summary_data.get('outcome_type', 'binary'),
                n_control=int(summary_data['n_control']) if summary_data.get('n_control') else None,
                n_treatment=int(summary_data['n_treatment']) if summary_data.get('n_treatment') else None,
                success_control=int(summary_data['success_control']) if summary_data.get('success_control') else None,
                success_treatment=int(summary_data['success_treatment']) if summary_data.get('success_treatment') else None,
                mean_control=summary_data.get('mean_control'),
                mean_treatment=summary_data.get('mean_treatment'),
                std_control=summary_data.get('std_control'),
                std_treatment=summary_data.get('std_treatment'),
                reported_p_value=summary_data.get('reported_p_value'),
                reported_effect_size=summary_data.get('reported_effect_size'),
                publication_year=summary_data.get('publication_year'),
                source=summary_data.get('source', 'synthetic')
            )
        except Exception as e:
            logger.warning(f"Failed to create ABTestSummary for {summary_id}: {e}")
            continue
        
        # Run validation (this is the inconsistency detection component)
        audit_record = create_audit_record(ab_summary)
        detected_inconsistent = audit_record.is_inconsistent
        
        # Compare with ground truth
        if detected_inconsistent and expected_inconsistent:
            tp += 1
        elif detected_inconsistent and not expected_inconsistent:
            fp += 1
        elif not detected_inconsistent and expected_inconsistent:
            fn += 1
        else:
            tn += 1
        
        results.append({
            'id': summary_id,
            'detected_inconsistent': detected_inconsistent,
            'expected_inconsistent': expected_inconsistent,
            'correct': detected_inconsistent == expected_inconsistent
        })
    
    # Calculate metrics
    precision = tp / (tp + fp) if (tp + fp) > 0 else 0.0
    recall = tp / (tp + fn) if (tp + fn) > 0 else 0.0
    f1 = 2 * (precision * recall) / (precision + recall) if (precision + recall) > 0 else 0.0
    
    return {
        'tp': tp,
        'fp': fp,
        'tn': tn,
        'fn': fn,
        'precision': precision,
        'recall': recall,
        'f1': f1,
        'total_evaluated': tp + fp + tn + fn,
        'results': results
    }


def validate_summary(metrics: Dict[str, Any]) -> Tuple[bool, str]:
    """
    Validate that metrics meet the required thresholds.
    
    Returns (True, message) if thresholds are met, (False, error_message) otherwise.
    """
    precision = metrics['precision']
    recall = metrics['recall']
    f1 = metrics['f1']
    
    errors = []
    
    if precision < PRECISION_THRESHOLD:
        errors.append(f"Precision {precision:.4f} < {PRECISION_THRESHOLD:.2f}")
    
    if recall < RECALL_THRESHOLD:
        errors.append(f"Recall {recall:.4f} < {RECALL_THRESHOLD:.2f}")
    
    if f1 < F1_THRESHOLD:
        errors.append(f"F1 {f1:.4f} < {F1_THRESHOLD:.2f}")
    
    if errors:
        error_msg = f"Validation failed: {'; '.join(errors)}"
        return False, error_msg
    
    return True, f"Validation passed: precision={precision:.4f}, recall={recall:.4f}, f1={f1:.4f}"


def write_evaluation_results(metrics: Dict[str, Any], output_path: Path) -> None:
    """Write evaluation results to JSON file."""
    # Remove detailed results for the main output (too large)
    summary_metrics = {k: v for k, v in metrics.items() if k != 'results'}
    summary_metrics['evaluation_timestamp'] = datetime.utcnow().isoformat()
    summary_metrics['thresholds'] = {
        'precision': PRECISION_THRESHOLD,
        'recall': RECALL_THRESHOLD,
        'f1': F1_THRESHOLD
    }
    
    with open(output_path, 'w', encoding='utf-8') as f:
        json.dump(summary_metrics, f, indent=2)
    
    # Also write detailed results to a separate file
    details_path = output_path.parent / "evaluation_details.json"
    with open(details_path, 'w', encoding='utf-8') as f:
        json.dump(metrics, f, indent=2)


def main() -> int:
    """
    Main entry point for T029 evaluation.
    
    Returns 0 on success, 1 on failure (thresholds not met).
    """
    set_rng_seed(42)  # Use config seed for reproducibility
    
    logger = get_default_logger(__name__)
    audit_logger = AuditLogger(logger)
    
    # Define paths
    synthetic_csv = Path("data/synthetic/synthetic_validation.csv")
    ground_truth_json = Path("data/synthetic/synthetic_ground_truth.json")
    output_dir = Path("output")
    output_dir.mkdir(exist_ok=True)
    output_json = output_dir / "evaluation_results.json"
    
    # Verify input files exist
    if not synthetic_csv.exists():
        error_msg = f"Synthetic validation dataset not found: {synthetic_csv}"
        audit_logger.error(ERROR_CODE_EVAL_THRESHOLD, error_msg)
        logger.error(error_msg)
        return 1
    
    if not ground_truth_json.exists():
        error_msg = f"Ground truth file not found: {ground_truth_json}"
        audit_logger.error(ERROR_CODE_EVAL_THRESHOLD, error_msg)
        logger.error(error_msg)
        return 1
    
    try:
        # Load data
        logger.info(f"Loading synthetic summaries from {synthetic_csv}")
        summaries = load_synthetic_summaries(synthetic_csv)
        logger.info(f"Loaded {len(summaries)} synthetic summaries")
        
        logger.info(f"Loading ground truth from {ground_truth_json}")
        ground_truth = load_ground_truth(ground_truth_json)
        logger.info(f"Loaded {len(ground_truth)} ground truth labels")
        
        # Evaluate
        logger.info("Running inconsistency detection evaluation...")
        metrics = evaluate_detection(summaries, ground_truth)
        
        # Log results
        logger.info(f"Evaluation complete: TP={metrics['tp']}, FP={metrics['fp']}, "
                   f"TN={metrics['tn']}, FN={metrics['fn']}")
        logger.info(f"Precision: {metrics['precision']:.4f}, Recall: {metrics['recall']:.4f}, "
                   f"F1: {metrics['f1']:.4f}")
        
        # Validate thresholds
        passed, message = validate_summary(metrics)
        
        if passed:
            logger.info(f"SUCCESS: {message}")
            write_evaluation_results(metrics, output_json)
            logger.info(f"Results written to {output_json}")
            return 0
        else:
            # Thresholds not met - raise ERR-800
            error_msg = f"{ERROR_CODE_EVAL_THRESHOLD}: {message}"
            audit_logger.error(ERROR_CODE_EVAL_THRESHOLD, message)
            logger.error(error_msg)
            logger.error("Inconsistency detection component does not meet required thresholds.")
            
            # Still write results for inspection
            write_evaluation_results(metrics, output_json)
            logger.info(f"Results written to {output_json} for inspection")
            
            return 1
            
    except Exception as e:
        error_msg = f"Unexpected error during evaluation: {e}"
        audit_logger.error("ERR-800", error_msg)
        logger.exception(error_msg)
        return 1


if __name__ == "__main__":
    sys.exit(main())
