"""
Domain Bias Subsampling Module (T044)

Implements FR-027: Create a balanced subsample of the corpus so that no single
domain exceeds 30% before bias adjustment.

This module reads the audit records (JSON) or summaries (CSV), calculates domain
proportions, and performs stratified subsampling to ensure no domain dominates
the dataset beyond the 30% threshold.
"""
import csv
import json
import logging
from datetime import datetime
from pathlib import Path
from typing import List, Dict, Any, Optional, Tuple
from collections import Counter

from code.src.utils.logger import get_default_logger

# Constants
MAX_DOMAIN_PROPORTION = 0.30
OUTPUT_PATH = Path("data/subsampled_balanced.csv")
INPUT_JSON_PATH = Path("output/audit_report.json")
INPUT_CSV_PATH = Path("output/summary_report.csv") # Fallback if JSON not ready, though JSON is preferred for domain info

logger = get_default_logger("domain_subsample")


def load_audit_records_from_json(path: Path) -> List[Dict[str, Any]]:
    """Load audit records from the JSON report generated by the validator."""
    if not path.exists():
        raise FileNotFoundError(f"Audit report not found at {path}. Run the pipeline first.")
    
    with open(path, 'r', encoding='utf-8') as f:
        data = json.load(f)
    
    # The JSON structure is typically a list of records or a dict with a 'records' key
    if isinstance(data, list):
        return data
    elif isinstance(data, dict) and 'records' in data:
        return data['records']
    else:
        # Fallback: try to find a list in the first value
        for key, value in data.items():
            if isinstance(value, list):
                return value
        raise ValueError(f"Unexpected JSON structure in {path}")


def load_summaries_from_csv(path: Path) -> List[Dict[str, Any]]:
    """Load summaries from a CSV file if JSON is unavailable."""
    if not path.exists():
        raise FileNotFoundError(f"Summary CSV not found at {path}")
    
    records = []
    with open(path, 'r', encoding='utf-8') as f:
        reader = csv.DictReader(f)
        for row in reader:
            records.append(row)
    return records


def extract_domain_from_record(record: Dict[str, Any]) -> str:
    """
    Extract the domain from an audit record.
    Tries common keys: 'domain', 'source_domain', 'url_domain'.
    Falls back to parsing from 'url' if available.
    """
    domain = record.get('domain') or record.get('source_domain') or record.get('url_domain')
    
    if domain:
        return str(domain).strip().lower()
    
    url = record.get('url')
    if url:
        # Simple extraction from URL
        try:
            # Remove protocol
            clean_url = url.split('://')[-1]
            domain = clean_url.split('/')[0].lower()
            return domain
        except Exception:
            pass
    
    return "unknown"


def calculate_domain_proportions(records: List[Dict[str, Any]]) -> Dict[str, float]:
    """Calculate the proportion of each domain in the dataset."""
    domains = [extract_domain_from_record(r) for r in records]
    counts = Counter(domains)
    total = len(domains)
    
    if total == 0:
        return {}
    
    return {domain: count / total for domain, count in counts.items()}


def create_balanced_subsample(
    records: List[Dict[str, Any]], 
    max_proportion: float = MAX_DOMAIN_PROPORTION
) -> List[Dict[str, Any]]:
    """
    Create a subsample where no single domain exceeds max_proportion of the total.
    
    Strategy:
    1. Identify the total size N we can keep.
    2. Calculate the maximum allowed count for each domain (N * max_proportion).
    3. Iterate through domains. If a domain exceeds its limit, sample down to the limit.
       If it doesn't, keep all.
    4. Recalculate N if the initial guess was too high (iterative approach) or
       simply cap the largest domains to a global N derived from the largest domain.
    
    Simplified robust approach:
    - Find the largest domain count: C_max.
    - To satisfy C_max <= N * max_proportion, we need N >= C_max / max_proportion.
    - Let N_target = ceil(C_max / max_proportion).
    - We will try to keep N_target records total.
    - For each domain, the max allowed is floor(N_target * max_proportion).
    - If a domain has fewer than that, we keep all.
    - If a domain has more, we sample down.
    - If the sum of kept records < N_target, we might need to adjust, but usually
      capping the largest is the constraint.
    """
    if not records:
        return []

    # Group by domain
    domain_groups: Dict[str, List[Dict[str, Any]]] = {}
    for record in records:
        domain = extract_domain_from_record(record)
        if domain not in domain_groups:
            domain_groups[domain] = []
        domain_groups[domain].append(record)
    
    # Find the maximum count of any single domain
    max_domain_count = max(len(group) for group in domain_groups.values())
    
    # Calculate the minimum total N required to accommodate the largest domain
    # N * max_proportion >= max_domain_count  =>  N >= max_domain_count / max_proportion
    import math
    min_total_n = math.ceil(max_domain_count / max_proportion)
    
    # We will aim for a total of min_total_n records, but we can't exceed the total available
    target_total = min(min_total_n, len(records))
    
    # Calculate the cap for each domain based on the target total
    # Cap = floor(target_total * max_proportion)
    domain_cap = math.floor(target_total * max_proportion)
    
    subsample = []
    
    # Randomly shuffle groups to avoid bias in selection order if we were picking specific indices
    # (Though here we are just taking the first N of a shuffled list)
    import random
    random.seed(42) # Deterministic for reproducibility per T010
    
    for domain, group in domain_groups.items():
        count = len(group)
        if count <= domain_cap:
            subsample.extend(group)
        else:
            # Sample down to the cap
            # Shuffle to ensure random selection
            shuffled = group.copy()
            random.shuffle(shuffled)
            subsample.extend(shuffled[:domain_cap])
    
    # If we ended up with more than target_total due to rounding/flooring logic
    # (e.g. many domains just under the cap), we might need to trim randomly
    if len(subsample) > target_total:
        random.shuffle(subsample)
        subsample = subsample[:target_total]
    
    # Log the result
    final_counts = Counter(extract_domain_from_record(r) for r in subsample)
    logger.info(f"Subsample created. Total records: {len(subsample)}")
    for domain, count in sorted(final_counts.items()):
        prop = count / len(subsample) if subsample else 0
        logger.info(f"  {domain}: {count} ({prop:.2%})")
        
    return subsample


def write_subsample_to_csv(records: List[Dict[str, Any]], output_path: Path) -> None:
    """Write the subsampled records to a CSV file."""
    if not records:
        logger.warning("No records to write.")
        output_path.parent.mkdir(parents=True, exist_ok=True)
        with open(output_path, 'w', newline='', encoding='utf-8') as f:
            f.write("") # Empty file
        return

    # Determine headers from the first record
    fieldnames = list(records[0].keys())
    
    output_path.parent.mkdir(parents=True, exist_ok=True)
    
    with open(output_path, 'w', newline='', encoding='utf-8') as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(records)
    
    logger.info(f"Subsample written to {output_path}")


def run_domain_subsample(
    input_json: Optional[Path] = None, 
    input_csv: Optional[Path] = None, 
    output_path: Optional[Path] = None
) -> Tuple[int, int]:
    """
    Main execution function for domain subsampling.
    
    Args:
        input_json: Path to the audit_report.json
        input_csv: Fallback path to summary CSV
        output_path: Path for the output subsampled CSV
    
    Returns:
        Tuple of (original_count, subsampled_count)
    """
    input_json = input_json or INPUT_JSON_PATH
    input_csv = input_csv or INPUT_CSV_PATH
    output_path = output_path or OUTPUT_PATH
    
    logger.info(f"Starting domain subsampling. Input: {input_json}")
    
    records = []
    try:
        records = load_audit_records_from_json(input_json)
    except FileNotFoundError:
        logger.warning(f"JSON input {input_json} not found. Trying CSV fallback.")
        try:
            records = load_summaries_from_csv(input_csv)
        except FileNotFoundError:
            logger.error("No input data found. Cannot proceed.")
            raise
    
    original_count = len(records)
    if original_count == 0:
        logger.warning("Input data is empty.")
        write_subsample_to_csv([], output_path)
        return 0, 0
    
    proportions = calculate_domain_proportions(records)
    logger.info(f"Original domain proportions: {proportions}")
    
    subsampled_records = create_balanced_subsample(records, MAX_DOMAIN_PROPORTION)
    subsampled_count = len(subsampled_records)
    
    write_subsample_to_csv(subsampled_records, output_path)
    
    return original_count, subsampled_count


def main() -> int:
    """CLI entry point."""
    logging.basicConfig(level=logging.INFO)
    try:
        orig, sub = run_domain_subsample()
        print(f"Successfully subsampled {orig} records to {sub} records.")
        print(f"Output written to: {OUTPUT_PATH}")
        return 0
    except Exception as e:
        logger.error(f"Error during domain subsampling: {e}")
        return 1


if __name__ == "__main__":
    import sys
    sys.exit(main())
