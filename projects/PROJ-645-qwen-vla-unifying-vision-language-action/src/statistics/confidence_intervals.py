def load_eval_results(input_path: str = "data/eval_results.json") -> Dict[str, Any]:
    """
    Loads the evaluation results JSON containing success-rate vectors.
    Expected structure:
    {
      "within-embodiment": { "success_rate": [0.8, 0.85, ...], ... },
      "cross-embodiment": { "success_rate": [0.6, 0.65, ...], ... }
    }
    Or potentially nested by ratio if T025 aggregates them first,
    but per T026 spec, we read the base eval results.
    """
    path = Path(input_path)
    if not path.exists():
        raise FileNotFoundError(f"Input file not found: {input_path}. "
                                f"Ensure T016 (libero_eval) has run.")

    with open(path, 'r', encoding='utf-8') as f:
        return json.load(f)

def bootstrap_confidence_interval(
    data: List[float],
    n_iterations: int = 1000,
    confidence_level: float = 0.95,
    seed: Optional[int] = None
) -> Dict[str, float]:
    """
    Computes a non-parametric bootstrap confidence interval for the mean of the data.

    Args:
        data: List of success rates (one per seed).
        n_iterations: Number of bootstrap resamples.
        confidence_level: Confidence level (e.g., 0.95).
        seed: Random seed for reproducibility.

    Returns:
        Dictionary with keys: 'mean', 'ci_lower', 'ci_upper', 'method'.
    """
    if not data:
        raise ValueError("Data list cannot be empty.")

    if seed is not None:
        random.seed(seed)

    n = len(data)
    bootstrap_means = []

    for _ in range(n_iterations):
        # Resample with replacement
        resample = [data[random.randint(0, n - 1)] for _ in range(n)]
        bootstrap_means.append(sum(resample) / n)

    bootstrap_means.sort()

    alpha = 1 - confidence_level
    lower_idx = int((alpha / 2) * n_iterations)
    upper_idx = int((1 - (alpha / 2)) * n_iterations)

    # Clamp indices just in case
    lower_idx = max(0, lower_idx)
    upper_idx = min(n_iterations - 1, upper_idx)

    ci_lower = bootstrap_means[lower_idx]
    ci_upper = bootstrap_means[upper_idx]
    mean_val = sum(data) / n

    return {
        "mean": mean_val,
        "ci_lower": ci_lower,
        "ci_upper": ci_upper,
        "method": "bootstrapping_non_parametric"
    }

def compute_ci_for_ratio(
    results: Dict[str, Any],
    ratio: float,
    seed: Optional[int] = None
) -> Dict[str, Any]:
    """
    Extracts success rates for a specific ratio from the eval results
    and computes the confidence interval.

    Args:
        results: The full eval_results.json content.
        ratio: The data composition ratio (0.0, 0.5, or 1.0).
        seed: Seed for bootstrapping.

    Returns:
        Dictionary containing ratio, mean, ci_lower, ci_upper, and raw data.
    """
    # The structure of eval_results might vary based on how T025/T016 organizes it.
    # Per T016 spec, it outputs 'within-embodiment' and 'cross-embodiment' keys.
    # However, T026 (Ablation) implies we need to compare different ratios.
    # If T016 only ran for one ratio (the default 1.0), we need to handle that.
    # Assuming the input 'results' might be structured as:
    # { "ratio_0.0": {...}, "ratio_0.5": {...}, "ratio_1.0": {...} }
    # OR the T025 runner aggregates them.
    #
    # If the input is just the base T016 output, it likely doesn't have multiple ratios yet.
    # But T026 spec says: "Input: Read data/eval_results.json ... for each ratio".
    # This implies the file might be updated by T025 or we are processing a specific run.
    #
    # Let's assume a flexible structure:
    # If 'ratio' key exists in results, use it.
    # If results contains 'within-embodiment' and we are looking for a specific ratio,
    # we might need to look for keys like "within-embodiment_0.0".
    #
    # Given the task description "Read data/eval_results.json ... containing success-rate vectors",
    # and the context of T025 running 3 times, it's most likely the file structure is:
    # {
    #   "0.0": { "within-embodiment": { "success_rate": [...] }, ... },
    #   "0.5": { ... },
    #   "1.0": { ... }
    # }
    # OR the T025 script writes a summary file.
    #
    # Let's implement a robust lookup:
    success_rates = None

    # Strategy 1: Direct key match
    if str(ratio) in results:
        subset = results[str(ratio)]
        if "within-embodiment" in subset:
            success_rates = subset["within-embodiment"].get("success_rate")
        elif "success_rate" in subset:
            success_rates = subset.get("success_rate")

    # Strategy 2: Nested within-embodiment/cross-embodiment if only one ratio exists in file
    elif "within-embodiment" in results:
        # If the file only has one ratio (e.g. 1.0), and we are asked for 1.0, use it.
        if ratio == 1.0:
            success_rates = results["within-embodiment"].get("success_rate")

    # Strategy 3: Check for specific keys like "within-embodiment_0.0"
    if success_rates is None:
        key = f"within-embodiment_{ratio}"
        if key in results:
            success_rates = results[key].get("success_rate")
        elif ratio in results:
            # Fallback to ratio key
            val = results[ratio]
            if isinstance(val, dict) and "success_rate" in val:
                success_rates = val["success_rate"]

    if success_rates is None:
        # Fallback: try to find any list of success rates if structure is unknown
        # This is a last resort
        logger.warning(f"Could not find success rates for ratio {ratio} in standard keys. Attempting scan.")
        for k, v in results.items():
            if isinstance(v, dict) and "success_rate" in v:
                if isinstance(v["success_rate"], list):
                    success_rates = v["success_rate"]
                    break

    if success_rates is None:
        raise KeyError(f"Could not locate success-rate vector for ratio {ratio} in input file.")

    if not isinstance(success_rates, list) or len(success_rates) == 0:
        raise ValueError(f"Success rate data for ratio {ratio} is empty or invalid.")

    ci_result = bootstrap_confidence_interval(success_rates, seed=seed)
    ci_result["ratio"] = ratio
    ci_result["n_seeds"] = len(success_rates)
    ci_result["raw_data"] = success_rates

    return ci_result

def run_ablation_ci_analysis(
    input_path: str = "data/eval_results.json",
    output_path: str = "data/ci_results.json",
    ratios: List[float] = [0.0, 0.5, 1.0],
    seed: Optional[int] = None
) -> Dict[str, Dict[str, Any]]:
    """
    Orchestrates the CI computation for all specified ratios.

    Args:
        input_path: Path to eval_results.json.
        output_path: Path to write the aggregated CI results.
        ratios: List of ratios to process.
        seed: Random seed for bootstrapping.

    Returns:
        Dictionary of results keyed by ratio.
    """
    logger.info(f"Loading evaluation results from {input_path}")
    results_data = load_eval_results(input_path)

    final_results = {}
    for r in ratios:
        try:
            logger.info(f"Computing CI for ratio: {r}")
            final_results[str(r)] = compute_ci_for_ratio(results_data, r, seed=seed)
        except (KeyError, ValueError) as e:
            logger.error(f"Failed to compute CI for ratio {r}: {e}")
            final_results[str(r)] = {"error": str(e), "ratio": r}

    # Write output
    Path(output_path).parent.mkdir(parents=True, exist_ok=True)
    with open(output_path, 'w', encoding='utf-8') as f:
        json.dump(final_results, f, indent=2)

    logger.info(f"CI results written to {output_path}")
    return final_results

def main():
    """CLI entry point for T026."""
    import argparse

    parser = argparse.ArgumentParser(description="Compute 95% CIs via bootstrapping for ablation study.")
    parser.add_argument("--input", type=str, default="data/eval_results.json", help="Input eval results JSON")
    parser.add_argument("--output", type=str, default="data/ci_results.json", help="Output CI results JSON")
    parser.add_argument("--ratios", type=float, nargs="+", default=[0.0, 0.5, 1.0], help="Ratios to process")
    parser.add_argument("--seed", type=int, default=None, help="Random seed for bootstrapping")

    args = parser.parse_args()

    setup_logging()
    run_ablation_ci_analysis(
        input_path=args.input,
        output_path=args.output,
        ratios=args.ratios,
        seed=args.seed
    )

if __name__ == "__main__":
    main()
