"""
Generate a curated Markdown report of top stability candidates.

This script reads the full ranked list of hypothetical perovskites from
`results/screening_full.csv`, selects the top N candidates, and generates
a Markdown report `results/screening_candidates.md` containing:
- A summary table of top candidates (formula, predicted energy, descriptors).
- Highlighting for candidates below the thermodynamic stability threshold.
- Metadata about the screening process.

Dependencies:
- results/screening_full.csv (generated by T040)
- utils/config.py (for threshold constants if needed)
"""
import os
import sys
import logging
import pandas as pd
from pathlib import Path
from typing import List, Dict, Any

# Add project root to path for imports
PROJECT_ROOT = Path(__file__).resolve().parents[2]
sys.path.insert(0, str(PROJECT_ROOT))

from utils.logging_config import get_logger, log_pipeline_event

# Configuration
RESULTS_DIR = PROJECT_ROOT / "results"
INPUT_FILE = RESULTS_DIR / "screening_full.csv"
OUTPUT_FILE = RESULTS_DIR / "screening_candidates.md"
TOP_N = 20
STABILITY_THRESHOLD = -0.1  # eV/atom, candidates below this are highlighted

logger = get_logger(__name__)

def load_ranked_candidates() -> pd.DataFrame:
    """Load the full ranked list of candidates."""
    if not INPUT_FILE.exists():
        raise FileNotFoundError(
            f"Input file not found: {INPUT_FILE}. "
            "Please run T040 (predict.py) first to generate screening_full.csv."
        )
    
    df = pd.read_csv(INPUT_FILE)
    logger.info(f"Loaded {len(df)} candidates from {INPUT_FILE}")
    return df

def select_top_candidates(df: pd.DataFrame, n: int) -> pd.DataFrame:
    """Select the top N candidates sorted by predicted energy."""
    # Ensure sorted by predicted_energy ascending (most stable first)
    df_sorted = df.sort_values(by="predicted_energy").head(n)
    logger.info(f"Selected top {n} candidates")
    return df_sorted

def generate_markdown_report(df: pd.DataFrame, threshold: float) -> str:
    """Generate a Markdown report string for the top candidates."""
    lines = []
    lines.append("# Top Perovskite Stability Candidates")
    lines.append("")
    lines.append(f"## Summary")
    lines.append(f"This report lists the top **{len(df)}** most stable hypothetical perovskite candidates")
    lines.append(f"identified through virtual screening. Candidates are ranked by predicted")
    lines.append(f"decomposition energy (eV/atom), where lower values indicate higher stability.")
    lines.append("")
    lines.append(f"**Stability Threshold:** < {threshold} eV/atom (highlighted in bold)")
    lines.append("")
    lines.append("## Top Candidates Table")
    lines.append("")
    
    # Define columns to display
    display_cols = [
        "formula", 
        "predicted_energy", 
        "tolerance_factor", 
        "octahedral_factor", 
        "ionic_mismatch",
        "electronegativity_diff"
    ]
    
    # Filter dataframe to only display columns
    df_display = df[display_cols].copy()
    
    # Format numeric columns
    for col in ["predicted_energy", "tolerance_factor", "octahedral_factor", 
                "ionic_mismatch", "electronegativity_diff"]:
        df_display[col] = df_display[col].apply(lambda x: f"{x:.4f}")
    
    # Create markdown table
    lines.append("| Formula | Pred. Energy (eV/atom) | Tolerance (t) | Octahedral (μ) | Ionic Mismatch | Δχ (EN) |")
    lines.append("|---|---|---|---|---|---|")
    
    for _, row in df_display.iterrows():
        is_stable = float(row["predicted_energy"]) < threshold
        formula = row["formula"]
        energy = row["predicted_energy"]
        t = row["tolerance_factor"]
        mu = row["octahedral_factor"]
        mismatch = row["ionic_mismatch"]
        dchi = row["electronegativity_diff"]
        
        if is_stable:
            # Bold stable candidates
            lines.append(f"| **{formula}** | **{energy}** | {t} | {mu} | {mismatch} | {dchi} |")
        else:
            lines.append(f"| {formula} | {energy} | {t} | {mu} | {mismatch} | {dchi} |")
    
    lines.append("")
    lines.append("## Methodology")
    lines.append("- **Source:** Combinatorial library of ABX₃ structures (A=K,Rb,Cs,Ba,Sr; B=Ti,Zr,Hf,Sn,Ge; X=F,Cl,Br,I)")
    lines.append("- **Filtering:** Geometric feasibility (0.8 ≤ t ≤ 1.1) and Out-of-Distribution checks")
    lines.append("- **Model:** RandomForestRegressor trained on Materials Project/OQMD data")
    lines.append("- **Metric:** Predicted decomposition energy (lower is more stable)")
    lines.append("")
    lines.append("---")
    lines.append(f"*Generated by llmXive pipeline on {pd.Timestamp.now().strftime('%Y-%m-%d %H:%M:%S')}*")
    
    return "\n".join(lines)

def save_report(content: str, output_path: Path):
    """Save the Markdown report to disk."""
    with open(output_path, "w", encoding="utf-8") as f:
        f.write(content)
    logger.info(f"Report saved to {output_path}")

def main():
    """Main entry point for generating the candidates report."""
    log_pipeline_event("Starting T041: Generate screening candidates report")
    
    try:
        # 1. Load data
        df = load_ranked_candidates()
        
        # 2. Select top candidates
        top_df = select_top_candidates(df, TOP_N)
        
        # 3. Generate report
        report_content = generate_markdown_report(top_df, STABILITY_THRESHOLD)
        
        # 4. Save report
        save_report(report_content, OUTPUT_FILE)
        
        log_pipeline_event("T041 completed successfully")
        return 0
        
    except FileNotFoundError as e:
        logger.error(f"Missing input data: {e}")
        return 1
    except Exception as e:
        logger.error(f"Unexpected error during report generation: {e}")
        return 1

if __name__ == "__main__":
    sys.exit(main())