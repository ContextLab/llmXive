"""
Task T032: Verify sensitivity consistency (SC-005).

Checks if the sign and significance of the quadratic term remain consistent
when using alternative metrics (e.g., Sentence Length vs. MTLD) as predictors
in the univariate regression models.

This script loads the regression results generated by `code/analyze.py`,
specifically looking at the quadratic terms for FK, MTLD, and Avg Sentence Length.
It verifies that the direction (sign) of the non-linear effect is consistent
across these metrics and that they are all significant (or all non-significant)
after Bonferroni correction, as required by SC-005.
"""
import os
import sys
import json
import logging
from pathlib import Path

# Configure logging
logging.basicConfig(
    level=logging.INFO,
    format='%(asctime)s - %(levelname)s - %(message)s'
)
logger = logging.getLogger(__name__)

# Paths
PROJECT_ROOT = Path(__file__).parent.parent
RESULTS_FILE = PROJECT_ROOT / "data" / "outputs" / "regression_results.json"
OUTPUT_REPORT = PROJECT_ROOT / "data" / "outputs" / "sensitivity_consistency_report.json"

def load_regression_results():
    """Load the regression results JSON generated by analyze.py."""
    if not RESULTS_FILE.exists():
        raise FileNotFoundError(
            f"Regression results file not found at {RESULTS_FILE}. "
            "Please run code/analyze.py first."
        )
    with open(RESULTS_FILE, 'r') as f:
        return json.load(f)

def check_sensitivity_consistency(results):
    """
    Analyze the regression results to check for sensitivity consistency (SC-005).

    Criteria:
    1. Identify quadratic terms for FK, MTLD, and Avg Sentence Length.
    2. Check if the sign of the quadratic coefficient is consistent across metrics.
    3. Check if the significance (p < adjusted_alpha) is consistent across metrics.
    4. Log the findings and return a pass/fail status.

    Returns:
        dict: Report containing the analysis details and pass/fail status.
    """
    models = results.get("models", {})
    adjusted_alpha = results.get("bonferroni", {}).get("adjusted_alpha", 0.05)

    metrics_of_interest = ["flesch_kincaid", "mtld", "avg_sentence_length"]
    quadratic_results = {}
    consistency_checks = []

    logger.info(f"Checking sensitivity consistency with adjusted alpha: {adjusted_alpha}")

    for metric in metrics_of_interest:
        model_data = models.get(metric)
        if not model_data:
            logger.warning(f"No model data found for metric: {metric}")
            continue

        # Look for the quadratic term (usually named {metric}_sq or similar)
        # The analyze.py script typically stores coefficients in a 'coefficients' dict
        coeffs = model_data.get("coefficients", {})
        
        # Identify the quadratic coefficient key
        # Based on standard naming in analyze.py: f"{metric}_sq"
        quad_key = f"{metric}_sq"
        quad_coeff = coeffs.get(quad_key)
        quad_pval = None

        # If exact key not found, search for keys containing the metric name and 'sq' or 'quadratic'
        if quad_coeff is None:
            for key, val in coeffs.items():
                if metric.replace("_", "") in key.lower() and ("sq" in key.lower() or "quad" in key.lower()):
                    quad_coeff = val
                    quad_pval = model_data.get("p_values", {}).get(key)
                    break

        if quad_coeff is None:
            logger.warning(f"Quadratic term for {metric} not found in coefficients.")
            continue

        # Get p-value if not found in loop
        if quad_pval is None:
            pvals = model_data.get("p_values", {})
            # Try to find the p-value for the quadratic term
            for key, p_val in pvals.items():
                if key == quad_key or (metric.replace("_", "") in key.lower() and ("sq" in key.lower() or "quad" in key.lower())):
                    quad_pval = p_val
                    break

        is_significant = False
        if quad_pval is not None:
            is_significant = quad_pval < adjusted_alpha

        quadratic_results[metric] = {
            "coefficient": quad_coeff,
            "p_value": quad_pval,
            "is_significant": is_significant,
            "sign": "positive" if quad_coeff > 0 else ("negative" if quad_coeff < 0 else "zero")
        }

    # Perform consistency checks
    signs = [r["sign"] for r in quadratic_results.values() if r["sign"] != "zero"]
    significant_flags = [r["is_significant"] for r in quadratic_results.values()]

    consistent_signs = len(set(signs)) <= 1 if signs else False
    consistent_significance = len(set(significant_flags)) <= 1 if significant_flags else False

    # SC-005 Requirement: Consistency in sign and significance
    # If we have valid results, we expect them to be consistent.
    passed = consistent_signs and consistent_significance

    report = {
        "task_id": "T032",
        "description": "Sensitivity Consistency Check (SC-005)",
        "adjusted_alpha": adjusted_alpha,
        "metrics_analyzed": list(quadratic_results.keys()),
        "results": quadratic_results,
        "consistency_checks": {
            "sign_consistency": {
                "unique_signs": list(set(signs)),
                "consistent": consistent_signs
            },
            "significance_consistency": {
                "significant_count": sum(significant_flags),
                "total_count": len(significant_flags),
                "consistent": consistent_significance
            }
        },
        "passed": passed,
        "reasoning": (
            "SC-005 passed: Quadratic terms show consistent signs and significance across metrics."
            if passed else
            "SC-005 failed: Inconsistency detected in signs or significance of quadratic terms across metrics."
        )
    }

    logger.info(f"Sensitivity Consistency Check Result: {'PASSED' if passed else 'FAILED'}")
    logger.info(f"Reasoning: {report['reasoning']}")

    return report

def save_report(report):
    """Save the sensitivity consistency report to a JSON file."""
    OUTPUT_REPORT.parent.mkdir(parents=True, exist_ok=True)
    with open(OUTPUT_REPORT, 'w') as f:
        json.dump(report, f, indent=2)
    logger.info(f"Sensitivity consistency report saved to {OUTPUT_REPORT}")

def main():
    """Main entry point for the script."""
    try:
        logger.info("Starting sensitivity consistency verification (T032)...")
        results = load_regression_results()
        report = check_sensitivity_consistency(results)
        save_report(report)
        
        if report["passed"]:
            logger.info("Task T032 completed successfully.")
            sys.exit(0)
        else:
            # Note: SC-005 is a verification check. If it fails, it might be due to data
            # characteristics, but the script itself ran correctly.
            # We exit 0 to indicate the script ran, but the report reflects the failure.
            # However, for strict CI, we might want to fail if the hypothesis is not supported.
            # Given the prompt "Verify sensitivity consistency", we report the status.
            logger.warning("Task T032 completed, but sensitivity consistency check FAILED.")
            sys.exit(0) 
    except Exception as e:
        logger.error(f"Error during verification: {e}")
        sys.exit(1)

if __name__ == "__main__":
    main()