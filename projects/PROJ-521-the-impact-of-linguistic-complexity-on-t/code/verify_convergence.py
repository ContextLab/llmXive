"""
T031: Verify regression model convergence and quadratic term significance.

This script loads the regression results generated by analyze.py,
verifies that at least one metric's model converged (no NaN/Inf coefficients),
and confirms that the quadratic term significance (p < 0.05) is reported.

Outputs a verification report to data/outputs/convergence_verification.json
and logs the status to the console.
"""
import os
import sys
import json
import logging
from pathlib import Path

# Configure logging
logging.basicConfig(
    level=logging.INFO,
    format='%(asctime)s - %(levelname)s - %(message)s'
)
logger = logging.getLogger(__name__)

PROJECT_ROOT = Path(__file__).parent.parent
RESULTS_PATH = PROJECT_ROOT / "data" / "outputs" / "regression_results.json"
VERIFICATION_OUTPUT = PROJECT_ROOT / "data" / "outputs" / "convergence_verification.json"

def load_results():
    """Load regression results from JSON."""
    if not RESULTS_PATH.exists():
        logger.error(f"Results file not found: {RESULTS_PATH}")
        return None
    
    try:
        with open(RESULTS_PATH, 'r', encoding='utf-8') as f:
            return json.load(f)
    except json.JSONDecodeError as e:
        logger.error(f"Failed to parse results JSON: {e}")
        return None

def check_convergence_and_significance(results):
    """
    Verify SC-004 (convergence) and SC-003 (quadratic significance).
    
    Returns:
        dict: Verification results with status and details
    """
    metrics = results.get("metrics", {})
    convergence_checks = {}
    significance_checks = {}
    
    has_converged = False
    has_significant_quadratic = False
    significant_metrics = []

    for metric_name, model_data in metrics.items():
        # Check for NaN/Inf in coefficients
        coeffs = model_data.get("coefficients", {})
        is_converged = True
        
        for key, value in coeffs.items():
            if value is None or (isinstance(value, float) and (value != value or abs(value) == float('inf'))):
                is_converged = False
                break
        
        convergence_checks[metric_name] = is_converged
        if is_converged:
            has_converged = True

        # Check quadratic term significance (p < 0.05)
        quadratic_p = model_data.get("quadratic_p_value")
        if quadratic_p is not None:
            is_significant = quadratic_p < 0.05
            significance_checks[metric_name] = {
                "p_value": quadratic_p,
                "significant": is_significant
            }
            if is_significant:
                has_significant_quadratic = True
                significant_metrics.append(metric_name)
        else:
            significance_checks[metric_name] = {
                "p_value": None,
                "significant": False,
                "reason": "Quadratic p-value not reported"
            }

    return {
        "convergence": {
            "passed": has_converged,
            "details": convergence_checks,
            "message": "At least one model converged" if has_converged else "No models converged"
        },
        "quadratic_significance": {
            "passed": has_significant_quadratic,
            "significant_metrics": significant_metrics,
            "details": significance_checks,
            "message": f"Quadratic term significant for: {significant_metrics}" if has_significant_quadratic else "No significant quadratic terms found"
        },
        "overall_status": "PASS" if (has_converged and has_significant_quadratic) else "FAIL"
    }

def save_verification_report(verification_data):
    """Save verification report to JSON."""
    VERIFICATION_OUTPUT.parent.mkdir(parents=True, exist_ok=True)
    with open(VERIFICATION_OUTPUT, 'w', encoding='utf-8') as f:
        json.dump(verification_data, f, indent=2)
    logger.info(f"Verification report saved to: {VERIFICATION_OUTPUT}")

def main():
    """Main entry point for T031 verification."""
    logger.info("Starting T031: Convergence and Significance Verification")
    
    results = load_results()
    if results is None:
        logger.error("T031 FAILED: Could not load regression results")
        print(json.dumps({"status": "FAIL", "reason": "Results file missing or invalid"}))
        sys.exit(1)

    verification = check_convergence_and_significance(results)
    
    # Log results
    logger.info(f"Convergence Check: {verification['convergence']['message']}")
    logger.info(f"Significance Check: {verification['quadratic_significance']['message']}")
    logger.info(f"Overall Status: {verification['overall_status']}")

    save_verification_report(verification)

    # Exit with appropriate code
    if verification['overall_status'] == "FAIL":
        logger.error("T031 FAILED: Requirements SC-003 or SC-004 not met")
        print(json.dumps(verification))
        sys.exit(1)
    
    logger.info("T031 PASSED: All convergence and significance requirements met")
    print(json.dumps(verification))
    sys.exit(0)

if __name__ == "__main__":
    main()