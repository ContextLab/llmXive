"""
Runtime verification module for the Memory Palaces project.

This module provides utilities to verify that the total runtime of the
training and evaluation pipeline stays within the specified limit (5 hours).
It reads the run summary generated by main.py and produces a runtime report.
"""
import json
import os
import sys
import time
from pathlib import Path
from typing import Dict, Any, Optional

# Constants
RUNTIME_LIMIT_SECONDS = 5 * 60 * 60  # 5 hours in seconds
RUN_SUMMARY_PATH = Path("artifacts/results/run_summary.json")
RUNTIME_REPORT_PATH = Path("artifacts/results/runtime_report.json")


def load_run_summary() -> Optional[Dict[str, Any]]:
    """
    Load the run summary JSON file generated by main.py.
    
    Returns:
        Dict containing run summary data, or None if file doesn't exist.
    """
    if not RUN_SUMMARY_PATH.exists():
        print(f"Error: Run summary file not found at {RUN_SUMMARY_PATH}", file=sys.stderr)
        return None
    
    try:
        with open(RUN_SUMMARY_PATH, 'r', encoding='utf-8') as f:
            return json.load(f)
    except json.JSONDecodeError as e:
        print(f"Error: Failed to parse run summary JSON: {e}", file=sys.stderr)
        return None
    except Exception as e:
        print(f"Error: Failed to read run summary file: {e}", file=sys.stderr)
        return None


def verify_runtime(run_summary: Dict[str, Any]) -> Dict[str, Any]:
    """
    Verify that the total runtime is within the 5-hour limit.
    
    Args:
        run_summary: Dictionary containing run summary data with 'runtime_seconds' key.
    
    Returns:
        Dictionary with 'runtime_seconds' and 'within_limit' boolean.
    """
    if 'runtime_seconds' not in run_summary:
        raise KeyError("run_summary missing required key 'runtime_seconds'")
    
    runtime_seconds = run_summary['runtime_seconds']
    within_limit = runtime_seconds <= RUNTIME_LIMIT_SECONDS
    
    return {
        'runtime_seconds': runtime_seconds,
        'within_limit': within_limit
    }


def write_runtime_report(report_data: Dict[str, Any]) -> None:
    """
    Write the runtime report to the specified output file.
    
    Args:
        report_data: Dictionary containing runtime report data.
    """
    # Ensure the output directory exists
    RUNTIME_REPORT_PATH.parent.mkdir(parents=True, exist_ok=True)
    
    with open(RUNTIME_REPORT_PATH, 'w', encoding='utf-8') as f:
        json.dump(report_data, f, indent=2)
    
    print(f"Runtime report written to {RUNTIME_REPORT_PATH}")


def main() -> int:
    """
    Main entry point for the runtime verification script.
    
    This function:
    1. Loads the run summary from main.py
    2. Verifies the runtime is within the 5-hour limit
    3. Writes the runtime report to artifacts/results/runtime_report.json
    
    Returns:
        0 on success, 1 on failure.
    """
    print("Starting runtime verification...")
    
    # Load run summary
    run_summary = load_run_summary()
    if run_summary is None:
        print("Failed to load run summary. Exiting.")
        return 1
    
    # Verify runtime
    try:
        report_data = verify_runtime(run_summary)
    except KeyError as e:
        print(f"Error: Invalid run summary format: {e}")
        return 1
    except Exception as e:
        print(f"Error during runtime verification: {e}")
        return 1
    
    # Write report
    try:
        write_runtime_report(report_data)
    except Exception as e:
        print(f"Error writing runtime report: {e}")
        return 1
    
    # Print summary
    print(f"\nRuntime Verification Results:")
    print(f"  Total runtime: {report_data['runtime_seconds']:.2f} seconds")
    print(f"  Limit: {RUNTIME_LIMIT_SECONDS} seconds (5 hours)")
    print(f"  Within limit: {report_data['within_limit']}")
    
    if not report_data['within_limit']:
        print("\nWARNING: Runtime exceeded the 5-hour limit!")
        return 1
    
    print("\nRuntime verification passed.")
    return 0


if __name__ == "__main__":
    sys.exit(main())