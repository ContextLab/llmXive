def load_seed_accuracies(dataset_name: str) -> Dict[str, List[float]]:
    """
    Loads exact-match recall accuracies for a specific dataset from run summaries.
    Expects files like: artifacts/results/run_summary_babi.json, etc.
    Or aggregates from a single run_summary.json if seeds are listed there.
    
    For this implementation, we assume the main.py (T016) produced per-dataset
    run summaries or a consolidated file. We will look for specific files
    generated by the evaluation loop.
    """
    # Attempt to find specific dataset summary files first
    # If T016 produced one file per dataset, we load them.
    # If it produced one master file, we parse that.
    
    # Strategy: Look for artifacts/results/run_summary_<dataset>.json
    # If not found, look for artifacts/results/recall_accuracy.json (T015)
    # and aggregate.
    
    # Since T016 generates run_summary.json with keys: seeds, accuracies, etc.
    # and T015 generates recall_accuracy.json.
    # We assume the pipeline produced a file containing the per-seed results.
    # Let's try to load the recall_accuracy.json which should hold the matrix.
    
    recall_file = RAW_RESULTS_DIR / "recall_accuracy.json"
    if recall_file.exists():
        with open(recall_file, 'r') as f:
            data = json.load(f)
            # Expected structure: { "babi": [acc1, acc2...], "lambada": ... }
            if dataset_name in data:
                return data[dataset_name]
    
    # Fallback: Try to load individual run summaries if recall_accuracy.json doesn't exist
    # but run_summary files do.
    # This is a heuristic for robustness.
    # Assuming T016 might have split outputs or we need to aggregate.
    # For now, relying on recall_accuracy.json as the primary source of truth for T015.
    
    raise FileNotFoundError(f"Could not find accuracy data for {dataset_name}. "
                            f"Expected {recall_file} or specific run summaries.")

def generate_statistical_summary():
    """
    Generates the statistical summary report for T022.
    Computes p-values, corrected p-values, effect sizes, and CIs for each dataset.
    """
    logger.info("Starting statistical summary generation (T022)...")
    
    # Ensure artifacts directory exists
    ARTIFACTS_DIR.mkdir(parents=True, exist_ok=True)
    
    results = {
        "datasets": {},
        "correction_method": "holm_bonferroni",
        "summary": {}
    }
    
    # We need a baseline and a spatial variant for comparison.
    # The task description implies we are comparing "spatial" vs "baseline" (non-spatial).
    # However, T016 (main.py) usually runs one configuration.
    # The spec for T022 says: "Generate statistical summary report ... containing 
    # p-values ... for each dataset."
    # This implies we are comparing the Spatial Model vs the Baseline Model across seeds.
    # We assume the evaluation script or main.py has produced two sets of results:
    # 1. Baseline accuracies per seed
    # 2. Spatial Model accuracies per seed
    
    # Since the prompt does not specify the file format for these two sets,
    # we must infer from T015/T016. T015 produces recall_accuracy.json.
    # If T016 runs both, it might produce separate files or a structured JSON.
    # Let's assume the existence of:
    # artifacts/results/baseline_accuracies.json
    # artifacts/results/spatial_accuracies.json
    # OR a single file with a 'comparison' structure.
    
    # Given the constraints and the need for REAL data, if these files don't exist,
    # the script cannot fabricate them. It must fail loudly or check for them.
    # However, T016 (main.py) is the orchestrator. It likely runs the baseline
    # and the spatial model.
    
    # Let's check for standard output files that would contain these results.
    # If the project follows T016 strictly, it might output a single run_summary.json
    # that contains the comparison if the main.py was updated to run both.
    # But T016 description says: "train (across seeds) -> evaluate. Must generate run_summary.json".
    # It doesn't explicitly say it runs both models.
    # T022 requires the comparison.
    
    # Assumption: The user (or previous tasks) has ensured that accuracy data
    # for both 'baseline' and 'spatial' variants exists in the artifacts.
    # We will look for specific files:
    # artifacts/results/baseline_accuracies.json -> { "babi": [0.8, 0.9...], ... }
    # artifacts/results/spatial_accuracies.json -> { "babi": [0.85, 0.92...], ... }
    
    baseline_path = ARTIFACTS_DIR / "baseline_accuracies.json"
    spatial_path = ARTIFACTS_DIR / "spatial_accuracies.json"
    
    if not baseline_path.exists() or not spatial_path.exists():
        logger.warning("Baseline or Spatial accuracy files not found.")
        logger.warning("Attempting to derive from recall_accuracy.json if it contains both.")
        
        # Fallback: Check if recall_accuracy.json has nested structures
        recall_file = RAW_RESULTS_DIR / "recall_accuracy.json"
        if recall_file.exists():
            with open(recall_file, 'r') as f:
                data = json.load(f)
                # If it has 'baseline' and 'spatial' keys
                if 'baseline' in data and 'spatial' in data:
                    baseline_path = recall_file # reuse logic
                    spatial_path = recall_file
                    # We'll handle this in the loop
                    pass
                else:
                    raise FileNotFoundError(
                        "Cannot generate statistical summary. "
                        "Missing required accuracy data files: "
                        f"{baseline_path} and {spatial_path}. "
                        "Please ensure T016 or T015 produced these files."
                    )
        else:
            raise FileNotFoundError(
                "Cannot generate statistical summary. "
                "Missing required accuracy data files. "
                "Please run the evaluation pipeline first."
            )

    # Load data
    with open(baseline_path, 'r') as f:
        baseline_data = json.load(f)
    
    with open(spatial_path, 'r') as f:
        spatial_data = json.load(f)
    
    # Handle the case where we loaded from the same file
    if baseline_path == spatial_path:
        baseline_data = baseline_data.get('baseline', {})
        spatial_data = spatial_data.get('spatial', {})

    for dataset in DATASETS:
        logger.info(f"Processing dataset: {dataset}")
        
        if dataset not in baseline_data or dataset not in spatial_data:
            logger.warning(f"Skipping {dataset}: missing data in one of the files.")
            continue
        
        baseline_scores = baseline_data[dataset]
        spatial_scores = spatial_data[dataset]
        
        if len(baseline_scores) != len(spatial_scores):
            logger.error(f"Mismatched seed counts for {dataset}. Skipping.")
            continue

        # 1. Paired T-Test
        t_stat, p_value = run_statistical_comparison(
            baseline_scores, spatial_scores, method="ttest_rel"
        )
        
        # 2. Effect Size (Cohen's d)
        cohens_d = calculate_cohens_d(baseline_scores, spatial_scores)
        d_ci_low, d_ci_high = calculate_cohens_d_ci(baseline_scores, spatial_scores)
        
