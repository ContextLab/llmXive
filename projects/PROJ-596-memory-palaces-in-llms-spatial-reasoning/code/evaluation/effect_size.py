"""
Effect size calculation module for User Story 2.
Implements Cohen's d calculation with 95% confidence intervals (FR-007).
"""
import json
import os
import sys
import logging
from pathlib import Path
from typing import Dict, List, Any, Optional, Tuple

import numpy as np
from scipy import stats

# Configure logging
logging.basicConfig(level=logging.INFO, format='%(asctime)s - %(levelname)s - %(message)s')
logger = logging.getLogger(__name__)


def compute_cohens_d(group1: List[float], group2: List[float]) -> float:
    """
    Compute Cohen's d effect size between two groups.

    Cohen's d = (mean1 - mean2) / pooled_std

    Args:
        group1: First group of values (e.g., spatial model accuracies)
        group2: Second group of values (e.g., baseline model accuracies)

    Returns:
        Cohen's d value
    """
    if len(group1) < 2 or len(group2) < 2:
        raise ValueError("Both groups must have at least 2 samples to compute Cohen's d.")

    mean1 = np.mean(group1)
    mean2 = np.mean(group2)
    diff = mean1 - mean2

    # Pooled standard deviation
    n1, n2 = len(group1), len(group2)
    var1 = np.var(group1, ddof=1)
    var2 = np.var(group2, ddof=1)

    pooled_std = np.sqrt(((n1 - 1) * var1 + (n2 - 1) * var2) / (n1 + n2 - 2))

    if pooled_std == 0:
        logger.warning("Pooled standard deviation is zero. Returning 0 for Cohen's d.")
        return 0.0

    return diff / pooled_std


def compute_cohens_d_confidence_interval(
    group1: List[float],
    group2: List[float],
    confidence_level: float = 0.95
) -> Tuple[float, float, float]:
    """
    Compute Cohen's d with 95% confidence interval using non-central t-distribution.

    This uses the method described by Hedges & Olkin (1985) and implemented
    in scipy.stats module for more accurate intervals.

    Args:
        group1: First group of values
        group2: Second group of values
        confidence_level: Confidence level for the interval (default 0.95)

    Returns:
        Tuple of (cohen_d, lower_bound, upper_bound)
    """
    if len(group1) < 2 or len(group2) < 2:
        raise ValueError("Both groups must have at least 2 samples to compute confidence interval.")

    cohen_d = compute_cohens_d(group1, group2)

    n1, n2 = len(group1), len(group2)
    df = n1 + n2 - 2

    # Standard error of Cohen's d
    # SE_d = sqrt((n1 + n2) / (n1 * n2) + d^2 / (2 * (n1 + n2)))
    # More accurate approximation using non-central t-distribution

    # Using the approximation from Hedges & Olkin (1985)
    se_d = np.sqrt((n1 + n2) / (n1 * n2) + (cohen_d ** 2) / (2 * (n1 + n2)))

    # Critical t-value for the confidence interval
    alpha = 1 - confidence_level
    t_crit = stats.t.ppf(1 - alpha / 2, df)

    lower_bound = cohen_d - t_crit * se_d
    upper_bound = cohen_d + t_crit * se_d

    return cohen_d, lower_bound, upper_bound


def get_cohen_interpretation(d: float) -> str:
    """
    Provide qualitative interpretation of Cohen's d value.

    Args:
        d: Cohen's d value

    Returns:
        String interpretation of the effect size
    """
    abs_d = abs(d)
    if abs_d < 0.2:
        return "negligible"
    elif abs_d < 0.5:
        return "small"
    elif abs_d < 0.8:
        return "medium"
    else:
        return "large"


def load_recall_results_from_json(filepath: Path) -> Dict[str, Dict[str, List[float]]]:
    """
    Load recall accuracy results from a JSON file generated by the training pipeline.

    Expected format:
    {
        "seeds": [seed1, seed2, ...],
        "accuracies": {
            "babi": [acc1, acc2, ...],
            "lambada": [acc1, acc2, ...],
            "story_cloze": [acc1, acc2, ...]
        }
    }

    Args:
        filepath: Path to the JSON file

    Returns:
        Dictionary mapping dataset names to lists of accuracies per seed
    """
    if not filepath.exists():
        raise FileNotFoundError(f"Results file not found: {filepath}")

    with open(filepath, 'r') as f:
        data = json.load(f)

    # Extract accuracies for each dataset
    results = {}
    for dataset_name, accuracies in data.get("accuracies", {}).items():
        if isinstance(accuracies, list):
            results[dataset_name] = accuracies
        elif isinstance(accuracies, dict):
            # Handle case where accuracies might be nested by model type
            for model_type, values in accuracies.items():
                if isinstance(values, list):
                    results[f"{dataset_name}_{model_type}"] = values

    if not results:
        raise ValueError("No accuracy data found in the results file.")

    return results


def calculate_effect_sizes_for_datasets(
    spatial_accuracies: Dict[str, List[float]],
    baseline_accuracies: Dict[str, List[float]]
) -> Dict[str, Dict[str, Any]]:
    """
    Calculate Cohen's d and confidence intervals for all datasets.

    Args:
        spatial_accuracies: Dictionary of spatial model accuracies per dataset
        baseline_accuracies: Dictionary of baseline model accuracies per dataset

    Returns:
        Dictionary with effect size results for each dataset
    """
    results = {}

    # Find common datasets
    common_datasets = set(spatial_accuracies.keys()) & set(baseline_accuracies.keys())

    if not common_datasets:
        logger.warning("No common datasets found between spatial and baseline results.")
        return results

    for dataset in common_datasets:
        spatial_vals = spatial_accuracies[dataset]
        baseline_vals = baseline_accuracies[dataset]

        if len(spatial_vals) != len(baseline_vals):
            logger.warning(f"Dataset {dataset}: mismatched sample sizes ({len(spatial_vals)} vs {len(baseline_vals)}). Skipping.")
            continue

        try:
            cohen_d, lower_ci, upper_ci = compute_cohens_d_confidence_interval(
                spatial_vals, baseline_vals, confidence_level=0.95
            )
            interpretation = get_cohen_interpretation(cohen_d)

            results[dataset] = {
                "cohen_d": float(cohen_d),
                "confidence_interval_95": {
                    "lower": float(lower_ci),
                    "upper": float(upper_ci)
                },
                "interpretation": interpretation,
                "n_samples": len(spatial_vals),
                "spatial_mean": float(np.mean(spatial_vals)),
                "baseline_mean": float(np.mean(baseline_vals))
            }
            logger.info(f"Dataset {dataset}: Cohen's d = {cohen_d:.4f} ({interpretation}), 95% CI [{lower_ci:.4f}, {upper_ci:.4f}]")

        except Exception as e:
            logger.error(f"Error calculating effect size for {dataset}: {e}")
            results[dataset] = {
                "error": str(e),
                "cohen_d": None,
                "confidence_interval_95": None,
                "interpretation": None
            }

    return results


def save_effect_size_results(
    results: Dict[str, Dict[str, Any]],
    output_path: Path
) -> None:
    """
    Save effect size calculation results to a JSON file.

    Args:
        results: Dictionary of effect size results
        output_path: Path to the output JSON file
    """
    output_path.parent.mkdir(parents=True, exist_ok=True)

    with open(output_path, 'w') as f:
        json.dump(results, f, indent=2)

    logger.info(f"Effect size results saved to {output_path}")


def main():
    """
    Main entry point for effect size calculation.
    Reads recall accuracy results and computes Cohen's d with 95% CIs.
    """
    # Define paths
    project_root = Path(__file__).resolve().parent.parent.parent
    results_dir = project_root / "artifacts" / "results"
    input_file = results_dir / "recall_accuracy.json"
    output_file = results_dir / "effect_size_results.json"

    logger.info(f"Loading recall accuracy results from {input_file}")

    try:
        # Load results
        all_results = load_recall_results_from_json(input_file)

        # Separate spatial and baseline results
        # Expected keys: "babi_spatial", "babi_baseline", etc.
        spatial_accuracies = {}
        baseline_accuracies = {}

        for key, values in all_results.items():
            if "_spatial" in key:
                dataset_name = key.replace("_spatial", "")
                spatial_accuracies[dataset_name] = values
            elif "_baseline" in key:
                dataset_name = key.replace("_baseline", "")
                baseline_accuracies[dataset_name] = values

        logger.info(f"Found {len(spatial_accuracies)} spatial datasets and {len(baseline_accuracies)} baseline datasets")

        if not spatial_accuracies or not baseline_accuracies:
            logger.error("Could not find both spatial and baseline results. Expected keys like 'babi_spatial' and 'babi_baseline'.")
            sys.exit(1)

        # Calculate effect sizes
        effect_results = calculate_effect_sizes_for_datasets(spatial_accuracies, baseline_accuracies)

        if not effect_results:
            logger.error("No effect sizes could be calculated.")
            sys.exit(1)

        # Save results
        save_effect_size_results(effect_results, output_file)

        logger.info("Effect size calculation completed successfully.")

    except FileNotFoundError as e:
        logger.error(f"Input file not found: {e}")
        logger.error("Make sure to run the training and evaluation pipeline first to generate recall_accuracy.json")
        sys.exit(1)
    except Exception as e:
        logger.error(f"Unexpected error during effect size calculation: {e}")
        sys.exit(1)


if __name__ == "__main__":
    main()