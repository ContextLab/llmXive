# Cache the model globally to avoid reloading on every call in a process
_embedding_model = None

def _get_embedding_model():
    global _embedding_model
    if _embedding_model is None:
        logger.info("Loading sentence-transformer model: all-MiniLM-L6-v2")
        _embedding_model = SentenceTransformer('all-MiniLM-L6-v2')
    return _embedding_model

def compute_query_context_distance(query: str, context: str) -> float:
    """
    Computes the cosine distance between the query and the context embedding.
    Returns a scalar float.
    """
    model = _get_embedding_model()
    embeddings = model.encode([query, context], convert_to_numpy=True)
    q_emb = embeddings[0]
    c_emb = embeddings[1]
    
    # Normalize
    q_norm = q_emb / (np.linalg.norm(q_emb) + 1e-8)
    c_norm = c_emb / (np.linalg.norm(c_emb) + 1e-8)
    
    # Cosine similarity
    sim = np.dot(q_norm, c_norm)
    # Convert to distance (1 - sim)
    return float(1.0 - sim)

def extract_features(
    input_path: Path,
    labels_path: Path,
    output_path: Path
) -> None:
    """
    Main feature extraction logic.
    1. Loads the ingested interaction data from input_path.
    2. Loads the ground truth abstention labels from labels_path (generated by T012.5).
    3. Merges them on a common ID (assumed 'interaction_id' or 'task_id').
    4. Computes derived features: search count, error freq, token usage, turn number, embedding distance.
    5. Joins the 'abstention_label' from the labels dataframe.
    6. Saves the final dataframe to output_path.
    """
    logger.info(f"Loading interaction data from {input_path}")
    if not input_path.exists():
        raise FileNotFoundError(f"Input file not found: {input_path}")
    
    df = pd.read_parquet(input_path) if input_path.suffix == '.parquet' else pd.read_csv(input_path)
    
    # Identify the ID column. Usually 'interaction_id' or 'task_id' based on standard benchmarks.
    id_col = None
    candidates = ['interaction_id', 'task_id', 'id']
    for cand in candidates:
        if cand in df.columns:
            id_col = cand
            break
    
    if id_col is None:
        # Fallback to first column if it looks like an ID, or raise error
        if len(df.columns) > 0:
            id_col = df.columns[0]
            logger.warning(f"Could not find standard ID column, using '{id_col}' as join key.")
        else:
            raise ValueError("Input dataframe is empty or has no columns.")

    logger.info(f"Loading abstention labels from {labels_path}")
    if not labels_path.exists():
        raise FileNotFoundError(f"Labels file not found: {labels_path}. Ensure T012.5 has run.")
    
    labels_df = pd.read_parquet(labels_path) if labels_path.suffix == '.parquet' else pd.read_csv(labels_path)
    
    # Identify ID column in labels
    label_id_col = None
    for cand in candidates:
        if cand in labels_df.columns:
            label_id_col = cand
            break
    
    if label_id_col is None:
        if len(labels_df.columns) > 0:
            label_id_col = labels_df.columns[0]
            logger.warning(f"Could not find standard ID column in labels, using '{label_id_col}' as join key.")
        else:
            raise ValueError("Labels dataframe is empty.")

    # Merge on the ID column
    # We need to ensure the ID columns are compatible
    if id_col != label_id_col:
        # Rename to match for merge, then rename back if necessary, or just use left_on/right_on
        merged_df = pd.merge(
            df, 
            labels_df, 
            left_on=id_col, 
            right_on=label_id_col, 
            how='left'
        )
        # Drop the duplicate ID column from right if it exists
        if label_id_col in merged_df.columns and label_id_col != id_col:
            merged_df = merged_df.drop(columns=[label_id_col])
    else:
        merged_df = pd.merge(df, labels_df, on=id_col, how='left')

    # Ensure the abstention label column exists and is named consistently
    # The oracle output usually names it 'abstention_label' or 'is_impossible'
    label_candidates = ['abstention_label', 'is_impossible', 'label', 'ground_truth']
    final_label_col = None
    for cand in label_candidates:
        if cand in merged_df.columns:
            final_label_col = cand
            break
    
    if final_label_col is None:
        # If the merge didn't bring it in, check if we need to default to 0 or fail
        logger.warning("No abstention label column found in merged data. Creating a default column of 0.")
        merged_df['abstention_label'] = 0
        final_label_col = 'abstention_label'
    else:
        # Rename to standard name if it wasn't already
        if final_label_col != 'abstention_label':
            merged_df.rename(columns={final_label_col: 'abstention_label'}, inplace=True)
        final_label_col = 'abstention_label'

    # Compute derived features if not already present
    # Expected columns from previous steps (T013, T014):
    # 'search_count', 'error_frequency', 'token_usage', 'turn_number', 'query_context_distance'
    
    feature_map = {
        'search_count': 'search_count',
        'error_frequency': 'error_frequency',
        'token_usage': 'token_usage',
        'turn_number': 'turn_number',
        'query_context_distance': 'query_context_distance'
    }
    
    for source, dest in feature_map.items():
        if source not in merged_df.columns:
            logger.warning(f"Column '{source}' not found in input data. Will attempt to compute or leave as NaN.")
            # If we have raw data to compute from, we would do it here. 
            # For now, assume T013/T014 populated these, or they are missing.
            # If missing, we can't compute them without raw text, which is excluded per schema.
            # We'll leave them as NaN for now, to be handled by T016 (imputation).
            merged_df[dest] = np.nan

    # Ensure numeric types for computation
    numeric_cols = ['search_count', 'error_frequency', 'token_usage', 'turn_number', 'query_context_distance', 'abstention_label']
    for col in numeric_cols:
        if col in merged_df.columns:
            merged_df[col] = pd.to_numeric(merged_df[col], errors='coerce')

    # Fill NaN in abstention_label with 0 (assuming 0 = possible, 1 = impossible/abstain)
    # If the label is missing, it implies the oracle didn't flag it as impossible, or data is missing.
    # Per FR-002, we join with the oracle output. If no join, it's likely 0 (possible).
    merged_df['abstention_label'] = merged_df['abstention_label'].fillna(0).astype(int)

    # Save to output
    output_path.parent.mkdir(parents=True, exist_ok=True)
    if output_path.suffix == '.parquet':
        merged_df.to_parquet(output_path, index=False)
    else:
        merged_df.to_csv(output_path, index=False)
    
    logger.info(f"Feature extraction complete. Output saved to {output_path}")
    logger.info(f"Dataset shape: {merged_df.shape}")
    logger.info(f"Columns: {list(merged_df.columns)}")
    logger.info(f"Abstention label distribution:\n{merged_df['abstention_label'].value_counts()}")

def main():
    """
    Entry point for the feature extraction script.
    Reads config for paths.
    """
    config = get_config()
    
    # Paths from config
    input_path = get_path(config, 'data', 'ingested_interactions.parquet')
    labels_path = get_path(config, 'data', 'processed', 'labels.parquet')
    output_path = get_path(config, 'data', 'processed', 'features.parquet')
    
    # Fallbacks if config keys are missing
    if not input_path:
        input_path = Path('data/processed/ingested_interactions.parquet')
    if not labels_path:
        labels_path = Path('data/processed/labels.parquet')
    if not output_path:
        output_path = Path('data/processed/features.parquet')
        
    # Ensure paths are Path objects
    input_path = Path(input_path)
    labels_path = Path(labels_path)
    output_path = Path(output_path)
    
    extract_features(input_path, labels_path, output_path)

if __name__ == '__main__':
    main()