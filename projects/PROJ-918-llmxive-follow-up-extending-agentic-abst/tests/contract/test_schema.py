"""
Contract test for code/data/extract_features.py schema validation.

This test ensures that the output of the feature extraction pipeline
strictly adheres to the schema defined in contracts/dataset.schema.yaml.

It validates:
1. Presence of all required columns.
2. Absence of forbidden columns (full semantic context text).
3. Correct data types for each column.
4. Absence of null values in critical fields.
"""
import os
import sys
import pytest
import pandas as pd
import yaml
from pathlib import Path
from typing import List, Dict, Any

# Add project root to path if running standalone
project_root = Path(__file__).parent.parent.parent
sys.path.insert(0, str(project_root))

from code.logging_config import setup_logging

logger = setup_logging()

# Path to the schema definition
SCHEMA_PATH = project_root / "contracts" / "dataset.schema.yaml"

# Path to the expected output file (generated by T016/T017)
# We test against the actual file if it exists, or raise a clear error if not
EXPECTED_OUTPUT_PATH = project_root / "data" / "processed" / "features.parquet"

@pytest.fixture(scope="module")
def schema_definition() -> Dict[str, Any]:
    """Load the dataset schema from the YAML contract."""
    if not SCHEMA_PATH.exists():
        pytest.fail(f"Schema file not found: {SCHEMA_PATH}")
    
    with open(SCHEMA_PATH, 'r') as f:
        return yaml.safe_load(f)

@pytest.fixture(scope="module")
def feature_dataset(schema_definition) -> pd.DataFrame:
    """Load the feature dataset to be validated."""
    if not EXPECTED_OUTPUT_PATH.exists():
        pytest.fail(
            f"Feature dataset not found at {EXPECTED_OUTPUT_PATH}. "
            "Please ensure T016 (preprocessing) and T017 (verification) have been completed "
            "to generate data/processed/features.parquet."
        )
    
    try:
        df = pd.read_parquet(EXPECTED_OUTPUT_PATH)
    except Exception as e:
        pytest.fail(f"Failed to read parquet file: {e}")
    
    return df

def test_required_columns_present(schema_definition: Dict[str, Any], feature_dataset: pd.DataFrame):
    """Verify all columns defined as 'required' in the schema are present."""
    required_fields = schema_definition.get("required_columns", [])
    missing_cols = [col for col in required_fields if col not in feature_dataset.columns]
    
    assert not missing_cols, f"Missing required columns: {missing_cols}"

def test_no_forbidden_columns(schema_definition: Dict[str, Any], feature_dataset: pd.DataFrame):
    """Verify that no columns containing full semantic context text exist."""
    # The schema usually defines forbidden patterns or specific column names to exclude
    forbidden_patterns = schema_definition.get("forbidden_columns", [])
    forbidden_patterns += schema_definition.get("forbidden_patterns", []) # Support both formats
    
    violations = []
    for col in feature_dataset.columns:
        for pattern in forbidden_patterns:
            if pattern in col.lower():
                violations.append(col)
    
    assert not violations, f"Dataset contains forbidden columns (potential full context text): {violations}"

def test_column_types(schema_definition: Dict[str, Any], feature_dataset: pd.DataFrame):
    """Verify data types match the schema definition."""
    type_map = schema_definition.get("column_types", {})
    
    for col, expected_type in type_map.items():
        if col not in feature_dataset.columns:
            # Handled by required_columns test, but skip here if optional
            continue
        
        actual_type = str(feature_dataset[col].dtype)
        
        # Map schema types to pandas dtypes
        # Support flexible matching (e.g., 'float' matches 'float64', 'int' matches 'int64')
        is_valid = False
        if expected_type == "float":
            is_valid = "float" in actual_type
        elif expected_type == "int":
            is_valid = "int" in actual_type
        elif expected_type == "bool":
            is_valid = "bool" in actual_type
        elif expected_type == "string":
            is_valid = "object" in actual_type or "str" in actual_type
        else:
            # Exact match for other types
            is_valid = expected_type == actual_type
        
        assert is_valid, f"Column '{col}' has type '{actual_type}', expected '{expected_type}'"

def test_no_nulls_in_critical_fields(schema_definition: Dict[str, Any], feature_dataset: pd.DataFrame):
    """Verify no null values exist in columns marked as critical."""
    critical_fields = schema_definition.get("critical_columns", [])
    
    for col in critical_fields:
        if col in feature_dataset.columns:
            null_count = feature_dataset[col].isnull().sum()
            assert null_count == 0, f"Column '{col}' contains {null_count} null values"

def test_feature_extraction_logic_coherence(feature_dataset: pd.DataFrame):
    """
    Additional business logic check: 
    Ensure 'search_count' and 'turn_number' are non-negative.
    """
    numeric_cols = ['search_count', 'turn_number', 'token_usage']
    for col in numeric_cols:
        if col in feature_dataset.columns:
            if feature_dataset[col].dtype.kind in ['i', 'f']: # integer or float
                assert (feature_dataset[col] >= 0).all(), f"Column '{col}' contains negative values"

def test_abstention_label_binary(feature_dataset: pd.DataFrame):
    """
    Ensure the 'abstention_label' (if present) is binary (0/1).
    """
    if 'abstention_label' in feature_dataset.columns:
        unique_vals = feature_dataset['abstention_label'].unique()
        valid_vals = {0, 1, 0.0, 1.0}
        invalid_vals = [v for v in unique_vals if v not in valid_vals]
        assert not invalid_vals, f"abstention_label contains non-binary values: {invalid_vals}"