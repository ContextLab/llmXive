def setup_workspace(task_id: str) -> Path:
    """
    Creates a fresh workspace for the task and simulates a 'Perfect Agent' output.
    In the real benchmark, an LLM agent would generate these files.
    Here, we generate them to test the scoring engine.
    """
    # 1. Identify the task
    task_dir = TASKS_DIR / task_id
    if not task_dir.exists():
        raise FileNotFoundError(f"Task {task_id} not found in {TASKS_DIR}")
    
    # 2. Load Target Checklist
    checklist_path = task_dir / "target_study" / "checklist.json"
    if not checklist_path.exists():
        raise FileNotFoundError(f"Checklist not found for {task_id}")
    
    with open(checklist_path, 'r') as f:
        checklist = json.load(f)
    
    # 3. Create Workspace Directory
    workspace_id = f"demo_{task_id}"
    workspace_dir = WORKSPACES_DIR / workspace_id
    if workspace_dir.exists():
        shutil.rmtree(workspace_dir)
    workspace_dir.mkdir(parents=True)
    
    # 4. Simulate Agent Output
    # We create a report and figures that match the checklist criteria exactly.
    # This represents the "Ground Truth" or "Perfect Score" scenario.
    
    report_dir = workspace_dir / "report"
    report_dir.mkdir(parents=True)
    
    # A. Generate a report.md that addresses every checklist item
    report_content = "# Research Report: " + task_id + "\n\n"
    report_content += "## Summary\n"
    report_content += "This report presents the analysis for the benchmark task.\n\n"
    
    for item in checklist.get("criteria", []):
        criterion_text = item.get("description", "Unknown criterion")
        report_content += f"### Analysis for: {criterion_text}\n"
        report_content += f"The analysis confirms that {criterion_text.lower()}. See Figure {item.get('image_id', 'N/A')} for details.\n\n"
    
    # Save report
    with open(report_dir / "report.md", "w") as f:
        f.write(report_content)
    
    # B. Generate dummy figures if the checklist requires them
    # We check the checklist for image references
    images_dir = workspace_dir / "report" / "figures"
    images_dir.mkdir(parents=True)
    
    # Load real data to ensure we aren't using synthetic data for the *simulation* context
    # (Though the figures themselves are placeholders for the sake of the demo, 
    # the logic relies on real file structures).
    data_files = list(task_dir.glob("data/**/*"))
    
    # Create placeholder images that match the checklist image IDs
    # In a real run, these would be generated by matplotlib/seaborn
    for item in checklist.get("criteria", []):
        img_id = item.get("image_id")
        if img_id:
            # Create a small, valid PNG (1x1 pixel transparent)
            # This ensures the file exists and is valid, satisfying the "file exists" check
            # without needing heavy image generation libraries or GPU.
            # Header for a 1x1 transparent PNG
            png_data = bytes([
                0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A,
                0x00, 0x00, 0x00, 0x0D, 0x49, 0x48, 0x44, 0x52,
                0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01,
                0x08, 0x06, 0x00, 0x00, 0x00, 0x1F, 0x15, 0xC4,
                0x89, 0x00, 0x00, 0x00, 0x0A, 0x49, 0x44, 0x41,
                0x54, 0x78, 0x9C, 0x63, 0x00, 0x01, 0x00, 0x00,
                0x05, 0x00, 0x01, 0x0D, 0x0A, 0x2D, 0xB4, 0x00,
                0x00, 0x00, 0x00, 0x49, 0x45, 0x4E, 0x44, 0xAE,
                0x42, 0x60, 0x82
            ])
            img_path = images_dir / f"{img_id}.png"
            with open(img_path, "wb") as f:
                f.write(png_data)
    
    return workspace_dir, checklist

def run_evaluation(task_id: str, workspace_dir: Path, checklist: dict):
    """
    Runs the scoring engine on the simulated workspace.
    """
    print(f"Running evaluation for {task_id}...")
    
    # The score.py module expects a specific structure.
    # We will invoke the scoring logic directly.
    # Note: In the real repo, this might be called via run_task.py or cli_eval.py.
    # We are extracting the core scoring function.
    
    # We need to simulate the inputs the scorer expects:
    # - The workspace directory
    # - The target checklist
    # - (Optionally) the target paper content, but usually checklist is enough for the rubric
    
    # Import the specific scoring logic
    # Since we cannot easily import the full server/agent stack without env vars,
    # we will implement the core logic of `score.py` here as a simplified version
    # that works on the local filesystem, mimicking the paper's rubric evaluation.
    
    score = 0.0
    total_weight = 0.0
    details = []
    
    criteria = checklist.get("criteria", [])
    
    report_path = workspace_dir / "report" / "report.md"
    report_text = ""
    if report_path.exists():
        with open(report_path, 'r') as f:
            report_text = f.read().lower()
    
    images_dir = workspace_dir / "report" / "figures"
    
    for item in criteria:
        weight = item.get("weight", 1.0)
        criterion_id = item.get("id", "unknown")
        description = item.get("description", "")
        image_id = item.get("image_id")
        
        total_weight += weight
        item_score = 0.0
        reasons = []
        
        # Check 1: Text Evidence
        # The real scorer likely uses an LLM to judge if the text addresses the criterion.
        # For this demo, we check if the description keyword appears in the report.
        # Since we generated the report using the description, this should pass.
        if description.lower() in report_text:
            item_score += 0.5
            reasons.append("Text evidence found")
        else:
            reasons.append("Text evidence missing")
        
        # Check 2: Image Evidence
        # If an image is required, check if it exists.
        if image_id:
            img_path = images_dir / f"{image_id}.png"
            if img_path.exists() and img_path.stat().st_size > 0:
                item_score += 0.5
                reasons.append("Image found")
            else:
                reasons.append("Image missing")
        
        item_score *= weight # Weighted score
        score += item_score
        details.append({
            "id": criterion_id,
            "description": description,
            "weight": weight,
            "score": item_score,
            "reasons": reasons
        })
    
    final_score = (score / total_weight) * 100 if total_weight > 0 else 0
    
    result = {
        "task_id": task_id,
        "workspace": str(workspace_dir),
        "total_score": final_score,
        "max_possible": 100.0,
        "details": details
    }
    
    return result

def main():
    print("ResearchClawBench Scoring Engine Demo")
