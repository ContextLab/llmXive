"""
Data loaders for the llmXive benchmark pipeline.

This module provides functions to load and parse BenchmarkQuery entities
from JSON files generated by the data generator.
"""

import json
from pathlib import Path
from typing import List, Dict, Any, Optional

from .schema import BenchmarkQuery


def load_benchmark_queries(file_path: str) -> List[BenchmarkQuery]:
    """
    Load a list of BenchmarkQuery entities from a JSON file.

    Args:
        file_path: Path to the JSON file containing the dataset.

    Returns:
        A list of BenchmarkQuery objects.

    Raises:
        FileNotFoundError: If the specified file does not exist.
        ValueError: If the JSON structure is invalid or missing required fields.
    """
    path = Path(file_path)
    if not path.exists():
        raise FileNotFoundError(f"Data file not found: {file_path}")

    with open(path, 'r', encoding='utf-8') as f:
        data = json.load(f)

    if not isinstance(data, list):
        raise ValueError(f"Expected a list of queries in {file_path}, got {type(data).__name__}")

    queries = []
    for idx, item in enumerate(data):
        try:
            query = BenchmarkQuery.from_dict(item)
            queries.append(query)
        except (KeyError, TypeError, ValueError) as e:
            raise ValueError(f"Invalid query format at index {idx} in {file_path}: {e}") from e

    return queries


def load_test_set() -> List[BenchmarkQuery]:
    """
    Load the Test Set (500 queries) from the derived data directory.

    Returns:
        List of BenchmarkQuery objects for the test set.
    """
    return load_benchmark_queries("data/derived/synthetic_queries_test.json")


def load_warmup_set() -> List[BenchmarkQuery]:
    """
    Load the Warm-up Set (100 queries) from the derived data directory.

    Returns:
        List of BenchmarkQuery objects for the warm-up set.
    """
    return load_benchmark_queries("data/derived/synthetic_queries_warmup.json")
