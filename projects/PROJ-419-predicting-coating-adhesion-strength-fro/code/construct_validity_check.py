"""
Construct Validity Check Implementation (Task T042)

Re-verifies derived proxies against defined thresholds before final model training.
Excludes any invalid proxies where |r| <= 0.3 or R² <= 0.05.
"""
import os
import logging
import numpy as np
import pandas as pd
from typing import Dict, List, Tuple, Optional
from scipy import stats

# Configure logging
logging.basicConfig(
    level=logging.INFO,
    format='%(asctime)s - %(levelname)s - %(message)s'
)
logger = logging.getLogger(__name__)

# Thresholds defined in Plan Phase 1.8
CORRELATION_THRESHOLD = 0.3
R_SQUARED_THRESHOLD = 0.05

def load_processed_data(filepath: str = "data/processed/coating_adhesion_dataset.csv") -> pd.DataFrame:
    """
    Loads the processed dataset generated by US1.
    """
    if not os.path.exists(filepath):
        raise FileNotFoundError(
            f"Processed dataset not found at {filepath}. "
            "Please run US1 (T031) first to generate the dataset."
        )
    return pd.read_csv(filepath)

def calculate_correlation(df: pd.DataFrame, feature: str, target: str = "adhesion_strength_mpa") -> Tuple[float, float]:
    """
    Calculates Pearson correlation (r) and R-squared for a specific feature against the target.
    
    Returns:
        Tuple of (r, r_squared)
    """
    # Drop rows with NaN in feature or target
    valid_data = df[[feature, target]].dropna()
    
    if len(valid_data) < 2:
        logger.warning(f"Insufficient data points for {feature}. Skipping.")
        return 0.0, 0.0

    x = valid_data[feature].values
    y = valid_data[target].values

    # Calculate Pearson correlation
    r, _ = stats.pearsonr(x, y)
    
    # Calculate R-squared (linear regression)
    slope, intercept, r_val, _, _ = stats.linregress(x, y)
    # R-squared is r^2 for simple linear regression
    r_squared = r_val ** 2

    return abs(r), r_squared

def perform_construct_validity_check(
    df: pd.DataFrame, 
    proxy_features: List[str], 
    target: str = "adhesion_strength_mpa"
) -> Dict[str, any]:
    """
    Re-verifies derived proxies against thresholds:
    - |r| > 0.3
    - R² > 0.05
    
    Returns a report containing valid and invalid proxies.
    """
    logger.info(f"Starting Construct Validity Check for {len(proxy_features)} proxies.")
    
    valid_proxies = []
    invalid_proxies = []
    results = []

    for feature in proxy_features:
        if feature not in df.columns:
            logger.warning(f"Feature '{feature}' not found in dataset. Skipping.")
            continue

        r, r_squared = calculate_correlation(df, feature, target)
        
        is_valid = (r > CORRELATION_THRESHOLD) and (r_squared > R_SQUARED_THRESHOLD)
        
        status = "VALID" if is_valid else "INVALID"
        logger.info(f"Proxy '{feature}': |r|={r:.4f}, R²={r_squared:.4f} -> {status}")

        result_entry = {
            "feature": feature,
            "correlation_abs": r,
            "r_squared": r_squared,
            "status": status
        }
        results.append(result_entry)

        if is_valid:
            valid_proxies.append(feature)
        else:
            invalid_proxies.append(feature)

    return {
        "total_proxies": len(proxy_features),
        "valid_proxies": valid_proxies,
        "invalid_proxies": invalid_proxies,
        "excluded_count": len(invalid_proxies),
        "detailed_results": results
    }

def run_validity_check_pipeline(
    data_path: str = "data/processed/coating_adhesion_dataset.csv",
    output_path: str = "data/processed/construct_validity_report.json",
    proxy_columns: Optional[List[str]] = None
) -> List[str]:
    """
    Main pipeline to run the validity check and return the list of valid proxies.
    
    Args:
        data_path: Path to the processed CSV.
        output_path: Path to save the JSON report.
        proxy_columns: List of column names to check. If None, attempts to infer 
                       common proxy columns or checks all numeric columns excluding target.
    
    Returns:
        List of valid proxy feature names.
    """
    logger.info(f"Loading data from {data_path}")
    df = load_processed_data(data_path)
    
    target_col = "adhesion_strength_mpa"
    if target_col not in df.columns:
        raise ValueError(f"Target column '{target_col}' not found in dataset.")

    # Determine proxy columns to check
    if proxy_columns:
        columns_to_check = [c for c in proxy_columns if c in df.columns]
    else:
        # Default: Check all numeric columns except the target and ID columns
        numeric_cols = df.select_dtypes(include=[np.number]).columns.tolist()
        exclude_cols = [target_col, "sample_id", "id"]
        columns_to_check = [c for c in numeric_cols if c not in exclude_cols]

    logger.info(f"Checking {len(columns_to_check)} candidate proxy features: {columns_to_check}")

    report = perform_construct_validity_check(df, columns_to_check, target_col)

    # Save report
    os.makedirs(os.path.dirname(output_path), exist_ok=True)
    import json
    with open(output_path, 'w') as f:
        json.dump(report, f, indent=2)
    
    logger.info(f"Validity check report saved to {output_path}")
    logger.info(f"Excluded {report['excluded_count']} invalid proxies.")

    return report['valid_proxies']

def main():
    """
    Entry point for the Construct Validity Check task.
    """
    try:
        # Define known derived proxies from T015 context if available, 
        # otherwise rely on the function to scan numeric features.
        # Based on T029/T030, likely candidates include:
        # 'atomic_radius_variance', 'crosslinker_density_proxy', 'surface_roughness_rms'
        known_proxies = [
            'atomic_radius_variance', 
            'crosslinker_density_proxy', 
            'surface_roughness_rms',
            'surface_skewness',
            'surface_kurtosis'
        ]
        
        valid_features = run_validity_check_pipeline(
            data_path="data/processed/coating_adhesion_dataset.csv",
            output_path="data/processed/construct_validity_report.json",
            proxy_columns=known_proxies
        )

        print(f"\nConstruct Validity Check Complete.")
        print(f"Valid Proxies ({len(valid_features)}): {valid_features}")
        
        if len(valid_features) == 0:
            logger.warning("No valid proxies found. Model training may proceed with caution or halt.")
        
        return valid_features

    except Exception as e:
        logger.error(f"Construct Validity Check failed: {e}")
        raise

if __name__ == "__main__":
    main()
