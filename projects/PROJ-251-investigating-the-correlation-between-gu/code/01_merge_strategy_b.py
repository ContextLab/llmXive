"""
Strategy B: Merge generated OTU table with serology metadata using subject_id.

This script is triggered only after T011c (DADA2 pipeline) succeeds.
It loads the OTU table generated by T011c and the serology metadata,
merges them on 'subject_id', and writes the combined dataset.
"""
import os
import sys
import logging
from pathlib import Path
import pandas as pd

from utils.config import get_raw_path, get_processed_path, get_random_seed
from utils.logging_config import get_logger, log_error_context

logger = get_logger(__name__)

def merge_otu_serology(otu_table_path: Path, serology_path: Path, output_path: Path) -> pd.DataFrame:
    """
    Merge OTU table and serology metadata on 'subject_id'.
    
    Args:
        otu_table_path: Path to the OTU table CSV (output of T011c).
        serology_path: Path to the serology metadata CSV.
        output_path: Path where the merged CSV will be written.
        
    Returns:
        Merged DataFrame.
        
    Raises:
        ValueError: If merge results in empty dataframe or missing required columns.
    """
    logger.info(f"Loading OTU table from: {otu_table_path}")
    if not otu_table_path.exists():
        raise FileNotFoundError(f"OTU table not found at {otu_table_path}. Ensure T011c has completed.")
    
    otu_df = pd.read_csv(otu_table_path)
    
    logger.info(f"Loading serology metadata from: {serology_path}")
    if not serology_path.exists():
        raise FileNotFoundError(f"Serology metadata not found at {serology_path}.")
        
    serology_df = pd.read_csv(serology_path)
    
    # Ensure subject_id is string for consistent merging
    if 'subject_id' in otu_df.columns:
        otu_df['subject_id'] = otu_df['subject_id'].astype(str)
    if 'subject_id' in serology_df.columns:
        serology_df['subject_id'] = serology_df['subject_id'].astype(str)
        
    # Identify required columns in serology
    required_serology_cols = ['subject_id', 'titer_baseline', 'titer_post']
    missing_cols = [col for col in required_serology_cols if col not in serology_df.columns]
    if missing_cols:
        raise ValueError(f"Serology metadata missing required columns: {missing_cols}")
        
    # Perform inner merge to ensure we only keep subjects with both OTU and Serology data
    merged_df = pd.merge(
        otu_df, 
        serology_df, 
        on='subject_id', 
        how='inner'
    )
    
    logger.info(f"Merged dataset shape: {merged_df.shape}")
    logger.info(f"Number of subjects in OTU table: {len(otu_df)}")
    logger.info(f"Number of subjects in Serology: {len(serology_df)}")
    logger.info(f"Number of subjects in merged result: {len(merged_df)}")
    
    if merged_df.empty:
        raise ValueError("Merge resulted in an empty dataframe. Check for matching subject_ids.")
        
    # Write to output
    logger.info(f"Writing merged dataset to: {output_path}")
    output_path.parent.mkdir(parents=True, exist_ok=True)
    merged_df.to_csv(output_path, index=False)
    
    return merged_df

def main():
    """Main entry point for Strategy B merge."""
    try:
        # Paths based on project structure
        raw_path = get_raw_path()
        processed_path = get_processed_path()
        
        # Strategy B outputs
        otu_table_path = raw_path / "otutable_raw.csv"
        serology_path = raw_path / "serology.csv"
        output_path = processed_path / "merged_strategy_b.csv"
        
        logger.info("Starting Strategy B: Merge OTU table and Serology metadata.")
        
        merged_df = merge_otu_serology(
            otu_table_path=otu_table_path,
            serology_path=serology_path,
            output_path=output_path
        )
        
        logger.info("Strategy B merge completed successfully.")
        logger.info(f"Output written to: {output_path}")
        
    except FileNotFoundError as e:
        log_error_context(logger, str(e))
        sys.exit(1)
    except ValueError as e:
        log_error_context(logger, str(e))
        sys.exit(1)
    except Exception as e:
        log_error_context(logger, f"Unexpected error during merge: {str(e)}")
        sys.exit(1)

if __name__ == "__main__":
    main()