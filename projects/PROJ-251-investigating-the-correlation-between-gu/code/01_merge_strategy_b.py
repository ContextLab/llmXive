"""
T011d: Merge generated OTU table with serology metadata using `subject_id`.

This script implements Strategy B's final step: merging the OTU table generated
by QIIME2/DADA2 (from raw FASTQ) with the serology metadata.

Prerequisites:
  - data/raw/otutable_raw.csv (generated by T011c)
  - data/raw/serology.csv (generated by T011a or T011b fallback)

Output:
  data/processed/merged_data.csv
"""

import os
import sys
import logging
from pathlib import Path

import pandas as pd

# Add project root to path to allow relative imports
PROJECT_ROOT = Path(__file__).resolve().parent.parent
sys.path.insert(0, str(PROJECT_ROOT))

from code.utils.config import get_raw_path, get_processed_path, get_output_path
from code.utils.logging_config import get_logger, log_exclusion_count

logger = get_logger(__name__)

def merge_otu_serology(otu_path: Path, serology_path: Path, output_path: Path) -> pd.DataFrame:
    """
    Merge OTU table and serology metadata on `subject_id`.

    Args:
        otu_path: Path to the OTU table CSV (generated by Strategy B).
        serology_path: Path to the serology metadata CSV.
        output_path: Path where the merged DataFrame will be saved.

    Returns:
        The merged DataFrame.

    Raises:
        FileNotFoundError: If input files do not exist.
        ValueError: If merge fails or results in empty DataFrame.
    """
    if not otu_path.exists():
        raise FileNotFoundError(f"OTU table not found at {otu_path}")
    if not serology_path.exists():
        raise FileNotFoundError(f"Serology metadata not found at {serology_path}")

    logger.info(f"Loading OTU table from {otu_path}")
    otu_df = pd.read_csv(otu_path)

    logger.info(f"Loading serology metadata from {serology_path}")
    serology_df = pd.read_csv(serology_path)

    # Ensure subject_id is string to avoid dtype mismatches during merge
    otu_df['subject_id'] = otu_df['subject_id'].astype(str)
    serology_df['subject_id'] = serology_df['subject_id'].astype(str)

    logger.info("Merging OTU table and serology metadata on 'subject_id' (inner join)")
    merged_df = pd.merge(
        otu_df,
        serology_df,
        on='subject_id',
        how='inner'
    )

    if merged_df.empty:
        raise ValueError("Merge resulted in an empty DataFrame. Check subject_id alignment.")

    total_otu = len(otu_df)
    total_serology = len(serology_df)
    merged_count = len(merged_df)
    excluded_otu = total_otu - merged_count
    excluded_serology = total_serology - merged_count

    logger.info(f"Merge complete: {merged_count} subjects retained.")
    logger.info(f"Excluded from OTU table: {excluded_otu}")
    logger.info(f"Excluded from Serology: {excluded_serology}")

    log_exclusion_count("T011d_merge", "otu_excluded", excluded_otu)
    log_exclusion_count("T011d_merge", "serology_excluded", excluded_serology)

    # Ensure output directory exists
    output_path.parent.mkdir(parents=True, exist_ok=True)

    logger.info(f"Writing merged data to {output_path}")
    merged_df.to_csv(output_path, index=False)

    return merged_df

def main():
    """Entry point for T011d."""
    raw_path = get_raw_path()
    processed_path = get_processed_path()

    otu_file = raw_path / "otutable_raw.csv"
    serology_file = raw_path / "serology.csv"
    output_file = processed_path / "merged_data.csv"

    try:
        merge_otu_serology(otu_file, serology_file, output_file)
        logger.info("T011d completed successfully.")
    except FileNotFoundError as e:
        logger.error(f"Input file missing: {e}")
        sys.exit(1)
    except ValueError as e:
        logger.error(f"Merge logic error: {e}")
        sys.exit(1)
    except Exception as e:
        logger.exception(f"Unexpected error during merge: {e}")
        sys.exit(1)

if __name__ == "__main__":
    main()
