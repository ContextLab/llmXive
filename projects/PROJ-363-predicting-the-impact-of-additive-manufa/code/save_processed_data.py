"""
Task T018: Save final processed dataset and update state.

This script loads the preprocessed data generated by preprocess.py,
saves it to the final output location, computes its hash, and updates
the project state.yaml.
"""
import os
import sys
import logging
from pathlib import Path

# Add project root to path for imports if running from subdirectory
project_root = Path(__file__).resolve().parent.parent
if str(project_root) not in sys.path:
    sys.path.insert(0, str(project_root))

from preprocess import preprocess_data
from utils import setup_logging, compute_file_hash, load_state, update_state

def main():
    # Setup logging
    logger = setup_logging()
    logger.info("Starting Task T018: Save processed data and update state")

    # Define paths
    raw_data_path = project_root / "data" / "raw" / "316L_LPBF_dataset.csv"
    processed_output_path = project_root / "data" / "processed" / "cleaned_316L.csv"
    state_path = project_root / "state" / "state.yaml"

    # Ensure output directory exists
    processed_output_path.parent.mkdir(parents=True, exist_ok=True)

    # Run preprocessing to get the dataframe (this re-runs the logic from T014-T017)
    # Note: In a real pipeline, we might load the intermediate file if it exists,
    # but to ensure consistency with the latest preprocess logic, we re-run.
    # The preprocess_data function handles loading raw, cleaning, and validation.
    try:
        df = preprocess_data(raw_data_path)
        logger.info(f"Preprocessing complete. Shape: {df.shape}")
    except Exception as e:
        logger.error(f"Preprocessing failed: {e}")
        sys.exit(1)

    # Save to CSV
    try:
        df.to_csv(processed_output_path, index=False)
        logger.info(f"Saved processed data to {processed_output_path}")
    except Exception as e:
        logger.error(f"Failed to save processed data: {e}")
        sys.exit(1)

    # Compute hash of the new file
    file_hash = compute_file_hash(processed_output_path)
    logger.info(f"Computed hash for {processed_output_path.name}: {file_hash}")

    # Update state.yaml
    try:
        state = load_state(state_path)
        state = update_state(
            state,
            artifact_name="cleaned_316L.csv",
            artifact_path=str(processed_output_path.relative_to(project_root)),
            hash_value=file_hash,
            description="Final processed dataset for US1"
        )
        logger.info("Updated state.yaml")
    except Exception as e:
        logger.error(f"Failed to update state.yaml: {e}")
        sys.exit(1)

    logger.info("Task T018 completed successfully")

if __name__ == "__main__":
    main()
