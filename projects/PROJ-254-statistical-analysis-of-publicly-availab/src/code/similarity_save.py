"""
Module to save similarity results to CSV.
This module implements T026: Save results to data/derived/yearly_similarity.csv.
"""
import os
import logging
import csv
from pathlib import Path
from typing import List, Dict, Any, Optional

from similarity import (
    load_yearly_embeddings,
    compute_pairwise_cosine_similarity,
    calculate_mean_off_diagonal_similarity,
    calculate_intra_genre_variance,
    process_year
)
from utils import setup_logging, get_logger

# Ensure output directory exists
OUTPUT_DIR = Path("data/derived")
OUTPUT_DIR.mkdir(parents=True, exist_ok=True)
OUTPUT_FILE = OUTPUT_DIR / "yearly_similarity.csv"

def save_similarity_results(results: List[Dict[str, Any]], output_path: Path = OUTPUT_FILE) -> None:
    """
    Save similarity results to a CSV file.

    Args:
        results: List of dictionaries containing year, mean_off_diagonal_similarity,
                 and intra_genre_variance.
        output_path: Path to the output CSV file.
    """
    if not results:
        logger = get_logger()
        logger.warning("No results to save.")
        return

    fieldnames = ["year", "mean_off_diagonal_similarity", "intra_genre_variance"]

    with open(output_path, "w", newline="", encoding="utf-8") as csvfile:
        writer = csv.DictWriter(csvfile, fieldnames=fieldnames)
        writer.writeheader()
        for row in results:
            writer.writerow({
                "year": row["year"],
                "mean_off_diagonal_similarity": row["mean_off_diagonal_similarity"],
                "intra_genre_variance": row["intra_genre_variance"]
            })

    logger = get_logger()
    logger.info(f"Similarity results saved to {output_path}")

def main() -> None:
    """
    Main function to orchestrate the similarity calculation and saving process.
    Loads yearly embeddings, computes metrics, and saves to CSV.
    """
    setup_logging()
    logger = get_logger()
    logger.info("Starting similarity results generation and saving.")

    # Load embeddings from the directory generated by T023
    embeddings_dir = Path("yearly_embeddings")
    if not embeddings_dir.exists():
        logger.error(f"Embeddings directory {embeddings_dir} does not exist. "
                     "Please run the embeddings pipeline first.")
        raise FileNotFoundError(f"Embeddings directory {embeddings_dir} not found.")

    # Process each year's embeddings
    results = []
    years = sorted([int(f.stem) for f in embeddings_dir.glob("*.npy")])

    if not years:
        logger.warning("No embedding files found in yearly_embeddings/.")
        return

    for year in years:
        logger.info(f"Processing year {year}...")
        try:
            year_result = process_year(year, embeddings_dir)
            if year_result:
                results.append(year_result)
        except Exception as e:
            logger.error(f"Failed to process year {year}: {e}")
            continue

    if not results:
        logger.warning("No valid results computed. No CSV will be written.")
        return

    # Save results to CSV
    save_similarity_results(results)

    logger.info("Similarity calculation and saving complete.")

if __name__ == "__main__":
    main()