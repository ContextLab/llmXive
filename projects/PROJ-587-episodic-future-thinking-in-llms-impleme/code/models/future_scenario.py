"""
Pydantic models for representing generated future scenarios in episodic future thinking.

This module defines the data structures for scenarios generated by the planning service,
including their reliance on episodic memories, counterfactual details, and confidence metrics.

Addresses the need to distinguish between retrieved episodic facts and simulated 
future projections, supporting the validation of 'mental time travel' capabilities.
"""

from typing import List, Dict, Any, Optional
from uuid import uuid4
from datetime import datetime
from pydantic import BaseModel, Field, field_validator, model_validator
from models.planning_task import PlanningTask


class EpisodicReference(BaseModel):
    """
    Reference to a specific episodic memory used to construct the future scenario.
    
    This links the generated future back to the discrete-state machine configurations
    (state-action-outcome tuples) stored in the episodic memory module, satisfying
    the requirement for specific event recollection rather than semantic pattern matching.
    """
    episode_id: str = Field(..., description="Unique identifier of the source episode")
    state_snapshot: str = Field(..., description="Textual representation of the state at retrieval")
    action_taken: str = Field(..., description="The action taken in the source episode")
    outcome_observed: str = Field(..., description="The outcome observed in the source episode")
    similarity_score: float = Field(
        ..., 
        ge=0.0, 
        le=1.0, 
        description="Cosine similarity score between current query and stored episode"
    )
    retrieval_timestamp: datetime = Field(
        default_factory=datetime.utcnow, 
        description="When this reference was retrieved"
    )

class ConfidenceScore(BaseModel):
    """
    Granular confidence metrics for different aspects of the generated scenario.
    
    Implements the WYSIATI (What You See Is All There Is) prevention logic by
    distinguishing confidence in retrieved facts versus simulated extrapolations.
    """
    overall_confidence: float = Field(
        ..., 
        ge=0.0, 
        le=1.0, 
        description="Global confidence in the scenario's validity"
    )
    episodic_alignment: float = Field(
        ..., 
        ge=0.0, 
        le=1.0, 
        description="Confidence that the scenario aligns with retrieved episodic memories"
    )
    counterfactual_uncertainty: float = Field(
        ..., 
        ge=0.0, 
        le=1.0, 
        description="Uncertainty level regarding counterfactual details (high = low confidence)"
    )
    temporal_coherence: float = Field(
        ..., 
        ge=0.0, 
        le=1.0, 
        description="Confidence in the logical flow of events over time"
    )

class CounterfactualDetail(BaseModel):
    """
    Details about a specific counterfactual element in the scenario.
    
    Tracks modifications made to the retrieved episodic memory to simulate a future state,
    allowing for explicit uncertainty marking as per Daniel Kahneman's feedback on 
    distinguishing simulation from evaluation.
    """
    original_element: str = Field(..., description="The element as found in the source episode")
    modified_element: str = Field(..., description="The hypothetical modification applied")
    modification_type: str = Field(
        ..., 
        description="Type of change: 'outcome_swap', 'state_perturbation', 'action_variant'"
    )
    justification: str = Field(..., description="Reasoning for this counterfactual change")
    confidence: float = Field(
        ..., 
        ge=0.0, 
        le=1.0, 
        description="Confidence in this specific counterfactual assumption"
    )

class FutureScenario(BaseModel):
    """
    Represents a generated future planning scenario derived from episodic retrieval.
    
    This model encapsulates the 'mental time travel' output, combining:
    1. A generated plan (the trajectory)
    2. References to the specific episodic memories used (the 'synaptic locus')
    3. Counterfactual details where the future diverges from the past
    4. Confidence scores to enable validation of the simulation heuristic
    
    Attributes:
        scenario_id: Unique identifier for this scenario instance
        generated_plan: The sequence of states and actions constituting the future plan
        episodic_references: List of source episodes used to construct this scenario
        counterfactual_details: Specific modifications made to source episodes to simulate the future
        confidence_scores: Granular confidence metrics for the scenario
        created_at: Timestamp of generation
        task_context: Optional reference to the original PlanningTask that triggered this
    """
    scenario_id: str = Field(
        default_factory=lambda: str(uuid4()), 
        description="Unique identifier for this scenario"
    )
    generated_plan: List[Dict[str, Any]] = Field(
        ..., 
        description="List of step dictionaries containing 'state', 'action', 'expected_outcome'"
    )
    episodic_references: List[EpisodicReference] = Field(
        ..., 
        min_length=1, 
        description="Non-empty list of source episodes that informed this scenario"
    )
    counterfactual_details: List[CounterfactualDetail] = Field(
        default_factory=list, 
        description="Details of any counterfactual modifications applied to source memories"
    )
    confidence_scores: ConfidenceScore = Field(
        ..., 
        description="Granular confidence metrics for the scenario"
    )
    created_at: datetime = Field(
        default_factory=datetime.utcnow, 
        description="Timestamp when this scenario was generated"
    )
    task_context: Optional[str] = Field(
        None, 
        description="Optional ID or context of the PlanningTask that triggered this generation"
    )

    @field_validator('generated_plan')
    @classmethod
    def validate_plan_structure(cls, v: List[Dict[str, Any]]) -> List[Dict[str, Any]]:
        """Ensure every step in the generated plan has required keys."""
        required_keys = {'state', 'action', 'expected_outcome'}
        for i, step in enumerate(v):
            if not isinstance(step, dict):
                raise ValueError(f"Step {i} must be a dictionary, got {type(step)}")
            missing = required_keys - set(step.keys())
            if missing:
                raise ValueError(f"Step {i} missing required keys: {missing}")
        return v

    @field_validator('episodic_references')
    @classmethod
    def validate_references_not_empty(cls, v: List[EpisodicReference]) -> List[EpisodicReference]:
        """Ensure at least one episodic reference exists."""
        if not v:
            raise ValueError("FutureScenario must have at least one episodic reference.")
        return v

    @model_validator(mode='after')
    def check_counterfactual_consistency(self) -> 'FutureScenario':
        """
        Validate that counterfactual details are logically consistent with the plan.
        
        If counterfactual details exist, they should reference elements that are 
        plausibly part of the generated plan's trajectory or the source episodes.
        """
        if self.counterfactual_details:
            # Basic sanity check: ensure confidence scores for counterfactuals are low
            # if the system claims high uncertainty (WYSIATI prevention)
            if self.confidence_scores.overall_confidence > 0.9 and self.confidence_scores.counterfactual_uncertainty > 0.5:
                # Warning scenario: High confidence but high uncertainty on details
                # This is allowed for research but flagged for review
                pass 
        return self

    def to_dict(self) -> Dict[str, Any]:
        """Convert the scenario to a dictionary for serialization."""
        return self.model_dump(mode='json', exclude_none=True)