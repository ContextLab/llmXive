"""
Future Scenario Model for Episodic Future Thinking.

This module defines the `FutureScenario` Pydantic model used to represent
generated future planning scenarios. It captures the plan, references to
episodic memories used for generation, counterfactual details, and
confidence scores associated with the simulation.

This model supports the "Mental Time Travel" architecture by structuring
the output of the planning service, ensuring that generated futures are
traceable to specific episodic retrievals and annotated with uncertainty
regarding counterfactual elements.
"""

from typing import List, Dict, Any, Optional
from uuid import uuid4
from datetime import datetime

from pydantic import BaseModel, Field, field_validator
from models.planning_task import PlanningTask


class EpisodicReference(BaseModel):
    """Reference to a specific episodic memory used in scenario generation."""
    episode_id: str = Field(..., description="Unique ID of the retrieved episode")
    similarity_score: float = Field(..., description="Cosine similarity score used for retrieval")
    retrieval_timestamp: datetime = Field(default_factory=datetime.utcnow)


class ConfidenceScore(BaseModel):
    """Confidence metrics for different aspects of the generated scenario."""
    plan_coherence: float = Field(
        ...,
        ge=0.0,
        le=1.0,
        description="Confidence in the logical flow of the generated plan"
    )
    episodic_alignment: float = Field(
        ...,
        ge=0.0,
        le=1.0,
        description="Confidence that the plan aligns with retrieved episodic memories"
    )
    counterfactual_uncertainty: float = Field(
        ...,
        ge=0.0,
        le=1.0,
        description="Uncertainty level regarding counterfactual details (WYSIATI metric)"
    )


class FutureScenario(BaseModel):
    """
    Represents a simulated future scenario generated by the LLM.

    This model encapsulates the output of the mental time travel process,
    including the generated plan, the episodic memories that influenced it,
    any counterfactual modifications applied, and confidence scores.

    Attributes:
        scenario_id (str): Unique identifier for this scenario instance.
        generated_plan (str): The textual representation of the future plan.
        episodic_references (List[EpisodicReference]): List of memory traces used.
        counterfactual_details (Dict[str, Any]): Details that differ from retrieved
            memories or represent hypothetical variations.
        confidence_scores (ConfidenceScore): Aggregated confidence metrics.
        created_at (datetime): Timestamp of generation.
    """
    scenario_id: str = Field(default_factory=lambda: str(uuid4()))
    generated_plan: str = Field(..., min_length=1, description="The generated future plan text")
    episodic_references: List[EpisodicReference] = Field(
        default_factory=list,
        description="References to episodic memories that informed this scenario"
    )
    counterfactual_details: Dict[str, Any] = Field(
        default_factory=dict,
        description="Details that are hypothetical or counter to retrieved facts"
    )
    confidence_scores: ConfidenceScore = Field(
        default_factory=ConfidenceScore,
        description="Confidence metrics for the scenario"
    )
    created_at: datetime = Field(default_factory=datetime.utcnow)

    @field_validator('generated_plan')
    @classmethod
    def validate_plan_not_empty(cls, v: str) -> str:
        if not v.strip():
            raise ValueError("Generated plan cannot be empty or whitespace only")
        return v

    @field_validator('confidence_scores')
    @classmethod
    def validate_confidence_bounds(cls, v: ConfidenceScore) -> ConfidenceScore:
        # Ensure all confidence values are within [0, 1]
        if not (0.0 <= v.plan_coherence <= 1.0):
            raise ValueError("plan_coherence must be between 0.0 and 1.0")
        if not (0.0 <= v.episodic_alignment <= 1.0):
            raise ValueError("episodic_alignment must be between 0.0 and 1.0")
        if not (0.0 <= v.counterfactual_uncertainty <= 1.0):
            raise ValueError("counterfactual_uncertainty must be between 0.0 and 1.0")
        return v

    def to_dict(self) -> Dict[str, Any]:
        """Convert the scenario to a dictionary, ensuring datetime serialization."""
        return {
            "scenario_id": self.scenario_id,
            "generated_plan": self.generated_plan,
            "episodic_references": [
                {
                    "episode_id": ref.episode_id,
                    "similarity_score": ref.similarity_score,
                    "retrieval_timestamp": ref.retrieval_timestamp.isoformat()
                }
                for ref in self.episodic_references
            ],
            "counterfactual_details": self.counterfactual_details,
            "confidence_scores": {
                "plan_coherence": self.confidence_scores.plan_coherence,
                "episodic_alignment": self.confidence_scores.episodic_alignment,
                "counterfactual_uncertainty": self.confidence_scores.counterfactual_uncertainty
            },
            "created_at": self.created_at.isoformat()
        }

    @classmethod
    def from_planning_task(cls, task: PlanningTask, plan_text: str) -> "FutureScenario":
        """
        Create a FutureScenario from a PlanningTask and a generated plan string.

        This factory method initializes a scenario linked to a specific planning task,
        pre-populating the scenario_id with the task's ID if available, or generating
        a new one.

        Args:
            task: The source PlanningTask.
            plan_text: The generated plan content.

        Returns:
            A new FutureScenario instance.
        """
        return cls(
            scenario_id=task.task_id,
            generated_plan=plan_text,
            created_at=datetime.utcnow()
        )