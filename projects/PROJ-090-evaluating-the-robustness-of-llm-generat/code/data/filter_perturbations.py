"""
T018: Filter perturbation candidates to create the primary dataset.

This script reads the raw log of all perturbation candidates generated by T017
and filters them to retain only the single selected variant per task that
satisfies the strict semantic similarity threshold (> 0.95).

Output: data/processed/perturbation_candidates.json
"""

import json
import logging
import os
import sys
from pathlib import Path
from typing import List, Dict, Any, Optional

# Add project root to path for imports
project_root = Path(__file__).resolve().parent.parent.parent
sys.path.insert(0, str(project_root))

from config import ensure_directories, get_config_dict
from utils.logging import init_logging, get_perturbation_logger

# Configuration constants based on spec FR-002/FR-003
SEMANTIC_THRESHOLD = 0.95
INPUT_FILE = "data/processed/perturbation_candidates_raw.json"
OUTPUT_FILE = "data/processed/perturbation_candidates.json"

logger = get_perturbation_logger()

def load_raw_candidates(input_path: Path) -> List[Dict[str, Any]]:
    """Load the raw log of all generated candidates."""
    if not input_path.exists():
        raise FileNotFoundError(
            f"Input file not found: {input_path}. "
            "Ensure T017 (generate_perturbations.py) has been run successfully."
        )
    
    with open(input_path, 'r', encoding='utf-8') as f:
        data = json.load(f)
    
    # Handle both list format and dict with 'candidates' key
    if isinstance(data, dict):
        if 'candidates' in data:
            return data['candidates']
        elif 'results' in data:
            return data['results']
        else:
            # Assume the whole dict is the list if it has keys that look like task_ids
            # This is a fallback for unexpected formats
            raise ValueError(
                f"Unexpected JSON structure in {input_path}. "
                "Expected a list or a dict with 'candidates' or 'results' key."
            )
    return data

def filter_candidates(candidates: List[Dict[str, Any]]) -> List[Dict[str, Any]]:
    """
    Filter candidates to retain exactly one valid variant per task.
    
    Logic:
    1. Group candidates by task_id.
    2. For each task, find candidates with raw_score > SEMANTIC_THRESHOLD.
    3. If multiple valid candidates exist, select the one with the highest score.
    4. If no valid candidate exists, log a warning and include nothing for that task.
    5. Return the list of selected candidates.
    """
    # Group by task_id
    candidates_by_task: Dict[str, List[Dict[str, Any]]] = {}
    for candidate in candidates:
        task_id = candidate.get('task_id')
        if not task_id:
            logger.warning(f"Skipping candidate without task_id: {candidate}")
            continue
        
        if task_id not in candidates_by_task:
            candidates_by_task[task_id] = []
        candidates_by_task[task_id].append(candidate)
    
    selected_candidates = []
    tasks_without_valid = []
    
    for task_id, task_candidates in candidates_by_task.items():
        # Filter for valid scores
        valid_candidates = [
            c for c in task_candidates 
            if c.get('raw_score', 0.0) > SEMANTIC_THRESHOLD
        ]
        
        if not valid_candidates:
            tasks_without_valid.append(task_id)
            logger.warning(
                f"No valid perturbation found for task {task_id} "
                f"(threshold > {SEMANTIC_THRESHOLD}). Skipping."
            )
            continue
        
        # Select the best candidate (highest score)
        # Sort descending by raw_score
        best_candidate = max(valid_candidates, key=lambda x: x.get('raw_score', 0.0))
        
        # Ensure the selected candidate is marked as valid
        best_candidate['is_valid'] = True
        selected_candidates.append(best_candidate)
        logger.info(
            f"Selected perturbation for task {task_id}: "
            f"type={best_candidate.get('perturbation_type')}, "
            f"score={best_candidate.get('raw_score'):.4f}"
        )
    
    if tasks_without_valid:
        logger.warning(
            f"Total tasks with no valid perturbation: {len(tasks_without_valid)}. "
            "First 5: {tasks_without_valid[:5]}"
        )
    
    logger.info(
        f"Filtering complete. Selected {len(selected_candidates)} valid candidates "
        f"from {len(candidates)} total candidates."
    )
    
    return selected_candidates

def save_filtered_results(candidates: List[Dict[str, Any]], output_path: Path) -> None:
    """Save the filtered results to the output JSON file."""
    output_path.parent.mkdir(parents=True, exist_ok=True)
    
    with open(output_path, 'w', encoding='utf-8') as f:
        json.dump(candidates, f, indent=2, ensure_ascii=False)
    
    logger.info(f"Saved {len(candidates)} filtered candidates to {output_path}")

def main() -> int:
    """Main entry point for the filtering script."""
    init_logging()
    ensure_directories()
    
    config = get_config_dict()
    input_path = Path(project_root) / INPUT_FILE
    output_path = Path(project_root) / OUTPUT_FILE
    
    logger.info(f"Starting filter process for {INPUT_FILE}")
    logger.info(f"Semantic threshold: {SEMANTIC_THRESHOLD}")
    
    try:
        # Load raw data
        candidates = load_raw_candidates(input_path)
        logger.info(f"Loaded {len(candidates)} raw candidates.")
        
        # Filter
        filtered = filter_candidates(candidates)
        
        # Save
        save_filtered_results(filtered, output_path)
        
        # Verification log
        logger.info("Filtering task T018 completed successfully.")
        return 0
        
    except FileNotFoundError as e:
        logger.error(f"File error: {e}")
        return 1
    except json.JSONDecodeError as e:
        logger.error(f"JSON decode error: {e}")
        return 1
    except Exception as e:
        logger.error(f"Unexpected error during filtering: {e}", exc_info=True)
        return 1

if __name__ == "__main__":
    sys.exit(main())