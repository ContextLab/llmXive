"""
Filtering Logic for Perturbation Candidates.

Implements T018: Reads the raw candidate log generated by T017, filters for the
single best valid variant per task (score > 0.95), and writes the primary dataset
`data/processed/perturbation_candidates.json`.

This script enforces the strict semantic threshold defined in FR-002/FR-003.
It does NOT implement fallback logic; if no variant > 0.95 exists for a task,
that task is excluded from the final dataset.
"""
import json
import logging
import os
import sys
from pathlib import Path
from typing import List, Dict, Any, Optional

# Ensure we can import sibling modules
sys.path.insert(0, str(Path(__file__).parent.parent))

from config import ensure_directories, get_config_dict
from utils.logging import get_perturbation_logger

# Initialize logger
logger = get_perturbation_logger("filter_perturbations")

def load_raw_candidates(input_path: str) -> List[Dict[str, Any]]:
    """
    Load the raw candidate pool JSON generated by T017.
    Expected structure: List of dicts with 'task_id', 'original_prompt', 'attempts'.
    """
    path = Path(input_path)
    if not path.exists():
        raise FileNotFoundError(f"Raw candidates file not found: {path}")

    with open(path, "r", encoding="utf-8") as f:
        data = json.load(f)

    if not isinstance(data, list):
        raise ValueError(f"Expected a list at {path}, got {type(data)}")

    return data

def filter_candidates(raw_data: List[Dict[str, Any]], threshold: float = 0.95) -> List[Dict[str, Any]]:
    """
    Filter the raw candidates to produce the final dataset.

    Logic:
    1. Iterate through each task in the raw data.
    2. Examine all 'attempts' for that task.
    3. Identify attempts where 'valid' is True AND 'score' > threshold.
    4. Select the attempt with the HIGHEST score (best semantic similarity).
    5. If no valid attempt exists > threshold, log a warning and skip the task.
    6. Construct the output record with flattened fields as per T018 spec.

    Args:
        raw_data: List of task objects with nested attempts.
        threshold: Minimum semantic similarity score required (default 0.95).

    Returns:
        List of filtered candidate objects.
    """
    filtered_results = []
    skipped_tasks = 0

    for task_item in raw_data:
        task_id = task_item.get("task_id")
        attempts = task_item.get("attempts", [])

        if not attempts:
            logger.warning(f"Task {task_id}: No attempts found.")
            skipped_tasks += 1
            continue

        # Filter for valid and above threshold
        valid_candidates = [
            a for a in attempts
            if a.get("valid") is True and isinstance(a.get("score"), (int, float)) and a.get("score") > threshold
        ]

        if not valid_candidates:
            logger.warning(f"Task {task_id}: No valid candidates found with score > {threshold}. Skipping.")
            skipped_tasks += 1
            continue

        # Select the best candidate (highest score)
        # Sort descending by score to pick the top one
        best_candidate = max(valid_candidates, key=lambda x: x.get("score", 0))

        # Construct the output record matching the filtered schema
        # Schema: task_id, perturbed_prompt, perturbation_type, raw_score, is_valid
        output_record = {
            "task_id": task_id,
            "perturbed_prompt": best_candidate.get("candidate", {}).get("code", "") or best_candidate.get("candidate", ""),
            "perturbation_type": best_candidate.get("type"),
            "raw_score": best_candidate.get("score"),
            "is_valid": True,
            "original_prompt": task_item.get("original_prompt", "")
        }

        filtered_results.append(output_record)
        logger.info(f"Task {task_id}: Selected candidate type={best_candidate.get('type')}, score={best_candidate.get('score'):.4f}")

    logger.info(f"Filtering complete. Total tasks processed: {len(raw_data)}, Selected: {len(filtered_results)}, Skipped: {skipped_tasks}")
    return filtered_results

def save_filtered_results(data: List[Dict[str, Any]], output_path: str) -> None:
    """
    Save the filtered results to a JSON file.
    """
    path = Path(output_path)
    ensure_directories(path)

    with open(path, "w", encoding="utf-8") as f:
        json.dump(data, f, indent=2, ensure_ascii=False)

    logger.info(f"Filtered results saved to {output_path}")

def main():
    """
    Main entry point for T018 execution.
    Reads data/processed/perturbation_candidates_raw.json and writes
    data/processed/perturbation_candidates.json.
    """
    config = get_config_dict()
    threshold = config.get("semantic_threshold", 0.95)

    input_path = Path("data/processed/perturbation_candidates_raw.json")
    output_path = Path("data/processed/perturbation_candidates.json")

    logger.info(f"Starting filtering process. Threshold: {threshold}")
    logger.info(f"Input: {input_path}, Output: {output_path}")

    try:
        raw_data = load_raw_candidates(str(input_path))
        logger.info(f"Loaded {len(raw_data)} tasks from raw candidates.")

        filtered_data = filter_candidates(raw_data, threshold=threshold)

        if not filtered_data:
            logger.error("No valid candidates found after filtering. Output file will be empty.")
            # Still write an empty list to satisfy schema validation expectations
            save_filtered_results([], str(output_path))
            return

        save_filtered_results(filtered_data, str(output_path))
        logger.info("T018 completed successfully.")

    except FileNotFoundError as e:
        logger.error(f"Input file missing: {e}")
        logger.error("Ensure T017 has run successfully and generated perturbation_candidates_raw.json")
        sys.exit(1)
    except Exception as e:
        logger.exception(f"Unexpected error during filtering: {e}")
        sys.exit(1)

if __name__ == "__main__":
    main()
