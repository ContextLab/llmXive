"""
Sensitivity analysis for statistical validity of ranking metrics.

Iterates across alpha values [0.01, 0.05, 0.10] and reports the count
of queries where significance status changes between alpha values.

Depends on: T023 (BH correction results in results/p_values/corrected_p_values.csv)
"""
import os
import csv
import logging
from typing import List, Dict, Tuple, Optional
from config import RESULTS_DIR

# Configure logging
logging.basicConfig(
    level=logging.INFO,
    format='%(asctime)s - %(levelname)s - %(message)s'
)
logger = logging.getLogger(__name__)

ALPHA_VALUES = [0.01, 0.05, 0.10]
CORRECTED_P_VALUES_PATH = os.path.join(RESULTS_DIR, "p_values", "corrected_p_values.csv")
OUTPUT_PATH = os.path.join(RESULTS_DIR, "sensitivity", "alpha_sweep.csv")


def load_corrected_p_values(filepath: str) -> List[Dict[str, any]]:
    """
    Load corrected p-values from the CSV generated by T023.
    
    Expected columns: query_id, metric, raw_p, corrected_p, is_significant
    """
    if not os.path.exists(filepath):
        raise FileNotFoundError(f"Corrected p-values file not found: {filepath}")
    
    results = []
    with open(filepath, 'r', newline='', encoding='utf-8') as f:
        reader = csv.DictReader(f)
        for row in reader:
            results.append({
                'query_id': int(row['query_id']),
                'metric': row['metric'],
                'raw_p': float(row['raw_p']),
                'corrected_p': float(row['corrected_p']),
                'is_significant': row['is_significant'].lower() == 'true'
            })
    return results


def determine_significance(corrected_p: float, alpha: float) -> bool:
    """Determine if a result is significant at the given alpha level."""
    return corrected_p <= alpha


def run_sensitivity_analysis(input_path: str = CORRECTED_P_VALUES_PATH, 
                             output_path: str = OUTPUT_PATH) -> None:
    """
    Run sensitivity analysis across alpha values.
    
    For each alpha value, count how many queries are significant.
    Also tracks changes in significance status between consecutive alpha values.
    
    Output: CSV with columns `alpha, significant_count`
    """
    logger.info(f"Loading corrected p-values from {input_path}")
    try:
        data = load_corrected_p_values(input_path)
    except FileNotFoundError as e:
        logger.error(str(e))
        raise
    
    if not data:
        logger.warning("No data found in corrected p-values file.")
        # Write empty result file if no data
        os.makedirs(os.path.dirname(output_path), exist_ok=True)
        with open(output_path, 'w', newline='', encoding='utf-8') as f:
            writer = csv.writer(f)
            writer.writerow(['alpha', 'significant_count'])
        return

    logger.info(f"Loaded {len(data)} query-metric pairs for sensitivity analysis")
    
    # Ensure output directory exists
    os.makedirs(os.path.dirname(output_path), exist_ok=True)
    
    # Track significance counts for each alpha
    results = []
    prev_significant_count = None
    changes = []
    
    for alpha in ALPHA_VALUES:
        significant_count = sum(
            1 for row in data 
            if determine_significance(row['corrected_p'], alpha)
        )
        results.append({
            'alpha': alpha,
            'significant_count': significant_count
        })
        
        if prev_significant_count is not None:
            change_count = abs(significant_count - prev_significant_count)
            changes.append({
                'from_alpha': ALPHA_VALUES[ALPHA_VALUES.index(alpha) - 1],
                'to_alpha': alpha,
                'change_count': change_count
            })
            logger.info(f"Alpha {ALPHA_VALUES[ALPHA_VALUES.index(alpha) - 1]:.2f} -> {alpha:.2f}: "
                        f"{prev_significant_count} -> {significant_count} significant "
                        f"(change: {change_count})")
        
        prev_significant_count = significant_count
        logger.info(f"Alpha {alpha:.2f}: {significant_count} significant results")
    
    # Write results to CSV
    with open(output_path, 'w', newline='', encoding='utf-8') as f:
        writer = csv.writer(f)
        writer.writerow(['alpha', 'significant_count'])
        for row in results:
            writer.writerow([row['alpha'], row['significant_count']])
    
    logger.info(f"Sensitivity analysis complete. Results written to {output_path}")
    
    # Log summary of changes
    if changes:
        logger.info("Significance status changes between alpha values:")
        for change in changes:
            logger.info(f"  {change['from_alpha']:.2f} -> {change['to_alpha']:.2f}: "
                        f"{change['change_count']} queries changed status")


def main():
    """Entry point for sensitivity analysis."""
    logger.info("Starting sensitivity analysis (T024)")
    try:
        run_sensitivity_analysis()
        logger.info("Sensitivity analysis completed successfully")
    except Exception as e:
        logger.error(f"Sensitivity analysis failed: {e}", exc_info=True)
        raise


if __name__ == "__main__":
    main()
