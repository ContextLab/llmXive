def apply_bh_correction_to_raw(raw_records: List[Dict[str, Any]]) -> List[Dict[str, Any]]:
    """
    Apply Benjamini-Hochberg correction to raw p-values grouped by metric.
    Returns a list of records with 'corrected_p' and 'is_significant' added.
    """
    from power_analysis import apply_bh_correction
    
    # Group by metric
    by_metric = {}
    for rec in raw_records:
        m = rec['metric']
        if m not in by_metric:
            by_metric[m] = []
        by_metric[m].append(rec)
    
    results = []
    
    for metric, records in by_metric.items():
        # Sort by raw_p for BH procedure
        records_sorted = sorted(records, key=lambda x: x['raw_p'])
        
        # Extract raw p-values
        raw_p_values = [r['raw_p'] for r in records_sorted]
        
        # Apply BH correction
        # apply_bh_correction returns a list of corrected p-values (q-values)
        corrected_p_values = apply_bh_correction(raw_p_values)
        
        # Determine significance (using alpha=0.05 as standard, though T024 sweeps alpha)
        # T026 asks for "is_significant". We'll use 0.05 as the default threshold.
        alpha = 0.05
        
        for i, (rec, corr_p) in enumerate(zip(records_sorted, corrected_p_values)):
            # Ensure corrected p-value is not > 1.0 (clipping)
            if corr_p > 1.0:
                corr_p = 1.0
            
            is_sig = corr_p <= alpha
            
            results.append({
                'query_id': rec['query_id'],
                'metric': rec['metric'],
                'raw_p': rec['raw_p'],
                'corrected_p': corr_p,
                'is_significant': is_sig
            })
    
    return results

def save_corrected_p_values(
    records: List[Dict[str, Any]], 
    output_path: Optional[str] = None
) -> None:
    """
    Save the corrected p-values to a CSV file.
    Columns: query_id, metric, raw_p, corrected_p, is_significant
    """
    if output_path is None:
        output_path = os.path.join(RESULTS_DIR, 'p_values', 'corrected_p_values.csv')
    
    # Ensure directory exists
    os.makedirs(os.path.dirname(output_path), exist_ok=True)
    
    logger.info(f"Saving corrected p-values to {output_path}")
    
    with open(output_path, 'w', newline='', encoding='utf-8') as f:
        fieldnames = ['query_id', 'metric', 'raw_p', 'corrected_p', 'is_significant']
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        
        writer.writeheader()
        for rec in records:
            writer.writerow({
                'query_id': rec['query_id'],
                'metric': rec['metric'],
                'raw_p': rec['raw_p'],
                'corrected_p': rec['corrected_p'],
                'is_significant': rec['is_significant']
            })
    
    logger.info(f"Successfully saved {len(records)} records.")

def run_corrected_p_values_generation(
    raw_p_values_path: Optional[str] = None,
    output_path: Optional[str] = None
) -> str:
    """
    Main entry point for T026.
    1. Loads raw p-values.
    2. Applies BH correction.
    3. Saves the result.
    Returns the path to the generated file.
    """
    raw_records = load_raw_p_values(raw_p_values_path)
    corrected_records = apply_bh_correction_to_raw(raw_records)
    final_path = output_path if output_path else os.path.join(RESULTS_DIR, 'p_values', 'corrected_p_values.csv')
    save_corrected_p_values(corrected_records, final_path)
    return final_path
