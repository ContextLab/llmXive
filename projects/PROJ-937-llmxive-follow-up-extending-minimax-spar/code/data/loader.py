import hashlib
import os
from pathlib import Path
from typing import Dict, Optional, Tuple

from datasets import load_dataset
from tqdm import tqdm

from utils.config import get_default_config
from utils.logger import get_structured_logger

logger = get_structured_logger("data_loader")


def calculate_sha256(file_path: str) -> str:
    """Calculate SHA-256 checksum of a file."""
    sha256_hash = hashlib.sha256()
    with open(file_path, "rb") as f:
        for byte_block in iter(lambda: f.read(4096), b""):
            sha256_hash.update(byte_block)
    return sha256_hash.hexdigest()


def verify_ruler_data_integrity(
    dataset_name: str = "hkust-nlp/ruler",
    subset: str = "needle",
    version: str = "1.3.0",
    data_dir: Optional[str] = None,
    expected_checksums: Optional[Dict[str, str]] = None,
) -> Tuple[bool, Dict[str, str]]:
    """
    Download RULER dataset from HuggingFace and verify file integrity via checksums.

    Args:
        dataset_name: HuggingFace dataset identifier
        subset: Specific subset of RULER (e.g., 'needle', 'synthetic')
        version: Dataset version tag
        data_dir: Local directory to save/download data (defaults to config)
        expected_checksums: Optional dict mapping file paths to expected SHA-256 hashes

    Returns:
        Tuple of (is_valid, checksum_report) where is_valid is True if all files match
    """
    config = get_default_config()
    if data_dir is None:
        data_dir = config.get("data_dir", "data/raw")

    data_path = Path(data_dir)
    data_path.mkdir(parents=True, exist_ok=True)

    logger.info(f"Loading dataset {dataset_name} (version {version}) subset {subset}")

    try:
        # Load dataset from HuggingFace
        ds = load_dataset(
            dataset_name,
            subset,
            split="train",
            cache_dir=str(data_path),
        )

        # Identify downloaded files (datasets library caches in HF cache dir)
        # We verify the cache files generated by the datasets library
        cache_files = []
        cache_dir = data_path / "datasets" / dataset_name.replace("/", "__") / f"{version}"

        if cache_dir.exists():
            for root, _, files in os.walk(cache_dir):
                for file in files:
                    if file.endswith(".arrow") or file.endswith(".json"):
                        cache_files.append(os.path.join(root, file))

        if not cache_files:
            # Fallback: try to find any cached dataset files
            for root, _, files in os.walk(data_path):
                for file in files:
                    if file.endswith(".arrow") or file.endswith(".parquet"):
                        cache_files.append(os.path.join(root, file))

        if not cache_files:
            logger.warning("No cache files found after dataset load. Verification skipped.")
            return True, {"status": "no_files_to_verify"}

        logger.info(f"Found {len(cache_files)} cache files to verify")

        checksum_report = {}
        all_valid = True

        for file_path in cache_files:
            computed_checksum = calculate_sha256(file_path)
            relative_path = os.path.relpath(file_path, data_path)

            if expected_checksums and relative_path in expected_checksums:
                expected = expected_checksums[relative_path]
                is_valid = computed_checksum == expected
                status = "valid" if is_valid else "mismatch"
                if not is_valid:
                    all_valid = False
                    logger.error(
                        f"Checksum mismatch for {relative_path}: "
                        f"expected {expected}, got {computed_checksum}"
                    )
            else:
                # If no expected checksum provided, just record the computed one
                status = "recorded"
                logger.info(f"Recorded checksum for {relative_path}: {computed_checksum}")

            checksum_report[relative_path] = {
                "checksum": computed_checksum,
                "status": status,
                "size_bytes": os.path.getsize(file_path),
            }

        if all_valid:
            logger.info("All dataset files verified successfully")
        else:
            logger.error("Dataset verification failed: checksum mismatches detected")

        return all_valid, checksum_report

    except Exception as e:
        logger.error(f"Error during dataset loading or verification: {e}")
        raise


def download_and_verify_ruler(
    dataset_name: str = "hkust-nlp/ruler",
    subset: str = "needle",
    version: str = "1.3.0",
    data_dir: Optional[str] = None,
) -> bool:
    """
    Convenience function to download RULER data and verify integrity.

    Returns:
        True if download and verification succeed, False otherwise
    """
    try:
        is_valid, report = verify_ruler_data_integrity(
            dataset_name=dataset_name,
            subset=subset,
            version=version,
            data_dir=data_dir,
        )

        if is_valid:
            logger.info("RULER dataset downloaded and verified successfully")
            return True
        else:
            logger.error("RULER dataset verification failed")
            return False

    except Exception as e:
        logger.error(f"Failed to download or verify RULER dataset: {e}")
        return False


if __name__ == "__main__":
    """
    Entry point for standalone verification.
    Usage: python code/data/loader.py
    """
    success = download_and_verify_ruler()
    if not success:
        exit(1)
    exit(0)
