"""
Runner script for Task T033: Hash all final artifacts and update state.yaml.

This script identifies the final output artifacts generated by the pipeline
(reports, processed data, figures) and updates the project's state.yaml
with their SHA-256 hashes, adhering to Constitution Principle V.
"""
import os
import sys
from pathlib import Path

# Add project root to path
_project_root = os.path.abspath(os.path.join(os.path.dirname(__file__), '..', '..'))
if _project_root not in sys.path:
    sys.path.insert(0, _project_root)

from code.utils.versioning import load_state, update_artifact_state, save_state
from code.utils.logging import setup_logger, log_pipeline_stage

# Setup logger
logger = setup_logger()

# Define the final artifacts to be hashed based on completed tasks (T030, T031, T032)
# These are the outputs produced by the pipeline.
ARTIFACT_REL_PATHS = [
    "data/processed/merged_data.csv",
    "data/reports/final_analysis.html",
    "data/reports/sensitivity_analysis.csv", # Assumed output of T031
    "figures/kaplan_meier.png",              # Assumed output of T030/T032
    "figures/usage_trajectory.png",          # Assumed output of T030/T032
]

def main():
    """
    Main entry point for T033.
    Iterates through defined final artifacts, updates their hash in state.yaml.
    """
    log_pipeline_stage(logger, "T033", "Starting artifact versioning update")
    
    state = load_state()
    updated_count = 0
    missing_count = 0

    for rel_path in ARTIFACT_REL_PATHS:
        abs_path = os.path.join(_project_root, rel_path)
        
        if not os.path.exists(abs_path):
            logger.warning(f"Artifact not found for hashing: {rel_path}")
            missing_count += 1
            continue

        try:
            # update_artifact_state handles the hashing and state dict modification
            # We need to pass the state dict to be modified in place
            update_artifact_state(rel_path, state)
            logger.info(f"Updated state for: {rel_path}")
            updated_count += 1
        except Exception as e:
            logger.error(f"Failed to update state for {rel_path}: {e}")
            missing_count += 1

    # Save the updated state
    save_state(state)
    
    log_pipeline_stage(logger, "T033", f"Versioning update complete. Updated: {updated_count}, Missing: {missing_count}")
    
    if missing_count > 0 and updated_count == 0:
        logger.error("No artifacts were updated. Check if output files exist.")
        return 1
        
    return 0

if __name__ == "__main__":
    sys.exit(main())