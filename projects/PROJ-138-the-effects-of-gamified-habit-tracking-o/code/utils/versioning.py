import hashlib
import yaml
import os
from pathlib import Path
from typing import Dict, List, Optional
from datetime import datetime
from code.utils.logging import setup_logger, log_pipeline_stage

# Initialize logger
logger = setup_logger("versioning")

STATE_FILE = Path("state.yaml")

def calculate_sha256(file_path: Path) -> str:
    """Calculate SHA-256 hash of a file."""
    if not file_path.exists():
        raise FileNotFoundError(f"File not found: {file_path}")
    
    sha256_hash = hashlib.sha256()
    try:
        with open(file_path, "rb") as f:
            for chunk in iter(lambda: f.read(4096), b""):
                sha256_hash.update(chunk)
        return sha256_hash.hexdigest()
    except Exception as e:
        logger.error(f"Error calculating hash for {file_path}: {e}")
        raise

def load_state() -> Dict:
    """Load the current state.yaml file."""
    if not STATE_FILE.exists():
        logger.info("state.yaml does not exist. Creating a new one.")
        return {"artifacts": {}, "metadata": {"last_updated": None, "version": "1.0.0"}}
    
    try:
        with open(STATE_FILE, "r") as f:
            state = yaml.safe_load(f)
            if state is None:
                return {"artifacts": {}, "metadata": {"last_updated": None, "version": "1.0.0"}}
            return state
    except Exception as e:
        logger.error(f"Error loading state.yaml: {e}")
        raise

def save_state(state: Dict) -> None:
    """Save the state dictionary to state.yaml."""
    try:
        with open(STATE_FILE, "w") as f:
            yaml.dump(state, f, default_flow_style=False, sort_keys=False)
        logger.info(f"State saved to {STATE_FILE}")
    except Exception as e:
        logger.error(f"Error saving state.yaml: {e}")
        raise

def update_artifact_state(state: Dict, artifact_path: Path, hash_value: str) -> Dict:
    """Update the state dictionary with a new artifact hash."""
    if "artifacts" not in state:
        state["artifacts"] = {}
    
    # Store relative path from project root
    rel_path = str(artifact_path)
    state["artifacts"][rel_path] = {
        "hash": hash_value,
        "updated_at": datetime.now().isoformat()
    }
    return state

def hash_all_final_artifacts() -> Dict:
    """
    Hash all final artifacts defined in the project specification and update state.yaml.
    
    Final Artifacts to hash:
    - data/processed/merged_data.csv (from T017)
    - data/reports/final_analysis.html (from T032)
    - data/raw/synthetic_data.csv (from T013a)
    - code/analysis/modeling.py (source)
    - code/analysis/survival.py (source)
    - code/reports/generate_report.py (source)
    - contracts/dataset.schema.yaml (from T009)
    
    Returns:
        Dict: The updated state dictionary.
    """
    logger.info("Starting hash calculation for final artifacts...")
    
    # Define the list of artifacts to hash
    # These are the critical outputs generated by the pipeline
    artifacts_to_hash = [
        Path("data/processed/merged_data.csv"),
        Path("data/reports/final_analysis.html"),
        Path("data/raw/synthetic_data.csv"),
        Path("contracts/dataset.schema.yaml"),
        # Include key source files for reproducibility tracking
        Path("code/analysis/modeling.py"),
        Path("code/analysis/survival.py"),
        Path("code/reports/generate_report.py"),
        Path("code/data/ingestion.py"),
        Path("code/data/aggregation.py"),
        Path("code/data/synthetic_generator.py"),
    ]
    
    missing_artifacts = []
    state = load_state()
    
    for artifact_path in artifacts_to_hash:
        if not artifact_path.exists():
            logger.warning(f"Artifact not found, skipping: {artifact_path}")
            missing_artifacts.append(str(artifact_path))
            continue
        
        try:
            file_hash = calculate_sha256(artifact_path)
            state = update_artifact_state(state, artifact_path, file_hash)
            logger.info(f"Hashed: {artifact_path} -> {file_hash[:16]}...")
        except Exception as e:
            logger.error(f"Failed to hash {artifact_path}: {e}")
            missing_artifacts.append(str(artifact_path))
    
    if missing_artifacts:
        logger.warning(f"Skipped {len(missing_artifacts)} missing artifacts: {missing_artifacts}")
    
    # Update metadata
    state["metadata"]["last_updated"] = datetime.now().isoformat()
    
    # Save the updated state
    save_state(state)
    
    logger.info("Final artifact hashing complete.")
    return state

def verify_artifacts(expected_hashes: Dict[str, str]) -> bool:
    """
    Verify that existing artifacts match expected hashes.
    
    Args:
        expected_hashes: Dict mapping relative paths to expected SHA-256 hashes.
    
    Returns:
        bool: True if all verified artifacts match, False otherwise.
    """
    all_match = True
    current_state = load_state()
    
    for rel_path, expected_hash in expected_hashes.items():
        file_path = Path(rel_path)
        if not file_path.exists():
            logger.error(f"Verification failed: {file_path} does not exist.")
            all_match = False
            continue
        
        try:
            actual_hash = calculate_sha256(file_path)
            if actual_hash != expected_hash:
                logger.error(f"Hash mismatch for {file_path}: expected {expected_hash}, got {actual_hash}")
                all_match = False
            else:
                logger.info(f"Verification passed: {file_path}")
        except Exception as e:
            logger.error(f"Error verifying {file_path}: {e}")
            all_match = False
    
    return all_match

def main():
    """Main entry point for the versioning script."""
    log_pipeline_stage("versioning", "Starting artifact hashing and state update.")
    
    try:
        state = hash_all_final_artifacts()
        print(f"State updated successfully. Total artifacts tracked: {len(state.get('artifacts', {}))}")
        print(f"State file: {STATE_FILE}")
    except Exception as e:
        logger.error(f"Versioning process failed: {e}")
        raise

if __name__ == "__main__":
    main()