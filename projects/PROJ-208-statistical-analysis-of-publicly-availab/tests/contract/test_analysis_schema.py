    def test_schema_consistency_across_files(self, contracts_dir: Path):
        """
        Contract Test: Ensure all schema files are syntactically valid JSON Schema.
        """
        schema_files = [
            "hypothesis_testing_schema.json",
            "mixed_effects_schema.json",
            "distribution_metrics_schema.json"
        ]

        for fname in schema_files:
            fpath = contracts_dir / fname
            if fpath.exists():
                try:
                    with open(fpath, 'r') as f:
                        schema = json.load(f)
                    # Try to compile the schema to catch structural errors
                    jsonschema.Draft7Validator.check_schema(schema)
                    logger.info(f"Schema is valid JSON Schema: {fname}")
                except jsonschema.SchemaError as e:
                    pytest.fail(f"Schema {fname} is invalid: {e.message}")
                except json.JSONDecodeError:
                    pytest.fail(f"Schema {fname} is not valid JSON")