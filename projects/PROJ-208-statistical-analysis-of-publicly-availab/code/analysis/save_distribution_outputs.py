"""
Save distribution analysis results and figures.

This script consumes the metrics generated by distribution_fitting.py
and saves them to the required output locations:
- data/processed/distribution_metrics.json
- data/figures/ecdf_plot.png
- data/figures/fit_comparison_plot.png
"""
import json
import logging
import sys
import os
from pathlib import Path
from typing import Dict, Any, Optional
import matplotlib.pyplot as plt
import seaborn as sns
import numpy as np

# Add project root to path for imports
project_root = Path(__file__).resolve().parent.parent.parent
sys.path.insert(0, str(project_root / "code"))

from utils.config import get_config, get_path
from analysis.distribution_fitting import (
    load_cleaned_data,
    analyze_distributions,
    generate_ecdf_plot,
    generate_fit_comparison_plot
)

logging.basicConfig(
    level=logging.INFO,
    format='%(asctime)s - %(levelname)s - %(message)s'
)
logger = logging.getLogger(__name__)

def save_json_results(results: Dict[str, Any], output_path: Path) -> None:
    """Save distribution metrics to a JSON file."""
    output_path.parent.mkdir(parents=True, exist_ok=True)
    with open(output_path, 'w', encoding='utf-8') as f:
        json.dump(results, f, indent=2)
    logger.info(f"Saved results to {output_path}")

def save_figures(ecdf_fig: Optional[plt.Figure], fit_fig: Optional[plt.Figure], output_dir: Path) -> None:
    """Save generated figures to disk."""
    output_dir.mkdir(parents=True, exist_ok=True)

    if ecdf_fig:
        ecdf_path = output_dir / "ecdf_plot.png"
        ecdf_fig.savefig(ecdf_path, dpi=300, bbox_inches='tight')
        plt.close(ecdf_fig)
        logger.info(f"Saved ECDF plot to {ecdf_path}")

    if fit_fig:
        fit_path = output_dir / "fit_comparison_plot.png"
        fit_fig.savefig(fit_path, dpi=300, bbox_inches='tight')
        plt.close(fit_fig)
        logger.info(f"Saved fit comparison plot to {fit_path}")

def main():
    """Main entry point for saving distribution outputs."""
    config = get_config()
    
    # Define paths
    cleaned_data_path = get_path("cleaned_issues_csv")
    results_output_path = get_path("distribution_metrics_json")
    figures_output_dir = get_path("figures_dir")

    logger.info(f"Loading cleaned data from {cleaned_data_path}")
    if not cleaned_data_path.exists():
        logger.error(f"Cleaned data file not found: {cleaned_data_path}")
        sys.exit(1)

    # Load data
    df = load_cleaned_data(cleaned_data_path)
    if df.empty:
        logger.error("Loaded dataset is empty.")
        sys.exit(1)

    # Filter for positive resolution times for distribution fitting
    df_positive = df[df['resolution_time_hours'] > 0]
    if df_positive.empty:
        logger.error("No positive resolution times found for distribution fitting.")
        sys.exit(1)

    logger.info(f"Analyzing distribution for {len(df_positive)} samples")
    
    # Analyze distributions (fits log-normal and Weibull)
    # This function returns the results dictionary and the fitted parameters
    results = analyze_distributions(df_positive['resolution_time_hours'])

    # Generate plots
    logger.info("Generating ECDF plot")
    ecdf_fig = generate_ecdf_plot(df_positive['resolution_time_hours'])

    logger.info("Generating fit comparison plot")
    fit_fig = generate_fit_comparison_plot(df_positive['resolution_time_hours'], results)

    # Save outputs
    save_json_results(results, results_output_path)
    save_figures(ecdf_fig, fit_fig, figures_output_dir)

    logger.info("Distribution analysis outputs saved successfully.")

if __name__ == "__main__":
    main()