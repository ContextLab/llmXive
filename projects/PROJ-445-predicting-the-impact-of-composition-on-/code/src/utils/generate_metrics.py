def load_json_artifact(path: Path, default: Optional[Dict] = None) -> Dict:
    """Load a JSON artifact, returning default if missing or invalid."""
    if not path.exists():
        if default is not None:
            logger.warning(f"Artifact {path} not found. Using default: {default}")
            return default
        raise FileNotFoundError(f"Required artifact {path} not found")
    try:
        with open(path, 'r') as f:
            return json.load(f)
    except json.JSONDecodeError as e:
        logger.error(f"Failed to parse {path}: {e}")
        if default is not None:
            return default
        raise

