def load_preprocessed_data() -> pd.DataFrame:
    """Load the processed dataset from data/processed/."""
    processed_path = DATA_DIR / "processed" / "chalcogenide_processed.csv"
    if not processed_path.exists():
        raise FileNotFoundError(f"Processed data not found at {processed_path}. Run T013 first.")
    logger.info(f"Loading processed data from {processed_path}")
    return pd.read_csv(processed_path)

def load_split_indices() -> Dict[str, Any]:
    """Load split indices from data/splits/."""
    split_path = DATA_DIR / "splits" / "split_indices.json"
    if not split_path.exists():
        raise FileNotFoundError(f"Split indices not found at {split_path}. Run T014 first.")
    logger.info(f"Loading split indices from {split_path}")
    with open(split_path, 'r') as f:
        return json.load(f)

def load_lofo_models() -> Dict[str, GradientBoostingRegressor]:
    """
    Load the N-1 family models trained by T025 (lofo_training.py).
    Returns a dict mapping family_name -> model.
    """
    if not LOFO_MODELS_DIR.exists():
        raise FileNotFoundError(f"LOFO models directory not found at {LOFO_MODELS_DIR}. "
                              "Ensure T025 (train_lofo_models) has been executed.")

    models = {}
    for model_file in LOFO_MODELS_DIR.glob("*.pkl"):
        family_name = model_file.stem.replace("_model", "")
        logger.info(f"Loading LOFO model for family: {family_name} from {model_file}")
        with open(model_file, 'rb') as f:
            models[family_name] = pickle.load(f)

    if not models:
        raise FileNotFoundError("No LOFO models found in the directory. T025 must be completed first.")

    return models

def compute_shap_values(model: GradientBoostingRegressor, X: pd.DataFrame) -> Tuple[np.ndarray, shap.Explainer]:
    """
    Compute SHAP values for a given model and dataset.
    Uses TreeExplainer for Gradient Boosting models.
    """
    logger.info("Computing SHAP values using TreeExplainer...")
    # Limit samples for speed if dataset is large (per SC-006)
    max_samples = 5000
    if len(X) > max_samples:
        logger.warning(f"Dataset has {len(X)} samples. Sampling {max_samples} for SHAP computation.")
        X_sample = X.sample(n=max_samples, random_state=42)
    else:
        X_sample = X

    explainer = shap.TreeExplainer(model)
    shap_values = explainer.shap_values(X_sample)

    # Handle case where shap_values might be a list (for multi-output, though we have single output)
    if isinstance(shap_values, list):
        shap_values = shap_values[0]

    return shap_values, explainer

def analyze_transferability(
    lofo_models: Dict[str, GradientBoostingRegressor],
    data: pd.DataFrame,
    split_indices: Dict[str, Any]
) -> Dict[str, Any]:
    """
    Perform Cross-Family Transferability Test.
    1. For each held-out family, load its corresponding N-1 model.
    2. Evaluate performance (RMSE, R2) on the held-out family.
    3. Compute SHAP values for the held-out family using the N-1 model.
    4. Compare feature importances (mean |SHAP|) across families.
    """
    logger.info("Starting Cross-Family Transferability Analysis...")

    results = {
        "transferability_metrics": {},
        "shap_importance_by_family": {},
        "feature_importance_rankings": []
    }

    # Get unique families from the data
    if "chemical_family" not in data.columns:
        raise ValueError("Column 'chemical_family' not found in processed data. Run T014 (split.py) first.")

    families = data["chemical_family"].unique()
    logger.info(f"Found {len(families)} chemical families: {families}")

    # Prepare feature columns (exclude target and metadata)
    target_col = "Tg"
    exclude_cols = [target_col, "composition", "chemical_family"]
    feature_cols = [c for c in data.columns if c not in exclude_cols]

    for family in families:
        if family not in lofo_models:
            logger.warning(f"No N-1 model found for held-out family: {family}. Skipping.")
            continue

        logger.info(f"--- Analyzing Transferability for Family: {family} ---")
        
        # 1. Get held-out data for this family
        held_out_mask = data["chemical_family"] == family
        X_held_out = data.loc[held_out_mask, feature_cols]
        y_held_out = data.loc[held_out_mask, target_col]

        if len(X_held_out) == 0:
            logger.warning(f"No samples found for family {family} in data.")
            continue

        model = lofo_models[family]

        # 2. Evaluate Performance
        y_pred = model.predict(X_held_out)
        rmse = np.sqrt(mean_squared_error(y_held_out, y_pred))
        r2 = r2_score(y_held_out, y_pred)

        results["transferability_metrics"][family] = {
            "rmse": float(rmse),
            "r2": float(r2),
            "n_samples": int(len(y_held_out))
        }
        logger.info(f"  Performance on {family}: RMSE={rmse:.2f}, R2={r2:.3f}")

        # 3. Compute SHAP values
        shap_vals, _ = compute_shap_values(model, X_held_out)
        
        # Mean absolute SHAP values
        mean_abs_shap = np.mean(np.abs(shap_vals), axis=0)
        
        # Create a series for ranking
        shap_importance = pd.Series(mean_abs_shap, index=feature_cols).sort_values(ascending=False)
        
        results["shap_importance_by_family"][family] = shap_importance.to_dict()
        
        # Store ranking
        results["feature_importance_rankings"].append({
            "family": family,
            "ranking": shap_importance.index.tolist(),
            "values": shap_importance.values.tolist()
        })

        logger.info(f"  Top 3 features for {family}: {shap_importance.index[:3].tolist()}")

    return results

def save_shap_results(results: Dict[str, Any]) -> str:
    """Save SHAP and transferability results to artifacts/ and update manifest."""
    output_path = ARTIFACTS_DIR / "shap_transferability_results.json"
    
    with open(output_path, 'w') as f:
        json.dump(results, f, indent=2, default=str)
    
    logger.info(f"Saved SHAP transferability results to {output_path}")
    
    # Update manifest
    manifest_path = STATE_DIR / "manifest.json"
    if manifest_path.exists():
        manifest = load_manifest(manifest_path)
        register_artifact(manifest, str(output_path.relative_to(PROJECT_ROOT)), compute_file_hash(output_path))
        save_manifest(manifest, manifest_path)
    
    return str(output_path)

def main():
    """Main entry point for Cross-Family Transferability Test."""
    logger.info("Starting T030: Cross-Family Transferability Test")
    
    try:
        # 1. Load Data
        data = load_preprocessed_data()
        split_indices = load_split_indices()
        
        # 2. Load Pre-trained N-1 Models (Dependency: T025)
        lofo_models = load_lofo_models()
        
        # 3. Analyze Transferability
        results = analyze_transferability(lofo_models, data, split_indices)
        
        # 4. Save Results
        output_path = save_shap_results(results)
        
        logger.info("T030 completed successfully.")
        print(f"Results saved to: {output_path}")
        
        # Return summary for potential downstream use
        return results

    except FileNotFoundError as e:
        logger.error(f"Missing required dependency: {e}")
        print(f"ERROR: {e}")
        print("Ensure T013 (preprocess), T014 (split), and T025 (lofo_training) are completed first.")
        sys.exit(1)
    except Exception as e:
        logger.exception(f"Unexpected error during T030 execution: {e}")
        sys.exit(1)

if __name__ == "__main__":
    main()