def test_manifest_update_after_training(temp_project_root, mock_manifest_path):
    """
    Integration Test: Verify that the manifest is updated with the new model artifacts
    and their checksums after training completes.
    """
    os.environ["PROJECT_ROOT"] = str(temp_project_root)
    
    train_models(project_root=str(temp_project_root))
    
    # Load updated manifest
    with open(mock_manifest_path, "r") as f:
        manifest = json.load(f)
    
    # Check that model artifacts are registered
    # The train module should register models and metrics
    assert "artifacts" in manifest
    
    # Check for specific expected artifacts (paths may vary slightly based on implementation)
    expected_artifacts = [
        "artifacts/performance_metrics.json",
        "data/models/linear_regression.pkl",
        "data/models/gradient_boosting.pkl"
    ]
    
    for artifact_path in expected_artifacts:
        full_path = temp_project_root / artifact_path
        # If the artifact exists on disk, it must be in the manifest
        if full_path.exists():
            assert artifact_path in manifest["artifacts"], \
                f"Artifact {artifact_path} exists on disk but not in manifest."
            # Verify hash matches
            recorded_hash = manifest["artifacts"][artifact_path].get("hash")
            actual_hash = compute_file_hash(full_path)
            assert recorded_hash == actual_hash, \
                f"Hash mismatch for {artifact_path}: recorded={recorded_hash}, actual={actual_hash}"


def test_cpu_only_execution(temp_project_root, mock_manifest_path):
    """
    Integration Test: Ensure that the training process runs without GPU/CUDA dependencies.
    This is verified by checking that no CUDA-related errors occur and that the code
    explicitly uses CPU devices (scikit-learn defaults to CPU).
    """
    os.environ["PROJECT_ROOT"] = str(temp_project_root)
    os.environ["CUDA_VISIBLE_DEVICES"] = ""  # Force CPU context
    
    try:
        metrics = train_models(project_root=str(temp_project_root))
    except RuntimeError as e:
        if "CUDA" in str(e) or "gpu" in str(e).lower():
            pytest.fail("Training failed due to GPU/CUDA dependency: " + str(e))
        else:
            raise e
    
    # If we reach here, CPU execution was successful
    assert metrics is not None, "Metrics were not returned."
    assert "gradient_boosting" in metrics, "Gradient Boosting metrics missing."