def test_cross_validation_timing(temp_project_root, mock_manifest_path):
    """
    Integration Test: Verify that 5-fold cross-validation completes within the 6-hour CI limit.
    
    This test:
    1. Loads the processed dataset from data/processed/processed_data.csv
    2. Trains the Linear Regression and Gradient Boosting models with 5-fold CV
    3. Measures the total time taken
    4. Asserts time < 6 hours (MAX_CV_TIME_SECONDS)
    """
    # Patch the project root path resolution in the train module if necessary
    # For this test, we assume the train module uses relative paths or we set the env
    os.environ["PROJECT_ROOT"] = str(temp_project_root)
    
    start_time = time.time()
    
    # Run the training pipeline
    # The train_models function is expected to:
    # - Load data from data/processed/processed_data.csv
    # - Perform 5-fold CV
    # - Save metrics to artifacts/performance_metrics.json
    try:
        metrics = train_models(project_root=str(temp_project_root))
    except Exception as e:
        pytest.fail(f"Training pipeline failed unexpectedly: {e}")
    
    elapsed_time = time.time() - start_time
    
    # Assert timing constraint
    assert elapsed_time < MAX_CV_TIME_SECONDS, \
        f"Cross-validation took {elapsed_time:.2f}s, exceeding the 6-hour limit."
    
    # Verify metrics structure
    assert "linear_regression" in metrics
    assert "gradient_boosting" in metrics
    assert "rmse" in metrics["gradient_boosting"]
    assert "r2" in metrics["gradient_boosting"]
    
    # Verify artifact was written
    metrics_file = temp_project_root / "artifacts" / "performance_metrics.json"
    assert metrics_file.exists(), "performance_metrics.json was not created."
    
    with open(metrics_file, "r") as f:
        saved_metrics = json.load(f)
    
    assert saved_metrics["gradient_boosting"]["rmse"] > 0
    assert saved_metrics["gradient_boosting"]["r2"] <= 1.0


def test_metrics_accuracy(temp_project_root, mock_manifest_path):
    """
    Integration Test: Verify that RMSE and R2 metrics are computed correctly
    and that the Gradient Boosting model outperforms or matches the Linear Baseline
    on the held-out test set (or via CV average).
    """
    os.environ["PROJECT_ROOT"] = str(temp_project_root)
    
    metrics = train_models(project_root=str(temp_project_root))
    
    # Basic sanity checks on metrics
    lr_rmse = metrics["linear_regression"]["rmse"]
    gb_rmse = metrics["gradient_boosting"]["rmse"]
    lr_r2 = metrics["linear_regression"]["r2"]
    gb_r2 = metrics["gradient_boosting"]["r2"]
    
