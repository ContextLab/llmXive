    assert lr_r2 <= 1.0, "Linear Regression R2 cannot exceed 1.0."
    assert gb_r2 <= 1.0, "Gradient Boosting R2 cannot exceed 1.0."
    
    # Log results for manual verification if needed
    print(f"Linear Regression - RMSE: {lr_rmse:.4f}, R2: {lr_r2:.4f}")
    print(f"Gradient Boosting - RMSE: {gb_rmse:.4f}, R2: {gb_r2:.4f}")
    
    # Optional: Assert GB is not drastically worse (heuristic check)
    # In real data, GB should generally perform better or similar to Linear
