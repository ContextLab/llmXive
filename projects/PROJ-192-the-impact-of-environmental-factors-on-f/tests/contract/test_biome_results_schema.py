"""
Contract test for biome-stratified results schema.

This test verifies that the results generated for biome-stratified analysis
conform to the expected schema defined in the project specifications.
It specifically checks for the presence of required columns and data types
in the output CSV files generated by User Story 2 (T028).

Schema Expectations (from data-model.md and spec.md):
- File: results/db_rda_biome_<NAME>.csv
- Required Columns:
    - term (string): The environmental variable name.
    - biome (string): The biome category this result belongs to.
    - R2 (float): The coefficient of determination.
    - p_value (float): The raw p-value.
    - p_value_adj (float): The FDR-corrected p-value.
    - rank (int): The ranking of the driver within this biome.
"""

import os
import tempfile
import pandas as pd
import pytest
from pathlib import Path

# Constants for schema validation
REQUIRED_COLUMNS = {
    "term",
    "biome",
    "R2",
    "p_value",
    "p_value_adj",
    "rank"
}

REQUIRED_NUMERIC_COLUMNS = {"R2", "p_value", "p_value_adj", "rank"}
REQUIRED_STRING_COLUMNS = {"term", "biome"}

# Expected file naming pattern
EXPECTED_FILE_PATTERN = "db_rda_biome_*.csv"


def _create_mock_biome_results(temp_dir: Path, biome_name: str, valid: bool = True) -> Path:
    """
    Helper to create a mock results file for testing.

    Args:
        temp_dir: Temporary directory to write the file.
        biome_name: Name of the biome to include in the filename and data.
        valid: If False, creates a file with missing/invalid columns.

    Returns:
        Path to the created file.
    """
    filename = f"db_rda_biome_{biome_name}.csv"
    filepath = temp_dir / filename

    if valid:
        data = {
            "term": ["pH", "Moisture", "Nutrients"],
            "biome": [biome_name, biome_name, biome_name],
            "R2": [0.45, 0.32, 0.15],
            "p_value": [0.001, 0.02, 0.15],
            "p_value_adj": [0.003, 0.04, 0.25],
            "rank": [1, 2, 3]
        }
    else:
        # Intentionally missing 'p_value_adj' and 'rank'
        data = {
            "term": ["pH", "Moisture"],
            "biome": [biome_name, biome_name],
            "R2": [0.45, 0.32],
            "p_value": [0.001, 0.02]
        }

    df = pd.DataFrame(data)
    df.to_csv(filepath, index=False)
    return filepath


class TestBiomeStratifiedResultsSchema:
    """Tests to ensure biome-stratified results adhere to the project schema."""

    def test_schema_validation_passes_on_valid_data(self, tmp_path):
        """
        Verify that a correctly formatted biome results file passes validation.
        """
        # Arrange
        biome_name = "Forest"
        filepath = _create_mock_biome_results(tmp_path, biome_name, valid=True)

        # Act
        df = pd.read_csv(filepath)
        columns_present = set(df.columns)

        # Assert
        assert REQUIRED_COLUMNS.issubset(
            columns_present
        ), f"Missing columns: {REQUIRED_COLUMNS - columns_present}"

        # Verify data types
        for col in REQUIRED_NUMERIC_COLUMNS:
            assert pd.api.types.is_numeric_dtype(df[col]), f"Column {col} must be numeric"

        for col in REQUIRED_STRING_COLUMNS:
            assert df[col].dtype == object or df[col].dtype.name == "string", f"Column {col} must be string"

        # Verify biome consistency
        assert df["biome"].unique() == [biome_name], "All rows must belong to the specified biome"

    def test_schema_validation_fails_on_missing_columns(self, tmp_path):
        """
        Verify that a file with missing required columns fails validation.
        """
        # Arrange
        biome_name = "Grassland"
        filepath = _create_mock_biome_results(tmp_path, biome_name, valid=False)

        # Act
        df = pd.read_csv(filepath)
        columns_present = set(df.columns)

        # Assert
        missing = REQUIRED_COLUMNS - columns_present
        assert len(missing) > 0, "Expected missing columns but file was valid"
        
        with pytest.raises(AssertionError) as exc_info:
            assert REQUIRED_COLUMNS.issubset(columns_present)
        
        assert "Missing columns" in str(exc_info.value)

    def test_schema_validation_fails_on_wrong_data_types(self, tmp_path):
        """
        Verify that a file with incorrect data types (e.g., string R2) fails validation.
        """
        # Arrange
        biome_name = "Desert"
        filepath = _create_mock_biome_results(tmp_path, biome_name, valid=True)
        
        # Corrupt the data: make R2 a string
        df = pd.read_csv(filepath)
        df["R2"] = df["R2"].astype(str)
        corrupted_path = tmp_path / "db_rda_biome_Desert_corrupted.csv"
        df.to_csv(corrupted_path, index=False)

        # Act
        df_corrupted = pd.read_csv(corrupted_path)

        # Assert
        assert not pd.api.types.is_numeric_dtype(df_corrupted["R2"]), "Test setup failed: R2 should be string now"
        
        with pytest.raises(AssertionError):
            for col in REQUIRED_NUMERIC_COLUMNS:
                assert pd.api.types.is_numeric_dtype(df_corrupted[col]), f"Column {col} must be numeric"

    def test_file_naming_convention(self, tmp_path):
        """
        Verify that the generated files follow the expected naming convention.
        """
        # Arrange
        valid_biome = "Tundra"
        invalid_biome = "Tundra (Invalid)" # Contains spaces/parentheses which might break simple parsers
        
        _create_mock_biome_results(tmp_path, valid_biome, valid=True)
        # Create a file that doesn't match the pattern (though technically valid CSV)
        (tmp_path / "random_file.csv").touch()

        # Act
        matched_files = list(tmp_path.glob(EXPECTED_FILE_PATTERN))

        # Assert
        assert len(matched_files) == 1, f"Expected 1 file matching pattern, found {len(matched_files)}"
        assert matched_files[0].stem.startswith("db_rda_biome_")

    def test_empty_dataframe_handling(self, tmp_path):
        """
        Verify behavior when a biome has 0 samples (though T026 should skip this,
        the schema should handle empty files gracefully if they exist).
        """
        # Arrange
        filepath = tmp_path / "db_rda_biome_Empty.csv"
        empty_df = pd.DataFrame(columns=list(REQUIRED_COLUMNS))
        empty_df.to_csv(filepath, index=False)

        # Act
        df = pd.read_csv(filepath)

        # Assert
        assert len(df) == 0, "Expected empty dataframe"
        assert set(df.columns) == REQUIRED_COLUMNS, "Columns must still be present even if empty"