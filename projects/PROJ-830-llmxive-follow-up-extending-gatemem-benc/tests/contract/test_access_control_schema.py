def test_file_exists(output_path):
    """Contract Test 1: Verify the output file exists."""
    assert os.path.exists(output_path), f"Output file not found: {output_path}"

def test_file_is_valid_json(output_path):
    """Contract Test 2: Verify the file is valid JSON."""
    try:
        with open(output_path, "r") as f:
            json.load(f)
    except json.JSONDecodeError as e:
        pytest.fail(f"Invalid JSON format: {e}")

def test_schema_compliance(output_path, schema_path):
    """
    Contract Test 3: Verify content matches results.schema.yaml.
    Checks structure, required fields, and value constraints.
    """
    with open(output_path, "r") as f:
        data = json.load(f)

    # Handle both single record (dict) and list of records
    records = data if isinstance(data, list) else [data]

    assert len(records) > 0, "Results file is empty"

    all_errors = []
    for i, record in enumerate(records):
        errors = validate_record(record, {})
        if errors:
            all_errors.append(f"Record {i}: {errors}")

    if all_errors:
        pytest.fail("Schema validation failed:\n" + "\n".join(all_errors))

def test_contains_expected_domains(output_path):
    """
    Contract Test 4: Verify results contain expected domains (medical, office).
    Based on T012 description: "Run automated evaluation on 'medical' and 'office' domains".
    """
    with open(output_path, "r") as f:
        data = json.load(f)

    records = data if isinstance(data, list) else [data]
    domains_found = {r.get("domain") for r in records if isinstance(r, dict)}

    expected_domains = {"medical", "office"}
    missing_domains = expected_domains - domains_found

    if missing_domains:
        pytest.fail(f"Missing expected domains in results: {missing_domains}. Found: {domains_found}")

def test_metrics_are_reasonable(output_path):
    """
    Contract Test 5: Verify metrics are within logical bounds.
    """
    with open(output_path, "r") as f:
        data = json.load(f)

    records = data if isinstance(data, list) else [data]

    for i, record in enumerate(records):
        if not isinstance(record, dict):
            continue
        
        score = record.get("access_control_score")
        if score is not None and (score < 0.0 or score > 1.0):
            pytest.fail(f"Record {i}: access_control_score {score} out of [0, 1] range")
        
        rate = record.get("unauthorized_exposure_rate")
        if rate is not None and (rate < 0.0 or rate > 1.0):
            pytest.fail(f"Record {i}: unauthorized_exposure_rate {rate} out of [0, 1] range")