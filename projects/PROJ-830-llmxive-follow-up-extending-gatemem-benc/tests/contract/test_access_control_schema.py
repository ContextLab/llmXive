"""
Contract test for T012: Verify data/processed/access_control_results.json matches results.schema.yaml.

This test validates that the output file generated by the Access Control evaluation
conforms to the schema defined in contracts/results.schema.yaml.
"""
import json
import os
import pytest
import yaml
from typing import Any, Dict, List

# Import the schema validation logic if available, or implement inline
# Since T005 (schema creation) is marked as rejected in the back-log, we implement
# the schema validation inline to ensure this test is self-contained and runnable.

# Expected structure based on results.schema.yaml (Access Control section)
EXPECTED_SCHEMA_KEYS = {
    "method": str,
    "domain": str,
    "access_control_score": (int, float),
    "unauthorized_exposure_rate": (int, float),
    "total_episodes": int,
    "failed_episodes": int,
    "timestamp": str,
    "details": dict
}

REQUIRED_FIELDS = [
    "method",
    "domain",
    "access_control_score",
    "unauthorized_exposure_rate",
    "total_episodes",
    "failed_episodes",
    "timestamp"
]

def load_schema(schema_path: str) -> Dict[str, Any]:
    """Load the YAML schema file."""
    if not os.path.exists(schema_path):
        # Fallback to inline definition if schema file is missing (as per back-log status)
        return {}
    with open(schema_path, "r") as f:
        return yaml.safe_load(f)

def validate_record(record: Dict[str, Any], schema: Dict[str, Any]) -> List[str]:
    """
    Validate a single record against the expected structure.
    Returns a list of error messages.
    """
    errors = []
    
    # Check required fields
    for field in REQUIRED_FIELDS:
        if field not in record:
            errors.append(f"Missing required field: {field}")
    
    # Type checking for known fields
    if "access_control_score" in record:
        if not isinstance(record["access_control_score"], (int, float)):
            errors.append("access_control_score must be numeric")
        elif not (0.0 <= record["access_control_score"] <= 1.0):
            errors.append("access_control_score must be between 0.0 and 1.0")

    if "unauthorized_exposure_rate" in record:
        if not isinstance(record["unauthorized_exposure_rate"], (int, float)):
            errors.append("unauthorized_exposure_rate must be numeric")
        
    if "total_episodes" in record:
        if not isinstance(record["total_episodes"], int) or record["total_episodes"] < 0:
            errors.append("total_episodes must be a non-negative integer")
            
    if "failed_episodes" in record:
        if not isinstance(record["failed_episodes"], int) or record["failed_episodes"] < 0:
            errors.append("failed_episodes must be a non-negative integer")

    # Cross-field consistency
    if "total_episodes" in record and "failed_episodes" in record:
        if record["failed_episodes"] > record["total_episodes"]:
            errors.append("failed_episodes cannot exceed total_episodes")

    return errors

