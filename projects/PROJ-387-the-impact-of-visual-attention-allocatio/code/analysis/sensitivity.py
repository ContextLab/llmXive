import os
import sys
import json
import argparse
from pathlib import Path
from typing import List, Dict, Any, Optional

import pandas as pd

# Import project utilities
sys.path.insert(0, str(Path(__file__).parent.parent))
from utils.config import get_project_root, get_output_path
from utils.logger import get_logger
from models.data_models import AnalysisResult

logger = get_logger(__name__)

ASSOCIATION_LABEL = "associational"

def load_lmm_results(results_path: Optional[Path] = None) -> List[Dict[str, Any]]:
    """
    Load LMM results from the JSON file generated by lmm_model.py.
    """
    if results_path is None:
        output_dir = get_output_path()
        results_path = output_dir / "results" / "lmm_results.json"
    
    if not results_path.exists():
        logger.error(f"LMM results not found at {results_path}")
        raise FileNotFoundError(f"LMM results not found at {results_path}")
    
    with open(results_path, 'r') as f:
        data = json.load(f)
    
    logger.info(f"Loaded {len(data)} LMM results")
    return data

def run_sensitivity_analysis(
    lmm_results: List[Dict[str, Any]],
    thresholds: List[float] = [0.01, 0.05, 0.1]
) -> Dict[str, Any]:
    """
    Perform sensitivity analysis by sweeping p-value thresholds.
    Reports the rate of significant findings at each threshold.
    """
    analysis_results = []
    
    # Filter out failed runs
    valid_results = [r for r in lmm_results if r.get('p_raw') is not None]
    total_count = len(valid_results)
    
    if total_count == 0:
        logger.warning("No valid LMM results to analyze")
        return {"thresholds": thresholds, "rates": [], "total_valid": 0}

    logger.info(f"Running sensitivity analysis on {total_count} valid results")

    for threshold in thresholds:
        significant_count = sum(1 for r in valid_results if r['p_raw'] <= threshold)
        rate = significant_count / total_count if total_count > 0 else 0.0
        
        analysis_results.append({
            "threshold": threshold,
            "significant_count": significant_count,
            "total_count": total_count,
            "rate": rate,
            "association_label": ASSOCIATION_LABEL  # FR-005 Requirement
        })
        
        logger.debug(f"Threshold {threshold}: {significant_count}/{total_count} significant (rate: {rate:.4f})")

    return {
        "thresholds": thresholds,
        "analysis": analysis_results,
        "total_valid_combinations": total_count,
        "association_label": ASSOCIATION_LABEL
    }

def save_results(analysis_results: Dict[str, Any], output_path: Optional[Path] = None):
    """
    Save sensitivity analysis results to JSON.
    """
    if output_path is None:
        output_dir = get_output_path()
        output_path = output_dir / "results"
    
    output_path.mkdir(parents=True, exist_ok=True)
    
    file_path = output_path / "sensitivity_analysis.json"
    
    with open(file_path, 'w') as f:
        json.dump(analysis_results, f, indent=2)
    
    logger.info(f"Saved sensitivity analysis to {file_path}")

def main():
    parser = argparse.ArgumentParser(description="Run Sensitivity Analysis on LMM Results")
    parser.add_argument("--thresholds", nargs="+", type=float, default=[0.01, 0.05, 0.1],
                        help="P-value thresholds to sweep")
    args = parser.parse_args()

    try:
        lmm_results = load_lmm_results()
        analysis = run_sensitivity_analysis(lmm_results, args.thresholds)
        save_results(analysis)
        logger.info("Sensitivity Analysis completed successfully.")
        sys.exit(0)
    except FileNotFoundError as e:
        logger.error(f"Input data error: {e}")
        sys.exit(1)
    except Exception as e:
        logger.error(f"Analysis failed: {e}")
        sys.exit(1)

if __name__ == "__main__":
    main()
