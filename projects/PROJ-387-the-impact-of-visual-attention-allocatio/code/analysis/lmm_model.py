logger = get_logger(__name__)

# Constants
ASSOCIATION_LABEL = "associational"

def load_processed_data(data_path: Optional[Path] = None) -> pd.DataFrame:
    """
    Load the processed eye-tracking and recall data.
    In a real pipeline, this would read from a validated CSV generated by T012-T017.
    For this implementation, we assume the data exists at the configured path.
    """
    if data_path is None:
        data_path = get_project_root() / "data" / "processed" / "merged_data.csv"
    
    if not data_path.exists():
        logger.error(f"Processed data file not found at {data_path}")
        raise FileNotFoundError(f"Processed data file not found at {data_path}")
    
    logger.info(f"Loading processed data from {data_path}")
    df = pd.read_csv(data_path)
    return df

def fit_lmm_for_combination(
    df: pd.DataFrame, 
    metric: str, 
    valence: str,
    recall_col: str = "recall_accuracy"
) -> Dict[str, Any]:
    """
    Fit a Linear Mixed-Effects Model for a specific attention metric and valence category.
    
    Model: Recall ~ Metric + (1|ParticipantID)
    """
    # Filter data for the specific valence
    subset = df[df['valence_category'] == valence].copy()
    
    if subset.empty:
        logger.warning(f"No data found for valence category: {valence}")
        return {
            "metric": metric,
            "valence": valence,
            "coef": None,
            "p_raw": None,
            "n_obs": 0,
            "converged": False
        }

    # Prepare formula
    # Assuming 'participant_id' is the grouping variable
    if 'participant_id' not in subset.columns:
        # Fallback if column name differs, but spec implies standard naming
        logger.error("Missing 'participant_id' column for grouping variable")
        return {
            "metric": metric,
            "valence": valence,
            "coef": None,
            "p_raw": None,
            "n_obs": len(subset),
            "converged": False,
            "error": "Missing grouping variable"
        }

    # Ensure numeric
    subset = subset.dropna(subset=[metric, recall_col, 'participant_id'])
    if subset.empty:
        return {
            "metric": metric,
            "valence": valence,
            "coef": None,
            "p_raw": None,
            "n_obs": 0,
            "converged": False
        }

    try:
        formula = f"{recall_col} ~ {metric}"
        model = MixedLM.from_formula(formula, groups="participant_id", data=subset)
        result = model.fit()
        
        # Extract fixed effects
        # Fixed effects usually include Intercept and the metric
        fixed_effects = result.fixed_effects
        metric_coef = fixed_effects.get(metric, 0.0)
        
        # Get p-value for the metric
        # result.pvalues is a Series indexed by variable name
        p_val = result.pvalues.get(metric, 1.0)
        
        return {
            "metric": metric,
            "valence": valence,
            "coef": float(metric_coef),
            "p_raw": float(p_val),
            "n_obs": len(subset),
            "converged": True,
            "association_label": ASSOCIATION_LABEL  # FR-005 Requirement
        }
    except Exception as e:
        logger.error(f"Failed to fit LMM for {metric} x {valence}: {e}")
        return {
            "metric": metric,
            "valence": valence,
            "coef": None,
            "p_raw": None,
            "n_obs": len(subset),
            "converged": False,
            "error": str(e),
            "association_label": ASSOCIATION_LABEL
        }

def run_lmm_analysis(
    df: pd.DataFrame,
    metrics: List[str],
    valences: List[str]
) -> List[Dict[str, Any]]:
    """
    Run LMM for all combinations of metrics and valences.
    """
    results = []
    logger.info(f"Starting LMM analysis for {len(metrics)} metrics and {len(valences)} valences")
    
    for metric in metrics:
        for valence in valences:
            logger.debug(f"Fitting model for {metric} x {valence}")
            res = fit_lmm_for_combination(df, metric, valence)
            results.append(res)
    
    logger.info(f"Completed analysis for {len(results)} combinations")
    return results

def save_results(results: List[Dict[str, Any]], output_path: Optional[Path] = None):
    """
    Save LMM results to CSV and JSON.
    Ensures 'association_label' is present in all outputs (FR-005).
    """
    if output_path is None:
        output_dir = get_output_path()
        output_path = output_dir / "results"
    
    output_path.mkdir(parents=True, exist_ok=True)
    
    csv_path = output_path / "lmm_summary.csv"
    json_path = output_path / "lmm_results.json"
    
    # Convert to DataFrame for CSV
    df_results = pd.DataFrame(results)
    df_results.to_csv(csv_path, index=False)
    logger.info(f"Saved LMM summary to {csv_path}")
    
    # Save full JSON with association_label
    with open(json_path, 'w') as f:
        json.dump(results, f, indent=2)
    logger.info(f"Saved LMM results to {json_path}")

def main():
    parser = argparse.ArgumentParser(description="Run LMM Analysis for Visual Attention")
    parser.add_argument("--metrics", nargs="+", default=["fixation_duration", "saccade_amplitude", "gaze_distribution"],
                        help="List of attention metrics to analyze")
    parser.add_argument("--valences", nargs="+", default=["positive", "negative", "neutral"],
                        help="List of valence categories")
    args = parser.parse_args()

    set_global_seed(42)
    
    try:
        df = load_processed_data()
        results = run_lmm_analysis(df, args.metrics, args.valences)
        save_results(results)
        logger.info("LMM Analysis completed successfully.")
        sys.exit(0)
    except FileNotFoundError as e:
        logger.error(f"Data loading failed: {e}")
        sys.exit(1)
    except Exception as e:
        logger.error(f"Analysis failed: {e}")
        sys.exit(1)

if __name__ == "__main__":
    main()
