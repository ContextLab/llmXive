"""
Model Training Module for Solar Irradiance Reconstruction.

Implements Cycle-Aware Random Forest and Gaussian Process models with
Leave-One-Cycle-Out (LOCO) Cross-Validation as per FR-003.
"""
import os
import json
import logging
from pathlib import Path
from typing import Dict, List, Any, Tuple

import pandas as pd
import numpy as np
from sklearn.ensemble import RandomForestRegressor
from sklearn.gaussian_process import GaussianProcessRegressor
from sklearn.gaussian_process.kernels import RBF, WhiteKernel, ConstantKernel
from sklearn.model_selection import cross_val_predict
from sklearn.metrics import mean_squared_error, r2_score
import joblib

# Import project utilities
from config import ensure_directories
from env_manager import get_data_path, setup_environment

# Configure logging
logging.basicConfig(level=logging.INFO, format='%(asctime)s - %(levelname)s - %(message)s')
logger = logging.getLogger(__name__)

# Constants
RANDOM_SEED = 42
RF_MAX_DEPTH = 10
RF_N_ESTIMATORS = 100
ARTIFACTS_DIR = "code/models/artifacts"
CV_REPORT_PATH = "data/processed/cv_report.json"
BEST_MODEL_PATH = "code/models/artifacts/best_model.joblib"

def load_preprocessed_data() -> pd.DataFrame:
    """
    Load the preprocessed dataset containing GSN, TSI, and Cycle IDs.
    Expects 'data/processed/preprocessed_data.parquet' generated by T014.
    """
    data_path = Path(get_data_path("processed")) / "preprocessed_data.parquet"
    if not data_path.exists():
        raise FileNotFoundError(
            f"Preprocessed data not found at {data_path}. "
            "Run T014 (preprocessing) first."
        )
    logger.info(f"Loading preprocessed data from {data_path}")
    df = pd.read_parquet(data_path)
    
    # Ensure Cycle ID is integer for categorical handling
    if 'cycle_id' in df.columns:
        df['cycle_id'] = df['cycle_id'].astype(int)
    
    return df

def prepare_features(df: pd.DataFrame) -> Tuple[pd.DataFrame, pd.Series]:
    """
    Prepare features (X) and target (y) for training.
    Features: GSN (smoothed or raw), Cycle ID (categorical).
    Target: TSI.
    """
    # Validate columns
    required_cols = ['tsi', 'gsn', 'cycle_id']
    missing = [c for c in required_cols if c not in df.columns]
    if missing:
        raise ValueError(f"Missing required columns in preprocessed data: {missing}")
    
    # Drop rows with NaN in critical columns
    df_clean = df.dropna(subset=['tsi', 'gsn', 'cycle_id'])
    
    X = df_clean[['gsn', 'cycle_id']].copy()
    y = df_clean['tsi']
    
    logger.info(f"Prepared features: {X.shape}, Target: {y.shape}")
    return X, y

def train_random_forest(X: pd.DataFrame, y: pd.Series) -> RandomForestRegressor:
    """Train Random Forest model with specified hyperparameters."""
    logger.info("Training Random Forest model...")
    model = RandomForestRegressor(
        n_estimators=RF_N_ESTIMATORS,
        max_depth=RF_MAX_DEPTH,
        random_state=RANDOM_SEED,
        n_jobs=-1
    )
    model.fit(X, y)
    logger.info("Random Forest training complete.")
    return model

def train_gaussian_process(X: pd.DataFrame, y: pd.Series) -> GaussianProcessRegressor:
    """Train Gaussian Process model with RBF kernel."""
    logger.info("Training Gaussian Process model...")
    # Normalize kernel scaling
    kernel = ConstantKernel(1.0) * RBF(length_scale=1.0) + WhiteKernel(noise_level=1e-5)
    model = GaussianProcessRegressor(kernel=kernel, random_state=RANDOM_SEED, n_jobs=-1)
    model.fit(X, y)
    logger.info("Gaussian Process training complete.")
    return model

def evaluate_model(model: Any, X: pd.DataFrame, y: pd.Series, method_name: str) -> Dict[str, float]:
    """
    Evaluate a model using LOCO logic if applicable, or standard metrics.
    Returns dict with RMSE and R2.
    """
    # For LOCO, we need to predict on held-out cycles. 
    # This function is a helper for the main LOCO loop.
    # Here we assume X contains 'cycle_id'.
    
    # We will use a simple split for this helper if called directly, 
    # but the main loop handles the specific LOCO logic.
    y_pred = model.predict(X)
    rmse = np.sqrt(mean_squared_error(y, y_pred))
    r2 = r2_score(y, y_pred)
    
    logger.info(f"{method_name} - RMSE: {rmse:.6f}, R²: {r2:.6f}")
    return {"rmse": float(rmse), "r2": float(r2)}

def run_loco_cv(X: pd.DataFrame, y: pd.Series) -> Tuple[Dict[str, Any], str]:
    """
    Execute Leave-One-Cycle-Out Cross-Validation.
    Returns detailed report and best model name.
    """
    unique_cycles = sorted(X['cycle_id'].unique())
    logger.info(f"Executing LOCO CV over {len(unique_cycles)} cycles: {unique_cycles}")
    
    cv_results = {
        "method": "Leave-One-Cycle-Out",
        "total_cycles": len(unique_cycles),
        "per_cycle_metrics": [],
        "model_comparison": {}
    }
    
    best_model_name = None
    best_score = float('inf') # Lower RMSE is better
    
    # We will train two models: RF and GP
    # We compare them based on average RMSE across LOCO folds
    
    rf_scores = []
    gp_scores = []
    
    # Temporary storage for best performing model instance for final save
    best_rf_model = None
    best_gp_model = None
    
    # Re-train on full data for final artifacts later, but first do CV
    # To save memory, we don't store all models, just the metrics.
    
    for i, held_out_cycle in enumerate(unique_cycles):
        logger.info(f"LOCO Fold {i+1}/{len(unique_cycles)}: Holding out Cycle {held_out_cycle}")
        
        # Split data
        mask_train = X['cycle_id'] != held_out_cycle
        mask_test = X['cycle_id'] == held_out_cycle
        
        X_train, y_train = X[mask_train], y[mask_train]
        X_test, y_test = X[mask_test], y[mask_test]
        
        if len(X_train) == 0 or len(X_test) == 0:
            logger.warning(f"Skipping cycle {held_out_cycle} due to empty train/test split.")
            continue
        
        # Train RF
        rf_model = train_random_forest(X_train, y_train)
        y_pred_rf = rf_model.predict(X_test)
        rf_rmse = np.sqrt(mean_squared_error(y_test, y_pred_rf))
        rf_r2 = r2_score(y_test, y_pred_rf)
        rf_scores.append(rf_rmse)
        
        # Train GP
        gp_model = train_gaussian_process(X_train, y_train)
        y_pred_gp = gp_model.predict(X_test)
        gp_rmse = np.sqrt(mean_squared_error(y_test, y_pred_gp))
        gp_r2 = r2_score(y_test, y_pred_gp)
        gp_scores.append(gp_rmse)
        
        cv_results["per_cycle_metrics"].append({
            "cycle_id": int(held_out_cycle),
            "rf_rmse": float(rf_rmse),
            "rf_r2": float(rf_r2),
            "gp_rmse": float(gp_rmse),
            "gp_r2": float(gp_r2)
        })
        
    # Aggregate results
    avg_rf_rmse = np.mean(rf_scores)
    avg_gp_rmse = np.mean(gp_scores)
    
    cv_results["model_comparison"] = {
        "random_forest": {"avg_rmse": float(avg_rf_rmse), "avg_r2": float(np.mean([m['rf_r2'] for m in cv_results['per_cycle_metrics']])), "std_rmse": float(np.std(rf_scores))},
        "gaussian_process": {"avg_rmse": float(avg_gp_rmse), "avg_r2": float(np.mean([m['gp_r2'] for m in cv_results['per_cycle_metrics']])), "std_rmse": float(np.std(gp_scores))}
    }
    
    # Determine winner
    if avg_rf_rmse < avg_gp_rmse:
        best_model_name = "random_forest"
        best_score = avg_rf_rmse
        logger.info(f"Winner: Random Forest (Avg RMSE: {avg_rf_rmse:.6f})")
    else:
        best_model_name = "gaussian_process"
        best_score = avg_gp_rmse
        logger.info(f"Winner: Gaussian Process (Avg RMSE: {avg_gp_rmse:.6f})")
        
    # Train final models on FULL data for artifact saving
    logger.info("Training final models on full dataset for artifact saving...")
    best_rf_model = train_random_forest(X, y)
    best_gp_model = train_gaussian_process(X, y)
    
    # Save best model artifact
    ensure_directories()
    artifacts_dir = Path(get_data_path("root")) / ARTIFACTS_DIR
    artifacts_dir.mkdir(parents=True, exist_ok=True)
    
    final_model_path = artifacts_dir / BEST_MODEL_PATH
    
    if best_model_name == "random_forest":
        joblib.dump(best_rf_model, final_model_path)
    else:
        joblib.dump(best_gp_model, final_model_path)
        
    logger.info(f"Saved best model ({best_model_name}) to {final_model_path}")
    
    # Add selection rationale
    cv_results["selection_rationale"] = (
        f"Selected {best_model_name} based on lowest average RMSE across LOCO folds. "
        f"RF Avg RMSE: {avg_rf_rmse:.6f}, GP Avg RMSE: {avg_gp_rmse:.6f}."
    )
    
    return cv_results, best_model_name

def save_report(report: Dict[str, Any], output_path: str):
    """Save the CV report to JSON."""
    path = Path(get_data_path("root")) / output_path
    path.parent.mkdir(parents=True, exist_ok=True)
    with open(path, 'w') as f:
        json.dump(report, f, indent=2)
    logger.info(f"Saved CV report to {path}")

def run_training_pipeline():
    """Main entry point for the training task."""
    setup_environment()
    logger.info("Starting Model Training Pipeline (T015)...")
    
    try:
        # 1. Load Data
        df = load_preprocessed_data()
        
        # 2. Prepare Features
        X, y = prepare_features(df)
        
        # 3. Run LOCO Cross-Validation
        cv_report, winner = run_loco_cv(X, y)
        
        # 4. Save Report
        save_report(cv_report, CV_REPORT_PATH)
        
        logger.info("Training pipeline completed successfully.")
        return cv_report, winner
        
    except Exception as e:
        logger.error(f"Training pipeline failed: {e}", exc_info=True)
        raise

if __name__ == "__main__":
    run_training_pipeline()
