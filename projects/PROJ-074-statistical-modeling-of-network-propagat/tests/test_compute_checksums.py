"""
Tests for compute_checksums.py functionality.

These tests verify that the checksum computation script:
1. Correctly computes SHA-256 checksums for files
2. Writes output in the expected format
3. Handles edge cases (empty directory, non-existent directory)
"""
import os
import tempfile
from pathlib import Path
import hashlib

import pytest

import sys
sys.path.insert(0, str(Path(__file__).resolve().parent.parent))

from pipeline.utils import set_global_seed, compute_checksum


class TestComputeChecksum:
    """Test the compute_checksum utility function."""

    def test_compute_checksum_basic(self, tmp_path):
        """Test checksum computation for a simple file."""
        # Create a test file with known content
        test_file = tmp_path / "test.txt"
        content = b"Hello, World!"
        test_file.write_bytes(content)

        # Compute checksum
        checksum = compute_checksum(str(test_file))

        # Verify against expected SHA-256
        expected = hashlib.sha256(content).hexdigest()
        assert checksum == expected

    def test_compute_checksum_empty_file(self, tmp_path):
        """Test checksum computation for an empty file."""
        test_file = tmp_path / "empty.txt"
        test_file.write_bytes(b"")

        checksum = compute_checksum(str(test_file))
        expected = hashlib.sha256(b"").hexdigest()
        assert checksum == expected

    def test_compute_checksum_binary_file(self, tmp_path):
        """Test checksum computation for binary content."""
        test_file = tmp_path / "binary.bin"
        content = bytes(range(256))  # All possible byte values
        test_file.write_bytes(content)

        checksum = compute_checksum(str(test_file))
        expected = hashlib.sha256(content).hexdigest()
        assert checksum == expected

    def test_compute_checksum_deterministic(self, tmp_path):
        """Test that checksum computation is deterministic."""
        test_file = tmp_path / "test.txt"
        content = b"Test content for determinism"
        test_file.write_bytes(content)

        checksum1 = compute_checksum(str(test_file))
        checksum2 = compute_checksum(str(test_file))

        assert checksum1 == checksum2

class TestChecksumOutputFormat:
    """Test the checksum output file format."""

    def test_checksum_file_format(self, tmp_path):
        """Verify checksums are written in correct format."""
        # Create test files
        raw_dir = tmp_path / "raw"
        raw_dir.mkdir()

        file1 = raw_dir / "file1.json"
        file1.write_text('{"test": 1}')

        file2 = raw_dir / "file2.json"
        file2.write_text('{"test": 2}')

        # Compute checksums manually
        checksum1 = compute_checksum(str(file1))
        checksum2 = compute_checksum(str(file2))

        # Write to output file in expected format
        output_file = tmp_path / "checksums.txt"
        with open(output_file, 'w') as f:
            f.write(f"{checksum1}  file1.json\n")
            f.write(f"{checksum2}  file2.json\n")

        # Read and verify format
        with open(output_file, 'r') as f:
            lines = f.readlines()

        # Each line should be: <64-char-hash>  <filename>
        assert len(lines) == 2
        for line in lines:
            parts = line.strip().split('  ')
            assert len(parts) == 2
            assert len(parts[0]) == 64  # SHA-256 hex is 64 chars
            assert parts[0].isalnum()  # Hex characters only

    def test_checksum_header_comments(self, tmp_path):
        """Verify checksum file includes header comments."""
        output_file = tmp_path / "checksums.txt"

        with open(output_file, 'w') as f:
            f.write("# SHA-256 checksums for files in data/raw/\n")
            f.write("# Generated by compute_checksums.py\n")
            f.write("# Total files: 2\n")
            f.write("#\n")
            f.write("abc123...  file1.json\n")

        with open(output_file, 'r') as f:
            content = f.read()

        assert "# SHA-256 checksums" in content
        assert "# Generated by compute_checksums.py" in content
        assert "# Total files:" in content

class TestEdgeCases:
    """Test edge cases in checksum computation."""

    def test_empty_directory(self, tmp_path):
        """Handle empty directory gracefully."""
        raw_dir = tmp_path / "raw"
        raw_dir.mkdir()
        output_file = tmp_path / "checksums.txt"

        # Should not raise exception
        checksums = []
        for root, dirs, files in os.walk(raw_dir):
            for filename in files:
                filepath = Path(root) / filename
                checksums.append(compute_checksum(str(filepath)))

        assert len(checksums) == 0

    def test_nonexistent_directory(self, tmp_path):
        """Handle non-existent directory gracefully."""
        raw_dir = tmp_path / "nonexistent"
        output_file = tmp_path / "checksums.txt"

        # Should not raise exception
        assert not raw_dir.exists()

    def test_nested_directories(self, tmp_path):
        """Handle nested directory structure."""
        raw_dir = tmp_path / "raw"
        nested = raw_dir / "subdir" / "nested"
        nested.mkdir(parents=True)

        file1 = nested / "deep.json"
        file1.write_text('{"nested": true}')

        # Should find files in nested directories
        found_files = []
        for root, dirs, files in os.walk(raw_dir):
            for filename in files:
                found_files.append(Path(root) / filename)

        assert len(found_files) == 1
        assert found_files[0].name == "deep.json"

