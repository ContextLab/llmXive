"""
Compute SHA-256 checksums for all files in data/raw/ directory.

This script scans the data/raw/ directory, computes SHA-256 checksums
for each file, and writes the results to data/checksums.txt.

Input:  All files in data/raw/
Output: data/checksums.txt containing hash and filepath pairs

Provenance:
  - Input directory: data/raw/
  - Output file: data/checksums.txt
  - Uses compute_checksum() from pipeline.utils (T006)

Transformation steps:
  1. Scan data/raw/ for all files (recursive)
  2. Compute SHA-256 hash for each file using compute_checksum()
  3. Write results in format: "<hash>  <relative_path>"

Output file paths:
  - data/checksums.txt
"""
import os
import sys
from pathlib import Path

# Add project root to path for imports
project_root = Path(__file__).resolve().parent.parent.parent
sys.path.insert(0, str(project_root))

from pipeline.utils import set_global_seed, setup_logger, compute_checksum

# Pin random seed at script start (per T060)
set_global_seed(12345)

# Setup logger
logger = setup_logger(__name__)

def compute_all_checksums(raw_dir: Path, output_file: Path) -> None:
    """
    Compute SHA-256 checksums for all files in raw_dir and write to output_file.

    Args:
        raw_dir: Path to the data/raw/ directory
        output_file: Path to write the checksums file
    """
    logger.info(f"Scanning directory: {raw_dir}")

    if not raw_dir.exists():
        logger.warning(f"Directory does not exist: {raw_dir}")
        # Create empty checksums file
        output_file.parent.mkdir(parents=True, exist_ok=True)
        with open(output_file, 'w') as f:
            f.write(f"# Checksums for {raw_dir}\n")
            f.write("# No files found\n")
        logger.info(f"Created empty checksum file: {output_file}")
        return

    checksums = []
    files_found = 0

    # Walk through all files recursively
    for root, dirs, files in os.walk(raw_dir):
        for filename in files:
            filepath = Path(root) / filename
            files_found += 1

            # Compute checksum using the utility function
            checksum = compute_checksum(str(filepath))

            # Store relative path from project root
            rel_path = filepath.relative_to(project_root)
            checksums.append((checksum, str(rel_path)))
            logger.debug(f"Computed checksum for {rel_path}: {checksum[:16]}...")

    # Write checksums to output file
    output_file.parent.mkdir(parents=True, exist_ok=True)
    with open(output_file, 'w') as f:
        f.write(f"# SHA-256 checksums for files in {raw_dir.relative_to(project_root)}\n")
        f.write(f"# Generated by compute_checksums.py\n")
        f.write(f"# Total files: {files_found}\n")
        f.write("#\n")
        for checksum, rel_path in checksums:
            f.write(f"{checksum}  {rel_path}\n")

    logger.info(f"Wrote {files_found} checksums to {output_file}")

def main() -> int:
    """Main entry point for checksum computation."""
    # Define paths relative to project root
    raw_dir = project_root / "data" / "raw"
    output_file = project_root / "data" / "checksums.txt"

    logger.info("Starting checksum computation for data/raw/")

    try:
        compute_all_checksums(raw_dir, output_file)
        logger.info("Checksum computation completed successfully")
        return 0
    except Exception as e:
        logger.error(f"Checksum computation failed: {e}")
        return 1

if __name__ == "__main__":
    sys.exit(main())
