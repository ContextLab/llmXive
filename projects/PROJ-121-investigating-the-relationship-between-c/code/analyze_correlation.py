def run_lomb_scargle_analysis(df: pd.DataFrame, detector_name: str) -> dict:
    """
    Run Lomb-Scargle periodogram on the dipole amplitude timeseries.
    
    Args:
        df: DataFrame with timeseries data.
        detector_name: Name of the detector (for logging).
        
    Returns:
        Dictionary with periodogram results.
    """
    logger.info(f"Running Lomb-Scargle analysis for {detector_name}...")
    
    # Prepare time and amplitude arrays
    # Assuming 'interval_start' is Julian Date or convertible to float
    try:
        t = df['interval_start'].astype(float).values
    except (ValueError, TypeError):
        # Try parsing as datetime and converting to float (e.g., MJD or JD)
        # For simplicity, if it's a date string, we might need a specific conversion
        # Here we assume the pipeline outputs numeric JD or similar
        raise ValueError(f"Could not convert 'interval_start' to float for {detector_name}")
        
    y = df['dipole_amp'].values
    
    # Remove NaNs
    valid_mask = ~(np.isnan(t) | np.isnan(y))
    t_clean = t[valid_mask]
    y_clean = y[valid_mask]
    
    if len(t_clean) < 10:
        logger.warning(f"Not enough data points ({len(t_clean)}) for Lomb-Scargle in {detector_name}. Skipping.")
        return {
            'status': 'skipped',
            'reason': 'insufficient_data',
            'detector': detector_name
        }
    
    # Compute periodogram
    frequency, power = compute_lomb_scargle_periodogram(t_clean, y_clean)
    
    # Find peaks
    peaks = find_significant_peaks(frequency, power)
    
    # Compute FAP for top peaks
    fap_results = []
    for peak_freq, peak_power in peaks[:5]:  # Top 5 peaks
        fap = compute_false_alarm_probability(frequency, power, peak_power)
        fap_results.append({
            'frequency': float(peak_freq),
            'period_days': float(1.0 / peak_freq) if peak_freq > 0 else None,
            'power': float(peak_power),
            'fap': float(fap)
        })
    
    return {
        'status': 'success',
        'detector': detector_name,
        'n_points': len(t_clean),
        'frequency_range': [float(frequency.min()), float(frequency.max())],
        'peaks': fap_results
    }

