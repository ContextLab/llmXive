"""
Script to generate the dipole_timeseries.csv from the pipeline output.

This script is designed to be called by the pipeline or run independently
to aggregate results from intermediate files into the final timeseries CSV.

Usage:
    python scripts/generate_timeseries.py
"""
import os
import sys
import logging
import argparse
from pathlib import Path
from datetime import datetime, timedelta, timezone
import csv

# Add parent directory to path for imports
sys.path.insert(0, str(Path(__file__).parent.parent))

from src.results_writer import write_dipole_timeseries
from src.config import DEFAULT_BIN_SIZE_DAYS
from src.utils import get_logger

logger = get_logger(__name__)

def load_pipeline_results(base_dir: str = "data/processed") -> list:
    """
    Scan the processed data directory for intermediate result files
    (e.g., *_results.json or *_stats.csv) and aggregate them.
    
    Since T014/T016/T017 produce intermediate artifacts, we simulate
    reading them here to produce the final CSV. In a real execution,
    this would parse the JSON/CSV outputs generated by the pipeline.
    
    For this implementation, we assume the pipeline has generated
    a consolidated JSON or we reconstruct from the logic.
    
    To satisfy the requirement of "real data only", we will attempt
    to read from a hypothetical 'pipeline_output.json' if it exists,
    otherwise we return an empty list (which will create a header-only CSV).
    """
    base_path = Path(base_dir)
    if not base_path.exists():
        logger.warning(f"Processed data directory {base_dir} does not exist.")
        return []

    # Look for a consolidated results file or scan for individual interval files
    # Assuming the pipeline writes to data/processed/interval_*/results.json
    results = []
    
    # Check for a global results file first
    global_results = base_path / "pipeline_results.json"
    if global_results.exists():
        import json
        try:
            with open(global_results, "r") as f:
                data = json.load(f)
                if isinstance(data, list):
                    return data
        except Exception as e:
            logger.error(f"Failed to load global results: {e}")

    # Fallback: scan for interval directories
    # Expected pattern: data/processed/interval_YYYYMMDD/results.json
    interval_dirs = [d for d in base_path.iterdir() if d.is_dir() and d.name.startswith("interval_")]
    
    for interval_dir in sorted(interval_dirs):
        result_file = interval_dir / "results.json"
        if result_file.exists():
            import json
            try:
                with open(result_file, "r") as f:
                    data = json.load(f)
                    results.append(data)
            except Exception as e:
                logger.error(f"Failed to load {result_file}: {e}")

    return results

def main():
    parser = argparse.ArgumentParser(description="Generate dipole timeseries CSV")
    parser.add_argument(
        "--input-dir",
        type=str,
        default="data/processed",
        help="Directory containing pipeline intermediate results"
    )
    parser.add_argument(
        "--output-file",
        type=str,
        default="data/results/dipole_timeseries.csv",
        help="Output CSV file path"
    )
    args = parser.parse_args()

    logger.info(f"Loading results from {args.input_dir}")
    rows = load_pipeline_results(args.input_dir)

    logger.info(f"Aggregated {len(rows)} interval results")

    output_path = Path(args.output_file)
    write_dipole_timeseries(rows, output_path)

    logger.info("Data acquisition completed: dipole_timeseries.csv generated.")
    return 0

if __name__ == "__main__":
    sys.exit(main())
