def load_timeseries_data(csv_path: str) -> pd.DataFrame:
    """Load the dipole timeseries CSV generated by the pipeline."""
    path = Path(csv_path)
    if not path.exists():
        raise FileNotFoundError(f"Timeseries data not found at {csv_path}")
    
    df = pd.read_csv(path)
    logger.info(f"Loaded {len(df)} rows from {csv_path}")
    return df

def load_correlation_results(json_path: str) -> dict:
    """Load correlation analysis results from JSON."""
    path = Path(json_path)
    if not path.exists():
        raise FileNotFoundError(f"Correlation results not found at {json_path}")
    
    import json
    with open(path, 'r') as f:
        return json.load(f)

def plot_periodogram(df: pd.DataFrame, detector: str, ax: plt.Axes):
    """
    Plot Lomb-Scargle periodogram for a given detector.
    Assumes df contains columns: 'interval_start', 'dipole_amp'
    """
    # Convert interval_start to numeric (Julian dates or timestamps)
    if 'interval_start' in df.columns:
        # Try to parse as datetime
        try:
            dates = pd.to_datetime(df['interval_start'])
            # Convert to fractional years for periodogram
            # Assuming data spans multiple years
            t0 = dates.min()
            times = (dates - t0).dt.total_seconds() / (365.25 * 24 * 3600)
        except Exception as e:
            logger.warning(f"Could not parse dates: {e}, using index")
            times = np.arange(len(df))
    else:
        times = np.arange(len(df))
    
    amplitudes = df['dipole_amp'].values

    # Compute periodogram
    freq, power = compute_lomb_scargle_periodogram(times, amplitudes)
    
    # Plot
    ax.semilogy(freq, power, 'b-', label=f'{detector} Periodogram')
    ax.set_xlabel('Frequency (cycles/year)')
    ax.set_ylabel('Power')
    ax.set_title(f'Lomb-Scargle Periodogram: {detector}')
    ax.grid(True, alpha=0.3)
    ax.legend()

def plot_correlation_heatmap(correlation_results: dict, ax: plt.Axes):
    """
    Plot correlation heatmap from correlation analysis results.
    Expected structure: correlation_results['correlations'] is a dict of dicts
    """
    correlations = correlation_results.get('correlations', {})
    
    if not correlations:
        ax.text(0.5, 0.5, 'No correlation data available', 
               ha='center', va='center', transform=ax.transAxes)
        ax.set_title('Correlation Heatmap')
        return

    # Prepare data for heatmap
    detectors = list(correlations.keys())
    proxy_types = ['sunspot', 'solar_wind', 'imf']  # Common proxy types
    
    # Build matrix
    matrix = []
    rows = []
    cols = []
    
    for det in detectors:
        det_corrs = correlations[det]
        for proxy in proxy_types:
            if proxy in det_corrs:
                matrix.append(det_corrs[proxy]['correlation'])
                rows.append(f'{det}_{proxy}')
                cols.append('Correlation')
    
    if matrix:
        matrix = np.array(matrix).reshape(-1, 1)
        df_matrix = pd.DataFrame(matrix, index=rows, columns=cols)
        
        sns.heatmap(df_matrix, annot=True, fmt='.3f', cmap='RdBu_r', 
                   center=0, ax=ax, cbar_kws={'label': 'Correlation Coefficient'})
        ax.set_title('Correlation Coefficients by Detector and Proxy')
    else:
        ax.text(0.5, 0.5, 'No valid correlation data', 
               ha='center', va='center', transform=ax.transAxes)
        ax.set_title('Correlation Heatmap')

def plot_fap_summary(correlation_results: dict, ax: plt.Axes):
    """
    Plot False Alarm Probability summary table.
    """
    fap_data = correlation_results.get('fap_results', {})
    
    if not fap_data:
        ax.text(0.5, 0.5, 'No FAP data available', 
               ha='center', va='center', transform=ax.transAxes)
        ax.set_title('FAP Summary')
        return

    # Prepare table data
    table_data = []
    headers = ['Detector', 'Proxy', 'P-value', 'Bonferroni Corrected', 'Significant?']
    
    for det, proxies in fap_data.items():
        for proxy, vals in proxies.items():
            p_val = vals.get('p_value', 0)
            bonf_p = vals.get('bonferroni_p', 0)
            sig = 'Yes' if vals.get('significant', False) else 'No'
            table_data.append([det, proxy, f'{p_val:.4f}', f'{bonf_p:.4f}', sig])
    
    if table_data:
        # Create table
        table = ax.table(cellText=table_data, colLabels=headers,
                       loc='center', cellLoc='center')
        table.auto_set_font_size(False)
        table.set_fontsize(9)
        table.scale(1.2, 1.5)
        ax.set_title('False Alarm Probability Summary')
        ax.axis('off')
    else:
        ax.text(0.5, 0.5, 'No FAP results', 
               ha='center', va='center', transform=ax.transAxes)
        ax.set_title('FAP Summary')

def generate_report_pdf(dipole_csv: str, correlation_json: str, output_pdf: str):
    """
    Generate comprehensive PDF report with all plots and tables.
    """
    logger.info(f"Generating report PDF: {output_pdf}")
    
    # Load data
    df = load_timeseries_data(dipole_csv)
    correlation_results = load_correlation_results(correlation_json)
    
    # Create PDF
    with PdfPages(output_pdf) as pdf:
        # Page 1: Periodograms for each detector
        detectors = ['IceCube', 'Auger']
        fig, axes = plt.subplots(len(detectors), 1, figsize=(10, 6 * len(detectors)))
        if len(detectors) == 1:
            axes = [axes]
        
        for ax, det in zip(axes, detectors):
            # Filter data for this detector if available
            # Assuming df has a 'detector' column or we process separately
            # For now, assume the CSV contains combined data or we process all
            plot_periodogram(df, det, ax)
        
        plt.tight_layout()
        pdf.savefig(fig)
        plt.close(fig)
        
        # Page 2: Correlation Heatmap
        fig, ax = plt.subplots(1, 1, figsize=(10, 8))
        plot_correlation_heatmap(correlation_results, ax)
        plt.tight_layout()
        pdf.savefig(fig)
        plt.close(fig)
        
        # Page 3: FAP Summary
        fig, ax = plt.subplots(1, 1, figsize=(10, 6))
        plot_fap_summary(correlation_results, ax)
        plt.tight_layout()
        pdf.savefig(fig)
        plt.close(fig)
        
        # Page 4: Additional diagnostics (optional)
        fig, axes = plt.subplots(1, 2, figsize=(14, 6))
        
        # Dipole amplitude time series
        ax1 = axes[0]
        ax1.plot(df['interval_start'], df['dipole_amp'], 'o-')
        ax1.set_xlabel('Interval Start')
        ax1.set_ylabel('Dipole Amplitude')
        ax1.set_title('Dipole Amplitude Time Series')
        ax1.tick_params(axis='x', rotation=45)
        
        # Quadrupole amplitude
        if 'quad_amp' in df.columns:
            ax2 = axes[1]
            ax2.plot(df['interval_start'], df['quad_amp'], 'o-', color='orange')
            ax2.set_xlabel('Interval Start')
            ax2.set_ylabel('Quadrupole Amplitude')
            ax2.set_title('Quadrupole Amplitude Time Series')
            ax2.tick_params(axis='x', rotation=45)
        else:
            axes[1].text(0.5, 0.5, 'No quadrupole data', 
                        ha='center', va='center', transform=axes[1].transAxes)
        
        plt.tight_layout()
        pdf.savefig(fig)
        plt.close(fig)
    
    logger.info(f"Report PDF generated successfully: {output_pdf}")

def main():
    parser = argparse.ArgumentParser(
        description='Generate PDF report with periodogram plots, correlation heatmaps, and FAP reports.'
    )
    parser.add_argument(
        '--dipole-csv',
        type=str,
        default='data/results/dipole_timeseries.csv',
        help='Path to dipole timeseries CSV file'
    )
    parser.add_argument(
        '--correlation-json',
        type=str,
        default='data/results/correlation_results.json',
        help='Path to correlation results JSON file'
    )
    parser.add_argument(
        '--output',
        type=str,
        default='reports/correlation_analysis_report.pdf',
        help='Output PDF file path'
    )
    
    args = parser.parse_args()
    
    # Setup logging
    logging.basicConfig(
        level=logging.INFO,
        format='%(asctime)s - %(name)s - %(levelname)s - %(message)s'
    )
    
    # Ensure output directory exists
    output_path = Path(args.output)
    output_path.parent.mkdir(parents=True, exist_ok=True)
    
    try:
        generate_report_pdf(args.dipole_csv, args.correlation_json, str(output_path))
        print(f"Report generated: {output_path}")
    except FileNotFoundError as e:
        logger.error(f"Input file not found: {e}")
        sys.exit(1)
    except Exception as e:
        logger.error(f"Error generating report: {e}")
        sys.exit(1)

if __name__ == '__main__':
    main()