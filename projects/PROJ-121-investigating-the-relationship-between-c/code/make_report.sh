#!/bin/bash
# make_report.sh - Compiles the LaTeX report and ensures figures are available.
# This script orchestrates the generation of figures, compilation of the LaTeX report,
# and verification of the final PDF.
#
# Usage: ./make_report.sh
#
# Prerequisites:
#   - pdflatex must be installed and available in PATH
#   - Python dependencies (matplotlib, numpy, etc.) must be installed
#   - The 'data/results' directory must contain the necessary CSV files
#   - The 'reports/report_template.tex' file must exist

set -e  # Exit immediately if a command exits with a non-zero status

# Configuration
PROJECT_ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
REPORT_TEMPLATE="${PROJECT_ROOT}/reports/report_template.tex"
REPORT_OUTPUT="${PROJECT_ROOT}/reports/report.pdf"
FIGURES_DIR="${PROJECT_ROOT}/reports/figures"
DATA_DIR="${PROJECT_ROOT}/data/results"
SCRIPTS_DIR="${PROJECT_ROOT}/code/scripts"

# Colors for output
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
NC='\033[0m' # No Color

log_info() {
    echo -e "${GREEN}[INFO]${NC} $1"
}

log_warn() {
    echo -e "${YELLOW}[WARN]${NC} $1"
}

log_error() {
    echo -e "${RED}[ERROR]${NC} $1"
}

# Check for required tools
check_dependencies() {
    log_info "Checking dependencies..."
    
    if ! command -v pdflatex &> /dev/null; then
        log_error "pdflatex is not installed. Please install a LaTeX distribution (e.g., TeX Live, MiKTeX)."
        exit 1
    fi
    
    if ! command -v python3 &> /dev/null; then
        log_error "python3 is not installed."
        exit 1
    fi

    # Check if required Python packages are available
    if ! python3 -c "import matplotlib; import numpy; import pandas" &> /dev/null; then
        log_error "Required Python packages (matplotlib, numpy, pandas) are not installed."
        log_info "Run: pip install -r requirements.txt"
        exit 1
    fi

    log_info "All dependencies satisfied."
}

# Check for required input files
check_inputs() {
    log_info "Checking input files..."
    
    if [ ! -f "$REPORT_TEMPLATE" ]; then
        log_error "Report template not found: $REPORT_TEMPLATE"
        exit 1
    fi

    if [ ! -f "${DATA_DIR}/dipole_timeseries.csv" ]; then
        log_warn "dipole_timeseries.csv not found. The report may be incomplete."
    fi

    if [ ! -f "${DATA_DIR}/correlation_results.json" ]; then
        log_warn "correlation_results.json not found. The report may be incomplete."
    fi

    log_info "Input files check completed."
}

# Generate figures if they don't exist or if forced
generate_figures() {
    log_info "Generating figures for the report..."
    
    mkdir -p "$FIGURES_DIR"
    
    # Run the figure generation script
    if [ -f "${SCRIPTS_DIR}/generate_report_plots.py" ]; then
        log_info "Running generate_report_plots.py..."
        python3 "${SCRIPTS_DIR}/generate_report_plots.py" --output-dir "$FIGURES_DIR"
        
        if [ $? -eq 0 ]; then
            log_info "Figures generated successfully."
        else
            log_error "Failed to generate figures."
            exit 1
        fi
    else
        log_warn "Figure generation script not found: ${SCRIPTS_DIR}/generate_report_plots.py"
        log_info "Skipping figure generation. Ensure figures are manually placed in $FIGURES_DIR if needed."
    fi
}

# Compile the LaTeX report
compile_report() {
    log_info "Compiling LaTeX report..."
    
    cd "${PROJECT_ROOT}/reports"
    
    # Run pdflatex twice to resolve references
    log_info "First pass..."
    if ! pdflatex -interaction=nonstopmode report_template.tex > /dev/null; then
        log_error "First LaTeX compilation failed. Check logs in reports/report_template.log"
        exit 1
    fi

    log_info "Second pass..."
    if ! pdflatex -interaction=nonstopmode report_template.tex > /dev/null; then
        log_error "Second LaTeX compilation failed. Check logs in reports/report_template.log"
        exit 1
    fi

    # Move the output to the expected name
    if [ -f "report_template.pdf" ]; then
        mv report_template.pdf report.pdf
        log_info "Report compiled successfully: $REPORT_OUTPUT"
    else
        log_error "PDF file not generated by pdflatex."
        exit 1
    fi

    cd - > /dev/null
}

# Cleanup temporary files
cleanup() {
    log_info "Cleaning up temporary files..."
    find "${PROJECT_ROOT}/reports" -type f \( -name "*.aux" -o -name "*.log" -o -name "*.out" -o -name "*.toc" -o -name "*.lof" -o -name "*.lot" -o -name "*.bbl" -o -name "*.blg" \) -delete
    log_info "Cleanup complete."
}

# Main execution
main() {
    log_info "Starting report generation process..."
    
    check_dependencies
    check_inputs
    generate_figures
    compile_report
    cleanup
    
    log_info "Report generation completed successfully!"
    log_info "Output: $REPORT_OUTPUT"
}

main "$@"