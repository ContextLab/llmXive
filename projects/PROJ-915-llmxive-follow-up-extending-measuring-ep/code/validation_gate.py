# FR-009 Threshold: Correlation coefficient must be >= 0.6 to pass validation
# This threshold represents the minimum acceptable agreement between automated
# linguistic features and human-perceived authority density.
FR_009_CORRELATION_THRESHOLD = 0.6

def load_correlation_data(correlation_file_path: str) -> Dict[str, Any]:
    """
    Load the raw correlation data generated by T017d.
    
    Args:
        correlation_file_path: Path to the JSON file containing correlation data
        
    Returns:
        Dictionary containing correlation metrics
        
    Raises:
        FileNotFoundError: If the correlation file does not exist
        json.JSONDecodeError: If the file is not valid JSON
    """
    path = Path(correlation_file_path)
    if not path.exists():
        raise FileNotFoundError(
            f"Correlation data file not found: {correlation_file_path}. "
            "Ensure T017d has been completed successfully."
        )
    
    with open(path, 'r', encoding='utf-8') as f:
        return json.load(f)

def validate_correlation_against_threshold(
    correlation_value: float,
    threshold: float = FR_009_CORRELATION_THRESHOLD
) -> bool:
    """
    Validate the correlation value against the FR-009 requirement threshold.
    
    Args:
        correlation_value: The computed correlation coefficient
        threshold: The minimum acceptable threshold (default: 0.6)
        
    Returns:
        True if correlation meets or exceeds threshold, False otherwise
    """
    return correlation_value >= threshold

def compute_p_value_and_ci(
    correlation_value: float,
    sample_size: int,
    confidence_level: float = 0.95
) -> Dict[str, float]:
    """
    Compute p-value and confidence interval for the correlation.
    
    Uses Fisher's z-transformation for confidence interval calculation.
    
    Args:
        correlation_value: The Pearson correlation coefficient
        sample_size: Number of observations
        confidence_level: Confidence level for interval (default: 0.95)
        
    Returns:
        Dictionary with p_value, ci_lower, ci_upper
    """
    if sample_size < 3:
        return {
            'p_value': 1.0,
            'ci_lower': None,
            'ci_upper': None,
            'error': 'Sample size too small for statistical inference'
        }
    
    # Two-tailed p-value for Pearson correlation
    # t = r * sqrt((n-2) / (1-r^2))
    if abs(correlation_value) >= 1.0:
        # Perfect correlation
        t_stat = float('inf') if correlation_value > 0 else float('-inf')
        p_value = 0.0
    else:
        t_stat = correlation_value * np.sqrt((sample_size - 2) / (1 - correlation_value**2))
        p_value = 2 * (1 - stats.t.cdf(abs(t_stat), df=sample_size - 2))
    
    # Fisher's z-transformation for confidence interval
    # z = 0.5 * ln((1+r)/(1-r))
    # SE_z = 1 / sqrt(n-3)
    # z_lower = z - z_crit * SE_z
    # z_upper = z + z_crit * SE_z
    # r = (exp(2z) - 1) / (exp(2z) + 1)
    
    z = 0.5 * np.log((1 + correlation_value) / (1 - correlation_value))
    se_z = 1 / np.sqrt(sample_size - 3)
    z_crit = stats.norm.ppf(1 - (1 - confidence_level) / 2)
    
    z_lower = z - z_crit * se_z
    z_upper = z + z_crit * se_z
    
    ci_lower = (np.exp(2 * z_lower) - 1) / (np.exp(2 * z_lower) + 1)
    ci_upper = (np.exp(2 * z_upper) - 1) / (np.exp(2 * z_upper) + 1)
    
    return {
        'p_value': p_value,
        'ci_lower': ci_lower,
        'ci_upper': ci_upper
    }

def generate_validation_report(
    correlation_data: Dict[str, Any],
    threshold: float = FR_009_CORRELATION_THRESHOLD,
    output_path: str = "data/results/annotation_correlation_report.md"
) -> Dict[str, Any]:
    """
    Generate a comprehensive validation report in Markdown format.
    
    Args:
        correlation_data: Raw correlation data from T017d
        threshold: Validation threshold
        output_path: Path to write the report
        
    Returns:
        Dictionary containing report metadata and status
    """
    # Extract key metrics
    feature_name = correlation_data.get('feature_name', 'authority_density')
    metric_name = correlation_data.get('metric_name', 'pearson_r')
    correlation_value = float(correlation_data.get('correlation', 0.0))
    sample_size = int(correlation_data.get('sample_size', 0))
    p_value_raw = correlation_data.get('p_value', None)
    
    # Compute additional statistics if not provided
    if p_value_raw is None:
        stats_result = compute_p_value_and_ci(correlation_value, sample_size)
        p_value = stats_result['p_value']
        ci_lower = stats_result['ci_lower']
        ci_upper = stats_result['ci_upper']
    else:
        p_value = float(p_value_raw)
        ci_lower = correlation_data.get('ci_lower', None)
        ci_upper = correlation_data.get('ci_upper', None)
    
    # Determine validation status
    is_valid = validate_correlation_against_threshold(correlation_value, threshold)
    status = "PASS" if is_valid else "FAIL"
    
    # Build report content
    report_lines = [
        "# Annotation Correlation Validation Report",
        "",
        f"**Generated**: {pd.Timestamp.now().isoformat()}",
        f"**Validation Gate**: T017e",
        f"**Requirement**: FR-009",
        "",
        "## Summary",
        "",
        f"- **Validation Status**: **{status}**",
        f"- **Threshold (FR-009)**: {threshold}",
        f"- **Computed Correlation**: {correlation_value:.4f}",
        f"- **Sample Size**: {sample_size}",
        "",
        "## Statistical Details",
        "",
        f"- **Feature**: {feature_name}",
        f"- **Metric**: {metric_name}",
        f"- **P-value**: {p_value:.6f}",
    ]
    
    if ci_lower is not None and ci_upper is not None:
        report_lines.append(
            f"- **95% Confidence Interval**: [{ci_lower:.4f}, {ci_upper:.4f}]"
        )
    
    report_lines.extend([
        "",
        "## Interpretation",
        "",
    ])
    
    if is_valid:
        report_lines.extend([
            f"The computed correlation of {correlation_value:.4f} **exceeds** the "
            f"required threshold of {threshold}. This indicates a statistically "
            f"significant agreement between automated linguistic features and "
            f"human-perceived authority density.",
            "",
            "The validation gate **PASSES**. User Story 1 feature extraction "
            "procedures are validated for downstream use.",
        ])
    else:
        report_lines.extend([
            f"The computed correlation of {correlation_value:.4f} **falls below** "
            f"the required threshold of {threshold}. This indicates insufficient "
            f"agreement between automated linguistic features and "
            f"human-perceived authority density.",
            "",
            "The validation gate **FAILS**. Automated feature extraction "
            "procedures require refinement before proceeding to User Story 2.",
            "",
            "### Recommended Actions",
            "",
            "1. Review feature extraction logic in `code/features.py`",
            "2. Re-examine rater annotations for consistency",
            "3. Consider additional linguistic features to improve prediction",
            "4. Increase sample size if possible",
        ])
    
    report_lines.extend([
        "",
        "## Raw Data Reference",
        "",
        f"- **Input File**: `data/interim/annotation_correlation_raw.json`",
        f"- **Output File**: `data/results/annotation_correlation_report.md`",
        "",
        "## Metadata",
        "",
        "```json",
    ])
    
    # Add metadata section
    metadata = {
        'task_id': 'T017e',
        'requirement': 'FR-009',
        'threshold': threshold,
        'correlation': correlation_value,
        'sample_size': sample_size,
        'p_value': p_value,
        'status': status,
        'feature': feature_name
    }
    
    report_lines.append(json.dumps(metadata, indent=2))
    report_lines.append("```")
    
    # Write report to file
    output_file = Path(output_path)
    output_file.parent.mkdir(parents=True, exist_ok=True)
    
    with open(output_file, 'w', encoding='utf-8') as f:
        f.write('\n'.join(report_lines))
    
    logger.info(f"Validation report written to {output_path}")
    
    return {
        'status': status,
        'is_valid': is_valid,
        'correlation': correlation_value,
        'threshold': threshold,
        'sample_size': sample_size,
        'p_value': p_value,
        'report_path': str(output_path)
    }

def run_validation_gate_pipeline(
    correlation_file: str = "data/interim/annotation_correlation_raw.json",
    output_file: str = "data/results/annotation_correlation_report.md",
    threshold: float = FR_009_CORRELATION_THRESHOLD
) -> Dict[str, Any]:
    """
    Main pipeline function to execute the T017e validation gate.
    
    This function:
    1. Loads correlation data from T017d
    2. Validates against FR-009 threshold
    3. Generates comprehensive validation report
    4. Returns validation status
    
    Args:
        correlation_file: Path to raw correlation data from T017d
        output_file: Path for the validation report
        threshold: FR-009 threshold value
        
    Returns:
        Dictionary with validation results
        
    Raises:
        FileNotFoundError: If correlation data is missing
        ValueError: If correlation data is malformed
    """
    logger.info(f"Starting T017e validation gate pipeline")
    logger.info(f"Loading correlation data from: {correlation_file}")
    
    # Load data
    correlation_data = load_correlation_data(correlation_file)
    
    if not isinstance(correlation_data, dict):
        raise ValueError("Correlation data must be a dictionary")
    
    if 'correlation' not in correlation_data:
        raise ValueError("Correlation data missing 'correlation' key")
    
    logger.info(f"Validating correlation: {correlation_data['correlation']}")
    
    # Generate report and get results
    results = generate_validation_report(
        correlation_data=correlation_data,
        threshold=threshold,
        output_path=output_file
    )
    
    logger.info(f"Validation gate completed with status: {results['status']}")
    
    return results

def main():
    """Entry point for the validation gate script."""
    config = Config()
    
    # Paths from configuration
    correlation_file = config.get_path(
        'data_interim', 
        'annotation_correlation_raw.json'
    )
    output_file = config.get_path(
        'data_results', 
        'annotation_correlation_report.md'
    )
    
    # Run validation
    try:
        results = run_validation_gate_pipeline(
            correlation_file=correlation_file,
            output_file=output_file,
            threshold=FR_009_CORRELATION_THRESHOLD
        )
        
        # Print summary to stdout
        print(f"\n{'='*60}")
        print(f"T017e Validation Gate Results")
        print(f"{'='*60}")
        print(f"Status: {results['status']}")
        print(f"Correlation: {results['correlation']:.4f}")
        print(f"Threshold: {results['threshold']}")
        print(f"Sample Size: {results['sample_size']}")
        print(f"P-value: {results['p_value']:.6f}")
        print(f"Report: {results['report_path']}")
        print(f"{'='*60}\n")
        
        # Exit with appropriate code
        sys.exit(0 if results['is_valid'] else 1)
        
    except FileNotFoundError as e:
        logger.error(f"Data file not found: {e}")
        print(f"\nERROR: {e}")
        print("Ensure T017d has been completed and generated the required output file.")
        sys.exit(2)
    except Exception as e:
        logger.error(f"Validation gate failed: {e}")
        print(f"\nERROR: Validation gate failed with unexpected error: {e}")
        sys.exit(3)

if __name__ == "__main__":
    main()
