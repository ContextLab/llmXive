"""
Save pathway mapping results to results/pathway_analysis.json.

This script loads the SHAP analysis generated by T026 (interpret.py),
extracts the top metabolites, and saves the structured pathway mapping
results to results/pathway_analysis.json.

Dependencies:
  - code/modeling/interpret.py (T026) must have been run to generate shap_analysis.json
  - utils.constants for path definitions
"""
import os
import json
import sys
from pathlib import Path
from datetime import datetime

# Add project root to path for imports
project_root = Path(__file__).resolve().parent.parent.parent
sys.path.insert(0, str(project_root))

from utils.constants import RESULTS_DIR, DATA_PROCESSED_DIR

def load_json_file(file_path):
    """Load a JSON file."""
    with open(file_path, 'r') as f:
        return json.load(f)

def save_json_file(data, file_path):
    """Save data to a JSON file."""
    with open(file_path, 'w') as f:
        json.dump(data, f, indent=2)

def main():
    """
    Main function to save pathway mapping results.
    
    1. Load shap_analysis.json from previous step (T026)
    2. Extract top metabolites and their pathway mappings
    3. Save to results/pathway_analysis.json
    """
    shap_analysis_path = RESULTS_DIR / "shap_analysis.json"
    output_path = RESULTS_DIR / "pathway_analysis.json"

    # Check if shap_analysis.json exists
    if not shap_analysis_path.exists():
        print(f"ERROR: {shap_analysis_path} not found. Please run T026 first.")
        sys.exit(1)

    # Load SHAP analysis results
    print(f"Loading SHAP analysis from {shap_analysis_path}...")
    shap_data = load_json_file(shap_analysis_path)

    # Verify expected structure
    if "top_metabolites" not in shap_data:
        print("ERROR: 'top_metabolites' not found in shap_analysis.json")
        sys.exit(1)

    if "pathway_mappings" not in shap_data:
        print("ERROR: 'pathway_mappings' not found in shap_analysis.json")
        sys.exit(1)

    # Construct pathway analysis output
    pathway_analysis = {
        "metadata": {
            "generated_at": datetime.now().isoformat(),
            "source_file": str(shap_analysis_path),
            "description": "Pathway mapping results for top metabolites based on SHAP values"
        },
        "summary": {
            "total_top_metabolites": len(shap_data["top_metabolites"]),
            "metabolites_with_pathways": len([m for m in shap_data["top_metabolites"] if m.get("pathways")]),
            "pathways_identified": len(set(p for m in shap_data["top_metabolites"] for p in m.get("pathways", [])))
        },
        "top_metabolites": shap_data["top_metabolites"],
        "pathway_mappings": shap_data["pathway_mappings"],
        "interpretation": {
            "biological_context": "Top metabolites by SHAP value mapped to KEGG/MetaCyc pathways",
            "note": "These associations are correlational and require experimental validation"
        }
    }

    # Save results
    print(f"Saving pathway analysis to {output_path}...")
    save_json_file(pathway_analysis, output_path)

    print(f"Successfully saved pathway analysis to {output_path}")
    print(f"Summary: {pathway_analysis['summary']['total_top_metabolites']} metabolites, "
          f"{pathway_analysis['summary']['metabolites_with_pathways']} with pathway mappings, "
          f"{pathway_analysis['summary']['pathways_identified']} unique pathways")

    return pathway_analysis

if __name__ == "__main__":
    main()
