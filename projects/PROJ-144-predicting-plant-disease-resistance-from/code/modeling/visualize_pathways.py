"""
T028: Generate visualization of top pathways and feature importances.

This script reads the pathway analysis results generated by T026/T027,
extracts the top pathways and their associated metabolite importances,
and generates a bar plot saved to results/pathway_barplot.png.

It depends on:
- results/pathway_analysis.json (generated by T027)
- results/metrics.json (optional, for context)
"""
import os
import json
import sys
from pathlib import Path

import matplotlib.pyplot as plt
import pandas as pd
import numpy as np

# Add project root to path to ensure imports work if run directly
project_root = Path(__file__).resolve().parent.parent.parent
sys.path.insert(0, str(project_root))

from utils.constants import RESULTS_DIR

def load_json_file(filepath: Path) -> dict:
    """Load a JSON file."""
    with open(filepath, 'r') as f:
        return json.load(f)

def aggregate_pathway_scores(pathway_data: list, top_n: int = 10) -> pd.DataFrame:
    """
    Aggregate metabolite scores by pathway.
    
    Assumes pathway_data is a list of dicts with structure:
    {
      "metabolite": "name",
      "importance": float,
      "pathways": ["pathway1", "pathway2"]
    }
    
    Returns a DataFrame of top N pathways by total importance.
    """
    pathway_scores = {}
    
    for item in pathway_data:
        importance = item.get('importance', 0.0)
        pathways = item.get('pathways', [])
        
        for pathway in pathways:
            if pathway not in pathway_scores:
                pathway_scores[pathway] = 0.0
            pathway_scores[pathway] += importance
    
    # Sort by total importance and take top N
    sorted_pathways = sorted(pathway_scores.items(), key=lambda x: x[1], reverse=True)
    top_pathways = sorted_pathways[:top_n]
    
    df = pd.DataFrame(top_pathways, columns=['Pathway', 'Total Importance'])
    return df

def plot_pathway_importance(df: pd.DataFrame, output_path: Path):
    """
    Create a horizontal bar plot of top pathway importances.
    """
    if df.empty:
        raise ValueError("No pathway data to plot. Check input JSON.")

    plt.style.use('seaborn-v0_8-whitegrid')
    fig, ax = plt.subplots(figsize=(10, 8))

    # Sort ascending for horizontal bar chart so highest is on top
    df_sorted = df.sort_values(by='Total Importance', ascending=True)

    bars = ax.barh(df_sorted['Pathway'], df_sorted['Total Importance'], color='#4C72B0', edgecolor='black')

    ax.set_xlabel('Total SHAP Importance', fontsize=12)
    ax.set_title('Top Metabolic Pathways Associated with Disease Resistance', fontsize=14, fontweight='bold')
    
    # Add value labels on bars
    for i, v in enumerate(df_sorted['Total Importance']):
        ax.text(v + 0.01, i, f"{v:.3f}", va='center', fontsize=10)

    plt.tight_layout()
    plt.savefig(output_path, dpi=150, bbox_inches='tight')
    plt.close()
    print(f"Visualization saved to: {output_path}")

def main():
    """Main entry point for T028."""
    # Ensure results directory exists
    results_path = Path(RESULTS_DIR)
    results_path.mkdir(parents=True, exist_ok=True)
    
    input_file = results_path / "pathway_analysis.json"
    output_file = results_path / "pathway_barplot.png"

    if not input_file.exists():
        raise FileNotFoundError(
            f"Input file not found: {input_file}. "
            "Please ensure T026/T027 (interpret.py/save_pathway_results.py) has been run first."
        )

    print(f"Loading pathway data from {input_file}...")
    data = load_json_file(input_file)

    # Handle different possible structures from T027
    # Expected: {"pathways": [...]} or a flat list
    if isinstance(data, dict) and 'pathways' in data:
        pathway_list = data['pathways']
    elif isinstance(data, list):
        pathway_list = data
    else:
        # Try to find a key that looks like a list of results
        key = next((k for k in data.keys() if isinstance(data[k], list)), None)
        if key:
            pathway_list = data[key]
        else:
            raise ValueError(f"Unexpected JSON structure in {input_file}: {list(data.keys())}")

    if not pathway_list:
        raise ValueError("Pathway list is empty in input file.")

    print(f"Processing {len(pathway_list)} metabolite-pathway associations...")
    
    df = aggregate_pathway_scores(pathway_list, top_n=10)
    
    print(f"Generating plot for top {len(df)} pathways...")
    plot_pathway_importance(df, output_file)

    print("T028 completed successfully.")

if __name__ == "__main__":
    main()
