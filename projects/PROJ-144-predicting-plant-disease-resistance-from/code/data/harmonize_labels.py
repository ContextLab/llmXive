"""
Label harmonization module.
Encodes resistance labels and applies z-scoring or stratification.
"""
import os
import glob
import pandas as pd
import numpy as np
from typing import Dict, Any

from code.utils.constants import DATA_RAW_DIR, DATA_PROCESSED_DIR

def harmonize_labels() -> Dict[str, Any]:
    """
    Harmonize resistance labels across studies.
    
    1. Load phenotype data from data/raw
    2. Encode resistance labels (binary/ordinal)
    3. Apply z-scoring for continuous/ordinal labels
    4. Save harmonized labels to data/processed/labels.csv
    
    Returns:
        Dictionary with harmonization statistics
    """
    os.makedirs(DATA_PROCESSED_DIR, exist_ok=True)
    
    # 1. Load phenotype data
    # Look for phenotype files generated by download.py or existing raw data
    phenotype_files = glob.glob(os.path.join(DATA_RAW_DIR, "*phenotype*.csv"))
    
    # Fallback if specific naming convention not met but raw data exists
    if not phenotype_files:
        all_csvs = glob.glob(os.path.join(DATA_RAW_DIR, "*.csv"))
        phenotype_files = [f for f in all_csvs if "phenotype" in os.path.basename(f).lower()]
    
    if not phenotype_files:
        # Final fallback: try to find any CSV in raw dir
        all_csvs = glob.glob(os.path.join(DATA_RAW_DIR, "*.csv"))
        if not all_csvs:
            raise FileNotFoundError(
                f"No phenotype data found in {DATA_RAW_DIR}. "
                "Ensure T012 (download.py) has been run successfully."
            )
        phenotype_files = all_csvs
    
    # Combine all found phenotype files if multiple exist
    dfs = []
    for f_path in phenotype_files:
        try:
            df = pd.read_csv(f_path)
            df['source_file'] = os.path.basename(f_path)
            dfs.append(df)
        except Exception as e:
            print(f"Warning: Could not read {f_path}: {e}")
    
    if not dfs:
        raise FileNotFoundError("No valid CSV files found to process as phenotypes.")
    
    phenotype_df = pd.concat(dfs, ignore_index=True)
    print(f"Loaded phenotype data: {len(phenotype_df)} samples from {len(phenotype_files)} files")
    
    # 2. Identify resistance label column
    label_col = None
    candidate_keywords = ["resistance", "label", "outcome", "class", "phenotype", "status"]
    
    for col in phenotype_df.columns:
        col_lower = col.lower()
        if any(kw in col_lower for kw in candidate_keywords):
            label_col = col
            break
    
    if not label_col:
        # Fallback: assume first numeric column that isn't an ID or count
        numeric_cols = phenotype_df.select_dtypes(include=[np.number]).columns
        # Filter out obvious non-label numeric columns (e.g., counts, indices)
        potential_labels = [c for c in numeric_cols if c.lower() not in ['count', 'n', 'index', 'id']]
        if potential_labels:
            label_col = potential_labels[0]
        elif len(numeric_cols) > 0:
            label_col = numeric_cols[0]
        else:
            raise ValueError(
                f"Could not identify resistance label column in {phenotype_df.columns}. "
                "Expected a column containing 'resistance', 'label', or similar."
            )
    
    print(f"Using label column: {label_col}")
    
    # 3. Encode labels
    original_dtype = phenotype_df[label_col].dtype
    
    if pd.api.types.is_numeric_dtype(phenotype_df[label_col]):
        # Already numeric
        unique_vals = sorted(phenotype_df[label_col].dropna().unique())
        if len(unique_vals) <= 2:
            # Binary: ensure 0/1 mapping (0 = susceptible, 1 = resistant)
            # Assume 0/1 or similar small integers exist
            phenotype_df[label_col] = phenotype_df[label_col].astype(int)
            print(f"Binary labels detected. Values: {unique_vals}")
        else:
            # Ordinal: ensure sorted integer codes
            # Use pd.Categorical to map unique values to 0, 1, 2...
            phenotype_df[label_col] = pd.Categorical(phenotype_df[label_col]).codes
            print(f"Ordinal labels detected. Mapped {len(unique_vals)} categories to codes.")
    else:
        # Categorical: encode to numeric
        # Sort categories to ensure consistent ordering (e.g., 'Low', 'Medium', 'High')
        phenotype_df[label_col] = pd.Categorical(phenotype_df[label_col]).codes
        print(f"Categorical labels detected. Encoded to numeric codes.")
    
    # 4. Apply z-scoring (optional, for continuous/ordinal labels)
    # For binary labels (0,1), z-scoring is less useful but can be done for standardization
    # However, typically we only z-score if we have >2 levels or continuous data
    label_unique_count = len(phenotype_df[label_col].dropna().unique())
    
    if label_unique_count > 2 or (label_unique_count == 2 and original_dtype != np.int64):
        mean_label = phenotype_df[label_col].mean()
        std_label = phenotype_df[label_col].std()
        
        if pd.notna(std_label) and std_label > 0:
            phenotype_df["resistance_zscore"] = (phenotype_df[label_col] - mean_label) / std_label
            print("Applied z-scoring to resistance labels.")
        else:
            phenotype_df["resistance_zscore"] = 0.0
            print("Standard deviation is zero or NaN; skipping z-scoring (filled with 0).")
    else:
        # For strict binary, we might not need z-score, but we create the column for consistency
        # Or we can skip. Let's skip z-score for binary but keep the column if needed later.
        # For now, we won't add it for binary to keep data clean, unless requested.
        # But the task asks for z-scoring OR stratification.
        # We'll add a placeholder 0 or skip. Let's skip adding the column for binary to avoid confusion.
        pass
    
    # 5. Prepare output
    # Ensure sample_id column exists
    sample_id_col = None
    id_keywords = ["sample_id", "sampleid", "id", "sample", "accession"]
    for col in phenotype_df.columns:
        if any(kw in col.lower() for kw in id_keywords):
            sample_id_col = col
            break
    
    if not sample_id_col:
        # Fallback: create a synthetic ID if none exists (not ideal, but prevents crash)
        # Or use the first column if it looks like an ID
        if len(phenotype_df.columns) > 0:
            sample_id_col = phenotype_df.columns[0]
            print(f"Warning: Using '{sample_id_col}' as sample_id column.")
        else:
            raise ValueError("No sample ID column found.")
    
    # Select columns for output
    output_data = {
        "sample_id": phenotype_df[sample_id_col],
        "resistance_label": phenotype_df[label_col]
    }
    
    if "resistance_zscore" in phenotype_df.columns:
        output_data["resistance_zscore"] = phenotype_df["resistance_zscore"]
    
    labels_df = pd.DataFrame(output_data)
    
    # Drop duplicates if any (based on sample_id)
    if labels_df.duplicated(subset=["sample_id"]).any():
        print("Warning: Duplicate sample IDs found. Keeping first occurrence.")
        labels_df = labels_df.drop_duplicates(subset=["sample_id"], keep="first")
    
    output_labels_path = os.path.join(DATA_PROCESSED_DIR, "labels.csv")
    labels_df.to_csv(output_labels_path, index=False)
    print(f"Saved harmonized labels to {output_labels_path}")
    
    return {
        "samples": len(labels_df),
        "unique_labels": int(len(labels_df["resistance_label"].unique())),
        "output_path": output_labels_path,
        "label_encoding": "numeric_codes"
    }