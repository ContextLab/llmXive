#!/bin/bash
# run_all_tests.sh
# Executes the full verification suite for the Chronotype-Moral Judgement pipeline.
#
# Prerequisites:
#   - R 4.3+ installed
#   - renv initialized (T002, T003)
#   - testthat package available
#   - RMarkdown and dependencies available for report rendering
#
# Usage:
#   ./code/run_all_tests.sh
#
# Exit Codes:
#   0 - All tests passed
#   1 - One or more tests failed

set -e

# Configuration
PROJECT_ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
CODE_DIR="${PROJECT_ROOT}/code"
TESTS_DIR="${PROJECT_ROOT}/tests"
TEST_DATA_DIR="${PROJECT_ROOT}/tests/testdata"
REPORT_INPUT="${TEST_DATA_DIR}/schema_check.csv"
REPORT_OUTPUT="${PROJECT_ROOT}/reports/chronotype_moral_analysis.html"
LOG_FILE="${PROJECT_ROOT}/logs/test_run.log"

# Ensure log directory exists
mkdir -p "${PROJECT_ROOT}/logs"
mkdir -p "${PROJECT_ROOT}/reports"
mkdir -p "${PROJECT_ROOT}/data/derived"

echo "=== Chronotype-Moral Judgement Pipeline Test Suite ===" | tee "${LOG_FILE}"
echo "Start Time: $(date)" | tee -a "${LOG_FILE}"
echo "Project Root: ${PROJECT_ROOT}" | tee -a "${LOG_FILE}"
echo "" | tee -a "${LOG_FILE}"

# Step 1: Run testthat unit tests
echo "[1/3] Running testthat unit tests..." | tee -a "${LOG_FILE}"
echo "Command: Rscript -e 'testthat::test_dir(\"${TESTS_DIR}\")'" | tee -a "${LOG_FILE}"

if Rscript -e "testthat::test_dir('${TESTS_DIR}', reporter = 'summary')" >> "${LOG_FILE}" 2>&1; then
    echo "✓ Unit tests passed." | tee -a "${LOG_FILE}"
else
    echo "✗ Unit tests failed. Check ${LOG_FILE} for details." | tee -a "${LOG_FILE}"
    exit 1
fi
echo "" | tee -a "${LOG_FILE}"

# Step 2: Run benchmark accuracy test with synthetic data (Test Mode Only)
echo "[2/3] Running benchmark accuracy test (T014)..." | tee -a "${LOG_FILE}"
echo "Command: Rscript ${CODE_DIR}/06_benchmark_accuracy.R --mode=test" | tee -a "${LOG_FILE}"

if Rscript "${CODE_DIR}/06_benchmark_accuracy.R" --mode=test >> "${LOG_FILE}" 2>&1; then
    echo "✓ Benchmark accuracy test passed." | tee -a "${LOG_FILE}"
else
    echo "✗ Benchmark accuracy test failed. Check ${LOG_FILE} for details." | tee -a "${LOG_FILE}"
    exit 1
fi
echo "" | tee -a "${LOG_FILE}"

# Step 3: Render the report using test data
echo "[3/3] Rendering RMarkdown report with test data..." | tee -a "${LOG_FILE}"
echo "Input: ${REPORT_INPUT}" | tee -a "${LOG_FILE}"
echo "Output: ${REPORT_OUTPUT}" | tee -a "${LOG_FILE}"

# Ensure the input file exists for the report render
if [ ! -f "${REPORT_INPUT}" ]; then
    echo "✗ Error: Test input file not found at ${REPORT_INPUT}" | tee -a "${LOG_FILE}"
    echo "This file is generated by T039. Ensure T039 has been completed." | tee -a "${LOG_FILE}"
    exit 1
fi

# Render the report using the Python helper if available, or direct R
# Attempt to use the Python wrapper first (as per existing API surface)
if [ -f "${CODE_DIR}/04_render_report.py" ]; then
    echo "Using Python helper (04_render_report.py)..." | tee -a "${LOG_FILE}"
    # Note: The Python helper expects specific arguments. We pass the input path.
    if python3 "${CODE_DIR}/04_render_report.py" --input "${REPORT_INPUT}" --output "${REPORT_OUTPUT}" >> "${LOG_FILE}" 2>&1; then
        echo "✓ Report rendered successfully." | tee -a "${LOG_FILE}"
    else
        echo "✗ Report rendering failed via Python helper. Falling back to direct R..." | tee -a "${LOG_FILE}"
        # Fallback to direct R command if helper fails
        if Rscript -e "rmarkdown::render('${CODE_DIR}/04_report.Rmd', output_file='${REPORT_OUTPUT}', params=list(data_input='${REPORT_INPUT}'))" >> "${LOG_FILE}" 2>&1; then
            echo "✓ Report rendered successfully (fallback)." | tee -a "${LOG_FILE}"
        else
            echo "✗ Report rendering failed completely. Check ${LOG_FILE}." | tee -a "${LOG_FILE}"
            exit 1
        fi
    fi
else
    # Fallback to direct R command
    echo "Direct R render (no Python helper found)..." | tee -a "${LOG_FILE}"
    if Rscript -e "rmarkdown::render('${CODE_DIR}/04_report.Rmd', output_file='${REPORT_OUTPUT}', params=list(data_input='${REPORT_INPUT}'))" >> "${LOG_FILE}" 2>&1; then
        echo "✓ Report rendered successfully." | tee -a "${LOG_FILE}"
    else
        echo "✗ Report rendering failed. Check ${LOG_FILE}." | tee -a "${LOG_FILE}"
        exit 1
    fi
fi

# Final Validation: Check if output file exists and is non-empty
if [ -f "${REPORT_OUTPUT}" ] && [ -s "${REPORT_OUTPUT}" ]; then
    echo "✓ Final report artifact verified: ${REPORT_OUTPUT}" | tee -a "${LOG_FILE}"
else
    echo "✗ Final report artifact missing or empty: ${REPORT_OUTPUT}" | tee -a "${LOG_FILE}"
    exit 1
fi

echo "" | tee -a "${LOG_FILE}"
echo "=== All Tests Passed ===" | tee -a "${LOG_FILE}"
echo "End Time: $(date)" | tee -a "${LOG_FILE}"
exit 0
