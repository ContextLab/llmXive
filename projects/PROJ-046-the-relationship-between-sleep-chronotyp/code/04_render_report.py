"""
Task T028: Generate the final HTML/PDF report for the Chronotype-Moral Judgement analysis.

This script renders the R Markdown document (code/04_report.Rmd) using the
processed data and results generated by previous tasks (T019, T027, etc.).

It relies on the 'renv' environment initialized in T002 and the R Markdown
infrastructure. It acts as a Python wrapper to ensure the R script runs
within the project's Python-managed environment.
"""
import os
import subprocess
import sys
from pathlib import Path

def get_project_root() -> Path:
    """Determine the project root directory."""
    # Assuming the script is run from the project root or code/ directory
    current = Path(__file__).resolve()
    # Traverse up until we find a marker or hit root
    for parent in current.parents:
        if (parent / ".git").exists() or (parent / "tasks.md").exists():
            return parent
    return current.parent.parent

def ensure_directories(root: Path) -> None:
    """Ensure output directories exist."""
    reports_dir = root / "reports"
    data_derived_dir = root / "data" / "derived"
    
    reports_dir.mkdir(parents=True, exist_ok=True)
    
    # Verify input files exist before running
    required_inputs = [
        data_derived_dir / "classified_data.csv",
        data_derived_dir / "ancova_results.csv",
        data_derived_dir / "effect_sizes.csv",
        data_derived_dir / "reliability_metrics.csv",
        data_derived_dir / "sensitivity_sweep.csv",
        data_derived_dir / "exclusion_counts.json",
        root / "code" / "04_report.Rmd"
    ]
    
    missing = [f for f in required_inputs if not f.exists()]
    if missing:
        missing_str = "\n  - ".join(str(m) for m in missing)
        raise FileNotFoundError(
            f"Cannot render report. Missing required input files:\n  - {missing_str}"
        )

def run_rmarkdown_render(root: Path) -> None:
    """
    Execute the R Markdown rendering process.
    
    This assumes R and the 'rmarkdown' package are available in the system PATH
    or the 'renv' environment.
    """
    r_script = root / "code" / "04_render_report.R"
    
    # Create a temporary R script to handle the rendering to avoid inline errors
    r_code = f"""
    library(rmarkdown)
    
    project_root <- "{root.resolve()}"
    input_file <- file.path(project_root, "code", "04_report.Rmd")
    output_file_html <- file.path(project_root, "reports", "chronotype_moral_analysis.html")
    
    # Set working directory to project root to ensure relative paths in Rmd work
    setwd(project_root)
    
    cat("Rendering report: ", input_file, "\\n")
    
    tryCatch({{
      render(
        input = input_file,
        output_file = "chronotype_moral_analysis.html",
        output_dir = file.path(project_root, "reports"),
        quiet = FALSE
      )
      cat("Report generated successfully: ", output_file_html, "\\n")
    }}, error = function(e) {{
      cat("ERROR rendering report: ", e$message, "\\n")
      quit(status = 1)
    }})
    """
    
    # Write the temporary R script
    with open(r_script, "w") as f:
        f.write(r_code)
    
    try:
        # Run Rscript
        result = subprocess.run(
            ["Rscript", str(r_script)],
            cwd=root,
            capture_output=True,
            text=True
        )
        
        if result.returncode != 0:
            print("STDOUT:", result.stdout)
            print("STDERR:", result.stderr)
            raise RuntimeError(f"R rendering failed with code {result.returncode}")
        
        print(result.stdout)
        
    finally:
        # Cleanup temporary script
        if r_script.exists():
            r_script.unlink()

def main():
    """Main entry point for T028."""
    print("Starting T028: Report Generation")
    root = get_project_root()
    print(f"Project root detected at: {root}")
    
    ensure_directories(root)
    run_rmarkdown_render(root)
    
    output_path = root / "reports" / "chronotype_moral_analysis.html"
    if output_path.exists():
        print(f"SUCCESS: Report generated at {output_path}")
    else:
        raise RuntimeError("Report generation completed but output file not found.")

if __name__ == "__main__":
    main()
