import os
import json

def create_data_directories():
    """
    Creates the required directory structure for the project data and logs.
    Directories created:
    - data/raw/
    - data/processed/
    - data/derived/
    - logs/
    """
    base_dirs = [
        "data/raw",
        "data/processed",
        "data/derived",
        "logs"
    ]

    created = []
    for dir_path in base_dirs:
        if not os.path.exists(dir_path):
            os.makedirs(dir_path, exist_ok=True)
            created.append(dir_path)
        else:
            # Ensure it's a directory if it exists but is a file (shouldn't happen in normal flow)
            if not os.path.isdir(dir_path):
                raise NotADirectoryError(f"Path exists but is not a directory: {dir_path}")
    
    return created

def create_gitignore_rules():
    """
    Generates the .gitignore content specific to the data and logs directories.
    Returns the content string.
    """
    rules = [
        "# Data directories",
        "data/raw/*",
        "!data/raw/.gitkeep",  # Keep directory but ignore content
        "data/processed/*",
        "!data/processed/.gitkeep",
        "data/derived/*",
        "!data/derived/.gitkeep",
        "",
        "# Logs",
        "logs/*",
        "!logs/.gitkeep",
        "",
        "# R specific",
        "*.RData",
        "*.rds",
        "renv/",
        ".Rhistory",
        "",
        "# OS specific",
        ".DS_Store",
        "Thumbs.db"
    ]
    return "\n".join(rules)

def create_gitkeep_files():
    """
    Creates .gitkeep files in empty directories to ensure they are tracked by git.
    """
    dirs = [
        "data/raw",
        "data/processed",
        "data/derived",
        "logs"
    ]
    for d in dirs:
        gitkeep_path = os.path.join(d, ".gitkeep")
        with open(gitkeep_path, "w") as f:
            f.write("# Keep directory in version control\n")

def main():
    print("Setting up data directory structure...")
    
    # 1. Create directories
    created_dirs = create_data_directories()
    print(f"Created directories: {created_dirs}")
    
    # 2. Create .gitkeep files to ensure empty dirs are tracked
    create_gitkeep_files()
    print("Created .gitkeep files.")
    
    # 3. Update or create .gitignore with specific rules
    gitignore_path = ".gitignore"
    new_rules = create_gitignore_rules()
    
    existing_rules = set()
    if os.path.exists(gitignore_path):
        with open(gitignore_path, "r") as f:
            existing_rules = set(line.strip() for line in f if line.strip())
    
    # Check if our specific rules are already present (simple check)
    # We will append if the specific block isn't there to avoid duplicates in simple runs
    # A robust implementation would parse, but for setup script, appending if missing is safer
    
    with open(gitignore_path, "a") as f:
        f.write("\n")
        f.write("# --- Project Data & Logs (Auto-generated by setup_data_structure.py) ---\n")
        f.write(new_rules)
        f.write("\n")
    
    print(f"Updated {gitignore_path} with data/log rules.")
    print("Data structure setup complete.")

if __name__ == "__main__":
    main()
