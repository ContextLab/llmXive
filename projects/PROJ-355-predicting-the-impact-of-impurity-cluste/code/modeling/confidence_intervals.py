"""
Confidence Interval Calculation Module for US2.

This module implements the calculation of confidence intervals for predictions
generated by the regression model (Linear Regression/OLS). It provides functions
to compute prediction intervals and confidence intervals for the mean response,
utilizing the covariance matrix from the OLS results.

Dependencies:
    - statsmodels (for OLS results and covariance extraction)
    - numpy
    - pandas
"""

import numpy as np
import pandas as pd
import logging
from pathlib import Path
from typing import Dict, Any, Optional, Tuple, List
from statsmodels.regression.linear_model import RegressionResults

logger = logging.getLogger(__name__)


def calculate_prediction_intervals(
    results: RegressionResults,
    X_new: np.ndarray,
    alpha: float = 0.05
) -> Tuple[np.ndarray, np.ndarray]:
    """
    Calculate prediction intervals for new observations.

    Prediction intervals account for both the uncertainty in the estimated
    regression coefficients AND the uncertainty in the error term (residuals).

    Formula:
      PI = y_hat ± t_{n-p, 1-alpha/2} * sqrt(MSE * (1 + x_new' * (X'X)^{-1} * x_new))

    Args:
        results: Fitted statsmodels OLS regression results object.
        X_new: Feature matrix for new observations (n_samples, n_features).
        alpha: Significance level (default 0.05 for 95% CI).

    Returns:
        Tuple containing (lower_bounds, upper_bounds) as numpy arrays.
    """
    if X_new.ndim == 1:
        X_new = X_new.reshape(1, -1)

    n_samples = X_new.shape[0]
    n_params = results.df_model + 1  # +1 for intercept
    n_obs = results.nobs

    # Degrees of freedom for residuals
    df_resid = results.df_resid

    # Critical t-value
    from scipy import stats
    t_crit = stats.t.ppf(1 - alpha / 2, df_resid)

    # Predicted values
    y_hat = results.predict(X_new)

    # Mean Squared Error (MSE)
    mse = results.mse_resid

    # Covariance matrix of coefficients: Var(beta) = MSE * (X'X)^{-1}
    # We need the variance of the prediction: Var(x_new' * beta) = x_new' * Var(beta) * x_new
    # statsmodels provides the covariance matrix of parameters directly
    cov_params = results.cov_params()

    # Calculate variance of the mean prediction: x_new' * Cov(beta) * x_new
    # This is a vectorized operation for multiple rows in X_new
    # For each row i: var_mean_i = X_new[i] @ cov_params @ X_new[i].T
    var_mean = np.diag(X_new @ cov_params @ X_new.T)

    # Variance of prediction (includes error term variance)
    var_pred = var_mean + mse

    # Standard error of prediction
    se_pred = np.sqrt(var_pred)

    # Prediction Interval bounds
    lower = y_hat - t_crit * se_pred
    upper = y_hat + t_crit * se_pred

    return lower, upper


def calculate_confidence_intervals_mean(
    results: RegressionResults,
    X_new: np.ndarray,
    alpha: float = 0.05
) -> Tuple[np.ndarray, np.ndarray]:
    """
    Calculate confidence intervals for the mean response.

    Confidence intervals for the mean account ONLY for the uncertainty in the
    estimated regression coefficients, NOT the error term.

    Formula:
      CI = y_hat ± t_{n-p, 1-alpha/2} * sqrt(x_new' * (X'X)^{-1} * x_new * MSE)

    Args:
        results: Fitted statsmodels OLS regression results object.
        X_new: Feature matrix for new observations (n_samples, n_features).
        alpha: Significance level (default 0.05 for 95% CI).

    Returns:
        Tuple containing (lower_bounds, upper_bounds) as numpy arrays.
    """
    if X_new.ndim == 1:
        X_new = X_new.reshape(1, -1)

    n_params = results.df_model + 1
    n_obs = results.nobs
    df_resid = results.df_resid

    from scipy import stats
    t_crit = stats.t.ppf(1 - alpha / 2, df_resid)

    y_hat = results.predict(X_new)
    mse = results.mse_resid
    cov_params = results.cov_params()

    # Variance of the mean prediction
    var_mean = np.diag(X_new @ cov_params @ X_new.T)

    # Standard error of the mean
    se_mean = np.sqrt(var_mean * mse)

    lower = y_hat - t_crit * se_mean
    upper = y_hat + t_crit * se_mean

    return lower, upper


def add_confidence_intervals_to_results(
    df_predictions: pd.DataFrame,
    results: RegressionResults,
    feature_columns: List[str],
    interval_type: str = 'prediction',
    alpha: float = 0.05
) -> pd.DataFrame:
    """
    Add confidence or prediction intervals to a DataFrame of predictions.

    Args:
        df_predictions: DataFrame containing the original data and predictions.
        results: Fitted statsmodels OLS regression results object.
        feature_columns: List of column names corresponding to model features.
        interval_type: 'prediction' for prediction intervals, 'confidence' for mean CIs.
        alpha: Significance level.

    Returns:
        DataFrame with added columns: [interval_type]_lower, [interval_type]_upper.
    """
    X_new = df_predictions[feature_columns].values

    if interval_type == 'prediction':
        lower, upper = calculate_prediction_intervals(results, X_new, alpha)
        col_suffix = 'pred'
    elif interval_type == 'confidence':
        lower, upper = calculate_confidence_intervals_mean(results, X_new, alpha)
        col_suffix = 'conf'
    else:
        raise ValueError(f"Unknown interval_type: {interval_type}. Use 'prediction' or 'confidence'.")

    df_out = df_predictions.copy()
    df_out[f'{interval_type}_lower'] = lower
    df_out[f'{interval_type}_upper'] = upper

    logger.info(f"Added {interval_type} intervals to {len(df_out)} predictions.")
    return df_out


def run_confidence_interval_analysis(
    model_results: RegressionResults,
    processed_data_path: Path,
    output_path: Path,
    feature_columns: List[str],
    target_column: str = 'segregation_energy',
    alpha: float = 0.05
) -> Path:
    """
    Main entry point to run confidence interval analysis on processed data.

    Loads processed data, loads the model results (assumed to be passed or
    re-fitted if necessary, but here we assume the caller provides the fitted object),
    calculates intervals, and saves the enriched DataFrame.

    Note: This function expects `model_results` to be a fitted statsmodels OLS object.
    If the model needs to be re-fitted inside this function, the training logic
    from `code/modeling/train.py` would need to be invoked here or passed in.
    For this task, we assume the model is already trained and available.

    Args:
        model_results: Fitted OLS results object.
        processed_data_path: Path to the processed dataset CSV (descriptors + energies).
        output_path: Path to save the results CSV with intervals.
        feature_columns: List of feature column names.
        target_column: Name of the target variable column.
        alpha: Significance level.

    Returns:
        Path to the saved output file.
    """
    logger.info(f"Loading processed data from {processed_data_path}")
    df_data = pd.read_csv(processed_data_path)

    # Ensure feature columns exist
    missing_cols = [col for col in feature_columns if col not in df_data.columns]
    if missing_cols:
        raise ValueError(f"Missing feature columns in data: {missing_cols}")

    # Calculate intervals
    df_with_intervals = add_confidence_intervals_to_results(
        df_data,
        model_results,
        feature_columns,
        interval_type='prediction',
        alpha=alpha
    )

    logger.info(f"Saving results to {output_path}")
    output_path.parent.mkdir(parents=True, exist_ok=True)
    df_with_intervals.to_csv(output_path, index=False)

    return output_path
