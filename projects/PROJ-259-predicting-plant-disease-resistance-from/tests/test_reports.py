"""
Contract test for top_features.csv schema (US2).

This test verifies that the artifacts/reports/top_features.csv file
generated by the biomarker report module adheres to the expected schema:
- Required columns: feature_id, modality, p_value, effect_size, selection_frequency, rank
- Data types and constraints (e.g., p_value in [0,1], non-negative effect sizes for absolute, etc.)
- No missing values in required columns.
"""
import os
import pytest
import pandas as pd
from pathlib import Path

# Project root detection
PROJECT_ROOT = Path(__file__).resolve().parent.parent
REPORTS_DIR = PROJECT_ROOT / "artifacts" / "reports"
TOP_FEATURES_FILE = REPORTS_DIR / "top_features.csv"

# Expected schema definition
REQUIRED_COLUMNS = [
    "feature_id",
    "modality",
    "p_value",
    "effect_size",
    "selection_frequency",
    "rank"
]

VALID_MODALITIES = {"SNP", "metabolite"}

def _load_top_features():
    """Load the top_features.csv file. Raises FileNotFoundError if missing."""
    if not TOP_FEATURES_FILE.exists():
        raise FileNotFoundError(
            f"Contract test failed: {TOP_FEATURES_FILE} does not exist. "
            "Run the pipeline (T027) to generate this artifact first."
        )
    return pd.read_csv(TOP_FEATURES_FILE)

def test_top_features_schema_columns():
    """
    Contract Test: Verify all required columns exist in top_features.csv.
    """
    df = _load_top_features()
    missing_cols = set(REQUIRED_COLUMNS) - set(df.columns)
    assert not missing_cols, (
        f"Contract test failed: Missing required columns in top_features.csv: {missing_cols}. "
        f"Expected: {REQUIRED_COLUMNS}"
    )

def test_top_features_no_missing_values():
    """
    Contract Test: Verify no missing values in required columns.
    """
    df = _load_top_features()
    for col in REQUIRED_COLUMNS:
        assert not df[col].isnull().any(), (
            f"Contract test failed: Column '{col}' contains missing values in top_features.csv."
        )

def test_top_features_p_value_range():
    """
    Contract Test: Verify p_value is in the range [0, 1].
    """
    df = _load_top_features()
    invalid_p = df[(df["p_value"] < 0) | (df["p_value"] > 1)]
    assert len(invalid_p) == 0, (
        f"Contract test failed: Found {len(invalid_p)} rows with p_value outside [0, 1]."
    )

def test_top_features_modality_validity():
    """
    Contract Test: Verify modality column only contains allowed values.
    """
    df = _load_top_features()
    invalid_modalities = set(df["modality"].unique()) - VALID_MODALITIES
    assert not invalid_modalities, (
        f"Contract test failed: Invalid modalities found: {invalid_modalities}. "
        f"Allowed: {VALID_MODALITIES}"
    )

def test_top_features_rank_sequence():
    """
    Contract Test: Verify rank column is a unique sequence from 1 to N.
    """
    df = _load_top_features()
    n = len(df)
    expected_ranks = set(range(1, n + 1))
    actual_ranks = set(df["rank"])
    assert expected_ranks == actual_ranks, (
        f"Contract test failed: Rank sequence is invalid. "
        f"Expected {expected_ranks}, got {actual_ranks}."
    )

def test_top_features_effect_size_numeric():
    """
    Contract Test: Verify effect_size is numeric.
    """
    df = _load_top_features()
    # Check if the column can be interpreted as numeric
    try:
        pd.to_numeric(df["effect_size"], errors='raise')
    except ValueError:
        pytest.fail("Contract test failed: effect_size column contains non-numeric values.")

def test_top_features_selection_frequency_range():
    """
    Contract Test: Verify selection_frequency is in the range [0, 1].
    """
    df = _load_top_features()
    invalid_freq = df[(df["selection_frequency"] < 0) | (df["selection_frequency"] > 1)]
    assert len(invalid_freq) == 0, (
        f"Contract test failed: Found {len(invalid_freq)} rows with selection_frequency outside [0, 1]."
    )