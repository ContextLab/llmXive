"""
T034: Generate holdout_metrics.json with final accuracy/AUC/R² and permutation p-value.

This module consumes the output of T033 (permutation testing on hold-out set)
and the trained model's predictions on the hold-out set to generate the final
`artifacts/reports/holdout_metrics.json` artifact.

It aggregates:
1. Model performance metrics (Accuracy, AUC, or R²) on the hold-out set.
2. The permutation p-value calculated in T033.
3. Metadata about the run (n_permutations, metric_type, seed).
"""
import os
import json
import logging
from pathlib import Path
from typing import Dict, Any, Optional

# Import from local project modules
from config import get_path
from utils.logging import setup_logger

logger = setup_logger(__name__)

def load_holdout_metrics_from_permutation(perm_test_path: Optional[Path] = None) -> Dict[str, Any]:
    """
    Load the permutation test results generated by T033.
    
    Expects `artifacts/reports/holdout_metrics.json` (generated by T033) to contain:
    {
        "p_value": float,
        "n_permutations": int,
        "observed_metric": float,
        ...
    }
    """
    if perm_test_path is None:
        perm_test_path = get_path("reports", "holdout_metrics.json")
    
    if not perm_test_path.exists():
        raise FileNotFoundError(f"Permutation test output not found at {perm_test_path}. "
                                "Ensure T033 has completed successfully.")
    
    with open(perm_test_path, 'r') as f:
        data = json.load(f)
    
    return data

def load_model_predictions(predictions_path: Optional[Path] = None) -> Dict[str, Any]:
    """
    Load model predictions and metrics calculated during validation (T032/T033).
    
    We expect the permutation test script (T033) to have already calculated
    the observed metric. If not, we look for a separate validation output.
    For this task, we rely on the `holdout_metrics.json` from T033 which
    contains the `observed_metric` and `p_value`.
    """
    # The primary source of truth for the final report is the T033 output
    # which already contains the observed metric and p-value.
    return {}

def compile_final_report(perm_results: Dict[str, Any], output_path: Optional[Path] = None) -> Dict[str, Any]:
    """
    Compile the final holdout_metrics.json report.
    
    Structure:
    {
      "model_performance": {
        "metric_name": "accuracy" | "auc" | "r2",
        "value": float,
        "null_model_value": float (optional),
        "improvement": float (optional)
      },
      "statistical_significance": {
        "p_value": float,
        "n_permutations": int,
        "significance_threshold": 0.05,
        "is_significant": bool
      },
      "metadata": {
        "source": "holdout_set",
        "timestamp": str
      }
    }
    """
    # Extract key metrics from T033 output
    observed_metric = perm_results.get('observed_metric')
    p_value = perm_results.get('p_value')
    n_permutations = perm_results.get('n_permutations', 1000)
    metric_name = perm_results.get('metric_type', 'accuracy')
    
    # Determine significance
    is_significant = p_value <= 0.05
    
    final_report = {
        "model_performance": {
            "metric_name": metric_name,
            "value": observed_metric,
            "description": f"Performance on independent hold-out set"
        },
        "statistical_significance": {
            "p_value": p_value,
            "n_permutations": n_permutations,
            "significance_threshold": 0.05,
            "is_significant": is_significant,
            "description": "Permutation test p-value (H0: model performance <= random)"
        },
        "metadata": {
            "source": "holdout_set",
            "task_id": "T034",
            "generated_from": "T033_permutation_results"
        }
    }
    
    # Add null model comparison if present in T033 results
    if 'null_model_metric' in perm_results:
        final_report["model_performance"]["null_model_value"] = perm_results['null_model_metric']
        final_report["model_performance"]["improvement"] = observed_metric - perm_results['null_model_metric']
    
    return final_report

def save_report(report: Dict[str, Any], output_path: Optional[Path] = None) -> Path:
    """
    Save the compiled report to `artifacts/reports/holdout_metrics.json`.
    """
    if output_path is None:
        output_path = get_path("reports", "holdout_metrics.json")
    
    # Ensure directory exists
    output_path.parent.mkdir(parents=True, exist_ok=True)
    
    with open(output_path, 'w') as f:
        json.dump(report, f, indent=2)
    
    logger.info(f"Final holdout metrics report saved to {output_path}")
    return output_path

def generate_holdout_report_pipeline(
    perm_test_input: Optional[Path] = None,
    output_path: Optional[Path] = None
) -> Dict[str, Any]:
    """
    Pipeline function to orchestrate T034:
    1. Load T033 permutation results.
    2. Compile final report.
    3. Save to disk.
    """
    logger.info("Starting T034: Generate holdout_metrics.json")
    
    # Load results from T033
    perm_results = load_holdout_metrics_from_permutation(perm_test_input)
    
    # Compile
    report = compile_final_report(perm_results)
    
    # Save
    saved_path = save_report(report, output_path)
    
    logger.info("T034 completed successfully.")
    return report

def main():
    """
    CLI entry point for T034.
    """
    import argparse
    
    parser = argparse.ArgumentParser(description="Generate final holdout metrics report (T034)")
    parser.add_argument("--input", type=str, default=None,
                        help="Path to T033 permutation output (default: artifacts/reports/holdout_metrics.json)")
    parser.add_argument("--output", type=str, default=None,
                        help="Path to save final report (default: artifacts/reports/holdout_metrics.json)")
    
    args = parser.parse_args()
    
    input_path = Path(args.input) if args.input else None
    output_path = Path(args.output) if args.output else None
    
    try:
        generate_holdout_report_pipeline(input_path, output_path)
    except Exception as e:
        logger.error(f"Failed to generate holdout report: {e}")
        raise

if __name__ == "__main__":
    main()
