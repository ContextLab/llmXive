"""
T028b: Success Criteria Check for Biomarker Stability (SC-002)

Verifies that at least 10 SNPs and 10 metabolites remain significant
across the entire sensitivity sweep (intersection of significant features
across all three thresholds: 0.01, 0.05, 0.1).

Writes success status to artifacts/reports/success_criteria.json.
"""
import os
import logging
import json
from pathlib import Path
from typing import Dict, Set, List, Tuple

import pandas as pd

# Import from existing API surface
from config import get_path, load_config
from utils.logging import setup_logger, log_pipeline_step

# Constants for thresholds defined in T016
THRESHOLDS = [0.01, 0.05, 0.1]
MIN_FEATURES_REQUIRED = 10

logger = setup_logger("success_criteria_check")

def load_selection_frequency(filepath: Path) -> pd.DataFrame:
    """Load the selection frequency CSV generated by feature_selection.py."""
    if not filepath.exists():
        raise FileNotFoundError(f"Selection frequency file not found: {filepath}")
    
    df = pd.read_csv(filepath)
    required_cols = {"feature_id", "threshold", "frequency"}
    if not required_cols.issubset(df.columns):
        missing = required_cols - set(df.columns)
        raise ValueError(f"Missing required columns in {filepath}: {missing}")
    
    return df

def get_significant_features(df: pd.DataFrame, threshold: float) -> Set[str]:
    """
    Get features that are significant at a specific threshold.
    Significant is defined as frequency == 1.0 (selected in all 3 iterations)
    AND the feature passed the statistical test (p-value < 0.05) implicitly
    handled by the feature selection process.
    
    Here we assume 'frequency' represents the selection rate across the 3 runs
    at that threshold. We require frequency == 1.0 for strict intersection.
    """
    # Filter for the specific threshold
    subset = df[df["threshold"] == threshold]
    # Filter for high frequency (selected in all runs)
    significant = subset[subset["frequency"] == 1.0]["feature_id"].tolist()
    return set(significant)

def categorize_features(feature_ids: List[str]) -> Tuple[int, int]:
    """
    Categorize features into SNPs and Metabolites based on naming convention.
    Assumes SNP IDs start with 'snp_' and metabolite IDs start with 'met_'
    or contain 'met'. Adjust if project naming convention differs.
    """
    snp_count = 0
    metabo_count = 0
    
    for fid in feature_ids:
        fid_lower = str(fid).lower()
        if fid_lower.startswith("snp_") or fid_lower.startswith("snp"):
            snp_count += 1
        elif "met" in fid_lower or fid_lower.startswith("met_"):
            metabo_count += 1
        else:
            # Fallback: check if it looks like a generic ID
            # In a real scenario, we might have a mapping file.
            # For now, we count unknowns as neither to be strict.
            pass
            
    return snp_count, metabo_count

def check_success_criteria(df: pd.DataFrame) -> Dict[str, any]:
    """
    Perform the intersection check across all thresholds.
    Returns a dict with the results and status.
    """
    logger.info(f"Checking success criteria for thresholds: {THRESHOLDS}")
    
    # Get significant features for each threshold
    significant_sets = []
    for thresh in THRESHOLDS:
        s_set = get_significant_features(df, thresh)
        logger.info(f"Threshold {thresh}: {len(s_set)} significant features")
        significant_sets.append(s_set)
    
    # Calculate intersection (features significant across ALL thresholds)
    if not significant_sets:
        intersection = set()
    else:
        intersection = set.intersection(*significant_sets)
    
    logger.info(f"Intersection size (stable across all thresholds): {len(intersection)}")
    
    # Categorize
    snp_count, metabo_count = categorize_features(list(intersection))
    
    logger.info(f"Stable SNPs: {snp_count}, Stable Metabolites: {metabo_count}")
    
    # Check against criteria
    success = (snp_count >= MIN_FEATURES_REQUIRED) and (metabo_count >= MIN_FEATURES_REQUIRED)
    
    status = "PASSED" if success else "FAILED"
    
    if not success:
        if snp_count < MIN_FEATURES_REQUIRED:
            logger.warning(f"SC-002 FAILED: Only {snp_count} SNPs found (required >= {MIN_FEATURES_REQUIRED})")
        if metabo_count < MIN_FEATURES_REQUIRED:
            logger.warning(f"SC-002 FAILED: Only {metabo_count} metabolites found (required >= {MIN_FEATURES_REQUIRED})")
    
    return {
        "status": status,
        "criteria": "SC-002",
        "description": "At least 10 SNPs and 10 metabolites significant across all thresholds",
        "thresholds_checked": THRESHOLDS,
        "intersection_size": len(intersection),
        "snp_count": snp_count,
        "metabolite_count": metabo_count,
        "minimum_required": MIN_FEATURES_REQUIRED,
        "stable_features": sorted(list(intersection))
    }

def write_success_report(result: Dict[str, any], output_path: Path):
    """Write the result to the JSON file."""
    output_path.parent.mkdir(parents=True, exist_ok=True)
    with open(output_path, "w") as f:
        json.dump(result, f, indent=2)
    logger.info(f"Success criteria report written to {output_path}")

def main():
    """Main entry point for the script."""
    config = load_config()
    
    # Define paths based on project structure
    input_path = get_path("data_processed_selection_frequency", config)
    output_path = get_path("reports_success_criteria", config)
    
    # Fallback paths if config keys are missing (standard project paths)
    if not input_path:
        input_path = Path("data/processed/selection_frequency.csv")
    if not output_path:
        output_path = Path("artifacts/reports/success_criteria.json")
    
    try:
        # Load data
        df = load_selection_frequency(input_path)
        
        # Check criteria
        result = check_success_criteria(df)
        
        # Write report
        write_success_report(result, output_path)
        
        # Exit with appropriate code if running in CI
        if result["status"] == "FAILED":
            logger.error("Pipeline failed SC-002 check.")
            return 1
        
        return 0
        
    except FileNotFoundError as e:
        logger.error(f"Data file missing: {e}")
        return 2
    except Exception as e:
        logger.exception(f"Unexpected error during success criteria check: {e}")
        return 3

if __name__ == "__main__":
    import sys
    sys.exit(main())
