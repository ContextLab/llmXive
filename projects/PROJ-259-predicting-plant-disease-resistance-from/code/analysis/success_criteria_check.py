import os
import logging
import json
from pathlib import Path
from typing import Dict, Set, List, Tuple
import pandas as pd

from config import get_artifacts_path
from utils.logging import get_logger, log_pipeline_step

logger = get_logger(__name__)

SELECTION_FREQUENCY_FILE = "selection_frequency.csv"
SUCCESS_CRITERIA_FILE = "success_criteria.json"
SIGNIFICANCE_THRESHOLD = 0.05
MIN_SIGNIFICANT_FEATURES = 10
THRESHOLDS = [0.01, 0.05, 0.1]


def load_selection_frequency() -> pd.DataFrame:
    """
    Load the selection frequency CSV generated by the sensitivity sweep.
    Expected columns: feature_id, threshold, frequency
    """
    reports_path = get_artifacts_path() / "reports"
    file_path = reports_path / SELECTION_FREQUENCY_FILE

    if not file_path.exists():
        raise FileNotFoundError(
            f"Selection frequency file not found at {file_path}. "
            "Ensure the sensitivity sweep (T016) has completed successfully."
        )

    df = pd.read_csv(file_path)
    required_cols = {"feature_id", "threshold", "frequency"}
    if not required_cols.issubset(df.columns):
        raise ValueError(
            f"Selection frequency file missing required columns. "
            f"Found: {df.columns}, Required: {required_cols}"
        )
    return df


def get_significant_features(df: pd.DataFrame, threshold: float) -> Set[str]:
    """
    Identify features with selection frequency >= 0.5 at a specific threshold.
    Returns a set of feature IDs.
    """
    # Filter for the specific threshold
    subset = df[df["threshold"] == threshold]
    # Filter for frequency >= 0.5 (majority selection across 3 runs)
    significant = subset[subset["frequency"] >= 0.5]["feature_id"]
    return set(significant)


def categorize_features(df: pd.DataFrame) -> Dict[str, Set[str]]:
    """
    Categorize features into SNPs and Metabolites based on prefix convention:
    - SNPs: feature_id starts with 'SNP_'
    - Metabolites: feature_id starts with 'MET_'
    """
    snps = set()
    metabolites = set()

    for feature_id in df["feature_id"].unique():
        if feature_id.startswith("SNP_"):
            snps.add(feature_id)
        elif feature_id.startswith("MET_"):
            metabolites.add(feature_id)
        else:
            logger.warning(f"Feature {feature_id} does not match expected SNP_ or MET_ prefix.")

    return {
        "SNPs": snps,
        "Metabolites": metabolites
    }


def check_success_criteria(df: pd.DataFrame) -> Tuple[bool, Dict[str, int]]:
    """
    Check if at least 10 SNPs and 10 metabolites remain significant
    across the entire sensitivity sweep (intersection of significant features
    across all three thresholds: 0.01, 0.05, 0.1).

    Returns:
        Tuple[success: bool, counts: Dict[str, int]]
    """
    all_significant_snps = None
    all_significant_metabolites = None

    categories = categorize_features(df)
    all_snps = categories["SNPs"]
    all_metabolites = categories["Metabolites"]

    logger.info(f"Total SNPs found: {len(all_snps)}, Total Metabolites found: {len(all_metabolites)}")

    for thresh in THRESHOLDS:
        sig_features = get_significant_features(df, thresh)
        
        # Filter current significant set by type
        current_snps = sig_features.intersection(all_snps)
        current_metabolites = sig_features.intersection(all_metabolites)

        logger.info(f"Threshold {thresh}: Significant SNPs={len(current_snps)}, Significant Metabolites={len(current_metabolites)}")

        if all_significant_snps is None:
            all_significant_snps = current_snps
        else:
            all_significant_snps = all_significant_snps.intersection(current_snps)

        if all_significant_metabolites is None:
            all_significant_metabolites = current_metabolites
        else:
            all_significant_metabolites = all_significant_metabolites.intersection(current_metabolites)

    final_snps = all_significant_snps if all_significant_snps else set()
    final_metabolites = all_significant_metabolites if all_significant_metabolites else set()

    count_snps = len(final_snps)
    count_metabolites = len(final_metabolites)

    success = (count_snps >= MIN_SIGNIFICANT_FEATURES) and (count_metabolites >= MIN_SIGNIFICANT_FEATURES)

    return success, {
        "snps_count": count_snps,
        "metabolites_count": count_metabolites,
        "total_intersection_snps": len(final_snps),
        "total_intersection_metabolites": len(final_metabolites)
    }


def write_success_report(success: bool, counts: Dict[str, int]) -> Path:
    """
    Write the success criteria result to artifacts/reports/success_criteria.json.
    """
    reports_path = get_artifacts_path() / "reports"
    reports_path.mkdir(parents=True, exist_ok=True)
    output_path = reports_path / SUCCESS_CRITERIA_FILE

    report_data = {
        "success_status": "PASSED" if success else "FAILED",
        "criteria": {
            "min_snps_required": MIN_SIGNIFICANT_FEATURES,
            "min_metabolites_required": MIN_SIGNIFICANT_FEATURES,
            "thresholds_swept": THRESHOLDS,
            "frequency_cutoff": 0.5
        },
        "results": counts,
        "message": (
            "Success criteria met: At least 10 SNPs and 10 metabolites remained significant "
            "across all sensitivity thresholds."
        ) if success else (
            "Success criteria FAILED: Fewer than 10 SNPs or 10 metabolites remained significant "
            "across all sensitivity thresholds. Review feature selection stability."
        )
    }

    with open(output_path, "w") as f:
        json.dump(report_data, f, indent=2)

    logger.info(f"Success criteria report written to {output_path}")
    return output_path


def main():
    """
    Main entry point for the success criteria check.
    """
    log_pipeline_step("Checking Success Criteria (SC-002)")
    logger.info("Starting success criteria verification...")

    try:
        df = load_selection_frequency()
        success, counts = check_success_criteria(df)
        write_success_report(success, counts)

        if not success:
            logger.warning("SUCCESS CRITERIA FAILED: Insufficient stable biomarkers found across thresholds.")
            # Do not raise exception, just log warning as per task description
        else:
            logger.info("SUCCESS CRITERIA PASSED.")

    except FileNotFoundError as e:
        logger.error(f"Data file missing: {e}")
        raise
    except Exception as e:
        logger.error(f"Error during success criteria check: {e}")
        raise


if __name__ == "__main__":
    main()