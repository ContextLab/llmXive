def load_selection_frequency(filepath: Optional[str] = None) -> pd.DataFrame:
    """
    Load the selection frequency CSV generated by feature selection.

    Args:
        filepath: Path to the CSV file. Defaults to project standard path.

    Returns:
        DataFrame with columns: feature_id, threshold, frequency
    """
    if filepath is None:
        # T023 specifies the output path for selection frequency
        reports_dir = get_reports_path()
        filepath = os.path.join(reports_dir, "selection_frequency.csv")

    if not os.path.exists(filepath):
        raise FileNotFoundError(f"Selection frequency file not found: {filepath}. "
                                "Ensure T016/T023 has been run successfully.")

    df = pd.read_csv(filepath)
    logger.info(f"Loaded selection frequency data: {len(df)} rows from {filepath}")
    return df


def load_effect_sizes(filepath: Optional[str] = None) -> pd.DataFrame:
    """
    Load effect size coefficients calculated during feature selection.

    Note: T026 extends feature_selection.py to calculate these.
    We assume they are saved in a standard location or passed via context.
    For this implementation, we look for a standard output file or
    reconstruct from the selection frequency if coefficients were saved there.
    However, strictly following the API, we expect a dedicated file or
    we calculate them if raw data is available.

    Since T026 output isn't explicitly defined as a separate file in the prompt's
    `tasks.md` text for T026 (it says "Extend... to calculate"), we assume
    the effect sizes are either:
    1. Saved in `artifacts/reports/effect_sizes.csv` (standard convention)
    2. Or we need to re-calculate if we have the model coefficients.

    To be robust and self-contained for T027, we will attempt to load
    a pre-calculated effect size file. If not found, we assume the
    `selection_frequency.csv` might have been extended or we need to
    derive from a `model_coefficients.csv` if T017 saved them.

    Given the strict "extend don't re-author" rule and T026's description,
    we assume T026 produced `artifacts/reports/effect_sizes.csv`.
    """
    if filepath is None:
        reports_dir = get_reports_path()
        filepath = os.path.join(reports_dir, "effect_sizes.csv")

    if not os.path.exists(filepath):
        # Fallback: If T026 didn't save a separate file, we might need to
        # look for a combined artifact or raise an error.
        # For now, we raise a clear error if the prerequisite data is missing.
        raise FileNotFoundError(
            f"Effect sizes file not found: {filepath}. "
            "Ensure T026 has executed and saved effect sizes."
        )

    df = pd.read_csv(filepath)
    logger.info(f"Loaded effect size data: {len(df)} rows from {filepath}")
    return df


def calculate_aggregated_metrics(
    freq_df: pd.DataFrame,
    effect_df: pd.DataFrame
) -> pd.DataFrame:
    """
    Aggregate selection frequency and effect sizes.

    Merges data to ensure only features present in both (or at least selected)
    are reported. Calculates mean effect size if multiple thresholds exist.

    Args:
        freq_df: Selection frequency dataframe
        effect_df: Effect size dataframe

    Returns:
        DataFrame with aggregated metrics per feature.
    """
    # Ensure feature_id is string for consistent joining
    freq_df['feature_id'] = freq_df['feature_id'].astype(str)
    effect_df['feature_id'] = effect_df['feature_id'].astype(str)

    # Aggregate frequency: max frequency across thresholds or mean?
    # Task T016 says "run 3 independent iterations... calculate selection frequency".
    # We want the most robust measure. Let's take the max frequency observed
    # across the sensitivity sweep thresholds for the final report.
    freq_agg = freq_df.groupby('feature_id')['frequency'].max().reset_index()
    freq_agg.rename(columns={'frequency': 'max_selection_frequency'}, inplace=True)

    # Aggregate effect sizes: mean absolute effect size
    # We want to know the magnitude of the effect regardless of sign direction
    # for ranking, but we also want the sign for interpretation.
    # Let's keep the mean effect size.
    effect_agg = effect_df.groupby('feature_id')['coefficient'].mean().reset_index()
    effect_agg.rename(columns={'coefficient': 'mean_effect_size'}, inplace=True)

    # Merge
    merged = pd.merge(freq_agg, effect_agg, on='feature_id', how='inner')

    # Calculate absolute effect size for ranking
    merged['abs_effect_size'] = merged['mean_effect_size'].abs()

    logger.info(f"Aggregated metrics for {len(merged)} features.")
    return merged


def apply_significance_filter(
    df: pd.DataFrame,
    p_values: Optional[pd.Series] = None,
    p_threshold: float = 0.05
) -> pd.DataFrame:
    """
    Filter features based on selection frequency and statistical significance.

    Args:
        df: DataFrame with aggregated metrics
        p_values: Series of raw p-values indexed by feature_id.
                  If None, we assume p-values are derived from the effect sizes
                  via a t-test or are already present.
                  Given T024 implements BH correction, we expect raw p-values
                  to be available or calculated.
                  For this implementation, if p_values are not provided,
                  we simulate a p-value calculation based on effect size magnitude
                  (as a placeholder for real statistical testing if raw data is missing)
                  OR we expect the input df to have 'p_value' column if T026
                  included it.

                  *Correction*: T024 is a test for BH correction. T026 calculates effect sizes.
                  We need raw p-values. If they aren't in the effect_sizes.csv,
                  we must assume they are calculated elsewhere or we calculate them
                  from the model coefficients and standard errors if available.

                  To satisfy T027 without assuming extra files not mentioned:
                  We will check if 'p_value' exists in df. If not, we will
                  assume a mock calculation for demonstration if this is the
                  only step, BUT the prompt says "Real data only".
                  Therefore, we MUST assume the `effect_sizes.csv` from T026
                  includes 'p_value' or 'std_error' to compute it.

                  Let's assume `effect_sizes.csv` has columns: feature_id, coefficient, p_value.
                  If not, we raise an error.

    Returns:
        Filtered DataFrame
    """
    if 'p_value' not in df.columns:
        # Attempt to find if it's in the raw effect_df or need to calculate
        # Since we can't access raw data here easily without re-running modeling,
        # we enforce that T026 must have saved p_values.
        # If missing, we cannot proceed with real filtering.
        raise ValueError(
            "Column 'p_value' not found in data. "
            "T026 must output p-values in the effect sizes file."
        )

    # Apply Benjamini-Hochberg correction if raw p-values are present
    # We assume the input 'p_value' column is raw.
    raw_p = df['p_value'].values
    if len(raw_p) > 0:
        # Calculate adjusted p-values
        # benjamini_hochberg returns a Series of adjusted p-values
        # We need to pass the values and get the adjusted ones
        # The function signature in utils.stats is benjamini_hochberg(p_values)
        adj_p = benjamini_hochberg(pd.Series(raw_p))
        df['adj_p_value'] = adj_p.values
    else:
        df['adj_p_value'] = 1.0

    # Filter based on threshold
    # T028 says: "filter and rank features based on selection frequency and BH-adjusted p < 0.05"
    # We also need a frequency threshold. Let's use 0.5 (selected in >50% of runs)
    # or the max frequency. The prompt implies "significant across the entire sensitivity sweep"
    # for T028b, but T027 is just the report.
    # Let's filter: adj_p < 0.05 AND max_selection_frequency >= 0.5
    significant = df[
        (df['adj_p_value'] < p_threshold) &
        (df['max_selection_frequency'] >= 0.5)
    ].copy()

    logger.info(f"Filtered to {len(significant)} significant features (p<{p_threshold}, freq>=0.5)")
    return significant


def rank_and_sort(df: pd.DataFrame) -> pd.DataFrame:
    """
    Rank features by significance and effect size.

    Primary sort: adj_p_value (ascending)
    Secondary sort: abs_effect_size (descending)

    Args:
        df: Filtered DataFrame

    Returns:
        Ranked DataFrame
    """
    if df.empty:
        logger.warning("No significant features found to rank.")
        return df

    df = df.sort_values(
        by=['adj_p_value', 'abs_effect_size'],
        ascending=[True, False]
    )
    df['rank'] = range(1, len(df) + 1)
    return df


def generate_biomarker_report(
    output_path: Optional[str] = None,
    freq_path: Optional[str] = None,
    effect_path: Optional[str] = None
) -> str:
    """
    Main entry point to generate the top_features.csv report.

    1. Load selection frequency.
    2. Load effect sizes (with p-values).
    3. Aggregate metrics.
    4. Apply significance filters (BH-adjusted p < 0.05, freq >= 0.5).
    5. Rank and sort.
    6. Save to CSV.

    Args:
        output_path: Path for the output CSV. Defaults to artifacts/reports/top_features.csv
        freq_path: Path to selection_frequency.csv
        effect_path: Path to effect_sizes.csv

    Returns:
        Path to the generated report.
    """
    if output_path is None:
        reports_dir = get_reports_path()
        output_path = os.path.join(reports_dir, "top_features.csv")

    # Ensure output directory exists
    Path(output_path).parent.mkdir(parents=True, exist_ok=True)

    # Load data
    freq_df = load_selection_frequency(freq_path)
    effect_df = load_effect_sizes(effect_path)

    # Process
    agg_df = calculate_aggregated_metrics(freq_df, effect_df)
    sig_df = apply_significance_filter(agg_df)
    ranked_df = rank_and_sort(sig_df)

    # Select final columns
    final_cols = [
        'rank', 'feature_id', 'max_selection_frequency',
        'mean_effect_size', 'p_value', 'adj_p_value'
    ]
    # Ensure all columns exist
    available_cols = [c for c in final_cols if c in ranked_df.columns]
    report_df = ranked_df[available_cols]

    # Save
    report_df.to_csv(output_path, index=False)
    logger.info(f"Biomarker report generated: {output_path} ({len(report_df)} features)")

    return output_path


