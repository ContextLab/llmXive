"""
Biomarker Report Generation Module (T027)

Implements User Story 2: Biomarker Exploration.
Generates `artifacts/reports/top_features.csv` containing p-values and effect sizes
for selected features based on the output of the feature selection pipeline.

This module aggregates results from:
1. Feature selection frequencies (from T016/T026)
2. Effect size coefficients (from T026)
3. Statistical significance (p-values via Benjamini-Hochberg correction from T024)

It filters features based on selection frequency and adjusted p-value thresholds,
ranks them, and outputs the final report.
"""

