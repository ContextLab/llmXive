"""
Generate the selection frequency report (artifacts/reports/selection_frequency.csv).

This task (T023) aggregates the results from the sensitivity sweep performed in
feature_selection.py. It reads the intermediate selection frequency data (generated
by T016) and compiles it into the final report required by FR-003.

Output:
    artifacts/reports/selection_frequency.csv
        Columns: feature_id, threshold, frequency
"""
import os
import logging
import pandas as pd
from pathlib import Path
from typing import Optional

# Import from existing API surface
from analysis.feature_selection import save_selection_frequency, load_split_data
from config import get_artifacts_path, get_reports_path
from utils.logging import get_logger, log_pipeline_step

logger = get_logger(__name__)

def load_selection_frequency_input() -> pd.DataFrame:
    """
    Load the selection frequency data generated by the sensitivity sweep.
    
    The feature_selection.py module (T016) is expected to have generated
    an intermediate file or returned data that we need to format.
    Based on the task description for T016, it outputs 'selection_frequency.csv'.
    We look for it in the processed data directory or artifacts.
    """
    # The T016 implementation likely saves this to the processed data or a temp artifacts dir.
    # We will look for a standard path where T016 would have saved it.
    # If it hasn't been saved yet, we might need to re-run the sweep or assume it exists.
    # For this task, we assume T016 has run and produced 'selection_frequency.csv' 
    # in the processed data directory or a specific intermediate location.
    
    # Let's check the processed data path first, as that's where intermediate feature tables go.
    processed_path = get_artifacts_path() / "processed"
    input_file = processed_path / "selection_frequency.csv"
    
    if not input_file.exists():
        # Fallback: check reports directory if T016 saved it there by mistake
        input_file = get_reports_path() / "selection_frequency.csv"
    
    if not input_file.exists():
        # If the file doesn't exist, we cannot generate the report.
        # This implies T016 hasn't run or failed to save.
        # We raise an error to fail loudly.
        raise FileNotFoundError(
            f"Input file for selection frequency not found at {input_file}. "
            "Ensure T016 (feature_selection) has run successfully and saved the intermediate results."
        )
    
    logger.info(f"Loading selection frequency data from {input_file}")
    df = pd.read_csv(input_file)
    
    # Validate schema
    required_cols = {'feature_id', 'threshold', 'frequency'}
    if not required_cols.issubset(df.columns):
        raise ValueError(
            f"Input file missing required columns. Expected: {required_cols}, Got: {df.columns.tolist()}"
        )
    
    return df

def generate_selection_frequency_report(
    input_df: Optional[pd.DataFrame] = None,
    output_file: Optional[Path] = None
) -> Path:
    """
    Generate the final selection frequency report.
    
    This function ensures the output directory exists, writes the DataFrame
    to CSV, and logs the completion.
    
    Args:
        input_df: The DataFrame containing selection frequency data. 
                  If None, it will be loaded from the standard input location.
        output_file: Optional path to write the report. Defaults to 
                     artifacts/reports/selection_frequency.csv.
                     
    Returns:
        Path to the generated report file.
    """
    log_pipeline_step(logger, "T023", "Starting selection frequency report generation")
    
    if input_df is None:
        input_df = load_selection_frequency_input()
    
    if output_file is None:
        reports_dir = get_reports_path()
        reports_dir.mkdir(parents=True, exist_ok=True)
        output_file = reports_dir / "selection_frequency.csv"
    
    # Ensure directory exists
    output_file.parent.mkdir(parents=True, exist_ok=True)
    
    # Write the report
    # The task requires: feature_id, threshold, selection frequency
    # The input data should already have these. We just ensure correct column names and order.
    output_df = input_df[['feature_id', 'threshold', 'frequency']].copy()
    output_df.to_csv(output_file, index=False)
    
    logger.info(f"Selection frequency report generated at {output_file}")
    log_pipeline_step(logger, "T023", "Completed selection frequency report generation")
    
    return output_file

def main():
    """Entry point for T023."""
    try:
        report_path = generate_selection_frequency_report()
        print(f"SUCCESS: Report generated at {report_path}")
        return 0
    except Exception as e:
        logger.error(f"Failed to generate selection frequency report: {e}")
        raise

if __name__ == "__main__":
    main()