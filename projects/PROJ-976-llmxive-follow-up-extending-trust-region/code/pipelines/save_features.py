"""
Pipeline to save feature matrices to data/processed/ with checksums.

This script loads the feature dictionaries generated by extract_features.py,
serializes them to JSONL format in data/processed/, and records file
checksums to the project state file for reproducibility (Constitution Principle III).
"""

import json
import hashlib
import os
import sys
from pathlib import Path
from typing import Dict, Any, List, Optional

# Add project root to path for imports
sys.path.insert(0, str(Path(__file__).parent.parent.parent))

from code.config import DATA_PROCESSED_DIR, ensure_directories
from code.utils.checksum_recorder import record_dataset_checksum, load_state_file, save_state_file

def compute_file_checksum(file_path: Path) -> str:
    """Compute SHA-256 checksum of a file."""
    sha256_hash = hashlib.sha256()
    with open(file_path, "rb") as f:
        for byte_block in iter(lambda: f.read(4096), b""):
            sha256_hash.update(byte_block)
    return sha256_hash.hexdigest()

def save_feature_matrix(
    features: List[Dict[str, Any]],
    dataset_name: str,
    split_name: str = "train"
) -> Path:
    """
    Save a list of feature dictionaries to a JSONL file.

    Args:
        features: List of feature dictionaries (each representing one instance).
        dataset_name: Name of the source dataset (e.g., 'book_corpus', 'beir').
        split_name: Name of the data split (e.g., 'train', 'test').

    Returns:
        Path to the saved file.
    """
    # Ensure output directory exists
    ensure_directories()
    output_dir = Path(DATA_PROCESSED_DIR)
    output_dir.mkdir(parents=True, exist_ok=True)

    # Construct filename
    filename = f"{dataset_name}_{split_name}_features.jsonl"
    output_path = output_dir / filename

    # Write features to JSONL
    with open(output_path, "w", encoding="utf-8") as f:
        for instance in features:
            # Ensure all values are JSON serializable (handle NaN/Inf if necessary)
            clean_instance = {}
            for k, v in instance.items():
                if isinstance(v, float):
                    if v != v:  # NaN check
                        clean_instance[k] = None
                    elif v == float('inf') or v == float('-inf'):
                        clean_instance[k] = None
                    else:
                        clean_instance[k] = v
                else:
                    clean_instance[k] = v
            f.write(json.dumps(clean_instance) + "\n")

    return output_path

def save_all_features(
    book_corpus_features: Optional[List[Dict[str, Any]]] = None,
    beir_features: Optional[List[Dict[str, Any]]] = None
) -> Dict[str, Path]:
    """
    Save feature matrices for all processed datasets.

    Args:
        book_corpus_features: List of features for Book Corpus.
        beir_features: List of features for BEIR.

    Returns:
        Dictionary mapping dataset names to output file paths.
    """
    saved_files = {}

    if book_corpus_features:
        path = save_feature_matrix(book_corpus_features, "book_corpus", "train")
        saved_files["book_corpus"] = path
        print(f"Saved Book Corpus features to: {path}")

    if beir_features:
        path = save_feature_matrix(beir_features, "beir", "train")
        saved_files["beir"] = path
        print(f"Saved BEIR features to: {path}")

    return saved_files

def record_feature_checksums(saved_files: Dict[str, Path]) -> None:
    """
    Record checksums of saved feature files to the project state.

    Args:
        saved_files: Dictionary mapping dataset names to file paths.
    """
    if not saved_files:
        print("No files to record checksums for.")
        return

    state_path = Path("state/projects/PROJ-976-llmxive-follow-up-extending-trust-region.yaml")
    state_path.parent.mkdir(parents=True, exist_ok=True)

    # Load existing state
    state = load_state_file(state_path)

    # Record checksums for each file
    for dataset_name, file_path in saved_files.items():
        checksum = compute_file_checksum(file_path)
        record_dataset_checksum(state, dataset_name, str(file_path), checksum)
        print(f"Recorded checksum for {dataset_name}: {checksum}")

    # Save updated state
    save_state_file(state_path, state)
    print(f"State file updated: {state_path}")

