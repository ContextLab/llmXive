def filter_valid_realizations(
    bias_results: List[Dict[str, Any]],
    min_realizations: int = MIN_REALIZATIONS
) -> Tuple[List[Dict[str, Any]], List[Dict[str, Any]]]:
    """
    Filter bias results to ensure minimum number of valid realizations.
    
    A realization is considered valid if:
    1. It has a non-NaN bias magnitude
    2. It passed the null model comparison (if available)
    3. It is within acceptable bounds defined by sensitivity analysis
    
    Args:
        bias_results: List of bias analysis results
        min_realizations: Minimum number of valid realizations required
        
    Returns:
        Tuple of (valid_realizations, excluded_realizations)
    """
    valid_realizations = []
    excluded_realizations = []
    
    # Load auxiliary data if available
    null_model = load_null_model_baseline()
    sensitivity = load_sensitivity_results()
    
    # Determine sensitivity thresholds if available
    bias_variance_threshold = None
    if sensitivity:
        # Use the 'medium' tolerance setting as baseline
        medium_results = [s for s in sensitivity.get('sweep_results', []) 
                        if s.get('tolerance') == 'medium']
        if medium_results:
            # Use the maximum bias_variance observed in medium tolerance
            bias_variance_threshold = max(s.get('bias_variance', 0) for s in medium_results)
    
    for result in bias_results:
        is_valid = True
        exclusion_reasons = []
        
        # Check 1: Non-NaN bias magnitude
        if result.get('bias_magnitude') is None or result.get('bias_magnitude') != result.get('bias_magnitude'):
            is_valid = False
            exclusion_reasons.append("NaN bias magnitude")
        
        # Check 2: Null model comparison (if available)
        if null_model and is_valid:
            # Check if this realization significantly deviates from null model
            # For simplicity, we assume the null_model comparison includes a flag
            # In a real implementation, we'd check specific statistical tests
            if not result.get('passed_null_test', True):
                is_valid = False
                exclusion_reasons.append("Failed null model test")
        
        # Check 3: Sensitivity bounds (if available)
        if bias_variance_threshold is not None and is_valid:
            if result.get('bias_magnitude', 0) > bias_variance_threshold * 3:
                is_valid = False
                exclusion_reasons.append("Exceeds sensitivity threshold")
        
        if is_valid:
            valid_realizations.append(result)
        else:
            excluded_realizations.append({
                'realization_id': result.get('realization_id'),
                'reasons': exclusion_reasons
            })
    
    logger.info(f"Filtered realizations: {len(valid_realizations)} valid, {len(excluded_realizations)} excluded")
    
    # If we don't have enough valid realizations, log a warning but proceed
    if len(valid_realizations) < min_realizations:
        logger.warning(
            f"Only {len(valid_realizations)} valid realizations found, "
            f"minimum required is {min_realizations}. "
            f"Proceeding with available data, but results may be limited."
        )
    
    return valid_realizations, excluded_realizations

def generate_aggregation_report(
    valid_realizations: List[Dict[str, Any]],
    excluded_realizations: List[Dict[str, Any]],
    null_model: Optional[Dict[str, Any]],
    sensitivity: Optional[Dict[str, Any]]
) -> Dict[str, Any]:
    """
    Generate a comprehensive aggregation report.
    
    Args:
        valid_realizations: List of valid realization results
        excluded_realizations: List of excluded realization details
        null_model: Null model baseline data
        sensitivity: Sensitivity analysis results
        
    Returns:
        Dictionary containing the aggregation report
    """
    report = {
        'summary': {
            'total_processed': len(valid_realizations) + len(excluded_realizations),
            'valid_count': len(valid_realizations),
            'excluded_count': len(excluded_realizations),
            'min_realizations_required': MIN_REALIZATIONS,
            'min_realizations_met': len(valid_realizations) >= MIN_REALIZATIONS
        },
        'statistics': {
            'mean_bias': 0.0,
            'std_bias': 0.0,
            'median_bias': 0.0,
            'bias_by_algorithm': {},
            'bias_by_gap_fraction': {},
            'bias_by_morphology': {}
        },
        'null_model_comparison': null_model,
        'sensitivity_analysis': sensitivity,
        'excluded_realizations': excluded_realizations,
        'timestamp': str(__import__('datetime').datetime.now())
    }
    
    if valid_realizations:
        import numpy as np
        
        bias_values = [r['bias_magnitude'] for r in valid_realizations]
        report['statistics']['mean_bias'] = float(np.mean(bias_values))
        report['statistics']['std_bias'] = float(np.std(bias_values))
        report['statistics']['median_bias'] = float(np.median(bias_values))
        
        # Calculate statistics by algorithm
        algorithms = set(r.get('algorithm', 'unknown') for r in valid_realizations)
        for algo in algorithms:
            algo_bas = [r['bias_magnitude'] for r in valid_realizations 
                       if r.get('algorithm') == algo]
            if algo_bas:
                report['statistics']['bias_by_algorithm'][algo] = {
                    'count': len(algo_bas),
                    'mean': float(np.mean(algo_bas)),
                    'std': float(np.std(algo_bas)),
                    'median': float(np.median(algo_bas))
                }
        
        # Calculate statistics by gap fraction
        gap_fractions = set(r.get('gap_fraction', 0) for r in valid_realizations)
        for gf in gap_fractions:
            gf_bas = [r['bias_magnitude'] for r in valid_realizations 
                     if r.get('gap_fraction') == gf]
            if gf_bas:
                report['statistics']['bias_by_gap_fraction'][str(gf)] = {
                    'count': len(gf_bas),
                    'mean': float(np.mean(gf_bas)),
                    'std': float(np.std(gf_bas)),
                    'median': float(np.median(gf_bas))
                }
        
        # Calculate statistics by morphology
        morphologies = set(r.get('morphology', 'unknown') for r in valid_realizations)
        for morph in morphologies:
            morph_bas = [r['bias_magnitude'] for r in valid_realizations 
                        if r.get('morphology') == morph]
            if morph_bas:
                report['statistics']['bias_by_morphology'][morph] = {
                    'count': len(morph_bas),
                    'mean': float(np.mean(morph_bas)),
                    'std': float(np.std(morph_bas)),
                    'median': float(np.median(morph_bas))
                }
    
    return report

def save_aggregation_report(report: Dict[str, Any], output_path: Optional[str] = None) -> str:
    """
    Save the aggregation report to a JSON file.
    
    Args:
        report: The aggregation report dictionary
        output_path: Optional custom output path
        
    Returns:
        Path to the saved report
    """
    if output_path is None:
        output_path = os.path.join(DATA_RESULTS_DIR, 'aggregation_report.json')
    
    with open(output_path, 'w') as f:
        json.dump(report, f, indent=2, default=str)
    
    logger.info(f"Aggregation report saved to {output_path}")
    return output_path

def run_aggregation_pipeline() -> Dict[str, Any]:
    """
    Run the full aggregation pipeline.
    
    This function:
    1. Loads bias summary results
    2. Filters for valid realizations (ensuring minimum count)
    3. Generates a comprehensive report
    4. Saves the report to disk
    
    Returns:
        The aggregation report dictionary
    """
    logger.info("Starting aggregation pipeline...")
    
    # Load bias results
    bias_results = load_bias_summary_results()
    if not bias_results:
        logger.error("No bias results found. Cannot proceed with aggregation.")
        return {}
    
    # Filter for valid realizations
    valid_realizations, excluded_realizations = filter_valid_realizations(bias_results)
    
    # Load auxiliary data
    null_model = load_null_model_baseline()
    sensitivity = load_sensitivity_results()
    
    # Generate report
    report = generate_aggregation_report(
        valid_realizations, 
        excluded_realizations, 
        null_model, 
        sensitivity
    )
    
    # Save report
    save_aggregation_report(report)
    
    logger.info("Aggregation pipeline completed successfully.")
    return report

def main():
    """Main entry point for the aggregation script."""
    try:
        report = run_aggregation_pipeline()
        if report:
            print(f"Aggregation complete. Valid realizations: {report['summary']['valid_count']}")
            print(f"Minimum requirement met: {report['summary']['min_realizations_met']}")
            return 0
        else:
            print("Aggregation failed: No results generated.")
            return 1
    except Exception as e:
        logger.exception(f"Aggregation pipeline failed: {e}")
        return 1

if __name__ == "__main__":
    sys.exit(main())
