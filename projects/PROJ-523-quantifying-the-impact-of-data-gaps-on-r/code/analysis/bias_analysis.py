"""
Bias Analysis Module for US3.

Implements calculation of bias magnitude between recovered and ground-truth
cosmological parameters. Outputs results to `data/results/bias_summary.csv`.
"""
import os
import sys
import json
import csv
import logging
from pathlib import Path
from typing import Dict, List, Any

from config import DATA_RESULTS_DIR, DATA_METADATA_DIR, N_SIDE
from data_io import load_metadata

logger = logging.getLogger(__name__)

def load_ground_truth_parameters(realization_id: int) -> Dict[str, float]:
    """
    Loads ground truth parameters for a specific realization from metadata.
    
    Args:
        realization_id: The ID of the realization.
        
    Returns:
        Dictionary with keys: H0, Omega_m, n_s, tau.
    """
    meta_path = DATA_METADATA_DIR / f"realization_{realization_id}.json"
    if not meta_path.exists():
        raise FileNotFoundError(f"Ground truth metadata not found: {meta_path}")
    
    data = load_metadata(meta_path)
    return {
        "H0": float(data["H0"]),
        "Omega_m": float(data["Omega_m"]),
        "n_s": float(data["n_s"]),
        "tau": float(data["tau"])
    }

def load_parameter_estimation_results(realization_id: int) -> Dict[str, Any]:
    """
    Loads parameter estimation results for a specific realization.
    These results are expected to be generated by `parameter_est.py`.
    
    Args:
        realization_id: The ID of the realization.
        
    Returns:
        Dictionary with estimated parameters.
    """
    # Assuming parameter_est.py saves results in a specific format or directory.
    # Based on T028b, results might be in metadata or a specific results file.
    # We look for a file generated by the estimator.
    result_path = DATA_METADATA_DIR / f"realization_{realization_id}_estimation.json"
    
    if not result_path.exists():
        # Fallback: try to construct from available data if the estimator didn't save a dedicated file
        # But per spec, it should have saved.
        logger.warning(f"Estimation results not found for realization {realization_id}. Skipping.")
        return None
        
    with open(result_path, 'r') as f:
        return json.load(f)

def calculate_bias(recovered: Dict[str, float], ground_truth: Dict[str, float]) -> Dict[str, float]:
    """
    Calculates the absolute bias for each parameter.
    
    Args:
        recovered: Dictionary of recovered parameters.
        ground_truth: Dictionary of ground truth parameters.
        
    Returns:
        Dictionary with bias values for each parameter and total magnitude.
    """
    params = ["H0", "Omega_m", "n_s", "tau"]
    bias_dict = {}
    total_magnitude = 0.0
    
    for p in params:
        if p in recovered and p in ground_truth:
            diff = abs(recovered[p] - ground_truth[p])
            bias_dict[f"{p}_bias"] = diff
            total_magnitude += diff
        else:
            bias_dict[f"{p}_bias"] = 0.0
            
    bias_dict["bias_magnitude"] = total_magnitude
    return bias_dict

def run_bias_analysis():
    """
    Orchestrates the bias analysis for all valid realizations.
    Reads parameter estimation results, compares with ground truth,
    and writes `bias_summary.csv` to `data/results/`.
    """
    output_path = DATA_RESULTS_DIR / "bias_summary.csv"
    logger.info(f"Starting bias analysis. Output will be written to {output_path}")
    
    # Ensure output directory exists
    output_path.parent.mkdir(parents=True, exist_ok=True)
    
    # Get list of realizations (simple heuristic: look for metadata files)
    # In a real pipeline, this would come from the pipeline controller.
    # We scan the metadata directory for ground truth files.
    gt_files = list(DATA_METADATA_DIR.glob("realization_*.json"))
    # Filter out estimation files if they match the pattern (they shouldn't if named correctly)
    gt_files = [f for f in gt_files if "_estimation" not in str(f)]
    
    if not gt_files:
        logger.warning("No ground truth metadata files found. Cannot run bias analysis.")
        # Create empty file with headers to satisfy contract
        with open(output_path, 'w', newline='') as f:
            writer = csv.DictWriter(f, fieldnames=[
                "realization_id", "gap_fraction", "gap_morphology", "algorithm",
                "H0_bias", "Omega_m_bias", "n_s_bias", "tau_bias", "bias_magnitude"
            ])
            writer.writeheader()
        return

    rows = []
    
    for gt_file in gt_files:
        try:
            # Extract ID
            try:
                rid = int(gt_file.stem.split("_")[1])
            except (IndexError, ValueError):
                logger.warning(f"Skipping file with invalid naming: {gt_file.name}")
                continue
            
            # Load Ground Truth
            gt_params = load_ground_truth_parameters(rid)
            
            # Load Estimated Parameters
            est_params = load_parameter_estimation_results(rid)
            if est_params is None:
                continue
            
            # Extract metadata for context (gap fraction, algo)
            # These might be in the estimation file or ground truth file
            gap_frac = est_params.get("gap_fraction", 0.0)
            gap_morph = est_params.get("gap_morphology", "unknown")
            algo = est_params.get("algo_name", "unknown")
            
            # Calculate Bias
            bias_data = calculate_bias(est_params, gt_params)
            
            row = {
                "realization_id": rid,
                "gap_fraction": gap_frac,
                "gap_morphology": gap_morph,
                "algorithm": algo,
                "H0_bias": bias_data["H0_bias"],
                "Omega_m_bias": bias_data["Omega_m_bias"],
                "n_s_bias": bias_data["n_s_bias"],
                "tau_bias": bias_data["tau_bias"],
                "bias_magnitude": bias_data["bias_magnitude"]
            }
            rows.append(row)
            
        except Exception as e:
            logger.error(f"Error processing realization {gt_file}: {e}", exc_info=True)
            continue
    
    # Write to CSV
    if rows:
        fieldnames = [
            "realization_id", "gap_fraction", "gap_morphology", "algorithm",
            "H0_bias", "Omega_m_bias", "n_s_bias", "tau_bias", "bias_magnitude"
        ]
        with open(output_path, 'w', newline='') as f:
            writer = csv.DictWriter(f, fieldnames=fieldnames)
            writer.writeheader()
            writer.writerows(rows)
        logger.info(f"Successfully wrote {len(rows)} bias records to {output_path}")
    else:
        logger.warning("No valid bias records generated. Writing empty CSV.")
        with open(output_path, 'w', newline='') as f:
            writer = csv.DictWriter(f, fieldnames=[
                "realization_id", "gap_fraction", "gap_morphology", "algorithm",
                "H0_bias", "Omega_m_bias", "n_s_bias", "tau_bias", "bias_magnitude"
            ])
            writer.writeheader()

def main():
    """Entry point for running bias analysis."""
    logging.basicConfig(level=logging.INFO)
    run_bias_analysis()

if __name__ == "__main__":
    main()
