logger = logging.getLogger(__name__)

NULL_MODEL_DIR = DATA_DERIVED_DIR / "null_model"
BIAS_SUMMARY_PATH = DATA_RESULTS_DIR / "bias_summary.csv"
NULL_COMPARISON_OUTPUT = DATA_RESULTS_DIR / "null_model_comparison.json"

def load_null_model_baseline() -> Dict[str, Any]:
    """
    Load the Null Model baseline statistics from the derived directory.
    Expects a file `data/derived/null_model/baseline_stats.json` generated by T014b2.
    """
    baseline_path = NULL_MODEL_DIR / "baseline_stats.json"
    if not baseline_path.exists():
        logger.error(f"Null Model baseline file not found: {baseline_path}")
        raise FileNotFoundError(f"Null Model baseline missing at {baseline_path}. "
                                "Ensure T014b2 has been completed.")
    
    with open(baseline_path, 'r') as f:
        return json.load(f)

def load_bias_summary() -> List[Dict[str, Any]]:
    """
    Load the bias summary CSV generated by T029a.
    """
    if not BIAS_SUMMARY_PATH.exists():
        logger.error(f"Bias summary file not found: {BIAS_SUMMARY_PATH}")
        raise FileNotFoundError(f"Bias summary missing at {BIAS_SUMMARY_PATH}. "
                                "Ensure T029a has been completed.")
    
    results = []
    with open(BIAS_SUMMARY_PATH, 'r') as f:
        reader = csv.DictReader(f)
        for row in reader:
            # Convert numeric strings to floats
            numeric_fields = ['bias_magnitude', 'gap_fraction', 'recovered_H0', 'recovered_Omega_m', 'recovered_n_s', 'recovered_tau']
            for field in numeric_fields:
                if field in row and row[field]:
                    try:
                        row[field] = float(row[field])
                    except ValueError:
                        row[field] = None
            results.append(row)
    return results

def compare_bias_distributions(
    observed_biases: List[float], 
    null_biases: List[float]
) -> Dict[str, Any]:
    """
    Perform statistical comparison between observed biases and the Null Model baseline.
    
    Returns a dictionary containing:
    - mean_diff: Difference in means
    - t_statistic: T-statistic from independent samples t-test
    - p_value: P-value from t-test
    - is_significant: Boolean indicating if p < 0.05
    - effect_size: Cohen's d
    """
    if not observed_biases or not null_biases:
        return {
            "mean_diff": None,
            "t_statistic": None,
            "p_value": None,
            "is_significant": False,
            "effect_size": None,
            "error": "Insufficient data for comparison"
        }

    observed_arr = np.array(observed_biases)
    null_arr = np.array(null_biases)

    # Remove NaNs if any
    valid_obs = observed_arr[~np.isnan(observed_arr)]
    valid_null = null_arr[~np.isnan(null_arr)]

    if len(valid_obs) < 2 or len(valid_null) < 2:
        return {
            "mean_diff": None,
            "t_statistic": None,
            "p_value": None,
            "is_significant": False,
            "effect_size": None,
            "error": "Insufficient valid data points"
        }

    # Independent samples t-test
    t_stat, p_val = stats.ttest_ind(valid_obs, valid_null)
    
    # Calculate Cohen's d (effect size)
    mean_obs = np.mean(valid_obs)
    mean_null = np.mean(valid_null)
    std_obs = np.std(valid_obs, ddof=1)
    std_null = np.std(valid_null, ddof=1)
    
    # Pooled standard deviation
    n1, n2 = len(valid_obs), len(valid_null)
    pooled_std = np.sqrt(((n1 - 1) * std_obs**2 + (n2 - 1) * std_null**2) / (n1 + n2 - 2))
    
    if pooled_std == 0:
        effect_size = 0.0
    else:
        effect_size = (mean_obs - mean_null) / pooled_std

    return {
        "mean_diff": float(mean_obs - mean_null),
        "mean_observed": float(mean_obs),
        "mean_null": float(mean_null),
        "t_statistic": float(t_stat),
        "p_value": float(p_val),
        "is_significant": bool(p_val < 0.05),
        "effect_size": float(effect_size)
    }

def run_null_model_comparison() -> Dict[str, Any]:
    """
    Main function to execute the Null Model comparison analysis.
    
    1. Loads bias summary from T029a.
    2. Loads Null Model baseline from T014b2.
    3. Groups observed biases by gap fraction and morphology.
    4. Compares distributions against Null Model.
    5. Saves results to data/results/null_model_comparison.json.
    """
    logger.info("Starting Null Model Comparison Analysis (T031)...")
    
    # Load data
    try:
        bias_data = load_bias_summary()
        null_baseline = load_null_model_baseline()
    except FileNotFoundError as e:
        logger.error(str(e))
        return {"status": "failed", "error": str(e)}
    
    if not bias_data:
        logger.warning("No bias data found in summary.")
        return {"status": "failed", "error": "Empty bias summary"}
    
    # Extract Null Model bias values (assuming the baseline stores a list of bias magnitudes)
    # The structure of null_baseline depends on T014b2 output. 
    # We assume it contains a key 'bias_magnitudes' or similar.
    null_biases = null_baseline.get('bias_magnitudes', [])
    if not null_biases:
        # Fallback: if the baseline only has mean/std, we can't do a t-test directly,
        # but we can compare means. However, the task asks for comparison of trends.
        # Let's assume T014b2 generates a list of simulated null biases.
        logger.warning("Null Model baseline missing 'bias_magnitudes' list. Cannot perform statistical test.")
        return {"status": "failed", "error": "Null Model baseline format invalid"}

    # Group observed biases by gap_fraction and morphology to check trends
    # We will compare the overall distribution first, then potentially by group.
    observed_biases = [row['bias_magnitude'] for row in bias_data if row.get('bias_magnitude') is not None]
    
    comparison_result = compare_bias_distributions(observed_biases, null_biases)
    
    # Group by gap fraction for trend analysis
    grouped_comparisons = {}
    gap_fractions = sorted(list(set([row['gap_fraction'] for row in bias_data if row.get('gap_fraction') is not None])))
    
    for gf in gap_fractions:
        gf_biases = [row['bias_magnitude'] for row in bias_data 
                     if row.get('gap_fraction') == gf and row.get('bias_magnitude') is not None]
        
        if gf_biases:
            gf_result = compare_bias_distributions(gf_biases, null_biases)
            grouped_comparisons[str(gf)] = gf_result

    # Compile final report
    report = {
        "task_id": "T031",
        "description": "Comparison of observed bias trends against Null Model baseline",
        "timestamp": str(Path(__file__).stat().st_mtime), # Placeholder for actual time
        "input_files": {
            "bias_summary": str(BIAS_SUMMARY_PATH),
            "null_model_baseline": str(NULL_MODEL_DIR / "baseline_stats.json")
        },
        "overall_comparison": comparison_result,
        "by_gap_fraction": grouped_comparisons,
        "conclusion": "Significant bias detected" if comparison_result.get('is_significant') else "No significant bias detected relative to Null Model"
    }

    # Ensure output directory exists
    DATA_RESULTS_DIR.mkdir(parents=True, exist_ok=True)
    
    # Save report
    with open(NULL_COMPARISON_OUTPUT, 'w') as f:
        json.dump(report, f, indent=2)
    
    logger.info(f"Null Model Comparison saved to {NULL_COMPARISON_OUTPUT}")
    return report

def main():
    """Entry point for the script."""
    logging.basicConfig(level=logging.INFO, format='%(asctime)s - %(levelname)s - %(message)s')
    try:
        result = run_null_model_comparison()
        if result.get('status') == 'failed':
            sys.exit(1)
        print(json.dumps(result, indent=2))
    except Exception as e:
        logger.exception("An error occurred during Null Model Comparison")
        sys.exit(1)

if __name__ == "__main__":
    main()