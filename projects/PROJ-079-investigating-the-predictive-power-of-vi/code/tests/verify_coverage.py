"""
Verify that pytest coverage report exists and indicates >80% line coverage for src/ modules.

This script checks for the existence of the coverage report generated by T043a
and validates that the line coverage for src/ modules meets the >80% threshold.
"""
import os
import sys
import json
import logging
from pathlib import Path
from typing import Dict, Any

# Configure logging
logging.basicConfig(
    level=logging.INFO,
    format='%(asctime)s - %(levelname)s - %(message)s'
)
logger = logging.getLogger(__name__)

# Define paths
PROJECT_ROOT = Path(__file__).parent.parent
COVERAGE_REPORT_PATH = PROJECT_ROOT / "coverage" / "coverage.json"
SRC_DIR = "src"

def load_coverage_report(report_path: Path) -> Dict[str, Any]:
    """Load coverage report from JSON file."""
    if not report_path.exists():
        raise FileNotFoundError(f"Coverage report not found at {report_path}")
    
    with open(report_path, 'r') as f:
        return json.load(f)

def calculate_src_coverage(coverage_data: Dict[str, Any]) -> float:
    """Calculate average line coverage for src/ modules."""
    if 'files' not in coverage_data:
        raise ValueError("Invalid coverage report format: missing 'files' key")
    
    src_files = {k: v for k, v in coverage_data['files'].items() if SRC_DIR in k}
    
    if not src_files:
        raise ValueError(f"No files found in coverage report matching '{SRC_DIR}'")
    
    total_lines = 0
    covered_lines = 0
    
    for file_path, file_data in src_files.items():
        if 'summary' in file_data:
          summary = file_data['summary']
          total_lines += summary.get('lines', {}).get('total', 0)
          covered_lines += summary.get('lines', {}).get('covered', 0)
        elif 'lines' in file_data:
          # Alternative format where line data is directly in file_data
          lines = file_data['lines']
          total_lines += len([l for l in lines if l[4] != 0])  # 0 = not executed
          covered_lines += len([l for l in lines if l[4] == 1])  # 1 = executed
        
    if total_lines == 0:
        raise ValueError("No executable lines found in src/ modules")
    
    coverage_pct = (covered_lines / total_lines) * 100
    return coverage_pct

def verify_coverage_threshold(coverage_pct: float, threshold: float = 80.0) -> bool:
    """Verify coverage meets the threshold."""
    return coverage_pct >= threshold

def main() -> int:
    """Main entry point for coverage verification."""
    logger.info(f"Checking coverage report at: {COVERAGE_REPORT_PATH}")
    
    try:
        # Load coverage report
        coverage_data = load_coverage_report(COVERAGE_REPORT_PATH)
        logger.info("Coverage report loaded successfully")
        
        # Calculate src/ coverage
        coverage_pct = calculate_src_coverage(coverage_data)
        logger.info(f"Calculated src/ line coverage: {coverage_pct:.2f}%")
        
        # Verify threshold
        is_valid = verify_coverage_threshold(coverage_pct)
        
        if is_valid:
            logger.info(f"✓ Coverage threshold met: {coverage_pct:.2f}% >= 80.0%")
            return 0
        else:
            logger.error(f"✗ Coverage threshold NOT met: {coverage_pct:.2f}% < 80.0%")
            return 1
            
    except FileNotFoundError as e:
        logger.error(f"Coverage report not found: {e}")
        logger.error("Please run pytest with coverage first (T043a)")
        return 1
    except ValueError as e:
        logger.error(f"Error processing coverage data: {e}")
        return 1
    except Exception as e:
        logger.error(f"Unexpected error: {e}")
        return 1

if __name__ == "__main__":
    sys.exit(main())
