#!/bin/bash
#
# code/06a_define_null_regions.sh
#
# Task: Define distal null regions (>10kb from genes) and output null_regions.bed
# Input: gene coordinates (assumed from T003/T005 processed data or manifest reference)
# Output: data/processed/null_regions.bed
#
# Requirements:
#   - Regions must be >10,000 bp away from any annotated gene boundary.
#   - Output must be in BED format (chrom, start, end).
#   - Must run on real data; if input gene file is missing, abort with error.
#
# Dependencies: bedtools (must be installed in environment)
#

set -euo pipefail

# Configuration
GENOME_SIZE_FILE="${GENOME_SIZE_FILE:-data/reference/yeast_s288c.genome}"
GENE_ANNOTATION_FILE="${GENE_ANNOTATION_FILE:-data/processed/genes.bed}"
OUTPUT_FILE="data/processed/null_regions.bed"
DISTANCE_THRESHOLD=10000

# Check for required inputs
if [[ ! -f "$GENE_ANNOTATION_FILE" ]]; then
    echo "ERROR: Gene annotation file not found: $GENE_ANNOTATION_FILE" >&2
    echo "This file is expected to be generated by prior steps (T005/T006)." >&2
    exit 1
fi

if [[ ! -f "$GENOME_SIZE_FILE" ]]; then
    echo "ERROR: Genome size file not found: $GENOME_SIZE_FILE" >&2
    echo "This file is required to define chromosome boundaries." >&2
    exit 1
fi

# Check for bedtools
if ! command -v bedtools &> /dev/null; then
    echo "ERROR: bedtools is not installed or not in PATH." >&2
    exit 1
fi

echo "Starting null region definition..."
echo "  Input genes: $GENE_ANNOTATION_FILE"
echo "  Genome size: $GENOME_SIZE_FILE"
echo "  Distance threshold: $DISTANCE_THRESHOLD bp"
echo "  Output: $OUTPUT_FILE"

# Ensure output directory exists
mkdir -p "$(dirname "$OUTPUT_FILE")"

# Step 1: Generate all genomic intervals (chromosomes)
# Step 2: Subtract gene regions extended by 10kb on both sides
# We use bedtools slop to expand gene regions, then subtract from genome

echo "Expanding gene regions by $DISTANCE_THRESHOLD bp..."
# bedtools slop -b 10000 extends 10kb upstream and downstream
# -g genome file ensures we don't extend beyond chromosome ends
bedtools slop -i "$GENE_ANNOTATION_FILE" -g "$GENOME_SIZE_FILE" -b $DISTANCE_THRESHOLD > data/processed/genes_extended.bed

echo "Computing complement (null regions)..."
# bedtools complement returns regions of the genome NOT covered by the input
bedtools complement -i data/processed/genes_extended.bed -g "$GENOME_SIZE_FILE" > "$OUTPUT_FILE"

# Cleanup intermediate
rm -f data/processed/genes_extended.bed

# Validate output
if [[ ! -s "$OUTPUT_FILE" ]]; then
    echo "WARNING: Output file is empty. No null regions found." >&2
    echo "This may indicate that the gene annotations cover the entire genome" >&2
    echo "or the distance threshold is too large." >&2
else
    REGION_COUNT=$(wc -l < "$OUTPUT_FILE")
    echo "SUCCESS: Generated $REGION_COUNT null regions."
    echo "Output written to: $OUTPUT_FILE"
fi
