"""Generate dataset size report with prime knot counts per crossing number.

This module implements T017: Generate dataset size report in
docs/reproducibility/dataset_counts.md with concrete prime knot counts
per crossing number (per US1 dataset enumeration requirement).
"""

import json
import csv
from pathlib import Path
from typing import Dict, List, Any, Optional
from collections import defaultdict

from reproducibility.logs import log_operation, get_logger
from reproducibility.hashing import record_artifact_hash


def load_cleaned_knots(csv_path: Path) -> List[Dict[str, Any]]:
    """Load cleaned knot data from CSV file.

    Args:
        csv_path: Path to the cleaned knots CSV file.

    Returns:
        List of knot records as dictionaries.
    """
    knots = []
    with open(csv_path, 'r', encoding='utf-8') as f:
        reader = csv.DictReader(f)
        for row in reader:
            knots.append(dict(row))
    return knots


def count_knots_per_crossing_number(knots: List[Dict[str, Any]]) -> Dict[int, int]:
    """Count prime knots per crossing number.

    Args:
        knots: List of knot records with crossing_number field.

    Returns:
        Dictionary mapping crossing number to count of prime knots.
    """
    counts: Dict[int, int] = defaultdict(int)
    for knot in knots:
        crossing_num = int(knot.get('crossing_number', 0))
        if crossing_num > 0:
            counts[crossing_num] += 1
    return dict(sorted(counts.items()))


def generate_dataset_counts_report(
    counts: Dict[int, int],
    total_knots: int
) -> str:
    """Generate markdown report content for dataset counts.

    Args:
        counts: Dictionary of crossing number to knot count.
        total_knots: Total number of prime knots in dataset.

    Returns:
        Markdown formatted report string.
    """
    lines = [
        "# Dataset Size Report",
        "",
        "## Prime Knot Counts per Crossing Number",
        "",
        "This report documents the concrete prime knot counts per crossing number",
        "for the Knot Atlas dataset (crossing number ≤ 13), as required by US1",
        "dataset enumeration requirement.",
        "",
        "### Summary",
        "",
        f"- **Total Prime Knots**: {total_knots}",
        f"- **Crossing Number Range**: {min(counts.keys())} - {max(counts.keys())}",
        "",
        "### Counts by Crossing Number",
        "",
        "| Crossing Number | Prime Knot Count |",
        "|-----------------|------------------|",
    ]

    for crossing_num, count in counts.items():
        lines.append(f"| {crossing_num} | {count} |")

    lines.extend([
        "",
        "### Data Source",
        "",
        "- **Source**: Knot Atlas (https://katlas.org)",
        "- **Data File**: `data/processed/knots_cleaned.csv`",
        "- **Generation Date**: Generated by `code/analysis/dataset_counts.py`",
        "",
        "### Methodology",
        "",
        "1. Load cleaned knot data from `data/processed/knots_cleaned.csv`",
        "2. Extract crossing number from each knot record",
        "3. Count occurrences per crossing number",
        "4. Generate summary statistics and tabulation",
        "",
        "### Notes",
        "",
        "- This dataset includes all prime knots with crossing number ≤ 13",
        "- Counts reflect the Knot Atlas tabulation as of download date",
        "- For comparison with OEIS A002863, see validation documentation",
        "",
        "---",
        "",
        "*Report generated for reproducibility documentation (T017)*",
    ])

    return "\n".join(lines)


def main(
    cleaned_data_path: Optional[Path] = None,
    report_output_path: Optional[Path] = None
) -> Dict[str, Any]:
    """Main entry point for generating dataset counts report.

    Args:
        cleaned_data_path: Path to cleaned knots CSV (default: data/processed/knots_cleaned.csv)
        report_output_path: Path for output report (default: docs/reproducibility/dataset_counts.md)

    Returns:
        Dictionary with generation results and metadata.
    """
    # Set default paths relative to project root
    project_root = Path(__file__).resolve().parent.parent.parent
    if cleaned_data_path is None:
        cleaned_data_path = project_root / "data" / "processed" / "knots_cleaned.csv"
    if report_output_path is None:
        report_output_path = project_root / "docs" / "reproducibility" / "dataset_counts.md"

    # Ensure output directory exists
    report_output_path.parent.mkdir(parents=True, exist_ok=True)

    # Log operation start
    logger = get_logger()
    log_operation(
        logger=logger,
        operation="generate_dataset_counts_report",
        input_file=str(cleaned_data_path),
        output_file=str(report_output_path),
        parameters={"crossing_number_range": "≤13"},
    )

    # Load cleaned data
    knots = load_cleaned_knots(cleaned_data_path)

    # Count knots per crossing number
    counts = count_knots_per_crossing_number(knots)
    total_knots = sum(counts.values())

    # Generate report content
    report_content = generate_dataset_counts_report(counts, total_knots)

    # Write report to file
    with open(report_output_path, 'w', encoding='utf-8') as f:
        f.write(report_content)

    # Record artifact hash for reproducibility
    record_artifact_hash(
        artifact_path=str(report_output_path),
        artifact_type="dataset_counts_report",
        description="Dataset size report with prime knot counts per crossing number"
    )

    # Log operation completion
    log_operation(
        logger=logger,
        operation="generate_dataset_counts_report_complete",
        input_file=str(cleaned_data_path),
        output_file=str(report_output_path),
        parameters={
            "crossing_number_range": "≤13",
            "total_knots": total_knots,
            "crossing_numbers_covered": list(counts.keys())
        },
        status="success"
    )

    return {
        "success": True,
        "total_knots": total_knots,
        "counts_per_crossing_number": counts,
        "report_path": str(report_output_path),
    }


if __name__ == "__main__":
    result = main()
    print(f"Dataset counts report generated successfully.")
    print(f"Total prime knots: {result['total_knots']}")
    print(f"Report saved to: {result['report_path']}")
