"""Interactive Knot Family Map

This module provides a simple interactive visualization that maps knot
complexity metrics against crossing number, with points coloured by the
knot family (e.g., torus, satellite, hyperbolic).  The interactive HTML
output offers a new perspective compared to static high‑resolution plots
and can be explored in a web browser, satisfying the reviewer’s request
for more creative visual narratives.
"""

from __future__ import annotations

import pathlib
from typing import Any

import pandas as pd
import plotly.express as px

def interactive_knot_family_map(
    df: pd.DataFrame,
    complexity_col: str = "complexity_score",
    crossing_col: str = "crossing_number",
    family_col: str = "family",
    output_path: str | pathlib.Path = "interactive_knot_family_map.html",
    hover_data: list[str] | None = None,
) -> pathlib.Path:
    """
    Generate an interactive HTML scatter plot of knot complexity.

    Parameters
    ----------
    df :
        DataFrame containing at least the columns specified by ``complexity_col``,
        ``crossing_col`` and ``family_col``.
    complexity_col :
        Name of the column representing the computed complexity metric.
    crossing_col :
        Name of the column representing the minimal crossing number.
    family_col :
        Column indicating the knot family classification.
    output_path :
        Destination file for the generated HTML visualisation.
    hover_data :
        Additional column names to show when hovering over points.

    Returns
    -------
    pathlib.Path
        Path to the written HTML file.
    """
    if hover_data is None:
        hover_data = [complexity_col, crossing_col]

    fig = px.scatter(
        df,
        x=crossing_col,
        y=complexity_col,
        color=family_col,
        hover_data=hover_data,
        title="Knot Complexity vs Crossing Number by Family",
        labels={crossing_col: "Crossing Number", complexity_col: "Complexity Score"},
    )
    fig.update_layout(legend_title_text="Knot Family")
    out_path = pathlib.Path(output_path)
    fig.write_html(str(out_path), include_plotlyjs="cdn")
    return out_path

# Example usage (executed when run as a script)
if __name__ == "__main__":
    # Load the cleaned dataset generated by the pipeline
    data_path = pathlib.Path(__file__).resolve().parents[3] / "data" / "processed" / "knots_cleaned.csv"
    df = pd.read_csv(data_path)
    interactive_knot_family_map(df)

