"""
SQLite database schema definitions for the experiment tracking system.

Implements tables for Participant, Session, Problem, Submission, and Metric
entities as defined in data-model.md and code/data/models.py.
"""
import sqlite3
import os
from typing import List, Tuple, Optional
from datetime import datetime
import uuid
import hashlib

# Ensure the data directory exists
DATA_DIR = "data"
DB_PATH = os.path.join(DATA_DIR, "experiment.db")

def get_connection() -> sqlite3.Connection:
    """Create and return a database connection, ensuring the DB file exists."""
    if not os.path.exists(DATA_DIR):
        os.makedirs(DATA_DIR)
    
    conn = sqlite3.connect(DB_PATH)
    conn.row_factory = sqlite3.Row
    return conn

def init_schema(conn: Optional[sqlite3.Connection] = None) -> None:
    """
    Initialize the database schema by creating all required tables.
    If no connection is provided, a new one is created and closed.
    """
    if conn is None:
        conn = get_connection()
        should_close = True
    else:
        should_close = False

    try:
        cursor = conn.cursor()
        
        # Participant table
        # Maps to code/data/models.py: Participant
        cursor.execute("""
        CREATE TABLE IF NOT EXISTS participant (
            id TEXT PRIMARY KEY,
            anonymized_id TEXT NOT NULL UNIQUE,
            consent_timestamp TEXT NOT NULL,
            experience_years INTEGER NOT NULL,
            condition_order TEXT NOT NULL, -- 'AB' or 'BA'
            created_at TEXT NOT NULL,
            updated_at TEXT NOT NULL
        );
        """)

        # Session table
        # Maps to code/data/models.py: Session
        cursor.execute("""
        CREATE TABLE IF NOT EXISTS session (
            id TEXT PRIMARY KEY,
            participant_id TEXT NOT NULL,
            condition TEXT NOT NULL, -- 'LLM_ASSISTED' or 'BASELINE'
            start_time TEXT NOT NULL,
            end_time TEXT,
            seed INTEGER NOT NULL,
            FOREIGN KEY (participant_id) REFERENCES participant(id) ON DELETE CASCADE
        );
        """)

        # Problem table
        # Maps to code/data/models.py: Problem
        cursor.execute("""
        CREATE TABLE IF NOT EXISTS problem (
            id TEXT PRIMARY KEY,
            source TEXT NOT NULL, -- 'HUMANEVAL' or 'CODEFORCES'
            problem_id TEXT NOT NULL, -- Original ID from dataset
            language TEXT NOT NULL, -- 'python' or 'java'
            difficulty TEXT NOT NULL,
            prompt TEXT NOT NULL,
            canonical_solution TEXT,
            test_suite TEXT,
            created_at TEXT NOT NULL,
            UNIQUE(source, problem_id)
        );
        """)

        # Submission table
        # Maps to code/data/models.py: Submission
        cursor.execute("""
        CREATE TABLE IF NOT EXISTS submission (
            id TEXT PRIMARY KEY,
            session_id TEXT NOT NULL,
            problem_id TEXT NOT NULL,
            code_content TEXT NOT NULL,
            submission_timestamp TEXT NOT NULL,
            model_response_id TEXT, -- Optional: if generated by LLM
            is_valid_syntax INTEGER NOT NULL DEFAULT 1, -- 1 = True, 0 = False
            error_message TEXT,
            FOREIGN KEY (session_id) REFERENCES session(id) ON DELETE CASCADE,
            FOREIGN KEY (problem_id) REFERENCES problem(id) ON DELETE CASCADE
        );
        """)

        # Metric table
        # Maps to code/data/models.py: Metric
        cursor.execute("""
        CREATE TABLE IF NOT EXISTS metric (
            id TEXT PRIMARY KEY,
            submission_id TEXT NOT NULL UNIQUE,
            pass_rate REAL,
            cyclomatic_complexity INTEGER,
            test_coverage REAL,
            static_analysis_warnings INTEGER,
            execution_time_seconds REAL,
            computed_at TEXT NOT NULL,
            FOREIGN KEY (submission_id) REFERENCES submission(id) ON DELETE CASCADE
        );
        """)

        # Create indexes for performance
        cursor.execute("CREATE INDEX IF NOT EXISTS idx_session_participant ON session(participant_id);")
        cursor.execute("CREATE INDEX IF NOT EXISTS idx_submission_session ON submission(session_id);")
        cursor.execute("CREATE INDEX IF NOT EXISTS idx_submission_problem ON submission(problem_id);")
        cursor.execute("CREATE INDEX IF NOT EXISTS idx_metric_submission ON metric(submission_id);")

        conn.commit()
    finally:
        if should_close:
            conn.close()

def verify_schema() -> Tuple[bool, List[str]]:
    """
    Verify that all expected tables exist in the database.
    Returns (is_valid, missing_tables).
    """
    conn = get_connection()
    try:
        cursor = conn.cursor()
        cursor.execute("SELECT name FROM sqlite_master WHERE type='table';")
        existing_tables = {row[0] for row in cursor.fetchall()}
        
        expected_tables = {'participant', 'session', 'problem', 'submission', 'metric'}
        missing = list(expected_tables - existing_tables)
        
        return len(missing) == 0, missing
    finally:
        conn.close()

if __name__ == "__main__":
    # Initialize the database if run directly
    conn = get_connection()
    init_schema(conn)
    conn.close()
    
    is_valid, missing = verify_schema()
    if is_valid:
        print("Database schema initialized successfully.")
    else:
        print(f"Schema verification failed. Missing tables: {missing}")
        exit(1)