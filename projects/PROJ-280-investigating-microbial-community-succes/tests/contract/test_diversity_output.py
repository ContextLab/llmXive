"""
Contract test for diversity output schema (US2).

Validates that the diversity metrics output generated by code/03_diversity.py
conforms to the schema defined in contracts/output-metrics.schema.yaml.
"""

import json
import os
import sys
from pathlib import Path
from typing import Any, Dict, List

import jsonschema
import pytest

# Add project root to path for imports if running from tests/
project_root = Path(__file__).parent.parent.parent
contracts_dir = project_root / "contracts"

SCHEMA_PATH = contracts_dir / "output-metrics.schema.yaml"
EXPECTED_OUTPUT_PATH = project_root / "data" / "processed" / "diversity_metrics.json"

def load_schema(schema_path: Path) -> Dict[str, Any]:
    """Load JSON Schema from a YAML file."""
    import yaml
    with open(schema_path, "r") as f:
        return yaml.safe_load(f)

def load_output(output_path: Path) -> Dict[str, Any]:
    """Load the generated diversity metrics output."""
    if not output_path.exists():
        pytest.fail(f"Output file not found: {output_path}. "
                    "Run code/03_diversity.py first to generate the artifact.")
    with open(output_path, "r") as f:
        return json.load(f)

def test_schema_exists():
    """Verify the schema file exists."""
    assert SCHEMA_PATH.exists(), f"Schema file not found: {SCHEMA_PATH}"

def test_output_exists():
    """Verify the output file exists (generated by implementation)."""
    assert EXPECTED_OUTPUT_PATH.exists(), (
        f"Output file {EXPECTED_OUTPUT_PATH} not found. "
        "Ensure code/03_diversity.py has been executed."
    )

def test_schema_validation():
    """
    Validate the diversity output against the contract schema.
    
    This test ensures that the output structure, required fields,
    and data types match the specification.
    """
    schema = load_schema(SCHEMA_PATH)
    output = load_output(EXPECTED_OUTPUT_PATH)

    try:
        jsonschema.validate(instance=output, schema=schema)
    except jsonschema.exceptions.ValidationError as e:
        pytest.fail(f"Output validation failed: {e.message}. "
                    f"Path: {' -> '.join(str(p) for p in e.path)}")

def test_required_top_level_keys():
    """Check for required top-level keys in the output."""
    output = load_output(EXPECTED_OUTPUT_PATH)
    required_keys = ["alpha_diversity", "beta_diversity", "permanova_results"]
    
    missing = [k for k in required_keys if k not in output]
    assert not missing, f"Missing required top-level keys: {missing}"

def test_alpha_diversity_structure():
    """Validate the structure of alpha diversity metrics."""
    output = load_output(EXPECTED_OUTPUT_PATH)
    alpha = output.get("alpha_diversity", {})
    
    assert "shannon" in alpha, "Missing 'shannon' in alpha_diversity"
    assert "simpson" in alpha, "Missing 'simpson' in alpha_diversity"
    
    # Check that these are dictionaries mapping sample_id to value
    for metric in ["shannon", "simpson"]:
        data = alpha[metric]
        assert isinstance(data, dict), f"alpha_diversity.{metric} must be a dict"
        for sample_id, value in data.items():
            assert isinstance(sample_id, str), f"Sample ID must be string, got {type(sample_id)}"
            assert isinstance(value, (int, float)), f"Value for {sample_id} must be numeric"

def test_beta_diversity_structure():
    """Validate the structure of beta diversity metrics."""
    output = load_output(EXPECTED_OUTPUT_PATH)
    beta = output.get("beta_diversity", {})
    
    assert "bray_curtis" in beta, "Missing 'bray_curtis' in beta_diversity"
    # Typically a distance matrix or a list of distances
    bray_curtis = beta["bray_curtis"]
    assert isinstance(bray_curtis, (dict, list)), "bray_curtis must be a dict or list"

def test_permanova_results_structure():
    """Validate the structure of PERMANOVA results."""
    output = load_output(EXPECTED_OUTPUT_PATH)
    permanova = output.get("permanova_results", {})
    
    required_permanova_keys = ["r_squared", "p_value", "f_statistic", "n_permutations"]
    missing = [k for k in required_permanova_keys if k not in permanova]
    assert not missing, f"Missing required PERMANOVA keys: {missing}"
    
    # Validate types
    assert isinstance(permanova["r_squared"], (int, float)), "R² must be numeric"
    assert isinstance(permanova["p_value"], (int, float)), "p-value must be numeric"

def test_fdr_correction_present():
    """Verify that FDR corrected p-values are present if pairwise tests were run."""
    output = load_output(EXPECTED_OUTPUT_PATH)
    # The schema should dictate if this is required, but we check for the key
    # if the schema allows optional pairwise results
    if "pairwise_permanova" in output:
        pairwise = output["pairwise_permanova"]
        for comparison in pairwise:
            assert "fdr_corrected_p" in comparison, "FDR corrected p-value missing in pairwise results"