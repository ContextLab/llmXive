"""
T019: Calculate Alpha and Beta Diversity Metrics.

Implements User Story 2 (US2) requirements for diversity analysis.
Reads processed feature tables and metadata from data/processed/,
calculates Shannon, Simpson (alpha), and Bray-Curtis (beta) diversity,
and saves results to data/processed/.
"""
import json
import logging
import os
import sys
from pathlib import Path
from typing import Dict, Any, List, Optional

import numpy as np
import pandas as pd
import skbio
from skbio.diversity import alpha_diversity, beta_diversity
from skbio.stats.distance import permanova
from scipy.spatial.distance import pdist, squareform

# Project root relative to this file
PROJECT_ROOT = Path(__file__).resolve().parents[1]
PROCESSED_DIR = PROJECT_ROOT / "data" / "processed"

# Setup logging
logging.basicConfig(
    level=logging.INFO,
    format="%(asctime)s - %(levelname)s - %(message)s",
    handlers=[
        logging.FileHandler(PROCESSED_DIR / "diversity_analysis.log"),
        logging.StreamHandler(sys.stdout)
    ]
)
logger = logging.getLogger(__name__)

def load_processed_data() -> tuple[pd.DataFrame, pd.DataFrame]:
    """
    Load the preprocessed feature table and sample metadata.
    Expects files generated by T013/T014 in data/processed/.
    """
    feature_table_path = PROCESSED_DIR / "filtered_feature_table.csv"
    metadata_path = PROCESSED_DIR / "sample_metadata.csv"

    if not feature_table_path.exists():
        logger.error(f"Feature table not found: {feature_table_path}")
        raise FileNotFoundError(f"Missing feature table: {feature_table_path}")
    
    if not metadata_path.exists():
        logger.error(f"Metadata not found: {metadata_path}")
        raise FileNotFoundError(f"Missing metadata: {metadata_path}")

    # Load feature table (OTUs as rows, Samples as columns)
    # T013/T014 output format: Rows=OTUs, Cols=Samples
    feature_table = pd.read_csv(feature_table_path, index_col=0)
    
    # Load metadata
    metadata = pd.read_csv(metadata_path, index_col=0)

    # Ensure alignment
    common_samples = feature_table.columns.intersection(metadata.index)
    if len(common_samples) == 0:
        raise ValueError("No common samples between feature table and metadata.")
    
    logger.info(f"Aligning data: {len(common_samples)} samples found.")
    
    # Filter to common samples (columns for feature table, index for metadata)
    feature_table = feature_table[common_samples]
    metadata = metadata.loc[common_samples]

    return feature_table, metadata

def calculate_alpha_metrics(feature_table: pd.DataFrame) -> pd.DataFrame:
    """
    Calculate Shannon and Simpson alpha diversity indices.
    
    Args:
        feature_table: DataFrame with OTUs as rows, Samples as columns.
        
    Returns:
        DataFrame with samples as index and diversity metrics as columns.
    """
    logger.info("Calculating Alpha Diversity (Shannon, Simpson)...")
    
    # Convert to skbio OrdinationTable or just use numpy array
    # skbio expects samples as rows, OTUs as columns for alpha_diversity
    # Our table is OTUs (rows) x Samples (cols) -> Transpose
    table_matrix = feature_table.T.values.astype(float)
    sample_ids = feature_table.columns.tolist()
    
    # Calculate Shannon
    shannon = alpha_diversity('shannon', table_matrix, ids=sample_ids)
    
    # Calculate Simpson
    simpson = alpha_diversity('simpson', table_matrix, ids=sample_ids)
    
    alpha_df = pd.DataFrame({
        'sample_id': sample_ids,
        'shannon': shannon,
        'simpson': simpson
    }).set_index('sample_id')
    
    logger.info(f"Alpha diversity calculated for {len(alpha_df)} samples.")
    return alpha_df

def calculate_beta_metrics(feature_table: pd.DataFrame) -> pd.DataFrame:
    """
    Calculate Bray-Curtis beta diversity distance matrix.
    
    Args:
        feature_table: DataFrame with OTUs as rows, Samples as columns.
        
    Returns:
        DataFrame representing the distance matrix (samples x samples).
    """
    logger.info("Calculating Beta Diversity (Bray-Curtis)...")
    
    # Transpose to Samples x OTUs
    table_matrix = feature_table.T.values.astype(float)
    sample_ids = feature_table.columns.tolist()
    
    # Calculate Bray-Curtis distance
    # skbio beta_diversity returns a DistanceMatrix object
    # We can also use scipy if we want a raw matrix, but skbio is consistent
    dist_matrix = beta_diversity('braycurtis', table_matrix, ids=sample_ids)
    
    # Convert to pandas DataFrame for easier handling
    beta_df = pd.DataFrame(
        dist_matrix.data.reshape(dist_matrix.shape),
        index=dist_matrix.ids,
        columns=dist_matrix.ids
    )
    
    logger.info(f"Beta diversity matrix calculated: {beta_df.shape}.")
    return beta_df

def save_results(alpha_df: pd.DataFrame, beta_df: pd.DataFrame) -> Dict[str, str]:
    """
    Save diversity results to JSON/CSV files.
    
    Returns:
        Dictionary mapping metric name to file path.
    """
    logger.info("Saving diversity results...")
    
    output_files = {}
    
    # Save Alpha Diversity
    alpha_path = PROCESSED_DIR / "alpha_diversity.csv"
    alpha_df.to_csv(alpha_path)
    output_files['alpha_diversity'] = str(alpha_path)
    logger.info(f"Saved alpha diversity to {alpha_path}")
    
    # Save Beta Diversity (Flattened or full matrix? Full matrix is heavy, saving as CSV)
    beta_path = PROCESSED_DIR / "beta_diversity_bray_curtis.csv"
    beta_df.to_csv(beta_path)
    output_files['beta_diversity'] = str(beta_path)
    logger.info(f"Saved beta diversity to {beta_path}")
    
    # Create a summary JSON
    summary = {
        "num_samples": len(alpha_df),
        "metrics_calculated": ["shannon", "simpson", "bray_curtis"],
        "files": output_files
    }
    
    summary_path = PROCESSED_DIR / "diversity_metrics.json"
    with open(summary_path, 'w') as f:
        json.dump(summary, f, indent=2)
    logger.info(f"Saved summary to {summary_path}")
    
    return output_files

def main():
    """Main entry point for T019."""
    logger.info("Starting T019: Diversity Analysis...")
    
    try:
        # 1. Load Data
        feature_table, metadata = load_processed_data()
        
        # 2. Calculate Alpha Diversity
        alpha_metrics = calculate_alpha_metrics(feature_table)
        
        # 3. Calculate Beta Diversity
        beta_metrics = calculate_beta_metrics(feature_table)
        
        # 4. Save Results
        output_files = save_results(alpha_metrics, beta_metrics)
        
        logger.info("T019 completed successfully.")
        return 0
        
    except Exception as e:
        logger.critical(f"T019 failed with error: {e}", exc_info=True)
        return 1

if __name__ == "__main__":
    sys.exit(main())
