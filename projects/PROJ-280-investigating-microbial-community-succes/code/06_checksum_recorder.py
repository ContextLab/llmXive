"""
T016: Implement checksum recording for data/processed/ files.

This script calculates SHA-256 checksums for all processed artifacts
generated by the sensitivity sweep and exclusion logging, then updates
the project state file with these hashes.
"""
import json
import logging
import os
import sys
from pathlib import Path
from typing import List, Dict, Any

# Import the state tracker utilities
from state_tracker import update_multiple_artifacts, load_state, save_state, ensure_state_file

# Import the checksum utility from utils
from utils import generate_checksum

# Configure logging
logging.basicConfig(
    level=logging.INFO,
    format='%(asctime)s - %(levelname)s - %(message)s',
    handlers=[
        logging.StreamHandler(sys.stdout)
    ]
)
logger = logging.getLogger(__name__)

# Define the expected processed files for this task
EXPECTED_PROCESSED_FILES = [
    "low_depth_results.json",
    "medium_depth_results.json",
    "high_depth_results.json",
    "sensitivity_sweep_results.json",
    "exclusion_log.json"
]

def record_checksums(
    processed_dir: Path,
    state_file_path: Path,
    project_id: str = "PROJ-280-investigating-microbial-community-succes"
) -> Dict[str, Any]:
    """
    Calculate checksums for expected processed files and update the state file.

    Args:
        processed_dir: Path to the data/processed/ directory.
        state_file_path: Path to the state YAML file.
        project_id: The project ID for the state file.

    Returns:
        A dictionary containing the results of the checksum operation.
    """
    logger.info(f"Starting checksum recording for project: {project_id}")
    logger.info(f"Target processed directory: {processed_dir}")
    logger.info(f"Target state file: {state_file_path}")

    if not processed_dir.exists():
        logger.error(f"Processed directory does not exist: {processed_dir}")
        raise FileNotFoundError(f"Processed directory not found: {processed_dir}")

    if not state_file_path.parent.exists():
        logger.warning(f"State directory does not exist. Creating: {state_file_path.parent}")
        state_file_path.parent.mkdir(parents=True, exist_ok=True)

    # Ensure the state file exists
    ensure_state_file(state_file_path, project_id)

    artifacts_to_hash = []
    missing_files = []

    for filename in EXPECTED_PROCESSED_FILES:
        file_path = processed_dir / filename
        if file_path.exists():
            logger.info(f"Found file: {filename}")
            artifacts_to_hash.append({
                "name": filename,
                "path": str(file_path)
            })
        else:
            logger.warning(f"File not found, skipping: {filename}")
            missing_files.append(filename)

    if not artifacts_to_hash:
        logger.warning("No artifacts found to checksum. Skipping update.")
        return {
            "status": "skipped",
            "reason": "No expected files found in processed directory",
            "missing_files": missing_files
        }

    # Calculate hashes and prepare update payload
    hash_updates = {}
    for artifact in artifacts_to_hash:
        file_path = Path(artifact["path"])
        try:
            checksum = generate_checksum(file_path)
            hash_updates[artifact["name"]] = {
                "hash": checksum,
                "size_bytes": file_path.stat().st_size,
                "status": "verified"
            }
            logger.info(f"Calculated checksum for {artifact['name']}: {checksum[:16]}...")
        except Exception as e:
            logger.error(f"Failed to calculate checksum for {artifact['name']}: {e}")
            hash_updates[artifact["name"]] = {
                "status": "error",
                "error": str(e)
            }

    # Update the state file
    try:
        load_state(state_file_path)
        update_multiple_artifacts(
            state_file_path=state_file_path,
            project_id=project_id,
            artifacts=hash_updates
        )
        logger.info(f"Successfully updated state file: {state_file_path}")
        return {
            "status": "success",
            "processed_count": len(artifacts_to_hash),
            "updated_artifacts": list(hash_updates.keys()),
            "missing_files": missing_files
        }
    except Exception as e:
        logger.error(f"Failed to update state file: {e}")
        raise

def main():
    """Main entry point for the checksum recorder."""
    # Determine project root
    # Assuming the script is run from the project root or code/ directory
    current_dir = Path.cwd()
    if current_dir.name == "code":
        project_root = current_dir.parent
    else:
        project_root = current_dir

    processed_dir = project_root / "data" / "processed"
    state_dir = project_root / "state" / "projects"
    state_file = state_dir / "PROJ-280-investigating-microbial-community-succes.yaml"

    logger.info(f"Project root detected at: {project_root}")

    try:
        result = record_checksums(processed_dir, state_file)
        logger.info(f"Checksum recording completed: {result['status']}")
        if result.get("missing_files"):
            logger.warning(f"Missing files: {result['missing_files']}")
        return 0
    except Exception as e:
        logger.critical(f"Checksum recording failed: {e}")
        return 1

if __name__ == "__main__":
    sys.exit(main())
