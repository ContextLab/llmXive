import os
from pathlib import Path

def main():
    """
    Create the project directory structure for PROJ-280.
    This script ensures all required folders exist as per the implementation plan.
    """
    project_root = Path("projects/PROJ-280-investigating-microbial-community-succes")
    
    # Define required directories relative to the project root
    required_dirs = [
        "data/raw",
        "data/processed",
        "data/config",
        "code",
        "tests/unit",
        "tests/contract",
        "tests/integration",
        "state/projects",
        "contracts"
    ]
    
    created_count = 0
    for dir_path in required_dirs:
        full_path = project_root / dir_path
        if not full_path.exists():
            full_path.mkdir(parents=True, exist_ok=True)
            print(f"Created directory: {full_path}")
            created_count += 1
        else:
            print(f"Directory already exists: {full_path}")
    
    # Create the MANIFEST.txt to verify structure
    manifest_path = project_root / "MANIFEST.txt"
    with open(manifest_path, "w") as f:
        f.write("# Project Structure Manifest for PROJ-280\n")
        f.write("# Generated by setup_project.py\n\n")
        for dir_path in required_dirs:
            f.write(f"{dir_path}/\n")
        f.write("\n# End of manifest\n")
    
    print(f"\nProject structure initialized at {project_root}")
    print(f"Created {created_count} new directories.")
    print(f"Manifest written to {manifest_path}")

if __name__ == "__main__":
    main()
