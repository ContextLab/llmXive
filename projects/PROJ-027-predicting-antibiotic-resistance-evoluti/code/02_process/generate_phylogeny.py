"""
Generate Phylogenetic Tree from SNP Data.

This script infers a phylogenetic tree (Newick format) from the SNP data
generated by T016 (build_feature_matrix.py) or the raw SNP matrix output.
It uses the `dendropy` library to perform distance-based tree inference
(Neighbor-Joining) which is computationally efficient for large genomic datasets.

Input:
    data/processed/snp_matrix.csv (or similar SNP count matrix)
    Expected columns: isolate_id, followed by numeric SNP columns.
    If the file from T016 (feature_matrix.csv) is used, it extracts
    the 'snp_counts' column or parses the raw SNP matrix if available.

Output:
    data/processed/phylogeny_tree.newick

Dependencies:
    - dendropy
    - pandas
    - numpy
"""

