def load_snp_matrix(file_path: Path) -> pd.DataFrame:
    """
    Load SNP matrix from CSV.
    Expects a format where rows are isolates and columns are SNP sites/counts.
    If the input is the feature_matrix.csv from T016, we look for specific columns.
    """
    logger = get_logger("generate_phylogeny")
    
    if not file_path.exists():
        raise FileNotFoundError(f"SNP matrix file not found: {file_path}")

    df = pd.read_csv(file_path)

    # Heuristic: If 'snp_counts' column exists and is a list/string, try to parse
    # However, standard practice for phylogeny is a matrix of 0/1 or counts per site.
    # The task T016 description mentions "Numeric SNP counts".
    # For phylogeny, we need a distance matrix.
    # If the file contains a 'snp_counts' column that is a serialized list, we expand it.
    # If it contains raw SNP site columns (e.g., snp_1, snp_2...), we use those.

    # Check for raw SNP columns (common in Snippy/Phylo pipelines)
    # If the file has many numeric columns that aren't gene_presence or cnv_counts
    potential_snp_cols = []
    for col in df.columns:
        if col.startswith('snp_') or col.isdigit():
            potential_snp_cols.append(col)
    
    # Fallback: If 'snp_counts' is a column, assume it holds the vector or aggregate
    if 'snp_counts' in df.columns:
        # Check if it's a list-like string or a single number
        sample_val = df['snp_counts'].iloc[0]
        if isinstance(sample_val, str) and '[' in sample_val:
            # It's a serialized list, need to expand
            try:
                import ast
                expanded_data = []
                for val in df['snp_counts']:
                    expanded_data.append(ast.literal_eval(val) if isinstance(val, str) else val)
                # Create a new dataframe with expanded columns
                max_len = max(len(row) for row in expanded_data)
                expanded_df = pd.DataFrame(expanded_data, index=df.index)
                expanded_df.columns = [f'snp_{i}' for i in range(max_len)]
                expanded_df['isolate_id'] = df['isolate_id']
                return expanded_df
            except Exception as e:
                logger.warning(f"Failed to parse 'snp_counts' as list: {e}. Attempting raw columns.")
        else:
            logger.warning("snp_counts column is not a list. Cannot infer tree from single count.")
            # If it's just a total count, we can't build a tree.
            # We must rely on the existence of a raw SNP matrix or a different input.
            # For this implementation, we assume the input file is a matrix of SNPs.
            # If the user passed feature_matrix.csv, they should have provided the raw SNP matrix separately
            # or the task T016 should have output a 'snp_matrix.csv'.
            # Let's assume the input file is the raw SNP matrix if 'snp_counts' is not a list.
            pass

    # If we have potential SNP columns, use them
    if potential_snp_cols:
        logger.info(f"Using {len(potential_snp_cols)} SNP columns for phylogeny.")
        # Ensure isolate_id is present
        if 'isolate_id' not in df.columns:
            raise ValueError("Input CSV must contain 'isolate_id' column.")
        return df[['isolate_id'] + potential_snp_cols]

    # Fallback: Try to find any numeric columns that might be SNPs
    numeric_cols = df.select_dtypes(include=[np.number]).columns.tolist()
    if 'isolate_id' in numeric_cols:
        numeric_cols.remove('isolate_id')
    
    if numeric_cols:
        logger.info(f"Using {len(numeric_cols)} numeric columns as SNPs.")
        return df[['isolate_id'] + numeric_cols]

    raise ValueError("Could not identify SNP columns in the input file. "
                     "Expected columns starting with 'snp_' or a 'snp_counts' list.")

def compute_distance_matrix(snp_df: pd.DataFrame) -> np.ndarray:
    """
    Compute pairwise Hamming distance (number of differing SNPs) between isolates.
    """
    logger = get_logger("generate_phylogeny")
    logger.info("Computing pairwise SNP distance matrix...")

    # Extract isolate IDs and SNP matrix
    ids = snp_df['isolate_id'].values
    matrix = snp_df.drop(columns=['isolate_id']).to_numpy()

    # Handle missing values (if any) by treating as 0 or imputing? 
    # For SNPs, missing is often ambiguous. Let's fill with 0 for distance calc
    # or mask. Here we fill 0.
    matrix = np.nan_to_num(matrix, nan=0.0)

    # Calculate pairwise distances
    # Using Euclidean distance on binary data is equivalent to Hamming distance * sqrt(2)
    # But for SNPs, simple count of differences is best.
    # We can use scipy or numpy broadcasting.
    from scipy.spatial.distance import pdist, squareform
    
    dist_matrix = squareform(pdist(matrix, metric='hamming'))
    # Hamming returns fraction of mismatches. Convert to count.
    n_sites = matrix.shape[1]
    dist_matrix = dist_matrix * n_sites

    logger.info(f"Distance matrix computed: {dist_matrix.shape[0]}x{dist_matrix.shape[1]}")
    return dist_matrix, ids

def infer_tree(dist_matrix: np.ndarray, ids: np.ndarray) -> dendropy.DnaTaxonSet:
    """
    Infer a phylogenetic tree using Neighbor-Joining algorithm via DendroPy.
    """
    logger = get_logger("generate_phylogeny")
    logger.info("Inferring tree using Neighbor-Joining (DendroPy)...")

    # Create a DendroPy DistanceMatrix
    # DendroPy expects a specific format. We can construct it from the numpy array.
    
    taxon_namespace = dendropy.TaxonNamespace()
    # Add taxa
    taxon_map = {}
    for i, label in enumerate(ids):
        # Ensure label is valid for DendroPy (no special chars usually)
        clean_label = str(label).replace(' ', '_').replace('-', '_')
        taxon = taxon_namespace.get_taxon(clean_label)
        if taxon is None:
            taxon = taxon_namespace.new_taxon(clean_label)
        taxon_map[i] = taxon

    # Create DistanceMatrix
    dm = dendropy.DistanceMatrix(
        matrix=dist_matrix.tolist(),
        taxon_namespace=taxon_namespace,
        is_distance_matrix=True
    )
    dm.taxon_namespace = taxon_namespace

    # Perform Neighbor-Joining
    tree = dendropy.treeconstruction.nj(dm)
    
    # Ensure tree has a root (NJ produces unrooted, but we can root arbitrarily or by midpoint)
    # For downstream use, a rooted tree is often preferred. Midpoint rooting is safe.
    tree.reroot_at_midpoint()

    logger.info(f"Tree inferred with {len(tree.leaf_nodes())} leaves.")
    return tree

def save_tree(tree: dendropy.Tree, output_path: Path):
    """
    Save the tree to a Newick file.
    """
    logger = get_logger("generate_phylogeny")
    logger.info(f"Saving tree to {output_path}")
    
    with open(output_path, 'w') as f:
        f.write(tree.as_string('newick'))
    
    logger.info("Tree saved successfully.")

def main():
    logger = init_pipeline_logging("generate_phylogeny")
    logger.info("Starting phylogeny generation (T019)...")

    # Get paths from config
    paths = get_paths()
    processed_dir = paths.get('processed_dir', paths.get('data_dir', 'data/processed'))
    processed_path = Path(processed_dir)
    processed_path.mkdir(parents=True, exist_ok=True)

    # Determine input file
    # The task says "from the SNP data (output of T016)".
    # T016 outputs feature_matrix.csv. However, feature_matrix might not have the raw SNP matrix.
    # We assume T016 also outputs a raw SNP matrix or the feature_matrix contains the necessary columns.
    # If T016 only outputs feature_matrix.csv, we try to load that.
    input_file = processed_path / "feature_matrix.csv"
    if not input_file.exists():
        # Fallback to a hypothetical raw SNP matrix if feature_matrix is not suitable
        # But per spec, we must use the output of T016.
        # If T016 doesn't produce a usable matrix, we fail loudly.
        logger.error(f"Input file {input_file} not found. T016 must run first.")
        sys.exit(1)

    try:
        # 1. Load SNP data
        snp_df = load_snp_matrix(input_file)
        
        if snp_df.empty or len(snp_df) < 2:
            logger.error("Not enough isolates to build a tree (need >= 2).")
            sys.exit(1)

        # 2. Compute distance matrix
        dist_matrix, ids = compute_distance_matrix(snp_df)

        # 3. Infer tree
        tree = infer_tree(dist_matrix, ids)

        # 4. Save tree
        output_file = processed_path / "phylogeny_tree.newick"
        save_tree(tree, output_file)

        logger.info("Phylogeny generation completed successfully.")
        return 0

    except Exception as e:
        logger.error(f"Failed to generate phylogeny: {e}")
        import traceback
        traceback.print_exc()
        sys.exit(1)

if __name__ == "__main__":
    main()
