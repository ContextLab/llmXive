"""
Preprocessing module for Caco-2 permeability data.
Filters raw data for non-NULL SMILES and logPapp, reports pass rate, and registers checksums.
"""
import csv
import logging
import sys
from pathlib import Path
from typing import List, Dict, Any, Optional

from utils.logging import get_logger, configure_root_logger
from utils.config import get_project_root, get_data_path
from utils.checksum import register_checksum

logger = get_logger(__name__)

# Constants
RAW_DATA_PATH = "data/raw/chembl_caco2_raw.csv"
PROCESSED_DATA_PATH = "data/processed/caco2_filtered.csv"
STATE_PATH = "state/projects/PROJ-266-exploring-the-correlation-between-molecu.yaml"

def load_raw_data() -> List[Dict[str, Any]]:
    """
    Load raw Caco-2 data from the CSV file generated by T009.
    """
    project_root = get_project_root()
    input_path = project_root / RAW_DATA_PATH

    if not input_path.exists():
        raise FileNotFoundError(f"Raw data file not found at {input_path}. "
                                "Please run T009 (retrieval.py) first.")

    data = []
    with open(input_path, 'r', encoding='utf-8') as f:
        reader = csv.DictReader(f)
        for row in reader:
            data.append(row)

    logger.info(f"Loaded {len(data)} raw records from {input_path}")
    return data

def preprocess_data(raw_data: List[Dict[str, Any]]) -> List[Dict[str, Any]]:
    """
    Filter raw data for non-NULL SMILES and logPapp.
    Reports pass rate and excluded records due to protocol heterogeneity (missing values).
    """
    if not raw_data:
        logger.warning("Raw data is empty. No filtering performed.")
        return []

    filtered_data = []
    excluded_count = 0
    excluded_reasons = {
        "missing_smiles": 0,
        "missing_logpapp": 0,
        "invalid_logpapp": 0
    }

    for i, record in enumerate(raw_data):
        smiles = record.get('smiles', '').strip()
        logpapp_str = record.get('logPapp', '').strip()

        # Check for missing SMILES
        if not smiles:
            excluded_count += 1
            excluded_reasons["missing_smiles"] += 1
            continue

        # Check for missing logPapp
        if not logpapp_str:
            excluded_count += 1
            excluded_reasons["missing_logpapp"] += 1
            continue

        # Validate logPapp is a number
        try:
            float(logpapp_str)
        except ValueError:
            excluded_count += 1
            excluded_reasons["invalid_logpapp"] += 1
            continue

        # Record is valid
        filtered_data.append(record)

    total = len(raw_data)
    passed = len(filtered_data)
    pass_rate = (passed / total * 100) if total > 0 else 0.0

    logger.info(f"Preprocessing complete:")
    logger.info(f"  Total records: {total}")
    logger.info(f"  Excluded records: {excluded_count}")
    logger.info(f"    - Missing SMILES: {excluded_reasons['missing_smiles']}")
    logger.info(f"    - Missing logPapp: {excluded_reasons['missing_logpapp']}")
    logger.info(f"    - Invalid logPapp: {excluded_reasons['invalid_logpapp']}")
    logger.info(f"  Passed records: {passed} ({pass_rate:.2f}%)")

    if excluded_count > 0:
        logger.warning(f"Excluded {excluded_count} records due to protocol heterogeneity (missing/invalid data).")

    return filtered_data

def write_clean_data(data: List[Dict[str, Any]], output_path: Path) -> None:
    """
    Write filtered data to a CSV file.
    """
    if not data:
        logger.warning("No data to write.")
        output_path.parent.mkdir(parents=True, exist_ok=True)
        with open(output_path, 'w', newline='', encoding='utf-8') as f:
            f.write("")
        return

    # Ensure output directory exists
    output_path.parent.mkdir(parents=True, exist_ok=True)

    fieldnames = data[0].keys()

    with open(output_path, 'w', newline='', encoding='utf-8') as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(data)

    logger.info(f"Wrote {len(data)} clean records to {output_path}")

def main():
    """
    Main entry point for the preprocessing script.
    """
    configure_root_logger()
    logger.info("Starting data preprocessing (T010)...")

    try:
        # 1. Load raw data
        raw_data = load_raw_data()

        # 2. Preprocess (filter) data
        filtered_data = preprocess_data(raw_data)

        # 3. Write clean data
        project_root = get_project_root()
        output_path = project_root / PROCESSED_DATA_PATH
        write_clean_data(filtered_data, output_path)

        # 4. Register checksum
        checksum_utils = project_root / "code/utils/checksum.py"
        # We need to call the register_checksum function directly
        # The function signature is: register_checksum(file_path: Path, state_path: Path, description: str)
        register_checksum(output_path, project_root / STATE_PATH, "Filtered Caco-2 dataset for US1")

        logger.info("Preprocessing completed successfully.")

    except FileNotFoundError as e:
        logger.error(f"File error: {e}")
        sys.exit(1)
    except Exception as e:
        logger.error(f"Unexpected error during preprocessing: {e}", exc_info=True)
        sys.exit(1)

if __name__ == "__main__":
    main()