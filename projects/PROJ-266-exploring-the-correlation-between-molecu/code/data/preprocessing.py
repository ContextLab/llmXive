"""
Preprocessing script for Caco-2 Permeability Data (US1).

This script loads the raw dataset retrieved from ChEMBL (via T009),
filters for records with non-NULL SMILES and logPapp values, and
reports statistics on the pass rate and excluded records.

Output: data/processed/caco2_clean.csv
"""

import csv
import logging
import sys
from pathlib import Path
from typing import List, Dict, Any, Optional

# Import project utilities
# Ensure the parent directory is in sys.path for imports to resolve during execution
# (Handled by runner, but good practice for local execution)
if __name__ == "__main__":
    parent_dir = Path(__file__).resolve().parent.parent
    if str(parent_dir) not in sys.path:
        sys.path.insert(0, str(parent_dir))

from utils.logging import get_logger, configure_root_logger
from utils.config import get_project_root

# Constants
RAW_DATA_FILENAME = "caco2_raw.csv"
CLEAN_DATA_FILENAME = "caco2_clean.csv"
MIN_VALID_RECORDS = 500
MIN_RAW_BATCH = 600

logger = get_logger(__name__)

def load_raw_data(input_path: Path) -> List[Dict[str, Any]]:
    """
    Loads the raw CSV data generated by T009.
    """
    if not input_path.exists():
        raise FileNotFoundError(f"Raw data file not found: {input_path}. "
                                "Please ensure T009 (retrieval.py) has been executed successfully.")

    data = []
    with open(input_path, 'r', encoding='utf-8') as f:
        reader = csv.DictReader(f)
        for row in reader:
            data.append(row)

    logger.info(f"Loaded {len(data)} raw records from {input_path}")
    return data

def preprocess_data(raw_data: List[Dict[str, Any]]) -> tuple[List[Dict[str, Any]], Dict[str, int]]:
    """
    Filters raw data for non-NULL SMILES and logPapp.

    Returns:
        filtered_data: List of valid records
        stats: Dictionary containing counts of total, valid, and excluded records
    """
    valid_data = []
    excluded_smiles = 0
    excluded_logpapp = 0
    excluded_both = 0

    for record in raw_data:
        smiles = record.get("smiles", "").strip()
        logpapp = record.get("logPapp", "").strip()

        # Check for valid SMILES and logPapp
        # logPapp is typically stored as a string in the raw CSV, need to ensure it's not empty
        if not smiles or not logpapp:
            if not smiles and not logpapp:
                excluded_both += 1
            elif not smiles:
                excluded_smiles += 1
            else:
                excluded_logpapp += 1
            continue

        # Attempt to parse logPapp as float to ensure it's a valid number
        try:
            float(logpapp)
            valid_data.append(record)
        except ValueError:
            # If it's not a number, treat as invalid logPapp
            excluded_logpapp += 1

    stats = {
        "total_raw": len(raw_data),
        "valid": len(valid_data),
        "excluded_smiles_only": excluded_smiles,
        "excluded_logpapp_only": excluded_logpapp,
        "excluded_both": excluded_both,
        "total_excluded": excluded_smiles + excluded_logpapp + excluded_both
    }

    return valid_data, stats

def write_clean_data(data: List[Dict[str, Any]], output_path: Path) -> None:
    """
    Writes the filtered data to a CSV file.
    """
    if not data:
        logger.warning("No data to write. Output file will be empty.")
        # Create empty file with headers if possible, or just touch
        with open(output_path, 'w', encoding='utf-8', newline='') as f:
            # We need headers, but if data is empty, we might not have them.
            # Assuming the first row of raw data had headers, we can infer them or use a standard set.
            # For safety, if data is empty, we skip writing or write a note.
            pass
        return

    fieldnames = data[0].keys()
    with open(output_path, 'w', encoding='utf-8', newline='') as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(data)

    logger.info(f"Successfully wrote {len(data)} clean records to {output_path}")

def main() -> int:
    """
    Main entry point for the preprocessing task.
    """
    configure_root_logger()
    project_root = get_project_root()
    raw_path = project_root / "data" / "raw" / RAW_DATA_FILENAME
    clean_path = project_root / "data" / "processed" / CLEAN_DATA_FILENAME

    # Ensure output directory exists
    clean_path.parent.mkdir(parents=True, exist_ok=True)

    try:
        # 1. Load Raw Data
        logger.info(f"Starting preprocessing. Input: {raw_path}")
        raw_data = load_raw_data(raw_path)

        if len(raw_data) < MIN_RAW_BATCH:
            logger.warning(f"Raw data count ({len(raw_data)}) is below expected threshold ({MIN_RAW_BATCH}). "
                           "Proceeding with filtering, but results may be insufficient.")

        # 2. Preprocess (Filter)
        clean_data, stats = preprocess_data(raw_data)

        # 3. Report Statistics
        pass_rate = (stats["valid"] / stats["total_raw"] * 100) if stats["total_raw"] > 0 else 0
        logger.info("Preprocessing Statistics:")
        logger.info(f"  Total Raw Records: {stats['total_raw']}")
        logger.info(f"  Valid Records: {stats['valid']}")
        logger.info(f"  Pass Rate: {pass_rate:.2f}%")
        logger.info(f"  Excluded (Missing SMILES): {stats['excluded_smiles_only']}")
        logger.info(f"  Excluded (Missing logPapp): {stats['excluded_logpapp_only']}")
        logger.info(f"  Excluded (Missing Both): {stats['excluded_both']}")

        # 4. Validate Minimum Threshold
        if stats["valid"] < MIN_VALID_RECORDS:
            logger.error(f"Validation Failed: Only {stats['valid']} valid records found. "
                         f"Minimum required: {MIN_VALID_RECORDS}. "
                         "The dataset may be too small or the filtering criteria too strict.")
            # We still write the file, but log the failure status.
            # The execution stage will likely fail if this threshold is critical.

        # 5. Write Clean Data
        write_clean_data(clean_data, clean_path)

        logger.info("Preprocessing completed successfully.")
        return 0

    except FileNotFoundError as e:
        logger.error(f"File not found: {e}")
        return 1
    except Exception as e:
        logger.exception(f"An unexpected error occurred during preprocessing: {e}")
        return 1

if __name__ == "__main__":
    sys.exit(main())