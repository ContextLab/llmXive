def test_is_binary_image():
    # Test true binary
    arr = np.array([[0, 0], [1, 1]])
    assert _is_binary_image(arr)
    
    arr2 = np.array([[0, 0], [255, 255]])
    assert _is_binary_image(arr2)
    
    # Test false binary
    arr3 = np.array([[0, 0], [100, 200]])
    assert not _is_binary_image(arr3)

