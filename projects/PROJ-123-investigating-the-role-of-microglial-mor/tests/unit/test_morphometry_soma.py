def test_pixel_size_scaling():
    """
    Verify that changing pixel_size_um scales the results correctly.
    """
    # Create a simple shape: 10x10 pixels square
    img = np.zeros((20, 20), dtype=np.uint8)
    img[5:15, 5:15] = 255
    
    # Area should be 100 pixels
    # Length should be 0 if we consider only processes (no soma subtraction logic for a square blob)
    # Wait, the logic: largest component is soma. So process mask is empty.
    # So length should be 0. Area should be 100 * (pixel_size^2).
    
    res1 = calculate_soma_area_and_length(img, pixel_size_um=1.0)
    res2 = calculate_soma_area_and_length(img, pixel_size_um=2.0)
    
    # Area scales with square of pixel size
    # 100 * 1^2 = 100
    # 100 * 2^2 = 400
    assert abs(res1['soma_area_um2'] - 100.0) < 0.01
    assert abs(res2['soma_area_um2'] - 400.0) < 0.01
    
    # Length should be 0 in both cases as there are no processes
    assert res1['total_process_length_um'] == 0.0
    assert res2['total_process_length_um'] == 0.0
