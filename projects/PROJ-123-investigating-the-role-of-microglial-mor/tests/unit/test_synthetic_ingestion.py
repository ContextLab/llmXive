import pytest
import pandas as pd
import numpy as np
from pathlib import Path
import sys
import os

# Ensure code directory is in path
sys.path.insert(0, os.path.abspath(os.path.join(os.path.dirname(__file__), '..', '..')))

from code.data_ingestion import run_ingestion_pipeline, exclude_missing_cognitive_or_region
from code.config import get_project_root

class TestSyntheticIngestion:
    """
    Tests for T012b: Synthetic data ingestion logic.
    Verifies that the ingestion pipeline correctly loads and structures synthetic data
    generated by T007, without falling back to real data or failing silently.
    """

    def test_synthetic_ingestion_loads_data(self):
        """
        Verify that run_ingestion_pipeline(use_synthetic=True) loads data
        and returns a DataFrame with the expected columns.
        """
        df = run_ingestion_pipeline(use_synthetic=True)
        
        assert isinstance(df, pd.DataFrame), "Output must be a DataFrame"
        assert len(df) > 0, "Synthetic ingestion must return non-empty DataFrame"
        
        # Check required columns per T018 and T006a
        expected_cols = ['image_path', 'brain_region', 'pathology_status', 'cognitive_score', 'is_synthetic']
        for col in expected_cols:
            assert col in df.columns, f"Missing column: {col}"

    def test_synthetic_data_has_valid_brain_regions(self):
        """
        Verify that the 'brain_region' column contains only valid enum values.
        """
        df = run_ingestion_pipeline(use_synthetic=True)
        
        valid_regions = ['Hippocampus', 'Prefrontal Cortex']
        # Filter out any potential NaNs if they exist (though synthetic should be valid)
        valid_mask = df['brain_region'].notna()
        regions = df.loc[valid_mask, 'brain_region'].unique()
        
        for region in regions:
            assert region in valid_regions, f"Invalid brain region found: {region}"

    def test_synthetic_data_has_valid_pathology_status(self):
        """
        Verify that the 'pathology_status' column contains only valid enum values.
        """
        df = run_ingestion_pipeline(use_synthetic=True)
        
        valid_statuses = ['Normal', 'Early AD']
        valid_mask = df['pathology_status'].notna()
        statuses = df.loc[valid_mask, 'pathology_status'].unique()
        
        for status in statuses:
            assert status in valid_statuses, f"Invalid pathology status found: {status}"

    def test_synthetic_flag_is_set(self):
        """
        Verify that the 'is_synthetic' column is True for all rows in synthetic mode.
        """
        df = run_ingestion_pipeline(use_synthetic=True)
        assert all(df['is_synthetic'] == True), "All rows in synthetic mode must have is_synthetic=True"

    def test_exclusion_filter_works_on_synthetic(self):
        """
        Verify that exclude_missing_cognitive_or_region correctly filters the synthetic data.
        """
        df_raw = run_ingestion_pipeline(use_synthetic=True)
        # Add a row with invalid region to test filtering
        invalid_row = pd.DataFrame([{
            'image_path': 'fake_invalid.tif',
            'brain_region': 'InvalidRegion',
            'pathology_status': 'Normal',
            'cognitive_score': 100.0,
            'is_synthetic': True
        }])
        df_mixed = pd.concat([df_raw, invalid_row], ignore_index=True)
        
        df_filtered = exclude_missing_cognitive_or_region(df_mixed)
        
        # Should have removed the invalid row
        assert len(df_filtered) == len(df_raw), "Invalid rows should be removed"
        assert not df_filtered['brain_region'].isin(['InvalidRegion']).any(), "Invalid region should not be present"

    def test_synthetic_output_path_created(self):
        """
        Verify that the ingestion pipeline writes the CSV to the expected location.
        """
        df = run_ingestion_pipeline(use_synthetic=True)
        # Check if the default output file exists
        project_root = get_project_root()
        output_path = Path(project_root) / "data" / "processed" / "ingestion_log.csv"
        
        # Note: The function write to disk in ingest_directory. 
        # If the directory doesn't exist, it creates it.
        assert output_path.exists(), f"Output CSV not created at {output_path}"
        
        # Verify content matches
        df_saved = pd.read_csv(output_path)
        assert len(df_saved) == len(df), "Saved CSV row count mismatch"

if __name__ == "__main__":
    pytest.main([__file__, "-v"])