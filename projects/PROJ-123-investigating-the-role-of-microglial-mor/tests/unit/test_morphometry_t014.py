import numpy as np
import pytest
from code.morphometry import skeletonize_and_count
from code.synthetic_data import set_seed, generate_microglia_cell

@pytest.fixture
def simple_cross():
    """Create a simple cross shape (1 branch point)."""
    img = np.zeros((50, 50), dtype=np.uint8)
    # Vertical line
    img[20:30, 25] = 255
    # Horizontal line
    img[25, 20:30] = 255
    return img

@pytest.fixture
def simple_line():
    """Create a simple line (0 branch points)."""
    img = np.zeros((50, 50), dtype=np.uint8)
    img[20:30, 25] = 255
    return img

@pytest.fixture
def complex_branch():
    """Create a Y shape (1 branch point)."""
    img = np.zeros((50, 50), dtype=np.uint8)
    # Stem
    img[20:30, 25] = 255
    # Left arm
    img[25, 20:25] = 255
    # Right arm
    img[25, 25:30] = 255
    return img

def test_skeletonize_cross(simple_cross):
    """Test branch point counting on a cross (expect 1)."""
    result = skeletonize_and_count(simple_cross)
    assert result['success'] is True
    assert result['branch_points'] == 1, f"Expected 1 branch point, got {result['branch_points']}"
    assert result['error'] is None

def test_skeletonize_line(simple_line):
    """Test branch point counting on a line (expect 0)."""
    result = skeletonize_and_count(simple_line)
    assert result['success'] is True
    assert result['branch_points'] == 0, f"Expected 0 branch points, got {result['branch_points']}"

def test_skeletonize_y_shape(complex_branch):
    """Test branch point counting on a Y shape (expect 1)."""
    result = skeletonize_and_count(complex_branch)
    assert result['success'] is True
    assert result['branch_points'] == 1, f"Expected 1 branch point, got {result['branch_points']}"

def test_skeletonize_empty():
    """Test behavior on empty image."""
    img = np.zeros((50, 50), dtype=np.uint8)
    result = skeletonize_and_count(img)
    assert result['success'] is True
    assert result['branch_points'] == 0

def test_skeletonize_synthetic_ground_truth():
    """
    Test against synthetic ground truth generated by synthetic_data.py.
    This validates the pipeline against a known generated structure.
    """
    set_seed(42)
    # Generate a synthetic cell with known properties
    cell_img, ground_truth = generate_microglia_cell(seed=42, complexity='medium')
    
    # The synthetic generator creates a structure; we verify the pipeline runs
    # and returns a non-negative integer for branch points.
    # Exact match is hard without manual annotation, but we verify logic integrity.
    result = skeletonize_and_count(cell_img)
    
    assert result['success'] is True
    assert isinstance(result['branch_points'], int)
    assert result['branch_points'] >= 0
    # Verify skeleton shape matches input
    assert result['skeleton'].shape == cell_img.shape