def _create_synthetic_branching_image(n_branches: int, img_size: int = 128) -> np.ndarray:
    """
    Creates a synthetic microglia-like image with a known number of branch points.
    This is a deterministic helper for testing the skeletonization logic.
    
    Args:
        n_branches: Target number of branch points to create
        img_size: Size of the square image
        
    Returns:
        numpy array representing the image
    """
    img = np.zeros((img_size, img_size), dtype=np.float32)
    cx, cy = img_size // 2, img_size // 2
    
    # Create a soma (center blob)
    soma_radius = 10
    y, x = np.ogrid[:img_size, :img_size]
    mask = (x - cx)**2 + (y - cy)**2 <= soma_radius**2
    img[mask] = 1.0
    
    # Create branches radiating from center
    # To get N branch points, we need a tree structure
    # A simple star graph has 1 branch point (the center)
    # To get more, we need bifurcations
    
    current_branches = 1
    segments = []
    
    # Add initial branches
    for i in range(current_branches):
        angle = (2 * np.pi * i) / current_branches
        length = 40
        end_x = int(cx + length * np.cos(angle))
        end_y = int(cy + length * np.sin(angle))
        segments.append(((cx, cy), (end_x, end_y)))
    
    # Add bifurcations to reach target branch points
    # Each bifurcation adds 1 branch point (where it splits)
    bifurcations_needed = n_branches
    
    for _ in range(bifurcations_needed):
        if not segments:
            break
        # Take a random-ish segment (use index for determinism)
        seg_idx = len(segments) % len(segments)
        start, end = segments[seg_idx]
        
        # Split this segment
        mid_x = (start[0] + end[0]) // 2
        mid_y = (start[1] + end[1]) // 2
        
        # Create two new segments from mid-point at different angles
        # This creates a branch point at mid-point
        new_len = (np.sqrt((end[0]-start[0])**2 + (end[1]-start[1])**2)) / 2
        
        # Add two new branches
        for angle_offset in [-np.pi/4, np.pi/4]:
            new_end_x = int(mid_x + new_len * np.cos(angle_offset))
            new_end_y = int(mid_y + new_len * np.sin(angle_offset))
            segments.append(((mid_x, mid_y), (new_end_x, new_end_y)))
        
        # Remove the old segment (it's now split)
        segments.pop(seg_idx)
    
    # Draw all segments
    for start, end in segments:
        # Draw line from start to end
        num_points = int(np.sqrt((end[0]-start[0])**2 + (end[1]-start[1])**2)) + 1
        x_coords = np.linspace(start[0], end[0], num_points).astype(int)
        y_coords = np.linspace(start[1], end[1], num_points).astype(int)
        
        # Ensure within bounds
        x_coords = np.clip(x_coords, 0, img_size-1)
        y_coords = np.clip(y_coords, 0, img_size-1)
        
        for x, y in zip(x_coords, y_coords):
            img[y, x] = 1.0
            
    return img

def test_branch_point_counting_accuracy(synthetic_ground_truth):
    """
    Unit test for branch point extraction accuracy.
    
    Verifies that the skeletonize_and_count function correctly identifies
    branch points in synthetic images within the acceptable tolerance.
    
    Note: Uses generate_synthetic_ground_truth fixture generated by 
    synthetic_data.py (T007) to create known branch counts.
    """
    # Test with specific known branch counts
    test_cases = [1, 3, 5, 10]
    
    for target_branches in test_cases:
        # Create synthetic image with known branch structure
        img = _create_synthetic_branching_image(target_branches)
        
        # Preprocess (denoise)
        processed = denoise_and_subtract(img)
        
        # Extract branch points
        detected_branches = skeletonize_and_count(processed)
        
        # Calculate error
        if target_branches == 0:
            # Special case: avoid division by zero
            error = abs(detected_branches - target_branches)
        else:
            error = abs(detected_branches - target_branches) / target_branches
        
        # Assert within tolerance (10%)
        assert error <= ACCURACY_TOLERANCE, (
            f"Branch point detection failed for {target_branches} target branches. "
            f"Detected: {detected_branches}. Error: {error:.2%} (tolerance: {ACCURACY_TOLERANCE:.2%})"
        )

def test_branch_point_counting_edge_cases():
    """
    Test edge cases for branch point counting.
    """
    # Test 1: Empty image (no branches)
    empty_img = np.zeros((64, 64), dtype=np.float32)
    count = skeletonize_and_count(empty_img)
    assert count == 0, "Empty image should return 0 branch points"
    
    # Test 2: Single line (no branches)
    line_img = np.zeros((64, 64), dtype=np.float32)
    line_img[32, :] = 1.0  # Horizontal line
    count = skeletonize_and_count(line_img)
    # A simple line should have 0 branch points (only endpoints)
    assert count == 0, "Single line should have 0 branch points"
    
    # Test 3: Cross shape (1 branch point at center)
    cross_img = np.zeros((64, 64), dtype=np.float32)
    cross_img[32, :] = 1.0  # Horizontal
    cross_img[:, 32] = 1.0  # Vertical
    count = skeletonize_and_count(cross_img)
    # A cross should have 1 branch point at the intersection
    assert count == 1, f"Cross shape should have 1 branch point, got {count}"

def test_skeletonization_preserves_topology():
    """
    Test that skeletonization preserves the topological structure
    necessary for accurate branch point counting.
    """
    # Create a Y-shape (1 branch point)
    y_img = np.zeros((100, 100), dtype=np.float32)
    # Vertical stem
    y_img[50:80, 50] = 1.0
    # Two branches going up-left and up-right
    for i in range(20):
        y_img[50 - i, 50 - i] = 1.0  # Left branch
        y_img[50 - i, 50 + i] = 1.0  # Right branch
    
    count = skeletonize_and_count(y_img)
    assert count == 1, f"Y-shape should have 1 branch point, got {count}"

def test_deterministic_results():
    """
    Test that the extraction process is deterministic given the same input.
    """
    img = _create_synthetic_branching_image(5)
    
    # Run multiple times
    results = []
    for _ in range(3):
        processed = denoise_and_subtract(img)
        count = skeletonize_and_count(processed)
        results.append(count)
    
    # All results should be identical
    assert all(r == results[0] for r in results), (
        f"Morphometry extraction is not deterministic: {results}"
    )

