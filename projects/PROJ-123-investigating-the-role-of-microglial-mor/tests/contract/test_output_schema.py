"""
Contract test for regression output schema (coefficients, p-values, interaction terms).

This test validates that the output generated by the analysis pipeline (specifically
the regression results) strictly adheres to the schema defined in
`specs/001-investigating-the-role-of-microglial-mor/contracts/output.schema.yaml`.

It ensures the presence of required fields:
- regression_results (coefficients, p_values, interaction_terms, vif_scores)
- validation_metrics (r2_mean, r2_std, sensitivity_variation)
"""

import json
import os
import pytest
from pathlib import Path
import yaml
import sys

# Add project root to path for imports if running from tests/
project_root = Path(__file__).resolve().parents[2]
sys.path.insert(0, str(project_root))

from code.config import get_path, get_project_root


def load_schema():
    """Load the output schema definition from the contracts directory."""
    schema_path = get_path("specs/001-investigating-the-role-of-microglial-mor/contracts/output.schema.yaml")
    if not os.path.exists(schema_path):
        pytest.fail(f"Schema file not found at {schema_path}. Ensure T006b is completed.")
    
    with open(schema_path, 'r') as f:
        return yaml.safe_load(f)


def load_sample_output():
    """
    Load a sample output file to validate against the schema.
    This relies on the output generated by T029 (run_report_pipeline).
    If the file doesn't exist, we simulate a minimal valid structure 
    to test the schema logic itself, but the test will fail if the 
    actual pipeline hasn't run yet.
    """
    output_path = get_path("reports/regression_results.json")
    
    if not os.path.exists(output_path):
        # Fallback for testing the schema logic in isolation if the pipeline hasn't run yet.
        # In a real CI/CD context, this would fail, indicating the pipeline didn't produce output.
        pytest.skip(f"Output file {output_path} not found. Run T029 first to generate regression_results.json.")
    
    with open(output_path, 'r') as f:
        return json.load(f)


def test_schema_structure():
    """
    Test that the output schema YAML file is valid and contains required sections.
    """
    schema = load_schema()
    
    # Check top-level keys
    assert "required_fields" in schema, "Schema must define 'required_fields'"
    required = schema["required_fields"]
    
    # Check for regression_results
    assert "regression_results" in required, "Schema must require 'regression_results'"
    regression_fields = required["regression_results"]
    assert "coefficients" in regression_fields, "regression_results must require 'coefficients'"
    assert "p_values" in regression_fields, "regression_results must require 'p_values'"
    assert "interaction_terms" in regression_fields, "regression_results must require 'interaction_terms'"
    assert "vif_scores" in regression_fields, "regression_results must require 'vif_scores'"
    
    # Check for validation_metrics
    assert "validation_metrics" in required, "Schema must require 'validation_metrics'"
    validation_fields = required["validation_metrics"]
    assert "r2_mean" in validation_fields, "validation_metrics must require 'r2_mean'"
    assert "r2_std" in validation_fields, "validation_metrics must require 'r2_std'"
    assert "sensitivity_variation" in validation_fields, "validation_metrics must require 'sensitivity_variation'"


def test_output_json_compliance():
    """
    Test that the actual generated regression_results.json file complies with the schema.
    """
    output = load_sample_output()
    schema = load_schema()
    required = schema["required_fields"]
    
    # Validate regression_results structure
    assert "regression_results" in output, "Output must contain 'regression_results'"
    reg_res = output["regression_results"]
    
    assert "coefficients" in reg_res, "regression_results must contain 'coefficients'"
    assert isinstance(reg_res["coefficients"], dict), "coefficients must be a dict"
    
    assert "p_values" in reg_res, "regression_results must contain 'p_values'"
    assert isinstance(reg_res["p_values"], dict), "p_values must be a dict"
    
    assert "interaction_terms" in reg_res, "regression_results must contain 'interaction_terms'"
    # interaction_terms can be a list or dict depending on implementation, but must exist
    assert reg_res["interaction_terms"] is not None, "interaction_terms cannot be None"
    
    assert "vif_scores" in reg_res, "regression_results must contain 'vif_scores'"
    assert isinstance(reg_res["vif_scores"], dict), "vif_scores must be a dict"
    
    # Validate validation_metrics structure
    assert "validation_metrics" in output, "Output must contain 'validation_metrics'"
    val_met = output["validation_metrics"]
    
    assert "r2_mean" in val_met, "validation_metrics must contain 'r2_mean'"
    assert isinstance(val_met["r2_mean"], (int, float)), "r2_mean must be numeric"
    
    assert "r2_std" in val_met, "validation_metrics must contain 'r2_std'"
    assert isinstance(val_met["r2_std"], (int, float)), "r2_std must be numeric"
    
    assert "sensitivity_variation" in val_met, "validation_metrics must contain 'sensitivity_variation'"
    # Check structure of sensitivity_variation if it's a dict
    if isinstance(val_met["sensitivity_variation"], dict):
        assert "p_value_deviation" in val_met["sensitivity_variation"], "sensitivity_variation must contain 'p_value_deviation' if dict"


def test_coefficient_types():
    """
    Verify that coefficients and p_values are numeric.
    """
    output = load_sample_output()
    reg_res = output["regression_results"]
    
    for key, value in reg_res["coefficients"].items():
        assert isinstance(value, (int, float)), f"Coefficient for {key} must be numeric, got {type(value)}"
    
    for key, value in reg_res["p_values"].items():
        assert isinstance(value, (int, float)), f"P-value for {key} must be numeric, got {type(value)}"
        assert 0.0 <= value <= 1.0, f"P-value for {key} must be between 0 and 1, got {value}"


def test_vif_scores_format():
    """
    Verify VIF scores are numeric and present.
    """
    output = load_sample_output()
    reg_res = output["regression_results"]
    
    vif_scores = reg_res["vif_scores"]
    assert len(vif_scores) > 0, "vif_scores must not be empty"
    
    for feature, score in vif_scores.items():
        assert isinstance(score, (int, float)), f"VIF score for {feature} must be numeric"
        assert score >= 1.0, f"VIF score for {feature} must be >= 1.0, got {score}"