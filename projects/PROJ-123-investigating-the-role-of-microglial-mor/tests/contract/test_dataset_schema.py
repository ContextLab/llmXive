import sys
from pathlib import Path

import pandas as pd
import pytest
import yaml

# Add project root to path for imports
project_root = Path(__file__).resolve().parent.parent.parent
sys.path.insert(0, str(project_root / "code"))

from synthetic_data import generate_synthetic_dataset, set_seed

# Import schema path from config if available, otherwise construct it
# Assuming standard project structure based on T006
SCHEMA_PATH = project_root / "specs" / "001-gene-regulation" / "contracts" / "dataset.schema.yaml"

@pytest.fixture(scope="module")
def synthetic_df():
    """Generate a deterministic synthetic dataset for testing."""
    set_seed(42)
    # Generate a small dataset (e.g., 10 cells) for fast contract testing
    df = generate_synthetic_dataset(num_cells=10)
    return df

@pytest.fixture(scope="module")
def schema_def():
    """Load the dataset schema definition."""
    if not SCHEMA_PATH.exists():
        pytest.fail(f"Schema file not found at {SCHEMA_PATH}. Ensure T006 is complete.")
    
    with open(SCHEMA_PATH, "r") as f:
        return yaml.safe_load(f)

def test_required_columns_exist(synthetic_df, schema_def):
    """Verify all required columns defined in the schema are present."""
    required_fields = schema_def.get("required_fields", [])
    missing = [col for col in required_fields if col not in synthetic_df.columns]
    assert not missing, f"Missing required columns: {missing}"

def test_column_types(synthetic_df, schema_def):
    """Verify data types of columns match the schema."""
    type_mapping = {
        "int64": ["int"],
        "float64": ["float"],
        "object": ["str", "object"],
        "bool": ["bool"]
    }

    for col, expected_dtype in schema_def.get("column_types", {}).items():
        if col not in synthetic_df.columns:
            continue # Handled by required_columns test
        
        actual_dtype = str(synthetic_df[col].dtype)
        # Check if actual dtype is in the list of acceptable types for the expected schema type
        acceptable = type_mapping.get(expected_dtype, [expected_dtype])
        assert any(t in actual_dtype for t in acceptable), \
            f"Column '{col}' has dtype '{actual_dtype}', expected one of {acceptable}"

def test_brain_region_values(synthetic_df, schema_def):
    """Verify brain_region values are from the allowed enum."""
    if "brain_region" not in synthetic_df.columns:
        pytest.skip("brain_region column not present")
    
    allowed_values = schema_def.get("allowed_values", {}).get("brain_region", [])
    if not allowed_values:
        pytest.skip("No allowed values defined for brain_region in schema")

    unique_regions = set(synthetic_df["brain_region"].unique())
    invalid = unique_regions - set(allowed_values)
    assert not invalid, f"Invalid brain_region values found: {invalid}. Allowed: {allowed_values}"

def test_non_null_constraints(synthetic_df, schema_def):
    """Verify columns marked as non-null do not contain NaN."""
    non_null_cols = schema_def.get("non_null_columns", [])
    
    for col in non_null_cols:
        if col in synthetic_df.columns:
            null_count = synthetic_df[col].isna().sum()
            assert null_count == 0, f"Column '{col}' contains {null_count} null values"

def test_morphological_ranges(synthetic_df, schema_def):
    """Verify morphological metrics are within plausible physical ranges."""
    # Define plausible ranges based on typical microglial dimensions (micrometers)
    # These are sanity checks to ensure the synthetic generator isn't producing absurd values
    ranges = {
        "branch_points": (0, 100),
        "total_length": (0, 500),
        "soma_area": (1, 100),
        "sholl_max_intersections": (0, 50)
    }

    for col, (min_val, max_val) in ranges.items():
        if col in synthetic_df.columns:
            col_min = synthetic_df[col].min()
            col_max = synthetic_df[col].max()
            
            assert col_min >= min_val, f"Column '{col}' has minimum {col_min} < {min_val}"
            assert col_max <= max_val, f"Column '{col}' has maximum {col_max} > {max_val}"