"""
Contract test for dataset schema compliance (US1).

This test verifies that the synthetic dataset generated by `code/synthetic_data.py`
adheres strictly to the schema defined in `specs/001-gene-regulation/contracts/dataset.schema.yaml`.
It checks for required columns, data types, and valid value ranges as per the contract.
"""

import json
