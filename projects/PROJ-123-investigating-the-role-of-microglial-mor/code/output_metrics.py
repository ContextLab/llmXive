"""
T018 Implementation: Output structured CSV for morphological metrics.

This module aggregates processed morphological data (from T013-T016) and
writes the final structured CSV to data/processed/morphological_metrics.csv.

It handles the specific requirement to store the Sholl vector as a JSON-encoded
string to support downstream sensitivity analysis (T034).
"""
import os
import json
import logging
import pandas as pd
from pathlib import Path
from typing import List, Dict, Any, Optional

from code.config import get_path, ensure_dirs, get_default_config
from code.logging_utils import get_logger, warn_missing_metadata

# Initialize logger
logger = get_logger(__name__)


def load_processed_intermediates() -> pd.DataFrame:
    """
    Loads the intermediate processed data generated by the morphometry pipeline.
    
    This function expects the data to be in a format where Sholl intersections
    are stored as lists or dictionaries, ready for JSON serialization.
    
    Returns:
        pd.DataFrame: The processed morphological data.
    
    Raises:
        FileNotFoundError: If the intermediate file is not found (indicating
                           the upstream pipeline T013-T016 has not run).
    """
    config = get_default_config()
    # The intermediate file is typically generated by the morphometry pipeline
    # before T018 runs. We assume it exists at this path based on the pipeline flow.
    # In a real execution, this would be the output of T017 (handle_empty_fields).
    intermediate_path = get_path("data_intermediates", "processed_morphology.csv")
    
    if not os.path.exists(intermediate_path):
        # If the intermediate file doesn't exist, we must fail loudly.
        # This ensures T018 does not run on missing upstream data.
        raise FileNotFoundError(
            f"Upstream intermediate file not found: {intermediate_path}. "
            "Please ensure T013-T017 (morphometry pipeline) has run successfully."
        )
    
    logger.info(f"Loading intermediate data from {intermediate_path}")
    df = pd.read_csv(intermediate_path)
    
    # Validate required columns exist
    required_cols = ['branch_points', 'total_length', 'soma_area', 'sholl_intersections', 'brain_region']
    missing_cols = [col for col in required_cols if col not in df.columns]
    if missing_cols:
        raise ValueError(f"Intermediate data missing required columns: {missing_cols}")
    
    return df


def format_sholl_vector(sholl_data: Any) -> str:
    """
    Formats the Sholl intersections vector into a JSON-encoded string.
    
    The T018 specification requires the `sholl_intersections` column to store
    the vector of counts per radius to support sensitivity analysis (T034).
    We convert lists or dicts to a JSON string.
    
    Args:
        sholl_data: The raw Sholl data (list, dict, or string).
    
    Returns:
        str: JSON-encoded string representation of the Sholl data.
    """
    if isinstance(sholl_data, str):
        # Already a string, verify it's valid JSON if possible, otherwise return as is
        try:
            json.loads(sholl_data)
            return sholl_data
        except json.JSONDecodeError:
            logger.warning(f"Sholl data is a non-JSON string: {sholl_data}")
            return sholl_data
    
    if isinstance(sholl_data, (list, dict)):
        return json.dumps(sholl_data)
    
    # Fallback for unexpected types
    logger.warning(f"Unexpected Sholl data type: {type(sholl_data)}, converting to string.")
    return str(sholl_data)


def aggregate_and_save_metrics(df: pd.DataFrame, output_path: str) -> None:
    """
    Aggregates the metrics and saves the final CSV.
    
    This function ensures the `sholl_intersections` column is formatted correctly
    and writes the final artifact to disk.
    
    Args:
        df: The DataFrame containing processed metrics.
        output_path: The full path to the output CSV file.
    """
    logger.info(f"Formatting Sholl vectors for {len(df)} records...")
    
    # Apply formatting to Sholl vectors
    df['sholl_intersections'] = df['sholl_intersections'].apply(format_sholl_vector)
    
    # Ensure brain_region is present and valid
    if 'brain_region' not in df.columns:
        raise ValueError("Data must contain 'brain_region' column for T018 output.")
    
    # Log any missing metadata (FR-001, FR-008)
    warn_missing_metadata(df, ['brain_region'])
    
    # Ensure output directory exists
    ensure_dirs(os.path.dirname(output_path))
    
    logger.info(f"Writing final metrics to {output_path}")
    df.to_csv(output_path, index=False)
    logger.info(f"Successfully wrote {len(df)} rows to {output_path}")


def run_output_pipeline() -> str:
    """
    Runs the full T018 output pipeline.
    
    1. Loads intermediate data from the morphometry pipeline.
    2. Formats Sholl vectors as JSON strings.
    3. Saves the final CSV to data/processed/morphological_metrics.csv.
    
    Returns:
        str: The path to the generated output file.
    
    Raises:
        FileNotFoundError: If upstream data is missing.
        ValueError: If data validation fails.
    """
    config = get_default_config()
    output_filename = config.get('output_metrics_filename', 'morphological_metrics.csv')
    output_dir = get_path('data_processed')
    output_path = os.path.join(output_dir, output_filename)
    
    logger.info("Starting T018: Output Morphological Metrics Pipeline")
    
    # Step 1: Load data
    df = load_processed_intermediates()
    
    # Step 2: Process and Save
    aggregate_and_save_metrics(df, output_path)
    
    logger.info("T018 Pipeline completed successfully.")
    return output_path


def main():
    """
    Entry point for running T018 directly from the command line.
    """
    logging.basicConfig(level=logging.INFO)
    try:
        output_path = run_output_pipeline()
        print(f"Output written to: {output_path}")
    except Exception as e:
        logger.error(f"Pipeline failed: {e}")
        raise


if __name__ == "__main__":
    main()
