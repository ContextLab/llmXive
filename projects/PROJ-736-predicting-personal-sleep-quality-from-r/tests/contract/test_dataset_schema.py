"""
Contract test for the data schema defined in tests/contract/dataset.schema.yaml.

This test verifies that the processed feature vectors and associated metadata
produced by the US1 pipeline conform to the expected schema.

Schema validation ensures:
1. The JSON metadata file exists and is valid JSON.
2. Required fields are present (subject_id, sleep_score, n_features, feature_file).
3. The feature file (NPY) exists and matches the declared number of features.
4. Data types match the schema expectations.
"""
import json
import os
import numpy as np
import pytest
from pathlib import Path
from typing import Any, Dict

# Import config to get paths
import sys
sys.path.insert(0, os.path.join(os.path.dirname(__file__), '..', '..', 'code'))
from config import get_paths

# Path to the schema definition
SCHEMA_PATH = Path(__file__).parent.parent / "contract" / "dataset.schema.yaml"

def load_schema() -> Dict[str, Any]:
    """Load the schema definition from YAML file."""
    # Since we cannot assume pyyaml is installed without checking requirements,
    # we will parse the simple YAML structure manually or use a safe loader if available.
    # However, for a robust test without external deps if possible, we define the
    # expected structure inline based on the task description and common patterns.
    # But the task says "schema definitions (dataset.schema.yaml)" exist in T010.
    # We will try to import yaml, and if not, fallback to a hardcoded expected structure
    # that matches the likely content of that schema file.
    try:
        import yaml
        with open(SCHEMA_PATH, 'r') as f:
            return yaml.safe_load(f)
    except ImportError:
        # Fallback schema definition matching the project context
        return {
            "type": "object",
            "required": ["subject_id", "sleep_score", "n_features", "feature_file"],
            "properties": {
                "subject_id": {"type": "string"},
                "sleep_score": {"type": "number"},
                "n_features": {"type": "integer", "minimum": 1},
                "feature_file": {"type": "string"},
                "processing_status": {"type": "string", "enum": ["success", "failed"]}
            }
        }

def validate_type(value: Any, expected_type: str) -> bool:
    """Validate a value against a simple type string."""
    if expected_type == "string":
        return isinstance(value, str)
    if expected_type == "number":
        return isinstance(value, (int, float))
    if expected_type == "integer":
        return isinstance(value, int)
    if expected_type == "boolean":
        return isinstance(value, bool)
    return False

def test_schema_file_exists():
    """Verify the schema definition file exists."""
    assert SCHEMA_PATH.exists(), f"Schema file not found at {SCHEMA_PATH}"

def test_dataset_metadata_structure():
    """
    Test that the dataset metadata (if generated by T014d) conforms to the schema.
    This test assumes the pipeline has been run and generated a metadata JSON.
    If the file doesn't exist, we skip the content validation but ensure the path is correct.
    """
    paths = get_paths()
    metadata_file = paths["data_processed"] / "dataset_metadata.json"
    
    if not metadata_file.exists():
        pytest.skip("Dataset metadata file not found. Pipeline may not have been run yet.")
    
    with open(metadata_file, 'r') as f:
        data = json.load(f)
    
    schema = load_schema()
    required_fields = schema.get("required", [])
    
    for field in required_fields:
        assert field in data, f"Missing required field in dataset metadata: {field}"
    
    # Validate specific types based on schema
    if "subject_id" in schema.get("properties", {}):
        assert validate_type(data.get("subject_id"), "string"), "subject_id must be a string"
    
    if "sleep_score" in schema.get("properties", {}):
        assert validate_type(data.get("sleep_score"), "number"), "sleep_score must be a number"
    
    if "n_features" in schema.get("properties", {}):
        assert validate_type(data.get("n_features"), "integer"), "n_features must be an integer"
        if data.get("n_features") is not None:
            assert data["n_features"] > 0, "n_features must be positive"

def test_feature_vector_consistency():
    """
    Test that the feature vectors referenced in metadata actually exist and match dimensions.
    """
    paths = get_paths()
    metadata_file = paths["data_processed"] / "dataset_metadata.json"
    
    if not metadata_file.exists():
        pytest.skip("Dataset metadata file not found.")
    
    with open(metadata_file, 'r') as f:
        data = json.load(f)
    
    feature_file_path = paths["data_processed"] / data["feature_file"]
    
    assert feature_file_path.exists(), f"Feature file {data['feature_file']} referenced in metadata does not exist."
    
    try:
        features = np.load(feature_file_path)
        expected_n = data["n_features"]
        actual_n = len(features)
        
        assert actual_n == expected_n, (
            f"Feature count mismatch: Metadata says {expected_n}, "
            f"but file contains {actual_n} features."
        )
    except Exception as e:
        pytest.fail(f"Failed to load or validate feature vector: {e}")

def test_schema_constraints():
    """
    Verify that the schema constraints (e.g., enum values, min values) are logically sound.
    """
    schema = load_schema()
    props = schema.get("properties", {})
    
    if "processing_status" in props:
        enum_vals = props["processing_status"].get("enum", [])
        assert "success" in enum_vals, "Schema must allow 'success' status"
        assert "failed" in enum_vals, "Schema must allow 'failed' status"
    
    if "n_features" in props:
        min_val = props["n_features"].get("minimum")
        assert min_val is not None and min_val >= 1, "n_features minimum must be >= 1"