"""
Contract test for visualization output schema (US3).

Validates that the visualization output file (SVG/PNG) exists and conforms
to the expected structure for a brain connectivity plot generated by Nilearn.

For SVG files, it parses the XML to count <line> and <path> elements to ensure
sufficient edges are rendered (SC-004).
"""
import os
import json
import xml.etree.ElementTree as ET
from pathlib import Path
import pytest
from config import get_paths


def test_viz_file_exists():
    """Verify that the visualization file exists in the expected location."""
    paths = get_paths()
    result_dir = paths["results"]
    
    # Check for expected output files based on T032
    # We check for the primary expected filename pattern
    viz_file_png = os.path.join(result_dir, "brain_connectivity_plot.png")
    viz_file_svg = os.path.join(result_dir, "brain_connectivity_plot.svg")
    
    # At least one must exist
    assert os.path.exists(viz_file_png) or os.path.exists(viz_file_svg), \
        f"Visualization file not found. Expected at {viz_file_png} or {viz_file_svg}"


def test_viz_schema_structure():
    """
    Validate the structure of the visualization output.
    
    - If SVG: Parse XML and count <line> and <path> elements.
      Must have >= 50 edges (lines/paths) as per SC-004.
    - If PNG: Verify file size is non-zero (basic sanity check).
    """
    paths = get_paths()
    result_dir = paths["results"]
    
    viz_file_svg = os.path.join(result_dir, "brain_connectivity_plot.svg")
    viz_file_png = os.path.join(result_dir, "brain_connectivity_plot.png")
    
    if os.path.exists(viz_file_svg):
        # Validate SVG schema
        try:
            tree = ET.parse(viz_file_svg)
            root = tree.getroot()
            
            # Handle namespace if present (Nilearn often uses namespaces)
            namespace = ""
            if root.tag.startswith("{"):
                namespace = root.tag.split("}")[0] + "}"
            
            # Count line and path elements (edges)
            lines = root.findall(f".//{namespace}line")
            paths = root.findall(f".//{namespace}path")
            
            total_edges = len(lines) + len(paths)
            
            assert total_edges >= 50, (
                f"Visualization contains only {total_edges} edges. "
                f"Minimum required by SC-004 is 50."
            )
            
        except ET.ParseError as e:
            pytest.fail(f"SVG file is not valid XML: {e}")
            
    elif os.path.exists(viz_file_png):
        # Basic sanity check for PNG (non-empty file)
        file_size = os.path.getsize(viz_file_png)
        assert file_size > 0, "PNG visualization file is empty."
    else:
        pytest.fail("Neither SVG nor PNG visualization file found.")


def test_viz_report_consistency():
    """
    Verify that ResultReport.json (T026) references the visualization file path.
    """
    paths = get_paths()
    report_path = os.path.join(paths["results"], "ResultReport.json")
    
    assert os.path.exists(report_path), "ResultReport.json not found."
    
    with open(report_path, "r") as f:
        report = json.load(f)
    
    # Check if visualization path is recorded
    # T032 requires logging the file path in ResultReport.json
    viz_path = report.get("visualization_path")
    
    assert viz_path is not None, (
        "ResultReport.json must contain 'visualization_path' key "
        "as per T032 requirement."
    )
    
    # Verify the referenced file actually exists
    full_viz_path = os.path.join(paths["results"], viz_path)
    assert os.path.exists(full_viz_path), (
        f"Visualization file referenced in ResultReport.json does not exist: {full_viz_path}"
    )