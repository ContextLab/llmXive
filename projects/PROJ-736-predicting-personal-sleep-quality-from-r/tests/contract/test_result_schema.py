"""
Contract test for ResultReport.json schema (US2).

This test verifies that the ResultReport.json file generated by the modeling
pipeline conforms to the expected schema defined in specs/001-predict-sleep-quality/result.schema.yaml.

It checks:
1. Top-level structure and required fields
2. Data types for all fields
3. Presence and structure of nested objects (metrics, confidence_intervals, sensitivity_analysis)
4. Specific constraints (e.g., p-value is approximation flag)
"""
import json
import os
import pytest
from pathlib import Path
import yaml
from typing import Any, Dict, List, Optional

# Import config to get paths
from config import get_paths


def load_schema() -> Dict[str, Any]:
    """Load the result schema from specs."""
    paths = get_paths()
    schema_path = paths['specs'] / '001-predict-sleep-quality' / 'result.schema.yaml'
    
    if not schema_path.exists():
        # Fallback for testing if specs directory structure differs slightly
        # In a real scenario, this would be a critical failure
        raise FileNotFoundError(f"Schema file not found at {schema_path}")
    
    with open(schema_path, 'r') as f:
        return yaml.safe_load(f)


def load_result_report() -> Dict[str, Any]:
    """Load the ResultReport.json file."""
    paths = get_paths()
    report_path = paths['results'] / 'ResultReport.json'
    
    if not report_path.exists():
        pytest.fail(f"ResultReport.json not found at {report_path}. "
                    "The modeling pipeline (T020-T026) must be run first to generate this file.")
    
    with open(report_path, 'r') as f:
        return json.load(f)


def validate_type(value: Any, expected_type: str, field_path: str) -> None:
    """Validate that a value matches the expected type string."""
    type_map = {
        'string': str,
        'number': (int, float),
        'integer': int,
        'boolean': bool,
        'array': list,
        'object': dict,
        'null': type(None)
    }
    
    expected_python_type = type_map.get(expected_type)
    if expected_python_type is None:
        raise ValueError(f"Unknown type definition: {expected_type} for field {field_path}")
    
    if not isinstance(value, expected_python_type):
        pytest.fail(f"Type mismatch at '{field_path}': expected {expected_type}, "
                    f"got {type(value).__name__} (value: {value})")


def validate_field(value: Any, field_def: Dict[str, Any], field_path: str) -> None:
    """Recursively validate a field against its schema definition."""
    
    # Check type
    if 'type' in field_def:
        validate_type(value, field_def['type'], field_path)
    
    # Check required fields for objects
    if field_def.get('type') == 'object' and isinstance(value, dict):
        if 'properties' in field_def:
            for prop_name, prop_def in field_def['properties'].items():
                prop_path = f"{field_path}.{prop_name}"
                if prop_name in value:
                    validate_field(value[prop_name], prop_def, prop_path)
                elif prop_def.get('required', False):
                    pytest.fail(f"Missing required field: {prop_path}")
        
        # Check additionalProperties if defined
        if 'additionalProperties' in field_def:
            allowed_keys = set(field_def.get('properties', {}).keys())
            extra_keys = set(value.keys()) - allowed_keys
            if extra_keys:
                pytest.fail(f"Unexpected fields in {field_path}: {extra_keys}")
    
    # Check array items
    if field_def.get('type') == 'array' and isinstance(value, list):
        if 'items' in field_def:
            for i, item in enumerate(value):
                validate_field(item, field_def['items'], f"{field_path}[{i}]")
    
    # Check enum
    if 'enum' in field_def:
        if value not in field_def['enum']:
            pytest.fail(f"Value at '{field_path}' must be one of {field_def['enum']}, got {value}")
    
    # Check minimum/maximum for numbers
    if isinstance(value, (int, float)):
        if 'minimum' in field_def and value < field_def['minimum']:
            pytest.fail(f"Value at '{field_path}' is below minimum {field_def['minimum']}")
        if 'maximum' in field_def and value > field_def['maximum']:
            pytest.fail(f"Value at '{field_path}' is above maximum {field_def['maximum']}")


def test_result_schema_structure():
    """
    Test that ResultReport.json conforms to the schema defined in result.schema.yaml.
    """
    schema = load_schema()
    report = load_result_report()
    
    # Validate the entire report against the root schema
    validate_field(report, schema, "root")


def test_required_metrics_exist():
    """
    Ensure all required top-level metrics are present.
    """
    report = load_result_report()
    
    required_top_level = [
        'model_performance', 
        'permutation_test', 
        'bootstrap_ci', 
        'sensitivity_analysis',
        'execution_metadata'
    ]
    
    for field in required_top_level:
        assert field in report, f"Missing required top-level field: {field}"


def test_permutation_test_approximation_flag():
    """
    Verify that the permutation test p-value is explicitly marked as an approximation
    derived from a subset, as per Plan Override of FR-006.
    """
    report = load_result_report()
    
    assert 'permutation_test' in report
    pt = report['permutation_test']
    
    assert 'is_approximation' in pt, "Missing 'is_approximation' flag in permutation_test"
    assert pt['is_approximation'] is True, "permutation_test.is_approximation must be True"
    
    assert 'subset_size' in pt, "Missing 'subset_size' in permutation_test"
    assert pt['subset_size'] == 100, "Permutation test must use a subset of 100 subjects"


def test_sensitivity_analysis_structure():
    """
    Verify the structure of the sensitivity analysis grid.
    """
    report = load_result_report()
    
    if 'sensitivity_analysis' not in report:
        pytest.skip("Sensitivity analysis not yet run (T024)")
    
    sa = report['sensitivity_analysis']
    
    assert 'grid_points' in sa, "Missing 'grid_points' in sensitivity_analysis"
    assert isinstance(sa['grid_points'], list), "'grid_points' must be an array"
    
    # If grid points exist, verify structure of at least one
    if len(sa['grid_points']) > 0:
        point = sa['grid_points'][0]
        assert 'variance_threshold' in point
        assert 'pca_retention' in point
        assert 'pearson_r' in point or 'r_squared' in point


def test_execution_metadata():
    """
    Verify execution metadata contains required fields.
    """
    report = load_result_report()
    
    assert 'execution_metadata' in report
    meta = report['execution_metadata']
    
    required_meta = ['timestamp', 'cpu_only', 'peak_ram_gb', 'wall_time_seconds']
    for field in required_meta:
        assert field in meta, f"Missing execution metadata field: {field}"


def test_no_manual_entry():
    """
    Verify that the report was generated programmatically and not manually edited.
    Checks for the presence of a generation signature or timestamp.
    """
    report = load_result_report()
    
    # Check for a generation signature or timestamp in metadata
    if 'execution_metadata' in report:
        assert 'timestamp' in report['execution_metadata'], \
            "Missing timestamp in execution metadata suggests manual editing"
    
    # Alternatively, check for a specific marker if defined in schema
    if 'generated_by' in report:
        assert report['generated_by'] == "llmXive-pipeline", \
            "Report marker mismatch"