"""
Validation script for User Story 3 visualization output (T033).

Verifies that the plot file generated by T032 exists and contains
at least 50 edges by parsing the SVG file using xml.etree.ElementTree.

Counts both <line> and <path> elements to handle different rendering backends.
Implements retry logic and error logging as per SC-004.

Does NOT use OpenCV.
"""
import os
import sys
import json
import time
import xml.etree.ElementTree as ET
from pathlib import Path
from typing import Tuple, Optional, List
from utils.logging import setup_logging, log_stage_start, log_stage_complete, log_stage_error
from config import get_paths


def count_svg_edges(svg_path: Path, min_edges: int = 50, max_retries: int = 3) -> Tuple[bool, int, Optional[str]]:
    """
    Parse an SVG file and count edge elements (<line> and <path>).
    
    Args:
        svg_path: Path to the SVG file to validate.
        min_edges: Minimum number of edges required (default 50).
        max_retries: Number of retry attempts if file access fails.
        
    Returns:
        Tuple of (success: bool, edge_count: int, error_message: Optional[str])
    """
    if not svg_path.exists():
        return False, 0, f"File not found: {svg_path}"

    for attempt in range(1, max_retries + 1):
        try:
            # Parse the SVG file
            tree = ET.parse(svg_path)
            root = tree.getroot()
            
            # Handle namespace if present (common in SVGs generated by matplotlib/nilearn)
            # Matplotlib often doesn't use namespaces, but nilearn might.
            # We'll try to find elements with or without namespace.
            
            # Define possible namespaces
            namespaces = {'svg': 'http://www.w3.org/2000/svg'}
            
            line_count = 0
            path_count = 0
            
            # Count <line> elements
            # Try with namespace first
            lines_with_ns = root.findall('.//svg:line', namespaces)
            if lines_with_ns:
                line_count = len(lines_with_ns)
            else:
                # Try without namespace
                lines_no_ns = root.findall('.//{http://www.w3.org/2000/svg}line')
                if not lines_no_ns:
                    # Fallback: look for any 'line' tag regardless of namespace
                    for elem in root.iter():
                        if elem.tag.endswith('line'):
                            line_count += 1
            
            # Count <path> elements (used for curves or complex edges)
            paths_with_ns = root.findall('.//svg:path', namespaces)
            if paths_with_ns:
                path_count = len(paths_with_ns)
            else:
                paths_no_ns = root.findall('.//{http://www.w3.org/2000/svg}path')
                if not paths_no_ns:
                    # Fallback: look for any 'path' tag
                    for elem in root.iter():
                        if elem.tag.endswith('path'):
                            path_count += 1
            
            total_edges = line_count + path_count
            
            if total_edges < min_edges:
                return False, total_edges, f"Insufficient edges: found {total_edges}, expected >= {min_edges}"
            
            return True, total_edges, None
            
        except ET.ParseError as e:
            if attempt < max_retries:
                time.sleep(0.5 * attempt)  # Exponential backoff
                continue
            return False, 0, f"Failed to parse SVG: {str(e)}"
        except PermissionError as e:
            if attempt < max_retries:
                time.sleep(0.5 * attempt)
                continue
            return False, 0, f"Permission denied accessing file: {str(e)}"
        except Exception as e:
            return False, 0, f"Unexpected error during validation: {str(e)}"
    
    return False, 0, "Max retries exceeded"


def main():
    """Main entry point for T033 validation."""
    # Setup logging
    paths = get_paths()
    logger = setup_logging(paths['log_dir'])
    
    log_stage_start("T033", "Validating plot file")
    
    try:
        # Load result report to get the plot file path
        result_report_path = paths['results_dir'] / "ResultReport.json"
        
        if not result_report_path.exists():
            log_stage_error("T033", f"ResultReport.json not found at {result_report_path}")
            print(f"ERROR: {result_report_path} not found. Cannot validate plot.")
            sys.exit(1)
        
        with open(result_report_path, 'r') as f:
            report = json.load(f)
        
        # Extract plot file path from report
        if 'visualization' not in report or 'plot_path' not in report['visualization']:
            log_stage_error("T033", "No plot_path found in ResultReport.json")
            print("ERROR: No 'visualization.plot_path' found in ResultReport.json")
            sys.exit(1)
        
        plot_path_str = report['visualization']['plot_path']
        plot_path = Path(plot_path_str)
        
        # Make path absolute if relative
        if not plot_path.is_absolute():
            plot_path = paths['project_root'] / plot_path
        
        log_stage_start("T033", f"Validating {plot_path}")
        
        if not plot_path.exists():
            log_stage_error("T033", f"Plot file not found: {plot_path}")
            print(f"ERROR: Plot file not found: {plot_path}")
            sys.exit(1)
        
        # Validate the plot
        success, edge_count, error_msg = count_svg_edges(plot_path)
        
        if success:
            log_stage_complete("T033", f"Validation passed: {edge_count} edges found (>= 50)")
            print(f"SUCCESS: Plot file contains {edge_count} edges (>= 50 required)")
            
            # Update ResultReport.json with validation result
            report['validation'] = {
                'plot_file': str(plot_path),
                'edge_count': edge_count,
                'min_required': 50,
                'passed': True,
                'validated_at': time.strftime("%Y-%m-%dT%H:%M:%SZ")
            }
            
            with open(result_report_path, 'w') as f:
                json.dump(report, f, indent=2)
            
            print(f"Updated {result_report_path} with validation result")
            sys.exit(0)
        else:
            log_stage_error("T033", f"Validation failed: {error_msg}")
            print(f"ERROR: {error_msg}")
            
            # Update ResultReport.json with failure
            report['validation'] = {
                'plot_file': str(plot_path),
                'edge_count': edge_count,
                'min_required': 50,
                'passed': False,
                'error': error_msg,
                'validated_at': time.strftime("%Y-%m-%dT%H:%M:%SZ")
            }
            
            with open(result_report_path, 'w') as f:
                json.dump(report, f, indent=2)
            
            print(f"Updated {result_report_path} with validation failure")
            sys.exit(1)
            
    except Exception as e:
        log_stage_error("T033", f"Unexpected error: {str(e)}")
        print(f"CRITICAL ERROR: {str(e)}")
        sys.exit(1)


if __name__ == "__main__":
    main()