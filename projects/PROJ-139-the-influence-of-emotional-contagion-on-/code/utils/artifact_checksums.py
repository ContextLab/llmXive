"""
Artifact Checksum Recording Module for PROJ-139.

This module computes SHA-256 hashes for all critical project artifacts
(data files, processed results, model outputs, and configuration) and
records them in a YAML state file for reproducibility verification.

Outputs:
    state/projects/PROJ-139-the-influence-of-emotional-contagion-on-.yaml
"""
import hashlib
import json
import logging
import os
import yaml
from pathlib import Path
from datetime import datetime
from typing import Dict, List, Any, Optional, Union

from utils.logging_config import get_logger
from config.settings import get_config

# Initialize logger
logger = get_logger(__name__)

# Define the project root relative to the code directory
# Assuming code/ is at repo root or parent of this module
PROJECT_ROOT = Path(__file__).resolve().parent.parent.parent
STATE_DIR = PROJECT_ROOT / "state" / "projects"
PROJECT_ID = "PROJ-139-the-influence-of-emotional-contagion-on-"
OUTPUT_FILE = STATE_DIR / f"{PROJECT_ID}.yaml"

# Patterns of files to hash (relative to PROJECT_ROOT)
# We focus on data artifacts, processed results, and key configuration files
ARTIFACT_PATTERNS = [
    "data/raw/*.json",
    "data/processed/*.csv",
    "data/processed/*.log",
    "data/processed/*.json",
    "state/artifacts/*.json",
    "docs/paper.md",
    "code/config/settings.yaml", # If exists
]

def compute_file_hash(file_path: Union[str, Path]) -> Optional[str]:
    """
    Compute SHA-256 hash of a file.
    
    Args:
        file_path: Path to the file.
        
    Returns:
        Hex digest string or None if file cannot be read.
    """
    try:
        sha256_hash = hashlib.sha256()
        with open(file_path, "rb") as f:
            for chunk in iter(lambda: f.read(4096), b""):
                sha256_hash.update(chunk)
        return sha256_hash.hexdigest()
    except (IOError, OSError) as e:
        logger.error(f"Failed to read file {file_path} for hashing: {e}")
        return None

def find_artifacts(patterns: List[str], root: Path) -> List[Path]:
    """
    Find all files matching the given glob patterns relative to root.
    
    Args:
        patterns: List of glob patterns (e.g., "data/raw/*.json").
        root: Base directory for globbing.
        
    Returns:
        List of matching Path objects.
    """
    artifacts = []
    for pattern in patterns:
        full_pattern = root / pattern
        # glob handles both * and ** if needed, but standard glob is safer for simple patterns
        matches = list(full_pattern.parent.glob(full_pattern.name))
        # If the pattern was a directory glob like **/*, glob() handles it differently,
        # but for our specific list, simple glob is sufficient.
        # Re-checking: if pattern is "data/raw/*.json", parent is "data/raw", name is "*.json".
        # If pattern is "docs/paper.md", parent is "docs", name is "paper.md".
        
        # Actually, Path.glob expects a pattern string.
        # Let's use root.glob(pattern) directly if pattern is relative to root.
        matches = list(root.glob(pattern))
        for match in matches:
            if match.is_file():
                artifacts.append(match)
    return artifacts

def record_checksums(output_path: Path, artifacts: List[Path], timestamp: Optional[datetime] = None) -> Dict[str, Any]:
    """
    Compute hashes for a list of artifacts and write them to a YAML file.
    
    Args:
        output_path: Destination path for the YAML report.
        artifacts: List of file paths to hash.
        timestamp: Optional timestamp override.
        
    Returns:
        The dictionary structure written to the file.
    """
    if timestamp is None:
        timestamp = datetime.now()

    artifact_hashes = {}
    missing_files = []

    for artifact in artifacts:
        # Store path relative to PROJECT_ROOT for portability
        relative_path = str(artifact.relative_to(PROJECT_ROOT))
        file_hash = compute_file_hash(artifact)
        
        if file_hash:
            artifact_hashes[relative_path] = file_hash
            logger.info(f"Hashed: {relative_path} -> {file_hash[:16]}...")
        else:
            missing_files.append(relative_path)
            logger.warning(f"Skipped missing or unreadable file: {relative_path}")

    report = {
        "project_id": PROJECT_ID,
        "generated_at": timestamp.isoformat(),
        "artifact_hashes": artifact_hashes,
        "total_artifacts": len(artifact_hashes),
        "skipped_missing": missing_files,
        "notes": "Checksums generated by T027 artifact_checksums.py"
    }

    # Ensure output directory exists
    output_path.parent.mkdir(parents=True, exist_ok=True)

    with open(output_path, "w", encoding="utf-8") as f:
        yaml.dump(report, f, default_flow_style=False, sort_keys=False, allow_unicode=True)

    logger.info(f"Checksum report written to: {output_path}")
    return report

def main():
    """
    Main entry point for T027: Record all artifact checksums.
    """
    logger.info("Starting artifact checksum recording (T027)...")
    
    # Ensure state directory exists
    STATE_DIR.mkdir(parents=True, exist_ok=True)

    # Find all relevant artifacts
    artifacts = find_artifacts(ARTIFACT_PATTERNS, PROJECT_ROOT)
    
    if not artifacts:
        logger.warning("No artifacts found matching the specified patterns. "
                     "Check if data/processed or data/raw contain files.")
        # Even if no files, we should record an empty state or a note
        # But the task implies recording existing artifacts.
        # We will proceed with an empty list if none found, but log it.
    
    logger.info(f"Found {len(artifacts)} artifacts to hash.")
    
    report = record_checksums(OUTPUT_FILE, artifacts)
    
    if not report["artifact_hashes"]:
        logger.warning("No artifact hashes were recorded. Ensure data files exist.")
    
    logger.info("T027 completed successfully.")
    return report

if __name__ == "__main__":
    main()
