import os
import logging
import pandas as pd
from pathlib import Path
from typing import Optional, Tuple, List
from utils.config import get_config

__all__ = [
    "load_pre_filter_dataset",
    "filter_samples",
    "write_filtered_dataset",
    "main",
]

def load_pre_filter_dataset(csv_path: str) -> pd.DataFrame:
    """
    Load the pre‑filter dataset generated by ``code/data/ingest.py``.

    Parameters
    ----------
    csv_path: str
        Path to the CSV file.

    Returns
    -------
    pd.DataFrame
        DataFrame with all original columns.
    """
    logging.info("Loading pre‑filter dataset from %s", csv_path)
    return pd.read_csv(csv_path)

def filter_samples(df: pd.DataFrame) -> pd.DataFrame:
    """
    Filter out samples based on ``validation_flag`` and ``depth_flag``.

    The function expects boolean columns named ``validation_flag`` and
    ``depth_flag``; rows where either flag is ``False`` are removed.

    Parameters
    ----------
    df: pd.DataFrame
        Input dataset.

    Returns
    -------
    pd.DataFrame
        Filtered dataset.
    """
    required_flags = ["validation_flag", "depth_flag"]
    for flag in required_flags:
        if flag not in df.columns:
            raise KeyError(f"Missing required flag column: {flag}")
    filtered = df[(df["validation_flag"] == True) & (df["depth_flag"] == True)]
    logging.info(
        "Filtered dataset: %d -> %d rows", len(df), len(filtered)
    )
    return filtered

def write_filtered_dataset(df: pd.DataFrame, out_path: str) -> None:
    """
    Write the filtered dataset to CSV.

    Parameters
    ----------
    df: pd.DataFrame
        Filtered DataFrame.
    out_path: str
        Destination CSV file path.
    """
    df.to_csv(out_path, index=False)
    logging.info("Filtered dataset written to %s", out_path)

def main(pre_filter_path: str, out_path: str) -> None:
    """
    Command‑line entry point for sample filtering.

    Parameters
    ----------
    pre_filter_path: str
        Path to the pre‑filter CSV.
    out_path: str
        Destination path for the primary analysis CSV.
    """
    config = get_config()
    df = load_pre_filter_dataset(pre_filter_path)
    filtered = filter_samples(df)
    write_filtered_dataset(filtered, out_path)
