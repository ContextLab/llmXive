import sys
sys.path.insert(0, str(Path(__file__).parent.parent / 'code'))

from utils.config import get_config
from code.utils.config import get_known_expert_ids, validate_routing_labels, filter_valid_rows

# Constants for testing
TEST_EXPERT_IDS = ["expert_0", "expert_1", "expert_2", "expert_3", "expert_4"]
REQUIRED_COLUMNS = ["prompt_embedding", "noise_level", "routing_label", "velocity_vector", "source_dataset"]
VELOCITY_DIM = 64  # Assumed dimension for velocity vectors


class TestSchemaValidation:
    """Tests for data schema validation functions."""

