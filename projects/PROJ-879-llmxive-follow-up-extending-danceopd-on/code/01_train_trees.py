import argparse
import sys
import json
import hashlib
import os
from pathlib import Path
from typing import Dict, Any, List, Tuple

import pandas as pd
from sklearn.tree import DecisionTreeClassifier
from sklearn.model_selection import train_test_split
from sklearn.metrics import accuracy_score
import joblib

from utils.config import get_config, get_path

# Constants for paths relative to project root
PROJECT_ROOT = Path(__file__).resolve().parent.parent
CONTRACTS_DIR = PROJECT_ROOT / "specs" / "contracts"
STATE_DIR = PROJECT_ROOT / "state"
MODELS_DIR = PROJECT_ROOT / "models" / "trained_trees"
RESULTS_DIR = PROJECT_ROOT / "data" / "results"
PROCESSED_DATA_DIR = PROJECT_ROOT / "data" / "processed"


def load_dataset() -> pd.DataFrame:
    """Load the teacher routing dataset generated by US1."""
    dataset_path = PROCESSED_DATA_DIR / "teacher_routing_dataset.parquet"
    if not dataset_path.exists():
        raise FileNotFoundError(f"Dataset not found at {dataset_path}. Run T014 first.")
    return pd.read_parquet(dataset_path)


def split_data(df: pd.DataFrame, test_size: float = 0.2, seed: int = 42) -> Tuple[pd.DataFrame, pd.DataFrame]:
    """Split data into train and test sets."""
    return train_test_split(df, test_size=test_size, random_state=seed, stratify=df['routing_label'])


def train_trees(train_df: pd.DataFrame, test_df: pd.DataFrame) -> Dict[int, Any]:
    """Train DecisionTreeClassifier for max_depth 2 to 20."""
    X_train = train_df.drop(columns=['routing_label'])
    y_train = train_df['routing_label']
    X_test = test_df.drop(columns=['routing_label'])
    y_test = test_df['routing_label']

    models = {}
    results = []

    for depth in range(2, 21):
        clf = DecisionTreeClassifier(max_depth=depth, random_state=42)
        clf.fit(X_train, y_train)
        y_pred = clf.predict(X_test)
        acc = accuracy_score(y_test, y_pred)

        models[depth] = clf
        results.append({'depth': depth, 'accuracy': acc})

    return models, results


def save_models_and_results(models: Dict[int, Any], results: List[Dict], config: Dict[str, Any]) -> str:
    """Save trained models and results table."""
    MODELS_DIR.mkdir(parents=True, exist_ok=True)
    RESULTS_DIR.mkdir(parents=True, exist_ok=True)

    for depth, clf in models.items():
        model_path = MODELS_DIR / f"tree_depth_{depth}.joblib"
        joblib.dump(clf, model_path)

    results_df = pd.DataFrame(results)
    results_path = RESULTS_DIR / "tree_accuracy.csv"
    results_df.to_csv(results_path, index=False)

    return str(results_path)


def validate_and_update_state(models: Dict[int, Any], results_path: str, config: Dict[str, Any]) -> None:
    """
    Validate model metadata against the schema from specs/contracts/DecisionTreeMetadata.json
    and update state/ with model hashes.
    """
    # Ensure directories exist
    STATE_DIR.mkdir(parents=True, exist_ok=True)
    MODELS_DIR.mkdir(parents=True, exist_ok=True)

    # Load schema if it exists (optional validation step)
    schema_path = CONTRACTS_DIR / "DecisionTreeMetadata.json"
    schema = None
    if schema_path.exists():
        with open(schema_path, 'r') as f:
            schema = json.load(f)

    state_record = {
        "task_id": "T024",
        "timestamp": config.get("timestamp", "unknown"),
        "models": [],
        "results_file": str(results_path),
        "validation_status": "passed"
    }

    for depth, clf in models.items():
        model_path = MODELS_DIR / f"tree_depth_{depth}.joblib"
        
        # Calculate hash
        with open(model_path, 'rb') as f:
            content = f.read()
            file_hash = hashlib.sha256(content).hexdigest()
        
        # Construct metadata object
        metadata = {
            "depth": depth,
            "model_type": "DecisionTreeClassifier",
            "file_path": str(model_path),
            "sha256": file_hash,
            "size_bytes": len(content),
            "accuracy": next(r['accuracy'] for r in results_path if r['depth'] == depth) if isinstance(results_path, list) else 0.0 # Placeholder logic, fixed below
        }

        # If we have the results list available, update accuracy
        # (In a real flow, we'd pass the results list here, but we can re-read CSV)
        try:
            res_df = pd.read_csv(results_path)
            acc_val = res_df[res_df['depth'] == depth]['accuracy'].values[0]
            metadata['accuracy'] = float(acc_val)
        except Exception:
            pass

        # Schema validation (if schema exists)
        if schema:
            # Basic structural validation against expected keys
            required_keys = schema.get('required', [])
            missing = [k for k in required_keys if k not in metadata]
            if missing:
                state_record['validation_status'] = "failed"
                print(f"Warning: Metadata for depth {depth} missing keys: {missing}")
        
        state_record['models'].append(metadata)

    # Write state record
    state_file = STATE_DIR / "tree_training_state.json"
    with open(state_file, 'w') as f:
        json.dump(state_record, f, indent=2)
    
    print(f"State updated at {state_file}")


def main():
    config = get_config()
    
    print("Loading dataset...")
    df = load_dataset()
    
    print("Splitting data...")
    train_df, test_df = split_data(df)
    
    print("Training trees...")
    models, results = train_trees(train_df, test_df)
    
    print("Saving models and results...")
    results_path = save_models_and_results(models, results, config)
    
    print("Validating metadata and updating state...")
    validate_and_update_state(models, results_path, config)
    
    print("Task T024 completed successfully.")


if __name__ == "__main__":
    main()
