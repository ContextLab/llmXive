logger = get_logger(__name__)


def load_embeddings_data() -> torch.Tensor:
    """
    Loads the pre-computed embeddings from the data/processed/embeddings directory.
    Expects a file named 'embeddings.pt' or 'embeddings.npy' generated by T012.
    
    Returns:
        A tensor of shape (N, D) where N is number of subjects and D is embedding dim.
    """
    config = get_config()
    embeddings_path = Path(config["paths"]["processed_data"]) / "embeddings" / "embeddings.pt"
    
    if not embeddings_path.exists():
        # Fallback to a generic location if specific one not found, or raise
        # For robustness in this task, we assume the file exists as per T012
        raise FileNotFoundError(
            f"Embeddings file not found at {embeddings_path}. "
            "Please ensure T012 (Pipeline script) has been run successfully."
        )

    try:
        data = torch.load(embeddings_path, map_location="cpu")
        if isinstance(data, dict) and "embeddings" in data:
            return data["embeddings"]
        return data
    except Exception as e:
        logger.error(f"Failed to load embeddings: {e}")
        raise


def train_autoencoder_for_dimension(
    target_dimension: int,
    output_dir: str,
) -> Dict[str, Any]:
    """
    Trains an autoencoder for a specific target dimension.

    Args:
        target_dimension: The desired latent dimension (e.g., 16, 32, ...).
        output_dir: Directory to save the model checkpoint and logs.

    Returns:
        Dictionary containing training metrics (final loss, convergence status).
    """
    config = get_config()
    seed = config["seeds"]["global_seed"]
    
    # Set seeds for reproducibility
    torch.manual_seed(seed)
    np.random.seed(seed)
    random.seed(seed)

    device = torch.device("cpu") # Enforce CPU as per US2
    
    # Load Data
    logger.info(f"Loading embeddings for dimension sweep...")
    embeddings = load_embeddings_data()
    if embeddings.dim() == 1:
        embeddings = embeddings.unsqueeze(0)
    
    dataset = TensorDataset(embeddings)
    dataloader = DataLoader(dataset, batch_size=config["hyperparameters"]["batch_size"], shuffle=True)

    # Initialize Model
    input_dim = embeddings.shape[1]
    model = Autoencoder(input_dim=input_dim, latent_dim=target_dimension)
    model = model.to(device)

    # Loss and Optimizer
    # Using MSE for reconstruction, but the prompt mentions cosine similarity loss.
    # We will use CosineSimilarity loss as requested in T015 description.
    # Note: CosineSimilarity returns 1 for identical vectors. We want to maximize this.
    # So Loss = 1 - CosineSimilarity.
    criterion = nn.CosineSimilarity(dim=1, eps=1e-6)
    optimizer = optim.Adam(model.parameters(), lr=config["hyperparameters"]["learning_rate"])

    epochs = config["hyperparameters"]["num_epochs"]
    patience = config["hyperparameters"]["early_stopping_patience"]
    
    best_loss = float("inf")
    patience_counter = 0
    training_losses = []

    logger.info(f"Starting training for dimension {target_dimension}...")

    for epoch in range(epochs):
        model.train()
        epoch_loss = 0.0
        
        for batch in dataloader:
            x = batch[0].to(device)
            
            optimizer.zero_grad()
            reconstruction = model(x)
            
            # Calculate Cosine Similarity Loss
            # We want reconstruction to be similar to x
            cos_sim = criterion(x, reconstruction)
            loss = 1.0 - cos_sim.mean()
            
            loss.backward()
            optimizer.step()
            
            epoch_loss += loss.item()

        avg_loss = epoch_loss / len(dataloader)
        training_losses.append(avg_loss)
        
        # Early Stopping Check
        if avg_loss < best_loss:
            best_loss = avg_loss
            patience_counter = 0
            # Save best model state
            best_model_path = os.path.join(output_dir, f"ae_dim_{target_dimension}_best.pt")
            torch.save({
                "epoch": epoch,
                "model_state_dict": model.state_dict(),
                "optimizer_state_dict": optimizer.state_dict(),
                "loss": best_loss,
                "input_dim": input_dim,
                "latent_dim": target_dimension,
            }, best_model_path)
        else:
            patience_counter += 1

        if patience_counter >= patience:
            logger.info(f"Early stopping triggered at epoch {epoch} for dim {target_dimension}")
            break

    # Final Metrics
    final_loss = training_losses[-1] if training_losses else 0.0
    converged = final_loss < 0.1 # Simple convergence heuristic

    metrics = {
        "final_loss": final_loss,
        "best_loss": best_loss,
        "epochs_run": len(training_losses),
        "converged": converged,
        "device": "cpu",
    }

    logger.info(f"Training finished for dim {target_dimension}. Final Loss: {final_loss:.4f}")
    return metrics
