"""
Integration tests for taxonomy_builder.py.
Tests the full flow of loading real taxonomy data and building centroids.
"""
import json
import os
import tempfile
from pathlib import Path

import pytest

from taxonomy_builder import load_taxonomy, build_centroids
from config import get_path


def test_full_centroid_build_integration():
    """
    Integration test: Load the actual taxonomy file generated by T005d
    and verify that centroids are built successfully.
    """
    taxonomy_path = str(get_path("data/raw/taxonomy.json"))
    
    # Skip if taxonomy file doesn't exist yet (T005d not run)
    if not os.path.exists(taxonomy_path):
        pytest.skip(f"Taxonomy file not found at {taxonomy_path}. Run T005d first.")
    
    # Load taxonomy
    taxonomy = load_taxonomy(taxonomy_path)
    assert "categories" in taxonomy
    assert len(taxonomy["categories"]) > 0, "Taxonomy must have categories for integration test"
    
    # Build centroids
    centroids = build_centroids(taxonomy)
    
    # Verify centroids
    assert len(centroids) == len(taxonomy["categories"]), \
        "Number of centroids must match number of categories"
    
    # Verify embedding dimensions (all-MiniLM-L6-v2 uses 384 dimensions)
    sample_embedding = next(iter(centroids.values()))
    assert len(sample_embedding) == 384, \
        f"Embedding dimension should be 384, got {len(sample_embedding)}"
    
    # Verify all values are floats
    for cat_id, emb in centroids.items():
        assert all(isinstance(v, float) for v in emb), \
            f"Centroid for {cat_id} contains non-float values"
    
    print(f"Integration test passed: {len(centroids)} centroids built successfully.")
