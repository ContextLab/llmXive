"""
Generate the final sensitivity summary markdown report.

This script reads the sensitivity sweep report (generated by T043) and the
ablation report (generated by T025b), extracts all Pearson r values, determines
whether every r meets the stability threshold (>= 0.7), and writes a markdown
summary to ``artifacts/reports/sensitivity_summary.md`` that includes the
stability field (``stable`` or ``unstable``).

The script is intended to be executed directly:

    python code/training/generate_sensitivity_summary.py

It can also be imported and the ``generate_summary`` function called
programmatically.
"""

import json
from pathlib import Path
from typing import List, Dict, Any

from utils.config import get_project_root


STABILITY_THRESHOLD = 0.7
SENSITIVITY_REPORT_PATH = Path("artifacts") / "reports" / "sensitivity_report.json"
ABLATION_REPORT_PATH = Path("artifacts") / "reports" / "ablation_report.json"
OUTPUT_MD_PATH = Path("artifacts") / "reports" / "sensitivity_summary.md"


def _load_json(path: Path) -> Dict[str, Any]:
    """Load a JSON file and return its content."""
    if not path.is_file():
        raise FileNotFoundError(f"Required report file not found: {path}")
    with path.open("r", encoding="utf-8") as f:
        return json.load(f)


def _extract_r_values(data: Any) -> List[float]:
    """
    Recursively walk through a JSON‑compatible structure and collect all
    numeric values that look like Pearson correlation coefficients.

    Heuristic:
    - Keys containing ``r`` or ``pearson`` (case‑insensitive) are inspected.
    - Values that are numbers between -1.0 and 1.0 are considered r values.
    """
    r_vals: List[float] = []

    def _walk(obj: Any):
        if isinstance(obj, dict):
            for k, v in obj.items():
                if isinstance(k, str) and ("r" in k.lower() or "pearson" in k.lower()):
                    if isinstance(v, (int, float)) and -1.0 <= float(v) <= 1.0:
                        r_vals.append(float(v))
                _walk(v)
        elif isinstance(obj, list):
            for item in obj:
                _walk(item)

    _walk(data)
    return r_vals


def _format_r_list(r_vals: List[float]) -> str:
    """Create a markdown bullet list of the extracted r values."""
    lines = []
    for idx, r in enumerate(r_vals, start=1):
        lines.append(f"- Pearson r #{idx}: {r:.4f}")
    return "\n".join(lines)


def generate_summary() -> None:
    """
    Generate ``sensitivity_summary.md`` based on the two prerequisite reports.
    The function performs the following steps:

    1. Load ``sensitivity_report.json`` and ``ablation_report.json``.
    2. Extract every Pearson r value from both JSON objects.
    3. Determine stability: ``stable`` if *all* r values are >= 0.7,
       otherwise ``unstable``.
    4. Write a markdown file containing:
       * A header.
       * The list of r values.
       * The stability field.
    """
    # Resolve absolute paths relative to the project root
    root = get_project_root()
    sens_path = root / SENSITIVITY_REPORT_PATH
    ablat_path = root / ABLATION_REPORT_PATH
    out_path = root / OUTPUT_MD_PATH

    # Load the JSON reports
    sens_data = _load_json(sens_path)
    ablat_data = _load_json(ablat_path)

    # Collect all r values
    r_values = _extract_r_values(sens_data) + _extract_r_values(ablat_data)

    if not r_values:
        raise ValueError("No Pearson r values could be extracted from the reports.")

    # Determine stability
    is_stable = all(r >= STABILITY_THRESHOLD for r in r_values)
    stability_str = "stable" if is_stable else "unstable"

    # Build markdown content
    md_content = [
        "# Sensitivity Summary Report",
        "",
        "## Extracted Pearson r values",
        _format_r_list(r_values),
        "",
        "## Stability Assessment",
        f"**stability:** `{stability_str}`",
        "",
        f"*All {len(r_values)} Pearson r values {'meet' if is_stable else 'do not meet'} the "
        f"threshold of {STABILITY_THRESHOLD:.2f}.*",
        "",
    ]

    # Ensure the target directory exists
    out_path.parent.mkdir(parents=True, exist_ok=True)

    # Write the markdown file
    out_path.write_text("\n".join(md_content), encoding="utf-8")
    print(f"Sensitivity summary written to: {out_path}")


def main() -> None:
    """Entry‑point for command‑line execution."""
    generate_summary()


if __name__ == "__main__":
    main()