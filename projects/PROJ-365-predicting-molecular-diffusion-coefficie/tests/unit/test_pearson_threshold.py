"""
Unit test for the Pearson correlation hypothesis status in the evaluation report.

This test loads the JSON file generated by the evaluation step
(`artifacts/reports/evaluation.json`) and verifies that the
``hypothesis_status`` field correctly reflects the value of ``pearson_r``
according to the specification:

- ``pearson_r > 0.7``   → ``positive``
- ``pearson_r < 0.3``   → ``null``
- otherwise            → ``inconclusive``
"""

import json
from pathlib import Path

import pytest


@pytest.fixture(scope="module")
def evaluation_path() -> Path:
    """Path to the evaluation JSON report."""
    return Path("artifacts") / "reports" / "evaluation.json"


def _expected_status(pearson_r: float) -> str:
    """Determine the expected hypothesis status from a Pearson r value."""
    if pearson_r > 0.7:
        return "positive"
    if pearson_r < 0.3:
        return "null"
    return "inconclusive"


def test_hypothesis_status_matches_pearson(evaluation_path: Path):
    """
    Load the evaluation report and assert that the ``hypothesis_status``
    matches the rule derived from ``pearson_r``.
    """
    # Ensure the evaluation file exists; if not, the test should fail with a clear message.
    assert evaluation_path.is_file(), f"Evaluation report not found at {evaluation_path}"

    # Load the JSON content.
    with evaluation_path.open("r", encoding="utf-8") as f:
        data = json.load(f)

    # Basic schema checks.
    assert "pearson_r" in data, "Missing 'pearson_r' in evaluation report"
    assert "hypothesis_status" in data, "Missing 'hypothesis_status' in evaluation report"

    pearson_r = data["pearson_r"]
    # Ensure the value is a number.
    assert isinstance(pearson_r, (int, float)), "'pearson_r' must be numeric"

    # Compute the expected status and compare.
    expected = _expected_status(float(pearson_r))
    actual = data["hypothesis_status"]
    assert (
        actual == expected
    ), f"Hypothesis status mismatch: expected '{expected}', got '{actual}' (pearson_r={pearson_r})"