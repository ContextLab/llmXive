"""
Unit test for the outlier analysis report.

This test verifies that the JSON report generated by
`code/training/robustness.py` (which writes to
``artifacts/reports/outlier_analysis.json``) contains the Pearson
correlation coefficients for both the full dataset and the trimmed
dataset (i.e. after removing the top residuals).

The test does **not** generate the report itself; it simply checks
that the file exists and that the expected keys are present with
numeric values.
"""

import json
from pathlib import Path

def _find_project_root() -> Path:
    """
    Walk up the directory tree until the repository root is found.
    The repository root is identified by the presence of a ``code``
    directory.
    """
    cur = Path(__file__).resolve()
    for parent in cur.parents:
        if (parent / "code").is_dir():
            return parent
    raise RuntimeError("Project root could not be determined.")

def test_outlier_analysis_contains_full_and_trimmed_r():
    """
    Ensure the outlier analysis JSON contains both a full‑set Pearson r
    and a trimmed‑set Pearson r.
    """
    project_root = _find_project_root()
    json_path = (
        project_root
        / "artifacts"
        / "reports"
        / "outlier_analysis.json"
    )

    # The report must exist; otherwise the pipeline step that creates it
    # has not run.
    assert json_path.is_file(), f"{json_path} does not exist."

    # Load the JSON content.
    with json_path.open("r", encoding="utf-8") as fh:
        report = json.load(fh)

    # Expected key names – these are the names used by the robustness
    # implementation (see ``code/training/robustness.py``).
    full_key = "full_set_r"
    trimmed_key = "trimmed_set_r"

    # Verify that both keys are present.
    assert full_key in report, f"Key '{full_key}' missing from outlier analysis report."
    assert trimmed_key in report, f"Key '{trimmed_key}' missing from outlier analysis report."

    # Verify that the associated values are numeric (int or float).
    for key in (full_key, trimmed_key):
        value = report[key]
        assert isinstance(value, (int, float)), f"Value for '{key}' must be numeric, got {type(value)}."