"""
Contract test for the evaluation report generated by the pipeline.

This test verifies that when the data source is marked as "real" in
``data/data_source_flag.json``, the evaluation report ``artifacts/reports/evaluation.json``
exists and contains the required ``hypothesis_status`` field.

The test is deliberately lightweight and does not depend on the actual
contents of the evaluation metrics – it only checks for the presence of
the field as required by the contract.
"""

import json
from pathlib import Path

import pytest

# Import the helper that returns the absolute project root.
# This function is defined in ``code/utils/config.py`` and is part of the
# public API surface.
from utils.config import get_project_root


@pytest.fixture(scope="module")
def project_root() -> Path:
    """Return the absolute path to the repository root."""
    return get_project_root()


def test_evaluation_report_contract(project_root: Path):
    """
    Contract test for ``artifacts/reports/evaluation.json``.

    - Reads ``data/data_source_flag.json`` to determine the data source.
    - If the source is ``real``, asserts that the evaluation JSON file exists.
    - Loads the JSON and asserts that the key ``hypothesis_status`` is present.
    - If the source is ``synthetic`` (or any other value), the test is skipped.
    """
    # Path to the flag file created by ``code/ingestion/flag_source.py``.
    flag_path = project_root / "data" / "data_source_flag.json"

    # If the flag file does not exist we cannot make a decision – fail fast.
    assert flag_path.is_file(), f"Flag file not found at {flag_path!s}"

    with flag_path.open("r", encoding="utf-8") as f:
        flag_data = json.load(f)

    data_source = flag_data.get("source", "").lower()
    if data_source != "real":
        pytest.skip(
            f"Data source is '{data_source}'. Evaluation report is not generated for synthetic data."
        )

    # Path to the evaluation report generated by ``code/training/evaluate.py``.
    evaluation_path = (
        project_root / "artifacts" / "reports" / "evaluation.json"
    )

    assert evaluation_path.is_file(), (
        f"Evaluation report not found at {evaluation_path!s} "
        "when data source is real."
    )

    # Load the JSON and verify the required field.
    with evaluation_path.open("r", encoding="utf-8") as f:
        evaluation_json = json.load(f)

    assert (
        "hypothesis_status" in evaluation_json
    ), "Evaluation JSON must contain the 'hypothesis_status' field."

    # Optional sanity check: the field should be one of the allowed values.
    valid_statuses = {"positive", "null", "inconclusive"}
    status = evaluation_json["hypothesis_status"]
    assert (
        status in valid_statuses
    ), f"Invalid hypothesis_status '{status}'. Expected one of {valid_statuses}."