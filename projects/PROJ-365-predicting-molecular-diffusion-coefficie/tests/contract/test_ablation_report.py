"""
Contract test for the ablation report generated by
``code/training/ablation_study.py``.

The test verifies that the JSON file exists, contains the required
top‑level fields, and that the numeric values are indeed numbers.
"""

import json
from pathlib import Path

from utils.config import get_project_root

def test_ablation_report_schema():
    """Ensure the ablation report matches the expected schema."""
    report_path = (
        get_project_root()
        / "artifacts"
        / "reports"
        / "ablation_report.json"
    )
    assert report_path.is_file(), f"Missing report: {report_path}"

    with report_path.open("r", encoding="utf-8") as fh:
        data = json.load(fh)

    # Required top‑level keys
    required_keys = {
        "gnn_variation_delta",
        "baseline_variation_delta",
        "full_model",
        "ablated_model",
    }
    assert required_keys.issubset(data.keys()), "Missing required keys"

    # Verify sub‑objects contain the correlation values
    for sub in ("full_model", "ablated_model"):
        assert isinstance(data[sub], dict), f"{sub} must be a dict"
        for corr_key in ("gnn_pearson_r", "baseline_pearson_r"):
            assert corr_key in data[sub], f"{corr_key} missing in {sub}"
            assert isinstance(data[sub][corr_key], (int, float)), (
                f"{corr_key} in {sub} must be numeric"
            )

    # Verify the variation deltas are numeric
    assert isinstance(data["gnn_variation_delta"], (int, float))
    assert isinstance(data["baseline_variation_delta"], (int, float))