import os
import sys
import json
import pandas as pd
import numpy as np
from typing import Dict, List, Any

def load_calibration_report(report_path: str) -> pd.DataFrame:
    """
    Load the calibration report CSV generated by T024.
    Expected columns: method, ece, interval_score, sharpness (and potentially others).
    """
    if not os.path.exists(report_path):
        raise FileNotFoundError(f"Calibration report not found at {report_path}. "
                                "Ensure T024 has been executed successfully.")
    df = pd.read_csv(report_path)
    required_cols = ['method', 'ece', 'interval_score']
    missing = [c for c in required_cols if c not in df.columns]
    if missing:
        raise ValueError(f"Calibration report missing required columns: {missing}")
    return df

def rank_methods_by_ece(df: pd.DataFrame) -> pd.DataFrame:
    """
    Rank methods primarily by ECE (lower is better).
    Returns a new DataFrame with a 'rank' column.
    """
    # Sort by ECE ascending
    ranked = df.sort_values(by='ece', ascending=True).reset_index(drop=True)
    ranked['rank'] = range(1, len(ranked) + 1)
    return ranked

def rank_methods_by_interval_score(df: pd.DataFrame) -> pd.DataFrame:
    """
    Rank methods primarily by Interval Score (lower is better).
    Returns a new DataFrame with a 'rank' column.
    """
    ranked = df.sort_values(by='interval_score', ascending=True).reset_index(drop=True)
    ranked['rank'] = ranked.index + 1
    return ranked

def determine_best_method(ranked_df: pd.DataFrame) -> Dict[str, Any]:
    """
    Identify the best performing method based on the primary metric (ECE).
    Returns a dictionary with the best method's details.
    """
    if ranked_df.empty:
        return {}
    
    best_row = ranked_df.iloc[0]
    return {
        "best_method": best_row['method'],
        "ece": float(best_row['ece']),
        "interval_score": float(best_row['interval_score']),
        "rank": int(best_row['rank'])
    }

def generate_ranking_report(ranked_df: pd.DataFrame, output_path: str) -> None:
    """
    Save the ranking results to a CSV file.
    """
    os.makedirs(os.path.dirname(output_path), exist_ok=True)
    ranked_df.to_csv(output_path, index=False)
    print(f"Ranking report saved to {output_path}")

def main():
    """
    Main entry point for T025: Implement ranking logic.
    Reads results/calibration_report.csv, ranks methods by ECE,
    identifies the best method, and saves results to results/ranking_report.csv.
    """
    # Paths relative to project root
    project_root = os.path.dirname(os.path.dirname(os.path.dirname(os.path.abspath(__file__))))
    calibration_report_path = os.path.join(project_root, "results", "calibration_report.csv")
    ranking_output_path = os.path.join(project_root, "results", "ranking_report.csv")
    best_method_output_path = os.path.join(project_root, "results", "best_method.json")

    print(f"Loading calibration report from {calibration_report_path}...")
    df = load_calibration_report(calibration_report_path)

    print(f"Found {len(df)} methods to rank.")
    print(df[['method', 'ece', 'interval_score']].to_string())

    # Rank by ECE (primary metric for calibration)
    ranked_by_ece = rank_methods_by_ece(df)
    
    # Determine the best method
    best_method_info = determine_best_method(ranked_by_ece)
    
    if best_method_info:
        print(f"\nBest performing method by ECE: {best_method_info['best_method']}")
        print(f"  ECE: {best_method_info['ece']:.6f}")
        print(f"  Interval Score: {best_method_info['interval_score']:.6f}")
        
        # Save best method info to JSON
        os.makedirs(os.path.dirname(best_method_output_path), exist_ok=True)
        with open(best_method_output_path, 'w') as f:
            json.dump(best_method_info, f, indent=2)
        print(f"Best method details saved to {best_method_output_path}")
    else:
        print("No methods found to rank.")

    # Save full ranking report
    generate_ranking_report(ranked_by_ece, ranking_output_path)

    # Also rank by Interval Score for reference (optional but useful)
    ranked_by_score = rank_methods_by_interval_score(df)
    ranking_score_path = os.path.join(project_root, "results", "ranking_by_interval_score.csv")
    generate_ranking_report(ranked_by_score, ranking_score_path)

    print("\nRanking logic complete.")

if __name__ == "__main__":
    main()