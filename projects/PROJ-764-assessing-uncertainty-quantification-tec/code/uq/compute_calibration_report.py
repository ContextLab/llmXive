"""
T024: Compute final metrics and save to results/calibration_report.csv.

This script consumes the predictions generated by the UQ pipeline (results/uq_predictions.csv)
and the uncertainty decomposition report (results/uncertainty_decomposition.csv) to compute
the final calibration metrics (ECE, Interval Score, Sharpness) for each method.

It outputs:
- results/calibration_report.csv: Summary metrics per method.
"""
import os
import sys
import logging
import json
import pandas as pd
import numpy as np
from pathlib import Path

# Add project root to path to ensure imports work when run as script
PROJECT_ROOT = Path(__file__).resolve().parent.parent.parent
if str(PROJECT_ROOT) not in sys.path:
    sys.path.insert(0, str(PROJECT_ROOT))

from code.uq.metrics import expected_calibration_error, interval_score, sharpness

# Setup logging
logging.basicConfig(
    level=logging.INFO,
    format='%(asctime)s - %(levelname)s - %(message)s',
    handlers=[
        logging.FileHandler(PROJECT_ROOT / 'logs' / 'pipeline.log', mode='a'),
        logging.StreamHandler(sys.stdout)
    ]
)
logger = logging.getLogger(__name__)

def load_predictions():
    """Load the UQ predictions from the pipeline output."""
    predictions_path = PROJECT_ROOT / 'results' / 'uq_predictions.csv'
    if not predictions_path.exists():
        raise FileNotFoundError(f"Predictions file not found: {predictions_path}")
    
    logger.info(f"Loading predictions from {predictions_path}")
    df = pd.read_csv(predictions_path)
    
    # Validate expected columns
    required_cols = ['sample_id', 'method', 'prediction', 'variance', 'lower_50', 'upper_50', 
                     'lower_90', 'upper_90', 'actual_value', 'uncertainty_type']
    missing_cols = [c for c in required_cols if c not in df.columns]
    if missing_cols:
        raise ValueError(f"Missing required columns in predictions: {missing_cols}")
    
    return df

def compute_metrics_for_method(df, method_name):
    """Compute ECE, Interval Score, and Sharpness for a specific method."""
    logger.info(f"Computing metrics for method: {method_name}")
    
    subset = df[df['method'] == method_name].copy()
    
    if subset.empty:
        logger.warning(f"No data found for method {method_name}")
        return None

    # Extract necessary arrays
    predictions = subset['prediction'].values
    actuals = subset['actual_value'].values
    variances = subset['variance'].values
    
    # 1. Expected Calibration Error (ECE)
    # We use the 90% interval bounds for calibration check
    lower_90 = subset['lower_90'].values
    upper_90 = subset['upper_90'].values
    
    ece = expected_calibration_error(
        predictions=predictions,
        actuals=actuals,
        lower_bounds=lower_90,
        upper_bounds=upper_90,
        confidence_level=0.90
    )
    
    # 2. Interval Score (for 90% interval)
    # Interval Score penalizes width and misses
    is_90 = interval_score(
        lower=lower_90,
        upper=upper_90,
        actual=actuals,
        alpha=0.10  # 90% interval implies alpha=0.10
    )
    
    # 3. Sharpness (mean width of the 90% interval)
    sharp = sharpness(
        lower=lower_90,
        upper=upper_90
    )
    
    # 4. Mean Squared Error (MSE) for prediction accuracy context
    mse = np.mean((predictions - actuals) ** 2)
    
    # 5. Mean Variance (from model)
    mean_var = np.mean(variances)
    
    return {
        'method': method_name,
        'ece_90': float(ece),
        'interval_score_90': float(is_90),
        'sharpness_90': float(sharp),
        'mse': float(mse),
        'mean_variance': float(mean_var),
        'n_samples': len(subset)
    }

def main():
    """Main entry point for T024."""
    logger.info("Starting T024: Compute final metrics and save calibration report")
    
    try:
        # Load data
        df = load_predictions()
        
        # Identify unique methods
        methods = df['method'].unique()
        logger.info(f"Found methods: {methods}")
        
        results = []
        for method in methods:
            metrics = compute_metrics_for_method(df, method)
            if metrics:
                results.append(metrics)
        
        if not results:
            raise RuntimeError("No metrics computed for any method.")
        
        # Create DataFrame and sort by ECE (lower is better)
        report_df = pd.DataFrame(results)
        report_df = report_df.sort_values(by='ece_90', ascending=True)
        
        # Save to CSV
        output_path = PROJECT_ROOT / 'results' / 'calibration_report.csv'
        output_path.parent.mkdir(parents=True, exist_ok=True)
        
        report_df.to_csv(output_path, index=False)
        logger.info(f"Successfully saved calibration report to {output_path}")
        
        # Print summary
        logger.info("Calibration Report Summary:")
        print(report_df.to_string(index=False))
        
        return 0
        
    except Exception as e:
        logger.error(f"Error computing calibration metrics: {e}", exc_info=True)
        return 1

if __name__ == "__main__":
    sys.exit(main())