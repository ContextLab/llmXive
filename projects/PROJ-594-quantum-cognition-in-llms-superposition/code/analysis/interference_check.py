"""
Interference Check Analysis (Task T036)

Validates the graded negative correlation between ambiguity scores and
interference cross-term values using Spearman rank correlation.

Input: Real data from data/results/interference_validation.json (produced by T025)
Output: data/results/interference_correlation.json
"""
import os
import sys
import json
import argparse
import torch
from scipy.stats import spearmanr

# Project root relative to this file
PROJECT_ROOT = os.path.abspath(os.path.join(os.path.dirname(__file__), "..", ".."))
DATA_DIR = os.path.join(PROJECT_ROOT, "data", "results")
INPUT_FILE = os.path.join(DATA_DIR, "interference_validation.json")
OUTPUT_FILE = os.path.join(DATA_DIR, "interference_correlation.json")

def load_interference_data(input_path: str) -> list:
    """
    Load the interference validation data generated by T025.
    Expected structure: {"samples": [{"ambiguity_score": float, "cross_term_value": float}, ...]}
    """
    if not os.path.exists(input_path):
        raise FileNotFoundError(
            f"Input file not found: {input_path}. "
            "Please ensure T025 (verify_interference.py) has been run successfully first."
        )

    with open(input_path, "r", encoding="utf-8") as f:
        data = json.load(f)

    if "samples" not in data or not isinstance(data["samples"], list):
        raise ValueError(f"Invalid data format in {input_path}. Expected 'samples' list.")

    samples = []
    for item in data["samples"]:
        if "ambiguity_score" in item and "cross_term_value" in item:
            samples.append({
                "ambiguity_score": float(item["ambiguity_score"]),
                "cross_term_value": float(item["cross_term_value"])
            })

    if not samples:
        raise ValueError(f"No valid samples found in {input_path}.")

    return samples

def compute_spearman_correlation(samples: list) -> dict:
    """
    Compute Spearman rank correlation between ambiguity_score and cross_term_value.
    Returns a dictionary with correlation coefficient, p-value, and sample count.
    """
    ambiguities = [s["ambiguity_score"] for s in samples]
    cross_terms = [s["cross_term_value"] for s in samples]

    # Convert to torch tensors for consistency, though scipy handles lists
    # Using scipy directly for statistical test
    correlation, p_value = spearmanr(ambiguities, cross_terms)

    return {
        "correlation_coefficient": float(correlation),
        "p_value": float(p_value),
        "sample_count": len(samples),
        "method": "Spearman Rank Correlation",
        "hypothesis": "Negative correlation expected (higher ambiguity -> more negative cross-term)"
    }

def save_results(results: dict, output_path: str):
    """Save results to JSON file."""
    os.makedirs(os.path.dirname(output_path), exist_ok=True)
    with open(output_path, "w", encoding="utf-8") as f:
        json.dump(results, f, indent=2)
    print(f"Results saved to {output_path}")

def main():
    parser = argparse.ArgumentParser(description="Check interference correlation with ambiguity.")
    parser.add_argument("--input", type=str, default=INPUT_FILE, help="Path to interference validation JSON")
    parser.add_argument("--output", type=str, default=OUTPUT_FILE, help="Path to output correlation JSON")
    args = parser.parse_args()

    print(f"Loading data from {args.input}...")
    try:
        samples = load_interference_data(args.input)
        print(f"Loaded {len(samples)} samples.")
    except Exception as e:
        print(f"ERROR: Failed to load data: {e}", file=sys.stderr)
        sys.exit(1)

    print("Computing Spearman rank correlation...")
    results = compute_spearman_correlation(samples)

    # Add metadata
    results["input_file"] = args.input
    results["output_file"] = args.output

    # Interpretation based on Feynman's "arrows" concept:
    # Negative correlation supports the quantum interference hypothesis where
    # ambiguous contexts lead to destructive interference (negative cross-term).
    rho = results["correlation_coefficient"]
    p_val = results["p_value"]

    print(f"\n--- Results ---")
    print(f"Spearman Correlation (rho): {rho:.4f}")
    print(f"P-value: {p_val:.4e}")
    print(f"Samples: {results['sample_count']}")

    if rho < 0 and p_val < 0.05:
        print("RESULT: Significant negative correlation found. Supports quantum interference hypothesis.")
    elif rho < 0:
        print("RESULT: Negative trend found, but not statistically significant at alpha=0.05.")
    else:
        print("RESULT: No significant negative correlation found.")

    save_results(results, args.output)

if __name__ == "__main__":
    main()
