"""
Contract tests for data schema validation.

This module validates that the processed data produced by the ingestion pipeline
conforms to the expected schema required for downstream modeling. Specifically,
it ensures that records missing critical 'deferral' flags are excluded from the dataset.

Referencing T014: Contract test for data schema validation (missing deferral flags excluded).
"""

import os
import sys
import json
import pandas as pd
import pytest
from pathlib import Path

# Add project root to path for imports if running from test directory
project_root = Path(__file__).parent.parent.parent
sys.path.insert(0, str(project_root))

from config import get_path

# --------------------------------------------------------------------------
# Schema Definitions
# --------------------------------------------------------------------------

REQUIRED_COLUMNS = {
    "participant_id",
    "trial_id",
    "deferral",
    "regret_proxy",
    "option_count",
    "perceived_risk",
    "potential_loss_magnitude",
}

# Columns that MUST NOT be null/NaN for valid modeling
CRITICAL_COLUMNS = {
    "participant_id",
    "trial_id",
    "deferral",
    "regret_proxy",
}

# Valid values for the deferral flag
VALID_DEFERRAL_VALUES = {0, 1}

# --------------------------------------------------------------------------
# Helper Functions
# --------------------------------------------------------------------------

def get_processed_data_path():
    """
    Returns the path to the processed dataset generated by US1.
    Expected path: data/processed/regret_proxy_v1.csv
    """
    return get_path("data/processed/regret_proxy_v1.csv")

def load_processed_data():
    """
    Loads the processed dataset. Raises FileNotFoundError if the file
    does not exist yet (indicating US1 has not completed).
    """
    path = get_processed_data_path()
    if not os.path.exists(path):
        raise FileNotFoundError(
            f"Processed data file not found at {path}. "
            "Ensure User Story 1 (T015-T019) has been executed successfully."
        )
    return pd.read_csv(path)

# --------------------------------------------------------------------------
# Contract Tests
# --------------------------------------------------------------------------

class TestDataSchemaContract:
    """
    Contract tests ensuring the processed data schema matches requirements.
    """

    def test_file_exists(self):
        """Contract: The processed data file must exist."""
        path = get_processed_data_path()
        assert os.path.exists(path), f"Processed data file missing: {path}"

    def test_required_columns_present(self):
        """
        Contract: All required columns must be present in the dataset.
        Missing columns indicate a failure in the ingestion or feature engineering pipeline.
        """
        df = load_processed_data()
        missing = REQUIRED_COLUMNS - set(df.columns)
        assert not missing, (
            f"Schema violation: Missing required columns: {missing}. "
            f"Found columns: {list(df.columns)}"
        )

    def test_critical_columns_not_null(self):
        """
        Contract: Critical columns (especially 'deferral') must not contain nulls.
        Rows with missing deferral flags must have been excluded during ingestion (T019).
        """
        df = load_processed_data()
        for col in CRITICAL_COLUMNS:
            null_count = df[col].isna().sum()
            assert null_count == 0, (
                f"Schema violation: Column '{col}' contains {null_count} null values. "
                "Critical columns must be non-null."
            )

    def test_deferral_values_valid(self):
        """
        Contract: The 'deferral' column must only contain valid binary values (0 or 1).
        """
        df = load_processed_data()
        invalid_values = set(df["deferral"].unique()) - VALID_DEFERRAL_VALUES
        assert not invalid_values, (
            f"Schema violation: 'deferral' column contains invalid values: {invalid_values}. "
            f"Expected only {VALID_DEFERRAL_VALUES}."
        )

    def test_no_missing_deferral_flags(self):
        """
        Contract: Explicitly verify that no rows were retained with missing deferral flags.
        This validates the filtering logic implemented in T017/T019.
        """
        df = load_processed_data()
        # Double check: count rows where deferral is missing (redundant with null check but explicit)
        missing_deferral = df["deferral"].isna().sum()
        assert missing_deferral == 0, (
            f"Contract failed: Found {missing_deferral} rows with missing deferral flags. "
            "All rows must have a valid deferral flag (0 or 1)."
        )

    def test_regret_proxy_non_negative(self):
        """
        Contract: The 'regret_proxy' (Min-Max Regret) must be non-negative.
        Regret is defined as opportunity cost, which cannot be negative.
        """
        df = load_processed_data()
        negative_regret = (df["regret_proxy"] < 0).sum()
        assert negative_regret == 0, (
            f"Schema violation: Found {negative_regret} rows with negative regret_proxy values."
        )

    def test_option_count_positive(self):
        """
        Contract: 'option_count' must be at least 1 (a decision context must have options).
        """
        df = load_processed_data()
        invalid_options = (df["option_count"] < 1).sum()
        assert invalid_options == 0, (
            f"Schema violation: Found {invalid_options} rows with option_count < 1."
        )