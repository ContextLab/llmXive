def validate_type(value, expected_type):
    """Helper to validate types, handling numpy types."""
    if isinstance(expected_type, tuple):
        return isinstance(value, expected_type)
    return isinstance(value, expected_type)

def validate_dataframe(df, schema):
    """Validate a DataFrame against a schema definition."""
    assert isinstance(df, pd.DataFrame), f"Expected DataFrame, got {type(df)}"
    
    # Check required columns
    missing_cols = set(schema["required_columns"]) - set(df.columns)
    assert not missing_cols, f"DataFrame missing columns: {missing_cols}"
    
    # Check column types
    for col, expected_type in schema["column_types"].items():
        if col in df.columns:
            # Check a sample value to infer type compatibility
            sample_val = df[col].iloc[0] if len(df) > 0 else None
            if sample_val is not None:
                assert validate_type(sample_val, expected_type), \
                    f"Column '{col}' has invalid type. Expected {expected_type}, got {type(sample_val)}"

def validate_dict(d, schema, path="root"):
    """Validate a dictionary against a schema definition."""
    assert isinstance(d, dict), f"{path}: Expected dict, got {type(d)}"
    
    # Check required keys
    missing_keys = set(schema["required_keys"]) - set(d.keys())
    assert not missing_keys, f"{path}: Missing keys: {missing_keys}"
    
    # Check key types
    for key, expected_type in schema.get("key_types", {}).items():
        if key in d:
            val = d[key]
            assert validate_type(val, expected_type), \
                f"{path}.{key}: Expected {expected_type}, got {type(val)}"

