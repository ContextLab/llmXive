"""
Disclaimer Injector for Surrogate Model Results.

This module adds an explicit "Surrogate Model" disclaimer to the JSON result files
generated by the training and generalization evaluation pipelines.
"""
import os
import json
import logging
import argparse
from pathlib import Path
from typing import Dict, Any, Optional

from utils.logger import get_logger

logger = get_logger(__name__)

DISCLAIMER_TEXT = (
    "These results are ML interpolations of DFT data, not first-principles solutions."
)

def load_json_file(path: Path) -> Dict[str, Any]:
    """Load a JSON file, returning an empty dict if it doesn't exist."""
    if not path.exists():
        logger.warning(f"File not found: {path}. Creating empty structure.")
        return {}
    try:
        with open(path, "r", encoding="utf-8") as f:
            return json.load(f)
    except json.JSONDecodeError as e:
        logger.error(f"Failed to parse JSON in {path}: {e}")
        raise

def save_json_file(path: Path, data: Dict[str, Any]) -> None:
    """Save data to a JSON file with pretty printing."""
    path.parent.mkdir(parents=True, exist_ok=True)
    with open(path, "w", encoding="utf-8") as f:
        json.dump(data, f, indent=2)
    logger.info(f"Saved updated JSON to {path}")

def add_disclaimer(data: Dict[str, Any], metadata_key: str = "metadata") -> Dict[str, Any]:
    """
    Add or update the 'metadata' key with the surrogate model disclaimer.

    Args:
        data: The loaded JSON data.
        metadata_key: The key to use for metadata (default: 'metadata').

    Returns:
        The updated data dictionary.
    """
    if metadata_key not in data:
        data[metadata_key] = {}

    data[metadata_key]["disclaimer"] = DISCLAIMER_TEXT
    data[metadata_key]["model_type"] = "Surrogate"
    data[metadata_key]["methodology"] = "ML interpolation of pre-computed DFT results"

    logger.info(f"Added disclaimer to {metadata_key}")
    return data

def process_result_file(file_path: Path) -> bool:
    """
    Process a single result file: load, add disclaimer, save.

    Args:
        file_path: Path to the JSON result file.

    Returns:
        True if successful, False otherwise.
    """
    try:
        data = load_json_file(file_path)
        updated_data = add_disclaimer(data)
        save_json_file(file_path, updated_data)
        return True
    except Exception as e:
        logger.error(f"Failed to process {file_path}: {e}")
        return False

def run_disclaimer_injection(
    training_logs_path: Optional[Path] = None,
    generalization_metrics_path: Optional[Path] = None,
) -> None:
    """
    Inject disclaimers into the specified result files.

    Args:
        training_logs_path: Path to training_logs.json.
        generalization_metrics_path: Path to generalization_metrics.json.
    """
    base_dir = Path("data/results")

    if training_logs_path is None:
        training_logs_path = base_dir / "training_logs.json"
    if generalization_metrics_path is None:
        generalization_metrics_path = base_dir / "generalization_metrics.json"

    files_to_process = [
        training_logs_path,
        generalization_metrics_path,
    ]

    success_count = 0
    for file_path in files_to_process:
        if process_result_file(file_path):
            success_count += 1

    logger.info(f"Disclaimer injection complete. Processed {success_count}/{len(files_to_process)} files.")

    if success_count < len(files_to_process):
        logger.warning("Some files could not be processed. Check logs for details.")

def main() -> None:
    """CLI entry point for disclaimer injection."""
    parser = argparse.ArgumentParser(
        description="Add surrogate model disclaimer to result JSON files."
    )
    parser.add_argument(
        "--training-logs",
        type=Path,
        default=None,
        help="Path to training_logs.json (default: data/results/training_logs.json)",
    )
    parser.add_argument(
        "--generalization-metrics",
        type=Path,
        default=None,
        help="Path to generalization_metrics.json (default: data/results/generalization_metrics.json)",
    )

    args = parser.parse_args()

    run_disclaimer_injection(
        training_logs_path=args.training_logs,
        generalization_metrics_path=args.generalization_metrics,
    )

if __name__ == "__main__":
    main()