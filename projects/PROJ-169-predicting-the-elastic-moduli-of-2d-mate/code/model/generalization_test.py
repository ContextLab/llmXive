def load_json(path: Path) -> Dict[str, Any]:
    """Load a JSON file."""
    with open(path, 'r', encoding='utf-8') as f:
        return json.load(f)

def save_json(path: Path, data: Dict[str, Any]) -> None:
    """Save a dictionary to a JSON file."""
    path.parent.mkdir(parents=True, exist_ok=True)
    with open(path, 'w', encoding='utf-8') as f:
        json.dump(data, f, indent=2)

def verify_family_disjoint(train_families: Set[str], test_families: Set[str]) -> bool:
    """
    Verify that no family appears in both train and test sets.
    Returns True if disjoint, False otherwise.
    """
    intersection = train_families.intersection(test_families)
    if intersection:
        logging.error(f"Family overlap detected: {intersection}")
        return False
    return True

def load_graphs_from_parquet(path: Path) -> List[Dict[str, Any]]:
    """
    Load graphs from the parquet file generated by T013.
    Returns a list of graph dictionaries.
    """
    try:
        import pandas as pd
        df = pd.read_parquet(path)
        # Convert to list of dicts for easier handling
        return df.to_dict('records')
    except Exception as e:
        logging.error(f"Failed to load parquet file {path}: {e}")
        raise

def build_family_mapping(graphs: List[Dict[str, Any]]) -> Dict[str, List[int]]:
    """
    Build a mapping from family_id to list of graph indices.
    """
    family_map = {}
    for idx, graph in enumerate(graphs):
        fid = str(graph.get('family_id', 'unknown'))
        if fid not in family_map:
            family_map[fid] = []
        family_map[fid].append(idx)
    return family_map

def run_generalization_test(
    graphs: List[Dict[str, Any]],
    test_indices: List[int],
    model_path: Path,
    output_path: Path,
    config: Config
) -> Dict[str, Any]:
    """
    Run the inter-family generalization test.
    
    1. Load the trained model.
    2. Identify test families and training families.
    3. Verify family disjointness.
    4. Calculate intra-family MAPE (baseline from T020a if available, else compute).
    5. Calculate inter-family MAPE on the test set.
    6. Output results to JSON.
    """
    logger = get_logger("generalization_test")
    
    # 1. Load Model
    logger.info(f"Loading model from {model_path}")
    device = torch.device('cpu')
    model = create_model(config)
    if model_path.exists():
        state = torch.load(model_path, map_location=device, weights_only=True)
        model.load_state_dict(state)
    else:
        raise FileNotFoundError(f"Model checkpoint not found at {model_path}")
    model.eval()
    model = model.to(device)

    # 2. Identify Families
    family_map = build_family_mapping(graphs)
    
    # Get families for test indices
    test_families = set()
    for idx in test_indices:
        if idx < len(graphs):
          fid = str(graphs[idx].get('family_id', 'unknown'))
          test_families.add(fid)
    
    # Get families for training indices (complement of test)
    all_indices = set(range(len(graphs)))
    train_indices = list(all_indices - set(test_indices))
    train_families = set()
    for idx in train_indices:
        fid = str(graphs[idx].get('family_id', 'unknown'))
        train_families.add(fid)
    
    logger.info(f"Test families: {len(test_families)}, Train families: {len(train_families)}")

    # 3. Verify Disjointness
    if not verify_family_disjoint(train_families, test_families):
        logger.error("Runtime check failed: Training and test families are not disjoint.")
        failure_report = {
            "status": "failed",
            "error": "Family overlap detected between training and test sets.",
            "overlap_families": list(train_families.intersection(test_families)),
            "disclaimer": DISCLAIMER
        }
        save_json(output_path, failure_report)
        return failure_report

    if not test_families:
        logger.error("No test families found.")
        failure_report = {
            "status": "failed",
            "error": "No valid test families found for generalization analysis.",
            "disclaimer": DISCLAIMER
        }
        save_json(output_path, failure_report)
        return failure_report

    # 4. Prepare Data for Evaluation
    # We need to construct PyG Data objects for the test set
    # Assuming the graphs list contains the necessary features in a parseable format
    # This part relies on the output of T013 (parquet) which should have serialized node/edge features
    
    # Helper to convert dict to PyG Data
    def dict_to_pyg(graph_dict: Dict[str, Any]) -> Data:
        # Expecting keys: 'x' (node features), 'edge_index', 'edge_attr', 'y' (target), 'family_id'
        # Adjust based on actual T013 output schema if different
        x = torch.tensor(graph_dict.get('node_features', []), dtype=torch.float)
        edge_index = torch.tensor(graph_dict.get('edge_index', []), dtype=torch.long)
        edge_attr = torch.tensor(graph_dict.get('edge_features', []), dtype=torch.float)
        y = torch.tensor(graph_dict.get('target_moduli', []), dtype=torch.float)
        
        return Data(x=x, edge_index=edge_index, edge_attr=edge_attr, y=y)

    test_graphs_data = [dict_to_pyg(graphs[i]) for i in test_indices]
    test_loader = DataLoader(test_graphs_data, batch_size=32, shuffle=False)

    # 5. Calculate Metrics
    logger.info("Running inference on test set...")
    all_preds = []
    all_targets = []

    with torch.no_grad():
        for batch in test_loader:
            batch = batch.to(device)
            preds = model(batch.x, batch.edge_index, batch.edge_attr, batch.batch)
            all_preds.append(preds.cpu().numpy())
            all_targets.append(batch.y.cpu().numpy())
    
    all_preds = np.concatenate(all_preds, axis=0)
    all_targets = np.concatenate(all_targets, axis=0)

    # Calculate Inter-Family MAPE
    # MAPE = mean(|y - y_hat| / |y|) * 100
    # Handle division by zero
    eps = 1e-8
    abs_targets = np.abs(all_targets)
    mape_inter = np.mean(np.abs(all_targets - all_preds) / (abs_targets + eps)) * 100
    rmse_inter = np.sqrt(np.mean((all_targets - all_preds) ** 2))
    r2_inter = 1 - (np.sum((all_targets - all_preds) ** 2) / (np.sum((all_targets - np.mean(all_targets)) ** 2) + eps))

    logger.info(f"Inter-family MAPE: {mape_inter:.4f}%")

    # 6. Calculate Intra-Family MAPE (Baseline)
    # Since we don't have the T020a result loaded automatically, we simulate the baseline
    # by calculating MAPE if we had trained on the same families (random split within families).
    # However, strictly speaking, T020a should have produced this. 
    # For this script, we will compute a "naive" intra-family baseline by training a simple mean predictor
    # on the train set and evaluating on the test set? No, that's not intra-family.
    # Intra-family means: split within the SAME families.
    # Since our test set is OUTSIDE families, we cannot compute true intra-family MAPE on this specific test set.
    # The requirement says: "Output schema MUST include intra_family_mape, inter_family_mape".
    # We must load the result from T020a (baseline_metrics.py) if it exists, or compute it if we have the logic.
    # Given the dependency T020a, we assume it exists at a standard path or we calculate a proxy.
    
    # Let's try to load the baseline result from T020a
    baseline_path = Path("data/results/baseline_metrics.json")
    intra_mape = None
    if baseline_path.exists():
        try:
            baseline_data = load_json(baseline_path)
            intra_mape = baseline_data.get('mape', None)
            logger.info(f"Loaded intra-family MAPE from baseline: {intra_mape}")
        except Exception as e:
            logger.warning(f"Could not load baseline metrics: {e}")
    
    if intra_mape is None:
        # Fallback: If baseline is missing, we cannot strictly report it without re-running T020a logic.
        # However, the task requires the output. We will set it to null or a computed proxy if possible.
        # For strict compliance, we assume the user ran T020a. If not, we report null.
        logger.warning("Intra-family MAPE not found in baseline_metrics.json. Setting to null.")
        intra_mape = None

    # 7. Construct Output
    result = {
        "status": "success",
        "metrics": {
            "inter_family_mape": float(mape_inter),
            "inter_family_rmse": float(rmse_inter),
            "inter_family_r2": float(r2_inter),
            "intra_family_mape": float(intra_mape) if intra_mape is not None else None
        },
        "families": {
            "test_count": len(test_families),
            "train_count": len(train_families)
        },
        "disclaimer": DISCLAIMER
    }

    save_json(output_path, result)
    logger.info(f"Generalization test results saved to {output_path}")
    return result

