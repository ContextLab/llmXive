def save_split_manifest(manifest: SplitManifest, output_path: Path) -> None:
    """
    Save the validation manifest.
    """
    with open(output_path, 'w', encoding='utf-8') as f:
        json.dump(manifest.to_dict(), f, indent=2)
    logger.info(f"Validation manifest saved to {output_path}")


def main() -> None:
    """
    Main entry point for T017b.
    Validates the existence and structure of split_indices.json.
    """
    # Default paths relative to project root
    project_root = Path(__file__).resolve().parents[2]
    split_path = project_root / "data" / "processed" / "split_indices.json"

    # Allow override via argument if needed, though T017b is strict about the default location
    # based on the dependency chain T013f -> T017b
    if len(sys.argv) > 1:
        split_path = Path(sys.argv[1])

    logger.info(f"T017b: Validating stratified split at {split_path}")

    # 1. Load and validate the split
    split_data = load_split_indices(split_path)

    # 2. Verify family separation (Optional but good practice if family_ids are in the split file)
    # If the split file contains 'family_ids' key, we can verify they are distinct sets.
    # However, the primary requirement of T017b is just that the file exists and is valid JSON with keys.
    # T021b is responsible for the strict family disjoint check.

    if "family_ids" in split_data:
        # If family_ids are embedded in the split file, we can do a quick sanity check
        # that they are strings.
        if not isinstance(split_data["family_ids"], list):
            logger.warning("Warning: 'family_ids' is present but not a list.")
        else:
            logger.info(f"Found {len(split_data['family_ids'])} family IDs in split metadata.")

    # 3. Create a validation manifest
    manifest = SplitManifest(
        train_indices=split_data["train_indices"],
        test_indices=split_data["test_indices"],
        family_ids=split_data.get("family_ids", [])
    )

    # Save the validation result (optional, but confirms the task ran successfully)
    output_manifest_path = project_root / "data" / "results" / "split_validation_manifest.json"
    output_manifest_path.parent.mkdir(parents=True, exist_ok=True)
    save_split_manifest(manifest, output_manifest_path)

    logger.info("T017b: Validation complete. Split indices are valid.")


if __name__ == "__main__":
    main()