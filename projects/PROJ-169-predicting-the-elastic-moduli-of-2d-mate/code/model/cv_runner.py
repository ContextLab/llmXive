def load_graphs_from_parquet(graphs_path: Path) -> List[Data]:
    """
    Load graphs from a Parquet file into PyTorch Geometric Data objects.
    
    This function expects the Parquet file to have been generated by the 
    ingestion pipeline (T013) and contains serialized graph data.
    """
    try:
        import pandas as pd
        df = pd.read_parquet(graphs_path)
    except ImportError:
        raise ImportError("pandas and pyarrow are required to load Parquet files")
    except Exception as e:
        raise RuntimeError(f"Failed to load graphs from {graphs_path}: {e}")

    graphs = []
    for _, row in df.iterrows():
        # Reconstruct PyG Data object from serialized row
        # Expected columns: edge_index (str), x (str), y (str), family (str), material_id (str)
        try:
            edge_index = np.array(json.loads(row['edge_index']), dtype=np.int64)
            x = np.array(json.loads(row['x']), dtype=np.float32)
            y = np.array(json.loads(row['y']), dtype=np.float32)
            
            # Convert to torch tensors
            edge_index_tensor = torch.tensor(edge_index, dtype=torch.long)
            x_tensor = torch.tensor(x, dtype=torch.float)
            y_tensor = torch.tensor(y, dtype=torch.float)
            
            # Create PyG Data object
            graph = Data(x=x_tensor, edge_index=edge_index_tensor, y=y_tensor)
            graph.family = row.get('family', 'unknown')
            graph.material_id = row.get('material_id', 'unknown')
            
            graphs.append(graph)
        except (KeyError, json.JSONDecodeError) as e:
            logging.warning(f"Skipping malformed row: {e}")
            continue

    if not graphs:
        raise ValueError("No valid graphs loaded from Parquet file")

    return graphs

def run_single_fold(
    fold_index: int,
    train_graphs: List[Data],
    val_graphs: List[Data],
    config: Config,
    logger: logging.Logger
) -> FoldResult:
    """
    Train and evaluate a single fold of cross-validation.
    
    Args:
        fold_index: Index of the current fold (0 to k-1)
        train_graphs: List of training graphs for this fold
        val_graphs: List of validation graphs for this fold
        config: Configuration object
        logger: Logger instance
        
    Returns:
        FoldResult object containing metrics for this fold
    """
    start_time = time.time()
    logger.info(f"Starting fold {fold_index + 1}/{config.cv_k_folds}")
    
    # Set seed for reproducibility within this fold
    fold_seed = config.seed + fold_index
    np.random.seed(fold_seed)
    torch.manual_seed(fold_seed)
    if torch.cuda.is_available():
        torch.cuda.manual_seed_all(fold_seed)
    
    # Create data loaders
    train_loader = DataLoader(train_graphs, batch_size=config.batch_size, shuffle=True)
    val_loader = DataLoader(val_graphs, batch_size=config.batch_size, shuffle=False)
    
    # Initialize model
    model = create_model(
        node_dim=train_graphs[0].x.shape[1],
        edge_dim=0,  # Edge features not used in current implementation
        hidden_dim=config.hidden_dim,
        output_dim=3,  # Young's, Shear, Poisson
        num_layers=config.num_layers
    )
    
    device = torch.device('cpu')  # CPU-only as per constraints
    model = model.to(device)
    
    criterion = torch.nn.MSELoss()
    optimizer = torch.optim.Adam(model.parameters(), lr=config.learning_rate)
    
    # Training loop
    best_val_loss = float('inf')
    patience_counter = 0
    train_losses = []
    val_losses = []
    
    for epoch in range(config.epochs):
        model.train()
        epoch_train_loss = 0.0
        
        for batch in train_loader:
            batch = batch.to(device)
            optimizer.zero_grad()
            
            outputs = model(batch.x, batch.edge_index)
            loss = criterion(outputs, batch.y)
            loss.backward()
            optimizer.step()
            
            epoch_train_loss += loss.item()
        
        avg_train_loss = epoch_train_loss / len(train_loader)
        train_losses.append(avg_train_loss)
        
        # Validation
        model.eval()
        with torch.no_grad():
            epoch_val_loss = 0.0
            for batch in val_loader:
                batch = batch.to(device)
                outputs = model(batch.x, batch.edge_index)
                loss = criterion(outputs, batch.y)
                epoch_val_loss += loss.item()
            
            avg_val_loss = epoch_val_loss / len(val_loader)
            val_losses.append(avg_val_loss)
        
        logger.debug(f"Fold {fold_index + 1}, Epoch {epoch + 1}: "
                    f"Train Loss={avg_train_loss:.6f}, Val Loss={avg_val_loss:.6f}")
        
        # Early stopping
        if avg_val_loss < best_val_loss:
            best_val_loss = avg_val_loss
            patience_counter = 0
            # Save best model state
            best_model_state = model.state_dict().copy()
        else:
            patience_counter += 1
            if patience_counter >= config.early_stopping_patience:
                logger.info(f"Early stopping triggered at epoch {epoch + 1}")
                break
    
    # Load best model state
    model.load_state_dict(best_model_state)
    
    # Final evaluation
    metrics = evaluate_model(model, val_loader, device, logger)
    
    training_time = time.time() - start_time
    
    fold_result = FoldResult(
        fold_index=fold_index + 1,
        train_loss=train_losses[-1] if train_losses else 0.0,
        val_loss=val_losses[-1] if val_losses else 0.0,
        youngs_modulus_mape=metrics['youngs_modulus']['mape'],
        youngs_modulus_rmse=metrics['youngs_modulus']['rmse'],
        youngs_modulus_r2=metrics['youngs_modulus']['r2'],
        shear_modulus_mape=metrics['shear_modulus']['mape'],
        shear_modulus_rmse=metrics['shear_modulus']['rmse'],
        shear_modulus_r2=metrics['shear_modulus']['r2'],
        poisson_ratio_mape=metrics['poisson_ratio']['mape'],
        poisson_ratio_rmse=metrics['poisson_ratio']['rmse'],
        poisson_ratio_r2=metrics['poisson_ratio']['r2'],
        training_time_seconds=training_time
    )
    
    logger.info(f"Fold {fold_index + 1} completed: "
               f"MAPE (Young's)={fold_result.youngs_modulus_mape:.4f}, "
               f"RMSE (Young's)={fold_result.youngs_modulus_rmse:.6f}")
    
    return fold_result

def run_k_fold_cross_validation(
    graphs_path: Path,
    output_dir: Path,
    config: Optional[Config] = None
) -> CrossValidationReport:
    """
    Run k-fold cross-validation on the dataset.
    
    Args:
        graphs_path: Path to the Parquet file containing graphs
        output_dir: Directory to save results
        config: Configuration object (optional, will create default if None)
        
    Returns:
        CrossValidationReport with aggregated metrics
    """
    start_time = time.time()
    
    # Setup
    if config is None:
        config = Config()
    
    output_dir = Path(output_dir)
    output_dir.mkdir(parents=True, exist_ok=True)
    
    logger = get_logger("cv_runner")
    configure_log_file(logger, output_dir / "cv_training.log")
    
    logger.info(f"Starting k-fold cross-validation with k={config.cv_k_folds}")
    logger.info(f"Loading graphs from {graphs_path}")
    
    # Load graphs
    graphs = load_graphs_from_parquet(graphs_path)
    logger.info(f"Loaded {len(graphs)} graphs")
    
    # Split by family to ensure family separation
    logger.info("Splitting graphs by family...")
    split_manifest = split_by_family(graphs, config.seed)
    
    # Perform k-fold split
    # Group graphs by family for stratified k-fold
    family_groups: Dict[str, List[Data]] = {}
    for graph in graphs:
        family = getattr(graph, 'family', 'unknown')
        if family not in family_groups:
            family_groups[family] = []
        family_groups[family].append(graph)
    
    family_names = list(family_groups.keys())
    np.random.seed(config.seed)
    np.random.shuffle(family_names)
    
    k = config.cv_k_folds
    fold_size = len(family_names) // k
    folds = []
    
    for i in range(k):
        if i == k - 1:
            # Last fold gets remaining families
            val_families = family_names[i * fold_size:]
        else:
            val_families = family_names[i * fold_size:(i + 1) * fold_size]
        
        train_families = [f for f in family_names if f not in val_families]
        
        train_graphs = []
        val_graphs = []
        
        for family in train_families:
            train_graphs.extend(family_groups[family])
        for family in val_families:
            val_graphs.extend(family_groups[family])
        
        folds.append((train_graphs, val_graphs))
    
    logger.info(f"Created {k} folds with family separation")
    
    # Run each fold
    fold_results = []
    for i, (train_graphs, val_graphs) in enumerate(folds):
        logger.info(f"Fold {i + 1}: {len(train_graphs)} train, {len(val_graphs)} val")
        fold_result = run_single_fold(i, train_graphs, val_graphs, config, logger)
        fold_results.append(fold_result)
    
    # Aggregate results
    total_time = time.time() - start_time
    
    # Calculate means and standard deviations
    youngs_mape = [f.youngs_modulus_mape for f in fold_results]
    youngs_rmse = [f.youngs_modulus_rmse for f in fold_results]
    youngs_r2 = [f.youngs_modulus_r2 for f in fold_results]
    shear_mape = [f.shear_modulus_mape for f in fold_results]
    shear_rmse = [f.shear_modulus_rmse for f in fold_results]
    poisson_mape = [f.poisson_ratio_mape for f in fold_results]
    
    report = CrossValidationReport(
        k_folds=k,
        config_seed=config.seed,
        mean_youngs_modulus_mape=float(np.mean(youngs_mape)),
        std_youngs_modulus_mape=float(np.std(youngs_mape)),
        mean_youngs_modulus_rmse=float(np.mean(youngs_rmse)),
        std_youngs_modulus_rmse=float(np.std(youngs_rmse)),
        mean_youngs_modulus_r2=float(np.mean(youngs_r2)),
        std_youngs_modulus_r2=float(np.std(youngs_r2)),
        mean_shear_modulus_mape=float(np.mean(shear_mape)),
        std_shear_modulus_mape=float(np.std(shear_mape)),
        mean_shear_modulus_rmse=float(np.mean(shear_rmse)),
        std_shear_modulus_rmse=float(np.std(shear_rmse)),
        mean_poisson_ratio_mape=float(np.mean(poisson_mape)),
        std_poisson_ratio_mape=float(np.std(poisson_mape)),
        fold_results=fold_results,
        total_time_seconds=total_time
    )
    
    # Save report
    report_path = output_dir / "cross_validation_report.json"
    with open(report_path, 'w') as f:
        json.dump(asdict(report), f, indent=2)
    
    logger.info(f"Cross-validation completed. Report saved to {report_path}")
    logger.info(f"Mean Young's Modulus MAPE: {report.mean_youngs_modulus_mape:.4f} ± {report.std_youngs_modulus_mape:.4f}")
    
    return report

def main():
    """Main entry point for k-fold cross-validation."""
    import argparse
    
    parser = argparse.ArgumentParser(description="Run k-fold cross-validation")
    parser.add_argument("--graphs-path", type=str, default="data/processed/graphs_v1.parquet",
                      help="Path to graphs Parquet file")
    parser.add_argument("--output-dir", type=str, default="data/results",
                      help="Directory to save results")
    parser.add_argument("--seed", type=int, default=42, help="Random seed")
    parser.add_argument("--k-folds", type=int, default=5, help="Number of folds")
    parser.add_argument("--epochs", type=int, default=50, help="Number of epochs per fold")
    parser.add_argument("--batch-size", type=int, default=32, help="Batch size")
    parser.add_argument("--hidden-dim", type=int, default=64, help="Hidden dimension")
    parser.add_argument("--num-layers", type=int, default=2, help="Number of GNN layers")
    parser.add_argument("--learning-rate", type=float, default=0.001, help="Learning rate")
    parser.add_argument("--early-stopping-patience", type=int, default=3, help="Early stopping patience")
    
    args = parser.parse_args()
    
    # Create config
    config = Config(
        seed=args.seed,
        cv_k_folds=args.k_folds,
        epochs=args.epochs,
        batch_size=args.batch_size,
        hidden_dim=args.hidden_dim,
        num_layers=args.num_layers,
        learning_rate=args.learning_rate,
        early_stopping_patience=args.early_stopping_patience
    )
    
    # Run cross-validation
    graphs_path = Path(args.graphs_path)
    output_dir = Path(args.output_dir)
    
    if not graphs_path.exists():
        raise FileNotFoundError(f"Graphs file not found: {graphs_path}")
    
    report = run_k_fold_cross_validation(graphs_path, output_dir, config)
    
    print(f"\nCross-Validation Summary:")
    print(f"  Folds: {report.k_folds}")
    print(f"  Total Time: {report.total_time_seconds:.2f}s")
    print(f"  Young's Modulus MAPE: {report.mean_youngs_modulus_mape:.4f} ± {report.std_youngs_modulus_mape:.4f}")
    print(f"  Young's Modulus RMSE: {report.mean_youngs_modulus_rmse:.6f} ± {report.std_youngs_modulus_rmse:.6f}")
    print(f"  Young's Modulus R²: {report.mean_youngs_modulus_r2:.4f} ± {report.std_youngs_modulus_r2:.4f}")
    print(f"  Shear Modulus MAPE: {report.mean_shear_modulus_mape:.4f} ± {report.std_shear_modulus_mape:.4f}")
    print(f"  Poisson Ratio MAPE: {report.mean_poisson_ratio_mape:.4f} ± {report.std_poisson_ratio_mape:.4f}")

if __name__ == "__main__":
    main()