"""
Generate SHA256 checksums for downloaded lottery data files.

This script reads the raw lottery draws CSV generated by T008,
calculates its SHA256 checksum using the existing data_utils module,
and saves the result to data/checksums.json.
"""
import json
import os
import sys

# Add parent directory to path to allow imports from code/
sys.path.insert(0, os.path.dirname(os.path.dirname(os.path.abspath(__file__))))

from data_utils import calculate_checksum
from exceptions import LotteryDataError

RAW_DATA_PATH = "data/raw/lottery_draws.csv"
CHECKSUMS_PATH = "data/checksums.json"

def main():
    """Generate checksums for raw data files and save to JSON."""
    if not os.path.exists(RAW_DATA_PATH):
        raise LotteryDataError(
            f"Raw data file not found: {RAW_DATA_PATH}. "
            "Please run T008 (ingestion) first to download the data."
        )

    checksum = calculate_checksum(RAW_DATA_PATH)
    
    checksum_data = {
        "lottery_draws.csv": checksum,
        "algorithm": "sha256"
    }

    # Ensure data directory exists
    os.makedirs(os.path.dirname(CHECKSUMS_PATH), exist_ok=True)

    with open(CHECKSUMS_PATH, 'w', encoding='utf-8') as f:
        json.dump(checksum_data, f, indent=2)

    print(f"Checksum generated for {RAW_DATA_PATH}")
    print(f"SHA256: {checksum}")
    print(f"Saved to {CHECKSUMS_PATH}")

if __name__ == "__main__":
    main()
