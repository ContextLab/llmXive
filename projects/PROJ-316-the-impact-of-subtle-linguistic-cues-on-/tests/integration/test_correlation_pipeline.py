    def test_json_output_contains_disclaimer(self, setup_test_data):
        """
        Test that any JSON outputs from the correlation pipeline include the disclaimer.
        """
        # Run correlation analysis
        output_file = self.derived_dir / "correlation_results.csv"
        json_output = self.derived_dir / "correlation_details.json"
        
        # The run_correlation_analysis function should create a JSON file with details
        run_correlation_analysis(
            features_path=str(setup_test_data / "processed" / "features.csv"),
            ratings_path=str(setup_test_data / "processed" / "ratings.csv"),
            output_path=str(output_file),
            save_details_json=str(json_output)
        )
        
        # If JSON file was created, verify it contains disclaimer
        if json_output.exists():
            with open(json_output, 'r', encoding='utf-8') as f:
                data = json.load(f)
            
            # Check for disclaimer in various possible locations
            has_disclaimer = (
                MANDATORY_DISCLAIMER in str(data) or
                (isinstance(data, dict) and data.get('disclaimer') == MANDATORY_DISCLAIMER) or
                (isinstance(data, dict) and MANDATORY_DISCLAIMER in str(data.get('notes', '')))
            )
            
            assert has_disclaimer, (
                f"JSON output must contain the mandatory disclaimer: '{MANDATORY_DISCLAIMER}'"
            )

    def test_all_pipeline_outputs_compliant(self, setup_test_data):
        """
        Comprehensive test that all pipeline outputs include the disclaimer.
        
        This test runs the full pipeline and checks all generated files.
        """
        # Run the full correlation pipeline
        results = run_correlation_analysis(
            features_path=str(setup_test_data / "processed" / "features.csv"),
            ratings_path=str(setup_test_data / "processed" / "ratings.csv"),
            output_path=str(self.derived_dir / "correlation_results.csv"),
            generate_plots=True,
            plot_dir=str(self.derived_dir),
            save_details_json=str(self.derived_dir / "correlation_details.json")
        )
        
        # Collect all output files
        output_files = []
        output_files.append(self.derived_dir / "correlation_results.csv")
        
        # Add plot files
        output_files.extend(list(self.derived_dir.glob("scatter_*.png")))
        
        # Add JSON file if it exists
        json_file = self.derived_dir / "correlation_details.json"
        if json_file.exists():
            output_files.append(json_file)
        
        # Verify each file exists and contains disclaimer (where applicable)
        for file_path in output_files:
            assert file_path.exists(), f"Output file {file_path} was not created"
            assert file_path.stat().st_size > 0, f"Output file {file_path} is empty"
            
            # For text-based files, check content directly
            if file_path.suffix in ['.csv', '.json', '.txt', '.md']:
                with open(file_path, 'r', encoding='utf-8') as f:
                    content = f.read()
                    assert MANDATORY_DISCLAIMER in content, (
                        f"{file_path.name} must contain: '{MANDATORY_DISCLAIMER}'"
                    )
        
        # Verify the function results also include disclaimer
        assert 'disclaimer' in results or MANDATORY_DISCLAIMER in str(results), (
            f"Pipeline results must include: '{MANDATORY_DISCLAIMER}'"
        )

    def test_disclaimer_in_correct_format(self, setup_test_data):
        """
        Test that the disclaimer appears exactly as specified in FR-004.
        """
        # Run analysis
        output_file = self.derived_dir / "correlation_results.csv"
        
        run_correlation_analysis(
            features_path=str(setup_test_data / "processed" / "features.csv"),
            ratings_path=str(setup_test_data / "processed" / "ratings.csv"),
            output_path=str(output_file)
        )
        
        # Read and check exact disclaimer text
        with open(output_file, 'r', encoding='utf-8') as f:
            content = f.read()
        
        # Verify exact match (case-sensitive)
        assert MANDATORY_DISCLAIMER in content, (
            f"Exact disclaimer text required: '{MANDATORY_DISCLAIMER}'"
        )
        
        # Ensure no variations or typos
        variations_to_avoid = [
            "association not causation",  # missing "These results indicate"
            "association, not causation",  # missing "These results indicate"
            "association - not causation",  # wrong punctuation
            "indicates association, not causation",  # missing "These results"
        ]
        
        for variation in variations_to_avoid:
            # Allow for the full correct disclaimer, but flag if only partial appears
            if variation in content and MANDATORY_DISCLAIMER not in content:
                pytest.fail(f"Found incorrect disclaimer variation: '{variation}' instead of '{MANDATORY_DISCLAIMER}'")

    def test_disclaimer_placement_in_csv(self, setup_test_data):
        """
        Test that the disclaimer appears in an appropriate location in the CSV output.
        
        The disclaimer should be in the file content, typically as a comment
        or in a dedicated metadata section.
        """
        # Run analysis
        output_file = self.derived_dir / "correlation_results.csv"
        
        run_correlation_analysis(
            features_path=str(setup_test_data / "processed" / "features.csv"),
            ratings_path=str(setup_test_data / "processed" / "ratings.csv"),
            output_path=str(output_file)
        )
        
        # Read file content
        with open(output_file, 'r', encoding='utf-8') as f:
            lines = f.readlines()
        
        # Find lines containing the disclaimer
        disclaimer_lines = [
            i for i, line in enumerate(lines) 
            if MANDATORY_DISCLAIMER in line
        ]
        
        assert len(disclaimer_lines) > 0, (
            f"Disclaimer not found in any line of {output_file.name}"
        )
        
        # Verify disclaimer is not buried in data rows
        # It should be in header comments or a dedicated section
        for line_idx in disclaimer_lines:
            line_content = lines[line_idx].strip()
            # The disclaimer should be clearly visible, not hidden in data
            assert len(line_content) < 200, (
                f"Disclaimer appears to be embedded in a data row at line {line_idx}"
            )

