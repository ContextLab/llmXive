"""
Integration tests for the correlation pipeline ensuring FR-004 compliance.

This test verifies that all outputs generated by the correlation analysis
include the mandatory disclaimer: "These results indicate association, not causation."

FR-004 Requirement: All statistical outputs must explicitly state that
results indicate association, not causation.
"""
import json
import os
import tempfile
from pathlib import Path
from typing import List

import pytest
import pandas as pd
import numpy as np

from src.analysis.correlation import run_correlation_analysis
from src.utils.data_loader import merge_features_and_ratings
from src.config import set_seed


# Mandatory disclaimer text required by FR-004
MANDATORY_DISCLAIMER = "These results indicate association, not causation."


class TestCorrelationPipelineDisclaimer:
    """Tests ensuring the correlation pipeline includes the required disclaimer."""

