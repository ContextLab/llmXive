"""
Ablation data generator for Socratic Transformers project.

This module implements FR-007: replacing critique text with neutral placeholder
text of equivalent token length to isolate the effect of the critique signal.

The ablation dataset is generated by taking the dialogue tuples produced by
T014 (generate_dialogue.py) and systematically neutralizing the critique
component while preserving token structure.
"""

import json
import os
import sys
from pathlib import Path
from typing import List, Dict, Any, Optional

from src.utils.config import get_config
from src.utils.logging import get_logger
from src.utils.metrics import compute_ngram_overlap

# Configure logger
logger = get_logger(__name__)


def count_tokens(text: str, tokenizer: Optional[Any] = None) -> int:
    """
    Estimate token count for a string.
    If a tokenizer is provided, use it for accurate counting.
    Otherwise, fall back to a simple whitespace-based approximation.
    """
    if tokenizer is not None:
        return len(tokenizer.encode(text, add_special_tokens=False))
    # Fallback: rough estimate (words + punctuation)
    # This is less accurate but avoids hard dependency on a loaded tokenizer
    # for this utility function if only used for planning.
    # However, for FR-007 compliance (equivalent token length), we ideally
    # need the tokenizer. We will assume the tokenizer is passed or config
    # provides a path.
    return len(text.split()) + text.count('.') + text.count(',')


def generate_neutral_placeholder(original_text: str, target_token_count: int, tokenizer: Optional[Any] = None) -> str:
    """
    Generate a neutral placeholder text that matches the token length of the original.

    Per FR-007, the critique text is replaced with neutral text of equivalent
    token length to control for length effects while removing semantic content.

    Args:
        original_text: The original critique text.
        target_token_count: The number of tokens to match.
        tokenizer: Optional tokenizer for accurate token counting.

    Returns:
        A neutral string with approximately the same token count.
    """
    if tokenizer is not None:
        # Strategy: Repeat a neutral phrase until we reach the target token count
        neutral_phrase = "The reasoning is neutral and does not contain specific analysis. "
        # Encode the phrase to see how many tokens it takes
        phrase_tokens = tokenizer.encode(neutral_phrase, add_special_tokens=False)
        phrase_len = len(phrase_tokens)

        if phrase_len == 0:
            return ""

        repetitions = target_token_count // phrase_len
        remainder = target_token_count % phrase_len

        base_text = neutral_phrase * repetitions

        # Add partial phrase for remainder
        if remainder > 0:
            partial_tokens = phrase_tokens[:remainder]
            # Decode the partial tokens
            partial_text = tokenizer.decode(partial_tokens)
            base_text += partial_text

        return base_text
    else:
        # Fallback without tokenizer: simple word repetition
        # This is less precise but ensures we have *some* neutral text
        words = ["neutral", "analysis", "placeholder", "text", "does", "not", "affect", "length"]
        # Estimate words per target tokens (roughly 1:1 for simple words)
        num_words = max(target_token_count, 1)
        return " ".join([words[i % len(words)] for i in range(num_words)])


def create_ablation_tuple(
    dialogue_tuple: Dict[str, Any],
    tokenizer: Optional[Any] = None
) -> Dict[str, Any]:
    """
    Convert a dialogue tuple into an ablation tuple by neutralizing the critique.

    The output maintains the same schema but the 'critique.reasoning_snippet'
    is replaced with neutral text of equivalent token length.

    Args:
        dialogue_tuple: A tuple from generate_dialogue.py with structure:
            {
                "question": str,
                "initial_answer": str,
                "critique": {
                    "confidence_score": float,
                    "reasoning_snippet": str,
                    "reasoning_type": str
                },
                "revised_answer": str
            }
        tokenizer: Optional tokenizer for accurate token matching.

    Returns:
        A new dictionary with the neutralized critique.
    """
    ablation_tuple = dialogue_tuple.copy()

    if "critique" not in ablation_tuple:
        logger.warning("Dialogue tuple missing 'critique' field. Skipping ablation.")
        return ablation_tuple

    critique = ablation_tuple["critique"].copy()
    original_reasoning = critique.get("reasoning_snippet", "")

    if not original_reasoning:
        # If no reasoning, just copy as is
        ablation_tuple["critique"] = critique
        return ablation_tuple

    # Count tokens in original
    if tokenizer is not None:
        token_count = len(tokenizer.encode(original_reasoning, add_special_tokens=False))
    else:
        # Fallback estimation
        token_count = len(original_reasoning.split()) + original_reasoning.count('.')

    # Generate neutral placeholder
    neutral_text = generate_neutral_placeholder(original_reasoning, token_count, tokenizer)

    # Update critique with neutral text
    critique["reasoning_snippet"] = neutral_text
    critique["ablation_applied"] = True
    critique["original_length_tokens"] = token_count

    ablation_tuple["critique"] = critique

    logger.debug(f"Ablation applied: {len(original_reasoning)} chars -> {len(neutral_text)} chars (approx {token_count} tokens)")

    return ablation_tuple


def generate_ablation_dataset(
    input_path: str,
    output_path: str,
    tokenizer: Optional[Any] = None,
    sample_size: Optional[int] = None
) -> List[Dict[str, Any]]:
    """
    Read dialogue tuples from input JSONL, apply ablation, and write to output.

    Args:
        input_path: Path to the JSONL file generated by T014 (generate_dialogue.py).
        output_path: Path to write the ablated JSONL file.
        tokenizer: Optional tokenizer for accurate token matching.
        sample_size: Optional limit on number of samples to process (for testing).

    Returns:
        List of ablated tuples.
    """
    input_file = Path(input_path)
    output_file = Path(output_path)

    if not input_file.exists():
        raise FileNotFoundError(f"Input file not found: {input_path}")

    ablated_tuples = []
    processed_count = 0

    logger.info(f"Reading dialogue tuples from {input_path}")

    with open(input_file, 'r', encoding='utf-8') as f:
        for line_num, line in enumerate(f, 1):
            if sample_size and processed_count >= sample_size:
                break

            line = line.strip()
            if not line:
                continue

            try:
                dialogue_tuple = json.loads(line)
                ablated_tuple = create_ablation_tuple(dialogue_tuple, tokenizer)
                ablated_tuples.append(ablated_tuple)
                processed_count += 1
            except json.JSONDecodeError as e:
                logger.error(f"Failed to parse JSON at line {line_num}: {e}")
                continue

    logger.info(f"Generated {len(ablated_tuples)} ablated tuples. Writing to {output_path}")

    output_file.parent.mkdir(parents=True, exist_ok=True)

    with open(output_file, 'w', encoding='utf-8') as f:
        for tup in ablated_tuples:
            f.write(json.dumps(tup, ensure_ascii=False) + '\n')

    logger.info(f"Ablation dataset successfully written to {output_path}")

    return ablated_tuples


def main():
    """
    Main entry point for generating the ablation dataset.

    Reads from the dialogue output of T014 and writes the ablated version.
    Uses the configuration to determine paths and optional tokenizer.
    """
    config = get_config()

    # Default paths based on project structure
    # T014 output is typically in data/processed/dialogues.jsonl
    input_path = config.get("ablation_input_path", "data/processed/dialogues.jsonl")
    output_path = config.get("ablation_output_path", "data/processed/dialogues_ablated.jsonl")

    # Optional: Load tokenizer if specified in config for precise token matching
    tokenizer = None
    if config.get("tokenizer_path"):
        try:
            from transformers import AutoTokenizer
            tokenizer = AutoTokenizer.from_pretrained(config["tokenizer_path"])
            logger.info(f"Loaded tokenizer from {config['tokenizer_path']} for precise token matching")
        except Exception as e:
            logger.warning(f"Could not load tokenizer for precise token matching: {e}. Using fallback.")

    # Optional sample size for testing
    sample_size = config.get("ablation_sample_size", None)

    try:
        ablated_data = generate_ablation_dataset(
            input_path=input_path,
            output_path=output_path,
            tokenizer=tokenizer,
            sample_size=sample_size
        )
        logger.info(f"Successfully generated ablation dataset: {output_path}")
        print(f"Ablation dataset generated: {output_path} ({len(ablated_data)} samples)")
    except FileNotFoundError as e:
        logger.error(f"Input file not found: {e}")
        sys.exit(1)
    except Exception as e:
        logger.error(f"Failed to generate ablation dataset: {e}")
        sys.exit(1)


if __name__ == "__main__":
    main()
