"""
Merge agency scores, adherence metrics, and demographics into a single dataset.

This script reads the processed CSV files generated by earlier pipeline steps:
- ``data/processed/agency_scores.csv`` (output of ``agency_scoring.compute_scores``)
- ``data/processed/adherence_metrics.csv`` (output of ``adherence_extraction.extract_metrics``)
- ``data/processed/demographics.csv`` (output of ``adherence_extraction.ingest_demographics``)

The files are merged on the most appropriate identifier(s) (``user_id`` or ``session_id``)
and the resulting dataframe is written to ``data/processed/merged_data.csv``.

The script logs each major step using the project's ``pipeline_logger`` utilities.
"""

from __future__ import annotations

import argparse
from pathlib import Path
from typing import List, Optional

import pandas as pd

from logging.pipeline_logger import get_logger, log_dict

logger = get_logger(__name__)

def _infer_merge_key(df_left: pd.DataFrame, df_right: pd.DataFrame) -> List[str]:
    """
    Determine the best column(s) to merge on between two dataframes.

    Preference order:
    1. ``user_id`` if present in both.
    2. ``session_id`` if present in both.
    3. Any other overlapping column(s).

    Returns a list of column names (may be empty if no overlap).
    """
    for candidate in ("user_id", "session_id"):
        if candidate in df_left.columns and candidate in df_right.columns:
            return [candidate]

    # Fallback to any intersection (could be multiple columns)
    overlap = list(set(df_left.columns) & set(df_right.columns))
    return overlap

def merge_datasets(
    agency_path: Path,
    adherence_path: Path,
    demographics_path: Path,
    output_path: Path,
) -> pd.DataFrame:
    """
    Merge the three processed datasets and write the result to ``output_path``.

    Parameters
    ----------
    agency_path :
        Path to ``agency_scores.csv``.
    adherence_path :
        Path to ``adherence_metrics.csv``.
    demographics_path :
        Path to ``demographics.csv``.
    output_path :
        Destination for the merged CSV.

    Returns
    -------
    pd.DataFrame
        The merged dataframe.
    """
    logger.info("Starting dataset merge")
    logger.debug(f"Reading agency scores from {agency_path}")
    agency_df = pd.read_csv(agency_path)
    logger.debug(f"Agency scores rows: {len(agency_df)}")

    logger.debug(f"Reading adherence metrics from {adherence_path}")
    adherence_df = pd.read_csv(adherence_path)
    logger.debug(f"Adherence metrics rows: {len(adherence_df)}")

    logger.debug(f"Reading demographics from {demographics_path}")
    demographics_df = pd.read_csv(demographics_path)
    logger.debug(f"Demographics rows: {len(demographics_df)}")

    # ---- First merge: agency + adherence ----
    key1 = _infer_merge_key(agency_df, adherence_df)
    if not key1:
        raise ValueError(
            "No common columns to merge agency scores with adherence metrics."
        )
    logger.info(f"Merging agency and adherence on key(s): {key1}")
    merged1 = pd.merge(agency_df, adherence_df, on=key1, how="inner")
    logger.debug(f"Rows after first merge: {len(merged1)}")

    # ---- Second merge: result + demographics ----
    key2 = _infer_merge_key(merged1, demographics_df)
    if not key2:
        raise ValueError(
            "No common columns to merge the combined dataset with demographics."
        )
    logger.info(f"Merging combined data with demographics on key(s): {key2}")
    final_df = pd.merge(merged1, demographics_df, on=key2, how="inner")
    logger.debug(f"Rows after final merge: {len(final_df)}")

    # Ensure output directory exists
    output_path.parent.mkdir(parents=True, exist_ok=True)

    logger.info(f"Writing merged dataset to {output_path}")
    final_df.to_csv(output_path, index=False)

    # Log a concise summary as a dict for downstream parsers
    log_dict(
        {
            "merged_rows": len(final_df),
            "output_path": str(output_path),
            "merge_keys_stage1": key1,
            "merge_keys_stage2": key2,
        }
    )

    return final_df

def _parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Merge agency scores, adherence metrics, and demographics."
    )
    parser.add_argument(
        "--agency",
        type=Path,
        default=Path("data/processed/agency_scores.csv"),
        help="Path to agency scores CSV (default: data/processed/agency_scores.csv)",
    )
    parser.add_argument(
        "--adherence",
        type=Path,
        default=Path("data/processed/adherence_metrics.csv"),
        help="Path to adherence metrics CSV (default: data/processed/adherence_metrics.csv)",
    )
    parser.add_argument(
        "--demographics",
        type=Path,
        default=Path("data/processed/demographics.csv"),
        help="Path to demographics CSV (default: data/processed/demographics.csv)",
    )
    parser.add_argument(
        "--output",
        type=Path,
        default=Path("data/processed/merged_data.csv"),
        help="Path for merged output CSV (default: data/processed/merged_data.csv)",
    )
    return parser.parse_args()

def main() -> None:
    """Entry point for ``python -m code.analysis.merge_datasets``."""
    args = _parse_args()
    merge_datasets(
        agency_path=args.agency,
        adherence_path=args.adherence,
        demographics_path=args.demographics,
        output_path=args.output,
    )
    logger.info("Dataset merge completed successfully.")

if __name__ == "__main__":
    main()