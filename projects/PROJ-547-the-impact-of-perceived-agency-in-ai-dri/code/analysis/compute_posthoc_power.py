"""
compute_posthoc_power.py

This module provides functionality to compute post‑hoc statistical power for
regression models that have already been fitted and summarized. It reads a
CSV file generated by the regression pipeline (``output/regression_summary.csv``),
calculates the power for each model using the effect size and degrees of
freedom reported in the file, logs a warning when the estimated power is
below the 0.80 threshold, and writes the results to ``data/processed/power_estimates.csv``.
"""

from __future__ import annotations

import argparse
import sys
from pathlib import Path
from typing import Any

import pandas as pd
from statsmodels.stats.power import FTestPower, TTestPower, NormalIndPower

# Project‑specific logging utilities
from logging.pipeline_logger import get_logger, log_dict

# Error handling utilities
from utils.error_handler import PipelineError, log_and_exit

LOGGER = get_logger(__name__)

# ----------------------------------------------------------------------
# Helper functions
# ----------------------------------------------------------------------


def _select_power_calculator(row: pd.Series) -> Any:
    """
    Choose an appropriate power calculator based on the model type.

    Parameters
    ----------
    row : pd.Series
        A row of the summary DataFrame. Expected columns:
        - ``model_type`` (e.g., ``'linear'``, ``'logistic'``, ``'beta'``)
        - ``effect_size`` (Cohen's f² for linear/beta, Cohen's d for t‑tests)

    Returns
    -------
    power_calculator : object
        An instance of a statsmodels power calculator.
    """
    model_type = str(row.get("model_type", "")).lower()
    if model_type in {"linear", "beta"}:
        return FTestPower()
    elif model_type == "logistic":
        # Logistic regression power can be approximated with a z‑test
        return NormalIndPower()
    elif model_type == "t_test":
        return TTestPower()
    else:
        # Default to F‑test power – this is a safe fallback
        return FTestPower()

def _compute_power_for_row(row: pd.Series) -> float:
    """
    Compute statistical power for a single model row.

    The function follows the conventions used in the original pipeline:
    - ``effect_size``: Cohen's f² for F‑tests, Cohen's d for t‑tests,
      or an approximation for logistic models.
    - ``df_num``: numerator degrees of freedom (model df)
    - ``df_denom``: denominator degrees of freedom (residual df)
    - ``alpha``: significance level (default 0.05)

    Parameters
    ----------
    row : pd.Series
        A row from the regression summary DataFrame.

    Returns
    -------
    power : float
        Estimated statistical power (0‑1).
    """
    effect_size = row.get("effect_size")
    if pd.isna(effect_size):
        raise PipelineError("Missing effect_size for power calculation.")

    alpha = float(row.get("alpha", 0.05))
    power_calc = _select_power_calculator(row)

    # Different calculators require different arguments
    if isinstance(power_calc, FTestPower):
        df_num = int(row.get("df_num", 1))
        df_denom = int(row.get("df_denom", 1))
        power = power_calc.solve_power(
            effect_size=effect_size,
            df_num=df_num,
            df_denom=df_denom,
            alpha=alpha,
            nobs=None,
        )
    elif isinstance(power_calc, TTestPower):
        # For t‑tests we need the total sample size
        nobs = int(row.get("sample_size", 0))
        power = power_calc.solve_power(
            effect_size=effect_size,
            nobs=nobs,
            alpha=alpha,
            alternative="two-sided",
        )
    elif isinstance(power_calc, NormalIndPower):
        # Approximation for logistic regression using a z‑test
        # ``effect_size`` interpreted as Cohen's d equivalent
        nobs = int(row.get("sample_size", 0))
        power = power_calc.solve_power(
            effect_size=effect_size,
            nobs1=nobs,
            alpha=alpha,
            ratio=1.0,
            alternative="two-sided",
        )
    else:
        # Should never reach here because of the selector logic
        raise PipelineError("Unsupported power calculator selected.")
    return float(power)

# ----------------------------------------------------------------------
# Core processing function
# ----------------------------------------------------------------------


def compute_posthoc_power(
    summary_path: Path,
    output_path: Path,
    power_threshold: float = 0.80,
) -> pd.DataFrame:
    """
    Compute post‑hoc power for each model in a regression summary CSV.

    Parameters
    ----------
    summary_path : Path
        Path to ``output/regression_summary.csv`` produced by the regression
        pipeline.
    output_path : Path
        Destination path for the CSV that will contain the original summary
        columns plus a new ``power`` column.
    power_threshold : float, default 0.80
        Threshold below which a warning is logged.

    Returns
    -------
    result_df : pd.DataFrame
        DataFrame with an added ``power`` column.
    """
    if not summary_path.is_file():
        raise PipelineError(f"Regression summary file not found: {summary_path}")

    # Load the regression summary
    try:
        df = pd.read_csv(summary_path)
    except Exception as exc:
        raise PipelineError(f"Failed to read regression summary CSV: {exc}") from exc

    # Ensure required columns exist
    required_cols = {"effect_size", "model_type"}
    missing = required_cols - set(df.columns)
    if missing:
        raise PipelineError(
            f"Regression summary missing required columns for power calculation: {missing}"
        )

    # Compute power row‑wise
    powers = []
    for idx, row in df.iterrows():
        try:
            power = _compute_power_for_row(row)
        except Exception as exc:
            LOGGER.error(
                "Power calculation failed for row %d: %s", idx, exc, exc_info=True
            )
            power = float("nan")
        powers.append(power)

    df["power"] = powers

    # Log warnings for low power
    low_power_mask = df["power"] < power_threshold
    for _, low_row in df[low_power_mask].iterrows():
        LOGGER.warning(
            "Estimated post‑hoc power (%.3f) for model '%s' is below the %.2f threshold.",
            low_row["power"],
            low_row.get("model_name", "Unnamed"),
            power_threshold,
        )

    # Write results
    output_path.parent.mkdir(parents=True, exist_ok=True)
    df.to_csv(output_path, index=False)

    # Log a summary dict for downstream consumers
    log_dict(
        {
            "task": "compute_posthoc_power",
            "input_file": str(summary_path),
            "output_file": str(output_path),
            "rows_processed": len(df),
            "rows_low_power": int(low_power_mask.sum()),
        }
    )

    return df

# ----------------------------------------------------------------------
# CLI entry point
# ----------------------------------------------------------------------


def _parse_args(argv: list[str] | None = None) -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Compute post‑hoc statistical power for regression models."
    )
    parser.add_argument(
        "--summary",
        type=Path,
        default=Path("output") / "regression_summary.csv",
        help="Path to the regression summary CSV generated by the pipeline.",
    )
    parser.add_argument(
        "--output",
        type=Path,
        default=Path("data") / "processed" / "power_estimates.csv",
        help="Destination CSV file that will contain power estimates.",
    )
    parser.add_argument(
        "--threshold",
        type=float,
        default=0.80,
        help="Power threshold below which a warning is emitted.",
    )
    return parser.parse_args(argv)

def main(argv: list[str] | None = None) -> None:
    """
    Command‑line interface for the power computation script.

    The function catches any :class:`PipelineError` raised during processing,
    logs the error, and exits with a non‑zero status code so that the pipeline
    can respond appropriately.
    """
    args = _parse_args(argv)

    try:
        compute_posthoc_power(
            summary_path=args.summary,
            output_path=args.output,
            power_threshold=args.threshold,
        )
    except PipelineError as exc:
        log_and_exit(str(exc), exit_code=1)

if __name__ == "__main__":
    main()
