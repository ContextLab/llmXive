"""Check validation thresholds and abort downstream analysis if not met.

This script reads the validation summary YAML generated by
``validation/generate_report.py`` and ensures that the split‑half reliability
and convergent validity (Pearson correlation) meet the predefined thresholds:

- reliability >= 0.80
- Pearson r >= 0.30
- p‑value < 0.05

If any of these conditions fail, the script logs a warning and aborts the
pipeline by exiting with a non‑zero status via ``log_and_exit``.
"""

from __future__ import annotations

import argparse
import sys
from pathlib import Path
from typing import Any, Dict

import yaml

from logging.pipeline_logger import get_logger, log_dict
from utils.error_handler import log_and_exit

# Threshold constants
MIN_RELIABILITY = 0.80
MIN_CORRELATION = 0.30
MAX_P_VALUE = 0.05


def _load_report(report_path: Path) -> Dict[str, Any]:
    """Load the validation YAML report.

    Args:
        report_path: Path to the YAML file produced by ``generate_report.py``.

    Returns:
        Dictionary with the report contents.

    Raises:
        FileNotFoundError: If the YAML file does not exist.
        yaml.YAMLError: If the file cannot be parsed.
    """
    if not report_path.is_file():
        raise FileNotFoundError(f"Validation report not found at {report_path}")

    with report_path.open("r", encoding="utf-8") as f:
        return yaml.safe_load(f)


def _check_thresholds(report: Dict[str, Any]) -> None:
    """Validate thresholds and abort if they are not satisfied.

    The expected keys in ``report`` are:
    - ``reliability`` (float)
    - ``pearson_r`` (float)
    - ``p_value`` (float)

    Args:
        report: Parsed validation report.

    Raises:
        SystemExit: Via ``log_and_exit`` when thresholds are not met.
    """
    logger = get_logger(__name__)

    reliability = float(report.get("reliability", 0.0))
    correlation = float(report.get("pearson_r", 0.0))
    p_value = float(report.get("p_value", 1.0))

    logger.info("Loaded validation metrics")
    log_dict(
        logger,
        {
            "reliability": reliability,
            "pearson_r": correlation,
            "p_value": p_value,
        },
    )

    thresholds_met = (
        reliability >= MIN_RELIABILITY
        and correlation >= MIN_CORRELATION
        and p_value < MAX_P_VALUE
    )

    if thresholds_met:
        logger.info("All validation thresholds met – proceeding with downstream analysis.")
        return

    # Build a detailed warning message
    messages = []
    if reliability < MIN_RELIABILITY:
        messages.append(
            f"Reliability {reliability:.3f} below minimum {MIN_RELIABILITY:.2f}"
        )
    if correlation < MIN_CORRELATION:
        messages.append(
            f"Pearson r {correlation:.3f} below minimum {MIN_CORRELATION:.2f}"
        )
    if p_value >= MAX_P_VALUE:
        messages.append(
            f"P‑value {p_value:.3f} not less than required {MAX_P_VALUE:.2f}"
        )
    warning_msg = "Validation thresholds not met: " + "; ".join(messages)

    # Log the warning and abort the pipeline
    logger.warning(warning_msg)
    log_dict(
        logger,
        {
            "reliability": reliability,
            "pearson_r": correlation,
            "p_value": p_value,
            "status": "thresholds_not_met",
        },
    )
    # Exit with a non‑zero status code to abort downstream steps
    log_and_exit(warning_msg)


def main(argv: list[str] | None = None) -> None:
    """Entry point for the threshold‑checking script.

    Args:
        argv: Optional list of command‑line arguments (for testing).
    """
    parser = argparse.ArgumentParser(
        description=(
            "Abort downstream analysis if validation thresholds are not met."
        )
    )
    parser.add_argument(
        "--report",
        type=Path,
        default=Path(__file__).resolve().parents[1] / "validation_report.yaml",
        help="Path to the validation summary YAML file.",
    )
    args = parser.parse_args(argv)

    try:
        report = _load_report(args.report)
        _check_thresholds(report)
    except Exception as exc:
        # Any unexpected error should also abort the pipeline
        log_and_exit(str(exc))


if __name__ == "__main__":
    main()