"""
preprocess_3d.py
-----------------
Extract 3‑dimensional molecular information (Cartesian coordinates, atom types,
and bond connectivity) from the QM9 10 k subset generated by ``create_subset.py``.
The script reads the parquet file ``data/processed/molecules_10k.parquet`` and
writes a new parquet file ``data/processed/features_3d.parquet`` that contains
one row per molecule with the following columns:

* ``molecule_id`` – integer index of the molecule in the subset
* ``coordinates`` – list of ``[x, y, z]`` float triples
* ``atom_types`` – list of atomic symbols (e.g. ``["C", "H", "O"]``)
* ``bonds`` – list of ``[i, j]`` integer pairs representing bonded atom indices

The implementation is deliberately defensive: if a molecule is missing any of the
required fields it is skipped with a warning rather than causing the whole pipeline
to fail.
"""

import json
import logging
from pathlib import Path
from typing import Any, List, Mapping

import pandas as pd

logger = logging.getLogger(__name__)
logging.basicConfig(level=logging.INFO, format="%(levelname)s: %(message)s")


def _load_subset() -> pd.DataFrame:
    """
    Load the 10 k QM9 subset created by ``create_subset.py``.

    Returns
    -------
    pd.DataFrame
        DataFrame where each row corresponds to a single molecule.
    """
    # Resolve the project root (three parents up from this file):
    #   .../code/data/preprocess_3d.py
    #   -> .../code
    #   -> .../projects/001-predicting-molecular-dipole-moments
    #   -> repository root
    project_root = Path(__file__).resolve().parents[3]
    subset_path = project_root / "data" / "processed" / "molecules_10k.parquet"
    if not subset_path.is_file():
        raise FileNotFoundError(f"Subset parquet not found at {subset_path}")
    logger.info("Loading QM9 subset from %s", subset_path)
    return pd.read_parquet(subset_path)


def _extract_molecule_features(row: Mapping[str, Any]) -> Mapping[str, Any] | None:
    """
    Pull the required 3‑D information from a single row of the QM9 dataframe.

    The QM9 dataset (as provided by ``datasets.load_dataset('qm9')``) contains a
    number of possible field names.  This helper looks for the most common ones
    and returns a dictionary that can be directly stored in the output DataFrame.
    If the essential fields are missing, ``None`` is returned and the caller will
    skip the molecule.

    Parameters
    ----------
    row: Mapping[str, Any]
        One row of the input DataFrame.

    Returns
    -------
    dict | None
        ``None`` if the row cannot be processed; otherwise a dict with keys
        ``coordinates``, ``atom_types`` and ``bonds``.
    """
    # Preferred field names (as used by the HuggingFace QM9 dataset)
    coord_key = "atom_positions"
    atom_type_key = "atom_symbols"
    bond_key = "bond_index"

    # Some earlier scripts may have stored the data under alternative keys.
    # Fall back to these alternatives if the primary ones are missing.
    if coord_key not in row and "positions" in row:
        coord_key = "positions"
    if atom_type_key not in row and "atomic_numbers" in row:
        atom_type_key = "atomic_numbers"
    if bond_key not in row and "bonds" in row:
        bond_key = "bonds"

    # Extract the fields; if any essential field is missing, skip the molecule.
    coordinates = row.get(coord_key)
    atom_types = row.get(atom_type_key)
    bonds = row.get(bond_key, [])

    if coordinates is None or atom_types is None:
        logger.warning(
            "Molecule %s is missing required fields (%s, %s). Skipping.",
            row.get("molecule_id", "<unknown>"),
            coord_key,
            atom_type_key,
        )
        return None

    # Normalise the data structures:
    #   * coordinates → list of [x, y, z] floats
    #   * atom_types → list of strings (symbols) or ints (atomic numbers)
    #   * bonds → list of [i, j] integer pairs
    # The dataset already stores them in a JSON‑compatible form, but we coerce
    # to Python lists to be safe.
    try:
        coordinates = [list(map(float, xyz)) for xyz in coordinates]
    except Exception as exc:
        logger.warning("Failed to parse coordinates for molecule %s: %s", row.get("molecule_id"), exc)
        return None

    # Convert atomic numbers to symbols if necessary.
    if all(isinstance(at, int) for at in atom_types):
        # Simple conversion table for the elements that appear in QM9 (H, C, N, O, F)
        num_to_sym = {1: "H", 6: "C", 7: "N", 8: "O", 9: "F"}
        atom_types = [num_to_sym.get(at, str(at)) for at in atom_types]

    # Ensure bonds are a list of two‑element integer lists.
    try:
        bonds = [list(map(int, pair)) for pair in bonds]
    except Exception as exc:
        logger.warning("Failed to parse bond list for molecule %s: %s", row.get("molecule_id"), exc)
        bonds = []

    return {
        "coordinates": coordinates,
        "atom_types": atom_types,
        "bonds": bonds,
    }


def extract_3d_features(df: pd.DataFrame) -> pd.DataFrame:
    """
    Transform the raw QM9 subset into a DataFrame that contains only the
    3‑D information required for downstream GNN training.

    Parameters
    ----------
    df: pd.DataFrame
        DataFrame loaded from ``molecules_10k.parquet``.

    Returns
    -------
    pd.DataFrame
        DataFrame with columns ``molecule_id``, ``coordinates``,
        ``atom_types`` and ``bonds``.
    """
    records: List[dict] = []
    for idx, row in df.iterrows():
        # Preserve a stable identifier for each molecule.
        molecule_id = int(row.get("molecule_id", idx))
        features = _extract_molecule_features(row)
        if features is None:
            continue
        record = {"molecule_id": molecule_id, **features}
        records.append(record)

    if not records:
        raise RuntimeError("No valid molecules were extracted from the input dataframe.")
    logger.info("Extracted 3‑D features for %d molecules.", len(records))
    return pd.DataFrame.from_records(records)


def save_features(df: pd.DataFrame, output_path: Path) -> None:
    """
    Write the processed 3‑D features to a parquet file.

    Parameters
    ----------
    df: pd.DataFrame
        DataFrame produced by :func:`extract_3d_features`.
    output_path: Path
        Destination ``.parquet`` file.
    """
    output_path.parent.mkdir(parents=True, exist_ok=True)
    logger.info("Saving 3‑D features to %s", output_path)
    df.to_parquet(output_path, engine="pyarrow", index=False)


def main() -> None:
    """
    Entry‑point for the script.  It loads the QM9 subset, extracts the required
    3‑D information and writes the result to ``data/processed/features_3d.parquet``.
    """
    subset_df = _load_subset()
    features_df = extract_3d_features(subset_df)

    project_root = Path(__file__).resolve().parents[3]
    output_file = project_root / "data" / "processed" / "features_3d.parquet"
    save_features(features_df, output_file)


if __name__ == "__main__":
    main()
