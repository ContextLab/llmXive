def ensure_output_dir() -> Path:
    """Ensure the processed data directory exists."""
    DATA_PROCESSED_DIR.mkdir(parents=True, exist_ok=True)
    return DATA_PROCESSED_DIR


def load_molecule_subset() -> pd.DataFrame:
    """
    Load the raw QM9 data and create a reproducible 10k subset.
    Expects raw data to be in data/raw/ as generated by download_qm9.py.
    """
    if not raw_data_path.exists():
        raise FileNotFoundError(f"Raw data not found at {raw_data_path}. Run download_qm9.py first.")
    
    # Load raw data (assuming CSV or similar format from download_qm9)
    # The download_qm9 task should produce a consolidated CSV or similar.
    # We assume the output is a CSV for now, adapting if it's parquet.
    if raw_data_path.suffix == '.csv':
        df = pd.read_csv(raw_data_path)
    elif raw_data_path.suffix == '.parquet':
        df = pd.read_parquet(raw_data_path)
    else:
        # Fallback: try to read as CSV
        df = pd.read_csv(raw_data_path)

    # Create reproducible subset of 10k
    subset_df = create_reproducible_subset(df, target_size=10000, seed=42)
    return subset_df


def extract_3d_features_wrapper(df: pd.DataFrame, output_path: Path) -> pd.DataFrame:
    """
    Extract 3D features (coordinates, atom types, connectivity) and save to parquet.
    Returns the processed dataframe for potential chaining.
    """
    print(f"Extracting 3D features for {len(df)} molecules...")
    
    # Call the existing extraction logic
    # Note: extract_3d_features is expected to return a processed DataFrame
    # or a list of dicts. We assume it returns a DataFrame with the required features.
    features_3d = extract_3d_features(df)
    
    # Handle missing coordinates if necessary (though extract_3d_features might do this)
    # We call the handler to generate the exclusion report as per T019
    exclusion_report = handle_missing_coordinates(df, features_3d)
    exclusion_path = output_path.parent / "excluded_molecules.csv"
    exclusion_report.to_csv(exclusion_path, index=False)
    print(f"Wrote exclusion report to {exclusion_path}")

    # Save to parquet
    ensure_output_dir(output_path.parent)
    features_3d.to_parquet(output_path, index=False)
    print(f"Saved 3D features to {output_path}")
    
    return features_3d


def extract_2d_features_wrapper(df: pd.DataFrame, output_path: Path) -> pd.DataFrame:
    """
    Extract 2D features (fingerprints, Coulomb matrix) and save to parquet.
    """
    print(f"Extracting 2D features for {len(df)} molecules...")
    
    features_2d = extract_2d_features(df)
    
    # Save to parquet
    ensure_output_dir(output_path.parent)
    features_2d.to_parquet(output_path, index=False)
    print(f"Saved 2D features to {output_path}")
    
    return features_2d


def generate_processed_data(
    raw_data_path: Path,
    subset_output_path: Path,
    features_3d_path: Path,
    features_2d_path: Path,
    seed: int = 42
) -> None:
    """
    Main orchestration function to generate all processed data artifacts.
    """
    set_seed(seed)
    
    # 1. Load and subset
    print("Loading and subsetting data...")
    df_subset = load_molecule_subset(raw_data_path)
    subset_output_path.parent.mkdir(parents=True, exist_ok=True)
    df_subset.to_parquet(subset_output_path, index=False)
    print(f"Saved subset to {subset_output_path}")
    
    # 2. Extract 3D features
    extract_3d_features_wrapper(df_subset, features_3d_path)
    
    # 3. Extract 2D features
    extract_2d_features_wrapper(df_subset, features_2d_path)


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Generate processed data from QM9 raw data.")
    parser.add_argument(
        "--raw-data", 
        type=Path, 
        default=Path("data/raw/qm9.csv"),
        help="Path to raw QM9 data file."
    )
    parser.add_argument(
        "--subset-output", 
        type=Path, 
        default=Path("data/processed/molecules_10k.parquet"),
        help="Path for the 10k subset parquet file."
    )
    parser.add_argument(
        "--features-3d", 
        type=Path, 
        default=Path("data/processed/features_3d.parquet"),
        help="Path for 3D features parquet file."
    )
    parser.add_argument(
        "--features-2d", 
        type=Path, 
        default=Path("data/processed/features_2d.parquet"),
        help="Path for 2D features parquet file."
    )
    parser.add_argument("--seed", type=int, default=42, help="Random seed for reproducibility.")
    return parser.parse_args()


def main() -> None:
    args = parse_args()
    generate_processed_data(
        raw_data_path=args.raw_data,
        subset_output_path=args.subset_output,
        features_3d_path=args.features_3d,
        features_2d_path=args.features_2d,
        seed=args.seed
    )


if __name__ == "__main__":
    main()