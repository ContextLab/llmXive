"""Validate that the RMSE variance across seeds is below a given threshold.

This script is part of the validation suite for the molecular dipole moment
prediction pipeline. It reads the ``results/metrics.csv`` file generated by
``code/generate_metrics.py`` (or the equivalent metrics generation step),
extracts the RMSE values for each random seed, and checks that the relative
standard deviation (coefficient of variation) is less than 10 % (the default
threshold).

The script exits with status code ``0`` if the condition is satisfied and
prints a short success message. If the condition is violated, the script
prints a warning and exits with a non‑zero status code so that it can be used
in CI pipelines.

The implementation relies only on the Python standard library and NumPy,
both of which are already part of the project's dependencies.
"""

from __future__ import annotations

import csv
import sys
from pathlib import Path
from typing import List

import numpy as np


def _read_rmse_values(csv_path: Path) -> List[float]:
    """Read the RMSE column from a CSV file.

    The CSV is expected to have a header row that includes a column named
    ``RMSE`` (case‑insensitive). All subsequent rows are parsed and the
    values are returned as a list of floats.

    Parameters
    ----------
    csv_path:
        Path to the ``metrics.csv`` file.

    Returns
    -------
    List[float]
        List of RMSE values (one per seed).
    """
    if not csv_path.is_file():
        raise FileNotFoundError(f"Metrics file not found: {csv_path}")

    with csv_path.open(newline="") as f:
        reader = csv.DictReader(f)
        # Normalise header names to lower case for case‑insensitive match
        lower_fieldnames = [name.lower() for name in reader.fieldnames or []]
        if "rmse" not in lower_fieldnames:
            raise ValueError("RMSE column not found in metrics CSV.")
        rmse_key = next(
            name for name in reader.fieldnames if name.lower() == "rmse"
        )
        values = []
        for row in reader:
            try:
                value = float(row[rmse_key])
                values.append(value)
            except (KeyError, ValueError) as exc:
                raise ValueError(
                    f"Invalid RMSE entry in row {reader.line_num}: {exc}"
                ) from exc
    if not values:
        raise ValueError("No RMSE values found in metrics CSV.")
    return values


def validate_rmse_variance(
    metrics_path: Path = Path("results/metrics.csv"),
    threshold: float = 0.10,
) -> bool:
    """Check that the coefficient of variation of RMSE is below ``threshold``.

    Parameters
    ----------
    metrics_path:
        Path to the CSV file containing per‑seed metrics.
    threshold:
        Maximum allowed coefficient of variation (std / mean). The default
        corresponds to a 10 % relative variance.

    Returns
    -------
    bool
        ``True`` if the condition is satisfied, ``False`` otherwise.
    """
    try:
        rmse_values = _read_rmse_values(metrics_path)
    except Exception as exc:
        print(f"[validate_variance] Error reading metrics: {exc}", file=sys.stderr)
        return False

    rmse_array = np.asarray(rmse_values, dtype=float)
    mean_rmse = rmse_array.mean()
    if mean_rmse == 0:
        print("[validate_variance] Mean RMSE is zero; cannot compute variance.", file=sys.stderr)
        return False

    std_rmse = rmse_array.std(ddof=1)  # sample standard deviation
    coeff_var = std_rmse / mean_rmse

    if coeff_var < threshold:
        print(
            f"[validate_variance] SUCCESS: RMSE coefficient of variation "
            f"{coeff_var:.2%} is below the {threshold:.2%} threshold."
        )
        return True
    else:
        print(
            f"[validate_variance] FAILURE: RMSE coefficient of variation "
            f"{coeff_var:.2%} exceeds the {threshold:.2%} threshold.",
            file=sys.stderr,
        )
        return False


def main() -> None:
    """Entry point for the script.

    Returns
    -------
    None
    """
    success = validate_rmse_variance()
    sys.exit(0 if success else 1)


if __name__ == "__main__":
    main()
