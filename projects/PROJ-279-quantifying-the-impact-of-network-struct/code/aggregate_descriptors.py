import json
import csv
import logging
from pathlib import Path
from typing import Dict, List, Any, Optional
import numpy as np

from models.atomic_config import AtomicConfiguration
from logging_config import get_logger
from config.env_config import get_processed_dir

logger = get_logger(__name__)

def load_processed_configs() -> List[Dict[str, Any]]:
    """
    Load processed configuration data from the descriptors CSV.
    This function reads the aggregated descriptor data generated by the pipeline.
    """
    processed_dir = get_processed_dir()
    descriptors_path = processed_dir / "descriptors.csv"
    
    if not descriptors_path.exists():
        logger.error(f"Descriptors file not found: {descriptors_path}")
        return []

    configs = []
    with open(descriptors_path, 'r', newline='', encoding='utf-8') as f:
        reader = csv.DictReader(f)
        for row in reader:
            # Convert numeric strings to appropriate types
            processed_row = {}
            for key, value in row.items():
                if value == '':
                    processed_row[key] = None
                elif key == 'config_id':
                    processed_row[key] = value
                else:
                    try:
                        # Try to convert to float
                        processed_row[key] = float(value)
                    except ValueError:
                        processed_row[key] = value
            configs.append(processed_row)
    
    return configs

def aggregate_descriptors(configs: List[Dict[str, Any]]) -> Dict[str, Any]:
    """
    Aggregate descriptor data, handling missing thermal conductivity values.
    
    This function:
    1. Filters out configurations with missing thermal conductivity (k) values.
    2. Counts the number of skipped configurations.
    3. Returns the filtered dataset and metadata about skipped entries.
    
    Args:
        configs: List of configuration dictionaries with descriptor data.
        
    Returns:
        Dictionary containing:
            - 'valid_configs': List of configs with complete data (including k)
            - 'skipped_count': Number of configs skipped due to missing k
            - 'skipped_ids': List of config IDs that were skipped
            - 'total_processed': Total number of configs processed
    """
    valid_configs = []
    skipped_ids = []
    skipped_count = 0
    
    for config in configs:
        config_id = config.get('config_id', 'unknown')
        k_value = config.get('thermal_conductivity', None)
        
        if k_value is None or (isinstance(k_value, (float, int)) and np.isnan(k_value)):
            skipped_count += 1
            skipped_ids.append(config_id)
            logger.warning(f"Skipping configuration {config_id}: Missing thermal conductivity value")
        else:
            valid_configs.append(config)
    
    if skipped_count > 0:
        logger.info(f"Summary: Skipped {skipped_count} configurations due to missing thermal conductivity values")
        logger.info(f"Valid configurations remaining for analysis: {len(valid_configs)}")
    
    return {
        'valid_configs': valid_configs,
        'skipped_count': skipped_count,
        'skipped_ids': skipped_ids,
        'total_processed': len(configs)
    }

def save_aggregated_data(aggregate_result: Dict[str, Any]) -> None:
    """
    Save the aggregated descriptor data to a new CSV file, excluding skipped entries.
    Also saves a report of skipped configurations to JSON.
    
    Args:
        aggregate_result: Result dictionary from aggregate_descriptors()
    """
    processed_dir = get_processed_dir()
    processed_dir.mkdir(parents=True, exist_ok=True)
    
    # Save filtered descriptors to CSV
    valid_configs = aggregate_result.get('valid_configs', [])
    if valid_configs:
        output_path = processed_dir / "descriptors_aggregated.csv"
        fieldnames = list(valid_configs[0].keys())
        
        with open(output_path, 'w', newline='', encoding='utf-8') as f:
            writer = csv.DictWriter(f, fieldnames=fieldnames)
            writer.writeheader()
            writer.writerows(valid_configs)
        
        logger.info(f"Saved {len(valid_configs)} valid configurations to {output_path}")
    else:
        logger.warning("No valid configurations to save after filtering missing thermal conductivity values")
    
    # Save skipped report to JSON
    report_path = processed_dir / "missing_k_report.json"
    report_data = {
        'skipped_count': aggregate_result.get('skipped_count', 0),
        'skipped_ids': aggregate_result.get('skipped_ids', []),
        'total_processed': aggregate_result.get('total_processed', 0),
        'reason': 'Missing or NaN thermal conductivity value'
    }
    
    with open(report_path, 'w', encoding='utf-8') as f:
        json.dump(report_data, f, indent=2)
    
    logger.info(f"Saved missing thermal conductivity report to {report_path}")

def main():
    """
    Main entry point for the descriptor aggregation pipeline.
    Handles missing thermal conductivity values by skipping those configurations
    and logging the count as required by the specification.
    """
    logger.info("Starting descriptor aggregation pipeline...")
    
    # Load processed configurations
    configs = load_processed_configs()
    if not configs:
        logger.error("No configurations loaded. Exiting.")
        return
    
    logger.info(f"Loaded {len(configs)} configurations for aggregation")
    
    # Aggregate and handle missing k values
    aggregate_result = aggregate_descriptors(configs)
    
    # Save results
    save_aggregated_data(aggregate_result)
    
    logger.info("Descriptor aggregation pipeline completed successfully")

if __name__ == "__main__":
    main()