"""
Integration test for T017: Save processed feature-engineered dataset.
Verifies that the script produces a valid CSV with the correct schema and traceability.
"""
import os
import sys
import pytest
import pandas as pd
from pathlib import Path

# Add project root to path
PROJECT_ROOT = Path(__file__).resolve().parents[2]
sys.path.insert(0, str(PROJECT_ROOT))

from utils.schema_validator import load_schema, validate_csv_schema
from utils.state_manager import compute_sha256

class TestT017SaveFeatures:
    """Tests for the save_features artifact generation."""

    @pytest.fixture(autouse=True)
    def setup(self):
        """Ensure required paths exist before tests."""
        self.output_path = PROJECT_ROOT / "data" / "processed" / "features.csv"
        self.schema_path = PROJECT_ROOT / "contracts" / "data_schema.yaml"

    def test_file_exists(self):
        """Verify that features.csv is created by the script."""
        # Note: This test assumes the script has been run.
        # In a CI pipeline, this would be preceded by running the script.
        # For the purpose of this task, we verify the existence of the file
        # if it was generated by the implementation of T017.
        # If the file doesn't exist, the test fails, indicating the script
        # hasn't been run or failed.
        assert self.output_path.exists(), f"Output file {self.output_path} does not exist. Run code/data/save_features.py first."

    def test_schema_matches_contracts(self):
        """Validate the output CSV against the contract schema."""
        if not self.output_path.exists():
            pytest.skip("Output file not found, skipping schema validation.")

        df = pd.read_csv(self.output_path)
        
        # Load expected schema
        if self.schema_path.exists():
            schema = load_schema(str(self.schema_path))
            is_valid, errors = validate_csv_schema(df, schema)
            assert is_valid, f"Schema validation failed: {errors}"
        else:
            # Fallback: check for essential columns if schema file is missing
            required_cols = ['composition', 'source_row_id']
            for col in required_cols:
                assert col in df.columns, f"Missing required column: {col}"

    def test_source_row_id_traceability(self):
        """Verify that source_row_id column exists and is numeric."""
        if not self.output_path.exists():
            pytest.skip("Output file not found.")

        df = pd.read_csv(self.output_path)
        assert 'source_row_id' in df.columns, "Missing source_row_id for traceability (Constitution Principle IV)"
        
        # Check that IDs are unique and non-null
        assert df['source_row_id'].notnull().all(), "source_row_id contains null values"
        assert df['source_row_id'].is_unique, "source_row_id values are not unique"

    def test_no_null_values_in_features(self):
        """Ensure critical feature columns do not have nulls for known elements."""
        if not self.output_path.exists():
            pytest.skip("Output file not found.")

        df = pd.read_csv(self.output_path)
        
        # Define critical columns that should not be null based on US1
        critical_cols = ['atomic_radius_mean', 'electronegativity_mean', 'VEC_avg']
        for col in critical_cols:
            if col in df.columns:
                assert df[col].notnull().all(), f"Column {col} contains null values"

    def test_artifact_hash_updated(self):
        """Verify that the state file was updated with the new artifact hash."""
        state_file = PROJECT_ROOT / "state" / "artifact_hashes.yaml"
        if not state_file.exists():
            # If state file doesn't exist, the script might not have run or state manager failed
            pytest.skip("State file not found. This might indicate the script didn't run successfully.")
        
        # Just verify the file exists and is valid yaml (importing yaml here to avoid dependency issues in test env if not installed)
        try:
            import yaml
            with open(state_file, 'r') as f:
                data = yaml.safe_load(f)
            
            # Check if our output file is in the list
            found = False
            if isinstance(data, list):
                for entry in data:
                    if isinstance(entry, dict) and entry.get('path') == str(self.output_path):
                        found = True
                        # Verify hash is not empty
                        assert entry.get('hash'), f"Hash for {self.output_path} is empty"
                        break
            elif isinstance(data, dict) and self.output_path.name in data:
                found = True
                assert data[self.output_path.name], f"Hash for {self.output_path} is empty"
            
            assert found, f"Artifact {self.output_path} not found in {state_file}"
        except ImportError:
            pytest.skip("PyYAML not installed, skipping hash verification.")