"""
Reporting module for generating summary statistics and analysis reports.

This module implements T030: Generate summary_report.txt with coefficient, 
p-value, MDES, CV scores, and data loss metrics.
"""
import os
import sys
import json
import logging
from pathlib import Path
from typing import Dict, Any, Optional
from datetime import datetime

# Import from sibling modules using the exact API surface provided
from logging_config import setup_logging, get_logger
from ingestion import generate_validation_report
from modeling import calculate_mdes, group_and_count_neurons

# Setup module logger
logger = get_logger(__name__)


def load_json_file(file_path: Path) -> Dict[str, Any]:
    """Load and parse a JSON file."""
    if not file_path.exists():
        raise FileNotFoundError(f"Required file not found: {file_path}")
    
    with open(file_path, 'r') as f:
        return json.load(f)


def load_validation_metrics(data_dir: Path) -> Dict[str, Any]:
    """
    Load data loss metrics from the validation report.
    
    Expects: data/processed/validation_report.json
    """
    validation_file = data_dir / "processed" / "validation_report.json"
    
    if not validation_file.exists():
        logger.warning(f"Validation report not found at {validation_file}. "
                     "Using default metrics.")
        return {
            "ingestion_rows_total": 0,
            "ingestion_rows_valid": 0,
            "ingestion_rows_dropped": 0,
            "validation_status": "unknown"
        }
    
    return load_json_file(validation_file)


def load_model_results(data_dir: Path) -> Dict[str, Any]:
    """
    Load modeling results from the modeling output.
    
    Expects: data/processed/model_results.json (generated by modeling.py)
    """
    results_file = data_dir / "processed" / "model_results.json"
    
    if not results_file.exists():
        logger.warning(f"Model results not found at {results_file}. "
                     "Report will indicate missing model output.")
        return {
            "coefficient": None,
            "p_value": None,
            "cv_score_mean": None,
            "cv_score_std": None,
            "mdes_80_power": None,
            "neuron_count": 0,
            "bonferroni_corrected_p": None
        }
    
    return load_json_file(results_file)


def generate_summary_report(
    data_dir: Path,
    output_path: Optional[Path] = None
) -> Path:
    """
    Generate a comprehensive summary report (T030).
    
    Creates summary_report.txt with:
    - GLM coefficient and p-value
    - MDES (Minimum Detectable Effect Size)
    - Cross-validation scores
    - Data loss metrics from validation
    - Neuron count and multiple comparisons correction
    
    Args:
        data_dir: Root directory containing data/processed/
        output_path: Optional custom output path. Defaults to data/processed/summary_report.txt
    
    Returns:
        Path to the generated report file
    """
    if output_path is None:
        output_path = data_dir / "processed" / "summary_report.txt"
    
    logger.info(f"Generating summary report at {output_path}")
    
    # Load metrics
    validation_metrics = load_validation_metrics(data_dir)
    model_results = load_model_results(data_dir)
    
    # Get current timestamp
    report_time = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    
    # Build report content
    report_lines = [
        "=" * 70,
        "NEURAL CORRELATES OF ANTICIPATORY REWARD PROCESSING",
        "SUMMARY ANALYSIS REPORT",
        "=" * 70,
        "",
        f"Generated: {report_time}",
        "",
        "-" * 70,
        "DATA QUALITY METRICS (from validation pipeline)",
        "-" * 70,
        f"  Total rows ingested:      {validation_metrics.get('ingestion_rows_total', 'N/A')}",
        f"  Valid rows retained:      {validation_metrics.get('ingestion_rows_valid', 'N/A')}",
        f"  Rows dropped:             {validation_metrics.get('ingestion_rows_dropped', 'N/A')}",
        f"  Validation status:        {validation_metrics.get('validation_status', 'N/A')}",
        "",
        "-" * 70,
        "STATISTICAL MODEL RESULTS (GLM: firing_rate ~ reward_magnitude)",
        "-" * 70,
    ]
    
    # Model coefficient and p-value
    coef = model_results.get('coefficient')
    p_val = model_results.get('p_value')
    
    if coef is not None:
        report_lines.append(f"  GLM Coefficient (β):      {coef:.6f}")
    else:
        report_lines.append("  GLM Coefficient (β):      N/A (model not run)")
    
    if p_val is not None:
        report_lines.append(f"  P-value:                    {p_val:.6e}")
        
        # Significance flag
        if p_val < 0.05:
            report_lines.append("  Significance:               ** STATISTICALLY SIGNIFICANT (p < 0.05) **")
        else:
            report_lines.append("  Significance:               Not significant (p >= 0.05)")
    else:
        report_lines.append("  P-value:                    N/A")
    
    report_lines.append("")
    
    # Multiple comparisons correction
    neuron_count = model_results.get('neuron_count', 0)
    bonf_p = model_results.get('bonferroni_corrected_p')
    
    report_lines.append(f"  Number of neurons analyzed: {neuron_count}")
    if bonf_p is not None:
        report_lines.append(f"  Bonferroni-corrected p:   {bonf_p:.6e}")
        if bonf_p < 0.05:
            report_lines.append("  Multiple comparisons:       ** SIGNIFICANT AFTER CORRECTION **")
        else:
            report_lines.append("  Multiple comparisons:       Not significant after correction")
    else:
        report_lines.append("  Bonferroni-corrected p:   N/A")
    
    report_lines.append("")
    
    # Power Analysis (MDES)
    mdes = model_results.get('mdes_80_power')
    report_lines.append("-" * 70)
    report_lines.append("POWER ANALYSIS")
    report_lines.append("-" * 70)
    
    if mdes is not None:
        report_lines.append(f"  MDES (80% power, α=0.05):   {mdes:.6f}")
        report_lines.append("  (Minimum Detectable Effect Size in Cohen's f² units)")
    else:
        report_lines.append("  MDES:                         N/A (power analysis not run)")
    
    report_lines.append("")
    
    # Cross-validation results
    cv_mean = model_results.get('cv_score_mean')
    cv_std = model_results.get('cv_score_std')
    
    report_lines.append("-" * 70)
    report_lines.append("CROSS-VALIDATION RESULTS")
    report_lines.append("-" * 70)
    
    if cv_mean is not None:
        report_lines.append(f"  R² (mean):                  {cv_mean:.4f}")
    else:
        report_lines.append("  R² (mean):                  N/A")
    
    if cv_std is not None:
        report_lines.append(f"  R² (std):                   {cv_std:.4f}")
    else:
        report_lines.append("  R² (std):                   N/A")
    
    report_lines.append("")
    
    # Additional model metadata
    model_family = model_results.get('model_family', 'N/A')
    dispersion = model_results.get('dispersion', 'N/A')
    
    report_lines.append("-" * 70)
    report_lines.append("MODEL CONFIGURATION")
    report_lines.append("-" * 70)
    report_lines.append(f"  Model family:               {model_family}")
    report_lines.append(f"  Dispersion parameter:       {dispersion}")
    
    report_lines.append("")
    report_lines.append("=" * 70)
    report_lines.append("END OF REPORT")
    report_lines.append("=" * 70)
    
    # Write report to file
    output_path.parent.mkdir(parents=True, exist_ok=True)
    
    report_content = "\n".join(report_lines)
    with open(output_path, 'w') as f:
        f.write(report_content)
    
    logger.info(f"Summary report successfully written to {output_path}")
    return output_path


def main():
    """
    Main entry point for the reporting module.
    
    Usage: python code/reporting.py
    
    Generates summary_report.txt in data/processed/
    """
    # Setup logging
    setup_logging(level=logging.INFO)
    
    # Determine project root (assumed to be parent of code/)
    script_path = Path(__file__).resolve()
    project_root = script_path.parent.parent
    data_dir = project_root / "data"
    
    logger.info(f"Project root: {project_root}")
    logger.info(f"Data directory: {data_dir}")
    
    try:
        output_path = generate_summary_report(data_dir)
        logger.info("Reporting pipeline completed successfully.")
        print(f"\nSummary report generated: {output_path}")
        return 0
    except FileNotFoundError as e:
        logger.error(f"Required data file not found: {e}")
        print(f"\nError: {e}")
        print("Ensure that previous pipeline stages (ingestion, modeling) have completed successfully.")
        return 1
    except Exception as e:
        logger.exception(f"Unexpected error during report generation: {e}")
        print(f"\nUnexpected error: {e}")
        return 1


if __name__ == "__main__":
    sys.exit(main())
