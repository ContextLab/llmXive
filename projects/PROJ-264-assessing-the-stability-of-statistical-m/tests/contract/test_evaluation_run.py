"""
Contract test for the EvaluationRun schema.

This test validates that the output structure of the evaluation engine
strictly adheres to the schema defined in specs/001-assess-model-stability/contracts/.
It ensures that every record generated by the repeated cross-validation
contains the required fields with the correct data types.
"""
import os
import csv
import pytest
from pathlib import Path

# Define the expected schema based on project specs
# Expected columns: dataset_id, model_name, fold_id, repeat_id, accuracy, f1_score
EXPECTED_COLUMNS = [
    "dataset_id",
    "model_name",
    "fold_id",
    "repeat_id",
    "accuracy",
    "f1_score"
]

# Expected data types for validation
EXPECTED_TYPES = {
    "dataset_id": int,
    "model_name": str,
    "fold_id": int,
    "repeat_id": int,
    "accuracy": float,
    "f1_score": float
}

# File path relative to project root
OUTPUT_PATH = "results/raw_evaluations.csv"


def get_output_path():
    """Resolve the output path relative to the project root."""
    # Try to find project root by looking for a known marker or just use current dir
    # In CI/CD, this is usually the repo root.
    project_root = Path(__file__).resolve().parents[2]
    return project_root / OUTPUT_PATH


def test_evaluation_run_schema_exists():
    """
    Contract Test: Verify that the evaluation run output file exists.
    This is a prerequisite for schema validation.
    """
    path = get_output_path()
    assert path.exists(), f"Contract failed: Output file {OUTPUT_PATH} does not exist. " \
                          f"Ensure the evaluation engine (T011-T014) has been run."


def test_evaluation_run_schema_columns():
    """
    Contract Test: Verify that the output file contains exactly the required columns.
    """
    path = get_output_path()
    if not path.exists():
        pytest.skip("Output file not found; skipping column validation.")

    with open(path, mode="r", encoding="utf-8") as f:
        reader = csv.DictReader(f)
        actual_columns = reader.fieldnames

        assert actual_columns is not None, "Contract failed: File is empty or has no header."

        # Check for exact match of required columns
        missing = set(EXPECTED_COLUMNS) - set(actual_columns)
        extra = set(actual_columns) - set(EXPECTED_COLUMNS)

        assert not missing, f"Contract failed: Missing required columns: {missing}"
        assert not extra, f"Contract failed: Unexpected columns found: {extra}"


def test_evaluation_run_schema_types_and_values():
    """
    Contract Test: Verify that each row contains valid data types and value ranges.
    - dataset_id: int > 0
    - model_name: str in ['Logistic Regression', 'Random Forest', 'Linear SVM']
    - fold_id: int in [0, 9] (for 10-fold CV)
    - repeat_id: int >= 0
    - accuracy: float in [0.0, 1.0]
    - f1_score: float in [0.0, 1.0]
    """
    path = get_output_path()
    if not path.exists():
        pytest.skip("Output file not found; skipping value validation.")

    valid_models = {"Logistic Regression", "Random Forest", "Linear SVM"}
    row_count = 0

    with open(path, mode="r", encoding="utf-8") as f:
        reader = csv.DictReader(f)
        for row in reader:
            row_count += 1

            # Validate dataset_id
            try:
                dataset_id = int(row["dataset_id"])
                assert dataset_id > 0, f"Row {row_count}: dataset_id must be positive."
            except ValueError:
                pytest.fail(f"Row {row_count}: dataset_id is not an integer.")

            # Validate model_name
            model_name = row["model_name"]
            assert model_name in valid_models, \
                f"Row {row_count}: model_name '{model_name}' not in allowed set {valid_models}."

            # Validate fold_id
            try:
                fold_id = int(row["fold_id"])
                # Assuming 10-fold CV as per T011 spec
                assert 0 <= fold_id <= 9, f"Row {row_count}: fold_id {fold_id} out of range [0, 9]."
            except ValueError:
                pytest.fail(f"Row {row_count}: fold_id is not an integer.")

            # Validate repeat_id
            try:
                repeat_id = int(row["repeat_id"])
                assert repeat_id >= 0, f"Row {row_count}: repeat_id must be non-negative."
            except ValueError:
                pytest.fail(f"Row {row_count}: repeat_id is not an integer.")

            # Validate accuracy
            try:
                accuracy = float(row["accuracy"])
                assert 0.0 <= accuracy <= 1.0, \
                    f"Row {row_count}: accuracy {accuracy} out of range [0.0, 1.0]."
            except ValueError:
                pytest.fail(f"Row {row_count}: accuracy is not a float.")

            # Validate f1_score
            try:
                f1_score = float(row["f1_score"])
                assert 0.0 <= f1_score <= 1.0, \
                    f"Row {row_count}: f1_score {f1_score} out of range [0.0, 1.0]."
            except ValueError:
                pytest.fail(f"Row {row_count}: f1_score is not a float.")

    assert row_count > 0, "Contract failed: Output file contains no data rows."