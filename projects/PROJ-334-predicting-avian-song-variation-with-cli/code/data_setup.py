"""
Data directory structure setup and checksum initialization.

This module creates the required directory hierarchy for the project's
data artifacts and initializes the checksums tracking file.
"""

import os
import hashlib
from pathlib import Path

# Define the base data directory relative to project root
DATA_DIR = Path("data")
RAW_DIR = DATA_DIR / "raw"
PROCESSED_DIR = DATA_DIR / "processed"
CHECKSUMS_FILE = DATA_DIR / "checksums.txt"

def ensure_directory(path: Path) -> None:
    """Create a directory if it does not exist."""
    if not path.exists():
        path.mkdir(parents=True, exist_ok=True)
        print(f"Created directory: {path}")
    else:
        print(f"Directory already exists: {path}")

def initialize_checksums_file() -> None:
    """
    Initialize the checksums.txt file if it doesn't exist.
    Adds a header and ensures the file is writable.
    """
    if not CHECKSUMS_FILE.exists():
        CHECKSUMS_FILE.write_text(
            "# Data Artifact Checksums\n"
            "# Format: <sha256_hash>  <relative_path>\n"
            "# Generated by data_setup.py\n"
            "\n"
        )
        print(f"Initialized checksums file: {CHECKSUMS_FILE}")
    else:
        print(f"Checksums file already exists: {CHECKSUMS_FILE}")

def main() -> None:
    """
    Main entry point to setup data directory structure and initialize checksums.
    """
    print("Setting up data directory structure...")
    
    # Create required directories
    ensure_directory(DATA_DIR)
    ensure_directory(RAW_DIR)
    ensure_directory(PROCESSED_DIR)
    
    # Initialize checksums file
    initialize_checksums_file()
    
    print("Data directory setup complete.")

if __name__ == "__main__":
    main()
