def create_test_data(output_path: Path) -> Path:
    """
    Creates a realistic synthetic dataset for testing.
    This mimics the structure of data/processed/unified_metrics.csv
    generated by the preprocessing pipeline.
    """
    # Ensure the directory exists
    output_path.parent.mkdir(parents=True, exist_ok=True)

    np.random.seed(42)
    n_rows = 200

    # Simulate a moderate positive correlation between churn and debt
    # debt = 0.5 * churn + noise
    total_lines_changed = np.random.exponential(scale=100, size=n_rows)
    noise = np.random.normal(0, 50, size=n_rows)
    debt_score = (0.5 * total_lines_changed) + noise + 10

    # Ensure no negative values
    debt_score = np.maximum(debt_score, 0)

    # Add some covariates
    avg_loc = np.random.uniform(20, 100, size=n_rows)
    contributor_count = np.random.randint(1, 50, size=n_rows)
    repo_id = np.random.choice(["repo_A", "repo_B", "repo_C"], size=n_rows)

    df = pd.DataFrame({
        "repo_id": repo_id,
        "file_path": [f"src/file_{i}.py" for i in range(n_rows)],
        "total_lines_changed": total_lines_changed,
        "debt_score": debt_score,
        "avg_loc": avg_loc,
        "contributor_count": contributor_count
    })

    df.to_csv(output_path, index=False)
    logger.info(f"Created test dataset at {output_path} with {n_rows} rows.")
    return output_path


