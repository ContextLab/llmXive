"""
Contract test for `correlation_results.csv` schema.
Validates the structure and required columns of the output generated by User Story 2.
"""
import os
import csv
import pytest
from pathlib import Path

# Define the expected schema based on T019, T020, T023 requirements
# The file must contain raw metrics correlations and model results
EXPECTED_COLUMNS = [
    "repo_id",
    "language",
    "threshold",          # From sensitivity analysis (5, 10, 20)
    "correlation_type",   # "pearson" or "spearman"
    "r_value",            # Correlation coefficient
    "p_value",            # P-value
    "n_samples",          # Number of files analyzed
    "model_intercept",    # Mixed-effects model intercept
    "slope_lines_changed", # Coefficient for total_lines_changed
    "vif_status",         # "ok" or "ridge_applied"
    "conclusion"          # e.g., "significant", "not_significant"
]

DATA_PATH = Path("data/results/correlation_results.csv")

def test_output_file_exists():
    """Verify the output file is generated."""
    assert DATA_PATH.exists(), f"Output file not found: {DATA_PATH}"

def test_schema_columns_present():
    """Verify all required columns exist in the CSV."""
    if not DATA_PATH.exists():
        pytest.skip("Output file not generated yet")

    with open(DATA_PATH, "r", newline="", encoding="utf-8") as f:
        reader = csv.DictReader(f)
        headers = reader.fieldnames

        assert headers is not None, "CSV file is empty or has no headers"
        missing = set(EXPECTED_COLUMNS) - set(headers)
        assert not missing, f"Missing required columns: {missing}"

def test_schema_data_types_and_ranges():
    """Verify data types and logical ranges for key columns."""
    if not DATA_PATH.exists():
        pytest.skip("Output file not generated yet")

    with open(DATA_PATH, "r", newline="", encoding="utf-8") as f:
        reader = csv.DictReader(f)
        rows = list(reader)

    assert len(rows) > 0, "CSV file contains no data rows"

    for i, row in enumerate(rows):
        # Check numeric columns can be parsed
        try:
            r_val = float(row["r_value"])
            p_val = float(row["p_value"])
            n_samples = int(row["n_samples"])
            
            # Logical constraints
            assert -1.0 <= r_val <= 1.0, f"Row {i}: r_value out of bounds [{r_val}]"
            assert 0.0 <= p_val <= 1.0, f"Row {i}: p_value out of bounds [{p_val}]"
            assert n_samples > 0, f"Row {i}: n_samples must be positive"
        except ValueError as e:
            pytest.fail(f"Row {i}: Invalid numeric format - {e}")

        # Check categorical columns
        assert row["correlation_type"] in ["pearson", "spearman"], \
            f"Row {i}: Invalid correlation_type '{row['correlation_type']}'"
        
        assert row["vif_status"] in ["ok", "ridge_applied"], \
            f"Row {i}: Invalid vif_status '{row['vif_status']}'"

        # Check threshold is one of the allowed values (5, 10, 20)
        try:
            threshold = int(row["threshold"])
            assert threshold in [5, 10, 20], f"Row {i}: Invalid threshold {threshold}"
        except ValueError:
            pytest.fail(f"Row {i}: Threshold is not an integer")

def test_no_nulls_in_critical_columns():
    """Ensure critical analysis columns are not empty."""
    if not DATA_PATH.exists():
        pytest.skip("Output file not generated yet")

    critical_cols = ["r_value", "p_value", "n_samples", "slope_lines_changed"]
    
    with open(DATA_PATH, "r", newline="", encoding="utf-8") as f:
        reader = csv.DictReader(f)
        for i, row in enumerate(reader):
            for col in critical_cols:
                val = row.get(col, "").strip()
                assert val != "", f"Row {i}: Critical column '{col}' is empty"