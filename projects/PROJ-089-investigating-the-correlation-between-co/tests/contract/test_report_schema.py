"""
Contract test for summary_report.txt content.
Verifies that the summary report generated by the pipeline meets schema requirements.
"""
import os
import re
from pathlib import Path
import pytest

# Constants for report validation
REPORT_PATH = Path("data/results/summary_report.txt")
REQUIRED_SECTIONS = [
  "Project Summary",
  "Per-Repository Results",
  "Aggregate Results",
  "Meta-Analysis Outcome",
  "Sensitivity Analysis",
  "Methodology Notes"
]

REQUIRED_METRICS_IN_REPORT = [
  "total_lines_changed",
  "debt_score",
  "avg_loc",
  "contributor_count",
  "correlation_coefficient",
  "p_value"
]

CORRELATION_FLAGS = ["moderate", "strong", "weak", "insignificant"]


def _load_report() -> str:
    """Load the summary report content."""
    if not REPORT_PATH.exists():
        pytest.fail(f"Report file not found: {REPORT_PATH}")
    return REPORT_PATH.read_text(encoding="utf-8")


def _check_section_presence(content: str) -> None:
    """Verify all required sections are present in the report."""
    missing_sections = []
    for section in REQUIRED_SECTIONS:
        if section not in content:
            missing_sections.append(section)
    
    if missing_sections:
        pytest.fail(f"Missing required sections in report: {missing_sections}")


def _check_metric_mentions(content: str) -> None:
    """Verify that required metrics are mentioned in the report."""
    missing_metrics = []
    for metric in REQUIRED_METRICS_IN_REPORT:
        # Check for metric name or common variations
        if metric not in content and metric.replace("_", " ") not in content:
            missing_metrics.append(metric)
    
    if missing_metrics:
        pytest.fail(f"Missing metric mentions in report: {missing_metrics}")


def _check_correlation_flags(content: str) -> None:
    """Verify that correlation strength flags are used appropriately."""
    # At least one correlation flag should be present
    found_flags = [flag for flag in CORRELATION_FLAGS if flag in content.lower()]
    
    if not found_flags:
        pytest.fail("No correlation strength flags found in report. "
                   "Expected at least one of: " + ", ".join(CORRELATION_FLAGS))


def _check_numeric_values(content: str) -> None:
    """Verify that numeric values are present for key metrics."""
    # Pattern to match correlation coefficients (e.g., r = 0.35, r= -0.12)
    r_pattern = re.compile(r"r\s*=\s*[-+]?\d*\.?\d+", re.IGNORECASE)
    p_pattern = re.compile(r"p\s*[<>=]\s*[-+]?\d*\.?\d+", re.IGNORECASE)
    
    if not r_pattern.search(content):
        pytest.fail("No correlation coefficient (r) found in report.")
    
    if not p_pattern.search(content):
        pytest.fail("No p-value found in report.")


def test_report_file_exists():
    """Test that the summary report file exists."""
    assert REPORT_PATH.exists(), f"Report file missing: {REPORT_PATH}"


def test_report_has_required_sections():
    """Test that the report contains all required sections."""
    content = _load_report()
    _check_section_presence(content)


def test_report_mentions_required_metrics():
    """Test that the report mentions all required metrics."""
    content = _load_report()
    _check_metric_mentions(content)


def test_report_contains_correlation_flags():
    """Test that the report uses appropriate correlation strength flags."""
    content = _load_report()
    _check_correlation_flags(content)


def test_report_contains_numeric_results():
    """Test that the report contains numeric correlation and p-values."""
    content = _load_report()
    _check_numeric_values(content)


def test_report_schema_compliance():
    """
    Comprehensive test for full schema compliance.
    This test runs all validation checks in sequence.
    """
    content = _load_report()
    
    # Run all validation checks
    _check_section_presence(content)
    _check_metric_mentions(content)
    _check_correlation_flags(content)
    _check_numeric_values(content)
    
    # Additional check: verify report is not empty
    assert len(content.strip()) > 100, "Report content is too short to be valid."


if __name__ == "__main__":
    pytest.main([__file__, "-v"])