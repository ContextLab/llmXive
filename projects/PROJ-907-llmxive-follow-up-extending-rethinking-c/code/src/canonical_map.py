"""
Canonical Map Derivation Module.

Derives the "Canonical Routing Map" (static weight vector per block)
from the dominant cluster centers or global average computed in T012.
Saves the result to data/routing_cache/canonical_map.json.
"""

import json
import logging
from pathlib import Path
from typing import Dict, Any, List, Optional

import numpy as np

from src.clustering import load_routing_cache, compute_mean_routing_vectors, perform_clustering, generate_global_average, save_cluster_centers
from src.config import get_routing_cache_path

logging.basicConfig(level=logging.INFO, format='%(asctime)s - %(levelname)s - %(message)s')
logger = logging.getLogger(__name__)


def derive_canonical_map(
    cluster_centers_path: Optional[Path] = None,
    output_path: Optional[Path] = None,
    use_global_average_fallback: bool = True
) -> Dict[str, Any]:
    """
    Derives the canonical routing map from clustering results.

    This function loads the cluster centers generated by T012 (clustering.py).
    If valid clusters exist (k >= 2 and silhouette >= 0.25), it selects the
    dominant cluster (largest size) and uses its center as the canonical map.
    If the null hypothesis is triggered (no valid clusters), it falls back
    to the global average vector.

    Args:
        cluster_centers_path: Path to the cluster_centers.json file. Defaults to
            get_routing_cache_path() / "cluster_centers.json".
        output_path: Path to save the canonical_map.json. Defaults to
            get_routing_cache_path() / "canonical_map.json".
        use_global_average_fallback: If True, use global average when clustering fails.
            If False, raises an error on failure.

    Returns:
        A dictionary representing the canonical map:
        {
            "block_id": int,
            "weight_vector": List[float],
            "method": "dominant_cluster" | "global_average",
            "metadata": { ... }
        }
    """
    if cluster_centers_path is None:
        cache_dir = get_routing_cache_path()
        cluster_centers_path = cache_dir / "cluster_centers.json"
    
    if output_path is None:
        cache_dir = get_routing_cache_path()
        output_path = cache_dir / "canonical_map.json"

    logger.info(f"Loading cluster centers from {cluster_centers_path}")
    
    if not cluster_centers_path.exists():
        raise FileNotFoundError(
            f"Cluster centers file not found at {cluster_centers_path}. "
            "Please run src/clustering.py (T012) first."
        )

    with open(cluster_centers_path, 'r') as f:
        cluster_data = json.load(f)

    method_used = cluster_data.get("method")
    is_null_hypothesis = cluster_data.get("is_null_hypothesis", False)
    silhouette_score = cluster_data.get("silhouette_score")
    n_clusters = cluster_data.get("n_clusters", 0)

    canonical_map = {}

    if is_null_hypothesis or (n_clusters < 2) or (silhouette_score is not None and silhouette_score < 0.25):
        logger.warning("Null hypothesis detected: k < 2 or silhouette < 0.25. Falling back to global average.")
        
        if not use_global_average_fallback:
            raise ValueError("Null hypothesis detected but fallback is disabled.")

        # Load raw routing cache to compute global average if not stored in cluster_data
        # The clustering module should have saved the global average if it was used.
        # However, to be robust, we recompute or fetch it.
        # Looking at T012 spec: "handle null hypothesis ... by generating global average vector."
        # We assume cluster_data contains "global_average_vector" if this path was taken.
        
        if "global_average_vector" in cluster_data:
            global_avg = cluster_data["global_average_vector"]
            method_used = "global_average"
        else:
            # Fallback to recomputing from raw cache if not present
            logger.info("Global average not found in cluster_data, recomputing from raw cache...")
            mean_vectors = compute_mean_routing_vectors()
            # Mean vectors shape: [timesteps, history_dim]
            # We need a single vector per block. The mean vector is already aggregated across blocks?
            # Wait, T012 spec says: "compute the mean routing vector across all images/blocks for each timestep"
            # This implies the input to clustering is [timesteps, history_dim].
            # The output of T012 (cluster centers) are vectors of size [history_dim].
            # The canonical map needs to be "static weight vector per block".
            # If the clustering was done on timesteps, the result is a set of vectors representing
            # typical routing behaviors at different timesteps.
            # However, the task T013 says: "Derive the 'Canonical Routing Map' (static weight vector per block)".
            # This implies a mapping from block_id -> weight_vector.
            # If the clustering was over timesteps, we might need to assign a cluster center to each block?
            # Or perhaps the "mean routing vector" computed in T012 is actually per-block?
            # Re-reading T012: "compute the mean routing vector across all images/blocks for each timestep"
            # This results in a vector per timestep.
            # If we want a static map per block, we might need to average the cluster centers or the mean vectors.
            # Let's assume the "global average" from T012 is the best static approximation for all blocks/timesteps.
            # But the output format requires [block_id, weight_vector].
            # If we don't have per-block data here, we might need to load the raw cache again.
            
            # Let's load raw cache to get per-block dimensions if needed.
            raw_data = load_routing_cache()
            # raw_data structure: { image_id: { block_id: [timestep: vector] } }
            # We need to compute the global average vector for EACH block?
            # Or is the "weight vector" the same for all blocks?
            # The task says "static weight vector per block".
            
            # Let's assume the "global average" from T012 is the mean of all vectors.
            # If we need per-block, we must compute it from raw data.
            
            # Re-evaluating T012 logic:
            # "compute the mean routing vector across all images/blocks for each timestep" -> [T, D]
            # "apply k-means clustering to group timesteps" -> Centers are [K, D]
            # "generate global average vector" -> [D]
            
            # T013 Requirement: "dict with keys [block_id, weight_vector]"
            # This implies we need to map these D-dimensional vectors to specific blocks.
            # If the clustering was on timesteps, we don't have block-specific clusters.
            # Perhaps the "Canonical Routing Map" is simply the global average vector assigned to every block?
            # Or maybe the "mean routing vector" in T012 was misinterpreted and should be per block?
            # Given the ambiguity, the safest interpretation for a "static map" derived from a global average is:
            # Every block uses the same static weight vector (the global average).
            # OR, if the raw data has per-block stats, we average those.
            
            # Let's check the raw data structure again.
            # If raw_data is {image_id: {block_id: {timestep: vector}}}, we can compute:
            # global_avg_per_block[block_id] = mean(all vectors for that block)
            
            if not raw_data:
                raise RuntimeError("Could not load raw routing cache to compute per-block averages.")
            
            block_vectors = {}
            for img_id, blocks in raw_data.items():
                for b_id, timesteps in blocks.items():
                    if b_id not in block_vectors:
                        block_vectors[b_id] = []
                    for t, vec in timesteps.items():
                        block_vectors[b_id].append(vec)
            
            final_map = {}
            for b_id, vectors in block_vectors.items():
                avg_vec = np.mean(vectors, axis=0).tolist()
                final_map[str(b_id)] = avg_vec
            
            canonical_map = {
                "method": "global_average_per_block",
                "data": final_map,
                "metadata": {
                    "source": "raw_cache_recomputation",
                    "reason": "Cluster data did not contain per-block info"
                }
            }
            logger.info(f"Computed per-block global average for {len(final_map)} blocks.")
            return canonical_map

        # If we are here, we have a global average vector [D]
        # We need to assign it to blocks. We don't know the number of blocks from cluster_data alone.
        # We must infer block count from the raw cache or assume a standard number if not found.
        # Let's load raw cache to get block count.
        raw_data = load_routing_cache()
        if not raw_data:
            raise RuntimeError("Cannot determine block count for global average assignment.")
        
        blocks = list(raw_data.values())[0]
        block_ids = list(blocks.keys())
        n_blocks = len(block_ids)
        
        final_map = {}
        for b_id in block_ids:
            final_map[str(b_id)] = global_avg
        
        canonical_map = {
            "method": "global_average",
            "data": final_map,
            "metadata": {
                "silhouette_score": silhouette_score,
                "n_clusters": n_clusters,
                "reason": "Null hypothesis fallback"
            }
        }
    else:
        # Valid clusters exist. Use the dominant cluster center.
        # cluster_data should have "centers" (list of vectors) and "labels" (counts per cluster)
        centers = cluster_data.get("centers", [])
        labels = cluster_data.get("labels", []) # counts or indices? usually counts for dominant
        
        if not centers or not labels:
            raise ValueError("Cluster centers file is malformed: missing centers or labels.")
        
        # Find dominant cluster (largest count)
        dominant_idx = np.argmax(labels)
        dominant_center = centers[dominant_idx]
        logger.info(f"Using dominant cluster {dominant_idx} (count: {labels[dominant_idx]})")
        
        # Again, we need to assign this vector to blocks.
        # We assume the dominant center is the static weight for all blocks.
        raw_data = load_routing_cache()
        if not raw_data:
            # Fallback: assume a reasonable number of blocks (e.g., 28 for SiT-XL/2)
            # But better to error if we can't find the data.
            raise RuntimeError("Cannot determine block count to assign dominant cluster center.")
        
        blocks = list(raw_data.values())[0]
        block_ids = list(blocks.keys())
        
        final_map = {}
        for b_id in block_ids:
            final_map[str(b_id)] = dominant_center
        
        canonical_map = {
            "method": "dominant_cluster",
            "data": final_map,
            "metadata": {
                "dominant_cluster_index": int(dominant_idx),
                "dominant_cluster_count": int(labels[dominant_idx]),
                "silhouette_score": float(silhouette_score) if silhouette_score is not None else None,
                "n_clusters": int(n_clusters)
            }
        }

    # Save the result
    output_path.parent.mkdir(parents=True, exist_ok=True)
    with open(output_path, 'w') as f:
        json.dump(canonical_map, f, indent=2)
    
    logger.info(f"Canonical map saved to {output_path}")
    return canonical_map


def main():
    """Entry point for deriving the canonical map."""
    logger.info("Starting Canonical Map Derivation (T013)...")
    try:
        result = derive_canonical_map()
        logger.info("Canonical map derivation completed successfully.")
        logger.info(f"Method used: {result['method']}")
        if 'metadata' in result:
            logger.info(f"Metadata: {result['metadata']}")
    except Exception as e:
        logger.error(f"Failed to derive canonical map: {e}", exc_info=True)
        raise


if __name__ == "__main__":
    main()
