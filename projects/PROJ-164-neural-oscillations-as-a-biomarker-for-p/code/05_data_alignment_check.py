"""
T015: Data Alignment Check
Verify subject overlap between EEG and tDCS within the single source.

This script runs immediately after T013 (download). It inspects the downloaded
files in data/raw/ to ensure that subjects present in the EEG data also have
corresponding tDCS response data.

If a mismatch is found (missing pairing), it sets the mode flag to 'Data Insufficient'
and terminates the pipeline gracefully (exit code 0), as per FR-011.
"""
import json
import logging
import os
import sys
from pathlib import Path
from typing import Set, Dict, Any

# Add project root to path for imports
sys.path.insert(0, str(Path(__file__).parent.parent))

from utils.logging_setup import get_logger, log_mode_switch
from utils.config import PROJECT_ROOT, STATE_DIR

# Constants
RAW_DATA_DIR = PROJECT_ROOT / "data" / "raw"
MANIFEST_PATH = PROJECT_ROOT / "verified_source_manifest.json"
MODE_FLAG_PATH = STATE_DIR / "mode_flag.json"

logger = get_logger(__name__)

def extract_subject_id(filename: str) -> str:
    """
    Extract subject ID from filename patterns like:
    sub-{subject_id}_run-{run_id}.edf
    sub-{subject_id}_tDCS_response.csv
    
    Returns the subject_id part.
    """
    # Handle .edf files (EEG)
    if filename.endswith(".edf"):
        parts = filename.split("_")
        if len(parts) >= 2 and parts[0].startswith("sub-"):
            return parts[0].replace("sub-", "")
    
    # Handle tDCS response files (assumed naming convention based on spec)
    # If the file contains "tDCS" or "response", we assume it belongs to a subject
    # We try to extract sub- prefix if present, otherwise assume the filename 
    # itself or a related metadata file defines the subject.
    # For this implementation, we assume the manifest or a sidecar JSON defines the pairing.
    # However, per FR-011, we must verify overlap.
    
    # Fallback: if no sub- prefix, we might need to look at a sidecar or manifest.
    # Since T013 downloads files, we assume the manifest contains the mapping.
    return "unknown"

def load_manifest() -> Dict[str, Any]:
    """Load the verified source manifest generated by T011."""
    if not MANIFEST_PATH.exists():
        raise FileNotFoundError(f"Manifest not found at {MANIFEST_PATH}. T011 must run first.")
    
    with open(MANIFEST_PATH, 'r') as f:
        return json.load(f)

def get_downloaded_files() -> Dict[str, Set[str]]:
    """
    Scan data/raw/ and categorize files by type (EEG vs tDCS) and subject.
    
    Returns a dict: {'eeg': set(subject_ids), 'tdcs': set(subject_ids)}
    """
    eeg_subjects: Set[str] = set()
    tdcs_subjects: Set[str] = set()
    
    if not RAW_DATA_DIR.exists():
        logger.warning(f"Raw data directory {RAW_DATA_DIR} does not exist.")
        return {'eeg': eeg_subjects, 'tdcs': tdcs_subjects}

    # We rely on the manifest to define which files are EEG and which are tDCS
    # and their subject mappings, as filenames alone might not be sufficient
    # if the dataset uses different conventions.
    manifest = load_manifest()
    
    # If the manifest has a 'files' section with metadata, use it.
    # Expected structure based on T011:
    # {
    #   "source": "...",
    #   "query": "...",
    #   "files": [
    #     {"filename": "sub-01_run-1.edf", "type": "eeg", "subject_id": "01"},
    #     {"filename": "sub-01_tDCS.csv", "type": "tdcs", "subject_id": "01"}
    #   ]
    # }
    
    files_info = manifest.get("files", [])
    
    for file_entry in files_info:
        filename = file_entry.get("filename", "")
        file_type = file_entry.get("type", "")
        subject_id = file_entry.get("subject_id", "")
        
        # Check if file actually exists on disk (T013 should have downloaded it)
        file_path = RAW_DATA_DIR / filename
        if not file_path.exists():
            logger.warning(f"File listed in manifest not found on disk: {filename}")
            continue
        
        if file_type == "eeg":
            eeg_subjects.add(subject_id)
        elif file_type == "tdcs":
            tdcs_subjects.add(subject_id)
    
    return {'eeg': eeg_subjects, 'tdcs': tdcs_subjects}

def verify_alignment(eeg_subjects: Set[str], tdcs_subjects: Set[str]) -> bool:
    """
    Verify that the set of subjects in EEG matches the set of subjects in tDCS.
    
    Returns True if aligned, False otherwise.
    """
    if not eeg_subjects or not tdcs_subjects:
        logger.error("No subjects found in either EEG or tDCS data.")
        return False
    
    missing_in_tdcs = eeg_subjects - tdcs_subjects
    missing_in_eeg = tdcs_subjects - eeg_subjects
    
    if missing_in_tdcs:
        logger.warning(f"Subjects with EEG but no tDCS response: {missing_in_tdcs}")
    if missing_in_eeg:
        logger.warning(f"Subjects with tDCS response but no EEG: {missing_in_eeg}")
        
    return len(missing_in_tdcs) == 0 and len(missing_in_eeg) == 0

def set_mode_insufficient():
    """Set the mode flag to Data Insufficient and log it."""
    mode_data = {
        "mode": "Data Insufficient",
        "reason": "Subject alignment check failed: EEG and tDCS data do not fully overlap.",
        "timestamp": str(Path(__file__).parent.parent) # Placeholder for actual timestamp logic if needed
    }
    # Ensure state directory exists
    STATE_DIR.mkdir(parents=True, exist_ok=True)
    
    with open(MODE_FLAG_PATH, 'w') as f:
        json.dump(mode_data, f, indent=2)
    
    log_mode_switch("Data Insufficient", "Subject alignment mismatch")
    logger.critical("Pipeline terminated: Data Insufficient mode set due to alignment failure.")

def main():
    """Main entry point for T015."""
    logger.info("Starting Data Alignment Check (T015)...")
    
    try:
        files_by_type = get_downloaded_files()
        eeg_subs = files_by_type['eeg']
        tdcs_subs = files_by_type['tdcs']
        
        logger.info(f"Found {len(eeg_subs)} subjects in EEG data.")
        logger.info(f"Found {len(tdcs_subs)} subjects in tDCS data.")
        
        if verify_alignment(eeg_subs, tdcs_subs):
            logger.info("Data alignment check PASSED. All subjects have paired data.")
            # No mode switch needed, proceed to next task
            return 0
        else:
            logger.error("Data alignment check FAILED. Mismatch detected.")
            set_mode_insufficient()
            return 0 # Exit gracefully as per spec
            
    except Exception as e:
        logger.exception(f"Error during alignment check: {e}")
        # If we can't even check, we must assume data is insufficient to proceed safely
        set_mode_insufficient()
        return 0

if __name__ == "__main__":
    sys.exit(main())
