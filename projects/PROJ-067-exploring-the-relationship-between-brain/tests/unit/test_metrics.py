def test_flexibility_random_walk_simulation():
    """
    Test with a simulated random walk of community assignments.
    This ensures the logic holds for non-deterministic inputs.
    """
    np.random.seed(42)
    n_windows = 100
    n_communities = 5
    
    # Generate a random sequence of community assignments
    communities = np.random.randint(0, n_communities, size=n_windows)
    
    # Calculate manually to verify
    manual_transitions = np.sum(communities[1:] != communities[:-1])
    manual_expected = manual_transitions / (n_windows - 1)
    
    result = calculate_flexibility(communities)
    
    assert np.isclose(result, manual_expected), \
        f"Manual calculation {manual_expected} does not match function result {result}"

