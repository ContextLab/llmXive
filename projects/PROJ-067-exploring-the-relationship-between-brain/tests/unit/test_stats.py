class TestPermutationTestLoop:
    """Tests for the permutation test loop and p-value derivation (T036)."""

    def test_permutation_loop_runs_exact_iterations(self):
        """Verify the loop runs exactly n_permutations times."""
        np.random.seed(42)
        x = np.random.randn(50)
        y = np.random.randn(50)
        n_perm = 100
        
        # We need to verify the loop count. Since the function returns the result,
        # we can check the returned n_permutations or length of null distribution.
        result = run_permutation_test(x, y, n_permutations=n_perm, seed=42)
        
        assert result['n_permutations'] == n_perm, f"Expected {n_perm} iterations, got {result['n_permutations']}"
        assert len(result['null_distribution']) == n_perm, f"Null distribution length {len(result['null_distribution'])} != {n_perm}"

    def test_null_distribution_generated_by_shuffling(self):
        """Verify that the null distribution is generated by shuffling labels."""
        # Create data with a known strong correlation
        np.random.seed(123)
        x = np.arange(100)
        y = x + np.random.normal(0, 0.1, 100)  # Strong positive correlation
        
        observed = calculate_observed_statistic(x, y)
        result = run_permutation_test(x, y, n_permutations=1000, seed=123)
        
        # The observed statistic should be high (close to 1)
        assert observed > 0.9, f"Expected high observed correlation, got {observed}"
        
        # The null distribution should be centered around 0 (no correlation)
        null_mean = np.mean(result['null_distribution'])
        assert np.abs(null_mean) < 0.2, f"Null distribution mean {null_mean} should be near 0"

    def test_p_value_derivation_correctness(self):
        """Verify the p-value is calculated correctly from the null distribution."""
        # Case 1: Observed statistic is extreme (should yield low p-value)
        np.random.seed(456)
        x = np.arange(50)
        y = x * 1.5 + np.random.normal(0, 0.5, 50)
        
        result = run_permutation_test(x, y, n_permutations=1000, seed=456)
        
        # With a strong correlation, p-value should be very small
        # (assuming the null distribution doesn't accidentally produce such extreme values)
        # We can't guarantee a specific value, but we can check it's a valid probability
        assert 0.0 <= result['p_value'] <= 1.0, f"P-value {result['p_value']} out of bounds"

    def test_p_value_two_tailed_logic(self):
        """Verify two-tailed test logic: |stat| >= |observed|."""
        # Create a case where observed is negative
        np.random.seed(789)
        x = np.arange(50)
        y = -x + np.random.normal(0, 0.5, 50)
        
        result = run_permutation_test(x, y, n_permutations=1000, seed=789)
        
        observed = result['observed_statistic']
        null_dist = result['null_distribution']
        
        # Manually calculate expected p-value
        expected_count = np.sum(np.abs(null_dist) >= np.abs(observed))
        expected_p = expected_count / len(null_dist)
        
        assert np.isclose(result['p_value'], expected_p), \
            f"P-value {result['p_value']} != expected {expected_p}"

    def test_deterministic_with_seed(self):
        """Verify that using a seed produces deterministic results."""
        np.random.seed(999)
        x = np.random.randn(30)
        y = np.random.randn(30)
        
        result1 = run_permutation_test(x, y, n_permutations=500, seed=42)
        result2 = run_permutation_test(x, y, n_permutations=500, seed=42)
        
        assert np.array_equal(result1['null_distribution'], result2['null_distribution']), \
            "Null distributions should be identical with same seed"
        assert result1['p_value'] == result2['p_value'], \
            "P-values should be identical with same seed"

    def test_edge_case_no_correlation(self):
        """Test behavior when there is truly no correlation."""
        np.random.seed(111)
        x = np.random.randn(50)
        y = np.random.randn(50)
        
        result = run_permutation_test(x, y, n_permutations=1000, seed=111)
        
        # Observed should be near 0
        assert np.abs(result['observed_statistic']) < 0.3, \
            f"Observed correlation {result['observed_statistic']} unexpectedly high for random data"
        
        # P-value should be high (not significant)
        assert result['p_value'] > 0.05, \
            f"P-value {result['p_value']} unexpectedly low for random data"

    def test_edge_case_perfect_correlation(self):
        """Test behavior with perfect correlation (y = x)."""
        np.random.seed(222)
        x = np.arange(50, dtype=float)
        y = x.copy()
        
        result = run_permutation_test(x, y, n_permutations=1000, seed=222)
        
        # Observed should be 1.0
        assert np.isclose(result['observed_statistic'], 1.0), \
            f"Expected perfect correlation 1.0, got {result['observed_statistic']}"
        
        # P-value should be 0.0 (or very close, depending on null distribution)
        # In practice, with random shuffling, it's possible to get a high correlation by chance,
        # but with n=50 and 1000 permutations, it's extremely unlikely.
        assert result['p_value'] == 0.0 or result['p_value'] < 0.001, \
            f"P-value {result['p_value']} unexpectedly high for perfect correlation"

    def test_input_data_types(self):
        """Verify the function handles numpy arrays and pandas Series."""
        np.random.seed(333)
        x_np = np.random.randn(30)
        y_np = np.random.randn(30)
        x_pd = pd.Series(x_np)
        y_pd = pd.Series(y_np)
        
        result_np = run_permutation_test(x_np, y_np, n_permutations=100, seed=333)
        result_pd = run_permutation_test(x_pd, y_pd, n_permutations=100, seed=333)
        
        # Results should be similar (not necessarily identical due to float precision)
        assert np.isclose(result_np['p_value'], result_pd['p_value'], rtol=1e-5), \
            "Results should be consistent for numpy arrays and pandas Series"

    def test_small_sample_size(self):
        """Test behavior with small sample sizes."""
        np.random.seed(444)
        x = np.random.randn(10)
        y = np.random.randn(10)
        
        # Should not raise an error
        result = run_permutation_test(x, y, n_permutations=100, seed=444)
        
        assert 'p_value' in result
        assert 'observed_statistic' in result
        assert len(result['null_distribution']) == 100