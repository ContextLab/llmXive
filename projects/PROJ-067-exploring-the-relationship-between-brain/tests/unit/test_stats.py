        @staticmethod
        def run_permutation_test(x, y, n_permutations=1000, method='spearman', seed=None):
            if seed is not None:
                np.random.seed(seed)
            
            observed = MockStats.calculate_observed_statistic(x, y, method)
            null_dist = []
            
            for _ in range(n_permutations):
                # Shuffle y
                y_shuffled = np.random.permutation(y)
                stat = MockStats.calculate_observed_statistic(x, y_shuffled, method)
