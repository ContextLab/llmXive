"""
T044: Ensure null results (p > 0.05) are fully reported with plots and coefficients, not suppressed.

This module loads the statistical results generated by the pipeline (T042),
verifies that even non-significant results (p > 0.05) are present in the output,
and ensures that the corresponding plots (T043) are generated and linked.

It acts as a final validation gate to prevent the accidental suppression
of null findings, ensuring scientific rigor.
"""
import os
import sys
import json
import logging
from pathlib import Path
from typing import Dict, List, Any, Optional

# Add project root to path to allow imports from sibling modules if needed
# though this task primarily reads outputs.
sys.path.insert(0, str(Path(__file__).resolve().parent.parent))

from analysis.plot_results import load_stats_results, generate_all_plots
from utils.config import get_config_summary

logger = logging.getLogger(__name__)

# Constants matching project paths
RESULTS_DIR = Path("results")
STATS_JSON_PATH = RESULTS_DIR / "stats.json"
PLOTS_DIR = RESULTS_DIR / "plots"

def validate_null_reporting(results: Dict[str, Any]) -> bool:
    """
    Validates that the results dictionary contains entries for all tested metrics,
    regardless of p-value significance.

    Args:
        results: The dictionary loaded from results/stats.json.

    Returns:
        True if all metrics are present with full reporting (including p-values),
        False if any metric is missing or suppressed.
    """
    if not results:
        logger.error("Results dictionary is empty.")
        return False

    # Expected structure based on T042/T043 output
    # Typically contains a list of correlations or a dict keyed by metric name
    correlations = results.get("correlations", [])
    
    if not correlations:
        # Check if it's a flat dict structure instead
        if "metrics" in results:
            correlations = results["metrics"]
        else:
            # If no correlations found, check if it's a single result
            if "rho" in results and "p_value" in results:
                correlations = [results]
            else:
                logger.warning("No correlation data found in results structure.")
                return False

    all_present = True
    for corr in correlations:
        metric_name = corr.get("metric_name") or corr.get("network")
        p_val = corr.get("p_value") or corr.get("p_uncorrected")
        p_fdr = corr.get("p_fdr_corrected")
        
        if metric_name is None:
            logger.error(f"Missing metric name in result entry: {corr}")
            all_present = False
            continue

        if p_val is None:
            logger.error(f"Missing p-value for {metric_name}. Result might be suppressed.")
            all_present = False
        else:
            # Explicitly log the status to ensure we are aware of null results
            if p_val > 0.05:
                logger.info(f"Null result detected for {metric_name}: p={p_val:.4f}. "
                            "Reporting coefficient and plot as required.")
            else:
                logger.info(f"Significant result for {metric_name}: p={p_val:.4f}")

        # Ensure coefficient is present
        if corr.get("rho") is None and corr.get("correlation_coefficient") is None:
            logger.error(f"Missing correlation coefficient for {metric_name}.")
            all_present = False

    return all_present

def ensure_plots_exist(results: Dict[str, Any]) -> bool:
    """
    Ensures that the required plots for null results are generated.
    If plots are missing, it attempts to regenerate them using the plot_results module.
    
    Returns:
        True if plots exist and are valid, False otherwise.
    """
    if not RESULTS_DIR.exists():
        logger.error(f"Results directory {RESULTS_DIR} does not exist.")
        return False

    if not PLOTS_DIR.exists():
        logger.warning(f"Plots directory {PLOTS_DIR} does not exist. Attempting creation.")
        PLOTS_DIR.mkdir(parents=True, exist_ok=True)

    # Expected plot files based on T043
    expected_plots = [
        "scatter_correlation.png",
        "null_distribution.png"
    ]

    missing_plots = []
    for plot_name in expected_plots:
        plot_path = PLOTS_DIR / plot_name
        if not plot_path.exists():
            missing_plots.append(plot_name)

    if missing_plots:
        logger.warning(f"Missing plots: {missing_plots}. Attempting regeneration...")
        try:
            # Attempt to regenerate plots using the existing plot_results module
            # This function is expected to load stats.json and generate plots
            generate_all_plots()
            
            # Verify again
            for plot_name in missing_plots:
                if not (PLOTS_DIR / plot_name).exists():
                    logger.error(f"Failed to regenerate plot: {plot_name}")
                    return False
            logger.info("Successfully regenerated missing plots.")
            return True
        except Exception as e:
            logger.error(f"Failed to regenerate plots: {e}")
            return False

    logger.info("All required plots exist.")
    return True

def main():
    """
    Main entry point for T044.
    Loads stats.json, validates that null results are reported, and ensures plots exist.
    """
    logging.basicConfig(level=logging.INFO, format='%(asctime)s - %(levelname)s - %(message)s')
    
    if not STATS_JSON_PATH.exists():
        logger.error(f"Stats file {STATS_JSON_PATH} not found. "
                     "Run statistical analysis tasks (T037-T042) first.")
        sys.exit(1)

    logger.info(f"Loading results from {STATS_JSON_PATH}...")
    try:
        with open(STATS_JSON_PATH, 'r') as f:
            results = json.load(f)
    except json.JSONDecodeError as e:
        logger.error(f"Failed to parse {STATS_JSON_PATH}: {e}")
        sys.exit(1)

    # Step 1: Validate that all metrics (including nulls) are present
    logger.info("Validating null result reporting...")
    if not validate_null_reporting(results):
        logger.error("Validation failed: Some results appear to be suppressed or incomplete.")
        sys.exit(1)
    
    # Step 2: Ensure plots exist
    logger.info("Ensuring plots exist...")
    if not ensure_plots_exist(results):
        logger.error("Validation failed: Could not ensure plots exist.")
        sys.exit(1)

    logger.info("T044 Validation Successful: All null results are reported with coefficients and plots.")
    return 0

if __name__ == "__main__":
    sys.exit(main())