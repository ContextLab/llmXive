"""
Task T029: Calculate Flexibility (state transitions per unit time) for DMN, Salience, and Hippocampal-Memory networks.

This module implements the flexibility metric calculation using the community assignments
generated by Louvain clustering (T028) and the network mapping from T026.
"""
import os
import sys
import logging
import json
import numpy as np
import pandas as pd
from pathlib import Path
from typing import Dict, List, Optional, Tuple, Any, Union

# Import from existing API surface
from utils.config import get_config
from analysis.metrics import load_network_mapping, get_region_to_network_map
from analysis.louvain_clustering import calculate_state_transitions

logger = logging.getLogger(__name__)

def load_community_assignments(community_file: Path) -> np.ndarray:
    """
    Load community assignments from the Louvain clustering output.
    
    Args:
        community_file: Path to the JSON file containing community assignments.
        
    Returns:
        2D numpy array of shape (n_windows, n_nodes) with community labels.
    """
    if not community_file.exists():
        raise FileNotFoundError(f"Community assignments file not found: {community_file}")
    
    with open(community_file, 'r') as f:
        data = json.load(f)
    
    # Expected format: {"assignments": [[window1_communities], [window2_communities], ...]}
    assignments = np.array(data['assignments'], dtype=np.int32)
    logger.info(f"Loaded community assignments: {assignments.shape[0]} windows, {assignments.shape[1]} nodes")
    return assignments

def calculate_network_flexibility(
    community_assignments: np.ndarray,
    region_to_network_map: Dict[int, str],
    target_networks: List[str]
) -> Dict[str, float]:
    """
    Calculate flexibility for specific networks.
    
    Flexibility is defined as the number of state transitions (changes in community
    assignment) divided by the number of time windows minus 1, averaged across all
    nodes belonging to the target network.
    
    Args:
        community_assignments: 2D array (n_windows, n_nodes) of community labels.
        region_to_network_map: Mapping from region ID to network name.
        target_networks: List of network names to calculate flexibility for.
        
    Returns:
        Dictionary mapping network name to flexibility value.
    """
    n_windows, n_nodes = community_assignments.shape
    
    if n_windows < 2:
        raise ValueError("Need at least 2 windows to calculate flexibility")
    
    flexibility_results = {}
    
    for network_name in target_networks:
        # Find nodes belonging to this network
        network_nodes = [
            node_id for node_id, net in region_to_network_map.items()
            if net == network_name
        ]
        
        if not network_nodes:
            logger.warning(f"No nodes found for network: {network_name}")
            flexibility_results[network_name] = 0.0
            continue
        
        # Extract community assignments for these nodes
        node_indices = sorted(network_nodes)
        node_assignments = community_assignments[:, node_indices]
        
        # Calculate flexibility using the existing utility
        # calculate_state_transitions returns (transitions, total_possible)
        total_transitions = 0
        total_possible = 0
        
        for node_idx, node_id in enumerate(node_indices):
            # Get community sequence for this node across windows
            node_sequence = node_assignments[:, node_idx]
            transitions, possible = calculate_state_transitions(node_sequence)
            total_transitions += transitions
            total_possible += possible
        
        # Flexibility = total transitions / total possible transitions
        if total_possible > 0:
            flexibility = total_transitions / total_possible
        else:
            flexibility = 0.0
        
        flexibility_results[network_name] = float(flexibility)
        logger.info(f"Flexibility for {network_name}: {flexibility:.4f} "
                   f"({total_transitions}/{total_possible} transitions)")
    
    return flexibility_results

def process_subject_flexibility(
    subject_id: str,
    subject_dir: Path,
    config: Dict[str, Any]
) -> Dict[str, float]:
    """
    Process a single subject to calculate flexibility metrics.
    
    Args:
        subject_id: Subject identifier.
        subject_dir: Directory containing subject's preprocessed data and clustering results.
        config: Configuration dictionary.
        
    Returns:
        Dictionary with subject_id and flexibility metrics for each target network.
    """
    target_networks = config.get('target_networks', ['DMN', 'Salience', 'Hippocampal-Memory'])
    atlas_type = config.get('atlas', 'Schaefer100')
    
    # Load network mapping (from T026/T025)
    try:
        region_to_network_map = get_region_to_network_map(atlas_type, config)
        logger.info(f"Loaded network mapping with {len(region_to_network_map)} regions")
    except Exception as e:
        logger.error(f"Failed to load network mapping: {e}")
        return {
            'subject_id': subject_id,
            'error': f"Failed to load network mapping: {str(e)}"
        }
    
    # Load community assignments (output from T028)
    community_file = subject_dir / 'community_assignments.json'
    if not community_file.exists():
        error_msg = f"Community assignments not found for subject {subject_id}"
        logger.error(error_msg)
        return {
            'subject_id': subject_id,
            'error': error_msg
        }
    
    try:
        community_assignments = load_community_assignments(community_file)
    except Exception as e:
        error_msg = f"Failed to load community assignments: {str(e)}"
        logger.error(error_msg)
        return {
            'subject_id': subject_id,
            'error': error_msg
        }
    
    # Calculate flexibility
    try:
        flexibility_results = calculate_network_flexibility(
            community_assignments,
            region_to_network_map,
            target_networks
        )
        
        # Add subject ID to results
        result = {'subject_id': subject_id}
        result.update(flexibility_results)
        return result
        
    except Exception as e:
        error_msg = f"Failed to calculate flexibility: {str(e)}"
        logger.error(error_msg)
        return {
            'subject_id': subject_id,
            'error': error_msg
        }

def main():
    """
    Main entry point for flexibility calculation.
    
    Processes all subjects in the processed directory and outputs flexibility metrics.
    """
    logging.basicConfig(
        level=logging.INFO,
        format='%(asctime)s - %(name)s - %(levelname)s - %(message)s'
    )
    
    config = get_config()
    data_dir = Path(config.get('data_dir', 'data'))
    processed_dir = data_dir / 'processed'
    metrics_output = data_dir / 'metrics'
    
    # Ensure output directory exists
    metrics_output.mkdir(parents=True, exist_ok=True)
    
    # Find all subject directories
    if not processed_dir.exists():
        logger.error(f"Processed directory not found: {processed_dir}")
        sys.exit(1)
    
    subject_dirs = sorted([d for d in processed_dir.iterdir() if d.is_dir()])
    logger.info(f"Found {len(subject_dirs)} subject directories")
    
    all_results = []
    
    for subject_dir in subject_dirs:
        subject_id = subject_dir.name
        logger.info(f"Processing subject: {subject_id}")
        
        result = process_subject_flexibility(subject_id, subject_dir, config)
        all_results.append(result)
        
        if 'error' in result:
            logger.warning(f"Skipping subject {subject_id} due to error: {result['error']}")
        else:
            logger.info(f"Completed subject {subject_id}: {result}")
    
    # Save results to CSV
    output_file = metrics_output / 'subject_flexibility.csv'
    if all_results:
        df = pd.DataFrame(all_results)
        df.to_csv(output_file, index=False)
        logger.info(f"Saved flexibility results to {output_file}")
        logger.info(f"Processed {len(all_results)} subjects")
        
        # Also save as JSON for easy inspection
        json_output = metrics_output / 'subject_flexibility.json'
        with open(json_output, 'w') as f:
            json.dump(all_results, f, indent=2)
        logger.info(f"Saved flexibility results (JSON) to {json_output}")
    else:
        logger.warning("No results to save")
        sys.exit(1)

if __name__ == '__main__':
    main()