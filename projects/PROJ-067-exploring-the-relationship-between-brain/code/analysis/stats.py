def prepare_analysis_data(metrics_data: List[Dict[str, Any]], dream_recall_map: Dict[str, Any]) -> Dict[str, np.ndarray]:
    """
    Prepare aligned arrays for statistical analysis.
    
    Args:
        metrics_data: List of metric dictionaries.
        dream_recall_map: Mapping of subject_id to dream recall frequency.
        
    Returns:
        Dictionary containing aligned numpy arrays for each metric and dream recall.
    """
    # Identify numeric metric columns (excluding subject_id and dream_recall_frequency)
    if not metrics_data:
        raise ValueError("No metrics data provided.")
        
    sample_row = metrics_data[0]
    metric_columns = [k for k in sample_row.keys() 
                     if k not in ['subject_id', 'dream_recall_frequency'] 
                     and k != 'metadata'] # Exclude metadata if present as string
    
    # Filter out columns that might not be numeric or are internal
    clean_metric_columns = []
    for col in metric_columns:
        try:
            float(sample_row[col])
            clean_metric_columns.append(col)
        except (ValueError, TypeError):
            logger.debug(f"Skipping non-numeric column: {col}")
    
    if not clean_metric_columns:
        raise ValueError("No valid numeric metric columns found.")
    
    logger.info(f"Analyzing columns: {clean_metric_columns}")
    
    aligned_data = {
        'dream_recall': [],
        'subjects': [],
        'metrics': {col: [] for col in clean_metric_columns}
    }
    
    for row in metrics_data:
        sid = row['subject_id']
        if sid in dream_recall_map:
            aligned_data['subjects'].append(sid)
            aligned_data['dream_recall'].append(dream_recall_map[sid])
            for col in clean_metric_columns:
                try:
                    val = float(row[col])
                    aligned_data['metrics'][col].append(val)
                except (ValueError, TypeError):
                    aligned_data['metrics'][col].append(np.nan)
    
    # Convert to numpy arrays
    aligned_data['dream_recall'] = np.array(aligned_data['dream_recall'])
    for col in clean_metric_columns:
        aligned_data['metrics'][col] = np.array(aligned_data['metrics'][col])
    
    return aligned_data

def calculate_spearman_correlation(data: Dict[str, np.ndarray]) -> Dict[str, Dict[str, float]]:
    """
    Calculate Spearman correlation between each metric and dream recall frequency.
    
    Args:
        data: Aligned data dictionary from prepare_analysis_data.
        
    Returns:
        Dictionary of results: {metric_name: {'rho': float, 'p': float}}
    """
    results = {}
    y = data['dream_recall']
    
    # Remove NaNs from y for safety, though we expect clean data
    valid_idx = ~np.isnan(y)
    y_clean = y[valid_idx]
    
    for metric_name, x in data['metrics'].items():
        x_clean = x[valid_idx]
        
        # Filter out NaNs in x as well
        valid_pair_idx = ~np.isnan(x_clean)
        x_pair = x_clean[valid_pair_idx]
        y_pair = y_clean[valid_pair_idx]
        
        if len(x_pair) < 3:
            logger.warning(f"Not enough valid pairs for {metric_name} ({len(x_pair)}). Skipping.")
            results[metric_name] = {'rho': np.nan, 'p': np.nan}
            continue
        
        rho, p_value = spearmanr(x_pair, y_pair)
        results[metric_name] = {'rho': float(rho), 'p': float(p_value)}
        logger.info(f"Correlation {metric_name}: rho={rho:.4f}, p={p_value:.4f}")
        
    return results

def apply_fdr_correction(results: Dict[str, Dict[str, float]]) -> Dict[str, Dict[str, float]]:
    """
    Apply False Discovery Rate (FDR) correction to all correlation p-values.
    Uses the Benjamini-Hochberg procedure.
    
    Args:
        results: Dictionary of correlation results from calculate_spearman_correlation.
        
    Returns:
        Updated results dictionary with 'p_fdr' key added.
    """
    logger.info("Applying FDR correction (Benjamini-Hochberg)...")
    
    metrics_names = []
    p_values = []
    
    for name, res in results.items():
        if not np.isnan(res['p']):
            metrics_names.append(name)
            p_values.append(res['p'])
    
    if len(p_values) == 0:
        logger.warning("No valid p-values to correct.")
        return results
        
    if len(p_values) == 1:
        # If only one test, FDR is the same as raw p-value
        results[metrics_names[0]]['p_fdr'] = p_values[0]
        logger.info(f"Single test FDR: {p_values[0]:.4f}")
        return results

    # Perform FDR correction
    try:
        # multipletests returns (reject, p_corrected, p_corrected_sidak, p_corrected_bonf)
        # We use method='fdr_bh' for Benjamini-Hochberg
        reject, p_corrected, _, _ = multipletests(p_values, alpha=0.05, method='fdr_bh')
        
        for i, name in enumerate(metrics_names):
            results[name]['p_fdr'] = float(p_corrected[i])
            results[name]['fdr_rejected'] = bool(reject[i])
            logger.info(f"FDR Corrected {name}: p_fdr={p_corrected[i]:.4f}, significant={reject[i]}")
            
    except Exception as e:
        logger.error(f"Error during FDR correction: {e}")
        # Fallback: assign NaN if correction fails
        for name in metrics_names:
            results[name]['p_fdr'] = float('nan')
            
    return results

def main():
    """
    Main entry point for statistical analysis including FDR correction.
    """
    logger.info("Starting Statistical Analysis (T037-T039)...")
    config = get_config_summary()
    logger.info(f"Config: {config}")
    
    try:
        # 1. Load Data
        metrics_data, dream_recall_map = load_metrics_and_dream_recall()
        
        # 2. Prepare Data
        aligned_data = prepare_analysis_data(metrics_data, dream_recall_map)
        
        # 3. Calculate Correlations
        corr_results = calculate_spearman_correlation(aligned_data)
        
        # 4. Apply FDR Correction (T039)
        final_results = apply_fdr_correction(corr_results)
        
        # 5. Save Results
        output_path = Path("results/stats.json")
        output_path.parent.mkdir(parents=True, exist_ok=True)
        
        with open(output_path, 'w', encoding='utf-8') as f:
            json.dump(final_results, f, indent=2)
            
        logger.info(f"Results saved to {output_path}")
        
        # Print summary
        print("\n--- Analysis Summary ---")
        for name, res in final_results.items():
            sig_marker = "*" if res.get('fdr_rejected', False) else ""
            print(f"{name}: rho={res['rho']:.3f}, p={res['p']:.3f}, p_fdr={res['p_fdr']:.3f} {sig_marker}")
            
    except Exception as e:
        logger.error(f"Analysis failed: {e}", exc_info=True)
        sys.exit(1)

if __name__ == "__main__":
    main()
