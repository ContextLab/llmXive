def generate_sliding_window_correlations(time_series: np.ndarray, window_size: int = 30, step_size: int = 10) -> List[np.ndarray]:
    """
    Generate sliding window correlation matrices.
    """
    n_timepoints = time_series.shape[1]
    n_windows = (n_timepoints - window_size) // step_size + 1
    correlations = []
    
    for i in range(n_windows):
        start = i * step_size
        end = start + window_size
        window_data = time_series[:, start:end]
        corr_matrix = np.corrcoef(window_data)
        correlations.append(corr_matrix)
    return correlations

