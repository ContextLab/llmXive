def load_metadata_subjects(metadata_path: str) -> Dict[str, Any]:
    """
    Helper to load the validated metadata JSON to check for dream recall frequency.
    This mirrors the structure expected from data/validate_metadata.py.
    """
    if not os.path.exists(metadata_path):
        raise FileNotFoundError(f"Metadata file not found: {metadata_path}")
    import json
    with open(metadata_path, 'r') as f:
        return json.load(f)

def main():
    """
    Main entry point for metrics calculation with T032 exclusion logic.
    """
    logging.basicConfig(level=logging.INFO, format='%(asctime)s - %(levelname)s - %(message)s')
    
    # Configuration paths (matching project structure)
    config = {
        'atlas_path': 'data/atlas/Schaefer100Parcels_7Networks_order_FSLMNI152.nii.gz',
        'network_map_path': 'data/atlas/network_label_map.csv',
        'metadata_path': 'data/raw/validated_metadata.json', # Expected output from T014
        'preprocessed_dir': 'data/processed/',
        'output_path': 'data/metrics/subject_metrics.csv'
    }

    # Load valid subjects list (from T015)
    valid_subjects_file = 'data/raw/valid_subjects.json'
    if not os.path.exists(valid_subjects_file):
        logging.error(f"Valid subjects list not found: {valid_subjects_file}")
        return

    with open(valid_subjects_file, 'r') as f:
        import json
        valid_subjects = json.load(f)

    # Load metadata to check for dream recall frequency (T032 Logic)
    # We assume the metadata file contains a list of subjects with their fields
    metadata_subjects = {}
    try:
        raw_metadata = load_metadata_subjects(config['metadata_path'])
        # Assuming structure: {'subjects': [{'subject_id': 'sub-001', 'dream_recall_frequency': 5.0}, ...]}
        if 'subjects' in raw_metadata:
            for sub in raw_metadata['subjects']:
                sid = sub.get('subject_id')
                if sid:
                    metadata_subjects[sid] = sub
        else:
            # Fallback if structure is different, try direct mapping
            metadata_subjects = raw_metadata
    except Exception as e:
        logging.warning(f"Could not load metadata for exclusion check: {e}. Proceeding with all valid subjects.")

    results = []
    excluded_count = 0

    for subject_info in valid_subjects:
        subject_id = subject_info.get('subject_id')
        
        # T032: Check if metadata exists for this subject
        if subject_id not in metadata_subjects:
            logging.warning(f"T032 EXCLUSION: Subject {subject_id} found in valid list but missing from metadata. Skipping analysis.")
            excluded_count += 1
            continue

        # Check specifically for the dream recall field if needed for downstream stats
        # The task specifically says "exclude ... if metadata is missing"
        # We assume the presence in metadata_subjects is the primary check.
        
        # Proceed with analysis
        nifti_path = os.path.join(config['preprocessed_dir'], f"{subject_id}_space-MNI152NLin2009cAsym_desc-preproc_bold.nii.gz")
        
        if not os.path.exists(nifti_path):
            logging.warning(f"NIfTI file not found for {subject_id}. Skipping.")
            continue

        try:
            # 1. Parcellate
            ts = parcellate_nifti(nifti_path, config['atlas_path'])
            
            # 2. Sliding Window
            windows = generate_sliding_window_correlations(ts)
            
            # 3. Louvain
            communities = run_louvain_clustering(windows)
            
            # 4. Load Network Map for specific networks (DMN, Salience, Hippocampal-Memory)
            net_map = load_network_map(config['network_map_path'])
            
            # Calculate metrics for specific networks
            # Note: In a real run, we would filter regions for each network
            # For this implementation, we calculate global flexibility/stability 
            # or assume the mask logic handles the specific network filtering.
            
            # Example: Calculate for Hippocampal-Memory if mapped
            hip_mask = None
            if 'Hippocampal-Memory' in net_map:
                mask_arr = np.zeros(ts.shape[0], dtype=bool)
                for idx in net_map['Hippocampal-Memory']:
                    if idx < ts.shape[0]:
                        mask_arr[idx] = True
                hip_mask = mask_arr

            flex = calculate_flexibility(communities, hip_mask)
            stab = calculate_stability(communities, hip_mask)
            
            # Get Dream Recall Frequency for output
            drf = metadata_subjects[subject_id].get('dream_recall_frequency', None)
            
            results.append({
                'subject_id': subject_id,
                'flexibility': flex,
                'stability': stab,
                'dream_recall_frequency': drf
            })
            
        except Exception as e:
            logging.error(f"Error processing {subject_id}: {e}")
            continue

    # Write output
    if results:
        os.makedirs(os.path.dirname(config['output_path']), exist_ok=True)
        with open(config['output_path'], 'w', newline='') as f:
            writer = csv.DictWriter(f, fieldnames=['subject_id', 'flexibility', 'stability', 'dream_recall_frequency'])
            writer.writeheader()
            writer.writerows(results)
        logging.info(f"Metrics written to {config['output_path']}")
        logging.info(f"Excluded {excluded_count} subjects due to missing metadata (T032).")
    else:
        logging.warning("No results to write.")

if __name__ == '__main__':
    main()