def calculate_flexibility(community_assignments: List[np.ndarray], network_mask: Optional[np.ndarray] = None) -> float:
    """
    Calculate flexibility: number of state transitions per unit time.
    """
    if not community_assignments:
        return 0.0
    
    transitions = 0
    total_time = len(community_assignments) - 1
    
    for i in range(1, len(community_assignments)):
        if network_mask is not None:
            # Only count transitions within the mask
            active_nodes = np.where(network_mask)[0]
            prev_states = community_assignments[i-1][active_nodes]
            curr_states = community_assignments[i][active_nodes]
            if not np.array_equal(prev_states, curr_states):
                transitions += 1
        else:
            if not np.array_equal(community_assignments[i-1], community_assignments[i]):
                transitions += 1
    
    return transitions / total_time if total_time > 0 else 0.0

def calculate_stability(community_assignments: List[np.ndarray], network_mask: Optional[np.ndarray] = None) -> float:
    """
    Calculate stability: Mean Dwell Time.
    """
    if not community_assignments:
        return 0.0
    
    dwell_times = []
    current_state = None
    current_dwell = 0
    
    for i, states in enumerate(community_assignments):
        if network_mask is not None:
            active_nodes = np.where(network_mask)[0]
            state_vec = tuple(states[active_nodes])
        else:
            state_vec = tuple(states)
        
        if state_vec == current_state:
            current_dwell += 1
        else:
            if current_dwell > 0:
                dwell_times.append(current_dwell)
            current_state = state_vec
            current_dwell = 1
    
    if current_dwell > 0:
        dwell_times.append(current_dwell)
    
    return np.mean(dwell_times) if dwell_times else 0.0

