def load_network_map(map_path: str) -> Dict[str, List[int]]:
    """
    Load the network mapping CSV generated by verify_atlas_labels.py.
    Returns a dict mapping network_name -> list of region_ids.
    """
    network_map = {}
    if not os.path.exists(map_path):
        logging.warning(f"Network map file not found: {map_path}. Using empty map.")
        return network_map
    
    with open(map_path, 'r') as f:
        reader = csv.DictReader(f)
        for row in reader:
            net_name = row.get('mapped_label', '').strip()
            region_id = int(row.get('region_id', -1))
            if net_name and region_id >= 0:
                if net_name not in network_map:
                    network_map[net_name] = []
                network_map[net_name].append(region_id)
    return network_map

