"""
Output subject-level metrics to CSV with validation.

This module aggregates flexibility and stability metrics calculated in previous
steps (T029, T030) and writes them to a standardized CSV file.

It also performs basic schema validation to ensure the output matches
the contract defined in `contracts/subject_metrics.schema.yaml`.
"""
import os
import sys
import json
import logging
import csv
from pathlib import Path
from typing import Dict, List, Any, Optional

# Import local modules based on the provided API surface
# Note: We assume the script is run from the project root or code/ is in PYTHONPATH
# The API surface indicates: from analysis.metrics import load_network_mapping, ...
# However, for output generation, we likely need to read the intermediate JSON files
# generated by T029 (flexibility) and T030 (stability) or aggregate them here.

# Since T029 and T030 are marked as completed, they should have produced intermediate
# data or updated a shared state. Based on typical pipeline patterns, we assume
# metrics are stored in JSON files per subject or a consolidated list.

# Let's define the expected schema based on FR-008 and the task description.
REQUIRED_COLUMNS = [
    "subject_id",
    "network_name",
    "flexibility",
    "stability",
    "dream_recall_frequency"
]

def load_subject_metrics_json(subject_id: str, metrics_dir: Path) -> Optional[Dict[str, Any]]:
    """
    Load metrics for a specific subject from JSON files.
    Assumes T029/T030 produced files like:
    data/metrics/intermediate/<subject_id>_flexibility.json
    data/metrics/intermediate/<subject_id>_stability.json
    """
    flex_path = metrics_dir / "intermediate" / f"{subject_id}_flexibility.json"
    stab_path = metrics_dir / "intermediate" / f"{subject_id}_stability.json"
    drf_path = metrics_dir / "intermediate" / f"{subject_id}_drf.json" # Assuming DRF is stored here or loaded from metadata

    metrics = {
        "subject_id": subject_id,
        "network_metrics": {}
    }

    if not flex_path.exists() or not stab_path.exists():
        logging.warning(f"Intermediate metrics missing for {subject_id}. Skipping.")
        return None

    try:
        with open(flex_path, 'r') as f:
            flex_data = json.load(f)
        with open(stab_path, 'r') as f:
            stab_data = json.load(f)
        
        # Merge data by network
        networks = set(flex_data.keys()) & set(stab_data.keys())
        for network in networks:
            metrics["network_metrics"][network] = {
                "flexibility": flex_data[network].get("flexibility"),
                "stability": stab_data[network].get("stability")
            }
        
        # Try to load DRF if available in the same directory structure
        if drf_path.exists():
            with open(drf_path, 'r') as f:
                drf_val = json.load(f)
            metrics["drf"] = drf_val.get("dream_recall_frequency")
        else:
            metrics["drf"] = None

        return metrics
    except (json.JSONDecodeError, KeyError) as e:
        logging.error(f"Error parsing metrics for {subject_id}: {e}")
        return None

def load_valid_subjects_list(subjects_file: Path) -> List[str]:
    """
    Load the list of valid subjects from T015 output.
    """
    if not subjects_file.exists():
        raise FileNotFoundError(f"Valid subjects file not found: {subjects_file}")
    
    with open(subjects_file, 'r') as f:
        data = json.load(f)
    
    # Expected format from T015: {"subjects": [...]} or a list directly
    if isinstance(data, list):
        return data
    elif isinstance(data, dict) and "subjects" in data:
        return data["subjects"]
    else:
        raise ValueError("Invalid format in valid_subjects.json")

def validate_row(row: Dict[str, Any]) -> bool:
    """
    Validate a single row against the schema.
    Checks for required columns and non-null numeric values.
    """
    for col in REQUIRED_COLUMNS:
        if col not in row:
            logging.error(f"Missing column: {col}")
            return False
        if row[col] is None:
            # DRF might be null if metadata failed, but metrics should exist
            if col in ["flexibility", "stability"]:
                logging.error(f"Metric value is null for {row['subject_id']}")
                return False
    return True

def aggregate_and_write_csv(
    valid_subjects_file: Path,
    metrics_dir: Path,
    output_file: Path
) -> int:
    """
    Main orchestration function to aggregate metrics and write CSV.
    
    Args:
        valid_subjects_file: Path to data/raw/valid_subjects.json
        metrics_dir: Path to data/metrics/
        output_file: Path to data/metrics/subject_metrics.csv
    
    Returns:
        Number of rows written.
    """
    subjects = load_valid_subjects_list(valid_subjects_file)
    rows = []
    
    metrics_dir = Path(metrics_dir)
    output_file = Path(output_file)
    
    # Ensure output directory exists
    output_file.parent.mkdir(parents=True, exist_ok=True)
    
    for subject_id in subjects:
        subject_metrics = load_subject_metrics_json(subject_id, metrics_dir)
        if not subject_metrics:
            continue
        
        drf = subject_metrics.get("drf")
        
        for network, values in subject_metrics.get("network_metrics", {}).items():
            row = {
                "subject_id": subject_id,
                "network_name": network,
                "flexibility": values["flexibility"],
                "stability": values["stability"],
                "dream_recall_frequency": drf
            }
            
            if validate_row(row):
                rows.append(row)
            else:
                logging.warning(f"Skipping invalid row for {subject_id} - {network}")
    
    if not rows:
        logging.error("No valid rows to write. Check intermediate files.")
        # Still write an empty file with headers to satisfy schema checkers
        with open(output_file, 'w', newline='') as f:
            writer = csv.DictWriter(f, fieldnames=REQUIRED_COLUMNS)
            writer.writeheader()
        return 0

    # Write to CSV
    with open(output_file, 'w', newline='') as f:
        writer = csv.DictWriter(f, fieldnames=REQUIRED_COLUMNS)
        writer.writeheader()
        writer.writerows(rows)
    
    logging.info(f"Successfully wrote {len(rows)} rows to {output_file}")
    return len(rows)

def main():
    """
    Entry point for the script.
    """
    logging.basicConfig(
        level=logging.INFO,
        format='%(asctime)s - %(levelname)s - %(message)s'
    )
    
    # Define paths relative to project root
    project_root = Path(__file__).resolve().parent.parent.parent
    valid_subjects_path = project_root / "data" / "raw" / "valid_subjects.json"
    metrics_dir = project_root / "data" / "metrics"
    output_path = project_root / "data" / "metrics" / "subject_metrics.csv"
    
    if not valid_subjects_path.exists():
        logging.error(f"Required input file not found: {valid_subjects_path}")
        logging.error("Ensure T015 (filter_subjects) has completed successfully.")
        sys.exit(1)
    
    count = aggregate_and_write_csv(valid_subjects_path, metrics_dir, output_path)
    logging.info(f"Pipeline T031 completed. Rows written: {count}")

if __name__ == "__main__":
    main()