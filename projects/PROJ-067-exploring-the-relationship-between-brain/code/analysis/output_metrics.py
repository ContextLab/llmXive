def aggregate_metrics(subject_data: List[Dict[str, Any]]) -> List[Dict[str, Any]]:
    """
    Aggregate metrics into a list of dictionaries ready for CSV export.
    Ensures all required fields are present.
    """
    required_fields = [
        "subject_id", 
        "network_name", 
        "flexibility", 
        "stability"
    ]
    
    validated_data = []
    for entry in subject_data:
        # Validate presence
        for field in required_fields:
            if field not in entry:
                raise ValueError(f"Missing required field '{field}' in metrics entry: {entry}")
        
        # Validate types
        if not isinstance(entry["subject_id"], str):
            raise TypeError(f"subject_id must be string, got {type(entry['subject_id'])}")
        if not isinstance(entry["flexibility"], (int, float)):
            raise TypeError(f"flexibility must be numeric, got {type(entry['flexibility'])}")
        if not isinstance(entry["stability"], (int, float)):
            raise TypeError(f"stability must be numeric, got {type(entry['stability'])}")
        
        validated_data.append(entry)
    
    return validated_data

def write_metrics_csv(metrics_data: List[Dict[str, Any]], output_path: Path) -> None:
    """
    Write the validated metrics to a CSV file.
    """
    if not metrics_data:
        logger.warning("No metrics data to write.")
        return

    # Ensure directory exists
    output_path.parent.mkdir(parents=True, exist_ok=True)

    # Define headers
    fieldnames = ["subject_id", "network_name", "flexibility", "stability"]
    
    with open(output_path, 'w', newline='', encoding='utf-8') as csvfile:
        writer = csv.DictWriter(csvfile, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(metrics_data)
    
    logger.info(f"Successfully wrote metrics to {output_path}")

def validate_schema(csv_path: Path) -> bool:
    """
    Basic validation that the CSV matches the expected schema structure.
    Reads the CSV and checks headers and row counts.
    """
    if not csv_path.exists():
        return False
    
    with open(csv_path, 'r', encoding='utf-8') as f:
        reader = csv.DictReader(f)
        headers = reader.fieldnames
        
        expected_headers = ["subject_id", "network_name", "flexibility", "stability"]
        if not headers:
            return False
        
        if set(headers) != set(expected_headers):
            logger.error(f"Header mismatch. Expected {expected_headers}, got {headers}")
            return False
        
        # Check at least one row exists
        rows = list(reader)
        if len(rows) == 0:
            logger.warning("CSV file is empty (no data rows).")
            return False
    
    return True

