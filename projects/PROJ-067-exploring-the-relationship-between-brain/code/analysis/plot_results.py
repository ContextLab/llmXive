def load_stats_results() -> Dict[str, Any]:
    """Load the statistics results from the JSON file generated by T042."""
    if not STATS_JSON_PATH.exists():
        raise FileNotFoundError(f"Stats results not found at {STATS_JSON_PATH}. "
                                "Run T042 (generate_stats_json) first.")
    
    with open(STATS_JSON_PATH, 'r') as f:
        return json.load(f)

def load_metrics_for_plotting() -> pd.DataFrame:
    """Load metrics and dream recall frequency for plotting."""
    # Re-use the data loading logic from stats.py to ensure consistency
    # We assume the metrics file is in the expected location from T031
    metrics_path = Path("data/metrics/subject_metrics.csv")
    if not metrics_path.exists():
        raise FileNotFoundError(f"Metrics CSV not found at {metrics_path}. "
                                "Run T031 (output_metrics) first.")
    
    df = pd.read_csv(metrics_path)
    
    # Ensure dream recall frequency column exists
    if 'dream_recall_frequency' not in df.columns:
        raise ValueError("Column 'dream_recall_frequency' not found in metrics CSV.")
    
    # Select relevant columns for plotting
    # We expect columns like: subject_id, flexibility, stability, dream_recall_frequency
    # We will plot flexibility and stability against dream recall
    required_cols = ['subject_id', 'dream_recall_frequency']
    metric_cols = [col for col in df.columns if col not in ['subject_id', 'dream_recall_frequency']]
    
    plot_df = df[required_cols + metric_cols].copy()
    return plot_df

def create_scatter_plot(
    x_data: np.ndarray,
    y_data: np.ndarray,
    x_label: str,
    y_label: str,
    title: str,
    output_path: Path,
    rho: Optional[float] = None,
    p_val: Optional[float] = None
) -> None:
    """Create a scatter plot with regression line and statistics annotation."""
    plt.figure(figsize=(10, 8))
    
    # Scatter plot
    plt.scatter(x_data, y_data, alpha=0.7, edgecolors='w', s=100, color='#3498db')
    
    # Add regression line
    if len(x_data) > 1:
        z = np.polyfit(x_data, y_data, 1)
        p = np.poly1d(z)
        plt.plot(x_data, p(x_data), "r--", alpha=0.8, linewidth=2, label=f'Regression (r={rho:.3f})')
    
    plt.xlabel(x_label, fontsize=12)
    plt.ylabel(y_label, fontsize=12)
    plt.title(title, fontsize=14)
    plt.grid(True, alpha=0.3)
    
    # Annotate statistics if provided
    if rho is not None and p_val is not None:
        annotation = f"Spearman ρ = {rho:.3f}\np = {p_val:.3g}"
        plt.annotate(annotation, xy=(0.05, 0.95), xycoords='axes fraction',
                    verticalalignment='top', bbox=dict(boxstyle='round', facecolor='wheat', alpha=0.5))
    
    plt.tight_layout()
    plt.savefig(output_path, dpi=300, bbox_inches='tight')
    plt.close()
    logger.info(f"Scatter plot saved to {output_path}")

def create_null_distribution_plot(
    null_distribution: np.ndarray,
    observed_stat: float,
    p_value: float,
    output_path: Path
) -> None:
    """Create a histogram of the null distribution with observed statistic marked."""
    plt.figure(figsize=(10, 8))
    
    # Histogram of null distribution
    plt.hist(null_distribution, bins=30, color='lightgray', edgecolor='black', alpha=0.7, density=True)
    
    # Mark observed statistic
    plt.axvline(x=observed_stat, color='red', linestyle='dashed', linewidth=2, 
               label=f'Observed (p={p_value:.3g})')
    
    # Shade tails for significance (approximate)
    plt.fill_betweenx(plt.gca().get_ylim(), observed_stat, max(null_distribution), 
                     color='red', alpha=0.1)
    
    plt.xlabel('Correlation Coefficient (Null Distribution)', fontsize=12)
    plt.ylabel('Density', fontsize=12)
    plt.title('Permutation Test: Null Distribution', fontsize=14)
    plt.legend()
    plt.grid(True, alpha=0.3)
    
    plt.tight_layout()
    plt.savefig(output_path, dpi=300, bbox_inches='tight')
    plt.close()
    logger.info(f"Null distribution plot saved to {output_path}")

def generate_all_plots() -> Dict[str, str]:
    """Generate all required plots for US3."""
    # Ensure plots directory exists
    PLOTS_DIR.mkdir(parents=True, exist_ok=True)
    
    # Load stats results
    stats_results = load_stats_results()
    
    # Load metrics for plotting
    metrics_df = load_metrics_for_plotting()
    
    plot_paths = {}
    
    # 1. Generate scatter plots for each metric vs dream recall frequency
    metric_columns = [col for col in metrics_df.columns 
                     if col not in ['subject_id', 'dream_recall_frequency']]
    
    for metric_name in metric_columns:
        if metric_name not in stats_results.get('correlations', {}):
            logger.warning(f"No stats result for {metric_name}, skipping plot.")
            continue
        
        stat_data = stats_results['correlations'][metric_name]
        rho = stat_data.get('rho')
        p_val = stat_data.get('p_fdr_corrected') or stat_data.get('p_uncorrected')
        
        x_data = metrics_df['dream_recall_frequency'].values
        y_data = metrics_df[metric_name].values
        
        # Remove NaN values
        mask = ~(np.isnan(x_data) | np.isnan(y_data))
        x_clean = x_data[mask]
        y_clean = y_data[mask]
        
        if len(x_clean) < 2:
            logger.warning(f"Not enough valid data points for {metric_name} plot.")
            continue
        
        output_file = PLOTS_DIR / f"scatter_{metric_name}_vs_dream_recall.png"
        create_scatter_plot(
            x_clean, y_clean,
            "Dream Recall Frequency",
            f"{metric_name.capitalize()}",
            f"Spearman Correlation: {metric_name} vs Dream Recall",
            output_file,
            rho,
            p_val
        )
        plot_paths[metric_name] = str(output_file)
    
    # 2. Generate null distribution plot
    if 'permutation_results' in stats_results:
        perm_results = stats_results['permutation_results']
        # We need to reconstruct the null distribution or use the saved one if available
        # Since the stats.py might not save the full array, we'll regenerate a representative one
        # or use the p-value to infer the position.
        # However, for a real plot, we need the data. Let's assume the stats.json 
        # might not have the full array, so we might need to re-run the permutation test 
        # or the stats.py should have saved it.
        # Given the constraint to use existing API, let's check if we can regenerate 
        # a plausible null distribution for visualization purposes based on the p-value 
        # and observed stat, OR we assume the stats.py T042 saved it.
        # Looking at T042 description: "Generate results/stats.json containing..."
        # It doesn't explicitly say it saves the full array. 
        # To be robust, let's check if the permutation test module can be called to 
        # regenerate the plot data, or we assume the stats.json has 'null_distribution'.
        
        # Let's check if 'null_distribution' key exists
        if 'null_distribution' in stats_results:
            null_dist = np.array(stats_results['null_distribution'])
            observed_stat = stats_results.get('observed_statistic', 0.0)
            p_val_perm = stats_results.get('p_permutation', 0.0)
            
            output_file = PLOTS_DIR / "null_distribution.png"
            create_null_distribution_plot(
                null_dist,
                observed_stat,
                p_val_perm,
                output_file
            )
            plot_paths['null_distribution'] = str(output_file)
        else:
            logger.warning("Null distribution not found in stats.json. "
                         "Skipping null distribution plot. "
                         "Ensure T042 saves the full null distribution array.")
    else:
        logger.warning("No permutation results found in stats.json. "
                     "Skipping null distribution plot.")
    
    return plot_paths

