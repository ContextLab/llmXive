"""
Task T045: Run quickstart.md validation.

This script parses docs/quickstart.md, extracts the documented CLI commands,
and validates them against the actual codebase entry points.

It verifies:
1. All referenced Python scripts exist.
2. All referenced CLI flags match the argparse definitions in the scripts.
3. All referenced output files are generated by the commands.

Exit code 0: Validation passed.
Exit code 1: Validation failed (errors logged).
"""
import sys
import re
import argparse
import subprocess
from pathlib import Path
from typing import List, Dict, Set, Tuple, Any
import logging

# Add project root to path for imports if running as script
PROJECT_ROOT = Path(__file__).parent.parent
sys.path.insert(0, str(PROJECT_ROOT / "code"))

from config import get_config
from logging_config import setup_logging, get_logger

# Initialize logging
logger = setup_logging(level="INFO")

class QuickstartValidator:
    def __init__(self, docs_path: Path, code_dir: Path):
        self.docs_path = docs_path
        self.code_dir = code_dir
        self.errors: List[str] = []
        self.warnings: List[str] = []
        self.commands_found: List[Dict[str, Any]] = []

    def load_document(self) -> str:
        if not self.docs_path.exists():
            raise FileNotFoundError(f"quickstart.md not found at {self.docs_path}")
        return self.docs_path.read_text(encoding="utf-8")

    def extract_code_blocks(self, content: str) -> List[str]:
        """Extract shell code blocks from markdown."""
        pattern = r'```bash(.*?)```'
        matches = re.findall(pattern, content, re.DOTALL)
        return [m.strip() for m in matches]

    def parse_commands(self, blocks: List[str]) -> List[Dict[str, Any]]:
        """Parse shell commands into structured data."""
        commands = []
        for block in blocks:
            lines = [l.strip() for l in block.split('\n') if l.strip() and not l.strip().startswith('#')]
            if not lines:
                continue
            
            # Handle multi-line commands or single commands
            full_cmd = ' '.join(lines)
            
            # Extract the main python script
            match = re.search(r'python\s+code/(\w+)\.py', full_cmd)
            if match:
                script_name = match.group(1)
                # Extract flags
                flags = re.findall(r'--(\w+)', full_cmd)
                commands.append({
                    "raw": full_cmd,
                    "script": script_name,
                    "flags": flags,
                    "output_hint": self._guess_output(full_cmd)
                })
        return commands

    def _guess_output(self, cmd: str) -> str:
        """Heuristic guess of output file from command."""
        # Look for common output patterns in the command
        if 'final_report' in cmd:
            return 'docs/outputs/final_report.md'
        if 'filtered_alloys' in cmd:
            return 'data/processed/filtered_alloys.csv'
        if 'model_metrics' in cmd:
            return 'docs/outputs/model_metrics.json'
        return ''

    def validate_script_exists(self, script_name: str) -> bool:
        script_path = self.code_dir / f"{script_name}.py"
        if not script_path.exists():
            self.errors.append(f"Script {script_name}.py referenced in quickstart.md does not exist at {script_path}")
            return False
        return True

    def validate_flags(self, script_name: str, flags: List[str]) -> bool:
        script_path = self.code_dir / f"{script_name}.py"
        if not script_path.exists():
            return False

        # Run --help to get actual flags
        try:
            result = subprocess.run(
                [sys.executable, str(script_path), "--help"],
                capture_output=True,
                text=True,
                timeout=10
            )
            help_text = result.stdout + result.stderr
            
            actual_flags = re.findall(r'--(\w+)', help_text)
            
            for flag in flags:
                if flag not in actual_flags:
                    self.errors.append(
                        f"Flag '--{flag}' used in quickstart.md for {script_name}.py "
                        f"was not found in the script's --help output."
                    )
                    return False
            return True
        except subprocess.TimeoutExpired:
            self.warnings.append(f"Timed out while checking --help for {script_name}.py")
            return True
        except Exception as e:
            self.errors.append(f"Error checking flags for {script_name}.py: {str(e)}")
            return False

    def validate_outputs(self, commands: List[Dict[str, Any]]) -> bool:
        """Check if documented output paths are plausible."""
        # We cannot run the full pipeline here as it requires data, 
        # but we can check if the output directories exist and are writable
        output_dirs = set()
        for cmd in commands:
            if cmd.get("output_hint"):
                output_path = PROJECT_ROOT / cmd["output_hint"]
                output_dirs.add(output_path.parent)
        
        for dir_path in output_dirs:
            if not dir_path.exists():
                # It's okay if they don't exist yet, they will be created by the script
                self.warnings.append(f"Output directory {dir_path} does not exist yet (will be created by script).")
        
        return True

    def run(self) -> bool:
        logger.info("Starting quickstart.md validation...")
        
        try:
            content = self.load_document()
        except FileNotFoundError as e:
            logger.error(str(e))
            return False

        blocks = self.extract_code_blocks(content)
        if not blocks:
            self.errors.append("No code blocks found in quickstart.md.")
            return False

        commands = self.parse_commands(blocks)
        if not commands:
            self.errors.append("No valid commands parsed from quickstart.md.")
            return False

        logger.info(f"Found {len(commands)} commands to validate.")

        all_valid = True
        for cmd in commands:
            logger.info(f"Validating: {cmd['script']}.py")
            
            if not self.validate_script_exists(cmd['script']):
                all_valid = False
                continue
            
            if not self.validate_flags(cmd['script'], cmd['flags']):
                all_valid = False
            
            self.commands_found.append(cmd)

        self.validate_outputs(self.commands_found)

        # Report results
        if self.errors:
            logger.error("Validation FAILED with the following errors:")
            for err in self.errors:
                logger.error(f"  - {err}")
        
        if self.warnings:
            logger.warning("Validation completed with warnings:")
            for warn in self.warnings:
                logger.warning(f"  - {warn}")

        if not self.errors:
            logger.info("Validation PASSED. All documented commands are valid.")
            # Create a validation report artifact
            report_path = PROJECT_ROOT / "docs/outputs/quickstart_validation_report.txt"
            report_path.parent.mkdir(parents=True, exist_ok=True)
            
            with open(report_path, 'w', encoding='utf-8') as f:
                f.write("Quickstart.md Validation Report\n")
                f.write("=" * 40 + "\n")
                f.write(f"Status: PASSED\n")
                f.write(f"Commands Validated: {len(self.commands_found)}\n")
                f.write(f"Errors: 0\n")
                f.write(f"Warnings: {len(self.warnings)}\n\n")
                f.write("Validated Commands:\n")
                for cmd in self.commands_found:
                    f.write(f"  - code/{cmd['script']}.py (flags: {cmd['flags']})\n")
            
            logger.info(f"Validation report saved to {report_path}")

        return len(self.errors) == 0

def main():
    config = get_config()
    docs_path = PROJECT_ROOT / "docs" / "quickstart.md"
    code_dir = PROJECT_ROOT / "code"
    
    validator = QuickstartValidator(docs_path, code_dir)
    success = validator.run()
    
    sys.exit(0 if success else 1)

if __name__ == "__main__":
    main()