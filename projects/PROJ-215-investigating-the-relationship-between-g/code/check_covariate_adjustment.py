def calculate_max_delta_pvals(unadjusted_path: str, adjusted_path: str) -> float:
    """
    Calculate |p_adjusted - p_unadjusted| for each taxon and return the maximum delta.
    
    Args:
        unadjusted_path: Path to CSV with unadjusted p-values
        adjusted_path: Path to CSV with adjusted p-values (q-values)
        
    Returns:
        Maximum absolute difference between adjusted and unadjusted p-values
    """
    df_unadj = pd.read_csv(unadjusted_path)
    df_adj = pd.read_csv(adjusted_path)
    
    # Ensure both have the same taxon identifier column (assuming 'taxon' or 'feature')
    # Check common column names
    id_cols = [col for col in df_unadj.columns if col in df_adj.columns and col.lower() in ['taxon', 'feature', 'id']]
    if not id_cols:
        raise ValueError("Could not find common taxon identifier column in both files")
    
    id_col = id_cols[0]
    
    # Merge on the identifier
    merged = pd.merge(df_unadj, df_adj, on=id_col, suffixes=('_unadj', '_adj'))
    
    # Identify p-value columns (look for columns containing 'pval')
    pval_cols_unadj = [col for col in merged.columns if 'pval' in col.lower() and 'unadj' in col.lower()]
    pval_cols_adj = [col for col in merged.columns if 'pval' in col.lower() and 'adj' in col.lower()]
    
    if not pval_cols_unadj or not pval_cols_adj:
        # Fallback: look for any p-value columns
        pval_cols_unadj = [col for col in merged.columns if 'pval' in col.lower() and 'unadj' not in col.lower() and 'adj' not in col.lower()]
        pval_cols_adj = [col for col in merged.columns if 'qval' in col.lower() or ('pval' in col.lower() and 'adj' in col.lower())]
        
    if not pval_cols_unadj or not pval_cols_adj:
        raise ValueError("Could not identify p-value columns in the data")
    
    # Calculate deltas for all corresponding p-value pairs
    # Assuming first p-value column in unadj matches first in adj
    max_delta = 0.0
    for i, col_unadj in enumerate(pval_cols_unadj):
        if i < len(pval_cols_adj):
            col_adj = pval_cols_adj[i]
            # Ensure numeric, coerce errors to NaN
            merged[col_unadj] = pd.to_numeric(merged[col_unadj], errors='coerce')
            merged[col_adj] = pd.to_numeric(merged[col_adj], errors='coerce')
            
            delta = (merged[col_adj] - merged[col_unadj]).abs()
            current_max = delta.max()
            if current_max > max_delta:
                max_delta = current_max
                
    return max_delta

def run_covariate_check():
    """
    Main function to perform the SC-005 check:
    - Calculate max delta between adjusted and unadjusted p-values
    - Report findings in results/temp_covariate_check.txt
    """
    # Define paths based on project structure
    # Assuming unadjusted and adjusted p-values are generated by previous tasks
    data_interim_path = Path("data/interim")
    results_path = Path("results")
    
    # Ensure results directory exists
    results_path.mkdir(parents=True, exist_ok=True)
    
    # Paths to input files (generated by T020a and T022)
    unadjusted_taxa_pvals_path = data_interim_path / "unadjusted_taxa_pvals.csv"
    adjusted_taxa_pvals_path = data_interim_path / "adjusted_taxa_pvals.csv"
    
    # Output file path
    output_file = results_path / "temp_covariate_check.txt"
    
    logger.info(f"Checking covariate adjustment impact on p-values")
    logger.info(f"Unadjusted p-values: {unadjusted_taxa_pvals_path}")
    logger.info(f"Adjusted p-values: {adjusted_taxa_pvals_path}")
    
    # Check if input files exist
    if not unadjusted_taxa_pvals_path.exists():
        logger.error(f"Unadjusted p-values file not found: {unadjusted_taxa_pvals_path}")
        raise FileNotFoundError(f"Unadjusted p-values file not found: {unadjusted_taxa_pvals_path}")
        
    if not adjusted_taxa_pvals_path.exists():
        logger.error(f"Adjusted p-values file not found: {adjusted_taxa_pvals_path}")
        raise FileNotFoundError(f"Adjusted p-values file not found: {adjusted_taxa_pvals_path}")
    
    # Calculate max delta
    max_delta = calculate_max_delta_pvals(
        str(unadjusted_taxa_pvals_path),
        str(adjusted_taxa_pvals_path)
    )
    
    # Determine if threshold is met (using a reasonable threshold, e.g., 0.1)
    # The task description doesn't specify a threshold value, so we use 0.1 as a common scientific threshold
    # If the project has a specific threshold in config, it should be used instead
    threshold = 0.1
    threshold_met = max_delta <= threshold
    
    # Format the report
    report_lines = [
        "SC-005 Covariate Adjustment Check",
        "=" * 40,
        f"Max Delta (|p_adjusted - p_unadjusted|): {max_delta:.4f}",
        f"Threshold: {threshold:.2f}",
        f"Threshold Met: {threshold_met}",
        "",
        "Note: This check assesses the impact of covariate adjustment (age, BMI) on",
        "p-values. A large delta indicates that covariates significantly influence",
        "the observed associations.",
        ""
    ]
    
    # Write report
    with open(output_file, 'w') as f:
        f.write('\n'.join(report_lines))
        
    logger.info(f"Report written to: {output_file}")
    logger.info(f"Max Delta: {max_delta:.4f}. Threshold Met: {threshold_met}")
    
    return {
        "max_delta": max_delta,
        "threshold": threshold,
        "threshold_met": threshold_met,
        "output_file": str(output_file)
    }

