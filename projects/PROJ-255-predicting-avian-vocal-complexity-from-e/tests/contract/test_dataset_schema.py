def test_vocal_metrics_schema_columns(schema):
    """
    T011: Contract test for the `vocal_metrics.csv` schema definition.
    Validates that the file exists and contains exactly the columns defined in the schema.
    """
    # Ensure the file exists (if not, the test fails as expected if data generation hasn't run)
    if not os.path.exists(os.path.join(PROCESSED_DIR, 'vocal_metrics.csv')):
        # Note: In a CI/CD pipeline, this would be a failure. In local dev, it might be skipped
        # if data generation is a separate step. However, for a contract test, we expect the file.
        # We raise an error to force the data generation step to run first.
        pytest.fail("vocal_metrics.csv not found. Please run data extraction (T019) first.")

    df = pd.read_csv(os.path.join(PROCESSED_DIR, 'vocal_metrics.csv'))
    
    # Load specific schema for vocal_metrics
    if 'vocal_metrics' not in schema:
        pytest.fail("Schema definition for 'vocal_metrics' not found in dataset.schema.yaml")
    
    expected_columns = set(schema['vocal_metrics']['columns'].keys())
    actual_columns = set(df.columns)

    missing_columns = expected_columns - actual_columns
    extra_columns = actual_columns - expected_columns

    assert not missing_columns, f"Missing required columns: {missing_columns}"
    assert not extra_columns, f"Unexpected extra columns: {extra_columns}"


def test_vocal_metrics_schema_types(schema):
    """
    Validates that the data types in vocal_metrics.csv match the schema.
    """
    if not os.path.exists(os.path.join(PROCESSED_DIR, 'vocal_metrics.csv')):
        pytest.fail("vocal_metrics.csv not found.")

    df = pd.read_csv(os.path.join(PROCESSED_DIR, 'vocal_metrics.csv'))
    expected_schema = schema['vocal_metrics']['columns']

    # Map pandas dtypes to expected schema types
    # Note: This is a simplified check. A robust check would compare specific numpy dtypes.
    for col, details in expected_schema.items():
        expected_type = details.get('type')
        if col not in df.columns:
            continue # Handled by column test

        actual_dtype = str(df[col].dtype)
        
        # Basic type mapping logic
        type_match = False
        if expected_type == 'string' and 'object' in actual_dtype:
            type_match = True
        elif expected_type == 'integer' and 'int' in actual_dtype:
            type_match = True
        elif expected_type == 'float' and 'float' in actual_dtype:
            type_match = True
        elif expected_type == 'boolean' and 'bool' in actual_dtype:
            type_match = True

        assert type_match, f"Column '{col}' has dtype {actual_dtype}, expected {expected_type}"


def test_vocal_metrics_schema_constraints(schema):
    """
    Validates that data constraints (e.g., non-null, ranges) are met.
    """
    if not os.path.join(PROCESSED_DIR, 'vocal_metrics.csv'):
        pytest.fail("vocal_metrics.csv not found.")

    df = pd.read_csv(os.path.join(PROCESSED_DIR, 'vocal_metrics.csv'))
    expected_schema = schema['vocal_metrics']['columns']

    for col, details in expected_schema.items():
        if col not in df.columns:
            continue

        # Check nullability
        if details.get('nullable') is False:
            null_count = df[col].isnull().sum()
            assert null_count == 0, f"Column '{col}' has {null_count} null values, but nullable=False"

        # Check range constraints if defined
        if 'min' in details:
            min_val = df[col].min()
            assert min_val >= details['min'], f"Column '{col}' min value {min_val} < {details['min']}"
        
        if 'max' in details:
            max_val = df[col].max()
            assert max_val <= details['max'], f"Column '{col}' max value {max_val} > {details['max']}"


