"""
State updater module for PROJ-255.
Updates the project state YAML file with artifact hashes generated by T039.
"""
import os
import yaml
import json
import logging
from pathlib import Path
from typing import Dict, Any, Optional

from src.utils.config import get_project_root
from src.utils.logging import setup_logger

logger = setup_logger(__name__)

STATE_FILE_PATH = "state/projects/PROJ-255-predicting-avian-vocal-complexity-from-e.yaml"

def load_hash_file(hash_file_path: Path) -> Dict[str, str]:
    """
    Load the hashes generated by versioning.py from the JSON file.
    """
    if not hash_file_path.exists():
        raise FileNotFoundError(f"Hash file not found: {hash_file_path}")
    
    with open(hash_file_path, 'r', encoding='utf-8') as f:
        data = json.load(f)
    
    # The versioning module saves a dict of {relative_path: hash}
    return data

def load_state_file(state_path: Path) -> Dict[str, Any]:
    """
    Load the existing state YAML file.
    If it doesn't exist, initialize a basic structure.
    """
    if not state_path.exists():
        logger.warning(f"State file not found at {state_path}. Initializing new state.")
        return {
            "project_id": "PROJ-255-predicting-avian-vocal-complexity-from-e",
            "status": "in_progress",
            "artifacts": {},
            "last_updated": None
        }
    
    with open(state_path, 'r', encoding='utf-8') as f:
        return yaml.safe_load(f)

def update_state_with_hashes(state: Dict[str, Any], hashes: Dict[str, str]) -> Dict[str, Any]:
    """
    Update the state dictionary with the new artifact hashes.
    """
    import datetime
    
    if "artifacts" not in state:
        state["artifacts"] = {}
    
    # Update or add hashes for each file
    for file_path, file_hash in hashes.items():
        # Normalize path separators for consistency
        normalized_path = file_path.replace(os.sep, '/')
        state["artifacts"][normalized_path] = {
            "hash": file_hash,
            "last_verified": datetime.datetime.now(datetime.timezone.utc).isoformat()
        }
    
    state["last_updated"] = datetime.datetime.now(datetime.timezone.utc).isoformat()
    
    # Ensure status is marked as having hashes if we successfully updated
    if state.get("status") == "in_progress":
        # We don't change status to complete here as other tasks might be pending,
        # but we ensure the hash data is present.
        pass
    
    return state

def save_state_file(state: Dict[str, Any], state_path: Path) -> None:
    """
    Save the updated state dictionary back to the YAML file.
    """
    state_path.parent.mkdir(parents=True, exist_ok=True)
    
    with open(state_path, 'w', encoding='utf-8') as f:
        yaml.dump(state, f, default_flow_style=False, sort_keys=False, allow_unicode=True)
    
    logger.info(f"State file updated successfully: {state_path}")

def main() -> int:
    """
    Main entry point for T040: Update state file with artifact hashes.
    
    This function:
    1. Locates the hash file generated by T039 (usually in data/ or root).
       Based on T039 implementation, it likely outputs to 'data/hashes.json' or similar.
       We will look for the standard output of the versioning main.
    2. Loads the current project state YAML.
    3. Updates the 'artifacts' section with the new hashes.
    4. Saves the updated state.
    
    Returns:
        int: 0 on success, 1 on failure.
    """
    project_root = get_project_root()
    state_file = project_root / STATE_FILE_PATH
    
    # The versioning main() typically saves to data/hashes.json or similar.
    # We need to find where T039 saved it.
    # Assuming standard output from src/utils/versioning.py main()
    # Let's check common locations or infer from config.
    # T039 output is usually in data/ directory.
    # We'll look for 'hashes.json' in the root or data directory.
    
    possible_hash_paths = [
        project_root / "hashes.json",
        project_root / "data" / "hashes.json",
        project_root / "data" / "processed" / "hashes.json",
        project_root / "state" / "hashes.json"
    ]
    
    hash_file_path = None
    for p in possible_hash_paths:
        if p.exists():
            hash_file_path = p
            break
    
    if hash_file_path is None:
        # If not found, try to find any recent json file with 'hash' in name
        import glob
        candidates = list(project_root.glob("**/*hash*.json"))
        if candidates:
            hash_file_path = candidates[-1] # Take the last one found
        else:
            logger.error("Could not locate the hashes.json file generated by T039.")
            return 1
    
    logger.info(f"Found hash file at: {hash_file_path}")
    
    try:
        # Load hashes
        hashes = load_hash_file(hash_file_path)
        logger.info(f"Loaded {len(hashes)} hashes from {hash_file_path}")
        
        # Load state
        state = load_state_file(state_file)
        
        # Update state
        updated_state = update_state_with_hashes(state, hashes)
        
        # Save state
        save_state_file(updated_state, state_file)
        
        logger.info("Task T040 completed successfully.")
        return 0
        
    except Exception as e:
        logger.error(f"Failed to update state file: {e}", exc_info=True)
        return 1

if __name__ == "__main__":
    import sys
    sys.exit(main())
