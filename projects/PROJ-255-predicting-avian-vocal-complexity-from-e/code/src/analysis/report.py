def extract_correlation_stats(model_results: pd.DataFrame) -> Dict[str, Any]:
    """Extract correlation direction, effect size, and corrected p-value from model results."""
    if model_results.empty:
        raise ValueError("Model results are empty.")

    # Assuming the first row or aggregation contains the summary stats
    # We look for columns typically generated by statsmodels or custom correlation logic
    # If the file contains per-metric results, we might need to aggregate or pick the primary one (noise vs complexity)
    
    primary_row = model_results.iloc[0]
    
    # Heuristic: look for specific column names or infer from data
    # Common output: 'coeff_noise', 'p_value', 'r_value', 'p_corr'
    
    r_val = None
    p_val = None
    p_corr = None
    coeff = None
    direction = "unknown"

    # Try to find correlation coefficient
    if 'r_value' in primary_row.index:
        r_val = primary_row['r_value']
    elif 'correlation' in primary_row.index:
        r_val = primary_row['correlation']
    
    # Try to find p-value
    if 'p_value' in primary_row.index:
        p_val = primary_row['p_value']
    elif 'pval' in primary_row.index:
        p_val = primary_row['pval']
    
    # Try to find corrected p-value
    if 'p_corr' in primary_row.index:
        p_corr = primary_row['p_corr']
    elif 'p_adjusted' in primary_row.index:
        p_corr = primary_row['p_adjusted']
    elif 'p_corrected' in primary_row.index:
        p_corr = primary_row['p_corrected']

    # Try to find coefficient (effect size proxy)
    if 'coeff_noise' in primary_row.index:
        coeff = primary_row['coeff_noise']
    elif 'coef' in primary_row.index:
        coeff = primary_row['coef']

    if r_val is not None:
        direction = "positive" if r_val > 0 else ("negative" if r_val < 0 else "no correlation")
    
    return {
        "r_value": r_val,
        "p_value": p_val,
        "p_corrected": p_corr,
        "coefficient": coeff,
        "direction": direction
    }

def generate_report_text(stats: Dict[str, Any], power_report: Optional[str], sensitivity_df: Optional[pd.DataFrame]) -> str:
    """Compile the summary report text."""
    lines = []
    lines.append("# Avian Vocal Complexity Analysis Report")
    lines.append("")
    lines.append("## 1. Statistical Summary")
    lines.append("")
    
    r = stats.get("r_value")
    p = stats.get("p_value")
    p_corr = stats.get("p_corrected")
    coeff = stats.get("coefficient")
    direction = stats.get("direction", "unknown")

    lines.append(f"- **Correlation Direction**: {direction}")
    if r is not None:
        lines.append(f"- **Correlation Coefficient (r)**: {r:.4f}")
    if p is not None:
        lines.append(f"- **Raw P-value**: {p:.4e}")
    if p_corr is not None:
        lines.append(f"- **FDR Corrected P-value**: {p_corr:.4e}")
    if coeff is not None:
        lines.append(f"- **Effect Size (Coefficient)**: {coeff:.4f}")
    
    lines.append("")
    lines.append("## 2. Power Analysis")
    lines.append("")
    if power_report:
        lines.append(power_report)
    else:
        lines.append("*Power analysis report not available.*")
    
    lines.append("")
    lines.append("## 3. Sensitivity Analysis Summary")
    lines.append("")
    if sensitivity_df is not None and not sensitivity_df.empty:
        lines.append("| Threshold | Sample Size | Correlation (r) | Variation % |")
        lines.append("| :--- | :--- | :--- | :--- |")
        for _, row in sensitivity_df.iterrows():
            th = row.get('threshold', 'N/A')
            ss = row.get('sample_size', 'N/A')
            corr = row.get('correlation_r', 'N/A')
            var = row.get('variation_percent', 'N/A')
            lines.append(f"| {th} | {ss} | {corr} | {var} |")
    else:
        lines.append("*Sensitivity analysis summary not available.*")
    
    lines.append("")
    lines.append("---")
    lines.append("*Report generated automatically by the llmXive pipeline.*")
    
    return "\n".join(lines)

def save_report(report_text: str) -> Path:
    """Save the report to the processed data directory."""
    processed_dir = get_processed_data_dir()
    output_path = processed_dir / "report.md"
    output_path.write_text(report_text)
    logger.info(f"Report saved to {output_path}")
    return output_path

