"""
Verification utilities for User Story 2.
Specifically handles species count verification (SC-004).
"""
import csv
import logging
import sys
from pathlib import Path
from typing import Tuple, Optional

# Import project paths
try:
    from src.utils.config import get_processed_data_dir
except ImportError:
    # Fallback for direct execution or different import context
    from pathlib import Path
    import sys
    sys.path.insert(0, str(Path(__file__).parent.parent.parent))
    from src.utils.config import get_processed_data_dir


def count_valid_species(
    dataset_path: Optional[Path] = None,
    species_column: str = "species_id",
    min_threshold: int = 50
) -> Tuple[int, bool, Optional[str]]:
    """
    Count unique valid species in the final dataset and verify against threshold.

    Args:
        dataset_path: Path to the final dataset CSV. Defaults to the processed dataset path.
        species_column: Name of the column containing species identifiers.
        min_threshold: Minimum required number of unique species (SC-004).

    Returns:
        Tuple containing:
            - count (int): Number of unique valid species found.
            - passed (bool): True if count >= min_threshold, False otherwise.
            - error_message (str or None): Description of failure if count < threshold.
    """
    if dataset_path is None:
        processed_dir = get_processed_data_dir()
        dataset_path = processed_dir / "final_dataset.csv"

    if not dataset_path.exists():
        msg = f"Dataset file not found at: {dataset_path}"
        logging.error(msg)
        return 0, False, msg

    unique_species = set()
    total_rows = 0

    try:
        with open(dataset_path, 'r', encoding='utf-8') as f:
            reader = csv.DictReader(f)
            for row in reader:
                total_rows += 1
                species_val = row.get(species_column)
                if species_val is not None and species_val.strip() != "":
                    unique_species.add(species_val)

        count = len(unique_species)
        passed = count >= min_threshold

        if not passed:
            msg = (
                f"SC-004 Verification Failed: "
                f"Found {count} unique species in '{dataset_path}', "
                f"but minimum required is {min_threshold}. "
                f"Modeling blocked."
            )
            logging.warning(msg)
            return count, False, msg
        
        logging.info(f"SC-004 Verification Passed: Found {count} unique species (>= {min_threshold}).")
        return count, True, None

    except Exception as e:
        msg = f"Error reading dataset for species count: {e}"
        logging.error(msg)
        return 0, False, msg


def verify_species_count_for_modeling(
    dataset_path: Optional[Path] = None,
    species_column: str = "species_id"
) -> Tuple[int, str]:
    """
    Wrapper to verify species count and return a status message.
    Blocks modeling if count < 50.
    """
    count, passed, error_msg = count_valid_species(
        dataset_path=dataset_path,
        species_column=species_column,
        min_threshold=50
    )

    if passed:
        return count, "OK"
    else:
        # Return the specific error message generated by the check
        return count, error_msg


def main():
    """
    Entry point for running the verification script.
    Reads data/processed/final_dataset.csv and prints the result.
    """
    logging.basicConfig(
        level=logging.INFO,
        format='%(asctime)s - %(levelname)s - %(message)s'
    )

    processed_dir = get_processed_data_dir()
    dataset_path = processed_dir / "final_dataset.csv"

    logging.info(f"Checking species count in: {dataset_path}")

    count, status = verify_species_count_for_modeling(dataset_path)

    if status == "OK":
        print(f"SUCCESS: Verified {count} species. Modeling can proceed.")
        sys.exit(0)
    else:
        print(f"FAILURE: {status}")
        sys.exit(1)


if __name__ == "__main__":
    main()
