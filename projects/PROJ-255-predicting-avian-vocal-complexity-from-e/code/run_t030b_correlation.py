"""
Runner script for T030b: Correlation Calculation for Sensitivity Analysis.

This script executes the correlation calculation for each SNR threshold
dataset generated by T030a.
"""
import os
import sys
import logging
from pathlib import Path

# Add project root to path
project_root = Path(__file__).parent
sys.path.insert(0, str(project_root))

from src.analysis.sensitivity import main as run_correlation_analysis
from src.utils.logging import setup_logger

def main():
    """Entry point for T030b execution."""
    logger = setup_logger(__name__)
    logger.info("Executing T030b: Correlation Calculation")
    
    try:
        results = run_correlation_analysis()
        logger.info("T030b execution completed successfully.")
        return 0
    except Exception as e:
        logger.error(f"T030b execution failed: {e}", exc_info=True)
        return 1

if __name__ == "__main__":
    sys.exit(main())
