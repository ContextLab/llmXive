"""
Integration test for T041: End-to-End Flow Verification (US-1).

This script verifies that the data acquisition, noise mapping, filtering,
extraction, and final dataset generation steps execute correctly on a 
representative subset of data.

It does NOT download fresh audio (to avoid rate limits/timeouts in CI) 
but relies on the existence of the `data/processed/final_dataset.csv` 
generated by the pipeline (T038) or a previous run.

If the final dataset is missing, it attempts to run the minimal 
pipeline steps required to generate it from raw/interim data if available,
or fails explicitly if no data source exists.
"""
import os
import sys
import logging
import pandas as pd
from pathlib import Path
from typing import List, Dict, Any

# Add project root to path
project_root = Path(__file__).resolve().parent.parent.parent
sys.path.insert(0, str(project_root))

from src.utils.config import get_project_root, get_raw_data_dir, get_interim_data_dir, get_processed_data_dir
from src.utils.logging import setup_logger
from src.data.preprocessing import load_csv, validate_against_schema, combine_datasets
from src.data.acquisition import save_noise_mapped_data
from src.analysis.verification import count_valid_species, verify_species_count_for_modeling

# Setup logging
logger = setup_logger("integration_test_t041", level=logging.INFO)

def check_file_exists(path: Path, description: str) -> bool:
    if not path.exists():
        logger.error(f"MISSING: {description} not found at {path}")
        return False
    logger.info(f"FOUND: {description} at {path}")
    return True

def validate_schema(df: pd.DataFrame, schema_path: Path, dataset_name: str) -> bool:
    """Validates DataFrame against a simple schema check (column presence)."""
    if not schema_path.exists():
        logger.warning(f"Schema file {schema_path} not found, skipping strict schema validation.")
        return True
    
    # Load schema (assuming simple YAML with required columns)
    try:
        import yaml
        with open(schema_path, 'r') as f:
            schema = yaml.safe_load(f)
        
        required_cols = schema.get('required_columns', [])
        missing_cols = [col for col in required_cols if col not in df.columns]
        
        if missing_cols:
            logger.error(f"SCHEMA VIOLATION: {dataset_name} missing columns: {missing_cols}")
            return False
        
        logger.info(f"SCHEMA OK: {dataset_name} has all required columns.")
        return True
    except Exception as e:
        logger.error(f"Error reading schema: {e}")
        return False

def run_e2e_verification() -> bool:
    """
    Executes the T041 verification logic.
    Returns True if all checks pass, False otherwise.
    """
    root = get_project_root()
    raw_dir = get_raw_data_dir()
    interim_dir = get_interim_data_dir()
    processed_dir = get_processed_data_dir()
    
    # Paths
    noise_mapped_path = interim_dir / "noise_mapped.csv"
    filtered_snr_path = interim_dir / "filtered_snr.csv"
    final_dataset_path = processed_dir / "final_dataset.csv"
    schema_path = root / "contracts" / "dataset.schema.yaml"
    
    logger.info("--- Starting T041 Integration Test ---")
    
    # 1. Check if final dataset exists (Fast path if pipeline already run)
    if final_dataset_path.exists():
        logger.info("Final dataset found. Loading for validation...")
        try:
            df = pd.read_csv(final_dataset_path)
            logger.info(f"Loaded {len(df)} records from final dataset.")
            
            # Verify content validity
            if df.empty:
                logger.error("Final dataset is empty.")
                return False
            
            # Check for required columns based on US-1 spec
            required_cols = ['species_id', 'noise_level_db', 'syllable_count', 'duration_sec', 'bandwidth_hz', 'spectral_entropy']
            missing = [c for c in required_cols if c not in df.columns]
            if missing:
                logger.error(f"Final dataset missing critical columns: {missing}")
                return False
            
            # Check for valid noise levels (should not be NaN)
            if df['noise_level_db'].isna().any():
                logger.warning("Final dataset contains NaN noise levels (violates Plan constraint).")
                # Strictly speaking, Plan says "drop missing", so this is a failure
                # But we might have partial data. Let's flag it.
                return False
            
            logger.info("Final dataset content validation PASSED.")
            return True
            
        except Exception as e:
            logger.error(f"Failed to load final dataset: {e}")
            return False
    
    # 2. If final dataset missing, attempt to reconstruct/verify intermediate steps
    logger.info("Final dataset missing. Verifying intermediate steps and attempting minimal run...")
    
    steps_ok = True
    
    # Step A: Check noise_mapped.csv
    if not check_file_exists(noise_mapped_path, "Noise Mapped Data"):
        steps_ok = False
    else:
        try:
            df_noise = pd.read_csv(noise_mapped_path)
            if df_noise.empty:
                logger.error("noise_mapped.csv is empty.")
                steps_ok = False
            else:
                logger.info(f"noise_mapped.csv has {len(df_noise)} records.")
                # Verify noise column
                if 'noise_level_db' not in df_noise.columns:
                    logger.error("noise_mapped.csv missing 'noise_level_db'")
                    steps_ok = False
        except Exception as e:
            logger.error(f"Error reading noise_mapped.csv: {e}")
            steps_ok = False
    
    # Step B: Check filtered_snr.csv
    if not check_file_exists(filtered_snr_path, "Filtered SNR Data"):
        steps_ok = False
    else:
        try:
            df_filtered = pd.read_csv(filtered_snr_path)
            if df_filtered.empty:
                logger.error("filtered_snr.csv is empty.")
                steps_ok = False
            else:
                logger.info(f"filtered_snr.csv has {len(df_filtered)} records.")
        except Exception as e:
            logger.error(f"Error reading filtered_snr.csv: {e}")
            steps_ok = False
    
    if not steps_ok:
        logger.error("Intermediate data files missing or invalid. Cannot proceed with E2E verification.")
        return False
    
    # Step C: If intermediates exist but final is missing, we assume the pipeline 
    # (T038) failed at the final combination step. We try to re-run the combination logic.
    # This simulates the "run integration test" requirement by actually executing the logic.
    logger.info("Attempting to regenerate final_dataset.csv from intermediates...")
    
    try:
        # Load intermediates
        df_noise = pd.read_csv(noise_mapped_path)
        df_filtered = pd.read_csv(filtered_snr_path)
        
        # Merge logic (simplified from preprocessing.combine_datasets)
        # Assuming both have a common key like 'recording_id' or 'row_id'
        common_key = None
        for key in ['recording_id', 'row_id', 'id']:
            if key in df_noise.columns and key in df_filtered.columns:
                common_key = key
                break
        
        if not common_key:
            logger.error("No common key found to merge noise and filtered data.")
            return False
        
        # In a real scenario, extraction metrics would be merged here too.
        # Since we don't have extraction metrics in this minimal check, we simulate
        # the existence of a merged dataset or fail if the structure is too broken.
        
        # For T041, we check if the *flow* works. If we have noise and filtered data,
        # and the schema is valid, we consider the flow verified if we can produce a valid output.
        # We will create a synthetic 'final' dataframe by merging what we have + dummy metrics
        # ONLY if the task requires generating the file.
        # However, the task says "Run integration test... to verify end-to-end flow".
        # If the file is missing, the flow failed. We should report failure unless we can fix it.
        
        # Let's try to load the extraction output if it exists
        extraction_path = interim_dir / "vocal_metrics.csv"
        if extraction_path.exists():
            df_metrics = pd.read_csv(extraction_path)
            # Merge all three
            df_temp = pd.merge(df_filtered, df_noise, on=common_key, how='inner')
            df_final = pd.merge(df_temp, df_metrics, on=common_key, how='inner')
            
            if df_final.empty:
                logger.error("Merge resulted in empty dataset.")
                return False
            
            df_final.to_csv(final_dataset_path, index=False)
            logger.info(f"Successfully regenerated {final_dataset_path} with {len(df_final)} records.")
            
            # Validate
            if not validate_schema(df_final, schema_path, "final_dataset"):
                return False
            
            return True
        else:
            logger.error("vocal_metrics.csv missing. Cannot complete E2E flow.")
            return False
            
    except Exception as e:
        logger.error(f"Error during regeneration attempt: {e}")
        return False

def main():
    """Entry point for T041."""
    success = run_e2e_verification()
    if success:
        logger.info("T041 Integration Test: PASSED")
        return 0
    else:
        logger.error("T041 Integration Test: FAILED")
        return 1

if __name__ == "__main__":
    sys.exit(main())
