"""
Script to generate checksums for all artifacts in the data/processed directory.

This script implements Task T018: Add checksum generation for all artifacts
in data/checksums.txt.

Usage:
    python code/generate_checksums.py
"""
import os
import sys
import logging
from pathlib import Path

# Add project root to path for imports
project_root = Path(__file__).parent.parent
sys.path.insert(0, str(project_root))

from utils.checksum_utils import (
    generate_checksums_for_directory,
    write_checksums_file
)
from utils.logging_utils import init_logging, get_logger

def main():
    """
    Main entry point for checksum generation.
    """
    # Initialize logging
    log_path = project_root / "state" / "projects" / "checksum_generation.log"
    logger = init_logging(log_path)
    logger.info("Starting checksum generation for T018")

    # Define directories to process
    data_processed_dir = project_root / "data" / "processed"
    checksums_output_path = project_root / "data" / "checksums.txt"

    if not data_processed_dir.exists():
        logger.error(f"Data processed directory not found: {data_processed_dir}")
        sys.exit(1)

    logger.info(f"Scanning directory: {data_processed_dir}")

    # Generate checksums for all relevant artifacts
    # Include .gpickle (graphs), .json (metadata), .csv (results), .md (reports), .png (plots)
    extensions = ['.gpickle', '.json', '.csv', '.md', '.png']
    
    checksums = generate_checksums_for_directory(
        data_processed_dir,
        extensions=extensions,
        recursive=True
    )

    if not checksums:
        logger.warning("No artifacts found to checksum in data/processed/")
        # Still write an empty file to indicate the task was attempted
        header = [
            "Checksums for project artifacts",
            "No artifacts found in data/processed/ at generation time",
            "Generated by: T018 (Checksum Generation)"
        ]
    else:
        logger.info(f"Generated {len(checksums)} checksums")
        header = [
            "Checksums for project artifacts",
            "Generated by: T018 (Checksum Generation)",
            f"Artifacts scanned: {data_processed_dir}"
        ]

    # Write checksums to file
    write_checksums_file(
        checksums,
        checksums_output_path,
        header_comments=header
    )

    logger.info(f"Checksums written to: {checksums_output_path}")
    print(f"Successfully generated {len(checksums)} checksums in {checksums_output_path}")

    # List the generated checksums for verification
    if checksums:
        print("\nGenerated checksums:")
        for algo, path, cksum in checksums:
            print(f"  {algo} {path}")

    return 0

if __name__ == "__main__":
    sys.exit(main())
