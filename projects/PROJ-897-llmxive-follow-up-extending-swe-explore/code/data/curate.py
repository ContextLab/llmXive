# Constants
HARD_INSTANCE_PERCENTILE_DEFAULT = 20  # Bottom 20%
VALIDATION_SAMPLE_SIZE_DEFAULT = 20

def load_derived_ground_truth(ground_truth_path: Path) -> Dict[str, Any]:
    """
    Load the derived ground truth JSONL file generated by T013.
    Returns a dictionary mapping issue_id to ground truth data.
    """
    if not ground_truth_path.exists():
        raise FileNotFoundError(f"Ground truth file not found: {ground_truth_path}")

    gt_map = {}
    with open(ground_truth_path, 'r', encoding='utf-8') as f:
        for line_num, line in enumerate(f, 1):
            line = line.strip()
            if not line:
                continue
            try:
                record = json.loads(line)
                # Ensure required fields exist
                if 'issue_id' not in record:
                    logger.warning(f"Skipping record at line {line_num}: missing 'issue_id'")
                    continue
                gt_map[record['issue_id']] = record
            except json.JSONDecodeError as e:
                logger.error(f"JSON decode error at line {line_num}: {e}")
                raise

    logger.info(f"Loaded {len(gt_map)} ground truth records from {ground_truth_path}")
    return gt_map

def filter_hard_instances(
    input_dataset_path: Path,
    output_path: Path,
    ground_truth_map: Dict[str, Any],
    percentile: float = HARD_INSTANCE_PERCENTILE_DEFAULT
) -> List[Dict[str, Any]]:
    """
    Filter the bottom `percentile` of issues based on existing `initial_coverage` scores.
    
    Per FR-001: Identify "Hard" instances.
    - Reads `initial_coverage` from the input dataset records.
    - Sorts ascending (lower coverage = harder).
    - Selects the bottom `percentile` (e.g., bottom 20%).
    - Consumes `ground_truth_lines` from the provided ground_truth_map (T013 output).
    
    Args:
        input_dataset_path: Path to the full benchmark JSONL (e.g., bench.final.public.jsonl).
        output_path: Path to write the filtered hard subset JSONL.
        ground_truth_map: Map of issue_id -> ground truth data (must contain 'ground_truth_lines').
        percentile: The bottom percentile to select (0-100).
        
    Returns:
        List of filtered records including enriched ground truth data.
    """
    if not input_dataset_path.exists():
        raise FileNotFoundError(f"Input dataset not found: {input_dataset_path}")

    logger.info(f"Loading dataset from {input_dataset_path}...")
    records = []
    with open(input_dataset_path, 'r', encoding='utf-8') as f:
        for line in f:
            line = line.strip()
            if not line:
                continue
            try:
                records.append(json.loads(line))
            except json.JSONDecodeError:
                logger.warning(f"Skipping invalid JSON line in input dataset")

    if not records:
        raise ValueError("Input dataset is empty.")

    # Filter out records missing 'initial_coverage'
    valid_records = [r for r in records if 'initial_coverage' in r]
    if len(valid_records) < len(records):
        logger.warning(f"Filtered out {len(records) - len(valid_records)} records missing 'initial_coverage'.")
    
    if not valid_records:
        raise ValueError("No records with 'initial_coverage' found in input dataset.")

    # Sort by initial_coverage ascending (lowest coverage = hardest)
    valid_records.sort(key=lambda x: x['initial_coverage'])

    # Calculate cutoff index
    count = len(valid_records)
    cutoff_index = max(1, int(count * (percentile / 100.0)))
    hard_records = valid_records[:cutoff_index]

    logger.info(f"Selected {len(hard_records)} hard instances (bottom {percentile}%) from {count} total.")

    # Enrich records with ground truth lines from T013
    enriched_records = []
    missing_gt_count = 0

    for record in hard_records:
        issue_id = record.get('issue_id')
        if not issue_id:
            logger.warning("Skipping record with no issue_id")
            continue

        gt_data = ground_truth_map.get(issue_id)
        if gt_data:
            record['ground_truth_lines'] = gt_data.get('ground_truth_lines', [])
            record['original_hash'] = gt_data.get('original_hash')
            record['patch_hash'] = gt_data.get('patch_hash')
            enriched_records.append(record)
        else:
            logger.warning(f"Ground truth not found for issue_id: {issue_id}")
            missing_gt_count += 1
            # Still include it but without ground_truth_lines to avoid data loss
            record['ground_truth_lines'] = []
            enriched_records.append(record)

    if missing_gt_count > 0:
        logger.warning(f"Missing ground truth for {missing_gt_count} hard instances. Proceeding with empty lists.")

    # Ensure output directory exists
    output_path.parent.mkdir(parents=True, exist_ok=True)

    # Write output
    with open(output_path, 'w', encoding='utf-8') as f:
        for record in enriched_records:
            f.write(json.dumps(record) + '\n')

    logger.info(f"Wrote {len(enriched_records)} hard instances to {output_path}")
    return enriched_records

def mutate_variable_names(code: str, seed: Optional[int] = None) -> str:
    """
    Mutate variable names in the code to create ambiguity.
    Uses AST parsing to ensure validity.
    """
    if seed is not None:
        random.seed(seed)
    
    try:
        tree = ast.parse(code)
    except SyntaxError:
        raise ValueError("Input code is not syntactically valid.")

    # Collect all names that are targets of assignments or function args
    # Simple heuristic: rename local variables that are not built-ins
    new_names = {}
    
    class RenameTransformer(ast.NodeTransformer):
        def visit_Name(self, node):
            if isinstance(node.ctx, ast.Store):
                if node.id not in new_names:
                    # Generate a confusing name
                    new_names[node.id] = f"__var_{random.randint(1000, 9999)}"
                node.id = new_names[node.id]
            elif isinstance(node.ctx, ast.Load):
                if node.id in new_names:
                    node.id = new_names[node.id]
            return node

    new_tree = RenameTransformer().visit(tree)
    ast.fix_missing_locations(new_tree)
    return ast.unparse(new_tree)

def remove_comments(code: str) -> str:
    """
    Remove comments from code.
    """
    # Simple regex-based removal for speed, though AST is safer for complex cases
    # For this task, we assume simple comments or use a robust approach
    lines = []
    for line in code.splitlines(keepends=True):
        if '#' in line:
            idx = line.find('#')
            # Check if inside string (simplified check)
            # For robustness, we'd use ast, but regex is often sufficient for "removing comments" task
            # Here we do a naive removal for the mutation effect
            if not (line[:idx].count('"') % 2 == 1 or line[:idx].count("'") % 2 == 1):
                line = line[:idx] + '\n'
        lines.append(line)
    return "".join(lines)

def reorder_control_flow(code: str) -> str:
    """
    Reorder control flow (e.g., swap if/else branches or loop bodies) to obfuscate logic.
    Note: This is a structural obfuscation.
    For safety, we will implement a specific transformation:
    Swap the bodies of an if/else block if present.
    """
    try:
        tree = ast.parse(code)
    except SyntaxError:
        raise ValueError("Input code is not syntactically valid.")

    class FlowReorderTransformer(ast.NodeTransformer):
        def visit_If(self, node):
            # If there's an else clause, swap body and orelse
            if node.orelse:
                # Swap
                node.body, node.orelse = node.orelse, node.body
                # Note: This changes semantics if the condition is true.
                # To keep semantics but obfuscate, we would negate the condition.
                # However, FR-009 asks for "structural obfuscations".
                # A safer "obfuscation" that keeps logic identical but changes structure:
                # Move code to a helper function or restructure.
                # Let's try negating condition and swapping for semantic preservation + obfuscation
                node.test = ast.UnaryOp(op=ast.Not(), operand=node.test)
                ast.fix_missing_locations(node)
            return node

    try:
        new_tree = FlowReorderTransformer().visit(tree)
        ast.fix_missing_locations(new_tree)
        return ast.unparse(new_tree)
    except Exception as e:
        logger.warning(f"Control flow reordering failed: {e}, returning original")
        return code

def change_api_signature(code: str) -> str:
    """
    Change API signatures (e.g., argument order or names) to obfuscate.
    """
    try:
        tree = ast.parse(code)
    except SyntaxError:
        raise ValueError("Input code is not syntactically valid.")

    class SignatureChanger(ast.NodeTransformer):
        def visit_FunctionDef(self, node):
            # Rename arguments to generic names
            new_args = []
            for i, arg in enumerate(node.args.args):
                arg.arg = f"arg_{i}"
                new_args.append(arg)
            node.args.args = new_args
            return node

    try:
        new_tree = SignatureChanger().visit(tree)
        ast.fix_missing_locations(new_tree)
        return ast.unparse(new_tree)
    except Exception as e:
        logger.warning(f"API signature change failed: {e}, returning original")
        return code

def is_code_valid(code: str) -> bool:
    """
    Check if code is syntactically valid (AST parseable).
    """
    try:
        ast.parse(code)
        return True
    except SyntaxError:
        return False

def generate_synthetic_issues(
    source_records: List[Dict[str, Any]],
    max_count: int = 50,
    output_path: Optional[Path] = None
) -> List[Dict[str, Any]]:
    """
    Generate synthetic ambiguous issues by mutating variable names, removing comments,
    and applying structural obfuscations.
    
    FR-009: Apply structural obfuscations.
    FR-008: Save ground_truth_lines from original code.
    
    Args:
        source_records: List of records to mutate (should have 'code' field).
        max_count: Maximum number of synthetic issues to generate.
        output_path: Optional path to write the synthetic issues JSONL.
        
    Returns:
        List of synthetic issue records.
    """
    synthetic_issues = []
    mutation_types = ['var_rename', 'comment_removal', 'control_flow_swap', 'api_sig_change']
    
    logger.info(f"Generating up to {max_count} synthetic issues from {len(source_records)} sources...")

    count = 0
    for record in source_records:
        if count >= max_count:
            break

        original_code = record.get('code')
        if not original_code:
            logger.warning(f"Skipping record {record.get('issue_id')}: no code field")
            continue

        # Pick a random mutation strategy
        strategy = random.choice(mutation_types)
        mutated_code = original_code
        mutation_applied = False

        try:
            if strategy == 'var_rename':
                mutated_code = mutate_variable_names(original_code, seed=hash(original_code) % 2**32)
                mutation_applied = True
            elif strategy == 'comment_removal':
                mutated_code = remove_comments(original_code)
                mutation_applied = True
            elif strategy == 'control_flow_swap':
                mutated_code = reorder_control_flow(original_code)
                mutation_applied = True
            elif strategy == 'api_sig_change':
                mutated_code = change_api_signature(original_code)
                mutation_applied = True
        except ValueError as e:
            logger.warning(f"Mutation failed for {record.get('issue_id')}: {e}")
            continue

        if mutation_applied and is_code_valid(mutated_code):
            synthetic_record = copy.deepcopy(record)
            synthetic_record['issue_id'] = f"synthetic_{record.get('issue_id', 'unknown')}_{strategy}"
            synthetic_record['code'] = mutated_code
            synthetic_record['mutation_type'] = strategy
            synthetic_record['original_code_hash'] = compute_sha256(original_code)
            synthetic_record['ground_truth_lines'] = record.get('ground_truth_lines', [])
            synthetic_record['is_synthetic'] = True
            synthetic_issues.append(synthetic_record)
            count += 1
        else:
            logger.warning(f"Mutation resulted in invalid code for {record.get('issue_id')}")

    logger.info(f"Successfully generated {len(synthetic_issues)} synthetic issues.")

    if output_path:
        output_path.parent.mkdir(parents=True, exist_ok=True)
        with open(output_path, 'w', encoding='utf-8') as f:
            for issue in synthetic_issues:
                f.write(json.dumps(issue) + '\n')
        logger.info(f"Wrote synthetic issues to {output_path}")

    return synthetic_issues

def main():
    """
    Main entry point for the curation script.
    Executes Part A: Filter Hard Instances.
    """
    config = get_config_summary()
    hard_percentile = config.get('HARD_INSTANCE_PERCENTILE', HARD_INSTANCE_PERCENTILE_DEFAULT)
    
    # Paths
    base_dir = Path(config.get('PROJECT_ROOT', '.'))
    input_dataset_path = base_dir / config.get('DATASET_PATH', 'data/raw/bench.final.public.jsonl')
    ground_truth_path = base_dir / config.get('GROUND_TRUTH_PATH', 'data/curated/ground_truth.jsonl')
    output_hard_path = base_dir / config.get('HARD_SUBSET_PATH', 'data/curated/hard_subset.jsonl')

    logger.info(f"Starting curation (Part A: Filter Hard Instances)...")
    logger.info(f"Input: {input_dataset_path}")
    logger.info(f"Ground Truth: {ground_truth_path}")
    logger.info(f"Output: {output_hard_path}")
    logger.info(f"Hard Percentile: {hard_percentile}%")

    # 1. Load Ground Truth (from T013)
    try:
        gt_map = load_derived_ground_truth(ground_truth_path)
    except FileNotFoundError as e:
        logger.error(f"Cannot proceed: {e}")
        sys.exit(1)

    # 2. Filter Hard Instances
    try:
        filter_hard_instances(
            input_dataset_path=input_dataset_path,
            output_path=output_hard_path,
            ground_truth_map=gt_map,
            percentile=hard_percentile
        )
    except Exception as e:
        logger.error(f"Failed to filter hard instances: {e}")
        sys.exit(1)

    logger.info("Curation Part A completed successfully.")

if __name__ == "__main__":
    main()
