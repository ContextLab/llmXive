"""
T033: Integrate hash_artifacts.py to hash final data/results/final_metrics.json.

This script computes the SHA256 hash of the final metrics file generated by the
analysis pipeline and updates the manifest in data/results/.
"""
import json
import sys
from pathlib import Path
from typing import Dict, Any

# Import the utility functions from the existing API surface
from utils.hash_artifacts import compute_sha256, generate_manifest

from config import get_config_summary


def hash_final_metrics_file(metrics_path: Path) -> Dict[str, Any]:
    """
    Compute the SHA256 hash of the final_metrics.json file.
    
    Args:
        metrics_path: Path to the final_metrics.json file.
        
    Returns:
        Dictionary containing the file path, hash, and metadata.
    """
    if not metrics_path.exists():
        raise FileNotFoundError(f"Final metrics file not found: {metrics_path}")
    
    file_hash = compute_sha256(metrics_path)
    
    return {
        "file": str(metrics_path),
        "sha256": file_hash,
        "size_bytes": metrics_path.stat().st_size,
        "status": "hashed"
    }


def main() -> int:
    """
    Main entry point for hashing the final metrics artifact.
    
    Returns:
        Exit code (0 for success, 1 for failure).
    """
    config = get_config_summary()
    project_root = Path(config.get("project_root", "."))
    
    # Define paths relative to project root
    results_dir = project_root / "data" / "results"
    metrics_file = results_dir / "final_metrics.json"
    manifest_file = results_dir / "manifest.json"
    
    print(f"Project root: {project_root}")
    print(f"Target metrics file: {metrics_file}")
    
    # Check if the metrics file exists
    if not metrics_file.exists():
        print(f"ERROR: Final metrics file not found at {metrics_file}")
        print("Please ensure T031c (stats analysis) has been completed first.")
        return 1
    
    try:
        # Hash the final metrics file
        hash_result = hash_final_metrics_file(metrics_file)
        
        print(f"Successfully hashed: {hash_result['file']}")
        print(f"SHA256: {hash_result['sha256']}")
        print(f"Size: {hash_result['size_bytes']} bytes")
        
        # Generate or update the manifest for the results directory
        # This ensures the final_metrics.json is tracked in the integrity manifest
        manifest = generate_manifest(results_dir)
        
        # Add the specific hash result to the manifest if not already present
        # or update it if it exists
        found = False
        for entry in manifest.get("files", []):
            if entry.get("file") == str(metrics_file):
                entry.update(hash_result)
                found = True
                break
        
        if not found:
            if "files" not in manifest:
                manifest["files"] = []
            manifest["files"].append(hash_result)
        
        # Write the updated manifest back to disk
        with open(manifest_file, "w", encoding="utf-8") as f:
            json.dump(manifest, f, indent=2)
        
        print(f"Manifest updated at: {manifest_file}")
        
        return 0
        
    except Exception as e:
        print(f"ERROR: Failed to hash final metrics: {e}", file=sys.stderr)
        return 1


if __name__ == "__main__":
    sys.exit(main())