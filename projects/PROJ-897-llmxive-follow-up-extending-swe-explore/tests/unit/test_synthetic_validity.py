"""
Unit tests for synthetic issue validity (AST parse check).

Tests that all synthetic issues generated by mutation functions
are syntactically valid Python code.
"""
import ast
import sys
from pathlib import Path
from typing import List, Dict, Any

# Add code directory to path for imports
sys.path.insert(0, str(Path(__file__).parent.parent))

from data.curate import (
    mutate_variable_names,
    remove_comments,
    reorder_control_flow,
    change_api_signature,
    is_code_valid,
    generate_synthetic_issues
)


class TestSyntheticValidity:
    """Test suite for synthetic issue validity."""

    def test_all_mutations_produce_valid_ast(self):
        """Test that all mutation functions produce AST-parseable code."""
        test_code = """
def example_function(x, y, z):
    # Calculate result
    result = x + y * z
    
    if result > 100:
  return result * 2
    else:
  return result / 2

class ExampleClass:
    def method(self, value):
  return value * 2
"""
        mutations = [
            ("mutate_variable_names", mutate_variable_names),
            ("remove_comments", remove_comments),
            ("reorder_control_flow", reorder_control_flow),
            ("change_api_signature", change_api_signature),
        ]
        
        for name, func in mutations:
          result = func(test_code)
          try:
              ast.parse(result)
              assert True, f"{name} produced valid AST"
          except SyntaxError as e:
              assert False, f"{name} produced invalid code: {e}"

    def test_generate_synthetic_issues_validity(self):
        """Test that generated synthetic issues are all valid."""
        # Create a small test dataset
        test_issues = [
            {
                "id": "test_1",
                "code": """
def add_numbers(a, b):
    return a + b
""",
                "ground_truth_lines": [2]
            },
            {
                "id": "test_2",
                "code": """
def multiply_values(x, y, z):
    result = x * y * z
    return result
""",
                "ground_truth_lines": [2]
            }
        ]
        
        # Generate synthetic issues
        synthetic_issues = generate_synthetic_issues(test_issues, max_issues=10)
        
        # Verify all generated issues are valid
        for issue in synthetic_issues:
            assert "code" in issue, "Issue should have code field"
            assert is_code_valid(issue["code"]), f"Issue {issue['id']} should be valid"
            
            # Verify AST parsing works
            try:
                ast.parse(issue["code"])
            except SyntaxError as e:
                assert False, f"Issue {issue['id']} failed AST parsing: {e}"

    def test_is_code_valid_comprehensive(self):
        """Test is_code_valid with various code patterns."""
        valid_cases = [
            "def f(): pass",
            "class C: pass",
            "x = 1",
            "if True: pass",
            "for i in range(10): pass",
            "while True: break",
            "try: pass\nexcept: pass",
            "def f(): return 42",
            "lambda x: x * 2",
            "[x for x in range(10)]",
            "{x: x*2 for x in range(10)}",
            "def f(a, b=1, *args, **kwargs): pass",
        ]
        
        invalid_cases = [
            "def f(: pass",
            "if True pass",
            "for i in : pass",
            "x = ",
            "def f():\n    return\n    x = 1",  # Unreachable code is valid, but let's test syntax
            "class C(: pass",
            "lambda : pass",
        ]
        
        for code in valid_cases:
            assert is_code_valid(code), f"Should be valid: {code}"
        
        for code in invalid_cases:
            assert not is_code_valid(code), f"Should be invalid: {code}"

    def test_complex_code_validity(self):
        """Test validity with complex code structures."""
        complex_code = """
import os
import sys
from typing import List, Dict, Optional

class DataProcessor:
    def __init__(self, config: Dict[str, Any]):
  self.config = config
  self.data: List[Any] = []
    
    def process(self, items: List[int]) -> Optional[int]:
  if not items:
      return None
  
  result = 0
  for item in items:
      if item > 0:
          result += item
      elif item < 0:
          result -= abs(item)
      else:
          continue
  
  return result if result != 0 else None
    
    @staticmethod
    def validate(value: Any) -> bool:
  return isinstance(value, (int, float))

def main():
    processor = DataProcessor({"threshold": 10})
    data = [1, -2, 3, -4, 0, 5]
    result = processor.process(data)
    print(f"Result: {result}")

if __name__ == "__main__":
    main()
"""
        assert is_code_valid(complex_code), "Complex code should be valid"
        ast.parse(complex_code)  # Should not raise

    def test_edge_cases_validity(self):
        """Test edge cases for code validity."""
        edge_cases = [
            ("Empty string", "", False),
            ("Whitespace only", "   \n\t  ", False),
            ("Single comment", "# just a comment", True),  # Comments are valid
            ("Single statement", "x = 1", True),
            ("Multiple statements", "x = 1\ny = 2\nz = 3", True),
            ("Nested functions", "def f():\n    def g():\n        pass", True),
            ("Lambda with default", "lambda x=1: x", True),
            ("Generator expression", "(x for x in range(10))", True),
        ]
        
        for name, code, expected_valid in edge_cases:
            result = is_code_valid(code)
            assert result == expected_valid, f"{name}: expected {expected_valid}, got {result}"

    def test_mutation_preserves_validity(self):
        """Test that mutations don't break code validity."""
        base_codes = [
            "def f(x): return x * 2",
            "class C:\n    def m(self): pass",
            "if True:\n    x = 1\nelse:\n    x = 2",
            "for i in range(10):\n    if i % 2 == 0:\n        print(i)",
        ]
        
        mutations = [
            mutate_variable_names,
            remove_comments,
            reorder_control_flow,
            change_api_signature,
        ]
        
        for base_code in base_codes:
            for mutation_func in mutations:
                try:
                    mutated = mutation_func(base_code)
                    assert is_code_valid(mutated), f"Mutation {mutation_func.__name__} broke validity"
                    ast.parse(mutated)  # Verify AST parsing
                except Exception as e:
                    assert False, f"Mutation {mutation_func.__name__} raised exception: {e}"

    def test_invalid_input_handling(self):
        """Test that invalid inputs are handled gracefully."""
        invalid_inputs = [
            None,
            "",
            "   ",
            "not valid python {{{",
        ]
        
        for invalid in invalid_inputs:
            # is_code_valid should return False for invalid inputs
            if invalid is None:
                continue  # Skip None as it might cause different errors
            result = is_code_valid(invalid)
            assert result is False, f"Invalid input should return False: {invalid}"


