# Setup logging
logger = logging.getLogger(__name__)

# Threshold values for sweep
THRESHOLD_K_VALUES = [0.5, 1.0, 1.5, 2.0, 2.5, 3.0]
SIGNIFICANCE_ALPHA = 0.05

def load_baseline_metrics(filepath: str = "data/processed/baseline_metrics.json") -> Optional[Dict[str, Any]]:
    """Load baseline metrics from JSON file."""
    if not os.path.exists(filepath):
        logger.warning(f"Baseline metrics file not found: {filepath}")
        return None
    with open(filepath, 'r') as f:
        return json.load(f)

def load_cleaned_metrics(filepath: str = "data/processed/cleaned_metrics.json") -> Optional[Dict[str, Any]]:
    """Load cleaned metrics from JSON file."""
    if not os.path.exists(filepath):
        logger.warning(f"Cleaned metrics file not found: {filepath}")
        return None
    with open(filepath, 'r') as f:
        return json.load(f)

def load_dataset_from_processed(dataset_name: str, processed_dir: str = "data/processed") -> Optional[pd.DataFrame]:
    """Load a dataset from the processed directory."""
    # Try to find the dataset file
    possible_patterns = [
        f"{processed_dir}/{dataset_name}.csv",
        f"{processed_dir}/{dataset_name}_cleaned.csv",
        f"{processed_dir}/cleaned_{dataset_name}.csv"
    ]
    
    for pattern in possible_patterns:
        if os.path.exists(pattern):
            try:
                df = pd.read_csv(pattern)
                logger.info(f"Loaded dataset from {pattern}")
                return df
            except Exception as e:
                logger.warning(f"Failed to load {pattern}: {e}")
                continue
    
    logger.warning(f"Could not find dataset: {dataset_name}")
    return None

def generate_null_dataset(df: pd.DataFrame, seed: int = 42) -> pd.DataFrame:
    """
    Generate a null dataset by shuffling the outcome variable while keeping predictors fixed.
    This is used for FPR estimation.
    """
    pin_random_seed(seed)
    df_null = df.copy()
    
    # Identify numeric columns (potential predictors and outcome)
    numeric_cols = df_null.select_dtypes(include=[np.number]).columns.tolist()
    
    if len(numeric_cols) < 2:
        logger.warning(f"Not enough numeric columns in {df_null.columns.tolist()} to generate null dataset")
        return df_null
    
    # Assume the last numeric column is the outcome (or a designated outcome column)
    # For simplicity, we'll shuffle the last numeric column
    outcome_col = numeric_cols[-1]
    predictors = numeric_cols[:-1]
    
    # Shuffle the outcome
    df_null[outcome_col] = np.random.permutation(df_null[outcome_col].values)
    
    return df_null

def estimate_fpr_for_dataset(
    df_null: pd.DataFrame,
    dataset_name: str,
    threshold_k: float,
    config: Optional[Config] = None
) -> Dict[str, Any]:
    """
    Estimate False Positive Rate (FPR) for a null dataset at a given outlier threshold.
    
    FPR = proportion of tests with p <= 0.05 in null datasets.
    """
    if config is None:
        config = get_config()
    
    seed = config.get("RANDOM_SEED", 42) if hasattr(config, 'get') else 42
    pin_random_seed(seed)
    
    # Apply outlier removal with the given threshold
    try:
        df_cleaned_null = apply_iqr_outlier_removal(df_null, k=threshold_k)
    except Exception as e:
        logger.error(f"Failed to apply outlier removal with k={threshold_k}: {e}")
        return {"success": False, "error": str(e)}
    
    # Run baseline analysis on the cleaned null dataset
    # We pass df directly and use dataset_name
    result = run_baseline_analysis(
        df=df_cleaned_null,
        dataset_name=f"null_k_{threshold_k}",
        config=config
    )
    
    if not result or not result.get('success', False):
        logger.warning(f"Analysis failed for null dataset with k={threshold_k}")
        return {"success": False, "p_values": [], "significant_count": 0, "total_tests": 0}
    
    # Extract p-values from the result
    p_values = []
    significant_count = 0
    total_tests = 0
    
    # The result structure varies; handle both dict and list formats
    if 'results' in result:
        results_list = result['results']
    elif isinstance(result, dict):
        results_list = [result]
    else:
        results_list = []
    
    for entry in results_list:
        if isinstance(entry, dict):
            # Check for t-test p-value
            if 't_test' in entry and isinstance(entry['t_test'], dict):
                p_val = entry['t_test'].get('p_value')
                if p_val is not None and np.isfinite(p_val):
                    p_values.append(p_val)
                    total_tests += 1
                    if p_val <= SIGNIFICANCE_ALPHA:
                        significant_count += 1
            
            # Check for regression p-values
            if 'regression' in entry and isinstance(entry['regression'], dict):
                coefs = entry['regression'].get('coefficients', [])
                for coef in coefs:
                    p_val = coef.get('p_value')
                    if p_val is not None and np.isfinite(p_val):
                        p_values.append(p_val)
                        total_tests += 1
                        if p_val <= SIGNIFICANCE_ALPHA:
                            significant_count += 1
    
    fpr = significant_count / total_tests if total_tests > 0 else 0.0
    
    return {
        "success": True,
        "dataset_name": dataset_name,
        "threshold_k": threshold_k,
        "p_values": p_values,
        "significant_count": significant_count,
        "total_tests": total_tests,
        "fpr": fpr
    }

def calculate_inconsistency_rate(
    baseline_metrics: Dict[str, Any],
    cleaned_metrics: Dict[str, Any],
    threshold_k: float
) -> Dict[str, Any]:
    """
    Calculate Inconsistency Rate as proportion of datasets where significance status changes.
    
    Inconsistency Rate = (number of datasets where significance changed) / (total number of datasets)
    """
    if not baseline_metrics or not cleaned_metrics:
        logger.warning("Cannot calculate inconsistency rate: missing baseline or cleaned metrics")
        return {"inconsistency_rate": None, "datasets_analyzed": 0, "changes": 0}
    
    baseline_datasets = baseline_metrics.get('datasets', [])
    cleaned_datasets = cleaned_metrics.get('datasets', [])
    
    # Filter cleaned datasets for the specific threshold
    cleaned_threshold_datasets = [
        d for d in cleaned_datasets 
        if d.get('cleaning_strategy', '').lower().startswith(f'outlier_removed_k{threshold_k}')
        or f'k{threshold_k}' in d.get('dataset_name', '').lower()
    ]
    
    if not baseline_datasets or not cleaned_threshold_datasets:
        logger.warning(f"No matching datasets for threshold k={threshold_k}")
        return {"inconsistency_rate": None, "datasets_analyzed": 0, "changes": 0}
    
    changes = 0
    total_comparisons = 0
    
    for baseline_entry in baseline_datasets:
        ds_name = baseline_entry.get('dataset_name')
        if not ds_name:
            continue
        
        # Find corresponding cleaned entry
        cleaned_entry = None
        for c_entry in cleaned_threshold_datasets:
            c_name = c_entry.get('dataset_name', '')
            # Match based on dataset name (handling naming conventions)
            if ds_name in c_name or c_name in ds_name:
                cleaned_entry = c_entry
                break
        
        if not cleaned_entry:
            continue
        
        total_comparisons += 1
        
        # Extract significance status from baseline
        base_significant = False
        base_p = None
        if 't_test' in baseline_entry and isinstance(baseline_entry['t_test'], dict):
            base_p = baseline_entry['t_test'].get('p_value')
            if base_p is not None and np.isfinite(base_p):
                base_significant = (base_p <= SIGNIFICANCE_ALPHA)
        
        # Extract significance status from cleaned
        clean_significant = False
        clean_p = None
        if 't_test' in cleaned_entry and isinstance(cleaned_entry['t_test'], dict):
            clean_p = cleaned_entry['t_test'].get('p_value')
            if clean_p is not None and np.isfinite(clean_p):
                clean_significant = (clean_p <= SIGNIFICANCE_ALPHA)
        
        # Check for change in significance
        if base_significant != clean_significant:
            changes += 1
    
    inconsistency_rate = changes / total_comparisons if total_comparisons > 0 else 0.0
    
    return {
        "inconsistency_rate": inconsistency_rate,
        "datasets_analyzed": total_comparisons,
        "changes": changes,
        "threshold_k": threshold_k
    }

def run_threshold_sweep(
    config: Optional[Config] = None,
    num_null_iterations: int = 10
) -> Dict[str, Any]:
    """
    Run the outlier threshold sweep for k ∈ {0.5, 1.0, 1.5, 2.0, 2.5, 3.0}.
    
    For each threshold:
    1. Calculate FPR using null datasets (shuffled outcomes)
    2. Calculate Inconsistency Rate using baseline vs cleaned metrics
    
    Returns a comprehensive report with all results.
    """
    if config is None:
        config = get_config()
    
    seed = config.get("RANDOM_SEED", 42) if hasattr(config, 'get') else 42
    pin_random_seed(seed)
    
    logger.info(f"Starting outlier threshold sweep with {num_null_iterations} null iterations per threshold")
    logger.info(f"Thresholds: {THRESHOLD_K_VALUES}")
    
    results = {
        "timestamp": datetime.now().isoformat(),
        "thresholds": THRESHOLD_K_VALUES,
        "num_null_iterations": num_null_iterations,
        "results": [],
        "summary": {}
    }
    
    # Load baseline and cleaned metrics for inconsistency rate calculation
    baseline_metrics = load_baseline_metrics()
    cleaned_metrics = load_cleaned_metrics()
    
    if not baseline_metrics:
        logger.warning("Baseline metrics not found. Inconsistency rate calculations will be skipped.")
    if not cleaned_metrics:
        logger.warning("Cleaned metrics not found. Inconsistency rate calculations will be skipped.")
    
    # Get list of raw datasets to generate null datasets from
    raw_dir = config.get("RAW_DATA_PATH", "data/raw") if hasattr(config, 'get') else "data/raw"
    datasets = load_datasets_from_raw(raw_dir)
    
    if not datasets:
        logger.error("No datasets found in raw directory. Cannot proceed with threshold sweep.")
        return results
    
    logger.info(f"Found {len(datasets)} datasets for threshold sweep")
    
    for threshold_k in THRESHOLD_K_VALUES:
        logger.info(f"Processing threshold k={threshold_k}")
        
        threshold_result = {
            "threshold_k": threshold_k,
            "fpr_results": [],
            "inconsistency_results": []
        }
        
        # --- FPR Calculation ---
        # Generate and analyze null datasets for this threshold
        fpr_p_values = []
        total_fpr_tests = 0
        total_fpr_significant = 0
        
        for i in range(num_null_iterations):
            for dataset in datasets:
                df = dataset.get('data')
                if df is None or df.empty:
                    continue
                
                dataset_name = dataset.get('name', f"dataset_{i}")
                
                # Generate null dataset (shuffle outcome)
                df_null = generate_null_dataset(df, seed=seed + i)
                
                # Estimate FPR for this null dataset
                fpr_result = estimate_fpr_for_dataset(
                    df_null, 
                    f"{dataset_name}_null_iter_{i}", 
                    threshold_k, 
                    config
                )
                
                if fpr_result.get('success', False):
                    threshold_result['fpr_results'].append(fpr_result)
                    fpr_p_values.extend(fpr_result.get('p_values', []))
                    total_fpr_significant += fpr_result.get('significant_count', 0)
                    total_fpr_tests += fpr_result.get('total_tests', 0)
        
        # Calculate overall FPR for this threshold
        overall_fpr = total_fpr_significant / total_fpr_tests if total_fpr_tests > 0 else 0.0
        threshold_result['overall_fpr'] = overall_fpr
        threshold_result['total_fpr_tests'] = total_fpr_tests
        threshold_result['total_fpr_significant'] = total_fpr_significant
        
        logger.info(f"Threshold k={threshold_k}: FPR = {overall_fpr:.4f} ({total_fpr_significant}/{total_fpr_tests} tests)")
        
        # --- Inconsistency Rate Calculation ---
        if baseline_metrics and cleaned_metrics:
            inconsistency_result = calculate_inconsistency_rate(
                baseline_metrics, 
                cleaned_metrics, 
                threshold_k
            )
            threshold_result['inconsistency_result'] = inconsistency_result
            logger.info(
                f"Threshold k={threshold_k}: Inconsistency Rate = {inconsistency_result.get('inconsistency_rate', 'N/A')}"
            )
        
        results['results'].append(threshold_result)
    
    # --- Summary ---
    results['summary'] = {
        "fpr_by_threshold": {
            str(r['threshold_k']): r.get('overall_fpr', 0.0) 
            for r in results['results']
        },
        "inconsistency_by_threshold": {
            str(r['threshold_k']): r.get('inconsistency_result', {}).get('inconsistency_rate')
            for r in results['results']
            if r.get('inconsistency_result')
        }
    }
    
    return results

def write_output(results: Dict[str, Any], output_path: str = "data/processed/threshold_sweep_results.json"):
    """Write the threshold sweep results to a JSON file."""
    os.makedirs(os.path.dirname(output_path), exist_ok=True)
    with open(output_path, 'w') as f:
        json.dump(results, f, indent=2, default=str)
    logger.info(f"Threshold sweep results written to {output_path}")

def main():
    """Main entry point for T033."""
    # Setup logging
    log_level = os.getenv("LOG_LEVEL", "INFO")
    setup_logging(log_level)
    
    logger.info("Starting T033: Outlier Threshold Sweep")
    
    # Load config
    config = get_config()
    
    # Get parameters from config or defaults
    num_null_iterations = config.get("BOOTSTRAP_ITERATIONS", 10) if hasattr(config, 'get') else 10
    # Clamp to reasonable range
    num_null_iterations = max(5, min(num_null_iterations, 50))
    
    # Run the threshold sweep
    results = run_threshold_sweep(config, num_null_iterations)
    
    # Write output
    output_path = config.get("OUTPUT_PATH", "data/processed") if hasattr(config, 'get') else "data/processed"
    output_file = os.path.join(output_path, "threshold_sweep_results.json")
    write_output(results, output_file)
    
    logger.info("T033 completed successfully")
    return 0

if __name__ == "__main__":
    sys.exit(main())
