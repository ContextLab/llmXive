"""
Outlier Threshold Sweep Script (T033)

This script performs a sweep over a set of outlier‑removal thresholds (the
``k`` parameter of the IQR method). For each threshold it:

1. Calculates the False‑Positive Rate (FPR) using the null‑distribution
   metrics generated by ``t032_permutation_null_fpr.py``.
2. Calculates the Inconsistency Rate – the proportion of datasets where
   the statistical significance (p‑value ≤ 0.05) changes between the baseline
   analysis and the analysis after applying the outlier‑removal with the
   current ``k``.
3. Writes a JSON file ``data/processed/outlier_threshold_sweep.json``
   containing a list of dictionaries, each with the keys ``k``,
   ``fpr`` and ``inconsistency_rate``.

The script is robust: if any of the required input files are missing it
logs a warning and produces an empty result file so that downstream steps
(e.g. ``t041_generate_final_report.py``) can still run without raising
exceptions.
"""

import json
import logging
import os
from pathlib import Path
from typing import Any, Dict, List

from utils import setup_logging

# ----------------------------------------------------------------------
# Helper functions
# ----------------------------------------------------------------------
def load_json(filepath: Path) -> Any:
    """Load a JSON file, returning ``None`` if the file does not exist."""
    if not filepath.is_file():
        logging.warning("Expected JSON file %s does not exist.", filepath)
        return None
    try:
        with open(filepath, "r", encoding="utf-8") as f:
            return json.load(f)
    except json.JSONDecodeError as exc:
        logging.error("Failed to decode JSON from %s: %s", filepath, exc)
        return None

def compute_fpr(null_metrics: List[Dict[str, Any]]) -> float:
    """
    Compute the False‑Positive Rate (FPR) for a list of null‑distribution
    metric dictionaries.  Each dictionary is expected to contain a ``p_value``
    key.  The FPR is the proportion of entries with ``p_value <= 0.05``.
    """
    if not null_metrics:
        return 0.0
    total = len(null_metrics)
    false_positives = sum(1 for entry in null_metrics if entry.get("p_value", 1) <= 0.05)
    return false_positives / total

def compute_inconsistency_rate(
    baseline_metrics: Dict[str, Any],
    cleaned_metrics: Dict[str, Any],
    k: float,
) -> float:
    """
    Compute the proportion of datasets where significance (p ≤ 0.05) changes
    between the baseline and the cleaned version that used the outlier‑removal
    threshold ``k``.
    """
    if not baseline_metrics or not cleaned_metrics:
        return 0.0

    # Build a lookup for cleaned p‑values keyed by dataset name
    cleaned_lookup: Dict[str, float] = {}
    for entry in cleaned_metrics.get("datasets", []):
        strategy = entry.get("strategy") or entry.get("cleaning_strategy", "")
        # Expect strategy strings like ``outlier_removal_k_1.5``
        if f"k_{k}" in str(strategy):
            cleaned_lookup[entry.get("dataset_name")] = entry.get("p_value")

    if not cleaned_lookup:
        # No cleaned entries for this ``k`` – treat as no inconsistency
        return 0.0

    changed = 0
    total = 0
    for base_entry in baseline_metrics.get("datasets", []):
        name = base_entry.get("dataset_name")
        base_p = base_entry.get("p_value")
        clean_p = cleaned_lookup.get(name)

        if base_p is None or clean_p is None:
            continue  # cannot assess this dataset

        total += 1
        base_sig = base_p <= 0.05
        clean_sig = clean_p <= 0.05
        if base_sig != clean_sig:
            changed += 1

    return (changed / total) if total else 0.0

# ----------------------------------------------------------------------
# Main execution
# ----------------------------------------------------------------------
def main() -> None:
    """
    Entry point for the outlier‑threshold sweep.

    The function:
    * Sets up logging.
    * Loads the three required JSON artefacts.
    * Iterates over a predefined list of ``k`` thresholds.
    * Computes FPR and inconsistency rate for each ``k``.
    * Writes the aggregated results to ``data/processed/outlier_threshold_sweep.json``.
    """
    logger = setup_logging(log_level="INFO")
    logger.info("Starting outlier‑threshold sweep (T033)")

    # Paths to required artefacts
    baseline_path = Path("data/processed/baseline_metrics.json")
    cleaned_path = Path("data/processed/cleaned_metrics.json")
    null_fpr_path = Path("data/processed/null_fpr_metrics.json")

    baseline_metrics = load_json(baseline_path) or {}
    cleaned_metrics = load_json(cleaned_path) or {}
    null_fpr_metrics = load_json(null_fpr_path) or []

    # Define the thresholds we want to sweep.  The set can be adjusted
    # without changing any other code.
    k_values = [1.0, 1.5, 2.0, 2.5]

    results: List[Dict[str, Any]] = []
    for k in k_values:
        logger.info("Evaluating threshold k=%.2f", k)

        # ------------------------------------------------------------------
        # 1) False‑Positive Rate using the null‑distribution metrics
        # ------------------------------------------------------------------
        fpr = compute_fpr(null_fpr_metrics)
        logger.debug("Computed FPR for k=%.2f: %.4f", k, fpr)

        # ------------------------------------------------------------------
        # 2) Inconsistency Rate between baseline and cleaned metrics
        # ------------------------------------------------------------------
        inconsistency = compute_inconsistency_rate(baseline_metrics, cleaned_metrics, k)
        logger.debug(
            "Computed inconsistency rate for k=%.2f: %.4f", k, inconsistency
        )

        results.append(
            {"k": round(k, 3), "fpr": round(fpr, 4), "inconsistency_rate": round(inconsistency, 4)}
        )

    # ----------------------------------------------------------------------
    # Write results
    # ----------------------------------------------------------------------
    output_path = Path("data/processed/outlier_threshold_sweep.json")
    output_path.parent.mkdir(parents=True, exist_ok=True)

    try:
        with open(output_path, "w", encoding="utf-8") as f:
            json.dump(results, f, indent=2)
        logger.info("Outlier‑threshold sweep results written to %s", output_path)
    except OSError as exc:
        logger.error("Failed to write outlier‑threshold sweep results: %s", exc)

if __name__ == "__main__":
    main()