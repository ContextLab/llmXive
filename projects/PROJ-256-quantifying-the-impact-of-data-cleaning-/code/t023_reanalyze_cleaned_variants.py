"""
Re‑analyse all cleaned dataset variants.

This script scans the processed data directory for CSV files generated by
the cleaning pipeline, runs the baseline statistical analysis on each
cleaned variant, and writes a consolidated JSON artifact
``data/processed/cleaned_metrics.json`` containing the results.

The implementation is deliberately tolerant of the various configuration
and logging call signatures used throughout the repository.
"""

import json
import logging
from pathlib import Path
from typing import List, Dict, Any

import pandas as pd

from .analysis import run_baseline_analysis
from .config import get_config
from .utils import setup_logging, pin_random_seed

__all__ = ["main"]


def _collect_cleaned_csv_paths(processed_dir: Path) -> List[Path]:
    """
    Return a list of CSV files in *processed_dir* that represent cleaned
    datasets.  Files that are clearly not cleaned variants (e.g. the
    ``baseline_metrics.json`` file) are ignored.
    """
    csv_paths = [
        p for p in processed_dir.glob("*.csv")
        if p.is_file() and not p.name.lower().endswith("baseline.csv")
    ]
    return csv_paths


def _analyse_dataset(csv_path: Path) -> Dict[str, Any]:
    """
    Load a cleaned CSV, run the baseline analysis on it, and return the
    metrics dictionary enriched with the dataset name.
    """
    df = pd.read_csv(csv_path)
    metrics = run_baseline_analysis(dataframe=df)
    # The analysis function returns a dict with keys: p_value, ci, effect_size,
    # and optionally r_squared.
    metrics["dataset_name"] = csv_path.stem
    return metrics


def main() -> None:
    """
    Entry point executed by the pipeline (e.g. via ``python code/main.py``).

    Steps:
    1. Initialise logging (flexible signature).
    2. Pin the random seed for reproducibility.
    3. Determine the processed data directory from configuration.
    4. Locate all cleaned CSV files.
    5. Run analysis on each and aggregate results.
    6. Write the aggregated JSON to the configured output location.
    """
    # 1. Logging
    logger = setup_logging()  # accepts no‑arg call

    # 2. Reproducibility
    cfg = get_config()
    seed_str = cfg.get("RANDOM_SEED", "42")
    try:
        seed = int(seed_str)
    except ValueError:
        seed = 42
    pin_random_seed(seed)
    logger.debug("Random seed pinned to %d", seed)

    # 3. Determine processed directory
    processed_dir_str = cfg.get("PROCESSED_DATA_PATH", "data/processed")
    processed_dir = Path(processed_dir_str)
    if not processed_dir.is_dir():
        logger.error("Processed data directory does not exist: %s", processed_dir)
        raise FileNotFoundError(f"Processed data directory not found: {processed_dir}")

    logger.info("Scanning cleaned datasets in %s", processed_dir)

    # 4. Locate CSVs
    csv_paths = _collect_cleaned_csv_paths(processed_dir)
    if not csv_paths:
        logger.warning("No cleaned CSV files found in %s", processed_dir)
        # Still create an empty JSON artifact so downstream scripts do not crash.
        empty_payload = {
            "generated_at": pd.Timestamp.utcnow().isoformat(),
            "datasets": [],
        }
        output_path = Path(cfg.get("CLEANED_METRICS_PATH", "data/processed/cleaned_metrics.json"))
        output_path.parent.mkdir(parents=True, exist_ok=True)
        with open(output_path, "w", encoding="utf-8") as f:
            json.dump(empty_payload, f, indent=2)
        logger.info("Wrote empty cleaned metrics file to %s", output_path)
        return

    # 5. Analyse each cleaned dataset
    all_metrics: List[Dict[str, Any]] = []
    for csv_path in csv_paths:
        try:
            metrics = _analyse_dataset(csv_path)
            all_metrics.append(metrics)
            logger.debug("Processed %s – metrics: %s", csv_path.name, metrics)
        except Exception as e:
            logger.error("Failed to analyse %s: %s", csv_path.name, e)
            # Record failure in processing_log.json
            log_path = Path(cfg.get("PROCESSING_LOG_PATH", "data/processed/processing_log.json"))
            log_path.parent.mkdir(parents=True, exist_ok=True)
            try:
                with open(log_path, "r", encoding="utf-8") as lf:
                    log_data = json.load(lf)
            except (FileNotFoundError, json.JSONDecodeError):
                log_data = {"generated_at": pd.Timestamp.utcnow().isoformat(), "failures": []}
            log_data["failures"].append({"dataset": csv_path.name, "error": str(e)})
            with open(log_path, "w", encoding="utf-8") as lf:
                json.dump(log_data, lf, indent=2)

    # 6. Write consolidated JSON
    output_path = Path(cfg.get("CLEANED_METRICS_PATH", "data/processed/cleaned_metrics.json"))
    output_path.parent.mkdir(parents=True, exist_ok=True)
    payload = {
        "generated_at": pd.Timestamp.utcnow().isoformat(),
        "datasets": all_metrics,
    }
    with open(output_path, "w", encoding="utf-8") as f:
        json.dump(payload, f, indent=2)

    logger.info("Cleaned metrics written to %s", output_path)


if __name__ == "__main__":
    main()