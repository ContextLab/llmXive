logger = logging.getLogger(__name__)

def load_json(filepath: str) -> Dict[str, Any]:
    """Load a JSON file and return its contents."""
    if not os.path.exists(filepath):
        raise FileNotFoundError(f"Required file not found: {filepath}")
    with open(filepath, 'r') as f:
        return json.load(f)

def load_baseline_metrics() -> Dict[str, Any]:
    """Load baseline metrics from the processed directory."""
    return load_json("data/processed/baseline_metrics.json")

def load_cleaned_metrics() -> Dict[str, Any]:
    """Load cleaned metrics from the processed directory."""
    return load_json("data/processed/cleaned_metrics.json")

def load_sensitivity_analysis() -> Dict[str, Any]:
    """
    Load sensitivity analysis results.
    Tries to load from multiple potential sources generated by T030-T033.
    """
    sources = [
        "data/processed/sensitivity_analysis.json",
        "data/processed/size_sensitivity.json",
        "data/processed/bootstrap_variance_results.json",
        "data/processed/null_fpr_metrics.json"
    ]

    combined_analysis = {
        "size_bins": [],
        "missingness_bins": [],
        "bootstrap_results": [],
        "fpr_estimates": [],
        "threshold_sweep": []
    }

    for source in sources:
        if os.path.exists(source):
            try:
                data = load_json(source)
                # Merge based on keys if they exist, or append to lists
                if isinstance(data, dict):
                    for key in combined_analysis:
                        if key in data:
                            if isinstance(data[key], list):
                                combined_analysis[key].extend(data[key])
                            else:
                                combined_analysis[key] = data[key]
                    # If it has a specific structure, keep it
                    if "overall_summary" in data:
                        combined_analysis["overall_summary"] = data["overall_summary"]
            except Exception as e:
                logger.warning(f"Could not load or parse {source}: {e}")

    return combined_analysis

def calculate_relative_diff(abs_diff: float, baseline_value: float) -> float:
    """Calculate relative difference (percentage change) handling zero baseline."""
    if baseline_value == 0:
        return 0.0 if abs_diff == 0 else float('inf')
    return abs_diff / abs(baseline_value)

def aggregate_metrics_for_comparison(
    baseline_data: Dict[str, Any],
    cleaned_data: Dict[str, Any]
) -> List[Dict[str, Any]]:
    """
    Aggregate baseline and cleaned metrics into a unified comparison structure.
    Returns a list of comparison records per dataset/strategy.
    """
    comparisons = []

    # Assuming structure: { "datasets": [ { "name": "...", "metrics": { ... } } ] }
    # or flat list of results. Adapting to likely structure from T012/T023.

    baseline_results = baseline_data.get("results", baseline_data.get("datasets", []))
    cleaned_results = cleaned_data.get("results", cleaned_data.get("datasets", []))

    # Create a lookup for baseline by dataset name
    baseline_lookup = {}
    for item in baseline_results:
        if isinstance(item, dict):
            name = item.get("dataset_name") or item.get("name")
            if name:
                baseline_lookup[name] = item

    # Iterate through cleaned results to find matches
    for cleaned_item in cleaned_results:
        if not isinstance(cleaned_item, dict):
            continue

        name = cleaned_item.get("dataset_name") or cleaned_item.get("name")
        strategy = cleaned_item.get("strategy") or cleaned_item.get("cleaning_strategy")

        if name not in baseline_lookup:
            logger.warning(f"Baseline metrics missing for dataset: {name}. Skipping comparison.")
            continue

        baseline_item = baseline_lookup[name]
        baseline_metrics = baseline_item.get("metrics", {})
        cleaned_metrics = cleaned_item.get("metrics", {})

        # Extract specific values with defaults
        p_baseline = baseline_metrics.get("p_value")
        p_cleaned = cleaned_metrics.get("p_value")
        ci_width_baseline = baseline_metrics.get("ci_width")
        ci_width_cleaned = cleaned_metrics.get("ci_width")
        effect_size_baseline = baseline_metrics.get("effect_size")
        effect_size_cleaned = cleaned_metrics.get("effect_size")

        if p_baseline is None or p_cleaned is None:
            logger.warning(f"Skipping {name}/{strategy}: Missing p-values.")
            continue

        abs_diff = calculate_p_value_shift(p_baseline, p_cleaned)
        rel_diff = calculate_relative_diff(abs_diff, p_baseline)
        ci_change = compute_ci_width_change(ci_width_baseline, ci_width_cleaned)
        es_delta = compute_effect_size_delta(effect_size_baseline, effect_size_cleaned)

        comparisons.append({
            "dataset_name": name,
            "strategy": strategy,
            "baseline_p_value": p_baseline,
            "cleaned_p_value": p_cleaned,
            "absolute_diff": abs_diff,
            "relative_diff": rel_diff,
            "ci_width_change": ci_change,
            "effect_size_delta": es_delta,
            "baseline_metrics": baseline_metrics,
            "cleaned_metrics": cleaned_metrics
        })

    return comparisons

def create_comparison_report(
    comparisons: List[Dict[str, Any]],
    sensitivity_analysis: Dict[str, Any]
) -> ComparisonReport:
    """
    Construct the ComparisonReport entity with all required fields.
    """
    # Calculate overall statistics
    total_comparisons = len(comparisons)
    if total_comparisons == 0:
        logger.warning("No comparisons found. Generating empty report.")
        return ComparisonReport(
            report_id="T040-EMPTY",
            generated_at=datetime.now().isoformat(),
            baseline_metrics={},
            cleaned_metrics={},
            absolute_diff=[],
            relative_diff=[],
            sensitivity_analysis=sensitivity_analysis,
            summary_statistics={
                "total_comparisons": 0,
                "mean_p_value_shift": 0.0,
                "median_p_value_shift": 0.0,
                "inconsistency_rate": 0.0
            }
        )

    p_shifts = [c["absolute_diff"] for c in comparisons]
    rel_shifts = [c["relative_diff"] for c in comparisons]

    mean_shift = float(np.mean(p_shifts))
    median_shift = float(np.median(p_shifts))

    # Inconsistency rate: proportion where significance status changes (p < 0.05)
    inconsistencies = 0
    for c in comparisons:
        sig_baseline = c["baseline_p_value"] < 0.05
        sig_cleaned = c["cleaned_p_value"] < 0.05
        if sig_baseline != sig_cleaned:
            inconsistencies += 1
    inconsistency_rate = inconsistencies / total_comparisons if total_comparisons > 0 else 0.0

    # Bonferroni correction on p-values if needed (aggregate)
    # For the report, we include the raw and corrected p-values if applicable
    # Here we just note the correction was considered in the process
    bonferroni_note = "Bonferroni correction logic available in reporting module."

    # Construct lists for the report
    abs_diff_list = [{"dataset": c["dataset_name"], "strategy": c["strategy"], "value": c["absolute_diff"]} for c in comparisons]
    rel_diff_list = [{"dataset": c["dataset_name"], "strategy": c["strategy"], "value": c["relative_diff"]} for c in comparisons]

    return ComparisonReport(
        report_id=f"T040-{datetime.now().strftime('%Y%m%d%H%M%S')}",
        generated_at=datetime.now().isoformat(),
        baseline_metrics=comparisons[0]["baseline_metrics"] if comparisons else {},
        cleaned_metrics=comparisons[0]["cleaned_metrics"] if comparisons else {},
        absolute_diff=abs_diff_list,
        relative_diff=rel_diff_list,
        sensitivity_analysis=sensitivity_analysis,
        summary_statistics={
            "total_comparisons": total_comparisons,
            "mean_p_value_shift": mean_shift,
            "median_p_value_shift": median_shift,
            "inconsistency_rate": inconsistency_rate,
            "bonferroni_note": bonferroni_note
        }
    )

def main():
    """Main entry point for T040."""
    setup_logging("INFO")
    config = get_config()
    pin_random_seed(config.get("RANDOM_SEED", 42))

    logger.info("Starting T040: Create Comparison Report")

    try:
        # Load inputs
        logger.info("Loading baseline metrics...")
        baseline_data = load_baseline_metrics()

        logger.info("Loading cleaned metrics...")
        cleaned_data = load_cleaned_metrics()

        logger.info("Loading sensitivity analysis...")
        sensitivity_data = load_sensitivity_analysis()

        # Aggregate
        logger.info("Aggregating metrics for comparison...")
        comparisons = aggregate_metrics_for_comparison(baseline_data, cleaned_data)

        if not comparisons:
            logger.error("No valid comparisons could be generated. Check data integrity.")
            return

        # Create Report Entity
        logger.info("Constructing ComparisonReport entity...")
        report = create_comparison_report(comparisons, sensitivity_data)

        # Serialize and Save
        output_path = "data/processed/comparison_report.json"
        logger.info(f"Writing comparison report to {output_path}...")

        # Pydantic model to dict conversion
        report_dict = report.model_dump()

        with open(output_path, 'w') as f:
            json.dump(report_dict, f, indent=2, default=str)

        logger.info(f"T040 completed successfully. Report saved to {output_path}")
        logger.info(f"Summary: {report.summary_statistics}")

    except FileNotFoundError as e:
        logger.error(f"Missing required data file: {e}")
        raise
    except Exception as e:
        logger.error(f"Unexpected error during T040 execution: {e}")
        raise

if __name__ == "__main__":
    main()