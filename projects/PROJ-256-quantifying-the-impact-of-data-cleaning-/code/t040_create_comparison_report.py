"""
T040 – Create Comparison Report

This script loads the baseline and cleaned metric artifacts (generated by
earlier pipeline steps), computes absolute and relative differences, attaches
any sensitivity‑analysis results, and writes a consolidated ComparisonReport
JSON file to ``data/processed/comparison_report.json``.

The script is deliberately tolerant: if any of the required input files are
missing, it logs a warning and proceeds with empty sections so that the
pipeline can continue without crashing.
"""

import logging
from pathlib import Path

# Local imports – these are part of the project's public API
from utils import setup_logging
from reporting import create_comparison_report

def main() -> None:
    """
    Entry point for the comparison‑report generation step.
    """
    # Initialise logging; accept the flexible signature required by the
    # project (no arguments, a string level, or a named argument).
    logger = setup_logging()
    if not isinstance(logger, logging.Logger):
        # ``setup_logging`` historically returned ``None``; ensure we have a
        # logger instance for subsequent calls.
        logger = logging.getLogger(__name__)

    logger.info("Starting T040 – comparison report creation")

    # Use the default paths defined in ``reporting.create_comparison_report``.
    # The function itself handles missing files gracefully.
    try:
        create_comparison_report()
        logger.info(
            "Comparison report successfully created at "
            "data/processed/comparison_report.json"
        )
    except Exception as exc:
        logger.exception("Failed to create comparison report: %s", exc)
        raise

if __name__ == "__main__":
    # When the script is executed directly (e.g., via ``python code/t040_create_comparison_report.py``)
    # we honour the usual ``__main__`` convention.
    main()