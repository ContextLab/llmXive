"""
Dataset Size Binning Sensitivity Analysis (T030)

This script reads the baseline metrics generated by the pipeline, groups
datasets by size bins (n < 50, 50 ≤ n ≤ 200, n > 200), computes the average
shift in key statistical metrics for each bin, and writes the results to
``data/processed/dataset_size_sensitivity.json``.

The script is deliberately tolerant of missing or malformed input:
- If ``baseline_metrics.json`` does not exist, an empty result file is
  written and a warning is logged.
- If a size bin contains no datasets, a ``CONSTRAINT_VIOLATION`` warning is
  emitted but processing continues.
"""

import json
import logging
from pathlib import Path
from typing import Any, Dict, List, Tuple

from utils import setup_logging

# --------------------------------------------------------------------------- #
# Logging configuration
# --------------------------------------------------------------------------- #
logger = setup_logging(log_level="INFO")


# --------------------------------------------------------------------------- #
# Helper functions
# --------------------------------------------------------------------------- #
def extract_dataset_info(baseline_path: Path) -> List[Dict[str, Any]]:
    """
    Load ``baseline_metrics.json`` and extract per‑dataset information required
    for the sensitivity analysis.

    The expected JSON structure is a dictionary where each key is a dataset
    identifier and the value is a mapping that includes at least the fields
    ``"n_rows"`` (int) and ``"metrics"`` (dict). If the file cannot be read,
    an empty list is returned.

    Parameters
    ----------
    baseline_path: Path
        Path to the baseline metrics JSON file.

    Returns
    -------
    List[Dict[str, Any]]
        List of dictionaries, each containing ``"name"``, ``"n_rows"``, and
        ``"metrics"`` keys.
    """
    if not baseline_path.is_file():
        logger.warning(
            f"Baseline metrics file not found at {baseline_path}. "
            "Proceeding with empty dataset list."
        )
        return []

    try:
        with baseline_path.open("r", encoding="utf-8") as f:
            data = json.load(f)
    except Exception as e:
        logger.error(f"Failed to read baseline metrics: {e}")
        return []

    dataset_info = []
    for ds_name, ds_content in data.items():
        n_rows = ds_content.get("n_rows")
        metrics = ds_content.get("metrics", {})
        if n_rows is None:
            logger.warning(f"Dataset {ds_name} missing 'n_rows' field – skipping.")
            continue
        dataset_info.append(
            {"name": ds_name, "n_rows": int(n_rows), "metrics": metrics}
        )
    return dataset_info


def bin_dataset_size(n_rows: int) -> str:
    """
    Assign a dataset size to one of the three bins.

    Returns
    -------
    str
        One of ``"small"``, ``"medium"``, ``"large"``.
    """
    if n_rows < 50:
        return "small"
    elif 50 <= n_rows <= 200:
        return "medium"
    else:
        return "large"


def analyze_size_bin(
    bin_label: str, datasets: List[Dict[str, Any]]
) -> Dict[str, Any]:
    """
    Compute aggregated metric shifts for a given size bin.

    For each dataset we compare the baseline metric values with the
    corresponding cleaned‑variant values (if available). The cleaned metrics are
    expected to be stored under ``data/processed/cleaned_metrics.json`` with the
    same dataset identifiers.

    Parameters
    ----------
    bin_label: str
        The bin name (``"small"``, ``"medium"``, ``"large"``).
    datasets: List[Dict[str, Any]]
        List of dataset info dictionaries belonging to this bin.

    Returns
    -------
    Dict[str, Any]
        Aggregated statistics for the bin, including the number of datasets
        and the mean absolute shift for each metric present.
    """
    # Load cleaned metrics once – they may be missing, in which case we treat
    # the shift as undefined.
    cleaned_path = Path("data/processed/cleaned_metrics.json")
    cleaned_data: Dict[str, Any] = {}
    if cleaned_path.is_file():
        try:
            with cleaned_path.open("r", encoding="utf-8") as f:
                cleaned_data = json.load(f)
        except Exception as e:
            logger.error(f"Failed to read cleaned metrics: {e}")

    # Accumulate shifts per metric
    metric_shifts: Dict[str, List[float]] = {}
    for ds in datasets:
        ds_name = ds["name"]
        baseline_metrics = ds.get("metrics", {})
        cleaned_metrics = cleaned_data.get(ds_name, {}).get("metrics", {})

        # If cleaned metrics are missing for this dataset, skip it.
        if not cleaned_metrics:
            logger.debug(
                f"Cleaned metrics missing for dataset {ds_name}; "
                "excluding from shift calculation."
            )
            continue

        for metric_name, baseline_val in baseline_metrics.items():
            cleaned_val = cleaned_metrics.get(metric_name)
            if cleaned_val is None:
                continue
            try:
                shift = abs(float(cleaned_val) - float(baseline_val))
            except (TypeError, ValueError):
                continue
            metric_shifts.setdefault(metric_name, []).append(shift)

    # Compute mean shift per metric
    aggregated: Dict[str, Any] = {"bin": bin_label, "n_datasets": len(datasets)}
    for metric, shifts in metric_shifts.items():
        if shifts:
            aggregated[f"{metric}_mean_abs_shift"] = round(
                sum(shifts) / len(shifts), 3
            )
        else:
            aggregated[f"{metric}_mean_abs_shift"] = None

    return aggregated


def run_sensitivity_analysis(baseline_path: Path) -> List[Dict[str, Any]]:
    """
    Orchestrate the full sensitivity analysis.

    Parameters
    ----------
    baseline_path: Path
        Path to ``baseline_metrics.json``.

    Returns
    -------
    List[Dict[str, Any]]
        List of aggregated results, one per size bin.
    """
    dataset_info = extract_dataset_info(baseline_path)

    # Bin datasets
    bins: Dict[str, List[Dict[str, Any]]] = {"small": [], "medium": [], "large": []}
    for ds in dataset_info:
        bin_label = bin_dataset_size(ds["n_rows"])
        bins[bin_label].append(ds)

    results: List[Dict[str, Any]] = []
    for bin_label, ds_list in bins.items():
        if not ds_list:
            logger.warning(
                f"CONSTRAINT_VIOLATION: No datasets fall into the '{bin_label}' bin."
            )
        results.append(analyze_size_bin(bin_label, ds_list))

    return results


def write_output(results: List[Dict[str, Any]], output_path: Path) -> None:
    """
    Write the analysis results to ``output_path`` as pretty‑printed JSON.

    Parameters
    ----------
    results: List[Dict[str, Any]]
        The aggregated sensitivity analysis data.
    output_path: Path
        Destination file.
    """
    try:
        output_path.parent.mkdir(parents=True, exist_ok=True)
        with output_path.open("w", encoding="utf-8") as f:
            json.dump(results, f, indent=2)
        logger.info(f"Sensitivity analysis written to {output_path}")
    except Exception as e:
        logger.error(f"Failed to write sensitivity analysis output: {e}")


# --------------------------------------------------------------------------- #
# Main entry point
# --------------------------------------------------------------------------- #
def main() -> None:
    """
    Execute the dataset size binning sensitivity analysis.

    The function looks for ``data/processed/baseline_metrics.json``,
    performs the analysis, and writes the result to
    ``data/processed/dataset_size_sensitivity.json``.
    """
    baseline_file = Path("data/processed/baseline_metrics.json")
    output_file = Path("data/processed/dataset_size_sensitivity.json")

    logger.info("Starting dataset size binning sensitivity analysis")
    results = run_sensitivity_analysis(baseline_file)
    write_output(results, output_file)


if __name__ == "__main__":
    main()