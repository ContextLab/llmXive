"""
T018: Save processed feature matrix to data/processed/descriptors.parquet.

This script loads the preprocessed data generated by preprocess_2d.py (which
handles descriptor computation, correlation filtering, and NaN handling) and
saves the final feature matrix and targets to a Parquet file for downstream
model training.
"""
import os
import sys
import logging
from pathlib import Path

# Add project root to path for imports
project_root = Path(__file__).resolve().parent.parent
sys.path.insert(0, str(project_root))

from utils.logging_config import get_logger, set_log_level
from data.preprocess_2d import preprocess_2d
import pandas as pd

# Configure logging
logger = get_logger(__name__)
set_log_level(logging.INFO)

def main():
    """
    Orchestrates the full preprocessing pipeline and saves the result.
    """
    logger.info("Starting T018: Saving processed descriptors to Parquet.")
    
    # Define paths relative to project root
    raw_data_path = project_root / "data" / "raw" / "qm9_smiles.csv"
    processed_dir = project_root / "data" / "processed"
    output_path = processed_dir / "descriptors.parquet"

    # Ensure output directory exists
    processed_dir.mkdir(parents=True, exist_ok=True)

    if not raw_data_path.exists():
        logger.error(f"Raw data file not found: {raw_data_path}")
        logger.error("Please ensure T013 (download_qm9.py) has been run successfully.")
        sys.exit(1)

    # Run the full preprocessing pipeline
    # This function (from T014-T017) handles:
    # 1. Loading SMILES
    # 2. Computing 2D descriptors
    # 3. Filtering high correlation features
    # 4. Handling NaNs (>5% drop, else median impute)
    logger.info(f"Processing raw data from: {raw_data_path}")
    
    df_processed = preprocess_2d(str(raw_data_path))

    if df_processed is None or df_processed.empty:
        logger.error("Preprocessing resulted in an empty dataframe.")
        sys.exit(1)

    # Verify expected columns exist (features + target)
    # The target is typically 'dipole_moment' or similar based on QM9
    target_col = 'dipole_moment'
    if target_col not in df_processed.columns:
        logger.warning(f"Target column '{target_col}' not found. Saving whatever is available.")
    else:
        logger.info(f"Target column '{target_col}' found. Shape: {df_processed.shape}")

    # Save to Parquet
    logger.info(f"Saving processed data to: {output_path}")
    df_processed.to_parquet(output_path, index=False)

    # Verify file was written
    if output_path.exists():
        file_size = output_path.stat().st_size
        logger.info(f"Successfully saved {output_path} ({file_size} bytes).")
        logger.info(f"Rows: {len(df_processed)}, Columns: {len(df_processed.columns)}")
    else:
        logger.error("Failed to write output file.")
        sys.exit(1)

    logger.info("T018 completed successfully.")

if __name__ == "__main__":
    main()