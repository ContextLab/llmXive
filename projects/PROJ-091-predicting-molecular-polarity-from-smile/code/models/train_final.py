"""
T026: Train final model on full training set and save to data/processed/model.pkl.

This script loads the split data indices (generated by split_data.py),
loads the full processed descriptors, selects the training set rows,
trains a LightGBM Regressor using the optimal hyperparameters found
during cross-validation (T024/T025), and saves the final model.
"""
import os
import sys
import logging
import pickle
import gc
from pathlib import Path

import pandas as pd
import numpy as np
import lightgbm as lgb
from sklearn.model_selection import train_test_split

# Project root adjustment
PROJECT_ROOT = Path(__file__).resolve().parent.parent.parent
if str(PROJECT_ROOT) not in sys.path:
    sys.path.insert(0, str(PROJECT_ROOT))

from utils.logging_config import get_logger
from utils.config import load_hyperparameters
from data.split_data import load_splits

# Ensure paths exist relative to project root
DATA_PROCESSED_DIR = PROJECT_ROOT / "data" / "processed"
DATA_RAW_DIR = PROJECT_ROOT / "data" / "raw"
LOGS_DIR = PROJECT_ROOT / "logs"

# Ensure directories exist
DATA_PROCESSED_DIR.mkdir(parents=True, exist_ok=True)
LOGS_DIR.mkdir(parents=True, exist_ok=True)

logger = get_logger(__name__, log_file=str(LOGS_DIR / "app.log"))

def load_training_data():
    """
    Load the full descriptor matrix and split indices to extract training data.
    Returns X_train (DataFrame), y_train (Series).
    """
    descriptors_path = DATA_PROCESSED_DIR / "descriptors.parquet"
    if not descriptors_path.exists():
        raise FileNotFoundError(
            f"Training data not found: {descriptors_path}. "
            "Please run data preprocessing first (T018)."
        )

    logger.info(f"Loading full descriptors from {descriptors_path}")
    df = pd.read_parquet(descriptors_path)

    # Load split indices
    splits_path = DATA_PROCESSED_DIR / "splits.npz"
    if not splits_path.exists():
        raise FileNotFoundError(
            f"Split indices not found: {splits_path}. "
            "Please run data splitting first (T022)."
        )

    logger.info(f"Loading split indices from {splits_path}")
    splits = load_splits(str(splits_path))

    train_indices = splits['train_indices']
    logger.info(f"Retrieved {len(train_indices)} training indices.")

    # Extract training data
    X_train = df.iloc[train_indices].drop(columns=['target'])
    y_train = df.iloc[train_indices]['target']

    logger.info(f"Training set shape: {X_train.shape}")
    return X_train, y_train

def train_final_model(X_train, y_train, config):
    """
    Train the final LightGBM model on the full training set.
    """
    logger.info("Starting final model training...")

    # Extract hyperparameters from config (T025 ensures these are saved)
    params = {
        'objective': 'regression',
        'metric': 'rmse',
        'boosting_type': 'gbdt',
        'num_leaves': config.get('num_leaves', 31),
        'learning_rate': config.get('learning_rate', 0.1),
        'feature_fraction': config.get('feature_fraction', 0.8),
        'bagging_fraction': config.get('bagging_fraction', 0.8),
        'bagging_freq': config.get('bagging_freq', 5),
        'verbose': -1,
        'seed': 42  # Hardcoded seed for reproducibility
    }

    # Create dataset
    train_data = lgb.Dataset(X_train, label=y_train)

    # Train model
    # Note: We use num_boost_round based on early stopping if available,
    # or a default high number if not. For T026, we assume a fixed round count
    # or use the best_iteration from CV if stored. Here we use a robust default.
    # Ideally, T024 would have saved the best_iteration. We'll use 1000 as a safe upper bound.
    num_boost_round = 1000

    logger.info(f"Training with params: {params}")
    
    model = lgb.train(
        params,
        train_data,
        num_boost_round=num_boost_round,
        # callbacks=[lgb.early_stopping(stopping_rounds=50)] # Only if val set provided
    )

    logger.info("Final model training completed.")
    return model

def save_model(model, output_path):
    """
    Save the trained model to a pickle file.
    """
    logger.info(f"Saving model to {output_path}")
    with open(output_path, 'wb') as f:
        pickle.dump(model, f)
    logger.info("Model saved successfully.")

def main():
    """
    Main entry point for T026.
    """
    logger.info("=== Starting Final Model Training (T026) ===")
    
    try:
        # Load config
        config_path = PROJECT_ROOT / "code" / "config.yaml"
        config = load_hyperparameters(str(config_path))
        logger.info(f"Loaded hyperparameters from {config_path}")

        # Load Data
        X_train, y_train = load_training_data()

        # Train Model
        model = train_final_model(X_train, y_train, config)

        # Save Model
        output_path = DATA_PROCESSED_DIR / "model.pkl"
        save_model(model, output_path)

        logger.info("=== T026 Completed Successfully ===")
        return 0

    except Exception as e:
        logger.error(f"Failed to train final model: {e}", exc_info=True)
        return 1

    finally:
        gc.collect()

if __name__ == "__main__":
    sys.exit(main())