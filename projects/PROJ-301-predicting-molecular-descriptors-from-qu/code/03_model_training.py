"""
Module: 03_model_training.py
Description:
    Trains Random Forest regressors on the 2‑D and 3‑D feature matrices generated by
    ``02_feature_extraction.py``. Implements k‑fold cross‑validation with a grid
    search, aggregates metrics, and monitors total runtime.
All debug statements have been removed and full type hints are provided.
"""
import argparse
import json
import logging
import os
import pickle
import time
from pathlib import Path
from typing import Any, Dict, Tuple

import numpy as np
import pandas as pd
from sklearn.ensemble import RandomForestRegressor
from sklearn.model_selection import GridSearchCV, KFold

logger = logging.getLogger(__name__)


def load_features_and_labels(
    features_2d_path: Path,
    features_3d_path: Path,
    labels_path: Path,
) -> Tuple[np.ndarray, np.ndarray, pd.DataFrame]:
    """
    Load feature matrices and the label dataframe.

    Returns:
        features_2d, features_3d, labels_df
    """
    logger.info("Loading 2D features from %s", features_2d_path)
    features_2d = np.load(features_2d_path)
    logger.info("Loading 3D features from %s", features_3d_path)
    features_3d = np.load(features_3d_path)
    logger.info("Loading labels from %s", labels_path)
    labels_df = pd.read_csv(labels_path)
    return features_2d, features_3d, labels_df


def train_model(
    X: np.ndarray,
    y: np.ndarray,
    param_grid: Dict[str, Any],
    cv_folds: int = 5,
    random_state: int = 42,
) -> Tuple[RandomForestRegressor, Dict[str, Any]]:
    """
    Perform a grid‑search CV to find the best Random Forest hyper‑parameters,
    then fit a final model on the full training data.

    Args:
        X: Feature matrix.
        y: Target vector (flattened for a single descriptor).
        param_grid: Hyper‑parameter grid for ``GridSearchCV``.
        cv_folds: Number of cross‑validation folds.
        random_state: Seed for reproducibility.

    Returns:
        Tuple of (trained model, best CV results dict).
    """
    logger.info("Starting GridSearchCV with %d folds.", cv_folds)
    rf = RandomForestRegressor(random_state=random_state, n_jobs=-1)
    cv = KFold(n_splits=cv_folds, shuffle=True, random_state=random_state)
    grid = GridSearchCV(
        estimator=rf,
        param_grid=param_grid,
        cv=cv,
        scoring="neg_mean_absolute_error",
        n_jobs=-1,
        verbose=0,
    )
    grid.fit(X, y)
    best_model = grid.best_estimator_
    logger.info(
        "Best params: %s, best MAE: %.4f",
        grid.best_params_,
        -grid.best_score_,
    )
    # Re‑fit on the whole dataset with best params
    best_model.fit(X, y)
    return best_model, {"best_params": grid.best_params_, "best_score": -grid.best_score_}


def train_model_2d(
    features_2d: np.ndarray,
    labels_df: pd.DataFrame,
    descriptor: str,
    output_path: Path,
) -> None:
    """
    Train a Random Forest for a single descriptor using 2‑D features.
    Saves the trained model to ``output_path``.
    """
    logger.info("Training 2D model for descriptor %s", descriptor)
    y = labels_df[descriptor].values
    param_grid = {
        "n_estimators": [100, 500, 1000],
        "max_depth": [10, 20, None],
    }
    model, metrics = train_model(features_2d, y, param_grid)
    with output_path.open("wb") as f:
        pickle.dump(model, f)
    logger.info("Saved 2D model to %s", output_path)
    # Store metrics for later aggregation
    metrics_path = output_path.with_suffix(".metrics.json")
    metrics_path.write_text(json.dumps(metrics, indent=2))


def train_model_3d(
    features_3d: np.ndarray,
    labels_df: pd.DataFrame,
    descriptor: str,
    output_path: Path,
) -> None:
    """
    Train a Random Forest for a single descriptor using 3‑D features.
    Saves the trained model to ``output_path``.
    """
    logger.info("Training 3D model for descriptor %s", descriptor)
    y = labels_df[descriptor].values
    param_grid = {
        "n_estimators": [100, 500, 1000],
        "max_depth": [10, 20, None],
    }
    model, metrics = train_model(features_3d, y, param_grid)
    with output_path.open("wb") as f:
        pickle.dump(model, f)
    logger.info("Saved 3D model to %s", output_path)
    metrics_path = output_path.with_suffix(".metrics.json")
    metrics_path.write_text(json.dumps(metrics, indent=2))


def aggregate_cv_metrics(
    model_dirs: Tuple[Path, Path],
    output_json: Path,
) -> None:
    """
    Collect per‑model CV metrics, compute mean/std MAE, and write a JSON summary.

    Args:
        model_dirs: Tuple containing the directories (or file prefixes) for the
            2D and 3D model metric JSON files.
        output_json: Destination path for the aggregated metrics.
    """
    all_metrics: Dict[str, Any] = {}
    for model_path in model_dirs:
        metrics_file = model_path.with_suffix(".metrics.json")
        if not metrics_file.is_file():
            logger.warning("Metrics file missing: %s", metrics_file)
            continue
        data = json.loads(metrics_file.read_text())
        descriptor = model_path.stem.split("_")[-1]  # assumes model_<desc>.pkl
        all_metrics[descriptor] = data

    # Simple aggregation: compute mean and std of the best_score across descriptors
    mae_values = [m["best_score"] for m in all_metrics.values() if "best_score" in m]
    if mae_values:
        mean_mae = sum(mae_values) / len(mae_values)
        std_mae = (sum((x - mean_mae) ** 2 for x in mae_values) / len(mae_values)) ** 0.5
    else:
        mean_mae = std_mae = float("nan")

    summary = {
        "per_descriptor": all_metrics,
        "mean_mae": mean_mae,
        "std_mae": std_mae,
    }
    output_json.write_text(json.dumps(summary, indent=2))
    logger.info("Aggregated CV metrics written to %s", output_json)


def monitor_runtime_and_train(
    train_callable,
    max_hours: float = 6.0,
    runtime_failure_path: Path = Path("artifacts/metrics/runtime_failure.json"),
) -> None:
    """
    Wrapper that measures runtime of a training callable. If the runtime exceeds
    ``max_hours`` a JSON file describing the failure is written and a
    ``RuntimeError`` is raised.
    """
    start = time.time()
    try:
        train_callable()
    finally:
        elapsed = time.time() - start
        hours = elapsed / 3600.0
        logger.info("Training completed in %.2f hours.", hours)
        if hours > max_hours:
            logger.error(
                "Training exceeded the maximum allowed runtime of %.2f hours.", max_hours
            )
            failure_info = {
                "elapsed_hours": hours,
                "max_allowed_hours": max_hours,
                "status": "runtime_exceeded",
            }
            runtime_failure_path.parent.mkdir(parents=True, exist_ok=True)
            runtime_failure_path.write_text(json.dumps(failure_info, indent=2))
            raise RuntimeError("Training runtime exceeded limit.")


def main() -> None:
    """
    Orchestrates model training for all three QM9 descriptors (dipole, HOMO,
    LUMO) using both 2‑D and 3‑D feature sets.
    """
    logging.basicConfig(
        level=logging.INFO,
        format="%(asctime)s %(levelname)s %(name)s – %(message)s",
    )

    processed_dir = Path("data/processed")
    artifacts_dir = Path("artifacts/models")
    artifacts_dir.mkdir(parents=True, exist_ok=True)

    features_2d_path = processed_dir / "features_2d.npy"
    features_3d_path = processed_dir / "features_3d.npy"
    labels_path = processed_dir / "labels.csv"

    features_2d, features_3d, labels_df = load_features_and_labels(
        features_2d_path, features_3d_path, labels_path
    )

    descriptors = ["dipole_moment", "homo", "lumo"]

    def train_all() -> None:
        for desc in descriptors:
            model_2d_path = artifacts_dir / f"model_2d_{desc}.pkl"
            train_model_2d(features_2d, labels_df, desc, model_2d_path)

            model_3d_path = artifacts_dir / f"model_3d_{desc}.pkl"
            train_model_3d(features_3d, labels_df, desc, model_3d_path)

    monitor_runtime_and_train(train_all)

    # Aggregate CV metrics
    metric_output = Path("artifacts/metrics/cv_metrics.json")
    model_paths = tuple(
        Path(f"artifacts/models/model_2d_{d}.pkl") for d in descriptors
    ) + tuple(
        Path(f"artifacts/models/model_3d_{d}.pkl") for d in descriptors
    )
    aggregate_cv_metrics(model_paths, metric_output)


if __name__ == "__main__":
    main()
