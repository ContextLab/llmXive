"""
Initialize project data directory structure and the checksums log file.

This script ensures that the required directory hierarchy for the project's
data artifacts exists (raw, cleaned, derived, results, figures) and creates
or initializes the `data/checksums.txt` logging mechanism if it does not
already exist.

It is designed to be run as part of the project setup (Phase 1/2) to
guarantee a consistent file system state before data download or processing
tasks begin.
"""
import os
import sys
from pathlib import Path

# Determine project root based on the known structure:
# code/scripts/init_data_dirs.py -> project root is two levels up
PROJECT_ROOT = Path(__file__).resolve().parent.parent.parent
DATA_DIR = PROJECT_ROOT / "data"

# Subdirectories required by the pipeline
SUBDIRS = [
    "raw",          # Raw downloaded data (e.g., HCP S3 dumps)
    "cleaned",      # Preprocessed and filtered subject-level data
    "derived",      # Computed features (entropy, noise variance)
    "results",      # Statistical maps, significant clusters
    "figures",      # Plots and visualizations
]

CHECKSUM_FILE = DATA_DIR / "checksums.txt"

HEADER = """# Project Data Checksums Log
# Format: <sha256_hash>  <relative_path>
# Generated by: init_data_dirs.py (T004)
# This file tracks integrity of all raw and derived data artifacts.

# No data files present yet.
# Placeholder for future checksums.
"""

def main():
    print(f"Initializing data directory structure at: {DATA_DIR}")
    
    # Ensure the root data directory exists
    if not DATA_DIR.exists():
        DATA_DIR.mkdir(parents=True)
        print(f"  Created root directory: {DATA_DIR}")
    else:
        print(f"  Root directory exists: {DATA_DIR}")
    
    # Create subdirectories
    created_count = 0
    for subdir_name in SUBDIRS:
        subdir_path = DATA_DIR / subdir_name
        if not subdir_path.exists():
            subdir_path.mkdir(parents=True)
            print(f"  Created subdirectory: {subdir_path}")
            created_count += 1
        else:
            print(f"  Subdirectory exists: {subdir_path}")
    
    # Initialize or verify checksums.txt
    if not CHECKSUM_FILE.exists():
        CHECKSUM_FILE.write_text(HEADER)
        print(f"  Initialized checksums log: {CHECKSUM_FILE}")
    else:
        print(f"  Checksums log already exists: {CHECKSUM_FILE}")
    
    print(f"Data directory initialization complete. Created {created_count} new subdirectories.")
    return 0

if __name__ == "__main__":
    sys.exit(main())