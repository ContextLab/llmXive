"""
Timing Reporter for SC-004 Verification.

This module generates the formal execution time measurement and pass/fail status
for SC-004 (N=20 game instances completes in <6 hours).

It reads the benchmark log generated by T041 (agent_loop.py execution) and
produces a structured JSON report in results/timing_report.json.
"""
import os
import json
import sys
from datetime import datetime
from typing import Dict, Any, Optional

# Constants
PROJECT_ROOT = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
BENCHMARK_LOG_PATH = os.path.join(PROJECT_ROOT, "results", "benchmark_log.json")
TIMING_REPORT_PATH = os.path.join(PROJECT_ROOT, "results", "timing_report.json")
MAX_EXECUTION_SECONDS = 6 * 3600  # 6 hours in seconds
TARGET_N = 20

def load_benchmark_log(path: str) -> Optional[Dict[str, Any]]:
    """Load the benchmark log generated by the agent loop execution."""
    if not os.path.exists(path):
        raise FileNotFoundError(f"Benchmark log not found at {path}. "
                                "Ensure T041 (agent_loop.py execution) has completed successfully.")
    
    with open(path, 'r', encoding='utf-8') as f:
        return json.load(f)

def calculate_pass_fail(total_seconds: float, n_completed: int) -> Dict[str, Any]:
    """
    Determine pass/fail status based on SC-004 criteria.
    
    Criteria:
    - N=20 game instances must complete
    - Total execution time must be < 6 hours (21600 seconds)
    """
    passed = True
    reasons = []

    if n_completed != TARGET_N:
        passed = False
        reasons.append(f"Expected {TARGET_N} instances, but only {n_completed} completed.")
    
    if total_seconds >= MAX_EXECUTION_SECONDS:
        passed = False
        hours = total_seconds / 3600
        reasons.append(f"Execution time ({hours:.2f}h) exceeded limit of 6 hours.")

    return {
        "passed": passed,
        "reasons": reasons,
        "threshold_seconds": MAX_EXECUTION_SECONDS,
        "actual_seconds": total_seconds,
        "instances_completed": n_completed,
        "target_instances": TARGET_N
    }

def generate_timing_report(benchmark_data: Dict[str, Any]) -> Dict[str, Any]:
    """Generate the formal timing report structure."""
    total_time = benchmark_data.get("total_execution_time_seconds", 0)
    n_completed = benchmark_data.get("instances_completed", 0)
    
    # Extract per-run details if available
    run_details = []
    if "runs" in benchmark_data:
        for i, run in enumerate(benchmark_data["runs"]):
            run_details.append({
                "run_id": run.get("run_id", i),
                "seed": run.get("seed"),
                "duration_seconds": run.get("duration_seconds", 0),
                "status": run.get("status", "unknown")
            })

    evaluation = calculate_pass_fail(total_time, n_completed)

    report = {
        "report_id": "SC-004-TIMING-REPORT",
        "generated_at": datetime.utcnow().isoformat() + "Z",
        "spec_reference": "SC-004",
        "description": "Verification of N=20 game instances completing in <6 hours",
        "execution_metrics": {
            "total_time_seconds": total_time,
            "total_time_formatted": f"{total_time/3600:.2f} hours",
            "instances_completed": n_completed,
            "instances_target": TARGET_N,
            "average_time_per_instance": total_time / n_completed if n_completed > 0 else 0
        },
        "evaluation": evaluation,
        "run_details": run_details,
        "source_file": BENCHMARK_LOG_PATH
    }

    return report

def save_report(report: Dict[str, Any], path: str) -> None:
    """Save the timing report to a JSON file."""
    os.makedirs(os.path.dirname(path), exist_ok=True)
    with open(path, 'w', encoding='utf-8') as f:
        json.dump(report, f, indent=2)
    print(f"Timing report saved to: {path}")

def main() -> int:
    """Main entry point for generating the timing report."""
    try:
        print(f"Loading benchmark log from: {BENCHMARK_LOG_PATH}")
        benchmark_data = load_benchmark_log(BENCHMARK_LOG_PATH)
        
        print("Generating timing report...")
        report = generate_timing_report(benchmark_data)
        
        print(f"Pass/Fail Status: {'PASS' if report['evaluation']['passed'] else 'FAIL'}")
        if not report['evaluation']['passed']:
            print("Reasons:")
            for reason in report['evaluation']['reasons']:
                print(f"  - {reason}")
        
        save_report(report, TIMING_REPORT_PATH)
        return 0

    except FileNotFoundError as e:
        print(f"ERROR: {e}", file=sys.stderr)
        print("Ensure T041 (agent_loop.py execution) has completed and generated results/benchmark_log.json", file=sys.stderr)
        return 1
    except json.JSONDecodeError as e:
        print(f"ERROR: Invalid JSON in benchmark log: {e}", file=sys.stderr)
        return 1
    except Exception as e:
        print(f"ERROR: Unexpected error generating timing report: {e}", file=sys.stderr)
        import traceback
        traceback.print_exc()
        return 1

if __name__ == "__main__":
    sys.exit(main())
