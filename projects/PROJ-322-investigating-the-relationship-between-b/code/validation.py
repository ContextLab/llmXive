"""
External Validation Module for Brain Network Reconfiguration Study.

This module implements the validation correlation logic (FR-007).
It is designed to be run ONLY if T024a successfully identified a real independent metric.

If no metric was found, this script should not be executed; instead,
T024a's logic should have generated data/results/gaps.json with validation_gap: true.
"""
import os
import json
import logging
import pandas as pd
import numpy as np
from pathlib import Path
from typing import Dict, Any, Optional, Tuple, List
from scipy import stats

# Import shared configuration
from config import is_synthetic, get_config
from logging_config import get_logger

# Configure logger
logger = get_logger(__name__)

# Constants
RESULTS_DIR = Path("data/results")
GAP_REPORT_PATH = RESULTS_DIR / "gaps.json"
VALIDATION_OUTPUT_PATH = RESULTS_DIR / "external_validation.json"
MANIFEST_PATH = RESULTS_DIR / "manifest.csv"

# Target metric identifiers found by T024a (example set based on typical mTBI datasets)
# If T024a found a specific column name, it would be passed here or configured.
# For this implementation, we check for common names in the manifest.
TARGET_METRIC_NAMES = [
    'ReturnToWork', 'RTW', 'EmploymentStatus', 
    'GOS-E', 'GOSE', 'GlasgowOutcomeScale',
    'PCOS', 'PostConcussionSymptomScale'
]

def load_manifest() -> Optional[pd.DataFrame]:
    """Load the data manifest generated by data_ingestion.py."""
    if not MANIFEST_PATH.exists():
        logger.error(f"Manifest not found at {MANIFEST_PATH}. Run data_ingestion.py first.")
        return None
    
    try:
        df = pd.read_csv(MANIFEST_PATH)
        logger.info(f"Loaded manifest with {len(df)} rows.")
        return df
    except Exception as e:
        logger.error(f"Failed to load manifest: {e}")
        return None

def find_target_column(df: pd.DataFrame) -> Optional[str]:
    """
    Identify the column in the manifest that corresponds to the external validation metric.
    Returns the column name if found, None otherwise.
    """
    columns = [c.lower() for c in df.columns]
    original_columns = df.columns.tolist()
    
    for target in TARGET_METRIC_NAMES:
        if target.lower() in columns:
            idx = columns.index(target.lower())
            logger.info(f"Found target metric column: {original_columns[idx]}")
            return original_columns[idx]
    
    logger.warning("No external validation metric column found in manifest.")
    return None

def calculate_correlation_with_metric(
    df: pd.DataFrame, 
    metric_col: str, 
    graph_metric_col: str = 'global_efficiency'
) -> Dict[str, Any]:
    """
    Calculate the correlation between the external metric and a graph metric.
    Handles missing values and returns detailed statistics.
    """
    # Drop rows where either variable is NaN
    valid_data = df[[metric_col, graph_metric_col]].dropna()
    
    if len(valid_data) < 3:
        logger.warning(f"Insufficient data points for correlation (n={len(valid_data)}).")
        return {
            "correlation": None,
            "p_value": None,
            "n": len(valid_data),
            "status": "insufficient_data",
            "message": "Not enough valid pairs to calculate correlation."
        }

    x = valid_data[graph_metric_col]
    y = valid_data[metric_col]

    try:
        corr, p_val = stats.pearsonr(x, y)
        
        # Also calculate Spearman for robustness
        spearman_corr, spearman_p = stats.spearmanr(x, y)

        return {
            "correlation": float(corr),
            "p_value": float(p_val),
            "spearman_correlation": float(spearman_corr),
            "spearman_p_value": float(spearman_p),
            "n": len(valid_data),
            "status": "success",
            "metric_used": metric_col,
            "graph_metric_used": graph_metric_col
        }
    except Exception as e:
        logger.error(f"Correlation calculation failed: {e}")
        return {
            "correlation": None,
            "p_value": None,
            "n": len(valid_data),
            "status": "error",
            "message": str(e)
        }

def run_validation_pipeline() -> Dict[str, Any]:
    """
    Main pipeline to run external validation.
    1. Load manifest.
    2. Find target metric column.
    3. Calculate correlations.
    4. Save results.
    """
    logger.info("Starting external validation pipeline...")
    
    # Load data
    manifest = load_manifest()
    if manifest is None:
        return {"status": "failed", "message": "Could not load manifest"}

    # Find metric
    target_col = find_target_column(manifest)
    if target_col is None:
        # This path should ideally be blocked by T024a logic, but we handle it gracefully
        logger.error("No validation metric found. Cannot proceed with validation.")
        return {
            "status": "failed", 
            "message": "No external validation metric found in manifest.",
            "validation_gap": True
        }

    # Determine which graph metric to use (default to global_efficiency if available)
    graph_metric_col = 'global_efficiency'
    if graph_metric_col not in manifest.columns:
        # Fallback: try to find any 'efficiency' column
        eff_cols = [c for c in manifest.columns if 'efficiency' in c.lower()]
        if eff_cols:
            graph_metric_col = eff_cols[0]
            logger.info(f"Using fallback graph metric: {graph_metric_col}")
        else:
            logger.error("No graph metric columns found in manifest.")
            return {"status": "failed", "message": "No graph metric columns found"}

    # Calculate correlation
    results = calculate_correlation_with_metric(manifest, target_col, graph_metric_col)
    
    # Add metadata
    results["config"] = {
        "is_synthetic": is_synthetic(),
        "dataset_id": os.getenv("OPENNEURO_DATASET_ID", "unknown"),
        "timestamp": pd.Timestamp.now().isoformat()
    }

    # Save results
    RESULTS_DIR.mkdir(parents=True, exist_ok=True)
    with open(VALIDATION_OUTPUT_PATH, 'w') as f:
        json.dump(results, f, indent=2)
    
    logger.info(f"Validation results saved to {VALIDATION_OUTPUT_PATH}")
    return results

def main():
    """Entry point for the validation script."""
    # Initialize logging
    from logging_config import initialize_logging
    initialize_logging()

    try:
        result = run_validation_pipeline()
        print(json.dumps(result, indent=2))
        
        if result.get("status") == "failed":
            logger.error("Validation pipeline failed.")
            return 1
        return 0
    except Exception as e:
        logger.exception(f"Unexpected error in validation pipeline: {e}")
        return 1

if __name__ == "__main__":
    exit(main())
