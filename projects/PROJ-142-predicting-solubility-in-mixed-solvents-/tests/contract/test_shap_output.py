"""
Contract tests for SHAP output format validation.

This module validates the format and structure of SHAP analysis outputs
generated by the model interpretability pipeline (User Story 3).

It ensures that:
1. The SHAP summary plot exists and is a valid image file.
2. The SHAP ranking JSON file exists and contains the required schema.
3. The JSON structure matches the expected contract for downstream tasks.
"""

import json
import os
from pathlib import Path

# Constants for expected paths relative to project root
PROJECT_ROOT = Path(__file__).resolve().parent.parent.parent
ARTIFACTS_DIR = PROJECT_ROOT / "data" / "artifacts"
SHAP_PLOT_PATH = ARTIFACTS_DIR / "shap_analysis.png"
SHAP_RANKING_PATH = ARTIFACTS_DIR / "shap_ranking.json"

# Expected schema keys for shap_ranking.json
REQUIRED_TOP_LEVEL_KEYS = {
    "model_metrics",
    "feature_importances",
    "interaction_terms",
    "sensitivity_analysis",
    "stability_analysis"
}

REQUIRED_METRICS_KEYS = {
    "rmse",
    "mae",
    "r_squared",
    "p_value_t_test"
}

REQUIRED_INTERACTION_KEYS = {
    "term_name",
    "contribution_score",
    "rank"
}

REQUIRED_SENSITIVITY_KEYS = {
    "threshold_low",
    "threshold_medium",
    "threshold_high",
    "jaccard_similarity"
}

REQUIRED_STABILITY_KEYS = {
    "spearman_correlation",
    "stability_status"
}

def test_shap_format_valid():
    """
    Contract test: Validates the format and content of SHAP output artifacts.
    
    This function performs the following checks:
    1. Verifies that the SHAP summary plot (PNG) exists.
    2. Verifies that the SHAP ranking JSON file exists.
    3. Validates the JSON structure against the required schema.
    4. Checks that all required fields are present and non-empty.
    
    Raises:
        AssertionError: If any validation check fails.
        FileNotFoundError: If expected artifact files are missing.
    """
    
    # Check 1: Verify SHAP summary plot exists
    assert SHAP_PLOT_PATH.exists(), (
        f"SHAP summary plot not found at {SHAP_PLOT_PATH}. "
        "Ensure code/04_evaluation.py has generated the plot."
    )
    
    # Verify it's a valid file (non-empty)
    assert SHAP_PLOT_PATH.stat().st_size > 0, (
        f"SHAP summary plot at {SHAP_PLOT_PATH} is empty."
    )
    
    # Check 2: Verify SHAP ranking JSON exists
    assert SHAP_RANKING_PATH.exists(), (
        f"SHAP ranking JSON not found at {SHAP_RANKING_PATH}. "
        "Ensure code/04_evaluation.py has generated the JSON file."
    )
    
    # Load and parse JSON
    try:
        with open(SHAP_RANKING_PATH, 'r', encoding='utf-8') as f:
            shap_data = json.load(f)
    except json.JSONDecodeError as e:
        raise AssertionError(
            f"Invalid JSON format in {SHAP_RANKING_PATH}: {e}"
        )
    
    # Check 3: Validate top-level keys
    missing_top_keys = REQUIRED_TOP_LEVEL_KEYS - set(shap_data.keys())
    assert not missing_top_keys, (
        f"Missing required top-level keys in SHAP ranking JSON: {missing_top_keys}"
    )
    
    # Check 4: Validate model_metrics structure
    metrics = shap_data.get("model_metrics", {})
    missing_metrics = REQUIRED_METRICS_KEYS - set(metrics.keys())
    assert not missing_metrics, (
        f"Missing required keys in 'model_metrics': {missing_metrics}"
    )
    
    # Validate numeric types for metrics
    for key in ["rmse", "mae", "r_squared", "p_value_t_test"]:
        value = metrics.get(key)
        assert isinstance(value, (int, float)), (
            f"Metric '{key}' must be numeric, got {type(value)}"
        )
    
    # Check 5: Validate feature_importances (list of dicts)
    importances = shap_data.get("feature_importances", [])
    assert isinstance(importances, list), (
        "'feature_importances' must be a list"
    )
    assert len(importances) > 0, (
        "'feature_importances' list cannot be empty"
    )
    
    # Check 6: Validate interaction_terms structure
    interactions = shap_data.get("interaction_terms", [])
    assert isinstance(interactions, list), (
        "'interaction_terms' must be a list"
    )
    
    if len(interactions) > 0:
        first_interaction = interactions[0]
        missing_interaction_keys = REQUIRED_INTERACTION_KEYS - set(first_interaction.keys())
        assert not missing_interaction_keys, (
            f"Missing required keys in first interaction term: {missing_interaction_keys}"
        )
    
    # Check 7: Validate sensitivity_analysis structure
    sensitivity = shap_data.get("sensitivity_analysis", {})
    missing_sensitivity_keys = REQUIRED_SENSITIVITY_KEYS - set(sensitivity.keys())
    assert not missing_sensitivity_keys, (
        f"Missing required keys in 'sensitivity_analysis': {missing_sensitivity_keys}"
    )
    
    # Check 8: Validate stability_analysis structure
    stability = shap_data.get("stability_analysis", {})
    missing_stability_keys = REQUIRED_STABILITY_KEYS - set(stability.keys())
    assert not missing_stability_keys, (
        f"Missing required keys in 'stability_analysis': {missing_stability_keys}"
    )
    
    # All checks passed
    assert True