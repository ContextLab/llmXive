"""
Contract test for model metrics JSON schema (T018).

This test validates the structure and required fields of the model metrics
output file generated by the training pipeline (T023).
"""
import json
import os
import pytest
from pathlib import Path

# Expected schema structure based on US2 requirements
EXPECTED_KEYS = {
    "r2_score",
    "rmse",
    "disclaimer",
    "model_type",
    "cv_folds",
    "timestamp"
}

DISCLAIMER_SUBSTRINGS = [
    "associational",
    "observational"
]

MODEL_METRICS_PATH = Path("data/results/model_metrics.json")

@pytest.fixture
def metrics_data():
    """Load the model metrics JSON file."""
    if not MODEL_METRICS_PATH.exists():
        pytest.skip(f"Model metrics file not found at {MODEL_METRICS_PATH}. "
                    "Run training pipeline (T020-T024) first.")
    
    with open(MODEL_METRICS_PATH, "r") as f:
        return json.load(f)

def test_metrics_file_exists():
    """Verify the model metrics file exists."""
    assert MODEL_METRICS_PATH.exists(), (
        f"Model metrics file not found at {MODEL_METRICS_PATH}. "
        "Ensure training pipeline has been executed."
    )

def test_metrics_schema_structure(metrics_data):
    """Validate that the metrics JSON has the expected top-level keys."""
    assert isinstance(metrics_data, dict), "Metrics data must be a dictionary."
    
    missing_keys = EXPECTED_KEYS - set(metrics_data.keys())
    assert not missing_keys, (
        f"Missing required keys in model metrics: {missing_keys}. "
        f"Expected: {EXPECTED_KEYS}"
    )

def test_metrics_numeric_fields(metrics_data):
    """Validate that numeric fields are actual numbers."""
    assert isinstance(metrics_data["r2_score"], (int, float)), (
        f"r2_score must be numeric, got {type(metrics_data['r2_score'])}"
    )
    assert isinstance(metrics_data["rmse"], (int, float)), (
        f"rmse must be numeric, got {type(metrics_data['rmse'])}"
    )
    assert isinstance(metrics_data["cv_folds"], int), (
        f"cv_folds must be an integer, got {type(metrics_data['cv_folds'])}"
    )

def test_metrics_disclaimer_present(metrics_data):
    """Validate that the associational disclaimer is present and contains required terms."""
    disclaimer = metrics_data.get("disclaimer", "")
    assert isinstance(disclaimer, str), "Disclaimer must be a string."
    assert len(disclaimer) > 0, "Disclaimer cannot be empty."
    
    # Check for required keywords
    disclaimer_lower = disclaimer.lower()
    found_keywords = [kw for kw in DISCLAIMER_SUBSTRINGS if kw in disclaimer_lower]
    assert found_keywords, (
        f"Disclaimer missing required keywords. Expected one of: {DISCLAIMER_SUBSTRINGS}. "
        f"Got: {disclaimer}"
    )

def test_metrics_model_type(metrics_data):
    """Validate that model_type is specified."""
    model_type = metrics_data.get("model_type", "")
    assert isinstance(model_type, str), "model_type must be a string."
    assert len(model_type) > 0, "model_type cannot be empty."
    assert "random" in model_type.lower() or "forest" in model_type.lower(), (
        f"Expected Random Forest model type, got: {model_type}"
    )

def test_metrics_timestamp(metrics_data):
    """Validate that a timestamp is present."""
    timestamp = metrics_data.get("timestamp", "")
    assert isinstance(timestamp, str), "timestamp must be a string."
    assert len(timestamp) > 0, "timestamp cannot be empty."

def test_metrics_r2_range(metrics_data):
    """Validate that R2 score is within plausible range (-inf, 1]."""
    r2 = metrics_data["r2_score"]
    # R2 can be negative for bad models, but should not exceed 1
    assert r2 <= 1.0, f"R2 score cannot exceed 1.0, got {r2}"

def test_metrics_rmse_positive(metrics_data):
    """Validate that RMSE is non-negative."""
    rmse = metrics_data["rmse"]
    assert rmse >= 0, f"RMSE cannot be negative, got {rmse}"