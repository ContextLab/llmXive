    def test_filter_replicates_contract(self):
        """
        Test that replicate filtering correctly excludes conditions with <3 replicates.
        
        Contract: After filtering, every condition_id must have >=3 samples.
        """
        # Load data
        data = load_stage1_data(self.stage1_path)
        
        # Create a scenario with <3 replicates for some conditions
        # We'll manipulate the data to have some conditions with only 1 or 2 samples
        test_data = data.copy()
        
        # Keep only first 2 samples for condition 'cond_A'
        cond_a_samples = test_data[test_data['condition_id'] == 'cond_A'].index[:2]
        test_data = test_data.drop([i for i in test_data.index if i not in cond_a_samples or test_data.loc[i, 'condition_id'] != 'cond_A'])
        
        # Actually, let's create a cleaner test by filtering the original data
        # to ensure we have conditions with <3 replicates
        test_data = data.copy()
        
        # Simulate missing data: remove some samples to create <3 replicates
        # Group by condition_id and count
        condition_counts = test_data.groupby('condition_id').size()
        
        # Find conditions with <3 samples
        low_replicate_conditions = condition_counts[condition_counts < 3].index.tolist()
        
        # If none exist, artificially create one by dropping samples
        if not low_replicate_conditions:
            # Drop samples from 'cond_A' until we have <3
            cond_a_indices = test_data[test_data['condition_id'] == 'cond_A'].index.tolist()
            if len(cond_a_indices) > 3:
                test_data = test_data.drop(cond_a_indices[3:])  # Keep only 3
                # Now drop one more to have <3
                test_data = test_data.drop(cond_a_indices[3:4])
        
        # Apply filter
        filtered_data = filter_replicates(test_data, min_replicates=3)
        
        # Verify contract: all remaining conditions have >=3 samples
        final_counts = filtered_data.groupby('condition_id').size()
        assert all(final_counts >= 3), "Filter failed: some conditions have <3 replicates"
        
        # Verify that conditions with <3 were actually removed
        removed_conditions = set(condition_counts[condition_counts < 3].index) - set(final_counts.index)
        assert len(removed_conditions) > 0, "Expected some conditions to be removed"

    def test_filter_environmental_metadata_contract(self):
        """
        Test that environmental metadata filtering correctly excludes samples with missing critical fields.
        
        Contract: After filtering, all samples must have non-null 'temperature' and 'light_intensity'.
        """
        data = load_stage1_data(self.stage1_path)
        
        # Create a copy with missing values
        test_data = data.copy()
        
        # Introduce missing values in critical fields
        missing_indices = test_data.index[:5].tolist()
        test_data.loc[missing_indices[0], 'temperature'] = np.nan
        test_data.loc[missing_indices[1], 'light_intensity'] = np.nan
        test_data.loc[missing_indices[2], 'temperature'] = np.nan
        test_data.loc[missing_indices[3], 'light_intensity'] = np.nan
        test_data.loc[missing_indices[4], 'temperature'] = np.nan
        
        # Apply filter
        filtered_data = filter_environmental_metadata(test_data)
        
        # Verify contract: no null values in critical fields
        assert not filtered_data['temperature'].isnull().any(), "Temperature has null values after filtering"
        assert not filtered_data['light_intensity'].isnull().any(), "Light intensity has null values after filtering"
        
        # Verify that samples with missing data were removed
        assert len(filtered_data) < len(test_data), "Expected some samples to be removed"

