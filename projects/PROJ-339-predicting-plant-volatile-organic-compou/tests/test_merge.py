    def _create_stage1_data(self):
        """Create a mock stage 1 data file with normalized values."""
        # Load raw data
        raw_df = pd.read_csv(self.raw_data_path)
        
        # Simulate TPM normalization (just scale values for testing)
        transcript_cols = [col for col in raw_df.columns if col.startswith('gene_')]
        for col in transcript_cols:
            if col in raw_df.columns:
                raw_df[col] = raw_df[col] * 0.5  # Simulate normalization
        
        # Ensure environmental columns exist
        required_env_cols = ['temperature', 'light_intensity', 'humidity', 'stress_type']
        for col in required_env_cols:
            if col not in raw_df.columns:
                # Create synthetic environmental data
                if col == 'temperature':
                    raw_df[col] = np.random.normal(25.0, 2.0, len(raw_df))
                elif col == 'light_intensity':
                    raw_df[col] = np.random.normal(500.0, 100.0, len(raw_df))
                elif col == 'humidity':
                    raw_df[col] = np.random.normal(60.0, 10.0, len(raw_df))
                elif col == 'stress_type':
                    raw_df[col] = np.random.choice(['drought', 'heat', 'cold', 'control'], len(raw_df))
        
        # Ensure VOC target columns exist
        voc_cols = ['voc_terpene_total', 'voc_benzenoid_total']
        for col in voc_cols:
            if col not in raw_df.columns:
                raw_df[col] = np.random.exponential(10.0, len(raw_df))
        
        # Add sample_id if not present
        if 'sample_id' not in raw_df.columns:
            raw_df['sample_id'] = [f"sample_{i:03d}" for i in range(len(raw_df))]
        
        # Add condition_id for replicate grouping
        if 'condition_id' not in raw_df.columns:
            # Create condition IDs with replicates
            conditions = ['cond_A', 'cond_B', 'cond_C', 'cond_D', 'cond_E']
            raw_df['condition_id'] = [conditions[i % len(conditions)] for i in range(len(raw_df))]
        
        raw_df.to_csv(self.stage1_path, index=False)

