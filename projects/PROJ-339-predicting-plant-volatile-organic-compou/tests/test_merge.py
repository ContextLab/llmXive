    def test_data_integrity_preserved(self):
        """
        Test that data values are preserved correctly through the merge process.
        
        Contract: Non-null values in input should match values in output for retained samples.
        """
        data = load_stage1_data(self.stage1_path)
        original_values = data[['sample_id', 'temperature', 'light_intensity']].copy()
        
        filtered = filter_replicates(data, min_replicates=3)
        final = filter_environmental_metadata(filtered)
        
        # Check that values match for retained samples
        for sample_id in final['sample_id']:
            original_row = original_values[original_values['sample_id'] == sample_id]
            if len(original_row) > 0:
                assert len(original_row) == 1, f"Multiple rows for sample_id {sample_id}"
                assert final[final['sample_id'] == sample_id]['temperature'].values[0] == original_row['temperature'].values[0]
                assert final[final['sample_id'] == sample_id]['light_intensity'].values[0] == original_row['light_intensity'].values[0]


