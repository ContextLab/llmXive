"""
Task T012a: Validate the downloaded corpus count against SC-001 (>=50 real-world meta-analyses).

Logic:
1. Scan `data/raw/` for downloaded meta-analysis files (generated by T012).
2. Count the number of valid meta-analyses.
3. If count < 50:
   - Log CRITICAL warning: "Primary data requirement (FR-001) not met. Switching to Simulation Mode."
   - Set mode = "simulation".
4. If count >= 50:
   - Log success.
   - Set mode = "real".
5. Write `data/output/success_rate_report.json` with `mode` and `count`.
"""

import os
import json
import logging
from pathlib import Path
from typing import Dict, Any

# Configure logging to match project standards
logging.basicConfig(
    level=logging.INFO,
    format='%(asctime)s - %(levelname)s - %(message)s'
)
logger = logging.getLogger(__name__)

# Constants
RAW_DATA_DIR = Path("data/raw")
OUTPUT_DIR = Path("data/output")
REPORT_PATH = OUTPUT_DIR / "success_rate_report.json"
MIN_CORPUS_SIZE = 50  # SC-001 requirement

def count_meta_analyses(raw_dir: Path) -> int:
    """
    Count the number of valid meta-analysis files in the raw data directory.
    Assumes T012 has populated this directory with files (e.g., .json, .csv, .parquet).
    """
    if not raw_dir.exists():
        logger.warning(f"Raw data directory does not exist: {raw_dir}")
        return 0

    count = 0
    # Look for common data file extensions generated by T012
    extensions = ['.json', '.csv', '.parquet', '.xlsx']
    
    for ext in extensions:
        files = list(raw_dir.glob(f"*{ext}"))
        count += len(files)
    
    # If no files found, check for directories that might represent individual meta-analyses
    if count == 0:
        # Count non-hidden directories that might represent meta-analysis IDs
        dirs = [d for d in raw_dir.iterdir() if d.is_dir() and not d.name.startswith('.')]
        count = len(dirs)

    return count

def write_report(mode: str, count: int) -> None:
    """
    Write the validation report to `data/output/success_rate_report.json`.
    """
    OUTPUT_DIR.mkdir(parents=True, exist_ok=True)
    
    report_data: Dict[str, Any] = {
        "mode": mode,
        "count": count,
        "target": MIN_CORPUS_SIZE,
        "status": "met" if count >= MIN_CORPUS_SIZE else "not_met"
    }
    
    with open(REPORT_PATH, 'w', encoding='utf-8') as f:
        json.dump(report_data, f, indent=2)
    
    logger.info(f"Report written to {REPORT_PATH}")

def main() -> None:
    """
    Main entry point for T012a validation.
    """
    logger.info("Starting corpus validation (T012a)...")
    
    count = count_meta_analyses(RAW_DATA_DIR)
    logger.info(f"Found {count} meta-analysis files/datasets in {RAW_DATA_DIR}")
    
    if count < MIN_CORPUS_SIZE:
        logger.critical(
            f"Primary data requirement (FR-001) not met. "
            f"Found {count} items, required {MIN_CORPUS_SIZE}. "
            "Switching to Simulation Mode."
        )
        mode = "simulation"
    else:
        logger.info(
            f"Corpus validation successful. "
            f"Found {count} items (>= {MIN_CORPUS_SIZE}). Proceeding to T016."
        )
        mode = "real"
    
    write_report(mode, count)
    logger.info("T012a validation complete.")

if __name__ == "__main__":
    main()