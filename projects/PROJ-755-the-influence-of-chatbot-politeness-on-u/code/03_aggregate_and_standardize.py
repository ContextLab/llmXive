"""
Implementation of Task T019: Aggregate politeness scores per dialogue and standardize.

This script reads the utterance-level scored data generated by T018,
computes the mean politeness score for each dialogue, and applies
z-score standardization across the dataset.

Output:
    data/processed/dialogue_scores.csv
        Contains: dialogue_id, mean_politeness_score, z_scored_politeness,
                  conversation_length, quality_rating, user_id, age, gender.
"""
import os
import sys
import logging
import argparse
from pathlib import Path
from typing import List, Dict, Any, Optional

import pandas as pd
import numpy as np

# Configure logging
logging.basicConfig(
    level=logging.INFO,
    format="%(asctime)s - %(levelname)s - %(message)s",
    handlers=[logging.StreamHandler(sys.stdout)]
)
logger = logging.getLogger(__name__)

# Ensure paths are relative to project root
PROJECT_ROOT = Path(__file__).resolve().parent.parent
INPUT_PATH = PROJECT_ROOT / "data" / "processed" / "scored_dialogues.parquet"
OUTPUT_PATH = PROJECT_ROOT / "data" / "processed" / "dialogue_scores.csv"


def ensure_directories() -> None:
    """Create output directories if they do not exist."""
    OUTPUT_PATH.parent.mkdir(parents=True, exist_ok=True)
    logger.info(f"Ensured output directory exists: {OUTPUT_PATH.parent}")


def load_scored_dialogues(path: Path) -> pd.DataFrame:
    """
    Load the utterance-level scored dialogues.
    Expects columns: dialogue_id, utterance_id, politeness_score, user_id, quality_rating, etc.
    """
    if not path.exists():
        raise FileNotFoundError(
            f"Input file not found: {path}. "
            "Please ensure T018 (scoring) has completed successfully."
        )
    
    logger.info(f"Loading data from {path}...")
    df = pd.read_parquet(path)
    logger.info(f"Loaded {len(df)} utterance records.")
    return df


def aggregate_dialogue_scores(df: pd.DataFrame) -> pd.DataFrame:
    """
    Compute mean politeness score per dialogue.
    
    Logic:
    1. Group by dialogue_id.
    2. Aggregate mean of politeness_score.
    3. Also aggregate conversation_length (count of utterances) and preserve user_id, quality_rating.
    """
    logger.info("Aggregating utterance scores to dialogue level...")
    
    # Validate required columns
    required_cols = ['dialogue_id', 'politeness_score', 'user_id', 'quality_rating']
    missing = [c for c in required_cols if c not in df.columns]
    if missing:
        raise ValueError(f"Missing required columns for aggregation: {missing}")
    
    # Determine aggregation strategy
    # - mean_politeness_score: mean of politeness_score
    # - conversation_length: count of utterances per dialogue
    # - user_id, quality_rating: take first (assuming constant per dialogue)
    # - age, gender: take first if present
    
    agg_dict = {
        'politeness_score': 'mean',
        'user_id': 'first',
        'quality_rating': 'first'
    }
    
    # Add optional columns if they exist
    optional_cols = ['age', 'gender']
    for col in optional_cols:
        if col in df.columns:
            agg_dict[col] = 'first'
    
    grouped = df.groupby('dialogue_id').agg(agg_dict).reset_index()
    
    # Rename the aggregated score column
    grouped = grouped.rename(columns={'politeness_score': 'mean_politeness_score'})
    
    # Calculate conversation length
    grouped['conversation_length'] = df.groupby('dialogue_id').size()
    
    logger.info(f"Aggregated to {len(grouped)} unique dialogues.")
    return grouped


def standardize_scores(df: pd.DataFrame, column: str = 'mean_politeness_score') -> pd.DataFrame:
    """
    Apply z-score standardization to the specified column.
    
    Formula: z = (x - mean) / std
    Handles cases where std is 0 or NaN.
    """
    logger.info(f"Standardizing '{column}' column...")
    
    if column not in df.columns:
        raise ValueError(f"Column '{column}' not found in dataframe for standardization.")
    
    values = df[column].astype(float)
    
    mean_val = values.mean()
    std_val = values.std()
    
    if std_val == 0 or np.isnan(std_val):
        logger.warning(f"Standard deviation is 0 or NaN for '{column}'. Setting all z-scores to 0.")
        df['z_scored_politeness'] = 0.0
    else:
        df['z_scored_politeness'] = (values - mean_val) / std_val
    
    logger.info(f"Standardization complete. Mean: {mean_val:.4f}, Std: {std_val:.4f}")
    return df


def save_results(df: pd.DataFrame, path: Path) -> None:
    """Save the final dataframe to CSV."""
    logger.info(f"Saving results to {path}...")
    df.to_csv(path, index=False)
    logger.info(f"Successfully saved {len(df)} rows to {path}")


def main() -> None:
    """Main entry point for T019."""
    logger.info("Starting T019: Aggregate and Standardize Politeness Scores")
    
    ensure_directories()
    
    try:
        # 1. Load data
        df_raw = load_scored_dialogues(INPUT_PATH)
        
        # 2. Aggregate to dialogue level
        df_agg = aggregate_dialogue_scores(df_raw)
        
        # 3. Standardize
        df_final = standardize_scores(df_agg, 'mean_politeness_score')
        
        # 4. Save
        save_results(df_final, OUTPUT_PATH)
        
        logger.info("T019 completed successfully.")
        
    except FileNotFoundError as e:
        logger.error(f"Data file error: {e}")
        sys.exit(1)
    except Exception as e:
        logger.error(f"Unexpected error during aggregation/standardization: {e}", exc_info=True)
        sys.exit(1)


if __name__ == "__main__":
    main()