"""
Validation script for User Story 1 (T016).

Computes Pearson correlation (r) between:
1. DAG depth (Logical Difficulty Score) generated by T014 (parser.py)
2. Human-rated logical complexity from T015 (gold_standard_annotations.json)

Outputs: data/processed/validation_report.json
Exit code: 0 if r >= 0.6, else 1.
"""

import json
import sys
import logging
import argparse
from pathlib import Path
from typing import List, Dict, Any, Tuple
from scipy.stats import pearsonr

# Add project root to path for imports
sys.path.insert(0, str(Path(__file__).parent.parent))

from code.src.parser import get_logical_difficulty
from code.src.parser_utils import load_json_file, save_json_file
from code.src.config import PROJECT_ROOT

logging.basicConfig(
    level=logging.INFO,
    format='%(asctime)s - %(levelname)s - %(message)s'
)
logger = logging.getLogger(__name__)

def load_dag_manifest(manifest_path: Path) -> Dict[str, Any]:
    """Load the DAG manifest containing depth scores."""
    logger.info(f"Loading DAG manifest from {manifest_path}")
    if not manifest_path.exists():
        raise FileNotFoundError(f"DAG manifest not found at {manifest_path}. "
                                "Ensure T018 has been completed or run parser on data.")
    return load_json_file(manifest_path)

def load_gold_standard(gold_path: Path) -> List[Dict[str, Any]]:
    """Load human annotations."""
    logger.info(f"Loading gold standard from {gold_path}")
    if not gold_path.exists():
        raise FileNotFoundError(f"Gold standard not found at {gold_path}. "
                                "Ensure T015 has created a template or filled file.")
    data = load_json_file(gold_path)
    # Handle both list format and dict with 'annotations' key
    if isinstance(data, dict) and 'annotations' in data:
        return data['annotations']
    return data

def extract_matching_data(manifest: Dict[str, Any], gold: List[Dict[str, Any]]) -> Tuple[List[float], List[float]]:
    """
    Extract matching depth scores and human ratings.
    
    We match by 'example_id' or 'trace_id' if present.
    If the manifest is a list of examples, we iterate.
    If the manifest is a dict of 'id' -> 'depth', we iterate.
    """
    depths = []
    human_scores = []
    matched_ids = set()

    # Normalize manifest to a list of (id, depth)
    manifest_items = []
    if isinstance(manifest, list):
        for item in manifest:
            item_id = item.get('example_id') or item.get('trace_id') or item.get('id')
            depth = item.get('logical_difficulty_score') or item.get('max_depth')
            if item_id is not None and depth is not None:
                manifest_items.append((item_id, float(depth)))
    elif isinstance(manifest, dict):
        for key, value in manifest.items():
            # value could be a dict with 'depth' or just the depth number
            if isinstance(value, dict):
                depth = value.get('logical_difficulty_score') or value.get('max_depth')
                item_id = value.get('example_id') or key
            else:
                depth = value
                item_id = key
            
            if depth is not None:
                manifest_items.append((item_id, float(depth)))

    # Iterate gold standard to find matches
    for entry in gold:
        # Try common ID fields
        entry_id = entry.get('example_id') or entry.get('trace_id') or entry.get('id')
        
        if entry_id is None:
            logger.warning(f"Skipping gold entry without ID: {entry.get('annotation', 'N/A')[:20]}...")
            continue

        # Find corresponding depth
        depth = None
        for m_id, m_depth in manifest_items:
            if m_id == entry_id:
                depth = m_depth
                break

        if depth is not None:
            human_score = entry.get('human_complexity_rating') or entry.get('logical_complexity_score')
            if human_score is not None:
                depths.append(depth)
                human_scores.append(float(human_score))
                matched_ids.add(entry_id)
            else:
                logger.warning(f"Skipping gold entry {entry_id}: missing human score")
        else:
            logger.debug(f"No DAG depth found for gold ID: {entry_id}")

    logger.info(f"Matched {len(depths)} examples out of {len(gold)} gold entries and {len(manifest_items)} manifest entries.")
    if len(depths) < 5:
        raise ValueError(f"Insufficient matched data points ({len(depths)}) to compute correlation. "
                         "Ensure the manifest and gold standard share example IDs.")
    
    return depths, human_scores

def main():
    parser = argparse.ArgumentParser(description="Validate DAG depth correlation with human ratings.")
    parser.add_argument("--manifest", type=str, default="data/processed/dag_manifest.json",
                        help="Path to DAG manifest JSON")
    parser.add_argument("--gold", type=str, default="data/processed/gold_standard_annotations.json",
                        help="Path to gold standard annotations JSON")
    parser.add_argument("--output", type=str, default="data/processed/validation_report.json",
                        help="Path to output validation report JSON")
    args = parser.parse_args()

    try:
        manifest_path = PROJECT_ROOT / args.manifest
        gold_path = PROJECT_ROOT / args.gold
        output_path = PROJECT_ROOT / args.output

        # Ensure output directory exists
        output_path.parent.mkdir(parents=True, exist_ok=True)

        manifest = load_dag_manifest(manifest_path)
        gold = load_gold_standard(gold_path)

        depths, human_scores = extract_matching_data(manifest, gold)

        # Compute Pearson correlation
        r_value, p_value = pearsonr(depths, human_scores)

        pass_threshold = 0.6
        passed = r_value >= pass_threshold

        report = {
            "task_id": "T016",
            "metric": "Pearson Correlation Coefficient (r)",
            "r_value": float(r_value),
            "p_value": float(p_value),
            "sample_size": len(depths),
            "threshold": pass_threshold,
            "passed": passed,
            "depths": depths,
            "human_scores": human_scores,
            "status": "PASS" if passed else "FAIL",
            "message": f"Correlation r={r_value:.4f} {'meets' if passed else 'does not meet'} threshold of {pass_threshold}."
        }

        save_json_file(output_path, report)
        logger.info(f"Validation report saved to {output_path}")
        logger.info(report["message"])

        if not passed:
            logger.error("Validation FAILED. Logical Difficulty Score does not correlate sufficiently with human ratings.")
            sys.exit(1)
        else:
            logger.info("Validation PASSED.")
            sys.exit(0)

    except Exception as e:
        logger.error(f"Validation script failed with error: {e}")
        # Write a failure report
        error_report = {
            "task_id": "T016",
            "status": "ERROR",
            "error": str(e),
            "message": f"Script execution failed: {e}"
        }
        try:
            save_json_file(output_path, error_report)
        except:
            pass
        sys.exit(1)

if __name__ == "__main__":
    main()
