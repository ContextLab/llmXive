"""
Export regularity scores to CSV.

This module provides functionality to load calculated regularity scores
and export them to a processed CSV file for downstream analysis.
"""
import csv
import sys
from pathlib import Path
from typing import List, Dict, Any, Optional

from config import get_path, ensure_directories
from stratification import load_scores_from_csv


def export_scores_to_csv(
    scores: List[Dict[str, Any]],
    output_path: Optional[Path] = None
) -> Path:
    """
    Export a list of repository scores to a CSV file.

    Args:
        scores: List of dictionaries containing repository data.
                Expected keys: 'repo_id', 'regularity_score' (and potentially others).
        output_path: Optional path for the output CSV. If None, uses default path.

    Returns:
        Path to the created CSV file.

    Raises:
        ValueError: If scores list is empty or contains invalid data.
        IOError: If unable to write to the output path.
    """
    if not scores:
        raise ValueError("Cannot export empty scores list")

    if output_path is None:
        output_path = get_path("data/processed/regularity_scores.csv")

    ensure_directories(output_path)

    # Ensure we have the required columns
    if not all("repo_id" in s and "regularity_score" in s for s in scores):
        raise ValueError("Each score entry must contain 'repo_id' and 'regularity_score'")

    with open(output_path, "w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=["repo_id", "regularity_score"])
        writer.writeheader()
        for score_entry in scores:
            # Filter to only include the required columns for the final export
            row = {
                "repo_id": score_entry["repo_id"],
                "regularity_score": score_entry["regularity_score"]
            }
            writer.writerow(row)

    return output_path


def main() -> int:
    """
    Main entry point for exporting scores from the stratification step.

    This function loads scores calculated by the static analysis pipeline,
    and writes them to the processed data directory.

    Returns:
        0 on success, 1 on failure.
    """
    try:
        # Load scores from the intermediate CSV generated by static analysis
        # The stratification module handles the loading logic
        scores = load_scores_from_csv()

        if not scores:
            print("No scores found to export. Ensure static analysis has been run.", file=sys.stderr)
            return 1

        output_path = export_scores_to_csv(scores)
        print(f"Successfully exported {len(scores)} scores to {output_path}")
        return 0

    except FileNotFoundError as e:
        print(f"Error: Could not find source scores file. {e}", file=sys.stderr)
        print("Ensure T011 (static_analysis.py) has been run to generate intermediate scores.", file=sys.stderr)
        return 1
    except ValueError as e:
        print(f"Error: Invalid data format. {e}", file=sys.stderr)
        return 1
    except Exception as e:
        print(f"Error exporting scores: {e}", file=sys.stderr)
        return 1


if __name__ == "__main__":
    sys.exit(main())
