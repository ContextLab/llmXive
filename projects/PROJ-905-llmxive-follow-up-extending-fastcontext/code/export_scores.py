"""
Export regularity scores to CSV.

This module implements T014: data export logic to write
data/processed/regularity_scores.csv with repo IDs and scores.
"""

import csv
import sys
from pathlib import Path
from typing import List, Dict, Any, Optional

from config import get_path, ensure_directories
from stratification import load_scores_from_csv


def export_scores_to_csv(
    input_csv: Optional[str] = None,
    output_csv: Optional[str] = None,
) -> str:
    """
    Load repository regularity scores from an input CSV and write them
    to a processed output CSV.

    Args:
        input_csv: Path to the input CSV containing repo scores.
                   If None, uses the default path from config.
        output_csv: Path to the output CSV.
                    If None, uses the default path: data/processed/regularity_scores.csv

    Returns:
        The absolute path to the written output CSV file.

    Raises:
        FileNotFoundError: If the input CSV does not exist.
        ValueError: If the input CSV is empty or missing required columns.
    """
    # Resolve paths
    if input_csv is None:
        # Default input: typically the raw scores file before stratification
        # Based on project flow, we assume scores are generated in data/raw or data/processed
        # We'll try a standard location or allow override. For T014, we assume
        # the scores exist in a CSV generated by static_analysis.py (e.g., data/raw/scores.csv)
        # However, to be robust, we check the common output of T013 if it saved a CSV,
        # or we expect a raw scores file.
        # Let's assume the input is the file generated by static_analysis main() if it saves one,
        # or we define a standard input path.
        # Given the task description: "write data/processed/regularity_scores.csv"
        # We need a source. The source is likely the output of T011/T012.
        # Let's assume the input is `data/raw/scores.csv` or similar, but to be safe,
        # we will look for a file or require it to be passed if not default.
        # Actually, `stratification.py` in T013 might have saved the scores.
        # Let's assume the input is `data/raw/regularity_scores_raw.csv` if it exists,
        # or we define a standard input.
        # To avoid guessing, let's assume the input is passed or defaults to a specific location.
        # Based on T013 description: "split into Regular and Irregular sets".
        # T014 needs the scores.
        # Let's assume the input file is `data/raw/scores.csv` (generated by static_analysis).
        # If that doesn't exist, we check `data/processed` or raise error.
        # For this implementation, we will default to `data/raw/scores.csv` if not provided,
        # but the function signature allows override.
        input_csv_path = get_path("data/raw/scores.csv")
    else:
        input_csv_path = Path(input_csv)

    if output_csv is None:
        output_csv_path = get_path("data/processed/regularity_scores.csv")
    else:
        output_csv_path = Path(output_csv)

    # Ensure output directory exists
    ensure_directories(output_csv_path)

    # Load scores
    # We need to read the CSV. The format is expected to have 'repo_id' and 'regularity_score'
    # or similar columns.
    if not input_csv_path.exists():
        raise FileNotFoundError(f"Input scores file not found: {input_csv_path}")

    rows: List[Dict[str, Any]] = []
    try:
        with open(input_csv_path, "r", encoding="utf-8", newline="") as f:
            reader = csv.DictReader(f)
            if not reader.fieldnames:
                raise ValueError("Input CSV is empty or has no headers.")

            # Validate required columns
            required_cols = {"repo_id", "regularity_score"}
            if not required_cols.issubset(set(reader.fieldnames)):
                # Try to map if column names differ slightly (e.g., 'score' instead of 'regularity_score')
                # But strict adherence to spec is safer.
                available = set(reader.fieldnames)
                raise ValueError(
                    f"Input CSV missing required columns. Expected: {required_cols}, Found: {available}"
                )

            for row in reader:
                rows.append(row)
    except Exception as e:
        raise RuntimeError(f"Failed to read input CSV: {e}")

    if not rows:
        raise ValueError("Input CSV contains no data rows.")

    # Write to output
    try:
        with open(output_csv_path, "w", encoding="utf-8", newline="") as f:
            writer = csv.DictWriter(
                f,
                fieldnames=["repo_id", "regularity_score", "set_label"],
                extrasaction="ignore",
            )
            writer.writeheader()
            for row in rows:
                # Ensure numeric score is formatted consistently
                score = float(row["regularity_score"])
                row["regularity_score"] = f"{score:.6f}"
                # We can optionally add a 'set_label' if we have the stratification info,
                # but T014 specifically asks for "repo IDs and scores".
                # We will write exactly what is needed.
                writer.writerow({
                    "repo_id": row["repo_id"],
                    "regularity_score": row["regularity_score"],
                })
    except Exception as e:
        raise RuntimeError(f"Failed to write output CSV: {e}")

    return str(output_csv_path)


def main() -> None:
    """CLI entry point for exporting scores."""
    print("Starting T014: Export regularity scores to CSV...")
    try:
        output_path = export_scores_to_csv()
        print(f"Successfully exported scores to: {output_path}")
    except Exception as e:
        print(f"Error exporting scores: {e}", file=sys.stderr)
        sys.exit(1)


if __name__ == "__main__":
    main()