"""
Integration test for end-to-end preprocessing and metric generation (US1).

This test verifies the complete pipeline flow:
1. Loads processed epochs from data/processed/ (generated by T006_run)
2. Computes connectivity matrices via code/network/connectivity.py
3. Computes network metrics via code/network/metrics.py
4. Validates that the final output contains expected columns and non-NaN values
   for valid epochs.

Prerequisites:
- T005_run: data/raw/ must exist
- T006_run: data/processed/ must contain epoch files
- T007_run: Connectivity matrices must be generated (or generated on-the-fly)
- T008_run: Metrics must be generated (or generated on-the-fly)

Note: This test assumes the pipeline scripts have been run to generate intermediate
artifacts. If artifacts are missing, the test will fail loudly as per requirements.
"""

import os
import json
import tempfile
import shutil
from pathlib import Path
from typing import Dict, List, Any, Optional

import numpy as np
import pytest

# Import from project modules (matching API surface)
from data.preprocess import preprocess_file
