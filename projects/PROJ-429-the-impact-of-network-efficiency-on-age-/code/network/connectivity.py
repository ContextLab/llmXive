"""
Connectivity module for calculating functional connectivity (Coherence)
using Welch's method on fixed-duration EEG epochs.

This module implements the coherence calculation as required by T007.
It expects preprocessed epoch data generated by `code/data/preprocess.py`.
"""
import os
import json
import logging
from pathlib import Path
from typing import Dict, List, Any, Optional, Tuple

import numpy as np
import mne
from scipy.signal import welch, coherency

from config import get_processed_path, get_results_path, CONFIG

# Configure logging
logging.basicConfig(level=logging.INFO, format='%(asctime)s - %(levelname)s - %(message)s')
logger = logging.getLogger(__name__)

# Standard 10-20 EEG channel subset (common in TUH)
# This list can be adjusted based on the actual channels present in the epochs
STANDARD_CHANNELS = [
    'Fp1', 'Fp2', 'F7', 'F3', 'Fz', 'F4', 'F8',
    'T3', 'C3', 'Cz', 'C4', 'T4',
    'T5', 'P3', 'Pz', 'P4', 'T6',
    'O1', 'O2'
]

def load_epochs_from_file(epochs_file: Path) -> mne.Epochs:
    """
    Load preprocessed epochs from a file.
    
    Args:
        epochs_file: Path to the processed epochs file (e.g., .fif)
        
    Returns:
        mne.Epochs object
    """
    if not epochs_file.exists():
        raise FileNotFoundError(f"Epochs file not found: {epochs_file}")
    
    logger.info(f"Loading epochs from {epochs_file}")
    epochs = mne.read_epochs(str(epochs_file), preload=True)
    return epochs

def select_channels(epochs: mne.Epochs, channel_names: List[str]) -> mne.Epochs:
    """
    Select a subset of channels from the epochs.
    
    Args:
        epochs: Input epochs object
        channel_names: List of channel names to keep
        
    Returns:
        mne.Epochs with selected channels
    """
    available = set(epochs.ch_names)
    requested = set(channel_names)
    missing = requested - available
    
    if missing:
        logger.warning(f"Channels not found in data, skipping: {missing}")
    
    selected = list(requested.intersection(available))
    if not selected:
        raise ValueError("No common channels found between data and standard list.")
    
    logger.info(f"Selecting channels: {selected}")
    return epochs.copy().pick_channels(selected)

def calculate_coherence_matrix(epochs: mne.Epochs, 
                               fmin: float = 1.0, 
                               fmax: float = 40.0,
                               n_overlap: Optional[int] = None) -> Tuple[np.ndarray, np.ndarray, np.ndarray]:
    """
    Calculate the coherence matrix for a given frequency band using Welch's method.
    
    This function computes pairwise coherence between all channels in the epochs
    across the specified frequency range and averages the results.
    
    Args:
        epochs: Preprocessed EEG epochs
        fmin: Minimum frequency for coherence calculation (Hz)
        fmax: Maximum frequency for coherence calculation (Hz)
        n_overlap: Number of samples to overlap between windows. Defaults to half window size.
        
    Returns:
        Tuple of (freqs, coherence_matrix, avg_coherence)
        - freqs: Array of frequencies
        - coherence_matrix: 3D array (n_epochs, n_channels, n_channels) of coherence values
        - avg_coherence: 2D array (n_channels, n_channels) of mean coherence
    """
    data = epochs.get_data()  # Shape: (n_epochs, n_channels, n_times)
    sfreq = epochs.info['sfreq']
    n_epochs, n_channels, n_times = data.shape
    
    logger.info(f"Calculating coherence for {n_epochs} epochs, {n_channels} channels, {n_times} time points")
    logger.info(f"Frequency range: {fmin}-{fmax} Hz")
    
    # Determine window size for Welch's method
    # Using 2 seconds for better frequency resolution in low frequencies
    window_sec = 2.0
    window_size = int(window_sec * sfreq)
    
    if n_times < window_size:
        logger.warning(f"Epoch length ({n_times/sfreq:.2f}s) is shorter than window size ({window_sec}s). Adjusting window.")
        window_size = n_times // 2
        if window_size < 10:
            raise ValueError("Epoch too short for coherence calculation.")
    
    if n_overlap is None:
        n_overlap = window_size // 2
    
    # Preallocate coherence array
    # We will compute coherence for each epoch and then average
    coherence_array = np.zeros((n_epochs, n_channels, n_channels))
    freqs_list = []
    
    for i in range(n_epochs):
        epoch_data = data[i]
        
        # Initialize coherence matrix for this epoch
        coh_epoch = np.zeros((n_channels, n_channels))
        
        for ch1 in range(n_channels):
            for ch2 in range(ch1, n_channels):
                # Calculate coherence between channel ch1 and ch2
                f, Cxy = coherency(
                    epoch_data[ch1], 
                    epoch_data[ch2], 
                    fs=sfreq, 
                    nperseg=window_size, 
                    noverlap=n_overlap,
                    scaling='density'
                )
                
                # Filter frequencies within the desired range
                mask = (f >= fmin) & (f <= fmax)
                if not np.any(mask):
                    # If no frequencies in range, use all (shouldn't happen with valid inputs)
                    mask = np.ones_like(f, dtype=bool)
                
                f_valid = f[mask]
                Cxy_valid = Cxy[mask]
                
                # Average coherence over the frequency band
                avg_coh = np.mean(Cxy_valid)
                coh_epoch[ch1, ch2] = avg_coh
                coh_epoch[ch2, ch1] = avg_coh
        
        coherence_array[i] = coh_epoch
        freqs_list.append(f[mask] if 'mask' in locals() else f)
    
    # Average across epochs
    avg_coherence = np.mean(coherence_array, axis=0)
    
    # Return representative frequency array (from first epoch)
    freqs = freqs_list[0] if freqs_list else np.array([])
    
    return freqs, coherence_array, avg_coherence

def save_connectivity_matrix(epochs: mne.Epochs, 
                             output_path: Path, 
                             fmin: float = 1.0, 
                             fmax: float = 40.0) -> Dict[str, Any]:
    """
    Calculate coherence and save the connectivity matrix to disk.
    
    Args:
        epochs: Preprocessed EEG epochs
        output_path: Path to save the output JSON/CSV
        fmin: Minimum frequency
        fmax: Maximum frequency
        
    Returns:
        Dictionary containing metadata about the calculation
    """
    # Select standard channels
    epochs_sel = select_channels(epochs, STANDARD_CHANNELS)
    
    # Calculate coherence
    freqs, coherence_array, avg_coherence = calculate_coherence_matrix(
        epochs_sel, fmin=fmin, fmax=fmax
    )
    
    n_channels = len(epochs_sel.ch_names)
    
    # Prepare output data
    output_data = {
        "metadata": {
            "subject_id": epochs_sel.info.get('subject_info', {}).get('his_id', 'unknown'),
            "n_channels": n_channels,
            "channel_names": epochs_sel.ch_names,
            "frequency_range": [fmin, fmax],
            "sfreq": epochs_sel.info['sfreq'],
            "n_epochs": len(epochs_sel),
            "epoch_length_sec": epochs_sel.tmax - epochs_sel.tmin
        },
        "connectivity_matrix": avg_coherence.tolist(),
        "frequency_vector": freqs.tolist()
    }
    
    # Ensure output directory exists
    output_path.parent.mkdir(parents=True, exist_ok=True)
    
    # Save as JSON
    json_path = output_path.with_suffix('.json')
    with open(json_path, 'w') as f:
        json.dump(output_data, f, indent=2)
    
    # Also save a CSV version of the matrix for easier inspection
    csv_path = output_path.with_suffix('.csv')
    import pandas as pd
    df = pd.DataFrame(avg_coherence, columns=epochs_sel.ch_names, index=epochs_sel.ch_names)
    df.to_csv(csv_path)
    
    logger.info(f"Saved connectivity matrix to {json_path} and {csv_path}")
    
    return {
        "file": str(json_path),
        "n_channels": n_channels,
        "fmin": fmin,
        "fmax": fmax
    }

def process_subject(subject_epochs_path: Path, output_dir: Path) -> Dict[str, Any]:
    """
    Process a single subject's epochs to generate connectivity matrices.
    
    Args:
        subject_epochs_path: Path to the subject's epochs file
        output_dir: Directory to save output files
        
    Returns:
        Dictionary with processing results
    """
    logger.info(f"Processing subject: {subject_epochs_path.name}")
    
    try:
        epochs = load_epochs_from_file(subject_epochs_path)
        
        # Create output filename based on input
        subject_id = subject_epochs_path.stem
        output_file = output_dir / f"{subject_id}_connectivity"
        
        result = save_connectivity_matrix(
            epochs, 
            output_file,
            fmin=CONFIG.get('freq_range_min', 1.0),
            fmax=CONFIG.get('freq_range_max', 40.0)
        )
        
        return {
            "status": "success",
            "subject_id": subject_id,
            "output_file": result["file"]
        }
        
    except Exception as e:
        logger.error(f"Failed to process {subject_epochs_path}: {e}")
        return {
            "status": "failed",
            "subject_id": subject_epochs_path.stem,
            "error": str(e)
        }

def main():
    """
    Main entry point for connectivity analysis.
    Processes all preprocessed epochs in data/processed/ and saves
    connectivity matrices to data/processed/connectivity_matrices/.
    """
    processed_path = get_processed_path()
    output_dir = get_results_path() / "connectivity_matrices"
    
    if not processed_path.exists():
        logger.error(f"Processed data directory not found: {processed_path}")
        return
    
    output_dir.mkdir(parents=True, exist_ok=True)
    
    # Find all epoch files
    epoch_files = list(processed_path.glob("*.fif"))
    
    if not epoch_files:
        logger.warning(f"No epoch files found in {processed_path}")
        return
    
    logger.info(f"Found {len(epoch_files)} epoch files to process")
    
    results = []
    for epoch_file in epoch_files:
        result = process_subject(epoch_file, output_dir)
        results.append(result)
    
    # Save summary report
    summary_path = output_dir / "connectivity_summary.json"
    with open(summary_path, 'w') as f:
        json.dump({
            "total_processed": len(results),
            "successful": sum(1 for r in results if r["status"] == "success"),
            "failed": sum(1 for r in results if r["status"] == "failed"),
            "results": results
        }, f, indent=2)
    
    logger.info(f"Connectivity analysis complete. Summary saved to {summary_path}")

if __name__ == "__main__":
    main()