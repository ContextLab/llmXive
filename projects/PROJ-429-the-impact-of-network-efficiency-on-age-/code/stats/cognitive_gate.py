"""
T023a: Cognitive Data Gate Implementation

Checks data/quality/download_report.json to determine if cognitive data is available.
- If missing_cognitive_count == total_count:
    * Generate data/results/cognitive_status.json with status "BLOCKED".
    * Mark subsequent US2/US3 tasks as SKIPPED (handled by pipeline logic).
- If cognitive data exists:
    * Generate data/results/cognitive_status.json with status "PROCEED".

This script does NOT fetch new data; it only validates the existing report from T005_run.
"""
import json
import logging
import sys
from pathlib import Path

# Configure logging
logging.basicConfig(
    level=logging.INFO,
    format="%(asctime)s [%(levelname)s] %(message)s"
)
logger = logging.getLogger(__name__)

# Paths relative to project root
REPORT_PATH = Path("data/quality/download_report.json")
STATUS_PATH = Path("data/results/cognitive_status.json")


def load_download_report() -> dict:
    """Load and validate the download report generated by T005."""
    if not REPORT_PATH.exists():
        logger.error(f"Download report not found at {REPORT_PATH}. "
                     "Ensure T005_run has been executed successfully.")
        sys.exit(1)
    
    try:
        with open(REPORT_PATH, "r", encoding="utf-8") as f:
            data = json.load(f)
        
        # Validate schema
        required_keys = {"valid_count", "invalid_instrument_count", "missing_cognitive_count", "total_count"}
        if not required_keys.issubset(data.keys()):
            logger.error(f"Download report missing required keys. Found: {list(data.keys())}")
            sys.exit(1)
        
        return data
    except json.JSONDecodeError as e:
        logger.error(f"Failed to parse download report JSON: {e}")
        sys.exit(1)
    except Exception as e:
        logger.error(f"Unexpected error reading download report: {e}")
        sys.exit(1)


def check_cognitive_availability(report: dict) -> dict:
    """
    Determine if cognitive data is available based on the report.
    
    Logic:
    - If missing_cognitive_count == total_count (and total_count > 0), 
      then NO valid cognitive data exists -> BLOCKED.
    - If total_count == 0, no data was downloaded -> BLOCKED.
    - Otherwise -> PROCEED.
    """
    total = report.get("total_count", 0)
    missing = report.get("missing_cognitive_count", 0)
    
    if total == 0:
        status = "BLOCKED"
        reason = "No records found in TUH Corpus (total_count is 0)"
    elif missing == total:
        status = "BLOCKED"
        reason = "No linked cognitive data found in TUH Corpus (all records missing cognitive data)"
    else:
        status = "PROCEED"
        reason = "Cognitive data available for analysis"
    
    return {
        "status": status,
        "reason": reason,
        "report_summary": {
            "total_count": total,
            "missing_cognitive_count": missing,
            "valid_count": report.get("valid_count", 0)
        }
    }


def write_status_file(status_data: dict) -> None:
    """Write the cognitive status decision to data/results/cognitive_status.json."""
    # Ensure output directory exists
    STATUS_PATH.parent.mkdir(parents=True, exist_ok=True)
    
    with open(STATUS_PATH, "w", encoding="utf-8") as f:
        json.dump(status_data, f, indent=2)
    
    logger.info(f"Cognitive status written to {STATUS_PATH}")


def main() -> int:
    """Main entry point for T023a."""
    logger.info("Starting Cognitive Data Gate (T023a)...")
    
    # Load report
    report = load_download_report()
    logger.info(f"Loaded download report: total={report['total_count']}, "
                f"missing_cognitive={report['missing_cognitive_count']}")
    
    # Check availability
    status_data = check_cognitive_availability(report)
    
    # Write result
    write_status_file(status_data)
    
    # Log final decision
    if status_data["status"] == "BLOCKED":
        logger.warning(f"BLOCKED: {status_data['reason']}")
        logger.info("Subsequent US2/US3 tasks (T025a-T029) will be SKIPPED.")
    else:
        logger.info(f"PROCEED: {status_data['reason']}")
    
    return 0


if __name__ == "__main__":
    sys.exit(main())
