"""
T017 Implementation: Update network_metrics.csv with signal_quality_flag.

Reads the existing data/results/network_metrics.csv generated by T008_run.
Reads the preprocessing flags/logs to determine SNR status.
Since T006 (preprocess.py) computes SNR but the current API surface does not
explicitly show a persistent SNR lookup table, this script assumes the existence
of a `data/processed/snr_flags.json` or similar artifact produced by T006_run
that maps subject_id to SNR status. If that specific artifact is missing,
it attempts to infer SNR status from the `data/processed/` directory structure
or logs, but strictly enforces the rule: if SNR < 10dB, flag as 'Low Signal Quality'.

If no SNR data is found for a record, it defaults to 'Unknown' or 'Good' based
on the strict requirement to flag *only* low SNR. To satisfy T017 strictly,
we will assume the preprocess step (T006) wrote a `snr_metadata.json` alongside
the epochs.

If the SNR data is missing entirely, this script will raise an error to prevent
silent failure, as per the "Fail loudly" constraint.
"""
import os
import json
import logging
import pandas as pd
from pathlib import Path

from config import ensure_dirs

# Configure logging
logging.basicConfig(level=logging.INFO, format='%(asctime)s - %(levelname)s - %(message)s')
logger = logging.getLogger(__name__)

# Constants
SNR_THRESHOLD = 10.0  # dB
INPUT_METRICS_PATH = Path("data/results/network_metrics.csv")
SNR_METADATA_PATH = Path("data/processed/snr_metadata.json")  # Assumed output of T006_run
OUTPUT_METRICS_PATH = Path("data/results/network_metrics.csv")

def load_snr_metadata():
    """
    Loads the SNR metadata generated by the preprocessing step.
    Expected format: {"subject_id": {"snr_db": float, "flag": str}}
    """
    if not SNR_METADATA_PATH.exists():
        logger.error(f"SNR metadata file not found at {SNR_METADATA_PATH}. "
                     "T006_run must generate this file for T017 to proceed.")
        raise FileNotFoundError(f"Required SNR metadata file missing: {SNR_METADATA_PATH}")

    with open(SNR_METADATA_PATH, 'r') as f:
        return json.load(f)

def main():
    ensure_dirs()

    if not INPUT_METRICS_PATH.exists():
        logger.error(f"Input metrics file not found at {INPUT_METRICS_PATH}. "
                     "T008_run must be executed first.")
        raise FileNotFoundError(f"Input metrics file missing: {INPUT_METRICS_PATH}")

    logger.info(f"Loading metrics from {INPUT_METRICS_PATH}")
    df = pd.read_csv(INPUT_METRICS_PATH)

    logger.info(f"Loading SNR metadata from {SNR_METADATA_PATH}")
    snr_data = load_snr_metadata()

    # Check if subject_id column exists to join
    if 'subject_id' not in df.columns:
        # Try to infer from filename if no ID column, but standard practice is ID column
        logger.error("Input CSV must contain a 'subject_id' column to map SNR data.")
        raise ValueError("Input CSV missing 'subject_id' column")

    # Create the new column, defaulting to 'Good' (or 'Unknown' if we want to be stricter)
    # The task says: values 'Low Signal Quality' for SNR < 10dB.
    # Implied: others are not 'Low Signal Quality'.
    df['signal_quality_flag'] = 'Good'

    low_snr_count = 0
    unknown_snr_count = 0

    for idx, row in df.iterrows():
        subject_id = row['subject_id']
        
        if subject_id in snr_data:
            snr_info = snr_data[subject_id]
            snr_db = snr_info.get('snr_db', None)
            
            if snr_db is not None:
                if snr_db < SNR_THRESHOLD:
                    df.at[idx, 'signal_quality_flag'] = 'Low Signal Quality'
                    low_snr_count += 1
                # else: remains 'Good'
            else:
                # SNR key exists but value is missing/null
                df.at[idx, 'signal_quality_flag'] = 'Unknown'
                unknown_snr_count += 1
        else:
            # Subject not found in SNR metadata
            df.at[idx, 'signal_quality_flag'] = 'Unknown'
            unknown_snr_count += 1

    logger.info(f"Update complete. Low SNR count: {low_snr_count}, Unknown SNR count: {unknown_snr_count}")

    # Save the updated CSV
    logger.info(f"Saving updated metrics to {OUTPUT_METRICS_PATH}")
    df.to_csv(OUTPUT_METRICS_PATH, index=False)

    logger.info("Task T017 completed successfully.")

if __name__ == "__main__":
    main()
