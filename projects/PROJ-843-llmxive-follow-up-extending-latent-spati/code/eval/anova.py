"""
ANOVA Analysis for US3: Comparative Metrics and Statistical Validation.

Performs Two-Way ANOVA on metrics vs. (Scene Dynamics, Texture Level).
Outputs p-value for interaction effects (significance threshold p < 0.05).
"""
import os
import sys
import json
import numpy as np
import pandas as pd
from pathlib import Path
import statsmodels.api as sm
from statsmodels.formula.api import ols

# Project imports
from config import get_results_dir, ensure_directories
from utils.seeds import set_global_seed

# Ensure reproducibility
set_global_seed(42)

def load_metrics_for_anova(metrics_path: Path) -> pd.DataFrame:
    """
    Load metrics from the JSON file generated by T017 (metrics.py).
    
    Expected structure in JSON:
    {
        "metrics": [
            {
                "sequence_id": "...",
                "scene_dynamics": "Static" | "Fast",
                "texture_level": "High" | "Low",
                "world_score": float,
                "sparse_consistency_score": float,
                ...
            },
            ...
        ]
    }
    
    Returns:
        pd.DataFrame: DataFrame with columns for ANOVA analysis.
    """
    if not metrics_path.exists():
        raise FileNotFoundError(f"Metrics file not found at {metrics_path}")
    
    with open(metrics_path, 'r') as f:
        data = json.load(f)
    
    if 'metrics' not in data:
        raise ValueError("Metrics file does not contain 'metrics' key.")
    
    df = pd.DataFrame(data['metrics'])
    
    # Ensure categorical columns are properly typed for ANOVA
    if 'scene_dynamics' in df.columns:
        df['scene_dynamics'] = df['scene_dynamics'].astype('category')
    if 'texture_level' in df.columns:
        df['texture_level'] = df['texture_level'].astype('category')
        
    return df

def run_anova(df: pd.DataFrame, metric_col: str = 'world_score') -> dict:
    """
    Perform Two-Way ANOVA on the specified metric column.
    
    Formula: metric ~ scene_dynamics * texture_level
    
    Args:
        df: DataFrame containing the metrics and grouping variables.
        metric_col: Name of the column to analyze (default: 'world_score').
        
    Returns:
        dict: Contains ANOVA table summary, p-values, and significance flags.
    """
    if metric_col not in df.columns:
        raise ValueError(f"Metric column '{metric_col}' not found in DataFrame.")
    
    # Check for sufficient data points
    if len(df) < 4:
        raise ValueError("Insufficient data points for ANOVA (need at least 4).")
    
    # Construct the formula for Two-Way ANOVA with interaction
    formula = f"{metric_col} ~ C(scene_dynamics) * C(texture_level)"
    
    try:
        model = ols(formula, data=df).fit()
        anova_table = sm.stats.anova_lm(model, typ=2)
        
        # Extract p-value for interaction term
        # The interaction term is named 'C(scene_dynamics)[T...]:C(texture_level)[T...]'
        # We look for the row containing ':' which indicates interaction
        interaction_row = anova_table[anova_table.index.str.contains(':')]
        
        if interaction_row.empty:
            # Fallback: try to find by index name if regex fails
            # Usually statsmodels names it explicitly
            interaction_p_value = None
            for idx in anova_table.index:
                if ':' in str(idx):
                    interaction_p_value = anova_table.loc[idx, 'PR(>F)']
                    break
        else:
            interaction_p_value = interaction_row['PR(>F)'].iloc[0]
        
        # Determine significance
        significance_threshold = 0.05
        is_significant = interaction_p_value < significance_threshold if interaction_p_value is not None else False
        
        result = {
            "metric_analyzed": metric_col,
            "anova_table": anova_table.to_dict(),
            "interaction_p_value": interaction_p_value,
            "significance_threshold": significance_threshold,
            "is_significant": is_significant,
            "model_summary": model.summary().as_text()
        }
        
        return result
        
    except Exception as e:
        raise RuntimeError(f"ANOVA calculation failed: {str(e)}")

def main():
    """
    Main entry point for running ANOVA analysis.
    
    1. Loads metrics from data/results/metrics.json (generated by T017).
    2. Runs Two-Way ANOVA for 'world_score' and 'sparse_consistency_score'.
    3. Saves results to data/results/anova_results.json.
    """
    ensure_directories()
    results_dir = get_results_dir()
    metrics_file = results_dir / "metrics.json"
    output_file = results_dir / "anova_results.json"
    
    print(f"Loading metrics from {metrics_file}...")
    try:
        df = load_metrics_for_anova(metrics_file)
        print(f"Loaded {len(df)} records.")
        print(f"Columns: {df.columns.tolist()}")
        print(f"Unique scene_dynamics: {df['scene_dynamics'].unique()}")
        print(f"Unique texture_level: {df['texture_level'].unique()}")
    except Exception as e:
        print(f"Error loading metrics: {e}")
        sys.exit(1)
    
    metrics_to_analyze = ['world_score', 'sparse_consistency_score']
    all_results = {}
    
    for metric in metrics_to_analyze:
        print(f"\nRunning ANOVA for metric: {metric}")
        try:
            result = run_anova(df, metric_col=metric)
            all_results[metric] = result
            print(f"  Interaction p-value: {result['interaction_p_value']}")
            print(f"  Significant (p < 0.05): {result['is_significant']}")
        except Exception as e:
            print(f"  Failed to run ANOVA for {metric}: {e}")
            all_results[metric] = {"error": str(e)}
    
    # Save results
    output_path = Path(output_file)
    with open(output_path, 'w') as f:
        # Convert numpy types to native python types for JSON serialization
        def convert_numpy_types(obj):
            if isinstance(obj, dict):
                return {k: convert_numpy_types(v) for k, v in obj.items()}
            elif isinstance(obj, list):
                return [convert_numpy_types(i) for i in obj]
            elif isinstance(obj, (np.integer, np.floating)):
                return float(obj)
            elif isinstance(obj, np.ndarray):
                return obj.tolist()
            else:
                return obj
        
        json.dump(convert_numpy_types(all_results), f, indent=2)
    
    print(f"\nANOVA results saved to {output_path}")
    return all_results

if __name__ == "__main__":
    main()