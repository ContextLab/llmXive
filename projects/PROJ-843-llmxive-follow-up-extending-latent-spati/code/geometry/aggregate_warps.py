"""
T012: Aggregate warped frames from User Story 2.

This script consumes the individual warped frame outputs generated by `code/geometry/warp.py`
and compiles them into a single artifact: `data/results/sparse_warped_frames.npy`.

It validates the shape, data types, and ensures no NaN/Inf values exist in the final artifact.
"""
import os
import sys
import json
import numpy as np
from pathlib import Path
from typing import List, Tuple, Optional

# Import project configuration
from config import get_results_dir, get_features_dir, ensure_directories
from utils.seeds import set_global_seed
from utils.memory_monitor import MemoryMonitor, check_memory_limit

# Set global seed for reproducibility
set_global_seed(42)

def scan_warped_frames(features_dir: Path) -> List[Path]:
    """
    Scan the features directory for warped frame files.
    
    We expect files to be named like: <sequence_id>_warped_frame_<idx>.npy
    located in subdirectories corresponding to strata (e.g., static_high, fast_low).
    
    Args:
        features_dir: Path to the data/features directory where warp outputs are stored.
        
    Returns:
        A sorted list of Path objects pointing to valid .npy files.
    """
    warped_files = []
    if not features_dir.exists():
        print(f"Warning: Features directory {features_dir} does not exist.")
        return warped_files
        
    # Recursively search for .npy files that match the naming convention
    # Assuming warp.py saves files with '_warped_frame_' in the name
    for root, _, files in os.walk(features_dir):
        for file in files:
            if file.endswith(".npy") and "warped_frame" in file:
                warped_files.append(Path(root) / file)
    
    # Sort to ensure deterministic ordering
    return sorted(warped_files)

def load_warped_frame(file_path: Path) -> np.ndarray:
    """
    Load a single warped frame from disk.
    
    Args:
        file_path: Path to the .npy file.
        
    Returns:
        The numpy array representing the frame.
        
    Raises:
        ValueError: If the loaded data is not 3D (H, W, C).
    """
    try:
        data = np.load(file_path)
        if data.ndim != 3:
            raise ValueError(f"Expected 3D frame (H, W, C), got shape {data.shape} from {file_path}")
        return data
    except Exception as e:
        print(f"Error loading {file_path}: {e}")
        raise

def validate_aggregated_data(data: np.ndarray) -> bool:
    """
    Validate the aggregated dataset.
    
    Checks:
    1. Shape is (N, H, W, C)
    2. Data type is float32 or float64
    3. No NaN or Inf values
    4. Values are in a reasonable range (e.g., [0, 1] or [0, 255])
    
    Args:
        data: The aggregated numpy array.
        
    Returns:
        True if valid.
        
    Raises:
        ValueError: If validation fails.
    """
    if data.ndim != 4:
        raise ValueError(f"Aggregated data must be 4D (N, H, W, C), got {data.ndim}D")
    
    if data.dtype not in [np.float32, np.float64]:
        raise ValueError(f"Expected float32 or float64, got {data.dtype}")
    
    if np.isnan(data).any():
        raise ValueError("Aggregated data contains NaN values")
        
    if np.isinf(data).any():
        raise ValueError("Aggregated data contains Inf values")
        
    # Check range (assuming normalized 0-1 or 0-255)
    min_val = data.min()
    max_val = data.max()
    if not ((0 <= min_val <= 1 and 0 <= max_val <= 1) or (0 <= min_val <= 255 and 0 <= max_val <= 255)):
        print(f"Warning: Data range [{min_val}, {max_val}] is unusual. Expected [0,1] or [0,255].")
        
    return True

def aggregate_warped_frames(output_path: Path, warped_files: List[Path]) -> np.ndarray:
    """
    Load and stack all warped frames into a single array.
    
    Implements memory monitoring to prevent OOM on large datasets.
    
    Args:
        output_path: Path where the final .npy will be saved.
        warped_files: List of paths to individual warped frames.
        
    Returns:
        The aggregated numpy array.
    """
    if not warped_files:
        raise FileNotFoundError("No warped frame files found to aggregate.")
        
    frames = []
    monitor = MemoryMonitor()
    
    print(f"Aggregating {len(warped_files)} warped frames...")
    
    for i, file_path in enumerate(warped_files):
        # Check memory before loading next chunk
        if check_memory_limit(threshold_gb=6.0):
            print(f"Memory usage high at frame {i}/{len(warped_files)}. Proceeding carefully.")
        
        frame = load_warped_frame(file_path)
        frames.append(frame)
        
        # Optional: Print progress every 10 frames
        if (i + 1) % 10 == 0:
            print(f"Loaded {i + 1}/{len(warped_files)} frames. Current shape: {np.array(frames).shape}")
    
    # Stack all frames
    # Ensure all frames have the same shape
    if len(set(f.shape for f in frames)) > 1:
        raise ValueError("All warped frames must have the same shape (H, W, C).")
        
    aggregated = np.stack(frames, axis=0)
    
    # Validate immediately after stacking
    validate_aggregated_data(aggregated)
    
    # Save to disk
    print(f"Saving aggregated data to {output_path}...")
    np.save(output_path, aggregated)
    
    # Verify save
    if output_path.exists():
        print(f"Successfully saved {output_path} with shape {aggregated.shape}")
    else:
        raise RuntimeError(f"Failed to write file to {output_path}")
        
    return aggregated

def main():
    """
    Main entry point for T012.
    """
    print("Starting T012: Aggregate Warped Frames")
    
    # Ensure directories exist
    ensure_directories()
    results_dir = get_results_dir()
    features_dir = get_features_dir()
    
    output_file = results_dir / "sparse_warped_frames.npy"
    
    try:
        # 1. Scan for input files
        warped_files = scan_warped_frames(features_dir)
        
        if not warped_files:
            print("ERROR: No warped frames found. Did T011 (warp.py) run successfully?")
            sys.exit(1)
            
        print(f"Found {len(warped_files)} warped frames to aggregate.")
        
        # 2. Aggregate
        aggregated_data = aggregate_warped_frames(output_file, warped_files)
        
        # 3. Write a small metadata JSON for reference
        metadata = {
            "task_id": "T012",
            "source_pattern": "*warped_frame*.npy",
            "total_frames": len(warped_files),
            "output_shape": list(aggregated_data.shape),
            "dtype": str(aggregated_data.dtype),
            "output_path": str(output_file)
        }
        
        metadata_path = results_dir / "sparse_warped_frames_meta.json"
        with open(metadata_path, "w") as f:
            json.dump(metadata, f, indent=2)
            
        print(f"T012 Complete. Metadata saved to {metadata_path}")
        
    except Exception as e:
        print(f"ERROR during aggregation: {e}")
        sys.exit(1)

if __name__ == "__main__":
    main()