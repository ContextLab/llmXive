def locate_memory_logs() -> List[Path]:
    """
    Locate all memory_profiler logs generated by previous phases.
    These are typically named with the script name and a timestamp or phase.
    """
    log_dir = Path("data")
    logs = []
    if log_dir.exists():
        for f in log_dir.rglob("*.log"):
            if "memory" in f.name or "profiler" in f.name:
                logs.append(f)
    return logs

def parse_memory_log(log_path: Path) -> Dict[str, Any]:
    """
    Parse a raw memory_profiler log file to extract peak RAM and duration.
    Returns a dict with 'peak_ram_gb', 'wall_clock_seconds', 'script_name'.
    """
    peak_ram_mb = 0.0
    duration = 0.0
    script_name = log_path.stem

    try:
        with open(log_path, 'r', encoding='utf-8') as f:
            content = f.read()
            
            # Look for peak memory patterns (e.g., "Maximum usage: 1234.5 MB")
            peak_match = re.search(r'Maximum usage:\s*([\d\.]+)\s*MB', content)
            if peak_match:
                peak_ram_mb = float(peak_match.group(1))
            
            # Look for duration patterns if logged, or estimate from timestamps if available
            # For now, we assume the log might contain a "Total time" line
            time_match = re.search(r'Total time:\s*([\d\.]+)\s*s', content)
            if time_match:
                duration = float(time_match.group(1))
            
            # Fallback: if no duration found, we might need to infer from file stats or other logs
            # but for this task, we rely on the log content. If missing, 0.0 is returned.
    except Exception as e:
        # Log error but don't crash the orchestrator
        print(f"Warning: Failed to parse memory log {log_path}: {e}", file=sys.stderr)

    return {
        "script_name": script_name,
        "peak_ram_gb": round(peak_ram_mb / 1024.0, 4),
        "wall_clock_seconds": round(duration, 4)
    }

def aggregate_memory_metrics(logs: List[Path]) -> Dict[str, Any]:
    """
    Aggregate metrics from multiple memory logs into a single summary.
    """
    total_peak_ram = 0.0
    total_duration = 0.0
    max_peak_ram = 0.0
    detailed_logs = []

    for log_path in logs:
        parsed = parse_memory_log(log_path)
        detailed_logs.append(parsed)
        if parsed["peak_ram_gb"] > max_peak_ram:
            max_peak_ram = parsed["peak_ram_gb"]
        total_duration += parsed["wall_clock_seconds"]

    return {
        "summary": {
            "max_peak_ram_gb": round(max_peak_ram, 4),
            "total_wall_clock_seconds": round(total_duration, 4),
            "num_logs_processed": len(logs)
        },
        "details": detailed_logs
    }

def write_metrics_json(metrics: Dict[str, Any], output_path: Path):
    """Write the aggregated metrics to the final metrics.json file."""
    with open(output_path, 'w', encoding='utf-8') as f:
        json.dump(metrics, f, indent=2)
    print(f"Metrics written to {output_path}")

def run_script(script_path: str, args: List[str] = None) -> bool:
    """
    Run a specific script with optional arguments.
    Returns True if successful (exit code 0), False otherwise.
    """
    cmd = [sys.executable, script_path]
    if args:
        cmd.extend(args)
    
    print(f"Running: {' '.join(cmd)}")
    try:
        result = subprocess.run(cmd, check=True, capture_output=False)
        return result.returncode == 0
    except subprocess.CalledProcessError as e:
        print(f"ERROR: Script {script_path} failed with return code {e.returncode}")
        return False
    except Exception as e:
        print(f"ERROR: Failed to run {script_path}: {e}")
        return False

