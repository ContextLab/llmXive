def aggregate_and_write_memory_logs():
    """
    Main function for T020:
    1. Locate memory logs from previous phases.
    2. Parse them.
    3. Aggregate with other metrics (if available).
    4. Write to data/results/metrics.json.
    """
    print("=== Aggregating Memory Logs (Task T020) ===")
    results_dir = ensure_results_dir()
    logs = locate_memory_logs()
    
    if not logs:
        print("Warning: No memory logs found. Creating an empty report.")
        metrics_report = {
            "memory_analysis": {
                "max_peak_ram_gb": 0.0,
                "total_wall_clock_seconds": 0.0,
                "num_logs_processed": 0,
                "details": []
            },
            "other_metrics": {}
        }
    else:
        aggregated = aggregate_memory_metrics(logs)
        metrics_report = {
            "memory_analysis": aggregated["summary"],
            "memory_details": aggregated["details"],
            "other_metrics": {} 
        }
    
    output_path = Path(results_dir) / "metrics.json"
    write_metrics_json(metrics_report, output_path)
    return True

