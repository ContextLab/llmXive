def load_motif_scan_results(scan_file: Path) -> List[Dict[str, Any]]:
    """
    Load motif scan results from a JSON or CSV file generated by FIMO.
    Returns a list of dictionaries containing motif match data.
    """
    if not scan_file.exists():
        raise FileNotFoundError(f"Scan file not found: {scan_file}")
    
    results = []
    # Assuming JSON format for complex nested data, or CSV for flat
    if scan_file.suffix == '.json':
        with open(scan_file, 'r') as f:
            data = json.load(f)
            # Flatten if necessary
            if isinstance(data, list):
                results = data
            elif isinstance(data, dict) and 'matches' in data:
                results = data['matches']
    elif scan_file.suffix == '.csv':
        import csv
        with open(scan_file, 'r') as f:
            reader = csv.DictReader(f)
            results = [row for row in reader]
    else:
        raise ValueError(f"Unsupported file format: {scan_file.suffix}")
    
    logger.info(f"Loaded {len(results)} motif matches from {scan_file}")
    return results

def load_background_peaks(background_file: Path) -> List[Tuple[str, int, int]]:
    """
    Load background peak regions (union of other cell types) from a BED file.
    Returns a list of (chrom, start, end) tuples.
    """
    if not background_file.exists():
        raise FileNotFoundError(f"Background file not found: {background_file}")
    
    peaks = []
    with open(background_file, 'r') as f:
        for line in f:
            if line.startswith('#') or not line.strip():
                continue
            parts = line.strip().split('\t')
            if len(parts) >= 3:
                chrom = parts[0]
                try:
                    start = int(parts[1])
                    end = int(parts[2])
                    peaks.append((chrom, start, end))
                except ValueError:
                    continue
    logger.info(f"Loaded {len(peaks)} background peaks from {background_file}")
    return peaks

