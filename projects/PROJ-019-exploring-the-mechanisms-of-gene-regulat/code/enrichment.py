def benjamini_hochberg_correction(results: List[Dict[str, Any]]) -> List[Dict[str, Any]]:
    """
    Apply Benjamini-Hochberg correction to a list of enrichment results.
    Returns results with updated 'q_value_adj'.
    """
    if not results:
        return []
    
    # Sort by p-value
    sorted_results = sorted(results, key=lambda x: x['p_value_raw'])
    n = len(sorted_results)
    
    # Apply BH correction
    for i, res in enumerate(sorted_results):
        rank = i + 1
        adjusted_p = (res['p_value_raw'] * n) / rank
        res['q_value_adj'] = min(adjusted_p, 1.0)
    
    return sorted_results

