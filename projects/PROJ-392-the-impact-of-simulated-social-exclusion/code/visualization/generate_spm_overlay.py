def load_summary_statistics(stats_path: str) -> List[Dict[str, Any]]:
    """
    Load summary statistics from the CSV generated by T024.
    Expected columns: roi, event, group_excluded_mean, group_included_mean,
                      t_statistic, p_value, cohens_d, n_excluded, n_included
    """
    if not os.path.exists(stats_path):
        raise FileNotFoundError(f"Summary statistics file not found: {stats_path}")

    results = []
    with open(stats_path, 'r', newline='', encoding='utf-8') as f:
        reader = csv.DictReader(f)
        for row in reader:
            # Convert numeric strings to float/int
            row['t_statistic'] = float(row['t_statistic'])
            row['p_value'] = float(row['p_value'])
            row['cohens_d'] = float(row['cohens_d'])
            row['n_excluded'] = int(row['n_excluded'])
            row['n_included'] = int(row['n_included'])
            # Keep means as float
            row['group_excluded_mean'] = float(row['group_excluded_mean'])
            row['group_included_mean'] = float(row['group_included_mean'])
            results.append(row)
    return results


def identify_significant_clusters(
    stats_data: List[Dict[str, Any]],
    alpha: float = 0.05,
    correction: str = 'bonferroni',
    n_tests: int = 4
) -> List[Dict[str, Any]]:
    """
    Filter results for significant clusters based on p-value and correction method.
    Returns a list of significant findings with their stats.
    """
    if correction == 'bonferroni':
        threshold = alpha / n_tests
    else:
        threshold = alpha

    significant = []
    for item in stats_data:
        if item['p_value'] <= threshold:
            # Determine direction
            if item['group_excluded_mean'] < item['group_included_mean']:
                direction = "decreased"
            else:
                direction = "increased"
            
            item['direction'] = direction
            item['threshold'] = threshold
            significant.append(item)
    
    return significant


def get_template_and_atlas_paths() -> Tuple[str, str]:
    """
    Retrieve paths for MNI template and ROI masks from config.
    Falls back to nilearn defaults if not explicitly configured.
    """
    config = get_config()
    
    # Default to nilearn's MNI template if not specified
    mni_template = config.get('paths', {}).get('mni_template', None)
    if not mni_template:
        from nilearn.datasets import load_mni152_template
        # Load and get path
        img = load_mni152_template()
        # nilearn returns an image object, we need the path or use the image directly
        # For plotting, we can pass the image object directly
        return img, None
    
    if not os.path.exists(mni_template):
        raise FileNotFoundError(f"MNI Template not found at: {mni_template}")
    
    return mni_template, None


def generate_spm_overlay(
    stats_data: List[Dict[str, Any]],
    output_dir: str,
    mni_template: Any,
    base_name: str = "spm_overlay"
) -> List[str]:
    """
    Generate SPM-style overlay plots for significant clusters.
    Since we are working with ROI-level summary statistics (not voxel-wise maps),
    we will create a visualization that highlights the ROIs with significant effects
    on a template brain, annotated with peak coordinates (from config) and t-values.
    
    Returns a list of generated file paths.
    """
    significant_clusters = identify_significant_clusters(stats_data)
    
    if not significant_clusters:
        logger.warning("No significant clusters found to overlay. Generating empty report.")
        # Still create a placeholder or empty figure indicating no results
        empty_path = os.path.join(output_dir, f"{base_name}_no_significance.png")
        # Create a simple text-based placeholder or empty plot
        import matplotlib.pyplot as plt
        fig, ax = plt.subplots(figsize=(10, 6))
        ax.text(0.5, 0.5, "No significant clusters found (p > threshold)", 
                ha='center', va='center', transform=ax.transAxes, fontsize=14)
        ax.axis('off')
        plt.savefig(empty_path, dpi=150, bbox_inches='tight')
        plt.close()
        return [empty_path]

    generated_files = []
    
    # Load config for ROI coordinates
    config = get_config()
    roi_coords = config.get('roi', {}).get('coordinates', {})
    
    # Group significant results by ROI for plotting
    roi_results = {}
    for item in significant_clusters:
        roi = item['roi']
        if roi not in roi_results:
            roi_results[roi] = []
        roi_results[roi].append(item)
    
    # Create a composite figure for all significant ROIs
    n_plots = len(roi_results)
    if n_plots == 0:
        return generated_files
        
    # Determine grid size
    cols = min(3, n_plots)
    rows = (n_plots + cols - 1) // cols
    
    fig, axes = plt.subplots(rows, cols, figsize=(15 * cols, 10 * rows))
    if rows == 1 and cols == 1:
        axes = [axes]
    else:
        axes = axes.flatten()
    
    # We need to simulate voxel-wise data for the overlay if we only have ROI stats.
    # Strategy: Create a sparse statistical map where only the ROI centers have the t-value.
    # This is a visualization approximation since we don't have the full statistical parametric map (SPM).
    
    # Load MNI template image object (nibabel or nilearn image)
    if isinstance(mni_template, str):
        template_img = nib.load(mni_template)
    else:
        template_img = mni_template
    
    # Get affine and shape from template
    affine = template_img.affine
    shape = template_img.shape
    data = np.zeros(shape)
    
    # Map ROI names to coordinates from config
    # Expected format in config: "Ventral Striatum": [x, y, z]
    for i, (roi, items) in enumerate(roi_results):
        if i >= len(axes):
            break
        
        ax = axes[i]
        
        # Find coordinates for this ROI
        coords = roi_coords.get(roi)
        if not coords:
            logger.warning(f"No coordinates found for ROI: {roi}. Skipping overlay.")
            continue
        
        # Convert MNI coordinates to voxel indices
        coords_arr = np.array(coords).reshape(1, 3)
        # Inverse transform MNI to voxel
        voxel_indices = nib.affines.apply_affine(np.linalg.inv(affine), coords_arr)[0]
        voxel_indices = np.round(voxel_indices).astype(int)
        
        # Create a small sphere of activation around the coordinate
        radius = 3  # 3 voxels radius
        z, y, x = voxel_indices
        
        # Ensure indices are within bounds
        z_min, z_max = max(0, z - radius), min(shape[0], z + radius + 1)
        y_min, y_max = max(0, y - radius), min(shape[1], y + radius + 1)
        x_min, x_max = max(0, x - radius), min(shape[2], x + radius + 1)
        
        # Create a sphere mask
        for zz in range(z_min, z_max):
            for yy in range(y_min, y_max):
                for xx in range(x_min, x_max):
                    dist = np.sqrt((zz - z)**2 + (yy - y)**2 + (xx - x)**2)
                    if dist <= radius:
                        # Use the maximum t-statistic for this ROI if multiple events
                        t_val = max([item['t_statistic'] for item in items])
                        data[zz, yy, xx] = t_val
        
        # Create a statistical map image
        stat_map_img = new_img_like(template_img, data)
        
        # Plot
        display = plotting.plot_stat_map(
            stat_map_img, 
            bg_img=template_img,
            threshold=1.5,  # Minimum t-value to display
            cut_coords=coords,
            display_mode='ortho',
            axes=ax,
            title=f"ROI: {roi}\nPeak t={max([item['t_statistic'] for item in items]):.2f}, p={min([item['p_value'] for item in items]):.4f}",
            cmap='cold_hot'
        )
        
        # Add text annotations for multiple events in same ROI
        if len(items) > 1:
            details = "\n".join([f"{it['event']}: t={it['t_statistic']:.2f}" for it in items])
            ax.text(0.5, -0.1, details, transform=ax.transAxes, ha='center', fontsize=10)
        
        plotting.close_all()
    
    # Save the composite figure
    output_path = os.path.join(output_dir, f"{base_name}.png")
    plt.savefig(output_path, dpi=150, bbox_inches='tight', facecolor='white')
    plt.close(fig)
    generated_files.append(output_path)
    
    # Also save individual slices if requested or as a standard practice
    # For now, just the composite is sufficient for the task requirement
    
    return generated_files


def generate_coordinates_table(
    stats_data: List[Dict[str, Any]],
    output_path: str,
    roi_coords: Dict[str, List[float]]
) -> None:
    """
    Generate a CSV table of coordinates and peak t-values for significant clusters.
    """
    significant = identify_significant_clusters(stats_data)
    
    with open(output_path, 'w', newline='', encoding='utf-8') as f:
        writer = csv.writer(f)
        writer.writerow(['ROI', 'Event', 'x (MNI)', 'y (MNI)', 'z (MNI)', 'Peak t-value', 'p-value', 'Direction'])
        
        for item in significant:
            roi = item['roi']
            coords = roi_coords.get(roi, [0, 0, 0])
            writer.writerow([
                roi,
                item['event'],
                coords[0],
                coords[1],
                coords[2],
                f"{item['t_statistic']:.3f}",
                f"{item['p_value']:.4f}",
                item['direction']
            ])


def run_spm_overlay_generation(
    stats_file: str,
    output_dir: str,
    config_path: Optional[str] = None,
    alpha: float = 0.05
) -> Dict[str, str]:
    """
    Main entry point for T026.
    """
    ensure_paths_exist([output_dir])
    
    # Load config
    if config_path:
        # If a specific config path is provided, we'd need to load it, 
        # but get_config() usually looks for a default location.
        # Assuming standard project config structure.
        pass
    
    # Load statistics
    stats_data = load_summary_statistics(stats_file)
    logger.info(f"Loaded {len(stats_data)} statistical results.")
    
    # Get MNI template
    mni_template, _ = get_template_and_atlas_paths()
    logger.info("MNI template loaded.")
    
    # Generate overlay
    config = get_config()
    roi_coords = config.get('roi', {}).get('coordinates', {})
    
    overlay_files = generate_spm_overlay(stats_data, output_dir, mni_template)
    logger.info(f"Generated overlay figures: {overlay_files}")
    
    # Generate coordinates table
    coords_table_path = os.path.join(output_dir, "significant_clusters_coordinates.csv")
    generate_coordinates_table(stats_data, coords_table_path, roi_coords)
    logger.info(f"Generated coordinates table: {coords_table_path}")
    
    # Generate provenance
    provenance_data = {
        "task_id": "T026",
        "description": "SPM overlay generation",
        "input_file": stats_file,
        "output_files": overlay_files + [coords_table_path],
        "parameters": {
            "alpha": alpha,
            "correction": "bonferroni"
        }
    }
    
    provenance_path = generate_provenance_sidecar(
        output_path=os.path.join(output_dir, "spm_overlay_provenance.yaml"),
        data=provenance_data
    )
    logger.info(f"Provenance saved to: {provenance_path}")
    
    return {
        "overlay_files": overlay_files,
        "coordinates_table": coords_table_path,
        "provenance": provenance_path
    }


def main():
    parser = argparse.ArgumentParser(description="Generate SPM overlay figures from summary statistics.")
    parser.add_argument("--stats-file", type=str, required=True, 
                        help="Path to the summary statistics CSV (from T024).")
    parser.add_argument("--output-dir", type=str, required=True,
                        help="Directory to save output figures and tables.")
    parser.add_argument("--config", type=str, default=None,
                        help="Path to config file (optional, uses default if not provided).")
    parser.add_argument("--alpha", type=float, default=0.05,
                        help="Significance threshold (default: 0.05).")
    
    args = parser.parse_args()
    
    try:
        results = run_spm_overlay_generation(
            stats_file=args.stats_file,
            output_dir=args.output_dir,
            config_path=args.config,
            alpha=args.alpha
        )
        logger.info("SPM overlay generation completed successfully.")
        logger.info(f"Outputs: {results}")
    except Exception as e:
        logger.error(f"Error during SPM overlay generation: {e}")
        sys.exit(1)


if __name__ == "__main__":
    main()