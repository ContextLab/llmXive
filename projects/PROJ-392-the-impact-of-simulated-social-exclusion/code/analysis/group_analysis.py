import argparse
import csv
import json
import logging
import os
import sys
from pathlib import Path
from typing import Dict, List, Tuple, Any

import numpy as np
import pandas as pd
from scipy import stats

# Configure logging
logging.basicConfig(
    level=logging.INFO,
    format='%(asctime)s - %(levelname)s - %(message)s',
    handlers=[
        logging.FileHandler('data/results/group_analysis.log'),
        logging.StreamHandler(sys.stdout)
    ]
)
logger = logging.getLogger(__name__)

def load_beta_estimates(file_path: str) -> pd.DataFrame:
    """
    Load beta estimates from the CSV file generated by T020.
    
    Args:
        file_path: Path to data/results/beta_estimates.csv
        
    Returns:
        DataFrame with columns: participant_id, group, roi, event_type, beta_value
    """
    path = Path(file_path)
    if not path.exists():
        raise FileNotFoundError(f"Beta estimates file not found: {file_path}")
    
    df = pd.read_csv(path)
    required_cols = {'participant_id', 'group', 'roi', 'event_type', 'beta_value'}
    if not required_cols.issubset(df.columns):
        missing = required_cols - set(df.columns)
        raise ValueError(f"Missing required columns in beta estimates: {missing}")
    
    logger.info(f"Loaded {len(df)} beta estimates from {file_path}")
    return df

def group_by_roi_event(df: pd.DataFrame) -> Dict[Tuple[str, str], pd.DataFrame]:
    """
    Group the dataframe by ROI and event_type.
    
    Returns:
        Dictionary mapping (roi, event_type) to DataFrame subset
    """
    groups = {}
    for (roi, event), group_df in df.groupby(['roi', 'event_type']):
        groups[(roi, event)] = group_df
    logger.info(f"Grouped data into {len(groups)} ROI/Event combinations")
    return groups

def perform_two_sample_ttest(group_a: pd.Series, group_b: pd.Series) -> Dict[str, float]:
    """
    Perform an independent two-sample t-test (Welch's t-test) between two groups.
    This satisfies FR-005 (Primary Analysis Method).
    
    Args:
        group_a: Series of beta values for group A (e.g., excluded)
        group_b: Series of beta values for group B (e.g., included)
        
    Returns:
        Dictionary containing t_statistic, p_value, mean_a, mean_b, std_a, std_b, n_a, n_b
    """
    if len(group_a) == 0 or len(group_b) == 0:
        raise ValueError("One of the groups has no data points for t-test.")
    
    # Welch's t-test (unequal variance assumed, robust for neuroimaging data)
    t_stat, p_val = stats.ttest_ind(group_a, group_b, equal_var=False)
    
    result = {
        't_statistic': float(t_stat),
        'p_value': float(p_val),
        'mean_a': float(group_a.mean()),
        'mean_b': float(group_b.mean()),
        'std_a': float(group_a.std()),
        'std_b': float(group_b.std()),
        'n_a': len(group_a),
        'n_b': len(group_b)
    }
    
    # Calculate Cohen's d (effect size)
    # Pooled standard deviation
    n1, n2 = len(group_a), len(group_b)
    s1, s2 = float(group_a.std()), float(group_b.std())
    if (n1 - 1) * s1**2 + (n2 - 1) * s2**2 == 0:
        pooled_std = 0
    else:
        pooled_std = np.sqrt(((n1 - 1) * s1**2 + (n2 - 1) * s2**2) / (n1 + n2 - 2))
    
    if pooled_std > 0:
        cohen_d = (result['mean_a'] - result['mean_b']) / pooled_std
    else:
        cohen_d = 0.0
    
    result['cohen_d'] = float(cohen_d)
    
    logger.info(f"T-test: t={t_stat:.4f}, p={p_val:.4e}, d={cohen_d:.4f}")
    return result

def bonferroni_correction(p_values: List[float], alpha: float = 0.05) -> List[float]:
    """
    Apply Bonferroni correction to a list of p-values.
    Satisfies T022 requirements.
    
    Args:
        p_values: List of raw p-values
        alpha: Significance level (default 0.05)
        
    Returns:
        List of corrected p-values (capped at 1.0)
    """
    n_tests = len(p_values)
    if n_tests == 0:
        return []
    
    corrected = [min(p * n_tests, 1.0) for p in p_values]
    logger.info(f"Bonferroni correction applied: {n_tests} tests, alpha={alpha}")
    return corrected

def get_significance_stars(p_value: float, threshold: float = 0.05) -> str:
    """
    Convert p-value to significance stars string.
    """
    if p_value < 0.001:
        return "***"
    elif p_value < 0.01:
        return "**"
    elif p_value < threshold:
        return "*"
    else:
        return "ns"

def run_group_analysis(df: pd.DataFrame, alpha: float = 0.05) -> List[Dict[str, Any]]:
    """
    Execute the primary group analysis logic.
    Iterates over all ROI/Event combinations, performs t-tests, and applies corrections.
    
    Args:
        df: Full beta estimates dataframe
        alpha: Significance threshold
        
    Returns:
        List of result dictionaries for each combination
    """
    groups = group_by_roi_event(df)
    results = []
    raw_p_values = []
    
    # First pass: collect raw p-values for correction
    for key, group_df in groups.items():
        roi, event = key
        excluded = group_df[group_df['group'] == 'excluded']['beta_value']
        included = group_df[group_df['group'] == 'included']['beta_value']
        
        if len(excluded) == 0 or len(included) == 0:
            logger.warning(f"Skipping {key}: missing data in one group.")
            continue
        
        t_result = perform_two_sample_ttest(excluded, included)
        raw_p_values.append(t_result['p_value'])
    
    # Apply Bonferroni correction
    if len(raw_p_values) > 0:
        corrected_p_values = bonferroni_correction(raw_p_values, alpha)
    else:
        corrected_p_values = []
    
    # Second pass: build final results with corrected p-values
    p_idx = 0
    for key, group_df in groups.items():
        roi, event = key
        excluded = group_df[group_df['group'] == 'excluded']['beta_value']
        included = group_df[group_df['group'] == 'included']['beta_value']
        
        if len(excluded) == 0 or len(included) == 0:
            continue
        
        t_result = perform_two_sample_ttest(excluded, included)
        t_result['roi'] = roi
        t_result['event_type'] = event
        t_result['raw_p_value'] = t_result['p_value']
        
        if p_idx < len(corrected_p_values):
            t_result['corrected_p_value'] = corrected_p_values[p_idx]
            t_result['is_significant'] = t_result['corrected_p_value'] < alpha
            t_result['stars'] = get_significance_stars(t_result['corrected_p_value'])
            p_idx += 1
        else:
            t_result['corrected_p_value'] = 1.0
            t_result['is_significant'] = False
            t_result['stars'] = "ns"
        
        results.append(t_result)
        logger.info(f"Completed analysis for {roi} - {event}: p_corr={t_result['corrected_p_value']:.4f}")
    
    return results

def save_results(results: List[Dict[str, Any]], output_path: str):
    """
    Save the group analysis results to a CSV file.
    """
    path = Path(output_path)
    path.parent.mkdir(parents=True, exist_ok=True)
    
    if not results:
        logger.warning("No results to save.")
        # Create empty file with headers
        with open(path, 'w', newline='') as f:
            writer = csv.writer(f)
            writer.writerow(['roi', 'event_type', 't_statistic', 'raw_p_value', 'corrected_p_value', 
                             'mean_excluded', 'mean_included', 'std_excluded', 'std_included', 
                             'n_excluded', 'n_included', 'cohen_d', 'is_significant', 'stars'])
        return

    fieldnames = ['roi', 'event_type', 't_statistic', 'raw_p_value', 'corrected_p_value', 
                  'mean_excluded', 'mean_included', 'std_excluded', 'std_included', 
                  'n_excluded', 'n_included', 'cohen_d', 'is_significant', 'stars']
    
    with open(path, 'w', newline='') as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        for res in results:
            writer.writerow(res)
    
    logger.info(f"Results saved to {output_path}")

def main():
    parser = argparse.ArgumentParser(description="Run group-level statistical analysis (t-tests) on beta estimates.")
    parser.add_argument(
        '--input', 
        type=str, 
        default='data/results/beta_estimates.csv',
        help='Path to the beta estimates CSV file'
    )
    parser.add_argument(
        '--output',
        type=str,
        default='data/results/group_analysis_results.csv',
        help='Path to save the analysis results'
    )
    parser.add_argument(
        '--alpha',
        type=float,
        default=0.05,
        help='Significance threshold for hypothesis testing'
    )
    
    args = parser.parse_args()
    
    try:
        logger.info(f"Starting group analysis with input: {args.input}")
        df = load_beta_estimates(args.input)
        results = run_group_analysis(df, alpha=args.alpha)
        save_results(results, args.output)
        logger.info("Group analysis completed successfully.")
    except Exception as e:
        logger.error(f"Group analysis failed: {e}", exc_info=True)
        sys.exit(1)

if __name__ == '__main__':
    main()
