def test_pgls_schema_conformance(pgls_results, schema):
    """
    Verify that the generated pgls_results.json matches the defined schema.
    
    Expected structure based on T027 and typical PGLS outputs:
    {
      "metadata": {
        "organisms": [...],
        "tree_source": "...",
        "method": "PGLS",
        "correction": "Benjamini-Hochberg"
      },
      "comparisons": [
        {
          "organism_a": "...",
          "organism_b": "...",
          "z_score": float,
          "p_value_raw": float,
          "p_value_adjusted": float,
          "significant": bool
        }
      ]
    }
    """
    # Validate the structure against the schema
    try:
        validate_against_schema(pgls_results, str(SCHEMA_PATH))
    except ValidationError as e:
        pytest.fail(f"PGSL results do not conform to schema: {e.message}")

def test_pgls_results_structure(pgls_results):
    """
    Additional structural checks to ensure critical fields are present
    even if the schema is loose.
    """
    assert "metadata" in pgls_results, "Missing 'metadata' key in PGLS results"
    assert "comparisons" in pgls_results, "Missing 'comparisons' key in PGLS results"
    
    metadata = pgls_results["metadata"]
    assert "organisms" in metadata, "Missing 'organisms' in metadata"
    assert "tree_source" in metadata, "Missing 'tree_source' in metadata"
    
    comparisons = pgls_results["comparisons"]
    assert isinstance(comparisons, list), "'comparisons' must be a list"
    
    if len(comparisons) > 0:
        first_comp = comparisons[0]
        required_fields = ["organism_a", "organism_b", "z_score", "p_value_raw", "p_value_adjusted"]
        for field in required_fields:
            assert field in first_comp, f"Missing required field '{field}' in comparison object"

def test_pgls_results_data_types(pgls_results):
    """
    Verify that numeric fields are actually numbers and booleans are booleans.
    """
    comparisons = pgls_results.get("comparisons", [])
    
    for i, comp in enumerate(comparisons):
        assert isinstance(comp.get("z_score"), (int, float)), f"z_score at index {i} is not numeric"
        assert isinstance(comp.get("p_value_raw"), (int, float)), f"p_value_raw at index {i} is not numeric"
        assert isinstance(comp.get("p_value_adjusted"), (int, float)), f"p_value_adjusted at index {i} is not numeric"
        assert isinstance(comp.get("significant"), bool), f"significant at index {i} is not boolean"