import os
import sys
import json
import logging
import argparse
import csv
from pathlib import Path
from typing import List, Dict, Any

# Import from sibling modules as per API surface
from utils.logger import setup_logging, get_logger
from config import TrainingConfig, ensure_dirs

def setup_logging(log_path: str) -> logging.Logger:
    """Setup logging for hyperparameter logging."""
    logger = setup_logging(log_path, name="hyperparameter_logger")
    return logger

def load_hyperparameter_log(json_path: str) -> List[Dict[str, Any]]:
    """
    Load the hyperparameter search results from the JSON file generated by train.py.
    Expected structure: list of dicts with keys: config_id, learning_rate, hidden_dim, dropout, r2_val, mae_val, etc.
    """
    path = Path(json_path)
    if not path.exists():
        raise FileNotFoundError(f"Hyperparameter log not found at {json_path}")
    
    with open(path, 'r') as f:
        data = json.load(f)
    
    if not isinstance(data, list):
        raise ValueError(f"Expected a list of results in {json_path}, got {type(data)}")
    
    return data

def format_hyperparameter_entry(entry: Dict[str, Any]) -> Dict[str, Any]:
    """
    Ensure the entry has the correct columns for the CSV output.
    Maps internal keys to the required CSV columns:
    config_id, learning_rate, hidden_dim, dropout, r2_val, mae_val
    """
    # Extract values, providing defaults or raising if missing
    config_id = entry.get('config_id')
    if config_id is None:
        # Fallback if config_id wasn't explicitly stored, generate one or use index
        # Assuming the list index acts as ID if not present, but we prefer the stored one.
        # If the training script didn't save it, we might need to handle it.
        # For safety, we'll try to use the index if the key is missing, but the task implies
        # T020/T021 generated this. Let's assume 'config_id' exists or derive from order.
        pass 

    return {
        'config_id': entry.get('config_id', 0),
        'learning_rate': entry.get('learning_rate'),
        'hidden_dim': entry.get('hidden_dim'),
        'dropout': entry.get('dropout'),
        'r2_val': entry.get('r2_val'),
        'mae_val': entry.get('mae_val')
    }

def generate_hyperparameter_log(
    json_input_path: str, 
    csv_output_path: str, 
    top_n: int = 10
) -> None:
    """
    Load hyperparameter search results, sort by validation R² (descending),
    take the top N, and save to CSV.
    
    Args:
        json_input_path: Path to the JSON log from train.py (e.g., artifacts/training_results.json)
        csv_output_path: Path to save the CSV (e.g., artifacts/hyperparameter_search.csv)
        top_n: Number of top configurations to include.
    """
    logger = logging.getLogger("hyperparameter_logger")
    logger.info(f"Loading hyperparameter log from {json_input_path}")
    
    results = load_hyperparameter_log(json_input_path)
    
    if not results:
        logger.warning("No results found in the hyperparameter log.")
        # Create an empty CSV with headers if no data
        with open(csv_output_path, 'w', newline='') as f:
            writer = csv.DictWriter(f, fieldnames=['config_id', 'learning_rate', 'hidden_dim', 'dropout', 'r2_val', 'mae_val'])
            writer.writeheader()
        return

    # Validate that required fields exist
    required_fields = ['config_id', 'learning_rate', 'hidden_dim', 'dropout', 'r2_val', 'mae_val']
    for i, res in enumerate(results):
        for field in required_fields:
            if field not in res:
                raise KeyError(f"Missing required field '{field}' in result at index {i}")

    # Sort by validation R² descending
    sorted_results = sorted(results, key=lambda x: x['r2_val'], reverse=True)
    
    # Take top N
    top_results = sorted_results[:top_n]
    
    # Format entries
    formatted_results = [format_hyperparameter_entry(r) for r in top_results]
    
    # Ensure output directory exists
    output_dir = Path(csv_output_path).parent
    output_dir.mkdir(parents=True, exist_ok=True)
    
    # Write to CSV
    logger.info(f"Writing top {len(formatted_results)} configurations to {csv_output_path}")
    fieldnames = ['config_id', 'learning_rate', 'hidden_dim', 'dropout', 'r2_val', 'mae_val']
    
    with open(csv_output_path, 'w', newline='') as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(formatted_results)
    
    logger.info("Hyperparameter search log generated successfully.")

def main():
    parser = argparse.ArgumentParser(description="Log top hyperparameter configurations to CSV.")
    parser.add_argument(
        "--input-json", 
        type=str, 
        default="artifacts/training_results.json",
        help="Path to the JSON file containing training results from T020/T021."
    )
    parser.add_argument(
        "--output-csv", 
        type=str, 
        default="artifacts/hyperparameter_search.csv",
        help="Path to save the CSV file with top hyperparameter configurations."
    )
    parser.add_argument(
        "--top-n", 
        type=int, 
        default=10,
        help="Number of top configurations to log."
    )
    parser.add_argument(
        "--log-file",
        type=str,
        default="logs/hyperparameter_log.log",
        help="Path to the log file."
    )

    args = parser.parse_args()
    
    # Setup logging
    setup_logging(args.log_file)
    logger = logging.getLogger("hyperparameter_logger")
    
    try:
        generate_hyperparameter_log(
            json_input_path=args.input_json,
            csv_output_path=args.output_csv,
            top_n=args.top_n
        )
        logger.info("Task completed successfully.")
    except FileNotFoundError as e:
        logger.error(f"File not found: {e}")
        sys.exit(1)
    except KeyError as e:
        logger.error(f"Missing required data in input file: {e}")
        sys.exit(1)
    except Exception as e:
        logger.error(f"Unexpected error: {e}")
        sys.exit(1)

if __name__ == "__main__":
    main()
