import os
import sys
import csv
import json
import logging
import argparse
from pathlib import Path
from typing import List, Dict, Any, Optional

# Ensure project root is in path for imports if running as script
project_root = Path(__file__).resolve().parent.parent.parent
if str(project_root) not in sys.path:
    sys.path.insert(0, str(project_root))

from config import DataConfig
from utils.logger import get_logger

logger = get_logger(__name__)

def load_exclusion_logs(log_paths: List[str]) -> List[Dict[str, Any]]:
    """
    Load exclusion logs from JSON files generated by T012 (clean.py) and T013 (descriptors.py).
    
    Args:
        log_paths: List of paths to JSON log files.
        
    Returns:
        List of exclusion records.
    """
    all_records = []
    
    for log_path in log_paths:
        path = Path(log_path)
        if not path.exists():
            logger.warning(f"Exclusion log not found: {log_path}. Skipping.")
            continue
        
        try:
            with open(path, 'r', encoding='utf-8') as f:
                logs = json.load(f)
                if isinstance(logs, list):
                    all_records.extend(logs)
                else:
                    # Handle case where it might be a single object wrapped or raw dict
                    all_records.append(logs)
        except json.JSONDecodeError as e:
            logger.error(f"Failed to decode JSON from {log_path}: {e}")
        except Exception as e:
            logger.error(f"Error reading log file {log_path}: {e}")
            
    return all_records

def map_error_reason(raw_reason: str) -> str:
    """
    Map raw error messages to standardized schema reasons.
    """
    raw_lower = raw_reason.lower()
    
    if 'canonicalization' in raw_lower or 'canonicalize' in raw_lower:
        return 'canonicalization_error'
    elif 'gasteiger' in raw_lower and ('convergence' in raw_lower or 'failed' in raw_lower):
        return 'gasteiger_convergence_error'
    elif 'steric' in raw_lower or 'hindrance' in raw_lower:
        return 'steric_hindrance_exceeded'
    elif 'primary' in raw_lower and 'substrate' in raw_lower:
        return 'primary_substrate'
    elif 'missing' in raw_lower and 'rate' in raw_lower:
        return 'missing_rate_constant'
    elif 'invalid' in raw_lower and 'smiles' in raw_lower:
        return 'invalid_smiles'
    else:
        # Default to a generic error code if mapping fails, but log it
        logger.warning(f"Could not map raw reason: '{raw_reason}'. Using 'unknown_error'.")
        return 'unknown_error'

def generate_exclusion_report(logs: List[Dict[str, Any]], output_path: str) -> None:
    """
    Aggregate exclusion logs into a single CSV report matching the schema.
    
    Args:
        logs: List of exclusion records.
        output_path: Path to save the CSV report.
    """
    output_file = Path(output_path)
    output_file.parent.mkdir(parents=True, exist_ok=True)
    
    # Ensure headers match schema exactly
    fieldnames = ['row_index', 'reason', 'original_smiles']
    
    records_written = 0
    
    with open(output_file, 'w', newline='', encoding='utf-8') as csvfile:
        writer = csv.DictWriter(csvfile, fieldnames=fieldnames)
        writer.writeheader()
        
        for record in logs:
            # Extract fields safely
            row_index = record.get('row_index')
            raw_reason = record.get('reason', 'unknown')
            smiles = record.get('original_smiles', record.get('smiles', ''))
            
            if row_index is None:
                logger.warning(f"Skipping record without row_index: {record}")
                continue
                
            mapped_reason = map_error_reason(raw_reason)
            
            writer.writerow({
                'row_index': row_index,
                'reason': mapped_reason,
                'original_smiles': smiles
            })
            records_written += 1
            
    logger.info(f"Exclusion report generated: {output_file} ({records_written} records)")

def main():
    """
    Main entry point for generating the exclusion report.
    Reads logs from T012 and T013 and aggregates them.
    """
    parser = argparse.ArgumentParser(description="Generate exclusion report from data cleaning logs.")
    parser.add_argument('--clean-log', type=str, required=False, 
                        help="Path to exclusion log from clean.py (T012).")
    parser.add_argument('--desc-log', type=str, required=False, 
                        help="Path to exclusion log from descriptors.py (T013).")
    parser.add_argument('--output', type=str, required=False,
                        help="Output path for exclusion_report.csv.")
    
    args = parser.parse_args()
    
    # Use config defaults if not provided
    config = DataConfig()
    
    clean_log = args.clean_log or str(config.clean_exclusion_log)
    desc_log = args.desc_log or str(config.descriptor_exclusion_log)
    output_path = args.output or str(config.exclusion_report_path)
    
    # Determine list of logs to process
    logs_to_process = []
    if os.path.exists(clean_log):
        logs_to_process.append(clean_log)
    if os.path.exists(desc_log):
        logs_to_process.append(desc_log)
        
    if not logs_to_process:
        logger.warning("No exclusion log files found. Creating empty report.")
        # Still create the file with headers to satisfy schema existence
        Path(output_path).parent.mkdir(parents=True, exist_ok=True)
        with open(output_path, 'w', newline='', encoding='utf-8') as f:
            writer = csv.DictWriter(f, fieldnames=['row_index', 'reason', 'original_smiles'])
            writer.writeheader()
        return

    logger.info(f"Loading exclusion logs from: {logs_to_process}")
    all_logs = load_exclusion_logs(logs_to_process)
    
    logger.info(f"Total exclusion records found: {len(all_logs)}")
    
    generate_exclusion_report(all_logs, output_path)

if __name__ == "__main__":
    main()