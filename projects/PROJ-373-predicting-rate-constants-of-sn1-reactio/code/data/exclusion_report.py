def load_exclusion_logs(log_dir: Path) -> List[Dict[str, Any]]:
    """
    Aggregates exclusion logs from the data processing pipeline.
    Looks for JSON log files in the specified directory that contain
    exclusion events from T012 (clean.py) and T013 (descriptors.py).
    """
    logs = []
    if not log_dir.exists():
        logging.warning(f"Log directory {log_dir} does not exist. No logs to load.")
        return logs

    # We expect logs to be JSON lines or JSON arrays within the log directory
    # Based on typical pipeline patterns, we look for specific log files generated by clean.py and descriptors.py
    # Assuming the clean.py and descriptors.py write to a 'logs' subdirectory or specific files
    
    # Strategy: Look for files ending in .log or .json that contain exclusion data
    # If the previous tasks (T012, T013) were implemented correctly, they should have written
    # to a standardized location. Let's assume they write to {log_dir}/exclusions_*.json
    
    exclusion_files = list(log_dir.glob("exclusions_*.json"))
    
    if not exclusion_files:
        # Fallback: Check if the log_dir itself contains the JSON data directly or if we need to scan subdirs
        # If T012/T013 wrote to a specific path defined in config, we should use that.
        # For now, let's assume a standard structure: data/logs/exclusions_clean.json and data/logs/exclusions_descriptors.json
        potential_clean = log_dir / "exclusions_clean.json"
        potential_desc = log_dir / "exclusions_descriptors.json"
        
        if potential_clean.exists():
            exclusion_files.append(potential_clean)
        if potential_desc.exists():
            exclusion_files.append(potential_desc)
        
        # If still nothing, try scanning for any json file with 'exclusion' in name recursively
        if not exclusion_files:
            for f in log_dir.rglob("*.json"):
                if "exclusion" in f.name:
                    exclusion_files.append(f)

    for file_path in exclusion_files:
        try:
            with open(file_path, 'r', encoding='utf-8') as f:
                content = f.read().strip()
                if not content:
                    continue
                # Try loading as JSON array first
                try:
                    data = json.loads(content)
                    if isinstance(data, list):
                        logs.extend(data)
                    elif isinstance(data, dict) and 'exclusions' in data:
                        logs.extend(data['exclusions'])
                    else:
                        # Maybe a single object?
                        logs.append(data)
                except json.JSONDecodeError:
                    # Try line-by-line JSON (JSONL)
                    for line in content.splitlines():
                        line = line.strip()
                        if not line:
                            continue
                        try:
                            entry = json.loads(line)
                            logs.append(entry)
                        except json.JSONDecodeError:
                            logging.warning(f"Failed to parse JSON line in {file_path}: {line}")
        except Exception as e:
            logging.error(f"Failed to load log file {file_path}: {e}")

    return logs

def map_error_reason(raw_reason: str) -> str:
    """
    Maps raw error strings from T012 and T013 to the standardized schema reasons.
    """
    raw_lower = raw_reason.lower()
    
    # Mapping from T012 (clean.py)
    if 'canonicalization failed' in raw_lower or 'canonicalize' in raw_lower:
        return 'canonicalization_error'
    if 'primary substrate' in raw_lower or 'filter primary' in raw_lower:
        return 'primary_substrate_filtered'
    if 'steric' in raw_lower and 'filter' in raw_lower:
        return 'steric_hindrance_exceeded'
    
    # Mapping from T013 (descriptors.py)
    if 'gasteiger' in raw_lower:
        if 'convergence' in raw_lower:
            return 'gasteiger_convergence_error'
        return 'gasteiger_calculation_error'
    if 'topological' in raw_lower:
        return 'topological_index_error'
    
    # Generic fallbacks
    if 'missing' in raw_lower:
        return 'missing_data'
    if 'invalid' in raw_lower:
        return 'invalid_data'
    
    return raw_reason  # Return original if no specific mapping found

def generate_exclusion_report(
    logs: List[Dict[str, Any]], 
    output_path: Path
) -> None:
    """
    Generates the final exclusion report CSV matching exclusion_report.schema.yaml.
    Columns: row_index, reason, original_smiles
    """
    output_path.parent.mkdir(parents=True, exist_ok=True)
    
    with open(output_path, 'w', newline='', encoding='utf-8') as csvfile:
        fieldnames = ['row_index', 'reason', 'original_smiles']
        writer = csv.DictWriter(csvfile, fieldnames=fieldnames)
        writer.writeheader()
        
        processed_rows = set() # To avoid duplicates if logs overlap
        
        for log_entry in logs:
            # Extract fields safely
            row_idx = log_entry.get('row_index') or log_entry.get('index')
            reason = log_entry.get('reason') or log_entry.get('error')
            smiles = log_entry.get('smiles') or log_entry.get('original_smiles')
            
            if row_idx is None:
                # Try to infer from log context if possible, otherwise skip
                continue
            
            # Create a unique key to prevent duplicate entries if logs are merged
            unique_key = (str(row_idx), str(smiles))
            if unique_key in processed_rows:
                continue
            processed_rows.add(unique_key)
            
            mapped_reason = map_error_reason(reason) if reason else 'unknown_error'
            
            writer.writerow({
                'row_index': row_idx,
                'reason': mapped_reason,
                'original_smiles': smiles or ''
            })
    
    logging.info(f"Exclusion report generated: {output_path}")

def main():
    parser = argparse.ArgumentParser(description="Generate exclusion report from pipeline logs.")
    parser.add_argument("--log-dir", type=str, default="data/logs", 
                        help="Directory containing exclusion logs from T012 and T013")
    parser.add_argument("--output", type=str, default="data/processed/exclusion_report.csv",
                        help="Output path for the exclusion report CSV")
    args = parser.parse_args()
    
    setup_logging()
    logger = get_logger(__name__)
    
    log_dir = Path(args.log_dir)
    output_path = Path(args.output)
    
    logger.info(f"Loading exclusion logs from {log_dir}...")
    logs = load_exclusion_logs(log_dir)
    logger.info(f"Loaded {len(logs)} exclusion entries.")
    
    if not logs:
        logger.warning("No exclusion logs found. Generating empty report.")
        # Still create the file with headers to satisfy schema expectations
        output_path.parent.mkdir(parents=True, exist_ok=True)
        with open(output_path, 'w', newline='', encoding='utf-8') as f:
            writer = csv.DictWriter(f, fieldnames=['row_index', 'reason', 'original_smiles'])
            writer.writeheader()
        return
    
    logger.info(f"Generating exclusion report at {output_path}...")
    generate_exclusion_report(logs, output_path)
    
    logger.info("Task T015 completed successfully.")

if __name__ == "__main__":
    main()
