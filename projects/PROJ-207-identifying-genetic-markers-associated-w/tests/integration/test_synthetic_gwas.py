def test_gwas_output_schema(setup_pipeline):
    """Verify the GWAS output matches the expected schema."""
    gwas_file = INTERIM_DIR / "gwas_raw.tsv"
    if not gwas_file.exists():
        pytest.skip("GWAS results not generated.")
    
    with open(gwas_file, "r") as f:
        header = f.readline().strip().split("\t")
    
    expected_cols = ["CHROM", "POS", "ID", "REF", "ALT", "A1", "TEST", "OBS_CT", "OR", "SE", "L95", "U95", "STAT", "P"]
    for col in expected_cols:
        assert col in header, f"Missing expected column: {col}"
    
    # Check at least one data row
    with open(gwas_file, "r") as f:
        lines = f.readlines()
    assert len(lines) > 1, "GWAS output is empty (no data rows)"