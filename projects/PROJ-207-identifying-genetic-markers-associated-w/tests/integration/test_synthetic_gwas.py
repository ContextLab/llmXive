def run_script(script_name: str, args: list = None):
    """Helper to run a script and check exit code."""
    cmd = [sys.executable, str(CODE_DIR / script_name)]
    if args:
        cmd.extend(args)
    
    result = subprocess.run(cmd, capture_output=True, text=True)
    if result.returncode != 0:
        pytest.fail(f"Script {script_name} failed:\n{result.stderr}")
    return result

def run_shell_script(script_name: str):
    """Helper to run a shell script."""
    cmd = ["bash", str(CODE_DIR / script_name)]
    result = subprocess.run(cmd, capture_output=True, text=True)
    if result.returncode != 0:
        pytest.fail(f"Shell script {script_name} failed:\n{result.stderr}")
    return result

