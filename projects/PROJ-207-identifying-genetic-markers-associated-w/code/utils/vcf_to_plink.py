"""
T015: Convert VCF to PLINK format.

Reads data/interim/variants.vcf (generated by T014 alignment/calling or T009 synthetic)
and outputs PLINK binary files:
- data/processed/synthetic.fam
- data/processed/synthetic.bim
- data/processed/synthetic.bed

Requires PLINK 1.9 or 2.0 to be installed and available in PATH.
Implements FR-003: Data format conversion for GWAS compatibility.
"""
import os
import sys
import subprocess
from pathlib import Path

PROJECT_ROOT = Path(__file__).parent.parent.parent
DATA_DIR = PROJECT_ROOT / "data"
INTERIM_DIR = DATA_DIR / "interim"
PROCESSED_DIR = DATA_DIR / "processed"

# Input: Output from T014 (real) or T009 (synthetic)
INPUT_VCF = INTERIM_DIR / "variants.vcf"
OUTPUT_PREFIX = PROCESSED_DIR / "synthetic"

def check_plink_availability():
    """
    Check if PLINK 1.9 or 2.0 is available in PATH.
    Returns the command to use ('plink' or 'plink2') or raises an error.
    """
    plink_cmd = None
    
    # Try PLINK 2.0 first
    try:
        result = subprocess.run(
            ["plink2", "--version"],
            capture_output=True,
            text=True,
            check=True
        )
        plink_cmd = "plink2"
        print(f"Found PLINK 2.0: {result.stdout.split(chr(10))[0]}")
        return plink_cmd
    except (subprocess.CalledProcessError, FileNotFoundError):
        pass
    
    # Try PLINK 1.9
    try:
        result = subprocess.run(
            ["plink", "--version"],
            capture_output=True,
            text=True,
            check=True
        )
        plink_cmd = "plink"
        print(f"Found PLINK 1.9: {result.stdout.split(chr(10))[0]}")
        return plink_cmd
    except (subprocess.CalledProcessError, FileNotFoundError):
        pass
    
    raise RuntimeError(
        "Error: PLINK (1.9 or 2.0) not found in PATH. "
        "Please install PLINK via conda: conda install -c bioconda plink plink2"
    )

def validate_input_vcf(vcf_path: Path):
    """
    Basic validation of the input VCF file before conversion.
    Checks for existence and minimum file size to ensure it's not empty.
    """
    if not vcf_path.exists():
        raise FileNotFoundError(f"Input VCF not found: {vcf_path}")
    
    file_size = vcf_path.stat().st_size
    if file_size == 0:
        raise ValueError(f"Input VCF is empty: {vcf_path}")
    
    # Check for VCF header
    with open(vcf_path, 'r') as f:
        first_line = f.readline().strip()
        if not first_line.startswith("##fileformat=VCF"):
            raise ValueError(
                f"Invalid VCF format: {vcf_path} does not start with VCF header. "
                f"First line: {first_line}"
            )

def convert_vcf_to_plink(vcf_path: Path, output_prefix: Path, plink_cmd: str):
    """
    Execute PLINK conversion from VCF to binary PED (bed/bim/fam).
    
    Args:
        vcf_path: Path to input VCF file
        output_prefix: Path prefix for output files (without extension)
        plink_cmd: PLINK command to use ('plink' or 'plink2')
    
    Returns:
        True if conversion successful
    
    Raises:
        RuntimeError if PLINK execution fails
    """
    cmd = [
        plink_cmd,
        "--vcf", str(vcf_path),
        "--make-bed",
        "--double-id",  # Avoid ID issues if sample IDs are missing
        "--allow-extra-chr", # Allow non-standard chromosome names
        "--out", str(output_prefix)
    ]
    
    print(f"Executing: {' '.join(cmd)}")
    result = subprocess.run(
        cmd,
        capture_output=True,
        text=True
    )
    
    if result.stdout:
        print(result.stdout)
    if result.stderr:
        print(result.stderr, file=sys.stderr)
    
    if result.returncode != 0:
        raise RuntimeError(
            f"PLINK conversion failed with code {result.returncode}.\n"
            f"STDOUT: {result.stdout}\n"
            f"STDERR: {result.stderr}"
        )
    
    return True

def verify_outputs(output_prefix: Path):
    """
    Verify that all expected PLINK output files were created.
    
    Args:
        output_prefix: Path prefix for output files
    
    Raises:
        FileNotFoundError if any expected file is missing
    """
    expected_files = [
        f"{output_prefix}.fam",
        f"{output_prefix}.bim",
        f"{output_prefix}.bed"
    ]
    
    missing = []
    for f in expected_files:
        if not os.path.exists(f):
            missing.append(f)
    
    if missing:
        raise FileNotFoundError(
            f"PLINK conversion incomplete. Missing files: {missing}\n"
            f"Expected: {expected_files}"
        )
    
    # Log file sizes for verification
    for f in expected_files:
        size = os.path.getsize(f)
        print(f"  Created: {f} ({size:,} bytes)")

def main():
    """Main entry point for T015."""
    print("=" * 60)
    print("T015: VCF to PLINK Conversion")
    print("FR-003: Data format conversion for GWAS compatibility")
    print("=" * 60)
    
    # Ensure output directory exists
    PROCESSED_DIR.mkdir(parents=True, exist_ok=True)
    
    try:
        # Validate input
        print(f"Input VCF: {INPUT_VCF}")
        validate_input_vcf(INPUT_VCF)
        
        # Check PLINK availability
        plink_cmd = check_plink_availability()
        
        # Perform conversion
        print("Starting conversion...")
        convert_vcf_to_plink(INPUT_VCF, OUTPUT_PREFIX, plink_cmd)
        
        # Verify outputs
        print("Verifying outputs...")
        verify_outputs(OUTPUT_PREFIX)
        
        print("=" * 60)
        print("SUCCESS: VCF converted to PLINK binary format.")
        print(f"Output prefix: {OUTPUT_PREFIX}")
        print("Files ready for downstream GWAS analysis (T017).")
        print("=" * 60)
        
    except FileNotFoundError as e:
        print(f"ERROR: {e}", file=sys.stderr)
        sys.exit(1)
    except ValueError as e:
        print(f"VALIDATION ERROR: {e}", file=sys.stderr)
        sys.exit(1)
    except RuntimeError as e:
        print(f"CONVERSION ERROR: {e}", file=sys.stderr)
        sys.exit(1)
    except Exception as e:
        print(f"UNEXPECTED ERROR: {e}", file=sys.stderr)
        sys.exit(1)

if __name__ == "__main__":
    main()