import os
import sys
import subprocess
import random
from pathlib import Path

def create_dummy_reference(output_path: Path):
    """Creates a minimal dummy reference FASTA file for dwgsim simulation."""
    # Create a small synthetic reference sequence (1000 bp)
    # In a real scenario, this would be the honeybee genome (e.g., Amel_HAv3.1)
    header = ">dummy_reference_1\n"
    # Generate a deterministic sequence based on a seed
    random.seed(42)
    bases = ['A', 'C', 'G', 'T']
    sequence = ''.join(random.choices(bases, k=1000))
    
    output_path.parent.mkdir(parents=True, exist_ok=True)
    with open(output_path, 'w') as f:
        f.write(header)
        # Write sequence in lines of 60 chars (FASTA standard)
        for i in range(0, len(sequence), 60):
            f.write(sequence[i:i+60] + "\n")
    return output_path

def main():
    """
    Simulates FASTQ files from a synthetic VCF using dwgsim.
    Reads: data/interim/synthetic.vcf (generated by T009)
    Outputs: data/interim/synthetic_R1.fastq, data/interim/synthetic_R2.fastq
    """
    # Paths
    project_root = Path(__file__).parent.parent
    vcf_path = project_root / "data" / "interim" / "synthetic.vcf"
    ref_path = project_root / "data" / "interim" / "synthetic_ref.fasta"
    out_prefix = project_root / "data" / "interim" / "synthetic"
    
    # Ensure output directory exists
    (project_root / "data" / "interim").mkdir(parents=True, exist_ok=True)

    # Check if VCF exists (generated by T009)
    if not vcf_path.exists():
        print(f"Error: Input VCF not found at {vcf_path}. Run T009 first.")
        sys.exit(1)

    # Create dummy reference if not exists
    if not ref_path.exists():
        print(f"Creating dummy reference at {ref_path}...")
        create_dummy_reference(ref_path)

    # Check if dwgsim is available
    try:
        subprocess.run(["dwgsim", "--help"], capture_output=True, check=True)
    except (subprocess.CalledProcessError, FileNotFoundError):
        print("Error: 'dwgsim' command not found. Please install dwgsim.")
        sys.exit(1)

    # Run dwgsim
    # -N: number of reads
    # -1, -2: read lengths
    # -e: error rate
    # -r: rate of indels
    # -R: fraction of reads with indels
    # -X: fraction of reads with errors
    # -f: fraction of reads to skip
    # -y: use random seed
    # -z: seed for random number generator (42 as required)
    cmd = [
        "dwgsim",
        "-N", "1000",       # 1000 reads
        "-1", "150",        # Read 1 length
        "-2", "150",        # Read 2 length
        "-e", "0.01",       # Error rate
        "-r", "0.001",      # Indel rate
        "-R", "0.5",        # Fraction of reads with indels
        "-X", "0.0",        # Fraction of reads with errors (0.0 to use -e)
        "-f", "0.0",        # Fraction of reads to skip
        "-y",               # Use random seed
        "-z", "42",         # Seed 42
        str(ref_path),
        str(out_prefix)
    ]

    print(f"Running: {' '.join(cmd)}")
    try:
        subprocess.run(cmd, check=True)
    except subprocess.CalledProcessError as e:
        print(f"Error running dwgsim: {e}")
        sys.exit(1)

    # Verify outputs
    r1_path = Path(f"{out_prefix}.1.fq")
    r2_path = Path(f"{out_prefix}.2.fq")
    
    if r1_path.exists() and r2_path.exists():
        print(f"Successfully generated FASTQ files:")
        print(f"  {r1_path}")
        print(f"  {r2_path}")
        # Rename to expected names if necessary (dwgsim outputs .1.fq/.2.fq)
        # The task expects synthetic_R1.fastq and synthetic_R2.fastq
        # We will rename them to match the task requirement
        final_r1 = project_root / "data" / "interim" / "synthetic_R1.fastq"
        final_r2 = project_root / "data" / "interim" / "synthetic_R2.fastq"
        
        if not final_r1.exists():
            r1_path.rename(final_r1)
        if not final_r2.exists():
            r2_path.rename(final_r2)
        
        print(f"Renamed to:")
        print(f"  {final_r1}")
        print(f"  {final_r2}")
    else:
        print(f"Error: Expected FASTQ files not found after dwgsim run.")
        sys.exit(1)

if __name__ == "__main__":
    main()