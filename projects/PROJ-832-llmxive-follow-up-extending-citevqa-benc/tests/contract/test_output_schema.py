"""
Contract test for User Story 1 (T012): Output Schema Validation.

This test verifies that the text-only pipeline (retrieval + reasoning)
produces outputs strictly adhering to the defined schema:
{
    "answer": str,
    "chunk_id": str (or int, but must be present and non-null),
    "query": str (optional context),
    "confidence": float (optional)
}

It runs against the actual pipeline logic in code/reasoning.py (once implemented)
or validates the structure of generated result files from T019 if the pipeline
has already been executed.

Since T013/T014 (implementation) are not yet marked complete in this task context,
this test assumes the existence of a 'pipeline_run' function or validates the
structure of data/results/text_pipeline_results.json if it exists.

To ensure this test is runnable and meaningful as a contract test:
1. It attempts to import the reasoning module.
2. If the reasoning module exists, it runs a mock inference or validates the
   output of a sample run.
3. If the reasoning module does not exist yet, it validates the schema of
   any existing result file in data/results/text_pipeline_results.json.
"""
import json
import os
import sys
from pathlib import Path
from typing import Any, Dict, List, Union

# Add project root to path for imports
project_root = Path(__file__).resolve().parent.parent.parent
sys.path.insert(0, str(project_root))

# Import configuration to find data paths
try:
    from config import get_config_dict
    config = get_config_dict()
    DATA_RESULTS_DIR = Path(config.get("data_results_dir", "data/results"))
except ImportError:
    # Fallback if config is not ready
    DATA_RESULTS_DIR = project_root / "data" / "results"


def validate_schema(record: Dict[str, Any], record_id: Union[int, str]) -> List[str]:
    """
    Validates a single record against the required output schema.

    Args:
        record: The dictionary to validate.
        record_id: Identifier for error reporting.

    Returns:
        List of error messages. Empty if valid.
    """
    errors = []

    # Required fields
    required_fields = ["answer", "chunk_id"]

    for field in required_fields:
        if field not in record:
            errors.append(f"Record {record_id}: Missing required field '{field}'")
            continue

        value = record[field]

        if field == "answer":
            if not isinstance(value, str):
                errors.append(f"Record {record_id}: Field 'answer' must be a string, got {type(value).__name__}")
        elif field == "chunk_id":
            # chunk_id can be string or int, but must not be None
            if value is None:
                errors.append(f"Record {record_id}: Field 'chunk_id' cannot be null")
            elif not isinstance(value, (str, int)):
                errors.append(f"Record {record_id}: Field 'chunk_id' must be str or int, got {type(value).__name__}")

    # Optional but expected fields check (warning level, not error for contract strictness,
    # but we ensure they are valid types if present)
    optional_types = {
        "query": str,
        "confidence": (int, float),
        "retrieved_chunks": list,
        "reasoning_trace": str
    }

    for field, expected_type in optional_types.items():
        if field in record:
            if not isinstance(record[field], expected_type):
                errors.append(f"Record {record_id}: Field '{field}' has invalid type {type(record[field]).__name__}, expected {expected_type}")

    return errors


def test_contract_output_schema_from_file():
    """
    Contract test: Validates the schema of results generated by the pipeline.
    If the pipeline results file does not exist, this test assumes the pipeline
    hasn't run yet and attempts to validate a single sample run if the reasoning
    module is available.
    """
    results_path = DATA_RESULTS_DIR / "text_pipeline_results.json"

    # Case 1: Results file exists
    if results_path.exists():
        with open(results_path, "r", encoding="utf-8") as f:
            try:
                data = json.load(f)
            except json.JSONDecodeError:
                assert False, "Results file exists but is not valid JSON"

        # Data might be a list of records or a dict with a 'results' key
        if isinstance(data, dict):
            if "results" in data:
                records = data["results"]
            else:
                # Assume the whole dict is a single record or a list of records
                records = [data] if not isinstance(data, list) else data
        elif isinstance(data, list):
            records = data
        else:
            assert False, "Results file structure is unknown"

        all_errors = []
        for idx, record in enumerate(records):
            if not isinstance(record, dict):
                all_errors.append(f"Record {idx}: Not a dictionary")
                continue
            errors = validate_schema(record, idx)
            all_errors.extend(errors)

        assert len(all_errors) == 0, f"Schema validation failed:\n" + "\n".join(all_errors)
        return

    # Case 2: Results file does not exist yet (Pipeline not run)
    # We attempt to run a minimal check if the reasoning module is available.
    # This ensures the contract test fails if the implementation is missing
    # or produces wrong types, rather than just skipping.
    reasoning_path = project_root / "code" / "reasoning.py"
    if reasoning_path.exists():
        try:
            # Dynamic import to check for the function signature
            from reasoning import run_reasoning_step # Assuming this function exists in T014

            # Create a mock input
            mock_query = "What is the function of the gene?"
            mock_context = [
                {"chunk_id": "1", "text": "The gene regulates cell growth."},
                {"chunk_id": "2", "text": "It is involved in apoptosis."}
            ]

            # Run a single step
            try:
                output = run_reasoning_step(mock_query, mock_context)
            except Exception as e:
                # If the function exists but fails to run due to model loading,
                # we check the error type. If it's a model loading error, we might
                # skip the schema check, but if it's a TypeError/AttributeError,
                # it's a code issue.
                # For a contract test, we primarily care about the output format
                # if the function returns something.
                # If it crashes before returning, the test environment (T014)
                # should handle the crash. Here we assume it returns.
                if "model" in str(e).lower() or "cuda" in str(e).lower():
                    # Model loading error, skip schema check for this specific run
                    # but assert that the function exists and is callable.
                    assert callable(run_reasoning_step), "run_reasoning_step must be callable"
                    return
                else:
                    raise e

            # Validate the output
            errors = validate_schema(output, "mock_run")
            assert len(errors) == 0, f"Mock run schema validation failed:\n" + "\n".join(errors)

        except ImportError:
            # Function not found, but file exists. This is a partial implementation.
            # The test should fail to indicate the contract is not met.
            assert False, "reasoning.py exists but 'run_reasoning_step' function not found."
        except FileNotFoundError:
            # Dependencies missing (e.g., model weights), which is expected if not downloaded.
            # We assert that the function signature is correct by checking the source?
            # Hard to do without executing. We assume if the file exists, T014 is in progress.
            # For a strict contract test, we fail if we can't verify.
            assert False, "Cannot verify contract: Model weights or dependencies missing for reasoning."
    else:
        # Neither results file nor reasoning module exists.
        # This is a "pending" state. In a CI/CD pipeline, this might be a failure
        # if the task is marked "done". Here, we assert False to indicate
        # the contract is not yet satisfied.
        assert False, "Contract test cannot pass: No results file and no reasoning module found."


if __name__ == "__main__":
    test_contract_output_schema_from_file()
    print("Contract test passed: Output schema is valid.")