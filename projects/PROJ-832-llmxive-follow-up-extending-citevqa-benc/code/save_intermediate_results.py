"""
T019: Save intermediate results (answers, predicted IDs) to data/results/text_pipeline_results.json

This script loads the raw output from the text-only pipeline evaluation (generated by main.py
or process_test_set in reasoning.py), structures it, and saves it to the designated results file.
It ensures the directory exists and handles potential errors in loading the source data.
"""
import os
import json
import logging
import traceback
from pathlib import Path
from typing import List, Dict, Any, Optional

# Import from existing project API
from config import get_config_dict
from main import load_test_set, run_evaluation_loop, save_results
from reasoning import process_test_set

logger = logging.getLogger(__name__)

def load_evaluation_results(config: Dict[str, Any]) -> Optional[List[Dict[str, Any]]]:
    """
    Load the evaluation results generated by the text-only pipeline.
    If the results file doesn't exist, it runs the evaluation first.
    """
    results_path = Path(config["paths"]["results"]) / "text_pipeline_raw_results.json"
    
    if not results_path.exists():
        logger.warning(f"Results file not found at {results_path}. Running evaluation...")
        # Run the evaluation to generate the results
        test_set = load_test_set(config)
        if not test_set:
            logger.error("Failed to load test set. Cannot proceed.")
            return None
        
        # Run the evaluation loop which processes the test set
        # Note: We assume run_evaluation_loop or process_test_set generates the raw results
        # For T019, we need the output of the reasoning step: answers and predicted chunk IDs
        results = run_evaluation_loop(test_set, config)
        
        if not results:
            logger.error("Evaluation loop returned no results.")
            return None
        
        # Save raw results first so we have a source for T019
        with open(results_path, 'w', encoding='utf-8') as f:
            json.dump(results, f, indent=2)
        logger.info(f"Raw evaluation results saved to {results_path}")
    else:
        logger.info(f"Loading existing results from {results_path}")
        with open(results_path, 'r', encoding='utf-8') as f:
            results = json.load(f)
    
    return results

def format_results_for_saving(results: List[Dict[str, Any]]) -> List[Dict[str, Any]]:
    """
    Format the raw results into the structure required for T019:
    A list of objects containing:
    - query_id (or similar identifier)
    - ground_truth_answer
    - predicted_answer
    - predicted_chunk_id
    - ground_truth_chunk_id (optional, for reference)
    """
    formatted = []
    for item in results:
        # Ensure we extract the specific fields required by T019
        # The structure depends on what run_evaluation_loop returns.
        # We assume it returns a list of dicts with keys like 'answer', 'predicted_chunk_id', etc.
        
        entry = {
            "query_id": item.get("query_id", item.get("id", "unknown")),
            "ground_truth_answer": item.get("ground_truth_answer", item.get("answer", "")),
            "predicted_answer": item.get("predicted_answer", item.get("response", "")),
            "predicted_chunk_id": item.get("predicted_chunk_id", item.get("chunk_id", None)),
            "ground_truth_chunk_id": item.get("ground_truth_chunk_id", item.get("gt_chunk_id", None)),
            # Include any other relevant metadata if present
            "metadata": item.get("metadata", {})
        }
        formatted.append(entry)
    return formatted

def save_intermediate_results(config: Dict[str, Any], results: List[Dict[str, Any]]) -> bool:
    """
    Save the formatted results to data/results/text_pipeline_results.json
    """
    output_dir = Path(config["paths"]["results"])
    output_dir.mkdir(parents=True, exist_ok=True)
    
    output_path = output_dir / "text_pipeline_results.json"
    
    try:
        with open(output_path, 'w', encoding='utf-8') as f:
            json.dump(results, f, indent=2, ensure_ascii=False)
        logger.info(f"Intermediate results successfully saved to {output_path}")
        return True
    except Exception as e:
        logger.error(f"Failed to save intermediate results: {e}")
        traceback.print_exc()
        return False

def main():
    """
    Main entry point for T019.
    """
    # Setup logging
    logging.basicConfig(
        level=logging.INFO,
        format='%(asctime)s - %(name)s - %(levelname)s - %(message)s'
    )
    
    config = get_config_dict()
    logger.info("Starting T019: Save intermediate results")
    
    # Load or generate the raw evaluation results
    raw_results = load_evaluation_results(config)
    if not raw_results:
        logger.error("Could not obtain evaluation results. Aborting T019.")
        return 1
    
    # Format the results
    formatted_results = format_results_for_saving(raw_results)
    
    # Save to the specific file required by T019
    success = save_intermediate_results(config, formatted_results)
    
    if not success:
        logger.error("T019 failed to save results.")
        return 1
    
    logger.info("T019 completed successfully.")
    return 0

if __name__ == "__main__":
    exit(main())