"""
T024: Generate distribution plot of IoU scores and SAA failure modes.

This script reads the SAA evaluation results generated by T022/T023a,
calculates the 'Attribution Hallucination' failure mode (where the model
answers correctly but points to the wrong box), and generates a distribution
plot of IoU scores with annotated failure modes.

Output:
    data/results/saa_analysis.png
    data/results/hallucination_rate.json (for T024b)
"""
import os
import json
import logging
from pathlib import Path
from typing import Dict, List, Any, Tuple
import numpy as np
import matplotlib.pyplot as plt
import seaborn as sns

# Configure logging
logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def load_saa_results(filepath: str) -> List[Dict[str, Any]]:
    """Load the SAA evaluation results JSON."""
    path = Path(filepath)
    if not path.exists():
        raise FileNotFoundError(f"Results file not found: {filepath}")
    
    with open(path, 'r') as f:
        data = json.load(f)
    
    # Handle if data is wrapped in a key like 'results' or is a list directly
    if isinstance(data, dict) and 'results' in data:
        return data['results']
    elif isinstance(data, list):
        return data
    else:
        raise ValueError(f"Unexpected data format in {filepath}")

def load_baseline_saa(filepath: str) -> float:
    """Load the baseline SAA scalar."""
    path = Path(filepath)
    if not path.exists():
        # Fallback for testing if baseline not strictly present, though task implies it exists
        logger.warning(f"Baseline file not found at {filepath}, using 0.0 as placeholder for plot context.")
        return 0.0
    
    with open(path, 'r') as f:
        data = json.load(f)
    
    if 'baseline_saa' in data:
        return float(data['baseline_saa'])
    elif 'value' in data:
        return float(data['value'])
    else:
        raise ValueError(f"Baseline SAA value not found in {filepath}")

def calculate_hallucination_rate(results: List[Dict[str, Any]]) -> Tuple[float, int, int]:
    """
    Calculate the 'Attribution Hallucination' failure rate.
    
    Definition (based on SC-003 and task description):
    Count of cases where:
      1. Answer is Correct (Exact Match OR Semantic Similarity >= 0.85)
      2. Spatial Correctness is False (IoU <= 0.5)
    
    Returns:
        rate: float (count / total_correct_answers)
        hallucination_count: int
        total_correct_answers: int
    """
    hallucination_count = 0
    total_correct_answers = 0

    for item in results:
        # Determine if answer is correct
        # The SAA metric logic (from T022) is: Correct Answer AND Spatial Correctness
        # We need to decompose this.
        # We assume the results JSON contains 'semantic_similarity' and 'iou' or 'is_correct' and 'is_spatially_correct'
        
        # Check for explicit flags first
        if 'answer_correct' in item and 'spatial_correct' in item:
            is_ans_correct = item['answer_correct']
            is_spatial_correct = item['spatial_correct']
        else:
            # Fallback to calculating from raw metrics if flags aren't present
            # Thresholds: Semantic Sim >= 0.85, IoU > 0.5
            sim = item.get('semantic_similarity', 0.0)
            iou = item.get('iou', 0.0)
            is_ans_correct = sim >= 0.85 or (item.get('exact_match', False))
            is_spatial_correct = iou > 0.5

        if is_ans_correct:
            total_correct_answers += 1
            # Hallucination: Correct answer, but wrong spatial attribution
            if not is_spatial_correct:
                hallucination_count += 1

    if total_correct_answers == 0:
        return 0.0, 0, 0

    rate = hallucination_count / total_correct_answers
    return rate, hallucination_count, total_correct_answers

def generate_plot(results: List[Dict[str, Any]], baseline_saa: float, output_path: str):
    """
    Generate the distribution plot of IoU scores and SAA failure modes.
    """
    # Prepare data
    iou_scores = []
    labels = []
    hallucination_indices = []

    for i, item in enumerate(results):
        iou = item.get('iou', 0.0)
        iou_scores.append(iou)
        
        # Determine label for plotting
        # SAA = Correct Answer AND IoU > 0.5
        # We need to know if the answer was correct to distinguish "Success" vs "Hallucination" vs "Wrong Answer"
        
        # Re-evaluate correctness logic
        sim = item.get('semantic_similarity', 0.0)
        is_ans_correct = sim >= 0.85 or item.get('exact_match', False)
        is_spatial_correct = iou > 0.5

        if is_ans_correct and is_spatial_correct:
            labels.append('SAA Success')
        elif is_ans_correct and not is_spatial_correct:
            labels.append('Attribution Hallucination')
            hallucination_indices.append(i)
        else:
            labels.append('Wrong Answer')

    # Setup plot
    plt.figure(figsize=(12, 7))
    sns.set_style("whitegrid")
    
    # Create a categorical plot or histogram
    # We will use a stripplot or swarmplot to show distribution, colored by failure mode
    df = None
    try:
        import pandas as pd
        df = pd.DataFrame({
            'IoU': iou_scores,
            'Category': labels
        })
        
        # Use boxplot or violinplot to show distribution + strip for individual points
        sns.violinplot(x='Category', y='IoU', data=df, palette="muted", inner="box")
        sns.stripplot(x='Category', y='IoU', data=df, color="black", alpha=0.3, size=3, jitter=True)
        
        plt.title(f'IoU Distribution by SAA Failure Mode\n(Baseline SAA: {baseline_saa:.4f})', fontsize=14)
        plt.ylabel('Intersection over Union (IoU)', fontsize=12)
        plt.xlabel('Failure Mode Category', fontsize=12)
        plt.ylim(-0.05, 1.05) # IoU is between 0 and 1
        
    except ImportError:
        # Fallback if pandas not available (though it is in requirements)
        logger.warning("Pandas not found, using matplotlib only for simple histogram.")
        # Simple histogram approach
        plt.hist(iou_scores, bins=20, alpha=0.7, label='All Samples')
        plt.axhline(baseline_saa, color='red', linestyle='--', label=f'Baseline SAA ({baseline_saa:.4f})')
        plt.title('IoU Distribution')
        plt.xlabel('IoU Score')
        plt.ylabel('Frequency')
        plt.legend()

    # Add annotation for hallucination count if using the detailed plot
    if df is not None:
        hallucination_count = len(hallucination_indices)
        total = len(results)
        rate = hallucination_count / total if total > 0 else 0
        plt.annotate(
            f'Attribution Hallucination Rate: {rate:.2%} ({hallucination_count}/{total})',
            xy=(0.02, 0.98),
            xycoords='axes fraction',
            fontsize=10,
            verticalalignment='top',
            bbox=dict(boxstyle='round', facecolor='wheat', alpha=0.5)
        )

    plt.tight_layout()
    
    # Ensure output directory exists
    Path(output_path).parent.mkdir(parents=True, exist_ok=True)
    plt.savefig(output_path, dpi=300)
    plt.close()
    logger.info(f"Plot saved to {output_path}")

def main():
    """Main entry point for T024."""
    # Paths
    project_root = Path(__file__).parent.parent
    results_path = project_root / "data" / "results" / "saa_evaluation_results.json"
    baseline_path = project_root / "data" / "baseline_saa.json"
    output_plot_path = project_root / "data" / "results" / "saa_analysis.png"
    hallucination_output_path = project_root / "data" / "results" / "hallucination_rate.json"

    logger.info(f"Loading results from {results_path}")
    try:
        results = load_saa_results(str(results_path))
    except FileNotFoundError as e:
        logger.error(f"Cannot proceed: {e}")
        raise

    logger.info(f"Loading baseline from {baseline_path}")
    try:
        baseline_saa = load_baseline_saa(str(baseline_path))
    except FileNotFoundError:
        baseline_saa = 0.0 # Fallback if missing, though task implies it exists

    # Calculate Hallucination Rate (T024b requirement, saved here)
    rate, h_count, total_correct = calculate_hallucination_rate(results)
    logger.info(f"Attribution Hallucination Rate: {rate:.4f} ({h_count} / {total_correct})")

    # Save hallucination rate JSON
    hallucination_data = {
        "hallucination_rate": rate,
        "hallucination_count": h_count,
        "total_correct_answers": total_correct,
        "description": "Rate of cases where answer is correct but spatial attribution (IoU) is incorrect."
    }
    Path(hallucination_output_path).parent.mkdir(parents=True, exist_ok=True)
    with open(hallucination_output_path, 'w') as f:
        json.dump(hallucination_data, f, indent=2)
    logger.info(f"Hallucination rate saved to {hallucination_output_path}")

    # Generate Plot
    logger.info("Generating SAA analysis plot...")
    generate_plot(results, baseline_saa, str(output_plot_path))

    logger.info("T024 completed successfully.")

if __name__ == "__main__":
    main()
