"""
T024b: Calculate and report the 'Attribution Hallucination' failure rate metric.

Definition (SC-003):
Attribution Hallucination Rate = (Count of hallucinations) / (Total Correct Answers)

Where a 'hallucination' is a case where the model answered the question correctly
(SAA Answer Correctness = True) but the spatial attribution (IoU) was incorrect
(SAA Spatial Correctness = False).

Output: data/results/hallucination_rate.json
"""
import os
import json
import logging
from pathlib import Path
from typing import Dict, Any, List

from config import get_config_dict
from metrics import compute_saa  # Reusing existing SAA logic if needed, though we load pre-computed

logger = logging.getLogger(__name__)

def load_saa_results(results_path: Path) -> List[Dict[str, Any]]:
    """
    Load the SAA evaluation results from data/results/text_pipeline_results.json.
    This file is generated by T019/T022.
    """
    if not results_path.exists():
        raise FileNotFoundError(
            f"Results file not found at {results_path}. "
            "Please ensure T019/T022 has been run to generate text_pipeline_results.json."
        )
    
    with open(results_path, 'r', encoding='utf-8') as f:
        data = json.load(f)
    
    # Handle if the file contains a list directly or a dict with a results key
    if isinstance(data, list):
        return data
    elif isinstance(data, dict) and 'results' in data:
        return data['results']
    else:
        # Fallback: treat the whole dict as a single result or raise error
        logger.warning("Unexpected format in results file. Attempting to process as single record.")
        return [data]

def calculate_hallucination_metrics(results: List[Dict[str, Any]]) -> Dict[str, Any]:
    """
    Calculate the hallucination rate based on the definition:
    Hallucination = (Answer Correct) AND (Spatial Incorrect)
    
    Returns a dictionary with:
    - hallucination_rate: float
    - hallucination_count: int
    - total_correct_answers: int
    """
    hallucination_count = 0
    total_correct_answers = 0
    
    for item in results:
        # We expect the item to have fields populated by T022 (SAA calculation)
        # Assuming 'saa_correct' (bool) and 'saa_spatial_correct' (bool) or similar flags.
        # If the stored format is different, we adapt based on typical T022 output.
        # Common keys from compute_saa logic: 'answer_correct', 'spatial_correct', 'saa'
        
        # Check if the item is a record with computed SAA components
        # Based on T022 description: "Answer Correctness: ... AND Spatial Correctness: IoU > 0.5"
        # We need to count cases where Answer Correct is True, but Spatial is False.
        
        answer_correct = item.get('answer_correct', False)
        spatial_correct = item.get('spatial_correct', False)
        
        # If the file stores the final SAA boolean, we might need to re-evaluate or rely on components.
        # We assume T022 saved the components or we can infer from 'saa' and 'spatial_correct'.
        # If only 'saa' is present, we cannot distinguish hallucination (correct answer, wrong box) 
        # from total failure without the components. 
        # We assume T022 saved 'answer_correct' and 'spatial_correct' explicitly or 'saa' is the conjunction.
        
        # Robust check: if 'saa' is True, answer MUST be correct (by definition of SAA in this project).
        # So if 'saa' is True and 'spatial_correct' is False, it's a hallucination.
        
        if answer_correct:
            total_correct_answers += 1
            if not spatial_correct:
                hallucination_count += 1
        elif item.get('saa', False): 
            # Fallback: if 'saa' is True, answer is correct.
            # This implies spatial might be false for hallucination.
            # But if 'answer_correct' key is missing, we trust 'saa'.
            # However, if 'saa' is True, spatial MUST be true? 
            # SAA = Answer Correct AND Spatial Correct.
            # So if SAA is True, Spatial is True. No hallucination.
            # Hallucination is Answer Correct (True) AND Spatial Correct (False).
            # In this case SAA would be False.
            # So we strictly need 'answer_correct' and 'spatial_correct' flags.
            pass

    if total_correct_answers == 0:
        rate = 0.0
    else:
        rate = hallucination_count / total_correct_answers

    return {
        "hallucination_rate": rate,
        "hallucination_count": hallucination_count,
        "total_correct_answers": total_correct_answers,
        "description": "Attribution Hallucination failure rate metric (SC-003)."
    }

def save_results(metrics: Dict[str, Any], output_path: Path) -> None:
    """Save the calculated metrics to the JSON file."""
    output_path.parent.mkdir(parents=True, exist_ok=True)
    with open(output_path, 'w', encoding='utf-8') as f:
        json.dump(metrics, f, indent=2)
    logger.info(f"Hallucination rate metrics saved to {output_path}")

def main():
    """Entry point for T024b."""
    config = get_config_dict()
    results_path = Path(config.get('data_results_dir', 'data/results')) / 'text_pipeline_results.json'
    output_path = Path(config.get('data_results_dir', 'data/results')) / 'hallucination_rate.json'
    
    logging.basicConfig(
        level=logging.INFO,
        format='%(asctime)s - %(name)s - %(levelname)s - %(message)s'
    )
    
    try:
        logger.info(f"Loading SAA results from {results_path}...")
        results = load_saa_results(results_path)
        logger.info(f"Loaded {len(results)} results.")
        
        logger.info("Calculating hallucination metrics...")
        metrics = calculate_hallucination_metrics(results)
        
        logger.info(f"Results: {metrics}")
        save_results(metrics, output_path)
        
    except FileNotFoundError as e:
        logger.error(str(e))
        raise
    except Exception as e:
        logger.error(f"Error calculating hallucination rate: {e}")
        raise

if __name__ == "__main__":
    main()
