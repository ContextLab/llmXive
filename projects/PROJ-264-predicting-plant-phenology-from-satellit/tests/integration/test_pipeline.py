def create_mock_satellite_data(site_ids, start_date, end_date, freq="10D"):
    """
    Generates mock satellite data (NDVI/EVI) for testing alignment.
    Simulates the output of T011 (Sentinel data download).
    """
    dates = pd.date_range(start=start_date, end=end_date, freq=freq)
    records = []
    for site_id in site_ids:
        for date in dates:
            # Simulate some noise
            ndvi = np.clip(np.random.normal(0.4, 0.1), 0, 1)
            evi = np.clip(np.random.normal(0.3, 0.1), 0, 1)
            records.append({
                "site_id": site_id,
                "timestamp": date,
                "ndvi": ndvi,
                "evi": evi,
                "source": "sentinel_mock"
            })
    return pd.DataFrame(records)


