def align_temporal_data(sat_df, climate_df, phen_df):
    """
    Core logic to align disparate temporal resolutions.
    This is the logic being tested for T010.
    
    Strategy:
    1. Aggregate climate data (daily) to match satellite (10-day).
    2. Join satellite and climate on (site_id, timestamp).
    3. Map phenology events to the nearest satellite timestamp window.
    """
    # Ensure timestamp columns are datetime
    sat_df["timestamp"] = pd.to_datetime(sat_df["timestamp"])
    climate_df["timestamp"] = pd.to_datetime(climate_df["timestamp"])
    
    # 1. Aggregate Climate to 10-day (matching satellite)
    # Group by site_id and 10-day window
    climate_df["window"] = climate_df["timestamp"].dt.to_period("10D").dt.start_time
    climate_agg = climate_df.groupby(["site_id", "window"]).agg({
        "temp_mean": "mean",
        "precip_sum": "sum"
    }).reset_index()
    climate_agg.rename(columns={"window": "timestamp"}, inplace=True)
    
    # 2. Join Satellite and Climate
    merged = pd.merge(
        sat_df, 
        climate_agg, 
        on=["site_id", "timestamp"], 
        how="inner"
    )
    
    # 3. Handle Phenology (Event-based vs Time-series)
    # For this integration test, we verify that phenology events
    # can be associated with the nearest time-series row.
    if not phen_df.empty:
        phen_df["event_date"] = pd.to_datetime(phen_df["event_date"])
        # Simple nearest neighbor join for verification
