"""
analysis/correlations.py
-------------------------

This module implements the final step of the analysis pipeline for User Story 2:
merging the raw network‑metric columns (produced by the metric extraction steps
T021/T022) with the PCA factor scores (produced by T023a) into a single
DataFrame that is persisted as ``data/analysis/full_metrics.csv``.  The
resulting CSV is required by downstream reporting (FR‑004) and by the FDR
correction step (FR‑005).

The public API of this module (as declared in the project’s API surface) is:

* ``load_metrics_data`` – Load the raw per‑subject metric table.
* ``load_pca_scores``   – Load the PCA factor‑score table.
* ``merge_metrics_and_pca_scores`` – Merge the two tables on ``subject_id``
  and write the combined CSV.
* ``main`` – Orchestrate the three steps above when the module is executed
  as a script (or via ``code/analysis/correlations_main_runner.py``).

All file paths are hard‑coded to match the locations used by the rest of the
pipeline.  The implementation deliberately avoids any synthetic data or random
number generation – it works exclusively on real outputs generated by earlier
tasks.
"""

import logging
from pathlib import Path
from typing import List

import pandas as pd

# ----------------------------------------------------------------------
# Configuration – where the various artefacts live
# ----------------------------------------------------------------------
METRICS_DIR = Path("data/analysis")
RAW_METRICS_CANDIDATES: List[Path] = [
    METRICS_DIR / "metrics.csv",               # canonical name used by earlier steps
    METRICS_DIR / "subject_metrics.csv",       # fallback used by some older scripts
    METRICS_DIR / "raw_metrics.csv",           # another possible legacy name
]
PCA_SCORES_PATH = METRICS_DIR / "factor_scores.csv"
FULL_METRICS_PATH = METRICS_DIR / "full_metrics.csv"

# ----------------------------------------------------------------------
# Helper functions
# ----------------------------------------------------------------------
def _first_existing_path(candidates: List[Path]) -> Path:
    """
    Return the first path that exists among ``candidates``.
    Raises ``FileNotFoundError`` if none are present.
    """
    for p in candidates:
        if p.is_file():
            return p
    raise FileNotFoundError(
        f"None of the expected metric files were found. Checked: "
        f"{', '.join(str(p) for p in candidates)}"
    )

# ----------------------------------------------------------------------
# Public API
# ----------------------------------------------------------------------
def load_metrics_data() -> pd.DataFrame:
    """
    Load the per‑subject raw network‑metric table produced by the
    metric‑extraction stage (T021/T022).

    Returns
    -------
    pd.DataFrame
        Columns include at minimum ``subject_id`` and the four raw metrics:
        ``modularity``, ``global_efficiency``, ``participation_coef``,
        ``within_module_degree``.  Additional columns are preserved unchanged.
    """
    metrics_path = _first_existing_path(RAW_METRICS_CANDIDATES)
    logging.info(f"Loading raw metrics from {metrics_path}")
    df = pd.read_csv(metrics_path)
    if "subject_id" not in df.columns:
        raise KeyError(
            f"The loaded metrics file {metrics_path} does not contain a "
            f"'subject_id' column – it is required for merging."
        )
    return df

def load_pca_scores() -> pd.DataFrame:
    """
    Load the PCA factor‑score table created by ``run_pca_analysis`` (T023a).

    Returns
    -------
    pd.DataFrame
        Must contain a ``subject_id`` column and one column per PCA factor
        (e.g. ``pca_factor_1``, ``pca_factor_2`` …).  The exact number of
        factors depends on the earlier analysis but the merge logic is
        agnostic to that.
    """
    if not PCA_SCORES_PATH.is_file():
        raise FileNotFoundError(
            f"PCA scores file not found at expected location: {PCA_SCORES_PATH}"
        )
    logging.info(f"Loading PCA factor scores from {PCA_SCORES_PATH}")
    df = pd.read_csv(PCA_SCORES_PATH)
    if "subject_id" not in df.columns:
        raise KeyError(
            f"The PCA scores file {PCA_SCORES_PATH} must contain a "
            f"'subject_id' column for merging."
        )
    return df

def merge_metrics_and_pca_scores(
    metrics_df: pd.DataFrame, pca_df: pd.DataFrame
) -> pd.DataFrame:
    """
    Merge the raw metric columns with the PCA factor scores on ``subject_id``
    and persist the combined table to ``data/analysis/full_metrics.csv``.

    Parameters
    ----------
    metrics_df : pd.DataFrame
        Raw metric table (output of :func:`load_metrics_data`).
    pca_df : pd.DataFrame
        PCA factor‑score table (output of :func:`load_pca_scores`).

    Returns
    -------
    pd.DataFrame
        The merged DataFrame – also written to disk.
    """
    logging.info("Merging raw metrics with PCA factor scores")
    merged = pd.merge(metrics_df, pca_df, on="subject_id", how="inner")
    # Ensure deterministic column ordering: raw metrics first, then PCA factors
    raw_cols = [c for c in metrics_df.columns if c != "subject_id"]
    pca_cols = [c for c in pca_df.columns if c != "subject_id"]
    ordered_cols = ["subject_id"] + raw_cols + pca_cols
    merged = merged[ordered_cols]

    # Write the result – create parent directories if they do not exist
    FULL_METRICS_PATH.parent.mkdir(parents=True, exist_ok=True)
    merged.to_csv(FULL_METRICS_PATH, index=False)
    logging.info(f"Full metrics table written to {FULL_METRICS_PATH}")
    return merged

def main() -> None:
    """
    Entry‑point used by ``code/analysis/correlations_main_runner.py`` and
    the ``analyze`` step of ``code/main.py``.  It performs the three
    sub‑steps in order and exits with a clear log message.
    """
    logging.basicConfig(
        level=logging.INFO,
        format="%(asctime)s %(levelname)s %(name)s – %(message)s",
    )
    logger = logging.getLogger(__name__)

    try:
        metrics = load_metrics_data()
        pca_scores = load_pca_scores()
        merge_metrics_and_pca_scores(metrics, pca_scores)
    except Exception as exc:
        logger.exception("Failed to generate full_metrics.csv")
        raise

# ----------------------------------------------------------------------
# When executed directly ``python -m analysis.correlations`` runs ``main``.
# ----------------------------------------------------------------------
if __name__ == "__main__":
    main()