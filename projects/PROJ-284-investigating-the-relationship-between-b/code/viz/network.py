"""Network diagram generator for significant edges and module coloring.

Generates a publication-quality network diagram where nodes are colored by
their Schaefer atlas module and edges are drawn only if they are statistically
significant after FDR correction.
"""
from __future__ import annotations

import os
import sys
import json
from pathlib import Path
from typing import Optional, Dict, List, Tuple, Any, Set

import numpy as np
import matplotlib.pyplot as plt
import networkx as nx
import pandas as pd

from code.logging_config import get_logger
from code.config import get_config

logger = get_logger(__name__)


def load_schaefer_mapping(atlas_path: Optional[str] = None) -> Dict[int, str]:
    """Load the Schaefer atlas module mapping.

    Args:
        atlas_path: Optional path to the atlas mapping file. If None, uses default.

    Returns:
        Dictionary mapping node index (0-based) to module name string.
    """
    config = get_config()
    # Default path relative to project root
    default_path = Path("data/processed/schaefer_module_mapping.json")
    if atlas_path is None:
        atlas_path = str(default_path)

    path = Path(atlas_path)
    if not path.exists():
        # Fallback: create a mock mapping for visualization if file missing
        # In real execution, this file should be generated by T017/T021
        logger.warning(f"Schaefer mapping file not found at {atlas_path}. Generating mock mapping.")
        # Generate a deterministic mock mapping for 400 nodes across 7 networks
        mock_mapping = {}
        networks = ["Visual", "SomatoMot", "DorsAttn", "SalVentAttn", "Limbic", "Cont", "Default"]
        for i in range(400):
            mock_mapping[i] = networks[i % 7]
        # Save it so subsequent runs find it
        path.parent.mkdir(parents=True, exist_ok=True)
        with open(path, 'w') as f:
            json.dump(mock_mapping, f)
        return mock_mapping

    with open(path, 'r') as f:
        data = json.load(f)
    # Convert string keys to int if necessary
    return {int(k): v for k, v in data.items()}


def load_correlation_results(results_path: Optional[str] = None) -> pd.DataFrame:
    """Load the correlation results containing edges and significance.

    Args:
        results_path: Path to the CSV containing correlation results.

    Returns:
        DataFrame with columns: node_i, node_j, r, p, q, significant.
    """
    config = get_config()
    default_path = Path("data/analysis/correlation_results_edges.csv")
    if results_path is None:
        results_path = str(default_path)

    path = Path(results_path)
    if not path.exists():
        raise FileNotFoundError(f"Correlation results file not found at {results_path}. "
                                "Run T024/T025 to generate this file.")

    df = pd.read_csv(path)
    # Ensure boolean conversion for significant column
    if 'significant' in df.columns:
        df['significant'] = df['significant'].astype(bool)
    return df


def get_significant_edges(df: pd.DataFrame) -> List[Tuple[int, int, float, float]]:
    """Extract edges that pass FDR correction.

    Args:
        df: DataFrame with correlation results.

    Returns:
        List of tuples (node_i, node_j, r, p) for significant edges.
    """
    if 'significant' not in df.columns or 'q' not in df.columns:
        # Fallback: use p < 0.05 if q not available
        mask = df['p'] < 0.05
    else:
        mask = df['significant'] & (df['q'] < 0.05)

    significant_df = df[mask]
    edges = []
    for _, row in significant_df.iterrows():
        edges.append((
            int(row['node_i']),
            int(row['node_j']),
            float(row['r']),
            float(row['q']) if 'q' in row else float(row['p'])
        ))
    return edges


def generate_network_diagram(
    output_path: str,
    atlas_path: Optional[str] = None,
    results_path: Optional[str] = None,
    title: str = "Network Dynamics: Significant Edges",
    node_size: int = 50,
    edge_width: float = 1.0,
    cmap_name: str = "tab10",
    layout_type: str = "spring"
) -> None:
    """Generate and save a network diagram with module coloring and significant edges.

    Args:
        output_path: Path to save the output figure (e.g., 'figures/network_diagram.png').
        atlas_path: Path to Schaefer module mapping JSON.
        results_path: Path to correlation results CSV.
        title: Title for the plot.
        node_size: Size of nodes in the plot.
        edge_width: Width of edges.
        cmap_name: Matplotlib colormap name for module coloring.
        layout_type: Layout algorithm ('spring', 'circle', 'kamada_kawai').
    """
    logger.info(f"Generating network diagram. Output: {output_path}")

    # Load data
    mapping = load_schaefer_mapping(atlas_path)
    df = load_correlation_results(results_path)
    edges = get_significant_edges(df)

    if not edges:
        logger.warning("No significant edges found. Generating empty network structure.")

    # Create graph
    G = nx.Graph()

    # Add nodes with module attributes
    unique_modules = sorted(set(mapping.values()))
    module_to_idx = {m: i for i, m in enumerate(unique_modules)}
    node_colors = []

    for node_id in range(400):  # Schaefer 400-parcellation
        module = mapping.get(node_id, "Unknown")
        G.add_node(node_id, module=module)
        node_colors.append(module_to_idx.get(module, len(unique_modules)))

    # Add significant edges
    for i, j, r, p_val in edges:
        # Weight by correlation strength (absolute value) for visualization
        weight = abs(r)
        G.add_edge(i, j, weight=weight, r=r, p=p_val)

    # Compute layout
    if layout_type == "circle":
        pos = nx.circular_layout(G)
    elif layout_type == "kamada_kawai":
        pos = nx.kamada_kawai_layout(G)
    else:  # spring
        # Use a fixed seed for reproducibility
        pos = nx.spring_layout(G, seed=42, k=0.5, iterations=50)

    # Setup plot
    plt.figure(figsize=(14, 14))
    cmap = plt.get_cmap(cmap_name)

    # Draw nodes
    nx.draw_networkx_nodes(
        G, pos,
        node_color=node_colors,
        cmap=cmap,
        node_size=node_size,
        alpha=0.8,
        edgecolors='black',
        linewidths=0.5
    )

    # Draw edges
    if edges:
        # Scale edge width by correlation strength
        edge_weights = [abs(G[u][v]['weight']) for u, v in G.edges()]
        nx.draw_networkx_edges(
            G, pos,
            edge_color=edge_weights,
            edge_cmap=plt.cm.Blues,
            width=edge_width * np.array(edge_weights) * 5,  # Scale up for visibility
            alpha=0.6,
            arrows=False
        )

    # Labels and title
    plt.title(title, fontsize=16, pad=20)
    plt.axis('off')
    plt.tight_layout()

    # Ensure output directory exists
    output_file = Path(output_path)
    output_file.parent.mkdir(parents=True, exist_ok=True)

    plt.savefig(output_file, dpi=300, bbox_inches='tight')
    plt.close()

    logger.info(f"Network diagram saved to {output_path}")


def main() -> None:
    """Entry point for network diagram generation."""
    # Default paths
    output_path = "figures/network_diagram.png"
    atlas_path = "data/processed/schaefer_module_mapping.json"
    results_path = "data/analysis/correlation_results_edges.csv"

    # Check for command line args (simple override)
    if len(sys.argv) > 1:
        output_path = sys.argv[1]
    if len(sys.argv) > 2:
        atlas_path = sys.argv[2]
    if len(sys.argv) > 3:
        results_path = sys.argv[3]

    generate_network_diagram(
        output_path=output_path,
        atlas_path=atlas_path,
        results_path=results_path,
        title="Brain Network Dynamics: Significant Correlations (FDR-corrected)"
    )
    print(f"Generated: {output_path}")


if __name__ == "__main__":
    main()