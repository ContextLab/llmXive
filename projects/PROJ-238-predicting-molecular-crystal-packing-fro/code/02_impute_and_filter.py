"""
T016: Implement missing-data handling for US1.

This script:
1. Loads the raw descriptors CSV generated by 01_ingest_and_descriptors.py.
2. Excludes rows with missing target (packing_coefficient) and logs the count.
3. Imputes auxiliary descriptors (specifically Dipole) with the training-set median.
   Note: Since we are preparing data for a split, we calculate the median on the 
   *available* data before the split, or simulate the "training set" logic if the 
   split already happened. The task implies pre-split imputation or preparing for split.
   We will calculate the median on the non-missing values of the current dataset 
   to ensure consistency for the subsequent split step (T017).
4. Flags rows where imputation occurred with a boolean column `dipole_imputed`.
5. Saves the cleaned CSV and the log file.
"""
import os
import pandas as pd
import logging
from pathlib import Path
from datetime import datetime
import json

# Import config for logging setup
try:
    from config import setup_logging, log_event
except ImportError:
    # Fallback if running directly without full package context
    import sys
    sys.path.insert(0, os.path.dirname(os.path.dirname(os.path.abspath(__file__))))
    from config import setup_logging, log_event

# Paths
INPUT_FILE = Path("data/descriptors/raw_descriptors.csv")
OUTPUT_FILE = Path("data/descriptors/raw_descriptors.csv") # Overwrite or save to processed? Task says "flag the row in raw_descriptors.csv"
