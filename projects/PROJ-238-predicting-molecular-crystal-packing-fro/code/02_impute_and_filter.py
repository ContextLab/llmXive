# generated it and this step refines it in place.

LOG_FILE = Path("data/processed/missing_target.log")
IMPUTE_LOG = Path("data/processed/imputation_log.txt")

def setup_logger():
    logger = logging.getLogger("T016_Impute")
    logger.setLevel(logging.INFO)
    if not logger.handlers:
        ch = logging.StreamHandler()
        ch.setFormatter(logging.Formatter('%(asctime)s - %(name)s - %(levelname)s - %(message)s'))
        logger.addHandler(ch)
    return logger

def main():
    logger = setup_logger()
    log_event("T016", "Starting missing data handling and imputation")
    
    if not INPUT_FILE.exists():
        logger.error(f"Input file not found: {INPUT_FILE}")
        raise FileNotFoundError(f"Input file {INPUT_FILE} not found. Run 01_ingest_and_descriptors.py first.")
    
    # 1. Load data
    df = pd.read_csv(INPUT_FILE)
    initial_count = len(df)
    logger.info(f"Loaded {initial_count} rows from {INPUT_FILE}")
    
    # 2. Exclude rows with missing target (packing_coefficient)
    # Target column is 'packing_coefficient'
    missing_target_mask = df['packing_coefficient'].isna()
    missing_target_count = missing_target_mask.sum()
    
    if missing_target_count > 0:
        logger.warning(f"Found {missing_target_count} rows with missing 'packing_coefficient'. Excluding them.")
        df_clean = df[~missing_target_mask].copy()
    else:
        df_clean = df.copy()
    
    # Write log for missing target
    LOG_FILE.parent.mkdir(parents=True, exist_ok=True)
    with open(LOG_FILE, 'w') as f:
        f.write(f"Timestamp: {datetime.now().isoformat()}\n")
        f.write(f"Total rows: {initial_count}\n")
        f.write(f"Rows with missing target: {missing_target_count}\n")
        f.write(f"Rows remaining: {len(df_clean)}\n")
    
    logger.info(f"Written missing target log to {LOG_FILE}")
    
    # 3. Impute auxiliary descriptors (Dipole) with median
    # Calculate median of non-missing values in the CURRENT dataset (acting as training set proxy)
    dipole_col = 'Dipole'
    if dipole_col not in df_clean.columns:
        logger.error(f"Column '{dipole_col}' not found in dataset.")
        raise ValueError(f"Missing column: {dipole_col}")
        
    # Calculate median on non-null values
    median_val = df_clean[dipole_col].median()
    
    if pd.isna(median_val):
        logger.warning("Median for Dipole is NaN (all values missing). Cannot impute.")
        # If all are missing, we can't impute.
        imputed_count = 0
        df_clean['dipole_imputed'] = False
    else:
        # Identify rows needing imputation
        missing_dipole_mask = df_clean[dipole_col].isna()
        imputed_count = missing_dipole_mask.sum()
        
        logger.info(f"Imputing {imputed_count} missing 'Dipole' values with median {median_val:.4f}")
        
        # Create flag column
        df_clean['dipole_imputed'] = False
        df_clean.loc[missing_dipole_mask, 'dipole_imputed'] = True
        
        # Perform imputation
        df_clean.loc[missing_dipole_mask, dipole_col] = median_val
        
        # Log imputation details
        with open(IMPUTE_LOG, 'w') as f:
            f.write(f"Timestamp: {datetime.now().isoformat()}\n")
            f.write(f"Column: {dipole_col}\n")
            f.write(f"Imputation Strategy: Median\n")
            f.write(f"Median Value: {median_val}\n")
            f.write(f"Rows Imputed: {imputed_count}\n")
    
    # 4. Save the updated dataset
    # The task says "flag the row in `data/descriptors/raw_descriptors.csv`"
    # We will overwrite it to satisfy the strict instruction, as it's the intermediate artifact.
    df_clean.to_csv(INPUT_FILE, index=False)
    logger.info(f"Saved updated dataset to {INPUT_FILE} with {len(df_clean)} rows")
    
    log_event("T016", "Completed missing data handling", {
        "rows_initial": initial_count,
        "rows_missing_target": missing_target_count,
        "rows_final": len(df_clean),
        "imputed_count": imputed_count
    })

if __name__ == "__main__":
    main()
