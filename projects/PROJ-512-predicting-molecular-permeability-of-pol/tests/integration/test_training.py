"""
Integration test for the full training and evaluation pipeline (US2).

This test verifies the end-to-end flow:
1. Load preprocessed data (polymers.h5) generated by T014/T020.
2. Perform Murcko scaffold split (T020 logic).
3. Train GNN, RF, and Linear Regression baselines (T021-T024).
4. Evaluate all models (T025).
5. Generate and validate the comparison report (T026).

Prerequisites:
- code/data/processed/polymers.h5 must exist (from T014).
- All model and evaluation modules must be functional.
"""

import os
import sys
import logging
import tempfile
import shutil
import json
from pathlib import Path

# Project root setup for imports
PROJECT_ROOT = Path(__file__).resolve().parent.parent.parent
if str(PROJECT_ROOT) not in sys.path:
    sys.path.insert(0, str(PROJECT_ROOT))

from data.utils import setup_logging, set_seed, get_seed
from data.preprocessing import murcko_scaffold_split, process_graphs
from models.gnn import create_gnn_model, PolymerGNN
from models.baselines import RandomForestBaseline, LinearRegressionBaseline
from models.trainer import create_trainer
from evaluation.metrics import compute_r2, compute_mae, compute_pearson_correlation
from evaluation.report import generate_final_report

# Configure logging for the test
logger = setup_logging("integration_test_training", level=logging.INFO)

def test_full_training_pipeline():
    """
    Execute the full training and evaluation pipeline on real data.
    Raises AssertionError if any step fails or metrics are invalid.
    """
    logger.info("Starting full training pipeline integration test.")

    # 1. Setup environment and seed
    set_seed(42)
    seed = get_seed()
    logger.info(f"Initialized with seed: {seed}")

    # 2. Locate input data
    data_path = PROJECT_ROOT / "code" / "data" / "processed" / "polymers.h5"
    if not data_path.exists():
        raise FileNotFoundError(
            f"Required dataset not found at {data_path}. "
            "Ensure T014 (data ingestion) and T020 (scaffold split) have been run."
        )
    logger.info(f"Found dataset at: {data_path}")

    # 3. Perform Scaffold Split (T020)
    # We assume the split function reads the HDF5 and returns train/val/test dicts
    # containing (graphs, targets) or similar structures.
    # Since the exact return signature of murcko_scaffold_split isn't fully detailed
    # in the API surface, we assume it follows the standard pattern:
    # (train_data, val_data, test_data)
    try:
        train_data, val_data, test_data = murcko_scaffold_split(str(data_path))
        logger.info(f"Scaffold split complete. Train: {len(train_data)}, Val: {len(val_data)}, Test: {len(test_data)}")
    except Exception as e:
        logger.error(f"Failed to perform scaffold split: {e}")
        raise

    if len(train_data) == 0 or len(test_data) == 0:
        raise AssertionError("Split resulted in empty train or test sets.")

    # 4. Initialize Models
    # GNN (T021)
    gnn_model = create_gnn_model()
    logger.info("GNN model created.")

    # Baselines (T022, T023)
    rf_baseline = RandomForestBaseline()
    lr_baseline = LinearRegressionBaseline()
    logger.info("Baseline models created.")

    # 5. Training (T024)
    # Create trainer
    trainer = create_trainer(gnn_model, device="cpu")

    # Train GNN
    logger.info("Training GNN...")
    # We assume train() takes train_data, val_data and returns history/metrics
    # If the API expects specific DataLoader objects, we adapt here.
    # For this integration test, we assume the trainer handles data loading internally
    # or the data is passed as lists of (graph, target) tuples.
    try:
        gnn_history = trainer.train(train_data, val_data, epochs=5) # Reduced epochs for speed
        logger.info(f"GNN training complete. Final loss: {gnn_history[-1]['loss']:.4f}")
    except Exception as e:
        logger.error(f"GNN training failed: {e}")
        raise

    # Train Baselines
    logger.info("Training Random Forest Baseline...")
    try:
        rf_metrics = rf_baseline.train_and_evaluate(train_data, test_data)
        logger.info(f"RF Baseline R2: {rf_metrics['r2']:.4f}")
    except Exception as e:
        logger.error(f"RF Baseline training failed: {e}")
        raise

    logger.info("Training Linear Regression Baseline...")
    try:
        lr_metrics = lr_baseline.train_and_evaluate(train_data, test_data)
        logger.info(f"LR Baseline R2: {lr_metrics['r2']:.4f}")
    except Exception as e:
        logger.error(f"LR Baseline training failed: {e}")
        raise

    # 6. Evaluate GNN (T025)
    logger.info("Evaluating GNN on test set...")
    try:
        gnn_predictions = trainer.predict(test_data)
        gnn_y_true = [item[1] for item in test_data] # Assuming (graph, target) format
        
        gnn_r2 = compute_r2(gnn_y_true, gnn_predictions)
        gnn_mae = compute_mae(gnn_y_true, gnn_predictions)
        gnn_pearson = compute_pearson_correlation(gnn_y_true, gnn_predictions)
        
        logger.info(f"GNN Test R2: {gnn_r2:.4f}, MAE: {gnn_mae:.4f}, Pearson: {gnn_pearson:.4f}")
    except Exception as e:
        logger.error(f"GNN evaluation failed: {e}")
        raise

    # 7. Generate Report (T026)
    logger.info("Generating final comparison report...")
    try:
        report_data = {
            "gnn": {
                "r2": gnn_r2,
                "mae": gnn_mae,
                "pearson": gnn_pearson,
                "loss_history": [h["loss"] for h in gnn_history]
            },
            "random_forest": rf_metrics,
            "linear_regression": lr_metrics,
            "seed": seed
        }
        
        # Generate report to a temp file to verify it works
        with tempfile.NamedTemporaryFile(mode='w', suffix='.json', delete=False) as f:
            temp_report_path = f.name
        
        generate_final_report(report_data, temp_report_path)
        
        # Verify report file exists and is valid JSON
        if not os.path.exists(temp_report_path):
            raise AssertionError("Report file was not created.")
        
        with open(temp_report_path, 'r') as f:
            loaded_report = json.load(f)
        
        logger.info(f"Report generated successfully at {temp_report_path}")
        os.remove(temp_report_path) # Cleanup
        
    except Exception as e:
        logger.error(f"Report generation failed: {e}")
        raise

    # 8. Assertions
    # Ensure metrics are within reasonable bounds (not NaN, not infinite)
    assert not (gnn_r2 != gnn_r2), "GNN R2 is NaN"
    assert not (rf_metrics['r2'] != rf_metrics['r2']), "RF R2 is NaN"
    assert not (lr_metrics['r2'] != lr_metrics['r2']), "LR R2 is NaN"
    
    # Ensure training actually happened (loss decreased or at least ran)
    assert len(gnn_history) > 0, "GNN training history is empty"

    logger.info("Integration test PASSED: Full training and evaluation pipeline executed successfully.")

if __name__ == "__main__":
    test_full_training_pipeline()