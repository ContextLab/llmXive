"""
T016b: Generate evidence artifact for data completeness.

This script consumes the validation results (typically generated by T016a logic
or passed as input) to produce the final JSON evidence artifact required by SC-005.

It reads the processed data, calculates the completeness rate, checks the
synthetic fallback flag, and writes `artifacts/reports/data_completeness_report.json`.
"""
import json
import sys
from pathlib import Path
from typing import Dict, Any, Optional

# Add project root to path for imports if running as script
project_root = Path(__file__).resolve().parent.parent
sys.path.insert(0, str(project_root))

from config import ensure_directories
from utils.logging import get_logger, log_info, log_error
from data_validation import validate_data_completeness
from data_models import GameRecord

logger = get_logger(__name__)

REQUIRED_COLUMNS = [
    'game_id', 'date', 'home_team', 'away_team', 'home_runs', 'away_runs',
    'home_hits', 'away_hits', 'home_era', 'away_era', 'home_woba', 'away_woba',
    'home_babip', 'away_babip'
]

def load_processed_data(data_path: Path) -> Any:
    """Loads the processed dataframe. Assumes T015 has run and produced data."""
    import pandas as pd
    
    if not data_path.exists():
        raise FileNotFoundError(f"Processed data file not found at {data_path}. "
                                "Ensure T015 (temporal split) has been executed first.")
    
    try:
        df = pd.read_csv(data_path)
        logger.info(f"Loaded processed data from {data_path} with shape {df.shape}")
        return df
    except Exception as e:
        log_error(f"Failed to load data from {data_path}: {e}")
        raise

def generate_report(
    completeness_rate: float,
    threshold: float = 0.95,
    is_real_data: bool = True,
    missing_columns: Optional[list] = None
) -> Dict[str, Any]:
    """
    Constructs the report dictionary satisfying SC-005 measurability.
    """
    report = {
        "task_id": "T016b",
        "artifact_type": "data_completeness_report",
        "completeness_rate": float(completeness_rate),
        "threshold_met": completeness_rate >= threshold,
        "threshold_value": float(threshold),
        "is_real_data": is_real_data,
        "empirical_hypothesis_untested": False,
        "missing_columns": missing_columns or [],
        "timestamp": "generated_by_script" 
    }

    # Logic for SC-005: If synthetic mode was used, flag the hypothesis as untested.
    # We assume is_real_data=False implies synthetic fallback was triggered in T012a.
    if not is_real_data:
        report["empirical_hypothesis_untested"] = True
        report["note"] = "Results are Validation-Only due to Synthetic Data Fallback."

    if not report["threshold_met"]:
        if is_real_data:
            # This state should ideally be caught by T016a's enforcement, 
            # but we log it here for the report.
            log_error(f"Completeness rate {completeness_rate:.4f} is below threshold {threshold}.")
        else:
            log_info(f"Completeness rate {completeness_rate:.4f} is below threshold, but synthetic mode active.")

    return report

def main():
    """
    Entry point for T016b.
    1. Ensure directories exist.
    2. Load processed data (output of T015).
    3. Validate completeness (re-using T016a logic).
    4. Generate and save the JSON report.
    """
    ensure_directories()
    
    # Paths
    processed_data_path = project_root / "data" / "processed" / "mlb_processed.csv"
    output_report_path = project_root / "artifacts" / "reports" / "data_completeness_report.json"

    logger.info(f"Starting T016b: Generating completeness report for {processed_data_path}")

    try:
        # Load data
        df = load_processed_data(processed_data_path)
        
        # Determine if data is real (check for a flag column or assume based on file source)
        # The data_loader (T012a) should have added an 'is_real_data' column if synthetic was used.
        # If not present, we assume real data (default behavior).
        is_real_data = True
        if 'is_real_data' in df.columns:
            # If the column exists, take the value from the first row (should be consistent)
            is_real_data = bool(df['is_real_data'].iloc[0])
        
        # Run validation logic (T016a)
        # This returns (rate, missing_cols, passed)
        completeness_rate, missing_cols, passed = validate_data_completeness(df, REQUIRED_COLUMNS)
        
        logger.info(f"Calculated completeness rate: {completeness_rate:.4f}")

        # Generate report
        report = generate_report(
            completeness_rate=completeness_rate,
            is_real_data=is_real_data,
            missing_columns=missing_cols
        )

        # Write artifact
        output_report_path.parent.mkdir(parents=True, exist_ok=True)
        with open(output_report_path, 'w') as f:
            json.dump(report, f, indent=2)

        logger.info(f"Successfully generated report at {output_report_path}")
        print(f"T016b Complete: {output_report_path}")
        
        return 0

    except FileNotFoundError as e:
        log_error(str(e))
        print(f"T016b Failed: {e}")
        return 1
    except Exception as e:
        log_error(f"Unexpected error during T016b: {e}")
        print(f"T016b Failed: {e}")
        return 1

if __name__ == "__main__":
    sys.exit(main())
