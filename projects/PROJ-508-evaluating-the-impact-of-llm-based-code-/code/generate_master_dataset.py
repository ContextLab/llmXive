import os
import json
import csv
import logging
from pathlib import Path
from typing import List, Dict, Any, Optional

from utils.metrics import (
    calculate_iteration_count,
    calculate_avg_comment_length,
    calculate_review_thread_depth,
    calculate_revert_frequency,
    calculate_diff_complexity_score,
    is_ai_noise_flag,
    calculate_domain_complexity,
    process_review_metrics,
)
from utils.config import get_config
from utils.data_validation import validate_csv_schema, ValidationResult

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

# Required columns for the master dataset as per spec
REQUIRED_COLUMNS = [
    "repo_id",
    "repo_name",
    "llm_adoption_flag",
    "iteration_count",
    "avg_comment_length",
    "review_thread_depth",
    "revert_frequency",
    "diff_complexity_score",
    "ai_noise_flag",
    "domain_complexity",
    "total_prs",
    "total_commits",
    "language_count",
    "dependency_count",
]

def load_ingestion_data(ingest_path: Path) -> List[Dict[str, Any]]:
    """
    Load the raw ingestion data generated by code/ingest.py.
    Expects a JSON file containing a list of repository dictionaries.
    """
    if not ingest_path.exists():
        raise FileNotFoundError(f"Ingestion data not found at {ingest_path}")
    
    with open(ingest_path, "r", encoding="utf-8") as f:
        data = json.load(f)
    
    if not isinstance(data, list):
        raise ValueError("Ingestion data must be a list of repository records.")
    
    return data

def calculate_repo_metrics(repo_record: Dict[str, Any]) -> Dict[str, Any]:
    """
    Calculate derived metrics for a single repository record using the
    metrics module utilities.
    """
    # Extract raw data from the ingestion record
    pr_data = repo_record.get("pr_data", [])
    commit_data = repo_record.get("commit_data", [])
    config_data = repo_record.get("config_data", {})
    lang_data = repo_record.get("languages", [])
    dep_data = repo_record.get("dependencies", [])
    
    # Calculate core metrics
    iteration_count = calculate_iteration_count(pr_data)
    avg_comment_length = calculate_avg_comment_length(pr_data)
    review_thread_depth = calculate_review_thread_depth(pr_data)
    revert_frequency = calculate_revert_frequency(commit_data)
    domain_complexity = calculate_domain_complexity(lang_data, dep_data)
    
    # Calculate diff complexity and AI noise flag
    # We aggregate diff complexity across commits for the repo
    total_diff_score = 0.0
    ai_noise_count = 0
    total_commits = len(commit_data)
    
    if total_commits > 0:
        for commit in commit_data:
            diff_score = calculate_diff_complexity_score(commit)
            total_diff_score += diff_score
            if is_ai_noise_flag(diff_score, commit.get("message", "")):
                ai_noise_count += 1
        
        avg_diff_complexity = total_diff_score / total_commits
        # Flag repo as having AI noise if significant portion of commits are flagged
        ai_noise_flag = 1 if (ai_noise_count / total_commits) > 0.1 else 0
    else:
        avg_diff_complexity = 0.0
        ai_noise_flag = 0
    
    # Process review metrics to ensure consistency
    processed_metrics = process_review_metrics(
        iteration_count, avg_comment_length, review_thread_depth
    )
    
    return {
        "repo_id": repo_record.get("repo_id"),
        "repo_name": repo_record.get("repo_name"),
        "llm_adoption_flag": repo_record.get("llm_adoption_flag", 0),
        "iteration_count": processed_metrics["iteration_count"],
        "avg_comment_length": processed_metrics["avg_comment_length"],
        "review_thread_depth": processed_metrics["review_thread_depth"],
        "revert_frequency": revert_frequency,
        "diff_complexity_score": avg_diff_complexity,
        "ai_noise_flag": ai_noise_flag,
        "domain_complexity": domain_complexity,
        "total_prs": len(pr_data),
        "total_commits": total_commits,
        "language_count": len(lang_data),
        "dependency_count": len(dep_data),
    }

def write_master_dataset(
    records: List[Dict[str, Any]], output_path: Path
) -> None:
    """
    Write the calculated metrics to a CSV file.
    """
    output_path.parent.mkdir(parents=True, exist_ok=True)
    
    with open(output_path, "w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=REQUIRED_COLUMNS)
        writer.writeheader()
        for record in records:
            # Ensure all required columns are present, defaulting to None if missing
            row = {col: record.get(col, None) for col in REQUIRED_COLUMNS}
            writer.writerow(row)
    
    logger.info(f"Master dataset written to {output_path}")

def validate_output(output_path: Path) -> ValidationResult:
    """
    Validate the generated CSV against the expected schema.
    """
    return validate_csv_schema(output_path, REQUIRED_COLUMNS)

def main():
    config = get_config()
    base_path = Path(config.get("project_root", "."))
    
    ingest_file = base_path / "data" / "derived" / "ingestion_results.json"
    output_file = base_path / "data" / "derived" / "master_dataset.csv"
    
    logger.info(f"Loading ingestion data from {ingest_file}")
    raw_data = load_ingestion_data(ingest_file)
    
    logger.info(f"Calculating metrics for {len(raw_data)} repositories...")
    calculated_records = []
    for idx, repo in enumerate(raw_data):
        try:
            metrics = calculate_repo_metrics(repo)
            calculated_records.append(metrics)
        except Exception as e:
            logger.error(f"Error processing repo {repo.get('repo_id')}: {e}")
            continue
    
    logger.info(f"Successfully processed {len(calculated_records)} repositories.")
    
    write_master_dataset(calculated_records, output_file)
    
    validation_result = validate_output(output_file)
    if validation_result.is_valid:
        logger.info("Output validation passed.")
    else:
        logger.warning(f"Output validation issues: {validation_result.errors}")
    
    logger.info("Master dataset generation complete.")

if __name__ == "__main__":
    main()
