# --- Data Loading ---

def load_standard_subset() -> pd.DataFrame:
    """
    Loads the standard subset of the dataset from the processed directory.
    Expects the file to be generated by code/standardize.py.
    """
    input_path = Path("data/processed/standard_subset.csv")
    if not input_path.exists():
        # Fallback to merged dataset if standard_subset not found, 
        # though T021 should have created it.
        logger.warning(f"Standard subset not found at {input_path}. Attempting to load merged dataset.")
        input_path = Path("data/processed/merged_drugs.csv")
    
    if not input_path.exists():
        raise FileNotFoundError(f"Required input data not found: {input_path}")
    
    df = pd.read_csv(input_path)
    logger.info(f"Loaded standard subset with {len(df)} records from {input_path}")
    return df

# --- Correlation Analysis ---

def compute_correlation_matrix(df: pd.DataFrame, feature_cols: List[str], target_col: str) -> pd.DataFrame:
    """
    Computes Pearson correlation matrix for features vs target.
    """
    # Ensure target is numeric
    if target_col not in df.columns:
        raise ValueError(f"Target column {target_col} not found in dataframe")
    
    valid_cols = [c for c in feature_cols if c in df.columns]
    if not valid_cols:
        raise ValueError("No valid feature columns found")
    
    data = df[valid_cols + [target_col]].dropna()
    
    if len(data) < 2:
        logger.warning("Insufficient data for correlation analysis")
        return pd.DataFrame()

    corr_matrix = data[valid_cols].corrwith(data[target_col])
    return corr_matrix

def compute_p_values(df: pd.DataFrame, feature_cols: List[str], target_col: str) -> Dict[str, float]:
    """
    Computes p-values for Pearson correlation for each feature.
    """
    data = df[feature_cols + [target_col]].dropna()
    p_values = {}
    
    for col in feature_cols:
        if col in data.columns and target_col in data.columns:
            corr, p = stats.pearsonr(data[col], data[target_col])
            p_values[col] = p
        else:
            p_values[col] = np.nan
    
    return p_values

def identify_significant_correlations(p_values: Dict[str, float], threshold: float = 0.05) -> List[str]:
    """
    Identifies features with p-values below the significance threshold.
    """
    return [k for k, v in p_values.items() if not np.isnan(v) and v < threshold]

# --- Sensitivity Analysis ---

def run_sensitivity_analysis(df: pd.DataFrame, feature_cols: List[str], target_col: str) -> Dict[str, Any]:
    """
    Sweeps correlation thresholds and saves counts of significant correlations.
    """
    thresholds = [0.01, 0.05, 0.10, 0.20]
    results = {}
    
    data = df[feature_cols + [target_col]].dropna()
    
    for thresh in thresholds:
        significant_count = 0
        for col in feature_cols:
            if col in data.columns:
                corr, p = stats.pearsonr(data[col], data[target_col])
                if p < thresh:
                    significant_count += 1
        results[str(thresh)] = significant_count
    
    return results

# --- Regression Modeling ---

def run_mlr(df: pd.DataFrame, feature_cols: List[str], target_col: str) -> Dict[str, Any]:
    """
    Runs Multiple Linear Regression.
    """
    data = df[feature_cols + [target_col]].dropna()
    if len(data) < len(feature_cols) + 1:
        raise ValueError("Insufficient data points for MLR")
    
    X = data[feature_cols]
    y = data[target_col]
    
    model = LinearRegression()
    model.fit(X, y)
    
    y_pred = model.predict(X)
    r2 = model.score(X, y)
    
    # Calculate p-values for coefficients using statsmodels logic manually or via OLS
    # For simplicity in this script, we return basic stats. 
    # A full implementation would use statsmodels.api.OLS for exact p-values of coefficients.
    # Here we approximate significance based on t-statistics if needed, but task asks for output.
    
    return {
        "r2": float(r2),
        "coefficients": dict(zip(feature_cols, model.coef_.tolist())),
        "intercept": float(model.intercept_),
        "n_samples": len(data)
    }

def run_conditional_covariate_model(df: pd.DataFrame, feature_cols: List[str], target_col: str, 
                                    include_covariates: bool, covariate_cols: Optional[List[str]] = None) -> Dict[str, Any]:
    """
    Runs MLR with optional covariates based on coverage check.
    """
    if include_covariates and covariate_cols:
        all_features = feature_cols + covariate_cols
    else:
        all_features = feature_cols
    
    return run_mlr(df, all_features, target_col)

def run_lasso_regression(df: pd.DataFrame, feature_cols: List[str], target_col: str, k_folds: int = 5) -> Dict[str, Any]:
    """
    Runs LASSO regression with k-fold cross-validation.
    """
    data = df[feature_cols + [target_col]].dropna()
    X = data[feature_cols]
    y = data[target_col]
    
    lasso = Lasso(alpha=0.1, max_iter=10000)
    kf = KFold(n_splits=k_folds, shuffle=True, random_state=42)
    
    scores = cross_val_score(lasso, X, y, cv=kf, scoring='r2')
    
    lasso.fit(X, y)
    final_r2 = lasso.score(X, y)
    
    return {
        "cv_r2_mean": float(scores.mean()),
        "cv_r2_std": float(scores.std()),
        "final_r2": float(final_r2),
        "selected_features": [f for f, c in zip(feature_cols, lasso.coef_) if c != 0],
        "coefficients": dict(zip(feature_cols, lasso.coef_.tolist()))
    }

# --- Diagnostics & Verification ---

def perform_residual_diagnostics(model, X: pd.DataFrame, y: pd.Series) -> Dict[str, Any]:
    """
    Performs Shapiro-Wilk and Breusch-Pagan tests on residuals.
    """
    y_pred = model.predict(X)
    residuals = y - y_pred
    
    # Shapiro-Wilk
    sw_stat, sw_p = stats.shapiro(residuals)
    
    # Breusch-Pagan
    # Requires statsmodels
    try:
        bp_stat, bp_p, _, _ = het_breuschpagan(residuals, X)
    except Exception as e:
        logger.warning(f"Breusch-Pagan test failed: {e}")
        bp_p = np.nan
    
    return {
        "shapiro_wilk_p": float(sw_p),
        "breusch_pagan_p": float(bp_p) if not np.isnan(bp_p) else None
    }

def verify_correlation_significance(p_values: Dict[str, float], threshold: float = 0.05) -> bool:
    """
    Verifies if at least one correlation is significant.
    """
    significant = [p for p in p_values.values() if not np.isnan(p) and p < threshold]
    return len(significant) > 0

# --- Output Generation ---

def save_analysis_results(results: Dict[str, Any], output_path: str):
    """
    Saves analysis results to a JSON file and validates schema.
    """
    output_file = Path(output_path)
    output_file.parent.mkdir(parents=True, exist_ok=True)
    
    with open(output_file, 'w') as f:
        json.dump(results, f, indent=2)
    
    # Validation
    if 'R2' not in results and 'r2' not in results:
        raise ValueError("Validation Failed: Result missing 'R2' or 'r2' key")
    
    logger.info(f"Analysis results saved and validated to {output_path}")

