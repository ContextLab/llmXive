"""
Contract tests for model output schemas.

This module validates that the output files generated by the training pipeline
conform to the schemas defined in contracts/model_output.schema.yaml.

It specifically tests:
- model_results.json
- feature_importance.json
- variance_partition.csv
"""
import json
import os
import pandas as pd
import pytest
import yaml
from pathlib import Path
from typing import Any, Dict, List

# Project root relative to tests/
ROOT_DIR = Path(__file__).parent.parent
CONTRACTS_DIR = ROOT_DIR / "contracts"
OUTPUTS_DIR = ROOT_DIR / "data" / "outputs"

# Schema file paths
MODEL_OUTPUT_SCHEMA_PATH = CONTRACTS_DIR / "model_output.schema.yaml"


def load_schema(schema_path: Path) -> Dict[str, Any]:
    """Load a YAML schema file."""
    if not schema_path.exists():
        raise FileNotFoundError(f"Schema file not found: {schema_path}")
    with open(schema_path, "r") as f:
        return yaml.safe_load(f)


def validate_type(value: Any, expected_type: str) -> bool:
    """Check if a value matches the expected type string."""
    type_mapping = {
        "string": str,
        "number": (int, float),
        "integer": int,
        "boolean": bool,
        "array": list,
        "object": dict,
    }
    expected_python_type = type_mapping.get(expected_type)
    if expected_python_type is None:
        # Fallback for complex types or unknowns
        return True
    return isinstance(value, expected_python_type)


def validate_schema(data: Dict[str, Any], schema: Dict[str, Any], path: str = "") -> List[str]:
    """
    Recursively validate data against a schema.
    Returns a list of error messages.
    """
    errors = []
    
    required_fields = schema.get("required", [])
    properties = schema.get("properties", {})

    # Check required fields
    for field in required_fields:
        if field not in data:
            errors.append(f"Missing required field at {path}: '{field}'")

    # Check types and constraints for present fields
    for key, value in data.items():
        field_path = f"{path}.{key}" if path else key
        
        if key in properties:
            field_schema = properties[key]
            
            # Type check
            if "type" in field_schema:
                if not validate_type(value, field_schema["type"]):
                    errors.append(f"Type mismatch at {field_path}: expected {field_schema['type']}, got {type(value).__name__}")
            
            # Constraint checks
            if "minimum" in field_schema and isinstance(value, (int, float)):
                if value < field_schema["minimum"]:
                    errors.append(f"Value at {field_path} ({value}) is less than minimum ({field_schema['minimum']})")
            
            if "maximum" in field_schema and isinstance(value, (int, float)):
                if value > field_schema["maximum"]:
                    errors.append(f"Value at {field_path} ({value}) is greater than maximum ({field_schema['maximum']})")
            
            # Nested object validation
            if field_schema.get("type") == "object" and "properties" in field_schema:
                errors.extend(validate_schema(value, field_schema, field_path))
            
            # Array validation
            if field_schema.get("type") == "array" and isinstance(value, list):
                if "items" in field_schema:
                    item_schema = field_schema["items"]
                    for i, item in enumerate(value):
                        item_path = f"{field_path}[{i}]"
                        if item_schema.get("type") == "object":
                            errors.extend(validate_schema(item, item_schema, item_path))
                        elif "type" in item_schema:
                            if not validate_type(item, item_schema["type"]):
                                errors.append(f"Array item type mismatch at {item_path}: expected {item_schema['type']}")

    return errors


def validate_csv_schema(df: pd.DataFrame, schema: Dict[str, Any], filename: str) -> List[str]:
    """Validate a DataFrame against a CSV schema definition."""
    errors = []
    required_columns = schema.get("required", [])
    columns_schema = schema.get("properties", {})

    # Check required columns
    for col in required_columns:
        if col not in df.columns:
            errors.append(f"Missing required column in {filename}: '{col}'")

    # Check column types and constraints
    for col in df.columns:
        if col in columns_schema:
            col_schema = columns_schema[col]
            
            # Basic type check (simplified for CSV)
            if "type" in col_schema:
                expected_type = col_schema["type"]
                if expected_type == "number" or expected_type == "integer":
                    # Try to convert to numeric
                    try:
                        pd.to_numeric(df[col], errors="raise")
                    except (ValueError, TypeError):
                        errors.append(f"Column '{col}' in {filename} contains non-numeric values")
            
            # Constraint checks
            if "minimum" in col_schema:
                numeric_col = pd.to_numeric(df[col], errors="coerce")
                if numeric_col.min() < col_schema["minimum"]:
                    errors.append(f"Column '{col}' in {filename} has values below minimum ({col_schema['minimum']})")
            
            if "maximum" in col_schema:
                numeric_col = pd.to_numeric(df[col], errors="coerce")
                if numeric_col.max() > col_schema["maximum"]:
                    errors.append(f"Column '{col}' in {filename} has values above maximum ({col_schema['maximum']})")

    return errors


class TestModelOutputContracts:
    """Test suite for model output contract validation."""

    @pytest.fixture(autouse=True)
    def setup(self):
        """Ensure schema file exists."""
        assert MODEL_OUTPUT_SCHEMA_PATH.exists(), f"Schema file missing: {MODEL_OUTPUT_SCHEMA_PATH}"
        self.schema = load_schema(MODEL_OUTPUT_SCHEMA_PATH)

    def test_model_results_schema(self):
        """Validate model_results.json against its schema."""
        # Skip if file doesn't exist yet (task T015 not run)
        model_results_path = OUTPUTS_DIR / "model_results.json"
        if not model_results_path.exists():
            pytest.skip("model_results.json not found. Run T015 first.")

        with open(model_results_path, "r") as f:
            data = json.load(f)

        # Get the schema for model_results (assuming top-level key or root)
        # The schema YAML usually defines the root object structure
        schema = self.schema.get("properties", {}).get("model_results", self.schema)
        
        errors = validate_schema(data, schema)
        
        assert not errors, f"model_results.json failed schema validation:\n" + "\n".join(errors)

    def test_feature_importance_schema(self):
        """Validate feature_importance.json against its schema."""
        feature_importance_path = OUTPUTS_DIR / "feature_importance.json"
        if not feature_importance_path.exists():
            pytest.skip("feature_importance.json not found. Run T019 first.")

        with open(feature_importance_path, "r") as f:
            data = json.load(f)

        schema = self.schema.get("properties", {}).get("feature_importance", self.schema)
        
        errors = validate_schema(data, schema)
        
        assert not errors, f"feature_importance.json failed schema validation:\n" + "\n".join(errors)

    def test_variance_partition_schema(self):
        """Validate variance_partition.csv against its schema."""
        variance_path = OUTPUTS_DIR / "variance_partition.csv"
        if not variance_path.exists():
            pytest.skip("variance_partition.csv not found. Run T019 first.")

        df = pd.read_csv(variance_path)
        
        schema = self.schema.get("properties", {}).get("variance_partition", self.schema)
        
        errors = validate_csv_schema(df, schema, "variance_partition.csv")
        
        assert not errors, f"variance_partition.csv failed schema validation:\n" + "\n".join(errors)

    def test_schema_file_structure(self):
        """Ensure the schema file itself has the expected structure."""
        assert "properties" in self.schema or "required" in self.schema, \
            "Schema file must define properties or required fields at the root level."