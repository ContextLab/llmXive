"""
T020 Implementation: Save extracted features to JSON conforming to static_features.schema.yaml.

This module loads the Parquet file generated by T018 (extract_features.py),
converts it to the JSON structure required by the schema, validates it,
and saves it to data/processed/static_features.json.
"""
import os
import sys
import json
import logging
import traceback
from pathlib import Path
from datetime import datetime
from typing import List, Dict, Any, Optional

# Add project root to path for imports
PROJECT_ROOT = Path(__file__).parent.parent.parent
sys.path.insert(0, str(PROJECT_ROOT))

# Import pandas for Parquet handling (dependency from requirements.txt)
try:
    import pandas as pd
except ImportError:
    print("ERROR: pandas is required. Install with: pip install pandas")
    sys.exit(1)

# Import schema validation utilities
import yaml

# Configure logging
logging.basicConfig(
    level=logging.INFO,
    format='%(asctime)s - %(levelname)s - %(message)s',
    handlers=[
        logging.StreamHandler(sys.stdout),
        logging.FileHandler(PROJECT_ROOT / 'logs' / 'feature_saver.log', mode='a')
    ]
)
logger = logging.getLogger(__name__)

SCHEMA_PATH = PROJECT_ROOT / 'contracts' / 'static_features.schema.yaml'
INPUT_PARQUET_PATH = PROJECT_ROOT / 'data' / 'processed' / 'static_features.parquet'
OUTPUT_JSON_PATH = PROJECT_ROOT / 'data' / 'processed' / 'static_features.json'


def load_schema(schema_path: Path) -> Dict[str, Any]:
    """Load the JSON schema from YAML file."""
    if not schema_path.exists():
        raise FileNotFoundError(f"Schema file not found: {schema_path}")
    
    with open(schema_path, 'r', encoding='utf-8') as f:
        return yaml.safe_load(f)


def validate_feature_record(record: Dict[str, Any], schema: Dict[str, Any]) -> bool:
    """
    Perform basic validation of a single feature record against the schema.
    Note: Full JSON Schema validation requires 'jsonschema' library, but we
    implement basic checks here to avoid adding new dependencies.
    """
    required_fields = ['example_id', 'token_id', 'token_text', 'feature_vector']
    
    for field in required_fields:
        if field not in record:
            logger.error(f"Missing required field: {field}")
            return False
    
    # Validate feature_vector structure
    fv = record['feature_vector']
    if not isinstance(fv, dict):
        logger.error("feature_vector must be a dictionary")
        return False
    
    if 'ngrams' not in fv or 'pos' not in fv or 'semantic' not in fv:
        logger.error("feature_vector must contain 'ngrams', 'pos', and 'semantic'")
        return False
    
    # Validate types
    if not isinstance(record['token_id'], int):
        logger.error("token_id must be an integer")
        return False
    
    if not isinstance(fv['pos'], list):
        logger.error("pos must be a list")
        return False
    
    if not isinstance(fv['semantic'], list):
        logger.error("semantic must be a list")
        return False
    
    return True


def convert_parquet_to_json(parquet_path: Path, schema: Dict[str, Any]) -> Dict[str, Any]:
    """
    Convert the Parquet file to the JSON structure defined in the schema.
    """
    if not parquet_path.exists():
        raise FileNotFoundError(f"Input Parquet file not found: {parquet_path}")
    
    logger.info(f"Loading Parquet file: {parquet_path}")
    df = pd.read_parquet(parquet_path)
    
    logger.info(f"Loaded {len(df)} records from Parquet file")
    
    # Build the JSON structure
    json_output = {
        "metadata": {
            "source_dataset": "gsm8k_verified",
            "feature_types": ["ngrams", "pos", "semantic"],
            "embedding_model": "sentence-transformers/all-MiniLM-L6-v2",
            "nlp_model": "en_core_web_sm",
            "generated_at": datetime.utcnow().isoformat() + "Z",
            "total_tokens": len(df),
            "oov_count": 0  # Will be calculated
        },
        "features": []
    }
    
    oov_count = 0
    
    for idx, row in df.iterrows():
        # Handle potential NaN values in feature vectors
        feature_record = {
            "example_id": str(row['example_id']),
            "token_id": int(row['token_id']),
            "token_text": str(row['token_text']),
            "is_oov": bool(row.get('is_oov', False))
        }
        
        # Process feature_vector
        fv_raw = row['feature_vector']
        if isinstance(fv_raw, dict):
            feature_record["feature_vector"] = fv_raw
        elif isinstance(fv_raw, str):
            # Try to parse if stored as string (edge case)
            try:
                feature_record["feature_vector"] = json.loads(fv_raw)
            except json.JSONDecodeError:
                logger.error(f"Failed to parse feature_vector for token {idx}")
                continue
        else:
            logger.error(f"Unexpected feature_vector type for token {idx}: {type(fv_raw)}")
            continue
        
        # Validate the record
        if not validate_feature_record(feature_record, schema):
            logger.warning(f"Skipping invalid record at index {idx}")
            continue
        
        json_output["features"].append(feature_record)
        
        if feature_record.get('is_oov', False):
            oov_count += 1
    
    json_output["metadata"]["oov_count"] = oov_count
    
    logger.info(f"Processed {len(json_output['features'])} valid records, {oov_count} OOV")
    
    return json_output


def save_json_output(data: Dict[str, Any], output_path: Path) -> None:
    """Save the JSON data to the specified path."""
    if not output_path.parent.exists():
        output_path.parent.mkdir(parents=True, exist_ok=True)
    
    logger.info(f"Saving JSON output to: {output_path}")
    
    with open(output_path, 'w', encoding='utf-8') as f:
        json.dump(data, f, indent=2, ensure_ascii=False)
    
    logger.info(f"Successfully saved {output_path}")


def main() -> int:
    """
    Main entry point for T020.
    Returns 0 on success, non-zero on failure.
    """
    try:
        logger.info("Starting T020: Save extracted features to JSON")
        
        # Load schema
        schema = load_schema(SCHEMA_PATH)
        logger.info(f"Loaded schema from {SCHEMA_PATH}")
        
        # Convert Parquet to JSON
        json_data = convert_parquet_to_json(INPUT_PARQUET_PATH, schema)
        
        # Save JSON output
        save_json_output(json_data, OUTPUT_JSON_PATH)
        
        # Final validation: check file exists and is not empty
        if not OUTPUT_JSON_PATH.exists():
            logger.error("Output file was not created")
            return 1
        
        if OUTPUT_JSON_PATH.stat().st_size == 0:
            logger.error("Output file is empty")
            return 1
        
        logger.info("T020 completed successfully")
        return 0
        
    except FileNotFoundError as e:
        logger.error(f"File not found: {e}")
        return 1
    except Exception as e:
        logger.error(f"Unexpected error: {e}")
        traceback.print_exc()
        return 1


if __name__ == "__main__":
    sys.exit(main())
