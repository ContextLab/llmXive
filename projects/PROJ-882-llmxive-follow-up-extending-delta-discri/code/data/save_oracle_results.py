def load_schema(schema_path: Path) -> Dict[str, Any]:
    """
    Load the JSON schema from the contracts directory.
    """
    if not schema_path.exists():
        raise FileNotFoundError(f"Schema file not found: {schema_path}")
    
    with open(schema_path, 'r', encoding='utf-8') as f:
        return json.load(f)


def validate_against_schema(data: List[Dict[str, Any]], schema: Dict[str, Any]) -> bool:
    """
    Basic validation of data against the schema structure.
    Checks for required fields and types as defined in the schema.
    Note: This is a lightweight validator. For strict JSON Schema validation,
    a library like `jsonschema` would be used, but we stick to standard lib + logic.
    """
    required_fields = schema.get('required', [])
    properties = schema.get('properties', {})

    for i, item in enumerate(data):
        if not isinstance(item, dict):
            logger.error(f"Item {i} is not a dictionary")
            return False
        
        # Check required fields
        for field in required_fields:
            if field not in item:
                logger.error(f"Item {i} missing required field: {field}")
                return False

        # Check types for known fields
        if 'token_id' in item and not isinstance(item['token_id'], int):
            logger.warning(f"Item {i} token_id is not int, attempting conversion or skip")
            # Depending on strictness, we might fail here. 
            # For now, we assume the generator (T013) produces correct types.

        if 'coefficient' in item:
            val = item['coefficient']
            if not isinstance(val, (int, float)):
                logger.error(f"Item {i} coefficient is not numeric: {type(val)}")
                return False
            # Check for NaN/Inf if required by schema logic (often implicit)
            if isinstance(val, float):
                if val != val: # NaN check
                    logger.error(f"Item {i} coefficient is NaN")
                    return False
                if abs(val) == float('inf'):
                    logger.error(f"Item {i} coefficient is Inf")
                    return False

        if 'variance_check' in item:
            if not isinstance(item['variance_check'], bool):
                logger.error(f"Item {i} variance_check is not boolean")
                return False

    return True


