"""
Contract test for UnifiedDataset schema compliance.

This test verifies that the unified dataset generated by the preprocessing
pipeline adheres to the schema defined in contracts/unified_dataset.schema.yaml.
It checks required columns, data types, and value constraints.
"""
import os
import sys
import json
import yaml
import pytest
from pathlib import Path
import pandas as pd
import numpy as np

# Add project root to path to import config if needed, though we use relative paths
project_root = Path(__file__).parent.parent.parent
contracts_dir = project_root / "contracts"
data_dir = project_root / "data"
processed_dir = data_dir / "processed"

# Schema file path
SCHEMA_PATH = contracts_dir / "unified_dataset.schema.yaml"

@pytest.fixture
def schema_definition():
    """Load the schema definition from the YAML file."""
    if not SCHEMA_PATH.exists():
        pytest.fail(f"Schema file not found: {SCHEMA_PATH}")
    
    with open(SCHEMA_PATH, 'r') as f:
        return yaml.safe_load(f)

@pytest.fixture
def unified_dataset_path():
    """Get the path to the unified dataset file."""
    # The dataset is expected to be at data/processed/unified_dataset.parquet
    # If it doesn't exist, we might need to run the preprocessing pipeline first
    # For this contract test, we assume the file exists or will be created by T021
    dataset_path = processed_dir / "unified_dataset.parquet"
    if not dataset_path.exists():
        # Check if mock data was generated instead
        # The preprocessing pipeline should have created this file
        pytest.skip("Unified dataset not found. Run preprocessing pipeline first (T021).")
    return dataset_path

def load_unified_dataset(path: Path) -> pd.DataFrame:
    """Load the unified dataset from parquet file."""
    try:
        return pd.read_parquet(path)
    except Exception as e:
        pytest.fail(f"Failed to load unified dataset: {str(e)}")

def test_schema_file_exists(schema_definition):
    """Test that the schema file exists and is valid YAML."""
    assert schema_definition is not None
    assert "columns" in schema_definition
    assert "required_columns" in schema_definition

def test_required_columns_present(schema_definition, unified_dataset_path):
    """Test that all required columns are present in the dataset."""
    df = load_unified_dataset(unified_dataset_path)
    required_columns = schema_definition.get("required_columns", [])
    
    missing_columns = []
    for col in required_columns:
        if col not in df.columns:
            missing_columns.append(col)
    
    if missing_columns:
        pytest.fail(f"Missing required columns: {missing_columns}")

def test_column_types(schema_definition, unified_dataset_path):
    """Test that columns have the expected data types."""
    df = load_unified_dataset(unified_dataset_path)
    columns_schema = schema_definition.get("columns", {})
    
    type_errors = []
    for col_name, col_spec in columns_schema.items():
        if col_name not in df.columns:
            continue  # Skip if column doesn't exist (caught by required columns test)
        
        expected_type = col_spec.get("type")
        actual_dtype = df[col_name].dtype
        
        # Map pandas dtypes to expected types
        type_mapping = {
            "integer": (np.integer, int),
            "float": (np.floating, float),
            "string": (object, str),
            "boolean": (bool, np.bool_),
            "category": (pd.CategoricalDtype,)
        }
        
        if expected_type in type_mapping:
            expected_types = type_mapping[expected_type]
            if not isinstance(actual_dtype.type, type) or not issubclass(actual_dtype.type, expected_types):
                # Special handling for categorical
                if expected_type == "category" and not isinstance(df[col_name].dtype, pd.CategoricalDtype):
                    type_errors.append(f"Column '{col_name}': expected category, got {actual_dtype}")
                elif expected_type != "category":
                    type_errors.append(f"Column '{col_name}': expected {expected_type}, got {actual_dtype}")
    
    if type_errors:
        pytest.fail("Type mismatches found:\n" + "\n".join(type_errors))

def test_genotype_encoding_range(schema_definition, unified_dataset_path):
    """Test that genotype columns are encoded as 0, 1, or 2."""
    df = load_unified_dataset(unified_dataset_path)
    columns_schema = schema_definition.get("columns", {})
    
    for col_name, col_spec in columns_schema.items():
        if col_spec.get("type") == "integer" and "genotype" in col_name.lower():
            unique_values = set(df[col_name].dropna().unique())
            valid_values = {0, 1, 2}
            
            invalid_values = unique_values - valid_values
            if invalid_values:
                pytest.fail(f"Genotype column '{col_name}' contains invalid values: {invalid_values}. Expected only 0, 1, 2.")

def test_no_missing_required_values(schema_definition, unified_dataset_path):
    """Test that required columns have no missing values."""
    df = load_unified_dataset(unified_dataset_path)
    required_columns = schema_definition.get("required_columns", [])
    
    missing_counts = {}
    for col in required_columns:
        if col in df.columns:
            missing_count = df[col].isna().sum()
            if missing_count > 0:
                missing_counts[col] = missing_count
    
    if missing_counts:
        pytest.fail(f"Required columns have missing values:\n" + "\n".join([f"{col}: {count}" for col, count in missing_counts.items()]))

def test_nutrient_condition_values(schema_definition, unified_dataset_path):
    """Test that nutrient_condition column has valid values."""
    df = load_unified_dataset(unified_dataset_path)
    
    if "nutrient_condition" in df.columns:
        valid_conditions = {"control", "low_n", "low_p", "low_k"}
        actual_conditions = set(df["nutrient_condition"].dropna().unique())
        
        invalid_conditions = actual_conditions - valid_conditions
        if invalid_conditions:
            pytest.fail(f"Invalid nutrient conditions found: {invalid_conditions}. Valid: {valid_conditions}")

def test_schema_compliance_report(schema_definition, unified_dataset_path):
    """Generate a compliance report for the dataset."""
    df = load_unified_dataset(unified_dataset_path)
    
    report = {
        "file_path": str(unified_dataset_path),
        "total_rows": len(df),
        "total_columns": len(df.columns),
        "required_columns_present": all(col in df.columns for col in schema_definition.get("required_columns", [])),
        "column_details": {}
    }
    
    for col_name, col_spec in schema_definition.get("columns", {}).items():
        if col_name in df.columns:
            report["column_details"][col_name] = {
                "dtype": str(df[col_name].dtype),
                "missing_count": int(df[col_name].isna().sum()),
                "unique_values": int(df[col_name].nunique()),
                "sample_values": df[col_name].dropna().head(3).tolist()
            }
    
    # Log report for debugging
    print("\n=== Schema Compliance Report ===")
    print(json.dumps(report, indent=2, default=str))
    
    assert report["required_columns_present"], "Dataset does not contain all required columns"