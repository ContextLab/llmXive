"""
Complexity Analysis Module for Code Review Impact Study.

This module calculates Lines of Code (LOC) and Cyclomatic Complexity
for code snippets using the `radon` library, as specified in FR-003 and FR-009.

It processes Parquet files containing code snippets (generated by T014b)
and outputs a processed dataset with complexity metrics.
"""

import os
import sys
import logging
from pathlib import Path
from typing import List, Dict, Any, Optional, Tuple

import pandas as pd
import numpy as np
from radon.complexity import cc_visit
from radon.raw import analyze as raw_analyze

# Configure logging
logging.basicConfig(
    level=logging.INFO,
    format='%(asctime)s - %(levelname)s - %(message)s'
)
logger = logging.getLogger(__name__)

# Constants
DEFAULT_INPUT_PATH = "data/processed/generated_snippets.parquet"
DEFAULT_OUTPUT_PATH = "data/processed/complexity_metrics.parquet"
MISSING_VALUE = -1.0

def calculate_snippet_complexity(code_snippet: str) -> Tuple[float, float]:
    """
    Calculate LOC and Cyclomatic Complexity for a single code snippet.

    Args:
        code_snippet (str): The Python code snippet to analyze.

    Returns:
        Tuple[float, float]: A tuple containing (loc, cyclomatic_complexity).
                             Returns (MISSING_VALUE, MISSING_VALUE) if analysis fails.
    """
    if not code_snippet or not isinstance(code_snippet, str):
        logger.warning("Invalid code snippet provided.")
        return MISSING_VALUE, MISSING_VALUE

    try:
        # Raw analysis for LOC
        raw_info = raw_analyze(code_snippet)
        loc = float(raw_info.loc)

        # Complexity analysis for Cyclomatic Complexity
        # cc_visit returns a list of complexity results for each function/class
        complexity_results = cc_visit(code_snippet)

        if complexity_results:
            # Sum the complexity of all functions/classes found
            # or take the max if we want the "worst" function.
            # Standard practice for a file/snippet metric is often the sum or max.
            # We will use the sum of all function complexities as the snippet's total complexity.
            total_complexity = sum(res.complexity for res in complexity_results)
            # If the snippet has no functions (just top-level code), radon might return 0
            # but top-level statements have complexity 1.
            # radon.cc_visit counts functions. If no functions, complexity is effectively 1 (the module itself).
            # However, for consistency with common metrics, we'll use the sum.
            # If no functions found, we assume a baseline complexity of 1 for the snippet itself.
            if total_complexity == 0:
                total_complexity = 1.0
        else:
            # No functions/classes detected, but code exists.
            # Assign a baseline complexity of 1.
            total_complexity = 1.0

        return loc, float(total_complexity)

    except Exception as e:
        logger.warning(f"Failed to analyze snippet complexity: {e}")
        return MISSING_VALUE, MISSING_VALUE

def process_dataset(input_path: str, output_path: str) -> None:
    """
    Load a dataset of code snippets, calculate complexity metrics, and save the result.

    Args:
        input_path (str): Path to the input Parquet file.
        output_path (str): Path to the output Parquet file.
    """
    input_file = Path(input_path)
    output_file = Path(output_path)

    if not input_file.exists():
        raise FileNotFoundError(f"Input file not found: {input_path}")

    # Ensure output directory exists
    output_file.parent.mkdir(parents=True, exist_ok=True)

    logger.info(f"Loading dataset from {input_path}")
    try:
        df = pd.read_parquet(input_file)
    except Exception as e:
        logger.error(f"Failed to read Parquet file: {e}")
        raise

    if df.empty:
        logger.warning("Input dataset is empty.")
        # Save an empty dataframe with expected columns
        empty_df = pd.DataFrame(columns=['snippet_id', 'source_code', 'loc', 'cyclomatic_complexity'])
        empty_df.to_parquet(output_file, index=False)
        return

    # Ensure 'source_code' column exists
    if 'source_code' not in df.columns:
        # Try to find a column that might contain code (common names)
        possible_code_cols = ['code', 'content', 'python_code', 'snippet']
        found_col = None
        for col in possible_code_cols:
            if col in df.columns:
                found_col = col
                break
        
        if found_col:
            logger.info(f"Using column '{found_col}' as source code.")
            code_col = found_col
        else:
            raise ValueError("Could not find a 'source_code' column or common alternatives in the dataset.")
    else:
        code_col = 'source_code'

    logger.info(f"Calculating complexity for {len(df)} snippets...")
    
    locs = []
    complexities = []
    valid_count = 0
    invalid_count = 0

    for idx, row in df.iterrows():
        code = row[code_col]
        loc, cc = calculate_snippet_complexity(code)
        locs.append(loc)
        complexities.append(cc)
        
        if loc != MISSING_VALUE:
            valid_count += 1
        else:
            invalid_count += 1

        if (idx + 1) % 100 == 0:
            logger.info(f"Processed {idx + 1}/{len(df)} snippets...")

    logger.info(f"Processing complete. Valid: {valid_count}, Failed: {invalid_count}")

    # Add metrics to dataframe
    df['loc'] = locs
    df['cyclomatic_complexity'] = complexities

    # Ensure output directory exists
    output_file.parent.mkdir(parents=True, exist_ok=True)

    logger.info(f"Saving processed dataset to {output_path}")
    df.to_parquet(output_file, index=False)
    logger.info("Done.")

def main():
    """Main entry point for the complexity extraction script."""
    # Allow overriding paths via command line arguments
    input_path = sys.argv[1] if len(sys.argv) > 1 else DEFAULT_INPUT_PATH
    output_path = sys.argv[2] if len(sys.argv) > 2 else DEFAULT_OUTPUT_PATH

    logger.info(f"Starting complexity extraction.")
    logger.info(f"Input: {input_path}")
    logger.info(f"Output: {output_path}")

    try:
        process_dataset(input_path, output_path)
    except Exception as e:
        logger.error(f"Pipeline failed: {e}")
        sys.exit(1)

if __name__ == "__main__":
    main()
