def load_generated_samples_metadata() -> pd.DataFrame:
    """
    Constructs a metadata DataFrame of all generated samples.
    Reads directory structure to map file paths to task_id and source_type.
    """
    paths = get_paths()
    generated_dir = paths['data_generated']

    if not generated_dir.exists():
        raise FileNotFoundError(
            f"Generated samples directory not found at {generated_dir}. "
            "Please run code/generate.py first."
        )

    records = []

    # Traverse: data/generated/{model}/{benchmark}/{task_id}/samples/{filename}
    for model_dir in generated_dir.iterdir():
        if not model_dir.is_dir():
            continue

        for benchmark_dir in model_dir.iterdir():
            if not benchmark_dir.is_dir():
                continue

            for task_dir in benchmark_dir.iterdir():
                if not task_dir.is_dir():
                    continue

                task_id = task_dir.name
                samples_dir = task_dir / 'samples'

                if not samples_dir.exists():
                    continue

                for sample_file in samples_dir.iterdir():
                    if sample_file.suffix.lower() == '.py':
                        records.append({
                            'file_path': str(sample_file),
                            'task_id': task_id,
                            'benchmark': benchmark_dir.name,
                            'model': model_dir.name,
                            'source_type': 'LLM' # All in this dir are LLM generated
                        })

    return pd.DataFrame(records)


def select_stratified_subset(df: pd.DataFrame, n: int = 20, seed: int = 42) -> pd.DataFrame:
    """
    Selects a stratified random sample of n items per group.
    Groups are determined by 'task_id' and 'source_type' (or just task_id if source is uniform).
    Uses deterministic seed for reproducibility.
    """
    set_seed(seed)
    np.random.seed(seed)

    # Group by task_id (and source_type if present)
    # The spec says "per group", usually implying per task or per CWE group.
    # Given the input is a list of files with potential vulnerabilities,
    # we stratify by the task_id to ensure coverage across tasks.
    group_col = 'task_id'

    if group_col not in df.columns:
        # Fallback if task_id is missing, use index or file_path
        logger.warning("task_id column missing, using file_path for stratification.")
        group_col = 'file_path'

    sampled_indices = []

    for name, group in df.groupby(group_col):
        if len(group) <= n:
            sampled_indices.extend(group.index.tolist())
        else:
            # Deterministic shuffle
            indices = group.index.to_numpy()
            np.random.shuffle(indices)
            sampled_indices.extend(indices[:n].tolist())

    return df.loc[sampled_indices]


def check_code_patterns(file_path: str, cwe_id: str) -> bool:
    """
    Reads the source file and checks if the code matches the regex patterns
    associated with the specific CWE ID.
    Returns True if a match is found (validating the vulnerability), False otherwise.
    """
    patterns = CWE_PATTERNS.get(cwe_id, GENERIC_PATTERNS)

    try:
        with open(file_path, 'r', encoding='utf-8', errors='ignore') as f:
            content = f.read()
    except Exception as e:
        logger.warning(f"Could not read file {file_path} for validation: {e}")
        return False

    for pattern in patterns:
        try:
            if re.search(pattern, content):
                return True
        except re.error:
            logger.warning(f"Invalid regex pattern: {pattern}")
            continue

    return False


def validate_vulnerabilities(
    raw_reports: List[Dict[str, Any]],
    samples_df: pd.DataFrame,
    seed: int = 42
) -> List[Dict[str, Any]]:
    """
    Main validation loop.
    1. Stratifies the samples (n=20 per task).
    2. For each selected sample, checks if the reported CWE matches code patterns.
    3. Returns a list of validation results.
    """
    # Step 1: Stratified Selection
    # We only validate samples that appear in the vulnerability reports.
    # Create a unique key for joining: file_path
    report_files = set(r.get('file_path') for r in raw_reports)
    samples_df = samples_df[samples_df['file_path'].isin(report_files)]

    if samples_df.empty:
        logger.warning("No samples found in vulnerability reports to validate.")
        return []

    selected_samples = select_stratified_subset(samples_df, n=20, seed=seed)
    logger.info(f"Selected {len(selected_samples)} samples for validation (stratified).")

    results = []

    # Step 2: Pattern Matching
    for _, row in selected_samples.iterrows():
        file_path = row['file_path']
        task_id = row['task_id']

        # Find all vulnerabilities reported for this file
        file_vulns = [r for r in raw_reports if r.get('file_path') == file_path]

        for vuln in file_vulns:
            cwe_id = vuln.get('cwe_id', 'Unknown')
            line_number = vuln.get('line_number', 0)

            # Check pattern
            is_valid = check_code_patterns(file_path, cwe_id)

            results.append({
                'sample_id': f"{task_id}_{cwe_id}_{line_number}",
                'file_path': file_path,
                'task_id': task_id,
                'cwe_id': cwe_id,
                'line_number': line_number,
                'is_valid': is_valid
            })

    return results


def save_validation_results(results: List[Dict[str, Any]], output_path: str) -> None:
    """
    Saves the validation results to a CSV file.
    Columns: sample_id, is_valid (and others for traceability).
    """
    if not results:
        logger.warning("No results to save.")
        # Create empty file with headers
        with open(output_path, 'w', newline='', encoding='utf-8') as f:
            writer = csv.DictWriter(f, fieldnames=['sample_id', 'is_valid'])
            writer.writeheader()
        return

    df = pd.DataFrame(results)
    # Ensure columns are in correct order if needed, but pandas handles this
    df.to_csv(output_path, index=False)
    logger.info(f"Validation results saved to {output_path}")


def main() -> None:
    """
    Entry point for the validator agent.
    """
    paths = get_paths()
    output_path = paths['data_processed'] / 'validator_flags.csv'

    # Ensure output directory exists
    paths['data_processed'].mkdir(parents=True, exist_ok=True)

    try:
        logger.info("Loading raw vulnerability reports...")
        raw_reports = load_raw_vulnerability_reports()
        logger.info(f"Loaded {len(raw_reports)} vulnerability findings.")

        logger.info("Loading generated samples metadata...")
        samples_df = load_generated_samples_metadata()
        logger.info(f"Found {len(samples_df)} generated samples.")

        logger.info("Running validation logic...")
        results = validate_vulnerabilities(raw_reports, samples_df)

        logger.info("Saving results...")
        save_validation_results(results, str(output_path))

        # Summary stats
        if results:
            valid_count = sum(1 for r in results if r['is_valid'])
            logger.info(f"Validation complete: {valid_count}/{len(results)} vulnerabilities validated as true positives.")
        else:
            logger.info("Validation complete: No findings to validate.")

    except Exception as e:
        logger.error(f"Validation failed: {e}")
        raise


if __name__ == '__main__':
    main()
