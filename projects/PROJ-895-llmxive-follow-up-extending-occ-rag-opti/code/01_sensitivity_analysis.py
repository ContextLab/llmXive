def load_sensitivity_results(csv_path: Path) -> List[Dict[str, Any]]:
    """
    Loads the sensitivity results CSV generated by the masking phase.
    Returns a list of dictionaries containing sensitivity data.
    """
    if not csv_path.exists():
        raise FileNotFoundError(f"Sensitivity results not found at {csv_path}. "
                                "Please run the masking phase first.")

    results = []
    with open(csv_path, 'r', newline='', encoding='utf-8') as f:
        reader = csv.DictReader(f)
        for row in reader:
            results.append({
                'layer_id': int(row['layer_id']),
                'param_id': row['param_id'],
                'sensitivity_score': float(row['sensitivity_score']),
                'delta_faithfulness': float(row['delta_faithfulness']),
                'random_baseline_score': float(row['random_baseline_score'])
            })
    return results


def identify_critical_subnetwork(
    sensitivity_results: List[Dict[str, Any]],
    retention_pct: float
) -> Tuple[List[Dict[str, Any]], Dict[str, Any]]:
    """
    Identifies the "Critical Sub-network" candidate by sorting parameters by
    magnitude of performance drop (delta_faithfulness) relative to random baseline.

    Args:
        sensitivity_results: List of parameter sensitivity data.
        retention_pct: Percentage of parameters to retain (0.0 to 1.0).

    Returns:
        Tuple of (selected_params, summary_stats)
    """
    if not sensitivity_results:
        raise ValueError("Sensitivity results list is empty.")

    # Sort by delta_faithfulness in descending order (highest impact first)
    # We want to keep the parameters that, when masked, cause the BIGGEST drop.
    sorted_results = sorted(
        sensitivity_results,
        key=lambda x: abs(x['delta_faithfulness']),
        reverse=True
    )

    total_params = len(sorted_results)
    retained_count = int(total_params * retention_pct)

    if retained_count == 0:
        logger.warning(f"Retention percentage {retention_pct} resulted in 0 parameters.")
        return [], {
            "total_params": total_params,
            "retained_count": 0,
            "retention_pct": retention_pct,
            "cutoff_threshold": None
        }

    critical_params = sorted_results[:retained_count]
    cutoff_threshold = critical_params[-1]['delta_faithfulness'] if critical_params else 0.0

    summary_stats = {
        "total_params": total_params,
        "retained_count": retained_count,
        "retention_pct": retention_pct,
        "cutoff_threshold": cutoff_threshold,
        "avg_sensitivity_retained": np.mean([p['sensitivity_score'] for p in critical_params]) if critical_params else 0.0,
        "avg_delta_retained": np.mean([p['delta_faithfulness'] for p in critical_params]) if critical_params else 0.0
    }

    logger.info(f"Identified Critical Sub-network: {retained_count}/{total_params} parameters retained.")
    logger.info(f"Cutoff threshold (delta_faithfulness): {cutoff_threshold:.4f}")

    return critical_params, summary_stats


def save_critical_subnetwork_results(
    critical_params: List[Dict[str, Any]],
    summary_stats: Dict[str, Any],
    output_csv_path: Path,
    stats_json_path: Path
):
    """
    Saves the identified critical sub-network to CSV and summary stats to JSON.
    """
    # Ensure output directory exists
    output_csv_path.parent.mkdir(parents=True, exist_ok=True)

    # Save CSV
    with open(output_csv_path, 'w', newline='', encoding='utf-8') as f:
        fieldnames = ['layer_id', 'param_id', 'sensitivity_score', 'delta_faithfulness', 'random_baseline_score', 'is_critical']
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()

        critical_param_ids = {p['param_id'] for p in critical_params}

        # We need to write the full list with a flag, or just the critical ones?
        # Task T014 says "identify the candidate", T016 uses it.
        # Usually, we save the critical set for the next step.
        # Let's save just the critical set to keep it clean for the pruning step.
        for param in critical_params:
            row = param.copy()
            row['is_critical'] = True
            writer.writerow(row)

    # Save Stats
    with open(stats_json_path, 'w', encoding='utf-8') as f:
        json.dump(summary_stats, f, indent=2)

    logger.info(f"Critical sub-network saved to {output_csv_path}")
    logger.info(f"Summary stats saved to {stats_json_path}")


