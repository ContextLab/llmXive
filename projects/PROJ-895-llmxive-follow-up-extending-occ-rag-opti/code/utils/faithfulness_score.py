"""
Faithfulness Score Calculation Module for OCC-RAG.

This module implements the "Context Faithfulness Score" metric, which is a weighted
combination of ConFiQA accuracy and Citation Precision. It is designed to run
efficiently on CPU-only environments.

Metrics:
- ConFiQA Accuracy: Measures the factual correctness of generated answers against
  a reference set of questions and gold answers.
- Citation Precision: Measures the proportion of cited references that are actually
  relevant to the generated answer.

The final Faithfulness Score is calculated as:
Score = (w_conf * ConfQA_Accuracy) + (w_cit * Citation_Precision)
where w_conf + w_cit = 1.
"""

import json
import math
import re
from typing import List, Dict, Any, Optional, Tuple

# Default weights for the faithfulness score
DEFAULT_CONFQA_WEIGHT = 0.6
DEFAULT_CITATION_WEIGHT = 0.4
DEFAULT_THRESHOLD = 0.5

def calculate_confqa_accuracy(
    generated_answers: List[str],
    gold_answers: List[str],
    questions: List[str],
    model_fn: Optional[callable] = None
) -> float:
    """
    Calculates the ConFiQA accuracy score.

    Since we are running on CPU and may not have the specific ConFiQA model
    readily available or the task implies a heuristic or external scorer,
    we implement a robust semantic similarity heuristic or a placeholder for
    the specific model inference if 'model_fn' is provided.

    For the purpose of this pipeline's CPU constraints and immediate execution:
    1. If model_fn is provided, it is used to score the (question, generated, gold) triplet.
    2. Otherwise, a semantic overlap heuristic (Jaccard on n-grams) is used as a proxy
       for factual alignment, normalized to [0, 1].

    Args:
        generated_answers: List of strings generated by the model.
        gold_answers: List of strings containing the ground truth.
        questions: List of strings containing the original questions.
        model_fn: Optional function taking (question, generated, gold) -> float.

    Returns:
        float: Average accuracy score between 0 and 1.
    """
    if not generated_answers or not gold_answers or not questions:
        return 0.0

    if len(generated_answers) != len(gold_answers) or len(generated_answers) != len(questions):
        raise ValueError("Input lists must be of equal length.")

    scores = []
    for gen, gold, q in zip(generated_answers, gold_answers, questions):
        if model_fn:
            score = model_fn(q, gen, gold)
        else:
            # Heuristic fallback for CPU-only execution without heavy model dependencies:
            # Normalize text and compute Jaccard similarity of bigrams.
            # This serves as a proxy for "factual alignment" in the absence of the specific
            # ConFiQA BERT model in the immediate environment.
            def normalize(text: str) -> set:
                text = text.lower()
                text = re.sub(r'[^\w\s]', '', text)
                tokens = text.split()
                return {
                    f"{tokens[i]}_{tokens[i+1]}" for i in range(len(tokens) - 1)
                }

            gen_set = normalize(gen)
            gold_set = normalize(gold)

            if not gen_set or not gold_set:
                scores.append(0.0)
                continue

            intersection = len(gen_set & gold_set)
            union = len(gen_set | gold_set)
            scores.append(intersection / union if union > 0 else 0.0)

    return sum(scores) / len(scores)

def calculate_citation_precision(
    generated_answers: List[str],
    cited_docs: List[List[str]],
    gold_docs: List[List[str]]
) -> float:
    """
    Calculates Citation Precision.

    Precision = (Number of cited docs that are in gold docs) / (Total number of cited docs)

    Args:
        generated_answers: List of generated answers (used for context if needed, though
                           primarily the cited_docs list is used).
        cited_docs: List of lists, where each inner list contains document IDs or texts
                    cited by the corresponding generated answer.
        gold_docs: List of lists, where each inner list contains the IDs or texts of
                   documents that *should* have been cited (gold standard).

    Returns:
        float: Average citation precision between 0 and 1.
    """
    if not cited_docs:
        return 0.0

    precisions = []
    for cited, gold in zip(cited_docs, gold_docs):
        if not cited:
            # If no citations are provided, precision is 0 (or undefined, but 0 is conservative)
            precisions.append(0.0)
            continue

        cited_set = set(cited)
        gold_set = set(gold)

        if not gold_set:
            # If gold has no docs but model cited some, precision is 0
            precisions.append(0.0)
            continue

        relevant_citations = len(cited_set & gold_set)
        precision = relevant_citations / len(cited_set)
        precisions.append(precision)

    return sum(precisions) / len(precisions)

def calculate_faithfulness_score(
    generated_answers: List[str],
    gold_answers: List[str],
    questions: List[str],
    cited_docs: List[List[str]],
    gold_docs: List[List[str]],
    confqa_weight: float = DEFAULT_CONFQA_WEIGHT,
    citation_weight: float = DEFAULT_CITATION_WEIGHT,
    model_fn: Optional[callable] = None
) -> float:
    """
    Calculates the composite Context Faithfulness Score.

    Score = (w_conf * ConfQA_Accuracy) + (w_cit * Citation_Precision)

    Args:
        generated_answers: List of generated answers.
        gold_answers: List of gold answers.
        questions: List of questions.
        cited_docs: List of lists of cited documents.
        gold_docs: List of lists of gold documents.
        confqa_weight: Weight for ConFiQA accuracy (default 0.6).
        citation_weight: Weight for Citation Precision (default 0.4).
        model_fn: Optional scorer for ConFiQA accuracy.

    Returns:
        float: The weighted faithfulness score.
    """
    if not generated_answers:
        return 0.0

    # Validate weights
    if confqa_weight < 0 or citation_weight < 0:
        raise ValueError("Weights must be non-negative.")
    total_weight = confqa_weight + citation_weight
    if total_weight == 0:
        raise ValueError("At least one weight must be non-zero.")

    # Normalize weights to sum to 1
    w_conf = confqa_weight / total_weight
    w_cit = citation_weight / total_weight

    confqa_acc = calculate_confqa_accuracy(
        generated_answers, gold_answers, questions, model_fn
    )
    cit_prec = calculate_citation_precision(
        generated_answers, cited_docs, gold_docs
    )

    return (w_conf * confqa_acc) + (w_cit * cit_prec)

def compute_batch_faithfulness(
    batch: Dict[str, Any],
    model_fn: Optional[callable] = None
) -> Dict[str, float]:
    """
    Computes faithfulness metrics for a single batch of data.

    Args:
        batch: Dictionary containing keys:
               - 'generated_answers': List[str]
               - 'gold_answers': List[str]
               - 'questions': List[str]
               - 'cited_docs': List[List[str]]
               - 'gold_docs': List[List[str]]
        model_fn: Optional function for scoring.

    Returns:
        Dictionary with keys:
        - 'confqa_accuracy': float
        - 'citation_precision': float
        - 'faithfulness_score': float
    """
    gen_ans = batch.get('generated_answers', [])
    gold_ans = batch.get('gold_answers', [])
    qs = batch.get('questions', [])
    cited = batch.get('cited_docs', [])
    gold_docs = batch.get('gold_docs', [])

    confqa_acc = calculate_confqa_accuracy(gen_ans, gold_ans, qs, model_fn)
    cit_prec = calculate_citation_precision(gen_ans, cited, gold_docs)

    score = calculate_faithfulness_score(
        gen_ans, gold_ans, qs, cited, gold_docs
    )

    return {
        'confqa_accuracy': confqa_acc,
        'citation_precision': cit_prec,
        'faithfulness_score': score
    }

def aggregate_faithfulness_metrics(
    results: List[Dict[str, float]]
) -> Dict[str, float]:
    """
    Aggregates faithfulness metrics from a list of batch results.

    Args:
        results: List of dictionaries returned by compute_batch_faithfulness.

    Returns:
        Dictionary with aggregated means:
        - 'avg_confqa_accuracy': float
        - 'avg_citation_precision': float
        - 'avg_faithfulness_score': float
    """
    if not results:
        return {
            'avg_confqa_accuracy': 0.0,
            'avg_citation_precision': 0.0,
            'avg_faithfulness_score': 0.0
        }

    total_confqa = 0.0
    total_cit = 0.0
    total_score = 0.0

    for r in results:
        total_confqa += r.get('confqa_accuracy', 0.0)
        total_cit += r.get('citation_precision', 0.0)
        total_score += r.get('faithfulness_score', 0.0)

    n = len(results)
    return {
        'avg_confqa_accuracy': total_confqa / n,
        'avg_citation_precision': total_cit / n,
        'avg_faithfulness_score': total_score / n
    }