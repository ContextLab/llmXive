"""
Faithfulness Score Calculation Module.

Calculates the "Context Faithfulness Score" defined as a weighted combination
of ConFiQA accuracy and Citation Precision.

This module is designed to run entirely on CPU and operates on inference
results generated by the OCC-RAG model.
"""

import json
import math
from typing import List, Dict, Any, Optional, Tuple

# Configuration constants for the faithfulness metric
# Weights sum to 1.0. These can be adjusted via config if needed,
# but defaults are provided here for immediate use.
DEFAULT_CONFIQ_WEIGHT = 0.6
DEFAULT_CITATION_WEIGHT = 0.4

def calculate_confqa_accuracy(
    generated_answer: str,
    ground_truth: str,
    retrieved_context: str,
    model_runner: Any
) -> float:
    """
    Calculates ConFiQA accuracy for a single sample.

    ConFiQA accuracy measures whether the generated answer is factually consistent
    with the ground truth given the retrieved context.

    In this implementation, we use a lightweight, deterministic heuristic
    based on token overlap and key entity matching if a heavy LLM judge is not
    available or to avoid GPU dependency. If `model_runner` is a callable
    that can perform a "judge" inference, it would be used there.

    For CPU-only efficiency in the pipeline, we implement a robust string-based
    heuristic that approximates semantic overlap (e.g., Jaccard on lower-cased
    n-grams) which correlates with factual consistency in multi-hop QA.

    Args:
        generated_answer: The answer string produced by the model.
        ground_truth: The reference ground truth answer.
        retrieved_context: The context used to generate the answer.
        model_runner: Placeholder for a potential LLM judge (not used in heuristic mode).

    Returns:
        A float between 0.0 and 1.0 representing the accuracy score.
    """
    if not generated_answer or not ground_truth:
        return 0.0

    # Heuristic: N-gram overlap (Jaccard similarity)
    # This is a standard, CPU-efficient proxy for semantic similarity in QA
    # when a full LLM-as-a-judge is too heavy for the initial pipeline run.
    def get_ngrams(text: str, n: int = 3) -> set:
        words = text.lower().split()
        return set(
            ' '.join(words[i:i+n])
            for i in range(len(words) - n + 1)
        )

    gen_ngrams = get_ngrams(generated_answer)
    truth_ngrams = get_ngrams(ground_truth)

    if not gen_ngrams or not truth_ngrams:
        return 0.0

    intersection = gen_ngrams.intersection(truth_ngrams)
    union = gen_ngrams.union(truth_ngrams)

    jaccard_score = len(intersection) / len(union) if union else 0.0

    # Bonus: Check if key entities from context are present in answer
    # This ensures the answer actually uses the retrieved context
    context_words = set(retrieved_context.lower().split())
    answer_words = set(generated_answer.lower().split())
    context_coverage = len(answer_words.intersection(context_words)) / len(answer_words) if answer_words else 0.0

    # Weighted combination: 70% overlap, 30% context usage
    return 0.7 * jaccard_score + 0.3 * context_coverage


def calculate_citation_precision(
    generated_answer: str,
    ground_truth: str,
    retrieved_context: str
) -> float:
    """
    Calculates Citation Precision.

    Measures how precisely the generated answer cites or relies on the
    provided retrieved context versus hallucinating external knowledge.

    Heuristic:
    1. Identify spans in the generated answer that are present in the context.
    2. Measure the ratio of answer tokens covered by context spans.
    3. Penalize if the answer claims facts not supported by the context (approximated).

    Args:
        generated_answer: The answer string.
        ground_truth: The reference answer (used for validation of relevance).
        retrieved_context: The source context.

    Returns:
        A float between 0.0 and 1.0.
    """
    if not generated_answer or not retrieved_context:
        return 0.0

    gen_lower = generated_answer.lower()
    ctx_lower = retrieved_context.lower()

    # Simple heuristic: Fraction of answer tokens that appear in context
    # This is a lower-bound estimate of "supported by context"
    gen_tokens = gen_lower.split()
    supported_tokens = 0

    for token in gen_tokens:
        if token in ctx_lower:
            supported_tokens += 1

    coverage_ratio = supported_tokens / len(gen_tokens) if gen_tokens else 0.0

    # Penalty: If the answer is significantly longer than the context, it might be hallucinating
    # (Very rough heuristic to prevent long, ungrounded rambles)
    len_penalty = 1.0
    if len(gen_tokens) > len(ctx_lower.split()) * 0.5:
        # Allow some expansion, but penalize excessive length relative to context
        len_penalty = max(0.5, 1.0 - (len(gen_tokens) / (len(ctx_lower.split()) * 0.5)))

    return min(1.0, coverage_ratio * len_penalty)


def calculate_faithfulness_score(
    sample: Dict[str, Any],
    confiq_weight: float = DEFAULT_CONFIQ_WEIGHT,
    citation_weight: float = DEFAULT_CITATION_WEIGHT
) -> Dict[str, float]:
    """
    Calculates the full Context Faithfulness Score for a single sample.

    Score = (w1 * ConFiQA_Accuracy) + (w2 * Citation_Precision)

    Args:
        sample: A dictionary containing:
            - 'generated_answer': str
            - 'ground_truth': str
            - 'retrieved_context': str
        confiq_weight: Weight for ConFiQA component.
        citation_weight: Weight for Citation component.

    Returns:
        A dictionary with:
            - 'total_score': The weighted sum.
            - 'confiq_score': The ConFiQA accuracy.
            - 'citation_score': The citation precision.
    """
    if 'generated_answer' not in sample or 'ground_truth' not in sample or 'retrieved_context' not in sample:
        raise ValueError(
            "Sample must contain 'generated_answer', 'ground_truth', and 'retrieved_context' keys."
        )

    # Ensure weights sum to 1.0
    total_weight = confiq_weight + citation_weight
    if total_weight == 0:
        confiq_weight = 0.5
        citation_weight = 0.5
    else:
        confiq_weight /= total_weight
        citation_weight /= total_weight

    confiq_score = calculate_confqa_accuracy(
        sample['generated_answer'],
        sample['ground_truth'],
        sample['retrieved_context'],
        model_runner=None  # Heuristic mode
    )

    citation_score = calculate_citation_precision(
        sample['generated_answer'],
        sample['ground_truth'],
        sample['retrieved_context']
    )

    total_score = (confiq_weight * confiq_score) + (citation_weight * citation_score)

    return {
        'total_score': total_score,
        'confiq_score': confiq_score,
        'citation_score': citation_score
    }


def compute_batch_faithfulness(
    samples: List[Dict[str, Any]],
    confiq_weight: float = DEFAULT_CONFIQ_WEIGHT,
    citation_weight: float = DEFAULT_CITATION_WEIGHT
) -> List[Dict[str, float]]:
    """
    Computes faithfulness scores for a batch of samples.

    Args:
        samples: List of sample dictionaries.
        confiq_weight: Weight for ConFiQA.
        citation_weight: Weight for Citation.

    Returns:
        List of score dictionaries corresponding to each sample.
    """
    results = []
    for i, sample in enumerate(samples):
        try:
            scores = calculate_faithfulness_score(
                sample,
                confiq_weight=confiq_weight,
                citation_weight=citation_weight
            )
            scores['sample_id'] = sample.get('sample_id', i)
            results.append(scores)
        except Exception as e:
            # Log error but continue processing other samples
            print(f"Warning: Failed to calculate faithfulness for sample {i}: {e}")
            results.append({
                'sample_id': sample.get('sample_id', i),
                'total_score': 0.0,
                'confiq_score': 0.0,
                'citation_score': 0.0,
                'error': str(e)
            })
    return results


def aggregate_faithfulness_metrics(
    scores_list: List[Dict[str, float]]
) -> Dict[str, float]:
    """
    Aggregates a list of sample scores into overall metrics.

    Args:
        scores_list: List of dictionaries from compute_batch_faithfulness.

    Returns:
        Dictionary with mean, std, min, max for total, confiq, and citation scores.
    """
    if not scores_list:
        return {
            'mean_total': 0.0,
            'std_total': 0.0,
            'mean_confiq': 0.0,
            'mean_citation': 0.0
        }

    total_scores = [s['total_score'] for s in scores_list if 'error' not in s]
    confiq_scores = [s['confiq_score'] for s in scores_list if 'error' not in s]
    citation_scores = [s['citation_score'] for s in scores_list if 'error' not in s]

    def calc_stats(values: List[float]) -> Tuple[float, float]:
        if not values:
            return 0.0, 0.0
        mean_val = sum(values) / len(values)
        variance = sum((x - mean_val) ** 2 for x in values) / len(values)
        std_val = math.sqrt(variance)
        return mean_val, std_val

    mean_total, std_total = calc_stats(total_scores)
    mean_confiq, _ = calc_stats(confiq_scores)
    mean_citation, _ = calc_stats(citation_scores)

    return {
        'mean_total': mean_total,
        'std_total': std_total,
        'mean_confiq': mean_confiq,
        'mean_citation': mean_citation,
        'count': len(total_scores)
    }