"""
Contract test for analysis output schema.

This test verifies that the analysis output generated by code/analysis.py
conforms to the expected schema required for downstream processing and
reporting. It checks for the presence of required fields, data types,
and value constraints (e.g., non-null values, valid ranges).

The schema validates:
- Kendall's tau correlation coefficient and p-value
- Bootstrap confidence interval width
- Tobit regression coefficients and model fit statistics
- Sample size and censoring information
"""

