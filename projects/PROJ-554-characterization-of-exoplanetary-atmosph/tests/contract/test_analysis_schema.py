# Define the expected schema structure based on T030 output requirements
EXPECTED_SCHEMA_KEYS = {
    "kendall_tau": {"type": float, "nullable": False},
    "kendall_tau_p_value": {"type": float, "nullable": False},
    "kendall_tau_ci_width": {"type": float, "nullable": False},
    "bootstrap_iterations": {"type": int, "nullable": False},
    "tobit_coefficients": {
        "type": dict,
        "nullable": False,
        "required_keys": ["temperature", "mass", "metallicity", "intercept"]
    },
    "tobit_p_values": {
        "type": dict,
        "nullable": False,
        "required_keys": ["temperature", "mass", "metallicity"]
    },
    "model_fit": {
        "type": dict,
        "nullable": False,
        "required_keys": ["log_likelihood", "aic", "bic"]
    },
    "sample_size": {"type": int, "nullable": False},
    "censored_count": {"type": int, "nullable": False},
    "uncensored_count": {"type": int, "nullable": False},
    "power_analysis": {
        "type": dict,
        "nullable": False,
        "required_keys": ["achieved_power", "target_power", "effect_size_estimate"]
    },
    "detection_limit_info": {
        "type": dict,
        "nullable": False,
        "required_keys": ["min_snr", "max_snr", "mean_resolution"]
    }
}

def validate_schema_structure(data: Dict[str, Any]) -> List[str]:
    """
    Validates the top-level structure and key existence of the analysis output.

    Args:
        data: The loaded JSON dictionary from analysis_results.json

    Returns:
        A list of error messages. Empty if valid.
    """
    errors = []

    if not isinstance(data, dict):
        errors.append(f"Root element must be a dictionary, got {type(data)}")
        return errors

    for key, spec in EXPECTED_SCHEMA_KEYS.items():
        if key not in data:
            errors.append(f"Missing required key: '{key}'")
            continue

        value = data[key]

        # Check type
        expected_type = spec["type"]
        if not isinstance(value, expected_type):
            errors.append(f"Key '{key}': expected type {expected_type.__name__}, got {type(value).__name__}")
            continue

        # Check nullability
        if not spec.get("nullable", True) and value is None:
            errors.append(f"Key '{key}': value cannot be null")
            continue

        # Check nested required keys for dicts
        if expected_type == dict and "required_keys" in spec:
            for nested_key in spec["required_keys"]:
                if nested_key not in value:
                    errors.append(f"Key '{key}': missing nested key '{nested_key}'")

    return errors

def validate_value_constraints(data: Dict[str, Any]) -> List[str]:
    """
    Validates logical constraints on the values (e.g., p-values between 0 and 1).

    Args:
        data: The loaded JSON dictionary

    Returns:
        A list of error messages. Empty if valid.
    """
    errors = []

    # Kendall's tau must be between -1 and 1
    tau = data.get("kendall_tau")
    if tau is not None:
        if not (-1.0 <= tau <= 1.0):
            errors.append(f"Kendall's tau ({tau}) must be between -1.0 and 1.0")

    # P-values must be between 0 and 1
    p_val = data.get("kendall_tau_p_value")
    if p_val is not None:
        if not (0.0 <= p_val <= 1.0):
            errors.append(f"P-value ({p_val}) must be between 0.0 and 1.0")

    # CI width must be non-negative
    ci_width = data.get("kendall_tau_ci_width")
    if ci_width is not None and ci_width < 0:
        errors.append(f"CI width ({ci_width}) must be non-negative")

    # Counts must be non-negative
    for key in ["sample_size", "censored_count", "uncensored_count"]:
        val = data.get(key)
        if val is not None and val < 0:
            errors.append(f"{key} ({val}) must be non-negative")

    # Sample size consistency
    if data.get("sample_size") is not None and data.get("censored_count") is not None and data.get("uncensored_count") is not None:
        if data["sample_size"] != (data["censored_count"] + data["uncensored_count"]):
            errors.append(f"Sample size ({data['sample_size']}) must equal sum of censored and uncensored counts")

    # Power analysis target check
    power_data = data.get("power_analysis")
    if power_data:
        target = power_data.get("target_power", 0.8)
        achieved = power_data.get("achieved_power", 0.0)
        # We don't fail if achieved < target (that's a scientific result),
        # but we verify the fields exist and are numeric.
        if not isinstance(achieved, (int, float)):
            errors.append("achieved_power must be numeric")

    return errors

