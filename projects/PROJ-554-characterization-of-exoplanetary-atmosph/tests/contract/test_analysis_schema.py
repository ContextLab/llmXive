def test_analysis_regression_coherence(analysis_output_path):
    """
    Test that the regression results are internally coherent.
    e.g., if coefficients exist, standard errors or p-values should exist.
    """
    if not analysis_output_path.exists():
        pytest.skip(f"Output file {analysis_output_path} not found. Run analysis first.")

    with open(analysis_output_path, 'r') as f:
        data = json.load(f)

    # Check Tobit regression coherence
    coeffs = data.get("tobit_coefficients", {})
    p_vals = data.get("tobit_p_values", {})

    # If we have coefficients for predictors, we should have p-values for them
    required_predictors = ["temperature", "mass", "metallicity"]
    for pred in required_predictors:
        if pred in coeffs and pred not in p_vals:
            pytest.fail(f"Tobit regression has coefficient for '{pred}' but missing p-value")

    # Check model fit coherence
    fit = data.get("model_fit", {})
    if fit:
        assert "log_likelihood" in fit, "Model fit missing log_likelihood"
        assert "aic" in fit, "Model fit missing AIC"
        assert "bic" in fit, "Model fit missing BIC"