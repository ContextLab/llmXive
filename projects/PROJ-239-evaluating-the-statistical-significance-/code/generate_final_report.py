"""
Script to generate the final report CSV (data/derived/final_report.csv).

This script aggregates results from the baseline and robust simulations
(T014 and T021) and computes the empirical Type I error rates with
95% confidence intervals using the Clopper-Pearson method.

It reads:
  - data/derived/baseline_results.csv
  - data/derived/robustResults.csv

It writes:
  - data/derived/final_report.csv
"""
import argparse
import os
import sys
import warnings
import numpy as np
import pandas as pd

# Add project root to path for imports
project_root = os.path.abspath(os.path.join(os.path.dirname(__file__), '..'))
if project_root not in sys.path:
    sys.path.insert(0, project_root)

from code.config import load_config, ALPHA_LEVELS
from code.analysis import aggregate_errors, select_ci_method

def parse_args():
    parser = argparse.ArgumentParser(
        description="Generate the final report CSV aggregating simulation results."
    )
    parser.add_argument(
        "--icc-range",
        type=float,
        nargs="+",
        default=None,
        help="List of ICC values to process (e.g., 0.0 0.1 0.2). Defaults to config."
    )
    parser.add_argument(
        "--icc-step",
        type=float,
        default=None,
        help="Step size for ICC range. Defaults to config."
    )
    parser.add_argument(
        "--alpha-list",
        type=float,
        nargs="+",
        default=None,
        help="List of alpha levels (e.g., 0.01 0.05 0.10). Defaults to config."
    )
    parser.add_argument(
        "--baseline-input",
        type=str,
        default="data/derived/baseline_results.csv",
        help="Path to baseline simulation results CSV."
    )
    parser.add_argument(
        "--robust-input",
        type=str,
        default="data/derived/robustResults.csv",
        help="Path to robust simulation results CSV."
    )
    parser.add_argument(
        "--output",
        type=str,
        default="data/derived/final_report.csv",
        help="Path for the output final report CSV."
    )
    return parser.parse_args()

def main():
    args = parse_args()

    # Load configuration
    cfg = load_config()
    
    # Override config with CLI args if provided
    if args.icc_range is not None:
        cfg['icc_range'] = args.icc_range
    if args.icc_step is not None:
        cfg['icc_step'] = args.icc_step
    if args.alpha_list is not None:
        cfg['alpha_levels'] = args.alpha_list
    else:
        cfg['alpha_levels'] = ALPHA_LEVELS

    # Validate input files exist
    if not os.path.exists(args.baseline_input):
        raise FileNotFoundError(f"Baseline results file not found: {args.baseline_input}")
    if not os.path.exists(args.robust_input):
        raise FileNotFoundError(f"Robust results file not found: {args.robust_input}")

    # Load results
    print(f"Loading baseline results from {args.baseline_input}...")
    baseline_df = pd.read_csv(args.baseline_input)
    
    print(f"Loading robust results from {args.robust_input}...")
    robust_df = pd.read_csv(args.robust_input)

    # Ensure necessary columns exist
    required_cols = ['icc', 'p_value']
    for col in required_cols:
        if col not in baseline_df.columns:
            raise ValueError(f"Baseline CSV missing required column: {col}")
        if col not in robust_df.columns:
            raise ValueError(f"Robust CSV missing required column: {col}")

    # Add method labels
    baseline_df['method'] = 'naive_ttest'
    robust_df['method'] = 'cluster_robust'
    
    # Note: The robust results file typically contains both robust and block permutation.
    # If the robust file has multiple methods, we rely on the 'method' column if present.
    # If not, we assume the whole file is 'cluster_robust' for this aggregation step.
    # However, T020/T021 imply robustResults.csv might contain multiple methods.
    # Let's check if 'method' column exists in robust_df. If not, we assume it's all cluster_robust.
    if 'method' not in robust_df.columns:
        # If the robust results file was generated by T021 which might have run both robust and block permutation,
        # we need to handle that. T021 description says "writes robustResults.csv".
        # T019 says "store their p-values alongside the naive result".
        # If robust_df already has a 'method' column, we keep it.
        # If not, we assume the whole file is the cluster robust method for now, 
        # or we need to split it. Given T020 expects 'method' column in the output,
        # the input robust file MUST have 'method' column if it contains multiple methods.
        # If it's a single method file, we add the column.
        robust_df['method'] = 'cluster_robust'

    # Combine results
    all_results = pd.concat([baseline_df, robust_df], ignore_index=True)

    # Aggregate errors
    print("Aggregating error rates and computing confidence intervals...")
    # The aggregate_errors function expects a list of dicts or a DataFrame with 'p_value' and 'method'
    # and computes error rates for each (icc, method, alpha) combination.
    # We pass the combined DataFrame.
    report_df = aggregate_errors(
        results_list=all_results.to_dict(orient='records'),
        alpha_levels=cfg['alpha_levels']
    )

    # Ensure columns match the required output format
    # Expected: ICC, Alpha, Method, Empirical_Error_Rate, CI_Lower, CI_Upper
    if report_df is None or report_df.empty:
        raise RuntimeError("Aggregation returned no results. Check input data and alpha levels.")

    # Rename columns to match spec
    final_df = report_df.rename(columns={
        'icc': 'ICC',
        'alpha': 'Alpha',
        'method': 'Method',
        'error_rate': 'Empirical_Error_Rate',
        'ci_lower': 'CI_Lower',
        'ci_upper': 'CI_Upper'
    })

    # Sort for readability
    final_df = final_df.sort_values(by=['ICC', 'Method', 'Alpha'])

    # Ensure output directory exists
    output_dir = os.path.dirname(args.output)
    if output_dir:
        os.makedirs(output_dir, exist_ok=True)

    # Write to CSV
    final_df.to_csv(args.output, index=False)
    print(f"Final report written to {args.output}")
    print(f"Total rows: {len(final_df)}")

    return 0

if __name__ == "__main__":
    sys.exit(main())