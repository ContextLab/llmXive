"""
Integration test for scheduler latency benchmarking.

Verifies that the scheduler's selection latency is < 10% of the simulated
batch execution time, ensuring the curriculum logic does not bottleneck training.
"""
import json
import time
import statistics
import sys
from pathlib import Path
from typing import List, Dict, Any

# Add project root to path to allow imports
project_root = Path(__file__).resolve().parent.parent.parent
sys.path.insert(0, str(project_root / "code"))

from scheduler.curriculum_scheduler import CurriculumScheduler
from utils.logging import get_logger, configure_logging

# Configure logging to stdout for visibility during test
configure_logging(level="INFO", format_type="text")
logger = get_logger("test_scheduler_latency")

# Configuration constants
NUM_RUNS = 5  # Number of iterations for stable latency measurement
SIMULATED_BATCH_EXECUTION_TIME_SECONDS = 10.0  # Simulated time for a batch of rollouts
LATENCY_THRESHOLD_RATIO = 0.10  # Scheduler must be < 10% of batch time
TARGET_LATENCY_MS = SIMULATED_BATCH_EXECUTION_TIME_SECONDS * LATENCY_THRESHOLD_RATIO * 1000

def load_mock_coverage_history() -> List[Dict[str, Any]]:
    """
    Loads the mock coverage history generated by T020.
    """
    fixture_path = project_root / "tests" / "fixtures" / "mock_coverage_history.json"
    if not fixture_path.exists():
        raise FileNotFoundError(
            f"Mock fixture not found at {fixture_path}. "
            "Please ensure T020 has been completed to generate this file."
        )
    with open(fixture_path, "r") as f:
        data = json.load(f)
    return data.get("history", [])

def run_scheduler_selection(scheduler: CurriculumScheduler, history: List[Dict]) -> float:
    """
    Runs a single selection cycle and returns the time taken in seconds.
    """
    start_time = time.perf_counter()
    # The scheduler's main logic or selection method is invoked here.
    # Based on the API, we call the main logic which returns a batch.
    # We simulate the call by triggering the selection logic.
    batch = scheduler.select_next_batch(history, batch_size=1)
    end_time = time.perf_counter()
    return end_time - start_time

def main():
    logger.info("Starting Latency Benchmarking Harness (T024)")
    logger.info(f"Target: Scheduler latency < {TARGET_LATENCY_MS:.2f}ms "
                f"(10% of {SIMULATED_BATCH_EXECUTION_TIME_SECONDS}s batch time)")

    # 1. Load Data
    try:
        history = load_mock_coverage_history()
        logger.info(f"Loaded {len(history)} history entries from mock fixture.")
    except FileNotFoundError as e:
        logger.error(str(e))
        return 1

    if not history:
        logger.error("Mock history is empty. Cannot run benchmark.")
        return 1

    # 2. Initialize Scheduler
    # Using a dummy config path or defaults since we are benchmarking logic speed
    scheduler = CurriculumScheduler(config_path=None)
    logger.info("Scheduler initialized.")

    # 3. Execute Benchmark
    latencies = []
    logger.info(f"Running {NUM_RUNS} selection cycles...")

    for i in range(NUM_RUNS):
        try:
            latency = run_scheduler_selection(scheduler, history)
            latencies.append(latency)
            logger.info(f"Run {i+1}/{NUM_RUNS}: Latency = {latency*1000:.3f}ms")
        except Exception as e:
            logger.error(f"Run {i+1} failed: {e}")
            return 1

    # 4. Analyze Results
    if not latencies:
        logger.error("No successful runs to analyze.")
        return 1

    avg_latency_sec = statistics.mean(latencies)
    avg_latency_ms = avg_latency_sec * 1000
    max_latency_ms = max(latencies) * 1000

    logger.info("-" * 50)
    logger.info("BENCHMARK RESULTS:")
    logger.info(f"  Average Latency: {avg_latency_ms:.3f}ms")
    logger.info(f"  Max Latency:     {max_latency_ms:.3f}ms")
    logger.info(f"  Simulated Batch Time: {SIMULATED_BATCH_EXECUTION_TIME_SECONDS * 1000:.3f}ms")
    logger.info(f"  Threshold (10%):      {TARGET_LATENCY_MS:.3f}ms")
    logger.info("-" * 50)

    # 5. Verification
    passed = avg_latency_sec < (SIMULATED_BATCH_EXECUTION_TIME_SECONDS * LATENCY_THRESHOLD_RATIO)
    
    if passed:
        logger.info("✅ PASS: Scheduler latency is within acceptable limits (< 10% of batch time).")
        return 0
    else:
        logger.error("❌ FAIL: Scheduler latency exceeds 10% of batch execution time.")
        logger.error(f"   Expected < {TARGET_LATENCY_MS:.2f}ms, got {avg_latency_ms:.2f}ms")
        return 1

if __name__ == "__main__":
    sys.exit(main())