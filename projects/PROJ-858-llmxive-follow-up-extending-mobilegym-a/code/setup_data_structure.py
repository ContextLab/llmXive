"""
Task T010: Setup data/raw/.checksums.txt and data/processed/ directory structure.

This script ensures the required directory structure for data artifacts exists
and initializes the .checksums.txt file if it does not already exist.
"""
import os
import sys
from pathlib import Path

def main():
    """Create required data directories and initialize .checksums.txt."""
    # Define project root relative to this script's location
    # The script is in code/, so root is one level up
    project_root = Path(__file__).resolve().parent.parent
    
    # Define required directories
    data_raw = project_root / "data" / "raw"
    data_processed = project_root / "data" / "processed"
    data_validation = project_root / "data" / "validation"
    
    # Create directories if they don't exist
    for directory in [data_raw, data_processed, data_validation]:
        directory.mkdir(parents=True, exist_ok=True)
        print(f"Ensured directory exists: {directory}")
    
    # Initialize .checksums.txt in data/raw/ if it doesn't exist
    checksums_file = data_raw / ".checksums.txt"
    if not checksums_file.exists():
        checksums_file.write_text(
            "# MobileGym Checksums\n"
            "# Format: <hash>  <filename>\n"
            "# Generated by T010 setup script\n"
            "\n"
        )
        print(f"Initialized checksums file: {checksums_file}")
    else:
        print(f"Checksums file already exists: {checksums_file}")
    
    # Verify structure
    print("\nData structure verification:")
    for directory in [data_raw, data_processed, data_validation]:
        if directory.exists():
            print(f"  ✓ {directory}")
        else:
            print(f"  ✗ {directory} (MISSING)")
    
    if checksums_file.exists():
        print(f"  ✓ {checksums_file}")
    else:
        print(f"  ✗ {checksums_file} (MISSING)")

if __name__ == "__main__":
    main()