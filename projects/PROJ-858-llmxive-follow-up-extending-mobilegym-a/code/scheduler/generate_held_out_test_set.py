"""
Generate the held-out test set for transfer evaluation (FR-005).

This script identifies state variables present in the training-time
State Coverage Vector and selects a disjoint set of variables to serve
as the held-out test set. This ensures that the evaluation of Sim-to-Real
transfer is performed on state dimensions the model was not explicitly
guided to cover during training.

Inputs:
  - data/processed/coverage_schema.json (generated by T005/T012 logic, contains all variables)
  - data/processed/coverage_vectors.json (generated by T028, contains used variables)

Outputs:
  - data/processed/held_out_test_set.json
    Contains:
      - 'held_out_variables': list of variable names NOT in training vectors
      - 'training_variables': list of variable names found in training vectors
      - 'total_schema_variables': list of all available variables
      - 'generation_timestamp': ISO timestamp
"""
import json
import os
import sys
from pathlib import Path
from datetime import datetime, timezone
from typing import List, Dict, Any, Set

# Add project root to path for imports
project_root = Path(__file__).resolve().parents[2]
sys.path.insert(0, str(project_root))

from utils.logging import get_logger, configure_logging
from utils.constants import ErrorCodes

logger = get_logger(__name__)

SCHEMA_PATH = project_root / "data" / "processed" / "coverage_schema.json"
TRAINING_VECTORS_PATH = project_root / "data" / "processed" / "coverage_vectors.json"
OUTPUT_PATH = project_root / "data" / "processed" / "held_out_test_set.json"

def load_json_file(path: Path) -> Any:
    """Load a JSON file and return its contents."""
    if not path.exists():
        raise FileNotFoundError(f"Required file not found: {path}")
    
    with open(path, 'r', encoding='utf-8') as f:
        return json.load(f)

def extract_training_variables(training_vectors_path: Path) -> Set[str]:
    """
    Extract all unique state variable names present in the training coverage vectors.
    
    The training vectors file is expected to be a list of vectors or a dictionary
    containing a 'vectors' key. Each vector is expected to be a dict mapping 
    variable_name -> boolean value.
    """
    logger.info(f"Loading training vectors from {training_vectors_path}")
    data = load_json_file(training_vectors_path)
    
    training_vars = set()
    
    # Handle different potential structures of the vectors file
    if isinstance(data, list):
        # List of vectors
        vectors = data
    elif isinstance(data, dict) and 'vectors' in data:
        vectors = data['vectors']
    elif isinstance(data, dict) and 'coverage_vectors' in data:
        vectors = data['coverage_vectors']
    else:
        # Assume the whole dict is a single vector or contains one
        vectors = [data] if not isinstance(data, list) else data

    for vector in vectors:
        if isinstance(vector, dict):
            # Keys are variable names
            training_vars.update(vector.keys())
        elif isinstance(vector, list) and len(vector) > 0 and isinstance(vector[0], dict):
            # List of dicts within the vector entry
            for item in vector:
                if isinstance(item, dict):
                    training_vars.update(item.keys())
    
    logger.info(f"Found {len(training_vars)} unique variables in training vectors")
    return training_vars

def extract_schema_variables(schema_path: Path) -> List[str]:
    """
    Extract all available state variable names from the coverage schema.
    
    The schema is expected to be a list of variable definitions or a dict
    with a 'variables' or 'semantic_proxies' key.
    """
    logger.info(f"Loading schema from {schema_path}")
    data = load_json_file(schema_path)
    
    all_vars = []
    
    if isinstance(data, list):
        # List of variable definitions
        for item in data:
            if isinstance(item, dict) and 'name' in item:
                all_vars.append(item['name'])
            elif isinstance(item, str):
                all_vars.append(item)
    elif isinstance(data, dict):
        # Check common keys
        for key in ['semantic_proxies', 'variables', 'state_variables', 'coverage_schema']:
            if key in data:
                sub_data = data[key]
                if isinstance(sub_data, list):
                    for item in sub_data:
                        if isinstance(item, dict) and 'name' in item:
                            all_vars.append(item['name'])
                        elif isinstance(item, str):
                            all_vars.append(item)
                elif isinstance(sub_data, dict):
                    # Dict mapping name -> definition
                    all_vars.extend(sub_data.keys())
        
        # If no specific key found, try iterating top-level keys
        if not all_vars:
            for key, value in data.items():
                if isinstance(value, list):
                    for item in value:
                        if isinstance(item, dict) and 'name' in item:
                            all_vars.append(item['name'])
                        elif isinstance(item, str):
                            all_vars.append(item)
    
    logger.info(f"Found {len(all_vars)} total variables in schema")
    return all_vars

def generate_held_out_set() -> Dict[str, Any]:
    """
    Generate the held-out test set by finding variables in the schema
    that are NOT present in the training vectors.
    """
    # Ensure output directory exists
    OUTPUT_PATH.parent.mkdir(parents=True, exist_ok=True)
    
    # Load data
    try:
        training_vars = extract_training_variables(TRAINING_VECTORS_PATH)
        schema_vars = extract_schema_variables(SCHEMA_PATH)
    except FileNotFoundError as e:
        logger.error(f"Missing required input file: {e}")
        raise
    except json.JSONDecodeError as e:
        logger.error(f"Invalid JSON in input file: {e}")
        raise
    
    if not schema_vars:
        raise ValueError("No variables found in schema file")
    
    if not training_vars:
        logger.warning("No training variables found. Using all schema variables as held-out set.")
        training_vars = set()
    
    # Compute held-out set
    schema_set = set(schema_vars)
    held_out_vars = sorted(list(schema_set - training_vars))
    training_vars_list = sorted(list(training_vars))
    
    if not held_out_vars:
        logger.warning("No held-out variables found! All schema variables are present in training.")
        logger.warning("This may indicate the curriculum has already covered all known states.")
    
    # Construct output
    result = {
        "held_out_variables": held_out_vars,
        "training_variables": training_vars_list,
        "total_schema_variables": sorted(schema_vars),
        "generation_timestamp": datetime.now(timezone.utc).isoformat(),
        "stats": {
            "total_schema_variables": len(schema_vars),
            "training_variables_count": len(training_vars_list),
            "held_out_variables_count": len(held_out_vars),
            "coverage_ratio": round(len(training_vars_list) / len(schema_vars), 4) if schema_vars else 0.0
        }
    }
    
    # Write output
    with open(OUTPUT_PATH, 'w', encoding='utf-8') as f:
        json.dump(result, f, indent=2, ensure_ascii=False)
    
    logger.info(f"Held-out test set generated successfully: {OUTPUT_PATH}")
    logger.info(f"Held-out variables: {len(held_out_vars)}")
    logger.info(f"Training variables: {len(training_vars_list)}")
    
    return result

def main():
    """Main entry point."""
    configure_logging()
    logger.info("Starting held-out test set generation (T029)")
    
    try:
        result = generate_held_out_set()
        logger.info("Task completed successfully")
        return 0
    except Exception as e:
        logger.error(f"Task failed: {e}", exc_info=True)
        return 1

if __name__ == "__main__":
    sys.exit(main())