logger = get_logger("contract_test_dataset_schema")

SCHEMA_PATH = project_root / "specs" / "001-emotional-synchrony-trust" / "contracts" / "dataset_schema.yaml"
# We expect the actual data file to be generated by T012 (data_loader.py)
# For this contract test, we check against a placeholder path or a generated sample if T012 has run.
# However, per task T010 description, it is a contract test against the schema.
# We will test the schema loading and validation logic with a minimal valid dataset structure.
# If T012 has produced data, we test against that. If not, we test the validator logic with a mock dataset.

def test_schema_loads_correctly():
    """Ensure the schema file exists and loads without error."""
    assert SCHEMA_PATH.exists(), f"Schema file not found at {SCHEMA_PATH}"
    schema = load_schema(str(SCHEMA_PATH))
    assert schema is not None
    assert "properties" in schema or "type" in schema

def test_validate_dataset_compliance():
    """
    Test the validation logic against a minimal valid dataset.
    Since T012 (data_loader) might not have run yet, we construct a minimal valid DataFrame
    that matches the expected schema to ensure the validator works.
    """
    try:
        import pandas as pd
    except ImportError:
        pytest.skip("pandas not installed")

    # Construct a minimal valid dataset based on typical schema expectations for this project
    # (interaction_id, trust_score, modalities, etc.)
    # The exact columns depend on dataset_schema.yaml content.
    # We assume the schema requires 'interaction_id' and 'trust_score' at minimum.
    
    # Attempt to load the actual schema to determine required fields
    schema = load_schema(str(SCHEMA_PATH))
    
    required_fields = []
    if "properties" in schema:
        required_fields = [
            k for k, v in schema["properties"].items() 
            if v.get("required", False)
        ]
    
    # Fallback if schema structure is different or fields not marked required explicitly
    if not required_fields:
        # Based on common patterns in this project (US1), we expect these:
        required_fields = ["interaction_id", "trust_score"]
        
    # Create a minimal valid dataset
    data = {
        "interaction_id": ["int_001", "int_002"],
        "trust_score": [3.5, 4.0]
    }
    
    # Add placeholder fields if they are expected but not in required list to ensure schema compliance
    # This is a heuristic; in a real scenario, the schema defines the exact structure.
    if "facial_features" in required_fields:
        data["facial_features"] = [[1.0], [2.0]]
    if "vocal_features" in required_fields:
        data["vocal_features"] = [[1.0], [2.0]]

    df = pd.DataFrame(data)
    
    # Run validation
    try:
        is_valid = validate_dataset(df, schema)
        assert is_valid, "Minimal dataset should pass schema validation"
    except ValidationError as e:
        # If validation fails, it might be because the schema expects more complex structure
        # or the mock data is insufficient. Log and fail the test if it's a structural mismatch.
        logger.warning(f"Validation failed with minimal data: {e}")
        # In a strict contract test, if the validator rejects valid-looking data, the test fails.
        # However, if the schema is strict about types (e.g., lists for features), we might need more.
        # For this task, we assert that the validator runs and handles the check.
        # If the schema is strictly defined, we might need to mock the exact structure.
        # Let's assume the schema requires specific column names and types.
        pytest.fail(f"Schema validation failed for minimal valid data: {e}")

