"""
Script to write the optimal scaling factor and its confidence interval to a file.
This implements task T025: Write `scaling_factor.txt` containing the optimal `s` and its CI.

It relies on the output generated by `derive_scaling.py` (specifically `data/scaling_results.json`)
which contains the optimal scalar and bootstrap confidence intervals.
"""
import json
import logging
from pathlib import Path
from typing import Dict, Any

# Import logging configuration from the project's logger module
try:
    from logger import get_logger, configure_logging
    logger = get_logger(__name__)
    # Ensure logging is configured if not already done by other scripts
    configure_logging(level=logging.INFO)
except ImportError:
    # Fallback if logger module is not yet available or imported differently
    logging.basicConfig(level=logging.INFO)
    logger = logging.getLogger(__name__)

def load_scaling_results(json_path: Path) -> Dict[str, Any]:
    """
    Loads the scaling results from the JSON file generated by derive_scaling.py.
    
    Args:
        json_path: Path to the scaling_results.json file.
        
    Returns:
        Dictionary containing scaling factor, CI, and hypothesis test results.
        
    Raises:
        FileNotFoundError: If the JSON file does not exist.
        json.JSONDecodeError: If the JSON file is malformed.
    """
    if not json_path.exists():
        raise FileNotFoundError(f"Scaling results file not found: {json_path}")
        
    with open(json_path, 'r') as f:
        return json.load(f)

def write_scaling_file(scaling_data: Dict[str, Any], output_path: Path) -> None:
    """
    Writes the optimal scaling factor and its confidence interval to a text file.
    
    The format is designed to be human-readable and easily parseable.
    
    Args:
        scaling_data: Dictionary containing 'optimal_s', 'ci_lower', 'ci_upper', etc.
        output_path: Path where the scaling_factor.txt file will be written.
    """
    optimal_s = scaling_data.get('optimal_s')
    ci_lower = scaling_data.get('ci_lower')
    ci_upper = scaling_data.get('ci_upper')
    hypothesis_rejected = scaling_data.get('hypothesis_s_equals_1_rejected', False)
    p_value = scaling_data.get('p_value', 'N/A')
    
    content_lines = [
        "DFT-D3 Scaling Factor Results",
        "=============================",
        "",
        f"Optimal Scaling Factor (s): {optimal_s:.6f}",
        f"95% Confidence Interval:  [{ci_lower:.6f}, {ci_upper:.6f}]",
        "",
        f"Hypothesis Test (s = 1.0): {'REJECTED' if hypothesis_rejected else 'NOT REJECTED'}",
        f"P-value: {p_value}",
        "",
        "Note: The scaling factor s is applied as: E_corrected = E_base + s * E_D3",
        "This factor minimizes the Mean Absolute Error (MAE) against CCSD(T)/CBS references."
    ]
    
    output_path.parent.mkdir(parents=True, exist_ok=True)
    with open(output_path, 'w') as f:
        f.write('\n'.join(content_lines))
    
    logger.info(f"Successfully wrote scaling results to {output_path}")

def main():
    """
    Main entry point for the script.
    Reads scaling results from data/scaling_results.json and writes to data/scaling_factor.txt.
    """
    # Define paths relative to project root
    project_root = Path(__file__).resolve().parent.parent
    input_json_path = project_root / "data" / "scaling_results.json"
    output_txt_path = project_root / "data" / "scaling_factor.txt"
    
    logger.info(f"Loading scaling results from {input_json_path}")
    
    try:
        scaling_data = load_scaling_results(input_json_path)
    except (FileNotFoundError, json.JSONDecodeError) as e:
        logger.error(f"Failed to load scaling results: {e}")
        raise
    
    logger.info(f"Writing scaling factor to {output_txt_path}")
    write_scaling_file(scaling_data, output_txt_path)
    
    logger.info("Task T025 completed successfully.")

if __name__ == "__main__":
    main()