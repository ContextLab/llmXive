logger = get_logger(__name__)

def load_raw_energies(csv_path: str) -> pd.DataFrame:
    """
    Load the raw energies CSV generated by analyze_energies.py.
    Expected columns: pair_id, reference_energy, dft_total_energy, d3_dispersion_energy, signed_error
    """
    path = Path(csv_path)
    if not path.exists():
        raise FileNotFoundError(f"Raw energies file not found: {csv_path}")
    
    df = pd.read_csv(path)
    required_cols = ['reference_energy', 'dft_total_energy', 'd3_dispersion_energy']
    missing = [c for c in required_cols if c not in df.columns]
    if missing:
        raise ValueError(f"Missing required columns in {csv_path}: {missing}")
    
    logger.info(f"Loaded {len(df)} ion pairs from {csv_path}")
    return df

def objective_function(s: float, df: pd.DataFrame) -> float:
    """
    Calculate the MAE for a given scaling factor s.
    E_corrected = E_base + s * E_D3
    where E_base = E_total - E_D3 (the non-dispersion DFT part)
    """
    # E_base is the DFT energy without D3 correction
    # E_total = E_base + E_D3  =>  E_base = E_total - E_D3
    e_base = df['dft_total_energy'] - df['d3_dispersion_energy']
    e_corrected = e_base + s * df['d3_dispersion_energy']
    
    errors = e_corrected - df['reference_energy']
    mae = np.mean(np.abs(errors))
    return mae

def fit_scaling_factor(df: pd.DataFrame) -> float:
    """
    Fit the optimal scaling factor s > 0 that minimizes MAE.
    """
    result = minimize_scalar(
        objective_function,
        args=(df,),
        bounds=(0.0, 5.0),
        method='bounded'
    )
    
    if not result.success:
        warning(f"Optimization may not have converged: {result.message}")
    
    logger.info(f"Optimal scaling factor s = {result.x:.6f} with MAE = {result.fun:.6f}")
    return result.x

def bootstrap_scaling_analysis(
    df: pd.DataFrame, 
    n_replicates: int = 1000, 
    seed: int = 42
) -> Dict[str, Any]:
    """
    Perform bootstrap resampling to estimate confidence intervals for s.
    """
    rng = np.random.default_rng(seed)
    s_values = []
    mae_values = []
    
    logger.info(f"Starting bootstrap analysis with {n_replicates} replicates...")
    
    for i in range(n_replicates):
        # Resample rows with replacement
        indices = rng.integers(0, len(df), len(df))
        bootstrap_df = df.iloc[indices].reset_index(drop=True)
        
        # Fit scaling factor on bootstrap sample
        s_opt = fit_scaling_factor(bootstrap_df)
        s_values.append(s_opt)
        
        # Calculate MAE on this bootstrap sample
        e_base = bootstrap_df['dft_total_energy'] - bootstrap_df['d3_dispersion_energy']
        e_corrected = e_base + s_opt * bootstrap_df['d3_dispersion_energy']
        errors = e_corrected - bootstrap_df['reference_energy']
        mae = np.mean(np.abs(errors))
        mae_values.append(mae)
        
        if (i + 1) % 200 == 0:
            logger.debug(f"Bootstrap progress: {i + 1}/{n_replicates}")
    
    s_values = np.array(s_values)
    mae_values = np.array(mae_values)
    
    # Calculate confidence intervals (2.5% and 97.5% percentiles)
    ci_lower = np.percentile(s_values, 2.5)
    ci_upper = np.percentile(s_values, 97.5)
    
    # Hypothesis test: does CI exclude 1.0?
    excludes_one = (ci_lower > 1.0) or (ci_upper < 1.0)
    hypothesis_result = "REJECTED" if excludes_one else "NOT_REJECTED"
    
    logger.info(f"Bootstrap CI for s: [{ci_lower:.6f}, {ci_upper:.6f}]")
    logger.info(f"Hypothesis test (s=1.0): {hypothesis_result}")
    
    return {
        's_opt': float(s_values.mean()),
        's_median': float(np.median(s_values)),
        'ci_lower': float(ci_lower),
        'ci_upper': float(ci_upper),
        'mae_opt': float(mae_values.mean()),
        'hypothesis_test': hypothesis_result,
        'excludes_one': excludes_one,
        'bootstrap_values': s_values.tolist()
    }

def write_scaling_results(
    s_opt: float, 
    stats: Dict[str, Any], 
    output_path: str
) -> None:
    """
    Write the scaling factor and statistics to a text file.
    """
    path = Path(output_path)
    path.parent.mkdir(parents=True, exist_ok=True)
    
    with open(path, 'w') as f:
        f.write(f"# Scaling Factor Results\n")
        f.write(f"# Generated by derive_scaling.py\n\n")
        f.write(f"optimal_s: {s_opt:.6f}\n")
        f.write(f"ci_lower: {stats['ci_lower']:.6f}\n")
        f.write(f"ci_upper: {stats['ci_upper']:.6f}\n")
        f.write(f"mae_opt: {stats['mae_opt']:.6f}\n")
        f.write(f"hypothesis_test: {stats['hypothesis_test']}\n")
        f.write(f"excludes_one: {stats['excludes_one']}\n")
    
    logger.info(f"Scaling results written to {output_path}")

def main():
    """
    Main entry point for the scaling derivation pipeline.
    """
    # Configuration
    input_csv = "data/derived/raw_energies.csv"
    output_txt = "data/derived/scaling_factor.txt"
    output_json = "data/derived/scaling_stats.json"
    n_replicates = 1000
    seed = 42
    
    logger.info("Starting scaling factor derivation...")
    
    # Load data
    df = load_raw_energies(input_csv)
    
    # Fit optimal scaling factor
    s_opt = fit_scaling_factor(df)
    
    # Bootstrap analysis
    stats = bootstrap_scaling_analysis(df, n_replicates=n_replicates, seed=seed)
    
    # Write results
    write_scaling_results(s_opt, stats, output_txt)
    
    # Save detailed stats as JSON for report generation
    stats_path = Path(output_json)
    stats_path.parent.mkdir(parents=True, exist_ok=True)
    with open(stats_path, 'w') as f:
        json.dump(stats, f, indent=2)
    logger.info(f"Statistics saved to {output_json}")
    
    # Summary
    logger.info("=" * 50)
    logger.info("SCALING FACTOR ANALYSIS COMPLETE")
    logger.info(f"Optimal s: {s_opt:.6f}")
    logger.info(f"95% CI: [{stats['ci_lower']:.6f}, {stats['ci_upper']:.6f}]")
    logger.info(f"Hypothesis (s=1.0): {stats['hypothesis_test']}")
    logger.info("=" * 50)
    
    return s_opt, stats

if __name__ == "__main__":
    main()