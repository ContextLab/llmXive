"""
Generate Baseline Results for Reproducibility Check (Task T030a)

This script runs the full analysis pipeline (run_statistics.py) once,
captures the resulting metrics and effect sizes, and saves them as
`data/analysis_results/baseline_results.json`. This file serves as the
ground truth for the 5% numerical tolerance check in the CI reproducibility
workflow (T030).

It relies on the real data generated by previous tasks (T013-T020) and
the analysis logic from T024a/T024b.
"""
import os
import sys
import json
import time
from pathlib import Path
from typing import Dict, Any

# Ensure project root is in path for imports
project_root = Path(__file__).resolve().parent.parent.parent
if str(project_root) not in sys.path:
    sys.path.insert(0, str(project_root))

from utils.logging_utils import setup_logging, get_logger
from utils.hash_artifacts import hash_file

# Setup logging
logger = setup_logging()
logger = get_logger("generate_baseline_results")

def load_analysis_results() -> Dict[str, Any]:
    """
    Load the results generated by run_statistics.py.
    We expect the following files to exist in data/analysis_results/:
    - results.csv
    - sensitivity_analysis.csv
    - outlier_flags.json
    
    We aggregate these into a single JSON structure for the baseline.
    """
    results_dir = project_root / "data" / "analysis_results"
    
    if not results_dir.exists():
        raise FileNotFoundError(
            f"Analysis results directory not found: {results_dir}. "
            "Please ensure T024a/T024b (run_statistics.py) has been executed successfully."
        )

    baseline_data = {
        "generated_at": time.strftime("%Y-%m-%dT%H:%M:%SZ", time.gmtime()),
        "source_files": {},
        "metrics": {}
    }

    # 1. Load results.csv (Top-K accuracy, speed, p-values, effect sizes)
    results_csv = results_dir / "results.csv"
    if results_csv.exists():
        import pandas as pd
        df = pd.read_csv(results_csv)
        baseline_data["metrics"]["main_results"] = df.to_dict(orient="records")
        baseline_data["source_files"]["results_csv"] = str(results_csv.relative_to(project_root))
    else:
        logger.warning(f"File not found: {results_csv}")

    # 2. Load sensitivity_analysis.csv
    sensitivity_csv = results_dir / "sensitivity_analysis.csv"
    if sensitivity_csv.exists():
        import pandas as pd
        df = pd.read_csv(sensitivity_csv)
        baseline_data["metrics"]["sensitivity_analysis"] = df.to_dict(orient="records")
        baseline_data["source_files"]["sensitivity_csv"] = str(sensitivity_csv.relative_to(project_root))
    else:
        logger.warning(f"File not found: {sensitivity_csv}")

    # 3. Load outlier_flags.json
    outlier_json = results_dir / "outlier_flags.json"
    if outlier_json.exists():
        with open(outlier_json, 'r') as f:
            baseline_data["metrics"]["outlier_flags"] = json.load(f)
        baseline_data["source_files"]["outlier_json"] = str(outlier_json.relative_to(project_root))
    else:
        logger.warning(f"File not found: {outlier_json}")

    return baseline_data

def save_baseline_results(data: Dict[str, Any], output_path: Path) -> None:
    """
    Save the aggregated baseline results to a JSON file.
    """
    with open(output_path, 'w') as f:
        json.dump(data, f, indent=2)
    logger.info(f"Baseline results saved to: {output_path}")

def verify_input_data() -> None:
    """
    Verify that the necessary input data files exist before running.
    """
    required_files = [
        project_root / "data" / "interaction_logs" / "anonymized_logs.csv",
        project_root / "data" / "summaries" / "llm_sim_summaries.csv",
        project_root / "data" / "summaries" / "rule_summaries.csv"
    ]
    
    missing = [f for f in required_files if not f.exists()]
    if missing:
        raise FileNotFoundError(
            f"Missing required input data files: {[str(m.relative_to(project_root)) for m in missing]}. "
            "Please ensure T013-T020 (Data Collection) have been completed."
        )

def main():
    logger.info("Starting baseline results generation (Task T030a)...")
    
    try:
        # 1. Verify inputs
        verify_input_data()
        
        # 2. Check if analysis has been run (by checking if output files exist)
        # If not, we could theoretically trigger run_statistics.py here, 
        # but the task description implies we "run the analysis script once" 
        # as a prerequisite step. We assume T024 has run.
        # However, to be robust, we check if results exist.
        results_csv = project_root / "data" / "analysis_results" / "results.csv"
        if not results_csv.exists():
            logger.error(
                "Analysis results not found. Please run 'python code/analysis/run_statistics.py' "
                "before generating the baseline."
            )
            sys.exit(1)

        # 3. Load the analysis results
        logger.info("Loading analysis results...")
        baseline_data = load_analysis_results()

        # 4. Calculate hash of the input data used (for reproducibility tracking)
        input_logs = project_root / "data" / "interaction_logs" / "anonymized_logs.csv"
        if input_logs.exists():
            baseline_data["input_data_hash"] = hash_file(input_logs)
            logger.info(f"Input data hash computed: {baseline_data['input_data_hash'][:16]}...")

        # 5. Save the baseline
        output_file = project_root / "data" / "analysis_results" / "baseline_results.json"
        save_baseline_results(baseline_data, output_file)

        logger.info("Baseline generation completed successfully.")
        print(f"SUCCESS: Baseline saved to {output_file}")

    except FileNotFoundError as e:
        logger.error(f"Missing required file: {e}")
        sys.exit(1)
    except Exception as e:
        logger.exception(f"Unexpected error during baseline generation: {e}")
        sys.exit(1)

if __name__ == "__main__":
    main()
