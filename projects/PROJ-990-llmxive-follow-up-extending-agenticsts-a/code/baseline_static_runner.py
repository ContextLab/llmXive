"""
T019: Static All-Layers Baseline Execution.

Executes the agenticsts-engine with the "Static" policy on the test set
and writes the results to data/processed/simulation_logs_static.json.

This task depends on:
  - T018 (engine_runner.py) for the execution engine.
  - T014a (splitter.py) for the test set data (data/processed/test_set.csv).
"""
import os
import sys
import json
import logging
from pathlib import Path
from typing import List, Dict, Any

# Add project root to path for imports if running as script
project_root = Path(__file__).parent.parent
if str(project_root) not in sys.path:
    sys.path.insert(0, str(project_root))

from engine_runner import run_simulation_batch
from config import ensure_directories

# Configure logging
logging.basicConfig(
    level=logging.INFO,
    format='%(asctime)s - %(name)s - %(levelname)s - %(message)s'
)
logger = logging.getLogger("T019_StaticBaseline")

# Constants
POLICY_NAME = "Static"
TEST_SET_PATH = "data/processed/test_set.csv"
OUTPUT_PATH = "data/processed/simulation_logs_static.json"

def load_test_trajectories(path: str) -> List[Dict[str, Any]]:
    """
    Loads the test set trajectories from the CSV generated by T014a.
    We expect the CSV to contain trajectory data that the engine can process.
    """
    import pandas as pd
    
    if not os.path.exists(path):
        raise FileNotFoundError(
            f"Test set file not found at {path}. "
            "Ensure T014a (splitter.py) has been executed successfully."
        )
    
    df = pd.read_csv(path)
    
    # The engine_runner expects a list of trajectory records.
    # Depending on the CSV schema, we might need to convert rows to dicts.
    # Assuming the CSV contains the necessary columns for the engine.
    trajectories = df.to_dict(orient='records')
    
    logger.info(f"Loaded {len(trajectories)} trajectories from {path}")
    return trajectories

def main():
    logger.info(f"Starting T019: {POLICY_NAME} Baseline Execution")
    
    # Ensure output directory exists
    ensure_directories([OUTPUT_PATH])
    
    # Load test set
    test_trajectories = load_test_trajectories(TEST_SET_PATH)
    
    if not test_trajectories:
        logger.warning("Test set is empty. Generating empty output file.")
        output_data = {
            "policy": POLICY_NAME,
            "count": 0,
            "results": [],
            "status": "completed_empty"
        }
        with open(OUTPUT_PATH, 'w') as f:
            json.dump(output_data, f, indent=2)
        return

    try:
        # Run simulation with Static policy
        # This invokes the engine_runner logic defined in T018
        results = run_simulation_batch(
            trajectories=test_trajectories,
            policy="Static"
        )
        
        # Compile output
        output_data = {
            "policy": POLICY_NAME,
            "count": len(results),
            "results": results,
            "status": "completed"
        }
        
        # Write output
        with open(OUTPUT_PATH, 'w') as f:
            json.dump(output_data, f, indent=2)
        
        logger.info(f"Successfully wrote {len(results)} results to {OUTPUT_PATH}")
        
    except Exception as e:
        logger.error(f"Simulation failed: {e}", exc_info=True)
        # Re-raise to indicate failure to the pipeline
        raise

if __name__ == "__main__":
    main()
