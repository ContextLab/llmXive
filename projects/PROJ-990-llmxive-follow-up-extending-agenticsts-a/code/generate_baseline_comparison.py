"""
T022: Generate summary CSV output for baseline comparison.

This script aggregates simulation results from Dynamic, Static, and Random
baseline runs to produce a summary CSV with win rates and average token usage.

Input: Simulation results (assumed to be in data/processed/ directory or loaded via stats module)
Output: data/processed/baseline_comparison.csv
"""
import os
import json
import logging
import pandas as pd
from pathlib import Path
from typing import Dict, List, Any, Optional
import sys

# Add parent directory to path for imports
sys.path.insert(0, str(Path(__file__).parent))

from stats import load_simulation_results, compute_aggregates

logging.basicConfig(
    level=logging.INFO,
    format='%(asctime)s - %(levelname)s - %(message)s'
)
logger = logging.getLogger(__name__)

def load_simulation_data() -> pd.DataFrame:
    """
    Load simulation results. This function attempts to load from the
    aggregation report generated by stats.py or falls back to loading
    individual result files if needed.
    """
    processed_dir = Path(__file__).parent.parent / "data" / "processed"
    
    # Try to load from the aggregation report
    aggregation_report_path = processed_dir / "aggregation_report.json"
    
    if aggregation_report_path.exists():
        logger.info(f"Loading aggregation report from {aggregation_report_path}")
        with open(aggregation_report_path, 'r') as f:
            data = json.load(f)
        
        # Convert to DataFrame
        records = []
        for condition, metrics in data.get('results', {}).items():
            if isinstance(metrics, dict):
                record = {
                    'condition': condition,
                    'win_rate': metrics.get('win_rate', 0.0),
                    'avg_tokens': metrics.get('avg_tokens', 0.0),
                    'total_runs': metrics.get('total_runs', 0)
                }
                records.append(record)
        
        if records:
            return pd.DataFrame(records)
    
    # Fallback: Try to load individual simulation result files
    # This assumes the simulator outputs results in a specific format
    logger.warning("Aggregation report not found. Attempting to load individual results.")
    
    # Check for common result file patterns
    result_files = [
        processed_dir / "dynamic_simulation_results.json",
        processed_dir / "static_simulation_results.json",
        processed_dir / "random_simulation_results.json"
    ]
    
    records = []
    for file_path in result_files:
        if file_path.exists():
            logger.info(f"Loading results from {file_path}")
            with open(file_path, 'r') as f:
                data = json.load(f)
            
            # Extract condition name from filename
            condition = file_path.stem.replace('_results', '').replace('_', ' ').title()
            
            if isinstance(data, list) and len(data) > 0:
                # Aggregate the results
                win_rates = [r.get('win', 0) for r in data]
                token_usages = [r.get('tokens_used', 0) for r in data]
                
                record = {
                    'condition': condition,
                    'win_rate': sum(win_rates) / len(win_rates) if win_rates else 0.0,
                    'avg_tokens': sum(token_usages) / len(token_usages) if token_usages else 0.0,
                    'total_runs': len(data)
                }
                records.append(record)
            elif isinstance(data, dict) and 'results' in data:
                # Handle dictionary format
                for condition_key, metrics in data['results'].items():
                    record = {
                        'condition': condition_key,
                        'win_rate': metrics.get('win_rate', 0.0),
                        'avg_tokens': metrics.get('avg_tokens', 0.0),
                        'total_runs': metrics.get('total_runs', 0)
                    }
                    records.append(record)
    
    if records:
        return pd.DataFrame(records)
    
    # If no data found, raise an error
    raise FileNotFoundError(
        "No simulation results found. Please run the simulation tasks (T015, T019, T020) first."
    )

def generate_baseline_comparison(df: pd.DataFrame, output_path: Path) -> None:
    """
    Generate the baseline comparison CSV with the required schema.
    
    Schema: condition, win_rate, avg_tokens
    """
    # Ensure the output directory exists
    output_path.parent.mkdir(parents=True, exist_ok=True)
    
    # Select only the required columns
    required_columns = ['condition', 'win_rate', 'avg_tokens']
    
    # Check if all required columns are present
    missing_columns = [col for col in required_columns if col not in df.columns]
    if missing_columns:
        raise ValueError(f"Missing required columns: {missing_columns}")
    
    # Create the output DataFrame
    output_df = df[required_columns].copy()
    
    # Round the numeric columns for better readability
    output_df['win_rate'] = output_df['win_rate'].round(4)
    output_df['avg_tokens'] = output_df['avg_tokens'].round(2)
    
    # Sort by condition for consistent output
    output_df = output_df.sort_values('condition').reset_index(drop=True)
    
    # Save to CSV
    output_df.to_csv(output_path, index=False)
    logger.info(f"Baseline comparison CSV saved to {output_path}")
    logger.info(f"Output shape: {output_df.shape}")
    logger.info(f"Output preview:\n{output_df.to_string()}")

def main():
    """Main entry point for the script."""
    logger.info("Starting baseline comparison generation (T022)")
    
    try:
        # Load simulation data
        df = load_simulation_data()
        
        # Define output path
        output_path = Path(__file__).parent.parent / "data" / "processed" / "baseline_comparison.csv"
        
        # Generate the comparison CSV
        generate_baseline_comparison(df, output_path)
        
        logger.info("T022 completed successfully")
        return 0
        
    except Exception as e:
        logger.error(f"T022 failed: {str(e)}", exc_info=True)
        return 1

if __name__ == "__main__":
    exit(main())