def compute_aggregates(df: pd.DataFrame) -> Dict[str, Any]:
    """Compute mean win_rate and mean tokens per condition."""
    aggregates = {}
    for condition in df['condition'].unique():
        subset = df[df['condition'] == condition]
        aggregates[condition] = {
            'mean_win_rate': subset['win_rate'].mean(),
            'mean_tokens': subset['token_usage'].mean()
        }
    return aggregates

