def run_permutation_test(y1: List[float], y2: List[float]) -> float:
    """
    Run a permutation test for two independent samples (Dynamic vs Static win rates).
    Returns the p-value.
    """
    # Convert to numpy arrays
    data = np.array(y1 + y2)
    group1 = np.array(y1)
    group2 = np.array(y2)
    
    # Use scipy permutation_test
    def stat_func(x, y):
        return np.mean(x) - np.mean(y)
    
    result = permutation_test((group1, group2), stat_func, alternative='two-sided', n_resamples=10000, random_state=42)
    return result.pvalue

