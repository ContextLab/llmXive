        raw_outcomes_path = "data/processed/raw_win_outcomes.csv"
        if os.path.exists(raw_outcomes_path):
            df_raw = pd.read_csv(raw_outcomes_path)
            dyn_wins = df_raw[df_raw['condition'] == 'Dynamic']['win'].values
            stat_wins = df_raw[df_raw['condition'] == 'Static']['win'].values
            p_win = run_permutation_test(dyn_wins, stat_wins)
