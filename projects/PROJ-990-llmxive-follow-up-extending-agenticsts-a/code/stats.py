def apply_bonferroni_correction(p_values: List[float], alpha: float = 0.05) -> Dict[str, Any]:
    """
    Apply Bonferroni correction to a list of p-values.
    Returns adjusted p-values and whether the null hypothesis is rejected.
    """
    n_tests = len(p_values)
    adjusted_p_values = [min(p * n_tests, 1.0) for p in p_values]
    significant = [p < alpha for p in adjusted_p_values]
    return {
        'adjusted_p_values': adjusted_p_values,
        'significant': significant,
        'alpha': alpha,
        'n_tests': n_tests
    }

