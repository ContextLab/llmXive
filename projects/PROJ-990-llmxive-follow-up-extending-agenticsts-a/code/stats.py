def compute_aggregates(df: pd.DataFrame) -> Dict[str, Any]:
    """Compute mean win rate and token usage per condition."""
    aggregates = {}
    for condition in df['condition'].unique():
        subset = df[df['condition'] == condition]
        aggregates[condition] = {
            'mean_win_rate': float(subset['win_rate'].mean()),
            'mean_tokens': float(subset['avg_tokens'].mean()),
            'n': len(subset)
        }
    return aggregates

