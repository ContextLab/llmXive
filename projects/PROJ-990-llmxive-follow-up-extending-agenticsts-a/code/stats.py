def run_permutation_test(
    dynamic_outcomes: List[int],
    static_outcomes: List[int],
    n_permutations: int = 10000,
    random_state: int = 42
) -> Dict[str, float]:
    """
    Run a permutation test for independent samples (win/loss outcomes).
    H0: There is no difference in win rates between Dynamic and Static.
    """
    if len(dynamic_outcomes) < 2 or len(static_outcomes) < 2:
        raise ValueError("Insufficient data for permutation test (need at least 2 per group).")
    
    result = permutation_test(
        (dynamic_outcomes, static_outcomes),
        lambda x, y: np.mean(x) - np.mean(y),
        alternative='two-sided',
        n_resamples=n_permutations,
        random_state=random_state
    )
    
    return {
        "p_value": float(result.pvalue),
        "statistic": float(result.statistic),
        "test_type": "permutation_test",
        "n_permutations": n_permutations
    }

def run_mcnemar_test(
    success_dynamic: int,
    failure_dynamic: int,
    success_static: int,
    failure_static: int,
    contingency_matrix: Optional[np.ndarray] = None
) -> Dict[str, float]:
    """
    Run McNemar's test for paired nominal data.
    Requires a 2x2 contingency table of discordant pairs.
    If contingency_matrix is provided, it should be:
    [[n00, n01],
     [n10, n11]]
    where n01 = Dynamic Fail / Static Pass
          n10 = Dynamic Pass / Static Fail
    """
    if contingency_matrix is not None:
        # McNemar's test statistic is based on discordant pairs (n01 and n10)
        n01 = contingency_matrix[0, 1]
        n10 = contingency_matrix[1, 0]
        
        if n01 + n10 == 0:
            return {
                "p_value": 1.0,
                "statistic": 0.0,
                "test_type": "mcnemar",
                "note": "No discordant pairs found"
            }
        
        # Exact test or asymptotic? Using asymptotic chi-squared for simplicity
        # statistic = (|n01 - n10| - 1)^2 / (n01 + n10)  # With continuity correction
        statistic = (abs(n01 - n10) - 1) ** 2 / (n01 + n10)
        p_value = 1 - chi2_contingency(contingency_matrix, correction=False)[1]
        
        # Recalculate p-value specifically for McNemar (1 d.f.)
        p_value = 1 - stats.chi2.cdf(statistic, df=1)
        
        return {
            "p_value": float(p_value),
            "statistic": float(statistic),
            "test_type": "mcnemar",
            "discordant_pairs": int(n01 + n10)
        }
    
    # Fallback if matrix not provided but counts are
    # This is a simplified version assuming independent counts (not strictly McNemar)
    # Real implementation requires paired data structure.
    logger.warning("McNemar's test called without contingency matrix. Using simplified approximation.")
    return {"p_value": 1.0, "statistic": 0.0, "test_type": "mcnemar_approx", "note": "Incomplete data"}

def run_ttest_token_usage(
    dynamic_tokens: List[float],
    static_tokens: List[float]
) -> Dict[str, float]:
    """
    Run a paired t-test for token usage (assuming paired trajectories).
    """
    if len(dynamic_tokens) != len(static_tokens):
        logger.warning("Token usage lists have different lengths. Using independent t-test.")
        t_stat, p_val = stats.ttest_ind(dynamic_tokens, static_tokens)
        test_type = "independent_ttest"
    else:
        t_stat, p_val = stats.ttest_rel(dynamic_tokens, static_tokens)
        test_type = "paired_ttest"
    
    return {
        "p_value": float(p_val),
        "statistic": float(t_stat),
        "test_type": test_type,
        "mean_diff": float(np.mean(dynamic_tokens) - np.mean(static_tokens))
    }

def apply_bonferroni_correction(
    p_values: List[float],
    alpha: float = 0.05
) -> Dict[str, Any]:
    """
    Apply Bonferroni correction to a list of p-values.
    Returns adjusted p-values and whether any are significant.
    """
    n_tests = len(p_values)
    if n_tests == 0:
        return {"adjusted_p_values": [], "significant_any": False, "alpha_corrected": alpha}
    
    adjusted_p_values = [min(p * n_tests, 1.0) for p in p_values]
    significant_any = any(p < alpha for p in adjusted_p_values)
    alpha_corrected = alpha / n_tests if n_tests > 0 else alpha
    
    return {
        "adjusted_p_values": adjusted_p_values,
        "significant_any": significant_any,
        "alpha_corrected": alpha_corrected,
        "n_tests": n_tests
    }

def save_statistical_results(results: Dict[str, Any]) -> Path:
    """Save the final statistical results to JSON."""
    DATA_PROCESSED_DIR.mkdir(parents=True, exist_ok=True)
    with open(RESULTS_FILE, 'w') as f:
        json.dump(results, f, indent=2)
    logger.info(f"Statistical results saved to {RESULTS_FILE}")
    return RESULTS_FILE

def main():
    """
    Main entry point for T027/T025: Generate final statistical report.
    1. Load divergence report (T024a).
    2. Load simulation results (T021).
    3. Select test based on divergence.
    4. Apply Bonferroni correction.
    5. Save report.
    """
    logger.info("Starting statistical analysis (T027)...")
    
    # 1. Load Divergence Report
    try:
        divergence_data = load_divergence_report()
        is_divergent = divergence_data.get('is_divergent', False)
    except FileNotFoundError as e:
        logger.error(f"Critical: Could not load divergence report. {e}")
        # If T024a failed, we cannot proceed with the correct test selection.
        # We will assume non-divergent (paired) as a fallback, but log an error.
        is_divergent = False
        logger.warning("Assuming is_divergent=False due to missing report.")

    # 2. Load Simulation Data
    try:
        df = load_simulation_results()
        # We need raw data for statistical tests, but baseline_comparison.csv has aggregates.
        # In a real pipeline, T021 should have also saved raw runs (e.g., raw_runs.csv).
        # Since we only have aggregates here, we will simulate the "raw" data structure
        # based on the aggregates to demonstrate the logic, OR we assume T021 produced a raw file.
        # Let's assume a raw file exists for the sake of the test logic.
        RAW_RUNS_FILE = DATA_PROCESSED_DIR / "raw_simulation_runs.csv"
        
        if RAW_RUNS_FILE.exists():
            raw_df = pd.read_csv(RAW_RUNS_FILE)
            dynamic_outcomes = raw_df[raw_df['condition'] == 'Dynamic']['outcome'].tolist()
            static_outcomes = raw_df[raw_df['condition'] == 'Static']['outcome'].tolist()
            dynamic_tokens = raw_df[raw_df['condition'] == 'Dynamic']['tokens'].tolist()
            static_tokens = raw_df[raw_df['condition'] == 'Static']['tokens'].tolist()
        else:
            # Fallback: If raw data is missing, we cannot run real tests.
            # We will generate a "failed" state or use the aggregates to approximate.
            # For the purpose of this task implementation, we assume raw data exists.
            logger.error("Raw simulation runs file not found. Cannot perform statistical tests.")
            raise FileNotFoundError("Raw simulation runs file missing.")

    except FileNotFoundError as e:
        logger.error(f"Failed to load simulation data: {e}")
        return

    # 3. Select and Run Test
    results = {
        "divergence_status": is_divergent,
        "tests_performed": [],
        "p_values_raw": [],
        "bonferroni_corrected": {}
    }

    if is_divergent:
        logger.info("Trajectories are divergent. Running Permutation Test for outcomes.")
        # Permutation test for win/loss (1 for win, 0 for loss)
        perm_result = run_permutation_test(dynamic_outcomes, static_outcomes)
        results["tests_performed"].append(perm_result)
        results["p_values_raw"].append(perm_result["p_value"])
        
        # T-test for tokens (independent if divergent)
        t_result = run_ttest_token_usage(dynamic_tokens, static_tokens)
        results["tests_performed"].append(t_result)
        results["p_values_raw"].append(t_result["p_value"])
        
    else:
        logger.info("Trajectories are paired/deterministic. Running McNemar's Test.")
        # For McNemar, we need the contingency table.
        # Since we don't have the raw paired data structure here, we approximate or assume T024a provided it.
        # Let's assume we have a contingency matrix from a previous step or derive it from raw_df if paired.
        # For this implementation, we will construct a dummy contingency table if not available,
        # but in reality, this requires T024a to output the matrix.
        
        # Simulating contingency table construction from raw paired data (if available)
        # Assuming raw_df has 'trajectory_id' to pair them.
        if 'trajectory_id' in raw_df.columns:
            paired_data = raw_df.pivot(index='trajectory_id', columns='condition', values='outcome')
            # Construct contingency table
            # n11: Both win, n10: Dynamic win, Static lose, n01: Dynamic lose, Static win, n00: Both lose
            n11 = ((paired_data['Dynamic'] == 1) & (paired_data['Static'] == 1)).sum()
            n10 = ((paired_data['Dynamic'] == 1) & (paired_data['Static'] == 0)).sum()
            n01 = ((paired_data['Dynamic'] == 0) & (paired_data['Static'] == 1)).sum()
            n00 = ((paired_data['Dynamic'] == 0) & (paired_data['Static'] == 0)).sum()
            
            contingency = np.array([[n00, n01], [n10, n11]])
            mcnemar_result = run_mcnemar_test(0, 0, 0, 0, contingency_matrix=contingency)
        else:
            # Fallback if no pairing info
            mcnemar_result = {"p_value": 1.0, "statistic": 0.0, "test_type": "mcnemar", "note": "No paired data found"}
        
        results["tests_performed"].append(mcnemar_result)
        results["p_values_raw"].append(mcnemar_result["p_value"])
        
        # Paired t-test for tokens
        t_result = run_ttest_token_usage(dynamic_tokens, static_tokens)
        results["tests_performed"].append(t_result)
        results["p_values_raw"].append(t_result["p_value"])

    # 4. Bonferroni Correction
    bonf_result = apply_bonferroni_correction(results["p_values_raw"])
    results["bonferroni_corrected"] = bonf_result

    # 5. Add SC-001/SC-003 metrics (Effect Size)
    # Cohen's d for token usage
    if len(dynamic_tokens) > 1 and len(static_tokens) > 1:
        mean_diff = np.mean(dynamic_tokens) - np.mean(static_tokens)
        std_pooled = np.sqrt((np.var(dynamic_tokens) + np.var(static_tokens)) / 2)
        effect_size = mean_diff / std_pooled if std_pooled > 0 else 0.0
        results["effect_size"] = float(effect_size)
    else:
        results["effect_size"] = 0.0

    results["test_type_summary"] = "permutation_test" if is_divergent else "mcnemar_test"

    # 6. Save
    save_statistical_results(results)
    logger.info("Statistical analysis complete.")
    return results

if __name__ == "__main__":
    main()