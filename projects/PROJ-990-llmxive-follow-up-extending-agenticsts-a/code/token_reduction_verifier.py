"""
Token Reduction Verification Module (T022a).

Implements verification logic to calculate percentage reduction in token usage
between the Dynamic Policy and the Static All-Layers baseline.

Input: data/processed/baseline_comparison.csv
Output: data/processed/token_reduction_verification.json

Build Logic: The build pipeline MUST fail if `passed` is false (reduction < 30%).
"""
import os
import json
import logging
import pandas as pd
from pathlib import Path
from typing import Dict, Any, Optional

# Configure logging
logging.basicConfig(
    level=logging.INFO,
    format='%(asctime)s - %(levelname)s - %(message)s'
)
logger = logging.getLogger(__name__)

# Constants
TARGET_REDUCTION_THRESHOLD = 0.30  # 30% reduction required
INPUT_FILE = "data/processed/baseline_comparison.csv"
OUTPUT_FILE = "data/processed/token_reduction_verification.json"

def load_baseline_comparison(input_path: str) -> pd.DataFrame:
    """
    Load the baseline comparison CSV generated by T022.

    Expected Schema: condition, win_rate, avg_tokens
    """
    path = Path(input_path)
    if not path.exists():
        raise FileNotFoundError(f"Input file not found: {input_path}")

    logger.info(f"Loading baseline comparison data from {input_path}")
    df = pd.read_csv(path)

    required_columns = {'condition', 'win_rate', 'avg_tokens'}
    if not required_columns.issubset(df.columns):
        missing = required_columns - set(df.columns)
        raise ValueError(f"Missing required columns in {input_path}: {missing}")

    return df

def calculate_reduction(df: pd.DataFrame) -> Optional[float]:
    """
    Calculate the percentage reduction in token usage.

    Formula: (Static_Tokens - Dynamic_Tokens) / Static_Tokens

    Returns:
        float: The reduction percentage (0.0 to 1.0), or None if data is missing.
    """
    # Filter for specific conditions
    static_row = df[df['condition'] == 'Static All-Layers']
    dynamic_row = df[df['condition'] == 'Dynamic Policy']

    if static_row.empty or dynamic_row.empty:
        logger.error("Missing required baseline conditions in data.")
        return None

    static_tokens = static_row['avg_tokens'].iloc[0]
    dynamic_tokens = dynamic_row['avg_tokens'].iloc[0]

    if static_tokens == 0:
        logger.error("Static baseline token usage is zero; cannot calculate reduction.")
        return None

    reduction = (static_tokens - dynamic_tokens) / static_tokens
    logger.info(f"Calculated reduction: {reduction:.4f} ({reduction*100:.2f}%)")
    return reduction

def generate_verification_report(reduction: float) -> Dict[str, Any]:
    """
    Generate the verification report dictionary.

    Args:
        reduction: The calculated reduction percentage.

    Returns:
        Dict containing 'passed' boolean and 'reduction_percentage'.
    """
    passed = reduction >= TARGET_REDUCTION_THRESHOLD
    report = {
        "passed": passed,
        "reduction_percentage": reduction * 100,
        "target_threshold_percentage": TARGET_REDUCTION_THRESHOLD * 100,
        "message": "Verification PASSED" if passed else "Verification FAILED: Reduction below 30% threshold"
    }
    return report

def save_report(report: Dict[str, Any], output_path: str) -> None:
    """
    Save the verification report to a JSON file.

    Args:
        report: The report dictionary.
        output_path: Path to save the JSON file.
    """
    path = Path(output_path)
    path.parent.mkdir(parents=True, exist_ok=True)

    with open(path, 'w', encoding='utf-8') as f:
        json.dump(report, f, indent=2)

    logger.info(f"Verification report saved to {output_path}")

    # Log failure condition explicitly for build pipeline visibility
    if not report['passed']:
        logger.error(f"BUILD FAILURE: Token reduction ({report['reduction_percentage']:.2f}%) is below threshold ({report['target_threshold_percentage']}%).")

def main() -> None:
    """
    Main entry point for T022a.
    Loads data, calculates reduction, and saves the verification report.
    """
    try:
        # 1. Load Data
        df = load_baseline_comparison(INPUT_FILE)

        # 2. Calculate Reduction
        reduction = calculate_reduction(df)
        if reduction is None:
            raise ValueError("Could not calculate token reduction due to missing or invalid data.")

        # 3. Generate Report
        report = generate_verification_report(reduction)

        # 4. Save Report
        save_report(report, OUTPUT_FILE)

        # 5. Final Status Check
        if not report['passed']:
            # Raise an exception to signal build failure to the runner
            raise RuntimeError(f"Verification failed: Token reduction {report['reduction_percentage']:.2f}% is below required {report['target_threshold_percentage']}%")

        logger.info("T022a completed successfully.")

    except FileNotFoundError as e:
        logger.critical(f"Data file missing: {e}")
        raise
    except ValueError as e:
        logger.critical(f"Data processing error: {e}")
        raise
    except RuntimeError as e:
        logger.critical(f"Verification failed: {e}")
        raise

if __name__ == "__main__":
    main()