"""
Task T014: Add `constraint_count` metadata column to filtered output.

This script reads the filtered dataset generated by T013 (which filters for
tasks with >= 5 progressive constraint reveals), computes the exact number of
constraints for each task, adds a `constraint_count` column, and saves the
result to `data/processed/filtered_tasks.csv`.

It relies on `code/dataset/loader.py` to load the raw data and apply the filter.
"""
import os
import sys
from pathlib import Path
from typing import List, Dict, Any

# Add project root to path to allow imports from code/
project_root = Path(__file__).resolve().parent.parent.parent
sys.path.insert(0, str(project_root))

from config import Paths
from dataset.loader import load_adaplanbench, filter_progressive_constraints


def compute_constraint_count(task: Dict[str, Any]) -> int:
    """
    Extract the number of progressive constraints from a task dictionary.
    
    The AdaPlanBench dataset typically stores constraints in a 'constraints' 
    field, which is a list of strings or objects. We count the length of this list.
    If the field is missing or not a list, we return 0.
    """
    constraints = task.get("constraints", [])
    if isinstance(constraints, list):
        return len(constraints)
    return 0


def add_constraint_count_column(df: Any) -> Any:
    """
    Adds the `constraint_count` column to the pandas DataFrame.
    """
    if "constraints" in df.columns:
        # Apply the count function row-wise
        df["constraint_count"] = df["constraints"].apply(compute_constraint_count)
    else:
        # Fallback: assume the count is already known or try to infer from other fields
        # For safety, we log a warning and set to 0 if the column is missing unexpectedly
        print("Warning: 'constraints' column not found in filtered dataset.")
        df["constraint_count"] = 0
    return df


def main():
    print("Starting T014: Adding constraint_count metadata column...")
    
    # Ensure output directory exists
    output_path = Paths.processed / "filtered_tasks.csv"
    output_path.parent.mkdir(parents=True, exist_ok=True)
    
    # 1. Load raw dataset
    print("Loading raw AdaPlanBench dataset...")
    try:
        raw_dataset = load_adaplanbench()
    except Exception as e:
        print(f"ERROR: Failed to load raw dataset: {e}")
        sys.exit(1)
    
    # 2. Apply filter (>= 5 progressive constraints)
    print("Filtering for tasks with >= 5 progressive constraints...")
    try:
        filtered_dataset = filter_progressive_constraints(raw_dataset, min_constraints=5)
    except Exception as e:
        print(f"ERROR: Failed to filter dataset: {e}")
        sys.exit(1)
    
    if filtered_dataset is None or len(filtered_dataset) == 0:
        print("ERROR: Filtered dataset is empty. Cannot add metadata column.")
        sys.exit(1)
    
    # 3. Convert to DataFrame
    df = filtered_dataset.to_pandas()
    
    # 4. Compute and add the constraint_count column
    print("Computing constraint_count for each task...")
    df = add_constraint_count_column(df)
    
    # 5. Save to CSV
    print(f"Saving filtered dataset with constraint_count to {output_path}...")
    df.to_csv(output_path, index=False)
    
    # Verification
    print(f"Saved {len(df)} tasks.")
    print(f"Sample constraint counts: {df['constraint_count'].head().tolist()}")
    print("T014 completed successfully.")


if __name__ == "__main__":
    main()