"""
Generate execution_traces.csv from the execution logs produced by T023.

This script reads the JSON execution logs generated by the dual-track and monolithic
agents (produced in T023), aggregates the data per task, and writes a CSV file
containing:
  - architecture_type: 'dual_track' or 'monolithic'
  - constraint_count: number of progressive constraints for the task
  - violation: boolean indicating if any constraint violation occurred
  - final_score: the final score achieved by the agent on the task

Output: data/processed/execution_traces.csv
"""
import os
import sys
import json
import csv
from pathlib import Path
from typing import List, Dict, Any, Optional

# Add project root to path for imports
project_root = Path(__file__).resolve().parent.parent
sys.path.insert(0, str(project_root))

from config import Paths


def load_execution_logs(log_dir: Path) -> List[Dict[str, Any]]:
    """
    Load all JSON execution log files from the specified directory.
    
    Args:
        log_dir: Path to the directory containing execution log JSON files.
        
    Returns:
        List of execution log dictionaries.
    """
    logs = []
    if not log_dir.exists():
        raise FileNotFoundError(f"Execution log directory not found: {log_dir}")
    
    for log_file in sorted(log_dir.glob("*.json")):
        try:
            with open(log_file, 'r', encoding='utf-8') as f:
                log_data = json.load(f)
                # Ensure the log has the expected structure
                if isinstance(log_data, list):
                    logs.extend(log_data)
                elif isinstance(log_data, dict):
                    logs.append(log_data)
        except json.JSONDecodeError as e:
            print(f"Warning: Failed to parse {log_file}: {e}", file=sys.stderr)
            continue
    
    return logs


def extract_trace_data(logs: List[Dict[str, Any]], filtered_tasks_map: Optional[Dict[str, int]] = None) -> List[Dict[str, Any]]:
    """
    Extract trace data from execution logs.
    
    Args:
        logs: List of execution log dictionaries.
        filtered_tasks_map: Optional mapping of task_id to constraint_count.
                           If provided, uses this for constraint_count; otherwise
                           extracts from log if available.
                            
    Returns:
        List of dictionaries with trace data.
    """
    traces = []
    
    for log in logs:
        # Extract required fields
        task_id = log.get('task_id')
        architecture = log.get('architecture_type', log.get('agent_type', 'unknown'))
        
        # Determine constraint count
        if filtered_tasks_map and task_id in filtered_tasks_map:
            constraint_count = filtered_tasks_map[task_id]
        else:
            # Try to extract from log
            constraint_count = log.get('constraint_count', log.get('num_constraints', 0))
            if constraint_count == 0 and 'constraints' in log:
                constraints = log['constraints']
                constraint_count = len(constraints) if isinstance(constraints, list) else 0
        
        # Determine if any violation occurred
        violations = log.get('violations', [])
        has_violation = len(violations) > 0
        
        # Extract final score
        final_score = log.get('final_score', log.get('score', log.get('result_score', None)))
        if final_score is None:
            # Try to compute from task results if available
            task_results = log.get('task_results', [])
            if task_results and isinstance(task_results, list):
                # Take the last result's score
                final_score = task_results[-1].get('score', 0)
            else:
                final_score = 0
        
        trace = {
            'task_id': task_id,
            'architecture_type': architecture,
            'constraint_count': constraint_count,
            'violation': has_violation,
            'final_score': final_score
        }
        traces.append(trace)
    
    return traces


def load_filtered_tasks_map(filtered_csv_path: Path) -> Dict[str, int]:
    """
    Load constraint counts from the filtered tasks CSV.
    
    Args:
        filtered_csv_path: Path to the filtered_tasks.csv file.
        
    Returns:
        Dictionary mapping task_id to constraint_count.
    """
    task_map = {}
    if not filtered_csv_path.exists():
        print(f"Warning: Filtered tasks file not found: {filtered_csv_path}", file=sys.stderr)
        return task_map
    
    with open(filtered_csv_path, 'r', encoding='utf-8') as f:
        reader = csv.DictReader(f)
        for row in reader:
            task_id = row.get('task_id', row.get('id', ''))
            constraint_count = int(row.get('constraint_count', 0))
            if task_id:
                task_map[task_id] = constraint_count
    
    return task_map


def write_traces_csv(traces: List[Dict[str, Any]], output_path: Path) -> None:
    """
    Write trace data to CSV file.
    
    Args:
        traces: List of trace dictionaries.
        output_path: Path to the output CSV file.
    """
    if not traces:
        print("Warning: No traces to write", file=sys.stderr)
    
    # Ensure output directory exists
    output_path.parent.mkdir(parents=True, exist_ok=True)
    
    fieldnames = ['task_id', 'architecture_type', 'constraint_count', 'violation', 'final_score']
    
    with open(output_path, 'w', newline='', encoding='utf-8') as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(traces)
    
    print(f"Wrote {len(traces)} traces to {output_path}")


def main() -> None:
    """Main entry point for generating execution traces."""
    paths = Paths()
    
    # Define paths
    log_dir = paths.intermediate / "agent_execution_logs"
    filtered_tasks_path = paths.processed / "filtered_tasks.csv"
    output_path = paths.processed / "execution_traces.csv"
    
    # Load constraint counts from filtered tasks
    constraint_map = load_filtered_tasks_map(filtered_tasks_path)
    
    # Load execution logs
    print(f"Loading execution logs from {log_dir}...")
    logs = load_execution_logs(log_dir)
    print(f"Loaded {len(logs)} execution logs")
    
    # Extract trace data
    traces = extract_trace_data(logs, constraint_map)
    print(f"Extracted {len(traces)} trace records")
    
    # Write output CSV
    write_traces_csv(traces, output_path)
    
    # Print summary statistics
    if traces:
        arch_counts = {}
        violation_counts = {}
        for trace in traces:
            arch = trace['architecture_type']
            arch_counts[arch] = arch_counts.get(arch, 0) + 1
            if trace['violation']:
                violation_counts[arch] = violation_counts.get(arch, 0) + 1
        
        print("\nSummary Statistics:")
        print(f"Total traces: {len(traces)}")
        for arch, count in sorted(arch_counts.items()):
            viol_count = violation_counts.get(arch, 0)
            print(f"  {arch}: {count} traces, {viol_count} with violations ({viol_count/count*100:.1f}%)")


if __name__ == "__main__":
    main()