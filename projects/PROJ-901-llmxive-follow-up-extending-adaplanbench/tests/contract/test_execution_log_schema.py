"""
Contract test for execution log schema.

Verifies that the execution traces produced by the agent execution loop
conform to the expected schema defined in the project contracts.
"""
import csv
import json
from pathlib import Path
from typing import Any, Dict, List, Set

import pytest

# Import paths from config
from config import Paths


# Expected schema definition based on T024 requirements
EXPECTED_COLUMNS: Set[str] = {
    "architecture_type",
    "constraint_count",
    "violation",
    "final_score",
    "task_id",
    "trace_id",
    "timestamp",
}

# Expected values for categorical columns
VALID_ARCHITECTURES: Set[str] = {"dual_track", "monolithic"}
VALID_VIOLATION_VALUES: Set[str] = {"true", "false", "True", "False", "1", "0"}

# Contract file path (if it exists, we could load schema from there,
# but for this contract test we hardcode the expected structure)
CONTRACT_PATH = Paths().specs / "contracts" / "execution-log.schema.yaml"


def _load_execution_traces() -> List[Dict[str, Any]]:
    """
    Load the execution traces CSV generated by the agent execution.

    This function assumes T023 and T024 have been run and produced
    data/processed/execution_traces.csv.
    """
    csv_path = Paths().processed / "execution_traces.csv"

    if not csv_path.exists():
        pytest.fail(
            f"Execution traces file not found at {csv_path}. "
            "Ensure T023 and T024 have been run to generate the file."
        )

    with open(csv_path, "r", encoding="utf-8") as f:
        reader = csv.DictReader(f)
        return list(reader)


def _validate_row_schema(row: Dict[str, Any], row_index: int) -> List[str]:
    """
    Validate a single row against the contract schema.

    Returns a list of validation errors (empty if valid).
    """
    errors = []

    # Check for required columns
    missing_columns = EXPECTED_COLUMNS - set(row.keys())
    if missing_columns:
        errors.append(
            f"Row {row_index}: Missing required columns: {missing_columns}"
        )

    # Validate architecture_type
    arch = row.get("architecture_type", "").strip()
    if arch and arch not in VALID_ARCHITECTURES:
        errors.append(
            f"Row {row_index}: Invalid architecture_type '{arch}'. "
            f"Must be one of {VALID_ARCHITECTURES}"
        )

    # Validate violation field (boolean-like)
    violation = row.get("violation", "").strip()
    if violation and violation not in VALID_VIOLATION_VALUES:
        errors.append(
            f"Row {row_index}: Invalid violation value '{violation}'. "
            f"Must be one of {VALID_VIOLATION_VALUES}"
        )

    # Validate constraint_count is numeric
    constraint_count = row.get("constraint_count", "").strip()
    if constraint_count:
        try:
            int(constraint_count)
        except ValueError:
            errors.append(
                f"Row {row_index}: constraint_count '{constraint_count}' "
                "is not a valid integer"
            )

    # Validate final_score is numeric
    final_score = row.get("final_score", "").strip()
    if final_score:
        try:
            float(final_score)
        except ValueError:
            errors.append(
                f"Row {row_index}: final_score '{final_score}' "
                "is not a valid number"
            )

    return errors


@pytest.mark.contract
def test_execution_log_schema():
    """
    Contract test: Verify execution_traces.csv matches the expected schema.

    This test ensures that the output of the dual-track and monolithic
    agent execution conforms to the agreed-upon data contract.
    """
    traces = _load_execution_traces()

    assert len(traces) > 0, "Execution traces file is empty"

    all_errors: List[str] = []

    for idx, row in enumerate(traces):
        row_errors = _validate_row_schema(row, idx)
        all_errors.extend(row_errors)

    if all_errors:
        pytest.fail(
            "Execution log schema validation failed:\n" + "\n".join(all_errors)
        )

@pytest.mark.contract
def test_execution_log_contains_both_architectures():
    """
    Contract test: Verify that both architectures are represented in the logs.

    The experiment requires comparing dual-track vs monolithic approaches,
    so both must be present in the execution traces.
    """
    traces = _load_execution_traces()

    architectures = {row.get("architecture_type", "").strip() for row in traces}

    missing = VALID_ARCHITECTURES - architectures
    if missing:
        pytest.fail(
            f"Execution logs missing required architectures: {missing}. "
            "Both 'dual_track' and 'monolithic' must be present."
        )

@pytest.mark.contract
def test_execution_log_constraint_count_range():
    """
    Contract test: Verify constraint counts are within expected range (>= 5).

    Based on T013, the filtered dataset should only contain tasks with
    >= 5 progressive constraints.
    """
    traces = _load_execution_traces()

    for idx, row in enumerate(traces):
        constraint_count = row.get("constraint_count", "").strip()
        if constraint_count:
            count = int(constraint_count)
            if count < 5:
                pytest.fail(
                    f"Row {idx}: constraint_count {count} is less than 5. "
                    "All tasks should have >= 5 constraints per T013 filter."
                )