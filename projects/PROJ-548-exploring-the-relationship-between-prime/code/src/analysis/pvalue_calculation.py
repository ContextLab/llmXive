def load_observed_ks_statistic(results_path: Path) -> float:
    """
    Load the observed KS statistic from the correlation results file.

    Args:
        results_path: Path to correlation_results.json

    Returns:
        The observed KS statistic value.

    Raises:
        FileNotFoundError: If the results file doesn't exist.
        KeyError: If the expected key is missing.
        ValueError: If the value is not a valid number.
    """
    if not results_path.exists():
        raise FileNotFoundError(f"Results file not found: {results_path}")

    with open(results_path, 'r') as f:
        data = json.load(f)

    if 'ks_statistic' not in data:
        raise KeyError("Expected key 'ks_statistic' not found in results file")

    ks_stat = float(data['ks_statistic'])
    logger.info(f"Loaded observed KS statistic: {ks_stat:.6f}")
    return ks_stat

def load_cramer_null_distribution(null_results_path: Path) -> np.ndarray:
    """
    Load the KS statistics from the Cramér model Monte Carlo simulation.

    Args:
        null_results_path: Path to the Cramér null distribution results.

    Returns:
        Array of KS statistics from the Monte Carlo simulation.

    Raises:
        FileNotFoundError: If the file doesn't exist.
        KeyError: If the expected key is missing.
    """
    if not null_results_path.exists():
        raise FileNotFoundError(f"Cramér null distribution file not found: {null_results_path}")

    with open(null_results_path, 'r') as f:
        data = json.load(f)

    if 'ks_statistics' not in data:
        raise KeyError("Expected key 'ks_statistics' not found in Cramér null distribution file")

    ks_null = np.array(data['ks_statistics'], dtype=np.float64)
    logger.info(f"Loaded {len(ks_null)} KS statistics from Cramér null distribution")
    return ks_null

def calculate_pvalue(
    observed_ks: float,
    null_ks_distribution: np.ndarray,
    alternative: str = 'greater'
) -> Tuple[float, Dict[str, Any]]:
    """
    Calculate the p-value for the observed KS statistic against the null distribution.

    The p-value is calculated as the proportion of null KS statistics that are
    greater than or equal to the observed KS statistic (for 'greater' alternative).

    Args:
        observed_ks: The observed KS statistic from the real data analysis.
        null_ks_distribution: Array of KS statistics from the Cramér model simulation.
        alternative: Type of alternative hypothesis ('greater', 'less', 'two-sided').
                    'greater' is appropriate here as we're testing if the observed
                    distribution is more extreme than the Cramér model predicts.

    Returns:
        Tuple of (p_value, stats_dict) where stats_dict contains additional statistics.
    """
    if len(null_ks_distribution) == 0:
        raise ValueError("Null distribution is empty; cannot calculate p-value")

    if not np.all(np.isfinite(null_ks_distribution)):
        raise ValueError("Null distribution contains non-finite values")

    n_simulations = len(null_ks_distribution)

    if alternative == 'greater':
        # P-value: proportion of null statistics >= observed
        # This tests if the observed distribution is more extreme (larger KS) than expected
        extreme_count = np.sum(null_ks_distribution >= observed_ks)
        p_value = extreme_count / n_simulations

    elif alternative == 'less':
        # P-value: proportion of null statistics <= observed
        extreme_count = np.sum(null_ks_distribution <= observed_ks)
        p_value = extreme_count / n_simulations

    elif alternative == 'two-sided':
        # Two-sided test: proportion of |null - mean| >= |observed - mean|
        mean_null = np.mean(null_ks_distribution)
        observed_deviation = abs(observed_ks - mean_null)
        null_deviations = np.abs(null_ks_distribution - mean_null)
        extreme_count = np.sum(null_deviations >= observed_deviation)
        p_value = extreme_count / n_simulations

    else:
        raise ValueError(f"Unknown alternative hypothesis: {alternative}")

    # Calculate additional statistics
    stats_dict = {
        'observed_ks': observed_ks,
        'mean_null_ks': float(np.mean(null_ks_distribution)),
        'std_null_ks': float(np.std(null_ks_distribution)),
        'min_null_ks': float(np.min(null_ks_distribution)),
        'max_null_ks': float(np.max(null_ks_distribution)),
        'n_simulations': n_simulations,
        'extreme_count': int(extreme_count),
        'p_value': p_value,
        'alternative': alternative
    }

    logger.info(f"Calculated p-value: {p_value:.6f} (extreme_count={extreme_count}, n={n_simulations})")
    logger.info(f"Null distribution: mean={stats_dict['mean_null_ks']:.6f}, std={stats_dict['std_null_ks']:.6f}")

    return p_value, stats_dict

def run_pipeline(
    observed_results_path: Optional[str] = None,
    null_results_path: Optional[str] = None,
    output_path: Optional[str] = None,
    alternative: str = 'greater'
) -> Dict[str, Any]:
    """
    Run the complete p-value calculation pipeline.

    This function:
    1. Loads the observed KS statistic from the correlation results.
    2. Loads the KS statistics from the Cramér model Monte Carlo simulation.
    3. Calculates the p-value.
    4. Saves the results to a JSON file.
    5. Updates the project state with the results.

    Args:
        observed_results_path: Path to correlation_results.json. Defaults to config value.
        null_results_path: Path to Cramér null distribution results. Defaults to config value.
        output_path: Path for the p-value results JSON. Defaults to config value.
        alternative: Type of alternative hypothesis for the p-value calculation.

    Returns:
        Dictionary containing the p-value and all related statistics.

    Raises:
        FileNotFoundError: If required input files don't exist.
        ValueError: If input data is invalid.
    """
    paths = get_project_paths()

    # Set default paths if not provided
    if observed_results_path is None:
        observed_results_path = str(paths['results_dir'] / 'correlation_results.json')
    if null_results_path is None:
        null_results_path = str(paths['results_dir'] / 'cramer_null_distribution.json')
    if output_path is None:
        output_path = str(paths['results_dir'] / 'pvalue_results.json')

    observed_path = Path(observed_results_path)
    null_path = Path(null_results_path)
    output_file = Path(output_path)

    # Ensure output directory exists
    output_file.parent.mkdir(parents=True, exist_ok=True)

    logger.info(f"Starting p-value calculation pipeline")
    logger.info(f"  Observed results: {observed_path}")
    logger.info(f"  Null distribution: {null_path}")
    logger.info(f"  Output: {output_file}")

    # Load data
    observed_ks = load_observed_ks_statistic(observed_path)
    null_ks_distribution = load_cramer_null_distribution(null_path)

    # Calculate p-value
    p_value, stats_dict = calculate_pvalue(observed_ks, null_ks_distribution, alternative)

    # Prepare final results
    results = {
        'p_value': p_value,
        'alternative_hypothesis': alternative,
        'observed_ks_statistic': observed_ks,
        'null_distribution_stats': {
            'mean': stats_dict['mean_null_ks'],
            'std': stats_dict['std_null_ks'],
            'min': stats_dict['min_null_ks'],
            'max': stats_dict['max_null_ks'],
            'n_simulations': stats_dict['n_simulations']
        },
        'extreme_count': stats_dict['extreme_count'],
        'interpretation': 'Significant' if p_value < 0.05 else 'Not significant',
        'threshold_5pct': 0.05,
        'threshold_1pct': 0.01
    }

    # Save results to JSON
    with open(output_file, 'w') as f:
        json.dump(results, f, indent=2)

    logger.info(f"Results saved to: {output_file}")
    logger.info(f"P-value: {p_value:.6f} -> {results['interpretation']} at 5% level")

    # Update project state
    try:
        state = load_state()
        state['results']['p_value_analysis'] = {
            'p_value': p_value,
            'observed_ks': observed_ks,
            'n_simulations': stats_dict['n_simulations'],
            'interpretation': results['interpretation'],
            'completed_at': None  # Will be set by update_state_checksums
        }
        update_state_checksums(state, str(output_file))
        save_state(state)
        logger.info("Project state updated with p-value results")
    except Exception as e:
        logger.warning(f"Could not update project state: {e}")

    return results

