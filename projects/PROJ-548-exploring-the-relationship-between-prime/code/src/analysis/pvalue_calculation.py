"""
Task T024: Calculate p-value for observed distributional alignment against the Cramér null distribution.

This module implements the calculation of the p-value by comparing the observed KS statistic
(from the comparison of empirical maximal gaps vs. GUE theoretical distribution) against the
null distribution of KS statistics generated by the Cramér model Monte Carlo simulation.

The p-value represents the probability of observing a KS statistic as extreme or more extreme
than the observed one, assuming the null hypothesis (that prime gaps follow the Cramér model) is true.
"""
