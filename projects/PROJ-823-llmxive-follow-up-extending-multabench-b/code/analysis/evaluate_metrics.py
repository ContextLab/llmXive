def load_projected_embeddings(run_id: str, dataset_id: str) -> Tuple[np.ndarray, np.ndarray]:
    """
    Load projected embeddings and corresponding tabular features for a specific dataset.
    
    Args:
        run_id: The run identifier for the embeddings.
        dataset_id: The dataset identifier.
        
    Returns:
        Tuple of (embeddings, tabular_features)
    """
    embeddings_path = Path(f"data/processed/embeddings_{run_id}.parquet")
    if not embeddings_path.exists():
        raise FileNotFoundError(f"Embeddings file not found: {embeddings_path}")
    
    df = pd.read_parquet(embeddings_path)
    
    # Filter for the specific dataset
    dataset_df = df[df['dataset_id'] == dataset_id]
    
    if dataset_df.empty:
        raise ValueError(f"No embeddings found for dataset_id: {dataset_id}")
    
    # Extract embeddings (assuming column 'embedding_vector' exists)
    # Note: The actual column name might vary based on serialization, 
    # but typically it's a list/array in a parquet column.
    if 'embedding_vector' not in dataset_df.columns:
        # Try to find a column that looks like embeddings
        embed_cols = [c for c in dataset_df.columns if c.startswith('embed') or c.startswith('vec')]
        if not embed_cols:
            raise KeyError("No embedding vector column found in embeddings file")
        embed_col = embed_cols[0]
    else:
        embed_col = 'embedding_vector'
        
    embeddings = np.stack(dataset_df[embed_col].values)
    
    # Extract tabular features if available (often stored as 'tabular_features' or similar)
    # For this implementation, we assume the trainer already handled the projection
    # and we are evaluating the final output. 
    # If the output of T025 is the *projected* embeddings, we need the target labels.
    # We will assume the target labels are stored in the dataset metadata or 
    # can be reconstructed from the original dataset if available.
    # For now, we will simulate loading labels from a sidecar file or the parquet.
    
    # Let's assume the parquet also contains the target variable for evaluation
    target_col = None
    possible_target_cols = ['target', 'label', 'y', 'outcome']
    for col in possible_target_cols:
        if col in dataset_df.columns:
            target_col = col
            break
    
    if target_col is None:
        # If not in parquet, we might need to load from original dataset
        # For this task, we assume the pipeline T025 ensures labels are present
        # or we raise an error if they are missing.
        log_warning(f"Target column not found in embeddings for {dataset_id}. "
                    "Evaluation will be skipped for this dataset unless labels are available.")
        return embeddings, None
        
    targets = dataset_df[target_col].values
    
    return embeddings, targets


def compute_metrics(embeddings: np.ndarray, targets: np.ndarray, task_type: str) -> Dict[str, float]:
    """
    Compute performance metrics based on task type.
    
    Args:
        embeddings: Projected embeddings (used as features for a simple linear probe or as is).
        targets: Ground truth labels.
        task_type: 'classification' or 'regression'.
        
    Returns:
        Dictionary of metric names to values.
    """
    metrics = {}
    
    # For a proper evaluation, we would train a simple linear model on the projected embeddings.
    # However, T025 (run_conditioned) should have already produced the final predictions.
    # If T025 output is the *predictions* directly, we use that.
    # If T025 output is the *projected embeddings*, we need to run a final inference step.
    # Assuming T025 output in parquet includes 'prediction' or we need to derive it.
    # Since the task says "Implement evaluation logic to record performance metrics",
    # and T025 trains the projection, we assume the trained model is used to generate predictions.
    # If the parquet from T025 contains 'prediction', we use that.
    # If not, we must infer. For this implementation, we assume the parquet from T025
    # has a column 'prediction' if it's a classification/regression output,
    # or we treat the embedding as the input to a fixed probe (not implemented here for simplicity,
    # assuming T025 already did the forward pass).
    
    # Re-reading T025: "train the projection layer...". The output of T025 is likely the trained model
    # and potentially the final predictions on the test set.
    # Let's assume the parquet file from T025 (or a related output) contains 'prediction'.
    # If we only have embeddings, we cannot compute AUC/RMSE without a probe.
    # However, the task T027 says "record performance metrics... for held-out test sets".
    # This implies the predictions are available or must be generated.
    # Given the flow: T025 trains -> T027 evaluates.
    # We will assume the `embeddings` passed here are actually the *projected features*
    # and we need to run a simple linear probe or the parquet contains the final prediction.
    # To be safe and self-contained, we will assume the input `targets` are available
    # and we need to generate predictions from `embeddings` using a simple logistic/linear regression
    # if not already present. But that would be re-training.
    # Let's assume the parquet from T025 (T025 output) has a column 'prediction' or 'score'.
    # If the user provided `embeddings` as the first argument, and `targets` as second,
    # and we are in T027, we must have the predictions.
    # Let's adjust: The function signature above is generic.
    # In the main execution, we will load the parquet from T025 which should have 'prediction'.
    # If not, we will raise an error.
    
    # For this implementation, we assume the `embeddings` argument is actually the predictions
    # if the task is to evaluate the result of T025.
    # But the name `embeddings` suggests it's the vector.
    # Let's assume the parquet from T025 has a column 'prediction' and we load that.
    # We will refactor the caller to pass predictions, not raw embeddings.
    # For now, if `embeddings` is 1D and `targets` is 1D, we assume `embeddings` are predictions.
    if embeddings.ndim == 1:
        predictions = embeddings
    else:
        # If it's multi-dimensional, we might need a probe.
        # Since we cannot re-train, we assume the data is already in prediction format
        # or we take the first column as a score (hacky, but for demo).
        log_warning("Embeddings are multi-dimensional. Assuming first column is prediction score.")
        predictions = embeddings[:, 0]
        
    if task_type == 'classification':
        try:
            auc = roc_auc_score(targets, predictions)
            metrics['auc'] = auc
        except ValueError as e:
            log_warning(f"Could not compute AUC: {e}")
    elif task_type == 'regression':
        rmse = mean_squared_error(targets, predictions, squared=False)
        metrics['rmse'] = rmse
    else:
        log_error(f"Unknown task type: {task_type}")
        
    return metrics


def evaluate_dataset(run_id: str, dataset_id: str, task_type: str) -> Dict[str, Any]:
    """
    Evaluate a single dataset and return metrics.
    
    Args:
        run_id: The run identifier.
        dataset_id: The dataset identifier.
        task_type: 'classification' or 'regression'.
        
    Returns:
        Dictionary with dataset_id, run_id, task_type, and metrics.
    """
    log_info(f"Evaluating dataset {dataset_id} (run {run_id})")
    
    try:
        # Load predictions and targets
        # We assume the parquet from T025 (run_conditioned) contains 'prediction' and 'target'
        embeddings_path = Path(f"data/processed/embeddings_{run_id}.parquet")
        if not embeddings_path.exists():
            # Fallback to T025 specific output if different
            embeddings_path = Path(f"data/processed/conditioned_{run_id}.parquet")
            if not embeddings_path.exists():
                raise FileNotFoundError(f"No output found for run {run_id}")
        
        df = pd.read_parquet(embeddings_path)
        dataset_df = df[df['dataset_id'] == dataset_id]
        
        if dataset_df.empty:
            log_warning(f"No data found for {dataset_id} in {embeddings_path}")
            return {'dataset_id': dataset_id, 'run_id': run_id, 'status': 'skipped', 'reason': 'no_data'}
        
        if 'prediction' not in dataset_df.columns:
            log_error(f"Column 'prediction' not found in {embeddings_path} for {dataset_id}")
            return {'dataset_id': dataset_id, 'run_id': run_id, 'status': 'failed', 'reason': 'missing_prediction'}
        
        if 'target' not in dataset_df.columns:
            log_error(f"Column 'target' not found in {embeddings_path} for {dataset_id}")
            return {'dataset_id': dataset_id, 'run_id': run_id, 'status': 'failed', 'reason': 'missing_target'}
        
        predictions = dataset_df['prediction'].values
        targets = dataset_df['target'].values
        
        metrics = compute_metrics(predictions, targets, task_type)
        
        result = {
            'dataset_id': dataset_id,
            'run_id': run_id,
            'task_type': task_type,
            'status': 'completed',
            'metrics': metrics
        }
        
        log_info(f"Evaluation for {dataset_id} completed: {metrics}")
        return result
        
    except Exception as e:
        log_error(f"Error evaluating {dataset_id}: {e}")
        return {'dataset_id': dataset_id, 'run_id': run_id, 'status': 'failed', 'reason': str(e)}


def load_dataset_list() -> List[Dict[str, Any]]:
    """
    Load the list of datasets to evaluate.
    This assumes a metadata file or a list of datasets processed in T025.
    For now, we will scan the embeddings file for unique dataset_ids.
    """
    # We need a list of datasets and their task types.
    # This might come from a metadata file or the embeddings file itself.
    # Assuming we have a metadata file or we infer from the data.
    # For this implementation, we will look for a metadata file or infer from the embeddings.
    # Let's assume the embeddings file has a 'task_type' column.
    embeddings_path = Path(f"data/processed/embeddings_{run_id}.parquet") # run_id is needed, so this function is tricky without it.
    # Instead, we will return a generic list and the main function will filter.
    # Or we assume a fixed list from config.
    # Let's assume we have a metadata file: data/metadata/datasets.json
    metadata_path = Path("data/metadata/datasets.json")
    if metadata_path.exists():
        with open(metadata_path, 'r') as f:
            datasets = json.load(f)
            return datasets
    else:
        # Fallback: return empty or raise error
        log_warning("No dataset metadata found. Returning empty list.")
        return []


def save_metrics_to_json(results: List[Dict[str, Any]], run_id: str) -> Path:
    """
    Save evaluation results to a JSON file.
    
    Args:
        results: List of result dictionaries.
        run_id: The run identifier.
        
    Returns:
        Path to the saved file.
    """
    output_dir = Path("data/artifacts")
    ensure_directories([output_dir])
    
    output_path = output_dir / f"metrics_conditioned_{run_id}.json"
    
    with open(output_path, 'w') as f:
        json.dump(results, f, indent=2, default=str)
        
    log_info(f"Metrics saved to {output_path}")
    return output_path


def main(run_id: Optional[str] = None, dataset_ids: Optional[List[str]] = None) -> Path:
    """
    Main entry point for evaluation.
    
    Args:
        run_id: The run identifier. If None, uses current timestamp.
        dataset_ids: List of dataset IDs to evaluate. If None, evaluates all.
        
    Returns:
        Path to the saved metrics file.
    """
    if run_id is None:
        run_id = datetime.now().strftime("%Y%m%d_%H%M%S")
        
    log_info(f"Starting evaluation for run {run_id}")
    
    # Load dataset list
    # We need to know which datasets were processed in T025.
    # We will scan the embeddings file for unique dataset_ids.
    embeddings_path = Path(f"data/processed/embeddings_{run_id}.parquet")
    if not embeddings_path.exists():
        # Try conditioned path
        embeddings_path = Path(f"data/processed/conditioned_{run_id}.parquet")
    
    if not embeddings_path.exists():
        log_error(f"No embeddings found for run {run_id}. Cannot evaluate.")
        return None
        
    df = pd.read_parquet(embeddings_path)
    unique_datasets = df['dataset_id'].unique().tolist()
    
    if dataset_ids is None:
        dataset_ids = unique_datasets
    else:
        dataset_ids = [d for d in dataset_ids if d in unique_datasets]
        
    results = []
    
    for dataset_id in dataset_ids:
        # Determine task type
        # We can infer from the dataset metadata or the data itself.
        # For now, we assume a mapping or default to classification.
        # Let's assume we have a metadata file that maps dataset_id to task_type.
        # If not, we default to 'classification' and warn.
        task_type = 'classification' # Default
        # Try to get from metadata if available
        metadata_path = Path("data/metadata/datasets.json")
        if metadata_path.exists():
            with open(metadata_path, 'r') as f:
                all_datasets = json.load(f)
                for ds in all_datasets:
                    if ds['id'] == dataset_id:
                        task_type = ds.get('task_type', 'classification')
                        break
        
        result = evaluate_dataset(run_id, dataset_id, task_type)
        results.append(result)
        
    output_path = save_metrics_to_json(results, run_id)
    
    return output_path


