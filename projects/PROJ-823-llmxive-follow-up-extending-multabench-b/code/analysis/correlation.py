"""
Correlation Analysis Module for llmXive MulTaBench Extension.

This module implements the calculation of the "Recovery Ratio" and prepares
data for subsequent statistical analysis (Pearson correlation, t-tests).

Recovery Ratio = (CPU-Conditioned - Frozen_Aggregated) / (GPU-Tuned - Frozen_Aggregated)

Inputs:
  - data/artifacts/gpu_tuned_baselines.csv (from T032a)
  - data/artifacts/frozen_baseline_aggregated_{run_id}.json (from T019c)
  - data/artifacts/metrics_conditioned_{run_id}.json (from T028)

Outputs:
  - data/processed/correlation_inputs_{run_id}.json (Intermediate dataset for T033/T034)
"""

import os
import sys
import json
import csv
import argparse
from pathlib import Path
from datetime import datetime
from typing import Dict, Any, List, Optional, Tuple

# Add project root to path for imports
project_root = Path(__file__).resolve().parent.parent.parent
sys.path.insert(0, str(project_root))

from utils.logging import get_logger, log_info, log_error, log_warning

logger = get_logger(__name__)

# Default paths relative to project root
DEFAULT_GPU_BASELINES_PATH = "data/artifacts/gpu_tuned_baselines.csv"
DEFAULT_FROZEN_AGGREGATED_PATH = "data/artifacts/frozen_baseline_aggregated_{run_id}.json"
DEFAULT_CONDITIONED_METRICS_PATH = "data/artifacts/metrics_conditioned_{run_id}.json"
DEFAULT_OUTPUT_PATH = "data/processed/correlation_inputs_{run_id}.json"

def load_gpu_tuned_baselines(csv_path: str) -> Dict[str, Dict[str, float]]:
    """
    Load GPU-Tuned baselines from the CSV generated by T032a.
    
    Returns a dict: { dataset_id: { task_type: baseline_value } }
    """
    baselines = {}
    path = Path(csv_path)
    
    if not path.exists():
        log_error(f"GPU-Tuned baselines file not found: {csv_path}")
        return baselines

    with open(path, 'r', newline='', encoding='utf-8') as f:
        reader = csv.DictReader(f)
        for row in reader:
            dataset_id = row.get('dataset_id')
            task_type = row.get('task_type')
            baseline_value_str = row.get('baseline_value')
            
            if not dataset_id or not task_type or not baseline_value_str:
                log_warning(f"Skipping incomplete row in baselines CSV: {row}")
                continue
            
            try:
                baseline_value = float(baseline_value_str)
            except ValueError:
                log_warning(f"Invalid baseline value for {dataset_id}/{task_type}: {baseline_value_str}")
                continue

            if dataset_id not in baselines:
                baselines[dataset_id] = {}
            baselines[dataset_id][task_type] = baseline_value

    log_info(f"Loaded {len(baselines)} datasets from GPU-Tuned baselines.")
    return baselines

def load_frozen_aggregated_metrics(json_path: str) -> Dict[str, Dict[str, Any]]:
    """
    Load aggregated frozen baseline metrics from T019c output.
    
    Expected structure:
    {
      "datasets": [
        {
          "dataset_id": "...",
          "task_type": "...",
          "mean_metric": float,
          "std_metric": float,
          ...
        }
      ]
    }
    """
    path = Path(json_path)
    if not path.exists():
        log_error(f"Frozen aggregated metrics file not found: {json_path}")
        return {}

    with open(path, 'r', encoding='utf-8') as f:
        data = json.load(f)

    # Normalize into a lookup dict
    result = {}
    datasets = data.get("datasets", [])
    for item in datasets:
        dataset_id = item.get("dataset_id")
        task_type = item.get("task_type")
        if dataset_id and task_type:
            result[dataset_id] = result.get(dataset_id, {})
            result[dataset_id][task_type] = item

    log_info(f"Loaded frozen aggregated metrics for {len(result)} datasets.")
    return result

def load_conditioned_metrics(json_path: str) -> Dict[str, Dict[str, float]]:
    """
    Load conditioned (CPU-Conditioned) metrics from T028 output.
    
    Expected structure:
    {
      "datasets": [
        {
          "dataset_id": "...",
          "task_type": "...",
          "metric_value": float,
          ...
        }
      ]
    }
    """
    path = Path(json_path)
    if not path.exists():
        log_error(f"Conditioned metrics file not found: {json_path}")
        return {}

    with open(path, 'r', encoding='utf-8') as f:
        data = json.load(f)

    result = {}
    datasets = data.get("datasets", [])
    for item in datasets:
        dataset_id = item.get("dataset_id")
        task_type = item.get("task_type")
        metric_value = item.get("metric_value") # Assuming the key for the main metric
        
        if dataset_id and task_type and metric_value is not None:
            result[dataset_id] = result.get(dataset_id, {})
            result[dataset_id][task_type] = float(metric_value)

    log_info(f"Loaded conditioned metrics for {len(result)} datasets.")
    return result

def calculate_recovery_ratio(
    gpu_baselines: Dict[str, Dict[str, float]],
    frozen_metrics: Dict[str, Dict[str, Any]],
    conditioned_metrics: Dict[str, Dict[str, float]]
) -> List[Dict[str, Any]]:
    """
    Calculate Recovery Ratio for all valid datasets.
    
    Formula: (CPU-Conditioned - Frozen_Aggregated) / (GPU-Tuned - Frozen_Aggregated)
    
    Returns a list of records containing dataset_id, task_type, and calculated values.
    """
    results = []
    processed_count = 0
    skipped_count = 0

    # Iterate over datasets that have all three components
    common_datasets = set(gpu_baselines.keys()) & set(frozen_metrics.keys()) & set(conditioned_metrics.keys())
    
    log_info(f"Found {len(common_datasets)} datasets with complete data for Recovery Ratio calculation.")

    for dataset_id in common_datasets:
        task_types = set(gpu_baselines[dataset_id].keys()) & set(frozen_metrics[dataset_id].keys()) & set(conditioned_metrics[dataset_id].keys())
        
        for task_type in task_types:
            gpu_val = gpu_baselines[dataset_id][task_type]
            frozen_val = frozen_metrics[dataset_id][task_type].get("mean_metric")
            conditioned_val = conditioned_metrics[dataset_id][task_type]

            if frozen_val is None:
                log_warning(f"Missing mean_metric for {dataset_id}/{task_type} in frozen data. Skipping.")
                skipped_count += 1
                continue

            denominator = gpu_val - frozen_val
            numerator = conditioned_val - frozen_val

            # Handle edge case where denominator is zero
            if abs(denominator) < 1e-9:
                log_warning(f"Denominator (GPU - Frozen) is near zero for {dataset_id}/{task_type}. Skipping.")
                skipped_count += 1
                continue

            recovery_ratio = numerator / denominator
            
            results.append({
                "dataset_id": dataset_id,
                "task_type": task_type,
                "cpu_conditioned": conditioned_val,
                "frozen_aggregated": frozen_val,
                "gpu_tuned": gpu_val,
                "recovery_ratio": recovery_ratio,
                "numerator": numerator,
                "denominator": denominator
            })
            processed_count += 1

    log_info(f"Calculated Recovery Ratio for {processed_count} dataset/task combinations. Skipped {skipped_count}.")
    return results

def save_correlation_inputs(results: List[Dict[str, Any]], output_path: str):
    """
    Save the calculated recovery ratios and input metrics to a JSON file
    for downstream tasks (T033, T034, T035).
    """
    path = Path(output_path)
    path.parent.mkdir(parents=True, exist_ok=True)

    output_data = {
        "generated_at": datetime.now().isoformat(),
        "description": "Recovery Ratio calculations for US3 correlation analysis",
        "records": results
    }

    with open(path, 'w', encoding='utf-8') as f:
        json.dump(output_data, f, indent=2)

    log_info(f"Saved correlation inputs to {output_path}")

def main():
    """
    Main entry point for T031.
    """
    parser = argparse.ArgumentParser(description="Calculate Recovery Ratio for US3")
    parser.add_argument("--run_id", type=str, default="baseline_run", help="Run ID for file naming")
    parser.add_argument("--gpu_baselines", type=str, default=DEFAULT_GPU_BASELINES_PATH, help="Path to GPU-Tuned baselines CSV")
    parser.add_argument("--frozen_aggregated", type=str, default=None, help="Path to Frozen Aggregated JSON (auto-derived if None)")
    parser.add_argument("--conditioned_metrics", type=str, default=None, help="Path to Conditioned Metrics JSON (auto-derived if None)")
    parser.add_argument("--output", type=str, default=None, help="Output JSON path (auto-derived if None)")
    
    args = parser.parse_args()

    # Auto-derive paths if not provided
    run_id = args.run_id
    if args.frozen_aggregated is None:
        args.frozen_aggregated = DEFAULT_FROZEN_AGGREGATED_PATH.replace("{run_id}", run_id)
    if args.conditioned_metrics is None:
        args.conditioned_metrics = DEFAULT_CONDITIONED_METRICS_PATH.replace("{run_id}", run_id)
    if args.output is None:
        args.output = DEFAULT_OUTPUT_PATH.replace("{run_id}", run_id)

    log_info(f"Starting T031: Correlation Analysis for run_id={run_id}")
    log_info(f"Inputs: GPU={args.gpu_baselines}, Frozen={args.frozen_aggregated}, Conditioned={args.conditioned_metrics}")

    # 1. Load Data
    gpu_baselines = load_gpu_tuned_baselines(args.gpu_baselines)
    if not gpu_baselines:
        log_error("No GPU baselines loaded. Cannot proceed.")
        sys.exit(1)

    frozen_metrics = load_frozen_aggregated_metrics(args.frozen_aggregated)
    if not frozen_metrics:
        log_error("No frozen metrics loaded. Cannot proceed.")
        sys.exit(1)

    conditioned_metrics = load_conditioned_metrics(args.conditioned_metrics)
    if not conditioned_metrics:
        log_error("No conditioned metrics loaded. Cannot proceed.")
        sys.exit(1)

    # 2. Calculate Recovery Ratio
    results = calculate_recovery_ratio(gpu_baselines, frozen_metrics, conditioned_metrics)

    if not results:
        log_warning("No results generated. Check for data gaps or zero-variance issues.")
        # Still save an empty file to indicate completion of the step
        save_correlation_inputs([], args.output)
        sys.exit(0)

    # 3. Save Output
    save_correlation_inputs(results, args.output)

    log_info("T031 completed successfully.")

if __name__ == "__main__":
    main()