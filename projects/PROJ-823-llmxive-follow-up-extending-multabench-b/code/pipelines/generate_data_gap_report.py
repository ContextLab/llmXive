import os
import sys
import json
import argparse
from pathlib import Path
from datetime import datetime
from typing import Dict, List, Any, Optional

# Add project root to path if not already present
project_root = Path(__file__).resolve().parent.parent
if str(project_root) not in sys.path:
    sys.path.insert(0, str(project_root))

from utils.logging import get_logger, log_info, log_error, log_warning
from config import ensure_directories

logger = get_logger(__name__)

def load_gap_report(gap_report_path: Path) -> Dict[str, Any]:
    """
    Load the Data Availability Gap report generated by validate_baselines.py.
    
    Args:
        gap_report_path: Path to the gap report JSON file
        
    Returns:
        Dictionary containing the gap report data
    """
    if not gap_report_path.exists():
        log_error(f"Gap report not found at {gap_report_path}")
        return {"missing_baselines": [], "summary": {}}
    
    with open(gap_report_path, 'r') as f:
        return json.load(f)

def load_correlation_report(correlation_report_path: Path) -> Dict[str, Any]:
    """
    Load the correlation report generated by generate_correlation_report.py.
    
    Args:
        correlation_report_path: Path to the correlation report JSON file
        
    Returns:
        Dictionary containing the correlation report data
    """
    if not correlation_report_path.exists():
        log_error(f"Correlation report not found at {correlation_report_path}")
        return {"results": [], "summary": {}}
    
    with open(correlation_report_path, 'r') as f:
        return json.load(f)

def merge_gap_into_correlation_report(
    gap_report: Dict[str, Any], 
    correlation_report: Dict[str, Any]
) -> Dict[str, Any]:
    """
    Merge the Data Availability Gap information into the correlation report.
    
    Args:
        gap_report: Data availability gap report
        correlation_report: Existing correlation report
        
    Returns:
        Updated correlation report with gap information
    """
    updated_report = correlation_report.copy()
    
    # Add gap information section
    updated_report["data_availability_gap"] = {
        "missing_baselines": gap_report.get("missing_baselines", []),
        "summary": gap_report.get("summary", {}),
        "affected_datasets": gap_report.get("affected_datasets", [])
    }
    
    # Update summary to include gap statistics
    if "summary" not in updated_report:
        updated_report["summary"] = {}
    
    total_datasets = updated_report["summary"].get("total_datasets_analyzed", 0)
    missing_count = len(gap_report.get("missing_baselines", []))
    
    updated_report["summary"]["total_datasets_with_gaps"] = missing_count
    updated_report["summary"]["coverage_percentage"] = (
        ((total_datasets - missing_count) / total_datasets * 100) 
        if total_datasets > 0 else 0
    )
    
    log_info(f"Gap report merged: {missing_count} datasets missing GPU-Tuned baselines")
    
    return updated_report

def save_enhanced_report(
    report: Dict[str, Any], 
    output_path: Path
) -> None:
    """
    Save the enhanced correlation report with gap information.
    
    Args:
        report: The enhanced report dictionary
        output_path: Path to save the report
    """
    output_path.parent.mkdir(parents=True, exist_ok=True)
    
    with open(output_path, 'w') as f:
        json.dump(report, f, indent=2, default=str)
    
    log_info(f"Enhanced report saved to {output_path}")

def main():
    """
    Main function to generate the enhanced correlation report with Data Availability Gap.
    """
    parser = argparse.ArgumentParser(
        description="Generate correlation report with Data Availability Gap information"
    )
    parser.add_argument(
        "--gap-report",
        type=str,
        required=True,
        help="Path to the gap report JSON file from validate_baselines.py"
    )
    parser.add_argument(
        "--correlation-report",
        type=str,
        required=True,
        help="Path to the correlation report JSON file"
    )
    parser.add_argument(
        "--output",
        type=str,
        required=True,
        help="Path to save the enhanced correlation report"
    )
    
    args = parser.parse_args()
    
    try:
        # Ensure directories exist
        ensure_directories()
        
        gap_report_path = Path(args.gap_report)
        correlation_report_path = Path(args.correlation_report)
        output_path = Path(args.output)
        
        log_info("Loading gap report...")
        gap_report = load_gap_report(gap_report_path)
        
        log_info("Loading correlation report...")
        correlation_report = load_correlation_report(correlation_report_path)
        
        log_info("Merging gap information into correlation report...")
        enhanced_report = merge_gap_into_correlation_report(
            gap_report, 
            correlation_report
        )
        
        log_info("Saving enhanced report...")
        save_enhanced_report(enhanced_report, output_path)
        
        # Print summary
        missing_count = len(gap_report.get("missing_baselines", []))
        total_analyzed = correlation_report.get("summary", {}).get("total_datasets_analyzed", 0)
        
        print(f"\nData Availability Gap Summary:")
        print(f"  - Datasets with missing GPU-Tuned baselines: {missing_count}")
        print(f"  - Total datasets analyzed: {total_analyzed}")
        if total_analyzed > 0:
            coverage = ((total_analyzed - missing_count) / total_analyzed * 100)
            print(f"  - Data coverage: {coverage:.2f}%")
        
        if missing_count > 0:
            print(f"\nMissing datasets:")
            for item in gap_report.get("missing_baselines", [])[:5]:
                dataset_id = item.get("dataset_id", "unknown")
                reason = item.get("reason", "unspecified")
                print(f"  - {dataset_id}: {reason}")
            if missing_count > 5:
                print(f"  ... and {missing_count - 5} more")
        
        log_info("Data Availability Gap reporting completed successfully")
        
    except Exception as e:
        log_error(f"Error generating enhanced report: {str(e)}")
        raise

if __name__ == "__main__":
    main()