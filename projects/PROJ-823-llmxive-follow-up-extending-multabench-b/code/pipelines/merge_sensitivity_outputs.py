"""
Merge sensitivity analysis outputs from multiple seeds into a single intermediate Parquet file.

This script aggregates the Parquet files generated by T019 (run_baseline_sensitivity.py)
for multiple seeds (e.g., 5 seeds) into a single consolidated file.

Input:
    data/processed/embeddings_seed_{seed}_{run_id}.parquet (for each seed in seeds)

Output:
    data/processed/embeddings_sensitivity_merged_{run_id}.parquet
"""

import os
import sys
import json
import hashlib
from datetime import datetime
from pathlib import Path
import pandas as pd
import pyarrow.parquet as pq
from typing import List, Dict, Any, Optional

# Add project root to path to allow imports
PROJECT_ROOT = Path(__file__).resolve().parents[2]
sys.path.insert(0, str(PROJECT_ROOT))

from utils.logging import get_logger, log_info, log_warning, log_error, log_debug
from config import DATA_PROCESSED_DIR, RANDOM_SEEDS, RUN_ID

logger = get_logger(__name__)

