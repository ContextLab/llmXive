"""
T019c: Aggregate sensitivity analysis outputs.

This script computes the mean and standard deviation of embeddings and metrics
from the merged sensitivity output files (generated by T019b) and writes the
aggregated results to a JSON artifact.

Input:
    data/intermediate/sensitivity_merged_{run_id}.parquet (produced by T019b)
Output:
    data/artifacts/frozen_baseline_aggregated_{run_id}.json
"""
import os
import sys
import json
import hashlib
from datetime import datetime
from pathlib import Path
from typing import Dict, Any, List, Optional

import numpy as np
import pandas as pd
import pyarrow.parquet as pq

# Add project root to path to allow imports
PROJECT_ROOT = Path(__file__).resolve().parent.parent.parent
if str(PROJECT_ROOT) not in sys.path:
    sys.path.insert(0, str(PROJECT_ROOT))

from config import get_config, get_output_path
from utils.logging import get_logger, log_info, log_error, log_debug

logger = get_logger(__name__)

def load_merged_sensitivity_data(run_id: str) -> pd.DataFrame:
    """
    Load the merged sensitivity data from the intermediate Parquet file.

    Args:
        run_id: The run identifier used in the file naming.

    Returns:
        A pandas DataFrame containing the merged sensitivity data.
    """
    input_path = get_output_path(
        "data/intermediate",
        f"sensitivity_merged_{run_id}.parquet"
    )

    if not input_path.exists():
        raise FileNotFoundError(
            f"Merged sensitivity file not found at {input_path}. "
            "Ensure T019b has completed successfully."
        )

    logger.info(f"Loading merged sensitivity data from {input_path}")
    df = pd.read_parquet(input_path)

    # Validate expected columns
    required_cols = ["dataset_id", "seed", "embedding_mean", "embedding_std", "metric_value"]
    missing_cols = [col for col in required_cols if col not in df.columns]
    if missing_cols:
        raise ValueError(f"Merged file missing required columns: {missing_cols}")

    logger.info(f"Loaded {len(df)} records from {input_path}")
    return df

def compute_aggregates(df: pd.DataFrame, run_id: str) -> Dict[str, Any]:
    """
    Compute mean and std of embeddings and metrics across seeds for each dataset.

    Args:
        df: The merged sensitivity DataFrame.
        run_id: The run identifier.

    Returns:
        A dictionary containing the aggregated results.
    """
    logger.info("Computing aggregates across seeds...")

    # Group by dataset_id
    grouped = df.groupby("dataset_id")

    aggregates = []

    for dataset_id, group in grouped:
        # Compute mean and std for embedding_mean (aggregating the mean embeddings)
        # Note: embedding_mean is a list/array in the parquet. We need to handle this carefully.
        # Assuming the parquet stores embedding_mean as a list of floats.
        
        embedding_means = group["embedding_mean"].tolist()
        embedding_stds = group["embedding_std"].tolist()
        metric_values = group["metric_value"].tolist()

        # Aggregate metric values
        metric_mean = float(np.mean(metric_values))
        metric_std = float(np.std(metric_values))

        # Aggregate embeddings
        # If embedding_means is a list of lists (each seed's mean embedding vector)
        if embedding_means and isinstance(embedding_means[0], (list, np.ndarray)):
            # Stack them to compute mean/std across seeds for each dimension
            try:
                stacked_means = np.stack([np.array(em) for em in embedding_means])
                stacked_stds = np.stack([np.array(es) for es in embedding_stds])
                
                aggregated_embedding_mean = np.mean(stacked_means, axis=0).tolist()
                aggregated_embedding_std = np.std(stacked_stds, axis=0).tolist()
            except Exception as e:
                logger.error(f"Error aggregating embeddings for {dataset_id}: {e}")
                # Fallback: store as list of lists if aggregation fails
                aggregated_embedding_mean = embedding_means
                aggregated_embedding_std = embedding_stds
        else:
            # Scalar embeddings or fallback
            aggregated_embedding_mean = float(np.mean([float(em) for em in embedding_means])) if embedding_means else None
            aggregated_embedding_std = float(np.std([float(es) for es in embedding_stds])) if embedding_stds else None

        aggregates.append({
            "dataset_id": dataset_id,
            "run_id": run_id,
            "num_seeds": len(group),
            "metric_aggregates": {
                "mean": metric_mean,
                "std": metric_std
            },
            "embedding_aggregates": {
                "mean": aggregated_embedding_mean,
                "std": aggregated_embedding_std
            }
        })

    logger.info(f"Computed aggregates for {len(aggregates)} datasets")
    return {
        "run_id": run_id,
        "generated_at": datetime.now().isoformat(),
        "dataset_count": len(aggregates),
        "results": aggregates
    }

def save_aggregated_results(aggregates: Dict[str, Any], run_id: str) -> Path:
    """
    Save the aggregated results to a JSON file.

    Args:
        aggregates: The computed aggregates dictionary.
        run_id: The run identifier.

    Returns:
        Path to the saved JSON file.
    """
    output_dir = get_output_path("data/artifacts")
    output_dir.mkdir(parents=True, exist_ok=True)

    output_path = output_dir / f"frozen_baseline_aggregated_{run_id}.json"

    logger.info(f"Saving aggregated results to {output_path}")
    with open(output_path, "w", encoding="utf-8") as f:
        json.dump(aggregates, f, indent=2, default=str)

    logger.info(f"Successfully saved aggregated results to {output_path}")
    return output_path

def main():
    """Main entry point for the sensitivity aggregation pipeline."""
    config = get_config()
    run_id = config.get("run_id", "default_run")

    log_info("Starting sensitivity analysis aggregation (T019c)")
    
    try:
        # 1. Load merged data
        df = load_merged_sensitivity_data(run_id)

        # 2. Compute aggregates
        aggregates = compute_aggregates(df, run_id)

        # 3. Save results
        output_path = save_aggregated_results(aggregates, run_id)

        log_info(f"Aggregation complete. Output: {output_path}")
        return 0

    except Exception as e:
        log_error(f"Aggregation failed: {str(e)}")
        raise

if __name__ == "__main__":
    sys.exit(main())