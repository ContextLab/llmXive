"""
T024: Compute and store out-of-sample metrics (R², RMSE) and feature importance maps.

This module reads the cleaned data and the Random Forest model results (generated by T022b)
to compute final out-of-sample performance metrics and extract feature importance maps.

Outputs:
    data/results/metrics_summary.json: Contains R², RMSE, and feature importance.
"""
import os
import json
import pandas as pd
import numpy as np
from pathlib import Path
from typing import Dict, Any, List

# Import from existing API surface
from utils.logging_config import get_logger
from utils.config import ConfigManager

# Ensure the directory structure exists
DATA_PROCESSED_DIR = Path("data/processed")
DATA_RESULTS_DIR = Path("data/results")

# Ensure output directory exists
DATA_RESULTS_DIR.mkdir(parents=True, exist_ok=True)

logger = get_logger(__name__)
config = ConfigManager()

def compute_out_of_sample_metrics(
    y_true: pd.Series, 
    y_pred: pd.Series
) -> Dict[str, float]:
    """
    Compute R² and RMSE for out-of-sample predictions.
    
    Args:
        y_true: Ground truth values.
        y_pred: Predicted values.
        
    Returns:
        Dictionary with 'r2' and 'rmse' keys.
    """
    # R² calculation
    ss_res = np.sum((y_true - y_pred) ** 2)
    ss_tot = np.sum((y_true - np.mean(y_true)) ** 2)
    r2 = 1 - (ss_res / ss_tot) if ss_tot != 0 else 0.0
    
    # RMSE calculation
    rmse = np.sqrt(np.mean((y_true - y_pred) ** 2))
    
    return {
        "r2": float(r2),
        "rmse": float(rmse)
    }

def extract_feature_importance(
    feature_names: List[str], 
    importances: np.ndarray
) -> Dict[str, float]:
    """
    Map feature names to their importance scores.
    
    Args:
        feature_names: List of feature names.
        importances: Array of importance scores.
        
    Returns:
        Dictionary mapping feature names to importance scores.
    """
    return {
        name: float(imp) 
        for name, imp in zip(feature_names, importances)
    }

def run_metric_computation():
    """
    Main execution logic for T024.
    
    1. Loads the cleaned data.
    2. Loads the Random Forest model predictions and feature importances 
       (simulated here as if they were saved by T022b, or we re-run the fit 
       if the artifact isn't explicitly saved yet, but per task flow T022b 
       should have produced the model or we rely on the config to re-fit).
       
    NOTE: Since T022b is marked complete in the prompt context, we assume 
    the necessary model artifacts or the ability to re-run the fit exists.
    However, to be robust and self-contained for this specific task, 
    we will re-fit the Random Forest model to ensure we have the 
    'out-of-sample' metrics (via OOF or CV) and importances.
    
    This aligns with FR-004 (Random Forest for feature importance) 
    and SC-002 (Out-of-sample metrics).
    """
    logger.info("Starting T024: Compute Out-of-Sample Metrics and Feature Importance")
    
    # Load cleaned data
    cleaned_data_path = DATA_PROCESSED_DIR / "cleaned_data.csv"
    if not cleaned_data_path.exists():
        logger.error(f"Cleaned data not found at {cleaned_data_path}. Abort.")
        raise FileNotFoundError(f"Missing required input: {cleaned_data_path}")
    
    df = pd.read_csv(cleaned_data_path)
    
    # Define features and target based on schema
    # Schema from T004: participant_id, latency, smoothness, lead_time, agency_score
    target_col = "agency_score"
    feature_cols = [col for col in ["latency", "smoothness", "lead_time"] if col in df.columns]
    
    if target_col not in df.columns:
        logger.error(f"Target column '{target_col}' not found in data.")
        raise ValueError(f"Missing target column: {target_col}")
        
    if not feature_cols:
        logger.error("No feature columns found.")
        raise ValueError("No feature columns found in data.")
        
    X = df[feature_cols]
    y = df[target_col]
    
    # Handle missing values if any (though T017 should have cleaned)
    X = X.dropna()
    y = y.loc[X.index]
    
    if len(X) < 5:
        logger.error("Insufficient data for modeling after cleaning.")
        raise ValueError("Insufficient data for modeling.")
    
    logger.info(f"Modeling with {len(X)} samples and features: {feature_cols}")
    
    # Re-fit Random Forest to ensure fresh OOF metrics (or load if available)
    # We use sklearn's RandomForestRegressor with cross-validation to get OOF predictions
    from sklearn.ensemble import RandomForestRegressor
    from sklearn.model_selection import cross_val_predict, KFold
    from sklearn.metrics import r2_score, mean_squared_error
    
    rf_model = RandomForestRegressor(
        n_estimators=100, 
        max_depth=3, # Constraint from T016 logic for small N
        random_state=42,
        n_jobs=-1
    )
    
    # Use K-Fold to get Out-of-Fold predictions
    kfold = KFold(n_splits=5, shuffle=True, random_state=42)
    y_pred_oof = cross_val_predict(rf_model, X, y, cv=kfold)
    
    # Fit the final model on full data to extract importances
    rf_model.fit(X, y)
    
    # Compute metrics
    metrics = compute_out_of_sample_metrics(y, y_pred_oof)
    
    # Extract feature importance
    importance_map = extract_feature_importance(feature_cols, rf_model.feature_importances_)
    
    # Prepare output structure
    output_data = {
        "model_type": "RandomForest",
        "n_samples": len(X),
        "features_used": feature_cols,
        "out_of_sample_metrics": {
            "r2": metrics["r2"],
            "rmse": metrics["rmse"]
        },
        "feature_importance": importance_map,
        "metadata": {
            "source": "T024_compute_metrics",
            "timestamp": pd.Timestamp.now().isoformat(),
            "note": "Metrics computed via 5-fold cross-validation out-of-fold predictions."
        }
    }
    
    # Write output
    output_path = DATA_RESULTS_DIR / "metrics_summary.json"
    with open(output_path, "w") as f:
        json.dump(output_data, f, indent=2)
    
    logger.info(f"Successfully wrote metrics to {output_path}")
    logger.info(f"R²: {metrics['r2']:.4f}, RMSE: {metrics['rmse']:.4f}")
    
    return output_data

if __name__ == "__main__":
    run_metric_computation()