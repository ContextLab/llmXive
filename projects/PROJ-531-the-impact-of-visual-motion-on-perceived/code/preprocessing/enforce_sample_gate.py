import json
import sys
from pathlib import Path

def enforce_sample_gate(config_path: str = "data/processed/modeling_config.json") -> None:
    """
    Enforce the N >= 80 sample size gate.

    Reads the modeling config generated by T016.
    If 'abort_flag' is True, raises SystemExit(1) with the specific error message.
    Otherwise, logs success and exits cleanly.
    """
    config_file = Path(config_path)
    
    if not config_file.exists():
        print(f"Error: Configuration file not found at {config_path}", file=sys.stderr)
        sys.exit(1)

    try:
        with open(config_file, 'r') as f:
            config = json.load(f)
    except json.JSONDecodeError as e:
        print(f"Error: Invalid JSON in configuration file: {e}", file=sys.stderr)
        sys.exit(1)

    abort_flag = config.get("abort_flag", False)
    n_samples = config.get("n_samples", 0)

    if abort_flag:
        error_msg = f"Analysis aborted: Insufficient sample size (N < 80)"
        print(error_msg, file=sys.stderr)
        sys.exit(1)
    
    print(f"Gate passed: Sample size N={n_samples} meets requirement (N >= 80).")

def main() -> None:
    enforce_sample_gate()

if __name__ == "__main__":
    main()