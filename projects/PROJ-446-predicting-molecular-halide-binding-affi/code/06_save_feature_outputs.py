"""
T027: Save analysis outputs to data/processed/feature_analysis.json and docs/paper/figures/.

This script orchestrates the final saving of feature analysis results.
It assumes T023 (stability), T024 (PDPs), T025 (plausibility), and T026 (summary)
have already executed and produced their intermediate artifacts.

It performs:
1. Aggregation of stability, plausibility, and summary data into a single JSON.
2. Ensuring figure files (generated by T024) are persisted in the correct directory.
3. Writing the consolidated JSON to data/processed/feature_analysis.json.
"""

import os
import json
import logging
import sys
from pathlib import Path
from typing import Dict, Any, Optional

# Add project root to path if running as script
if __name__ == "__main__" and __package__ is None:
    project_root = Path(__file__).resolve().parent.parent
    if str(project_root) not in sys.path:
        sys.path.insert(0, str(project_root))

from code.utils.logger import get_logger
from code.utils.config import get_path, get_data_path
from code.utils.validators import load_schema, validate_dataset

logger = get_logger(__name__)

# Output paths
FEATURE_ANALYSIS_JSON_PATH = "data/processed/feature_analysis.json"
FIGURES_DIR = "docs/paper/figures"

# Input artifact paths (generated by previous tasks)
STABILITY_RESULTS_PATH = "data/processed/feature_stability_results.json"
PLUASIBILITY_RESULTS_PATH = "data/processed/physical_plausibility_results.json"
SUMMARY_TABLE_PATH = "data/processed/feature_summary_table.csv"
PDP_PLOTS_DIR = "data/processed/partial_dependence_plots" # Where T024 likely saves raw plots before moving

def load_json_safe(path: str) -> Optional[Dict[str, Any]]:
    """Load JSON file safely, returning None if missing."""
    full_path = Path(path)
    if not full_path.exists():
        logger.warning(f"Input file not found: {full_path}")
        return None
    try:
        with open(full_path, 'r', encoding='utf-8') as f:
            return json.load(f)
    except json.JSONDecodeError as e:
        logger.error(f"Failed to parse JSON {full_path}: {e}")
        return None

def load_csv_safe(path: str):
    """Load CSV file safely, returning None if missing."""
    import pandas as pd
    full_path = Path(path)
    if not full_path.exists():
        logger.warning(f"Input file not found: {full_path}")
        return None
    try:
        return pd.read_csv(full_path)
    except Exception as e:
        logger.error(f"Failed to parse CSV {full_path}: {e}")
        return None

def ensure_figures_directory():
    """Ensure the target figures directory exists."""
    target_dir = get_path(FIGURES_DIR)
    target_dir.mkdir(parents=True, exist_ok=True)
    logger.info(f"Ensured figures directory exists at {target_dir}")
    return target_dir

def move_figures(source_dir_name: str = "data/processed/partial_dependence_plots"):
    """Move generated PDP plots to the final figures directory."""
    source_dir = get_path(source_dir_name)
    target_dir = get_path(FIGURES_DIR)
    
    if not source_dir.exists():
        logger.warning(f"Source figures directory not found: {source_dir}. Skipping move.")
        return

    import shutil
    moved_count = 0
    for file_path in source_dir.glob("*.png"):
        dest_path = target_dir / file_path.name
        shutil.copy2(str(file_path), str(dest_path))
        moved_count += 1
        logger.info(f"Copied figure: {file_path.name} -> {dest_path}")
    
    logger.info(f"Moved {moved_count} figures to {target_dir}")

def aggregate_results(stability: Optional[Dict], plausibility: Optional[Dict], summary_df: Any) -> Dict[str, Any]:
    """Aggregate all analysis results into a single dictionary."""
    result = {
        "task_id": "T027",
        "description": "Aggregated Feature Analysis Outputs",
        "status": "complete",
        "components": {}
    }

    if stability:
        result["components"]["feature_stability"] = stability
    else:
        result["components"]["feature_stability"] = {"status": "missing_input"}

    if plausibility:
        result["components"]["physical_plausibility"] = plausibility
    else:
        result["components"]["physical_plausibility"] = {"status": "missing_input"}

    if summary_df is not None:
        # Convert DataFrame to serializable format
        result["components"]["feature_summary_table"] = summary_df.to_dict(orient="records")
    else:
        result["components"]["feature_summary_table"] = {"status": "missing_input"}

    result["metadata"] = {
        "aggregated_at": "runtime", # Placeholder for actual timestamp if needed
        "project": "PROJ-446-predicting-molecular-halide-binding-affi"
    }

    return result

def save_final_outputs(aggregate_data: Dict[str, Any]):
    """Save the aggregated JSON and ensure figures are in place."""
    output_json_path = get_path(FEATURE_ANALYSIS_JSON_PATH)
    
    # Save JSON
    output_json_path.parent.mkdir(parents=True, exist_ok=True)
    with open(output_json_path, 'w', encoding='utf-8') as f:
        json.dump(aggregate_data, f, indent=2)
    
    logger.info(f"Saved feature analysis JSON to {output_json_path}")

    # Ensure figures are moved
    ensure_figures_directory()
    move_figures()

def main():
    """Main entry point for T027."""
    logger.info("Starting T027: Saving analysis outputs.")
    
    # 1. Load intermediate results
    stability_data = load_json_safe(STABILITY_RESULTS_PATH)
    plausibility_data = load_json_safe(PLUASIBILITY_RESULTS_PATH)
    summary_data = load_csv_safe(SUMMARY_TABLE_PATH)

    # 2. Aggregate
    final_report = aggregate_results(stability_data, plausibility_data, summary_data)

    # 3. Save outputs
    save_final_outputs(final_report)

    logger.info("T027 completed successfully.")
    return 0

if __name__ == "__main__":
    sys.exit(main())