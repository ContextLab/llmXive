def generate_report_header(sim_mode: bool) -> str:
    """Generate the header section of the report."""
    header = "# Molecular Halide Binding Affinity Prediction: Final Report\n\n"
    header += "## Project Overview\n\n"
    header += "This report summarizes the results of the machine learning pipeline designed to "
    header += "predict molecular halide binding affinities and analyze the relative binding strengths "
    header += "of fluoride, chloride, bromide, and iodide ions to organic hosts.\n\n"
    
    if sim_mode:
        header += "⚠️ **WARNING: SIMULATED DATA MODE ACTIVE**\n\n"
        header += "The comparative analysis described in this report is **unanswerable** because "
        header += "the pipeline fell back to simulated data due to insufficient real-world data "
        header += "(fewer than 50 hosts found). The results presented below are derived from "
        header += "simulated data and **do not** reflect experimental reality. The primary "
        header += "research question regarding comparative halide binding trends **cannot be "
        header += "answered** with this dataset.\n\n"
    else:
        header += "The analysis was performed on experimentally derived data from NIST/PubChem sources.\n\n"
    
    return header

def generate_methods_section() -> str:
    """Generate the methods section of the report."""
    methods = "## Methods\n\n"
    methods += "### Data Ingestion and Preprocessing\n\n"
    methods += "Experimental binding data was scraped from NIST and PubChem sources using "
    methods += "automated scripts with rate limiting and exponential backoff. Data was filtered "
    methods += "for specific solvents (acetonitrile, chloroform, DCM) and standardized to "
    methods += "log K units. Host molecules were required to have measurements for at least "
    methods += "three different halides to be included in the comparative analysis.\n\n"
    
    methods += "### Feature Engineering\n\n"
    methods += "Molecular descriptors were generated using RDKit, including:\n"
    methods += "- ECFP fingerprints for structural representation\n"
    methods += "- Charge density calculations\n"
    methods += "- Cavity volume estimations\n"
    methods += "- Hydrogen-bond donor/acceptor counts\n\n"
    
    methods += "### Model Training\n\n"
    methods += "Two machine learning models were trained using host-identity stratified cross-validation "
    methods += "to prevent data leakage:\n"
    methods += "1. **Random Forest**: Default scikit-learn hyperparameters\n"
    methods += "2. **Gradient Boosting**: Default scikit-learn hyperparameters\n\n"
    
    methods += "### Statistical Analysis\n\n"
    methods += "Pairwise comparisons between halide groups were performed using bootstrap confidence "
    methods += "intervals (10,000 resamples). Power analysis was conducted to verify sufficient sample "
    methods += "size (N ≥ 10 per group).\n\n"
    
    return methods

def generate_results_section(stats_summary: Dict, feature_summary: Dict, model_artifacts: Dict) -> str:
    """Generate the results section of the report."""
    results = "## Results\n\n"
    
    if not stats_summary:
        results += "### Statistical Analysis\n\n"
        results += "No statistical summary data was available for inclusion in this report.\n\n"
    else:
        results += "### Statistical Analysis\n\n"
        if stats_summary.get("underpowered"):
            results += "⚠️ **Underpowered Analysis**: The dataset did not meet the minimum sample size "
            results += "requirement (N ≥ 10 per halide group) for some or all halide groups. "
            results += "Confidence intervals are reported as **wide** and should be interpreted with caution.\n\n"
        
        results += "#### Halide Pairwise Comparisons\n\n"
        results += "| Comparison | Mean Difference | 95% CI Lower | 95% CI Upper | Significant? |\n"
        results += "|------------|-----------------|--------------|--------------|---------------|\n"
        
        comparisons = stats_summary.get("comparisons", [])
        for comp in comparisons:
            sig = "Yes" if comp.get("significant", False) else "No"
            ci_lower = comp.get("ci_lower", "wide")
            ci_upper = comp.get("ci_upper", "wide")
            results += f"| {comp['pair']} | {comp['mean_diff']:.3f} | {ci_lower} | {ci_upper} | {sig} |\n"
        results += "\n"
    
    if not model_artifacts:
        results += "### Model Performance\n\n"
        results += "No model artifacts were available for inclusion in this report.\n\n"
    else:
        results += "### Model Performance\n\n"
        results += "| Model Type | Mean R² | Mean RMSE | Best Fold R² |\n"
        results += "|------------|---------|-----------|---------------|\n"
        
        runs = model_artifacts.get("runs", [])
        for run in runs:
            model_type = run.get("model_type", "Unknown")
            mean_r2 = run.get("metrics", {}).get("mean_r2", "N/A")
            mean_rmse = run.get("metrics", {}).get("mean_rmse", "N/A")
            best_r2 = run.get("metrics", {}).get("best_fold_r2", "N/A")
            results += f"| {model_type} | {mean_r2} | {mean_rmse} | {best_r2} |\n"
        results += "\n"
    
    if not feature_summary:
        results += "### Feature Importance\n\n"
        results += "No feature analysis results were available for inclusion in this report.\n\n"
    else:
        results += "### Feature Importance and Physical Plausibility\n\n"
        stable_features = feature_summary.get("stable_features", [])
        if stable_features:
            results += "#### Top Stable Features (CV < 0.3)\n\n"
            results += "| Rank | Feature | Stability Score (CV) | Physical Plausibility |\n"
            results += "|------|---------|----------------------|-----------------------|\n"
            for i, feat in enumerate(stable_features, 1):
                plausibility = "Plausible" if feat.get("physically_plausible", True) else "Implausible"
                results += f"| {i} | {feat['feature_name']} | {feat['cv_score']:.3f} | {plausibility} |\n"
            results += "\n"
        else:
            results += "No features met the stability threshold (CV < 0.3).\n\n"
    
    return results

def generate_discussion_section(sim_mode: bool) -> str:
    """Generate the discussion section of the report."""
    discussion = "## Discussion\n\n"
    
    discussion += "### Limitations\n\n"
    if sim_mode:
        discussion += "1. **Simulated Data**: This analysis was performed on simulated data due to "
        discussion += "insufficient real-world experimental data. The results **do not reflect "
        discussion += "actual molecular binding behavior** and should not be used for scientific "
        discussion += "conclusions.\n\n"
        discussion += "2. **No Comparative Validity**: The primary research question regarding "
        discussion += "comparative halide binding trends cannot be answered with this dataset.\n\n"
    else:
        discussion += "1. **Data Sources**: The analysis is limited to data available from NIST and "
        discussion += "PubChem, which may not represent the full diversity of halide-hosting systems.\n\n"
        discussion += "2. **Solvent Constraints**: Only three solvents were analyzed, limiting "
        discussion += "generalizability to other solvent environments.\n\n"
    
    discussion += "### Associational Nature of Findings\n\n"
    discussion += "⚠️ **Important Disclaimer**: All findings presented in this report are **associational, "
    discussion += "not causal**. The machine learning models identify statistical correlations between "
    discussion += "molecular descriptors and binding affinities but do not establish causal mechanisms.\n\n"
    discussion += "### Verification Accuracy Note\n\n"
    discussion += "This study does not utilize validated questionnaires or psychometric instruments. "
    discussion += "The 'Verified Accuracy' gate for measurement validity, as defined in standard "
    discussion += "clinical or social science research, does not apply to this computational chemistry "
    discussion += "project. Binding affinities were derived from experimental chemical data or "
    discussion += "physics-based simulations, not human-reported measures.\n\n"
    
    return discussion

def generate_report_content(stats_summary: Dict, feature_summary: Dict, model_artifacts: Dict) -> str:
    """Generate the complete report content."""
    sim_mode = is_simulated_mode()
    
    content = generate_report_header(sim_mode)
    content += generate_methods_section()
    content += generate_results_section(stats_summary, feature_summary, model_artifacts)
    content += generate_discussion_section(sim_mode)
    
    return content

def save_report(content: str, output_path: Path) -> None:
    """Save the report to the specified path."""
    output_path.parent.mkdir(parents=True, exist_ok=True)
    with open(output_path, 'w') as f:
        f.write(content)
    logger.info(f"Report saved to {output_path}")

def main():
    """Main entry point for generating the final report."""
    logger.info("Starting final report generation (T032)")
    
    # Load all required artifacts
    stats_summary = load_statistical_summary()
    feature_summary = load_feature_summary()
    model_artifacts = load_model_artifacts()
    
    # Generate report content
    report_content = generate_report_content(stats_summary, feature_summary, model_artifacts)
    
    # Save report
    report_path = get_path() / "docs" / "paper" / "report.md"
    save_report(report_content, report_path)
    
    # Also ensure statistical_summary.json exists (it should be created by T028-T031)
    # T032 is responsible for saving the summary stats as well, but they should already exist
    # from T028-T031. We just need to make sure the report.md is generated.
    
    logger.info("Final report generation complete")
    return 0

if __name__ == "__main__":
    sys.exit(main())
