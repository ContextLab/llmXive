def generate_methods_section() -> str:
    """Generate the methods section of the report."""
    return """## Methods

### Data Ingestion and Preprocessing

- **Source:** Experimental binding data retrieved from NIST and PubChem databases
- **Solvents Filtered:** Acetonitrile, Chloroform, Dichloromethane (DCM)
- **Quality Control:** SMILES/InChI validation, unit standardization (log K vs ΔG)
- **Filtering:** Hosts retained only if ≥3 different halide measurements (F⁻, Cl⁻, Br⁻, I⁻) available

### Feature Engineering

- **Molecular Descriptors:** ECFP fingerprints and RDKit-calculated properties
- **Key Features:** Charge density, cavity volume, hydrogen-bond donor count, steric parameters
- **Standardization:** All continuous features scaled to zero mean and unit variance

### Model Training

- **Algorithms:** Random Forest (RF) and Gradient Boosting (GB)
- **Validation Strategy:** Host-identity stratified k-fold cross-validation (prevents data leakage)
- **Hardware Constraints:** CPU-only execution, ≤7GB RAM, ≤6h runtime
- **Hyperparameters:** Default scikit-learn settings (no tuning performed)

### Statistical Analysis

- **Confidence Intervals:** Bootstrap resampling (95% CI)
- **Power Analysis:** Minimum N ≥ 10 per halide group required
- **Comparative Analysis:** Pairwise differences in model performance across halide groups

"""

def generate_results_section(summary: Optional[Dict], models: Optional[Dict], features: Optional[Dict]) -> str:
    """Generate the results section based on loaded data."""
    section = "## Results\n\n"

    if summary is None:
        section += "⚠️ **No statistical summary available.** The statistical analysis pipeline (T028-T031) may not have completed successfully.\n\n"
        return section

    # Power Analysis Results
    if 'power_analysis' in summary:
        pa = summary['power_analysis']
        section += "### Statistical Power\n\n"
        if pa.get('is_powered', False):
            section += f"- **Status:** Adequately powered (N ≥ 10 per halide group)\n"
            for halide, count in pa.get('halide_counts', {}).items():
                section += f"  - {halide}: {count} samples\n"
        else:
            section += f"- **Status:** ⚠️ **Underpowered**\n"
            section += f"- **Issue:** One or more halide groups have fewer than 10 samples.\n"
            section += f"- **Impact:** Confidence intervals may be **wide** and statistical comparisons unreliable.\n"
            for halide, count in pa.get('halide_counts', {}).items():
                section += f"  - {halide}: {count} samples\n"
        section += "\n"

    # Model Performance
    if models:
        section += "### Model Performance\n\n"
        for model_name, model_data in models.items():
            section += f"#### {model_name}\n\n"
            if 'metrics' in model_data:
                metrics = model_data['metrics']
                avg_r2 = metrics.get('avg_r2', 'N/A')
                avg_rmse = metrics.get('avg_rmse', 'N/A')
                section += f"- **Average R²:** {avg_r2}\n"
                section += f"- **Average RMSE:** {avg_rmse}\n"
            
            if 'fold_metrics' in model_data:
                section += "- **Cross-Validation Folds:**\n"
                for fold_idx, fold_metrics in enumerate(model_data['fold_metrics'], 1):
                    r2 = fold_metrics.get('r2', 'N/A')
                    rmse = fold_metrics.get('rmse', 'N/A')
                    section += f"  - Fold {fold_idx}: R²={r2}, RMSE={rmse}\n"
            section += "\n"

    # Statistical Comparisons
    if 'comparisons' in summary:
        section += "### Pairwise Halide Comparisons\n\n"
        comparisons = summary['comparisons']
        section += "The following table shows the difference in model performance between halide pairs, "
        section += "with 95% confidence intervals derived from bootstrap resampling.\n\n"
        section += "| Comparison | Mean Difference | 95% CI Lower | 95% CI Upper | Significant (|Δ| ≥ 0.1) |\n"
        section += "|------------|-----------------|--------------|--------------|-------------------------|\n"
        
        for comp in comparisons:
            pair = f"{comp.get('halide_a', 'N/A')} vs {comp.get('halide_b', 'N/A')}"
            diff = comp.get('mean_diff', 'N/A')
            ci_lower = comp.get('ci_lower', 'N/A')
            ci_upper = comp.get('ci_upper', 'N/A')
            significant = "Yes" if comp.get('significant', False) else "No"
            section += f"| {pair} | {diff} | {ci_lower} | {ci_upper} | {significant} |\n"
        section += "\n"

    # Feature Importance
    if features:
        section += "### Feature Importance and Stability\n\n"
        if 'top_features' in features:
            section += "The following features were identified as the most stable predictors (CV < 0.3):\n\n"
            for i, feat in enumerate(features['top_features'][:5], 1):
                name = feat.get('name', 'Unknown')
                stability = feat.get('stability_score', 'N/A')
                cv = feat.get('cv', 'N/A')
                section += f"{i}. **{name}** (Stability: {stability}, CV: {cv})\n"
            section += "\n"
        
        if 'physical_plausibility' in features:
            pp = features['physical_plausibility']
            section += "### Physical Plausibility Check\n\n"
            if pp.get('is_plausible', True):
                section += f"✓ **Result:** The top feature's coefficient sign aligns with first-principles electrostatics.\n"
            else:
                section += f"⚠ **Result:** The top feature's coefficient sign **contradicts** expected physical principles.\n"
            section += f"- **Top Feature:** {pp.get('top_feature', 'N/A')}\n"
            section += f"- **Expected Sign:** {pp.get('expected_sign', 'N/A')}\n"
            section += f"- **Observed Sign:** {pp.get('observed_sign', 'N/A')}\n"
            section += "\n"

    return section

