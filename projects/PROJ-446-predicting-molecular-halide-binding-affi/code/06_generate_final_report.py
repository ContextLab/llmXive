def generate_report_content(summary: Optional[Dict], models: Optional[Dict], features: Optional[Dict]) -> str:
    """Assemble the complete report."""
    report = ""
    report += generate_report_header()
    report += generate_methods_section()
    report += generate_results_section(summary, models, features)
    report += generate_discussion_section(summary)
    return report

def save_report(report_content: str, output_path: Path):
    """Write the report to disk."""
    output_path.parent.mkdir(parents=True, exist_ok=True)
    with open(output_path, 'w', encoding='utf-8') as f:
        f.write(report_content)
    logger.info(f"Final report saved to {output_path}")

