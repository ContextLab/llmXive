"""
Contract test for analysis_results.schema.yaml.

Validates that any generated analysis results JSON file conforms to the
schema defined in contracts/analysis_results.schema.yaml.

This test ensures:
1. The schema file exists and is valid YAML.
2. Example output files (if generated by the pipeline) match the schema structure.
3. Required fields are present and have the correct types.
"""

import json
import os
import pytest
import yaml
from pathlib import Path
from typing import Any, Dict, List, Optional

# Project root is assumed to be the parent of 'tests'
PROJECT_ROOT = Path(__file__).resolve().parents[2]
SCHEMA_PATH = PROJECT_ROOT / "contracts" / "analysis_results.schema.yaml"
RESULTS_DIR = PROJECT_ROOT / "data" / "results"


def load_schema() -> Dict[str, Any]:
    """Load the analysis results schema from the contracts directory."""
    if not SCHEMA_PATH.exists():
        pytest.fail(f"Schema file not found: {SCHEMA_PATH}")
    
    with open(SCHEMA_PATH, "r", encoding="utf-8") as f:
        schema = yaml.safe_load(f)
    
    if not isinstance(schema, dict):
        pytest.fail("Schema must be a YAML dictionary/object")
    
    return schema


def validate_type(value: Any, expected_type: str, field_path: str) -> None:
    """Validate that a value matches the expected JSON Schema type."""
    type_map = {
        "string": str,
        "integer": int,
        "number": (int, float),
        "boolean": bool,
        "array": list,
        "object": dict,
        "null": type(None)
    }

    if expected_type not in type_map:
        # Allow unknown types to pass or fail based on strictness; here we warn
        return

    expected_python_types = type_map[expected_type]
    if not isinstance(value, expected_python_types):
        pytest.fail(
            f"Field '{field_path}' has invalid type: "
            f"expected {expected_type}, got {type(value).__name__}"
        )


def validate_object(
    data: Dict[str, Any], 
    schema: Dict[str, Any], 
    path: str = "root"
) -> None:
    """Recursively validate a dictionary against a schema definition."""
    if "properties" in schema:
        for prop_name, prop_schema in schema["properties"].items():
            field_path = f"{path}.{prop_name}" if path else prop_name
            
            if "required" in schema and prop_name in schema["required"]:
                if prop_name not in data:
                    pytest.fail(f"Missing required field: {field_path}")
            
            if prop_name in data:
                value = data[prop_name]
                prop_type = prop_schema.get("type")
                
                if prop_type:
                    validate_type(value, prop_type, field_path)
                
                if prop_type == "object" and isinstance(value, dict):
                    validate_object(value, prop_schema, field_path)
                elif prop_type == "array" and isinstance(value, list):
                    validate_array(value, prop_schema, field_path)


def validate_array(
    data: List[Any], 
    schema: Dict[str, Any], 
    path: str
) -> None:
    """Validate an array against schema constraints."""
    if "items" in schema:
        item_schema = schema["items"]
        item_type = item_schema.get("type")
        
        for idx, item in enumerate(data):
            item_path = f"{path}[{idx}]"
            if item_type:
                validate_type(item, item_type, item_path)
            
            if item_type == "object" and isinstance(item, dict):
                validate_object(item, item_schema, item_path)


def test_schema_file_exists_and_valid():
    """Test that the schema file exists and is valid YAML."""
    schema = load_schema()
    
    assert "type" in schema, "Schema must define a root type"
    assert schema["type"] == "object", "Root schema type must be 'object'"
    assert "properties" in schema, "Root schema must define properties"


def test_schema_structure():
    """Test that the schema defines the expected top-level fields."""
    schema = load_schema()
    required_fields = schema.get("required", [])
    properties = schema.get("properties", {})
    
    expected_fields = [
        "threshold_reduction_pct",
        "confidence_interval",
        "statistical_significance",
        "tradeoff_curve_points"
    ]
    
    for field in expected_fields:
        assert field in properties, f"Schema missing expected field: {field}"
        assert field in required_fields, f"Schema missing required field: {field}"


def test_analysis_results_json_contracts():
    """
    Contract test: Verify that any existing analysis results JSON files
    conform to the schema.
    """
    if not RESULTS_DIR.exists():
        pytest.skip("Results directory does not exist yet; no files to validate.")
    
    json_files = list(RESULTS_DIR.glob("*.json"))
    
    if not json_files:
        pytest.skip("No JSON files found in results directory.")
    
    schema = load_schema()
    
    for json_file in json_files:
        try:
            with open(json_file, "r", encoding="utf-8") as f:
                data = json.load(f)
            
            validate_object(data, schema, json_file.name)
            
        except json.JSONDecodeError as e:
            pytest.fail(f"Invalid JSON in {json_file.name}: {e}")


def test_threshold_reduction_pct_format():
    """
    Specific contract test for threshold_reduction_pct:
    Must be a number rounded to 2 decimal places.
    """
    schema = load_schema()
    prop_schema = schema["properties"].get("threshold_reduction_pct", {})
    
    assert prop_schema.get("type") == "number", "threshold_reduction_pct must be a number"
    
