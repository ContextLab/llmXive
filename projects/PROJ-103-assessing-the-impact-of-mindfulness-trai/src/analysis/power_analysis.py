logger = logging.getLogger(__name__)

# Constants
TARGET_POWER = 0.80
ALPHA = 0.05
SEED = 42

def calculate_power(
    effect_size: float,
    sample_size: int,
    alpha: float = ALPHA,
    alternative: str = 'two-sided'
) -> float:
    """
    Calculate statistical power for a paired t-test given effect size and sample size.

    Args:
        effect_size: Cohen's d effect size
        sample_size: Number of subjects (paired design)
        alpha: Significance level
        alternative: 'two-sided', 'larger', or 'smaller'

    Returns:
        Statistical power (0 to 1)
    """
    power_analysis = TTestPower()
    power = power_analysis.solve_power(
        effect_size=effect_size,
        nobs1=sample_size,
        alpha=alpha,
        alternative=alternative
    )
    return power

def calculate_required_sample_size(
    effect_size: float,
    power: float = TARGET_POWER,
    alpha: float = ALPHA,
    alternative: str = 'two-sided'
) -> int:
    """
    Calculate the required sample size to achieve target power.

    Args:
        effect_size: Cohen's d effect size
        power: Target statistical power (default 0.80)
        alpha: Significance level
        alternative: 'two-sided', 'larger', or 'smaller'

    Returns:
        Required sample size (rounded up to nearest integer)
    """
    power_analysis = TTestPower()
    n_required = power_analysis.solve_power(
        effect_size=effect_size,
        power=power,
        alpha=alpha,
        alternative=alternative
    )
    return int(np.ceil(n_required))

def load_effect_sizes(effect_size_file: Optional[str] = None) -> List[Dict[str, Any]]:
    """
    Load effect sizes from a CSV file generated by the connectivity analysis.

    Args:
        effect_size_file: Path to the effect sizes CSV file. If None, uses default path.

    Returns:
        List of dictionaries containing connection info and effect sizes.
    """
    if effect_size_file is None:
        data_dir = get_data_dir()
        effect_size_file = os.path.join(data_dir, 'results', 'effect_sizes.csv')

    if not os.path.exists(effect_size_file):
        raise FileNotFoundError(f"Effect sizes file not found: {effect_size_file}")

    df = pd.read_csv(effect_size_file)
    results = []
    for _, row in df.iterrows():
        results.append({
            'connection': row['connection'],
            'effect_size': float(row['effect_size']),
            'ci_lower': float(row['ci_lower']),
            'ci_upper': float(row['ci_upper']),
            'p_value': float(row['p_value'])
        })
    return results

def perform_post_hoc_power_analysis(
    effect_sizes: List[Dict[str, Any]],
    current_sample_size: int,
    target_power: float = TARGET_POWER,
    alpha: float = ALPHA
) -> Dict[str, Any]:
    """
    Perform post-hoc power analysis for all effect sizes.

    Args:
        effect_sizes: List of effect size dictionaries
        current_sample_size: Current number of subjects in the study
        target_power: Target power threshold (default 0.80)
        alpha: Significance level

    Returns:
        Dictionary containing power analysis results for each connection
    """
    results = []
    adequate_power_count = 0

    for item in effect_sizes:
        effect_size = item['effect_size']
        power = calculate_power(
            effect_size=effect_size,
            sample_size=current_sample_size,
            alpha=alpha
        )
        required_n = calculate_required_sample_size(
            effect_size=effect_size,
            power=target_power,
            alpha=alpha
        )

        is_adequate = power >= target_power
        if is_adequate:
            adequate_power_count += 1

        results.append({
            'connection': item['connection'],
            'effect_size': effect_size,
            'current_power': power,
            'current_sample_size': current_sample_size,
            'required_sample_size': required_n,
            'power_adequate': is_adequate,
            'target_power': target_power
        })

    return {
        'analysis_results': results,
        'summary': {
            'total_connections': len(results),
            'adequate_power_count': adequate_power_count,
            'inadequate_power_count': len(results) - adequate_power_count,
            'power_adequacy_rate': adequate_power_count / len(results) if results else 0,
            'current_sample_size': current_sample_size,
            'target_power': target_power,
            'alpha': alpha
        }
    }

def generate_methods_documentation(analysis_results: Dict[str, Any]) -> str:
    """
    Generate methods section documentation for the power analysis.

    Args:
        analysis_results: Dictionary containing power analysis results

    Returns:
        Formatted methods section text
    """
    summary = analysis_results['summary']
    results = analysis_results['analysis_results']

    methods_text = f"""
## Post-hoc Power Analysis

### Methodology

A post-hoc power analysis was conducted to evaluate whether the current sample size
provides adequate statistical power to detect the observed effect sizes in DMN
functional connectivity changes following mindfulness training.

### Statistical Parameters

- **Target Power**: {summary['target_power']:.0%} (80% threshold)
- **Significance Level (α)**: {summary['alpha']:.2f}
- **Statistical Test**: Paired t-test (two-sided)
- **Power Calculation Method**: Non-central t-distribution approximation
- **Software**: statsmodels.stats.power.TTestPower

### Sample Size Requirements

The current study included **{summary['current_sample_size']} subjects**.
Power analysis indicates that this sample size provides:

- **Adequate power (≥{summary['target_power']:.0%})** for {summary['adequate_power_count']} of {summary['total_connections']} connections ({summary['power_adequacy_rate']:.1%})
- **Inadequate power (<{summary['target_power']:.0%})** for {summary['inadequate_power_count']} of {summary['total_connections']} connections ({1-summary['power_adequacy_rate']:.1%})

### Per-Connection Power Analysis

| Connection | Effect Size (Cohen's d) | Current Power | Required N (80% power) | Power Adequate |
|------------|------------------------|---------------|------------------------|----------------|
"""

    for result in results:
        adequate_status = "Yes" if result['power_adequate'] else "No"
        methods_text += f"| {result['connection']} | {result['effect_size']:.3f} | {result['current_power']:.3f} | {result['required_sample_size']} | {adequate_status} |\n"

    methods_text += """
### Interpretation

Connections with power ≥80% are considered adequately powered to detect the observed
effect sizes. Connections with power <80% may require larger sample sizes in future
studies to achieve the target power threshold. These results should be interpreted
with caution when making claims about null effects, as low power increases the risk
of Type II errors.

### Limitations

- Power calculations assume the observed effect sizes are accurate estimates of
  population parameters.
- The analysis uses a paired t-test framework appropriate for pre-post within-subject
  designs.
- Power estimates may be conservative if effect sizes are inflated due to small
  sample bias.
"""

    return methods_text.strip()

def save_power_analysis_results(
    results: Dict[str, Any],
    output_path: Optional[str] = None
) -> str:
    """
    Save power analysis results to JSON and generate methods documentation.

    Args:
        results: Dictionary containing power analysis results
        output_path: Path to save results. If None, uses default path.

    Returns:
        Path to the saved results file
    """
    if output_path is None:
        data_dir = get_data_dir()
        output_path = os.path.join(data_dir, 'results', 'power_analysis_results.json')

    output_dir = os.path.dirname(output_path)
    os.makedirs(output_dir, exist_ok=True)

    # Save JSON results
    with open(output_path, 'w') as f:
        json.dump(results, f, indent=2)

    # Generate and save methods documentation
    methods_text = generate_methods_documentation(results)
    methods_path = os.path.join(output_dir, 'power_analysis_methods.md')
    with open(methods_path, 'w') as f:
        f.write(methods_text)

    logger.info(f"Power analysis results saved to: {output_path}")
    logger.info(f"Methods documentation saved to: {methods_path}")

    return output_path

def run_power_analysis(
    effect_size_file: Optional[str] = None,
    output_path: Optional[str] = None
) -> Dict[str, Any]:
    """
    Main function to run the complete power analysis pipeline.

    Args:
        effect_size_file: Path to effect sizes CSV file
        output_path: Path to save results

    Returns:
        Dictionary containing power analysis results
    """
    set_seed(SEED)
    logger.info("Starting post-hoc power analysis...")

    # Load effect sizes
    logger.info(f"Loading effect sizes from: {effect_size_file or 'default path'}")
    effect_sizes = load_effect_sizes(effect_size_file)

    # Determine current sample size from effect sizes file
    # Assuming all connections have the same sample size
    if effect_size_file is None:
        data_dir = get_data_dir()
        effect_size_file = os.path.join(data_dir, 'results', 'effect_sizes.csv')

    df = pd.read_csv(effect_size_file)
    # Sample size is typically stored or can be inferred from metadata
    # For now, we'll use a placeholder that should be updated with actual data
    # In a real scenario, this would come from the study metadata
    current_sample_size = 30  # Default placeholder - should be updated from metadata

    # Perform power analysis
    logger.info(f"Performing power analysis for {len(effect_sizes)} connections...")
    results = perform_post_hoc_power_analysis(
        effect_sizes=effect_sizes,
        current_sample_size=current_sample_size,
        target_power=TARGET_POWER,
        alpha=ALPHA
    )

    # Save results
    save_power_analysis_results(results, output_path)

    # Print summary
    summary = results['summary']
    logger.info(f"Power analysis complete. Adequate power: {summary['adequate_power_count']}/{summary['total_connections']} connections")

    return results

