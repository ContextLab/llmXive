"""
Motion exclusion filter for fMRI preprocessing outputs.

Filters subjects based on translation (>3mm) or rotation (>3°) thresholds.
Reads motion parameters extracted by extract_motion.py and outputs a CSV
of included subjects and a log of excluded subjects.
"""

import os
import csv
import logging
from pathlib import Path
from typing import List, Dict, Any, Optional, Tuple

import pandas as pd

from src.config.env import get_data_dir
from src.utils.logging import get_logger

# Default thresholds per task specification
DEFAULT_TRANS_THRESHOLD_MM = 3.0
DEFAULT_ROT_THRESHOLD_DEG = 3.0

class MotionFilterError(Exception):
    """Custom exception for motion filtering errors."""
    pass

def load_motion_data(motion_csv_path: Path) -> pd.DataFrame:
    """
    Load motion parameters from the CSV generated by extract_motion.py.

    Expected columns: subject_id, translation_x, translation_y, translation_z,
                      rotation_x, rotation_y, rotation_z

    Args:
        motion_csv_path: Path to the motion parameters CSV file.

    Returns:
        DataFrame containing motion parameters.

    Raises:
        MotionFilterError: If file not found or missing required columns.
    """
    if not motion_csv_path.exists():
        raise MotionFilterError(f"Motion CSV not found: {motion_csv_path}")

    try:
        df = pd.read_csv(motion_csv_path)
    except Exception as e:
        raise MotionFilterError(f"Failed to read motion CSV: {e}")

    required_cols = [
        'subject_id',
        'translation_x', 'translation_y', 'translation_z',
        'rotation_x', 'rotation_y', 'rotation_z'
    ]

    missing = [c for c in required_cols if c not in df.columns]
    if missing:
        raise MotionFilterError(f"Missing required columns in motion CSV: {missing}")

    return df

def calculate_max_displacement(row: pd.Series) -> Tuple[float, float]:
    """
    Calculate maximum translation (mm) and maximum rotation (degrees) for a row.

    Args:
        row: A row from the motion DataFrame.

    Returns:
        Tuple of (max_translation_mm, max_rotation_deg).
    """
    trans_x = row['translation_x']
    trans_y = row['translation_y']
    trans_z = row['translation_z']
    
    # Calculate Euclidean norm for translation (total displacement)
    # Alternatively, sum of absolute values or max absolute value. 
    # Standard practice often uses Framewise Displacement (FD), but task specifies
    # ">3mm translation". Interpreting as max absolute translation or total displacement.
    # Given the simple threshold, we will use the maximum absolute translation value 
    # across the 3 axes as the "translation" metric, or the sum of absolute values.
    # Let's use the maximum absolute value across axes for strictness against the "3mm" limit.
    max_trans = max(abs(trans_x), abs(trans_y), abs(trans_z))

    rot_x = row['rotation_x']
    rot_y = row['rotation_y']
    rot_z = row['rotation_z']
    
    # Similarly for rotation
    max_rot = max(abs(rot_x), abs(rot_y), abs(rot_z))

    return max_trans, max_rot

def filter_subjects(
    df: pd.DataFrame,
    trans_threshold: float = DEFAULT_TRANS_THRESHOLD_MM,
    rot_threshold: float = DEFAULT_ROT_THRESHOLD_DEG
) -> Tuple[pd.DataFrame, pd.DataFrame, List[Dict[str, Any]]]:
    """
    Filter subjects based on motion thresholds.

    A subject is excluded if:
    - Max translation > trans_threshold (mm)
    - Max rotation > rot_threshold (degrees)

    Args:
        df: DataFrame with motion parameters.
        trans_threshold: Maximum allowed translation in mm.
        rot_threshold: Maximum allowed rotation in degrees.

    Returns:
        Tuple of (included_df, excluded_df, exclusion_log).
    """
    results = []
    included_rows = []
    excluded_rows = []

    for _, row in df.iterrows():
        max_trans, max_rot = calculate_max_displacement(row)
        
        is_excluded = False
        reason = []

        if max_trans > trans_threshold:
            is_excluded = True
            reason.append(f"Trans {max_trans:.2f}mm > {trans_threshold}mm")
        
        if max_rot > rot_threshold:
            is_excluded = True
            reason.append(f"Rot {max_rot:.2f}deg > {rot_threshold}deg")

        status = "EXCLUDED" if is_excluded else "INCLUDED"
        
        log_entry = {
            "subject_id": row['subject_id'],
            "max_translation_mm": max_trans,
            "max_rotation_deg": max_rot,
            "status": status,
            "reason": "; ".join(reason) if reason else "Within limits"
        }
        results.append(log_entry)

        if is_excluded:
            excluded_rows.append(row.to_dict())
        else:
            included_rows.append(row.to_dict())

    included_df = pd.DataFrame(included_rows) if included_rows else pd.DataFrame(columns=df.columns)
    excluded_df = pd.DataFrame(excluded_rows) if excluded_rows else pd.DataFrame(columns=df.columns)

    return included_df, excluded_df, results

def write_exclusion_report(
    exclusion_log: List[Dict[str, Any]],
    output_path: Path
) -> None:
    """
    Write the exclusion report to a CSV file.

    Args:
        exclusion_log: List of dictionaries containing exclusion details.
        output_path: Path to the output CSV file.
    """
    if not exclusion_log:
        # Create empty file with headers if no data
        pd.DataFrame(columns=["subject_id", "max_translation_mm", "max_rotation_deg", "status", "reason"]).to_csv(output_path, index=False)
        return

    df_log = pd.DataFrame(exclusion_log)
    df_log.to_csv(output_path, index=False)

def run_motion_filter(
    input_csv_path: Optional[Path] = None,
    output_included_path: Optional[Path] = None,
    output_excluded_path: Optional[Path] = None,
    output_report_path: Optional[Path] = None,
    trans_threshold: float = DEFAULT_TRANS_THRESHOLD_MM,
    rot_threshold: float = DEFAULT_ROT_THRESHOLD_DEG
) -> Dict[str, Any]:
    """
    Main entry point to run the motion filtering pipeline.

    Reads motion data, filters subjects, and writes results.

    Args:
        input_csv_path: Path to input motion CSV. Defaults to data/processed/motion_params.csv.
        output_included_path: Path to output included subjects CSV. Defaults to data/processed/included_subjects.csv.
        output_excluded_path: Path to output excluded subjects CSV. Defaults to data/processed/excluded_subjects.csv.
        output_report_path: Path to output exclusion report CSV. Defaults to data/processed/motion_exclusion_report.csv.
        trans_threshold: Translation threshold in mm.
        rot_threshold: Rotation threshold in degrees.

    Returns:
        Dictionary with summary statistics.
    """
    logger = get_logger(__name__)
    
    # Resolve paths
    data_dir = get_data_dir()
    if input_csv_path is None:
        input_csv_path = Path(data_dir) / "processed" / "motion_params.csv"
    if output_included_path is None:
        output_included_path = Path(data_dir) / "processed" / "included_subjects.csv"
    if output_excluded_path is None:
        output_excluded_path = Path(data_dir) / "processed" / "excluded_subjects.csv"
    if output_report_path is None:
        output_report_path = Path(data_dir) / "processed" / "motion_exclusion_report.csv"

    # Ensure output directories exist
    for p in [output_included_path, output_excluded_path, output_report_path]:
        p.parent.mkdir(parents=True, exist_ok=True)

    logger.info(f"Loading motion data from {input_csv_path}")
    df = load_motion_data(input_csv_path)
    logger.info(f"Loaded {len(df)} subjects")

    logger.info(f"Filtering with thresholds: Trans > {trans_threshold}mm, Rot > {rot_threshold}deg")
    included_df, excluded_df, exclusion_log = filter_subjects(df, trans_threshold, rot_threshold)

    logger.info(f"Writing {len(included_df)} included subjects to {output_included_path}")
    included_df.to_csv(output_included_path, index=False)

    if not excluded_df.empty:
        logger.info(f"Writing {len(excluded_df)} excluded subjects to {output_excluded_path}")
        excluded_df.to_csv(output_excluded_path, index=False)
    else:
        logger.info("No subjects excluded.")

    logger.info(f"Writing exclusion report to {output_report_path}")
    write_exclusion_report(exclusion_log, output_report_path)

    return {
        "total_subjects": len(df),
        "included_count": len(included_df),
        "excluded_count": len(excluded_df),
        "included_path": str(output_included_path),
        "excluded_path": str(output_excluded_path),
        "report_path": str(output_report_path)
    }

def main():
    """Command line entry point."""
    import argparse

    parser = argparse.ArgumentParser(description="Filter subjects based on motion thresholds.")
    parser.add_argument("--input", type=str, help="Path to input motion CSV")
    parser.add_argument("--output-included", type=str, help="Path to output included CSV")
    parser.add_argument("--output-excluded", type=str, help="Path to output excluded CSV")
    parser.add_argument("--output-report", type=str, help="Path to output report CSV")
    parser.add_argument("--trans-threshold", type=float, default=DEFAULT_TRANS_THRESHOLD_MM,
                        help=f"Max translation threshold (mm) [default: {DEFAULT_TRANS_THRESHOLD_MM}]")
    parser.add_argument("--rot-threshold", type=float, default=DEFAULT_ROT_THRESHOLD_DEG,
                        help=f"Max rotation threshold (deg) [default: {DEFAULT_ROT_THRESHOLD_DEG}]")

    args = parser.parse_args()

    input_path = Path(args.input) if args.input else None
    out_inc = Path(args.output_included) if args.output_included else None
    out_exc = Path(args.output_excluded) if args.output_excluded else None
    out_rep = Path(args.output_report) if args.output_report else None

    result = run_motion_filter(
        input_csv_path=input_path,
        output_included_path=out_inc,
        output_excluded_path=out_exc,
        output_report_path=out_rep,
        trans_threshold=args.trans_threshold,
        rot_threshold=args.rot_threshold
    )

    print(f"Motion Filtering Complete:")
    print(f"  Total: {result['total_subjects']}")
    print(f"  Included: {result['included_count']}")
    print(f"  Excluded: {result['excluded_count']}")
    print(f"  Report: {result['report_path']}")

if __name__ == "__main__":
    main()
