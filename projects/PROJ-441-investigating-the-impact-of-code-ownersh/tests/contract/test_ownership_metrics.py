"""
Contract test for ownership metrics schema.

This test validates that the output from code/extractors/git_metrics.py
conforms to the expected OwnershipMetrics schema defined in the project specs.

It checks:
1. Root structure is a dictionary (or list of dicts if multiple repos).
2. Required fields exist: repo_name, gini_coefficient, developer_count, total_commits.
3. Data types are correct (floats for gini, ints for counts).
4. Value constraints (Gini between 0 and 1).
"""
import json
import os
import sys
from pathlib import Path
from typing import Any, Dict, List, Union

import pytest

# Add project root to path to allow imports from code/
# Assuming tests are run from project root or the script handles paths
project_root = Path(__file__).parent.parent.parent
sys.path.insert(0, str(project_root))

from code.utils.config import get_path

# Expected schema definition based on T012/T016 requirements
# T012 produces LOC-weighted Gini and developer count.
# T016 saves to data/processed/ownership_metrics.json
OWNERSHIP_METRICS_SCHEMA = {
    "required_fields": [
        "repo_name",
        "gini_coefficient",
        "developer_count",
        "total_commits",
        "timestamp",
    ],
    "type_constraints": {
        "repo_name": str,
        "gini_coefficient": (float, int),  # Will be float, but allow int for 0
        "developer_count": int,
        "total_commits": int,
        "timestamp": str,
    },
    "value_constraints": {
        "gini_coefficient": {"min": 0.0, "max": 1.0},
        "developer_count": {"min": 0},
        "total_commits": {"min": 0},
    },
}


def load_generated_metrics() -> Union[Dict[str, Any], List[Dict[str, Any]]]:
    """
    Loads the ownership metrics file generated by the extraction pipeline.
    If the file doesn't exist, it attempts to run the extraction script first.
    """
    metrics_path = get_path("data/processed/ownership_metrics.json")
    
    if not metrics_path.exists():
        # If not found, we might need to run the extraction to generate it for the test
        # However, in a strict contract test, we often assume the producer has run.
        # For this test to pass in CI, the extraction task (T012) must have run.
        # We will raise a clear error if the file is missing to indicate the pipeline
        # hasn't been executed yet.
        raise FileNotFoundError(
            f"Metrics file not found at {metrics_path}. "
            "Please run the extraction pipeline (T012) before running this contract test."
        )
    
    with open(metrics_path, "r", encoding="utf-8") as f:
        data = json.load(f)
    
    return data


def validate_single_metric(metric: Dict[str, Any]) -> List[str]:
    """
    Validates a single metric dictionary against the schema.
    Returns a list of error messages.
    """
    errors = []
    
    # Check required fields
    for field in OWNERSHIP_METRICS_SCHEMA["required_fields"]:
        if field not in metric:
            errors.append(f"Missing required field: {field}")
    
    if errors:
        return errors
    
    # Check types
    for field, expected_type in OWNERSHIP_METRICS_SCHEMA["type_constraints"].items():
        if field in metric:
            if not isinstance(metric[field], expected_type):
                errors.append(
                    f"Field '{field}' has type {type(metric[field]).__name__}, "
                    f"expected {expected_type}"
                )
    
    # Check value constraints
    gini = metric.get("gini_coefficient")
    if gini is not None:
        min_val = OWNERSHIP_METRICS_SCHEMA["value_constraints"]["gini_coefficient"]["min"]
        max_val = OWNERSHIP_METRICS_SCHEMA["value_constraints"]["gini_coefficient"]["max"]
        if not (min_val <= gini <= max_val):
            errors.append(
                f"Gini coefficient {gini} is out of range [{min_val}, {max_val}]"
            )
    
    dev_count = metric.get("developer_count")
    if dev_count is not None:
        min_val = OWNERSHIP_METRICS_SCHEMA["value_constraints"]["developer_count"]["min"]
        if dev_count < min_val:
            errors.append(
                f"Developer count {dev_count} is below minimum {min_val}"
            )
    
    total_commits = metric.get("total_commits")
    if total_commits is not None:
        min_val = OWNERSHIP_METRICS_SCHEMA["value_constraints"]["total_commits"]["min"]
        if total_commits < min_val:
            errors.append(
                f"Total commits {total_commits} is below minimum {min_val}"
            )
    
    return errors


class TestOwnershipMetricsSchema:
    """
    Contract test suite for Ownership Metrics output.
    """

    def test_file_exists(self):
        """Verify the metrics file exists."""
        metrics_path = get_path("data/processed/ownership_metrics.json")
        assert metrics_path.exists(), f"Metrics file missing at {metrics_path}"

    def test_valid_json_structure(self):
        """Verify the file is valid JSON and is a list or dict."""
        data = load_generated_metrics()
        assert isinstance(data, (dict, list)), "Root must be a dict or list of dicts"

    def test_all_metrics_conform_to_schema(self):
        """
        Verify every metric entry in the output file conforms to the schema.
        This is the core contract test.
        """
        data = load_generated_metrics()
        
        # Normalize to list for uniform processing
        if isinstance(data, dict):
            metrics_list = [data]
        else:
            metrics_list = data
        
        assert len(metrics_list) > 0, "No metrics found in the file"
        
        all_errors = []
        for idx, metric in enumerate(metrics_list):
            errors = validate_single_metric(metric)
            if errors:
                all_errors.append(f"Metric {idx}: {errors}")
        
        if all_errors:
            pytest.fail(f"Schema validation failed for {len(all_errors)} metrics:\n" + "\n".join(all_errors))

    def test_gini_is_loc_weighted(self):
        """
        Contract check: Ensure Gini is not just a simple commit count ratio.
        While we can't easily verify the internal calculation without re-running git blame,
        we can check that the value is a float and not an integer (which would imply a simple ratio).
        The Plan explicitly requires LOC-weighted Gini.
        """
        data = load_generated_metrics()
        if isinstance(data, dict):
            metrics_list = [data]
        else:
            metrics_list = data
        
        for idx, metric in enumerate(metrics_list):
            gini = metric.get("gini_coefficient")
            if gini is not None:
                # If it's an integer, it might be a simple ratio or a bug, 
                # though 0.0 is possible. We expect float precision.
                if isinstance(gini, int) and gini != 0:
                    # This is a heuristic check. A true Gini from LOC distribution 
                    # will likely have decimals unless the distribution is perfectly uniform or extreme.
                    # We assert it's a float to enforce the precision requirement.
                    # However, JSON loads int as int. So we check if it was written as float.
                    # A safer check is just that it exists and is numeric.
                    pass 
                # We rely on the implementation in T012 to ensure it calculates correctly.
                # This test primarily ensures the field exists and is numeric.
                assert isinstance(gini, (int, float)), "Gini must be numeric"

    def test_developer_count_is_integer(self):
        """Verify developer count is an integer."""
        data = load_generated_metrics()
        if isinstance(data, dict):
            metrics_list = [data]
        else:
            metrics_list = data
        
        for idx, metric in enumerate(metrics_list):
            count = metric.get("developer_count")
            assert isinstance(count, int), f"Developer count must be int, got {type(count)}"