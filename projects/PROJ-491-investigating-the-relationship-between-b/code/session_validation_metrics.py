"""
Task T013b: Calculate pass-rate percentage of subjects with distinct session IDs.

Reads the validation state generated by T013 (validate_session_distinctness),
calculates the percentage of subjects that passed the distinctness check,
and writes the metric to data/processed/session_validation_metrics.json.
"""
import os
import json
import logging
import sys
from pathlib import Path
from typing import Dict, Any

# Add project root to path to allow relative imports if run as script
project_root = Path(__file__).resolve().parent.parent
sys.path.insert(0, str(project_root))

from config import ensure_directories
from state_manager import load_state

# Configure logging
logging.basicConfig(
    level=logging.INFO,
    format='%(asctime)s - %(levelname)s - %(message)s',
    handlers=[
        logging.StreamHandler(sys.stdout),
        logging.FileHandler(project_root / 'data' / 'processed' / 'session_validation_metrics.log')
    ]
)
logger = logging.getLogger(__name__)

# Constants
VALIDATION_STATE_FILE = 'data/processed/session_validation_state.json'
METRICS_OUTPUT_FILE = 'data/processed/session_validation_metrics.json'
SUBJECTS_REQUIRED = 50

def calculate_pass_rate() -> Dict[str, Any]:
    """
    Reads the session validation state, calculates pass rate, and returns metrics.
    
    Returns:
        Dict containing pass_rate, total_subjects, passed_subjects, failed_subjects.
    """
    state_path = project_root / VALIDATION_STATE_FILE
    
    if not state_path.exists():
        raise FileNotFoundError(
            f"Validation state file not found at {state_path}. "
            "Ensure T013 (validate_session_distinctness) has been executed first."
        )
    
    try:
        with open(state_path, 'r') as f:
            validation_data = json.load(f)
    except json.JSONDecodeError as e:
        raise ValueError(f"Failed to parse validation state file: {e}")
    
    # Extract counts from the state file
    # Expected structure from T013:
    # {
    #   "total_subjects_checked": int,
    #   "distinct_session_count": int,
    #   "duplicate_session_ids": [...],
    #   "excluded_subjects": [...]
    # }
    
    total_checked = validation_data.get('total_subjects_checked', 0)
    distinct_count = validation_data.get('distinct_session_count', 0)
    excluded_subjects = validation_data.get('excluded_subjects', [])
    
    if total_checked == 0:
        logger.warning("No subjects were checked. Pass rate is 0% by default.")
        return {
            "pass_rate_percent": 0.0,
            "total_subjects_checked": 0,
            "subjects_with_distinct_sessions": 0,
            "subjects_excluded": 0,
            "excluded_count": 0,
            "status": "no_data"
        }
    
    # Calculate pass rate
    pass_rate = (distinct_count / total_checked) * 100
    
    metrics = {
        "pass_rate_percent": round(pass_rate, 2),
        "total_subjects_checked": total_checked,
        "subjects_with_distinct_sessions": distinct_count,
        "subjects_excluded": len(excluded_subjects),
        "excluded_count": len(excluded_subjects),
        "excluded_subject_ids": excluded_subjects,
        "status": "passed" if pass_rate > 0 else "failed",
        "timestamp": None  # Will be set by caller if needed, or left for state_manager
    }
    
    logger.info(f"Calculated pass rate: {pass_rate:.2f}% ({distinct_count}/{total_checked})")
    logger.info(f"Excluded {len(excluded_subjects)} subjects due to session ID conflicts.")
    
    return metrics

def write_metrics(metrics: Dict[str, Any]) -> Path:
    """
    Writes the metrics dictionary to the output JSON file.
    
    Args:
        metrics: Dictionary containing pass rate metrics.
        
    Returns:
        Path to the written file.
    """
    output_dir = project_root / 'data' / 'processed'
    ensure_directories([output_dir])
    
    output_path = output_dir / METRICS_OUTPUT_FILE
    
    with open(output_path, 'w') as f:
        json.dump(metrics, f, indent=2)
    
    logger.info(f"Metrics written to {output_path}")
    return output_path

def main():
    """Main entry point for T013b."""
    logger.info("Starting T013b: Session validation metrics calculation")
    
    try:
        # Calculate metrics
        metrics = calculate_pass_rate()
        
        # Write to file
        output_path = write_metrics(metrics)
        
        # Update state manager with the new artifact
        try:
            from state_manager import update_state_artifact
            update_state_artifact(output_path)
            logger.info("Updated state manager with new artifact hash.")
        except Exception as e:
            logger.warning(f"Could not update state manager: {e}")
        
        # Validate minimum threshold (optional, based on project requirements)
        if metrics['pass_rate_percent'] == 0:
            logger.error("ERROR: 0% pass rate. No subjects passed distinct session validation.")
            sys.exit(1)
        
        logger.info("T013b completed successfully.")
        return 0
        
    except FileNotFoundError as e:
        logger.error(f"Missing required data: {e}")
        sys.exit(1)
    except Exception as e:
        logger.error(f"Unexpected error during metrics calculation: {e}")
        sys.exit(1)

if __name__ == "__main__":
    main()
