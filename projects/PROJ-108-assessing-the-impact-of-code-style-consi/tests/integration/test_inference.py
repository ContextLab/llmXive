def test_inference_output_format():
    """
    Test that running the inference script produces a valid JSONL file
    with the expected schema (file_path, summary, bug_prediction, etc.).
    
    Note: We mock the actual model loading and generation to ensure this
    is a fast integration test that doesn't require downloading StarCoder.
    """
    if not INFERENCE_SCRIPT.exists():
        pytest.skip("Inference script (03_inference.py) not found. T020 must be implemented first.")

    # Mock the heavy lifting to simulate a successful run without GPU/Model download
    with patch("subprocess.run") as mock_run, \
         patch("code.03_inference.load_model") as mock_load, \
         patch("code.03_inference.generate_text") as mock_gen:
        
        # Setup mocks
        mock_load.return_value = MagicMock()
        mock_gen.return_value = "This is a mock summary."
        
        # Run the script
        result = subprocess.run(
            [sys.executable, str(INFERENCE_SCRIPT), "--input", str(PROCESSED_DIR / "style_scores.csv")],
            cwd=str(PROJECT_ROOT),
            capture_output=True,
            text=True,
            timeout=60 # 60s timeout for the script itself
        )

        # Assert script executed successfully
        assert result.returncode == 0, f"Script failed: {result.stderr}"

        # Assert output file was created
        assert EXPECTED_OUTPUT.exists(), f"Output file {EXPECTED_OUTPUT} was not created."

        # Validate JSONL content and schema
        with open(EXPECTED_OUTPUT, "r", encoding="utf-8") as f:
            lines = f.readlines()
        
        assert len(lines) > 0, "Output file is empty."

        required_keys = {"file_path", "summary", "bug_prediction", "confidence"}
        
        for i, line in enumerate(lines):
            try:
                data = json.loads(line)
            except json.JSONDecodeError as e:
                raise AssertionError(f"Line {i} is not valid JSON: {e}")
            
            # Check required keys
            missing_keys = required_keys - set(data.keys())
            assert not missing_keys, f"Line {i} missing keys: {missing_keys}"
            
            # Check types
            assert isinstance(data["file_path"], str), "file_path must be a string"
            assert isinstance(data["summary"], str), "summary must be a string"
            assert isinstance(data["bug_prediction"], (str, type(None))), "bug_prediction must be string or null"
            assert isinstance(data["confidence"], (float, int)), "confidence must be numeric"

