# Ensure we are in the project root context for imports if needed,
# though this script primarily orchestrates the external eval and calculates metrics.

def run_evaluation_suite():
    """
    Executes the external SDAR evaluation script.
    This assumes T022-T025 are complete and external/SDAR/agent_system/eval.py
    exists and logs to outputs/logs/eval_log.json.
    """
    eval_script_path = Path("external/SDAR/agent_system/eval.py")
    if not eval_script_path.exists():
        raise FileNotFoundError(f"Evaluation script not found at {eval_script_path}")
    
    # Run the external evaluation script
    # We rely on the environment configuration (T005, T006) being active
    cmd = [sys.executable, str(eval_script_path)]
    
    try:
        # Run with a timeout to prevent hanging, though T023 handles internal timeouts
        result = subprocess.run(
            cmd,
            check=True,
            capture_output=True,
            text=True,
            timeout=600 # 10 minutes max for the whole suite
        )
        return result.returncode == 0
    except subprocess.TimeoutExpired:
        print("Evaluation suite timed out.")
        return False
    except subprocess.CalledProcessError as e:
        print(f"Evaluation script failed with code {e.returncode}")
        print(f"stderr: {e.stderr}")
        return False

def load_eval_log(log_path):
    """
    Loads the evaluation log generated by T025.
    Expects a JSON file containing a list of task results or summary stats.
    """
    path = Path(log_path)
    if not path.exists():
        raise FileNotFoundError(f"Evaluation log not found at {log_path}. "
                                "Ensure T025 has run and generated the file.")
    
    with open(path, 'r') as f:
        return json.load(f)

def calculate_success_rate(eval_data):
    """
    Calculates the success rate from the evaluation data.
    
    Expected data structure (based on T025 context):
    - A list of task results: [{'task_id': ..., 'success': True/False}, ...]
    - OR a summary object: {'total_tasks': N, 'successful_tasks': M}
    
    Returns a float between 0.0 and 1.0.
    """
    total_tasks = 0
    successful_tasks = 0

    if isinstance(eval_data, list):
        # List of individual task results
        for task in eval_data:
            total_tasks += 1
            if task.get('success', False):
                successful_tasks += 1
    elif isinstance(eval_data, dict):
        # Summary object
        total_tasks = eval_data.get('total_tasks', 0)
        successful_tasks = eval_data.get('successful_tasks', 0)
        # Fallback if keys differ but structure implies counts
        if total_tasks == 0 and 'results' in eval_data:
            results = eval_data['results']
            total_tasks = len(results)
            successful_tasks = sum(1 for r in results if r.get('success', False))
    else:
        raise ValueError("Unexpected evaluation log format. Expected list or dict.")

    if total_tasks == 0:
        return 0.0

    return successful_tasks / total_tasks

