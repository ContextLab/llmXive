"""
Topology Audit Script for Molecular Graphs.

This script generates a topology audit report (analysis/topology_audit.md)
by analyzing the processed graphs generated by code/data/graph_build.py.
It lists node counts, edge counts, and pruning statistics for the dataset.
"""
import os
import sys
import logging
import json
from pathlib import Path
from typing import List, Dict, Any, Optional

# Add project root to path for imports
PROJECT_ROOT = Path(__file__).parent.parent
sys.path.insert(0, str(PROJECT_ROOT))

from data.graph_build import run_topology_audit, build_interface_graph
from utils.exceptions import DataError

logging.basicConfig(
    level=logging.INFO,
    format='%(asctime)s - %(levelname)s - %(message)s'
)
logger = logging.getLogger(__name__)

def load_graph_stats(graphs_path: Path) -> List[Dict[str, Any]]:
    """
    Load graph statistics from the processed graphs file.
    Expects a PyTorch tensor file or a JSON metadata file generated by graph_build.
    """
    if not graphs_path.exists():
        raise DataError(f"Graphs file not found: {graphs_path}. Run graph_build.py first.")
    
    # The graph_build.py script is expected to save metadata or the graphs themselves.
    # We rely on the run_topology_audit function from graph_build to extract stats 
    # if it returns them, or we re-parse if necessary.
    # For this implementation, we assume graph_build.py saves a 'graphs.pt' and 
    # potentially a 'graph_metadata.json' or similar.
    # However, the task requires us to LIST stats. 
    # We will invoke the logic from graph_build to extract these stats.
    
    # Since we cannot easily import the internal state of the graphs.pt without torch,
    # and the task implies we should use the output of graph_build, 
    # we will assume graph_build.py has a mechanism to export stats or we re-run the 
    # analysis logic on the source CSV if the graphs are too complex to load without torch.
    
    # Given the API surface: from data.graph_build import run_topology_audit
    # We assume run_topology_audit can either return stats or write them.
    # Let's check if run_topology_audit returns the stats dict.
    
    # If run_topology_audit is designed to write the audit file, we call it directly.
    # If it returns stats, we format them here.
    
    # Based on the task description: "Generate ... from graph_build.py listing ..."
    # It implies graph_build.py does the heavy lifting.
    
    # Strategy: Call run_topology_audit. If it returns data, format it. 
    # If it writes the file directly, we are done.
    
    # Let's assume run_topology_audit returns a list of stats dicts for flexibility.
    stats = run_topology_audit(graphs_path)
    return stats

def generate_markdown_report(stats: List[Dict[str, Any]], output_path: Path) -> None:
    """
    Generates a Markdown report summarizing the topology audit.
    """
    total_nodes = 0
    total_edges = 0
    total_pruned = 0
    
    # Header
    lines = [
        "# Topology Audit Report",
        "",
        "**Generated by**: `code/analysis/topology_audit.py`",
        "",
        "This report summarizes the graph topology statistics extracted from the processed molecular graphs.",
        "",
        "## Summary Statistics",
        "",
    ]
    
    if stats:
        for entry in stats:
            total_nodes += entry.get('node_count', 0)
            total_edges += entry.get('edge_count', 0)
            total_pruned += entry.get('pruned_count', 0)
    
    avg_nodes = total_nodes / len(stats) if stats else 0
    avg_edges = total_edges / len(stats) if stats else 0

    lines.extend([
        f"- **Total Graphs**: {len(stats)}",
        f"- **Total Nodes**: {total_nodes}",
        f"- **Total Edges**: {total_edges}",
        f"- **Average Nodes per Graph**: {avg_nodes:.2f}",
        f"- **Average Edges per Graph**: {avg_edges:.2f}",
        f"- **Total Pruned Elements**: {total_pruned}",
        "",
        "## Detailed Statistics",
        "",
        "| Graph ID | Node Count | Edge Count | Pruned Count |",
        "|----------|------------|------------|--------------|"
    ])

    for entry in stats:
        graph_id = entry.get('graph_id', 'Unknown')
        nodes = entry.get('node_count', 0)
        edges = entry.get('edge_count', 0)
        pruned = entry.get('pruned_count', 0)
        lines.append(f"| {graph_id} | {nodes} | {edges} | {pruned} |")

    lines.extend([
        "",
        "## Pruning Analysis",
        "",
        f"A total of {total_pruned} elements were pruned during graph construction.",
        "Pruning typically occurs for disconnected components or invalid molecular fragments.",
        ""
    ])

    content = "\n".join(lines)
    
    output_path.parent.mkdir(parents=True, exist_ok=True)
    output_path.write_text(content)
    logger.info(f"Topology audit report generated: {output_path}")

def main():
    """
    Main entry point for the topology audit.
    """
    graphs_path = PROJECT_ROOT / "data" / "processed" / "graphs.pt"
    output_path = PROJECT_ROOT / "analysis" / "topology_audit.md"

    logger.info(f"Starting topology audit for {graphs_path}")

    try:
        # Ensure the graphs file exists
        if not graphs_path.exists():
            logger.error(f"Graphs file not found at {graphs_path}. Please run code/data/graph_build.py first.")
            sys.exit(1)

        # Load stats using the utility from graph_build
        # We assume run_topology_audit returns a list of dicts or writes the file.
        # If it writes the file, we just need to ensure it's called.
        # To be safe and adhere to "Generate ... from graph_build.py", we call the function.
        
        # Check if run_topology_audit returns data or writes it.
        # The API surface says: from data.graph_build import run_topology_audit
        # We will assume it returns the stats list to allow this script to format the MD.
        
        stats = run_topology_audit(graphs_path)
        
        if not isinstance(stats, list):
            # If it returns something else (e.g., None because it wrote the file), 
            # we might need to read the file it wrote. 
            # But the task asks THIS script to generate the MD.
            # Let's assume it returns the data.
            logger.warning("run_topology_audit did not return a list of stats. Attempting to read output if it wrote one.")
            # Fallback: if it wrote a temp file, read it. But we don't know the path.
            # We will assume the function returns the stats.
            raise DataError("run_topology_audit failed to return statistics data.")

        generate_markdown_report(stats, output_path)
        logger.info("Topology audit completed successfully.")

    except DataError as e:
        logger.error(f"Data error during topology audit: {e}")
        sys.exit(1)
    except Exception as e:
        logger.error(f"Unexpected error during topology audit: {e}", exc_info=True)
        sys.exit(1)

if __name__ == "__main__":
    main()
