import pytest
from fetch_data import extract_commit_keywords, check_labels, classify_pr

class TestClassifier:
    def test_extract_commit_keywords_no_match(self):
        commits = [
            {"message": "Fix bug in login"},
            {"message": "Update documentation"}
        ]
        result = extract_commit_keywords(commits)
        assert result == []

    def test_extract_commit_keywords_single_match(self):
        commits = [
            {"message": "Fix bug"},
            {"message": "Generated by copilot"}
        ]
        result = extract_commit_keywords(commits)
        assert "copilot" in result

    def test_extract_commit_keywords_multiple_matches(self):
        commits = [
            {"message": "Copilot generated this"},
            {"message": "AI-generated code"}
        ]
        result = extract_commit_keywords(commits)
        assert "copilot" in result
        assert "ai-generated" in result

    def test_check_labels_no_match(self):
        labels = [{"name": "bug"}, {"name": "enhancement"}]
        result = check_labels(labels)
        assert result == []

    def test_check_labels_single_match(self):
        labels = [{"name": "bug"}, {"name": "ai-generated"}]
        result = check_labels(labels)
        assert "ai-generated" in result

    def test_check_labels_multiple_matches(self):
        labels = [{"name": "copilot-assisted"}, {"name": "llm-code"}]
        result = check_labels(labels)
        assert "copilot-assisted" in result
        assert "llm-code" in result

    def test_classify_pr_ai_by_keywords(self):
        commits = [{"message": "Generated by copilot"}]
        labels = [{"name": "bug"}]
        result = classify_pr(commits, labels)
        assert result["is_ai_assisted"] is True
        assert result["reason"] == "ai_keywords"

    def test_classify_pr_ai_by_labels(self):
        commits = [{"message": "Fix bug"}]
        labels = [{"name": "ai-generated"}]
        result = classify_pr(commits, labels)
        assert result["is_ai_assisted"] is True
        assert result["reason"] == "ai_labels"

    def test_classify_pr_ai_by_both(self):
        commits = [{"message": "Copilot generated"}]
        labels = [{"name": "llm-code"}]
        result = classify_pr(commits, labels)
        assert result["is_ai_assisted"] is True
        assert result["reason"] == "ai_keywords"

    def test_classify_pr_non_ai(self):
        commits = [{"message": "Fix bug"}]
        labels = [{"name": "bug"}]
        result = classify_pr(commits, labels)
        assert result["is_ai_assisted"] is False
        assert result["reason"] == "none"

    def test_classify_pr_empty_inputs(self):
        commits = []
        labels = []
        result = classify_pr(commits, labels)
        assert result["is_ai_assisted"] is False
        assert result["reason"] == "none"