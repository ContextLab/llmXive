import json
import logging
import sys
from pathlib import Path
from typing import Dict, List, Any, Optional, Tuple

import numpy as np
import pandas as pd

from src.utils.logging import get_logger, log_metric

logger = get_logger(__name__)

def load_feature_importance_results(input_path: Path) -> pd.DataFrame:
    """
    Load the feature importance results from the CSV generated by T033.
    Expected columns: 'descriptor', 'importance_score', 'variance_explained', 'cumulative_variance'
    """
    if not input_path.exists():
        raise FileNotFoundError(f"Input file not found: {input_path}")
    
    df = pd.read_csv(input_path)
    
    required_cols = ['descriptor', 'importance_score', 'variance_explained']
    missing_cols = [c for c in required_cols if c not in df.columns]
    if missing_cols:
        raise ValueError(f"Input CSV missing required columns: {missing_cols}")
    
    # Ensure sorting by importance (descending) if not already sorted
    # T033 should have done this, but we enforce it here for safety
    if 'cumulative_variance' not in df.columns:
        df = df.sort_values(by='importance_score', ascending=False)
        df['cumulative_variance'] = df['variance_explained'].cumsum()
    
    return df

def select_top_descriptors_subset(
    df: pd.DataFrame, 
    threshold: float = 0.60
) -> Tuple[List[str], List[float], float]:
    """
    Select the smallest subset of top descriptors where cumulative 
    variance_explained >= threshold.
    
    Args:
        df: DataFrame with 'descriptor', 'variance_explained', 'cumulative_variance'
        threshold: The cumulative variance threshold (default 0.60)
        
    Returns:
        Tuple of (selected_descriptors, selected_scores, final_cumulative_variance)
    """
    if df.empty:
        raise ValueError("Input DataFrame is empty")
    
    # Ensure sorted by importance (descending)
    df_sorted = df.sort_values(by='importance_score', ascending=False).reset_index(drop=True)
    
    # Find the first index where cumulative variance meets or exceeds threshold
    mask = df_sorted['cumulative_variance'] >= threshold
    
    if not mask.any():
        # If even all descriptors don't reach threshold, take all
        logger.warning(f"Cumulative variance {df_sorted['cumulative_variance'].iloc[-1]:.4f} "
                     f"did not reach threshold {threshold}. Selecting all descriptors.")
        selected_df = df_sorted
    else:
        cutoff_idx = mask.idxmax()
        selected_df = df_sorted.loc[:cutoff_idx]
    
    selected_descriptors = selected_df['descriptor'].tolist()
    selected_scores = selected_df['variance_explained'].tolist()
    final_cumulative = selected_df['cumulative_variance'].iloc[-1]
    
    return selected_descriptors, selected_scores, final_cumulative

def save_descriptor_subset(
    descriptors: List[str],
    scores: List[float],
    cumulative_variance: float,
    threshold: float,
    output_path: Path
) -> None:
    """
    Save the selected descriptor subset to a JSON file.
    """
    output_dir = output_path.parent
    output_dir.mkdir(parents=True, exist_ok=True)
    
    result = {
        "threshold": threshold,
        "cumulative_variance_achieved": float(cumulative_variance),
        "num_descriptors_selected": len(descriptors),
        "selected_descriptors": [
            {
                "name": name,
                "variance_explained": float(score)
            }
            for name, score in zip(descriptors, scores)
        ]
    }
    
    with open(output_path, 'w') as f:
        json.dump(result, f, indent=2)
    
    logger.info(f"Saved {len(descriptors)} descriptors to {output_path}")
    log_metric("descriptor_subset_size", len(descriptors))
    log_metric("cumulative_variance_achieved", cumulative_variance)

def run_descriptor_ranking(
    input_path: Path,
    output_path: Path,
    threshold: float = 0.60
) -> Dict[str, Any]:
    """
    Main logic to rank descriptors and select the subset meeting variance threshold.
    """
    logger.info(f"Loading feature importance results from {input_path}")
    df = load_feature_importance_results(input_path)
    
    logger.info(f"Calculating subset with cumulative variance >= {threshold}")
    descriptors, scores, cumulative_var = select_top_descriptors_subset(
        df, threshold=threshold
    )
    
    logger.info(f"Selected {len(descriptors)} descriptors. "
              f"Cumulative variance: {cumulative_var:.4f}")
    
    save_descriptor_subset(
        descriptors, scores, cumulative_var, threshold, output_path
    )
    
    return {
        "selected_count": len(descriptors),
        "cumulative_variance": cumulative_var,
        "threshold": threshold
    }

def main() -> None:
    """
    Entry point for the descriptor ranking script.
    Reads from data/results/feature_importance.csv and writes to 
    data/results/top_descriptors_subset.json
    """
    # Define paths relative to project root
    project_root = Path(__file__).resolve().parents[3]
    input_path = project_root / "data" / "results" / "feature_importance.csv"
    output_path = project_root / "data" / "results" / "top_descriptors_subset.json"
    
    if not input_path.exists():
        logger.error(f"Input file not found: {input_path}")
        logger.error("Please ensure T033 (feature_importance.py) has been run first.")
        sys.exit(1)
    
    try:
        result = run_descriptor_ranking(input_path, output_path, threshold=0.60)
        logger.info(f"Task completed successfully. Output: {output_path}")
        logger.info(f"Result: {json.dumps(result, indent=2)}")
    except Exception as e:
        logger.exception(f"Error during descriptor ranking: {e}")
        sys.exit(1)

if __name__ == "__main__":
    main()
