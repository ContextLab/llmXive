"""
Contract test for correlation_report.json schema (Task T019).

This test validates that the correlation report generated by User Story 2
adheres to the expected schema defined in the project specifications.

Schema Requirements:
- Root must be a JSON object (dict).
- Must contain 'coefficient' key (float).
- Must contain 'descriptive_statement' key (str).
- Must NOT contain confidence intervals (N=3 constraint).
- Optional: 'method' (str), 'domains' (list), 'notes' (str).
"""
import json
import os
import pytest
from pathlib import Path

# Path to the expected output file
REPORT_PATH = Path("results/correlation_report.json")


class TestCorrelationReportSchema:
    """Contract tests for correlation_report.json structure."""

    def test_file_exists(self):
        """Verify that the correlation report file exists."""
        assert REPORT_PATH.exists(), f"Report file not found: {REPORT_PATH}"

    def test_valid_json(self):
        """Verify that the file contains valid JSON."""
        try:
            with open(REPORT_PATH, "r", encoding="utf-8") as f:
                json.load(f)
        except json.JSONDecodeError as e:
            pytest.fail(f"Invalid JSON in {REPORT_PATH}: {e}")

    def test_root_is_object(self):
        """Verify that the root element is a JSON object (dict)."""
        with open(REPORT_PATH, "r", encoding="utf-8") as f:
            data = json.load(f)
        assert isinstance(data, dict), "Root must be a JSON object (dict)."

    def test_required_coefficient_field(self):
        """Verify presence and type of 'coefficient' field."""
        with open(REPORT_PATH, "r", encoding="utf-8") as f:
            data = json.load(f)

        assert "coefficient" in data, "Missing required field: 'coefficient'"
        assert isinstance(data["coefficient"], (int, float)), (
            "Field 'coefficient' must be a number (int or float)."
        )
        # Spearman correlation is between -1 and 1
        assert -1.0 <= data["coefficient"] <= 1.0, (
            "Coefficient must be between -1.0 and 1.0."
        )

    def test_required_descriptive_statement_field(self):
        """Verify presence and type of 'descriptive_statement' field."""
        with open(REPORT_PATH, "r", encoding="utf-8") as f:
            data = json.load(f)

        assert "descriptive_statement" in data, (
            "Missing required field: 'descriptive_statement'"
        )
        assert isinstance(data["descriptive_statement"], str), (
            "Field 'descriptive_statement' must be a string."
        )
        assert len(data["descriptive_statement"].strip()) > 0, (
            "Field 'descriptive_statement' cannot be empty."
        )

    def test_no_confidence_intervals(self):
        """
        Verify that confidence intervals are NOT present.
        Constraint: Since N=3 (three domains), CIs are not calculated.
        """
        with open(REPORT_PATH, "r", encoding="utf-8") as f:
            data = json.load(f)

        forbidden_keys = ["ci_lower", "ci_upper", "confidence_interval", "p_value"]
        for key in forbidden_keys:
            assert key not in data, (
                f"Forbidden field '{key}' found. N=3 constraint prohibits CIs."
            )

    def test_optional_fields_types(self):
        """Verify types of optional fields if present."""
        with open(REPORT_PATH, "r", encoding="utf-8") as f:
            data = json.load(f)

        if "method" in data:
            assert isinstance(data["method"], str), "'method' must be a string."

        if "domains" in data:
            assert isinstance(data["domains"], list), "'domains' must be a list."
            for item in data["domains"]:
                assert isinstance(item, str), "Items in 'domains' must be strings."

        if "notes" in data:
            assert isinstance(data["notes"], str), "'notes' must be a string."