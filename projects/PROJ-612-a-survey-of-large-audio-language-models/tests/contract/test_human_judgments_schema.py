"""
Contract test for human_judgments.csv schema.

This test verifies that the human_judgments.csv file generated by the 
crowdsourcing retrieval pipeline (T029) adheres to the expected schema.

Expected Schema:
- sample_id: string (non-empty)
- domain: string (one of 'Speech', 'Music', 'Env')
- model_name: string (non-empty)
- caption_text: string (non-empty)
- is_hallucination: boolean (0/1 or True/False)
- worker_id: string (non-empty)
- timestamp: string (ISO format or similar)
"""

import csv
import os
import pytest
from pathlib import Path
from typing import List, Dict, Any

# Expected output path based on project structure
OUTPUT_PATH = Path("data/human_judgments.csv")

REQUIRED_COLUMNS = [
    "sample_id",
    "domain",
    "model_name",
    "caption_text",
    "is_hallucination",
    "worker_id",
    "timestamp"
]

VALID_DOMAINS = {"Speech", "Music", "Env"}

def read_csv_rows(filepath: Path) -> List[Dict[str, Any]]:
    """Helper to read CSV file into list of dicts."""
    if not filepath.exists():
        return []
    with open(filepath, "r", encoding="utf-8") as f:
        reader = csv.DictReader(f)
        return list(reader)

def test_schema_file_exists():
    """Test that the output file exists."""
    # Note: In a real CI run, this file might not exist yet if T029 hasn't run.
    # However, the contract test ensures that IF it exists, it matches the schema.
    # For this test to be meaningful in isolation, we might check if the file
    # exists or if the schema definition is correct.
    # We will assert existence to ensure the pipeline produces the file.
    assert OUTPUT_PATH.exists(), f"Expected output file {OUTPUT_PATH} does not exist. Run T029 first."

def test_schema_has_required_columns():
    """Test that the CSV contains all required columns."""
    with open(OUTPUT_PATH, "r", encoding="utf-8") as f:
        reader = csv.DictReader(f)
        headers = reader.fieldnames
    
    assert headers is not None, "CSV file is empty or has no headers."
    
    missing_cols = set(REQUIRED_COLUMNS) - set(headers)
    assert len(missing_cols) == 0, f"Missing required columns: {missing_cols}"

def test_schema_column_types_and_values():
    """Test that values in columns match expected types and valid ranges."""
    rows = read_csv_rows(OUTPUT_PATH)
    
    assert len(rows) > 0, "CSV file has no data rows."
    
    for row_idx, row in enumerate(rows):
        # sample_id: non-empty string
        assert row["sample_id"] and isinstance(row["sample_id"], str), \
            f"Row {row_idx}: sample_id must be a non-empty string."
        
        # domain: must be one of valid domains
        domain = row["domain"]
        assert domain in VALID_DOMAINS, \
            f"Row {row_idx}: Invalid domain '{domain}'. Must be one of {VALID_DOMAINS}."
        
        # model_name: non-empty string
        assert row["model_name"] and isinstance(row["model_name"], str), \
            f"Row {row_idx}: model_name must be a non-empty string."
        
        # caption_text: non-empty string
        assert row["caption_text"] and isinstance(row["caption_text"], str), \
            f"Row {row_idx}: caption_text must be a non-empty string."
        
        # is_hallucination: boolean (0/1 or True/False)
        val = row["is_hallucination"]
        valid_bools = {"0", "1", "True", "False", "true", "false"}
        assert val in valid_bools, \
            f"Row {row_idx}: is_hallucination must be 0/1/True/False, got '{val}'."
        
        # worker_id: non-empty string
        assert row["worker_id"] and isinstance(row["worker_id"], str), \
            f"Row {row_idx}: worker_id must be a non-empty string."
        
        # timestamp: non-empty string (format validation could be stricter)
        assert row["timestamp"] and isinstance(row["timestamp"], str), \
            f"Row {row_idx}: timestamp must be a non-empty string."