logger = logging.getLogger(__name__)

# Constants
HALLUCINATION_RATES_PATH = Path("results/hallucination_rates.csv")
HUMAN_JUDGMENTS_PATH = Path("results/human_judgments.csv")
COHEN_KAPPA_OUTPUT_PATH = Path("results/cohen_kappa_report.json")
LOW_AGREEMENT_THRESHOLD = 0.6

def load_hallucination_rates(path: Path = HALLUCINATION_RATES_PATH) -> List[Dict[str, Any]]:
    """
    Load hallucination rates from the CSV file generated by T017b.
    Returns a list of dictionaries with keys: domain, rate, ci_lower, ci_upper.
    """
    if not path.exists():
        raise FileNotFoundError(f"Hallucination rates file not found at {path}")
    
    results = []
    with open(path, 'r', newline='', encoding='utf-8') as f:
        reader = csv.DictReader(f)
        for row in reader:
            results.append({
                'domain': row['domain'],
                'rate': float(row['rate']),
                'ci_lower': float(row['ci_lower']),
                'ci_upper': float(row['ci_upper'])
            })
    return results

def load_human_judgments(path: Path = HUMAN_JUDGMENTS_PATH) -> List[Dict[str, Any]]:
    """
    Load human judgments from the CSV file generated by T029.
    Expected columns: sample_id, model_name, domain, caption, is_hallucinated (binary 0/1), human_label (0/1)
    """
    if not path.exists():
        raise FileNotFoundError(f"Human judgments file not found at {path}")
    
    judgments = []
    with open(path, 'r', newline='', encoding='utf-8') as f:
        reader = csv.DictReader(f)
        for row in reader:
            # Ensure we have the necessary fields
            if 'human_label' not in row or 'is_hallucinated' not in row:
                logger.warning(f"Skipping row due to missing labels: {row}")
                continue
            
            judgments.append({
                'sample_id': row.get('sample_id', ''),
                'model_name': row.get('model_name', ''),
                'domain': row.get('domain', ''),
                'caption': row.get('caption', ''),
                'algorithm_label': int(row['is_hallucinated']),
                'human_label': int(row['human_label'])
            })
    return judgments

def calculate_cohens_kappa(judgments: List[Dict[str, Any]]) -> Tuple[float, Dict[str, int]]:
    """
    Calculate Cohen's Kappa coefficient between algorithm labels and human labels.
    
    Formula: kappa = (Po - Pe) / (1 - Pe)
    where:
      Po = observed agreement
      Pe = expected agreement by chance
    
    Returns:
      Tuple of (kappa_value, confusion_counts)
    """
    if not judgments:
        logger.error("No judgments provided for Cohen's Kappa calculation.")
        return 0.0, {'n00': 0, 'n01': 0, 'n10': 0, 'n11': 0}

    # Count agreements
    n00 = 0 # Both 0 (No hallucination)
    n01 = 0 # Algo 0, Human 1
    n10 = 0 # Algo 1, Human 0
    n11 = 0 # Both 1 (Hallucination)

    for j in judgments:
        algo = j['algorithm_label']
        human = j['human_label']
        
        if algo == 0 and human == 0:
            n00 += 1
        elif algo == 0 and human == 1:
            n01 += 1
        elif algo == 1 and human == 0:
            n10 += 1
        elif algo == 1 and human == 1:
            n11 += 1

    total = n00 + n01 + n10 + n11
    if total == 0:
        return 0.0, {'n00': 0, 'n01': 0, 'n10': 0, 'n11': 0}

    # Observed agreement (Po)
    Po = (n00 + n11) / total

    # Expected agreement (Pe)
    # Row totals
    row0 = n00 + n01
    row1 = n10 + n11
    # Col totals
    col0 = n00 + n10
    col1 = n01 + n11

    p0 = (row0 / total) * (col0 / total)
    p1 = (row1 / total) * (col1 / total)
    Pe = p0 + p1

    if math.isclose(Pe, 1.0):
        # Avoid division by zero if chance agreement is 100%
        logger.warning("Expected agreement is 1.0, Kappa is undefined.")
        return 0.0, {'n00': n00, 'n01': n01, 'n10': n10, 'n11': n11}

    kappa = (Po - Pe) / (1 - Pe)
    
    return kappa, {'n00': n00, 'n01': n01, 'n10': n10, 'n11': n11}

def generate_kappa_report(kappa: float, counts: Dict[str, int], low_agreement: bool) -> Dict[str, Any]:
    """
    Generate the report dictionary for the Kappa analysis.
    """
    return {
        "kappa_coefficient": round(kappa, 4),
        "agreement_counts": counts,
        "low_agreement_flag": low_agreement,
        "threshold": LOW_AGREEMENT_THRESHOLD,
        "interpretation": "Low agreement" if low_agreement else "Acceptable agreement"
    }

def main():
    """
    Main entry point for T030: Implement Cohen’s κ calculation and low-agreement flagging.
    
    1. Load human judgments from results/human_judgments.csv.
    2. Calculate Cohen's Kappa between algorithm labels and human labels.
    3. Flag if kappa < 0.6.
    4. Write report to results/cohen_kappa_report.json.
    """
    logger.info("Starting T030: Cohen's Kappa Calculation")
    
    try:
        # Load data
        judgments = load_human_judgments(HUMAN_JUDGMENTS_PATH)
        logger.info(f"Loaded {len(judgments)} human judgments.")
        
        if len(judgments) == 0:
            logger.error("No valid judgments found. Cannot calculate Kappa.")
            # Write a minimal error report
            report = {
                "kappa_coefficient": 0.0,
                "agreement_counts": {'n00': 0, 'n01': 0, 'n10': 0, 'n11': 0},
                "low_agreement_flag": True,
                "threshold": LOW_AGREEMENT_THRESHOLD,
                "interpretation": "Error: No data available",
                "error": "No valid judgments found in input file."
            }
            with open(COHEN_KAPPA_OUTPUT_PATH, 'w', encoding='utf-8') as f:
                json.dump(report, f, indent=2)
            return 1

        # Calculate Kappa
        kappa, counts = calculate_cohens_kappa(judgments)
        logger.info(f"Calculated Cohen's Kappa: {kappa:.4f}")
        
        # Determine low agreement flag
        low_agreement = kappa < LOW_AGREEMENT_THRESHOLD
        logger.info(f"Low agreement flag: {low_agreement} (Threshold: {LOW_AGREEMENT_THRESHOLD})")
        
        # Generate report
        report = generate_kappa_report(kappa, counts, low_agreement)
        
        # Ensure output directory exists
        COHEN_KAPPA_OUTPUT_PATH.parent.mkdir(parents=True, exist_ok=True)
        
        # Write report
        with open(COHEN_KAPPA_OUTPUT_PATH, 'w', encoding='utf-8') as f:
            json.dump(report, f, indent=2)
        
        logger.info(f"Report written to {COHEN_KAPPA_OUTPUT_PATH}")
        
        if low_agreement:
            logger.warning(f"Kappa ({kappa:.4f}) is below threshold ({LOW_AGREEMENT_THRESHOLD}). Human validation indicates low agreement with algorithm.")
        
        return 0

    except Exception as e:
        logger.error(f"Error during T030 execution: {e}", exc_info=True)
        return 1

if __name__ == "__main__":
    sys.exit(main())