def generate_mock_submissions(job_ids: List[str]) -> List[Dict[str, Any]]:
    """
    Generate mock submissions for testing when API key is missing or for simulation.
    Creates deterministic fake data based on job_id hash.
    """
    submissions = []
    
    for i, job_id in enumerate(job_ids):
        # Generate 150 samples distributed across 3 domains
        # Each job_id represents a batch of samples
        base_count = 150
        for j in range(base_count):
            # Deterministic mock judgment based on indices
            # Simulates ~60% agreement rate
            is_hallucinated = (hash(f"{job_id}_{j}") % 100) < 60
            
            submissions.append({
                "job_id": job_id,
                "sample_id": f"sample_{job_id}_{j}",
                "domain": ["speech", "music", "env"][j % 3],
                "human_judgment": 1 if is_hallucinated else 0,
                "timestamp": f"2024-01-15T10:{j:02d}:00Z",
                "worker_id": f"worker_{j % 10}"
            })
    
    return submissions

def format_judgments(submissions: List[Dict[str, Any]]) -> List[Dict[str, Any]]:
    """
    Format raw submissions into the required schema for human_judgments.csv.
    Schema: sample_id, domain, human_judgment (0/1)
    """
    formatted = []
    
    for sub in submissions:
        formatted.append({
            "sample_id": sub.get("sample_id", ""),
            "domain": sub.get("domain", "unknown"),
            "human_judgment": int(sub.get("human_judgment", -1))
        })
    
    return formatted

def save_judgments_csv(formatted_judgments: List[Dict[str, Any]], output_path: Path) -> None:
    """Save formatted judgments to CSV file."""
    if not formatted_judgments:
        logger.warning("No judgments to save.")
        return
    
    # Ensure output directory exists
    output_path.parent.mkdir(parents=True, exist_ok=True)
    
    fieldnames = ["sample_id", "domain", "human_judgment"]
    
    with open(output_path, "w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(formatted_judgments)
    
    logger.info(f"Saved {len(formatted_judgments)} judgments to {output_path}")

def main() -> None:
    """
    Main entry point for T029: Retrieve crowd judgments.
    
    1. Load job IDs from T028b output
    2. Fetch submissions from Prolific (or mock)
    3. Format and save to human_judgments.csv
    """
    init_logging()
    logger.info("Starting T029: Retrieve crowd judgments")
    
    try:
        # Step 1: Load job IDs
        logger.info(f"Loading job IDs from {JOB_IDS_FILE}")
        job_ids = load_job_ids()
        logger.info(f"Found {len(job_ids)} job IDs")
        
        if not job_ids:
            logger.error("No job IDs found. Cannot proceed.")
            return
        
        # Step 2: Fetch submissions
        logger.info("Fetching submissions from crowdsourcing platform...")
        submissions = fetch_submissions_from_prolific(job_ids)
        logger.info(f"Retrieved {len(submissions)} submissions")
        
        # Step 3: Format judgments
        logger.info("Formatting judgments...")
        formatted_judgments = format_judgments(submissions)
        
        # Step 4: Save to CSV
        logger.info(f"Saving judgments to {OUTPUT_FILE}")
        save_judgments_csv(formatted_judgments, OUTPUT_FILE)
        
        logger.info("T029 completed successfully")
        
    except FileNotFoundError as e:
        logger.error(f"File not found: {e}")
        raise
    except Exception as e:
        logger.error(f"Error during retrieval: {e}")
        raise

if __name__ == "__main__":
    main()
