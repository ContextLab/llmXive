"""
T024: Create expression CSV with normalized TPM/FPKM values.

Logic:
1. Load raw expression data from data/raw/ (generated by T021/T022).
2. Load pairing report from logs/data_pairing.json to identify valid sample IDs.
3. Prefer TPM if available in source.
4. If TPM missing but raw counts present, calculate TPM:
   - TPM = (ReadsPerKilobase / Sum(RPK)) * 1,000,000
   - RPK = RawCounts / (GeneLength_kb)
5. If neither available, log error and skip sample.
6. Output: data/processed/expression_matrix.csv with columns: sample_id, gene_id, value (TPM).

Dependencies:
- data_download.py (for raw data structure assumption)
- logging_utils.py (for error logging)
- exceptions.py (for E-DATASET)
"""

import csv
import json
import logging
import sys
from pathlib import Path
from typing import Dict, List, Optional, Any

# Add project root to path for imports if run as script
project_root = Path(__file__).resolve().parent.parent
if str(project_root) not in sys.path:
    sys.path.insert(0, str(project_root))

from code.logging_utils import log_data_pairing_mismatch
from code.exceptions import E_DATASET

# Configure logging
logging.basicConfig(
    level=logging.INFO,
    format='%(asctime)s - %(name)s - %(levelname)s - %(message)s',
    handlers=[
        logging.FileHandler(project_root / 'logs' / 't024_expression_creation.log'),
        logging.StreamHandler(sys.stdout)
    ]
)
logger = logging.getLogger(__name__)

DATA_RAW_DIR = project_root / 'data' / 'raw'
DATA_PROCESSED_DIR = project_root / 'data' / 'processed'
LOGS_DIR = project_root / 'logs'
PAIRING_REPORT_PATH = LOGS_DIR / 'data_pairing.json'

# Output file path as per task requirements
OUTPUT_EXPRESSION_MATRIX = DATA_PROCESSED_DIR / 'expression_matrix.csv'

# Ensure output directory exists
DATA_PROCESSED_DIR.mkdir(parents=True, exist_ok=True)
LOGS_DIR.mkdir(parents=True, exist_ok=True)

def load_pairing_report() -> Dict[str, Any]:
    """Load the pairing report generated by T023/T027."""
    if not PAIRING_REPORT_PATH.exists():
        raise FileNotFoundError(f"Pairing report not found at {PAIRING_REPORT_PATH}. "
                                "Ensure T023 and T027 have completed successfully.")
    with open(PAIRING_REPORT_PATH, 'r') as f:
        return json.load(f)

def load_raw_expression_data() -> List[Dict[str, Any]]:
    """
    Load raw expression data from data/raw/.
    Assumes T021 has populated this directory with JSON/CSV files per study.
    Structure expected: List of samples with 'sample_id', 'gene_id', 'counts' (or 'tpm'), 'length_kb'.
    """
    raw_data = []
    if not DATA_RAW_DIR.exists():
        raise FileNotFoundError(f"Raw data directory {DATA_RAW_DIR} does not exist. "
                                "Run T021 to download GEO data first.")
    
    # Scan for expression files (assumed JSON for flexibility, or CSV)
    # In a real pipeline, this would iterate specific files from T021
    for file_path in DATA_RAW_DIR.glob('*expression*.json'):
        logger.info(f"Loading expression data from {file_path}")
        with open(file_path, 'r') as f:
            data = json.load(f)
            if isinstance(data, list):
                raw_data.extend(data)
            elif isinstance(data, dict) and 'samples' in data:
                raw_data.extend(data['samples'])
    
    if not raw_data:
        # Fallback: check for CSV if JSON not found (common in GEO SOFT)
        for file_path in DATA_RAW_DIR.glob('*expression*.csv'):
            logger.info(f"Loading expression data from {file_path}")
            with open(file_path, 'r', newline='') as f:
                reader = csv.DictReader(f)
                for row in reader:
                    # Normalize keys if necessary
                    raw_data.append(row)
                    
    if not raw_data:
        raise E_DATASET(f"No expression data found in {DATA_RAW_DIR}. "
                        "Cannot proceed with T024 without input data.")
                        
    return raw_data

def calculate_tpm(raw_counts: float, gene_length_kb: float, total_rpk: float) -> float:
    """Calculate TPM from raw counts and gene length."""
    if gene_length_kb <= 0:
        return 0.0
    rpk = raw_counts / gene_length_kb
    if total_rpk == 0:
        return 0.0
    return (rpk / total_rpk) * 1_000_000

def process_expression_data(raw_data: List[Dict[str, Any]], paired_sample_ids: set) -> List[Dict[str, Any]]:
    """
    Process raw data to create normalized TPM matrix.
    Rules:
    1. Filter to only paired samples.
    2. Prefer TPM if available.
    3. If TPM missing, calculate from counts + length.
    4. If neither, log error and skip.
    """
    processed_rows = []
    skipped_samples = set()
    
    # Group by sample_id to calculate per-sample normalization factors (Total RPK)
    # Structure: { sample_id: { gene_id: (counts, length_kb), ... } }
    sample_gene_map = {}
    
    for row in raw_data:
        sample_id = row.get('sample_id') or row.get('geo_accession')
        if not sample_id:
            logger.warning(f"Skipping row missing sample_id: {row}")
            continue
            
        if sample_id not in paired_sample_ids:
            continue # Skip unpaired samples
            
        gene_id = row.get('gene_id') or row.get('probe_id')
        if not gene_id:
            continue
            
        if sample_id not in sample_gene_map:
            sample_gene_map[sample_id] = {}
            
        sample_gene_map[sample_id][gene_id] = row
        
    # Calculate TPM for each sample
    for sample_id, genes in sample_gene_map.items():
        # Check if we have TPM or Counts+Length
        has_tpm = any('tpm' in g or 'TPM' in g for g in genes.values())
        has_counts = any('counts' in g or 'raw_count' in g for g in genes.values())
        has_length = any('length_kb' in g or 'gene_length' in g for g in genes.values())
        
        if has_tpm:
            # Prefer TPM: extract and normalize if needed (assume already normalized, just extract)
            for gene_id, data in genes.items():
                tpm_val = data.get('tpm') or data.get('TPM') or 0.0
                try:
                    tpm_val = float(tpm_val)
                except (ValueError, TypeError):
                    tpm_val = 0.0
                processed_rows.append({
                    'sample_id': sample_id,
                    'gene_id': gene_id,
                    'value': tpm_val,
                    'source': 'tpm'
                })
        elif has_counts and has_length:
            # Calculate TPM
            # First, calculate RPK for all genes in this sample
            rpk_values = {}
            for gene_id, data in genes.items():
                counts = data.get('counts') or data.get('raw_count') or 0
                length = data.get('length_kb') or data.get('gene_length') or 0
                try:
                    counts = float(counts)
                    length = float(length)
                except (ValueError, TypeError):
                    continue
                
                if length > 0:
                    rpk_values[gene_id] = counts / length
                
            total_rpk = sum(rpk_values.values())
            
            if total_rpk == 0:
                logger.warning(f"Sample {sample_id}: Total RPK is 0. Skipping TPM calculation.")
                skipped_samples.add(sample_id)
                continue
                
            for gene_id, rpk in rpk_values.items():
                tpm = (rpk / total_rpk) * 1_000_000
                processed_rows.append({
                    'sample_id': sample_id,
                    'gene_id': gene_id,
                    'value': tpm,
                    'source': 'calculated_tpm'
                })
        else:
            # Neither TPM nor (Counts + Length) available
            logger.error(f"Sample {sample_id}: Missing required data for normalization. "
                         f"Has TPM: {has_tpm}, Has Counts: {has_counts}, Has Length: {has_length}")
            skipped_samples.add(sample_id)
            # Log to data_pairing.json as per spec edge case handling? 
            # Spec says "log error and skip sample". We'll log to main log and potentially update pairing log.
            log_data_pairing_mismatch(
                sample_id=sample_id,
                expression_source="raw_data",
                metabolite_source="unknown",
                reason="no_normalization_data_available"
            )
            
    logger.info(f"Processed {len(processed_rows)} expression values.")
    logger.info(f"Skipped {len(skipped_samples)} samples due to missing data.")
    
    return processed_rows

def write_expression_matrix(rows: List[Dict[str, Any]], output_path: Path):
    """Write the final expression matrix to CSV."""
    logger.info(f"Writing expression matrix to {output_path}")
    
    with open(output_path, 'w', newline='') as f:
        writer = csv.DictWriter(f, fieldnames=['sample_id', 'gene_id', 'value'])
        writer.writeheader()
        writer.writerows(rows)
        
def main():
    logger.info("Starting T024: Create Expression Matrix")
    
    try:
        # 1. Load pairing report to get valid sample IDs
        pairing_report = load_pairing_report()
        # The report structure from T023/T027 should have a 'paired_samples' or similar list
        # Assuming structure: {'paired_samples': [{'sample_id': '...'}, ...]} or similar
        # Adjust based on actual T023 output structure.
        # If T027 validated, we expect a list of paired IDs.
        
        paired_ids = set()
        if 'paired_samples' in pairing_report:
            for item in pairing_report['paired_samples']:
                if isinstance(item, dict) and 'sample_id' in item:
                    paired_ids.add(item['sample_id'])
                elif isinstance(item, str):
                    paired_ids.add(item)
        elif 'sample_ids' in pairing_report:
             paired_ids = set(pairing_report['sample_ids'])
        
        if not paired_ids:
            raise E_DATASET("No paired samples found in pairing report. T027 must pass first.")
        
        logger.info(f"Found {len(paired_ids)} paired samples to process.")
        
        # 2. Load raw data
        raw_data = load_raw_expression_data()
        logger.info(f"Loaded {len(raw_data)} raw records.")
        
        # 3. Process and normalize
        processed_rows = process_expression_data(raw_data, paired_ids)
        
        if not processed_rows:
            raise E_DATASET("No expression data could be processed after normalization logic.")
        
        # 4. Write output
        write_expression_matrix(processed_rows, OUTPUT_EXPRESSION_MATRIX)
        
        logger.info("T024 completed successfully.")
        
    except Exception as e:
        logger.error(f"T024 failed: {e}", exc_info=True)
        if isinstance(e, E_DATASET):
            raise
        raise E_DATASET(f"Failed to create expression matrix: {e}") from e

if __name__ == '__main__':
    main()