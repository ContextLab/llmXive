"""
Calculate Compression Ratio and Fidelity Loss for the Trade-off Curve.

This script iterates through the rule sets generated by T027a (sweep_thresholds),
runs the CompressedAgent on a sample of held-out traces for each rule set,
and computes the Compression Ratio and Fidelity Loss.

Output: data/processed/trade_off_curve.csv
"""
import csv
import json
import os
import sys
import time
from pathlib import Path
from typing import Dict, Any, List, Optional, Tuple

# Add project root to path for imports
PROJECT_ROOT = Path(__file__).resolve().parents[2]
sys.path.insert(0, str(PROJECT_ROOT))

from config import get_config
from utils.loaders import TraceLoader
from agents.compressed import CompressedAgent


class CompressionRatioCalculator:
    """Calculates compression ratios and fidelity losses for trade-off analysis."""

    def __init__(self, config: Dict[str, Any]):
        self.config = config
        self.held_out_dir = Path(config['data_paths']['held_out'])
        self.sweep_rules_dir = Path(config['data_paths']['processed']) / 'rules' / 'sweeps'
        self.output_path = Path(config['data_paths']['processed']) / 'trade_off_curve.csv'
        
        # Ensure output directory exists
        self.output_path.parent.mkdir(parents=True, exist_ok=True)

    def load_sweep_results(self) -> List[Dict[str, Any]]:
        """
        Load all rule sets generated by the sweep (T027a).
        Returns a list of dicts containing 'threshold', 'rules' (path or content), and 'original_size'.
        """
        if not self.sweep_rules_dir.exists():
            raise FileNotFoundError(
                f"Sweep rules directory not found: {self.sweep_rules_dir}. "
                "Please ensure T027a (sweep_thresholds) has been run successfully."
            )

        sweep_configs = []
        for file_path in self.sweep_rules_dir.glob("*.json"):
            with open(file_path, 'r') as f:
                data = json.load(f)
            
            # The sweep output typically contains metadata about the threshold and the rules
            # We expect the file to contain the pruned rules and the threshold used
            threshold = data.get('threshold', data.get('min_support', 0))
            rules = data.get('rules', [])
            original_size = data.get('original_rule_count', len(rules)) # Fallback if not stored
            
            # If the file stores the full rule set, we use it directly.
            # If it stores a reference, we would load it here. Assuming direct content for now.
            sweep_configs.append({
                'threshold': threshold,
                'rules': rules,
                'file_path': str(file_path),
                'original_size': original_size
            })
        
        if not sweep_configs:
            raise ValueError(f"No sweep rule files found in {self.sweep_rules_dir}")
        
        return sweep_configs

    def load_sample_traces(self, sample_size: int = 50) -> List[Dict[str, Any]]:
        """
        Load a sample of traces from the held-out set for benchmarking.
        """
        loader = TraceLoader(self.held_out_dir)
        traces = list(loader.load_all())
        
        if not traces:
            raise FileNotFoundError(f"No traces found in {self.held_out_dir}")
        
        if len(traces) > sample_size:
            # Deterministic sample based on config seed
            import random
            seed = self.config.get('seed', 42)
            random.seed(seed)
            traces = random.sample(traces, sample_size)
        
        return traces

    def calculate_compression_ratio(self, original_count: int, pruned_count: int) -> float:
        """
        Calculate the compression ratio.
        Ratio = Pruned Size / Original Size. Lower is better compression.
        """
        if original_count == 0:
            return 1.0
        return pruned_count / original_count

    def run_agent_evaluation(self, agent: CompressedAgent, traces: List[Dict[str, Any]]) -> Tuple[float, int]:
        """
        Run the agent on the traces and calculate fidelity (accuracy).
        Returns (accuracy, count_of_correct).
        """
        correct_count = 0
        total_count = len(traces)
        
        if total_count == 0:
            return 0.0, 0

        for trace in traces:
            try:
                # The agent's run method should take the trace context and return a result
                # We assume the trace contains 'input' and 'expected_output' or similar structure
                # based on the synthetic generation schema.
                # For the purpose of this task, we simulate the fidelity check:
                # Fidelity is defined as the agent successfully reproducing the 'final_state'
                # or matching the expected edit sequence.
                
                # Simplified logic: Check if the agent produces a valid output structure
                # In a real scenario, this would compare agent_output vs trace['final_state']
                result = agent.run(trace)
                
                # Assume 'result' has a 'success' flag or we compare edit sequences
                # Here we assume the agent returns a dict with 'edit_sequence'
                if result and 'edit_sequence' in result:
                    # Check if the edit sequence matches the expected one in the trace
                    expected = trace.get('exact_tool_sequence', [])
                    generated = result['edit_sequence']
                    
                    if generated == expected:
                        correct_count += 1
                else:
                    # If the agent fails to produce the expected structure, it's a mismatch
                    pass
                    
            except Exception as e:
                # Agent failed to execute, count as error
                pass

        accuracy = correct_count / total_count if total_count > 0 else 0.0
        return accuracy, correct_count

    def calculate_trade_off(self) -> List[Dict[str, Any]]:
        """
        Main logic: Iterate through sweep thresholds, run agent, compute metrics.
        """
        results = []
        sweep_configs = self.load_sweep_results()
        sample_traces = self.load_sample_traces(sample_size=self.config.get('sweep_sample_size', 50))
        
        # Initialize the agent once, but we need to reload rules for each iteration
        # Or pass rules to the agent. The CompressedAgent typically loads from a file.
        # We will modify the agent to accept rules in memory or write temp files.
        # To avoid file I/O overhead in a loop, we'll inject rules into the agent instance.
        
        base_agent = CompressedAgent(self.config)
        
        # We need to know the baseline fidelity (using the full global rule set)
        # to calculate "Fidelity Loss".
        # Load the global rules (from T026b) to establish the 100% fidelity baseline.
        global_rules_path = Path(self.config['data_paths']['processed']) / 'rules' / 'global_rules.json'
        if not global_rules_path.exists():
            raise FileNotFoundError(f"Global rules file not found: {global_rules_path}. Run T026b first.")
        
        with open(global_rules_path, 'r') as f:
            global_rules_data = json.load(f)
            global_rules = global_rules_data.get('rules', [])
        
        # Calculate Baseline Fidelity
        base_agent.rules = global_rules
        baseline_acc, _ = self.run_agent_evaluation(base_agent, sample_traces)
        
        if baseline_acc == 0:
            # If baseline is 0, we can't calculate loss meaningfully, but we proceed.
            pass

        for sweep_item in sweep_configs:
            threshold = sweep_item['threshold']
            pruned_rules = sweep_item['rules']
            original_size = sweep_item['original_size']
            
            # Update agent with pruned rules
            base_agent.rules = pruned_rules
            
            # Run evaluation
            pruned_acc, _ = self.run_agent_evaluation(base_agent, sample_traces)
            
            # Calculate metrics
            compression_ratio = self.calculate_compression_ratio(original_size, len(pruned_rules))
            fidelity_loss = baseline_acc - pruned_acc
            
            results.append({
                'threshold': threshold,
                'compression_ratio': compression_ratio,
                'fidelity_loss': fidelity_loss,
                'baseline_accuracy': baseline_acc,
                'pruned_accuracy': pruned_acc,
                'rule_count': len(pruned_rules),
                'original_rule_count': original_size
            })
        
        return results

    def save_results(self, results: List[Dict[str, Any]]):
        """Save results to CSV."""
        if not results:
            raise ValueError("No results to save.")

        fieldnames = [
            'threshold', 'compression_ratio', 'fidelity_loss', 
            'baseline_accuracy', 'pruned_accuracy', 'rule_count', 'original_rule_count'
        ]

        with open(self.output_path, 'w', newline='') as f:
            writer = csv.DictWriter(f, fieldnames=fieldnames)
            writer.writeheader()
            writer.writerows(results)

        print(f"Trade-off curve saved to: {self.output_path}")


def calculate_compression_ratios(config: Optional[Dict[str, Any]] = None):
    """Entry point for calculating compression ratios."""
    if config is None:
        config = get_config()
    
    calculator = CompressionRatioCalculator(config)
    results = calculator.calculate_trade_off()
    calculator.save_results(results)
    return results


def main():
    """Main entry point for the script."""
    config = get_config()
    print("Starting compression ratio calculation for trade-off curve...")
    try:
        results = calculate_compression_ratios(config)
        print(f"Successfully calculated {len(results)} data points.")
    except FileNotFoundError as e:
        print(f"Error: {e}")
        sys.exit(1)
    except Exception as e:
        print(f"Unexpected error during calculation: {e}")
        sys.exit(1)


if __name__ == "__main__":
    main()
