"""
Rule Induction Module for MemSlides Compressibility Analysis.

Implements global rule induction using a Decision Tree classifier to predict
trace outcomes based on structural metrics, calculates per-trace compressibility
scores, and evaluates model fidelity on held-out data.
"""

