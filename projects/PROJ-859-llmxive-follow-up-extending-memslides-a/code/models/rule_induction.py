class DataLoadError(Exception):
    """Raised when required data files are missing or malformed."""
    pass


class ModelTrainingError(Exception):
    """Raised when model training or evaluation fails."""
    pass


class PerTraceRuleInducer:
    """
    Performs rule induction on structural metrics to derive compressibility scores.

    This class loads the feature matrix generated by T020, splits it into
    training and held-out sets based on the source directory metadata,
    trains a Decision Tree, and calculates compressibility scores for the
    held-out set.
    """

    def __init__(self, config: Dict[str, Any]):
        self.config = config
        self.feature_matrix_path = Path(config['paths']['feature_matrix'])
        self.output_dir = Path(config['paths']['processed'])
        self.rules_dir = self.output_dir / 'rules'
        self.threshold = config['models']['rule_induction']['fidelity_threshold']
        self.max_depth = config['models']['rule_induction']['max_depth']
        self.min_samples_split = config['models']['rule_induction']['min_samples_split']
        
        # Ensure output directories exist
        self.output_dir.mkdir(parents=True, exist_ok=True)
        self.rules_dir.mkdir(parents=True, exist_ok=True)

    def _load_feature_matrix(self) -> pd.DataFrame:
        """
        Loads the feature matrix and validates its existence and content.
        
        Raises:
            DataLoadError: If the file is missing or empty.
        """
        if not self.feature_matrix_path.exists():
            raise DataLoadError(
                f"Feature matrix not found at {self.feature_matrix_path}. "
                "Run code/metrics/extract.py (T020) first."
            )
        
        try:
            df = pd.read_csv(self.feature_matrix_path)
            if df.empty:
                raise DataLoadError("Feature matrix is empty.")
            
            # Validate required columns exist
            required_cols = [
                'trace_id', 'sequence_entropy', 'tool_repetition_frequency', 
                'argument_variance', 'source_split', 'final_state_hash'
            ]
            missing_cols = [col for col in required_cols if col not in df.columns]
            if missing_cols:
                raise DataLoadError(f"Missing required columns: {missing_cols}")
            
            return df
        except Exception as e:
            raise DataLoadError(f"Failed to load feature matrix: {e}")

    def _prepare_data(self, df: pd.DataFrame) -> Tuple[np.ndarray, np.ndarray, np.ndarray, np.ndarray, List[str], List[str]]:
        """
        Prepares data for training and evaluation.
        
        Returns:
            Tuple of (X_train, X_test, y_train, y_test, train_ids, test_ids)
        """
        # Filter for valid numeric data
        feature_cols = ['sequence_entropy', 'tool_repetition_frequency', 'argument_variance']
        
        # Handle potential NaNs in features
        df = df.dropna(subset=feature_cols + ['final_state_hash'])
        
        if len(df) < 10:
            raise DataLoadError(
                f"Insufficient valid data points for training after cleaning. "
                f"Found {len(df)} rows."
            )

        # Split based on 'source_split' column (training vs held_out)
        train_df = df[df['source_split'] == 'training']
        test_df = df[df['source_split'] == 'held_out']

        if train_df.empty or test_df.empty:
            # Fallback: if split column is missing or inconsistent, do random split
            # But we strictly follow T012 split logic which uses source_split
            raise DataLoadError(
                "Training or Held-out split is empty in feature matrix. "
                "Ensure T012 correctly set 'source_split' in trace metadata."
            )

        X = df[feature_cols].values
        y = df['final_state_hash'].values  # Using hash as categorical target
        ids = df['trace_id'].values
        splits = df['source_split'].values

        train_mask = splits == 'training'
        test_mask = splits == 'held_out'

        return (
            X[train_mask], X[test_mask], 
            y[train_mask], y[test_mask],
            ids[train_mask].tolist(), ids[test_mask].tolist()
        )

    def _train_model(self, X_train: np.ndarray, y_train: np.ndarray) -> DecisionTreeClassifier:
        """
        Trains a Decision Tree classifier.
        
        Args:
            X_train: Feature matrix for training.
            y_train: Target labels for training.
        
        Returns:
            Fitted DecisionTreeClassifier.
        """
        model = DecisionTreeClassifier(
            max_depth=self.max_depth,
            min_samples_split=self.min_samples_split,
            random_state=self.config['seeds']['global_seed']
        )
        
        try:
            model.fit(X_train, y_train)
        except Exception as e:
            raise ModelTrainingError(f"Failed to train model: {e}")
        
        return model

    def _calculate_fidelity(self, model: DecisionTreeClassifier, X: np.ndarray, y: np.ndarray) -> float:
        """Calculates accuracy-based fidelity."""
        try:
            y_pred = model.predict(X)
            return accuracy_score(y, y_pred)
        except NotFittedError:
            raise ModelTrainingError("Model is not fitted.")
        except Exception as e:
            raise ModelTrainingError(f"Error calculating fidelity: {e}")

    def _calculate_compressibility_score(
        self, 
        trace_id: str, 
        trace_length: int, 
        rule_count: int, 
        fidelity: float
    ) -> float:
        """
        Calculates compressibility score.
        
        Score = RuleSetSize / TraceLength, conditioned on Fidelity >= threshold.
        If fidelity is below threshold, score is penalized or set to NaN/0 depending on policy.
        Here we return the raw ratio but flag low fidelity in the output.
        """
        if trace_length == 0:
            return 0.0
        
        raw_score = rule_count / trace_length
        return raw_score

    def _extract_rule_count(self, model: DecisionTreeClassifier) -> int:
        """
        Extracts the number of rules (leaf nodes) from the trained model.
        In Decision Trees, the number of leaves approximates the rule count.
        """
        return model.tree_.node_count  # Or specifically count leaves: np.sum(model.tree_.children_left == -1)
    
    def _count_leaves(self, model: DecisionTreeClassifier) -> int:
        """Counts actual leaf nodes."""
        return int(np.sum(model.tree_.children_left == -1))

    def run(self) -> pd.DataFrame:
        """
        Executes the full rule induction pipeline.
        
        Returns:
            DataFrame containing per-trace scores.
        """
        print(f"[Rule Induction] Starting pipeline for {self.feature_matrix_path}")
        
        # 1. Load Data
        df_raw = self._load_feature_matrix()
        X_train, X_test, y_train, y_test, train_ids, test_ids = self._prepare_data(df_raw)

        print(f"[Rule Induction] Loaded {len(X_train)} training samples, {len(X_test)} held-out samples.")

        # 2. Train Model
        model = self._train_model(X_train, y_train)
        
        # 3. Evaluate Fidelity
        train_fidelity = self._calculate_fidelity(model, X_train, y_train)
        holdout_fidelity = self._calculate_fidelity(model, X_test, y_test)
        
        print(f"[Rule Induction] Train Fidelity: {train_fidelity:.4f}, Holdout Fidelity: {holdout_fidelity:.4f}")

        # 4. Extract Rule Count (Global Rule Set Size)
        rule_count = self._count_leaves(model)
        print(f"[Rule Induction] Global Rule Set Size (Leaves): {rule_count}")

        # 5. Calculate Per-Trace Scores for Held-Out Set
        # We need the original trace lengths for the held-out set.
        # Since the feature matrix might not have 'trace_length' explicitly, 
        # we assume it's derivable or we need to load the original traces.
        # However, T020 (extract.py) usually adds metrics. 
        # Let's assume we need to re-load trace metadata for length.
        
        # Re-load trace metadata for held-out traces to get lengths
        # We assume the 'trace_id' in feature_matrix matches the filename (without extension)
        # Or we stored it in the feature matrix. If not, we must load from disk.
        
        # Check if 'trace_length' exists in feature matrix, if not, calculate it
        if 'trace_length' not in df_raw.columns:
            # Fallback: estimate or load from raw traces. 
            # For strict compliance, we assume T020 should have included it or we calculate from raw.
            # Let's try to load from raw traces for held-out set.
            loader = TraceLoader(Path(self.config['paths']['held_out']))
            trace_lengths = {}
            for trace in loader.iter_traces():
                trace_lengths[trace['trace_id']] = len(trace.get('exact_tool_sequence', []))
            
            # Map to test_ids
            trace_lengths_test = [trace_lengths.get(tid, 1) for tid in test_ids]
        else:
            # Use existing column if available (mapping to test_ids order)
            test_df = df_raw[df_raw['source_split'] == 'held_out']
            trace_lengths_test = test_df['trace_length'].tolist()

        results = []
        
        # We need the actual trace lengths for each held-out trace.
        # Since we split by 'source_split', we can map back to the original dataframe.
        held_out_df = df_raw[df_raw['source_split'] == 'held_out']
        
        for i, (tid, x_test_row) in enumerate(zip(test_ids, X_test)):
            # Get trace length
            # If we have it in held_out_df, match by trace_id
            row = held_out_df[held_out_df['trace_id'] == tid]
            if not row.empty:
                t_len = row['trace_length'].values[0] if 'trace_length' in row else 1
            else:
                t_len = 1 # Fallback
            
            if t_len == 0: t_len = 1

            score = self._calculate_compressibility_score(tid, t_len, rule_count, holdout_fidelity)
            
            results.append({
                'trace_id': tid,
                'score': score,
                'rule_count': rule_count,
                'fidelity': holdout_fidelity,
                'train_fidelity': train_fidelity,
                'holdout_fidelity': holdout_fidelity
            })

        results_df = pd.DataFrame(results)
        
        # 6. Save Results
        output_path = self.output_dir / 'per_trace_scores.csv'
        results_df.to_csv(output_path, index=False)
        print(f"[Rule Induction] Saved per-trace scores to {output_path}")

        # 7. Save Model Rules (Global Rule Set) for T026b
        # Export text representation
        rule_text = export_text(model, feature_names=['sequence_entropy', 'tool_repetition_frequency', 'argument_variance'])
        rules_path = self.rules_dir / 'global_rules.json'
        
        rules_data = {
            'model_type': 'DecisionTreeClassifier',
            'max_depth': self.max_depth,
            'rule_count': rule_count,
            'train_fidelity': train_fidelity,
            'holdout_fidelity': holdout_fidelity,
            'threshold': self.threshold,
            'rules_text': rule_text,
            'feature_importances': dict(zip(
                ['sequence_entropy', 'tool_repetition_frequency', 'argument_variance'],
                model.feature_importances_.tolist()
            ))
        }
        
        with open(rules_path, 'w') as f:
            json.dump(rules_data, f, indent=2)
        print(f"[Rule Induction] Saved global rules to {rules_path}")

        return results_df


def process_all_traces_for_induction(config: Optional[Dict[str, Any]] = None) -> pd.DataFrame:
    """
    Convenience function to run the induction pipeline.
    """
    if config is None:
        config = get_config()
    
    inducer = PerTraceRuleInducer(config)
    return inducer.run()


def main():
    """
    Entry point for the rule induction script.
    """
    print("Starting Rule Induction (T023)...")
    
    try:
        config = get_config()
        results = process_all_traces_for_induction(config)
        print(f"Successfully processed {len(results)} traces.")
        print(results.head())
    except DataLoadError as e:
        print(f"Data Error: {e}")
        sys.exit(1)
    except ModelTrainingError as e:
        print(f"Model Error: {e}")
        sys.exit(1)
    except Exception as e:
        print(f"Unexpected Error: {e}")
        import traceback
        traceback.print_exc()
        sys.exit(1)


if __name__ == "__main__":
    main()