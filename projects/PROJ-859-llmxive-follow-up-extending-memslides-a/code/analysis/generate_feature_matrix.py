"""
T022: Generate data/processed/feature_matrix.csv containing structural metrics for every trace.

This script consumes the trace files generated by US1 and the metrics extracted by T020/T021
to produce a consolidated CSV file suitable for downstream analysis (T023+).
"""
import csv
import json
import sys
from pathlib import Path
from typing import Dict, Any, List, Optional

# Add project root to path for imports if running as script
project_root = Path(__file__).resolve().parent.parent.parent
sys.path.insert(0, str(project_root))

from config import Config
from utils.loaders import TraceLoader, MetricsLoader
from utils.validators import MetricsValidator

def load_traces_as_dicts(traces_dir: Path) -> List[Dict[str, Any]]:
    """
    Loads all JSON trace files from the directory into a list of dictionaries.
    """
    loader = TraceLoader(traces_dir)
    traces = []
    for trace_path in loader.iterate_traces():
        try:
            with open(trace_path, 'r', encoding='utf-8') as f:
                data = json.load(f)
                traces.append(data)
        except (json.JSONDecodeError, IOError) as e:
            print(f"Warning: Could not load {trace_path}: {e}")
    return traces

def load_metrics_as_dicts(metrics_dir: Path) -> Dict[str, Dict[str, Any]]:
    """
    Loads metrics JSON files and returns a dictionary keyed by trace_id.
    """
    loader = MetricsLoader(metrics_dir)
    metrics_map = {}
    for metrics_path in loader.iterate_metrics():
        try:
            with open(metrics_path, 'r', encoding='utf-8') as f:
                data = json.load(f)
                # Assuming the metrics file contains a 'trace_id' field or filename mapping
                trace_id = data.get('trace_id')
                if not trace_id:
                    # Fallback: derive from filename if structure allows
                    trace_id = metrics_path.stem.replace('metrics_', '')
                metrics_map[trace_id] = data
        except (json.JSONDecodeError, IOError) as e:
            print(f"Warning: Could not load metrics {metrics_path}: {e}")
    return metrics_map

def generate_feature_matrix(
    traces_dir: Path,
    metrics_dir: Path,
    output_path: Path
) -> int:
    """
    Merges trace metadata with extracted metrics and writes to CSV.
    Returns the number of rows written.
    """
    # Ensure output directory exists
    output_path.parent.mkdir(parents=True, exist_ok=True)

    # Load data
    traces = load_traces_as_dicts(traces_dir)
    metrics_map = load_metrics_as_dicts(metrics_dir)

    if not traces:
        print("Error: No traces found in source directory.")
        return 0

    if not metrics_map:
        print("Warning: No metrics found. Generating matrix with trace metadata only.")

    # Define columns based on expected metrics from T020/T021
    # T020: sequence_entropy, tool_repetition_frequency
    # T021: argument_semantic_variance
    # Plus metadata: trace_id, sequence_length, tool_count
    columns = [
        'trace_id',
        'sequence_length',
        'tool_count',
        'sequence_entropy',
        'tool_repetition_frequency',
        'argument_semantic_variance'
    ]

    rows_written = 0
    with open(output_path, 'w', newline='', encoding='utf-8') as csvfile:
        writer = csv.DictWriter(csvfile, fieldnames=columns)
        writer.writeheader()

        for trace in traces:
            trace_id = trace.get('trace_id') or str(trace.get('id', ''))
            
            # Basic metadata
            tool_sequence = trace.get('exact_tool_sequence', [])
            sequence_length = len(tool_sequence)
            tool_count = len(set(tool_sequence))
            
            # Retrieve metrics
            metrics = metrics_map.get(trace_id, {})
            
            # Extract specific metrics, defaulting to None if missing
            sequence_entropy = metrics.get('sequence_entropy')
            tool_repetition_frequency = metrics.get('tool_repetition_frequency')
            argument_semantic_variance = metrics.get('argument_semantic_variance')

            row = {
                'trace_id': trace_id,
                'sequence_length': sequence_length,
                'tool_count': tool_count,
                'sequence_entropy': sequence_entropy,
                'tool_repetition_frequency': tool_repetition_frequency,
                'argument_semantic_variance': argument_semantic_variance
            }
            
            writer.writerow(row)
            rows_written += 1

    return rows_written

def main():
    config = Config()
    
    traces_dir = config.get_path('raw_traces')
    metrics_dir = config.get_path('processed_metrics')
    output_path = config.get_path('feature_matrix')

    print(f"Generating feature matrix from: {traces_dir}")
    print(f"Using metrics from: {metrics_dir}")
    print(f"Output will be written to: {output_path}")

    try:
        count = generate_feature_matrix(traces_dir, metrics_dir, output_path)
        print(f"Successfully generated feature matrix with {count} rows.")
    except Exception as e:
        print(f"Error generating feature matrix: {e}")
        sys.exit(1)

if __name__ == "__main__":
    main()