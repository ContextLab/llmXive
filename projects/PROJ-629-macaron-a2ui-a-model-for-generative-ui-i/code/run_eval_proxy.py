#!/usr/bin/env python3
"""
Macaron-A2UI Evaluation Proxy (CPU-Scale)

This script reproduces the evaluation logic of the Macaron-A2UI paper without
requiring GPU-heavy model generation or external API calls.

It performs:
1. Loading of pre-cached task data (simulating model outputs).
2. Parsing of A2UI messages (JSON validation).
3. Rule-based scoring (L1: Structure, L2: Components, L3: Utility).
4. Aggregation and visualization.

Run: python code/run_eval_proxy.py
"""

import json
import os
import random
from pathlib import Path
from typing import Any, Dict, List, Tuple
import csv

