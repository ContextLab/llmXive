def load_regression_results_from_memory() -> pd.DataFrame:
    """
    Loads regression results from the in-memory execution of T020/T022.
    In a real pipeline, this might load from a temporary pickle or JSON if T020
    wrote intermediate results. Here, we simulate the immediate downstream call
    to ensure the data flow is correct.
    """
    # Run models if not already run (or assume T020 populated a global/cache)
    # Since T020 is a separate task, we assume it produces a DataFrame.
    # To ensure this script is self-contained for the "save" task, we re-run the logic
    # or load from a known intermediate state if the pipeline structure dictates.
    # Given the task description "Save regression outputs...", we assume the results
    # are available via the models module's extraction function or a temporary file.
    
    # Attempt to load from a potential intermediate cache generated by T020
    cache_path = PROJECT_ROOT / "data" / "results" / "raw_regression_results.json"
    if cache_path.exists():
        logger.info(f"Loading raw regression results from {cache_path}")
        return pd.read_json(cache_path)
    
    # Fallback: Run the models again if cache is missing (ensures data exists)
    logger.warning("Raw regression cache not found. Running models again to generate results.")
    try:
        # This calls the logic from T020
        df_results = run_all_models()
        # Save intermediate cache for next step
        cache_path.parent.mkdir(parents=True, exist_ok=True)
        df_results.to_json(cache_path, orient='records')
        return df_results
    except Exception as e:
        logger.error(f"Failed to generate regression results: {e}")
        raise

def load_bootstrap_cis() -> pd.DataFrame:
    """
    Loads or computes bootstrap CIs (from T021) and returns them as a DataFrame
    ready for merging.
    """
    cache_path = PROJECT_ROOT / "data" / "results" / "bootstrap_cis.json"
    if cache_path.exists():
        logger.info(f"Loading bootstrap CIs from {cache_path}")
        return pd.read_json(cache_path)
    
    logger.warning("Bootstrap CI cache not found. Running bootstrap analysis.")
    # Run bootstrap analysis (T021)
    # We need to pass the models or the data. Assuming we can derive it from the raw results.
    raw_results = load_regression_results_from_memory()
    seed_config = load_seed_config()
    seed = seed_config.get('bootstrap_seed', 42)
    
    # Re-run bootstrap on the models implied by raw_results
    # Note: This assumes run_bootstrap_analysis can accept the raw results or re-fit models.
    # For robustness, we assume it re-runs the necessary fitting with the seed.
    df_cis = run_bootstrap_analysis(seed=seed)
    
    cache_path.parent.mkdir(parents=True, exist_ok=True)
    df_cis.to_json(cache_path, orient='records')
    return df_cis

def merge_results(raw_df: pd.DataFrame, ci_df: pd.DataFrame) -> pd.DataFrame:
    """
    Merges raw regression results with bootstrap confidence intervals.
    """
    # Ensure both are on the same index or join keys
    # Assuming 'outcome' and 'term' are the keys
    merged = raw_df.merge(
        ci_df,
        on=['outcome', 'term'],
        how='left',
        suffixes=('_raw', '_ci')
    )
    
    # Flatten columns if necessary
    if 'p_value' in merged.columns and 'p_value_ci' in merged.columns:
        merged = merged.drop(columns=['p_value_ci']) # Keep raw p-value, we add adjusted later
    
    return merged

def apply_fdr_and_save(df: pd.DataFrame, output_path: Path) -> None:
    """
    Applies FDR correction to p-values and saves the final CSV.
    """
    logger.info(f"Applying FDR correction to {len(df)} results.")
    
    # Ensure p-value column exists
    if 'p_value' not in df.columns:
        raise ValueError("Missing 'p_value' column in regression results.")
    
    # Apply FDR
    df_corrected = add_fdr_correction_to_results(df)
    
    # Select final columns for CSV
    final_columns = [
        'outcome', 'term', 'coef', 'std_err', 'p_value', 
        'p_value_adj', 'conf_int_low', 'conf_int_high'
    ]
    
    # Filter columns that exist
    available_cols = [c for c in final_columns if c in df_corrected.columns]
    # Ensure outcome and term are present
    if 'outcome' not in available_cols or 'term' not in available_cols:
        raise ValueError("Missing 'outcome' or 'term' columns.")
    
    final_df = df_corrected[available_cols].copy()
    
    # Ensure output directory exists
    output_path.parent.mkdir(parents=True, exist_ok=True)
    
    # Save to CSV
    final_df.to_csv(output_path, index=False)
    logger.info(f"Saved regression results to {output_path}")
    logger.info(f"Final shape: {final_df.shape}")
    print(final_df.head())

def main():
    """
    Main entry point for T024.
    1. Load raw regression results (from T020).
    2. Load/Compute Bootstrap CIs (from T021).
    3. Merge results.
    4. Apply FDR correction (from T023).
    5. Save to data/results/regression_results.csv.
    """
    output_path = PROJECT_ROOT / "data" / "results" / "regression_results.csv"
    
    try:
        # Step 1: Load Raw Results
        df_raw = load_regression_results_from_memory()
        
        # Step 2: Load/Compute Bootstrap CIs
        df_cis = load_bootstrap_cis()
        
        # Step 3: Merge
        df_merged = merge_results(df_raw, df_cis)
        
        # Step 4: FDR & Save
        apply_fdr_and_save(df_merged, output_path)
        
        return 0
    except Exception as e:
        logger.error(f"Pipeline failed in T024: {e}")
        import traceback
        traceback.print_exc()
        return 1

if __name__ == "__main__":
    sys.exit(main())