"""
Ingestion module for loading synthetic 16S and cognitive data.

This module loads the synthetic microbiome and cognitive data generated by
synthetic_gen.py, merges them on participant ID, and saves the combined
dataset for downstream filtering and analysis.
"""

import os
import pandas as pd
from pathlib import Path
from code.src.utils.config import SEED, DATA_DIR, RAW_DATA_DIR
from code.src.data.synthetic_gen import generate_synthetic_cohort


def load_microbiome_data(file_path: Path) -> pd.DataFrame:
    """
    Load synthetic 16S microbiome data from CSV.
    
    Args:
        file_path: Path to the microbiome CSV file.
        
    Returns:
        DataFrame containing microbiome data with columns:
        - participant_id
        - shannon_diversity
        - simpson_diversity
        - chao1_diversity
        - bray_curtis_beta
        - unifracs_beta (placeholder for weighted/unweighted)
        - age
        - sex
        - bmi
        - fiber_intake
        - antibiotics_use
    """
    if not file_path.exists():
        raise FileNotFoundError(f"Microbiome data file not found: {file_path}")
        
    df = pd.read_csv(file_path)
    
    # Validate required columns
    required_cols = [
        'participant_id', 'shannon_diversity', 'simpson_diversity',
        'chao1_diversity', 'age', 'sex', 'bmi', 'fiber_intake', 'antibiotics_use'
    ]
    
    missing_cols = [col for col in required_cols if col not in df.columns]
    if missing_cols:
        raise ValueError(f"Microbiome data missing required columns: {missing_cols}")
        
    return df


def load_cognitive_data(file_path: Path) -> pd.DataFrame:
    """
    Load synthetic cognitive flexibility data from CSV.
    
    Args:
        file_path: Path to the cognitive data CSV file.
        
    Returns:
        DataFrame containing cognitive data with columns:
        - participant_id
        - cognitive_flexibility_score
        - age
        - sex
        - bmi
        - fiber_intake
        - antibiotics_use
    """
    if not file_path.exists():
        raise FileNotFoundError(f"Cognitive data file not found: {file_path}")
        
    df = pd.read_csv(file_path)
    
    # Validate required columns
    required_cols = [
        'participant_id', 'cognitive_flexibility_score', 'age',
        'sex', 'bmi', 'fiber_intake', 'antibiotics_use'
    ]
    
    missing_cols = [col for col in required_cols if col not in df.columns]
    if missing_cols:
        raise ValueError(f"Cognitive data missing required columns: {missing_cols}")
        
    return df


def merge_datasets(microbiome_df: pd.DataFrame, cognitive_df: pd.DataFrame) -> pd.DataFrame:
    """
    Merge microbiome and cognitive datasets on participant_id.
    
    This function performs an inner join to ensure only participants
    with complete data in both datasets are included.
    
    Args:
        microbiome_df: DataFrame with microbiome data.
        cognitive_df: DataFrame with cognitive data.
        
    Returns:
        Merged DataFrame with columns from both datasets.
        
    Raises:
        ValueError: If merge results in empty DataFrame.
    """
    # Merge on participant_id
    merged = pd.merge(
        microbiome_df,
        cognitive_df,
        on='participant_id',
        suffixes=('_micro', '_cog')
    )
    
    # Handle potential column conflicts for covariates
    # Keep microbiome version of covariates as source of truth
    covariate_cols = ['age', 'sex', 'bmi', 'fiber_intake', 'antibiotics_use']
    
    for col in covariate_cols:
        if f"{col}_micro" in merged.columns and f"{col}_cog" in merged.columns:
            # Verify they match (should be identical in synthetic data)
            if not merged[f"{col}_micro"].equals(merged[f"{col}_cog"]):
                raise ValueError(f"Mismatch in {col} between datasets for some participants")
            # Keep only one version
            merged = merged.drop(columns=[f"{col}_cog"])
            merged = merged.rename(columns={f"{col}_micro": col})
    
    if merged.empty:
        raise ValueError("Merge resulted in empty DataFrame - no matching participant IDs")
        
    return merged


def ingest_synthetic_cohort() -> pd.DataFrame:
    """
    Main ingestion function that generates, loads, and merges synthetic data.
    
    This function:
    1. Generates synthetic microbiome and cognitive datasets
    2. Loads them from disk
    3. Merges them on participant_id
    4. Returns the combined dataset
    
    Returns:
        DataFrame containing merged microbiome and cognitive data.
        
    Side Effects:
        Writes synthetic data files to data/raw/ directory.
    """
    # Ensure raw data directory exists
    RAW_DATA_DIR.mkdir(parents=True, exist_ok=True)
    
    # Generate synthetic data if not already present
    microbiome_path = RAW_DATA_DIR / "microbiome_data.csv"
    cognitive_path = RAW_DATA_DIR / "cognitive_data.csv"
    
    # Generate data (this will overwrite if files exist, ensuring consistency)
    generate_synthetic_cohort()
    
    # Load the generated data
    print(f"Loading microbiome data from {microbiome_path}")
    microbiome_df = load_microbiome_data(microbiome_path)
    
    print(f"Loading cognitive data from {cognitive_path}")
    cognitive_df = load_cognitive_data(cognitive_path)
    
    # Merge datasets
    print(f"Merging datasets on participant_id...")
    merged_df = merge_datasets(microbiome_df, cognitive_df)
    
    print(f"Ingestion complete. Merged dataset shape: {merged_df.shape}")
    print(f"Columns: {list(merged_df.columns)}")
    
    return merged_df


def save_merged_cohort(df: pd.DataFrame, output_path: Path = None) -> None:
    """
    Save the merged cohort to a CSV file.
    
    Args:
        df: DataFrame to save.
        output_path: Path to save the file. Defaults to data/processed/merged_cohort.csv
    """
    if output_path is None:
        output_path = DATA_DIR / "processed" / "merged_cohort.csv"
        
    output_path.parent.mkdir(parents=True, exist_ok=True)
    
    df.to_csv(output_path, index=False)
    print(f"Merged cohort saved to {output_path}")


def main():
    """
    Entry point for the ingestion script.
    
    This function:
    1. Ingests synthetic microbiome and cognitive data
    2. Merges the datasets
    3. Saves the merged cohort to data/processed/merged_cohort.csv
    """
    print("Starting data ingestion pipeline...")
    
    # Ingest and merge data
    merged_df = ingest_synthetic_cohort()
    
    # Save merged cohort
    save_merged_cohort(merged_df)
    
    print("Ingestion pipeline completed successfully.")


if __name__ == "__main__":
    main()
