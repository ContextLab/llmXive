def calculate_residuals(model_results: pd.DataFrame, user_track_df: pd.DataFrame) -> pd.DataFrame:
    """
    Calculate residuals for the mixed-effects model.
    
    Since statsmodels MixedLM doesn't always expose residuals directly in the summary
    in a simple dataframe format, we reconstruct them if the model summary contains
    necessary coefficients, or we assume the 'residuals' column exists if pre-calculated.
    
    For this pipeline, we assume the regression_summary.csv contains model fit stats,
    but we need the raw data to calculate residuals if not stored. 
    
    However, to be robust, we will attempt to calculate residuals based on the 
    fixed effects coefficients if available, or fallback to a placeholder if 
    the specific model object isn't serialized.
    
    NOTE: In a full implementation, we would refit the model here to get residuals
    or the summary file would need to include the residual vector. 
    Given the constraints of CSV summary, we will simulate the residual calculation
    logic assuming we can reconstruct the prediction or we will focus on 
    the distribution of the dependent variable as a proxy if the model object 
    is not available.
    
    BETTER APPROACH: Refit the model in this function to get true residuals.
    """
    try:
        import statsmodels.api as sm
        from statsmodels.regression.mixed_linear_model import MixedLM
        
        # We need to refit to get residuals reliably without the original model object
        # Identify the dependent variable (usually mean_vividness or mean_valence)
        # Based on T033/T034, we modeled 'mean_vividness' primarily.
        y_col = 'mean_vividness'
        x_cols = ['residualized_exposure_score', 'overall_popularity_score']
        groups = 'user_id'
        
        if y_col not in user_track_df.columns:
            # Fallback to valence if vividness missing
            if 'mean_valence' in user_track_df.columns:
                y_col = 'mean_valence'
                logger.warning(f"{y_col} not found, using mean_valence instead.")
            else:
                raise ValueError("Neither mean_vividness nor mean_valence found in dataset.")
        
        # Ensure no NaNs in regression columns
        clean_df = user_track_df[[y_col] + x_cols + [groups]].dropna()
        
        endog = clean_df[y_col]
        exog = clean_df[x_cols]
        groups_var = clean_df[groups]
        
        model = MixedLM(endog, exog, groups=groups_var)
        result = model.fit()
        
        residuals = result.resid
        
        # Create a dataframe with residuals
        residuals_df = pd.DataFrame({
            'fitted_values': result.fittedvalues,
            'residuals': residuals,
            'observed': endog.values,
            'user_id': groups_var.values
        })
        
        return residuals_df
        
    except ImportError:
        logger.error("statsmodels not available. Cannot calculate residuals.")
        raise
    except Exception as e:
        logger.error(f"Error calculating residuals: {e}")
        raise

def create_residuals_plot(residuals_df: pd.DataFrame, save_path: Path):
    """
    Create a Residuals vs Fitted plot to check for homoscedasticity.
    """
    plt.figure()
    plt.scatter(residuals_df['fitted_values'], residuals_df['residuals'], alpha=0.6)
    plt.axhline(0, color='red', linestyle='--')
    plt.xlabel('Fitted Values')
    plt.ylabel('Residuals')
    plt.title('Residuals vs Fitted')
    plt.tight_layout()
    plt.savefig(save_path)
    plt.close()
    logger.info(f"Saved residuals plot to {save_path}")

def create_qq_plot(residuals_df: pd.DataFrame, save_path: Path):
    """
    Create a QQ plot to check for normality of residuals.
    """
    from scipy import stats
    
    plt.figure()
    stats.probplot(residuals_df['residuals'], dist="norm", plot=plt)
    plt.title('Normal Q-Q')
    plt.tight_layout()
    plt.savefig(save_path)
    plt.close()
    logger.info(f"Saved QQ plot to {save_path}")

def create_scale_location_plot(residuals_df: pd.DataFrame, save_path: Path):
    """
    Create a Scale-Location plot (sqrt(|residuals|) vs fitted values).
    """
    plt.figure()
    sqrt_resid = np.sqrt(np.abs(residuals_df['residuals']))
    plt.scatter(residuals_df['fitted_values'], sqrt_resid, alpha=0.6)
    # Add a smoothed line
    z = np.polyfit(residuals_df['fitted_values'], sqrt_resid, 1)
    p = np.poly1d(z)
    plt.plot(residuals_df['fitted_values'], p(residuals_df['fitted_values']), "r--")
    
    plt.xlabel('Fitted Values')
    plt.ylabel(r'$\sqrt{|Residuals|}$')
    plt.title('Scale-Location')
    plt.tight_layout()
    plt.savefig(save_path)
    plt.close()
    logger.info(f"Saved Scale-Location plot to {save_path}")

def create_residuals_leverage_plot(residuals_df: pd.DataFrame, user_track_df: pd.DataFrame, save_path: Path):
    """
    Create a Residuals vs Leverage plot.
    Requires calculating leverage (hat matrix diagonal).
    """
    try:
        import statsmodels.api as sm
        from statsmodels.stats.outliers_influence import OLSInfluence
        
        # We need to use the OLS influence logic on the fixed effects part
        # Reconstruct the design matrix for fixed effects
        y_col = 'mean_vividness' if 'mean_vividness' in user_track_df.columns else 'mean_valence'
        x_cols = ['residualized_exposure_score', 'overall_popularity_score']
        
        clean_df = user_track_df[[y_col] + x_cols].dropna()
        endog = clean_df[y_col]
        exog = sm.add_constant(clean_df[x_cols])
        
        # Fit a simple OLS for leverage calculation (approximation for fixed effects)
        ols_model = sm.OLS(endog, exog).fit()
        influence = OLSInfluence(ols_model)
        leverage = influence.hat_matrix_diag
        
        # Match leverage back to residuals dataframe if lengths match
        # Note: This is an approximation because MixedLM leverage is complex.
        # We will plot the OLS leverage against residuals for diagnostic purposes.
        
        if len(leverage) == len(residuals_df):
            plt.figure()
            plt.scatter(leverage, residuals_df['residuals'], alpha=0.6)
            plt.axhline(0, color='red', linestyle='--')
            
            # Cook's distance contour (optional but good)
            cook_d = influence.cooks_distance[0]
            threshold = 4 / len(leverage)
            plt.axhline(threshold, color='green', linestyle=':', label=f'Threshold {threshold:.4f}')
            
            plt.xlabel('Leverage')
            plt.ylabel('Residuals')
            plt.title('Residuals vs Leverage')
            plt.legend()
            plt.tight_layout()
            plt.savefig(save_path)
            plt.close()
            logger.info(f"Saved Residuals vs Leverage plot to {save_path}")
        else:
            logger.warning("Length mismatch for leverage plot, skipping.")
            # Create a placeholder or skip
            
    except Exception as e:
        logger.error(f"Error creating leverage plot: {e}")
        # Create a placeholder file to indicate the attempt
        plt.figure()
        plt.text(0.5, 0.5, f'Error: {str(e)}', ha='center', va='center')
        plt.axis('off')
        plt.savefig(save_path)
        plt.close()

