"""
Generate the final ingested_cohort.parquet artifact from processed data.

This script performs the following steps:
1. Loads the intermediate processed data (output of T016 logic).
2. Validates the presence of required columns.
3. Saves the data as a Parquet file to data/processed/ingested_cohort.parquet.
4. Calculates a SHA-256 checksum of the file.
5. Updates state.yaml with the file metadata and checksum.
"""
import os
import sys
import logging
import hashlib
import yaml
from datetime import datetime
from pathlib import Path

import pandas as pd
import pyarrow.parquet as pq

# Import project utilities
from config import get_project_root, get_config_dict
from utils import setup_logging, get_logger
from state_manager import register_file, save_state

logger = get_logger(__name__)

def calculate_file_checksum(file_path: Path) -> str:
    """
    Calculate SHA-256 checksum of a file.
    
    Args:
        file_path: Path to the file.
        
    Returns:
        Hex digest of the SHA-256 hash.
    """
    sha256_hash = hashlib.sha256()
    with open(file_path, "rb") as f:
        # Read in chunks to handle large files
        for byte_block in iter(lambda: f.read(4096), b""):
            sha256_hash.update(byte_block)
    return sha256_hash.hexdigest()

def save_state_entry(
    file_path: Path, 
    checksum: str, 
    artifact_type: str = "processed_data"
) -> None:
    """
    Register the file in state.yaml.
    
    Args:
        file_path: Path to the generated file.
        checksum: SHA-256 checksum of the file.
        artifact_type: Type of artifact (e.g., 'processed_data', 'final_result').
    """
    state_data = {
        "path": str(file_path.relative_to(get_project_root())),
        "checksum": checksum,
        "type": artifact_type,
        "generated_at": datetime.utcnow().isoformat(),
        "task_id": "T018"
    }
    
    # Use state_manager to handle saving
    register_file(file_path, checksum, artifact_type)
    save_state()
    logger.info(f"State updated for {file_path.name}")

def main():
    """Main execution function for T018."""
    setup_logging()
    root = get_project_root()
    config = get_config_dict()
    
    # Define paths
    # The intermediate data is expected to be at data/processed/intermediate_cohort.csv
    # or generated by the previous step in the pipeline.
    # Based on T016, the data is in memory or a temp file. 
    # We assume the pipeline orchestration (T028) has prepared the data 
    # and saved it to a temporary CSV or we load from the expected intermediate location.
    # For robustness, we check for the intermediate CSV generated by T016 logic.
    
    intermediate_csv = root / "data" / "processed" / "intermediate_cohort.csv"
    output_parquet = root / "data" / "processed" / "ingested_cohort.parquet"
    
    if not intermediate_csv.exists():
        logger.error(f"Intermediate data not found at {intermediate_csv}. "
                     f"Ensure T016 (calculate_residualized_score) has run and saved output.")
        sys.exit(1)
    
    logger.info(f"Loading intermediate data from {intermediate_csv}")
    try:
        df = pd.read_csv(intermediate_csv)
    except Exception as e:
        logger.error(f"Failed to load intermediate data: {e}")
        sys.exit(1)
    
    # Validate required columns based on T014/T016 outputs
    required_cols = [
        'track_id', 
        'adolescent_exposure_score', 
        'residualized_exposure_score',
        'overall_popularity_score'
    ]
    
    missing_cols = [col for col in required_cols if col not in df.columns]
    if missing_cols:
        logger.error(f"Missing required columns in intermediate data: {missing_cols}")
        sys.exit(1)
    
    logger.info(f"Data shape: {df.shape}")
    logger.info(f"Columns: {list(df.columns)}")
    
    # Ensure output directory exists
    output_parquet.parent.mkdir(parents=True, exist_ok=True)
    
    logger.info(f"Saving to Parquet: {output_parquet}")
    try:
        # Use pyarrow for efficient Parquet writing
        table = pa.Table.from_pandas(df)
        pq.write_table(table, output_parquet)
    except Exception as e:
        logger.error(f"Failed to save Parquet file: {e}")
        sys.exit(1)
    
    # Verify file exists and calculate checksum
    if not output_parquet.exists():
        logger.error("Output file was not created.")
        sys.exit(1)
    
    checksum = calculate_file_checksum(output_parquet)
    logger.info(f"Checksum calculated: {checksum}")
    
    # Update state.yaml
    save_state_entry(output_parquet, checksum, "processed_data")
    
    logger.info("T018 completed successfully.")

if __name__ == "__main__":
    main()
