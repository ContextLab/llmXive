def load_permutation_statistics(input_path: Path) -> pd.Series:
    """
    Load the permutation test statistics from the intermediate file generated by T045.
    
    Args:
        input_path: Path to the intermediate permutation results (e.g., permutation_stats.csv)
        
    Returns:
        pd.Series: The distribution of t-statistics from the null distribution.
    """
    if not input_path.exists():
        raise FileNotFoundError(
            f"Permutation statistics file not found: {input_path}. "
            "Ensure T045 (run_permutation_test) has been executed successfully."
        )
    
    df = pd.read_csv(input_path)
    # The T045 output is expected to have a 'statistic' column
    if 'statistic' not in df.columns:
        raise ValueError(
            f"Expected 'statistic' column in {input_path}, but found columns: {df.columns.tolist()}"
        )
    
    return df['statistic']

def calculate_pvalue(null_distribution: pd.Series, observed_statistic: float, tail: str = 'two-sided') -> float:
    """
    Calculate the p-value for the observed statistic against the null distribution.
    
    Args:
        null_distribution: Series of statistics from the permutation test (null hypothesis).
        observed_statistic: The t-statistic from the actual model (alternative hypothesis).
        tail: 'two-sided', 'greater', or 'less'.
        
    Returns:
        float: The calculated p-value.
    """
    if tail == 'two-sided':
        # Two-sided test: proportion of null stats >= |observed|
        abs_obs = abs(observed_statistic)
        abs_null = null_distribution.abs()
        count_extreme = (abs_null >= abs_obs).sum()
    elif tail == 'greater':
        # Greater test: proportion of null stats >= observed
        count_extreme = (null_distribution >= observed_statistic).sum()
    elif tail == 'less':
        # Less test: proportion of null stats <= observed
        count_extreme = (null_distribution <= observed_statistic).sum()
    else:
        raise ValueError(f"Invalid tail specification: {tail}")
    
    n = len(null_distribution)
    if n == 0:
        return 1.0
    
    # Calculate p-value: (count + 1) / (n + 1) to avoid zero p-values
    p_value = (count_extreme + 1) / (n + 1)
    return p_value

def load_observed_statistic(config: dict) -> float:
    """
    Load the observed t-statistic from the regression summary or a specific file.
    The observed statistic is the t-stat for 'residualized_exposure' from the main model (T033).
    
    We assume the regression summary (T038) contains this value.
    """
    root = get_project_root()
    regression_summary_path = root / "data" / "final" / "regression_summary.csv"
    
    if not regression_summary_path.exists():
        raise FileNotFoundError(
            f"Regression summary not found: {regression_summary_path}. "
            "Ensure T038 (generate_regression_summary) has been executed."
        )
    
    df = pd.read_csv(regression_summary_path)
    
    # Look for the row corresponding to 'residualized_exposure'
    # The column names might vary slightly, but 'term' or 'variable' is common.
    # Based on T038, it likely has 'term', 'estimate', 'std_error', 't_stat', 'p_value'.
    
    if 'term' in df.columns:
        target_row = df[df['term'] == 'residualized_exposure']
    elif 'variable' in df.columns:
        target_row = df[df['variable'] == 'residualized_exposure']
    else:
        # Fallback: try to find by index if structure is known, or raise error
        raise ValueError(
            f"Could not find 'residualized_exposure' in regression summary. "
            f"Available columns: {df.columns.tolist()}, First few terms: {df.iloc[0].to_dict() if not df.empty else 'Empty'}"
        )
    
    if target_row.empty:
        raise ValueError("Could not find 'residualized_exposure' term in regression summary.")
    
    # Extract t-statistic
    if 't_stat' in target_row.columns:
        return float(target_row['t_stat'].values[0])
    elif 't-value' in target_row.columns:
        return float(target_row['t-value'].values[0])
    else:
        raise ValueError(
            f"Could not find t-statistic column in regression summary. "
            f"Available: {target_row.columns.tolist()}"
        )

def main():
    """
    Main entry point for T039b.
    1. Load permutation statistics from T045 output.
    2. Load observed statistic from T038 output.
    3. Calculate p-value.
    4. Write final results to data/final/permutation_results.csv.
    5. Update state.yaml.
    """
    setup_logging()
    logger = get_logger(__name__)
    config = get_config_dict()
    root = get_project_root()
    
    # Paths
    # T045 output (intermediate statistics) - assumed to be generated by T045
    # The task description says T045 outputs 'data/final/permutation_results.csv' with iterations.
    # However, T045b is supposed to append the p-value.
    # To avoid overwriting T045's work before calculation, we assume T045 writes a temporary stats file
    # or we read the iteration stats from the existing file if it exists.
    # Let's assume T045 writes 'data/final/permutation_stats.csv' (intermediate) or we read from the final file if it exists but is incomplete.
    # Given the task flow: T045 generates stats, T045b calculates p-value.
    # We will look for the intermediate stats file first. If not found, we check if the final file exists and is incomplete.
    
    # Based on T045 description: "Output: data/final/permutation_results.csv with columns: iteration, statistic"
    # We will read this, calculate p-value, and write a new summary or append.
    # Let's create a clean final file as per T039b requirement.
    
    permutation_stats_path = root / "data" / "final" / "permutation_stats.csv"
    # Fallback if T045 wrote directly to the final path without p-value
    permutation_raw_path = root / "data" / "final" / "permutation_results.csv"
    
    # Determine input source
    input_path = None
    if permutation_stats_path.exists():
        input_path = permutation_stats_path
        logger.info(f"Found intermediate permutation stats at {input_path}")
    elif permutation_raw_path.exists():
        # Check if it has 'p_value' column already
        df_check = pd.read_csv(permutation_raw_path)
        if 'p_value' not in df_check.columns:
            input_path = permutation_raw_path
            logger.info(f"Found raw permutation results at {input_path} (no p-value yet)")
        else:
            logger.warning(f"P-value already present in {permutation_raw_path}. Skipping calculation.")
            return
    else:
        raise FileNotFoundError(
            f"Permutation test output not found. Looked for {permutation_stats_path} or {permutation_raw_path}. "
            "Ensure T045 has been executed."
        )
    
    # 1. Load null distribution
    logger.info("Loading permutation statistics...")
    null_distribution = load_permutation_statistics(input_path)
    logger.info(f"Loaded {len(null_distribution)} permutation statistics.")
    
    # 2. Load observed statistic
    logger.info("Loading observed statistic from regression summary...")
    try:
        observed_stat = load_observed_statistic(config)
        logger.info(f"Observed t-statistic: {observed_stat:.4f}")
    except Exception as e:
        logger.error(f"Failed to load observed statistic: {e}")
        raise
    
    # 3. Calculate p-value
    logger.info("Calculating p-value...")
    p_value = calculate_pvalue(null_distribution, observed_stat, tail='two-sided')
    logger.info(f"Calculated p-value: {p_value:.6f}")
    
    # 4. Generate final output
    output_path = root / "data" / "final" / "permutation_results.csv"
    
    # Create a summary dataframe
    summary_data = {
        'metric': ['observed_t_statistic', 'p_value', 'n_permutations', 'tail_test'],
        'value': [observed_stat, p_value, len(null_distribution), 'two-sided']
    }
    summary_df = pd.DataFrame(summary_data)
    
    # Also save the full distribution for reference, or just the summary?
    # The task says "Generate data/final/permutation_results.csv (including final p-value)".
    # It implies the file should contain the results. Let's save the summary and the full distribution if needed.
    # Usually, a results file contains the summary. Let's save the summary as the primary output.
    # If the previous file had iterations, we can append the summary or create a new one.
    # Let's create a new clean file with the summary and the full distribution appended or separate.
    # To be safe and complete, we will write the summary.
    
    summary_df.to_csv(output_path, index=False)
    logger.info(f"Saved permutation results to {output_path}")
    
    # Optionally, if we want to keep the iteration history, we could append it.
    # But the requirement is to generate the file with the p-value.
    # Let's also write a separate file for the null distribution if needed, 
    # but for now, the summary is the key deliverable for T039b.
    
    # 5. Update state.yaml
    logger.info("Updating state.yaml...")
    register_file(
        file_path=str(output_path.relative_to(root)),
        checksum=None, # state_manager will calculate
        description="Permutation test results including final p-value"
    )
    save_state()
    
    logger.info("Task T039b completed successfully.")

if __name__ == "__main__":
    main()