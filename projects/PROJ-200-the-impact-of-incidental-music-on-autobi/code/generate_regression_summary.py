"""
Generate regression summary CSV containing coefficients, standard errors, p-values, and VIFs.

This script loads the regression results generated by `modeling.py` (specifically 
`save_regression_results`), calculates Variance Inflation Factors (VIF) for the 
fixed effects, and outputs a comprehensive summary table to 
`data/final/regression_summary.csv`.

The output format matches the requirements for T038:
- Coefficients, SEs, p-values for all fixed effects
- VIF scores for multicollinearity assessment
- Metadata about the model (formula, R-squared, etc.)
"""
import os
import logging
import pandas as pd
import numpy as np
from pathlib import Path
from typing import Dict, List, Optional, Tuple, Any
from statsmodels.stats.outliers_influence import variance_inflation_factor

from config import get_project_root, get_config_dict
from utils import setup_logging, get_logger
from state_manager import register_file, save_state

logger = get_logger(__name__)

def calculate_vif(df: pd.DataFrame, formula: str) -> pd.DataFrame:
    """
    Calculate Variance Inflation Factors (VIF) for the predictors in the model.
    
    Args:
        df: DataFrame containing the data used for the model.
        formula: The model formula string (e.g., 'mean_vividness ~ residualized_exposure + popularity').
    
    Returns:
        DataFrame with columns: ['term', 'vif'].
    """
    # Parse formula to extract predictor terms (exclude the response variable)
    # Simple parsing: split by '~' and take the right side
    if '~' not in formula:
        logger.warning(f"Invalid formula format: {formula}. Cannot calculate VIF.")
        return pd.DataFrame({'term': [], 'vif': []})
    
    predictors_str = formula.split('~')[1].strip()
    
    # Extract terms: handle simple cases like 'A + B + C'
    # Remove parentheses for random effects if present (e.g., '(1|user_id)')
    predictors_str = predictors_str.replace('(1|user_id)', '').replace('(1|track_id)', '')
    
    terms = [t.strip() for t in predictors_str.split('+') if t.strip()]
    
    # Remove intercept if explicitly listed (usually not needed for VIF)
    terms = [t for t in terms if t != 'Intercept' and t != '1']
    
    if not terms:
        logger.warning("No predictor terms found for VIF calculation.")
        return pd.DataFrame({'term': [], 'vif': []})
    
    # Build feature matrix
    # We need to handle categorical variables and interactions if present
    # For this specific task, we assume simple numeric predictors based on spec
    # If categorical, we'd need patsy.dmatrix or similar
    
    try:
        # Use patsy to create the design matrix for the fixed effects
        import patsy
        # Create design matrix for fixed effects only (exclude random effects part if any)
        # The formula passed here should be the fixed effects part
        # We use the original formula but patsy handles the intercept
        y, X = patsy.dmatrices(formula, df, return_type='dataframe')
        
        # Remove intercept column for VIF calculation (standard practice)
        if 'Intercept' in X.columns:
            X = X.drop('Intercept', axis=1)
        
        # Calculate VIF for each column
        vif_data = []
        for i, col in enumerate(X.columns):
            try:
                vif = variance_inflation_factor(X.values, i)
                vif_data.append({'term': col, 'vif': vif})
            except Exception as e:
                logger.warning(f"Could not calculate VIF for {col}: {e}")
                vif_data.append({'term': col, 'vif': np.nan})
        
        return pd.DataFrame(vif_data)
        
    except ImportError:
        logger.error("patsy is required for VIF calculation but not installed.")
        return pd.DataFrame({'term': [], 'vif': []})
    except Exception as e:
        logger.error(f"Error calculating VIF: {e}")
        return pd.DataFrame({'term': [], 'vif': []})

def load_regression_results() -> Optional[pd.DataFrame]:
    """
    Load the regression results saved by modeling.py.
    
    Expected file: data/processed/regression_results.parquet or .csv
    Returns a DataFrame with columns: model_name, term, estimate, std_err, pvalue, etc.
    """
    root = get_project_root()
    # Try parquet first, then csv
    parquet_path = root / 'data' / 'processed' / 'regression_results.parquet'
    csv_path = root / 'data' / 'processed' / 'regression_results.csv'
    
    if parquet_path.exists():
        try:
            return pd.read_parquet(parquet_path)
        except Exception as e:
            logger.warning(f"Failed to load parquet: {e}")
    
    if csv_path.exists():
        try:
            return pd.read_csv(csv_path)
        except Exception as e:
            logger.warning(f"Failed to load csv: {e}")
    
    logger.error("Regression results file not found in data/processed/")
    return None

def generate_summary_dataframe(regression_results: pd.DataFrame, user_track_data: pd.DataFrame) -> pd.DataFrame:
    """
    Generate the final summary dataframe combining coefficients, p-values, and VIFs.
    
    Args:
        regression_results: DataFrame from load_regression_results()
        user_track_data: DataFrame containing the data used for modeling (for VIF calculation)
    
    Returns:
        DataFrame with columns: model, term, estimate, std_err, pvalue, vif, significance
    """
    if regression_results is None or user_track_data is None:
        logger.error("Missing required data for summary generation.")
        return pd.DataFrame()
    
    # Filter for fixed effects only (assuming model_type column or similar distinguishes)
    # The modeling.py save_regression_results should have saved fixed effects
    if 'model_type' in regression_results.columns:
        fixed_effects = regression_results[regression_results['model_type'] == 'fixed']
    else:
        # Assume all are fixed if no column exists
        fixed_effects = regression_results
    
    summary_rows = []
    
    # Group by model (vividness vs valence)
    models = fixed_effects['model_name'].unique()
    
    for model_name in models:
        model_data = fixed_effects[fixed_effects['model_name'] == model_name]
        
        # Extract formula from metadata if available, otherwise reconstruct
        formula = model_data.iloc[0].get('formula', '')
        
        # Calculate VIF for this model's predictors
        vif_df = calculate_vif(user_track_data, formula)
        
        for _, row in model_data.iterrows():
            term = row['term']
            estimate = row['estimate']
            std_err = row['std_err']
            pvalue = row['pvalue']
            
            # Get VIF for this term
            vif_val = vif_df[vif_df['term'] == term]['vif'].values
            vif = vif_val[0] if len(vif_val) > 0 else np.nan
            
            # Determine significance
            significance = '*' if pvalue < 0.05 else ('**' if pvalue < 0.01 else ('***' if pvalue < 0.001 else 'ns'))
            
            summary_rows.append({
                'model': model_name,
                'term': term,
                'estimate': estimate,
                'std_err': std_err,
                'pvalue': pvalue,
                'vif': vif,
                'significance': significance
            })
    
    summary_df = pd.DataFrame(summary_rows)
    
    # Sort by model and term
    if not summary_df.empty:
        summary_df = summary_df.sort_values(['model', 'term'])
    
    return summary_df

def main():
    """Main entry point for generating the regression summary."""
    setup_logging()
    logger.info("Starting regression summary generation (T038)...")
    
    root = get_project_root()
    config = get_config_dict()
    
    # Ensure output directory exists
    output_dir = root / 'data' / 'final'
    output_dir.mkdir(parents=True, exist_ok=True)
    
    # Load regression results
    regression_results = load_regression_results()
    if regression_results is None:
        logger.error("Cannot proceed: regression results not found.")
        return 1
    
    logger.info(f"Loaded regression results with {len(regression_results)} rows.")
    
    # Load user-track data for VIF calculation
    user_track_path = root / 'data' / 'processed' / 'user_track_pairs.parquet'
    if not user_track_path.exists():
        # Try CSV
        user_track_path = root / 'data' / 'processed' / 'user_track_pairs.csv'
    
    if user_track_path.exists():
        try:
            if user_track_path.suffix == '.parquet':
                user_track_data = pd.read_parquet(user_track_path)
            else:
                user_track_data = pd.read_csv(user_track_path)
            logger.info(f"Loaded user-track data with {len(user_track_data)} rows for VIF calculation.")
        except Exception as e:
            logger.error(f"Failed to load user-track data: {e}")
            user_track_data = None
    else:
        logger.warning("User-track data not found. VIF calculation will be skipped.")
        user_track_data = None
    
    # Generate summary
    summary_df = generate_summary_dataframe(regression_results, user_track_data)
    
    if summary_df.empty:
        logger.error("Summary dataframe is empty. Check input data.")
        return 1
    
    # Save to CSV
    output_file = output_dir / 'regression_summary.csv'
    summary_df.to_csv(output_file, index=False)
    
    logger.info(f"Saved regression summary to {output_file}")
    logger.info(f"Summary shape: {summary_df.shape}")
    logger.info(f"Columns: {list(summary_df.columns)}")
    
    # Register file in state
    register_file(str(output_file), 'regression_summary')
    save_state()
    
    logger.info("Regression summary generation completed successfully.")
    return 0

if __name__ == '__main__':
    exit(main())
