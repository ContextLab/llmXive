import os
import sys
import logging
import pandas as pd
import numpy as np
from pathlib import Path

# Ensure parent directory is in path for imports if running as script
# However, standard project structure assumes 'code' is added to sys.path
# or this is run via python -m. We assume standard import works per API surface.
from config import get_project_root, get_config_dict
from utils import setup_logging, get_logger

logger = get_logger(__name__)

def load_sensitivity_intermediate_results(intermediate_dir: Path) -> pd.DataFrame:
    """
    Load aggregated sensitivity analysis results from intermediate CSV files.
    
    T044 (run_sensitivity_analysis) is expected to generate intermediate results
    for each threshold. This function consolidates them.
    
    We expect files named like: sensitivity_threshold_<val>.csv
    """
    config = get_config_dict()
    thresholds = config.get('LEVENSHTEIN_THRESHOLDS', [2, 3, 4, 5, 6])
    
    all_results = []
    
    for threshold in thresholds:
        file_path = intermediate_dir / f"sensitivity_threshold_{threshold}.csv"
        if file_path.exists():
            logger.info(f"Loading intermediate results for threshold {threshold} from {file_path}")
            df = pd.read_csv(file_path)
            df['levenshtein_threshold'] = threshold
            all_results.append(df)
        else:
            logger.warning(f"Intermediate file not found for threshold {threshold}: {file_path}")
    
    if not all_results:
        raise FileNotFoundError(
            f"No intermediate sensitivity results found in {intermediate_dir}. "
            "Ensure T044 (run_sensitivity_analysis) has completed successfully."
        )
    
    combined_df = pd.concat(all_results, ignore_index=True)
    return combined_df

def generate_sensitivity_summary(df: pd.DataFrame) -> pd.DataFrame:
    """
    Generate the final sensitivity analysis summary table.
    
    Ensures required columns exist and formats the output as per spec.
    Expected columns from T044:
    - levenshtein_threshold
    - match_rate
    - vividness_coefficient
    - vividness_pvalue
    - vividness_se
    - valence_coefficient
    - valence_pvalue
    - valence_se
    """
    required_cols = [
        'levenshtein_threshold', 'match_rate', 
        'vividness_coefficient', 'vividness_pvalue', 'vividness_se',
        'valence_coefficient', 'valence_pvalue', 'valence_se'
    ]
    
    # Filter to only required columns if they exist
    available_cols = [c for c in required_cols if c in df.columns]
    summary_df = df[available_cols].copy()
    
    # Sort by threshold
    summary_df = summary_df.sort_values('levenshtein_threshold').reset_index(drop=True)
    
    logger.info(f"Generated sensitivity summary with {len(summary_df)} rows.")
    return summary_df

def save_sensitivity_analysis(df: pd.DataFrame, output_path: Path) -> None:
    """
    Save the sensitivity analysis results to the final output file.
    """
    output_path.parent.mkdir(parents=True, exist_ok=True)
    df.to_csv(output_path, index=False)
    logger.info(f"Sensitivity analysis saved to {output_path}")

def main():
    """
    Main entry point for T039a: Generate sensitivity_analysis.csv
    """
    setup_logging()
    root = get_project_root()
    
    # Paths
    intermediate_dir = root / "data" / "final" / "intermediate_sensitivity"
    output_path = root / "data" / "final" / "sensitivity_analysis.csv"
    
    logger.info("Starting T039a: Generating sensitivity analysis summary...")
    
    try:
        # 1. Load intermediate results generated by T044
        # T044 is expected to have written files to intermediate_dir
        combined_data = load_sensitivity_intermediate_results(intermediate_dir)
        
        # 2. Generate summary
        summary_df = generate_sensitivity_summary(combined_data)
        
        # 3. Save final output
        save_sensitivity_analysis(summary_df, output_path)
        
        logger.info("T039a completed successfully.")
        
    except FileNotFoundError as e:
        logger.error(f"Data dependency missing: {e}")
        logger.error("T044 (run_sensitivity_analysis) must be run first to generate intermediate results.")
        raise
    except Exception as e:
        logger.error(f"Error during sensitivity analysis generation: {e}")
        raise

if __name__ == "__main__":
    main()