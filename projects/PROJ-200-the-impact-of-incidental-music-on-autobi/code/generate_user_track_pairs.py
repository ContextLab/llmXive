"""
Script to generate the final User-Track Pairs dataset.

This module loads the aggregated data from the previous stage (T026),
ensures it is saved as a Parquet file, calculates its checksum,
and updates the state.yaml registry.
"""

import os
import logging
from pathlib import Path
import pandas as pd
import pyarrow.parquet as pq
import hashlib
import yaml
from datetime import datetime

from config import get_project_root, get_config_dict
from state_manager import load_state, save_state, register_file
from utils import setup_logging, get_logger

# Configure logger
logger = get_logger(__name__)


def calculate_file_checksum(file_path: Path) -> str:
    """
    Calculate the SHA-256 checksum of a file.

    Args:
        file_path: Path to the file.

    Returns:
        Hexadecimal checksum string.
    """
    sha256_hash = hashlib.sha256()
    with open(file_path, "rb") as f:
        # Read in chunks to handle large files
        for byte_block in iter(lambda: f.read(4096), b""):
            sha256_hash.update(byte_block)
    return sha256_hash.hexdigest()


def load_aggregated_data() -> pd.DataFrame:
    """
    Load the aggregated User-Track pairs from the intermediate Parquet file.

    This file is produced by `code/aggregation.py` (T026).

    Returns:
        DataFrame containing User-Track pairs.

    Raises:
        FileNotFoundError: If the intermediate file does not exist.
    """
    project_root = get_project_root()
    # The intermediate file is typically generated by the aggregation step
    # and saved before this step. Based on the pipeline flow, it should be
    # in data/processed/ with a temporary name or a specific name defined in config.
    # For this implementation, we assume the aggregation step saved to:
    # data/processed/aggregated_user_track_pairs.parquet
    # However, looking at T026 description, it aggregates to User-Track Pair level.
    # Let's check the config or standard path.
    # Standard path for intermediate aggregation output:
    input_path = project_root / "data" / "processed" / "aggregated_user_track_pairs.parquet"

    if not input_path.exists():
        # Fallback: try to find the most recent parquet in processed if name varies
        # But strictly, we should fail if the expected source is missing.
        raise FileNotFoundError(
            f"Intermediate aggregated data not found at {input_path}. "
            "Ensure T026 (aggregation) has completed successfully."
        )

    logger.info(f"Loading aggregated data from {input_path}")
    df = pd.read_parquet(input_path)
    
    # Basic validation
    required_cols = ['user_id', 'track_id', 'mean_vividness', 'mean_valence']
    missing_cols = [c for c in required_cols if c not in df.columns]
    if missing_cols:
        raise ValueError(f"Aggregated data missing required columns: {missing_cols}")
    
    logger.info(f"Loaded {len(df)} user-track pairs with {len(df.columns)} columns.")
    return df


def save_final_dataset(df: pd.DataFrame, output_path: Path) -> None:
    """
    Save the DataFrame to the final Parquet location.

    Args:
        df: The DataFrame to save.
        output_path: The destination path.
    """
    output_path.parent.mkdir(parents=True, exist_ok=True)
    
    # Write to parquet
    df.to_parquet(output_path, index=False, compression='snappy')
    logger.info(f"Saved final dataset to {output_path} ({len(df)} rows)")


def save_state_entry(file_path: Path, checksum: str, description: str) -> None:
    """
    Update the state.yaml with the new file's checksum and metadata.

    Args:
        file_path: Path to the generated file.
        checksum: SHA-256 checksum of the file.
        description: Human-readable description of the file.
    """
    state = load_state()
    
    entry = {
        "path": str(file_path.relative_to(get_project_root())),
        "checksum": checksum,
        "created_at": datetime.now().isoformat(),
        "description": description,
        "task_id": "T029"
    }
    
    # Register or update the entry
    register_file(state, entry)
    save_state(state)
    logger.info(f"Updated state.yaml with entry for {file_path.name}")


def main():
    """
    Main entry point for T029: Generate user_track_pairs.parquet.
    """
    setup_logging()
    logger.info("Starting T029: Generate user_track_pairs.parquet")
    
    project_root = get_project_root()
    output_filename = "user_track_pairs.parquet"
    output_path = project_root / "data" / "processed" / output_filename
    
    try:
        # 1. Load the aggregated data from the previous step
        df = load_aggregated_data()
        
        # 2. Save to the final location
        save_final_dataset(df, output_path)
        
        # 3. Calculate checksum
        checksum = calculate_file_checksum(output_path)
        logger.info(f"Calculated checksum: {checksum}")
        
        # 4. Update state.yaml
        description = "Aggregated User-Track pairs with mean vividness and valence, ready for modeling."
        save_state_entry(output_path, checksum, description)
        
        logger.info("T029 completed successfully.")
        return 0
        
    except FileNotFoundError as e:
        logger.error(f"Missing required input file: {e}")
        return 1
    except Exception as e:
        logger.error(f"Error during T029 execution: {e}", exc_info=True)
        return 1


if __name__ == "__main__":
    import sys
    sys.exit(main())