"""
Task T021: Output flexibility scores to data/processed/flexibility_scores.parquet.

This script loads dynamic connectivity results generated by T019,
aggregates the flexibility metrics per subject, and writes them to a
consolidated Parquet file for downstream statistical analysis.
"""
import os
import sys
import logging
from pathlib import Path
from typing import Dict, Any, List

import pandas as pd
import numpy as np

# Add project root to path if running as script
if "code" not in sys.path[0]:
    project_root = Path(__file__).resolve().parent.parent
    sys.path.insert(0, str(project_root))

from analysis.dynamic_connectivity import load_scrubbed_timeseries, compute_flexibility_metric, main as dynamic_main
from utils.logging_config import setup_logging
from utils.config import set_all_seeds

# Configure logging
logger = logging.getLogger(__name__)

# Constants
INPUT_DIR = Path("data/processed/dynamic_connectivity_results")
OUTPUT_FILE = Path("data/processed/flexibility_scores.parquet")
WINDOW_LENGTHS = [60, 90]  # Primary window lengths used in analysis

def load_subject_flexibility_results(subject_id: str) -> Dict[str, float]:
    """
    Load flexibility results for a single subject from the dynamic connectivity results.
    
    Args:
        subject_id: The subject identifier (e.g., '100003')
        
    Returns:
        Dictionary mapping window length to flexibility score
    """
    subject_dir = INPUT_DIR / subject_id
    if not subject_dir.exists():
        logger.warning(f"Dynamic connectivity results not found for subject {subject_id}")
        return {}

    scores = {}
    for window_len in WINDOW_LENGTHS:
        result_file = subject_dir / f"flexibility_window_{window_len}.json"
        if result_file.exists():
            try:
                import json
                with open(result_file, 'r') as f:
                    data = json.load(f)
                    # Assuming the JSON contains a 'flexibility_score' key
                    score = data.get('flexibility_score')
                    if score is not None:
                        scores[window_len] = float(score)
            except (json.JSONDecodeError, ValueError) as e:
                logger.error(f"Error reading flexibility results for {subject_id} (window {window_len}): {e}")
        else:
            logger.warning(f"Flexibility result file missing for {subject_id} (window {window_len})")
    
    return scores

def aggregate_flexibility_scores(subject_ids: List[str]) -> pd.DataFrame:
    """
    Aggregate flexibility scores across all subjects and window lengths.
    
    Args:
        subject_ids: List of subject identifiers to process
        
    Returns:
        DataFrame with columns: subject_id, window_length, flexibility_score
    """
    records = []
    
    for subject_id in subject_ids:
        subject_scores = load_subject_flexibility_results(subject_id)
        
        if not subject_scores:
            logger.warning(f"No flexibility scores found for subject {subject_id}, skipping")
            continue
            
        for window_len, score in subject_scores.items():
            records.append({
                'subject_id': subject_id,
                'window_length': window_len,
                'flexibility_score': score
            })
            
    if not records:
        logger.error("No flexibility scores aggregated. Check input data.")
        return pd.DataFrame(columns=['subject_id', 'window_length', 'flexibility_score'])
        
    return pd.DataFrame(records)

def save_flexibility_scores(df: pd.DataFrame, output_path: Path):
    """
    Save flexibility scores to a Parquet file.
    
    Args:
        df: DataFrame containing flexibility scores
        output_path: Path to save the Parquet file
    """
    output_path.parent.mkdir(parents=True, exist_ok=True)
    df.to_parquet(output_path, index=False)
    logger.info(f"Flexibility scores saved to {output_path}")
    logger.info(f"Total records: {len(df)}, Subjects: {df['subject_id'].nunique()}")

def get_available_subjects() -> List[str]:
    """
    Get list of available subjects from the dynamic connectivity results directory.
    
    Returns:
        List of subject IDs that have results
    """
    if not INPUT_DIR.exists():
        logger.error(f"Input directory {INPUT_DIR} does not exist. Run T019 first.")
        return []
    
    return [d.name for d in INPUT_DIR.iterdir() if d.is_dir()]

def main():
    """Main entry point for T021."""
    # Setup logging
    setup_logging()
    
    # Set random seeds for reproducibility
    set_all_seeds()
    
    logger.info("Starting T021: Output flexibility scores")
    
    # Get available subjects
    subject_ids = get_available_subjects()
    
    if not subject_ids:
        logger.error("No subjects found with dynamic connectivity results. Exiting.")
        sys.exit(1)
        
    logger.info(f"Found {len(subject_ids)} subjects with results")
    
    # Aggregate scores
    df_scores = aggregate_flexibility_scores(subject_ids)
    
    if df_scores.empty:
        logger.error("No flexibility scores could be aggregated. Exiting.")
        sys.exit(1)
        
    # Save results
    save_flexibility_scores(df_scores, OUTPUT_FILE)
    
    logger.info("T021 completed successfully")
    return 0

if __name__ == "__main__":
    sys.exit(main())