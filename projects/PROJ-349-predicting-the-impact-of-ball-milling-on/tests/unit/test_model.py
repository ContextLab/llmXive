class TestSplitsAreStratifiedByD50:
    """Test that generated splits are stratified by quantile-binned D50."""

    def test_splits_are_stratified_by_d50(self, sample_dataset):
        """
        Verify that the train/test splits generated by generate_splits
        maintain the same distribution of quantile-binned D50 values
        in both training and testing sets.
        """
        # Generate splits with 5 repeats and 3 inner folds
        n_repeats = 5
        n_inner_folds = 3
        seed = 42
        
        splits = generate_splits(n_repeats=n_repeats, seed=seed)
        
        assert len(splits) > 0, "No splits were generated"
        
        # Check that splits are tuples of (train_idx, test_idx)
        for train_idx, test_idx in splits:
            assert isinstance(train_idx, np.ndarray), "train_idx should be a numpy array"
            assert isinstance(test_idx, np.ndarray), "test_idx should be a numpy array"
            
            # Verify indices are within bounds
            assert np.all(train_idx >= 0) and np.all(train_idx < len(sample_dataset)), \
                "train_idx out of bounds"
            assert np.all(test_idx >= 0) and np.all(test_idx < len(sample_dataset)), \
                "test_idx out of bounds"
            
            # Verify no overlap between train and test
            assert len(np.intersect1d(train_idx, test_idx)) == 0, \
                "Train and test sets overlap"
            
            # Verify that all samples are used (train + test = total)
            assert len(np.union1d(train_idx, test_idx)) == len(sample_dataset), \
                "Not all samples are used in the split"
            
            # Extract D50 values for train and test sets
            train_d50 = sample_dataset.iloc[train_idx]['d50'].values
            test_d50 = sample_dataset.iloc[test_idx]['d50'].values
            
            # Bin D50 values into quantiles (e.g., 4 bins)
            n_bins = 4
            train_bins = pd.qcut(train_d50, q=n_bins, labels=False, duplicates='drop')
            test_bins = pd.qcut(test_d50, q=n_bins, labels=False, duplicates='drop')
            
            # Calculate distribution of bins in train and test
            train_distribution = np.bincount(train_bins, minlength=n_bins) / len(train_bins)
            test_distribution = np.bincount(test_bins, minlength=n_bins) / len(test_bins)
            
            # Check that distributions are similar (within 10% tolerance)
            # This is a loose check to ensure stratification is working
            max_diff = np.max(np.abs(train_distribution - test_distribution))
            assert max_diff < 0.15, \
                f"Stratification failed: max distribution difference is {max_diff:.3f} " \
                f"(train: {train_distribution}, test: {test_distribution})"

    def test_repeats_produce_different_splits(self, sample_dataset):
        """
        Verify that different repeats produce different splits
        (ensuring randomness is actually used).
        """
        splits1 = generate_splits(n_repeats=2, seed=42)
        splits2 = generate_splits(n_repeats=2, seed=43)
        
        # Check that at least some splits are different
        all_different = True
        for (t1_train, t1_test), (t2_train, t2_test) in zip(splits1, splits2):
            if np.array_equal(t1_train, t2_train) and np.array_equal(t1_test, t2_test):
                all_different = False
                break
        
        # With different seeds, we expect at least some differences
        # (though theoretically possible to get same splits by chance, very unlikely)
        assert not all_different, "Different seeds should produce different splits"

    def test_single_repeat_returns_correct_structure(self, sample_dataset):
        """
        Test that a single repeat returns the expected structure.
        """
        splits = generate_splits(n_repeats=1, seed=42)
        
        assert len(splits) == 1, "Single repeat should return one split"
        train_idx, test_idx = splits[0]
        
        assert isinstance(train_idx, np.ndarray)
        assert isinstance(test_idx, np.ndarray)
        assert len(train_idx) + len(test_idx) == len(sample_dataset)

    def test_large_dataset_stratification(self):
        """
        Test stratification on a larger dataset to ensure robustness.
        """
        np.random.seed(123)
        n_samples = 1000
        data = {
            'd50': np.random.uniform(1, 100, n_samples),
            'other_feature': np.random.uniform(0, 1, n_samples)
        }
        df = pd.DataFrame(data)
        
        splits = generate_splits(n_repeats=3, seed=999)
        
        for train_idx, test_idx in splits:
            train_d50 = df.iloc[train_idx]['d50'].values
            test_d50 = df.iloc[test_idx]['d50'].values
            
            # Bin into 5 quantiles
            n_bins = 5
            train_bins = pd.qcut(train_d50, q=n_bins, labels=False, duplicates='drop')
            test_bins = pd.qcut(test_d50, q=n_bins, labels=False, duplicates='drop')
            
            train_dist = np.bincount(train_bins, minlength=n_bins) / len(train_bins)
            test_dist = np.bincount(test_bins, minlength=n_bins) / len(test_bins)
            
            max_diff = np.max(np.abs(train_dist - test_dist))
            assert max_diff < 0.15, \
                f"Stratification failed on large dataset: {max_diff:.3f}"

