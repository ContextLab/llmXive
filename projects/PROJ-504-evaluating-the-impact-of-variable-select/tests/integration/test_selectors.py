class TestFullSelectionPipeline:
    """Integration tests for the complete selection and refit pipeline."""
    
    def test_forward_stepwise_selection_integration(self, simulated_datasets: List[SimulatedDataset]):
        """Test that forward stepwise selection correctly identifies true non-zero coefficients."""
        results = []
        
        for dataset in simulated_datasets:
            # Run forward stepwise selection
            selected_indices = forward_stepwise_selection(dataset.X, dataset.Y)
            
            # Calculate power metrics
            power, fdr = calculate_empirical_power(
                dataset.true_coefficients,
                selected_indices,
                alpha=0.05
            )
            
            results.append({
                "method": "forward_stepwise",
                "snr": dataset.snr,
                "sparsity": dataset.sparsity,
                "power": power,
                "fdr": fdr,
                "selected_count": len(selected_indices)
            })
        
        # Validate results structure
        assert len(results) == len(simulated_datasets), "Should have one result per dataset"
        assert all("power" in r for r in results), "All results should have power metric"
        assert all("fdr" in r for r in results), "All results should have FDR metric"
        
        # Validate power values are in valid range
        for r in results:
            assert 0 <= r["power"] <= 1, f"Power should be between 0 and 1, got {r['power']}"
            assert 0 <= r["fdr"] <= 1, f"FDR should be between 0 and 1, got {r['fdr']}"
        
        logger.info(f"Forward stepwise results: {results}")
    
