import logging
import json
import os
from pathlib import Path
from typing import Dict, List, Tuple, Optional, Any

import pandas as pd
import numpy as np
from sklearn.inspection import permutation_importance
from sklearn.linear_model import LinearRegression
from sklearn.ensemble import RandomForestRegressor
from sklearn.model_selection import train_test_split

from src.utils.logging_config import setup_logging

logger = logging.getLogger(__name__)

# Descriptor columns expected based on T031/T032
DESCRIPTOR_COLUMNS = [
    'avg_electronegativity',
    'valence_electron_concentration',
    'atomic_radii_variance',
    'avg_d_electrons',
    'atomic_size_mismatch'
]

TARGET_COLUMN = 'coercivity_Oe'  # Standardized target from T020

def load_features_data(features_path: Path) -> pd.DataFrame:
    """Load the features dataset generated by T032."""
    if not features_path.exists():
        raise FileNotFoundError(f"Features file not found at {features_path}")
    
    df = pd.read_csv(features_path)
    logger.info(f"Loaded features data: {df.shape[0]} rows, {df.shape[1]} columns")
    
    # Verify required columns exist
    missing_cols = [col for col in DESCRIPTOR_COLUMNS + [TARGET_COLUMN] if col not in df.columns]
    if missing_cols:
        raise ValueError(f"Missing required columns in features data: {missing_cols}")
    
    return df

def prepare_data(df: pd.DataFrame, test_size: float = 0.2, random_state: int = 42) -> Tuple[pd.DataFrame, pd.DataFrame, np.ndarray, np.ndarray]:
    """Split data into features (X) and target (y) and perform train/test split."""
    X = df[DESCRIPTOR_COLUMNS]
    y = df[TARGET_COLUMN]
    
    X_train, X_test, y_train, y_test = train_test_split(
        X, y, test_size=test_size, random_state=random_state
    )
    
    logger.info(f"Data split: Train={X_train.shape[0]}, Test={X_test.shape[0]}")
    return X_train, X_test, y_train, y_test

def calculate_permutation_importance(
    model,
    X_train: pd.DataFrame,
    y_train: np.ndarray,
    X_test: pd.DataFrame,
    y_test: np.ndarray,
    n_repeats: int = 10,
    random_state: int = 42,
    scoring: Optional[str] = None
) -> pd.DataFrame:
    """
    Calculate permutation importance for a trained model.
    
    Args:
        model: Trained sklearn estimator
        X_train, y_train: Training data
        X_test, y_test: Test data
        n_repeats: Number of times to permute a feature
        random_state: Random seed
        scoring: Scoring metric (defaults to R2 for regression)
    
    Returns:
        DataFrame with feature names, mean importance, and std deviation
    """
    result = permutation_importance(
        model, X_test, y_test,
        n_repeats=n_repeats,
        random_state=random_state,
        scoring=scoring,
        n_jobs=1
    )
    
    importance_df = pd.DataFrame({
        'feature': X_test.columns,
        'importance_mean': result.importances_mean,
        'importance_std': result.importances_std,
        'importance_raw': result.importances
    })
    
    # Sort by absolute mean importance (descending)
    importance_df = importance_df.sort_values(
        by='importance_mean', ascending=False
    ).reset_index(drop=True)
    
    return importance_df

def rank_top_descriptors(importance_df: pd.DataFrame, top_n: int = 5) -> List[Dict[str, Any]]:
    """Rank and format top N descriptors by importance."""
    top_features = importance_df.head(top_n)
    
    ranked_list = []
    for idx, row in top_features.iterrows():
        ranked_list.append({
            'rank': idx + 1,
            'feature': row['feature'],
            'importance_mean': float(row['importance_mean']),
            'importance_std': float(row['importance_std']),
            'is_positive_correlation': row['importance_mean'] > 0
        })
    
    return ranked_list

def save_importance_results(
    importance_df: pd.DataFrame,
    ranked_features: List[Dict],
    output_path: Path,
    model_type: str
) -> None:
    """Save importance results to JSON and CSV."""
    output_path.parent.mkdir(parents=True, exist_ok=True)
    
    # Save detailed CSV
    csv_path = output_path.with_suffix('.csv')
    importance_df.to_csv(csv_path, index=False)
    logger.info(f"Saved detailed importance results to {csv_path}")
    
    # Save summary JSON
    json_path = output_path.with_suffix('.json')
    summary = {
        'model_type': model_type,
        'timestamp': pd.Timestamp.now().isoformat(),
        'top_features': ranked_features,
        'total_features': len(importance_df),
        'all_features': importance_df.to_dict(orient='records')
    }
    
    with open(json_path, 'w') as f:
        json.dump(summary, f, indent=2)
    logger.info(f"Saved importance summary to {json_path}")

def run_feature_importance_analysis(
    features_path: Path,
    model_type: str = 'random_forest',
    output_dir: Path = None,
    n_repeats: int = 10,
    test_size: float = 0.2,
    random_state: int = 42
) -> Dict[str, Any]:
    """
    Main function to run permutation importance analysis.
    
    Args:
        features_path: Path to alloys_features.csv
        model_type: 'linear' or 'random_forest'
        output_dir: Directory to save results (default: data/processed/)
        n_repeats: Number of permutation repeats
        test_size: Test set fraction
        random_state: Random seed
    
    Returns:
        Dictionary with analysis results
    """
    if output_dir is None:
        output_dir = Path('data/processed')
    
    output_dir = Path(output_dir)
    output_dir.mkdir(parents=True, exist_ok=True)
    
    # Load data
    logger.info(f"Loading features from {features_path}")
    df = load_features_data(features_path)
    
    # Prepare data
    X_train, X_test, y_train, y_test = prepare_data(
        df, test_size=test_size, random_state=random_state
    )
    
    # Train model
    logger.info(f"Training {model_type} model for importance analysis")
    if model_type == 'linear':
        model = LinearRegression()
    elif model_type == 'random_forest':
        model = RandomForestRegressor(n_estimators=100, random_state=random_state, n_jobs=1)
    else:
        raise ValueError(f"Unknown model_type: {model_type}")
    
    model.fit(X_train, y_train)
    logger.info("Model training complete")
    
    # Calculate importance
    logger.info("Calculating permutation importance...")
    importance_df = calculate_permutation_importance(
        model, X_train, y_train, X_test, y_test,
        n_repeats=n_repeats, random_state=random_state
    )
    
    # Rank top features
    ranked_features = rank_top_descriptors(importance_df, top_n=5)
    
    # Save results
    output_file = output_dir / f"feature_importance_{model_type}"
    save_importance_results(importance_df, ranked_features, output_file, model_type)
    
    # Return summary
    return {
        'model_type': model_type,
        'n_samples': len(df),
        'n_features': len(DESCRIPTOR_COLUMNS),
        'top_feature': ranked_features[0]['feature'] if ranked_features else None,
        'top_importance': ranked_features[0]['importance_mean'] if ranked_features else None,
        'output_csv': str(output_file.with_suffix('.csv')),
        'output_json': str(output_file.with_suffix('.json'))
    }

def main():
    """Entry point for running feature importance analysis."""
    setup_logging()
    
    # Default paths
    features_path = Path('data/processed/alloys_features.csv')
    output_dir = Path('data/processed')
    
    logger.info("Starting Feature Importance Analysis (T036)")
    logger.info(f"Features path: {features_path}")
    logger.info(f"Output directory: {output_dir}")
    
    try:
        # Run for Random Forest (primary model)
        logger.info("=" * 50)
        logger.info("Running Random Forest Importance Analysis")
        logger.info("=" * 50)
        rf_results = run_feature_importance_analysis(
            features_path=features_path,
            model_type='random_forest',
            output_dir=output_dir,
            n_repeats=10,
            test_size=0.2,
            random_state=42
        )
        logger.info(f"Random Forest Top Feature: {rf_results['top_feature']} "
                   f"(Importance: {rf_results['top_importance']:.4f})")
        
        # Run for Linear Regression (baseline)
        logger.info("=" * 50)
        logger.info("Running Linear Regression Importance Analysis")
        logger.info("=" * 50)
        lr_results = run_feature_importance_analysis(
            features_path=features_path,
            model_type='linear',
            output_dir=output_dir,
            n_repeats=10,
            test_size=0.2,
            random_state=42
        )
        logger.info(f"Linear Regression Top Feature: {lr_results['top_feature']} "
                   f"(Importance: {lr_results['top_importance']:.4f})")
        
        # Save combined summary
        summary_path = output_dir / "feature_importance_summary.json"
        with open(summary_path, 'w') as f:
            json.dump({
                'random_forest': rf_results,
                'linear_regression': lr_results
            }, f, indent=2)
        logger.info(f"Saved combined summary to {summary_path}")
        
        logger.info("Feature Importance Analysis (T036) completed successfully")
        
    except FileNotFoundError as e:
        logger.error(f"Data file not found: {e}")
        logger.error("Ensure T032 (Feature Engineering) has been completed first.")
        raise
    except Exception as e:
        logger.error(f"Error during analysis: {e}")
        raise

if __name__ == "__main__":
    main()
