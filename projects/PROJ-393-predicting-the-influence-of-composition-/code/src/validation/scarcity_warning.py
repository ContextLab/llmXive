import logging
from pathlib import Path
from typing import Optional

from src.utils.logging_config import setup_logging, create_logger

logger = create_logger(__name__)

WARNING_REPORT_PATH = Path("docs/reports/data_scarcity_warning.md")

def check_and_warn(n: int, threshold: int) -> None:
    """
    Generate a data scarcity warning report when N < threshold.
    
    This function implements the warning logic required by T028b and T046.
    It creates a markdown report documenting the scarcity issue.
    
    Args:
        n: The actual number of data points.
        threshold: The minimum threshold (default 50).
    """
    setup_logging()
    
    logger.warning(f"Generating scarcity warning: N={n}, Threshold={threshold}")
    
    # Ensure docs/reports directory exists
    WARNING_REPORT_PATH.parent.mkdir(parents=True, exist_ok=True)
    
    report_content = f"""# Data Scarcity Warning

## Summary

A data scarcity condition has been detected in the Heusler alloy dataset.

## Details

- **Actual Data Points (N)**: {n}
- **Threshold**: {threshold}
- **Status**: SCARCITY DETECTED (N < {threshold})

## Implications

1. **Reduced Statistical Power**: With only {n} data points, the statistical power of the analysis is significantly reduced. This increases the risk of Type II errors (failing to detect true effects).

2. **Potential Overfitting**: Machine learning models trained on this small dataset are at high risk of overfitting. The models may memorize noise rather than learning generalizable patterns.

3. **Limited Generalizability**: Conclusions drawn from this analysis may not generalize well to the broader population of Heusler alloys.

4. **Uncertainty in Estimates**: Confidence intervals and error estimates may be wider and less reliable than desired.

## Specification Reference

This warning is generated in accordance with **Spec FR-008**, which mandates:

> "If N < 50, generate a warning report documenting reduced statistical power and potential overfitting."

## Recommendations

1. **Interpret Results Cautiously**: All findings should be treated as preliminary and exploratory.
2. **Cross-Validation**: Ensure rigorous cross-validation is used, but acknowledge its limitations on small datasets.
3. **Data Collection**: Consider expanding the dataset through additional literature review or experimental data collection.
4. **Sensitivity Analysis**: Perform sensitivity analyses to understand how robust the results are to the small sample size.

## Generated At

This report was automatically generated by the scarcity checker module.

---
*End of Report*
"""
    
    try:
        with open(WARNING_REPORT_PATH, 'w') as f:
            f.write(report_content)
        
        logger.info(f"Scarcity warning report written to {WARNING_REPORT_PATH}")
    except Exception as e:
        logger.error(f"Failed to write scarcity warning report: {e}")
        raise

def main() -> int:
    """
    CLI entry point for standalone testing.
    
    Returns:
        int: Exit code (0 for success, 1 for failure).
    """
    try:
        # Default test values if called without arguments
        check_and_warn(30, 50)
        return 0
    except Exception as e:
        logger.critical(f"Scarcity warning generation failed: {e}")
        return 1

if __name__ == "__main__":
    import sys
    sys.exit(main())