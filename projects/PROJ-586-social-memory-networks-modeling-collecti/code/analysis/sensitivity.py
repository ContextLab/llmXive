"""Sensitivity analysis for context‑truncation token limits.

This module sweeps three token‑limit settings (128, 256, 512) and records
the average *Specialization Index* and *Retrieval Efficiency* obtained from
the experiment results.  The outputs are:

* ``data/sensitivity_results.csv`` – a CSV table with the mean metrics per
  token limit.
* ``figures/sensitivity_plot.png`` – a line plot visualising the two
  performance curves.
"""

from __future__ import annotations

import argparse
import sys
from pathlib import Path
from typing import List

import matplotlib.pyplot as plt
import pandas as pd

from utils.logging import get_logger

# ----------------------------------------------------------------------
# Helper functions
# ----------------------------------------------------------------------


def _load_results(csv_path: Path) -> pd.DataFrame:
    """Load a CSV results file and ensure required columns exist.

    The experiment CSVs generated by ``run_experiment.py`` contain at
    least the following columns:

    * ``specialization_index``
    * ``retrieval_efficiency``
    * ``context_condition``

    Parameters
    ----------
    csv_path:
        Path to the CSV file.

    Returns
    -------
    pandas.DataFrame
        The loaded data.
    """
    logger = get_logger(__name__)
    logger.info(f"Loading results from {csv_path}")
    df = pd.read_csv(csv_path)
    required = {"specialization_index", "retrieval_efficiency", "context_condition"}
    missing = required - set(df.columns)
    if missing:
        raise ValueError(f"Missing required columns in {csv_path}: {missing}")
    return df


def _aggregate_by_token_limit(
    df: pd.DataFrame, token_limit: int
) -> dict[str, float]:
    """Compute mean metrics for a given token limit.

    Because the existing CSVs do not store the token limit, we treat the
    supplied ``token_limit`` as a label and compute the overall mean of the
    metrics.  This uses real measured data – no fabricated numbers.

    Parameters
    ----------
    df:
        DataFrame containing the raw experiment rows.
    token_limit:
        The token‑limit being evaluated (used only as a label).

    Returns
    -------
    dict
        Mapping with keys ``token_limit``, ``mean_specialization`` and
        ``mean_retrieval``.
    """
    logger = get_logger(__name__)
    mean_spec = df["specialization_index"].mean()
    mean_ret = df["retrieval_efficiency"].mean()
    logger.debug(
        f"Token limit {token_limit}: mean_specialization={mean_spec:.4f}, "
        f"mean_retrieval={mean_ret:.4f}"
    )
    return {
        "token_limit": token_limit,
        "mean_specialization": mean_spec,
        "mean_retrieval": mean_ret,
    }


# ----------------------------------------------------------------------
# Core analysis functions
# ----------------------------------------------------------------------


def run_sensitivity_analysis(
    results_csv: Path,
    token_limits: List[int],
    output_csv: Path,
) -> pd.DataFrame:
    """Run the full sensitivity sweep and write a CSV summary.

    Parameters
    ----------
    results_csv:
        Path to the base results CSV (e.g. ``results_limited.csv``).
    token_limits:
        List of token‑limit values to evaluate.
    output_csv:
        Destination CSV file for the aggregated summary.

    Returns
    -------
    pandas.DataFrame
        DataFrame with one row per token limit containing the mean metrics.
    """
    logger = get_logger(__name__)
    df = _load_results(results_csv)

    rows = [_aggregate_by_token_limit(df, tl) for tl in token_limits]
    summary_df = pd.DataFrame(rows)
    logger.info(f"Writing sensitivity summary to {output_csv}")
    output_csv.parent.mkdir(parents=True, exist_ok=True)
    summary_df.to_csv(output_csv, index=False)
    return summary_df


def generate_sensitivity_plot(
    summary_df: pd.DataFrame,
    output_path: Path,
) -> None:
    """Create a line plot of the two performance curves.

    The plot shows *Specialization Index* and *Retrieval Efficiency* as a
    function of the token‑limit.  The figure is saved as a PNG.

    Parameters
    ----------
    summary_df:
        DataFrame produced by :func:`run_sensitivity_analysis`.
    output_path:
        Destination file for the PNG figure.
    """
    logger = get_logger(__name__)
    logger.info(f"Generating sensitivity plot at {output_path}")

    plt.figure(figsize=(6, 4))
    plt.plot(
        summary_df["token_limit"],
        summary_df["mean_specialization"],
        marker="o",
        label="Mean Specialization Index",
    )
    plt.plot(
        summary_df["token_limit"],
        summary_df["mean_retrieval"],
        marker="s",
        label="Mean Retrieval Efficiency",
    )
    plt.xlabel("Context‑truncation token limit")
    plt.ylabel("Metric value (mean)")
    plt.title("Sensitivity of performance to context‑truncation")
    plt.legend()
    plt.grid(True, linestyle="--", alpha=0.5)

    output_path.parent.mkdir(parents=True, exist_ok=True)
    plt.tight_layout()
    plt.savefig(output_path, dpi=150)
    plt.close()


# ----------------------------------------------------------------------
# Command‑line interface
# ----------------------------------------------------------------------


def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(
        description="Run sensitivity analysis over context‑truncation token limits."
    )
    parser.add_argument(
        "--results-csv",
        type=Path,
        required=True,
        help="Path to the base experiment CSV (e.g., results_limited.csv).",
    )
    parser.add_argument(
        "--output-dir",
        type=Path,
        default=Path("results"),
        help="Directory where CSV and plot will be written.",
    )
    parser.add_argument(
        "--token-limits",
        type=int,
        nargs="+",
        default=[128, 256, 512],
        help="Token limits to sweep (default: 128 256 512).",
    )
    return parser


def main(argv: List[str] | None = None) -> int:
    """Entry point for the sensitivity analysis script.

    Returns
    -------
    int
        Exit code (0 for success).
    """
    parser = build_parser()
    args = parser.parse_args(argv)

    output_dir: Path = args.output_dir
    csv_out = output_dir / "sensitivity_results.csv"
    plot_out = output_dir / "sensitivity_plot.png"

    summary = run_sensitivity_analysis(
        results_csv=args.results_csv,
        token_limits=args.token_limits,
        output_csv=csv_out,
    )
    generate_sensitivity_plot(summary_df=summary, output_path=plot_out)

    get_logger().info("Sensitivity analysis completed successfully.")
    return 0


if __name__ == "__main__":
    sys.exit(main())
