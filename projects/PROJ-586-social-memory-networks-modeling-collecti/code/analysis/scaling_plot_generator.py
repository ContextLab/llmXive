"""Scaling plot generator for User Story 3.

Generates a PDF plot showing specialization index and retrieval efficiency
versus agent count (3, 5, 7), with fitted power-law curves and an explicit
note that 3 data points limit power-law reliability.
"""
from __future__ import annotations

import argparse
import math
import sys
from dataclasses import dataclass
from pathlib import Path
from typing import List, Tuple, Optional, Dict, Any

import numpy as np
import pandas as pd
import matplotlib
matplotlib.use('Agg')  # Non-interactive backend for CI
import matplotlib.pyplot as plt
from scipy.optimize import curve_fit
from scipy.stats import t

@dataclass
class ScalingPlotResult:
    """Result of scaling plot generation."""
    output_path: Path
    fitted_exponents: Dict[str, float]
    note: str
    n_points: int


def power_law(x: np.ndarray, a: float, b: float) -> np.ndarray:
    """Power-law function: y = a * x^b."""
    return a * np.power(x, b)


def fit_power_law_with_ci(
    x: np.ndarray,
    y: np.ndarray,
    metric_name: str
) -> Tuple[float, float, float]:
    """
    Fit a power-law to data and compute 95% confidence interval for exponent.

    Returns:
        (a, b, b_ci_half_width) where y = a * x^b
    """
    if len(x) < 2:
        raise ValueError("Need at least 2 points to fit a power-law.")

    # Log-transform to linearize: log(y) = log(a) + b * log(x)
    log_x = np.log(x)
    log_y = np.log(y)

    # Fit linear model
    coeffs = np.polyfit(log_x, log_y, 1)
    b_hat = coeffs[0]
    log_a_hat = coeffs[1]
    a_hat = np.exp(log_a_hat)

    # Compute residuals and standard error of the slope
    y_pred = np.polyval(coeffs, log_x)
    residuals = log_y - y_pred
    n = len(x)
    dof = n - 2
    if dof <= 0:
        # Cannot compute CI with fewer than 3 points
        return a_hat, b_hat, float('nan')

    mse = np.sum(residuals ** 2) / dof
    se_b = np.sqrt(mse / np.sum((log_x - np.mean(log_x)) ** 2))

    # 95% CI using t-distribution
    t_crit = t.ppf(0.975, dof)
    ci_half_width = t_crit * se_b

    return a_hat, b_hat, ci_half_width


def load_scaling_data_real(results_dir: Path) -> pd.DataFrame:
    """
    Load scaling data from CSV files generated by run_scaling_experiment.py.

    Expects files: results_scaling_N=3.csv, results_scaling_N=5.csv, results_scaling_N=7.csv
    Each file contains columns: game_id, specialization_index, retrieval_efficiency, ...
    """
    agent_counts = [3, 5, 7]
    data_rows: List[Dict[str, Any]] = []

    for n in agent_counts:
        file_path = results_dir / f"results_scaling_N={n}.csv"
        if not file_path.exists():
            raise FileNotFoundError(f"Expected scaling results file not found: {file_path}")

        df = pd.read_csv(file_path)
        # Aggregate metrics for this agent count
        avg_spec = df['specialization_index'].mean()
        avg_ret = df['retrieval_efficiency'].mean()
        data_rows.append({
            'agent_count': n,
            'avg_specialization_index': avg_spec,
            'avg_retrieval_efficiency': avg_ret,
            'n_games': len(df)
        })

    return pd.DataFrame(data_rows)


def generate_scaling_plot_with_notes(
    results_dir: Path,
    output_path: Path
) -> ScalingPlotResult:
    """
    Generate the scaling plot PDF with fitted power-law curves and reliability note.

    Args:
        results_dir: Directory containing scaling result CSVs.
        output_path: Path to write the PDF plot.

    Returns:
        ScalingPlotResult with metadata.
    """
    df = load_scaling_data_real(results_dir)

    agent_counts = df['agent_count'].values
    spec_vals = df['avg_specialization_index'].values
    ret_vals = df['avg_retrieval_efficiency'].values

    # Prepare figure
    fig, (ax1, ax2) = plt.subplots(1, 2, figsize=(14, 6))

    # Plot Specialization Index
    ax1.scatter(agent_counts, spec_vals, color='blue', label='Measured', zorder=5)
    if len(agent_counts) >= 2:
        try:
            a_spec, b_spec, b_spec_ci = fit_power_law_with_ci(agent_counts, spec_vals, 'specialization')
            x_fit = np.linspace(min(agent_counts), max(agent_counts), 100)
            y_fit = power_law(x_fit, a_spec, b_spec)
            ax1.plot(x_fit, y_fit, 'b--', label=f'Power-law fit (b={b_spec:.3f})')
            ax1.set_title(f'Specialization Index vs. Agent Count\nFit: y = {a_spec:.3f} * x^{b_spec:.3f}')
        except Exception as e:
            ax1.set_title('Specialization Index vs. Agent Count\n(Fit failed: insufficient data)')
    ax1.set_xlabel('Number of Agents')
    ax1.set_ylabel('Specialization Index')
    ax1.grid(True, linestyle='--', alpha=0.6)
    ax1.legend()

    # Plot Retrieval Efficiency
    ax2.scatter(agent_counts, ret_vals, color='green', label='Measured', zorder=5)
    if len(agent_counts) >= 2:
        try:
            a_ret, b_ret, b_ret_ci = fit_power_law_with_ci(agent_counts, ret_vals, 'retrieval')
            x_fit = np.linspace(min(agent_counts), max(agent_counts), 100)
            y_fit = power_law(x_fit, a_ret, b_ret)
            ax2.plot(x_fit, y_fit, 'g--', label=f'Power-law fit (b={b_ret:.3f})')
            ax2.set_title(f'Retrieval Efficiency vs. Agent Count\nFit: y = {a_ret:.3f} * x^{b_ret:.3f}')
        except Exception as e:
            ax2.set_title('Retrieval Efficiency vs. Agent Count\n(Fit failed: insufficient data)')
    ax2.set_xlabel('Number of Agents')
    ax2.set_ylabel('Retrieval Efficiency')
    ax2.grid(True, linestyle='--', alpha=0.6)
    ax2.legend()

    # Add explicit note about 3 data points limitation
    note_text = (
        "NOTE: Power-law fitting is based on only 3 data points (N=3, 5, 7). "
        "This severely limits the statistical reliability of the exponent estimates. "
        "Results should be interpreted as exploratory trends rather than definitive laws."
    )
    fig.text(0.5, 0.02, note_text, ha='center', va='bottom', fontsize=10,
             bbox=dict(boxstyle="round,pad=0.5", facecolor="lightyellow", edgecolor="orange"))

    plt.tight_layout(rect=[0, 0.08, 1, 1])

    # Ensure output directory exists
    output_path.parent.mkdir(parents=True, exist_ok=True)
    plt.savefig(output_path, dpi=150, bbox_inches='tight')
    plt.close(fig)

    # Compute exponents for result object
    exponents = {}
    if len(agent_counts) >= 2:
        try:
            _, b_spec, _ = fit_power_law_with_ci(agent_counts, spec_vals, 'specialization')
            exponents['specialization'] = b_spec
        except:
            exponents['specialization'] = float('nan')
        try:
            _, b_ret, _ = fit_power_law_with_ci(agent_counts, ret_vals, 'retrieval')
            exponents['retrieval'] = b_ret
        except:
            exponents['retrieval'] = float('nan')

    return ScalingPlotResult(
        output_path=output_path,
        fitted_exponents=exponents,
        note=note_text,
        n_points=len(agent_counts)
    )


def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(
        description="Generate scaling plot PDF with power-law fits and reliability note."
    )
    parser.add_argument(
        "--results-dir",
        type=Path,
        default=Path("projects/PROJ-586-social-memory-networks-modeling-collecti/results"),
        help="Directory containing scaling result CSVs."
    )
    parser.add_argument(
        "--output",
        type=Path,
        default=Path("projects/PROJ-586-social-memory-networks-modeling-collecti/results/scaling_plot.pdf"),
        help="Output path for the PDF plot."
    )
    return parser


def main() -> int:
    parser = build_parser()
    args = parser.parse_args()

    try:
        result = generate_scaling_plot_with_notes(args.results_dir, args.output)
        print(f"Generated scaling plot: {result.output_path}")
        print(f"Data points used: {result.n_points}")
        print(f"Fitted exponents: {result.fitted_exponents}")
        print(f"Note: {result.note}")
        return 0
    except FileNotFoundError as e:
        print(f"Error: {e}", file=sys.stderr)
        return 1
    except Exception as e:
        print(f"Unexpected error: {e}", file=sys.stderr)
        import traceback
        traceback.print_exc()
        return 1


if __name__ == "__main__":
    sys.exit(main())
