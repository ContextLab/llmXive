def power_law(x: np.ndarray, a: float, b: float) -> np.ndarray:
    """Power law function: y = a * x^b."""
    return a * np.power(x, b)


def fit_power_law_with_ci(
    x: np.ndarray, y: np.ndarray
) -> Tuple[float, float, float, float]:
    """
    Fit a power law y = a * x^b using log-log linear regression.
    Returns (a, b, b_lower_ci, b_upper_ci) for 95% confidence.

    Uses log-log transformation for robustness with small N.
    """
    if len(x) < 2:
        raise ValueError("Need at least 2 points to fit a trend.")

    # Log transform
    log_x = np.log(x)
    log_y = np.log(y)

    # Linear regression on logs
    slope, intercept, r_value, p_value, std_err = linregress(log_x, log_y)

    # Parameters
    b = slope
    a = math.exp(intercept)

    # 95% CI for slope (b)
    # t-critical for 95% CI with n-2 df
    from scipy import stats
    df = len(x) - 2
    t_crit = stats.t.ppf(0.975, df) if df > 0 else 1.96

    b_lower = b - t_crit * std_err
    b_upper = b + t_crit * std_err

    return a, b, b_lower, b_upper


def load_scaling_data_real() -> pd.DataFrame:
    """
    Load scaling metrics from the real experiment output.
    Expects `data/scaling_metrics.csv` generated by T027/T028.
    """
    data_path = _ROOT / "data" / "scaling_metrics.csv"
    if not data_path.exists():
        raise FileNotFoundError(
            f"Scaling data not found at {data_path}. "
            "Please run the scaling simulation (T027) first."
        )

    df = pd.read_csv(data_path)

    # Aggregate by agent_count if multiple games were run
    # Expected columns: agent_count, specialization_index, retrieval_efficiency
    if "agent_count" not in df.columns:
        raise ValueError("Input CSV must contain 'agent_count' column.")

    # Mean metrics per agent count
    summary = df.groupby("agent_count").agg(
        {
            "specialization_index": "mean",
            "retrieval_efficiency": "mean",
            "game_id": "count",
        }
    ).reset_index()
    summary.rename(columns={"game_id": "n_games"}, inplace=True)

    return summary


def generate_scaling_plot_with_notes(
    data: pd.DataFrame,
    output_path: Path,
) -> Dict[str, Any]:
    """
    Generate the scaling plot with fitted power-law curves and explicit reliability note.

    Args:
        data: DataFrame with columns [agent_count, specialization_index, retrieval_efficiency]
        output_path: Where to save the PDF.

    Returns:
        Dict with fitting results.
    """
    agent_counts = data["agent_count"].values.astype(float)
    spec_indices = data["specialization_index"].values.astype(float)
    ret_effs = data["retrieval_efficiency"].values.astype(float)

    # Fit power laws
    try:
        a_spec, b_spec, b_spec_lo, b_spec_hi = fit_power_law_with_ci(agent_counts, spec_indices)
        a_ret, b_ret, b_ret_lo, b_ret_hi = fit_power_law_with_ci(agent_counts, ret_effs)
    except ValueError as e:
        # Fallback if fitting fails (e.g., too few points)
        b_spec, b_ret = 0.0, 0.0
        b_spec_lo, b_spec_hi = 0.0, 0.0
        b_ret_lo, b_ret_hi = 0.0, 0.0
        a_spec, a_ret = 1.0, 1.0

    # Create plot
    fig, ax = plt.subplots(figsize=(10, 6))

    # Plot raw data points
    ax.scatter(agent_counts, spec_indices, color="blue", label="Specialization Index (Measured)", zorder=5, s=80)
    ax.scatter(agent_counts, ret_effs, color="green", label="Retrieval Efficiency (Measured)", zorder=5, s=80, marker='^')

    # Generate smooth curve for fit
    x_fit = np.linspace(min(agent_counts), max(agent_counts), 100)
    y_spec_fit = power_law(x_fit, a_spec, b_spec)
    y_ret_fit = power_law(x_fit, a_ret, b_ret)

    ax.plot(x_fit, y_spec_fit, color="blue", linestyle="--", alpha=0.7,
            label=f"Power-law Fit (Spec): $y = {a_spec:.2f}x^{{{b_spec:.2f}}}$")
    ax.plot(x_fit, y_ret_fit, color="green", linestyle="--", alpha=0.7,
            label=f"Power-law Fit (Ret): $y = {a_ret:.2f}x^{{{b_ret:.2f}}}$")

    # Annotations
    ax.set_xlabel("Number of Agents (N)", fontsize=12)
    ax.set_ylabel("Metric Value", fontsize=12)
    ax.set_title("Scaling of Collective Remembering Metrics (User Story 3)", fontsize=14)
    ax.legend(loc="upper left")
    ax.grid(True, linestyle=":", alpha=0.6)

    # Explicit note about 3 data points limitation
    note_text = (
        "NOTE: Only 3 data points (N=3, 5, 7) available.\n"
        "Power-law reliability is limited; exponent estimates are preliminary."
    )
    fig.text(
        0.5, 0.02,
        note_text,
        ha="center",
        fontsize=10,
        style="italic",
        bbox=dict(facecolor="white", alpha=0.8, boxstyle="round,pad=0.5")
    )

    # Ensure output directory exists
    output_path.parent.mkdir(parents=True, exist_ok=True)

    # Save
    plt.tight_layout(rect=[0, 0.08, 1, 1])  # Make room for the note
    plt.savefig(output_path, dpi=300)
    plt.close()

    return {
        "specialization_exponent": b_spec,
        "specialization_exponent_ci": (b_spec_lo, b_spec_hi),
        "retrieval_exponent": b_ret,
        "retrieval_exponent_ci": (b_ret_lo, b_ret_hi),
        "output_path": str(output_path),
    }


def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(
        description="Generate scaling plot with power-law fits and reliability note."
    )
    parser.add_argument(
        "--input",
        type=str,
        default="data/scaling_metrics.csv",
        help="Path to input scaling metrics CSV.",
    )
    parser.add_argument(
        "--output",
        type=str,
        default="projects/PROJ-586-social-memory-networks-modeling-collecti/results/scaling_plot.pdf",
        help="Path for output PDF.",
    )
    return parser


