"""
Scaling Plot Generator for Social Memory Networks.

Generates scaling_plot.pdf with fitted power-law curves and explicit notes
about the limitation of having only 3 data points.

This module loads real scaling data (generated by run_scaling_experiment),
fits a power-law model, computes confidence intervals, and produces a PDF
plot with appropriate annotations.
"""
from __future__ import annotations

import argparse
import math
import sys
from dataclasses import dataclass
from pathlib import Path
from typing import Any, Dict, List, Optional, Tuple

import numpy as np
import pandas as pd

# Try to import matplotlib, fall back gracefully if not available
try:
    import matplotlib
    matplotlib.use('Agg')  # Non-interactive backend for PDF generation
    import matplotlib.pyplot as plt
    HAS_MATPLOTLIB = True
except ImportError:
    HAS_MATPLOTLIB = False


@dataclass
class ScalingPlotResult:
    """Result of scaling plot generation."""
    success: bool
    plot_path: str
    exponent: Optional[float]
    exponent_ci_lower: Optional[float]
    exponent_ci_upper: Optional[float]
    r_squared: Optional[float]
    message: str


def power_law(x: np.ndarray, beta: float, alpha: float) -> np.ndarray:
    """
    Compute power-law function: y = alpha * x^beta
    
    Args:
        x: Input values
        beta: Power-law exponent
        alpha: Scaling coefficient
    
    Returns:
        Computed y values
    """
    return alpha * np.power(x, beta)


def fit_power_law_with_ci(
    x: np.ndarray,
    y: np.ndarray,
    confidence_level: float = 0.95
) -> Tuple[float, float, float, float, float]:
    """
    Fit a power-law model to data and compute confidence intervals.
    
    The model is: y = alpha * x^beta
    Linearized as: log(y) = log(alpha) + beta * log(x)
    
    Args:
        x: Independent variable values
        y: Dependent variable values
        confidence_level: Confidence level for intervals (default 0.95)
    
    Returns:
        Tuple of (exponent, alpha, exponent_ci_lower, exponent_ci_upper, r_squared)
    """
    if len(x) < 2:
        raise ValueError("At least 2 data points required for fitting")
    
    # Filter out non-positive values for log transformation
    mask = (x > 0) & (y > 0)
    x_filtered = x[mask]
    y_filtered = y[mask]
    
    if len(x_filtered) < 2:
        raise ValueError("Need at least 2 positive data points for power-law fitting")
    
    # Linearize: log(y) = log(alpha) + beta * log(x)
    log_x = np.log(x_filtered)
    log_y = np.log(y_filtered)
    
    # Fit linear model
    n = len(log_x)
    sum_x = np.sum(log_x)
    sum_y = np.sum(log_y)
    sum_xy = np.sum(log_x * log_y)
    sum_x2 = np.sum(log_x ** 2)
    
    # Slope (beta) and intercept (log(alpha))
    denominator = n * sum_x2 - sum_x ** 2
    if abs(denominator) < 1e-10:
        raise ValueError("Cannot fit: insufficient variance in log(x)")
    
    beta = (n * sum_xy - sum_x * sum_y) / denominator
    log_alpha = (sum_y - beta * sum_x) / n
    alpha = np.exp(log_alpha)
    
    # Compute R-squared
    y_pred = beta * log_x + log_alpha
    ss_res = np.sum((log_y - y_pred) ** 2)
    ss_tot = np.sum((log_y - np.mean(log_y)) ** 2)
    r_squared = 1 - (ss_res / ss_tot) if ss_tot > 0 else 0.0
    
    # Compute standard error of slope for confidence interval
    if n > 2:
        se_beta = np.sqrt(ss_res / (n - 2)) / np.sqrt(np.sum((log_x - np.mean(log_x)) ** 2))
        # t-critical value for 95% CI (approximate with 1.96 for large n)
        t_crit = 1.96 if n > 30 else 2.0  # Simplified; would use scipy.stats.t for exact
        ci_lower = beta - t_crit * se_beta
        ci_upper = beta + t_crit * se_beta
    else:
        # With only 3 points, CI is unreliable; report wide bounds
        ci_lower = beta - 1.0
        ci_upper = beta + 1.0
    
    return beta, alpha, ci_lower, ci_upper, r_squared


def load_scaling_data_real(data_path: Path) -> pd.DataFrame:
    """
    Load scaling experiment results from CSV.
    
    Args:
        data_path: Path to scaling data CSV file
    
    Returns:
        DataFrame with agent counts and metrics
    """
    if not data_path.exists():
        raise FileNotFoundError(f"Scaling data not found at {data_path}")
    
    df = pd.read_csv(data_path)
    
    required_cols = ['agent_count', 'specialization_index', 'retrieval_efficiency']
    missing = [col for col in required_cols if col not in df.columns]
    if missing:
        raise ValueError(f"Missing required columns: {missing}")
    
    return df


def generate_scaling_plot_with_notes(
    data_path: Path,
    output_path: Path,
    metric: str = 'specialization_index'
) -> ScalingPlotResult:
    """
    Generate scaling plot with power-law fit and explicit reliability notes.
    
    Args:
        data_path: Path to input scaling data CSV
        output_path: Path for output PDF
        metric: Metric to plot ('specialization_index' or 'retrieval_efficiency')
    
    Returns:
        ScalingPlotResult with plot details
    """
    if not HAS_MATPLOTLIB:
        return ScalingPlotResult(
            success=False,
            plot_path=str(output_path),
            exponent=None,
            exponent_ci_lower=None,
            exponent_ci_upper=None,
            r_squared=None,
            message="matplotlib not available for plot generation"
        )
    
    try:
        # Load data
        df = load_scaling_data_real(data_path)
        
        # Filter for agent counts 3, 5, 7
        valid_counts = [3, 5, 7]
        df_filtered = df[df['agent_count'].isin(valid_counts)].copy()
        
        if len(df_filtered) < 3:
            return ScalingPlotResult(
                success=False,
                plot_path=str(output_path),
                exponent=None,
                exponent_ci_lower=None,
                exponent_ci_upper=None,
                r_squared=None,
                message=f"Insufficient data: only {len(df_filtered)} agent counts found"
            )
        
        # Aggregate by agent count (mean of multiple runs)
        aggregated = df_filtered.groupby('agent_count').agg({
            metric: 'mean',
            'specialization_index': 'std',
            'retrieval_efficiency': 'std'
        }).reset_index()
        
        # Sort by agent count
        aggregated = aggregated.sort_values('agent_count')
        
        x = aggregated['agent_count'].values.astype(float)
        y = aggregated[metric].values.astype(float)
        
        # Fit power-law
        try:
            beta, alpha, ci_lower, ci_upper, r_squared = fit_power_law_with_ci(x, y)
        except ValueError as e:
            return ScalingPlotResult(
                success=False,
                plot_path=str(output_path),
                exponent=None,
                exponent_ci_lower=None,
                exponent_ci_upper=None,
                r_squared=None,
                message=f"Power-law fitting failed: {str(e)}"
            )
        
        # Create plot
        fig, ax = plt.subplots(figsize=(10, 6))
        
        # Plot data points with error bars
        ax.errorbar(
            x, y,
            yerr=aggregated[f'{metric}'],
            fmt='o',
            capsize=5,
            label='Measured Data',
            color='blue',
            ecolor='blue',
            alpha=0.7
        )
        
        # Plot fitted curve
        x_fit = np.linspace(min(x), max(x), 100)
        y_fit = power_law(x_fit, beta, alpha)
        ax.plot(x_fit, y_fit, 'r-', linewidth=2, label=f'Power-law fit: y = {alpha:.4f} * x^({beta:.4f})')
        
        # Add annotation about 3 data points limitation
        annotation_text = (
            "⚠ CAUTION: Only 3 data points (agent counts 3, 5, 7).\n"
            "Power-law reliability is limited; exponent estimates\n"
            "should be interpreted with caution."
        )
        
        # Position annotation in upper right, slightly inset
        ax.text(
            0.98, 0.02,
            annotation_text,
            transform=ax.transAxes,
            fontsize=9,
            verticalalignment='bottom',
            horizontalalignment='right',
            bbox=dict(boxstyle='round', facecolor='wheat', alpha=0.8),
            fontfamily='monospace'
        )
        
        # Add fit statistics
        stats_text = (
            f"Exponent (β): {beta:.4f}\n"
            f"95% CI: [{ci_lower:.4f}, {ci_upper:.4f}]\n"
            f"R²: {r_squared:.4f}"
        )
        ax.text(
            0.02, 0.98,
            stats_text,
            transform=ax.transAxes,
            fontsize=10,
            verticalalignment='top',
            fontfamily='monospace',
            bbox=dict(boxstyle='round', facecolor='lightblue', alpha=0.5)
        )
        
        ax.set_xlabel('Number of Agents', fontsize=12)
        ax.set_ylabel(metric.replace('_', ' ').title(), fontsize=12)
        ax.set_title(f'Scaling Analysis: {metric.replace("_", " ").title()} vs. Agent Count', fontsize=14)
        ax.legend(loc='upper right')
        ax.grid(True, alpha=0.3)
        
        # Ensure output directory exists
        output_path.parent.mkdir(parents=True, exist_ok=True)
        
        # Save plot
        plt.tight_layout()
        plt.savefig(output_path, dpi=150, bbox_inches='tight')
        plt.close(fig)
        
        return ScalingPlotResult(
            success=True,
            plot_path=str(output_path),
            exponent=beta,
            exponent_ci_lower=ci_lower,
            exponent_ci_upper=ci_upper,
            r_squared=r_squared,
            message=f"Successfully generated plot for {metric}"
        )
        
    except Exception as e:
        return ScalingPlotResult(
            success=False,
            plot_path=str(output_path),
            exponent=None,
            exponent_ci_lower=None,
            exponent_ci_upper=None,
            r_squared=None,
            message=f"Error generating plot: {str(e)}"
        )


def build_parser() -> argparse.ArgumentParser:
    """Build argument parser for the script."""
    parser = argparse.ArgumentParser(
        description='Generate scaling plot with power-law fit and reliability notes.'
    )
    parser.add_argument(
        '--data',
        type=str,
        default='data/scaling_results.csv',
        help='Path to scaling data CSV (default: data/scaling_results.csv)'
    )
    parser.add_argument(
        '--output',
        type=str,
        default='results/scaling_plot.pdf',
        help='Path for output PDF (default: results/scaling_plot.pdf)'
    )
    parser.add_argument(
        '--metric',
        type=str,
        choices=['specialization_index', 'retrieval_efficiency'],
        default='specialization_index',
        help='Metric to plot (default: specialization_index)'
    )
    return parser


def main() -> int:
    """Main entry point for the script."""
    parser = build_parser()
    args = parser.parse_args()
    
    data_path = Path(args.data)
    output_path = Path(args.output)
    
    print(f"Loading scaling data from: {data_path}")
    print(f"Generating plot for metric: {args.metric}")
    print(f"Output will be saved to: {output_path}")
    
    result = generate_scaling_plot_with_notes(data_path, output_path, args.metric)
    
    if result.success:
        print(f"\n✓ Plot generated successfully: {result.plot_path}")
        print(f"  Exponent (β): {result.exponent:.4f}")
        print(f"  95% CI: [{result.exponent_ci_lower:.4f}, {result.exponent_ci_upper:.4f}]")
        print(f"  R²: {result.r_squared:.4f}")
        print(f"\n  ⚠ Note: Only 3 data points limit power-law reliability.")
        return 0
    else:
        print(f"\n✗ Failed to generate plot: {result.message}")
        return 1


if __name__ == '__main__':
    sys.exit(main())
