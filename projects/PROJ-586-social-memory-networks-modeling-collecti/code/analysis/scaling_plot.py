def build_parser() -> argparse.ArgumentParser:
    """Build argument parser for the scaling plot script."""
    parser = argparse.ArgumentParser(
        description="Generate scaling plot with power-law fits and limitation notes."
    )
    parser.add_argument(
        "--results-dir",
        type=str,
        default="projects/PROJ-586-social-memory-networks-modeling-collecti/results",
        help="Directory containing scaling_confidence_intervals.json"
    )
    parser.add_argument(
        "--output",
        type=str,
        default=None,
        help="Output path for the PDF (default: results/scaling_plot.pdf)"
    )
    return parser


def main():
    """Main entry point for the scaling plot generation."""
    parser = build_parser()
    args = parser.parse_args()

    results_dir = Path(args.results_dir)
    output_path = Path(args.output) if args.output else None

    print(f"Loading scaling results from {results_dir}...")
    try:
        agent_counts, spec_indices, ret_effs, ci_bounds = load_scaling_results_for_plot(results_dir)
    except FileNotFoundError as e:
        print(f"ERROR: {e}", file=sys.stderr)
        sys.exit(1)

    if len(agent_counts) < 3:
        print(
            f"WARNING: Only {len(agent_counts)} data points available. "
            "Power-law fitting requires at least 3 points for meaningful results.",
            file=sys.stderr
        )

    print(f"Generating scaling plot...")
    print(f"  Agent counts: {agent_counts}")
    print(f"  Specialization indices: {spec_indices}")
    print(f"  Retrieval efficiencies: {ret_effs}")

    try:
        output_path = generate_scaling_plot_with_notes(
            agent_counts=agent_counts,
            specialization_indices=spec_indices,
            retrieval_efficiencies=ret_effs,
            ci_bounds=ci_bounds,
            output_path=output_path
        )
        print(f"SUCCESS: Scaling plot saved to {output_path}")
    except Exception as e:
        print(f"ERROR generating plot: {e}", file=sys.stderr)
        sys.exit(1)


if __name__ == "__main__":
    main()
