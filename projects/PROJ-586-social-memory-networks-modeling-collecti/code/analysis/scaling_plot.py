"""Scaling plot generation for User Story 3.

Generates a PDF plot of specialization index and retrieval efficiency vs.
agent count, with fitted power-law curves and a note about data limitations.
"""
from __future__ import annotations

import argparse
import json
import math
import sys
import warnings
from pathlib import Path
from typing import Any, Dict, List, Optional, Tuple

import matplotlib
matplotlib.use('Agg')  # Non-interactive backend for CI
import matplotlib.pyplot as plt
import numpy as np
import pandas as pd

# Import from existing project modules
from analysis.scaling import fit_power_law, load_scaling_data


def power_law(x: np.ndarray, a: float, b: float) -> np.ndarray:
    """Power law function: y = a * x^b."""
    return a * np.power(x, b)


def load_scaling_results_for_plot(results_dir: Path) -> pd.DataFrame:
    """Load scaling results from JSON file generated by scaling_ci analysis.

    Args:
        results_dir: Path to the results directory containing scaling_confidence_intervals.json

    Returns:
        DataFrame with agent_count, specialization_index, retrieval_efficiency,
        and their confidence intervals.
    """
    json_path = results_dir / "scaling_confidence_intervals.json"
    if not json_path.exists():
        raise FileNotFoundError(
            f"Scaling results not found at {json_path}. "
            "Run T029 (scaling_ci analysis) first to generate this file."
        )

    with open(json_path, 'r') as f:
        data = json.load(f)

    # Convert to DataFrame
    records = []
    for entry in data.get('results', []):
        records.append({
            'agent_count': entry['agent_count'],
            'specialization_index': entry['specialization']['mean'],
            'specialization_ci_lower': entry['specialization']['ci_lower'],
            'specialization_ci_upper': entry['specialization']['ci_upper'],
            'retrieval_efficiency': entry['retrieval']['mean'],
            'retrieval_ci_lower': entry['retrieval']['ci_lower'],
            'retrieval_ci_upper': entry['retrieval']['ci_upper'],
            'specialization_exponent': entry.get('specialization_exponent'),
            'retrieval_exponent': entry.get('retrieval_exponent'),
        })

    df = pd.DataFrame(records)
    return df


def generate_scaling_plot_with_notes(
    df: pd.DataFrame,
    output_path: Path,
    note_text: str = "3 data points limit power-law reliability"
) -> Dict[str, Any]:
    """Generate scaling plot with power-law fits and limitation note.

    Args:
        df: DataFrame with agent counts and metrics
        output_path: Path to save the PDF plot
        note_text: Text note about data limitations

    Returns:
        Dictionary with plot metadata and fit parameters
    """
    if len(df) < 2:
        raise ValueError("Need at least 2 data points to fit a power law")

    # Sort by agent count
    df = df.sort_values('agent_count').reset_index(drop=True)

    agent_counts = df['agent_count'].values
    spec_indices = df['specialization_index'].values
    ret_effs = df['retrieval_efficiency'].values

    # Fit power laws
    spec_a, spec_b = fit_power_law(agent_counts, spec_indices)
    ret_a, ret_b = fit_power_law(agent_counts, ret_effs)

    # Generate smooth curve for plotting
    x_smooth = np.linspace(agent_counts.min(), agent_counts.max(), 100)
    spec_curve = power_law(x_smooth, spec_a, spec_b)
    ret_curve = power_law(x_smooth, ret_a, ret_b)

    # Create figure with two subplots
    fig, (ax1, ax2) = plt.subplots(1, 2, figsize=(14, 6))

    # Plot specialization index
    ax1.errorbar(
        agent_counts, spec_indices,
        yerr=[spec_indices - df['specialization_ci_lower'],
              df['specialization_ci_upper'] - spec_indices],
        fmt='o-', color='blue', capsize=5, label='Specialization Index'
    )
    ax1.plot(x_smooth, spec_curve, 'b--', label=f'Power-law fit (β={spec_b:.3f})')
    ax1.set_xlabel('Number of Agents')
    ax1.set_ylabel('Specialization Index')
    ax1.set_title('Specialization Index vs. Agent Count')
    ax1.legend()
    ax1.grid(True, alpha=0.3)
    ax1.set_xscale('log')
    ax1.set_yscale('log')

    # Plot retrieval efficiency
    ax2.errorbar(
        agent_counts, ret_effs,
        yerr=[ret_effs - df['retrieval_ci_lower'],
              df['retrieval_ci_upper'] - ret_effs],
        fmt='s-', color='green', capsize=5, label='Retrieval Efficiency'
    )
    ax2.plot(x_smooth, ret_curve, 'g--', label=f'Power-law fit (β={ret_b:.3f})')
    ax2.set_xlabel('Number of Agents')
    ax2.set_ylabel('Retrieval Efficiency')
    ax2.set_title('Retrieval Efficiency vs. Agent Count')
    ax2.legend()
    ax2.grid(True, alpha=0.3)
    ax2.set_xscale('log')
    ax2.set_yscale('log')

    # Add note about data limitations
    fig.suptitle(
        f'Scaling Analysis of Collective Remembering\n({note_text})',
        fontsize=14, fontweight='bold', y=1.02
    )

    # Adjust layout to prevent overlap
    plt.tight_layout()

    # Save to PDF
    plt.savefig(output_path, dpi=150, bbox_inches='tight')
    plt.close(fig)

    # Return metadata
    return {
        'output_path': str(output_path),
        'agent_counts': list(agent_counts),
        'specialization_fit': {'a': float(spec_a), 'b': float(spec_b)},
        'retrieval_fit': {'a': float(ret_a), 'b': float(ret_b)},
        'data_points': len(df),
        'note': note_text,
    }


def build_parser() -> argparse.ArgumentParser:
    """Build argument parser for scaling plot generation."""
    parser = argparse.ArgumentParser(
        description='Generate scaling plot with power-law fits and limitation notes'
    )
    parser.add_argument(
        '--results-dir',
        type=Path,
        default=Path('projects/PROJ-586-social-memory-networks-modeling-collecti/results'),
        help='Directory containing scaling_confidence_intervals.json'
    )
    parser.add_argument(
        '--output',
        type=Path,
        default=Path('projects/PROJ-586-social-memory-networks-modeling-collecti/results/scaling_plot.pdf'),
        help='Output path for the PDF plot'
    )
    parser.add_argument(
        '--note',
        type=str,
        default='3 data points limit power-law reliability',
        help='Text note about data limitations to include in the plot'
    )
    return parser


def main() -> int:
    """Main entry point for scaling plot generation."""
    parser = build_parser()
    args = parser.parse_args()

    # Ensure output directory exists
    args.output.parent.mkdir(parents=True, exist_ok=True)

    try:
        # Load data
        df = load_scaling_results_for_plot(args.results_dir)

        # Generate plot
        metadata = generate_scaling_plot_with_notes(
            df=df,
            output_path=args.output,
            note_text=args.note
        )

        print(f"Scaling plot generated: {args.output}")
        print(f"Data points: {metadata['data_points']}")
        print(f"Specialization exponent: {metadata['specialization_fit']['b']:.4f}")
        print(f"Retrieval exponent: {metadata['retrieval_fit']['b']:.4f}")
        print(f"Note: {metadata['note']}")

        # Save metadata
        meta_path = args.output.with_suffix('.json')
        with open(meta_path, 'w') as f:
            json.dump(metadata, f, indent=2)
        print(f"Metadata saved: {meta_path}")

        return 0

    except FileNotFoundError as e:
        print(f"Error: {e}", file=sys.stderr)
        return 1
    except Exception as e:
        print(f"Error generating plot: {e}", file=sys.stderr)
        import traceback
        traceback.print_exc()
        return 1


if __name__ == '__main__':
    sys.exit(main())