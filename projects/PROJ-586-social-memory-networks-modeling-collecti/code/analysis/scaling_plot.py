"""
Scaling Plot Generation for Social Memory Networks (Task T030).

Generates `scaling_plot.pdf` containing fitted power-law curves for
specialization index and retrieval efficiency versus agent count.

Includes an explicit text note stating that "3 data points limit power-law reliability".
"""
from __future__ import annotations

import argparse
import json
import math
import sys
import warnings
from pathlib import Path
from typing import Dict, List, Optional, Tuple

import matplotlib
import matplotlib.pyplot as plt
import numpy as np
from scipy.optimize import curve_fit

# Ensure non-interactive backend for CI/headless environments
matplotlib.use("Agg")

# --- Power Law Fitting Utilities ---

def power_law(x: np.ndarray, a: float, b: float) -> np.ndarray:
    """
    Power law function: y = a * x^b
    """
    return a * np.power(x, b)

def fit_power_law(
    x: np.ndarray, y: np.ndarray
) -> Tuple[float, float, Optional[Dict[str, float]]]:
    """
    Fit a power law y = a * x^b to the data using non-linear least squares.

    Returns:
        a, b: Fitted parameters.
        perr: Standard deviation of parameters (if fit succeeds), else None.
    """
    if len(x) < 2:
        raise ValueError("At least two data points are required for fitting.")

    # Filter out non-positive values which break log transforms and power laws
    mask = (x > 0) & (y > 0)
    if np.sum(mask) < 2:
        raise ValueError("Insufficient positive data points for power-law fitting.")

    x_fit = x[mask]
    y_fit = y[mask]

    # Initial guess: a=1, b=1
    p0 = [1.0, 1.0]

    try:
        with warnings.catch_warnings():
            warnings.simplefilter("ignore", RuntimeWarning)
            popt, pcov = curve_fit(power_law, x_fit, y_fit, p0=p0, maxfev=5000)
            a, b = popt
            perr = np.sqrt(np.diag(pcov))
        return a, b, {"a_std": float(perr[0]), "b_std": float(perr[1])}
    except RuntimeError:
        # Fallback to log-log linear regression if non-linear fit fails
        # log(y) = log(a) + b * log(x)
        log_x = np.log(x_fit)
        log_y = np.log(y_fit)
        slope, intercept = np.polyfit(log_x, log_y, 1)
        a = math.exp(intercept)
        b = slope
        # Approximate error estimation
        residuals = log_y - (slope * log_x + intercept)
        std_err = np.std(residuals)
        return a, b, {"a_std": float(std_err), "b_std": float(std_err)}

# --- Data Loading ---

def load_scaling_results_for_plot(
    results_path: Path,
) -> Dict[str, List[Tuple[int, float, float]]]:
    """
    Load scaling results from a JSON file generated by the scaling analysis.

    Expected JSON structure:
    {
        "agent_counts": [3, 5, 7],
        "specialization_indices": [0.1, 0.2, 0.3],
        "retrieval_efficiencies": [0.9, 0.85, 0.8]
    }

    Returns:
        Dict mapping metric names to list of (agent_count, value) tuples.
    """
    if not results_path.exists():
        raise FileNotFoundError(f"Scaling results file not found: {results_path}")

    with open(results_path, "r", encoding="utf-8") as f:
        data = json.load(f)

    agent_counts = data.get("agent_counts", [])
    spec_indices = data.get("specialization_indices", [])
    ret_effs = data.get("retrieval_efficiencies", [])

    if not agent_counts or not spec_indices or not ret_effs:
        raise ValueError("Scaling results file is missing required data arrays.")

    if not (len(agent_counts) == len(spec_indices) == len(ret_effs)):
        raise ValueError("Mismatched array lengths in scaling results file.")

    return {
        "specialization_index": list(zip(agent_counts, spec_indices)),
        "retrieval_efficiency": list(zip(agent_counts, ret_effs)),
    }

# --- Plot Generation ---

def generate_scaling_plot_with_notes(
    results_path: Path, output_path: Path, note_text: str
) -> None:
    """
    Generate a PDF plot with fitted power-law curves and a reliability note.

    Args:
        results_path: Path to the JSON file containing scaling results.
        output_path: Path where the PDF plot will be saved.
        note_text: Text to display in the plot (e.g., reliability warning).
    """
    data = load_scaling_results_for_plot(results_path)

    # Create figure with two subplots
    fig, (ax1, ax2) = plt.subplots(1, 2, figsize=(14, 6))

    # Common settings
    colors = {"specialization_index": "blue", "retrieval_efficiency": "green"}
    labels = {
        "specialization_index": "Specialization Index",
        "retrieval_efficiency": "Retrieval Efficiency",
    }

    for metric_name, points in data.items():
        if not points:
            continue

        agent_counts = np.array([p[0] for p in points])
        values = np.array([p[1] for p in points])

        # Plot raw data points
        ax = ax1 if metric_name == "specialization_index" else ax2
        ax.scatter(
            agent_counts,
            values,
            color=colors[metric_name],
            label="Measured Data",
            zorder=5,
        )

        # Fit power law
        try:
            a, b, _ = fit_power_law(agent_counts, values)
            x_fit = np.linspace(min(agent_counts), max(agent_counts), 100)
            y_fit = power_law(x_fit, a, b)
            ax.plot(x_fit, y_fit, color=colors[metric_name], linestyle="--", alpha=0.7)
        except ValueError as e:
            # If fitting fails (e.g., not enough points), just plot points
            warnings.warn(f"Could not fit power law for {metric_name}: {e}")

        ax.set_xlabel("Number of Agents", fontsize=12)
        ax.set_ylabel(labels[metric_name], fontsize=12)
        ax.set_title(labels[metric_name], fontsize=14)
        ax.grid(True, alpha=0.3)
        ax.legend()

    # Add the reliability note to the figure
    fig.text(
        0.5,
        0.02,
        note_text,
        ha="center",
        va="bottom",
        fontsize=10,
        style="italic",
        bbox=dict(facecolor="white", alpha=0.7, boxstyle="round,pad=0.5"),
    )

    plt.tight_layout(rect=[0, 0.08, 1, 1])

    # Ensure output directory exists
    output_path.parent.mkdir(parents=True, exist_ok=True)

    # Save as PDF
    plt.savefig(output_path, format="pdf", dpi=300)
    plt.close(fig)

# --- Main Execution ---

def run_scaling_analysis(results_path: Path, output_path: Path) -> None:
    """
    Run the scaling analysis and generate the plot.
    """
    note = "3 data points limit power-law reliability"
    generate_scaling_plot_with_notes(results_path, output_path, note)
    print(f"Scaling plot generated successfully: {output_path}")

def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(description="Generate scaling plot with power-law fits.")
    parser.add_argument(
        "--input",
        type=Path,
        default=Path("projects/PROJ-586-social-memory-networks-modeling-collecti/results/scaling_results.json"),
        help="Path to the input JSON file with scaling results.",
    )
    parser.add_argument(
        "--output",
        type=Path,
        default=Path("projects/PROJ-586-social-memory-networks-modeling-collecti/results/scaling_plot.pdf"),
        help="Path for the output PDF plot.",
    )
    return parser

def main() -> None:
    parser = build_parser()
    args = parser.parse_args(args)

    agent_counts = [int(x) for x in args.agent_counts.split(',')]

    try:
        run_scaling_analysis(args.input, args.output)
    except FileNotFoundError as e:
        print(f"Error: {e}", file=sys.stderr)
        sys.exit(1)
    except ValueError as e:
        print(f"Error processing data: {e}", file=sys.stderr)
        sys.exit(1)
    except Exception as e:
        print(f"Unexpected error: {e}", file=sys.stderr)
        sys.exit(1)

if __name__ == "__main__":
    main()