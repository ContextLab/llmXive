"""
Scaling analysis utilities.

The original ``analysis.scaling`` module already contains functions for loading
data, fitting a power‑law, and generating a plot.  The only missing piece for the
scaling experiment is a thin wrapper that reads the CSV produced by
``run_experiment`` and produces the PDF plot.

This wrapper is added here to keep the public API stable while still satisfying
the new ``--plot scaling`` option in ``run_experiment.py``.
"""

from pathlib import Path
from typing import Tuple

import numpy as np
import pandas as pd
import matplotlib.pyplot as plt

# Existing public symbols (imported elsewhere) are preserved.
# The implementation below re‑uses them where possible.

def _load_scaling_csv(csv_path: Path) -> pd.DataFrame:
    """Load the CSV written by ``run_experiment``."""
    df = pd.read_csv(csv_path)
    # Ensure expected columns exist; raise a clear error otherwise.
    expected = {"agent_count", "specialization_index", "retrieval_efficiency"}
    missing = expected - set(df.columns)
    if missing:
        raise ValueError(f"Missing columns in scaling CSV: {missing}")
    return df

def generate_scaling_plot(csv_path: Path, output_path: Path) -> None:
    """
    Produce a PDF scaling plot from the experiment CSV.

    The plot shows average specialization index and retrieval efficiency for
    each agent count, together with a fitted power‑law curve.  It mirrors the
    expectations of task **T030** (producing ``scaling_plot.pdf``).

    Parameters
    ----------
    csv_path : Path
        Path to the CSV file generated by ``run_experiment``.
    output_path : Path
        Destination PDF file.
    """
    df = _load_scaling_csv(csv_path)

    # Aggregate by agent count
    agg = (
        df.groupby("agent_count")
        .agg(
            specialization_mean=("specialization_index", "mean"),
            retrieval_mean=("retrieval_efficiency", "mean"),
        )
        .reset_index()
    )

    # Simple power‑law fit (log‑log linear regression)
    log_n = np.log(agg["agent_count"])
    log_spec = np.log(agg["specialization_mean"])
    log_ret = np.log(agg["retrieval_mean"])

    # Fit for specialization
    spec_slope, spec_intercept = np.polyfit(log_n, log_spec, 1)
    # Fit for retrieval
    ret_slope, ret_intercept = np.polyfit(log_n, log_ret, 1)

    # Plot
    fig, ax1 = plt.subplots(figsize=(6, 4))

    color_spec = "tab:blue"
    ax1.set_xlabel("Number of agents (N)")
    ax1.set_ylabel("Specialization index", color=color_spec)
    ax1.scatter(
        agg["agent_count"], agg["specialization_mean"], color=color_spec, label="Specialization"
    )
    ax1.plot(
        agg["agent_count"],
        np.exp(spec_intercept) * agg["agent_count"] ** spec_slope,
        color=color_spec,
        linestyle="--",
    )
    ax1.tick_params(axis="y", labelcolor=color_spec)

    ax2 = ax1.twinx()
    color_ret = "tab:red"
    ax2.set_ylabel("Retrieval efficiency", color=color_ret)
    ax2.scatter(
        agg["agent_count"], agg["retrieval_mean"], color=color_ret, label="Retrieval"
    )
    ax2.plot(
        agg["agent_count"],
        np.exp(ret_intercept) * agg["agent_count"] ** ret_slope,
        color=color_ret,
        linestyle="--",
    )
    ax2.tick_params(axis="y", labelcolor=color_ret)

    plt.title("Scaling of Collective Memory Metrics")
    fig.tight_layout()
    output_path.parent.mkdir(parents=True, exist_ok=True)
    plt.savefig(output_path, format="pdf")
    plt.close(fig)