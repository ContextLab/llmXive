import os
from typing import List, Dict, Any, Optional, Tuple, Union
from pathlib import Path
import numpy as np
import pandas as pd
from .synthetic import generate_all_datasets

class DatasetLoader:
    """
    Dataset loader for social memory experiments.
    
    Supports loading synthetic datasets generated by the project.
    """
    
    def __init__(self, data_dir: str = 'data/synthetic'):
        self.data_dir = Path(data_dir)
        self._datasets: Dict[str, pd.DataFrame] = {}
    
    def load_dataset(self, name: str) -> pd.DataFrame:
        """Load a dataset by name."""
        if name in self._datasets:
            return self._datasets[name]
        
        filepath = self.data_dir / f"{name}.csv"
        if not filepath.exists():
            raise FileNotFoundError(f"Dataset not found: {filepath}")
        
        df = pd.read_csv(filepath)
        self._datasets[name] = df
        return df
    
    def list_datasets(self) -> List[str]:
        """List available datasets."""
        if not self.data_dir.exists():
            return []
        
        return [f.stem for f in self.data_dir.glob('*.csv')]

def get_dataset(name: str, data_dir: str = 'data/synthetic') -> pd.DataFrame:
    """Get a dataset by name."""
    loader = DatasetLoader(data_dir)
    return loader.load_dataset(name)

def load_experiment_results(filepath: str) -> pd.DataFrame:
    """Load experiment results from a CSV file."""
    return pd.read_csv(filepath)

def save_experiment_results(df: pd.DataFrame, filepath: str):
    """Save experiment results to a CSV file."""
    Path(filepath).parent.mkdir(parents=True, exist_ok=True)
    df.to_csv(filepath, index=False)

def get_dataset_spec() -> Dict[str, Any]:
    """Get the specification for datasets used in this project."""
    return {
        'synthetic': {
            'description': 'Synthetic game data for social memory experiments',
            'fields': ['game_id', 'agent_count', 'context_condition', 'seed', 'items', 'actions'],
            'generated_by': 'data.synthetic.generate_synthetic_games'
        }
    }

def generate_all_datasets(output_dir: str = 'data/synthetic') -> Dict[str, str]:
    """
    Generate all synthetic datasets needed for experiments.
    
    Returns a dictionary mapping dataset names to file paths.
    """
    from .synthetic import generate_all_datasets as gen_all
    return gen_all(output_dir)
