import os
from typing import List, Dict, Any, Optional, Tuple, Union
from pathlib import Path
import numpy as np
import pandas as pd
from .synthetic import generate_all_datasets, verify_datasets, generate_synthetic_games, SyntheticGameConfig

class DatasetLoader:
    """
    Unified dataset loader for social memory experiments.
    
    Currently supports loading synthetic datasets generated by the project.
    External datasets (Hanabi, CoQA) are not used due to lack of verified URLs.
    """
    
    def __init__(self, data_dir: str = 'data'):
        self.data_dir = Path(data_dir)
        self._cache: Dict[str, pd.DataFrame] = {}
    
    def get_dataset(self, name: str, regenerate: bool = False) -> pd.DataFrame:
        """
        Get a dataset by name.
        
        Args:
            name: Dataset name (currently only 'synthetic' is supported)
            regenerate: If True, regenerate the dataset even if cached
            
        Returns:
            DataFrame containing the dataset
        """
        if name == 'synthetic':
            if name in self._cache and not regenerate:
                return self._cache[name]
            
            # Generate synthetic data
            games = generate_all_datasets(str(self.data_dir / 'generated'))
            df = pd.DataFrame([{
                'game_id': g['game_id'],
                'num_agents': g['config']['num_agents'],
                'context_type': g['config']['context_type'],
                'num_items': g['config']['num_items'],
                'num_turns': g['config']['num_turns'],
                'total_stores': g['final_state']['total_stores'],
                'total_retrievals': g['final_state']['total_retrievals'],
                'unique_items': g['final_state']['unique_items_stored']
            } for g in games])
            
            self._cache[name] = df
            return df
        else:
            raise ValueError(f"Unknown dataset: {name}. Only 'synthetic' is supported.")
    
    def load_experiment_results(self, context: str, agent_count: Optional[int] = None) -> pd.DataFrame:
        """
        Load experiment results from CSV files.
        
        Args:
            context: Context condition ('full' or 'limited')
            agent_count: Optional specific agent count to filter
            
        Returns:
            DataFrame with experiment results
        """
        results_dir = self.data_dir / 'results'
        if not results_dir.exists():
            return pd.DataFrame()
        
        filename = f"results_{context}"
        if agent_count:
            filename += f"_agents{agent_count}"
        filename += ".csv"
        
        file_path = results_dir / filename
        if not file_path.exists():
            return pd.DataFrame()
        
        return pd.read_csv(file_path)
    
    def save_experiment_results(self, df: pd.DataFrame, context: str, agent_count: Optional[int] = None):
        """Save experiment results to CSV."""
        results_dir = self.data_dir / 'results'
        results_dir.mkdir(parents=True, exist_ok=True)
        
        filename = f"results_{context}"
        if agent_count:
            filename += f"_agents{agent_count}"
        filename += ".csv"
        
        df.to_csv(results_dir / filename, index=False)

def get_dataset(name: str, data_dir: str = 'data', regenerate: bool = False) -> pd.DataFrame:
    """Convenience function to get a dataset."""
    loader = DatasetLoader(data_dir)
    return loader.get_dataset(name, regenerate)

def load_experiment_results(context: str, agent_count: Optional[int] = None, data_dir: str = 'data') -> pd.DataFrame:
    """Convenience function to load experiment results."""
    loader = DatasetLoader(data_dir)
    return loader.load_experiment_results(context, agent_count)

def save_experiment_results(df: pd.DataFrame, context: str, agent_count: Optional[int] = None, data_dir: str = 'data'):
    """Convenience function to save experiment results."""
    loader = DatasetLoader(data_dir)
    loader.save_experiment_results(df, context, agent_count)

def get_dataset_spec() -> Dict[str, Any]:
    """Return the specification for available datasets."""
    return {
        'synthetic': {
            'description': 'Synthetic game data for social memory experiments',
            'generated': True,
            'fields': ['game_id', 'num_agents', 'context_type', 'num_items', 'num_turns', 'total_stores', 'total_retrievals', 'unique_items']
        }
    }
