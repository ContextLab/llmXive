"""
Dataset loaders for the social memory network project.
Supports loading synthetic datasets generated by the project.
"""
import os
from typing import List, Dict, Any, Optional, Tuple, Union
from pathlib import Path
import numpy as np
import pandas as pd
from .synthetic import generate_all_datasets, verify_datasets, generate_synthetic_games, SyntheticGameConfig

class DatasetLoader:
    """Base class for dataset loaders."""
    
    def __init__(self, dataset_path: str):
        self.dataset_path = Path(dataset_path)
        self._data: Optional[Dict[str, Any]] = None
    
    def load(self) -> Dict[str, Any]:
        """Load the dataset."""
        if self._data is None:
            raise ValueError("Dataset not loaded. Call load() first.")
        return self._data

def get_dataset(name: str = 'synthetic') -> Any:
    """
    Get a dataset by name.
    
    Args:
        name: Dataset name ('synthetic' is the only supported option)
    
    Returns:
        Dataset object with get_game() method
    """
    if name != 'synthetic':
        raise ValueError(f"Unsupported dataset: {name}. Only 'synthetic' is supported.")
    
    # Generate synthetic dataset if needed
    generate_all_datasets()
    
    # Load synthetic dataset
    dataset_path = Path("data/synthetic_games.csv")
    if not dataset_path.exists():
        raise FileNotFoundError(f"Synthetic dataset not found at {dataset_path}")
    
    return SyntheticDataset(dataset_path)

class SyntheticDataset:
    """Synthetic dataset for social memory experiments."""
    
    def __init__(self, path: Path):
        self.path = path
        self._data: Optional[pd.DataFrame] = None
    
    def _load(self) -> pd.DataFrame:
        """Load dataset from CSV."""
        if self._data is None:
            self._data = pd.read_csv(self.path)
        return self._data
    
    def get_game(self, game_id: int) -> Optional[Dict[str, Any]]:
        """Get game data by ID."""
        df = self._load()
        row = df[df['game_id'] == game_id]
        if row.empty:
            return None
        return row.iloc[0].to_dict()
    
    def get_games(self, count: int) -> List[Dict[str, Any]]:
        """Get multiple games."""
        df = self._load()
        games = []
        for i in range(count):
            if i < len(df):
                games.append(df.iloc[i].to_dict())
        return games

def load_experiment_results(path: str) -> pd.DataFrame:
    """Load experiment results from CSV."""
    return pd.read_csv(path)

def save_experiment_results(df: pd.DataFrame, path: str) -> None:
    """Save experiment results to CSV."""
    os.makedirs(os.path.dirname(path), exist_ok=True)
    df.to_csv(path, index=False)

def get_dataset_spec(name: str = 'synthetic') -> Dict[str, Any]:
    """Get dataset specification."""
    return {
        'name': 'synthetic',
        'description': 'Synthetic game data for social memory experiments',
        'format': 'csv',
        'fields': ['game_id', 'state', 'rounds', 'success']
    }
