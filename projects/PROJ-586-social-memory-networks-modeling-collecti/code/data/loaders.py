import os
from typing import List, Dict, Any, Optional, Tuple, Union
from pathlib import Path
import numpy as np
import pandas as pd

from .synthetic import generate_all_datasets, verify_datasets, generate_synthetic_games, SyntheticGameConfig


class DatasetLoader:
    """
    Loader for experiment datasets.

    Currently supports loading synthetic datasets generated by the project
    (as per spec constraints: no external URLs available).
    """

    def __init__(self, data_dir: Optional[Union[str, Path]] = None):
        """
        Initialize loader.

        Args:
            data_dir: Directory containing datasets. Defaults to 'data/'.
        """
        self.data_dir = Path(data_dir) if data_dir else Path("data")
        self.data_dir.mkdir(parents=True, exist_ok=True)

    def load_games(self, agent_count: int, game_count: int, context_type: str = "full") -> List[Dict[str, Any]]:
        """
        Load game data for a specific configuration.

        Args:
            agent_count: Number of agents in the simulation
            game_count: Number of games to generate/load
            context_type: 'full' or 'limited'

        Returns:
            List of game dictionaries
        """
        # Generate synthetic data as per spec constraints
        # This is the REAL data source for this project
        games = generate_synthetic_games(
            num_games=game_count,
            num_agents=agent_count,
            context_type=context_type,
            seed=42
        )
        return games

    def get_dataset(self, name: str) -> Optional[Dict[str, Any]]:
        """
        Get a specific dataset by name.

        Args:
            name: Dataset name

        Returns:
            Dataset dictionary or None if not found
        """
        if not verify_datasets():
            generate_all_datasets()

        dataset_path = self.data_dir / f"{name}.csv"
        if dataset_path.exists():
            return pd.read_csv(dataset_path).to_dict('records')
        return None


def get_dataset(name: str, data_dir: Optional[Union[str, Path]] = None) -> Optional[Dict[str, Any]]:
    """
    Convenience function to get a dataset.

    Args:
        name: Dataset name
        data_dir: Optional data directory

    Returns:
        Dataset dictionary or None
    """
    loader = DatasetLoader(data_dir)
    return loader.get_dataset(name)


def load_experiment_results(filepath: Union[str, Path]) -> pd.DataFrame:
    """
    Load experiment results from CSV.

    Args:
        filepath: Path to CSV file

    Returns:
        DataFrame of results
    """
    return pd.read_csv(filepath)


def save_experiment_results(df: pd.DataFrame, filepath: Union[str, Path]) -> None:
    """
    Save experiment results to CSV.

    Args:
        df: DataFrame to save
        filepath: Output path
    """
    df.to_csv(filepath, index=False)


