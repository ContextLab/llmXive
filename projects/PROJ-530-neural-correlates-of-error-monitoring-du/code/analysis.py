def main():
    """
    Main entry point for the analysis module.
    Orchestrates data loading, model fitting, and sensitivity analysis.
    """
    logger = get_logger(__name__)
    start_time = time.time()
    process = psutil.Process(os.getpid())
    initial_memory = process.memory_info().rss
    
    try:
        # 1. Load Configuration
        config = load_config()
        set_global_seed(config.get('random_seed', 42))
        
        # 2. Load Data
        df = load_processed_data()
        
        # 3. Run Sensitivity Sweep (Task T023)
        # Output path from config or default
        output_path = config.get('paths', {}).get('sensitivity_results', 'results/diagnostics/sensitivity_summary.csv')
        
        results = run_sensitivity_sweep(df, output_path=output_path)
        
        # 4. Feasibility Check (SC-005)
        end_time = time.time()
        runtime_seconds = end_time - start_time
        current_memory = process.memory_info().rss
        peak_memory_mb = (current_memory - initial_memory) / (1024 * 1024)
        
        if runtime_seconds > 21600 or peak_memory_mb > 7168:
            raise FeasibilityError(f"Resource limits exceeded: Runtime={runtime_seconds}s, Memory={peak_memory_mb}MB")
        
        # 5. Save Feasibility Report (T018 requirement, executed here if not done elsewhere)
        feasibility_report = {
            "runtime_seconds": runtime_seconds,
            "peak_memory_mb": peak_memory_mb,
            "status": "completed"
        }
        feasibility_path = Path("results/diagnostics/feasibility_report.json")
        feasibility_path.parent.mkdir(parents=True, exist_ok=True)
        with open(feasibility_path, 'w') as f:
            json.dump(feasibility_report, f, indent=2)
        
        logger.info(f"Analysis complete. Feasibility report saved to {feasibility_path}")
        
    except FeasibilityError as e:
        logger.critical(f"Feasibility check failed: {str(e)}")
        sys.exit(1)
    except Exception as e:
        logger.critical(f"Analysis failed: {str(e)}")
        sys.exit(1)

if __name__ == "__main__":
    main()
