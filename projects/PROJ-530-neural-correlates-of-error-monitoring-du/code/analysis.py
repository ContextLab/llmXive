def fit_linear_mixed_effects_model(df, formula="mean_amplitude ~ error_magnitude + (1|participant_id)"):
    """
    Fit a Linear Mixed-Effects Model using statsmodels.
    
    Args:
        df: DataFrame containing the data.
        formula: Statsmodels formula string.
        
    Returns:
        model_results: Fitted model results object.
    """
    logger = get_logger(__name__)
    logger.info(f"Fitting LME model with formula: {formula}")
    
    try:
        # Handle potential NaNs in the specific columns used
        model_df = df.dropna(subset=['mean_amplitude', 'error_magnitude', 'participant_id'])
        
        if len(model_df) == 0:
            raise ValueError("No valid data points remaining after dropping NaNs for LME fitting.")
        
        model = mixedlm.from_formula(formula, groups="participant_id", data=model_df)
        result = model.fit()
        
        logger.info("Model fitting successful.")
        return result
    except Exception as e:
        logger.error(f"Model fitting failed: {str(e)}")
        raise

