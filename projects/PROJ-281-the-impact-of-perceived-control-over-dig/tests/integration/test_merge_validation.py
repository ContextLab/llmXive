def test_merge_and_validate_creates_output(setup_env):
    """
    Test that stage_05_merge_and_validate creates final_analysis.csv
    and correctly merges data.
    """
    # We need to ensure the files are in the expected location for the function to find them.
    # We'll copy our temp files to the actual DATA_PROCESSED_DIR for the test duration.
    # Note: This assumes the test runner has write access to DATA_PROCESSED_DIR.
    # In a CI environment, this is usually a temp directory.
    
    scoring_src = setup_env['scoring_path']
    proxy_src = setup_env['proxy_path']
    output_dest = setup_env['output_path']
    
    # Ensure directory exists
    output_dest.parent.mkdir(parents=True, exist_ok=True)
    
    # Copy files to expected location
    shutil.copy(scoring_src, DATA_PROCESSED_DIR / "scoring_results.csv")
    shutil.copy(proxy_src, DATA_PROCESSED_DIR / "proxy_results.csv")
    
    # Remove output if exists to test creation
    if output_dest.exists():
        output_dest.unlink()
    
    # Run the stage
    # We expect it to succeed
    try:
        stage_05_merge_and_validate()
    except Exception as e:
        pytest.fail(f"stage_05_merge_and_validate raised {e}")
    
    # Verify output exists
    assert output_dest.exists(), "final_analysis.csv was not created"
    
    # Verify content
    df_result = pd.read_csv(output_dest)
    
    # Check columns
    expected_cols = ['post_id', 'text', 'anxiety_score', 'confidence_score', 'user_id', 'control_proxy', 'timestamp_regularity']
    for col in expected_cols:
        assert col in df_result.columns, f"Missing column: {col}"
    
    # Check row count (inner join: p1, p2, p3 should be present; p4 missing in proxy, p5 missing in score)
    # Expected: 3 rows
    assert len(df_result) == 3, f"Expected 3 rows, got {len(df_result)}"
    
    # Verify confidence filtering confirmation (min confidence >= 0.6)
    assert df_result['confidence_score'].min() >= 0.6, "Confidence filter appears not applied or data corrupted"
    
    # Clean up
    if output_dest.exists():
        output_dest.unlink()
    if (DATA_PROCESSED_DIR / "scoring_results.csv").exists():
        (DATA_PROCESSED_DIR / "scoring_results.csv").unlink()
    if (DATA_PROCESSED_DIR / "proxy_results.csv").exists():
        (DATA_PROCESSED_DIR / "proxy_results.csv").unlink()

