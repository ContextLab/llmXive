import os
import sys
import logging
import json
import argparse
from pathlib import Path
from typing import Dict, Any, Optional, List

import numpy as np
import pandas as pd
import matplotlib
matplotlib.use('Agg')  # Non-interactive backend for headless execution
import matplotlib.pyplot as plt
import seaborn as sns

# Import project utilities
from utils.config import get_paths, ensure_directories, set_random_seed
from utils.logging_config import get_logger

logger = get_logger(__name__)

def load_processed_data(data_path: str) -> pd.DataFrame:
    """
    Load the processed CSV data containing descriptors and shear modulus.
    """
    if not os.path.exists(data_path):
        raise FileNotFoundError(f"Processed data file not found: {data_path}")
    
    logger.info(f"Loading processed data from {data_path}")
    df = pd.read_csv(data_path)
    
    # Verify required columns exist
    required_cols = ['delta', 'dH_mix', 'VEC', 'dchi', 'shear_modulus_GPa']
    missing = [c for c in required_cols if c not in df.columns]
    if missing:
        raise ValueError(f"Missing required columns in data: {missing}")
    
    logger.info(f"Loaded {len(df)} rows. Columns: {list(df.columns)}")
    return df

def calculate_correlation_matrix(df: pd.DataFrame, feature_cols: List[str], target_col: str) -> pd.DataFrame:
    """
    Calculate the correlation matrix for descriptors and the target variable.
    """
    cols_to_corr = feature_cols + [target_col]
    corr_df = df[cols_to_corr].corr()
    return corr_df

def generate_correlation_heatmap(
    corr_df: pd.DataFrame,
    output_path: str,
    title: str = "Correlation: Descriptors vs Shear Modulus",
    figsize: tuple = (10, 8),
    cmap: str = "coolwarm",
    annot: bool = True,
    fmt: str = ".2f"
) -> None:
    """
    Generate and save a correlation heatmap of descriptors vs shear modulus.
    """
    plt.figure(figsize=figsize)
    
    # Create mask for the upper triangle to make it cleaner (optional, but standard)
    # However, for a small set of features vs target, showing the full matrix is often clearer.
    # We will show the full matrix for clarity of the target relationship.
    
    sns.heatmap(
        corr_df,
        annot=annot,
        fmt=fmt,
        cmap=cmap,
        center=0,
        square=True,
        linewidths=0.5,
        cbar_kws={"shrink": 0.8},
        vmin=-1,
        vmax=1
    )
    
    plt.title(title, fontsize=14, pad=15)
    plt.xticks(rotation=45, ha='right')
    plt.yticks(rotation=0)
    
    # Ensure output directory exists
    os.makedirs(os.path.dirname(output_path), exist_ok=True)
    
    plt.tight_layout()
    plt.savefig(output_path, dpi=300, bbox_inches='tight')
    plt.close()
    
    logger.info(f"Heatmap saved to {output_path}")

def run_correlation_analysis(
    input_path: Optional[str] = None,
    output_dir: Optional[str] = None,
    seed: int = 42
) -> Dict[str, Any]:
    """
    Main entry point to run the correlation analysis and generate the heatmap.
    """
    set_random_seed(seed)
    paths = get_paths()
    
    # Determine input path
    if input_path is None:
        # Default to the processed data file generated by the pipeline
        input_path = str(paths["processed_data"])
    
    # Determine output path
    if output_dir is None:
        output_dir = str(paths["artifacts"])
    
    output_filename = "correlation_heatmap.png"
    output_path = str(Path(output_dir) / output_filename)
    
    logger.info(f"Starting correlation analysis. Input: {input_path}, Output: {output_path}")
    
    # Load data
    df = load_processed_data(input_path)
    
    # Define feature columns (descriptors) and target
    feature_cols = ['delta', 'dH_mix', 'VEC', 'dchi']
    target_col = 'shear_modulus_GPa'
    
    # Calculate correlations
    corr_df = calculate_correlation_matrix(df, feature_cols, target_col)
    
    # Generate heatmap
    generate_correlation_heatmap(corr_df, output_path)
    
    # Save the correlation matrix as JSON for programmatic access
    json_output_path = str(Path(output_dir) / "correlation_matrix.json")
    corr_dict = corr_df.round(4).to_dict()
    with open(json_output_path, 'w') as f:
        json.dump(corr_dict, f, indent=2)
    logger.info(f"Correlation matrix JSON saved to {json_output_path}")
    
    return {
        "heatmap_path": output_path,
        "json_path": json_output_path,
        "correlation_with_target": corr_df[target_col].to_dict()
    }

def main():
    """
    CLI entry point for T038.
    """
    parser = argparse.ArgumentParser(description="Generate correlation heatmap for BMG descriptors.")
    parser.add_argument("--input", type=str, default=None, help="Path to processed CSV data.")
    parser.add_argument("--output-dir", type=str, default=None, help="Directory to save artifacts.")
    parser.add_argument("--seed", type=int, default=42, help="Random seed for reproducibility.")
    
    args = parser.parse_args()
    
    try:
        result = run_correlation_analysis(
            input_path=args.input,
            output_dir=args.output_dir,
            seed=args.seed
        )
        logger.info("Correlation analysis completed successfully.")
        logger.info(f"Results: {result}")
    except Exception as e:
        logger.error(f"Correlation analysis failed: {e}", exc_info=True)
        sys.exit(1)

if __name__ == "__main__":
    main()