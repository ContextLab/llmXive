"""
T021: Validation to ensure no missing values in target variable after cleaning and filtering.

This module validates the processed dataset to ensure the target variable (Shear Modulus)
contains no missing values (NaN, None, or empty strings) before proceeding to model training.
"""
import os
import sys
import logging
import csv
from pathlib import Path
from typing import List, Dict, Any, Optional

# Import logging configuration from existing utility
sys.path.insert(0, str(Path(__file__).parent.parent))
from utils.logging_config import get_logger

# Constants
TARGET_COLUMN = "shear_modulus_gpa"
OUTPUT_FILE_NAME = "processed_data_validation_report.csv"
OUTPUT_PATH = Path("data/processed")

logger = get_logger(__name__)


def load_processed_data(file_path: str) -> List[Dict[str, Any]]:
    """
    Load the processed CSV data generated by previous pipeline steps.

    Args:
        file_path: Path to the processed CSV file.

    Returns:
        List of dictionaries representing rows in the CSV.

    Raises:
        FileNotFoundError: If the file does not exist.
        ValueError: If the file is empty or malformed.
    """
    path = Path(file_path)
    if not path.exists():
        raise FileNotFoundError(f"Processed data file not found: {file_path}")

    data = []
    try:
        with open(path, "r", encoding="utf-8") as f:
            reader = csv.DictReader(f)
            for row in reader:
                data.append(row)
    except csv.Error as e:
        logger.error(f"Error reading CSV {file_path}: {e}")
        raise

    if not data:
        raise ValueError(f"Processed data file {file_path} is empty.")

    return data


def validate_target_no_missing(data: List[Dict[str, Any]], target_col: str = TARGET_COLUMN) -> Dict[str, Any]:
    """
    Validate that the target variable contains no missing values.

    Args:
        data: List of dictionaries containing the dataset.
        target_col: The name of the target column to validate.

    Returns:
        A dictionary containing validation results:
        - 'valid': bool, True if no missing values found.
        - 'total_rows': int, total number of rows checked.
        - 'missing_count': int, number of rows with missing target values.
        - 'missing_indices': List[int], indices of rows with missing values.
        - 'missing_values': List[Any], the actual missing values found.
    """
    missing_indices = []
    missing_values = []

    for idx, row in enumerate(data):
        value = row.get(target_col)

        # Check for various forms of missing data
        if value is None:
            missing_indices.append(idx)
            missing_values.append(None)
        elif isinstance(value, str):
            if value.strip() == "" or value.lower() in ("nan", "na", "n/a", "none", "null"):
                missing_indices.append(idx)
                missing_values.append(value)
        else:
            # Check for float('nan') if parsed as float
            try:
                float_val = float(value)
                if math.isnan(float_val):
                    missing_indices.append(idx)
                    missing_values.append(float_val)
            except (ValueError, TypeError):
                # If it can't be converted to float and isn't a known missing string,
                # it might be a valid string representation or an error.
                # For this task, we strictly check for explicit emptiness.
                pass

    total_rows = len(data)
    missing_count = len(missing_indices)
    is_valid = missing_count == 0

    return {
        "valid": is_valid,
        "total_rows": total_rows,
        "missing_count": missing_count,
        "missing_indices": missing_indices,
        "missing_values": missing_values
    }


def save_validation_report(report: Dict[str, Any], output_dir: Path, filename: str = OUTPUT_FILE_NAME) -> Path:
    """
    Save the validation report to a CSV file.

    Args:
        report: The validation results dictionary.
        output_dir: Directory to save the report.
        filename: Name of the output file.

    Returns:
        Path to the saved file.
    """
    output_dir.mkdir(parents=True, exist_ok=True)
    output_path = output_dir / filename

    with open(output_path, "w", newline="", encoding="utf-8") as f:
        writer = csv.writer(f)
        writer.writerow(["Metric", "Value"])
        writer.writerow(["Valid", report["valid"]])
        writer.writerow(["Total Rows", report["total_rows"]])
        writer.writerow(["Missing Count", report["missing_count"]])
        writer.writerow(["Missing Indices", report["missing_indices"]])
        writer.writerow(["Missing Values", report["missing_values"]])

    logger.info(f"Validation report saved to {output_path}")
    return output_path


def main():
    """
    Main entry point for T021 validation task.

    Expects the processed data file to be at data/processed/processed_bmg_data.csv
    (or the path defined in the pipeline configuration).
    """
    # Determine the input file path.
    # Assuming the previous step (T020 split or T019 features) outputs to data/processed/
    # We look for the most recent processed file or a standard name.
    # For robustness, we check for a standard output name from the feature engineering step.
    input_file = Path("data/processed/processed_bmg_data.csv")

    if not input_file.exists():
        # Fallback: check if there are any CSVs in the processed directory
        processed_dir = Path("data/processed")
        if processed_dir.exists():
            csv_files = list(processed_dir.glob("*.csv"))
            if csv_files:
                input_file = csv_files[-1] # Take the last one found
                logger.warning(f"Standard input file not found. Using latest found: {input_file}")
            else:
                logger.error("No CSV files found in data/processed/ directory.")
                sys.exit(1)
        else:
            logger.error("data/processed/ directory does not exist.")
            sys.exit(1)

    logger.info(f"Validating target variable in: {input_file}")

    try:
        data = load_processed_data(str(input_file))
        report = validate_target_no_missing(data)

        save_validation_report(report, Path("data/processed"))

        if report["valid"]:
            logger.info("SUCCESS: No missing values found in target variable.")
            sys.exit(0)
        else:
            logger.warning(f"FAILURE: Found {report['missing_count']} missing values in target variable.")
            logger.warning(f"Indices: {report['missing_indices']}")
            # Exit with error code to halt pipeline if strict validation is required
            sys.exit(1)

    except Exception as e:
        logger.error(f"Validation failed with error: {e}")
        sys.exit(1)


if __name__ == "__main__":
    import math
    main()
