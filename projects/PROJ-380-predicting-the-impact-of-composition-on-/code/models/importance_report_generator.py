"""
Generate the final importance report JSON with ranked descriptors and p-values.

This module implements Task T040: Generate `artifacts/importance_report.json`
with ranked descriptors and p-values based on permutation importance analysis.
"""
import os
import sys
import json
import logging
from pathlib import Path
from typing import Dict, Any, List, Optional

# Add the project root to the path to allow imports from sibling modules
project_root = Path(__file__).resolve().parent.parent.parent
if str(project_root) not in sys.path:
    sys.path.insert(0, str(project_root))

from utils.config import get_paths
from utils.logging_config import get_logger
from utils.provenance import record_artifact, ensure_state_directory, get_provenance_state_file
from models.importance import load_model_and_data, compute_permutation_importance

logger = get_logger(__name__)


def load_importance_results(importance_file_path: str) -> List[Dict[str, Any]]:
    """
    Load the raw importance results from the JSON file generated by T034/T035.
    
    Args:
        importance_file_path: Path to the importance results JSON file.
        
    Returns:
        List of dictionaries containing feature importance data.
    """
    if not os.path.exists(importance_file_path):
        raise FileNotFoundError(f"Importance results file not found: {importance_file_path}")
        
    with open(importance_file_path, 'r') as f:
        data = json.load(f)
        
    return data.get('results', [])


def rank_descriptors(importance_data: List[Dict[str, Any]]) -> List[Dict[str, Any]]:
    """
    Rank descriptors by their permutation importance scores.
    
    Args:
        importance_data: List of importance result dictionaries.
        
    Returns:
        List of ranked descriptors with metadata.
    """
    # Sort by mean importance score in descending order
    ranked = sorted(
        importance_data,
        key=lambda x: x.get('mean_importance', 0),
        reverse=True
    )
    
    # Add rank index
    for i, item in enumerate(ranked):
        item['rank'] = i + 1
        
    return ranked


def extract_p_values(importance_data: List[Dict[str, Any]]) -> Dict[str, float]:
    """
    Extract p-values from the importance analysis results.
    
    Args:
        importance_data: List of importance result dictionaries.
        
    Returns:
        Dictionary mapping feature names to their p-values.
    """
    p_values = {}
    for item in importance_data:
        feature_name = item.get('feature', 'unknown')
        p_val = item.get('p_value', 1.0)
        p_values[feature_name] = p_val
    return p_values


def generate_importance_report(
    importance_file: str,
    model_file: Optional[str] = None,
    output_path: Optional[str] = None
) -> str:
    """
    Generate the final importance report JSON with ranked descriptors and p-values.
    
    This function:
    1. Loads the raw permutation importance results
    2. Ranks descriptors by importance
    3. Extracts p-values
    4. Generates a comprehensive report JSON
    5. Saves the report to the specified output path
    
    Args:
        importance_file: Path to the permutation importance results JSON.
        model_file: Optional path to the trained model (for metadata).
        output_path: Optional output path for the report. Defaults to 
                    `artifacts/importance_report.json`.
                    
    Returns:
        Path to the generated report file.
    """
    paths = get_paths()
    artifacts_dir = paths.get('artifacts', 'data/artifacts')
    
    if output_path is None:
        output_path = os.path.join(artifacts_dir, 'importance_report.json')
        
    # Ensure output directory exists
    os.makedirs(os.path.dirname(output_path), exist_ok=True)
    
    # Load raw importance data
    logger.info(f"Loading importance results from {importance_file}")
    importance_data = load_importance_results(importance_file)
    
    if not importance_data:
        raise ValueError("Importance data is empty. Cannot generate report.")
    
    # Rank descriptors
    logger.info("Ranking descriptors by importance")
    ranked_descriptors = rank_descriptors(importance_data)
    
    # Extract p-values
    p_values = extract_p_values(importance_data)
    
    # Build the report structure
    report = {
        'metadata': {
            'generated_at': 'N/A',  # Will be updated if we have timestamp
            'source_file': importance_file,
            'model_file': model_file,
            'total_features': len(ranked_descriptors),
            'method': 'permutation_importance',
            'n_permutations': importance_data[0].get('n_permutations', 100) if importance_data else 100
        },
        'ranking': [
            {
                'rank': item['rank'],
                'feature': item['feature'],
                'mean_importance': item.get('mean_importance', 0),
                'std_importance': item.get('std_importance', 0),
                'p_value': item.get('p_value', 1.0),
                'significant': item.get('p_value', 1.0) < 0.05
            }
            for item in ranked_descriptors
        ],
        'p_values': p_values,
        'summary': {
            'top_feature': ranked_descriptors[0]['feature'] if ranked_descriptors else None,
            'top_importance': ranked_descriptors[0].get('mean_importance', 0) if ranked_descriptors else 0,
            'significant_features_count': sum(1 for item in ranked_descriptors if item.get('p_value', 1.0) < 0.05),
            'total_features': len(ranked_descriptors)
        }
    }
    
    # Save the report
    logger.info(f"Saving importance report to {output_path}")
    with open(output_path, 'w') as f:
        json.dump(report, f, indent=2)
        
    # Record provenance
    ensure_state_directory()
    record_artifact(
        artifact_path=output_path,
        artifact_type='importance_report',
        description='Ranked feature importance report with p-values from permutation analysis',
        metadata={
            'source': importance_file,
            'method': 'permutation_importance',
            'task_id': 'T040'
        }
    )
    
    logger.info(f"Importance report successfully generated: {output_path}")
    return output_path


def main():
    """Main entry point for the importance report generation."""
    import argparse
    
    parser = argparse.ArgumentParser(
        description='Generate importance report with ranked descriptors and p-values'
    )
    parser.add_argument(
        '--importance-file',
        type=str,
        default=None,
        help='Path to the permutation importance results JSON file'
    )
    parser.add_argument(
        '--model-file',
        type=str,
        default=None,
        help='Path to the trained model file (optional)'
    )
    parser.add_argument(
        '--output',
        type=str,
        default=None,
        help='Output path for the importance report JSON'
    )
    
    args = parser.parse_args()
    
    paths = get_paths()
    artifacts_dir = paths.get('artifacts', 'data/artifacts')
    
    # Default to the expected importance results file if not provided
    if args.importance_file is None:
        # Try to find the importance results file
        importance_candidates = [
            os.path.join(artifacts_dir, 'permutation_importance_results.json'),
            os.path.join(artifacts_dir, 'importance_results.json'),
            os.path.join(artifacts_dir, 'feature_importance.json')
        ]
        
        for candidate in importance_candidates:
            if os.path.exists(candidate):
                args.importance_file = candidate
                break
        
        if args.importance_file is None:
            # Use a reasonable default path that matches the pipeline flow
            args.importance_file = os.path.join(artifacts_dir, 'permutation_importance_results.json')
            logger.warning(f"Using default importance file path: {args.importance_file}")
            
    if args.model_file is None:
        model_candidates = [
            os.path.join(artifacts_dir, 'best_model.pkl'),
            os.path.join(artifacts_dir, 'model.pkl')
        ]
        for candidate in model_candidates:
            if os.path.exists(candidate):
                args.model_file = candidate
                break
    
    try:
        output_path = generate_importance_report(
            importance_file=args.importance_file,
            model_file=args.model_file,
            output_path=args.output
        )
        print(f"Successfully generated importance report: {output_path}")
        return 0
    except Exception as e:
        logger.error(f"Failed to generate importance report: {e}")
        raise


if __name__ == '__main__':
    sys.exit(main())
