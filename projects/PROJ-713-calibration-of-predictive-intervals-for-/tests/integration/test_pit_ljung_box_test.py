class TestPITLjungBox(unittest.TestCase):
    """Integration test suite for PIT uniformity and Ljung-Box test."""

    @classmethod
    def setUpClass(cls):
        """Set up test fixtures. Load a small subset of real M4 data."""
        logger.info("Setting up TestPITLjungBox: Loading real M4 hourly data...")
        
        try:
            # Load M4 hourly data. We expect this to download or load from cache.
            # If the full dataset is too large, we might limit to a few series,
            # but the loader should handle the full dataset logic.
            # For the sake of a quick integration test, we'll load the first few series.
            # Note: load_m4_hourly returns a dict of series.
            data_dict = load_m4_hourly()
            
            if not data_dict:
                raise DataValidationError("M4 hourly data is empty or unavailable.")
            
            # Take the first 2 series for this integration test to keep it fast
            series_keys = list(data_dict.keys())[:2]
            cls.test_series = {k: data_dict[k] for k in series_keys}
            
            logger.info(f"Loaded {len(cls.test_series)} series for testing.")
            
        except Exception as e:
            logger.error(f"Failed to load M4 data: {e}")
            # We cannot proceed without real data
            raise e

    def test_pit_values_computation(self):
        """Test that PIT values are computed correctly for a fitted ARIMA model."""
        series_id = list(self.test_series.keys())[0]
        series_data = self.test_series[series_id]

        # Split data: 80% train, 20% test
        train, test = split_series(series_data, train_ratio=0.8)
        
        # Standardize
        train_std, test_std, mean, std = standardize(train, test)

        # Fit ARIMA model
        logger.info(f"Fitting ARIMA model for series {series_id}...")
        model = ARIMAModel()
        
        try:
            model.fit(train_std)
        except ModelConvergenceError:
            self.skipTest(f"ARIMA model failed to converge for series {series_id}. Skipping test.")
        except Exception as e:
            self.fail(f"ARIMA model fitting raised unexpected error: {e}")

        # Generate forecasts and intervals
        # We need forecasts for the test set
        n_test = len(test_std)
        forecasts, lower, upper = model.predict(n_test, confidence_level=0.95)

        # Compute PIT values
        # PIT = CDF(Forecast_Distribution, Actual_Value)
        # For Gaussian forecast distribution: CDF = norm.cdf((y - mean) / sigma)
        # Here, forecasts is the mean, and we derive sigma from (upper - lower)
        # Assuming 95% interval: upper = mean + 1.96 * sigma => sigma = (upper - mean) / 1.96
        
        pit_values = compute_pit_values(test_std, forecasts, lower, upper, confidence_level=0.95)

        self.assertIsNotNone(pit_values)
        self.assertEqual(len(pit_values), n_test)
        self.assertTrue(np.all(np.isfinite(pit_values)))
        self.assertTrue(np.all((pit_values >= 0) & (pit_values <= 1)))

        logger.info(f"PIT values computed successfully for {series_id}. Range: [{pit_values.min():.4f}, {pit_values.max():.4f}]")

    def test_ljung_box_test_uniformity(self):
        """Test the Ljung-Box test for PIT uniformity."""
        series_id = list(self.test_series.keys())[0]
        series_data = self.test_series[series_id]

        train, test = split_series(series_data, train_ratio=0.8)
        train_std, test_std, _, _ = standardize(train, test)

        model = ARIMAModel()
        try:
            model.fit(train_std)
        except ModelConvergenceError:
            self.skipTest(f"ARIMA model failed to converge for series {series_id}.")
        
        n_test = len(test_std)
        forecasts, lower, upper = model.predict(n_test, confidence_level=0.95)
        pit_values = compute_pit_values(test_std, forecasts, lower, upper, confidence_level=0.95)

        # Perform Ljung-Box test
        # Null hypothesis: PIT values are independent and identically distributed (uniform)
        # We test for autocorrelation in the PIT values. If p-value > alpha, we fail to reject H0.
        lb_stat, p_value = perform_ljung_box_test(pit_values)

        self.assertIsNotNone(lb_stat)
        self.assertIsNotNone(p_value)
        self.assertTrue(np.isfinite(lb_stat))
        self.assertTrue(np.isfinite(p_value))
        self.assertGreaterEqual(lb_stat, 0)
        self.assertGreaterEqual(p_value, 0)
        self.assertLessEqual(p_value, 1)

        logger.info(f"Ljung-Box test for {series_id}: Statistic={lb_stat:.4f}, p-value={p_value:.4f}")
        
        # Note: We do NOT assert p_value > 0.05 here because real forecasts might be miscalibrated.
        # The test is to verify the *mechanism* works correctly, not that the model is perfect.
        # The mechanism must run without error and return valid statistics.

