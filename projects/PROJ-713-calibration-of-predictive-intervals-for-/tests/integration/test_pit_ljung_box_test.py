    def test_integration_with_multiple_series(self):
        """Test the full pipeline on multiple series."""
        results = []
        
        for series_id, series_data in self.test_series.items():
            train, test = split_series(series_data, train_ratio=0.8)
            train_std, test_std, _, _ = standardize(train, test)

            model = ARIMAModel()
            try:
                model.fit(train_std)
            except ModelConvergenceError:
                logger.warning(f"Skipping {series_id} due to convergence failure.")
                continue
            
            n_test = len(test_std)
            forecasts, lower, upper = model.predict(n_test, confidence_level=0.95)
            pit_values = compute_pit_values(test_std, forecasts, lower, upper, confidence_level=0.95)
            
            lb_stat, p_value = perform_ljung_box_test(pit_values)
            
            results.append({
                "series_id": series_id,
                "lb_stat": lb_stat,
                "p_value": p_value,
                "n_test": n_test
            })

        self.assertGreater(len(results), 0, "No series were processed successfully.")
        
        for res in results:
            self.assertTrue(np.isfinite(res["lb_stat"]))
            self.assertTrue(np.isfinite(res["p_value"]))
            self.assertGreaterEqual(res["n_test"], 1)
        
        logger.info(f"Successfully processed {len(results)} series in integration test.")


