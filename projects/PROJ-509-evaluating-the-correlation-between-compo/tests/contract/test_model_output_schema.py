"""
Contract test for model output schema (T018).

Validates that the model output artifacts generated by the training pipeline
conform to the schema defined in `contracts/model_output.schema.yaml`.

This test ensures:
1. The `data/evaluation/model_metrics.json` file exists.
2. The JSON structure matches the expected schema keys.
3. Required fields (model_name, r2, mae, rmse, overfitting_ratio) are present.
4. Numeric fields are of the correct type.
"""

import json
import os
import pytest
from pathlib import Path
import yaml

# Import project configuration
# Note: We assume the test runner adds the project root to sys.path
# or we run from the project root.
import sys
from pathlib import Path

# Ensure we can import config if needed, though for this contract test
# we rely on the schema file directly.
# The schema is expected to be at: contracts/model_output.schema.yaml
# The data is expected to be at: data/evaluation/model_metrics.json

PROJECT_ROOT = Path(__file__).parent.parent.parent
SCHEMA_PATH = PROJECT_ROOT / "contracts" / "model_output.schema.yaml"
METRICS_PATH = PROJECT_ROOT / "data" / "evaluation" / "model_metrics.json"


def load_schema():
    """Load the model output schema from YAML."""
    if not SCHEMA_PATH.exists():
        pytest.fail(f"Schema file not found at {SCHEMA_PATH}. "
                    "Ensure T005 (contracts setup) and the specific schema file creation are complete.")
    with open(SCHEMA_PATH, "r") as f:
        return yaml.safe_load(f)


def load_metrics():
    """Load the generated model metrics JSON."""
    if not METRICS_PATH.exists():
        pytest.fail(f"Model metrics file not found at {METRICS_PATH}. "
                    "Ensure the training pipeline (T020-T026) has been executed.")
    with open(METRICS_PATH, "r") as f:
        return json.load(f)


class TestModelOutputSchema:
    """Contract tests for the model output schema."""

    def test_schema_file_exists(self):
        """Verify the schema definition file exists."""
        assert SCHEMA_PATH.exists(), "contracts/model_output.schema.yaml is missing."

    def test_metrics_file_exists(self):
        """Verify the metrics output file exists."""
        assert METRICS_PATH.exists(), "data/evaluation/model_metrics.json is missing."

    def test_schema_structure(self):
        """Verify the schema has the expected structure (list of objects with specific properties)."""
        schema = load_schema()
        assert isinstance(schema, dict), "Schema must be a dictionary."
        assert "properties" in schema, "Schema must have 'properties' key."
        assert "type" in schema, "Schema must have 'type' key."
        assert schema["type"] == "object", "Root schema type must be 'object'."

    def test_metrics_against_schema(self):
        """
        Validate the content of model_metrics.json against the schema.
        
        This is the core contract test. It checks that the generated data
        conforms to the definition.
        """
        schema = load_schema()
        metrics = load_metrics()

        # Basic type check
        assert isinstance(metrics, dict), "Metrics must be a dictionary."

        # Check required top-level keys based on typical schema requirements
        # The schema usually defines the structure of the object.
        # We assume the schema defines keys like 'random_forest' and 'gradient_boosting'
        # or a list of results. Let's check for common keys expected by US2.
        
        # Based on T025/T026, we expect entries for models and potentially global metrics.
        # Let's verify the schema allows the keys present in the file.
        schema_props = schema.get("properties", {})
        
        # If the schema defines specific keys, ensure they exist in metrics
        # If the schema is a generic object, we check for the presence of expected keys
        # derived from the task description (T023, T025).
        
        expected_keys = ["random_forest", "gradient_boosting", "overfitting_ratio"]
        
        # We need to check if the schema expects these keys.
        # Since we can't dynamically validate against a complex schema without a library like jsonschema,
        # we perform a structural check based on the schema definition.
        
        # 1. Check if the schema expects 'properties' that match our data keys
        # If the schema is strict, we might need to check specific fields.
        # For this contract test, we verify that the keys in the file
        # are defined in the schema's properties, or if the schema is permissive.
        
        # Let's assume the schema defines the structure of the models.
        # We will check that for every model key in metrics, it is defined in schema.
        # And that the inner structure (r2, mae, rmse) is present.
        
        for key in metrics:
            if key in expected_keys or key in ["random_forest", "gradient_boosting"]:
                # If it's a model entry, check inner fields
                model_data = metrics[key]
                if isinstance(model_data, dict):
                    assert "r2" in model_data, f"Missing 'r2' in {key}"
                    assert "mae" in model_data, f"Missing 'mae' in {key}"
                    assert "rmse" in model_data, f"Missing 'rmse' in {key}"
                    # Check types
                    assert isinstance(model_data["r2"], (int, float)), f"'r2' must be numeric in {key}"
                    assert isinstance(model_data["mae"], (int, float)), f"'mae' must be numeric in {key}"
                    assert isinstance(model_data["rmse"], (int, float)), f"'rmse' must be numeric in {key}"
            
            if key == "overfitting_ratio":
                assert isinstance(metrics[key], (int, float)), "'overfitting_ratio' must be numeric"

    def test_model_metrics_validity(self):
        """
        Validate the logical validity of the metrics (e.g., R2 range, non-negative errors).
        """
        metrics = load_metrics()
        
        for model_name in ["random_forest", "gradient_boosting"]:
            if model_name in metrics:
                data = metrics[model_name]
                r2 = data.get("r2", 0)
                mae = data.get("mae", 0)
                rmse = data.get("rmse", 0)
                
                # R2 can be negative, but typically we check for reasonable bounds if known.
                # MAE and RMSE must be non-negative.
                assert mae >= 0, f"MAE for {model_name} must be non-negative"
                assert rmse >= 0, f"RMSE for {model_name} must be non-negative"
                assert rmse >= mae, f"RMSE should generally be >= MAE for {model_name}"

    def test_overfitting_ratio_logic(self):
        """
        Validate the overfitting ratio calculation if present.
        """
        metrics = load_metrics()
        if "overfitting_ratio" in metrics:
            ratio = metrics["overfitting_ratio"]
            # Just check it's a number. Logic validation is in code, not schema contract.
            assert isinstance(ratio, (int, float)), "Overfitting ratio must be numeric"
            # Avoid division by zero artifacts if train_r2 was 0
            # (Handled in code, but good to check for sanity)
            # assert ratio >= 0, "Overfitting ratio should be non-negative if train_r2 > 0"