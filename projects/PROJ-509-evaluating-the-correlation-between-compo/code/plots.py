logger = get_logger(__name__)

def load_feature_names() -> List[str]:
    """
    Load feature names from the computed descriptors CSV.
    Assumes the CSV has columns: 'composition', 'formation_energy', and descriptor columns.
    We need to identify the descriptor columns.
    """
    paths = load_paths()
    descriptors_path = paths['data_processed'] / 'computed_descriptors.csv'
    
    if not descriptors_path.exists():
        raise FileNotFoundError(f"Descriptors file not found at {descriptors_path}")
    
    df = pd.read_csv(descriptors_path)
    
    # Filter out non-feature columns
    exclude_cols = {'composition', 'formation_energy', 'crystal_system', 'material_id'}
    feature_cols = [col for col in df.columns if col not in exclude_cols]
    
    # Sort to ensure consistent ordering
    feature_cols.sort()
    return feature_cols

def load_models() -> Dict[str, Any]:
    """
    Load the trained models from the pickle file.
    """
    paths = load_paths()
    models_path = paths['data_evaluation'] / 'trained_models.pkl'
    
    if not models_path.exists():
        raise FileNotFoundError(f"Trained models file not found at {models_path}")
    
    with open(models_path, 'rb') as f:
        models = pickle.load(f)
    
    return models

def load_feature_ranking() -> List[Dict[str, Any]]:
    """
    Load the feature ranking from the JSON file generated by T040.
    """
    paths = load_paths()
    ranking_path = paths['data_evaluation'] / 'feature_ranking.json'
    
    if not ranking_path.exists():
        raise FileNotFoundError(f"Feature ranking file not found at {ranking_path}")
    
    with open(ranking_path, 'r') as f:
        ranking = json.load(f)
    
    return ranking

def generate_pdp(
    model: Any,
    X: pd.DataFrame,
    features: List[str],
    feature_names: List[str],
    output_dir: Path
) -> None:
    """
    Generate Partial Dependence Plots for a list of features.
    """
    # Ensure output directory exists
    output_dir.mkdir(parents=True, exist_ok=True)
    
    # Map feature names to indices for sklearn
    # sklearn expects indices or feature names that match the training data
    # We assume the X passed here matches the model's training data columns
    
    feature_indices = []
    for feat in features:
        if feat in feature_names:
            feature_indices.append(feature_names.index(feat))
        else:
            logger.warning(f"Feature {feat} not found in model features, skipping.")
    
    if not feature_indices:
        logger.error("No valid features found for PDP generation.")
        return

    # Calculate partial dependence
    # Using 'auto' for kind to let sklearn decide (usually 'average')
    pdp_results = partial_dependence(
        model, 
        X, 
        features=feature_indices,
        kind='average'
    )
    
    # pdp_results is a dict with 'values' and 'average' (or 'values' and 'standard_deviation' if kind='both')
    # For 'average' kind: 'values' is list of arrays (one per feature), 'average' is list of arrays
    
    # Create a figure for each feature or a grid? 
    # Let's create a grid if multiple features, otherwise one plot.
    n_features = len(features)
    if n_features == 0:
        return

    fig, axes = plt.subplots(1, n_features, figsize=(5 * n_features, 5))
    if n_features == 1:
        axes = [axes]
    
    for i, (feat_idx, feat_name) in enumerate(zip(feature_indices, features)):
        ax = axes[i]
        values = pdp_results['values'][feat_idx]
        pdp_values = pdp_results['average'][feat_idx]
        
        ax.plot(values, pdp_values, marker='o', linestyle='-', linewidth=2)
        ax.set_title(f'PDP: {feat_name}')
        ax.set_xlabel('Feature Value')
        ax.set_ylabel('Partial Dependence (Formation Energy)')
        ax.grid(True, alpha=0.3)
        
        # Rotate x-axis labels if they are too long or numerous
        plt.setp(ax.xaxis.get_majorticklabels(), rotation=45, ha='right')

    plt.tight_layout()
    output_file = output_dir / 'pdp_top_features.png'
    plt.savefig(output_file, dpi=300)
    plt.close()
    
    logger.info(f"PDPs saved to {output_file}")

def main():
    """
    Main entry point for PDP generation.
    """
    paths = load_paths()
    output_dir = paths['data_evaluation'] / 'pdp_plots'
    
    logger.info("Starting PDP generation...")
    
    try:
        # 1. Load models
        models = load_models()
        # Expecting 'rf' key based on T026/T038
        if 'rf' not in models:
            raise KeyError("Random Forest model ('rf') not found in trained_models.pkl")
        rf_model = models['rf']
        
        # 2. Load feature names from the data used for training
        # We need the X that was used to train the model. 
        # Since we don't have the exact X object, we load the descriptors CSV and assume the same columns.
        # Note: In a real pipeline, we might save the X_train or feature list separately.
        # Here we infer from the processed data file.
        feature_names = load_feature_names()
        logger.info(f"Loaded {len(feature_names)} feature names.")
        
        # 3. Load feature ranking to get top features
        ranking = load_feature_ranking()
        if not ranking:
            raise ValueError("Feature ranking is empty.")
        
        # Get top N features (e.g., top 5)
        top_n = min(5, len(ranking))
        top_features = [item['feature'] for item in ranking[:top_n]]
        logger.info(f"Generating PDPs for top {top_n} features: {top_features}")
        
        # 4. Load the full dataset to compute PDPs (we need X)
        # We load the descriptors CSV again to get the X matrix
        descriptors_path = paths['data_processed'] / 'computed_descriptors.csv'
        df = pd.read_csv(descriptors_path)
        
        # Filter to feature columns only
        exclude_cols = {'composition', 'formation_energy', 'crystal_system', 'material_id'}
        X = df[[col for col in df.columns if col not in exclude_cols]]
        
        # Ensure column order matches feature_names
        # (load_feature_names returns sorted, so we sort X columns too)
        X = X.sort_index(axis=1)
        
        if list(X.columns) != feature_names:
            logger.warning("Column order mismatch between loaded data and feature names. Attempting to align.")
            # If the sets are the same but order different, reorder
            if set(X.columns) == set(feature_names):
                X = X[feature_names]
            else:
                raise ValueError("Column mismatch between X and feature_names. Cannot proceed.")
        
        # 5. Generate PDPs
        generate_pdp(rf_model, X, top_features, feature_names, output_dir)
        
        logger.info("PDP generation completed successfully.")
        
    except Exception as e:
        logger.error(f"Error during PDP generation: {e}", exc_info=True)
        sys.exit(1)

if __name__ == '__main__':
    main()