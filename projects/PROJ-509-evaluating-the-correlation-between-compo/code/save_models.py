def save_artifacts(
    models: Dict[str, Any],
    metrics: Dict[str, Any],
    output_models_path: Path,
    output_metrics_path: Path
) -> None:
    """
    Saves the model objects and metrics dictionary to disk.
    
    Args:
        models: Dictionary containing trained model objects.
        metrics: Dictionary containing evaluation metrics (R2, MAE, RMSE, TVD, etc.).
        output_models_path: Path to save the pickle file.
        output_metrics_path: Path to save the JSON metrics file.
    """
    # Ensure output directory exists
    output_models_path.parent.mkdir(parents=True, exist_ok=True)
    
    # Save models
    with open(output_models_path, 'wb') as f:
        pickle.dump(models, f)
    logging.info(f"Models saved to {output_models_path}")
    
    # Save metrics
    with open(output_metrics_path, 'w') as f:
        json.dump(metrics, f, indent=2)
    logging.info(f"Metrics saved to {output_metrics_path}")


