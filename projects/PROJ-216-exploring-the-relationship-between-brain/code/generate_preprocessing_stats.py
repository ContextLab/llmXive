"""
Generate preprocessing statistics report.

This script reads the output of the preprocessing pipeline (T015/T016)
and generates a JSON summary file containing:
- total_subjects: Total number of subjects attempted
- successful_subjects: Number of subjects that passed all checks
- success_rate_percentage: Percentage of successful subjects
"""
import json
import os
import sys
from pathlib import Path
from typing import Dict, Any, List

# Add project root to path to allow relative imports if needed, 
# though this script is standalone logic.
project_root = Path(__file__).resolve().parent.parent
processed_dir = project_root / "data" / "processed"
output_file = processed_dir / "preprocessing_stats.json"

def load_subject_logs(log_dir: Path) -> List[Dict[str, Any]]:
    """
    Load all subject processing logs from the processed directory.
    Expects files named like 'sub-<id>_preprocess_log.json' or similar.
    """
    if not log_dir.exists():
        return []
    
    logs = []
    for file_path in log_dir.glob("*_preprocess_log.json"):
        try:
            with open(file_path, 'r') as f:
                data = json.load(f)
                # Ensure we have the status key
                if 'status' in data:
                    logs.append(data)
        except (json.JSONDecodeError, IOError):
            continue
    return logs

def calculate_stats(logs: List[Dict[str, Any]]) -> Dict[str, Any]:
    """
    Calculate statistics from the list of processing logs.
    """
    total_subjects = len(logs)
    successful_subjects = sum(1 for log in logs if log.get('status') == 'success')
    
    if total_subjects > 0:
        success_rate = (successful_subjects / total_subjects) * 100
    else:
        success_rate = 0.0

    return {
        "total_subjects": total_subjects,
        "successful_subjects": successful_subjects,
        "success_rate_percentage": round(success_rate, 2)
    }

def main():
    """
    Main entry point to generate the preprocessing stats file.
    """
    # Ensure output directory exists
    processed_dir.mkdir(parents=True, exist_ok=True)

    # Load logs generated by preprocess.py
    # The preprocess.py script (T015/T016) is expected to write individual logs
    # for each subject. We aggregate them here.
    logs = load_subject_logs(processed_dir)

    # Calculate statistics
    stats = calculate_stats(logs)

    # Write the output file
    with open(output_file, 'w') as f:
        json.dump(stats, f, indent=2)

    print(f"Preprocessing statistics written to: {output_file}")
    print(f"Total: {stats['total_subjects']}, Successful: {stats['successful_subjects']}, Rate: {stats['success_rate_percentage']}%")

if __name__ == "__main__":
    main()
