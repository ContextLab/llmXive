"""
Classifier Evaluation Module

This module implements the logic to calculate precision and recall
for the CodeBERT classifier against ground truth labels.
"""
import os
import sys
import json
import csv
from pathlib import Path
from typing import Dict, Any, List, Tuple

# Add project root to path for imports
PROJECT_ROOT = Path(__file__).parent.parent
sys.path.insert(0, str(PROJECT_ROOT))

from utils.logging_config import get_logger
from utils.models import LabelType

logger = get_logger(__name__)

def setup_output_directories():
    """Ensure output directories exist."""
    output_dir = PROJECT_ROOT / "data" / "ground_truth"
    output_dir.mkdir(parents=True, exist_ok=True)
    return output_dir

def load_ground_truth_labels(file_path: Path) -> List[Dict[str, Any]]:
    """
    Load ground truth labels from a CSV file.
    Expected columns: block_id, true_label (LLM or Human)
    """
    if not file_path.exists():
        raise FileNotFoundError(f"Ground truth file not found: {file_path}")

    data = []
    with open(file_path, 'r', newline='', encoding='utf-8') as f:
        reader = csv.DictReader(f)
        for row in reader:
            # Normalize label to enum or string
            label = row.get('true_label', '').strip()
            if label.upper() == 'LLM':
                label = LabelType.LLM.value
            elif label.upper() == 'HUMAN':
                label = LabelType.HUMAN.value
            else:
                logger.warning(f"Unknown label type in ground truth: {label}")
                continue

            data.append({
                'block_id': row.get('block_id'),
                'true_label': label
            })
    return data

def load_predicted_labels(file_path: Path) -> List[Dict[str, Any]]:
    """
    Load predicted labels from the matched pairs or blocks CSV.
    We assume the file contains 'block_id' and 'predicted_label' columns.
    If the file is 'matched_pairs.csv', we might need to join or filter.
    For T017b, we expect the data to come from the tagging process in 01_data_curation.py.
    We will look for 'data/processed/blocks_tagged.csv' or similar, or parse matched_pairs if it contains tags.
    
    However, T017a selects from the tagged blocks. The most reliable source for predictions
    is the file generated by T013/T015. Let's assume `data/processed/matched_pairs.csv`
    contains the 'predicted_label' column as per the pipeline flow, or we read the source
    tagging file if it exists.
    
    To be robust: We will look for the specific ground truth block IDs in the
    `data/processed/matched_pairs.csv` (which contains the tags from T013).
    """
    # Try to find the predicted labels. 
    # Based on T015 description, matched_pairs.csv contains the tags.
    predicted_file = PROJECT_ROOT / "data" / "processed" / "matched_pairs.csv"
    
    if not predicted_file.exists():
        # Fallback to blocks_tagged.csv if matched_pairs is not ready yet
        predicted_file = PROJECT_ROOT / "data" / "processed" / "blocks_tagged.csv"

    if not predicted_file.exists():
        raise FileNotFoundError(f"Predicted labels file not found at {predicted_file}")

    predictions = {}
    with open(predicted_file, 'r', newline='', encoding='utf-8') as f:
        reader = csv.DictReader(f)
        for row in reader:
            block_id = row.get('block_id')
            if not block_id:
                continue
            
            pred_label = row.get('predicted_label', '').strip()
            if pred_label.upper() == 'LLM':
                pred_label = LabelType.LLM.value
            elif pred_label.upper() == 'HUMAN':
                pred_label = LabelType.HUMAN.value
            else:
                # Skip if prediction is invalid or low confidence (though T013 should have filtered)
                continue

            predictions[block_id] = pred_label

    return predictions

def calculate_metrics(ground_truth: List[Dict], predictions: Dict[str, str]) -> Dict[str, float]:
    """
    Calculate Precision and Recall for the classifier.
    
    Precision = TP / (TP + FP)
    Recall = TP / (TP + FN)
    
    We calculate these for the LLM class (positive class).
    """
    tp = 0.0
    fp = 0.0
    fn = 0.0
    tn = 0.0

    for gt in ground_truth:
        block_id = gt['block_id']
        true_label = gt['true_label']
        
        if block_id not in predictions:
            # If we can't find the prediction, we might treat it as a failure or skip
            # For now, assume it's a negative prediction (Human) or skip. 
            # Given the pipeline, it should exist. If not, it's a mismatch.
            logger.warning(f"Block ID {block_id} in ground truth not found in predictions.")
            continue
        
        pred_label = predictions[block_id]

        if true_label == LabelType.LLM.value and pred_label == LabelType.LLM.value:
            tp += 1
        elif true_label == LabelType.HUMAN.value and pred_label == LabelType.LLM.value:
            fp += 1
        elif true_label == LabelType.LLM.value and pred_label == LabelType.HUMAN.value:
            fn += 1
        elif true_label == LabelType.HUMAN.value and pred_label == LabelType.HUMAN.value:
            tn += 1

    precision = tp / (tp + fp) if (tp + fp) > 0 else 0.0
    recall = tp / (tp + fn) if (tp + fn) > 0 else 0.0
    f1 = 2 * (precision * recall) / (precision + recall) if (precision + recall) > 0 else 0.0
    
    accuracy = (tp + tn) / (tp + tn + fp + fn) if (tp + tn + fp + fn) > 0 else 0.0

    return {
        "precision": round(precision, 4),
        "recall": round(recall, 4),
        "f1_score": round(f1, 4),
        "accuracy": round(accuracy, 4),
        "confusion_matrix": {
            "true_positive": int(tp),
            "false_positive": int(fp),
            "true_negative": int(tn),
            "false_negative": int(fn)
        },
        "sample_size": len(ground_truth)
    }

def save_metrics(metrics: Dict[str, Any], output_path: Path):
    """Save metrics to a JSON file."""
    with open(output_path, 'w', encoding='utf-8') as f:
        json.dump(metrics, f, indent=2)
    logger.info(f"Classifier metrics saved to {output_path}")

def main():
    """Main entry point for classifier evaluation."""
    logger.info("Starting classifier evaluation (T017b)...")
    
    output_dir = setup_output_directories()
    ground_truth_path = output_dir / "manual_labels.csv"
    metrics_output_path = output_dir / "classifier_metrics.json"

    # Load data
    try:
        ground_truth = load_ground_truth_labels(ground_truth_path)
        if not ground_truth:
            logger.error("No ground truth labels found. T017a must be completed first.")
            return
        
        predictions = load_predicted_labels(output_dir)
    except FileNotFoundError as e:
        logger.error(str(e))
        return

    # Calculate metrics
    metrics = calculate_metrics(ground_truth, predictions)
    
    # Save results
    save_metrics(metrics, metrics_output_path)
    
    logger.info(f"Evaluation complete. Precision: {metrics['precision']}, Recall: {metrics['recall']}")

if __name__ == "__main__":
    main()