"""
T017b: Calculate classifier precision and recall on ground truth subset.

Loads the ground truth manual labels (generated by T017a) and the
corresponding predicted labels from the matched pairs dataset.
Calculates precision, recall, and F1-score for the LLM vs Human classification.
Saves results to data/ground_truth/classifier_metrics.json.
"""
import os
import sys
import json
import csv
from pathlib import Path
from typing import Dict, Any, List, Tuple
import logging

# Add project root to path for imports
project_root = Path(__file__).parent.parent
sys.path.insert(0, str(project_root))

from utils.logging_config import get_logger, setup_logging
from utils.models import LabelType

logger = get_logger(__name__)

def setup_output_directories() -> Path:
    """Ensure the ground_truth directory exists."""
    output_dir = project_root / "data" / "ground_truth"
    output_dir.mkdir(parents=True, exist_ok=True)
    return output_dir

def load_ground_truth_labels(file_path: Path) -> Dict[str, str]:
    """
    Load manual labels from CSV.
    Returns a dict: {block_id: 'LLM' or 'Human'}
    """
    if not file_path.exists():
        raise FileNotFoundError(f"Ground truth file not found: {file_path}")
    
    labels = {}
    with open(file_path, 'r', newline='', encoding='utf-8') as f:
        reader = csv.DictReader(f)
        for row in reader:
            block_id = row.get('block_id')
            label = row.get('label')
            if block_id and label:
                labels[block_id] = label
    
    logger.info(f"Loaded {len(labels)} ground truth labels from {file_path}")
    return labels

def load_predicted_labels(file_path: Path) -> Dict[str, str]:
    """
    Load predicted labels from matched pairs CSV.
    Returns a dict: {block_id: 'LLM' or 'Human'}
    """
    if not file_path.exists():
        raise FileNotFoundError(f"Matched pairs file not found: {file_path}")
    
    predictions = {}
    with open(file_path, 'r', newline='', encoding='utf-8') as f:
        reader = csv.DictReader(f)
        for row in reader:
            block_id = row.get('block_id')
            # The classifier output is typically stored in 'predicted_label' or 'tag'
            # Based on typical pipeline flow, we look for the classifier's output
            pred_label = row.get('predicted_label') or row.get('tag')
            if block_id and pred_label:
                # Normalize label to 'LLM' or 'Human'
                if pred_label.upper() in ['LLM', 'AI', 'GENERATED']:
                    pred_label = 'LLM'
                elif pred_label.upper() in ['HUMAN', 'MANUAL']:
                    pred_label = 'Human'
                else:
                    continue # Skip invalid labels
                
                predictions[block_id] = pred_label
    
    logger.info(f"Loaded {len(predictions)} predicted labels from {file_path}")
    return predictions

def calculate_metrics(ground_truth: Dict[str, str], predictions: Dict[str, str]) -> Dict[str, Any]:
    """
    Calculate precision, recall, and F1-score for LLM and Human classes.
    """
    # Filter to only blocks present in both
    common_ids = set(ground_truth.keys()) & set(predictions.keys())
    
    if not common_ids:
        logger.warning("No overlapping blocks between ground truth and predictions.")
        return {
            "precision": {"LLM": 0.0, "Human": 0.0},
            "recall": {"LLM": 0.0, "Human": 0.0},
            "f1_score": {"LLM": 0.0, "Human": 0.0},
            "confusion_matrix": {"TP_LLM": 0, "FP_LLM": 0, "FN_LLM": 0, "TN_LLM": 0},
            "total_evaluated": 0
        }

    # Count True Positives, False Positives, False Negatives for LLM class
    tp_llm = 0
    fp_llm = 0
    fn_llm = 0
    tn_llm = 0

    for bid in common_ids:
        true_label = ground_truth[bid]
        pred_label = predictions[bid]
        
        if true_label == 'LLM':
            if pred_label == 'LLM':
                tp_llm += 1
            else:
                fn_llm += 1
        else: # true_label == 'Human'
            if pred_label == 'LLM':
                fp_llm += 1
            else:
                tn_llm += 1

    # Precision = TP / (TP + FP)
    precision_llm = tp_llm / (tp_llm + fp_llm) if (tp_llm + fp_llm) > 0 else 0.0
    precision_human = tn_llm / (tn_llm + fn_llm) if (tn_llm + fn_llm) > 0 else 0.0
    
    # Recall = TP / (TP + FN)
    recall_llm = tp_llm / (tp_llm + fn_llm) if (tp_llm + fn_llm) > 0 else 0.0
    recall_human = tn_llm / (tn_llm + fp_llm) if (tn_llm + fp_llm) > 0 else 0.0
    
    # F1 = 2 * (P * R) / (P + R)
    f1_llm = 2 * (precision_llm * recall_llm) / (precision_llm + recall_llm) if (precision_llm + recall_llm) > 0 else 0.0
    f1_human = 2 * (precision_human * recall_human) / (precision_human + recall_human) if (precision_human + recall_human) > 0 else 0.0

    metrics = {
        "precision": {
            "LLM": round(precision_llm, 4),
            "Human": round(precision_human, 4)
        },
        "recall": {
            "LLM": round(recall_llm, 4),
            "Human": round(recall_human, 4)
        },
        "f1_score": {
            "LLM": round(f1_llm, 4),
            "Human": round(f1_human, 4)
        },
        "confusion_matrix": {
            "TP_LLM": tp_llm,
            "FP_LLM": fp_llm,
            "FN_LLM": fn_llm,
            "TN_LLM": tn_llm
        },
        "total_evaluated": len(common_ids),
        "ground_truth_file": "data/ground_truth/manual_labels.csv",
        "predictions_file": "data/processed/matched_pairs.csv"
    }
    
    return metrics

def save_metrics(metrics: Dict[str, Any], output_path: Path) -> None:
    """Save metrics to JSON file."""
    with open(output_path, 'w', encoding='utf-8') as f:
        json.dump(metrics, f, indent=2)
    logger.info(f"Classifier metrics saved to {output_path}")

def main():
    """Main entry point for T017b."""
    setup_logging()
    output_dir = setup_output_directories()
    
    ground_truth_path = output_dir / "manual_labels.csv"
    predictions_path = project_root / "data" / "processed" / "matched_pairs.csv"
    output_metrics_path = output_dir / "classifier_metrics.json"

    try:
        logger.info("Starting classifier evaluation (T017b)...")
        
        # Load data
        ground_truth = load_ground_truth_labels(ground_truth_path)
        predictions = load_predicted_labels(predictions_path)
        
        # Calculate metrics
        metrics = calculate_metrics(ground_truth, predictions)
        
        # Save results
        save_metrics(metrics, output_metrics_path)
        
        logger.info(f"Evaluation complete. Precision: {metrics['precision']}, Recall: {metrics['recall']}")
        
    except FileNotFoundError as e:
        logger.error(f"Missing required data file: {e}")
        logger.error("Ensure T017a (ground truth selection) and T015 (matching) have been completed.")
        raise
    except Exception as e:
        logger.error(f"Error during evaluation: {e}")
        raise

if __name__ == "__main__":
    main()