def load_matched_pairs(filepath: str = "data/processed/matched_pairs.csv") -> List[Dict[str, Any]]:
    """Load matched pairs from the CSV file generated by T015."""
    if not os.path.exists(filepath):
        raise FileNotFoundError(f"Matched pairs file not found: {filepath}")
    
    pairs = []
    with open(filepath, 'r', encoding='utf-8') as f:
        reader = csv.DictReader(f)
        for row in reader:
            # Convert numeric strings to appropriate types if necessary
            if 'line_start_llm' in row:
                row['line_start_llm'] = int(row['line_start_llm'])
                row['line_end_llm'] = int(row['line_end_llm'])
                row['line_start_human'] = int(row['line_start_human'])
                row['line_end_human'] = int(row['line_end_human'])
            pairs.append(row)
    logger.info(f"Loaded {len(pairs)} matched pairs from {filepath}")
    return pairs

def parse_date(date_str: str) -> datetime:
    """Parse ISO format date string to datetime object."""
    try:
        return datetime.fromisoformat(date_str.replace('Z', '+00:00'))
    except ValueError:
        # Fallback for common formats
        return datetime.strptime(date_str, "%Y-%m-%dT%H:%M:%S")

def clone_repo_shallow(repo_url: str, dest_dir: Path, depth: int = 100):
    """Clone a repository shallowly to a temporary directory."""
    try:
        subprocess.run(
            ["git", "clone", "--depth", str(depth), "--no-tags", repo_url, str(dest_dir)],
            check=True,
            capture_output=True,
            text=True,
            timeout=300
        )
    except subprocess.CalledProcessError as e:
        if "404" in str(e.stderr):
            raise RepositoryNotFoundError(f"Repository not found or deleted: {repo_url}")
        raise MetricExtractionError(f"Failed to clone repository: {e.stderr}")
    except subprocess.TimeoutExpired:
        raise MetricExtractionError(f"Timeout cloning repository: {repo_url}")

def get_commit_history_for_block(repo_path: Path, file_path: str, start_line: int, end_line: int, since: Optional[str] = None):
    """
    Retrieve commit history for a specific block of code.
    Returns a list of commits affecting this block.
    """
    log_cmd = [
        "git", "-C", str(repo_path), "log",
        "--follow",
        "--numstat",
        "--pretty=format:%H|%ai|%s",
        "--", file_path
    ]
    
    if since:
        log_cmd.insert(4, f"--since={since}")

    try:
        result = subprocess.run(log_cmd, capture_output=True, text=True, timeout=60)
        if result.returncode != 0:
            return []
        
        commits = []
        current_commit = None
        
        for line in result.stdout.splitlines():
            if "|" in line and len(line.split("|")) >= 3:
                # Header: hash|date|message
                parts = line.split("|", 2)
                current_commit = {
                    "hash": parts[0],
                    "date": parts[1],
                    "message": parts[2],
                    "changes": []
                }
            elif current_commit and "\t" in line:
                # Numstat: added\tdeleted\tfilename
                parts = line.split("\t")
                if len(parts) >= 3 and parts[2] == file_path:
                    added = int(parts[0]) if parts[0] != '-' else 0
                    deleted = int(parts[1]) if parts[1] != '-' else 0
                    current_commit["changes"].append({
                        "added": added,
                        "deleted": deleted
                    })
        
        return commits
    except subprocess.TimeoutExpired:
        logger.warning(f"Timeout retrieving history for {file_path}")
        return []

def extract_metrics_for_pair(pair: Dict[str, Any], repo_cache: Dict[str, Path]) -> BlockHistory:
    """
    Extract longitudinal metrics for a single matched pair.
    Returns a BlockHistory object populated with metrics.
    """
    repo_id = pair.get("repo_id")
    llm_file = pair.get("file_path_llm")
    llm_start = int(pair.get("line_start_llm"))
    llm_end = int(pair.get("line_end_llm"))
    human_file = pair.get("file_path_human")
    human_start = int(pair.get("line_start_human"))
    human_end = int(pair.get("line_end_human"))
    
    history = BlockHistory(
        block_id=pair.get("pair_id", f"{repo_id}_{llm_file}"),
        repo_id=repo_id,
        file_path=llm_file, # Primary file for reference
        start_line=llm_start,
        end_line=llm_end,
        label=pair.get("label_llm", "LLM")
    )

    if repo_id not in repo_cache:
        raise RepositoryNotFoundError(f"Repository {repo_id} not in cache.")

    repo_path = repo_cache[repo_id]
    
    # Determine the time window (e.g., 6 months from repo creation or last commit)
    # For simplicity, we look at all commits since the block was introduced (approximated by repo age)
    # In a real scenario, we'd parse the first commit of the file.
    since_date = None 
    
    # 1. Calculate Code Churn (Lines Changed)
    # We aggregate changes for the file containing the block. 
    # A more precise implementation would map line ranges to hunks, but file-level is a strong proxy for block maintenance.
    llm_commits = get_commit_history_for_block(repo_path, llm_file, llm_start, llm_end, since=since_date)
    human_commits = get_commit_history_for_block(repo_path, human_file, human_start, human_end, since=since_date)
    
    llm_churn = 0
    for commit in llm_commits:
        for change in commit["changes"]:
            llm_churn += change["added"] + change["deleted"]
    
    human_churn = 0
    for commit in human_commits:
        for change in commit["changes"]:
            human_churn += change["added"] + change["deleted"]
    
    history.total_lines_changed = llm_churn # Storing LLM churn as primary, or we could store both
    history.commits = llm_commits # Store raw data for debugging if needed

    # 2. Calculate Bug Fix Latency
    # Look for "Fixes #N" or "Closes #N" in commit messages
    # We assume the block was introduced at time 0 (simplified) and measure time to first fix
    # A more robust method: diff the block content to see if it changed significantly (bug fix vs refactor)
    
    first_fix_date = None
    for commit in llm_commits:
        msg = commit["message"].lower()
        if "fixes #" in msg or "closes #" in msg or "bug" in msg:
            first_fix_date = parse_date(commit["date"])
            break
    
    if first_fix_date:
        # Assuming the "start" is the repo creation time or a fixed baseline.
        # Since we don't have the exact block creation time without full history, 
        # we approximate latency as the time from the first commit of the file to the fix.
        # For this task, we will record the date of the fix and calculate relative to a baseline if available.
        # If baseline is missing, we store the date and let analysis handle nulls (T023).
        # However, to provide a value, let's assume the block exists from the first commit of the file.
        first_commit_date = parse_date(llm_commits[-1]["date"]) if llm_commits else first_fix_date
        latency = (first_fix_date - first_commit_date).days
        history.days_to_first_fix = max(0, latency)
        history.bug_fixes_count = len([c for c in llm_commits if "fixes #" in c["message"].lower() or "closes #" in c["message"].lower()])

    return history

def run_extraction_pipeline():
    """Main pipeline to extract metrics for all matched pairs."""
    setup_output_directories()
    
    try:
        pairs = load_matched_pairs()
    except FileNotFoundError as e:
        logger.error(str(e))
        return

    # Cache repositories to avoid re-cloning
    repo_cache = {}
    
    results = []
    skipped_count = 0

    for i, pair in enumerate(pairs):
        repo_id = pair.get("repo_id")
        if not repo_id:
            logger.warning(f"Skipping pair {i}: Missing repo_id")
            continue

        if repo_id not in repo_cache:
            # Clone repository
            repo_url = pair.get("repo_url")
            if not repo_url:
                logger.warning(f"Skipping pair {i}: Missing repo_url for {repo_id}")
                continue
            
            temp_dir = Path(tempfile.mkdtemp(prefix=f"repo_{repo_id}_"))
            try:
                clone_repo_shallow(repo_url, temp_dir)
                repo_cache[repo_id] = temp_dir
            except (RepositoryNotFoundError, MetricExtractionError) as e:
                logger.warning(f"Skipping pair {i}: {e}")
                # T024: Gracefully exclude deleted repos
                continue

        try:
            history = extract_metrics_for_pair(pair, repo_cache)
            results.append(history)
        except Exception as e:
            logger.error(f"Error extracting metrics for pair {pair.get('pair_id')}: {e}")
            # T023: Log exclusion reasons for null latency/churn
            continue

    # Write results to CSV
    output_path = "data/processed/metrics_longitudinal.csv"
    if not results:
        logger.warning("No metrics extracted. Creating empty file with headers.")
    
    fieldnames = [
        "block_id", "repo_id", "file_path", "start_line", "end_line", "label",
        "total_lines_changed", "days_to_first_fix", "bug_fixes_count",
        "commit_count", "last_commit_date"
    ]

    with open(output_path, 'w', newline='', encoding='utf-8') as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        
        for h in results:
            row = {
                "block_id": h.block_id,
                "repo_id": h.repo_id,
                "file_path": h.file_path,
                "start_line": h.start_line,
                "end_line": h.end_line,
                "label": h.label,
                "total_lines_changed": h.total_lines_changed,
                "days_to_first_fix": h.days_to_first_fix if h.days_to_first_fix is not None else "",
                "bug_fixes_count": h.bug_fixes_count,
                "commit_count": len(h.commits) if h.commits else 0,
                "last_commit_date": h.commits[0]["date"] if h.commits else ""
            }
            writer.writerow(row)

    logger.info(f"Successfully saved {len(results)} metric records to {output_path}")

    # Cleanup temp dirs
    for path in repo_cache.values():
        try:
            shutil.rmtree(path)
        except Exception as e:
            logger.warning(f"Failed to remove temp dir {path}: {e}")

def main():
    """Entry point for the metric extraction script."""
    setup_logging()
    run_extraction_pipeline()

if __name__ == "__main__":
    main()