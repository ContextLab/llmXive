"""
Task T014: Data Integrity Checks for User Story 1.

Verifies that the CSV files generated by fetch_gdelt.py and fetch_google_trends.py
contain non-empty rows for the target date range.
Writes a validation_status.json file with the results or exits non-zero on failure.
"""
import os
import sys
import json
import logging
from datetime import datetime
from typing import Dict, Any, List

import pandas as pd

# Add project root to path to allow relative imports if running as script
project_root = os.path.dirname(os.path.dirname(os.path.dirname(os.path.abspath(__file__))))
if project_root not in sys.path:
    sys.path.insert(0, project_root)

from utils.logging import get_logger

logger = get_logger(__name__)

# Configuration
TARGET_DATA_DIR = "data/raw"
GDLET_FILE = "gdelt_events.csv"
GOOGLE_TRENDS_FILE = "google_trends.csv"
OUTPUT_STATUS_FILE = "validation_status.json"
MIN_ROWS_THRESHOLD = 1

def check_csv_integrity(file_path: str, min_rows: int = MIN_ROWS_THRESHOLD) -> Dict[str, Any]:
    """
    Checks if a CSV file exists, is readable, and contains at least min_rows rows.
    
    Args:
        file_path: Relative path to the CSV file from project root.
        min_rows: Minimum number of data rows required.
        
    Returns:
        Dictionary with status, row_count, and error message if any.
    """
    result = {
        "file": file_path,
        "exists": False,
        "valid": False,
        "row_count": 0,
        "error": None
    }

    full_path = os.path.join(project_root, file_path)
    
    if not os.path.exists(full_path):
        result["error"] = f"File not found: {full_path}"
        logger.error(result["error"])
        return result

    result["exists"] = True

    try:
        df = pd.read_csv(full_path)
        result["row_count"] = len(df)
        
        if result["row_count"] < min_rows:
            result["error"] = f"Insufficient data: found {result['row_count']} rows, required >= {min_rows}"
            logger.warning(result["error"])
            return result
        
        # Check for date column if it exists to ensure it's not just headers
        date_cols = [col for col in df.columns if 'date' in col.lower() or 'time' in col.lower()]
        if not date_cols:
            logger.warning(f"No date/time column detected in {file_path}, assuming valid if row count ok.")
        
        result["valid"] = True
        logger.info(f"Integrity check passed for {file_path}: {result['row_count']} rows.")
        
    except Exception as e:
        result["error"] = f"Failed to read/parse CSV: {str(e)}"
        logger.error(result["error"])
        
    return result

def main():
    """
    Main entry point for data integrity validation.
    Reads raw CSVs, checks row counts, and writes validation_status.json.
    """
    logger.info("Starting data integrity checks (Task T014)...")
    
    # Define files to check relative to project root
    files_to_check = [
        os.path.join(TARGET_DATA_DIR, GDLET_FILE),
        os.path.join(TARGET_DATA_DIR, GOOGLE_TRENDS_FILE)
    ]
    
    validation_results = {
        "timestamp": datetime.now().isoformat(),
        "task_id": "T014",
        "overall_status": "passed",
        "checks": []
    }
    
    all_passed = True
    
    for file_path in files_to_check:
        check_result = check_csv_integrity(file_path)
        validation_results["checks"].append(check_result)
        if not check_result["valid"]:
            all_passed = False
    
    validation_results["overall_status"] = "passed" if all_passed else "failed"
    
    # Write output status file
    output_path = os.path.join(project_root, OUTPUT_STATUS_FILE)
    try:
        with open(output_path, 'w') as f:
            json.dump(validation_results, f, indent=2)
        logger.info(f"Validation status written to {output_path}")
    except Exception as e:
        logger.error(f"Failed to write validation status file: {e}")
        all_passed = False
    
    if all_passed:
        logger.info("All integrity checks passed.")
        sys.exit(0)
    else:
        logger.error("One or more integrity checks failed.")
        sys.exit(1)

if __name__ == "__main__":
    main()