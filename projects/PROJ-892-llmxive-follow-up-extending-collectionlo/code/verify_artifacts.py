"""
Verification script for T033: Final review of state/artifacts.yaml.

This script validates that all critical model weights and data artifacts
generated by the pipeline are present in state/artifacts.yaml and have
valid SHA-256 checksums.

It performs the following checks:
1. Loads state/artifacts.yaml
2. Verifies existence of:
   - Base model weights
   - CollectionLoRA adapter (FP16)
   - Quantized adapters (INT8, INT4)
   - Generated reference images
   - Generated test images
   - Subspace ranks data
   - Analysis results
3. Validates that each artifact entry has a non-empty SHA-256 hash
4. Optionally re-computes hashes for artifacts that exist on disk
5. Reports any missing or invalid entries
"""
import os
import sys
import json
import hashlib
import logging
from pathlib import Path
from typing import Dict, Any, List, Tuple, Optional
import yaml

# Configure logging
logging.basicConfig(
    level=logging.INFO,
    format='%(asctime)s - %(levelname)s - %(message)s'
)
logger = logging.getLogger(__name__)

def compute_sha256(file_path: Path) -> Optional[str]:
    """Compute SHA-256 hash of a file."""
    if not file_path.exists():
        logger.warning(f"File not found: {file_path}")
        return None
    
    sha256_hash = hashlib.sha256()
    try:
        with open(file_path, "rb") as f:
            for chunk in iter(lambda: f.read(4096), b""):
                sha256_hash.update(chunk)
        return sha256_hash.hexdigest()
    except Exception as e:
        logger.error(f"Error computing hash for {file_path}: {e}")
        return None

def load_artifacts_state(state_path: Path) -> Dict[str, Any]:
    """Load the artifacts state file."""
    if not state_path.exists():
        raise FileNotFoundError(f"State file not found: {state_path}")
    
    with open(state_path, 'r') as f:
        return yaml.safe_load(f)

def validate_artifact_entry(
    entry_name: str, 
    entry: Dict[str, Any], 
    project_root: Path,
    verify_on_disk: bool = False
) -> Tuple[bool, List[str]]:
    """
    Validate a single artifact entry.
    
    Returns:
        Tuple of (is_valid, list_of_issues)
    """
    issues = []
    
    # Check required fields
    if 'path' not in entry:
        issues.append(f"Missing 'path' field for {entry_name}")
        return False, issues
    
    if 'hash' not in entry:
        issues.append(f"Missing 'hash' field for {entry_name}")
        return False, issues
    
    if not entry['hash']:
        issues.append(f"Empty hash for {entry_name}")
        return False, issues
    
    # Resolve path
    artifact_path = project_root / entry['path']
    
    # If verify_on_disk is True, check file existence and recompute hash
    if verify_on_disk:
        if not artifact_path.exists():
            issues.append(f"Artifact file missing on disk: {artifact_path}")
            return False, issues
        
        computed_hash = compute_sha256(artifact_path)
        if computed_hash is None:
            issues.append(f"Could not compute hash for {artifact_path}")
            return False, issues
        
        if computed_hash != entry['hash']:
            issues.append(
                f"Hash mismatch for {entry_name}: "
                f"expected {entry['hash']}, got {computed_hash}"
            )
            return False, issues
    else:
        # Just check that the path is valid (doesn't need to exist for this check)
        # but warn if it's clearly malformed
        if not entry['path'].startswith(('data/', 'state/', 'figures/')):
            issues.append(
                f"Artifact path for {entry_name} doesn't follow project structure: "
                f"{entry['path']}"
            )
    
    if not issues:
        logger.info(f"✓ {entry_name}: Valid")
    
    return len(issues) == 0, issues

def main(verify_on_disk: bool = False) -> int:
    """
    Main verification routine.
    
    Args:
        verify_on_disk: If True, re-compute hashes and verify files exist on disk.
                       If False, only check that entries exist in artifacts.yaml.
    
    Returns:
        0 if all checks pass, 1 otherwise.
    """
    project_root = Path(__file__).parent.parent
    state_path = project_root / "state" / "artifacts.yaml"
    
    logger.info(f"Loading artifacts state from: {state_path}")
    
    try:
        artifacts_state = load_artifacts_state(state_path)
    except FileNotFoundError as e:
        logger.error(str(e))
        return 1
    except yaml.YAMLError as e:
        logger.error(f"Invalid YAML in state file: {e}")
        return 1
    
    if not artifacts_state or 'artifacts' not in artifacts_state:
        logger.error("No 'artifacts' key found in state file")
        return 1
    
    artifacts = artifacts_state['artifacts']
    
    # Define expected critical artifacts based on completed tasks
    critical_artifacts = {
        # From T007/T007b: Base model and LoRA adapter
        "base_model": "data/models/sd_base_model.safetensors",
        "adapter_fp16": "data/models/adapter_fp16.safetensors",
        
        # From T009: Subspace ranks
        "subspace_ranks": "data/subspace_ranks.json",
        
        # From T011c: FP16 reference images
        "fp16_references_dir": "data/references/fp16_refs/",
        
        # From T016: Quantized adapters
        "adapter_int8": "data/quantized/adapter_int8.safetensors",
        "adapter_int4": "data/quantized/adapter_int4.safetensors",
        
        # From T014/T020: Generated results
        "results_csv": "data/results.csv",
        "generated_images_dir": "data/generated/",
        
        # From T024/T026: Analysis results
        "analysis_results": "data/analysis_results.json"
    }
    
    # Build a map of registered artifacts by path
    registered_paths = {}
    for name, entry in artifacts.items():
        if isinstance(entry, dict) and 'path' in entry:
            registered_paths[entry['path']] = {
                'name': name,
                'entry': entry
            }
    
    # Check for critical artifacts
    missing_critical = []
    invalid_entries = []
    
    logger.info("\n--- Checking Critical Artifacts ---")
    
    for category, expected_path in critical_artifacts.items():
        # Check if path exists in registered artifacts
        found = False
        for reg_path, reg_info in registered_paths.items():
            if reg_path == expected_path or reg_path.startswith(expected_path.rstrip('/')):
                found = True
                is_valid, issues = validate_artifact_entry(
                    f"{category} ({reg_info['name']})",
                    reg_info['entry'],
                    project_root,
                    verify_on_disk
                )
                if not is_valid:
                    invalid_entries.extend(issues)
                break
        
        if not found:
            # Special case for directories - check if any artifacts match the prefix
            if expected_path.endswith('/'):
                dir_prefix = expected_path.rstrip('/')
                matching = [p for p in registered_paths.keys() if p.startswith(dir_prefix)]
                if not matching:
                    missing_critical.append(
                        f"Critical artifact missing: {category} (expected path: {expected_path})"
                    )
            else:
                missing_critical.append(
                    f"Critical artifact missing: {category} (expected path: {expected_path})"
                )
    
    # Report results
    logger.info("\n--- Verification Summary ---")
    
    if missing_critical:
        logger.error(f"Missing {len(missing_critical)} critical artifacts:")
        for item in missing_critical:
            logger.error(f"  - {item}")
    
    if invalid_entries:
        logger.error(f"Found {len(invalid_entries)} invalid artifact entries:")
        for item in invalid_entries:
            logger.error(f"  - {item}")
    
    # Check for any artifacts without hashes (catch-all)
    unhash_artifacts = []
    for name, entry in artifacts.items():
        if isinstance(entry, dict):
            if 'hash' not in entry or not entry.get('hash'):
                unhash_artifacts.append(name)
    
    if unhash_artifacts:
        logger.error(f"Found {len(unhash_artifacts)} artifacts without hashes:")
        for item in unhash_artifacts:
            logger.error(f"  - {item}")
    
    # Final verdict
    total_issues = len(missing_critical) + len(invalid_entries) + len(unhash_artifacts)
    
    if total_issues == 0:
        logger.info("\n✓ All critical artifacts are present and checksummed!")
        return 0
    else:
        logger.error(f"\n✗ Verification failed with {total_issues} issue(s)")
        return 1

if __name__ == "__main__":
    # Default to not verifying on disk (just checking artifacts.yaml structure)
    # Pass --verify to re-compute hashes and check file existence
    verify_on_disk = "--verify" in sys.argv
    sys.exit(main(verify_on_disk))
