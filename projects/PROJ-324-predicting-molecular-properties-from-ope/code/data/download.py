def fetch_molecule_properties(cids: List[int], properties: List[str]) -> List[Dict[str, Any]]:
    """
    Fetch molecule properties from PubChem using pubchempy.
    Uses the verified recipe from the execution feedback.
    """
    records = []
    logger.info(f"Fetching properties for {len(cids)} CIDs from PubChem...")
    
    try:
        compounds = pcp.get_compounds(cids, properties=properties)
    except Exception as e:
        logger.error(f"Failed to fetch compounds from PubChem: {e}")
        raise RuntimeError(f"PubChem fetch failed: {e}")

    for c in compounds:
        rec = {}
        # Map pubchempy attributes to standard keys
        if getattr(c, 'isomeric_smiles', None):
            rec['SMILES'] = c.isomeric_smiles
        elif getattr(c, 'canonical_smiles', None):
            rec['SMILES'] = c.canonical_smiles
        
        if getattr(c, 'xlogp', None) is not None:
            rec['LogP'] = c.xlogp
        
        if getattr(c, 'water_solubility', None) is not None:
            rec['Solubility'] = c.water_solubility
        
        if getattr(c, 'boiling_point', None) is not None:
            rec['Boiling Point'] = c.boiling_point
        
        if getattr(c, 'molecular_weight', None) is not None:
            rec['Molecular Weight'] = c.molecular_weight
        
        if getattr(c, 'record_type', None) is not None:
            rec['Property Type'] = c.record_type
        
        # Capture CID for traceability
        rec['CID'] = c.cid

        if rec:
            records.append(rec)
    
    logger.info(f"Successfully fetched {len(records)} valid records.")
    return records

