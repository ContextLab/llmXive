def save_metadata(metadata: Dict[str, Any], output_path: Path):
    """Save metadata to a JSON file."""
    with open(output_path, 'w') as f:
        json.dump(metadata, f, indent=2)
    logger.info(f"Metadata saved to {output_path}")

def compute_file_hash(file_path: Path) -> str:
    """Compute SHA-256 hash of a file."""
    sha256_hash = hashlib.sha256()
    with open(file_path, "rb") as f:
        for byte_block in iter(lambda: f.read(4096), b""):
            sha256_hash.update(byte_block)
    return sha256_hash.hexdigest()

def create_dataset_metadata(raw_csv_path: Path, query_params: Dict[str, Any]) -> Dict[str, Any]:
    """
    Create the dataset metadata JSON file with runtime schema checks.
    
    This function explicitly documents the experimental source, measurement conditions,
    and source confidence. It performs a runtime schema check for 'measurement_uncertainty'
    and 'quantity_of_substance' fields. If absent, it records them as 'Not Available in Source'.
    
    Args:
        raw_csv_path: Path to the raw CSV file generated by the download process.
        query_params: The parameters used to fetch the data.
        
    Returns:
        The metadata dictionary.
    """
    # Check if the raw CSV exists to derive schema info
    measurement_uncertainty_status = "Not Available in Source"
    quantity_of_substance_status = "Not Available in Source"
    experimental_ratio = 0.0
    
    if raw_csv_path.exists():
        try:
            df = pd.read_csv(raw_csv_path)
            columns = set(df.columns)
            
            # Runtime schema check for specific fields
            if 'measurement_uncertainty' in columns:
                measurement_uncertainty_status = "Available"
            if 'quantity_of_substance' in columns:
                quantity_of_substance_status = "Available"
            
            # Calculate experimental ratio if 'Property Type' or similar exists
            if 'Property Type' in columns:
                total = len(df)
                if total > 0:
                    experimental_count = df[df['Property Type'] == 'Experimental'].shape[0]
                    experimental_ratio = experimental_count / total
            elif 'source_type' in columns:
                total = len(df)
                if total > 0:
                    experimental_count = df[df['source_type'] == 'Experimental'].shape[0]
                    experimental_ratio = experimental_count / total
                    
        except Exception as e:
            logger.warning(f"Could not analyze raw CSV for schema status: {e}")
    else:
        logger.warning(f"Raw CSV not found at {raw_csv_path} for metadata derivation.")

    # Compute source hash if file exists
    source_hash = None
    if raw_csv_path.exists():
        source_hash = compute_file_hash(raw_csv_path)

    metadata = {
        "source": "PubChem",
        "query_parameters": query_params,
        "measurement_uncertainty_status": measurement_uncertainty_status,
        "quantity_of_substance_status": quantity_of_substance_status,
        "experimental_ratio": round(experimental_ratio, 4),
        "timestamp": datetime.utcnow().isoformat(),
        "raw_file_path": str(raw_csv_path),
        "source_hash": source_hash
    }
    
    return metadata

