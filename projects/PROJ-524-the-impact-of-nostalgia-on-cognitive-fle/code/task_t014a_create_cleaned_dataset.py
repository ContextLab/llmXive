"""
T014a: Create data/processed/cleaned_dataset.csv from validated data.

This script loads the validated dataset (produced by T010/T011), applies
the exclusion logic (from T012), checks the MMSE flag (from T013b), and
writes the final cleaned CSV to data/processed/cleaned_dataset.csv.

Columns:
- participant_id
- stimulus_type (nostalgia/control)
- perseverative_errors
- categories_completed
- age
"""

import os
import json
import logging
import pandas as pd
from pathlib import Path

# Import from project modules
from config import get_config, get_env_bool
from ingestion import load_dataset
from utils import setup_logging, log_info, log_warning, log_error, get_timestamp

def load_exclusion_log(exclusion_path: Path) -> dict:
    """Load the exclusion log JSON generated by T012."""
    if not exclusion_path.exists():
        log_warning(f"Exclusion log not found at {exclusion_path}. Assuming no exclusions.")
        return {"excluded_ids": []}
    
    try:
        with open(exclusion_path, 'r', encoding='utf-8') as f:
            return json.load(f)
    except json.JSONDecodeError as e:
        log_error(f"Failed to parse exclusion log: {e}")
        return {"excluded_ids": []}

def load_mmse_flag(config: dict) -> bool:
    """Read the has_mmse flag from config (set by T013b)."""
    # The flag is typically stored in the config under a specific key or environment
    # We check the config dict passed from load_config
    return config.get("has_mmse", False)

def main():
    setup_logging()
    log_info(f"Starting T014a: Creating cleaned dataset at {get_timestamp()}")

    config = get_config()
    
    # Paths
    processed_dir = Path(config.get("paths", {}).get("processed", "data/processed"))
    processed_dir.mkdir(parents=True, exist_ok=True)
    
    exclusion_log_path = processed_dir / "exclusion_log.json"
    output_csv_path = processed_dir / "cleaned_dataset.csv"

    # Load exclusion data
    exclusion_data = load_exclusion_log(exclusion_log_path)
    excluded_ids = set(exclusion_data.get("excluded_ids", []))
    log_info(f"Loaded {len(excluded_ids)} excluded IDs from exclusion log.")

    # Load the validated dataset (from T010/T011)
    # We assume the ingestion module has a way to load the intermediate validated state
    # If T010/T011 writes a specific intermediate file, we load that. 
    # Based on typical flow, we might load from data/processed/validated_data.csv or similar.
    # However, the ingestion module `load_dataset` usually returns a DataFrame.
    # We need to ensure we are loading the data AFTER validation (T011) but BEFORE final filtering.
    # Since T011 filters age >= 65, we assume the dataset loaded here is already filtered by age.
    # The task says "Depends on T012 (exclusions)", so we apply exclusion now.
    
    # Attempt to load the raw/validated data source. 
    # We will use the ingestion function to load the data source defined in config.
    # If the data was already written to a temp file by T011, we load that.
    # For this implementation, we assume `load_dataset` can fetch the data from the source 
    # or a cached intermediate file if available. 
    # To be safe and deterministic, we look for a "validated" file if it exists, 
    # otherwise we fetch again.
    
    validated_data_path = processed_dir / "validated_data.csv"
    if validated_data_path.exists():
        log_info(f"Loading validated data from {validated_data_path}")
        df = pd.read_csv(validated_data_path)
    else:
        # Fallback: fetch from source again (T010 logic)
        log_warning("validated_data.csv not found. Fetching from source (T010).")
        df = load_dataset()
        if df is None or df.empty:
            log_error("Failed to load dataset. Exiting.")
            return

    # Ensure required columns exist
    required_cols = ['participant_id', 'stimulus_type', 'perseverative_errors', 'categories_completed', 'age']
    missing_cols = [c for c in required_cols if c not in df.columns]
    if missing_cols:
        log_error(f"Missing required columns in dataset: {missing_cols}")
        return

    # Apply exclusions (T012)
    initial_count = len(df)
    df_clean = df[~df['participant_id'].isin(excluded_ids)].copy()
    excluded_count = initial_count - len(df_clean)
    log_info(f"Applied exclusions. Removed {excluded_count} records.")

    # MMSE Filter (T013b context)
    # T013b sets has_mmse=True in config if MMSE column exists in raw data.
    # T027 (later) will use this to filter MMSE < 24, but T014a just needs to 
    # ensure the dataset is clean based on current rules. 
    # The task description for T014a does NOT explicitly say to filter MMSE < 24 here,
    # but T027 says "If has_mmse=True, exclude participants with MMSE < 24".
    # Since T014a is the creation of the base cleaned dataset, and T027 is a sensitivity analysis step,
    # we typically do NOT apply the MMSE < 24 filter in the base cleaned dataset unless specified.
    # However, the dependency says "T014a depends on T013b (MMSE flag for downstream filtering logic)".
    # This implies T014a might need to check the flag. 
    # Standard practice: The "cleaned_dataset" is the primary analysis dataset. 
    # If the study design requires MMSE >= 24 for the primary analysis, we apply it.
    # Given T027 is a "robustness check" (sensitivity), it implies the base analysis (T014a)
    # might NOT include the MMSE < 24 exclusion, OR the MMSE exclusion is part of the base.
    # Let's re-read T027: "If has_mmse=True... exclude... and re-run analysis". 
    # This "re-run" suggests the base analysis (T018+) runs on the dataset from T014a.
    # If T014a already excludes MMSE < 24, then T027's "re-run" would be redundant or different.
    # Usually, "cleaned dataset" implies standard inclusion/exclusion. 
    # If MMSE < 24 is a standard exclusion for cognitive studies, it should be here.
    # BUT T027 says "re-run analysis" implying the exclusion is an *additional* step for sensitivity.
    # Therefore, T014a should NOT filter MMSE < 24. It just prepares the data.
    # We will proceed without MMSE < 24 filtering here, as T027 handles the sensitivity check.
    
    # Select and order columns
    output_df = df_clean[required_cols].copy()

    # Ensure types
    output_df['participant_id'] = output_df['participant_id'].astype(str)
    output_df['stimulus_type'] = output_df['stimulus_type'].astype(str)
    output_df['perseverative_errors'] = pd.to_numeric(output_df['perseverative_errors'], errors='coerce')
    output_df['categories_completed'] = pd.to_numeric(output_df['categories_completed'], errors='coerce')
    output_df['age'] = pd.to_numeric(output_df['age'], errors='coerce')

    # Drop rows with NaN in critical numeric columns if any (safety)
    before_drop = len(output_df)
    output_df = output_df.dropna(subset=['perseverative_errors', 'categories_completed', 'age'])
    if len(output_df) < before_drop:
        log_warning(f"Dropped {before_drop - len(output_df)} rows with NaN in metric columns.")

    # Save to CSV
    output_df.to_csv(output_csv_path, index=False)
    log_info(f"Successfully wrote {len(output_df)} records to {output_csv_path}")
    log_info("T014a completed.")

if __name__ == "__main__":
    main()