def run_analysis_pipeline() -> Dict[str, Any]:
    """
    Run the full statistical analysis pipeline if intermediate results are missing.
    Returns the analysis results dictionary.
    """
    log_info("Running full statistical analysis pipeline...")
    # run_full_analysis is expected to produce the intermediate JSON and return the data
    # If it doesn't return data, we might need to read the file it just wrote.
    # Assuming run_full_analysis returns the dict or we read it back.
    # Based on T018-T020, they likely write to disk. Let's assume run_full_analysis handles the flow.
    # If run_full_analysis is a wrapper that does everything, we call it.
    
    # Fallback: if run_full_analysis doesn't return the dict, we re-read the file it creates.
    # For safety, we'll run the analysis and then load the file.
    try:
        # This function should trigger T018, T019, T020 logic
        # We assume it writes to data/results/statistical_analysis.json
        # Let's try to call it and see if it returns data, otherwise read.
        result = run_full_analysis() 
        if isinstance(result, dict):
            return result
    except Exception as e:
        log_warning(f"run_full_analysis returned non-dict or failed: {e}. Attempting to read file directly.")
    
    # Read the file directly as a fallback
    config = get_config()
    results_dir = Path(config['paths']['results'])
    file_path = results_dir / "statistical_analysis.json"
    if file_path.exists():
        with open(file_path, 'r') as f:
            return json.load(f)
    
    raise FileNotFoundError("Statistical analysis results file not found and could not be generated.")

def run_power_pipeline() -> Dict[str, Any]:
    """
    Run the power analysis pipeline if intermediate results are missing.
    Returns the power analysis results dictionary.
    """
    log_info("Running power analysis pipeline...")
    try:
        result = run_power_analysis()
        if isinstance(result, dict):
            return result
    except Exception as e:
        log_warning(f"run_power_analysis returned non-dict or failed: {e}. Attempting to read file directly.")

    config = get_config()
    results_dir = Path(config['paths']['results'])
    file_path = results_dir / "power_analysis.json"
    if file_path.exists():
        with open(file_path, 'r') as f:
            return json.load(f)

    raise FileNotFoundError("Power analysis results file not found and could not be generated.")

def compile_final_report(analysis_results: Dict[str, Any], power_results: Dict[str, Any]) -> Dict[str, Any]:
    """
    Combine analysis and power results into the final report structure.
    """
    report = {
        "metadata": {
            "generated_at": get_timestamp(),
            "task_id": "T022",
            "version": "1.0.0"
        },
        "statistical_tests": analysis_results.get("tests", []),
        "effect_sizes": analysis_results.get("effect_sizes", []),
        "corrections": analysis_results.get("corrections", {}),
        "power_analysis": power_results,
        "summary": {
            "total_comparisons": len(analysis_results.get("tests", [])),
            "significant_findings": 0,
            "power_achieved": power_results.get("power", 0.0) if isinstance(power_results, dict) else 0.0,
            "minimum_detectable_effect": power_results.get("mdes", 0.0) if isinstance(power_results, dict) else 0.0
        }
    }

    # Count significant findings
    for test in report["statistical_tests"]:
        p_val = test.get("p_value", 1.0)
        p_adj = test.get("p_value_corrected", p_val)
        if p_adj < 0.05:
            report["summary"]["significant_findings"] += 1

    return report

def main():
    """
    Main entry point for Task T022.
    """
    setup_logging()
    log_info("Starting Task T022: Generate Statistical Report")
    
    config = get_config()
    ensure_dirs(config['paths']['results'])
    
    # 1. Ensure analysis results exist
    analysis_results = load_statistical_analysis_results()
    if analysis_results is None:
        log_info("No existing statistical analysis found. Running pipeline.")
        analysis_results = run_analysis_pipeline()
    else:
        log_info("Loaded existing statistical analysis results.")

    # 2. Ensure power analysis results exist
    power_results = load_power_analysis_results()
    if power_results is None:
        log_info("No existing power analysis found. Running pipeline.")
        power_results = run_power_pipeline()
    else:
        log_info("Loaded existing power analysis results.")

    # 3. Compile final report
    final_report = compile_final_report(analysis_results, power_results)

    # 4. Write to disk
    output_path = Path(config['paths']['results']) / "statistical_report.json"
    with open(output_path, 'w') as f:
        json.dump(final_report, f, indent=2)
    
    log_info(f"Successfully generated {output_path}")
    return final_report

if __name__ == "__main__":
    main()