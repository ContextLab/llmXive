logger = logging.getLogger(__name__)

def load_statistical_analysis_results() -> Optional[Dict[str, Any]]:
    """
    Attempt to load intermediate statistical analysis results.
    If not found, returns None to trigger a re-run.
    """
    config = get_config()
    results_dir = Path(config['paths']['results'])
    file_path = results_dir / "statistical_analysis.json"
    
    if file_path.exists():
        try:
            with open(file_path, 'r') as f:
                return json.load(f)
        except Exception as e:
            log_warning(f"Failed to load existing statistical analysis results: {e}. Re-running analysis.")
            return None
    return None

