"""
Task T022: Generate statistical report with p-values, effect sizes, power, and MDES.

This script aggregates results from the statistical analysis (T018-T020) and power analysis (T021)
to produce the final `data/results/statistical_report.json`.

It relies on the existence of:
- data/processed/cleaned_dataset.csv (produced by T014a)
- data/results/statistical_analysis.json (intermediate output from analysis pipeline, assumed to be generated by T018-T020 logic)
- data/results/power_analysis.json (intermediate output from T021)

If intermediate files do not exist, this script attempts to run the necessary analysis steps
via `analysis.run_full_analysis` and `task_t021_power_analysis.run_power_analysis`.
"""
import os
import json
import logging
import numpy as np
import pandas as pd
from pathlib import Path
from typing import Dict, Any, Optional

# Import from project modules
from config import get_config, ensure_dirs, get_env_bool
from utils import setup_logging, log_info, log_warning, get_timestamp
from analysis import run_full_analysis, welch_t_test, calculate_cohen_d, bonferroni_correction
