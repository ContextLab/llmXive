"""
Integration test for nested permutation test (T018).

This test validates the nested permutation testing logic for interaction terms
as specified in US2. It ensures that:
1. Feature selection is performed only on the training fold.
2. The null distribution is generated by shuffling the raw interaction term.
3. The observed R² improvement is compared against the 95th percentile of the null.
4. The process correctly identifies significant interactions.

Dependencies:
- scikit-learn (for models, CV, metrics, permutation_test_score)
- pandas, numpy
- pytest
"""

import pytest
import numpy as np
import pandas as pd
from sklearn.ensemble import RandomForestRegressor
from sklearn.linear_model import LinearRegression
from sklearn.model_selection import KFold
from sklearn.metrics import r2_score
from sklearn.feature_selection import SelectKBest, f_regression
from typing import Tuple, List
import warnings

warnings.filterwarnings("ignore")

# We implement the logic here to ensure the test is self-contained and runnable
# without relying on a potentially incomplete `src/models/evaluate.py` yet,
# while adhering to the API surface for data handling if needed.
# However, the task asks for the TEST file. We will implement the logic
