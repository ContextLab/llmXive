def generate_synthetic_steel_data(n_samples: int = 200, random_state: int = 42) -> pd.DataFrame:
    """
    Generate a synthetic dataset mimicking steel yield strength data.
    This is used ONLY for testing the permutation logic, not for final research.
    In the real pipeline, this would be replaced by `load_data` from `src/data/loader`.
    """
    np.random.seed(random_state)
    
    # Main effects
    C = np.random.uniform(0.05, 0.30, n_samples)
    Mn = np.random.uniform(0.5, 1.5, n_samples)
    Cr = np.random.uniform(0.0, 2.0, n_samples)
    Ni = np.random.uniform(0.0, 2.0, n_samples)
    
    # Heat treatment
    temp = np.random.uniform(800, 1100, n_samples)
    cooling_rate = np.random.uniform(0.1, 10.0, n_samples)
    holding_time = np.random.uniform(1, 4, n_samples)
    
    # Create a specific interaction: Cooling Rate * Holding Time
    # This interaction will be significant
    interaction_term = cooling_rate * holding_time
    
    # Target: Yield Strength
    # Y = 200 + 500*C + 50*Mn + 20*Cr + 15*Ni + 0.1*Temp + 5*Interaction + noise
    noise = np.random.normal(0, 5, n_samples)
    y = (200 + 500*C + 50*Mn + 20*Cr + 15*Ni + 0.05*temp + 8*interaction_term + noise)
    
    df = pd.DataFrame({
        'C': C, 'Mn': Mn, 'Cr': Cr, 'Ni': Ni,
        'temp': temp, 'cooling_rate': cooling_rate, 'holding_time': holding_time,
        'interaction_rt_ht': interaction_term, # Pre-calculated interaction for testing
        'yield_strength': y
    })
    
    return df

def nested_permutation_test(
    X: pd.DataFrame, 
    y: pd.Series, 
    interaction_col: str, 
    n_permutations: int = 100, 
    random_state: int = 42
) -> Tuple[float, float, float]:
    """
    Perform a nested permutation test for a specific interaction term.
    
    Logic:
    1. Split data into train/test (using KFold).
    2. For each fold:
       a. Train a feature selector (SelectKBest) on TRAIN data only.
       b. Select top k features.
       c. Train a model (RF) on selected features.
       d. Calculate R² on TEST data.
       e. Calculate R² baseline (no interaction).
    3. Generate Null Distribution:
       a. Shuffle the INTERACTION column in the WHOLE dataset.
       b. Repeat steps 2a-2d with shuffled data.
    4. Compare observed R² improvement against null distribution.
    
    Returns:
      observed_r2: R2 of the real model
      null_95th: 95th percentile of null distribution
      p_value: Fraction of null values >= observed improvement
    """
    np.random.seed(random_state)
    kf = KFold(n_splits=3, shuffle=True, random_state=random_state)
    
    observed_scores = []
    null_scores = []
    
    # --- Observed Run ---
    for train_idx, test_idx in kf.split(X):
        X_train, X_test = X.iloc[train_idx], X.iloc[test_idx]
        y_train, y_test = y.iloc[train_idx], y.iloc[test_idx]
        
        # Feature selection on TRAIN ONLY
        selector = SelectKBest(f_regression, k=5)
        selector.fit(X_train, y_train)
        X_train_sel = selector.transform(X_train)
        X_test_sel = selector.transform(X_test)
        
        # Model
        model = RandomForestRegressor(n_estimators=50, max_depth=3, random_state=random_state)
        model.fit(X_train_sel, y_train)
        r2 = r2_score(y_test, model.predict(X_test_sel))
        observed_scores.append(r2)
    
    observed_mean_r2 = np.mean(observed_scores)
    
    # --- Null Distribution Run (Shuffle Interaction) ---
    for _ in range(n_permutations):
        X_null = X.copy()
        # Shuffle the specific interaction column
        X_null[interaction_col] = np.random.permutation(X_null[interaction_col].values)
        
        fold_scores = []
        for train_idx, test_idx in kf.split(X_null):
            X_train, X_test = X_null.iloc[train_idx], X_null.iloc[test_idx]
            y_train, y_test = y.iloc[train_idx], y.iloc[test_idx]
            
            # Feature selection on TRAIN ONLY (with shuffled data)
            selector = SelectKBest(f_regression, k=5)
            selector.fit(X_train, y_train)
            X_train_sel = selector.transform(X_train)
            X_test_sel = selector.transform(X_test)
            
            model = RandomForestRegressor(n_estimators=50, max_depth=3, random_state=random_state)
            model.fit(X_train_sel, y_train)
            r2 = r2_score(y_test, model.predict(X_test_sel))
            fold_scores.append(r2)
        
        null_scores.append(np.mean(fold_scores))
    
    null_95th = np.percentile(null_scores, 95)
    p_value = np.sum(np.array(null_scores) >= observed_mean_r2) / n_permutations
    
    return observed_mean_r2, null_95th, p_value

class TestNestedPermutation:
    """Integration tests for nested permutation testing logic."""

    def test_nested_permutation_detects_significant_interaction(self):
        """
        Test that the nested permutation test correctly identifies a known
        significant interaction (cooling_rate * holding_time) as significant.
        """
        df = generate_synthetic_steel_data(n_samples=200)
        X = df.drop(columns=['yield_strength'])
        y = df['yield_strength']
        
        # We expect the interaction 'interaction_rt_ht' to be significant
        obs_r2, null_95, p_val = nested_permutation_test(
            X, y, interaction_col='interaction_rt_ht', n_permutations=20
        )
        
        # Assert that the observed R2 is better than the 95th percentile of null
        # (This is a simplified check; in reality, we check p-value)
        assert p_val < 0.05, f"Interaction should be significant (p={p_val})"
        assert obs_r2 > null_95, "Observed R2 should exceed null 95th percentile"

    def test_nested_permutation_fails_on_noise(self):
        """
        Test that the test does not flag a random noise column as significant.
        """
        df = generate_synthetic_steel_data(n_samples=200)
        df['noise_col'] = np.random.normal(0, 100, 200)
        X = df.drop(columns=['yield_strength'])
        y = df['yield_strength']
        
        obs_r2, null_95, p_val = nested_permutation_test(
            X, y, interaction_col='noise_col', n_permutations=20
        )
        
        # The noise column should NOT be significant
        assert p_val >= 0.05, f"Noise column should not be significant (p={p_val})"

    def test_feature_selection_leakage_prevention(self):
        """
        Verify that feature selection is indeed restricted to the training fold.
        We do this by checking that the model can be trained without errors
        even when the interaction column is the ONLY significant feature
        and others are noise.
        """
        df = generate_synthetic_steel_data(n_samples=200)
        # Add many noise columns
        for i in range(10):
            df[f'noise_{i}'] = np.random.normal(0, 1, 200)
        
        X = df.drop(columns=['yield_strength'])
        y = df['yield_strength']
        
        # Run test
        obs_r2, null_95, p_val = nested_permutation_test(
            X, y, interaction_col='interaction_rt_ht', n_permutations=10
        )
        
        # Should still run and produce a result
        assert isinstance(obs_r2, float)
        assert isinstance(null_95, float)
        assert isinstance(p_val, float)
        assert 0 <= p_val <= 1

