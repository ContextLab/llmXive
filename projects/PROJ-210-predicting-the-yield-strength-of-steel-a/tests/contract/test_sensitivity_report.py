"""
Contract test for sensitivity report schema.
Validates that the sensitivity report generated by src/models/sensitivity.py
matches the expected schema defined in contracts/output.schema.yaml.
"""
import os
import json
import pytest
import yaml
from typing import Dict, Any, List

# Ensure we can import from the project root if run from code/
# Assuming the test is run with PYTHONPATH set to the project root or code/
# Based on the project structure provided: code/tests/contract/...
# We assume the schema file is at code/contracts/output.schema.yaml relative to project root
# or code/contracts/ if run from code/.
# Let's assume the project root is the parent of 'code'.
# However, standard practice in these prompts is to run from project root.
# We will try to locate the schema file relative to this test file.

def load_schema():
    """Load the output schema from the contracts directory."""
    # Try relative paths based on common project structures
    possible_paths = [
        os.path.join(os.path.dirname(__file__), "..", "..", "contracts", "output.schema.yaml"),
        os.path.join(os.path.dirname(__file__), "..", "..", "contracts", "output.schema.yml"),
        os.path.join(os.path.dirname(__file__), "..", "contracts", "output.schema.yaml"),
    ]
    
    schema_path = None
    for p in possible_paths:
        if os.path.exists(p):
            schema_path = p
            break
    
    if not schema_path:
        # Fallback: try to find it in the current working directory
        cwd_schema = os.path.join(os.getcwd(), "contracts", "output.schema.yaml")
        if os.path.exists(cwd_schema):
            schema_path = cwd_schema
        else:
            raise FileNotFoundError(
                "Could not find contracts/output.schema.yaml. "
                "Please ensure the schema file exists and the test is run from the project root."
            )
    
    with open(schema_path, 'r') as f:
        return yaml.safe_load(f)

def validate_structure(data: Dict[str, Any], schema: Dict[str, Any], path: str = "") -> List[str]:
    """
    Recursively validate the data structure against the JSON Schema-like definition.
    Returns a list of error messages.
    """
    errors = []
    
    if schema.get("type") == "object":
        if not isinstance(data, dict):
            errors.append(f"{path}: Expected object, got {type(data).__name__}")
            return errors
        
        properties = schema.get("properties", {})
        required = schema.get("required", [])
        
        # Check required fields
        for field in required:
            if field not in data:
                errors.append(f"{path}: Missing required field '{field}'")
        
        # Check defined properties
        for key, value in data.items():
            if key in properties:
                sub_errors = validate_structure(value, properties[key], f"{path}.{key}" if path else key)
                errors.extend(sub_errors)
            elif key not in schema.get("additionalProperties", True):
                # If additionalProperties is false or a schema that doesn't match
                if schema.get("additionalProperties") is False:
                    errors.append(f"{path}: Unexpected field '{key}'")
    
    elif schema.get("type") == "array":
        if not isinstance(data, list):
            errors.append(f"{path}: Expected array, got {type(data).__name__}")
            return errors
        
        items_schema = schema.get("items", {})
        for i, item in enumerate(data):
            sub_errors = validate_structure(item, items_schema, f"{path}[{i}]")
            errors.extend(sub_errors)
    
    elif schema.get("type") == "string":
        if not isinstance(data, str):
            errors.append(f"{path}: Expected string, got {type(data).__name__}")
    
    elif schema.get("type") == "number":
        if not isinstance(data, (int, float)):
            errors.append(f"{path}: Expected number, got {type(data).__name__}")
    
    elif schema.get("type") == "integer":
        if not isinstance(data, int):
            errors.append(f"{path}: Expected integer, got {type(data).__name__}")
    
    elif schema.get("type") == "boolean":
        if not isinstance(data, bool):
            errors.append(f"{path}: Expected boolean, got {type(data).__name__}")
    
    return errors

@pytest.fixture
def sensitivity_report_schema():
    return load_schema()

@pytest.mark.contract
def test_sensitivity_report_schema(sensitivity_report_schema):
    """
    Contract test: Validates that the sensitivity report structure matches the schema.
    This test assumes the report is generated by running the sensitivity analysis pipeline
    and saving it to data/results/sensitivity_report.json (or similar).
    
    Since T029-T032 (implementation of sensitivity.py) are not yet completed,
    this test currently checks the schema definition itself and validates against
    a manually constructed expected report structure that the implementation MUST produce.
    
    Once the implementation is done, this test should be updated to load the real file.
    """
    
    # Define the expected structure based on T029-T032 requirements:
    # - Threshold Sweep Results
    # - Stability Metrics (Jaccard, Spearman, Kuncheva)
    # - Justification
    
    expected_report_structure = {
        "thresholds": [0.01, 0.05, 0.10],
        "results": {
            "threshold_0.01": {
                "feature_count": int,
                "selected_features": list,
                "stability_metrics": {
                    "jaccard_index": float,
                    "spearman_correlation": float,
                    "kuncheva_index": float
                },
                "is_stable": bool
            },
            "threshold_0.05": {
                "feature_count": int,
                "selected_features": list,
                "stability_metrics": {
                    "jaccard_index": float,
                    "spearman_correlation": float,
                    "kuncheva_index": float
                },
                "is_stable": bool
            },
            "threshold_0.10": {
                "feature_count": int,
                "selected_features": list,
                "stability_metrics": {
                    "jaccard_index": float,
                    "spearman_correlation": float,
                    "kuncheva_index": float
                },
                "is_stable": bool
            }
        },
        "justification": {
            "threshold_source": str,
            "references": list
        }
    }
    
    # Validate the expected structure against the schema
    errors = validate_structure(expected_report_structure, sensitivity_report_schema)
    
    # If the schema validation fails, it means the schema definition in the file
    # does not match the expected output of the sensitivity analysis.
    # This is a contract failure.
    assert not errors, f"Sensitivity report schema validation failed: {errors}"
    
    # Also verify the schema has the required top-level keys
    schema_props = sensitivity_report_schema.get("properties", {})
    assert "thresholds" in schema_props, "Schema missing 'thresholds' property"
    assert "results" in schema_props, "Schema missing 'results' property"
    assert "justification" in schema_props, "Schema missing 'justification' property"

@pytest.mark.contract
def test_sensitivity_report_file_exists_and_valid(sensitivity_report_schema):
    """
    Contract test: Checks if the sensitivity report file exists and validates its content.
    This test will fail if the implementation tasks (T029-T032) haven't been run yet,
    which is expected behavior for a contract test before implementation is complete.
    """
    report_paths = [
        os.path.join(os.path.dirname(__file__), "..", "..", "data", "results", "sensitivity_report.json"),
        os.path.join(os.getcwd(), "data", "results", "sensitivity_report.json"),
        os.path.join(os.getcwd(), "data", "results", "sensitivity_report.md") # Check MD as fallback if JSON not generated
    ]
    
    report_path = None
    for p in report_paths:
        if os.path.exists(p):
            report_path = p
            break
    
    if not report_path:
        # If the file doesn't exist, we cannot validate it.
        # In a real CI/CD pipeline, this might be a failure if the report is expected.
        # For now, we skip or mark as pending if the implementation isn't done.
        pytest.skip("Sensitivity report file not found. Implementation tasks (T029-T032) may not be complete.")
    
    # If it's a JSON file
    if report_path.endswith('.json'):
        with open(report_path, 'r') as f:
            report_data = json.load(f)
        
        errors = validate_structure(report_data, sensitivity_report_schema)
        assert not errors, f"Report file validation failed: {errors}"
    
    elif report_path.endswith('.md'):
        # If it's a markdown file, we do a basic structural check
        # Since the schema is for JSON, we can't fully validate MD against it.
        # We check for the presence of required headers.
        with open(report_path, 'r') as f:
            content = f.read()
        
        required_headers = [
            "Threshold Sweep Results",
            "Stability Metrics",
            "Justification"
        ]
        
        missing_headers = [h for h in required_headers if h not in content]
        assert not missing_headers, f"Markdown report missing required headers: {missing_headers}"
