"""
Visualization module for GNN explainability outputs.

This module handles the generation of visualizations for subgraph motifs
identified by GNNExplainer. It includes mandatory disclaimers on all outputs
to ensure users understand that these are associational patterns, not causal drivers.
"""

import os
import json
import logging
from typing import List, Dict, Any, Optional, Tuple
import matplotlib
matplotlib.use('Agg')  # Non-interactive backend for server environments
import matplotlib.pyplot as plt
import networkx as nx
from rdkit import Chem
from rdkit.Chem import Draw, rdDepictor
from rdkit.Chem.Draw import SimilarityMaps

# Ensure RDKit depicts coordinates
rdDepictor.SetPreferCoordGen(True)

from src.utils.logging import get_logger, log_message

# Mandatory disclaimer text as per Plan.md Phase 3 Critical Methodology Update
MANDATORY_DISCLAIMER = (
    "These subgraphs represent associational patterns and may reflect dataset bias; "
    "they are not proven causal drivers."
)

logger = get_logger(__name__)

def add_disclaimer_to_artifact(artifact_path: str, artifact_type: str) -> None:
    """
    Adds the mandatory disclaimer to a generated artifact.
    
    For JSON artifacts, adds a 'disclaimer' field.
    For image artifacts, writes a sidecar .txt file or embeds text if image format supports it.
    Here we implement a sidecar text file for images and a field for JSON.
    """
    if artifact_type == "json":
        try:
            with open(artifact_path, 'r', encoding='utf-8') as f:
                data = json.load(f)
            data['disclaimer'] = MANDATORY_DISCLAIMER
            with open(artifact_path, 'w', encoding='utf-8') as f:
                json.dump(data, f, indent=2)
            logger.info(f"Added disclaimer to JSON artifact: {artifact_path}")
        except Exception as e:
            logger.error(f"Failed to add disclaimer to JSON {artifact_path}: {e}")
            raise

    elif artifact_type == "image":
        # Create a sidecar text file with the disclaimer
        sidecar_path = artifact_path + ".disclaimer.txt"
        with open(sidecar_path, 'w', encoding='utf-8') as f:
            f.write(f"Artifact: {os.path.basename(artifact_path)}\n")
            f.write(f"Generated by: src/analysis/viz.py\n")
            f.write(f"Warning: {MANDATORY_DISCLAIMER}\n")
        logger.info(f"Created disclaimer sidecar: {sidecar_path}")
    else:
        logger.warning(f"Unknown artifact type {artifact_type}, skipping disclaimer injection.")

def visualize_motif_with_disclaimer(
    smiles: str,
    motif_nodes: List[int],
    motif_edges: List[Tuple[int, int]],
    output_path: str,
    title: Optional[str] = None
) -> None:
    """
    Visualizes a molecular motif (subgraph) on the RDKit molecule.
    
    Args:
        smiles: The SMILES string of the molecule.
        motif_nodes: List of atom indices included in the motif.
        motif_edges: List of (atom_idx, atom_idx) tuples for bonds in the motif.
        output_path: Path to save the visualization (PNG).
        title: Optional title for the plot. If None, uses a default with disclaimer hint.
    """
    mol = Chem.MolFromSmiles(smiles)
    if mol is None:
        logger.error(f"Could not parse SMILES for visualization: {smiles}")
        return

    # Generate 2D coordinates if not present
    if mol.GetNumConformers() == 0:
        Chem.Compute2DCoords(mol)

    # Create a highlight map for the motif atoms
    atom_indices = tuple(motif_nodes)
    bond_indices = []
    
    # Map bond indices if necessary, but RDKit Draw uses atom pairs for highlighting bonds
    # We need to find the bond object indices corresponding to the atom pairs
    for i, j in motif_edges:
        bond = mol.GetBondBetweenAtoms(i, j)
        if bond:
            bond_indices.append(bond.GetIdx())

    # Create the plot
    plt.figure(figsize=(10, 6))
    img = Draw.MolToImage(
        mol,
        size=(600, 400),
        highlightAtoms=atom_indices,
        highlightBonds=bond_indices,
        kekulize=False,
        fitImage=False
    )
    
    # Prepare title with disclaimer
    safe_title = title if title else "Motif Visualization"
    full_title = f"{safe_title}\n[WARNING: Associational Pattern - Not Causal]"
    
    plt.imshow(img)
    plt.title(full_title, fontsize=12, wrap=True)
    plt.axis('off')
    
    plt.tight_layout()
    plt.savefig(output_path, dpi=150, bbox_inches='tight')
    plt.close()
    
    logger.info(f"Saved motif visualization to {output_path}")
    
    # Add mandatory disclaimer sidecar
    add_disclaimer_to_artifact(output_path, "image")

def generate_top_motifs_report_with_disclaimer(
    motifs_data: List[Dict[str, Any]],
    output_path: str
) -> None:
    """
    Generates a JSON report of top motifs and ensures the disclaimer is present.
    
    Args:
        motifs_data: List of dictionaries containing motif information (SMILES, nodes, edges, scores).
        output_path: Path to save the JSON report.
    """
    # Ensure the disclaimer is in every motif entry as well
    for motif in motifs_data:
        if 'disclaimer' not in motif:
            motif['disclaimer'] = MANDATORY_DISCLAIMER
    
    with open(output_path, 'w', encoding='utf-8') as f:
        json.dump(motifs_data, f, indent=2)
    
    logger.info(f"Saved motifs report to {output_path}")
    
    # Also add the top-level disclaimer to the file content itself via sidecar
    add_disclaimer_to_artifact(output_path, "json")

def generate_motif_visualizations_from_explainer_output(
    explainer_output_path: str,
    output_dir: str
) -> List[str]:
    """
    Reads GNNExplainer output and generates visualizations for top motifs.
    
    Args:
        explainer_output_path: Path to the JSON file containing GNNExplainer results.
        output_dir: Directory to save generated images.
        
    Returns:
        List of paths to generated image files.
    """
    if not os.path.exists(explainer_output_path):
        logger.error(f"Explainer output file not found: {explainer_output_path}")
        return []

    os.makedirs(output_dir, exist_ok=True)

    with open(explainer_output_path, 'r', encoding='utf-8') as f:
        data = json.load(f)

    motifs = data.get('top_motifs', [])
    generated_paths = []

    for i, motif in enumerate(motifs):
        smiles = motif.get('smiles')
        nodes = motif.get('nodes', [])
        edges = motif.get('edges', [])
        score = motif.get('score', 0.0)
        
        if not smiles:
            continue

        filename = f"motif_{i+1}_score_{score:.4f}.png"
        output_path = os.path.join(output_dir, filename)

        try:
            visualize_motif_with_disclaimer(
                smiles=smiles,
                motif_nodes=nodes,
                motif_edges=edges,
                output_path=output_path,
                title=f"Motif {i+1} (Score: {score:.4f})"
            )
            generated_paths.append(output_path)
        except Exception as e:
            logger.error(f"Failed to visualize motif {i+1}: {e}")

    return generated_paths

