"""
T024: Evaluate Model Performance
Calculates ROC-AUC, accuracy, and F1-score per fold and mean.
Outputs results to data/processed/performance_report.json.

Prerequisites:
- code/04_train_model.py must have been run to generate data/processed/model.pkl
- data/processed/graph_metrics.csv must exist with the features and labels used for training
"""
import os
import sys
import json
import time
from pathlib import Path
import numpy as np
import pandas as pd
import joblib
from sklearn.metrics import roc_auc_score, accuracy_score, f1_score, confusion_matrix
from sklearn.model_selection import cross_val_predict
from sklearn.ensemble import RandomForestClassifier
from sklearn.pipeline import Pipeline
from sklearn.preprocessing import StandardScaler

# Import project utilities
from utils.logger import get_logger
from utils.io import load_csv, save_json, ensure_dir
from config import get_config

def get_logger_wrapper():
    """Initialize logger for this script."""
    return get_logger("evaluate_model")

def calculate_metrics(y_true, y_pred, y_prob):
    """
    Calculate standard classification metrics.
    
    Args:
        y_true: Array of true labels
        y_pred: Array of predicted labels
        y_prob: Array of predicted probabilities for the positive class
        
    Returns:
        dict: Dictionary containing ROC-AUC, accuracy, and F1-score
    """
    try:
        # Ensure we have at least two classes in y_true for ROC-AUC
        unique_classes = np.unique(y_true)
        if len(unique_classes) < 2:
            logger = get_logger("evaluate_model")
            logger.warning("Only one class present in y_true. ROC-AUC cannot be calculated.")
            return {
                "roc_auc": None,
                "accuracy": float(accuracy_score(y_true, y_pred)),
                "f1_score": float(f1_score(y_true, y_pred, zero_division=0)),
                "note": "Single class in ground truth"
            }

        roc_auc = roc_auc_score(y_true, y_prob)
        acc = accuracy_score(y_true, y_pred)
        f1 = f1_score(y_true, y_pred, zero_division=0)
        
        return {
            "roc_auc": float(roc_auc),
            "accuracy": float(acc),
            "f1_score": float(f1)
        }
    except Exception as e:
        logger = get_logger("evaluate_model")
        logger.error(f"Error calculating metrics: {str(e)}")
        raise

def evaluate_model(model_path, data_path, output_path):
    """
    Load the trained model and data, perform evaluation, and save results.
    
    The model.pkl generated by 04_train_model.py contains the final fitted pipeline.
    We need to re-run cross-validation predictions to get per-fold metrics,
    or if the model was trained with cross-validation stored, we extract those.
    
    Since 04_train_model.py uses nested CV, we assume the 'model.pkl' contains
    the final model trained on the full dataset. To get per-fold metrics for
    the report, we need to re-run a single CV loop on the data using the 
    selected hyperparameters, or if the training script saved the CV results,
    we load those.
    
    For this implementation, we will:
    1. Load the graph metrics data.
    2. Load the best parameters from the model (if stored) or re-train with a simple CV 
       to generate predictions for the report.
       
    Note: To strictly follow the task "Calculate ... per fold", we need predictions 
    for each fold. If the training script didn't save the CV predictions, we perform 
    a single 5-fold CV on the loaded data using the Random Forest with the best 
    parameters found during training (or defaults if not found) to generate the report.
    """
    logger = get_logger("evaluate_model")
    
    # Paths
    model_path = Path(model_path)
    data_path = Path(data_path)
    output_path = Path(output_path)
    
    ensure_dir(output_path.parent)
    
    # 1. Load Data
    if not data_path.exists():
        logger.error(f"Graph metrics file not found: {data_path}")
        raise FileNotFoundError(f"Graph metrics file not found: {data_path}")
        
    df = load_csv(data_path)
    
    # Identify feature columns and target column
    # Assuming the target column is 'decline_label' or similar, created in T023
    # We look for the label column. If not explicitly named, we assume the last column 
    # or a specific known name. Let's assume 'decline_label' based on T023 description.
    target_col = 'decline_label'
    if target_col not in df.columns:
        # Fallback: try to find a column with 'label' or 'target'
        candidates = [c for c in df.columns if 'label' in c.lower() or 'target' in c.lower()]
        if candidates:
            target_col = candidates[0]
            logger.warning(f"Target column '{target_col}' not found, using '{target_col}' instead.")
        else:
            logger.error(f"Could not find target column. Available columns: {list(df.columns)}")
            raise ValueError("Target column not found in data.")
    
    feature_cols = [c for c in df.columns if c != target_col]
    if not feature_cols:
        logger.error("No feature columns found.")
        raise ValueError("No feature columns found.")
        
    X = df[feature_cols].values
    y = df[target_col].values
    
    logger.info(f"Loaded {len(X)} subjects with {len(feature_cols)} features.")
    
    # 2. Load Model or Parameters
    # The model.pkl from T023 should contain the final pipeline or the best estimator.
    # We need to know the hyperparameters to re-run CV for the report if predictions aren't saved.
    best_params = {'n_estimators': 100, 'max_depth': None}
    
    if model_path.exists():
        try:
            loaded_model = joblib.load(model_path)
            # Check if it's a pipeline or just an estimator
            if hasattr(loaded_model, 'get_params'):
                params = loaded_model.get_params()
                if 'n_estimators' in params:
                    best_params['n_estimators'] = params['n_estimators']
                if 'max_depth' in params:
                    best_params['max_depth'] = params['max_depth']
            logger.info(f"Loaded model from {model_path}. Params: {best_params}")
        except Exception as e:
            logger.warning(f"Could not load model params from {model_path}: {e}. Using defaults.")
    else:
        logger.warning(f"Model file {model_path} not found. Using default parameters for evaluation.")
    
    # 3. Perform Cross-Validation to get per-fold metrics
    # We use the same 5-fold strategy as the training to ensure consistency
    from sklearn.model_selection import KFold
    
    kf = KFold(n_splits=5, shuffle=True, random_state=42)
    
    fold_metrics = []
    all_y_true = []
    all_y_pred = []
    all_y_prob = []
    
    logger.info("Performing 5-fold cross-validation to generate per-fold metrics...")
    
    for fold_idx, (train_idx, test_idx) in enumerate(kf.split(X)):
        X_train, X_test = X[train_idx], X[test_idx]
        y_train, y_test = y[train_idx], y[test_idx]
        
        # Train a model for this fold
        # We use a simple pipeline: Scaler + RandomForest
        model = Pipeline([
            ('scaler', StandardScaler()),
            ('clf', RandomForestClassifier(
                n_estimators=best_params['n_estimators'],
                max_depth=best_params['max_depth'],
                random_state=42,
                n_jobs=1 # Avoid nested parallelism issues
            ))
        ])
        
        model.fit(X_train, y_train)
        
        # Predict
        y_pred = model.predict(X_test)
        y_prob = model.predict_proba(X_test)[:, 1]
        
        # Calculate metrics
        metrics = calculate_metrics(y_test, y_pred, y_prob)
        metrics['fold'] = fold_idx + 1
        fold_metrics.append(metrics)
        
        all_y_true.extend(y_test)
        all_y_pred.extend(y_pred)
        all_y_prob.extend(y_prob)
        
        logger.info(f"Fold {fold_idx + 1}: ROC-AUC={metrics.get('roc_auc', 'N/A'):.4f}, "
                    f"Acc={metrics.get('accuracy', 'N/A'):.4f}, F1={metrics.get('f1_score', 'N/A'):.4f}")
    
    # 4. Calculate Mean Metrics
    mean_roc_auc = np.mean([m['roc_auc'] for m in fold_metrics if m['roc_auc'] is not None])
    mean_acc = np.mean([m['accuracy'] for m in fold_metrics])
    mean_f1 = np.mean([m['f1_score'] for m in fold_metrics])
    
    # 5. Compile Report
    report = {
        "description": "Model Performance Evaluation (5-Fold Cross-Validation)",
        "model_params_used": best_params,
        "num_folds": 5,
        "total_samples": len(X),
        "fold_metrics": fold_metrics,
        "mean_metrics": {
            "roc_auc": float(mean_roc_auc),
            "accuracy": float(mean_acc),
            "f1_score": float(mean_f1)
        },
        "timestamp": time.strftime("%Y-%m-%d %H:%M:%S")
    }
    
    # 6. Save Output
    save_json(report, output_path)
    logger.info(f"Performance report saved to {output_path}")
    
    return report

def main():
    """Main entry point for T024."""
    logger = get_logger_wrapper()
    logger.info("Starting T024: Evaluate Model")
    
