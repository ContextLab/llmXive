"""Evaluate a trained RandomForest model using 5‑fold stratified cross‑validation.

This script loads the feature matrix and binary decline label generated by the
earlier pipeline steps, loads the trained ``RandomForestClassifier`` model
(saved as ``data/processed/model.pkl``), and re‑evaluates it on the same data
using an outer 5‑fold cross‑validation loop. For each fold the script records
ROC‑AUC, accuracy and F1‑score, then writes a JSON performance report to
``data/processed/performance_report.json``.

The implementation follows the contract expected by the rest of the project:
- ``get_logger_wrapper`` returns a tolerant logger (see ``utils/logger.py``).
- ``load_features_and_labels`` returns ``X`` (pandas.DataFrame) and ``y``
  (pandas.Series) with the binary decline label.
- ``load_trained_model`` loads the pickled model using ``joblib``.
- ``calculate_metrics`` computes the three metrics from true labels,
  predicted labels and predicted probabilities.
- ``evaluate_model`` orchestrates the CV loop and writes the JSON report.
"""

from __future__ import annotations

import json
import sys
from pathlib import Path
from typing import Any, Dict, List, Tuple

import joblib
import numpy as np
import pandas as pd
from sklearn.ensemble import RandomForestClassifier
from sklearn.metrics import accuracy_score, f1_score, roc_auc_score
from sklearn.model_selection import StratifiedKFold

# Project utilities
from utils.io import ensure_dir, load_csv, save_json
from utils.logger import get_logger


# --------------------------------------------------------------------------- #
# Helper: tolerant logger wrapper
# --------------------------------------------------------------------------- #
def get_logger_wrapper(name: str = "05_evaluate_model"):
    """
    Return a project‑wide logger. The underlying implementation in
    ``utils/logger.py`` tolerates any call signature, so we simply forward the
    name.
    """
    return get_logger(name)


# --------------------------------------------------------------------------- #
# Data loading
# --------------------------------------------------------------------------- #
def load_features_and_labels(
    csv_path: Path = Path("data/processed/graph_metrics.csv")
) -> Tuple[pd.DataFrame, pd.Series]:
    """
    Load the feature matrix and binary decline label.

    The CSV is expected to contain a ``subject_id`` column, a label column
    named ``decline`` (0/1), and one column per graph metric feature.
    All columns except ``subject_id`` and ``decline`` are treated as features.
    """
    logger = get_logger_wrapper()
    logger.info("Loading features and labels from %s", csv_path)

    if not csv_path.is_file():
        logger.error("Feature file not found: %s", csv_path)
        raise FileNotFoundError(csv_path)

    df = load_csv(csv_path)  # returns a pandas.DataFrame

    # Verify required label column exists
    required_label = "decline"
    if required_label not in df.columns:
        logger.error("Required label column '%s' missing in %s", required_label, csv_path)
        raise KeyError(f"Missing label column '{required_label}'")

    # Separate features and label
    feature_cols = [c for c in df.columns if c not in {"subject_id", required_label}]
    X = df[feature_cols].copy()
    y = df[required_label].copy()

    logger.info(
        "Loaded %d subjects with %d feature columns.",
        X.shape[0],
        X.shape[1],
    )
    return X, y


# --------------------------------------------------------------------------- #
# Model loading
# --------------------------------------------------------------------------- #
def load_trained_model(
    model_path: Path = Path("data/processed/model.pkl")
) -> RandomForestClassifier:
    """
    Load the pickled RandomForest model produced by ``04_train_model.py``.
    """
    logger = get_logger_wrapper()
    logger.info("Loading trained model from %s", model_path)

    if not model_path.is_file():
        logger.error("Model file not found: %s", model_path)
        raise FileNotFoundError(model_path)

    model = joblib.load(model_path)
    if not isinstance(model, RandomForestClassifier):
        logger.warning(
            "Loaded model is not a RandomForestClassifier (type=%s). "
            "Proceeding anyway; metric calculation may still work.",
            type(model),
        )
    return model


# --------------------------------------------------------------------------- #
# Metric calculation
# --------------------------------------------------------------------------- #
def calculate_metrics(
    y_true: np.ndarray, y_pred: np.ndarray, y_proba: np.ndarray
) -> Dict[str, float]:
    """
    Compute ROC‑AUC, accuracy and F1‑score.

    Parameters
    ----------
    y_true : np.ndarray
        Ground‑truth binary labels.
    y_pred : np.ndarray
        Predicted class labels.
    y_proba : np.ndarray
        Predicted probability for the positive class (shape = (n_samples,)).

    Returns
    -------
    dict
        Mapping with keys ``roc_auc``, ``accuracy``, ``f1``.
    """
    # Guard against pathological cases (e.g. single‑class folds)
    if len(np.unique(y_true)) == 1:
        # ROC‑AUC is undefined when only one class is present; assign NaN
        roc_auc = float("nan")
    else:
        roc_auc = roc_auc_score(y_true, y_proba)

    accuracy = accuracy_score(y_true, y_pred)
    f1 = f1_score(y_true, y_pred, zero_division=0)

    return {"roc_auc": float(roc_auc), "accuracy": float(accuracy), "f1": float(f1)}


# --------------------------------------------------------------------------- #
# Evaluation driver
# --------------------------------------------------------------------------- #
def evaluate_model(
    n_splits: int = 5,
    random_state: int = 42,
    output_path: Path = Path("data/processed/performance_report.json"),
) -> None:
    """
    Perform stratified 5‑fold CV, compute per‑fold metrics, and write a JSON
    performance report.

    The JSON structure is::

        {
            "folds": [
                {"fold": 1, "roc_auc": ..., "accuracy": ..., "f1": ...},
                ...
            ],
            "mean": {"roc_auc": ..., "accuracy": ..., "f1": ...}
        }
    """
    logger = get_logger_wrapper()
    logger.info("Starting model evaluation (outer CV).")

    # Load data and model
    X, y = load_features_and_labels()
    base_model = load_trained_model()

    # Ensure output directory exists
    ensure_dir(output_path.parent)

    skf = StratifiedKFold(
        n_splits=n_splits, shuffle=True, random_state=random_state
    )

    fold_results: List[Dict[str, Any]] = []
    for fold_idx, (train_idx, test_idx) in enumerate(skf.split(X, y), start=1):
        logger.info("Evaluating fold %d/%d", fold_idx, n_splits)

        X_train, X_test = X.iloc[train_idx], X.iloc[test_idx]
        y_train, y_test = y.iloc[train_idx], y.iloc[test_idx]

        # Clone the trained model's hyper‑parameters to ensure identical settings
        model = RandomForestClassifier(**base_model.get_params())
        model.fit(X_train, y_train)

        # Predictions
        y_pred = model.predict(X_test)
        # Probability of the positive class (class 1)
        if hasattr(model, "predict_proba"):
            y_proba = model.predict_proba(X_test)[:, 1]
        else:
            # Fall back to decision function if probability unavailable
            y_proba = model.decision_function(X_test)
            # Scale to [0,1] via sigmoid for ROC‑AUC compatibility
            y_proba = 1 / (1 + np.exp(-y_proba))

        metrics = calculate_metrics(y_test.values, y_pred, y_proba)
        metrics["fold"] = fold_idx
        fold_results.append(metrics)

        logger.info(
            "Fold %d metrics – ROC‑AUC: %.4f, Acc: %.4f, F1: %.4f",
            fold_idx,
            metrics["roc_auc"],
            metrics["accuracy"],
            metrics["f1"],
        )

    # Compute means (ignore NaN values for ROC‑AUC)
    mean_metrics = {
        "roc_auc": float(np.nanmean([r["roc_auc"] for r in fold_results])),
        "accuracy": float(np.mean([r["accuracy"] for r in fold_results])),
        "f1": float(np.mean([r["f1"] for r in fold_results])),
    }

    report = {"folds": fold_results, "mean": mean_metrics}

    # Write JSON report
    with output_path.open("w", encoding="utf-8") as f:
        json.dump(report, f, indent=2, ensure_ascii=False)

    logger.info("Performance report written to %s", output_path)


# --------------------------------------------------------------------------- #
# CLI entry point
# --------------------------------------------------------------------------- #
def main() -> int:
    """
    CLI entry point used by the quick‑start run‑book.

    Returns
    -------
    int
        Exit code (0 on success, non‑zero on failure).
    """
    try:
        evaluate_model()
        return 0
    except Exception as exc:  # pragma: no cover – top‑level safety net
        logger = get_logger_wrapper()
        logger.error("Evaluation failed: %s", exc, exc_info=True)
        return 1


if __name__ == "__main__":
    sys.exit(main())