"""Compute graph‑theoretic metrics for each subject's connectivity matrix.

This script:
1. Loads the list of eligible subjects (generated by 01_download_and_filter.py).
2. For each subject, loads a pre‑computed 90×90 connectivity matrix
   (expected under ``data/processed/connectivity_matrices/<subj>_conn.npy``).
3. Calculates:
   * Mean node degree
   * Global efficiency
   * Mean clustering coefficient
   * Mean shortest‑path length (averaged over the largest connected component)
4. Writes a CSV file ``data/processed/graph_metrics.csv`` with one row per
   subject.

The implementation is deliberately memory‑conservative: each matrix is
loaded, processed, and discarded before moving to the next subject, keeping
peak RAM well below the 7 GB limit.
"""

from __future__ import annotations

import sys
import traceback
from pathlib import Path
from typing import List, Dict, Any

import numpy as np
import networkx as nx

from utils.io import ensure_dir, load_csv, save_csv
from utils.logger import get_logger


# --------------------------------------------------------------------------- #
# Helper functions
# --------------------------------------------------------------------------- #

def load_config() -> dict[str, Any]:
    """Return a minimal configuration dictionary.

    In a full project this would read a YAML/JSON config file; for the
    purposes of this task we hard‑code the relevant paths.
    """
    base_dir = Path(__file__).parents[1]  # project root
    return {
        "processed_dir": base_dir / "data" / "processed",
        "connectivity_dir": base_dir / "data" / "processed" / "connectivity_matrices",
        "eligible_subjects_path": base_dir / "data" / "processed" / "eligible_subjects.csv",
        "output_path": base_dir / "data" / "processed" / "graph_metrics.csv",
    }


def get_data_directories(cfg: dict[str, Any]) -> dict[str, Path]:
    """Convenient wrapper returning Path objects for directories."""
    return {
        "processed": Path(cfg["processed_dir"]),
        "connectivity": Path(cfg["connectivity_dir"]),
    }


def load_subject_list(cfg: dict[str, Any]) -> List[str]:
    """Read eligible_subjects.csv and return a list of subject identifiers."""
    csv_path = Path(cfg["eligible_subjects_path"])
    if not csv_path.is_file():
        get_logger().warning(
            "Eligible subjects file not found; proceeding with empty list."
        )
        return []
    rows = load_csv(csv_path)
    # The CSV is expected to have a column named 'subject_id' (or similar)
    # Fall back to the first column if the expected name is absent.
    if not rows:
        return []
    first_row = rows[0]
    subject_key = "subject_id" if "subject_id" in first_row else list(first_row.keys())[0]
    return [row[subject_key] for row in rows]


def load_connectivity_matrix(subject_id: str, cfg: dict[str, Any]) -> np.ndarray | None:
    """Load a subject's connectivity matrix; return None if missing."""
    matrix_path = Path(cfg["connectivity_dir"]) / f"{subject_id}_conn.npy"
    if not matrix_path.is_file():
        get_logger().warning(
            f"Connectivity matrix not found for subject {subject_id}: {matrix_path}"
        )
        return None
    try:
        return np.load(matrix_path)
    except Exception as exc:
        get_logger().error(
            f"Failed to load matrix for {subject_id}: {exc}"
        )
        return None


def compute_subject_metrics(matrix: np.ndarray) -> Dict[str, float]:
    """Calculate the required graph metrics from a numpy adjacency matrix."""
    # Ensure the matrix is symmetric and non‑negative (basic sanity check)
    if matrix.shape[0] != matrix.shape[1]:
        raise ValueError("Adjacency matrix must be square")

    # Build an undirected NetworkX graph
    G = nx.from_numpy_array(matrix)

    # ---- Degree ---------------------------------------------------------- #
    degrees = dict(G.degree())
    mean_degree = float(np.mean(list(degrees.values())))

    # ---- Global Efficiency ------------------------------------------------ #
    # NetworkX provides global_efficiency in the efficiency_measures module
    try:
        from networkx.algorithms import efficiency_measures as nx_eff
        global_eff = nx_eff.global_efficiency(G)
    except Exception:
        # Fallback: compute manually (average of inverse shortest path lengths)
        lengths = dict(nx.all_pairs_shortest_path_length(G))
        inv_lengths = [
            1 / d if d > 0 else 0
            for src, targets in lengths.items()
            for dst, d in targets.items()
            if src != dst
        ]
        global_eff = float(np.mean(inv_lengths)) if inv_lengths else 0.0

    # ---- Clustering Coefficient ------------------------------------------- #
    clustering = nx.clustering(G)
    mean_clustering = float(np.mean(list(clustering.values())))

    # ---- Shortest Path Length --------------------------------------------- #
    if nx.is_connected(G):
        avg_path_len = nx.average_shortest_path_length(G)
    else:
        # Compute average over the largest connected component
        largest_cc = max(nx.connected_components(G), key=len)
        subgraph = G.subgraph(largest_cc)
        avg_path_len = nx.average_shortest_path_length(subgraph)
    avg_path_len = float(avg_path_len)

    return {
        "mean_degree": mean_degree,
        "global_efficiency": global_eff,
        "mean_clustering": mean_clustering,
        "mean_path_length": avg_path_len,
    }


def write_outputs(
    results: List[Dict[str, Any]], cfg: dict[str, Any]
) -> None:
    """Write the aggregated metrics to CSV."""
    output_path = Path(cfg["output_path"])
    ensure_dir(output_path.parent)
    # Ensure deterministic column order
    fieldnames = ["subject_id", "mean_degree", "global_efficiency", "mean_clustering", "mean_path_length"]
    rows = [
        {
            "subject_id": r["subject_id"],
            "mean_degree": r["mean_degree"],
            "global_efficiency": r["global_efficiency"],
            "mean_clustering": r["mean_clustering"],
            "mean_path_length": r["mean_path_length"],
        }
        for r in results
    ]
    save_csv(rows, output_path, header=fieldnames)
    get_logger().info(f"Wrote graph metrics for {len(rows)} subjects to {output_path}")


# --------------------------------------------------------------------------- #
# Main entry point
# --------------------------------------------------------------------------- #

def main() -> None:
    """Orchestrate the graph‑metric computation pipeline."""
    logger = get_logger("graph_metrics")
    try:
        cfg = load_config()
        dirs = get_data_directories(cfg)

        # Ensure required directories exist (they may be empty at this stage)
        ensure_dir(dirs["processed"])
        ensure_dir(dirs["connectivity"])

        subject_ids = load_subject_list(cfg)
        if not subject_ids:
            logger.warning("No eligible subjects found; producing empty metrics file.")
            write_outputs([], cfg)
            return

        results: List[Dict[str, Any]] = []
        for sid in subject_ids:
            matrix = load_connectivity_matrix(sid, cfg)
            if matrix is None:
                continue  # Skip subjects without a matrix
            try:
                metrics = compute_subject_metrics(matrix)
                metrics["subject_id"] = sid
                results.append(metrics)
            except Exception as exc:
                logger.error(f"Failed to compute metrics for {sid}: {exc}")
                logger.debug(traceback.format_exc())

        write_outputs(results, cfg)

    except Exception as exc:
        logger.error(f"Unhandled exception in graph‑metric script: {exc}")
        logger.debug(traceback.format_exc())
        sys.exit(1)


if __name__ == "__main__":
    main()