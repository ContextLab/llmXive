"""
Compute graph theoretical metrics from preprocessed connectivity matrices.

This script loads connectivity matrices generated by 02_preprocess_and_parcellate.py,
calculates global and nodal graph metrics (degree, efficiency, clustering, path length),
and outputs them to data/processed/graph_metrics.csv.

Implementation uses joblib for parallel processing to meet the <30min runtime target.
"""
import os
import sys
import time
import logging
import json
import psutil
from pathlib import Path
from typing import Dict, List, Any, Optional
import numpy as np
import pandas as pd
import networkx as nx
from joblib import Parallel, delayed

# Add project root to path for imports
project_root = Path(__file__).parent.parent
sys.path.insert(0, str(project_root))

from utils.io import load_csv, save_dataframe
from utils.logger import get_logger
from utils.graph import create_graph_from_adjacency, calculate_global_efficiency, calculate_clustering_coefficient, calculate_degree_centrality, calculate_shortest_path_length

# Constants
MEMORY_LIMIT_GB = 7.0
OUTPUT_PATH = project_root / "data" / "processed" / "graph_metrics.csv"
ELIGIBLE_SUBJECTS_PATH = project_root / "data" / "processed" / "eligible_subjects.csv"
MATRICES_DIR = project_root / "data" / "processed" / "connectivity_matrices"

# Initialize logger
logger = get_logger("graph_metrics")

def get_memory_usage_gb() -> float:
    """Get current memory usage in GB."""
    process = psutil.Process(os.getpid())
    mem_info = process.memory_info()
    return mem_info.rss / (1024 ** 3)

def check_memory_limit(limit_gb: float = MEMORY_LIMIT_GB) -> bool:
    """Check if current memory usage is within limit."""
    current_usage = get_memory_usage_gb()
    if current_usage > limit_gb:
        logger.error(f"Memory limit exceeded: {current_usage:.2f}GB > {limit_gb}GB")
        return False
    return True

def load_eligible_subjects() -> pd.DataFrame:
    """Load the list of eligible subjects from the filtering step."""
    if not ELIGIBLE_SUBJECTS_PATH.exists():
        raise FileNotFoundError(f"Eligible subjects file not found: {ELIGIBLE_SUBJECTS_PATH}")
    
    df = load_csv(ELIGIBLE_SUBJECTS_PATH)
    if df.empty:
        raise ValueError("Eligible subjects file is empty")
    
    # Ensure subject_id column exists
    if 'subject_id' not in df.columns:
        # Try common alternatives
        for col in ['subject', 'sub_id', 'participant_id']:
            if col in df.columns:
                df['subject_id'] = df[col]
                break
        else:
            raise KeyError("Could not find subject_id column in eligible subjects file")
    
    return df

def process_single_subject_matrix(subject_id: str, matrix_path: Path) -> Optional[Dict[str, Any]]:
    """
    Process a single subject's connectivity matrix and compute graph metrics.
    
    Args:
        subject_id: Unique identifier for the subject
        matrix_path: Path to the subject's connectivity matrix file
        
    Returns:
        Dictionary containing subject_id and computed graph metrics, or None if processing fails.
    """
    try:
        # Check memory before processing
        if not check_memory_limit():
            logger.warning(f"Skipping {subject_id} due to memory constraints")
            return None
        
        # Load adjacency matrix
        matrix = np.loadtxt(matrix_path)
        
        if matrix.shape[0] != matrix.shape[1]:
            logger.warning(f"Non-square matrix for {subject_id}: {matrix.shape}")
            return None
        
        # Create graph from adjacency matrix
        # Threshold small values to avoid numerical issues
        matrix = np.maximum(matrix, 0)
        np.fill_diagonal(matrix, 0)  # Remove self-loops
        
        G = create_graph_from_adjacency(matrix)
        
        if G.number_of_nodes() == 0:
            logger.warning(f"Empty graph for {subject_id}")
            return None
        
        # Calculate global metrics
        global_efficiency = calculate_global_efficiency(G)
        clustering_coeff = calculate_clustering_coefficient(G)
        avg_degree = calculate_degree_centrality(G).mean() if len(G.nodes()) > 0 else 0.0
        
        # Calculate average shortest path length (global efficiency alternative)
        try:
            avg_path_length = calculate_shortest_path_length(G)
        except nx.NetworkXError:
            # Graph might be disconnected
            avg_path_length = float('inf')
        
        # Calculate nodal metrics (degree centrality for all nodes)
        degree_centrality = calculate_degree_centrality(G)
        
        # Aggregate nodal metrics into subject-level features
        # We'll store the mean degree centrality and also individual node degrees
        # For simplicity in this version, we store mean and std of degree centrality
        nodal_degree_mean = degree_centrality.mean()
        nodal_degree_std = degree_centrality.std()
        
        # Compile results
        metrics = {
            'subject_id': subject_id,
            'global_efficiency': global_efficiency,
            'clustering_coefficient': clustering_coeff,
            'average_degree': avg_degree,
            'average_path_length': avg_path_length,
            'nodal_degree_mean': nodal_degree_mean,
            'nodal_degree_std': nodal_degree_std,
            'n_nodes': G.number_of_nodes(),
            'n_edges': G.number_of_edges()
        }
        
        # Add individual node degree centralities as separate columns
        for node, degree in degree_centrality.items():
            metrics[f'degree_node_{node}'] = degree
        
        return metrics
        
    except Exception as e:
        logger.error(f"Error processing {subject_id}: {str(e)}")
        return None

def process_subject_wrapper(args: tuple) -> Optional[Dict[str, Any]]:
    """Wrapper for parallel processing with proper argument unpacking."""
    subject_id, matrix_path = args
    return process_single_subject_matrix(subject_id, matrix_path)

def write_outputs(results: List[Dict[str, Any]]) -> None:
    """Write computed metrics to CSV file."""
    if not results:
        logger.error("No results to write")
        return
    
    # Convert to DataFrame
    df = pd.DataFrame(results)
    
    # Ensure subject_id is first column
    cols = ['subject_id'] + [c for c in df.columns if c != 'subject_id']
    df = df[cols]
    
    # Create output directory if it doesn't exist
    OUTPUT_PATH.parent.mkdir(parents=True, exist_ok=True)
    
    # Save to CSV
    df.to_csv(OUTPUT_PATH, index=False)
    logger.info(f"Wrote {len(results)} subjects to {OUTPUT_PATH}")

def log_runtime_metrics(start_time: float, end_time: float, n_subjects: int) -> None:
    """Log runtime statistics."""
    duration = end_time - start_time
    avg_time_per_subject = duration / n_subjects if n_subjects > 0 else 0
    
    logger.info(f"Completed processing {n_subjects} subjects in {duration:.2f} seconds")
    logger.info(f"Average time per subject: {avg_time_per_subject:.2f} seconds")
    logger.info(f"Estimated time for 100 subjects: {avg_time_per_subject * 100:.2f} seconds ({avg_time_per_subject * 100 / 60:.2f} minutes)")

def main() -> int:
    """Main entry point for graph metrics computation."""
    start_time = time.time()
    
    logger.info("Starting graph metrics computation (T035 Refactored)")
    
    try:
        # Load eligible subjects
        eligible_df = load_eligible_subjects()
        eligible_subjects = eligible_df['subject_id'].tolist()
        logger.info(f"Found {len(eligible_subjects)} eligible subjects")
        
        if not eligible_subjects:
            logger.error("No eligible subjects found")
            return 1
        
        # Prepare input data for parallel processing
        tasks = []
        for subject_id in eligible_subjects:
            # Construct expected matrix path
            matrix_path = MATRICES_DIR / f"{subject_id}_connectivity.npy"
            if not matrix_path.exists():
                # Try alternative extension
                matrix_path = MATRICES_DIR / f"{subject_id}_connectivity.txt"
            
            if matrix_path.exists():
                tasks.append((subject_id, matrix_path))
            else:
                logger.warning(f"Matrix file not found for {subject_id}: {matrix_path}")
        
        if not tasks:
            logger.error("No matrix files found for eligible subjects")
            return 1
        
        logger.info(f"Processing {len(tasks)} subjects with joblib.Parallel(n_jobs=2)")
        
        # Process subjects in parallel using joblib
        # Using n_jobs=2 to respect the 2-core runner constraint
        results = Parallel(n_jobs=2, verbose=10)(
            delayed(process_subject_wrapper)(task) for task in tasks
        )
        
        # Filter out None results (failed processing)
        valid_results = [r for r in results if r is not None]
        
        if not valid_results:
            logger.error("No valid results produced")
            return 1
        
        logger.info(f"Successfully processed {len(valid_results)} out of {len(tasks)} subjects")
        
        # Write outputs
        write_outputs(valid_results)
        
        # Log runtime metrics
        end_time = time.time()
        log_runtime_metrics(start_time, end_time, len(valid_results))
        
        # Check if we met the performance target
        duration_minutes = (end_time - start_time) / 60
        if len(valid_results) >= 10:  # Extrapolate if we have at least 10 subjects
            estimated_100_time = duration_minutes * (100 / len(valid_results))
            if estimated_100_time < 30:
                logger.info(f"Performance target met: Estimated time for 100 subjects is {estimated_100_time:.2f} minutes (< 30 min)")
            else:
                logger.warning(f"Performance target NOT met: Estimated time for 100 subjects is {estimated_100_time:.2f} minutes (>= 30 min)")
        
        return 0
        
    except FileNotFoundError as e:
        logger.error(str(e))
        return 1
    except Exception as e:
        logger.error(f"Unexpected error: {str(e)}")
        import traceback
        traceback.print_exc()
        return 1

if __name__ == "__main__":
    sys.exit(main())