"""Compute graph-theoretic metrics for each subject’s resting‑state connectivity matrix.

This script is part of User Story 1. It reads the list of eligible subjects
(generated by ``code/01_download_and_filter.py``) and, for each subject,
loads a pre‑computed 90 × 90 connectivity matrix (NumPy ``.npy`` file) from
``data/processed/connectivity_matrices/<subject_id>.npy``.  Using NetworkX it
calculates the following node‑level and global metrics:

* **Mean node degree** (average of degree centrality values)
* **Global efficiency** (average inverse shortest‑path length)
* **Mean clustering coefficient** (average of local clustering)
* **Average shortest‑path length** (mean of all finite shortest‑path lengths)

Results are written to ``data/processed/graph_metrics.csv`` with one row per
subject.  The implementation processes subjects one‑by‑one to stay well
within the 7 GB RAM limit.  If a subject’s connectivity matrix is missing the
script logs a warning (via the tolerant ``utils.logger``) and skips that
subject.  If no eligible subjects are found an empty CSV with only the
header is created so downstream steps can still run.

The script is deliberately defensive: missing input files do not raise
uncaught exceptions; instead they are reported and the pipeline proceeds.
"""

from __future__ import annotations

import csv
import sys
import traceback
from pathlib import Path
from typing import List, Tuple

import numpy as np
import networkx as nx

# Local utilities ---------------------------------------------------------

try:
    # The project provides a tolerant logger implementation (see
    # ``code/utils/logger.py``).  Importing it will succeed even if the
    # underlying logger is a no‑op.
    from utils.logger import get_logger
except Exception:  # pragma: no cover
    # Fallback to a very small stub if the logger cannot be imported.
    class _DummyLogger:
        def __getattr__(self, name):
            return lambda *a, **k: None

    def get_logger(*a, **k):
        return _DummyLogger()

logger = get_logger(__name__)

# Configuration -----------------------------------------------------------

MEMORY_LIMIT_GB = 7  # maximum RAM usage for this script


# Helper functions ---------------------------------------------------------

def load_config_wrapper() -> dict:
    """Return a minimal configuration dictionary.

    The original project used a more elaborate config system; for this
    isolated task we only need the memory limit.
    """
    return {"memory_limit_gb": MEMORY_LIMIT_GB}


def check_memory_limit(limit_gb: float) -> None:
    """Abort if the current process exceeds *limit_gb* gigabytes."""
    try:
        import psutil
    except Exception as exc:  # pragma: no cover
        logger.warning(
            "psutil not available – cannot enforce memory limit: %s", exc
        )
        return

    process = psutil.Process()
    mem_bytes = process.memory_info().rss
    mem_gb = mem_bytes / (1024 ** 3)
    if mem_gb > limit_gb:
        logger.error(
            "Memory usage %.2f GB exceeds limit of %.2f GB", mem_gb, limit_gb
        )
        sys.exit(1)


def load_subject_list() -> List[str]:
    """Return a list of subject IDs from ``eligible_subjects.csv``.

    The CSV is expected to have a header with at least a column named
    ``subject_id``.  If the file is missing or empty, an empty list is
    returned.
    """
    csv_path = Path("data/processed/eligible_subjects.csv")
    if not csv_path.is_file():
        logger.warning(
            "eligible_subjects.csv not found at %s; returning empty subject list",
            csv_path,
        )
        return []

    try:
        with csv_path.open(newline="") as f:
            reader = csv.DictReader(f)
            subjects = [row["subject_id"] for row in reader if "subject_id" in row]
        logger.info("Loaded %d eligible subjects", len(subjects))
        return subjects
    except Exception as exc:  # pragma: no cover
        logger.error("Failed to read eligible_subjects.csv: %s", exc)
        return []


def load_connectivity_matrix(subject_id: str) -> np.ndarray:
    """Load a subject‑specific connectivity matrix.

    Expected location:
    ``data/processed/connectivity_matrices/<subject_id>.npy``

    Raises:
        FileNotFoundError: if the file does not exist.
    """
    matrix_path = Path("data/processed/connectivity_matrices") / f"{subject_id}.npy"
    if not matrix_path.is_file():
        raise FileNotFoundError(matrix_path)
    return np.load(matrix_path)


def compute_subject_metrics(matrix: np.ndarray) -> Tuple[float, float, float, float]:
    """Calculate graph metrics for a single connectivity matrix.

    Returns a tuple:
    (mean_degree, global_efficiency, mean_clustering, avg_shortest_path)

    The matrix is assumed to be symmetric and non‑negative.
    """
    # Ensure the matrix is a proper adjacency representation.
    # Small negative values (numerical noise) are set to zero.
    adj = np.where(matrix < 0, 0, matrix)

    # Create an undirected NetworkX graph.
    G = nx.from_numpy_array(adj)

    # Degree centrality returns values in [0, 1]; convert to raw degree.
    degree_centrality = nx.degree_centrality(G)
    mean_degree = float(np.mean(list(degree_centrality.values())) * (len(G) - 1))

    # Global efficiency (NetworkX expects a graph, not a matrix).
    global_eff = nx.global_efficiency(G)

    # Mean clustering coefficient.
    clustering = nx.average_clustering(G)
    mean_clustering = float(clustering)

    # Average shortest path length – only for connected graphs.
    if nx.is_connected(G):
        avg_path = nx.average_shortest_path_length(G)
    else:
        # Use the average over the largest connected component.
        largest_cc = max(nx.connected_components(G), key=len)
        subgraph = G.subgraph(largest_cc)
        avg_path = nx.average_shortest_path_length(subgraph)

    return mean_degree, global_eff, mean_clustering, float(avg_path)


def write_metrics_csv(
    rows: List[dict], output_path: Path = Path("data/processed/graph_metrics.csv")
) -> None:
    """Write the metric rows to ``graph_metrics.csv``."""
    output_path.parent.mkdir(parents=True, exist_ok=True)

    fieldnames = [
        "subject_id",
        "mean_degree",
        "global_efficiency",
        "mean_clustering_coefficient",
        "average_shortest_path_length",
    ]
    try:
        with output_path.open("w", newline="") as f:
            writer = csv.DictWriter(f, fieldnames=fieldnames)
            writer.writeheader()
            for row in rows:
                writer.writerow(row)
        logger.info("Graph metrics written to %s (%d rows)", output_path, len(rows))
    except Exception as exc:  # pragma: no cover
        logger.error("Failed to write graph metrics CSV: %s", exc)
        raise


# Main orchestration -------------------------------------------------------

def main() -> None:
    """Entry point for the ``03_compute_graph_metrics`` script."""
    try:
        config = load_config_wrapper()
        check_memory_limit(config["memory_limit_gb"])

        subjects = load_subject_list()
        if not subjects:
            logger.warning(
                "No eligible subjects found – creating empty graph_metrics.csv"
            )
            write_metrics_csv([])
            return

        metric_rows: List[dict] = []
        for sid in subjects:
            try:
                matrix = load_connectivity_matrix(sid)
            except FileNotFoundError:
                logger.warning(
                    "Connectivity matrix not found for subject %s; skipping", sid
                )
                continue
            except Exception as exc:
                logger.error(
                    "Error loading matrix for subject %s: %s", sid, traceback.format_exc()
                )
                continue

            try:
                mean_deg, glob_eff, mean_clust, avg_path = compute_subject_metrics(
                    matrix
                )
            except Exception as exc:  # pragma: no cover
                logger.error(
                    "Failed to compute metrics for %s: %s", sid, traceback.format_exc()
                )
                continue

            metric_rows.append(
                {
                    "subject_id": sid,
                    "mean_degree": f"{mean_deg:.6f}",
                    "global_efficiency": f"{glob_eff:.6f}",
                    "mean_clustering_coefficient": f"{mean_clust:.6f}",
                    "average_shortest_path_length": f"{avg_path:.6f}",
                }
            )

        write_metrics_csv(metric_rows)

    except Exception as exc:  # pragma: no cover
        logger.error("Unhandled exception in graph‑metric computation: %s", exc)
        sys.exit(1)


if __name__ == "__main__":
    main()