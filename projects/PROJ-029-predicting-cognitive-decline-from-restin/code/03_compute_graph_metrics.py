"""Compute graph-theoretic metrics for each eligible subject.

This script:
  1. Loads the list of eligible subjects (generated by T017).
  2. For each subject, loads its pre‑computed 90×90 connectivity matrix.
  3. Constructs a NetworkX graph from the matrix.
  4. Computes node‑degree (average), global efficiency, clustering coefficient,
     and average shortest path length.
  5. Writes a CSV ``data/processed/graph_metrics.csv`` with one row per subject.

The implementation processes subjects one‑by‑one to keep RAM usage below the
7 GB limit defined for the pipeline.
"""
from __future__ import annotations

import csv
import pathlib
import sys
import time
from typing import List, Dict

import numpy as np

from utils.io import ensure_dir, load_csv, save_csv
from utils.logger import get_logger
from utils.graph import (
    create_graph_from_adjacency,
    calculate_global_efficiency,
    calculate_clustering_coefficient,
    calculate_degree_centrality,
    calculate_shortest_path_length,
)

# --------------------------------------------------------------------------- #
# Configuration
# --------------------------------------------------------------------------- #
MEMORY_LIMIT_GB = 7.0
ELIGIBLE_SUBJECTS_PATH = pathlib.Path("data/processed/eligible_subjects.csv")
CONNECTIVITY_DIR = pathlib.Path("data/processed/connectivity_matrices")
OUTPUT_CSV = pathlib.Path("data/processed/graph_metrics.csv")


# --------------------------------------------------------------------------- #
# Helper functions
# --------------------------------------------------------------------------- #
def check_memory_limit() -> None:
    """Abort if the current process exceeds the RAM limit."""
    try:
        import psutil
    except ImportError:
        # psutil is a declared dependency; if unavailable we raise a clear error.
        raise RuntimeError("psutil is required for memory checking but is not installed.")

    process = psutil.Process()
    mem_gb = process.memory_info().rss / (1024 ** 3)
    if mem_gb > MEMORY_LIMIT_GB:
        logger = get_logger("graph_metrics")
        logger.log(
            "memory_limit_exceeded",
            used_gb=mem_gb,
            limit_gb=MEMORY_LIMIT_GB,
        )
        sys.exit(1)


def load_eligible_subjects() -> List[str]:
    """Return a list of subject identifiers from ``eligible_subjects.csv``."""
    logger = get_logger("graph_metrics")
    if not ELIGIBLE_SUBJECTS_PATH.is_file():
        logger.log("eligible_subjects_missing", path=str(ELIGIBLE_SUBJECTS_PATH))
        sys.exit(1)

    rows = load_csv(ELIGIBLE_SUBJECTS_PATH)
    # Expect a header with at least a column named ``subject_id``.
    if not rows:
        logger.log("eligible_subjects_empty")
        sys.exit(1)

    header = rows[0]
    try:
        idx = header.index("subject_id")
    except ValueError:
        logger.log(
            "eligible_subjects_missing_column",
            expected="subject_id",
            found=header,
        )
        sys.exit(1)

    subject_ids = [row[idx] for row in rows[1:] if len(row) > idx]
    logger.log("eligible_subjects_loaded", count=len(subject_ids))
    return subject_ids


def load_connectivity_matrix(subject_id: str) -> np.ndarray:
    """Load a subject's 90×90 connectivity matrix stored as ``.npy``."""
    logger = get_logger("graph_metrics")
    matrix_path = CONNECTIVITY_DIR / f"{subject_id}_conn.npy"
    if not matrix_path.is_file():
        logger.log(
            "connectivity_matrix_missing",
            subject_id=subject_id,
            path=str(matrix_path),
        )
        raise FileNotFoundError(f"Connectivity matrix not found for {subject_id}")
    matrix = np.load(matrix_path)
    if matrix.shape != (90, 90):
        logger.log(
            "connectivity_matrix_unexpected_shape",
            subject_id=subject_id,
            shape=matrix.shape,
        )
        raise ValueError(f"Unexpected matrix shape for {subject_id}: {matrix.shape}")
    return matrix


def compute_subject_metrics(matrix: np.ndarray) -> Dict[str, float]:
    """Compute the required graph metrics from a connectivity matrix."""
    # Build an undirected weighted graph from the adjacency matrix.
    G = create_graph_from_adjacency(matrix)

    # Degree centrality returns a dict {node: value}; we take the mean.
    degree_dict = calculate_degree_centrality(G)
    avg_degree = float(np.mean(list(degree_dict.values())))

    # Global efficiency (scalar)
    global_eff = float(calculate_global_efficiency(G))

    # Clustering coefficient (scalar)
    clustering = float(calculate_clustering_coefficient(G))

    # Average shortest path length (scalar). The helper returns the mean over all pairs.
    avg_path_len = float(calculate_shortest_path_length(G))

    return {
        "degree": avg_degree,
        "global_efficiency": global_eff,
        "clustering_coefficient": clustering,
        "average_path_length": avg_path_len,
    }


def write_graph_metrics_csv(
    rows: List[Dict[str, str | float]],
    header: List[str],
) -> None:
    """Write the collected metric rows to ``OUTPUT_CSV``."""
    logger = get_logger("graph_metrics")
    ensure_dir(OUTPUT_CSV.parent)
    with OUTPUT_CSV.open("w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=header)
        writer.writeheader()
        for row in rows:
            writer.writerow(row)
    logger.log("graph_metrics_written", path=str(OUTPUT_CSV), rows=len(rows))


def process_all_subjects() -> None:
    """Main driver – iterate over subjects, compute metrics, and persist them."""
    logger = get_logger("graph_metrics")
    start_time = time.time()

    subject_ids = load_eligible_subjects()
    if not subject_ids:
        logger.log("no_eligible_subjects")
        sys.exit(1)

    header = [
        "subject_id",
        "degree",
        "global_efficiency",
        "clustering_coefficient",
        "average_path_length",
    ]
    rows: List[Dict[str, str | float]] = []

    for idx, subj in enumerate(subject_ids, start=1):
        try:
            matrix = load_connectivity_matrix(subj)
            metrics = compute_subject_metrics(matrix)
            row = {"subject_id": subj, **metrics}
            rows.append(row)
            logger.log(
                "subject_processed",
                subject_id=subj,
                index=idx,
                total=len(subject_ids),
            )
        except Exception as exc:
            logger.log(
                "subject_failed",
                subject_id=subj,
                error=str(exc),
            )
            # Continue with remaining subjects rather than aborting entirely.

        # Periodically check memory usage to stay within limits.
        if idx % 10 == 0:
            check_memory_limit()

    write_graph_metrics_csv(rows, header)
    elapsed = time.time() - start_time
    logger.log("graph_metrics_completed", subjects=len(subject_ids), elapsed_seconds=elapsed)


