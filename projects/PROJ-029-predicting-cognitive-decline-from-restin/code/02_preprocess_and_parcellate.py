"""
Preprocess and Parcellate rs-fMRI data for eligible subjects.

This script loads raw BIDS data for subjects listed in
`data/processed/eligible_subjects.csv`, performs motion correction using
FSL (mcflirt), normalization using nilearn, applies the AAL atlas, and
extracts time series to compute connectivity matrices.

Outputs:
    - data/processed/connectivity_matrices/{subject_id}.nii.gz
    - data/processed/connectivity_matrices/{subject_id}_ts.npy
    - data/processed/preprocessing_status.json
    - data/processed/preprocessing_excluded.log
"""
