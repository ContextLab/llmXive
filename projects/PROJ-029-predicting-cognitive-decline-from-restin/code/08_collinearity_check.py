"""
Task T044: Reconcile run-book vs implementation for collinearity check.

This script performs a standalone collinearity analysis on the graph metrics
generated by T019 (03_compute_graph_metrics.py). It calculates the correlation
matrix, identifies feature pairs with Pearson correlation > 0.95, and outputs
a report of highly correlated pairs to data/processed/collinearity_report.json.

This script is invoked by the run-book (quickstart.md) to ensure the step
exists as documented, even though the actual filtering logic is integrated
into the nested CV loop in 04_train_model.py.
"""
import os
import sys
import json
import argparse
from pathlib import Path
import pandas as pd
import numpy as np
from scipy.stats import pearsonr

# Add project root to path
project_root = Path(__file__).parent.parent
sys.path.insert(0, str(project_root / "code"))

from utils.logger import get_logger
from utils.io import ensure_dir

logger = get_logger("08_collinearity_check")

def calculate_correlation_matrix(df: pd.DataFrame) -> pd.DataFrame:
    """Calculate Pearson correlation matrix for numeric columns."""
    numeric_cols = df.select_dtypes(include=[np.number]).columns
    if len(numeric_cols) < 2:
        logger.warning("Not enough numeric columns to calculate correlation.")
        return pd.DataFrame()
    
    corr_matrix = df[numeric_cols].corr(method="pearson")
    return corr_matrix

def find_highly_correlated_pairs(
    corr_matrix: pd.DataFrame, 
    threshold: float = 0.95
) -> list:
    """
    Identify pairs of features with absolute correlation > threshold.
    Returns a list of dicts: {'feature1': str, 'feature2': str, 'correlation': float}
    """
    pairs = []
    cols = corr_matrix.columns
    for i in range(len(cols)):
        for j in range(i + 1, len(cols)):
            val = corr_matrix.iloc[i, j]
            if abs(val) > threshold:
                pairs.append({
                    "feature1": cols[i],
                    "feature2": cols[j],
                    "correlation": float(val)
                })
    return pairs

def main():
    parser = argparse.ArgumentParser(description="Analyze collinearity in graph metrics.")
    parser.add_argument(
        "--input", 
        type=str, 
        default="data/processed/graph_metrics.csv",
        help="Path to the graph metrics CSV file."
    )
    parser.add_argument(
        "--output", 
        type=str, 
        default="data/processed/collinearity_report.json",
        help="Path to the output JSON report."
    )
    parser.add_argument(
        "--threshold", 
        type=float, 
        default=0.95,
        help="Correlation threshold to flag high collinearity."
    )
    args = parser.parse_args()

    input_path = Path(args.input)
    output_path = Path(args.output)

    if not input_path.exists():
        logger.error(f"Input file not found: {input_path}")
        logger.error("This script requires 'data/processed/graph_metrics.csv' to be generated first by code/03_compute_graph_metrics.py.")
        sys.exit(1)

    logger.info(f"Loading data from {input_path}")
    try:
        # Use pandas directly to handle potential schema mismatches robustly
        df = pd.read_csv(input_path)
    except Exception as e:
        logger.error(f"Failed to load data: {e}")
        sys.exit(1)

    logger.info(f"Loaded {len(df)} rows with {len(df.columns)} columns.")
    
    # Calculate correlation matrix
    logger.info("Calculating correlation matrix...")
    corr_matrix = calculate_correlation_matrix(df)
    
    if corr_matrix.empty:
        logger.warning("No numeric correlations found. Writing empty report.")
        report = {
            "input_file": str(input_path),
            "threshold": args.threshold,
            "pairs": [],
            "message": "No numeric features found or insufficient data."
        }
    else:
        # Find high correlations
        pairs = find_highly_correlated_pairs(corr_matrix, args.threshold)
        
        report = {
            "input_file": str(input_path),
            "threshold": args.threshold,
            "total_features": len(corr_matrix.columns),
            "highly_correlated_pairs_count": len(pairs),
            "pairs": pairs
        }
        
        if pairs:
            logger.warning(f"Found {len(pairs)} highly correlated pairs (>|{args.threshold}|).")
            for p in pairs:
                logger.warning(f"  {p['feature1']} <-> {p['feature2']}: {p['correlation']:.4f}")
        else:
            logger.info("No highly correlated pairs found.")

    # Ensure output directory exists
    ensure_dir(output_path)
    
    # Write report
    logger.info(f"Writing report to {output_path}")
    with open(output_path, "w") as f:
        json.dump(report, f, indent=2)

    logger.info("Collinearity check complete.")

if __name__ == "__main__":
    main()