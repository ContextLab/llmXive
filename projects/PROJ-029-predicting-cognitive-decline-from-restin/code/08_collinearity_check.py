"""Collinearity check for graph‑metric features.

This script loads the graph metric table generated by
``code/03_compute_graph_metrics.py``, computes a Pearson correlation matrix
across all numeric features, identifies pairs of features whose absolute
correlation exceeds a default threshold (0.95), and writes a concise JSON
report.  It also logs its progress using the shared reproducibility logger.

The script is invoked from the quick‑start run‑book; if the run‑book does not
contain an explicit call, the entry point guarded by ``if __name__ == "__main__"``
ensures it can still be executed directly.
"""

from __future__ import annotations

import json
from pathlib import Path
from typing import List, Tuple

import pandas as pd

from utils.logger import get_logger, log_operation
from utils.stats import calculate_correlation_matrix, check_collinearity
from utils.io import ensure_dir

# --------------------------------------------------------------------------- #
# Configuration
# --------------------------------------------------------------------------- #

INPUT_CSV = Path("data/processed/graph_metrics.csv")
OUTPUT_JSON = Path("data/processed/collinearity_report.json")
CORRELATION_THRESHOLD = 0.95  # absolute Pearson correlation considered collinear


# --------------------------------------------------------------------------- #
# Core functions
# --------------------------------------------------------------------------- #

def _load_metrics(csv_path: Path) -> pd.DataFrame:
    """Load the graph‑metric CSV as a DataFrame."""
    if not csv_path.is_file():
        raise FileNotFoundError(f"Graph metrics file not found: {csv_path}")
    df = pd.read_csv(csv_path)
    return df


def _identify_collinear_pairs(df: pd.DataFrame, threshold: float) -> List[Tuple[str, str, float]]:
    """Return a list of (feature_a, feature_b, correlation) for highly correlated pairs."""
    # Exclude non‑numeric columns (e.g., subject identifiers)
    numeric_df = df.select_dtypes(include=["number"])
    if numeric_df.empty:
        return []

    corr_matrix = calculate_correlation_matrix(numeric_df)
    # ``check_collinearity`` expects the matrix and a threshold; it returns a list
    # of (i, j, value) where |value| > threshold.
    raw_pairs = check_collinearity(corr_matrix, threshold)
    # Convert matrix indices back to column names.
    pairs = [
        (numeric_df.columns[i], numeric_df.columns[j], float(corr_matrix.iloc[i, j]))
        for i, j, _ in raw_pairs
    ]
    return pairs


def _write_report(pairs: List[Tuple[str, str, float]], out_path: Path) -> None:
    """Serialise the collinearity information to JSON."""
    report = {
        "threshold": CORRELATION_THRESHOLD,
        "collinear_pairs": [
            {"feature_a": a, "feature_b": b, "correlation": corr}
            for a, b, corr in pairs
        ],
        "generated_at": pd.Timestamp.utcnow().isoformat(),
    }
    ensure_dir(out_path.parent)
    with out_path.open("w", encoding="utf-8") as f:
        json.dump(report, f, indent=2, ensure_ascii=False)


@log_operation("collinearity_check")
def main() -> None:
    """Entry point for the collinearity check."""
    logger = get_logger("collinearity_check")
    logger.info("Starting collinearity check")

    df = _load_metrics(INPUT_CSV)
    logger.debug(f"Loaded {df.shape[0]} rows and {df.shape[1]} columns")

    pairs = _identify_collinear_pairs(df, CORRELATION_THRESHOLD)
    if pairs:
        logger.info(f"Found {len(pairs)} collinear feature pairs (|r| > {CORRELATION_THRESHOLD})")
    else:
        logger.info("No collinear feature pairs found")

    _write_report(pairs, OUTPUT_JSON)
    logger.info(f"Collinearity report written to {OUTPUT_JSON}")


if __name__ == "__main__":
    main()
