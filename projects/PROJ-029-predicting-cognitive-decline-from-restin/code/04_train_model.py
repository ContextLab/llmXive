"""Training script for predicting cognitive decline from rs‑fMRI graph metrics.

This script implements the full training pipeline required by task T023.
It:
  1. Loads graph‑metric features and eligibility information.
  2. Defines a binary decline label (drop ≥ 3 points on MMSE or MoCA).
  3. Performs a 5‑fold outer stratified CV.
  4. Within each outer fold runs an inner grid‑search CV (3‑fold) over
     ``n_estimators`` ∈ {50, 100, 200} and ``max_depth`` ∈ {5, 10, None}.
  5. Inside the inner loop applies:
     - Collinearity removal (corr > 0.95, keep higher‑variance feature)
     - Variance thresholding (variance > 0.01)
     - Recursive feature elimination (≤ 20 features)
  6. Persists the best model to ``data/processed/model.pkl``.
  7. Writes a JSON performance report to ``data/processed/performance_report.json``.
"""

