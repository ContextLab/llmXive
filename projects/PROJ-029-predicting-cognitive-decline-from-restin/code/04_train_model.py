"""Train a Random Forest model with nested cross‑validation.

This script implements the requirements of task T023:
- Define cognitive decline label (drop ≥ 3 points on MMSE or MOCA).
- Perform a 5‑fold outer CV with an inner grid‑search (n_estimators ∈ {50,100,200},
  max_depth ∈ {5,10,None}).
- Inside the inner loop apply:
    * Collinearity filtering (correlation > 0.95, keep higher‑variance feature)
    * Variance thresholding (variance > 0.01)
    * Recursive feature elimination (≤ 20 features)
- Fit a RandomForestClassifier and log the final selected parameters.
- Persist the best model to ``data/processed/model.pkl``.
- Write a performance report (metrics per outer fold + best hyper‑parameters) to
  ``data/processed/performance_report.json``.

The implementation relies only on the public API of the project and the
tolerant logger defined in ``code/utils/logger.py``.
"""

from __future__ import annotations

import json
import sys
import warnings
from pathlib import Path
from typing import List, Tuple

import joblib
import numpy as np
import pandas as pd
from sklearn.base import BaseEstimator, TransformerMixin
from sklearn.ensemble import RandomForestClassifier
from sklearn.feature_selection import RFE, VarianceThreshold
from sklearn.metrics import accuracy_score, f1_score, roc_auc_score
from sklearn.model_selection import GridSearchCV, StratifiedKFold

# Project utilities
from utils.logger import get_logger, log_operation

# Suppress any sklearn warnings that are not critical for the pipeline
warnings.filterwarnings("ignore", category=UserWarning)

LOGGER = get_logger("04_train_model")


# --------------------------------------------------------------------------- #
# Helper functions
# --------------------------------------------------------------------------- #

def load_features() -> pd.DataFrame:
    """Load graph‑metric features for all eligible subjects.

    Expected location: ``data/processed/graph_metrics.csv``.
    Returns a DataFrame where the index is ``subject_id`` and the remaining
    columns are numeric graph metrics.
    """
    features_path = Path("data/processed/graph_metrics.csv")
    if not features_path.is_file():
        LOGGER.error("Feature file not found", path=str(features_path))
        raise FileNotFoundError(f"Features file missing: {features_path}")
    df = pd.read_csv(features_path)
    if "subject_id" not in df.columns:
        LOGGER.error("subject_id column missing in features")
        raise KeyError("subject_id column required in graph_metrics.csv")
    df = df.set_index("subject_id")
    LOGGER.info("Loaded features", shape=df.shape)
    return df

def load_eligible_subjects() -> pd.DataFrame:
    """Load the CSV generated by ``code/01_download_and_filter.py``."""
    path = Path("data/processed/eligible_subjects.csv")
    if not path.is_file():
        LOGGER.error("Eligible subjects file missing", path=str(path))
        raise FileNotFoundError(f"eligible_subjects.csv not found at {path}")
    df = pd.read_csv(path)
    required = {"subject_id", "mmse_t1", "mmse_t2", "moca_t1", "moca_t2"}
    missing = required - set(df.columns)
    if missing:
        LOGGER.error("Missing columns in eligible_subjects", missing=list(missing))
        raise KeyError(f"eligible_subjects.csv missing columns: {missing}")
    LOGGER.info("Loaded eligible subjects", count=len(df))
    return df


def define_decline_label(df: pd.DataFrame) -> pd.Series:
    """Create a binary label indicating cognitive decline.

    Decline = drop of ≥3 points in MMSE or MOCA between the two timepoints.
    """
    mmse_drop = df["mmse_t1"] - df["mmse_t2"]
    moca_drop = df["moca_t1"] - df["moca_t2"]
    decline = ((mmse_drop >= 3) | (moca_drop >= 3)).astype(int)
    LOGGER.info("Defined decline label", positive=int(decline.sum()))
    return decline


# --------------------------------------------------------------------------- #
# Collinearity transformer
# --------------------------------------------------------------------------- #

class CollinearityTransformer(BaseEstimator, TransformerMixin):
    """Drop one feature from each pair whose absolute Pearson correlation > 0.95.

    The feature with the lower variance is removed. If variances are equal,
    the feature that appears later alphabetically is dropped.
    """

    def __init__(self, threshold: float = 0.95):
        self.threshold = threshold
        self.features_to_keep_: List[str] = []

    def fit(self, X: pd.DataFrame, y=None):
        corr = X.corr().abs()
        # Upper triangle mask without the diagonal
        upper = corr.where(np.triu(np.ones(corr.shape), k=1).astype(bool))

        # Determine features to drop
        to_drop = set()
        variances = X.var()

        for col in upper.columns:
            high_corr = upper[col][upper[col] > self.threshold].index.tolist()
            for row in high_corr:
                if col in to_drop or row in to_drop:
                    continue
                # Compare variances
                if variances[col] < variances[row]:
                    to_drop.add(col)
                elif variances[col] > variances[row]:
                    to_drop.add(row)
                else:
                    # Tie‑breaker: drop later alphabetically
                    to_drop.add(max(col, row))

        self.features_to_keep_ = [c for c in X.columns if c not in to_drop]
        LOGGER.info("CollinearityTransformer fit", kept=len(self.features_to_keep_), dropped=len(to_drop))
        return self

    def transform(self, X: pd.DataFrame):
        return X[self.features_to_keep_]


# --------------------------------------------------------------------------- #
# Pipeline construction
# --------------------------------------------------------------------------- #

def make_inner_pipeline(random_state: int = 42) -> Tuple:
    """Create the inner pipeline steps (excluding the classifier)."""
    collinearity = ("collinearity", CollinearityTransformer(threshold=0.95))
    variance = ("var_thresh", VarianceThreshold(threshold=0.01))
    # RFE will be attached later once we know the classifier hyper‑parameters
    # Placeholder estimator for RFE (will be overwritten in GridSearch)
    placeholder_clf = RandomForestClassifier(random_state=random_state, n_estimators=10)
    rfe = ("rfe", RFE(estimator=placeholder_clf, n_features_to_select=20, step=1))
    return collinearity, variance, rfe


# --------------------------------------------------------------------------- #
# Nested cross‑validation
# --------------------------------------------------------------------------- #

def train_and_evaluate_nested_cv(
    X: pd.DataFrame,
    y: pd.Series,
    random_state: int = 42,
) -> Tuple[RandomForestClassifier, List[dict]]:
    """Perform nested CV and return the best model (trained on full data)."""

    outer_cv = StratifiedKFold(n_splits=5, shuffle=True, random_state=random_state)

    # Parameter grid for the inner GridSearch
    param_grid = {
        "clf__n_estimators": [50, 100, 200],
        "clf__max_depth": [5, 10, None],
    }

    # Store metrics for each outer fold
    outer_metrics = []

    # Hold the best estimator across outer folds (by mean ROC‑AUC)
    best_score = -np.inf
    best_estimator = None
    best_params = None

    for fold_idx, (train_idx, test_idx) in enumerate(outer_cv.split(X, y), start=1):
        X_train, X_test = X.iloc[train_idx], X.iloc[test_idx]
        y_train, y_test = y.iloc[train_idx], y.iloc[test_idx]

        # Build the inner pipeline – note that RFE needs the final classifier,
        # which will be set by GridSearch.
        steps = list(make_inner_pipeline(random_state=random_state))
        steps.append(("clf", RandomForestClassifier(random_state=random_state)))

        from sklearn.pipeline import Pipeline

        pipeline = Pipeline(steps=steps)

        # GridSearchCV (inner CV)
        inner_cv = StratifiedKFold(n_splits=5, shuffle=True, random_state=random_state)
        grid = GridSearchCV(
            estimator=pipeline,
            param_grid=param_grid,
            cv=inner_cv,
            scoring="roc_auc",
            n_jobs=2,
            refit=True,
        )

        grid.fit(X_train, y_train)

        # Evaluate on the outer‑test set
        y_pred_proba = grid.predict_proba(X_test)[:, 1]
        y_pred = grid.predict(X_test)

        fold_metrics = {
            "fold": fold_idx,
            "roc_auc": roc_auc_score(y_test, y_pred_proba),
            "accuracy": accuracy_score(y_test, y_pred),
            "f1": f1_score(y_test, y_pred, zero_division=0),
            "best_params": grid.best_params_,
        }
        outer_metrics.append(fold_metrics)

        LOGGER.info(
            "Outer fold completed",
            fold=fold_idx,
            roc_auc=fold_metrics["roc_auc"],
            accuracy=fold_metrics["accuracy"],
            f1=fold_metrics["f1"],
            best_params=grid.best_params_,
        )

        # Keep track of the best performing outer model
        if fold_metrics["roc_auc"] > best_score:
            best_score = fold_metrics["roc_auc"]
            best_estimator = grid.best_estimator_
            best_params = grid.best_params_

    # Retrain the best estimator on the full dataset (using the best hyper‑params)
    if best_estimator is None:
        raise RuntimeError("No model was trained during nested CV.")

    # Replace the classifier inside the pipeline with the best hyper‑parameters
    best_estimator.set_params(**best_params)
    best_estimator.fit(X, y)

    LOGGER.info("Training completed", best_outer_roc_auc=best_score, best_params=best_params)
    return best_estimator, outer_metrics


# --------------------------------------------------------------------------- #
# Persistence helpers
# --------------------------------------------------------------------------- #

def persist_model(model: RandomForestClassifier, path: Path) -> None:
    """Write the trained model to disk using joblib."""
    path.parent.mkdir(parents=True, exist_ok=True)
    joblib.dump(model, path)
    LOGGER.info("Model persisted", path=str(path))


def write_performance_report(metrics: List[dict], path: Path) -> None:
    """Write a JSON report containing per‑fold metrics and the best params."""
    path.parent.mkdir(parents=True, exist_ok=True)
    report = {
        "outer_folds": metrics,
        "mean_roc_auc": np.mean([m["roc_auc"] for m in metrics]),
        "mean_accuracy": np.mean([m["accuracy"] for m in metrics]),
        "mean_f1": np.mean([m["f1"] for m in metrics]),
    }
    with path.open("w", encoding="utf-8") as f:
        json.dump(report, f, indent=2)
    LOGGER.info("Performance report written", path=str(path))


# --------------------------------------------------------------------------- #
# Main entry point
# --------------------------------------------------------------------------- #

@log_operation("train_model")
def main() -> None:
    """Execute the full training pipeline."""
    try:
        # Load data
        features_df = load_features()
        subjects_df = load_eligible_subjects()

        # Align features with subjects
        X = features_df.loc[subjects_df["subject_id"]].copy()
        y = define_decline_label(subjects_df)

        # Train / evaluate
        model, outer_metrics = train_and_evaluate_nested_cv(X, y, random_state=42)

        # Persist artifacts
        model_path = Path("data/processed/model.pkl")
        report_path = Path("data/processed/performance_report.json")
        persist_model(model, model_path)
        write_performance_report(outer_metrics, report_path)

        LOGGER.info("Training pipeline completed successfully")
    except Exception as exc:
        LOGGER.error("Training pipeline failed", error=str(exc))
        raise


if __name__ == "__main__":
    sys.exit(main())