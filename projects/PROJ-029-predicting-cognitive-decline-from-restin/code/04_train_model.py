@log_operation
def define_decline_label(df: pd.DataFrame) -> pd.DataFrame:
    """
    Add a binary ``label`` column:
      1 – subject shows cognitive decline (≥ 3‑point drop on MMSE or MoCA)
      0 – otherwise

    Expected score columns (case‑insensitive):
      - ``mmse_baseline`` / ``mmse_followup``
      - ``moca_baseline`` / ``moca_followup``

    If a column is missing we simply ignore that modality.
    """
    df = df.copy()
    # Normalise column names to lower case for robustness.
    cols = {c.lower(): c for c in df.columns}

    def _decline(row: pd.Series) -> int:
        mmse_drop = None
        moca_drop = None
        if "mmse_baseline" in cols and "mmse_followup" in cols:
            mmse_drop = row[cols["mmse_baseline"]] - row[cols["mmse_followup"]]
        if "moca_baseline" in cols and "moca_followup" in cols:
            moca_drop = row[cols["moca_baseline"]] - row[cols["moca_followup"]]

        drops = [d for d in (mmse_drop, moca_drop) if d is not None]
        return int(any(d >= 3 for d in drops))

    df["label"] = df.apply(_decline, axis=1)
    return df


class CollinearityTransformer:
    """
    Scikit‑learn compatible transformer that drops one feature from each pair
    with Pearson correlation > 0.95, keeping the feature with the higher
    variance.
    """

    def __init__(self, threshold: float = 0.95):
        self.threshold = threshold
        self.keep_: List[str] = []

    def fit(self, X: pd.DataFrame, y=None):
        """Identify features to keep."""
        corr = X.corr().abs()
        # Upper triangle without diagonal.
        upper = corr.where(np.triu(np.ones(corr.shape), k=1).astype(bool))

        to_drop = set()
        for col in upper.columns:
            high_corr = [row for row, val in upper[col].items() if val > self.threshold]
            for other in high_corr:
                if other in to_drop:
                    continue
                # Keep the feature with larger variance.
                var_col = X[col].var()
                var_other = X[other].var()
                keep = col if var_col >= var_other else other
                drop = other if keep == col else col
                to_drop.add(drop)
                # Record the one we keep.
                self.keep_.append(keep)
        # Ensure we keep any feature not flagged for dropping.
        self.keep_ = [c for c in X.columns if c not in to_drop]
        return self

    def transform(self, X: pd.DataFrame) -> pd.DataFrame:
        """Return the dataframe with only the retained columns."""
        return X[self.keep_]

    def fit_transform(self, X: pd.DataFrame, y=None) -> pd.DataFrame:
        self.fit(X, y)
        return self.transform(X)

    def get_feature_names_out(self, input_features=None):
        if self._keep_mask is None:
            raise RuntimeError("CollinearityTransformer not fitted")
        return [input_features[i] for i in self._keep_mask] if input_features is not None else None

