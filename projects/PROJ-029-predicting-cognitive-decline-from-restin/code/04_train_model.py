def main() -> int:
    """
    Entry‑point for the training script.

    Returns:
        0 on success, non‑zero error code otherwise.
    """
    logger = get_logger("04_train_model")
    logger.log(operation="start")

    try:
        # Load data.
        features_df = load_features()
        eligible_df = load_eligible_subjects()

        # Merge on a common subject identifier.  The column name may differ;
        # we attempt a few sensible defaults.
        possible_id_cols = ["subject_id", "sub_id", "participant_id"]
        id_col = None
        for col in possible_id_cols:
            if col in features_df.columns and col in eligible_df.columns:
                id_col = col
                break
        if id_col is None:
            raise KeyError(
                "No common subject identifier column found between features and eligibility tables."
            )

        merged = pd.merge(
            features_df,
            eligible_df,
            on=id_col,
            how="inner",
            suffixes=("_feat", "_elig"),
        )

        # Create the binary label.
        merged = define_decline_label(merged)

        # Separate features and label.
        label_series = merged["label"]
        X = merged.drop(columns=["label"])

        # Train / evaluate.
        best_model, report = train_and_evaluate_nested_cv(X, label_series)

        # Persist artefacts.
        persist_model(best_model)
        write_performance_report(report)

        logger.log(operation="complete", **report)
        return 0
    except Exception as exc:  # pragma: no cover – defensive logging
        logger.log(operation="error", error=str(exc))
        return 1


if __name__ == "__main__":
    sys.exit(main())
