"""04_train_model.py
Implements nested cross‑validation training for predicting cognitive decline
from resting‑state fMRI graph metrics.

Produces:
  - data/processed/model.pkl
  - data/processed/performance_report.json
"""
from __future__ import annotations

import json
import sys
from pathlib import Path
from typing import Any, Dict, List, Tuple

import joblib
import numpy as np
import pandas as pd
from sklearn.base import BaseEstimator, TransformerMixin
from sklearn.ensemble import RandomForestClassifier
from sklearn.feature_selection import VarianceThreshold, RFE
from sklearn.metrics import accuracy_score, f1_score, roc_auc_score
from sklearn.model_selection import GridSearchCV, StratifiedKFold
from sklearn.pipeline import Pipeline
from sklearn.ensemble import RandomForestClassifier

# --------------------------------------------------------------------------- #
# Logging utilities – use the shared logger implementation
# --------------------------------------------------------------------------- #
from utils.logger import get_logger

LOGGER = get_logger("nested_cv")

# --------------------------------------------------------------------------- #
# Helper functions for data loading
# --------------------------------------------------------------------------- #
def load_eligible_subjects() -> pd.DataFrame:
    """
    Load the CSV generated by ``code/01_download_and_filter.py`` that lists
    eligible subjects together with their baseline and follow‑up cognitive scores.

    Expected columns (at minimum):
      - subject_id
      - mmse_baseline
      - mmse_followup
      - moca_baseline
      - moca_followup
    """
    csv_path = Path("data/processed/eligible_subjects.csv")
    if not csv_path.is_file():
        LOGGER.log("error", message=f"Eligible subjects file not found: {csv_path}")
        sys.exit(1)
    df = pd.read_csv(csv_path)
    return df

def load_features() -> pd.DataFrame:
    """
    Load graph metrics (generated by ``code/03_compute_graph_metrics.py``) and
    merge them with the eligible subjects DataFrame so that each row contains
    the subject identifier, the cognitive scores and the graph‑metric features.
    """
    subjects_df = load_eligible_subjects()
    metrics_path = Path("data/processed/graph_metrics.csv")
    if not metrics_path.is_file():
        LOGGER.log("error", message=f"Graph metrics file not found: {metrics_path}")
        sys.exit(1)
    metrics_df = pd.read_csv(metrics_path)

    # Expect a column named ``subject_id`` in both tables for the merge
    merged = pd.merge(subjects_df, metrics_df, on="subject_id", how="inner")
    if merged.empty:
        LOGGER.log(
            "error",
            message="Merged dataset is empty – check subject IDs in eligible_subjects.csv and graph_metrics.csv",
        )
        sys.exit(1)
    return merged

# --------------------------------------------------------------------------- #
# Label definition
# --------------------------------------------------------------------------- #
def define_decline_label(df: pd.DataFrame) -> pd.Series:
    """
    Define a binary decline label.

    A subject is considered to have declined if the drop between baseline
    and follow‑up is **≥ 3 points** on either the MMSE or the MoCA.
    """
    # Prefer MMSE; fall back to MoCA where MMSE is missing
    mmse_drop = df["mmse_baseline"] - df["mmse_followup"]
    moca_drop = df["moca_baseline"] - df["moca_followup"]

    # Both drops may contain NaNs – treat NaN as 0 drop for that instrument
    mmse_drop = mmse_drop.fillna(0)
    moca_drop = moca_drop.fillna(0)

    decline = (mmse_drop >= 3) | (moca_drop >= 3)
    return decline.astype(int)

# --------------------------------------------------------------------------- #
# Collinearity transformer
# --------------------------------------------------------------------------- #
class CollinearityTransformer(BaseEstimator, TransformerMixin):
    """
    Remove one feature from each pair of features whose absolute Pearson
    correlation exceeds ``threshold`` (default 0.95). The feature retained
    from each pair is the one with the higher variance.
    """

    def __init__(self, threshold: float = 0.95):
        self.threshold = threshold
        self.features_to_keep_: List[str] = []

    def fit(self, X: pd.DataFrame, y: Any = None) -> "CollinearityTransformer":
        # Compute correlation matrix
        corr = X.corr().abs()
        # Upper triangle mask to avoid duplicate pairs and self‑correlation
        upper = corr.where(np.triu(np.ones(corr.shape), k=1).astype(bool))

        # Identify pairs above threshold
        to_drop = set()
        for col in upper.columns:
            high_corr = upper[col][upper[col] > self.threshold].index.tolist()
            for row in high_corr:
                if row in to_drop or col in to_drop:
                    continue
                # Compare variances and keep the higher‑variance column
                var_row = X[row].var()
                var_col = X[col].var()
                if var_row >= var_col:
                    to_drop.add(col)
                else:
                    to_drop.add(row)

        self.features_to_keep_ = [c for c in X.columns if c not in to_drop]
        return self

    def transform(self, X: pd.DataFrame) -> pd.DataFrame:
        return X[self.features_to_keep_]

# --------------------------------------------------------------------------- #
# Inner pipeline constructor
# --------------------------------------------------------------------------- #
def make_inner_pipeline(max_features: int = 20) -> Pipeline:
    """
    Build a scikit‑learn Pipeline that performs:

      1. Collinearity filtering
      2. Variance thresholding (threshold=0.01)
      3. Recursive feature elimination (RFE) down to ``max_features`` (or fewer if
         fewer features remain after the previous steps)
      4. RandomForest classification
    """
    # The RFE step will be configured later once we know the number of
    # remaining features; we therefore create a placeholder estimator that will
    # be replaced inside the GridSearchCV ``fit_params``.
    rfe = RFE(
        estimator=RandomForestClassifier(random_state=42, n_jobs=1),
        n_features_to_select=max_features,
    )
    pipeline = Pipeline(
        [
            ("collinearity", CollinearityTransformer(threshold=0.95)),
            ("var_thresh", VarianceThreshold(threshold=0.01)),
            ("rfe", rfe),
            ("clf", RandomForestClassifier(random_state=42, n_jobs=1)),
        ]
    )
    return pipeline

# --------------------------------------------------------------------------- #
# Nested cross‑validation training
# --------------------------------------------------------------------------- #
def train_and_evaluate_nested_cv(
    X: pd.DataFrame,
    y: pd.Series,
    n_outer_folds: int = 5,
    param_grid: Dict[str, List[Any]] | None = None,
    max_features: int = 20,
) -> Tuple[Dict[str, Any], List[Dict[str, Any]]]:
    """
    Perform nested cross‑validation.

    Returns
    -------
    best_overall_params : dict
        Hyper‑parameters of the best model across outer folds (based on mean ROC‑AUC).
    outer_fold_results : list[dict]
        Per‑fold performance metrics and selected hyper‑parameters.
    """
    if param_grid is None:
        param_grid = {
            "rf__n_estimators": [50, 100, 200],
            "rf__max_depth": [5, 10, None],
        }

    outer_cv = StratifiedKFold(n_splits=n_outer_folds, shuffle=True, random_state=42)

    outer_results = []
    best_score = -np.inf
    best_params: Dict[str, Any] = {}

    fold_idx = 1
    for train_idx, test_idx in outer_cv.split(X, y):
        LOGGER.log(
            "info",
            message=f"Outer CV fold {fold_idx}/{n_outer_folds}",
        )
        X_train, X_test = X.iloc[train_idx], X.iloc[test_idx]
        y_train, y_test = y.iloc[train_idx], y.iloc[test_idx]

        # Build pipeline *inside* the outer loop so that feature‑selection
        # does not leak information from the test split.
        pipeline = make_inner_pipeline(max_features=max_features)

        # Adjust RFE ``n_features_to_select`` if fewer features are available
        # after collinearity & variance filtering.
        # We let GridSearchCV handle this implicitly; however, we need to
        # ensure the value is not larger than the number of columns.
        # Therefore we set a sensible upper bound later when fitting.

        grid = GridSearchCV(
            estimator=pipeline,
            param_grid=param_grid,
            cv=StratifiedKFold(n_splits=3, shuffle=True, random_state=42),
            scoring="roc_auc",
            n_jobs=1,  # Respect the 2‑core runner limit
        )
        grid.fit(X_train, y_train)

        # Predict on outer test set
        y_pred_proba = grid.predict_proba(X_test)[:, 1]
        y_pred = grid.predict(X_test)

        roc_auc = roc_auc_score(y_test, y_pred_proba)
        acc = accuracy_score(y_test, y_pred)
        f1 = f1_score(y_test, y_pred)

        outer_results.append(
            {
                "fold": fold_idx,
                "roc_auc": roc_auc,
                "accuracy": acc,
                "f1": f1,
                "best_params": grid.best_params_,
            }
        )

        if roc_auc > best_score:
            best_score = roc_auc
            best_params = grid.best_params_

        fold_idx += 1

    # Summary of mean metrics
    summary = {
        "mean_roc_auc": np.mean([r["roc_auc"] for r in outer_results]),
        "mean_accuracy": np.mean([r["accuracy"] for r in outer_results]),
        "mean_f1": np.mean([r["f1"] for r in outer_results]),
        "best_params_overall": best_params,
    }

    return summary, outer_results

# --------------------------------------------------------------------------- #
# Model persistence
# --------------------------------------------------------------------------- #
def persist_model(
    X: pd.DataFrame,
    y: pd.Series,
    best_params: Dict[str, Any],
    max_features: int = 20,
) -> None:
    """
    Train a final model on the *entire* dataset using the best hyper‑parameters
    discovered during nested CV and persist it to ``data/processed/model.pkl``.
    """
    pipeline = make_inner_pipeline(max_features=max_features)

    # Apply the best hyper‑parameters to the final estimator inside the pipeline
    # (the classifier step is named ``clf``)
    pipeline.set_params(**best_params)

    pipeline.fit(X, y)

    model_path = Path("data/processed/model.pkl")
    model_path.parent.mkdir(parents=True, exist_ok=True)
    joblib.dump(pipeline, model_path)
    LOGGER.log("info", message=f"Model persisted to {model_path}")

# --------------------------------------------------------------------------- #
# Performance report writer
# --------------------------------------------------------------------------- #
def write_performance_report(
    summary: Dict[str, Any],
    outer_results: List[Dict[str, Any]],
) -> None:
    """
    Write a JSON file summarising nested‑CV performance.
    """
    report = {
        "summary": summary,
        "folds": outer_results,
    }
    out_path = Path("data/processed/performance_report.json")
    out_path.parent.mkdir(parents=True, exist_ok=True)
    with out_path.open("w", encoding="utf-8") as f:
        json.dump(report, f, indent=2)
    LOGGER.log("info", message=f"Performance report written to {out_path}")

# --------------------------------------------------------------------------- #
# Main orchestration
# --------------------------------------------------------------------------- #
def main() -> None:
    """
    Entry point for the training script.
    """
    LOGGER.log("info", message="Loading data...")
    data = load_features()
    X = data.drop(columns=["subject_id", "mmse_baseline", "mmse_followup", "moca_baseline", "moca_followup"])
    y = define_decline_label(data)

    LOGGER.log("info", message="Starting nested cross‑validation...")
    summary, outer_results = train_and_evaluate_nested_cv(
        X,
        y,
        n_outer_folds=5,
        max_features=20,
    )

    LOGGER.log("info", message="Persisting final model...")
    persist_model(X, y, best_params=summary["best_params_overall"], max_features=20)

    LOGGER.log("info", message="Writing performance report...")
    write_performance_report(summary, outer_results)

    LOGGER.log(
        "info",
        message=(
            f"Nested CV completed. Mean ROC‑AUC: {summary['mean_roc_auc']:.3f}, "
            f"Mean Accuracy: {summary['mean_accuracy']:.3f}, "
            f"Mean F1: {summary['mean_f1']:.3f}"
        ),
    )

if __name__ == "__main__":
    main()