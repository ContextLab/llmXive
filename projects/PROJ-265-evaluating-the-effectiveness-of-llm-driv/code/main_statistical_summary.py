"""
Generate the statistical summary JSON file from benchmark results.

This script reads the statistical analysis output generated by main_benchmark.py
and produces a clean JSON report containing only p-values and significance flags,
with no narrative text, as required by SC-001 and SC-002.

Input: results/statistical_analysis.json (output of main_benchmark.py)
Output: results/statistical_summary.json
"""
import json
import sys
import logging
from pathlib import Path
from typing import Dict, Any, List, Optional

# Add project root to path for imports
sys.path.insert(0, str(Path(__file__).parent.parent))

from utils.logger import get_logger, log_stage_start, log_stage_complete, log_stage_error

logger = get_logger(__name__)

def load_statistical_results(input_path: Path) -> Dict[str, Any]:
    """Load the statistical analysis results from JSON."""
    if not input_path.exists():
        raise FileNotFoundError(f"Statistical results file not found: {input_path}")
    
    with open(input_path, 'r', encoding='utf-8') as f:
        return json.load(f)

def extract_summary_data(results: Dict[str, Any]) -> Dict[str, Any]:
    """
    Extract only p-values and significance flags from the full statistical results.
    
    The output structure matches the requirements for SC-001/SC-002:
    - No narrative text
    - Only numerical metrics and boolean flags
    """
    summary = {
        "metadata": {
            "total_pairs_analyzed": results.get("metadata", {}).get("total_pairs_analyzed", 0),
            "normality_check_passed": results.get("metadata", {}).get("normality_check_passed", False),
            "test_type_used": results.get("metadata", {}).get("test_type_used", "unknown")
        },
        "stratified_results": {},
        "aggregate_results": {}
    }

    # Process stratified results
    for stratum, data in results.get("stratified_results", {}).items():
        summary["stratified_results"][stratum] = {
            "count": data.get("count", 0),
            "mean_delta": data.get("mean_delta"),
            "std_delta": data.get("std_delta"),
            "p_value": data.get("p_value"),
            "is_significant": data.get("is_significant", False),
            "effect_size": data.get("effect_size")
        }

    # Process aggregate results
    agg = results.get("aggregate_results", {})
    summary["aggregate_results"] = {
        "total_count": agg.get("total_count", 0),
        "overall_mean_delta": agg.get("overall_mean_delta"),
        "overall_std_delta": agg.get("overall_std_delta"),
        "overall_p_value": agg.get("overall_p_value"),
        "overall_is_significant": agg.get("overall_is_significant", False),
        "overall_effect_size": agg.get("overall_effect_size"),
        "bonferroni_corrected_p_value": agg.get("bonferroni_corrected_p_value"),
        "bonferroni_is_significant": agg.get("bonferroni_is_significant", False)
    }

    return summary

def save_statistical_summary(summary: Dict[str, Any], output_path: Path) -> None:
    """Save the summary to a JSON file with proper formatting."""
    output_path.parent.mkdir(parents=True, exist_ok=True)
    
    with open(output_path, 'w', encoding='utf-8') as f:
        json.dump(summary, f, indent=2, ensure_ascii=False)
    
    logger.info(f"Statistical summary saved to {output_path}")

def run_summary_generation(input_path: Path, output_path: Path) -> Dict[str, Any]:
    """
    Main pipeline to generate the statistical summary.
    
    Args:
        input_path: Path to statistical_analysis.json
        output_path: Path where statistical_summary.json will be written
        
    Returns:
        The generated summary dictionary
    """
    log_stage_start(logger, "statistical_summary_generation", {
        "input_file": str(input_path),
        "output_file": str(output_path)
    })

    try:
        # Load results
        logger.info(f"Loading statistical results from {input_path}")
        results = load_statistical_results(input_path)
        
        # Extract summary data
        logger.info("Extracting summary data (p-values and significance flags only)")
        summary = extract_summary_data(results)
        
        # Save output
        logger.info(f"Saving statistical summary to {output_path}")
        save_statistical_summary(summary, output_path)
        
        log_stage_complete(logger, "statistical_summary_generation", {
            "output_file": str(output_path),
            "pairs_analyzed": summary["metadata"]["total_pairs_analyzed"]
        })
        
        return summary
        
    except FileNotFoundError as e:
        log_stage_error(logger, "statistical_summary_generation", str(e))
        raise
    except Exception as e:
        log_stage_error(logger, "statistical_summary_generation", str(e))
        raise

def main():
    """Entry point for the statistical summary generation script."""
    # Define paths relative to project root
    project_root = Path(__file__).parent.parent
    input_file = project_root / "results" / "statistical_analysis.json"
    output_file = project_root / "results" / "statistical_summary.json"
    
    if not input_file.exists():
        logger.error(f"Input file not found: {input_file}")
        logger.error("Please run main_benchmark.py first to generate statistical_analysis.json")
        sys.exit(1)
    
    try:
        run_summary_generation(input_file, output_file)
        logger.info("Statistical summary generation completed successfully")
    except Exception as e:
        logger.error(f"Failed to generate statistical summary: {e}")
        sys.exit(1)

if __name__ == "__main__":
    main()