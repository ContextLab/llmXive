import json
import logging
import os
import sys
from dataclasses import dataclass
from typing import Optional, Tuple

from config import load_config
from utils import setup_logging

@dataclass
class BudgetResult:
    """Container for experiment budget calculation results."""
    mean_step_time_ms: float
    total_steps: int
    estimated_total_time_ms: float
    estimated_total_time_hours: float
    ci_limit_hours: float
    passes_budget: bool
    margin_hours: float
    status: str  # "PASS", "FAIL", "WARNING"

def load_profiling_report(report_path: str) -> dict:
    """Load the profiling report generated by T008a-profile."""
    if not os.path.exists(report_path):
        raise FileNotFoundError(f"Profiling report not found at {report_path}")
    
    with open(report_path, 'r') as f:
        return json.load(f)

def calculate_experiment_budget(
    profiling_report: dict, 
    trial_count: int, 
    ci_limit_hours: float = 6.0
) -> BudgetResult:
    """
    Calculate total experiment time based on profiling results.
    
    Args:
        profiling_report: Dict containing 'mean_step_time_ms' from T008a-profile
        trial_count: Number of trials (from config, default 50)
        ci_limit_hours: CI time limit in hours (default 6.0)
        
    Returns:
        BudgetResult with validation status
    """
    # Extract mean step time from profiling report
    mean_step_time_ms = profiling_report.get('mean_step_time_ms')
    if mean_step_time_ms is None:
        raise ValueError("Profiling report missing 'mean_step_time_ms'")
    
    # Estimate total steps: trials * steps_per_trial
    # Assuming ~100 steps per trial based on typical RL trajectories
    steps_per_trial = 100
    total_steps = trial_count * steps_per_trial
    
    # Calculate total time
    estimated_total_time_ms = mean_step_time_ms * total_steps
    estimated_total_time_hours = estimated_total_time_ms / (1000 * 60 * 60)
    
    # Check against CI limit
    passes_budget = estimated_total_time_hours <= ci_limit_hours
    margin_hours = ci_limit_hours - estimated_total_time_hours
    
    # Determine status
    if passes_budget and margin_hours > 1.0:
        status = "PASS"
    elif passes_budget:
        status = "WARNING"  # Close to limit
    else:
        status = "FAIL"
    
    return BudgetResult(
        mean_step_time_ms=mean_step_time_ms,
        total_steps=total_steps,
        estimated_total_time_ms=estimated_total_time_ms,
        estimated_total_time_hours=estimated_total_time_hours,
        ci_limit_hours=ci_limit_hours,
        passes_budget=passes_budget,
        margin_hours=margin_hours,
        status=status
    )

def save_validation_report(result: BudgetResult, output_path: str):
    """Save the budget validation result to JSON."""
    report_data = {
        "mean_step_time_ms": result.mean_step_time_ms,
        "total_steps": result.total_steps,
        "estimated_total_time_ms": result.estimated_total_time_ms,
        "estimated_total_time_hours": result.estimated_total_time_hours,
        "ci_limit_hours": result.ci_limit_hours,
        "passes_budget": result.passes_budget,
        "margin_hours": result.margin_hours,
        "status": result.status,
        "validation_message": f"Experiment time: {result.estimated_total_time_hours:.2f}h / {result.ci_limit_hours}h limit. Status: {result.status}"
    }
    
    with open(output_path, 'w') as f:
        json.dump(report_data, f, indent=2)

def main():
    """Main entry point for experiment time validation."""
    logger = setup_logging("experiment_time_validator")
    
    try:
        # Load configuration
        config = load_config()
        trial_count = config.experiment.trial_count
        ci_limit_hours = config.experiment.ci_time_limit_hours
        
        # Load profiling report from T008a-profile
        profiling_path = "data/results/profiling_report.json"
        logger.info(f"Loading profiling report from {profiling_path}")
        profiling_report = load_profiling_report(profiling_path)
        
        # Calculate budget
        logger.info(f"Calculating experiment budget for {trial_count} trials")
        result = calculate_experiment_budget(
            profiling_report, 
            trial_count, 
            ci_limit_hours
        )
        
        # Save validation report
        output_path = "data/results/experiment_budget_validation.json"
        logger.info(f"Saving validation report to {output_path}")
        save_validation_report(result, output_path)
        
        # Log results
        logger.info(f"Mean step time: {result.mean_step_time_ms:.2f}ms")
        logger.info(f"Total estimated time: {result.estimated_total_time_hours:.2f} hours")
        logger.info(f"CI limit: {result.ci_limit_hours} hours")
        logger.info(f"Status: {result.status}")
        
        if not result.passes_budget:
            logger.error(f"EXCEEDS BUDGET: {result.estimated_total_time_hours:.2f}h > {result.ci_limit_hours}h")
            sys.exit(1)
        else:
            logger.info("Budget validation PASSED")
            sys.exit(0)
            
    except FileNotFoundError as e:
        logger.error(f"Required file not found: {e}")
        sys.exit(1)
    except ValueError as e:
        logger.error(f"Invalid data in profiling report: {e}")
        sys.exit(1)
    except Exception as e:
        logger.error(f"Unexpected error: {e}")
        sys.exit(1)

if __name__ == "__main__":
    main()
