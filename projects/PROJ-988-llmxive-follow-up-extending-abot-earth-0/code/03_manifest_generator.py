"""
T017: Save aligned pairs, patches, and degraded scenes to data/processed/ with checksums.

This script scans the data/processed directory for artifacts generated by previous
tasks (T012, T013, T014a, T014b) and generates a unified manifest file (data/manifest.json)
containing file paths, sizes, and SHA256 checksums for integrity verification.
"""
import os
import sys
import json
import hashlib
import logging
import csv
from pathlib import Path
from typing import Dict, List, Any, Optional
from datetime import datetime

# Project structure constants
PROJECT_ROOT = Path(__file__).resolve().parent.parent
DATA_PROCESSED_DIR = PROJECT_ROOT / "data" / "processed"
DATA_RAW_DIR = PROJECT_ROOT / "data" / "raw"
OUTPUT_MANIFEST_PATH = PROJECT_ROOT / "data" / "manifest.json"

# Expected artifact directories based on previous tasks
ARTIFACT_DIRS = [
    "aligned_pairs",       # From T012
    "patches_100m2",       # From T013
    "degraded_base",       # From T014a
    "nnf_varied_scenes",   # From T014b
    "real_cloud_masks_subset" # From T015 (reference data)
]

# Expected manifest files to include in the summary
EXISTING_MANIFESTS = [
    "aligned_pairs.csv",
    "patch_manifest.csv",
    "degraded_manifest.json"
]

logger = logging.getLogger(__name__)

def calculate_sha256(file_path: Path) -> str:
    """Calculate SHA256 checksum of a file."""
    sha256_hash = hashlib.sha256()
    try:
        with open(file_path, "rb") as f:
            for byte_block in iter(lambda: f.read(4096), b""):
                sha256_hash.update(byte_block)
        return sha256_hash.hexdigest()
    except Exception as e:
        logger.error(f"Failed to calculate checksum for {file_path}: {e}")
        return "ERROR"

def get_file_info(file_path: Path) -> Dict[str, Any]:
    """Get file metadata including size and checksum."""
    try:
        stat = file_path.stat()
        return {
            "path": str(file_path.relative_to(PROJECT_ROOT)),
            "size_bytes": stat.st_size,
            "checksum_sha256": calculate_sha256(file_path),
            "modified_time": datetime.fromtimestamp(stat.st_mtime).isoformat()
        }
    except Exception as e:
        logger.error(f"Error processing file {file_path}: {e}")
        return None

def scan_directory(dir_path: Path, file_extensions: Optional[List[str]] = None) -> List[Dict[str, Any]]:
    """Recursively scan a directory for files and collect metadata."""
    files_info = []
    if not dir_path.exists():
        logger.warning(f"Directory does not exist: {dir_path}")
        return files_info

    for root, _, files in os.walk(dir_path):
        for file in files:
            file_path = Path(root) / file
            
            # Filter by extension if specified
            if file_extensions and not any(file.endswith(ext) for ext in file_extensions):
                continue
            
            info = get_file_info(file_path)
            if info:
                files_info.append(info)
    
    return files_info

def load_existing_manifests() -> Dict[str, Any]:
    """Load and summarize existing manifest files."""
    manifests_summary = {}
    
    for manifest_name in EXISTING_MANIFESTS:
        # Check common locations
        possible_paths = [
            DATA_PROCESSED_DIR / manifest_name,
            DATA_PROCESSED_DIR / "aligned_pairs" / manifest_name,
            DATA_PROCESSED_DIR / "patches_100m2" / manifest_name,
            DATA_PROCESSED_DIR / "degraded_base" / manifest_name
        ]
        
        for path in possible_paths:
            if path.exists():
                try:
                    if path.suffix == '.csv':
                        with open(path, 'r', encoding='utf-8') as f:
                            reader = csv.DictReader(f)
                            rows = list(reader)
                            manifests_summary[manifest_name] = {
                                "path": str(path.relative_to(PROJECT_ROOT)),
                                "record_count": len(rows),
                                "columns": list(rows[0].keys()) if rows else []
                            }
                    elif path.suffix == '.json':
                        with open(path, 'r', encoding='utf-8') as f:
                            data = json.load(f)
                            manifests_summary[manifest_name] = {
                                "path": str(path.relative_to(PROJECT_ROOT)),
                                "type": "json",
                                "size_bytes": path.stat().st_size
                            }
                except Exception as e:
                    logger.error(f"Error loading manifest {path}: {e}")
                break
    
    return manifests_summary

def generate_manifest() -> Dict[str, Any]:
    """Generate the complete manifest for all processed data."""
    logger.info("Starting manifest generation...")
    
    manifest = {
        "version": "1.0",
        "generated_at": datetime.now().isoformat(),
        "project_root": str(PROJECT_ROOT.relative_to(PROJECT_ROOT)),
        "summary": {
            "total_files": 0,
            "total_size_bytes": 0,
            "directories_scanned": []
        },
        "artifacts": {
            "aligned_pairs": [],
            "patches_100m2": [],
            "degraded_base": [],
            "nnf_varied_scenes": [],
            "real_cloud_masks_subset": []
        },
        "existing_manifests": load_existing_manifests()
    }

    total_files = 0
    total_size = 0

    for dir_name in ARTIFACT_DIRS:
        dir_path = DATA_PROCESSED_DIR / dir_name
        if dir_path.exists():
            logger.info(f"Scanning directory: {dir_path}")
            files = scan_directory(dir_path)
            
            if dir_name in manifest["artifacts"]:
                manifest["artifacts"][dir_name] = files
            
            total_files += len(files)
            total_size += sum(f.get("size_bytes", 0) for f in files)
            manifest["summary"]["directories_scanned"].append(str(dir_path.relative_to(PROJECT_ROOT)))
        else:
            logger.warning(f"Skipping non-existent directory: {dir_path}")

    manifest["summary"]["total_files"] = total_files
    manifest["summary"]["total_size_bytes"] = total_size

    logger.info(f"Manifest generation complete. Total files: {total_files}, Total size: {total_size} bytes")
    return manifest

def save_manifest(manifest: Dict[str, Any], output_path: Path):
    """Save the manifest to a JSON file."""
    try:
        output_path.parent.mkdir(parents=True, exist_ok=True)
        with open(output_path, 'w', encoding='utf-8') as f:
            json.dump(manifest, f, indent=2)
        logger.info(f"Manifest saved to {output_path}")
        
        # Verify the saved file
        if output_path.exists():
            checksum = calculate_sha256(output_path)
            logger.info(f"Manifest checksum: {checksum}")
    except Exception as e:
        logger.error(f"Failed to save manifest: {e}")
        raise

def main():
    """Main entry point for the manifest generator."""
    # Setup logging
    logging.basicConfig(
        level=logging.INFO,
        format='%(asctime)s - %(name)s - %(levelname)s - %(message)s',
        handlers=[
            logging.StreamHandler(sys.stdout),
            logging.FileHandler(PROJECT_ROOT / "data" / "results" / "manifest_generation.log")
        ]
    )

    try:
        logger.info("T017 Manifest Generator started")
        
        # Generate manifest
        manifest = generate_manifest()
        
        # Save manifest
        save_manifest(manifest, OUTPUT_MANIFEST_PATH)
        
        logger.info("T017 Manifest Generator completed successfully")
        return 0
        
    except Exception as e:
        logger.error(f"T017 Manifest Generator failed: {e}", exc_info=True)
        return 1

if __name__ == "__main__":
    sys.exit(main())