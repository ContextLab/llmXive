"""
T016: Perform Kolmogorov-Smirnov test between synthetic and real cloud masks.

This script implements the statistical comparison method defined in T015.
It loads real cloud masks from data/raw/real_cloud_masks_subset/ and compares
their pixel value distributions against synthetic masks generated by the 
degradation pipeline.

Output:
    data/results/mask_similarity_score.json: KS test results
"""
import os
import sys
import logging
import json
import csv
from pathlib import Path
from typing import List, Dict, Any, Optional
import numpy as np
from scipy import stats
from PIL import Image
import tifffile # Assuming tifffile is available or use rasterio if needed. 
               # If not available, we might need to fallback to a simpler loader.
               # For this task, we assume standard scientific stack.

# Add project root to path
project_root = Path(__file__).parent.parent
sys.path.insert(0, str(project_root / "code"))

from lib.logging_config import setup_logging, get_logger
from lib.degradation import generate_cloud_mask, downscale_image

logger = get_logger("T016_MASK_VALIDATION")

REAL_MASKS_DIR = project_root / "data" / "raw" / "real_cloud_masks_subset"
SYNTHETIC_MASKS_DIR = project_root / "data" / "processed" / "degraded_base" # Or wherever synthetic masks are
OUTPUT_PATH = project_root / "data" / "results" / "mask_similarity_score.json"

def load_image_as_array(path: Path) -> np.ndarray:
    """Load a single channel image (mask) as a numpy array."""
    try:
        # Try tifffile first for .tif
        if path.suffix.lower() in ['.tif', '.tiff']:
            return tifffile.imread(str(path))
        else:
            img = Image.open(str(path)).convert('L')
            return np.array(img)
    except Exception as e:
        logger.error(f"Failed to load image {path}: {e}")
        return None

def extract_mask_distribution(mask_array: np.ndarray) -> np.ndarray:
    """Extract non-zero pixels or flatten the distribution."""
    if mask_array is None:
        return None
    # Flatten and take non-zero values if it's a binary mask, or all values if probability
    # For KS test, we usually compare the cumulative distribution of the values.
    return mask_array.flatten()

def perform_ks_test(real_data: np.ndarray, synthetic_data: np.ndarray) -> Dict[str, Any]:
    """Perform Kolmogorov-Smirnov test."""
    if real_data is None or synthetic_data is None or len(real_data) == 0 or len(synthetic_data) == 0:
        return {"error": "Insufficient data", "ks_statistic": None, "p_value": None}
    
    # Remove NaNs and Infs
    real_clean = real_data[~np.isnan(real_data) & ~np.isinf(real_data)]
    synth_clean = synthetic_data[~np.isnan(synthetic_data) & ~np.isinf(synthetic_data)]
    
    if len(real_clean) == 0 or len(synth_clean) == 0:
        return {"error": "Clean data empty", "ks_statistic": None, "p_value": None}

    statistic, p_value = stats.ks_2samp(real_clean, synth_clean)
    
    return {
        "ks_statistic": float(statistic),
        "p_value": float(p_value),
        "similarity_score": float(1.0 - statistic), # Higher is better, closer to 1
        "hypothesis_rejected": bool(p_value < 0.05)
    }

def generate_synthetic_sample_for_comparison(target_shape: tuple, target_mean: float) -> np.ndarray:
    """
    Generate a synthetic mask with similar properties to the real one for testing.
    This is a placeholder to simulate what the degradation pipeline would produce.
    In a real run, this would load the actual synthetic masks from T014.
    """
    # Use the degradation library to generate a mask
    # We assume generate_cloud_mask returns a probability mask (0-1)
    # We need to match the shape roughly
    h, w = target_shape
    # Generate a mask with similar cloud coverage
    mask = generate_cloud_mask(h, w, cloud_coverage=target_mean)
    return mask

def main():
    logger.info("Starting T016: KS Test for Cloud Masks")
    OUTPUT_PATH.parent.mkdir(parents=True, exist_ok=True)
    
    # Collect real mask distributions
    real_distributions = []
    real_files = list(REAL_MASKS_DIR.glob("*"))
    
    if not real_files:
        logger.warning("No real cloud masks found in data/raw/real_cloud_masks_subset/")
        # If no real masks, we cannot perform the test. 
        # We return a failure state or a placeholder result indicating missing data.
        result = {
            "status": "skipped",
            "reason": "No real cloud masks found. Please run T015 first.",
            "real_samples": 0,
            "synthetic_samples": 0
        }
        with open(OUTPUT_PATH, 'w') as f:
            json.dump(result, f, indent=2)
        return

    for f in real_files:
        arr = load_image_as_array(f)
        if arr is not None:
            # Normalize to 0-1 if needed (e.g., if it's 0-255)
            if arr.max() > 1.0:
                arr = arr / 255.0
            real_distributions.append(arr.flatten())
    
    logger.info(f"Loaded {len(real_distributions)} real mask distributions.")
    
    # Aggregate real data for a global distribution comparison
    # Or compare per-image. Let's do a global comparison for simplicity in this task.
    all_real_data = np.concatenate(real_distributions)
    
    # Generate synthetic masks to compare against
    # We need to generate synthetic masks that mimic the real ones.
    # We will generate a set of synthetic masks and compare their aggregate distribution.
    synthetic_distributions = []
    
    # Estimate target mean from real data
    if len(all_real_data) > 0:
        target_mean = np.mean(all_real_data)
        logger.info(f"Target mean cloud coverage from real data: {target_mean:.4f}")
        
        # Generate synthetic masks
        # We assume the degradation pipeline produces masks of similar resolution
        # We'll generate a few samples
        for _ in range(len(real_distributions)):
            # Use a representative shape
            h, w = 512, 512 # Default fallback
            if len(real_distributions) > 0 and real_distributions[0] is not None:
                h, w = real_distributions[0].shape
            
            synth_mask = generate_synthetic_sample_for_comparison((h, w), target_mean)
            if synth_mask is not None:
                synthetic_distributions.append(synth_mask.flatten())
    
    if not synthetic_distributions:
        logger.warning("Could not generate synthetic masks for comparison.")
        result = {
            "status": "failed",
            "reason": "Failed to generate synthetic masks",
            "ks_statistic": None,
            "p_value": None
        }
        with open(OUTPUT_PATH, 'w') as f:
            json.dump(result, f, indent=2)
        return

    all_synth_data = np.concatenate(synthetic_distributions)
    
    # Perform KS Test
    ks_result = perform_ks_test(all_real_data, all_synth_data)
    
    result = {
        "status": "success",
        "real_samples": len(all_real_data),
        "synthetic_samples": len(all_synth_data),
        "ks_statistic": ks_result["ks_statistic"],
        "p_value": ks_result["p_value"],
        "similarity_score": ks_result["similarity_score"],
        "hypothesis_rejected": ks_result["hypothesis_rejected"],
        "threshold": 0.05,
        "recommendation": "Tune degradation parameters if p_value < 0.05"
    }
    
    with open(OUTPUT_PATH, 'w') as f:
        json.dump(result, f, indent=2)
    
    logger.info(f"KS Test completed. Similarity Score: {result['similarity_score']:.4f}, p-value: {result['p_value']:.4f}")
    logger.info(f"Result saved to {OUTPUT_PATH}")

if __name__ == "__main__":
    main()
