from pydantic import BaseModel, Field, field_validator
from typing import Literal, Optional
import json

ModificationType = Literal[
    "add_layer",
    "remove_layer",
    "increase_heads",
    "decrease_heads",
    "increase_hidden_dim",
    "decrease_hidden_dim",
    "increase_vocab",
    "decrease_vocab",
    "change_activation",
    "change_dropout",
    "add_residual_branch",
    "prune_parameters"
]

class ModificationProposal(BaseModel):
    """
    Schema for an architectural modification proposal generated by the model.
    
    This schema enforces strict typing and validation for any proposed change
    to the model architecture, ensuring that modifications are well-defined,
    quantifiable, and trackable across refinement cycles.
    """
    modification_type: ModificationType = Field(
        ...,
        description="The type of architectural modification being proposed."
    )
    magnitude: float = Field(
        ...,
        ge=0.0,
        description="The magnitude of the change (e.g., number of layers to add, percentage change in hidden dim)."
    )
    rationale: str = Field(
        ...,
        min_length=1,
        max_length=1000,
        description="A textual explanation of why this modification is proposed, based on current performance metrics."
    )
    estimated_param_count: int = Field(
        ...,
        gt=0,
        description="The estimated total number of parameters after applying this modification."
    )

    @field_validator('rationale')
    @classmethod
    def validate_rationale(cls, v: str) -> str:
        if not v.strip():
            raise ValueError("Rationale cannot be empty or whitespace only.")
        return v.strip()

    def to_json(self) -> str:
        """Serialize the proposal to a JSON string."""
        return self.model_dump_json(indent=2)

    @classmethod
    def from_json(cls, json_str: str) -> "ModificationProposal":
        """Deserialize a proposal from a JSON string."""
        return cls.model_validate_json(json_str)