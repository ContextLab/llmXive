from pydantic import BaseModel, Field, field_validator
from typing import Literal, Optional
import json


class ModificationProposal(BaseModel):
    """
    Schema for an architectural modification proposal generated by the LLM.
    
    This schema defines the structure of a self-proposed change to the model's
    architecture, ensuring that modifications are typed, bounded, and justified.
    """
    modification_type: Literal[
        "add_layer",
        "remove_layer",
        "increase_heads",
        "decrease_heads",
        "increase_hidden_dim",
        "decrease_hidden_dim",
        "increase_vocab",
        "decrease_vocab",
        "change_activation",
        "change_dropout",
        "add_residual_connection",
        "remove_residual_connection"
    ] = Field(
        ...,
        description="The type of architectural modification to apply."
    )
    
    magnitude: float = Field(
        ...,
        ge=0.0,
        le=1.0,
        description="The magnitude of the change (0.0 to 1.0). "
                    "e.g., 0.5 for 'add 50% of a layer' or 'increase heads by 50%'."
    )
    
    rationale: str = Field(
        ...,
        min_length=1,
        max_length=1000,
        description="A natural language explanation of why this modification is proposed."
    )
    
    estimated_param_count: int = Field(
        ...,
        gt=0,
        description="The estimated total parameter count after applying this modification."
    )
    
    @field_validator('rationale')
    @classmethod
    def validate_rationale_not_empty(cls, v: str) -> str:
        if not v.strip():
            raise ValueError("Rationale cannot be empty or whitespace only.")
        return v

    def to_json(self, indent: int = 2) -> str:
        """Serialize the proposal to a JSON string."""
        return self.model_dump_json(indent=indent)

    @classmethod
    def from_json(cls, json_str: str) -> "ModificationProposal":
        """Deserialize a JSON string into a ModificationProposal."""
        return cls.model_validate_json(json_str)