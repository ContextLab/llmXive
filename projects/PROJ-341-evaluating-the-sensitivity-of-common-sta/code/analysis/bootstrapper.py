def load_simulated_power_distribution(filepath: str = "data/simulation/error_rates_summary.csv") -> pd.DataFrame:
    """
    Load the simulated error rates to compare against real data power.
    We treat (1 - Type II error rate) as Power for the alternative hypothesis conditions.
    """
    if not os.path.exists(filepath):
        raise FileNotFoundError(f"Simulated error rates file not found at {filepath}. "
                                "Run the main simulation first.")
    return pd.read_csv(filepath)

def bootstrap_power_estimate(p_values: pd.Series, alpha: float = 0.05, n_bootstrap: int = 1000) -> Dict[str, Any]:
    """
    Calculate bootstrapped power estimate from a series of p-values.
    Power is estimated as the proportion of tests where p < alpha.
    Returns mean power, confidence interval, and standard error.
    """
    if len(p_values) == 0:
        raise ValueError("Cannot bootstrap power from empty p-value series.")

    n = len(p_values)
    bootstrap_powers = []

    for _ in range(n_bootstrap):
        # Resample with replacement
        sample = p_values.sample(n=n, replace=True, random_state=np.random.randint(0, 2**31))
        # Calculate power for this bootstrap sample (proportion of significant results)
        power = (sample < alpha).mean()
        bootstrap_powers.append(power)

    bootstrap_powers = np.array(bootstrap_powers)
    mean_power = np.mean(bootstrap_powers)
    std_error = np.std(bootstrap_powers, ddof=1)
    ci_lower, ci_upper = np.percentile(bootstrap_powers, [2.5, 97.5])

    return {
        "mean_power": mean_power,
        "std_error": std_error,
        "ci_95_lower": ci_lower,
        "ci_95_upper": ci_upper,
        "n_bootstrap": n_bootstrap,
        "n_original": n
    }

def calculate_ks_distance(simulated_pvals: np.ndarray, real_pvals: np.ndarray) -> float:
    """
    Calculate the Kolmogorov-Smirnov distance between two distributions of p-values.
    """
    if len(simulated_pvals) == 0 or len(real_pvals) == 0:
        raise ValueError("KS distance requires non-empty p-value arrays.")
    
    # Use scipy.stats.ks_2samp
    statistic, _ = stats.ks_2samp(simulated_pvals, real_pvals)
    return float(statistic)

def run_bootstrapped_validation(
    real_data_file: str = "data/simulation/real_data_pvalues.csv",
    simulated_data_file: str = "data/simulation/p_values_raw.csv",
    alpha: float = 0.05,
    n_bootstrap: int = 1000,
    ks_threshold: float = 0.10
) -> Dict[str, Any]:
    """
    Main function to run bootstrapped power estimation and KS distance validation.
    """
    # Load real data p-values
    real_df = load_real_data_pvalues(real_data_file)
    
    # Load simulated p-values
    sim_df = load_p_values_raw(simulated_data_file)

    results = {
        "alpha": alpha,
        "n_bootstrap": n_bootstrap,
        "ks_threshold": ks_threshold,
        "validation_status": "pending",
        "details": []
    }

    # Group by test type and sample size to compare like-with-like
    # We assume real data has columns: test_type, sample_size, p_value
    # And simulated data has: test_type, sample_size, p_value (and others)
    
    real_groups = real_df.groupby(["test_type", "sample_size"])
    sim_groups = sim_df.groupby(["test_type", "sample_size"])

    for (test_type, sample_size), real_group in real_groups:
        real_pvals = real_group["p_value"].values
        
        # Find corresponding simulated data
        sim_mask = (sim_df["test_type"] == test_type) & (sim_df["sample_size"] == sample_size)
        if not sim_mask.any():
            # If exact sample size not found in simulation, skip or interpolate?
            # For now, skip to avoid hallucination
            continue
        
        sim_pvals = sim_df.loc[sim_mask, "p_value"].values

        if len(sim_pvals) == 0:
            continue

        # 1. Bootstrap Power Estimate on Real Data
        power_stats = bootstrap_power_estimate(real_pvals, alpha=alpha, n_bootstrap=n_bootstrap)
        
        # 2. KS Distance
        ks_dist = calculate_ks_distance(sim_pvals, real_pvals)
        
        # 3. Verification against threshold
        passed = ks_dist <= ks_threshold
        
        detail = {
            "test_type": test_type,
            "sample_size": int(sample_size),
            "real_n": len(real_pvals),
            "sim_n": len(sim_pvals),
            "power_estimate": power_stats["mean_power"],
            "power_ci_95": [power_stats["ci_95_lower"], power_stats["ci_95_upper"]],
            "ks_distance": ks_dist,
            "ks_threshold": ks_threshold,
            "passed_threshold": passed
        }
        
        results["details"].append(detail)
        if not passed:
            results["validation_status"] = "failed"
        elif results["validation_status"] == "pending":
            results["validation_status"] = "passed"

    # If no matches found, mark as failed with reason
    if results["validation_status"] == "pending":
        results["validation_status"] = "failed"
        results["reason"] = "No matching sample sizes found between real and simulated data."

    return results

