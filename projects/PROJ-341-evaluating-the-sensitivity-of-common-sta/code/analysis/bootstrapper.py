import json
import os
from typing import Dict, Any, List, Tuple
import numpy as np
import pandas as pd
from scipy import stats
from code.simulation.logging_config import get_logger

logger = get_logger(__name__)

# Constants
BOOTSTRAP_ITERATIONS = 1000
KS_DISTANCE_THRESHOLD = 0.10

def load_real_data_pvalues(filepath: str = "data/simulation/real_data_pvalues.csv") -> pd.DataFrame:
    """
    Load the real data p-values generated by T031.
    Raises FileNotFoundError if the file does not exist.
    """
    if not os.path.exists(filepath):
        raise FileNotFoundError(f"Real data p-values file not found: {filepath}. "
                                "Ensure T031 has run successfully.")
    df = pd.read_csv(filepath)
    logger.info(f"Loaded {len(df)} rows from {filepath}")
    return df

def load_simulated_power_distribution(filepath: str = "data/simulation/error_rates_summary.csv") -> pd.DataFrame:
    """
    Load the simulated error rates to derive the expected power distribution.
    We assume the CSV has columns: sample_size, test_type, effect_size, type1_rate, type2_rate, power.
    If 'power' is missing, calculate it as 1 - type2_rate.
    """
    if not os.path.exists(filepath):
        raise FileNotFoundError(f"Simulated error rates file not found: {filepath}. "
                                "Ensure T017/T018 has run successfully.")
    df = pd.read_csv(filepath)
    
    # Ensure power column exists
    if 'power' not in df.columns:
        if 'type2_rate' in df.columns:
            df['power'] = 1.0 - df['type2_rate']
        else:
            raise ValueError("Simulated data file missing 'power' or 'type2_rate' column.")
    
    logger.info(f"Loaded simulated power data for {len(df)} conditions")
    return df

def bootstrap_power_estimate(p_values: pd.Series, alpha: float = 0.05, n_iterations: int = BOOTSTRAP_ITERATIONS) -> Dict[str, Any]:
    """
    Perform bootstrapped power estimation on a set of p-values.
    Power is estimated as the proportion of p-values < alpha in the bootstrap distribution.
    """
    n = len(p_values)
    if n == 0:
        return {"power": np.nan, "ci_lower": np.nan, "ci_upper": np.nan}
    
    bootstrap_powers = []
    
    for _ in range(n_iterations):
        # Resample with replacement
        sample = p_values.sample(n=n, replace=True, random_state=np.random.randint(0, 2**31))
        # Estimate power for this bootstrap sample
        power_est = (sample < alpha).mean()
        bootstrap_powers.append(power_est)
    
    bootstrap_powers = np.array(bootstrap_powers)
    
    # Calculate 95% Confidence Interval using percentiles
    ci_lower = np.percentile(bootstrap_powers, 2.5)
    ci_upper = np.percentile(bootstrap_powers, 97.5)
    mean_power = np.mean(bootstrap_powers)
    
    return {
        "power": float(mean_power),
        "ci_lower": float(ci_lower),
        "ci_upper": float(ci_upper),
        "n_samples": n,
        "n_iterations": n_iterations
    }

def calculate_ks_distance(real_pvalues: pd.Series, simulated_pvalues: pd.Series) -> float:
    """
    Calculate the Kolmogorov-Smirnov distance between real and simulated p-value distributions.
    """
    if len(real_pvalues) == 0 or len(simulated_pvalues) == 0:
        return np.nan
    
    # KS test
    ks_stat, _ = stats.ks_2samp(real_pvalues, simulated_pvalues)
    return float(ks_stat)

def run_bootstrapped_validation(real_df: pd.DataFrame, simulated_df: pd.DataFrame, alpha: float = 0.05) -> List[Dict[str, Any]]:
    """
    Run bootstrapped power estimation for each unique condition in the real data
    and compare against simulated predictions.
    
    Returns a list of dictionaries containing the results for each condition.
    """
    results = []
    
    # Group real data by test_type, sample_size, effect_size (or similar identifiers)
    # Assuming real_df has columns: test_type, sample_size, effect_size, p_value
    # Adjust column names if the real data structure differs
    group_cols = ['test_type', 'sample_size', 'effect_size']
    
    # Ensure columns exist
    missing_cols = [c for c in group_cols if c not in real_df.columns]
    if missing_cols:
        raise ValueError(f"Real data missing required columns: {missing_cols}")
    
    for (test_type, sample_size, effect_size), group in real_df.groupby(group_cols):
        real_pvals = group['p_value']
        
        # Get corresponding simulated power prediction
        sim_row = simulated_df[
            (simulated_df['test_type'] == test_type) &
            (simulated_df['sample_size'] == sample_size) &
            (simulated_df['effect_size'] == effect_size)
        ]
        
        if sim_row.empty:
            logger.warning(f"No simulated prediction found for {test_type}, n={sample_size}, eff={effect_size}")
            predicted_power = None
        else:
            # Take the first match if multiple exist (e.g. different seeds)
            predicted_power = sim_row['power'].iloc[0]
        
        # Bootstrap power estimation
        boot_stats = bootstrap_power_estimate(real_pvals, alpha=alpha)
        
        # Calculate KS distance if we have simulated p-values (we don't have a direct simulated p-value list here,
        # but we can compare the distribution of real p-values to a theoretical uniform or the aggregated simulated behavior).
        # Since we don't have the raw simulated p-values per condition easily accessible here without re-running simulation,
        # we will calculate KS distance against a Uniform(0,1) distribution as a baseline for null,
        # OR we can skip KS if we strictly only have summary stats.
        # However, the task asks for KS distance against "simulated predictions".
        # To be rigorous, we need the distribution of simulated p-values for this condition.
        # If we can't load raw simulated p-values per condition, we approximate KS distance using the power estimate difference
        # or we assume the simulated_df contains the necessary distribution info.
        # Given the constraints, we will calculate KS distance between the REAL p-values and a theoretical distribution
        # implied by the predicted power (e.g., if power is high, p-values should be skewed low).
        # A simpler, robust interpretation for this specific task context:
        # Compare the empirical CDF of real p-values to the empirical CDF of p-values generated in the simulation for this condition.
        # Since we don't have the raw simulated p-values loaded here, we will skip the KS calculation or set it to NaN if not available,
        # but the task requires it.
        # Let's assume we can generate a theoretical distribution based on the predicted power.
        # Actually, the most direct way is to load the raw simulated p-values if T016 produced them.
        # Let's try to load raw simulated p-values if available.
        
        ks_dist = np.nan
        simulated_pvals_for_ks = None
        
        # Attempt to load raw simulated p-values for KS comparison
        try:
            raw_sim_path = "data/simulation/p_values_raw.csv"
            if os.path.exists(raw_sim_path):
                raw_sim = pd.read_csv(raw_sim_path)
                sim_group = raw_sim[
                    (raw_sim['test_type'] == test_type) &
                    (raw_sim['sample_size'] == sample_size) &
                    (raw_sim['effect_size'] == effect_size)
                ]['p_value']
                if len(sim_group) > 0:
                    simulated_pvals_for_ks = sim_group
                    ks_dist = calculate_ks_distance(real_pvals, sim_group)
        except Exception as e:
            logger.warning(f"Could not load raw simulated p-values for KS distance: {e}")
        
        result = {
            "test_type": test_type,
            "sample_size": int(sample_size),
            "effect_size": float(effect_size),
            "real_power": boot_stats['power'],
            "real_power_ci_lower": boot_stats['ci_lower'],
            "real_power_ci_upper": boot_stats['ci_upper'],
            "predicted_power": predicted_power,
            "ks_distance": ks_dist,
            "ks_threshold_met": ks_dist <= KS_DISTANCE_THRESHOLD if not np.isnan(ks_dist) else False,
            "n_real_samples": len(real_pvals)
        }
        results.append(result)
    
    return results

def save_power_results(results: List[Dict[str, Any]], filepath: str = "data/simulation/real_data_power.json"):
    """
    Save the bootstrapped power estimation results to a JSON file.
    """
    os.makedirs(os.path.dirname(filepath), exist_ok=True)
    with open(filepath, 'w') as f:
        json.dump(results, f, indent=2)
    logger.info(f"Saved power results to {filepath}")

def main():
    """
    Main entry point for T032: Bootstrapped Power Estimation.
    """
    logger.info("Starting T032: Bootstrapped Power Estimation")
    
    try:
        # 1. Load real data p-values (from T031)
        real_df = load_real_data_pvalues()
        
        # 2. Load simulated power distribution (from T017/T018)
        sim_df = load_simulated_power_distribution()
        
        # 3. Run bootstrapped validation
        results = run_bootstrapped_validation(real_df, sim_df)
        
        # 4. Save results
        save_power_results(results)
        
        logger.info("T032 completed successfully.")
        
        # Print summary
        print(f"Processed {len(results)} conditions.")
        met_count = sum(1 for r in results if r['ks_threshold_met'])
        print(f"KS Distance <= {KS_DISTANCE_THRESHOLD} for {met_count}/{len(results)} conditions.")
        
    except FileNotFoundError as e:
        logger.error(f"Data file missing: {e}")
        raise
    except Exception as e:
        logger.error(f"Error during bootstrapping: {e}")
        raise

if __name__ == "__main__":
    main()
