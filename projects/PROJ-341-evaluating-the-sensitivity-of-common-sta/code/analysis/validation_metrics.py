import os
import json
import pandas as pd
from typing import Dict, Any, Optional, List
from datetime import datetime
from code.analysis.bootstrapper import load_simulated_power_distribution, calculate_ks_distance

def load_real_data_pvalues(filepath: str = "data/simulation/real_data_pvalues.csv") -> pd.DataFrame:
    """
    Load the real data p-values generated by T031.
    """
    if not os.path.exists(filepath):
        raise FileNotFoundError(f"Required file not found: {filepath}. Run T031 first.")
    return pd.read_csv(filepath)

def load_power_results(filepath: str = "data/simulation/real_data_power.json") -> Dict[str, Any]:
    """
    Load the bootstrapped power results generated by T032.
    """
    if not os.path.exists(filepath):
        raise FileNotFoundError(f"Required file not found: {filepath}. Run T032 first.")
    with open(filepath, 'r') as f:
        return json.load(f)

def aggregate_validation_metrics(pvalues_df: pd.DataFrame, power_results: Dict[str, Any]) -> Dict[str, Any]:
    """
    Aggregate validation metrics including KS statistics and summary counts.
    Combines data from T031 (p-values) and T032 (power/KS).
    """
    metrics = {
        "generated_at": datetime.now().isoformat(),
        "source_files": {
            "pvalues": "data/simulation/real_data_pvalues.csv",
            "power": "data/simulation/real_data_power.json"
        },
        "summary": {
            "total_real_datasets": len(pvalues_df['dataset_name'].unique()) if 'dataset_name' in pvalues_df.columns else 0,
            "total_tests_run": len(pvalues_df),
            "tests_with_valid_pvalue": len(pvalues_df[pvalues_df['p_value'].notna()])
        },
        "ks_statistics": {},
        "power_analysis": power_results,
        "validation_status": "pending"
    }

    # Calculate aggregate KS statistics if available in power_results
    if 'ks_distances' in power_results:
        ks_data = power_results['ks_distances']
        metrics["ks_statistics"] = ks_data
        
        # Determine overall validation status based on KS threshold (<= 0.10)
        max_ks = 0.0
        if isinstance(ks_data, dict):
            for key, val in ks_data.items():
                if isinstance(val, (int, float)) and val > max_ks:
                    max_ks = val
        
        # Also check if power_results contains a global 'ks_distance' field
        if 'ks_distance' in power_results:
            if isinstance(power_results['ks_distance'], (int, float)) and power_results['ks_distance'] > max_ks:
                max_ks = power_results['ks_distance']

        if max_ks <= 0.10:
            metrics["validation_status"] = "PASSED"
            metrics["validation_note"] = f"All KS distances <= 0.10. Max observed KS: {max_ks:.4f}"
        else:
            metrics["validation_status"] = "FAILED"
            metrics["validation_note"] = f"KS distance exceeded 0.10 threshold. Max observed KS: {max_ks:.4f}"
    else:
        metrics["validation_status"] = "INCOMPLETE"
        metrics["validation_note"] = "No KS statistics found in power results."

    return metrics

def save_validation_metrics(metrics: Dict[str, Any], filepath: str = "data/simulation/validation_metrics.json") -> None:
    """
    Save the aggregated validation metrics to a JSON file.
    """
    os.makedirs(os.path.dirname(filepath), exist_ok=True)
    with open(filepath, 'w') as f:
        json.dump(metrics, f, indent=2)

def main():
    """
    Main entry point for T034: Save validation metrics and KS statistics.
    Loads outputs from T031 and T032, aggregates them, and saves to validation_metrics.json.
    """
    try:
        # Load inputs from previous tasks
        print("Loading real data p-values (T031 output)...")
        pvalues_df = load_real_data_pvalues()
        
        print("Loading power results (T032 output)...")
        power_results = load_power_results()

        # Aggregate metrics
        print("Aggregating validation metrics...")
        metrics = aggregate_validation_metrics(pvalues_df, power_results)

        # Save output
        output_path = "data/simulation/validation_metrics.json"
        print(f"Saving validation metrics to {output_path}...")
        save_validation_metrics(metrics, output_path)

        print(f"Success. Validation status: {metrics['validation_status']}")
        print(f"Note: {metrics['validation_note']}")

    except FileNotFoundError as e:
        print(f"Error: {e}")
        print("Ensure T031 (real_data_pvalues.csv) and T032 (real_data_power.json) have been completed.")
        raise
    except Exception as e:
        print(f"Unexpected error during validation metrics aggregation: {e}")
        raise

if __name__ == "__main__":
    main()