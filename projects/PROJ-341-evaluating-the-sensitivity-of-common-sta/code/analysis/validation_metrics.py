"""
validation_metrics.py

Implements the calculation and saving of validation metrics comparing
real-world dataset results against simulation predictions.

Specifically calculates:
- Real data power estimates via bootstrapping
- Kolmogorov-Smirnov (KS) distance between real and simulated p-value distributions
- Overall validation metrics (deviation flags, pass/fail status)

Outputs:
- data/simulation/validation_metrics.json
"""
