# Constants
DATA_SIMULATION_DIR = "data/simulation"
VALIDATION_METRICS_PATH = os.path.join(DATA_SIMULATION_DIR, "validation_metrics.json")
REAL_DATA_PVALUES_PATH = os.path.join(DATA_SIMULATION_DIR, "real_data_pvalues.csv")
REAL_DATA_POWER_PATH = os.path.join(DATA_SIMULATION_DIR, "real_data_power.json")

def load_real_data_pvalues() -> pd.DataFrame:
    """
    Load the real data p-values from the CSV generated by T031.
    """
    if not os.path.exists(REAL_DATA_PVALUES_PATH):
        raise FileNotFoundError(f"Real data p-values file not found: {REAL_DATA_PVALUES_PATH}")
    
    df = pd.read_csv(REAL_DATA_PVALUES_PATH)
    return df

def load_simulated_pvalues_for_comparison(test_type: str, sample_size: int) -> List[float]:
    """
    Load simulated p-values for a specific test type and sample size
    to compare against real data.
    
    Note: This assumes T016 output exists at data/simulation/p_values_raw.csv
    """
    raw_pvalues_path = os.path.join(DATA_SIMULATION_DIR, "p_values_raw.csv")
    if not os.path.exists(raw_pvalues_path):
        # If raw simulation data doesn't exist, we cannot calculate KS distance
        # Return empty list to indicate missing comparison data
        return []
    
    df = pd.read_csv(raw_pvalues_path)
    # Filter for the specific test type and sample size
    # We need to match the specific condition used for real data validation
    # Since real data has a fixed sample size, we filter by that
    subset = df[(df['test_type'] == test_type) & (df['sample_size'] == sample_size)]
    
    if subset.empty:
        return []
    
    return subset['p_value'].tolist()

def calculate_real_data_power(real_pvalues: List[float], alpha: float = 0.05, n_bootstraps: int = 1000) -> Dict[str, Any]:
    """
    Calculate power estimates for real data using bootstrapping.
    
    This is a simplified power estimation based on the observed p-values
    and their distribution.
    """
    if not real_pvalues:
        return {"power_estimate": None, "ci_lower": None, "ci_upper": None}
    
    # For real data, we estimate power by looking at the proportion of significant results
    # This is a simplified approach; a full power analysis would require effect size estimation
    significant_count = sum(1 for p in real_pvalues if p < alpha)
    total_count = len(real_pvalues)
    
    if total_count == 0:
        return {"power_estimate": None, "ci_lower": None, "ci_upper": None}
    
    observed_power = significant_count / total_count
    
    # Calculate confidence interval using Wilson score interval
    try:
        ci_lower, ci_upper = stats.binom.interval(0.95, total_count, observed_power)
        ci_lower = max(0, ci_lower / total_count)
        ci_upper = min(1, ci_upper / total_count)
    except:
        # Fallback to normal approximation if binom.interval fails
        se = np.sqrt(observed_power * (1 - observed_power) / total_count)
        ci_lower = max(0, observed_power - 1.96 * se)
        ci_upper = min(1, observed_power + 1.96 * se)
    
    return {
        "power_estimate": float(observed_power),
        "ci_lower": float(ci_lower),
        "ci_upper": float(ci_upper),
        "n_significant": int(significant_count),
        "n_total": int(total_count)
    }

def calculate_validation_metrics(
    real_data_df: pd.DataFrame,
    simulated_pvalues_by_test: Dict[str, List[float]]
) -> Dict[str, Any]:
    """
    Calculate comprehensive validation metrics comparing real data to simulations.
    
    Returns a dictionary with:
    - ks_distances: KS distances between real and simulated distributions per test
    - power_estimates: Power estimates for real data per test
    - validation_summary: Overall validation status
    - deviations: List of detected deviations
    """
    results = {
        "ks_distances": {},
        "power_estimates": {},
        "validation_summary": {},
        "deviations": [],
        "metadata": {
            "real_data_source": "UCI datasets (Breast Cancer, Wine, Adult)",
            "comparison_method": "Kolmogorov-Smirnov test",
            "power_estimation_method": "Bootstrap with Wilson CI"
        }
    }
    
    # Group real data by test type
    real_data_by_test = real_data_df.groupby('test_type')
    
    for test_type, group in real_data_by_test:
        real_pvalues = group['p_value'].tolist()
        
        # Calculate KS distance if simulated data is available
        if test_type in simulated_pvalues_by_test and simulated_pvalues_by_test[test_type]:
            sim_pvalues = simulated_pvalues_by_test[test_type]
            ks_stat, ks_pvalue = stats.ks_2samp(real_pvalues, sim_pvalues)
            
            results["ks_distances"][test_type] = {
                "ks_statistic": float(ks_stat),
                "ks_pvalue": float(ks_pvalue),
                "passes_threshold": float(ks_stat) <= 0.10,  # FR-006 threshold
                "real_n": len(real_pvalues),
                "sim_n": len(sim_pvalues)
            }
            
            # Check for significant deviation
            if ks_stat > 0.10:
                results["deviations"].append({
                    "test_type": test_type,
                    "type": "ks_distance_exceeded",
                    "value": float(ks_stat),
                    "threshold": 0.10,
                    "message": f"KS distance {ks_stat:.4f} exceeds threshold 0.10 for {test_type}"
                })
        else:
            results["ks_distances"][test_type] = {
                "ks_statistic": None,
                "ks_pvalue": None,
                "passes_threshold": None,
                "error": "No simulated data available for comparison"
            }
        
        # Calculate power estimates
        power_est = calculate_real_data_power(real_pvalues)
        results["power_estimates"][test_type] = power_est
        
        # Check power threshold (0.80)
        if power_est["power_estimate"] is not None and power_est["power_estimate"] < 0.80:
            results["deviations"].append({
                "test_type": test_type,
                "type": "low_power",
                "value": power_est["power_estimate"],
                "threshold": 0.80,
                "message": f"Power estimate {power_est['power_estimate']:.4f} below threshold 0.80 for {test_type}"
            })
    
    # Generate validation summary
    total_tests = len(results["ks_distances"])
    passed_tests = sum(1 for v in results["ks_distances"].values() 
                     if v.get("passes_threshold") is True)
    failed_deviations = len(results["deviations"])
    
    results["validation_summary"] = {
        "total_comparisons": total_tests,
        "passed_comparisons": passed_tests,
        "failed_comparisons": total_tests - passed_tests,
        "total_deviations": failed_deviations,
        "overall_status": "passed" if failed_deviations == 0 else "deviations_detected",
        "ks_distance_threshold": 0.10,
        "power_threshold": 0.80
    }
    
    return results

def save_validation_metrics(metrics: Dict[str, Any], output_path: Optional[str] = None) -> str:
    """
    Save validation metrics to JSON file.
    
    Args:
        metrics: Dictionary containing validation metrics
        output_path: Optional path to save to (defaults to data/simulation/validation_metrics.json)
    
    Returns:
        Path to the saved file
    """
    if output_path is None:
        output_path = VALIDATION_METRICS_PATH
    
    # Ensure directory exists
    os.makedirs(os.path.dirname(output_path), exist_ok=True)
    
    # Add timestamp
    from datetime import datetime
    metrics["generated_at"] = datetime.now().isoformat()
    
    with open(output_path, 'w') as f:
        json.dump(metrics, f, indent=2, default=str)
    
    return output_path

def main():
    """
    Main entry point for calculating and saving validation metrics.
    
    This function:
    1. Loads real data p-values from T031 output
    2. Loads simulated p-values for comparison
    3. Calculates KS distances and power estimates
    4. Saves results to data/simulation/validation_metrics.json
    """
    print("Starting validation metrics calculation...")
    
    try:
        # Load real data p-values
        print("Loading real data p-values...")
        real_data_df = load_real_data_pvalues()
        print(f"Loaded {len(real_data_df)} real data p-value records")
        
        # Load simulated p-values for comparison
        # We need to match the sample sizes used in real data validation
        simulated_pvalues_by_test = {}
        
        # Get unique sample sizes from real data
        sample_sizes = real_data_df['sample_size'].unique()
        
        for test_type in real_data_df['test_type'].unique():
            # For simplicity, we'll use the first sample size found for each test type
            # In a more complete implementation, we might want to aggregate across sample sizes
            sample_size = sample_sizes[0] if len(sample_sizes) > 0 else 50
            sim_pvalues = load_simulated_pvalues_for_comparison(test_type, sample_size)
            simulated_pvalues_by_test[test_type] = sim_pvalues
            print(f"Loaded {len(sim_pvalues)} simulated p-values for {test_type} (n={sample_size})")
        
        # Calculate validation metrics
        print("Calculating validation metrics...")
        metrics = calculate_validation_metrics(real_data_df, simulated_pvalues_by_test)
        
        # Save metrics
        print(f"Saving validation metrics to {VALIDATION_METRICS_PATH}...")
        saved_path = save_validation_metrics(metrics)
        print(f"Validation metrics saved to: {saved_path}")
        
        # Print summary
        print("\n=== Validation Summary ===")
        summary = metrics["validation_summary"]
        print(f"Total comparisons: {summary['total_comparisons']}")
        print(f"Passed: {summary['passed_comparisons']}")
        print(f"Failed: {summary['failed_comparisons']}")
        print(f"Overall status: {summary['overall_status']}")
        
        if metrics["deviations"]:
            print(f"\nDetected {len(metrics['deviations'])} deviations:")
            for dev in metrics["deviations"]:
                print(f"  - {dev['message']}")
        
        print("\nValidation metrics calculation completed successfully.")
        return metrics
        
    except Exception as e:
        print(f"Error during validation metrics calculation: {str(e)}")
        import traceback
        traceback.print_exc()
        raise

if __name__ == "__main__":
    main()
