#!/usr/bin/env python3
"""
CPU-Tractable Training Proxy for StepAudio 2.5.

Original Goal: Train a massive unified audio-language model using RLHF 
across ASR, TTS, and Realtime tasks. This involves heavy GPU compute, 
transformers, and reinforcement learning loops.

Adaptation Strategy:
1. **Proxy Task**: Instead of training a 7B+ model, we train a tiny 
   logistic regression / linear classifier on the synthetic audio features.
2. **Simulation of RLHF**: We simulate the "optimization" step by 
   minimizing a simple cross-entropy loss on the synthetic data.
3. **Metrics**: We compute and report "Accuracy" and "Loss" to mimic 
   the quantitative results section of the paper.
4. **Artifacts**: Writes `data/results.csv` and `figures/training_curve.png`.
   These are the verifiable outputs for the execution stage.

This validates the *training loop logic* and *metric reporting* of the paper 
without the impossible compute requirements.
"""

import argparse
import json
import os
import sys
from pathlib import Path
import numpy as np
import matplotlib
matplotlib.use('Agg')  # Non-interactive backend for CPU-only CI
import matplotlib.pyplot as plt

# Simple ML components (no heavy dependencies)
def sigmoid(x):
    return 1 / (1 + np.exp(-x))

def cross_entropy_loss(y_true, y_pred):
    # Clip predictions to avoid log(0)
    y_pred = np.clip(y_pred, 1e-7, 1 - 1e-7)
    return -np.mean(y_true * np.log(y_pred) + (1 - y_true) * np.log(1 - y_pred))

def train_proxy_model(data_dir: str, output_dir: str, epochs: int = 10, learning_rate: float = 0.1):
    """
    Trains a tiny proxy model on the synthetic data generated by prepare_cpu.py.
    """
    data_path = Path(data_dir)
    text_file = data_path / "text"
    meta_file = data_path / "metadata.json"

    if not text_file.exists() or not meta_file.exists():
        print(f"ERROR: Data not found in {data_dir}. Run prepare_cpu.py first.", file=sys.stderr)
        return False

    # Load metadata
    with open(meta_file, 'r') as f:
        metadata = json.load(f)
    
    # Load text labels
    labels_map = {}
    with open(text_file, 'r') as f:
        for line in f:
            parts = line.strip().split('\t')
            if len(parts) == 2:
                sid, text = parts
                labels_map[sid] = text

    # Prepare features and labels
    # Feature: Mean energy of the audio segment (simple proxy for "content")
    # Label: Binary classification (e.g., "Is the text about 'StepAudio' or not?" or "Long vs Short")
    # We'll create a synthetic task: Classify if the audio duration is > 3.0s.
    
    X = []
    y = []
    sids = []

    for item in metadata:
        sid = item['sid']
        duration = item['duration']
        file_path = data_path / item['file']
        
        if file_path.exists():
            audio = np.load(file_path)
            # Simple feature: RMS Energy
            feature = np.sqrt(np.mean(audio**2))
            
            # Target: 1 if duration > 3.0s, else 0
            target = 1 if duration > 3.0 else 0
            
            X.append(feature)
            y.append(target)
            sids.append(sid)
        else:
            print(f"Warning: File not found for {sid}")

    if len(X) == 0:
        print("ERROR: No valid data loaded.", file=sys.stderr)
        return False

    X = np.array(X).reshape(-1, 1)
    y = np.array(y).reshape(-1, 1)
    
    # Normalize features
    X_mean = X.mean()
    X_std = X.std()
    X_norm = (X - X_mean) / (X_std + 1e-8)

    # Initialize weights
    weights = np.zeros((1, 1))
    bias = 0.0
    
    history = {'loss': [], 'accuracy': []}
    
    print(f"Training proxy model on {len(X)} samples...")
    
    for epoch in range(epochs):
        # Forward pass
        z = np.dot(X_norm, weights) + bias
        predictions = sigmoid(z)
        
        # Loss
        loss = cross_entropy_loss(y, predictions)
        
        # Accuracy
        predicted_classes = (predictions >= 0.5).astype(int)
        accuracy = np.mean(predicted_classes == y)
        
        history['loss'].append(loss)
        history['accuracy'].append(accuracy)
        
        # Backward pass (Gradient Descent)
        error = predictions - y
        dw = np.dot(X_norm.T, error) / len(X)
        db = np.mean(error)
        
        weights -= learning_rate * dw
        bias -= learning_rate * db
        
        if (epoch + 1) % 2 == 0 or epoch == 0:
            print(f"  Epoch {epoch+1}/{epochs} - Loss: {loss:.4f}, Accuracy: {accuracy:.4f}")

    # Save Results
    output_path = Path(output_dir)
    output_path.mkdir(parents=True, exist_ok=True)
    
    # 1. CSV Results
    results_csv = output_path / "results.csv"
    with open(results_csv, 'w') as f:
        f.write("epoch,loss,accuracy\n")
        for i, (l, a) in enumerate(zip(history['loss'], history['accuracy'])):
            f.write(f"{i+1},{l:.6f},{a:.4f}\n")
    
    # 2. Plot
    fig, ax = plt.subplots(1, 2, figsize=(10, 4))
    
    ax[0].plot(history['loss'], label='Loss')
    ax[0].set_title('Training Loss')
    ax[0].set_xlabel('Epoch')
    ax[0].set_ylabel('Cross Entropy')
    ax[0].legend()
    ax[0].grid(True)
    
    ax[1].plot(history['accuracy'], label='Accuracy', color='orange')
    ax[1].set_title('Training Accuracy')
    ax[1].set_xlabel('Epoch')
    ax[1].set_ylabel('Accuracy')
    ax[1].legend()
    ax[1].grid(True)
    
    plt.tight_layout()
    plot_path = output_path / "training_curve.png"
    plt.savefig(plot_path, dpi=100)
    plt.close()
    
    print(f"\nDone.")
    print(f"  Results: {results_csv}")
    print(f"  Plot: {plot_path}")
    
    # Final summary
    final_acc = history['accuracy'][-1]
    print(f"  Final Accuracy: {final_acc:.4f}")
    
    return True

def main():
    parser = argparse.ArgumentParser(description="CPU-Tractable Proxy Training")
    parser.add_argument("--data-dir", type=str, default="data/synthetic_wenet",
                        help="Directory containing prepared data")
    parser.add_argument("--output-dir", type=str, default="data/results",
                        help="Directory for output artifacts")
    parser.add_argument("--epochs", type=int, default=10,
                        help="Number of training epochs")
    parser.add_argument("--lr", type=float, default=0.1,
                        help="Learning rate")
    
    args = parser.parse_args()

    try:
        success = train_proxy_model(
            data_dir=args.data_dir,
            output_dir=args.output_dir,
            epochs=args.epochs,
            learning_rate=args.lr
        )
        if not success:
            sys.exit(1)
    except Exception as e:
        print(f"ERROR: Training failed: {e}", file=sys.stderr)
        sys.exit(1)

if __name__ == "__main__":
    main()
