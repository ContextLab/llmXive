def calculate_effect_size_difference(group1: List[float], group2: List[float]) -> Dict[str, float]:
    """
    Calculate Cohen's d (effect size) and delta between two groups.
    
    Cohen's d = (mean1 - mean2) / pooled_std
    Pooled std = sqrt(((n1-1)*std1^2 + (n2-1)*std2^2) / (n1+n2-2))
    """
    if not group1 or not group2:
        return {"delta": 0.0, "cohens_d": 0.0, "pooled_std": 0.0, "n1": 0, "n2": 0}

    n1, n2 = len(group1), len(group2)
    mean1 = sum(group1) / n1
    mean2 = sum(group2) / n2
    delta = mean1 - mean2

    var1 = sum((x - mean1) ** 2 for x in group1) / (n1 - 1) if n1 > 1 else 0
    var2 = sum((x - mean2) ** 2 for x in group2) / (n2 - 1) if n2 > 1 else 0

    # Pooled standard deviation
    pooled_var = ((n1 - 1) * var1 + (n2 - 1) * var2) / (n1 + n2 - 2) if (n1 + n2 - 2) > 0 else 0
    pooled_std = math.sqrt(pooled_var)

    cohens_d = delta / pooled_std if pooled_std > 0 else 0.0

    return {
        "delta": delta,
        "cohens_d": cohens_d,
        "pooled_std": pooled_std,
        "n1": n1,
        "n2": n2,
        "mean1": mean1,
        "mean2": mean2
    }

def merge_metrics(existing: List[Dict[str, Any]], new_samples: List[Dict[str, Any]]) -> List[Dict[str, Any]]:
    """
    Merge new sensitivity samples into the existing dataset.
    The existing dataset is keyed by task_id. If a task_id exists in both,
    we append the new sample with a distinct source_type or update if it's the same source.
    For T045, we assume new_samples are from a different model (CodeLlama) and should be appended.
    """
    # Create a map of existing data by task_id for quick lookup
    existing_map = {item['task_id']: item for item in existing}
    
    merged = []
    # Add all existing items first
    merged.extend(existing)

    for new_item in new_samples:
        task_id = new_item['task_id']
        # Check if this task_id already exists in the merged list
        # We want to keep both human/CodeGen and CodeLlama samples if they are distinct sources
        # The schema allows multiple entries per task_id with different source_types
        merged.append(new_item)
    
    return merged

def save_metrics(data: List[Dict[str, Any]], filepath: str):
    """Save the merged metrics to the canonical file."""
    # Ensure directory exists
    os.makedirs(os.path.dirname(filepath), exist_ok=True)
    
    # Write with indentation for readability
    with open(filepath, 'w', encoding='utf-8') as f:
        json.dump(data, f, indent=2)
    
    logger.info(f"Saved merged metrics to {filepath}")

def main():
    """Main entry point for T045."""
    setup_logging()
    logger.info("Starting T045: Sensitivity Analysis Update")

    metrics_path = "data/analysis/metrics.json"
    
    # 1. Load existing metrics (from T017)
    try:
        existing_metrics = load_existing_metrics(metrics_path)
        logger.info(f"Loaded {len(existing_metrics)} existing metrics records.")
    except FileNotFoundError as e:
        logger.error(f"Cannot proceed: {e}")
        sys.exit(1)

    # 2. Load sensitivity samples (generated by T028/T029)
    # We expect CodeLlama samples to be in data/generated/sensitivity/codellama-7b or similar
    # We need to check what models were actually used. 
    # Based on T028/T029, we likely have 'codellama-7b' and/or 'codegen-mono' (as fallback)
    # We will look for directories under data/generated/sensitivity/
    
    sensitivity_dir = "data/generated/sensitivity"
    new_samples = []
    
    if os.path.exists(sensitivity_dir):
        for model_folder in os.listdir(sensitivity_dir):
            model_path = os.path.join(sensitivity_dir, model_folder)
            if os.path.isdir(model_path):
                logger.info(f"Processing sensitivity samples for model: {model_folder}")
                samples = load_sensitivity_samples(sensitivity_dir, model_folder)
                new_samples.extend(samples)
    else:
        logger.warning(f"Sensitivity directory {sensitivity_dir} not found. No new samples to merge.")

    if not new_samples:
        logger.warning("No new sensitivity samples found. The canonical file will remain unchanged.")
        # Still save to ensure integrity if we did any processing, but nothing changed
        save_metrics(existing_metrics, metrics_path)
        return

    logger.info(f"Found {len(new_samples)} new sensitivity samples to merge.")

    # 3. Compute Effect Sizes (Cohen's d) for analysis purposes (logged)
    # We compare the main source (e.g., 'codegen-mono') vs the sensitivity source (e.g., 'codellama-7b')
    # We need to group by metric and source_type
    
    def get_values(data: List[Dict], metric: str, source: str):
        return [d[metric] for d in data if d.get('source_type') == source and d.get(metric) is not None]

    # Identify source types present
    sources = list(set(d.get('source_type') for d in existing_metrics + new_samples if d.get('source_type')))
    logger.info(f"Detected source types: {sources}")

    # Calculate effect sizes for continuous metrics: cyclomatic_complexity, halstead_volume
    metrics_to_check = ['cyclomatic_complexity', 'halstead_volume']
    effect_size_report = {}

    for metric in metrics_to_check:
        # Assume the first source in 'sources' is the baseline (e.g., CodeGen) and second is sensitivity (e.g., CodeLlama)
        # This is a simplification; in reality, we might have multiple.
        # We will compare 'codegen-mono' vs 'codellama-7b' if both exist.
        
        baseline_source = 'codegen-mono'
        sensitivity_source = 'codellama-7b'
        
        if baseline_source not in sources or sensitivity_source not in sources:
            # Fallback to first two available
            if len(sources) >= 2:
                baseline_source = sources[0]
                sensitivity_source = sources[1]
            else:
                continue

        group1 = get_values(existing_metrics, metric, baseline_source)
        group2 = get_values(new_samples, metric, sensitivity_source)
        
        if group1 and group2:
            es = calculate_effect_size_difference(group1, group2)
            effect_size_report[metric] = es
            logger.info(f"Effect Size for {metric} ({baseline_source} vs {sensitivity_source}): Cohen's d = {es['cohens_d']:.4f}, Delta = {es['delta']:.4f}")
        else:
            logger.warning(f"Insufficient data for {metric} comparison between {baseline_source} and {sensitivity_source}")

    # 4. Merge metrics
    merged_data = merge_metrics(existing_metrics, new_samples)
    
    # 5. Save the updated canonical file
    save_metrics(merged_data, metrics_path)
    
    # Log the effect size report for T046 to potentially pick up or for immediate logging
    logger.info("Sensitivity analysis update complete. Effect sizes logged above.")

    # Return the effect size report for potential use in downstream tasks (T046)
    # In a real pipeline, this might be saved to state/ or logs/
    with open("logs/effect_size_report.json", 'w', encoding='utf-8') as f:
        json.dump(effect_size_report, f, indent=2)

    logger.info("T045 completed successfully.")

if __name__ == "__main__":
    main()
