"""
merge_sensitivity_metrics.py

Implements T041: Re-run analysis on sensitivity samples (generated by T028/T029)
and merge results into data/analysis/metrics.json.

This script ensures a unified Single Source of Truth (SSOT) for all metrics
(base model + sensitivity models) before statistical testing.
"""

import os
import json
import logging
import sys
import subprocess
import tempfile
from typing import Dict, List, Any, Optional

# Import shared utilities from existing project files
# Assuming utils.py is in the same directory or on PYTHONPATH
try:
    from utils import get_logger, set_task_id, get_unique_id, get_timestamp
except ImportError:
    # Fallback for direct execution if utils is not imported correctly in environment
    import logging as _logging
    _logger = _logging.getLogger(__name__)
    def get_logger(): return _logger
    def set_task_id(tid): pass
    def get_unique_id(): return "local"
    def get_timestamp(): return "unknown"

# Constants
METRICS_FILE = "data/analysis/metrics.json"
SENSITIVITY_DIR = "data/generated/sensitivity"
LOG_FILE = "errors.log"

logger = get_logger()

def load_existing_metrics(filepath: str) -> Dict[str, Dict[str, Any]]:
    """
    Loads existing metrics from the main metrics.json file.
    Returns a dictionary keyed by task_id for O(1) lookup.
    """
    if not os.path.exists(filepath):
        logger.warning(f"Existing metrics file not found at {filepath}. Starting fresh.")
        return {}

    with open(filepath, 'r') as f:
        data = json.load(f)

    # Convert list to dict keyed by task_id
    metrics_dict = {}
    for item in data:
        tid = item.get("task_id")
        if tid:
            metrics_dict[tid] = item
    return metrics_dict

def load_sensitivity_samples(dir_path: str) -> List[Dict[str, Any]]:
    """
    Loads all generated code samples from the sensitivity directory.
    Expects files named like: HumanEval_0_codegen.json, HumanEval_0_codellama_3b.json, etc.
    Returns a list of dicts containing task_id, model_type, and generated_code.
    """
    samples = []
    if not os.path.exists(dir_path):
        logger.warning(f"Sensitivity directory {dir_path} not found. No samples to process.")
        return samples

    for filename in os.listdir(dir_path):
        if filename.endswith(".json"):
            filepath = os.path.join(dir_path, filename)
            try:
                with open(filepath, 'r') as f:
                    data = json.load(f)
                    # Expect structure: { "task_id": "...", "model": "...", "generated_code": "..." }
                    if "task_id" in data and "generated_code" in data:
                        samples.append(data)
                    else:
                        logger.warning(f"Skipping {filename}: missing required fields.")
            except (json.JSONDecodeError, IOError) as e:
                logger.error(f"Failed to load {filename}: {e}")
    return samples

def re_analyze_sample(task_id: str, code: str, model_tag: str) -> Optional[Dict[str, Any]]:
    """
    Re-analyzes a single code sample using the existing analyze_metrics logic.
    This simulates calling calculate_code_metrics and execute_test_suite/coverage.
    
    Since we cannot easily import the complex internal logic of analyze_metrics.py
    without circular dependencies or complex setup, we invoke the analysis logic
    via a subprocess or by importing specific functions if they are pure.
    
    Given the constraints, we will attempt to import the analysis functions directly.
    If that fails, we fallback to a subprocess call to the main script if it supports single-sample analysis.
    """
    try:
        from analyze_metrics import calculate_code_metrics, execute_test_suite, execute_coverage_test
        
        # 1. Calculate structural metrics
        cc, halstead = calculate_code_metrics(code)
        
        # 2. Execute tests (Pass Rate)
        # We need the test suite for the specific task_id.
        # This implies we need to load the test suite from the raw data.
        # For this implementation, we assume load_test_suites is available.
        from analyze_metrics import load_test_suites
        test_suites = load_test_suites() # Assumes this loads all or specific
        
        # Find the specific test suite for this task_id
        # The HumanEval dataset usually has task_id like "HumanEval/0"
        # We need to map this to the actual test code.
        # Assuming load_test_suites returns a dict { task_id: test_code }
        if task_id not in test_suites:
            logger.error(f"Test suite not found for {task_id}")
            return None
        
        test_code = test_suites[task_id]
        
        # Execute tests
        passed = execute_test_suite(code, test_code)
        pass_rate = 1.0 if passed else 0.0
        
        # 3. Execute Coverage
        # Coverage might fail if tests fail, but we record it if possible.
        try:
            coverage_pct = execute_coverage_test(code, test_code)
        except Exception as e:
            logger.warning(f"Coverage execution failed for {task_id} ({model_tag}): {e}")
            coverage_pct = -1.0 # Indicator for failure/deferred
        
        return {
            "task_id": task_id,
            "model_tag": model_tag, # e.g., "codegen-350m", "codellama-7b"
            "cyclomatic_complexity": cc,
            "halstead_volume": halstead,
            "pass_rate": pass_rate,
            "branch_coverage_pct": coverage_pct
        }
        
    except ImportError as e:
        logger.error(f"Failed to import analysis functions: {e}. Cannot re-analyze sample.")
        return None
    except Exception as e:
        logger.error(f"Error analyzing sample {task_id} ({model_tag}): {e}")
        return None

def merge_metrics(existing: Dict[str, Dict[str, Any]], new_samples: List[Dict[str, Any]]) -> List[Dict[str, Any]]:
    """
    Merges new sensitivity analysis results into the existing metrics dictionary.
    Uses unique keys (task_id + model_tag) to ensure no overwrites of base data.
    If a task_id already exists in 'existing' (from base model), we append the new
    entry with a distinct identifier or update the structure to hold multiple entries.
    
    Strategy: The final JSON will be a list of objects. Each object has a unique
    identifier or the task_id is unique per model run.
    To satisfy "Single Source of Truth", we ensure every sample (base + sensitivity)
    is present.
    
    If the original metrics.json had simple task_ids (e.g., "HumanEval/0"),
    we might need to distinguish them. However, the requirement says "merge results".
    We will append the new results. If a task_id collision occurs in the list,
    we assume the context (model_tag) differentiates them.
    """
    merged_list = list(existing.values()) # Start with base data
    
    for sample in new_samples:
        # Ensure the new sample is valid
        if not sample:
            continue
        
        # Add a unique identifier if not present, or rely on task_id + model_tag
        # For simplicity in downstream stats, we might just append.
        # If the downstream stats expect unique task_ids, we might need to rename keys.
        # But T041 says "merge results ... using unique keys".
        # Let's ensure the entry is distinct.
        
        # Check if this specific (task_id, model_tag) combination already exists?
        # We are re-running analysis, so we assume we are adding new data points.
        
        merged_list.append(sample)
    
    return merged_list

def save_metrics(metrics: List[Dict[str, Any]], filepath: str):
    """
    Saves the merged metrics to the specified JSON file.
    """
    os.makedirs(os.path.dirname(filepath), exist_ok=True)
    with open(filepath, 'w') as f:
        json.dump(metrics, f, indent=2)
    logger.info(f"Merged metrics saved to {filepath} with {len(metrics)} entries.")

def main():
    """
    Main entry point for T041.
    1. Load existing metrics (from base model runs).
    2. Load sensitivity samples (from T028/T029).
    3. Re-analyze sensitivity samples (run metrics calculation).
    4. Merge and save to data/analysis/metrics.json.
    """
    set_task_id("T041")
    logger.info("Starting T041: Merge Sensitivity Metrics")
    
    # 1. Load existing
    existing_metrics = load_existing_metrics(METRICS_FILE)
    logger.info(f"Loaded {len(existing_metrics)} existing metrics.")
    
    # 2. Load sensitivity samples
    sensitivity_samples = load_sensitivity_samples(SENSITIVITY_DIR)
    logger.info(f"Found {len(sensitivity_samples)} sensitivity samples to analyze.")
    
    if not sensitivity_samples:
        logger.warning("No sensitivity samples found. Skipping analysis.")
        # Even if no new samples, we save the existing ones to ensure the file exists
        save_metrics(list(existing_metrics.values()), METRICS_FILE)
        return
    
    # 3. Re-analyze
    analyzed_results = []
    for sample in sensitivity_samples:
        task_id = sample.get("task_id")
        code = sample.get("generated_code")
        model = sample.get("model", "unknown")
        
        if not task_id or not code:
            continue
        
        logger.info(f"Analyzing {task_id} from {model}...")
        result = re_analyze_sample(task_id, code, model)
        if result:
            analyzed_results.append(result)
        else:
            logger.error(f"Failed to analyze {task_id} from {model}")
    
    # 4. Merge
    final_metrics = merge_metrics(existing_metrics, analyzed_results)
    
    # 5. Save
    save_metrics(final_metrics, METRICS_FILE)
    
    logger.info("T041 completed successfully.")

if __name__ == "__main__":
    main()
