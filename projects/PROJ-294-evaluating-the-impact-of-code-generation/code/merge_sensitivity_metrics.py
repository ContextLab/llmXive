def load_existing_metrics(filepath: str) -> List[Dict[str, Any]]:
    """Load the existing metrics dataset."""
    if not os.path.exists(filepath):
        raise FileNotFoundError(f"Existing metrics file not found at {filepath}")
    with open(filepath, 'r', encoding='utf-8') as f:
        data = json.load(f)
    if not isinstance(data, list):
        raise ValueError(f"Expected metrics file to be a JSON list, got {type(data)}")
    return data

def load_sensitivity_samples(base_path: str, model_name: str) -> List[Dict[str, Any]]:
    """
    Load sensitivity samples generated by T028/T029.
    These are expected to be in data/generated/sensitivity/{model_name}/
    containing code files and potentially pre-computed metrics if T028/T029 ran the analysis.
    If T028/T029 only generated code, we must analyze them here.
    """
    sensitivity_dir = os.path.join("data", "generated", "sensitivity", model_name)
    if not os.path.exists(sensitivity_dir):
        logger.warning(f"Sensitivity directory not found for {model_name}: {sensitivity_dir}")
        return []

    samples = []
    for task_file in os.listdir(sensitivity_dir):
        if task_file.endswith(".py"):
            task_id = task_file.replace(".py", "")
            code_path = os.path.join(sensitivity_dir, task_file)
            with open(code_path, 'r', encoding='utf-8') as f:
                code_content = f.read()
            
            # Analyze the code to get metrics (Complexity, Halstead)
            # We reuse the existing analysis logic from analyze_metrics
            metrics = calculate_code_metrics(code_content, task_id, model_name)
            samples.append(metrics)
    
    return samples

