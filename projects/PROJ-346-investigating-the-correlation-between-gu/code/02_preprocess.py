logger = setup_logger("preprocess")

def load_cognitive_raw_data():
    """
    Load the raw cognitive data generated by T012.
    Expects: data/raw/cognitive_raw.parquet
    """
    raw_path = get_data_raw_path()
    file_path = raw_path / "cognitive_raw.parquet"
    
    if not file_path.exists():
        raise FileNotFoundError(f"Raw cognitive data not found at {file_path}. Run T012 first.")
    
    logger.info(f"Loading cognitive raw data from {file_path}")
    df = pd.read_parquet(file_path)
    return df

def apply_mice_imputation(df, target_cols):
    """
    Apply Multiple Imputation by Chained Equations (MICE) to handle missing values.
    Uses sklearn's IterativeImputer as a proxy for MICE.
    """
    if df.empty:
        return df

    logger.info(f"Applying MICE imputation to columns: {target_cols}")
    
    # Identify columns with missing values
    cols_to_impute = [col for col in target_cols if col in df.columns and df[col].isnull().any()]
    
    if not cols_to_impute:
        logger.info("No missing values found in target columns. Skipping imputation.")
        return df

    # Prepare data for imputer
    X = df[cols_to_impute].copy()
    
    # Initialize and fit the imputer
    # max_iter=10 is a reasonable default for MICE
    imputer = IterativeImputer(max_iter=10, random_state=42)
    X_imputed = imputer.fit_transform(X)
    
    # Update dataframe with imputed values
    df[cols_to_impute] = X_imputed
    
    logger.info(f"MICE imputation complete. Missing values filled in {len(cols_to_impute)} columns.")
    return df

def compute_z_scores(df, target_cols):
    """
    Compute z-scores for specified columns.
    Formula: (x - mean) / std
    """
    if df.empty:
        return df

    logger.info(f"Computing z-scores for columns: {target_cols}")
    
    for col in target_cols:
        if col in df.columns:
            mean = df[col].mean()
            std = df[col].std()
            if std == 0:
                logger.warning(f"Standard deviation is 0 for column {col}. Setting z-scores to 0.")
                df[col + '_zscore'] = 0.0
            else:
                df[col + '_zscore'] = (df[col] - mean) / std
        else:
            logger.warning(f"Column {col} not found in dataframe for z-score computation.")
    
    return df

def filter_outliers_by_zscore(df, threshold=3.0):
    """
    Filter out rows where any z-scored column exceeds the absolute threshold.
    Returns the filtered dataframe and a log of removed indices.
    """
    if df.empty:
        return df, []

    logger.info(f"Filtering outliers with z-score threshold > {threshold}")
    
    # Identify z-score columns
    zscore_cols = [col for col in df.columns if col.endswith('_zscore')]
    
    if not zscore_cols:
        logger.warning("No z-score columns found. No outlier filtering performed.")
        return df, []
    
    # Create a mask for outliers
    outlier_mask = pd.DataFrame(False, index=df.index, columns=zscore_cols)
    for col in zscore_cols:
        outlier_mask[col] = df[col].abs() > threshold
    
    # Any row with an outlier in any z-score column
    rows_to_drop = outlier_mask.any(axis=1)
    removed_indices = df[rows_to_drop].index.tolist()
    
    if removed_indices:
        logger.info(f"Removing {len(removed_indices)} rows due to outlier z-scores.")
        df_filtered = df.drop(index=removed_indices)
    else:
        logger.info("No outliers detected.")
        df_filtered = df.copy()
    
    return df_filtered, removed_indices

def save_outlier_log(removed_indices, log_path):
    """
    Save the log of removed outlier indices to a JSON file.
    """
    log_data = {
        "timestamp": datetime.now().isoformat(),
        "filter_type": "z-score",
        "threshold": 3.0,
        "removed_count": len(removed_indices),
        "removed_indices": removed_indices
    }
    write_json_log(log_data, log_path)
    logger.info(f"Outlier log saved to {log_path}")

def save_processed_data(df, filename="cognitive_processed.parquet"):
    """
    Save the processed dataframe to the data/processed directory.
    """
    processed_path = get_data_processed_path()
    processed_path.mkdir(parents=True, exist_ok=True)
    
    output_file = processed_path / filename
    df.to_parquet(output_file, index=False)
    logger.info(f"Processed data saved to {output_file}")
    return output_file

def attempt_merge_and_report_gap():
    """
    Attempt to merge microbiome and cognitive data at the individual level.
    If merge fails (no common IDs), invoke the gap report generator.
    Returns True if merge successful, False otherwise.
    """
    raw_path = get_data_raw_path()
    processed_path = get_data_processed_path()
    
    # Paths to raw data files
    microbe_file = raw_path / "microbiome_raw.parquet"
    cognitive_file = raw_path / "cognitive_raw.parquet"
    processed_cognitive_file = processed_path / "cognitive_processed.parquet"
    
    if not microbe_file.exists():
        raise FileNotFoundError(f"Microbiome raw data not found at {microbe_file}. Run T011 first.")
    if not cognitive_file.exists():
        raise FileNotFoundError(f"Cognitive raw data not found at {cognitive_file}. Run T012 first.")
    
    logger.info("Loading microbiome and cognitive datasets for merge attempt...")
    df_microbe = pd.read_parquet(microbe_file)
    df_cognitive = pd.read_parquet(processed_cognitive_file)
    
    # Identify common ID columns
    # Assuming 'sample_id' or 'participant_id' are the keys
    potential_keys = ['sample_id', 'participant_id', 'id', 'subject_id']
    common_keys = [k for k in potential_keys if k in df_microbe.columns and k in df_cognitive.columns]
    
    if not common_keys:
        reason = "Individual-level linkage failed: No common sample ID columns found between microbiome and cognitive datasets."
        logger.error(reason)
        generate_gap_report(reason=reason)
        return False
    
    merge_key = common_keys[0]
    logger.info(f"Attempting merge on key: {merge_key}")
    
    # Perform inner join to find common samples
    df_merged = pd.merge(df_microbe, df_cognitive, on=merge_key, how='inner')
    
    if df_merged.empty:
        reason = f"Individual-level linkage failed: No common sample IDs found between microbiome and cognitive datasets on key '{merge_key}'."
        logger.error(reason)
        generate_gap_report(reason=reason)
        return False
    
    logger.info(f"Merge successful! Found {len(df_merged)} common samples.")
    
    # Save the merged dataset
    merged_file = processed_path / "merged_dataset.parquet"
    df_merged.to_parquet(merged_file, index=False)
    logger.info(f"Merged dataset saved to {merged_file}")
    
    return True

def main():
    """
    Main execution flow for preprocessing:
    1. Load cognitive raw data
    2. Apply MICE imputation
    3. Compute z-scores
    4. Filter outliers
    5. Save processed cognitive data
    6. Attempt merge with microbiome data
    7. If merge fails, generate gap report
    """
    try:
        # 1. Load data
        df = load_cognitive_raw_data()
        
        # 2. Imputation
        cognitive_cols = ['cognitive_score', 'reaction_time'] # Adjust based on actual schema
        df = apply_mice_imputation(df, cognitive_cols)
        
        # 3. Z-scores
        df = compute_z_scores(df, cognitive_cols)
        
        # 4. Outlier filtering
        df_filtered, removed_indices = filter_outliers_by_zscore(df)
        
        # 5. Save outlier log
        qc_path = get_data_qc_path()
        qc_path.mkdir(parents=True, exist_ok=True)
        outlier_log_path = qc_path / "filtering_log.json"
        save_outlier_log(removed_indices, outlier_log_path)
        
        # 6. Save processed data
        save_processed_data(df_filtered)
        
        # 7. Attempt merge
        success = attempt_merge_and_report_gap()
        
        if success:
            logger.info("Preprocessing and merge completed successfully.")
        else:
            logger.info("Merge failed. Gap report generated. Pipeline terminated.")
            # Exit with specific code to indicate gap
            sys.exit(0) 
            
    except Exception as e:
        logger.error(f"Error during preprocessing: {e}", exc_info=True)
        sys.exit(1)

if __name__ == "__main__":
    main()
