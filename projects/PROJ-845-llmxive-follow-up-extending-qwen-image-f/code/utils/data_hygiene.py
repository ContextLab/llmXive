"""
Data hygiene utilities for the llmXive pipeline.

This module provides functions to generate and record SHA256 checksums
for dataset files to ensure data integrity and reproducibility.
"""
import hashlib
import json
import os
from pathlib import Path
from typing import Dict, List, Optional

from config import get_config
from utils.logger import get_logger

# Define the expected data directories relative to the project root
# Based on T001, data is stored in projects/PROJ-845-llmxive-follow-up-extending-qwen-image-f/data/raw/
# We assume the script runs from the project root or we handle relative paths carefully.
# The task specifies CSVs are in data/raw/
DATA_RAW_DIR = "data/raw"
CHECKSUM_FILE = "data/processed/checksums.json"

logger = get_logger(__name__)

def compute_sha256(file_path: str) -> str:
    """
    Compute the SHA256 hash of a file.
    
    Args:
        file_path: Path to the file to hash.
        
    Returns:
        Hexadecimal string of the SHA256 hash.
        
    Raises:
        FileNotFoundError: If the file does not exist.
        IOError: If the file cannot be read.
    """
    sha256_hash = hashlib.sha256()
    with open(file_path, "rb") as f:
        # Read in chunks to handle large files efficiently
        for chunk in iter(lambda: f.read(4096), b""):
            sha256_hash.update(chunk)
    return sha256_hash.hexdigest()

def record_checksums(file_paths: List[str], output_path: Optional[str] = None) -> Dict[str, str]:
    """
    Generate SHA256 checksums for a list of files and record them.
    
    Args:
        file_paths: List of paths to the files to checksum.
        output_path: Path where the JSON checksum record will be saved.
                    If None, uses the default CHECKSUM_FILE.
                    
    Returns:
        Dictionary mapping file paths to their SHA256 hashes.
        
    Logs:
        Logs the checksum values to the logger.
        
    Raises:
        FileNotFoundError: If any file in file_paths does not exist.
    """
    if output_path is None:
        output_path = CHECKSUM_FILE
        
    # Ensure output directory exists
    output_dir = os.path.dirname(output_path)
    if output_dir:
        os.makedirs(output_dir, exist_ok=True)
        
    checksums = {}
    config = get_config()
    
    logger.info(f"Starting checksum generation for {len(file_paths)} files.")
    
    for file_path in file_paths:
        if not os.path.exists(file_path):
            logger.error(f"File not found for checksum: {file_path}")
            raise FileNotFoundError(f"File not found: {file_path}")
            
        try:
            checksum = compute_sha256(file_path)
            checksums[file_path] = checksum
            logger.info(f"Checksum for {os.path.basename(file_path)}: {checksum}")
        except IOError as e:
            logger.error(f"Failed to read file {file_path}: {e}")
            raise
            
    # Save to JSON
    try:
        with open(output_path, "w", encoding="utf-8") as f:
            json.dump(checksums, f, indent=2)
        logger.info(f"Checksums saved to {output_path}")
    except IOError as e:
        logger.error(f"Failed to write checksums to {output_path}: {e}")
        raise
        
    return checksums

def main():
    """
    Main entry point for generating checksums for the generated datasets.
    
    This function identifies the CSV files generated by the US1 pipeline
    (high_entropy.csv, low_entropy.csv, target_specific.csv, test_set.csv)
    and generates their checksums.
    """
    # Define the expected CSV files based on T016
    csv_files = [
        os.path.join(DATA_RAW_DIR, "high_entropy.csv"),
        os.path.join(DATA_RAW_DIR, "low_entropy.csv"),
        os.path.join(DATA_RAW_DIR, "target_specific.csv"),
        os.path.join(DATA_RAW_DIR, "test_set.csv"),
    ]
    
    # Filter to only existing files to avoid errors if some are missing
    # (though in a successful run, all should exist)
    existing_files = [f for f in csv_files if os.path.exists(f)]
    
    if not existing_files:
        logger.warning("No CSV files found in data/raw/. Did the generator run?")
        return
        
    logger.info(f"Found {len(existing_files)} CSV files to checksum.")
    
    try:
        record_checksums(existing_files)
        logger.info("Data hygiene checksum generation completed successfully.")
    except Exception as e:
        logger.error(f"Checksum generation failed: {e}")
        raise

if __name__ == "__main__":
    main()