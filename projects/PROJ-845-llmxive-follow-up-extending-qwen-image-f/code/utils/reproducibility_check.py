#
# Re-reading the task: "Add reproducibility verification script ... that runs the generator twice"
# If the generator doesn't exist, we can't run it.
# However, the task is to IMPLEMENT T041.
# The script must be valid Python.
# We will assume the generator script exists at `code/generators/generate_data.py`
# or similar. If it doesn't, the script will raise an error, which is appropriate
