"""
Reproducibility verification script for llmXive pipeline.

This script verifies that the data generation process is deterministic by:
1. Running the generator twice with the same seed from Config.
2. Computing SHA256 checksums of the generated CSV files.
3. Comparing the checksums to ensure they are identical.
4. Failing (exiting with code 1) if checksums differ, which will fail CI.

Usage:
    python code/utils/reproducibility_check.py
"""

