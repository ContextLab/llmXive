"""
Unit tests for perturbation construction.

Tests verify rank preservation, sparsity patterns, symmetry, and value scaling
of perturbation matrices generated by `code.generators.perturbation.create_perturbation`.
"""
import numpy as np
from scipy import sparse
from code.generators.perturbation import create_perturbation

def test_perturbation_diagonal_rank():
    """Verify rank is preserved in diagonal perturbation."""
    N = 100
    theta = 2.5
    rank = 1
    P = create_perturbation(N, theta, rank, pattern="diagonal")
    
    # Check rank by counting non-zero diagonal entries
    non_zero_diag = np.count_nonzero(P.diagonal())
    assert non_zero_diag == rank, f"Expected {rank} non-zero diagonal entries, got {non_zero_diag}"

def test_perturbation_diagonal_values():
    """Verify specific values in diagonal perturbation."""
    N = 100
    theta = 2.5
    rank = 2
    P = create_perturbation(N, theta, rank, pattern="diagonal")
    
    assert P[0, 0] == theta, f"Expected P[0,0] == {theta}, got {P[0,0]}"
    assert P[1, 1] == theta, f"Expected P[1,1] == {theta}, got {P[1,1]}"
    assert P[2, 2] == 0.0, f"Expected P[2,2] == 0.0, got {P[2,2]}"

def test_perturbation_sparse_rank():
    """Verify random sparse perturbations are sparse and symmetric."""
    N = 200
    theta = 3.0
    rank = 5
    density = 0.1
    
    P = create_perturbation(N, theta, rank, pattern="random_sparse", density=density)
    
    assert sparse.issparse(P), "Perturbation must be a sparse matrix"
    
    # Check symmetry
    diff = P - P.T
    assert diff.nnz == 0 or np.allclose(diff.data, 0.0), "Perturbation must be symmetric"

def test_perturbation_block_sparse_rank():
    """Verify block-sparse perturbation construction."""
    N = 100
    theta = 2.0
    block_size = 4
    num_blocks = 2
    rank = block_size * num_blocks
    
    P = create_perturbation(N, theta, rank, pattern="block_sparse", block_size=block_size, num_blocks=num_blocks)
    
    assert sparse.issparse(P), "Block sparse perturbation must be sparse"
    
    diag = P.diagonal()
    non_zero_count = np.count_nonzero(diag)
    assert non_zero_count == rank, f"Expected {rank} non-zero diagonal entries, got {non_zero_count}"

def test_perturbation_symmetry():
    """Ensure all perturbation patterns produce symmetric matrices."""
    N = 50
    theta = 1.5
    rank = 1
    
    patterns = ["diagonal", "random_sparse", "block_sparse"]
    kwargs_list = [
        {},
        {"density": 0.2},
        {"block_size": 2, "num_blocks": 1}
    ]
    
    for pattern, kwargs in zip(patterns, kwargs_list):
        try:
            P = create_perturbation(N, theta, rank, pattern=pattern, **kwargs)
            assert sparse.issparse(P), f"{pattern} must be sparse"
            
            diff = P - P.T
            if diff.nnz > 0:
                assert np.allclose(diff.data, 0.0), f"{pattern} perturbation is not symmetric"
        except TypeError:
            # Skip if kwargs don't match pattern requirements
            continue

def test_perturbation_zero_rank():
    """Test edge case: rank=0 should produce a zero matrix."""
    N = 10
    theta = 5.0
    rank = 0
    
    P = create_perturbation(N, theta, rank, pattern="diagonal")
    
    assert P.nnz == 0, "Rank 0 perturbation must be a zero matrix"
    assert np.allclose(P.toarray(), 0.0), "Rank 0 perturbation must be numerically zero"

def test_perturbation_scale():
    """Verify that the perturbation values match the specified theta."""
    N = 50
    theta = 4.2
    rank = 3
    
    P = create_perturbation(N, theta, rank, pattern="diagonal")
    diag = P.diagonal()
    non_zero_vals = diag[diag != 0]
    
    assert len(non_zero_vals) == rank
    assert np.allclose(non_zero_vals, theta), "Non-zero entries must equal theta"