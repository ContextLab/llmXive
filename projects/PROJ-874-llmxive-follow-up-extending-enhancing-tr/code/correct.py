"""
Correct naive baseline videos using optical flow (RAFT-Small) and non-differentiable warping.
Implements T021 (flow computation) and T022 (warping/smoothing) and T023 (fallback logic).
"""
import os
import sys
import argparse
import logging
import json
from pathlib import Path
from typing import List, Dict, Optional, Tuple
import numpy as np
import cv2

# Import from project modules
# Assuming these are in the same directory or PYTHONPATH is set
from utils.video import extract_frames_to_list, write_video
from utils.flow import (
    estimate_flow, 
    apply_nearest_neighbor_fallback, 
    is_flow_valid,
    load_raft_small
)

logger = logging.getLogger(__name__)

def get_naive_baseline_paths() -> List[str]:
    """
    Returns paths to naive baseline videos generated by T013.
    """
    data_dir = Path("data/processed")
    naive_dir = data_dir / "naive_baseline"
    
    if not naive_dir.exists():
        logger.error(f"Naive baseline directory not found: {naive_dir}")
        return []
        
    return [str(p) for p in naive_dir.glob("*.mp4")]

def verify_naive_baselines_exist() -> bool:
    """
    Verifies that naive baseline videos exist before processing.
    """
    paths = get_naive_baseline_paths()
    if not paths:
        logger.error("No naive baseline videos found. Aborting.")
        return False
    logger.info(f"Found {len(paths)} naive baseline videos.")
    return True

def process_video_flow(
    video_path: str,
    output_dir: str,
    prev_flow_cache: Optional[List[np.ndarray]] = None
) -> Tuple[bool, Optional[List[np.ndarray]]]:
    """
    Process a single video: compute flow, handle failures with fallback, and warp.
    
    Implements T023: Fallback logic for failed flow estimation using nearest-neighbor interpolation.
    
    Args:
        video_path: Path to the input video.
        output_dir: Directory to save the corrected video.
        prev_flow_cache: List of flow fields from the previous video (for cross-video continuity if needed, 
                         though typically flow is per-video).
                         
    Returns:
        Tuple of (success, list_of_corrected_frames).
    """
    logger.info(f"Processing video: {video_path}")
    
    # Extract frames
    frames = extract_frames_to_list(video_path)
    if not frames:
        logger.error(f"Failed to extract frames from {video_path}")
        return False, None
    
    logger.info(f"Extracted {len(frames)} frames from {video_path}")
    
    corrected_frames = []
    flow_fields = []
    current_flow_cache = []
    
    # Iterate through frame pairs
    for i in range(len(frames) - 1):
        frame1 = frames[i]
        frame2 = frames[i + 1]
        
        # 1. Estimate Flow
        flow = estimate_flow(frame1, frame2)
        
        # 2. T023 Fallback Logic: Check validity
        if not is_flow_valid(flow):
            logger.warning(f"Flow estimation failed for frame pair {i}-{i+1}. Applying fallback.")
            
            # Nearest-neighbor interpolation: Use previous flow if available
            fallback_flow = None
            if i > 0 and len(current_flow_cache) > 0:
                fallback_flow = current_flow_cache[-1]
            elif prev_flow_cache and len(prev_flow_cache) > 0:
                # Fallback to the last flow of the previous video (edge case)
                fallback_flow = prev_flow_cache[-1]
            
            if fallback_flow is not None:
                logger.info(f"Using fallback flow from frame {i-1}.")
                flow = fallback_flow
            else:
                logger.warning("No fallback flow available. Using zero flow.")
                flow = np.zeros((frame1.shape[0], frame1.shape[1], 2), dtype=np.float32)
        
        current_flow_cache.append(flow)
        
        # 3. Warp frame2 to frame1's perspective (or vice versa depending on convention)
        # T022: Non-differentiable warping and smoothing
        # We use cv2.remap for warping.
        # Flow maps displacement (x, y).
        # To warp frame2 to match frame1, we need to map each pixel in frame1 to its source in frame2.
        # Actually, usually flow (u, v) at (x, y) means the pixel at (x, y) in frame1 comes from (x+u, y+v) in frame2?
        # Or does it mean the pixel at (x, y) in frame1 moves to (x+u, y+v) in frame2?
        # RAFT usually outputs flow where flow[y, x] = (u, v) is the displacement of the pixel at (x, y) in frame1 to frame2.
        # To warp frame2 back to frame1 (to align with frame1), we need the inverse flow or a different mapping.
        # Standard approach: Create a grid of coordinates, add flow, and sample frame2.
        # But here we are generating "Condition C" outputs which implies smoothing/fixing.
        # Let's assume we warp frame2 to align with frame1 using the forward flow (inverse warping).
        # Actually, to smooth, we often average or use the flow to stabilize.
        # For this task, we will perform a simple warp of frame2 using the flow to align it with frame1.
        
        # Create meshgrid
        h, w = frame1.shape[:2]
        y, x = np.meshgrid(np.arange(h), np.arange(w), indexing='ij')
        
        # Flow is (u, v) = (x_new - x_old, y_new - y_old)
        # If flow[y, x] = (u, v), then pixel at (x, y) in frame1 corresponds to (x+u, y+v) in frame2.
        # To warp frame2 to frame1, we want to know for each (x, y) in frame1, where in frame2 to sample.
        # That is exactly (x+u, y+v).
        
        map_x = (x + flow[:, :, 0]).astype(np.float32)
        map_y = (y + flow[:, :, 1]).astype(np.float32)
        
        # Warp frame2 using the flow field
        warped_frame = cv2.remap(frame2, map_x, map_y, interpolation=cv2.INTER_LINEAR, borderMode=cv2.BORDER_REPLICATE)
        
        # Optional: Smoothing (T022)
        # Apply a simple blur to reduce artifacts
        smoothed_frame = cv2.GaussianBlur(warped_frame, (3, 3), 0)
        
        corrected_frames.append(smoothed_frame)
        flow_fields.append(flow)
    
    # Append the last frame (no flow for it)
    corrected_frames.append(frames[-1])
    
    # Save the corrected video
    output_path = os.path.join(output_dir, os.path.basename(video_path))
    write_video(corrected_frames, output_path)
    
    logger.info(f"Saved corrected video: {output_path}")
    
    return True, corrected_frames

def main():
    parser = argparse.ArgumentParser(description="Correct naive baseline videos using optical flow.")
    parser.add_argument("--output-dir", type=str, default="data/results/corrected", help="Output directory for corrected videos.")
    args = parser.parse_args()
    
    logging.basicConfig(level=logging.INFO)
    
    # Verify prerequisites
    if not verify_naive_baselines_exist():
        sys.exit(1)
    
    # Ensure output directory exists
    os.makedirs(args.output_dir, exist_ok=True)
    
    naive_paths = get_naive_baseline_paths()
    total_videos = len(naive_paths)
    success_count = 0
    
    for i, video_path in enumerate(naive_paths):
        logger.info(f"Processing video {i+1}/{total_videos}")
        success, _ = process_video_flow(video_path, args.output_dir)
        if success:
            success_count += 1
    
    logger.info(f"Completed. Success: {success_count}/{total_videos}")
    
    # Save a summary
    summary = {
        "total_videos": total_videos,
        "success": success_count,
        "output_dir": args.output_dir
    }
    with open(os.path.join(args.output_dir, "correction_summary.json"), "w") as f:
        json.dump(summary, f, indent=2)

if __name__ == "__main__":
    main()