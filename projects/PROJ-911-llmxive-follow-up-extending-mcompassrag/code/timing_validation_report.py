"""
Script to generate a validation report for the T017 timing constraint.
Reads the JSONL log generated by timing_logger and outputs a summary.
"""
import json
import sys
from pathlib import Path
from code.config import RESULTS_DIR
from code.timing_logger import TIMING_LOG_PATH, run_timing_validation

def main():
    if not TIMING_LOG_PATH.exists():
        print("No timing log found. Please run the pipeline first.")
        sys.exit(1)

    print(f"Analyzing timing log: {TIMING_LOG_PATH}")
    
    total_docs = 0
    violations = []
    avg_time = 0.0
    max_time = 0.0
    
    with open(TIMING_LOG_PATH, "r", encoding="utf-8") as f:
        for line in f:
            if not line.strip():
                continue
            entry = json.loads(line)
            if entry.get("status") == "error":
                continue
            
            duration = entry.get("duration_seconds", 0.0)
            total_docs += 1
            avg_time += duration
            if duration > max_time:
                max_time = duration
            if duration >= 60.0:
                violations.append(entry)
    
    if total_docs == 0:
        print("No successful processing entries found.")
        sys.exit(1)
        
    avg_time /= total_docs
    
    print(f"\n--- Timing Validation Report ---")
    print(f"Total Documents Processed: {total_docs}")
    print(f"Average Processing Time: {avg_time:.2f}s")
    print(f"Maximum Processing Time: {max_time:.2f}s")
    print(f"Constraint Threshold: 60.0s")
    
    if violations:
        print(f"\n❌ VALIDATION FAILED: {len(violations)} documents exceeded the 60s limit.")
        for v in violations:
            print(f"   - {v['doc_id']}: {v['duration_seconds']:.2f}s")
        sys.exit(1)
    else:
        print(f"\n✅ VALIDATION PASSED: All documents processed within the 60s limit.")
        sys.exit(0)

if __name__ == "__main__":
    main()