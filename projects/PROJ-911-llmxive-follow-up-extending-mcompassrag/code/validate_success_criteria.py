"""
T032: Validate results against Success Criteria (SC-001 to SC-005).

This script loads the correlation results generated by T028/T031,
checks the hypothesis status (r > 0.6), and logs the findings.
It does NOT raise exceptions on low correlation; it only logs the status.
"""

import json
import logging
import sys
from pathlib import Path
from typing import Dict, Any, Optional

# Import from existing API surface
from code.config import RESULTS_DIR, PROJECT_ROOT
from code.final_metrics_writer import load_spearman_correlation_results

# Configure logging
logging.basicConfig(
    level=logging.INFO,
    format='%(asctime)s - %(levelname)s - %(message)s',
    handlers=[
        logging.StreamHandler(sys.stdout),
        logging.FileHandler(PROJECT_ROOT / "logs" / "validation.log", mode='a', encoding='utf-8')
    ]
)
logger = logging.getLogger(__name__)

# Define paths
METRICS_PATH = RESULTS_DIR / "metrics.json"
CORRELATION_PATH = RESULTS_DIR / "correlation.csv"
VALIDATION_OUTPUT_PATH = RESULTS_DIR / "validation_report.json"

# Success Criteria Thresholds (from SC-001 to SC-005 logic)
# Hypothesis supported if Spearman r > 0.6
HYPOTHESIS_THRESHOLD_R = 0.6

def load_metrics_data() -> Dict[str, Any]:
    """Load the final metrics generated by T031."""
    if not METRICS_PATH.exists():
        logger.error(f"Metrics file not found at {METRICS_PATH}. Ensure T031 has run.")
        return {}
    
    with open(METRICS_PATH, 'r', encoding='utf-8') as f:
        return json.load(f)

def load_correlation_results() -> Dict[str, Any]:
    """Load Spearman correlation results from the final metrics writer."""
    try:
        # Using the helper from final_metrics_writer to load the correlation data
        # This assumes T028 has run and populated the metrics
        results = load_spearman_correlation_results()
        return results
    except Exception as e:
        logger.warning(f"Could not load correlation results via helper: {e}. Attempting direct load.")
        return {}

def validate_hypothesis(r_value: float, p_value: float) -> Dict[str, Any]:
    """
    Validate the hypothesis based on correlation coefficient r.
    
    Returns a status dict indicating if the hypothesis was supported.
    Does NOT raise an exception on low r.
    """
    status = "unsupported"
    if r_value > HYPOTHESIS_THRESHOLD_R:
        status = "supported"
        logger.info(f"Hypothesis SUPPORTED: r={r_value:.4f} > {HYPOTHESIS_THRESHOLD_R}")
    else:
        logger.info(f"Hypothesis UNSUPPORTED: r={r_value:.4f} <= {HYPOTHESIS_THRESHOLD_R}")
    
    return {
        "hypothesis_status": status,
        "correlation_coefficient_r": r_value,
        "p_value": p_value,
        "threshold_r": HYPOTHESIS_THRESHOLD_R,
        "details": f"Correlation r={r_value:.4f} (p={p_value:.4e}). Threshold: {HYPOTHESIS_THRESHOLD_R}"
    }

def run_validation() -> Dict[str, Any]:
    """
    Main validation routine.
    1. Load metrics.
    2. Extract r and p-value.
    3. Determine hypothesis status.
    4. Log results.
    5. Write validation report.
    """
    logger.info("Starting Success Criteria Validation (T032)...")

    # Load data
    metrics = load_metrics_data()
    correlation_data = load_correlation_results()

    if not metrics and not correlation_data:
        logger.error("No metrics or correlation data found. Validation cannot proceed.")
        return {"error": "No data found"}

    # Extract r and p-value
    # Try to get from correlation_data first, then metrics
    r_value = None
    p_value = None

    if correlation_data:
        if isinstance(correlation_data, dict):
            r_value = correlation_data.get('r') or correlation_data.get('correlation_r')
            p_value = correlation_data.get('p_value') or correlation_data.get('p')
    
    if r_value is None and metrics:
        r_value = metrics.get('correlation_r') or metrics.get('r')
        p_value = metrics.get('p_value') or metrics.get('p')

    if r_value is None:
        logger.warning("Could not determine correlation coefficient r from available data.")
        # Fallback to 0.0 to ensure a valid report structure, though status will be unsupported
        r_value = 0.0
        p_value = 1.0

    # Perform validation
    validation_result = validate_hypothesis(r_value, p_value)

    # Prepare full report
    report = {
        "task_id": "T032",
        "validation_status": "completed",
        "success_criteria_met": validation_result["hypothesis_status"] == "supported",
        "results": validation_result,
        "timestamp": None # Can be added if datetime imported, but kept simple per constraints
    }

    # Write report to disk
    try:
        with open(VALIDATION_OUTPUT_PATH, 'w', encoding='utf-8') as f:
            json.dump(report, f, indent=2)
        logger.info(f"Validation report written to {VALIDATION_OUTPUT_PATH}")
    except Exception as e:
        logger.error(f"Failed to write validation report: {e}")

    return report

def main():
    """Entry point for the validation script."""
    try:
        report = run_validation()
        print(json.dumps(report, indent=2))
        return 0
    except Exception as e:
        logger.critical(f"Validation pipeline failed: {e}")
        return 1

if __name__ == "__main__":
    sys.exit(main())