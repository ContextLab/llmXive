"""
T016: Write graph artifacts and feature vectors to data/processed/

This script orchestrates the final output phase of User Story 1.
It loads graphs generated by T013 and features extracted by T014/T015,
then writes them to the required JSON and CSV formats.

Outputs:
  - data/processed/graphs.json: Serialized graph structures (adjacency lists)
  - data/processed/features.csv: Tabular topological features per document
"""
import json
import csv
import logging
import sys
from pathlib import Path
from typing import List, Dict, Any

# Import from existing project API surface
from code.config import PROCESSED_DIR, RANDOM_SEED
from code.graph_builder import load_graphs, save_graphs as graph_builder_save_graphs
from code.topology_extractor import load_graphs as topology_load_graphs, extract_topological_features, save_features as topology_save_features

# Configure logging
logging.basicConfig(
    level=logging.INFO,
    format='%(asctime)s - %(levelname)s - %(message)s',
    stream=sys.stdout
)
logger = logging.getLogger(__name__)

def load_graphs_from_builder() -> List[Dict[str, Any]]:
    """
    Load the list of graphs generated by T013 (graph_builder).
    Returns a list of dicts where each dict represents a document's graph.
    """
    # The graph_builder module saves graphs to PROCESSED_DIR / 'graphs.json' 
    # during its pipeline, but we need to ensure we load the latest state.
    # We'll re-run the load logic from graph_builder to get the raw graph objects.
    graph_path = PROCESSED_DIR / 'graphs.json'
    if not graph_path.exists():
        logger.error(f"Graph file not found at {graph_path}. Did T013 run?")
        raise FileNotFoundError(f"Missing graph artifacts: {graph_path}")
    
    with open(graph_path, 'r', encoding='utf-8') as f:
        return json.load(f)

def load_features_from_extractor() -> List[Dict[str, Any]]:
    """
    Load the features generated by T014/T015 (topology_extractor).
    Returns a list of dicts containing topological metrics per document.
    """
    # The topology_extractor saves to PROCESSED_DIR / 'features.csv'
    # We need to load this into memory to combine with graph metadata if needed,
    # but primarily we just ensure the final clean version is written.
    features_path = PROCESSED_DIR / 'features.csv'
    if not features_path.exists():
        logger.error(f"Features file not found at {features_path}. Did T014/T015 run?")
        raise FileNotFoundError(f"Missing feature artifacts: {features_path}")
    
    features = []
    with open(features_path, 'r', encoding='utf-8') as f:
        reader = csv.DictReader(f)
        for row in reader:
            # Convert numeric strings back to float/int where appropriate
            processed_row = {}
            for k, v in row.items():
                if k == 'doc_id':
                    processed_row[k] = v
                else:
                    try:
                        processed_row[k] = float(v)
                    except ValueError:
                        processed_row[k] = v
            features.append(processed_row)
    return features

def write_final_graphs_json(graphs: List[Dict[str, Any]]) -> Path:
    """
    Write the final graphs.json artifact.
    Ensures the file is valid JSON and contains the expected structure.
    """
    output_path = PROCESSED_DIR / 'graphs.json'
    logger.info(f"Writing {len(graphs)} graphs to {output_path}")
    
    with open(output_path, 'w', encoding='utf-8') as f:
        json.dump(graphs, f, indent=2, ensure_ascii=False)
    
    logger.info(f"Successfully wrote graphs to {output_path}")
    return output_path

def write_final_features_csv(features: List[Dict[str, Any]]) -> Path:
    """
    Write the final features.csv artifact.
    Ensures the file is valid CSV with proper headers.
    """
    if not features:
        logger.warning("No features to write.")
        return PROCESSED_DIR / 'features.csv'
    
    output_path = PROCESSED_DIR / 'features.csv'
    fieldnames = list(features[0].keys())
    
    logger.info(f"Writing {len(features)} feature rows to {output_path}")
    
    with open(output_path, 'w', newline='', encoding='utf-8') as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(features)
    
    logger.info(f"Successfully wrote features to {output_path}")
    return output_path

def run_pipeline():
    """
    Main entry point for T016.
    1. Loads intermediate artifacts from T013 and T014/T015.
    2. Validates data integrity.
    3. Writes final artifacts to data/processed/.
    """
    logger.info("Starting T016: Writing graph artifacts and feature vectors")
    
    try:
        # 1. Load graphs from T013
        graphs = load_graphs_from_builder()
        logger.info(f"Loaded {len(graphs)} graph objects.")
        
        # 2. Load features from T014/T015
        features = load_features_from_extractor()
        logger.info(f"Loaded {len(features)} feature records.")
        
        # 3. Validation: Check count consistency
        if len(graphs) != len(features):
            logger.warning(
                f"Count mismatch: {len(graphs)} graphs vs {len(features)} features. "
                "Proceeding anyway, but this may indicate a pipeline issue."
            )
        
        # 4. Write final artifacts
        write_final_graphs_json(graphs)
        write_final_features_csv(features)
        
        logger.info("T016 completed successfully.")
        
    except FileNotFoundError as e:
        logger.critical(f"Pipeline failed due to missing input: {e}")
        sys.exit(1)
    except Exception as e:
        logger.critical(f"Pipeline failed with unexpected error: {e}")
        raise

if __name__ == "__main__":
    run_pipeline()
