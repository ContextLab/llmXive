def test_score_ospan_trial():
    """Test single trial scoring."""
    # Correct trial
    result = score_ospan_trial(
        stimulus="apple",
        recall="apple",
        accuracy=True
    )
    assert result["correct"] is True
    assert result["stimulus"] == "apple"

    # Incorrect trial
    result = score_ospan_trial(
        stimulus="apple",
        recall="orange",
        accuracy=False
    )
    assert result["correct"] is False

