def test_batch_scoring_consistency():
    """Test that batch scoring produces same results as individual scoring."""
    data = load_synthetic_baseline_data()
    
    participant_ids = list(set(row['participant_id'] for row in data))
    
    # Prepare batch data
    batch_pss10 = []
    batch_panas_pos = []
    batch_panas_neg = []
    
    for pid in participant_ids[:3]:
        pss10_resp = extract_pss10_responses(data, pid)
        panas_pos_resp = extract_panas_responses(data, pid, 'positive')
        panas_neg_resp = extract_panas_responses(data, pid, 'negative')
        
        if pss10_resp:
            batch_pss10.append({
                'participant_id': pid,
                'responses': [pss10_resp[i] for i in range(1, 11)]
            })
        
        if panas_pos_resp:
            batch_panas_pos.append({
                'participant_id': pid,
                'responses': [panas_pos_resp[i] for i in range(1, 11)]
            })
        
        if panas_neg_resp:
            batch_panas_neg.append({
                'participant_id': pid,
                'responses': [panas_neg_resp[i] for i in range(1, 11)]
            })
    
    # Score individually
    individual_pss10_scores = {}
    for item in batch_pss10:
        score = score_pss10_session(item['responses'])
        individual_pss10_scores[item['participant_id']] = score['total_score']
    
    individual_panas_pos_scores = {}
    for item in batch_panas_pos:
        score = score_panas_session(item['responses'], scale='positive')
        individual_panas_pos_scores[item['participant_id']] = score['total_score']
    
    individual_panas_neg_scores = {}
    for item in batch_panas_neg:
        score = score_panas_session(item['responses'], scale='negative')
        individual_panas_neg_scores[item['participant_id']] = score['total_score']
    
    # Score in batch
    batch_results = score_questionnaires_batch(
        pss10=batch_pss10,
        panas_pos=batch_panas_pos,
        panas_neg=batch_panas_neg
    )
    
    # Compare results
    for pid, score in batch_results.get('pss10', {}).items():
        assert pid in individual_pss10_scores, f"Missing participant {pid} in individual scores"
        assert score['total_score'] == individual_pss10_scores[pid], \
            f"PSS-10 batch score mismatch for {pid}: {score['total_score']} vs {individual_pss10_scores[pid]}"
    
    for pid, score in batch_results.get('panas_pos', {}).items():
        assert pid in individual_panas_pos_scores, f"Missing participant {pid} in individual scores"
        assert score['total_score'] == individual_panas_pos_scores[pid], \
            f"PANAS Pos batch score mismatch for {pid}: {score['total_score']} vs {individual_panas_pos_scores[pid]}"
    
    for pid, score in batch_results.get('panas_neg', {}).items():
        assert pid in individual_panas_neg_scores, f"Missing participant {pid} in individual scores"
        assert score['total_score'] == individual_panas_neg_scores[pid], \
            f"PANAS Neg batch score mismatch for {pid}: {score['total_score']} vs {individual_panas_neg_scores[pid]}"

