def load_synthetic_baseline_data():
    """Load the synthetic baseline data generated by T017."""
    data_path = project_root / 'data' / 'raw' / 'synthetic_baseline.csv'
    
    if not data_path.exists():
        raise FileNotFoundError(
            f"Synthetic baseline data not found at {data_path}. "
            "Please run T017 (code/validation/synthetic_baseline.py) first."
        )
    
    data = []
    with open(data_path, 'r') as f:
        reader = csv.DictReader(f)
        for row in reader:
            data.append(row)
    
    return data

def extract_pss10_responses(data, participant_id):
    """Extract PSS-10 responses for a specific participant."""
    pss10_items = {}
    for row in data:
        if row['participant_id'] == participant_id and row['metric_type'].startswith('PSS10_'):
            item_num = int(row['metric_type'].split('_')[1])
            pss10_items[item_num] = float(row['value'])
    
    return pss10_items if len(pss10_items) == 10 else None

def extract_panas_responses(data, participant_id, scale_type='positive'):
    """Extract PANAS responses for a specific participant."""
    prefix = 'PANAS_POS' if scale_type == 'positive' else 'PANAS_NEG'
    panas_items = {}
    
    for row in data:
        if row['participant_id'] == participant_id and row['metric_type'].startswith(prefix):
            item_num = int(row['metric_type'].split('_')[1])
            panas_items[item_num] = float(row['value'])
    
    return panas_items if len(panas_items) == 10 else None

def test_pss10_score_range():
    """Test that PSS-10 scores are within valid range [0, 40]."""
    data = load_synthetic_baseline_data()
    
    # Get unique participant IDs
    participant_ids = list(set(row['participant_id'] for row in data))
    
    for pid in participant_ids[:5]:  # Test first 5 participants
        responses = extract_pss10_responses(data, pid)
        if responses is None:
            continue
        
        # Convert to list format expected by scoring function
        responses_list = [responses[i] for i in range(1, 11)]
        
        score = score_pss10_session(responses_list)
        
        assert isinstance(score, dict), "PSS-10 score should be a dictionary"
        assert 'total_score' in score, "PSS-10 score should contain 'total_score'"
        assert 'item_scores' in score, "PSS-10 score should contain 'item_scores'"
        
        total = score['total_score']
        assert PSS10_MIN_SCORE <= total <= PSS10_MAX_SCORE, \
            f"PSS-10 score {total} out of range [{PSS10_MIN_SCORE}, {PSS10_MAX_SCORE}]"

