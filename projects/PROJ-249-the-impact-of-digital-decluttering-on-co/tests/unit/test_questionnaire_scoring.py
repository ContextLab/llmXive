def test_pss10_reverse_scoring():
    """Test that reverse-scored items are handled correctly in PSS-10."""
    # Create a test case where all items are 5 (max)
    # Items 4, 7, 8, 11 are reverse-scored in PSS-10
    # So if all raw responses are 5, reverse items become 1 (5-5+1)
    # Total should be: 6*5 + 4*1 = 30 + 4 = 34
    test_responses = [5.0] * 10  # All max responses
    
    score = score_pss10_session(test_responses)
    
    # Expected: 6 forward items (5 each) + 4 reverse items (1 each) = 34
    expected_score = 34
    assert score['total_score'] == expected_score, \
        f"PSS-10 reverse scoring failed: expected {expected_score}, got {score['total_score']}"

