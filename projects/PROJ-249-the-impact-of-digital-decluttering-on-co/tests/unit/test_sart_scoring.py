def test_integration_with_synthetic_data(synthetic_data_path):
    """
    End-to-End Test:
    1. Load the synthetic data generated by T017.
    2. Filter for SART metrics.
    3. Reconstruct trials.
    4. Verify scoring runs without error and produces plausible ranges.
    """
    if not synthetic_data_path.exists():
        pytest.skip("Synthetic data file not found (T017 might not have run).")

    trials = []
    with open(synthetic_data_path, 'r') as f:
        reader = csv.DictReader(f)
        for row in reader:
            if row['metric_type'] == 'SART':
                # Reconstruct trial structure from synthetic CSV
                # Synthetic CSV format: participant_id, metric_type, value, timestamp
                # We assume 'value' in synthetic data for SART might be encoded or we need to mock the trial structure
                # Since T017 generates raw metrics, we simulate the trial reconstruction for testing the scorer
                # In a real scenario, raw JSON from T014.1 would be used.
                # Here we create a deterministic trial based on the row to ensure the scorer works.
                try:
                    rt = float(row['value'])
                    # Deterministic assignment for testing based on index
                    is_target = (rt % 2 == 0)
                    # Simulate accuracy based on RT (faster = more likely commission on non-target)
                    acc = True
                    if not is_target and rt < 300:
                        acc = False # Commission
                    
                    trial = {
                        'response_time': rt,
                        'accuracy': acc,
                        'stimulus_type': 'Target' if is_target else 'Non-Target'
                    }
                    trials.append(trial)
                except ValueError:
                    continue

    if not trials:
        pytest.skip("No SART trials found in synthetic data.")

    session_result = score_sart_session(trials)
    
    # Plausibility checks (FR-009)
    assert session_result['commission_errors'] >= 0
    assert session_result['omission_errors'] >= 0
    assert session_result['mean_rt'] >= 0
    
    # Verify that errors are not negative or impossibly large for the sample size
    assert session_result['commission_errors'] <= len(trials)
    assert session_result['omission_errors'] <= len(trials)