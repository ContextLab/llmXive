def test_score_sart_session_aggregation():
    """
    OSF Ref: Verify session-level aggregation of multiple trials.
    Creates a mock session with known errors.
    """
    trials = [
        # Target, Correct
        {'response_time': 300.0, 'accuracy': True, 'stimulus_type': 'Target'},
        # Target, Miss (Omission)
        {'response_time': 0.0, 'accuracy': False, 'stimulus_type': 'Target'},
        # Non-Target, False Alarm (Commission)
        {'response_time': 250.0, 'accuracy': True, 'stimulus_type': 'Non-Target'},
        # Non-Target, Correct
        {'response_time': 0.0, 'accuracy': False, 'stimulus_type': 'Non-Target'},
        # Target, Correct
        {'response_time': 320.0, 'accuracy': True, 'stimulus_type': 'Target'},
    ]
    
    result = score_sart_session(trials)
    
    # Expected: 1 Omission, 1 Commission
    assert result['commission_errors'] == 1, f"Expected 1 commission, got {result['commission_errors']}"
    assert result['omission_errors'] == 1, f"Expected 1 omission, got {result['omission_errors']}"
    
    # Mean RT should only count valid responses (accuracy=True)
    # Valid RTs: 300.0, 250.0, 320.0. Sum = 870. Count = 3. Mean = 290.0
    # Note: Implementation logic in score_sart_session determines if 0.0 is included.
    # Standard SART scoring usually excludes non-responses from RT mean.
    # Assuming standard logic: mean_rt = 290.0
    expected_mean_rt = 290.0
    assert np.isclose(result['mean_rt'], expected_mean_rt, atol=0.1), \
        f"Expected mean RT ~{expected_mean_rt}, got {result['mean_rt']}"

