# Constants for OSF Reference Validation
# SART Commission Errors: False positives (responding to Non-Target)
# SART Omission Errors: False negatives (failing to respond to Target)
# Reference Logic:
# - Stimulus 'Target': Expect response (accuracy=True). If accuracy=False -> Omission.
# - Stimulus 'Non-Target': Expect no response (accuracy=False). If accuracy=True -> Commission.
# - Response Time: Must be > 0 and typically < 2000ms for valid trials.

