def validate_record(record, schema_entity):
    """
    Validate a single record against the ComplianceLog schema entity.
    
    Args:
        record: Dictionary containing the log data.
        schema_entity: The definition for ComplianceLog from the schema.
    
    Raises:
        AssertionError: If validation fails.
    """
    required_fields = schema_entity.get("required", [])
    properties = schema_entity.get("properties", {})

    # Check required fields
    for field in required_fields:
        if field not in record:
            raise AssertionError(f"Missing required field: {field}")

    # Check field types and constraints
    for field, value in record.items():
        if field not in properties:
            # Allow extra fields? Usually strict schema validation rejects them.
            # For this contract test, we assume strict adherence.
            raise AssertionError(f"Unexpected field in compliance log: {field}")

        prop_def = properties[field]
        expected_type = prop_def.get("type")

        # Type checking
        if expected_type == "string":
            if not isinstance(value, str):
                raise AssertionError(f"Field {field} must be string, got {type(value)}")
            # Check pattern if defined (e.g., participant_id P\d{3})
            if "pattern" in prop_def:
                import re
                if not re.match(prop_def["pattern"], str(value)):
                    raise AssertionError(f"Field {field} value '{value}' does not match pattern {prop_def['pattern']}")
        
        elif expected_type == "integer":
            if not isinstance(value, int):
                raise AssertionError(f"Field {field} must be integer, got {type(value)}")
            # Check range if defined
            if "minimum" in prop_def and value < prop_def["minimum"]:
                raise AssertionError(f"Field {field} value {value} is below minimum {prop_def['minimum']}")
        
        elif expected_type == "boolean":
            if not isinstance(value, bool):
                raise AssertionError(f"Field {field} must be boolean, got {type(value)}")
        
        elif expected_type == "number":
            if not isinstance(value, (int, float)):
                raise AssertionError(f"Field {field} must be number, got {type(value)}")
        
        # Check enum constraints
        if "enum" in prop_def:
            if value not in prop_def["enum"]:
                raise AssertionError(f"Field {field} value '{value}' not in allowed values: {prop_def['enum']}")

@pytest.mark.contract
def test_compliance_schema_structure():
    """Test that the schema file exists and defines the ComplianceLog entity."""
    schema = load_schema()
    assert "ComplianceLog" in schema.get("entities", {}), "ComplianceLog entity missing from schema"

@pytest.mark.contract
def test_compliance_log_valid_records():
    """Test that valid compliance records pass schema validation."""
    schema = load_schema()
    compliance_schema = schema["entities"]["ComplianceLog"]
    records = load_sample_data()

    for i, record in enumerate(records):
        try:
            validate_record(record, compliance_schema)
        except AssertionError as e:
            pytest.fail(f"Record {i} failed validation: {e}")

@pytest.mark.contract
def test_compliance_log_invalid_type():
    """Test that records with wrong types fail validation."""
    schema = load_schema()
    compliance_schema = schema["entities"]["ComplianceLog"]
    
    invalid_record = {
        "participant_id": 123,  # Should be string
        "date": "2023-10-01",
        "platform_type": "social_media",
        "minutes": 45,
        "notifications_disabled": True,
        "news_consumed": False,
        "timestamp": "2023-10-01T23:59:59"
    }
    
    with pytest.raises(AssertionError) as exc_info:
        validate_record(invalid_record, compliance_schema)
    assert "participant_id" in str(exc_info.value)

