"""
Contract test for compliance log schema (T021).

Validates that compliance log data strictly adheres to the schema defined
in contracts/dataset.schema.yaml, specifically for the ComplianceLog entity.

This test ensures:
1. All required fields are present.
2. Field types match the schema (e.g., participant_id is string, minutes is int/float).
3. Enum constraints are respected (e.g., platform_type).
4. Value ranges are valid (e.g., minutes >= 0).
"""

