def load_schema():
    """Load the YAML schema definition."""
    if not SCHEMA_PATH.exists():
        pytest.fail(f"Schema file not found at {SCHEMA_PATH}. Ensure T007 is complete.")
    
    import yaml
    with open(SCHEMA_PATH, 'r') as f:
        return yaml.safe_load(f)

