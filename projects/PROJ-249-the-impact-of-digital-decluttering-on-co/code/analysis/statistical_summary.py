logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def load_p_values_from_json(filepath: str) -> Dict[str, float]:
    """
    Load p-values from a JSON file generated by bootstrap analysis or Wilcoxon fallback.
    Expected format: {"metric_name": p_value, ...}
    """
    try:
        with open(filepath, 'r') as f:
            data = json.load(f)
        # Ensure we return a dict of string -> float
        return {str(k): float(v) for k, v in data.items()}
    except FileNotFoundError:
        logger.error(f"P-values file not found: {filepath}")
        return {}
    except json.JSONDecodeError as e:
        logger.error(f"Invalid JSON in p-values file {filepath}: {e}")
        return {}

def aggregate_results(
    change_scores_path: str,
    bootstrap_results_path: str,
    holm_results_path: str
) -> Dict[str, Any]:
    """
    Aggregate results from change scores, bootstrap CIs, and Holm-Bonferroni corrections
    into a single statistical summary dictionary.
    """
    # Load change scores
    change_data = {}
    if os.path.exists(change_scores_path):
        # Assuming change scores are stored as JSON or CSV
        # We'll use the load_merged_data utility if needed, but for simplicity:
        try:
            with open(change_scores_path, 'r') as f:
                change_data = json.load(f)
        except Exception as e:
            logger.warning(f"Could not load change scores: {e}")
    else:
        logger.warning(f"Change scores file not found: {change_scores_path}")

    # Load bootstrap results (mean change, CI)
    bootstrap_results = {}
    if os.path.exists(bootstrap_results_path):
        try:
            with open(bootstrap_results_path, 'r') as f:
                bootstrap_results = json.load(f)
        except Exception as e:
            logger.warning(f"Could not load bootstrap results: {e}")
    else:
        logger.warning(f"Bootstrap results file not found: {bootstrap_results_path}")

    # Load Holm-Bonferroni corrected p-values
    holm_results = load_p_values_from_json(holm_results_path)

    # Aggregate into final summary
    summary = {
        "metrics": {},
        "metadata": {
            "generated_from": {
                "change_scores": change_scores_path,
                "bootstrap_results": bootstrap_results_path,
                "holm_results": holm_results_path
            }
        }
    }

    # Get all unique metric names
    all_metrics = set()
    all_metrics.update(change_data.keys())
    all_metrics.update(bootstrap_results.keys())
    all_metrics.update(holm_results.keys())

    for metric in all_metrics:
        metric_summary = {
            "mean_change": None,
            "ci_95_lower": None,
            "ci_95_upper": None,
            "p_value_raw": None,
            "p_value_corrected": None,
            "significant_after_correction": False
        }

        if metric in change_data:
            metric_summary["mean_change"] = change_data[metric]

        if metric in bootstrap_results:
            br = bootstrap_results[metric]
            if isinstance(br, dict):
                metric_summary["ci_95_lower"] = br.get("ci_lower")
                metric_summary["ci_95_upper"] = br.get("ci_upper")
                metric_summary["p_value_raw"] = br.get("p_value")

        if metric in holm_results:
            metric_summary["p_value_corrected"] = holm_results[metric]
            if holm_results[metric] is not None and holm_results[metric] < 0.05:
                metric_summary["significant_after_correction"] = True

        summary["metrics"][metric] = metric_summary

    return summary

def write_summary(summary: Dict[str, Any], output_path: str) -> None:
    """Write the statistical summary to a JSON file."""
    os.makedirs(os.path.dirname(output_path), exist_ok=True)
    with open(output_path, 'w') as f:
        json.dump(summary, f, indent=2)
    logger.info(f"Statistical summary written to {output_path}")

def main():
    """Main entry point to generate the statistical summary."""
    config = get_config()
    
    # Define paths based on config or defaults
    # These should match the outputs from previous tasks
    change_scores_path = get_path("results/change_scores.json")
    bootstrap_results_path = get_path("results/bootstrap_results.json")
    holm_results_path = get_path("results/holm_bonferroni.json")
    output_path = get_path("results/statistical_summary.json")

    # Ensure input files exist (they should from previous tasks)
    missing_inputs = []
    if not os.path.exists(change_scores_path):
        missing_inputs.append(change_scores_path)
    if not os.path.exists(bootstrap_results_path):
        missing_inputs.append(bootstrap_results_path)
    if not os.path.exists(holm_results_path):
        missing_inputs.append(holm_results_path)

    if missing_inputs:
        logger.error(f"Missing required input files: {missing_inputs}")
        logger.error("Please ensure T035 (change_scores), T036 (bootstrap_ci), and T038 (holm_bonferroni) are completed.")
        return

    logger.info("Aggregating statistical results...")
    summary = aggregate_results(change_scores_path, bootstrap_results_path, holm_results_path)
    
    logger.info(f"Writing statistical summary to {output_path}")
    write_summary(summary, output_path)
    
    logger.info("Statistical summary generation complete.")
    return summary

if __name__ == "__main__":
    main()
