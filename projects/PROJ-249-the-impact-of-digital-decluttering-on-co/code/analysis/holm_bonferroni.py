"""
Holm-Bonferroni step-down correction for multiple hypothesis testing.

Implements the Holm-Bonferroni method to control the Family-Wise Error Rate (FWER)
when performing multiple statistical tests. This is used in the post-intervention
analysis to correct p-values across multiple cognitive and emotional metrics.

FR-008: Apply Holm-Bonferroni correction to all primary hypothesis tests.
"""
import os
import json
import csv
import logging
from pathlib import Path
from typing import List, Dict, Any, Optional, Tuple
from dataclasses import dataclass, asdict

# Import random seed utility for reproducibility if needed, though this is deterministic
from utils.random_seed import get_seed

logger = logging.getLogger(__name__)


@dataclass
class HolmBonferroniResult:
    """
    Result of the Holm-Bonferroni correction for a single metric.

    Attributes:
        metric_name: Name of the metric being tested.
        raw_p_value: The uncorrected p-value from the statistical test.
        corrected_p_value: The Holm-Bonferroni corrected p-value.
        is_significant: True if corrected_p_value < alpha (default 0.05).
        rank: The rank of this p-value in the sorted list (1-indexed).
    """
    metric_name: str
    raw_p_value: float
    corrected_p_value: float
    is_significant: bool
    rank: int


def calculate_holm_bonferroni(p_values: List[Dict[str, Any]], alpha: float = 0.05) -> List[HolmBonferroniResult]:
    """
    Apply Holm-Bonferroni step-down correction to a list of p-values.

    The algorithm:
    1. Sort p-values in ascending order.
    2. For each p-value at rank i (1-indexed), calculate adjusted p-value = p_i * (m - i + 1).
    3. Ensure monotonicity: adjusted p-values must be non-decreasing.
    4. Compare against alpha to determine significance.

    Args:
        p_values: List of dicts with keys 'metric_name' and 'p_value'.
        alpha: Significance threshold (default 0.05).

    Returns:
        List of HolmBonferroniResult objects sorted by raw p-value.
    """
    if not p_values:
        logger.warning("No p-values provided for Holm-Bonferroni correction.")
        return []

    m = len(p_values)
    if m == 1:
        # Single test: correction factor is 1
        res = p_values[0]
        corrected = min(res['p_value'] * m, 1.0)
        return [HolmBonferroniResult(
            metric_name=res['metric_name'],
            raw_p_value=res['p_value'],
            corrected_p_value=corrected,
            is_significant=corrected < alpha,
            rank=1
        )]

    # Sort by p-value
    sorted_items = sorted(p_values, key=lambda x: x['p_value'])

    results = []
    max_corrected = 0.0

    for i, item in enumerate(sorted_items):
        rank = i + 1
        raw_p = item['p_value']
        
        # Holm-Bonferroni adjustment: p * (m - i)
        # i is 0-indexed here, so (m - i) is equivalent to (m - rank + 1)
        correction_factor = m - i
        adjusted_p = raw_p * correction_factor
        
        # Ensure p-value does not exceed 1.0
        adjusted_p = min(adjusted_p, 1.0)
        
        # Enforce monotonicity: corrected p-value must be >= previous corrected p-value
        if adjusted_p < max_corrected:
            adjusted_p = max_corrected
        
        max_corrected = adjusted_p
        
        is_sig = adjusted_p < alpha

        results.append(HolmBonferroniResult(
            metric_name=item['metric_name'],
            raw_p_value=raw_p,
            corrected_p_value=adjusted_p,
            is_significant=is_sig,
            rank=rank
        ))

    return results


def load_p_values_from_json(file_path: str) -> List[Dict[str, Any]]:
    """
    Load p-values from a JSON file generated by the bootstrap or Wilcoxon analysis.
    
    Expected format:
    [
        {"metric_name": "SART_errors", "p_value": 0.032},
        ...
    ]

    Args:
        file_path: Path to the JSON file.

    Returns:
        List of dicts containing metric_name and p_value.
    """
    path = Path(file_path)
    if not path.exists():
        raise FileNotFoundError(f"P-value input file not found: {file_path}")

    with open(path, 'r') as f:
        data = json.load(f)

    if not isinstance(data, list):
        raise ValueError("Expected a list of p-value objects in the JSON file.")

    validated = []
    for item in data:
        if 'metric_name' not in item or 'p_value' not in item:
            logger.warning(f"Skipping invalid item in p-value list: {item}")
            continue
        validated.append({
            'metric_name': item['metric_name'],
            'p_value': float(item['p_value'])
        })

    return validated


def write_holm_results(results: List[HolmBonferroniResult], output_path: str) -> None:
    """
    Write Holm-Bonferroni results to a JSON file.

    Args:
        results: List of HolmBonferroniResult objects.
        output_path: Path to the output JSON file.
    """
    path = Path(output_path)
    path.parent.mkdir(parents=True, exist_ok=True)

    data = [asdict(r) for r in results]
    
    with open(path, 'w') as f:
        json.dump(data, f, indent=2)
    
    logger.info(f"Holm-Bonferroni results written to {output_path}")


def run_holm_bonferroni_pipeline(input_p_values_path: str, output_results_path: str, alpha: float = 0.05) -> List[HolmBonferroniResult]:
    """
    Main pipeline function to load p-values, apply correction, and save results.

    Args:
        input_p_values_path: Path to JSON file containing raw p-values.
        output_results_path: Path where corrected results will be saved.
        alpha: Significance threshold.

    Returns:
        List of corrected results.
    """
    logger.info(f"Starting Holm-Bonferroni correction pipeline.")
    logger.info(f"Input: {input_p_values_path}, Output: {output_results_path}, Alpha: {alpha}")

    p_values = load_p_values_from_json(input_p_values_path)
    
    if not p_values:
        logger.warning("No valid p-values found in input file. Creating empty result.")
        write_holm_results([], output_results_path)
        return []

    results = calculate_holm_bonferroni(p_values, alpha)
    write_holm_results(results, output_results_path)

    significant_count = sum(1 for r in results if r.is_significant)
    logger.info(f"Correction complete. {significant_count}/{len(results)} tests significant at alpha={alpha}.")

    return results


def main():
    """
    Entry point for running the Holm-Bonferroni correction script.
    
    Usage:
    python -m code.analysis.holm_bonferroni --input <path> --output <path> --alpha <float>
    """
    import argparse

    parser = argparse.ArgumentParser(description="Apply Holm-Bonferroni correction to p-values.")
    parser.add_argument("--input", required=True, help="Path to JSON file with raw p-values.")
    parser.add_argument("--output", required=True, help="Path to save corrected results.")
    parser.add_argument("--alpha", type=float, default=0.05, help="Significance threshold.")

    args = parser.parse_args()

    # Ensure logging is configured
    logging.basicConfig(level=logging.INFO, format='%(asctime)s - %(levelname)s - %(message)s')

    run_holm_bonferroni_pipeline(args.input, args.output, args.alpha)


if __name__ == "__main__":
    main()
