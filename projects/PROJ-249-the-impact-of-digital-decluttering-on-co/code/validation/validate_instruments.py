def load_synthetic_data(filepath: Path) -> List[Dict[str, Any]]:
    """Load the synthetic baseline CSV file."""
    if not filepath.exists():
        raise FileNotFoundError(f"Synthetic data file not found: {filepath}")
    
    data = []
    with open(filepath, 'r', newline='') as f:
        reader = csv.DictReader(f)
        for row in reader:
            # Convert numeric fields
            row['value'] = float(row['value'])
            row['metric_type'] = row['metric_type']
            data.append(row)
    return data

def group_data_by_participant(data: List[Dict]) -> Dict[str, List[Dict]]:
    """Group raw data rows by participant_id."""
    groups = {}
    for row in data:
        pid = row['participant_id']
        if pid not in groups:
            groups[pid] = []
        groups[pid].append(row)
    return groups

