    # PANAS: 10 Positive, 10 Negative items. 1-5 scale.
    # Total Positive: 10-50, Negative: 10-50.
    rng = np.random.default_rng(45)
    
    pos_items = [rng.integers(1, 6) for _ in range(10)]
    neg_items = [rng.integers(1, 6) for _ in range(10)]
    
    try:
        result = score_panas_session(pos_items, neg_items)
    except Exception as e:
        return {}, False, f"Scorer failed: {str(e)}"
    
