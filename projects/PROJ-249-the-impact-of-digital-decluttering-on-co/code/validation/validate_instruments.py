def validate_sart_logic(participant_data: List[Dict]) -> Tuple[Dict[str, Any], bool, str]:
    """
    Validate SART scoring logic.
    Since the CSV from T017 is likely a summary, we generate raw trials here to test the scorer.
    """
    # Generate raw trials for SART to test the scorer
    # Simulate 40 trials
    n_trials = 40
    trials = []
    
    # Use a fixed seed for reproducibility within this validation
    rng = np.random.default_rng(42)
    
    for i in range(n_trials):
        # Simulate response time (normal distribution around 400ms)
        rt = rng.normal(400, 50)
        rt = max(100, rt) # Clamp
        
        # Simulate accuracy (90% correct)
        is_correct = rng.random() > 0.1
        
        trials.append({
            'response_time': rt,
            'accuracy': is_correct,
            'stimulus_type': 'target' if i % 10 == 0 else 'non-target'
        })
    
    try:
        result = score_sart_session(trials)
    except Exception as e:
        return {}, False, f"Scorer failed: {str(e)}"
    
    # Validate ranges
    errors = []
    valid = True
    
    if 'commission_errors' in result:
        val = result['commission_errors']
        if not (SART_COMMISSION_RANGE[0] <= val <= SART_COMMISSION_RANGE[1]):
            errors.append(f"Commission errors {val} out of range {SART_COMMISSION_RANGE}")
            valid = False
    else:
        errors.append("Commission errors missing in result")
        valid = False
        
    if 'omission_errors' in result:
        val = result['omission_errors']
        if not (SART_OMISSION_RANGE[0] <= val <= SART_OMISSION_RANGE[1]):
            errors.append(f"Omission errors {val} out of range {SART_OMISSION_RANGE}")
            valid = False
            
    if 'mean_rt' in result:
        val = result['mean_rt']
        if not (SART_RT_RANGE[0] <= val <= SART_RT_RANGE[1]):
            errors.append(f"Mean RT {val} out of range {SART_RT_RANGE}")
            valid = False
    
    return result, valid, "; ".join(errors) if errors else "OK"

def validate_ospan_logic(participant_data: List[Dict]) -> Tuple[Dict[str, Any], bool, str]:
    """Validate Ospan scoring logic by generating raw trial data."""
    # Generate raw Ospan trials
    # Ospan usually involves sets of items. Let's simulate 5 sets.
    sets_data = []
    rng = np.random.default_rng(43)
    
    for set_num in range(5):
        set_size = set_num + 1 # 1 to 5 items
        items = []
        for i in range(set_size):
            stimulus = f"Word_{i}"
            recall = f"Word_{i}" # Correct recall
            # 85% accuracy
            is_correct = rng.random() > 0.15
            items.append({
                'stimulus': stimulus,
                'recall': recall if is_correct else f"Wrong_{i}",
                'accuracy': is_correct
            })
        sets_data.append(items)
    
    try:
        result = score_ospan_session(sets_data)
    except Exception as e:
        return {}, False, f"Scorer failed: {str(e)}"
    
    errors = []
    valid = True
    
    if 'span_score' in result:
        val = result['span_score']
        if not (OSPAN_SPAN_RANGE[0] <= val <= OSPAN_SPAN_RANGE[1]):
            errors.append(f"Span score {val} out of range {OSPAN_SPAN_RANGE}")
            valid = False
    else:
        errors.append("Span score missing")
        valid = False
        
    return result, valid, "; ".join(errors) if errors else "OK"

