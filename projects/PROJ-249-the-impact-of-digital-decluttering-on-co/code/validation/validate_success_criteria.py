"""
Validation script to check results against success criteria (SC-001 to SC-005).

This script loads the statistical summary generated by the analysis pipeline
and validates it against the predefined success criteria:
- SC-001: SART commission errors show significant reduction (p < 0.05, negative change)
- SC-002: Ospan scores show significant improvement (p < 0.05, positive change)
- SC-003: PSS-10 scores show significant reduction (p < 0.05, negative change)
- SC-004: PANAS positive affect shows significant increase (p < 0.05, positive change)
- SC-005: Effect sizes (Cohen's d) >= 0.2 for all significant metrics

The script generates a validation report and exits with appropriate status codes.
"""

import os
import sys
import json
import logging
from pathlib import Path
from typing import Dict, Any, List, Tuple, Optional
from dataclasses import dataclass, asdict

# Configure logging
logging.basicConfig(
    level=logging.INFO,
    format='%(asctime)s - %(levelname)s - %(message)s'
)
logger = logging.getLogger(__name__)

# Define success criteria
@dataclass
class SuccessCriterion:
    id: str
    metric: str
    expected_direction: str  # 'negative' for reduction, 'positive' for increase
    p_threshold: float
    d_threshold: float
    description: str

SUCCESS_CRITERIA = {
    'SC-001': SuccessCriterion(
        id='SC-001',
        metric='SART_commission_errors',
        expected_direction='negative',
        p_threshold=0.05,
        d_threshold=0.2,
        description='SART commission errors show significant reduction'
    ),
    'SC-002': SuccessCriterion(
        id='SC-002',
        metric='Ospan_score',
        expected_direction='positive',
        p_threshold=0.05,
        d_threshold=0.2,
        description='Ospan scores show significant improvement'
    ),
    'SC-003': SuccessCriterion(
        id='SC-003',
        metric='PSS10_total',
        expected_direction='negative',
        p_threshold=0.05,
        d_threshold=0.2,
        description='PSS-10 scores show significant reduction'
    ),
    'SC-004': SuccessCriterion(
        id='SC-004',
        metric='PANAS_positive',
        expected_direction='positive',
        p_threshold=0.05,
        d_threshold=0.2,
        description='PANAS positive affect shows significant increase'
    ),
    'SC-005': SuccessCriterion(
        id='SC-005',
        metric='all_significant',
        expected_direction='any',
        p_threshold=0.05,
        d_threshold=0.2,
        description='Effect sizes (Cohen\'s d) >= 0.2 for all significant metrics'
    )
}

@dataclass
class ValidationResult:
    criterion_id: str
    metric: str
    passed: bool
    reason: str
    observed_p: Optional[float] = None
    observed_d: Optional[float] = None
    observed_change: Optional[float] = None
    expected_direction: Optional[str] = None

@dataclass
class ValidationReport:
    overall_passed: bool
    total_criteria: int
    passed_criteria: int
    failed_criteria: int
    results: List[ValidationResult]
    summary: str

def load_statistical_summary(file_path: str) -> Dict[str, Any]:
    """Load the statistical summary JSON file."""
    path = Path(file_path)
    if not path.exists():
        raise FileNotFoundError(f"Statistical summary file not found: {file_path}")

    with open(path, 'r') as f:
        return json.load(f)

def get_metric_key(metric_name: str) -> str:
    """Convert metric name to the key used in statistical summary."""
    # Map metric names to their keys in the summary
    mapping = {
        'SART_commission_errors': 'SART_commission_errors',
        'Ospan_score': 'Ospan_score',
        'PSS10_total': 'PSS10_total',
        'PANAS_positive': 'PANAS_positive',
        'PANAS_negative': 'PANAS_negative'
    }
    return mapping.get(metric_name, metric_name)

def check_criterion(
    criterion: SuccessCriterion,
    summary_data: Dict[str, Any]
) -> ValidationResult:
    """Check a single success criterion against the statistical summary."""
    metric_key = get_metric_key(criterion.metric)

    # If criterion is for "all significant", we handle it separately
    if criterion.metric == 'all_significant':
        return check_all_significant_criteria(criterion, summary_data)

    # Check if metric exists in summary
    if metric_key not in summary_data.get('metrics', {}):
        return ValidationResult(
            criterion_id=criterion.id,
            metric=criterion.metric,
            passed=False,
            reason=f"Metric '{criterion.metric}' not found in statistical summary",
            observed_p=None,
            observed_d=None,
            observed_change=None,
            expected_direction=criterion.expected_direction
        )

    metric_data = summary_data['metrics'][metric_key]

    # Extract observed values
    observed_p = metric_data.get('p_value')
    observed_d = metric_data.get('effect_size', {}).get('cohens_d')
    observed_change = metric_data.get('mean_change')

    # Check significance (p < threshold)
    is_significant = (
        observed_p is not None and
        observed_p < criterion.p_threshold
    )

    # Check direction of effect
    direction_correct = False
    if observed_change is not None:
        if criterion.expected_direction == 'negative' and observed_change < 0:
            direction_correct = True
        elif criterion.expected_direction == 'positive' and observed_change > 0:
            direction_correct = True

    # Check effect size threshold
    effect_size_ok = (
        observed_d is not None and
        abs(observed_d) >= criterion.d_threshold
    )

    # Determine if criterion passed
    passed = is_significant and direction_correct and effect_size_ok

    # Generate reason
    if not is_significant:
        reason = f"Not significant (p={observed_p:.4f} >= {criterion.p_threshold})"
    elif not direction_correct:
        reason = f"Wrong direction (change={observed_change:.4f}, expected {criterion.expected_direction})"
    elif not effect_size_ok:
        reason = f"Effect size too small (d={observed_d:.4f} < {criterion.d_threshold})"
    else:
        reason = "Passed all checks"

    return ValidationResult(
        criterion_id=criterion.id,
        metric=criterion.metric,
        passed=passed,
        reason=reason,
        observed_p=observed_p,
        observed_d=observed_d,
        observed_change=observed_change,
        expected_direction=criterion.expected_direction
    )

def check_all_significant_criteria(
    criterion: SuccessCriterion,
    summary_data: Dict[str, Any]
) -> ValidationResult:
    """Check that all significant metrics have effect sizes >= threshold."""
    metrics = summary_data.get('metrics', {})
    failed_metrics = []

    for metric_key, metric_data in metrics.items():
        observed_d = metric_data.get('effect_size', {}).get('cohens_d')
        observed_p = metric_data.get('p_value')

        # Only check metrics that are significant
        if observed_p is not None and observed_p < criterion.p_threshold:
            if observed_d is None or abs(observed_d) < criterion.d_threshold:
                failed_metrics.append({
                    'metric': metric_key,
                    'p_value': observed_p,
                    'd_value': observed_d
                })

    passed = len(failed_metrics) == 0

    if passed:
        reason = f"All {len(metrics)} significant metrics have effect sizes >= {criterion.d_threshold}"
    else:
        failed_list = [m['metric'] for m in failed_metrics]
        reason = f"Effect size too small for: {', '.join(failed_list)}"

    return ValidationResult(
        criterion_id=criterion.id,
        metric=criterion.metric,
        passed=passed,
        reason=reason,
        observed_p=None,
        observed_d=None,
        observed_change=None,
        expected_direction=criterion.expected_direction
    )

def validate_success_criteria(
    summary_path: str,
    output_path: Optional[str] = None
) -> ValidationReport:
    """
    Validate all success criteria against the statistical summary.

    Args:
        summary_path: Path to the statistical_summary.json file
        output_path: Optional path to write the validation report

    Returns:
        ValidationReport with all validation results
    """
    logger.info(f"Loading statistical summary from {summary_path}")
    summary_data = load_statistical_summary(summary_path)

    results = []
    passed_count = 0
    failed_count = 0

    for criterion_id, criterion in SUCCESS_CRITERIA.items():
        logger.info(f"Checking {criterion_id}: {criterion.description}")
        result = check_criterion(criterion, summary_data)
        results.append(result)

        if result.passed:
            passed_count += 1
            logger.info(f"  ✓ {criterion_id} PASSED: {result.reason}")
        else:
            failed_count += 1
            logger.info(f"  ✗ {criterion_id} FAILED: {result.reason}")

    overall_passed = (failed_count == 0)

    report = ValidationReport(
        overall_passed=overall_passed,
        total_criteria=len(SUCCESS_CRITERIA),
        passed_criteria=passed_count,
        failed_criteria=failed_count,
        results=results,
        summary=f"Validation {'PASSED' if overall_passed else 'FAILED'}: {passed_count}/{len(SUCCESS_CRITERIA)} criteria met"
    )

    # Write report if output path specified
    if output_path:
        write_validation_report(report, output_path)
        logger.info(f"Validation report written to {output_path}")

    return report

def write_validation_report(report: ValidationReport, output_path: str) -> None:
    """Write the validation report to a JSON file."""
    # Convert dataclasses to dicts for JSON serialization
    report_dict = {
        'overall_passed': report.overall_passed,
        'total_criteria': report.total_criteria,
        'passed_criteria': report.passed_criteria,
        'failed_criteria': report.failed_criteria,
        'summary': report.summary,
        'results': [asdict(r) for r in report.results]
    }

    with open(output_path, 'w') as f:
        json.dump(report_dict, f, indent=2)

def main():
    """Main entry point for the validation script."""
    # Default paths
    project_root = Path(__file__).parent.parent.parent
    summary_path = project_root / 'results' / 'statistical_summary.json'
    output_path = project_root / 'results' / 'validation_report.json'

    # Parse command line arguments if provided
    if len(sys.argv) > 1:
        summary_path = Path(sys.argv[1])
    if len(sys.argv) > 2:
        output_path = Path(sys.argv[2])

    try:
        logger.info("Starting success criteria validation...")
        report = validate_success_criteria(str(summary_path), str(output_path))

        # Print summary
        print("\n" + "="*60)
        print("VALIDATION SUMMARY")
        print("="*60)
        print(f"Overall Status: {'PASSED' if report.overall_passed else 'FAILED'}")
        print(f"Criteria Met: {report.passed_criteria}/{report.total_criteria}")
        print(f"Summary: {report.summary}")
        print("\nDetailed Results:")
        print("-"*60)

        for result in report.results:
            status = "✓ PASSED" if result.passed else "✗ FAILED"
            print(f"{result.criterion_id}: {status}")
            print(f"  Metric: {result.metric}")
            print(f"  Expected: {result.expected_direction} change")
            if result.observed_change is not None:
                print(f"  Observed change: {result.observed_change:.4f}")
            if result.observed_p is not None:
                print(f"  p-value: {result.observed_p:.4f}")
            if result.observed_d is not None:
                print(f"  Cohen's d: {result.observed_d:.4f}")
            print(f"  Reason: {result.reason}")
            print()

        print("="*60)

        # Exit with appropriate code
        sys.exit(0 if report.overall_passed else 1)

    except FileNotFoundError as e:
        logger.error(f"File not found: {e}")
        sys.exit(2)
    except json.JSONDecodeError as e:
        logger.error(f"Invalid JSON in statistical summary: {e}")
        sys.exit(3)
    except Exception as e:
        logger.error(f"Validation failed with error: {e}")
        sys.exit(4)

if __name__ == '__main__':
    main()