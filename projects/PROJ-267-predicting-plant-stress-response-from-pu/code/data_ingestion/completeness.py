"""
Data Completeness Calculation Module (Task T018).

Calculates the Data Completeness % metric based on the output of the
data ingestion pipeline (T014) and writes the result to:
results/data_completeness.json

Formula:
  Data Completeness % = (Retained Datasets / Initial Query Results) * 100

Inputs:
  - results/pipeline_summary.json (Output of T014: run_pipeline)
    Expected keys: 'initial_query_count', 'retained_dataset_count'

Output:
  - results/data_completeness.json
    Structure:
      {
        "initial_query_count": <int>,
        "retained_dataset_count": <int>,
        "completeness_percentage": <float>,
        "timestamp": <ISO8601 string>
      }
"""

import os
import sys
import json
import logging
from pathlib import Path
from datetime import datetime
from typing import Dict, Any, Optional

# Import project utilities
# Note: Using relative imports for package structure, or absolute if installed in site-packages
try:
    from utils.logging_config import get_logger
    from utils.config import RESULTS_PATH, PIPELINE_SUMMARY_PATH
except ImportError:
    # Fallback for direct script execution (code/data_ingestion/)
    sys.path.insert(0, str(Path(__file__).resolve().parent.parent))
    from utils.logging_config import get_logger
    from utils.config import RESULTS_PATH, PIPELINE_SUMMARY_PATH

logger = get_logger(__name__)

def load_pipeline_summary(path: Optional[Path] = None) -> Dict[str, Any]:
    """
    Loads the pipeline summary JSON generated by T014 (pipeline.py).

    Args:
        path: Optional path to the summary file. Defaults to config.PIPELINE_SUMMARY_PATH.

    Returns:
        Dictionary containing pipeline execution stats.

    Raises:
        FileNotFoundError: If the summary file does not exist.
        json.JSONDecodeError: If the file is not valid JSON.
        KeyError: If expected keys are missing.
    """
    if path is None:
        path = PIPELINE_SUMMARY_PATH

    if not path.exists():
        raise FileNotFoundError(
            f"Pipeline summary not found at {path}. "
            "Ensure T014 (pipeline.py) has been run successfully."
        )

    logger.info(f"Loading pipeline summary from {path}")
    with open(path, 'r', encoding='utf-8') as f:
        data = json.load(f)

    # Validate required keys
    required_keys = ['initial_query_count', 'retained_dataset_count']
    missing_keys = [k for k in required_keys if k not in data]
    if missing_keys:
        raise KeyError(
            f"Pipeline summary missing required keys: {missing_keys}. "
            "Ensure T014 writes these fields."
        )

    return data

def calculate_completeness(
    initial_count: int,
    retained_count: int
) -> float:
    """
    Calculates the Data Completeness percentage.

    Args:
        initial_count: Number of datasets initially queried/found.
        retained_count: Number of datasets successfully retained after filtering.

    Returns:
        Completeness percentage (0.0 to 100.0).
    """
    if initial_count == 0:
        logger.warning("Initial query count is 0. Completeness is undefined; returning 0.0.")
        return 0.0

    percentage = (retained_count / initial_count) * 100.0
    return round(percentage, 4)

def write_completeness_report(
    metrics: Dict[str, Any],
    output_path: Optional[Path] = None
) -> Path:
    """
    Writes the completeness metrics to a JSON file.

    Args:
        metrics: Dictionary containing the calculated metrics.
        output_path: Optional output path. Defaults to config.RESULTS_PATH/data_completeness.json.

    Returns:
        Path to the written file.
    """
    if output_path is None:
        output_path = RESULTS_PATH / "data_completeness.json"

    output_path.parent.mkdir(parents=True, exist_ok=True)

    logger.info(f"Writing completeness report to {output_path}")
    with open(output_path, 'w', encoding='utf-8') as f:
        json.dump(metrics, f, indent=2)

    return output_path

def run_completeness_check() -> Dict[str, Any]:
    """
    Orchestrates the completeness calculation:
    1. Load pipeline summary from T014.
    2. Calculate completeness %.
    3. Write report to results/data_completeness.json.

    Returns:
        The generated metrics dictionary.
    """
    logger.info("Starting Data Completeness Calculation (T018)")

    # 1. Load Input
    try:
        pipeline_data = load_pipeline_summary()
    except (FileNotFoundError, json.JSONDecodeError, KeyError) as e:
        logger.error(f"Failed to load pipeline summary: {e}")
        raise

    initial_count = pipeline_data['initial_query_count']
    retained_count = pipeline_data['retained_dataset_count']

    logger.info(f"Initial Queries: {initial_count}, Retained: {retained_count}")

    # 2. Calculate
    completeness_pct = calculate_completeness(initial_count, retained_count)

    # 3. Prepare Output
    report = {
        "initial_query_count": initial_count,
        "retained_dataset_count": retained_count,
        "completeness_percentage": completeness_pct,
        "timestamp": datetime.utcnow().isoformat() + "Z"
    }

    # 4. Write Output
    output_file = write_completeness_report(report)

    logger.info(f"Data Completeness: {completeness_pct:.2f}%")
    logger.info(f"Report saved to: {output_file}")

    return report

def main():
    """
    Entry point for running the completeness check as a script.
    """
    try:
        run_completeness_check()
        logger.info("T018 completed successfully.")
    except Exception as e:
        logger.error(f"T018 failed: {e}")
        sys.exit(1)

if __name__ == "__main__":
    main()
