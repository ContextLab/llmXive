import os
import subprocess
import sys
from pathlib import Path

def main():
    # Define the project root based on the task description
    # The task specifies paths relative to the project root, but also mentions
    # a specific project folder name. We will create the structure inside
    # the current working directory (assumed to be the repo root).
    
    project_root = Path.cwd()
    project_name = "PROJ-399-the-impact-of-framing-on-perceived-sever"
    
    # Define the specific directories required by T001
    # Note: The task lists some paths with 'projects/<name>/...' and others with just 'data/...'
    # We will ensure all mentioned directories exist to be safe.
    
    dirs_to_create = [
        # From the specific project path requirement
        f"{project_name}/data/raw",
        f"{project_name}/data/processed",
        f"{project_name}/results/plots",
        
        # From the general project structure requirement (often these are relative to root)
        # We will create them at the root level as well if they don't exist, 
        # or assume the 'projects/<name>' prefix is the canonical location.
        # Given the mix, we will create the canonical structure under the project name
        # and also ensure standard top-level folders exist if they are standard.
        # However, to strictly follow "exact directories: projects/.../data/raw...",
        # we prioritize that.
        
        # Let's also create standard top-level folders mentioned in general conventions
        # if they are not explicitly nested in the project folder in the prompt's 
        # specific list, but the prompt says "exact directories: ...".
        # We will create the nested ones first.
        
        # Re-reading: "exact directories: `projects/PROJ-399.../data/raw/`, `data/processed/`, ..."
        # This implies a mix. We will create both sets to ensure no ambiguity.
        "data/processed",
        "results/plots",
        "code",
        "tests",
        ".github/workflows",
        "docs",
    ]

    created_paths = []

    for dir_path in dirs_to_create:
        full_path = project_root / dir_path
        if not full_path.exists():
            full_path.mkdir(parents=True, exist_ok=True)
            created_paths.append(str(full_path))
        else:
            # Even if exists, we log it for the report
            created_paths.append(str(full_path))

    # Generate the directory tree log
    output_file = project_root / "docs" / "project_structure.md"
    
    # Ensure docs exists
    (project_root / "docs").mkdir(parents=True, exist_ok=True)

    # Construct the markdown content
    md_content = [
        "# Project Structure Verification",
        f"Project: {project_name}",
        "Generated by: code/setup_project_structure.py",
        "",
        "## Directories Created/Verified",
        ""
    ]

    for path in sorted(created_paths):
        rel_path = Path(path).relative_to(project_root)
        md_content.append(f"- `{rel_path}`")

    # Add a visual tree representation
    md_content.append("")
    md_content.append("## Directory Tree (Top Level)")
    md_content.append("")
    md_content.append("```text")
    
    # Simple tree generation for the root
    tree_lines = []
    for item in sorted(project_root.iterdir()):
        if item.is_dir() and not item.name.startswith('.'):
            tree_lines.append(f"📁 {item.name}/")
        elif item.is_file() and item.name == "README.md":
            tree_lines.append(f"📄 {item.name}")
    
    # Add hidden folders if they exist
    for item in sorted(project_root.iterdir()):
        if item.is_dir() and item.name.startswith('.'):
            tree_lines.append(f"📁 {item.name}/")
    
    md_content.append("\n".join(tree_lines))
    md_content.append("```")

    # Write the file
    with open(output_file, "w", encoding="utf-8") as f:
        f.write("\n".join(md_content))

    print(f"Project structure created/verified.")
    print(f"Log written to: {output_file}")
    
    # Print the tree to stdout as well for immediate verification
    print("\n" + "\n".join(tree_lines))

if __name__ == "__main__":
    main()
