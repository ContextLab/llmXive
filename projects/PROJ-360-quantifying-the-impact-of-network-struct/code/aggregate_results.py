"""
T024: Aggregate CV results (mean ± std dev) and save to results/model_performance.json.

This script loads the cross-validation results generated by T023 (run_kfold_cross_validation),
calculates the mean and standard deviation for R² and RMSE, and saves the aggregated
statistics to results/model_performance.json.

It relies on the existing infrastructure in code/analyze.py to load metrics and
perform the validation logic, but focuses specifically on the aggregation and
serialization step required for this task.
"""
import os
import json
import logging
import numpy as np
from pathlib import Path
from typing import Dict, Any, List

# Import existing utilities from the project
from analyze import (
    setup_analysis_logger,
    load_metrics_csv,
    get_network_metrics,
    get_thermal_conductivity_col,
    run_kfold_cross_validation
)
from config import get_config

# Ensure consistent paths
PROJECT_ROOT = Path(__file__).parent.parent
RESULTS_DIR = PROJECT_ROOT / "results"
FILTERED_FEATURES_PATH = PROJECT_ROOT / "data" / "processed" / "filtered_features.csv"
MODEL_PERFORMANCE_PATH = RESULTS_DIR / "model_performance.json"

def aggregate_cv_results(cv_results: List[Dict[str, float]]) -> Dict[str, Any]:
    """
    Calculate mean and standard deviation for R² and RMSE from CV fold results.

    Args:
        cv_results: List of dictionaries, each containing 'r2' and 'rmse' for a fold.

    Returns:
        Dictionary with aggregated statistics.
    """
    if not cv_results:
        raise ValueError("No cross-validation results provided for aggregation.")

    r2_scores = [res['r2'] for res in cv_results]
    rmse_scores = [res['rmse'] for res in cv_results]

    mean_r2 = np.mean(r2_scores)
    std_r2 = np.std(r2_scores, ddof=1) if len(r2_scores) > 1 else 0.0

    mean_rmse = np.mean(rmse_scores)
    std_rmse = np.std(rmse_scores, ddof=1) if len(rmse_scores) > 1 else 0.0

    return {
        "r2": {
            "mean": float(mean_r2),
            "std": float(std_r2)
        },
        "rmse": {
            "mean": float(mean_rmse),
            "std": float(std_rmse)
        },
        "n_folds": len(cv_results),
        "r2_interpretation": None  # Will be populated by T023/T025 logic if needed
    }

def main():
    """
    Main entry point for T024.
    1. Load filtered features and thermal conductivity.
    2. Run cross-validation (or load existing if T023 already ran, but here we re-run to ensure fresh data flow).
    3. Aggregate results.
    4. Save to results/model_performance.json.
    """
    logger = setup_analysis_logger("T024_Aggregate")
    logger.info("Starting T024: Aggregating Cross-Validation Results.")

    # Ensure results directory exists
    RESULTS_DIR.mkdir(parents=True, exist_ok=True)

    # Check if filtered features exist (produced by T021)
    if not FILTERED_FEATURES_PATH.exists():
        logger.error(f"Required input file not found: {FILTERED_FEATURES_PATH}")
        logger.error("Please ensure T021 (VIF Filtering) has been completed successfully.")
        return

    # Load data
    try:
        df = load_metrics_csv(FILTERED_FEATURES_PATH)
        logger.info(f"Loaded metrics from {FILTERED_FEATURES_PATH}. Shape: {df.shape}")
    except Exception as e:
        logger.error(f"Failed to load metrics: {e}")
        return

    # Identify features and target
    # T021 ensures filtered_features.csv only contains VIF < 5 features + target
    # We assume the last column or a specific column name is the target.
    # Based on T015/T015b, the target is 'thermal_conductivity_scalar'.
    target_col = get_thermal_conductivity_col(df)
    if target_col is None:
        # Fallback: assume the last column is the target if specific name not found
        # This is a safety net, but the schema should be strict.
        logger.warning("Target column 'thermal_conductivity_scalar' not found explicitly. Using last column.")
        target_col = df.columns[-1]

    feature_cols = [c for c in df.columns if c != target_col and not c.startswith('#')]
    
    if len(feature_cols) == 0:
        logger.error("No features found to train on after filtering.")
        return

    logger.info(f"Using features: {feature_cols}")
    logger.info(f"Using target: {target_col}")

    # Run Cross-Validation
    # T023 logic is encapsulated in run_kfold_cross_validation
    # We call it here to generate the fold results.
    try:
        cv_results = run_kfold_cross_validation(
            df=df,
            feature_cols=feature_cols,
            target_col=target_col,
            n_splits=5,
            random_state=get_config().random_seed
        )
        logger.info(f"Cross-validation completed. {len(cv_results)} folds generated.")
    except Exception as e:
        logger.error(f"Cross-validation failed: {e}")
        return

    # Aggregate Results
    aggregated = aggregate_cv_results(cv_results)

    # Determine R2 Interpretation based on T023 requirement (Mean R2 < 0.30)
    if aggregated['r2']['mean'] < 0.30:
        interpretation = "Weak predictive power (R² < 0.30), consistent with null hypothesis."
        aggregated['r2_interpretation'] = interpretation
        logger.warning(f"Low R² detected: {interpretation}")
    else:
        aggregated['r2_interpretation'] = "Predictive power is statistically meaningful (R² >= 0.30)."

    # Save to JSON
    try:
        with open(MODEL_PERFORMANCE_PATH, 'w') as f:
            json.dump(aggregated, f, indent=2)
        logger.info(f"Successfully saved aggregated results to {MODEL_PERFORMANCE_PATH}")
    except Exception as e:
        logger.error(f"Failed to save results to {MODEL_PERFORMANCE_PATH}: {e}")
        return

    logger.info("T024 completed successfully.")

if __name__ == "__main__":
    main()