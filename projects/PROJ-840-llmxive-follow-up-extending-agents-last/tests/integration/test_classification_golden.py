def test_classification_accuracy_on_golden_set(golden_data, setup_seed):
    """
    End-to-end integration test:
    1. Parse the raw trace data (simulated via step_state).
    2. Normalize the state.
    3. Classify the failure mode using the local LLM classifier.
    4. Compare against ground_truth_label.
    5. Report accuracy.
    
    Requires that T015 generated data with known ground truth labels 
    ('State Persistence Error' or 'Reasoning Deficit').
    """
    if not OUTPUT_DIR.exists():
        OUTPUT_DIR.mkdir(parents=True, exist_ok=True)

    results = []
    correct = 0
    total = len(golden_data)

    for record in golden_data:
        trace_id = record["trace_id"]
        ground_truth = record["ground_truth_label"]
        step_state = record["step_state"]
        task_description = record.get("task_description", "")

        try:
            # 1. Parse (using the step_state as the 'raw' input for this synthetic trace)
            # The parser expects a raw log string or dict. We simulate the parsed output
            # by constructing a minimal dict that the classifier expects.
            # In a real scenario, parse_ale_trace would ingest a log file.
            # Here we assume the 'step_state' is the environment state to be classified.
            parsed_data = parse_ale_trace(step_state) 
            
            # 2. Normalize
            normalized_state = normalize_state(parsed_data.get("state", step_state))
            
            # 3. Classify
            # The classifier expects a dict with 'state' and optionally 'task_description'
            classification_input = {
                "state": normalized_state,
                "task_description": task_description,
                "trace_id": trace_id
            }
            
            # Note: classify_failure_mode might require a model path. 
            # If T014 is not fully implemented with a real model, this might fail or return a default.
            # We assume T014 provides a working classifier that returns a label string.
            predicted_label = classify_failure_mode(classification_input)
            
            # 4. Compare
            is_correct = (predicted_label == ground_truth)
            if is_correct:
                correct += 1
            
            results.append({
                "trace_id": trace_id,
                "ground_truth": ground_truth,
                "predicted": predicted_label,
                "correct": is_correct
            })

        except Exception as e:
            # If the classifier fails (e.g., no model), record as incorrect or skip
            # For this test, we mark as incorrect to ensure the pipeline is robust
            results.append({
                "trace_id": trace_id,
                "ground_truth": ground_truth,
                "predicted": "ERROR",
                "correct": False,
                "error": str(e)
            })

    accuracy = correct / total if total > 0 else 0.0

    # Save results to disk as required by the pipeline
    with open(RESULTS_PATH, 'w') as f:
        json.dump({
            "total": total,
            "correct": correct,
            "accuracy": accuracy,
            "details": results
        }, f, indent=2)

    # Assert a minimum accuracy threshold (e.g., 80%)
    # This threshold might need adjustment based on the synthetic data quality
    # and the classifier's capability. For a "golden set" with known ground truth,
    # we expect high accuracy if the pipeline is working.
    # If the classifier is not yet trained/configured, this will fail, 
    # driving the implementation of T014.
    assert accuracy >= 0.80, f"Classification accuracy {accuracy:.2%} is below the 80% threshold. Check T014 implementation."