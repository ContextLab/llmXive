"""
ALE Log Parser Module.

Extracts environment state and agent actions from raw trace data.
"""
from typing import Dict, Any, Optional
import json
import re
from pathlib import Path

def parse_ale_trace(trace_data: Dict[str, Any]) -> Dict[str, Any]:
    """
    Parses a raw ALE trace dictionary into a structured format.
    
    Args:
        trace_data: A dictionary containing 'trace_id', 'step_state', 'task_description', etc.
    
    Returns:
        A dictionary with extracted 'state', 'actions', and 'metadata'.
    """
    # Extract state
    # The golden set provides 'step_state' which is assumed to be the raw state string or dict
    raw_state = trace_data.get("step_state", "")
    
    parsed_state = {}
    
    if isinstance(raw_state, str):
        # Attempt to parse as JSON if it's a string representation
        try:
            parsed_state = json.loads(raw_state)
        except json.JSONDecodeError:
            # Fallback: treat as raw string state
            parsed_state = {"raw_state": raw_state}
    elif isinstance(raw_state, dict):
        parsed_state = raw_state
    else:
        parsed_state = {"raw_state": str(raw_state)}
    
    # Extract actions if present, otherwise initialize empty
    # If the trace_data contains a list of actions, parse them
    raw_actions = trace_data.get("actions", [])
    parsed_actions = []
    
    if isinstance(raw_actions, str):
        try:
            parsed_actions = json.loads(raw_actions)
        except json.JSONDecodeError:
            parsed_actions = []
    elif isinstance(raw_actions, list):
        parsed_actions = raw_actions
    
    return {
        "trace_id": trace_data.get("trace_id", "unknown"),
        "state": parsed_state,
        "actions": parsed_actions,
        "metadata": {
            "task_description": trace_data.get("task_description", ""),
            "ground_truth_label": trace_data.get("ground_truth_label", "unknown"),
            "source": "golden_subset"
        }
    }

def main():
    """
    Main entry point to parse the golden subset dataset.
    Reads data from data/raw/golden_subset.json (generated by T015),
    parses each trace, and writes the structured output to data/processed/parsed_traces.json.
    """
    input_path = Path("data/raw/golden_subset.json")
    output_path = Path("data/processed/parsed_traces.json")

    if not input_path.exists():
        raise FileNotFoundError(
            f"Input file not found: {input_path}. "
            "Please ensure T015 has been executed to generate the golden subset."
        )

    # Ensure output directory exists
    output_path.parent.mkdir(parents=True, exist_ok=True)

    # Load input data
    with open(input_path, "r", encoding="utf-8") as f:
        traces = json.load(f)

    parsed_traces = []
    for trace in traces:
        parsed = parse_ale_trace(trace)
        parsed_traces.append(parsed)

    # Write output
    with open(output_path, "w", encoding="utf-8") as f:
        json.dump(parsed_traces, f, indent=2)

    print(f"Successfully parsed {len(parsed_traces)} traces.")
    print(f"Output written to: {output_path}")

if __name__ == "__main__":
    main()