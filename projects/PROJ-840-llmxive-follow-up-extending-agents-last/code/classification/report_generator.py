"""
Report Generator for Automated Failure Mode Classification (US1).

This module generates a JSON report aggregating classification results from the
semantic classifier. It calculates counts for 'State Persistence Error' and
'Reasoning Deficit', computes the total failures, and determines the proportion
of State Persistence Errors as per SC-001.

Input: data/processed/classification_results.json (generated by T014)
Output: data/processed/failure_analysis_report.json
"""
import json
import os
import sys
from pathlib import Path
from typing import Dict, Any, List, Optional

# Add project root to path for imports
project_root = Path(__file__).resolve().parent.parent.parent
if str(project_root) not in sys.path:
    sys.path.insert(0, str(project_root))

from utils.logging_config import get_logger

logger = get_logger(__name__)

# Constants
FAILURE_MODE_STATE_PERSISTENCE = "State Persistence Error"
FAILURE_MODE_REASONING_DEFICIT = "Reasoning Deficit"
INPUT_FILE = project_root / "data" / "processed" / "classification_results.json"
OUTPUT_FILE = project_root / "data" / "processed" / "failure_analysis_report.json"


def load_classification_results() -> List[Dict[str, Any]]:
    """
    Loads the classification results from the JSON file generated by T014.

    Returns:
        List of classification records.

    Raises:
        FileNotFoundError: If the input file does not exist.
        json.JSONDecodeError: If the file content is not valid JSON.
    """
    if not INPUT_FILE.exists():
        raise FileNotFoundError(
            f"Input file not found: {INPUT_FILE}. "
            "Ensure T014 (semantic_classifier) has run successfully."
        )

    with open(INPUT_FILE, 'r', encoding='utf-8') as f:
        data = json.load(f)

    if not isinstance(data, list):
        raise ValueError(f"Expected a list of records in {INPUT_FILE}, got {type(data)}")

    return data


def calculate_report_metrics(records: List[Dict[str, Any]]) -> Dict[str, Any]:
    """
    Aggregates metrics from the classification records.

    Calculates:
    - state_persistence_count: Number of failures labeled as 'State Persistence Error'
    - reasoning_deficit_count: Number of failures labeled as 'Reasoning Deficit'
    - total_failures: Sum of the above two counts
    - state_persistence_proportion: ratio of state_persistence_count to total_failures
    - classification_confidence: Average confidence score across all classified failures

    Args:
        records: List of classification records containing 'label' and 'confidence'.

    Returns:
        Dictionary containing the calculated metrics.
    """
    state_persistence_count = 0
    reasoning_deficit_count = 0
    total_confidence_sum = 0.0
    valid_classification_count = 0

    for record in records:
        label = record.get("label", "")
        confidence = record.get("confidence", 0.0)

        # Only count valid failure classifications
        if label == FAILURE_MODE_STATE_PERSISTENCE:
            state_persistence_count += 1
            total_confidence_sum += confidence
            valid_classification_count += 1
        elif label == FAILURE_MODE_REASONING_DEFICIT:
            reasoning_deficit_count += 1
            total_confidence_sum += confidence
            valid_classification_count += 1
        else:
            # Log if there are other labels, but don't include in failure counts
            logger.debug(f"Record {record.get('trace_id', 'unknown')} has unknown label: {label}")

    total_failures = state_persistence_count + reasoning_deficit_count

    # Calculate proportion (SC-001)
    if total_failures > 0:
        state_persistence_proportion = state_persistence_count / total_failures
    else:
        state_persistence_proportion = 0.0

    # Calculate average confidence
    if valid_classification_count > 0:
        classification_confidence = total_confidence_sum / valid_classification_count
    else:
        classification_confidence = 0.0

    return {
        "state_persistence_count": state_persistence_count,
        "reasoning_deficit_count": reasoning_deficit_count,
        "total_failures": total_failures,
        "state_persistence_proportion": round(state_persistence_proportion, 4),
        "classification_confidence": round(classification_confidence, 4),
        "total_records_processed": len(records)
    }


def generate_report(metrics: Dict[str, Any]) -> None:
    """
    Writes the calculated metrics to the output JSON file.

    Args:
        metrics: Dictionary containing the report metrics.
    """
    # Ensure output directory exists
    OUTPUT_FILE.parent.mkdir(parents=True, exist_ok=True)

    report_data = {
        "report_type": "failure_mode_analysis",
        "source_file": str(INPUT_FILE),
        "generated_metrics": metrics
    }

    with open(OUTPUT_FILE, 'w', encoding='utf-8') as f:
        json.dump(report_data, f, indent=2)

    logger.info(f"Report generated successfully: {OUTPUT_FILE}")


def main() -> int:
    """
    Main entry point for the report generation script.

    Returns:
        0 on success, 1 on failure.
    """
    try:
        logger.info(f"Loading classification results from {INPUT_FILE}...")
        records = load_classification_results()
        logger.info(f"Loaded {len(records)} records.")

        logger.info("Calculating metrics...")
        metrics = calculate_report_metrics(records)

        logger.info("Generating report...")
        generate_report(metrics)

        logger.info("Task T016 completed successfully.")
        return 0

    except FileNotFoundError as e:
        logger.error(f"Data file missing: {e}")
        return 1
    except json.JSONDecodeError as e:
        logger.error(f"Invalid JSON in input file: {e}")
        return 1
    except Exception as e:
        logger.error(f"Unexpected error during report generation: {e}", exc_info=True)
        return 1


if __name__ == "__main__":
    sys.exit(main())