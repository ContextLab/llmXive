import json
import os
import sys
from pathlib import Path
from typing import Dict, Any, List, Optional
from utils.logging_config import get_logger

logger = get_logger(__name__)

def load_classification_results(input_path: str) -> List[Dict[str, Any]]:
    """
    Load classification results from a JSON file.
    Expects a list of records, each containing 'trace_id', 'failure_mode', and optionally 'confidence'.
    """
    path = Path(input_path)
    if not path.exists():
        raise FileNotFoundError(f"Classification results file not found: {input_path}")
    
    with open(path, 'r', encoding='utf-8') as f:
        data = json.load(f)
    
    if not isinstance(data, list):
        raise ValueError(f"Expected JSON list in {input_path}, got {type(data)}")
    
    return data

def calculate_report_metrics(results: List[Dict[str, Any]]) -> Dict[str, Any]:
    """
    Calculate aggregate metrics from classification results.
    
    Expected fields in each result:
      - 'failure_mode': str (e.g., 'State Persistence Error', 'Reasoning Deficit', or other)
      - 'confidence': float (0.0 to 1.0), optional; if missing, defaults to 0.0
    
    Returns a dict with:
      - state_persistence_count: int
      - reasoning_deficit_count: int
      - total_failures: int
      - classification_confidence: float (average confidence)
      - state_persistence_proportion: float (0.0 to 1.0)
    """
    state_persistence_count = 0
    reasoning_deficit_count = 0
    total_failures = len(results)
    total_confidence = 0.0
    
    for record in results:
        mode = record.get('failure_mode', '').strip()
        confidence = record.get('confidence', 0.0)
        
        if isinstance(confidence, (int, float)):
            total_confidence += float(confidence)
        else:
            total_confidence += 0.0
        
        if mode == "State Persistence Error":
            state_persistence_count += 1
        elif mode == "Reasoning Deficit":
            reasoning_deficit_count += 1
        # Other failure modes are counted in total_failures but not in specific categories
    
    classification_confidence = (
        total_confidence / total_failures if total_failures > 0 else 0.0
    )
    
    state_persistence_proportion = (
        state_persistence_count / total_failures if total_failures > 0 else 0.0
    )
    
    return {
        "state_persistence_count": state_persistence_count,
        "reasoning_deficit_count": reasoning_deficit_count,
        "total_failures": total_failures,
        "classification_confidence": round(classification_confidence, 6),
        "state_persistence_proportion": round(state_persistence_proportion, 6)
    }

def generate_report(metrics: Dict[str, Any], output_path: str) -> None:
    """
    Write the final report metrics to a JSON file.
    """
    path = Path(output_path)
    path.parent.mkdir(parents=True, exist_ok=True)
    
    with open(path, 'w', encoding='utf-8') as f:
        json.dump(metrics, f, indent=2)
    
    logger.info(f"Report generated successfully: {output_path}")

def main():
    """
    Entry point for generating the classification report.
    Expects:
      - Input: data/processed/classification_results.json (generated by T014)
      - Output: data/processed/classification_report.json
    """
    # Default paths relative to project root
    input_file = "data/processed/classification_results.json"
    output_file = "data/processed/classification_report.json"
    
    # Allow override via command line
    if len(sys.argv) >= 2:
        input_file = sys.argv[1]
    if len(sys.argv) >= 3:
        output_file = sys.argv[2]
    
    logger.info(f"Loading classification results from {input_file}")
    try:
        results = load_classification_results(input_file)
    except FileNotFoundError as e:
        logger.error(str(e))
        sys.exit(1)
    except json.JSONDecodeError as e:
        logger.error(f"Invalid JSON in {input_file}: {e}")
        sys.exit(1)
    
    if not results:
        logger.warning("No classification results found. Generating empty report.")
    
    logger.info("Calculating report metrics...")
    metrics = calculate_report_metrics(results)
    
    logger.info(f"Metrics: {metrics}")
    logger.info(f"Writing report to {output_file}")
    generate_report(metrics, output_file)
    
    print(f"Report generated: {output_file}")
    print(f"  State Persistence Errors: {metrics['state_persistence_count']}")
    print(f"  Reasoning Deficits: {metrics['reasoning_deficit_count']}")
    print(f"  Total Failures: {metrics['total_failures']}")
    print(f"  Avg Confidence: {metrics['classification_confidence']}")
    print(f"  State Persistence Proportion: {metrics['state_persistence_proportion']}")

if __name__ == "__main__":
    main()