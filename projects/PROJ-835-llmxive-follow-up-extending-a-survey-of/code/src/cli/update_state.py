"""
Task T030: Implement state update logic to compute SHA-256 hashes for all artifacts
and update the project state YAML file.
"""
import os
import sys
import json
import hashlib
import argparse
from pathlib import Path
import yaml

# Ensure project root is in path
PROJECT_ROOT = Path(__file__).resolve().parent.parent.parent
if str(PROJECT_ROOT) not in sys.path:
    sys.path.insert(0, str(PROJECT_ROOT))

from src.utils.config import get_path, load_state, save_state, compute_file_hash, update_artifact_hash

# Define the artifact paths relative to the project root that need hashing
# These are the outputs generated by the completed pipeline tasks
ARTIFACT_REL_PATHS = [
    # From T002/T003 (Setup) - assuming these are at root or code root
    "requirements.txt",
    "pyproject.toml", # For ruff/black config
    
    # From T014 (Embeddings)
    "data/embeddings.parquet",
    
    # From T022b (Anomaly Scores)
    "data/anomaly_scores.parquet",
    
    # From T024 (Predictions)
    "results/predictions.csv",
    
    # From T027b (Correlation)
    "results/correlation.json",
    
    # From T029 (Report & Resource Log)
    "results/report.md",
    "results/resource_log.json",
    
    # State file itself (excluding its own hash entry to avoid circularity, 
    # but we hash the content of other files that went into it)
    # Note: We do NOT hash the state file itself in its own content, 
    # we update it with hashes of *other* files.
]

# Specific state file path
STATE_FILE_REL_PATH = "state/projects/PROJ-835-llmxive-follow-up-extending-a-survey-of.yaml"

def get_artifact_files():
    """Yields absolute paths of artifact files that exist."""
    for rel_path in ARTIFACT_REL_PATHS:
        abs_path = PROJECT_ROOT / rel_path
        if abs_path.exists():
            yield abs_path
        else:
            # Log warning but don't fail if a non-critical intermediate file is missing
            # though for a full run, these should exist.
            print(f"Warning: Artifact not found, skipping: {abs_path}", file=sys.stderr)

def compute_all_hashes():
    """Computes SHA-256 hashes for all existing artifacts."""
    hashes = {}
    for abs_path in get_artifact_files():
        # Use the relative path as the key
        rel_key = str(abs_path.relative_to(PROJECT_ROOT))
        try:
            file_hash = compute_file_hash(str(abs_path))
            hashes[rel_key] = file_hash
        except Exception as e:
            print(f"Error hashing {abs_path}: {e}", file=sys.stderr)
    return hashes

def update_state_file(hashes):
    """Updates the state YAML file with the new artifact hashes."""
    state_path = PROJECT_ROOT / STATE_FILE_REL_PATH
    
    if not state_path.exists():
        print(f"Error: State file not found at {state_path}", file=sys.stderr)
        return False

    # Load existing state
    try:
        with open(state_path, 'r', encoding='utf-8') as f:
            state_data = yaml.safe_load(f)
    except yaml.YAMLError as e:
        print(f"Error parsing state file YAML: {e}", file=sys.stderr)
        return False
    except Exception as e:
        print(f"Error reading state file: {e}", file=sys.stderr)
        return False

    if state_data is None:
        state_data = {}

    # Ensure 'artifact_hashes' key exists
    if 'artifact_hashes' not in state_data:
        state_data['artifact_hashes'] = {}

    # Update hashes
    # We overwrite the entire map or merge? The task implies updating the map.
    # To be safe and idempotent, we update the specific keys we found.
    # But typically, we want to snapshot the current state of *all* tracked artifacts.
    # Let's replace the map with the newly computed one for the known artifacts.
    # Or better: update the existing map with new values, keeping old ones if they aren't in the new list?
    # The requirement says "compute SHA-256 hashes for all artifacts". 
    # This implies a full snapshot of the current artifacts.
    
    # We will update the map with the newly computed hashes.
    # If an artifact was previously tracked but is now missing, we might remove it or keep it?
    # Given the "update" nature, we'll refresh the known set.
    
    current_hashes = state_data['artifact_hashes']
    
    # Remove keys that are no longer in our artifact list (optional, but clean)
    # Actually, let's just update the ones we have.
    for key, value in hashes.items():
        current_hashes[key] = value
    
    state_data['artifact_hashes'] = current_hashes

    # Save state back
    try:
        with open(state_path, 'w', encoding='utf-8') as f:
            yaml.dump(state_data, f, default_flow_style=False, sort_keys=False)
        print(f"Successfully updated state file: {state_path}")
        return True
    except Exception as e:
        print(f"Error writing state file: {e}", file=sys.stderr)
        return False

def main():
    parser = argparse.ArgumentParser(description="Update project state with artifact hashes.")
    parser.add_argument('--state-file', type=str, default=STATE_FILE_REL_PATH,
                        help=f"Path to the state file relative to project root. Default: {STATE_FILE_REL_PATH}")
    args = parser.parse_args()

    # Recompute hashes for the standard set
    hashes = compute_all_hashes()

    if not hashes:
        print("No artifacts found to hash. State file not updated.")
        return 1

    print(f"Computed hashes for {len(hashes)} artifacts.")
    for k, v in hashes.items():
        print(f"  {k}: {v[:16]}...")

    success = update_state_file(hashes)
    return 0 if success else 1

if __name__ == "__main__":
    sys.exit(main())