# Ensure src is in path for imports if running from project root
project_root = Path(__file__).parent.parent.parent
if str(project_root) not in sys.path:
    sys.path.insert(0, str(project_root))

from src.utils.config import ensure_dir


class TestReportSchema:
    """Tests for the evaluation report schema."""

    def test_report_schema_structure(self, tmp_path):
        """Verify that a valid report contains all required fields."""
        # Simulate a valid report content
        valid_report = {
            "model_metrics": {
                "precision": 0.85,
                "recall": 0.80,
                "f1": 0.82,
                "accuracy": 0.83
            },
            "baseline_metrics": {
                "precision": 0.50,
                "recall": 0.50,
                "f1": 0.50,
                "accuracy": 0.50
            },
            "resource_stats": {
                "wall_clock_time_seconds": 120.5,
                "peak_memory_mb": 1024.0,
                "current_memory_mb": 512.0
            },
            "conclusion": "The model outperforms the baseline."
        }

        report_path = tmp_path / "test_report.json"
        with open(report_path, 'w') as f:
            json.dump(valid_report, f)

        # Load and validate
        with open(report_path, 'r') as f:
            data = json.load(f)

        # Check top-level keys
        assert "model_metrics" in data
        assert "baseline_metrics" in data
        assert "resource_stats" in data
        assert "conclusion" in data

        # Check model_metrics keys
        for key in ["precision", "recall", "f1", "accuracy"]:
            assert key in data["model_metrics"]
            assert isinstance(data["model_metrics"][key], (int, float))

        # Check baseline_metrics keys
        for key in ["precision", "recall", "f1", "accuracy"]:
            assert key in data["baseline_metrics"]
            assert isinstance(data["baseline_metrics"][key], (int, float))

        # Check resource_stats keys
        for key in ["wall_clock_time_seconds", "peak_memory_mb", "current_memory_mb"]:
            assert key in data["resource_stats"]
            assert isinstance(data["resource_stats"][key], (int, float))

        # Check conclusion is a string
        assert isinstance(data["conclusion"], str)

    def test_report_schema_invalid_missing_field(self, tmp_path):
        """Verify that a report with missing required fields is invalid."""
        invalid_report = {
            "model_metrics": {
                "precision": 0.85,
                "recall": 0.80
                # Missing f1 and accuracy
            },
            "baseline_metrics": {},
            "resource_stats": {},
            "conclusion": "Test"
        }

        report_path = tmp_path / "invalid_report.json"
        with open(report_path, 'w') as f:
            json.dump(invalid_report, f)

        with open(report_path, 'r') as f:
            data = json.load(f)

        # This test confirms the schema requires all fields
        # In a real implementation, a schema validator would raise an error here.
        # For now, we assert that the data is indeed missing the fields.
        assert "f1" not in data["model_metrics"]

    def test_report_schema_types(self, tmp_path):
        """Verify that metric values are numeric."""
        invalid_report = {
            "model_metrics": {
                "precision": "not_a_number", # Should be float
                "recall": 0.80,
                "f1": 0.82,
                "accuracy": 0.83
            },
            "baseline_metrics": {
                "precision": 0.50,
                "recall": 0.50,
                "f1": 0.50,
                "accuracy": 0.50
            },
            "resource_stats": {
                "wall_clock_time_seconds": 120.5,
                "peak_memory_mb": 1024.0,
                "current_memory_mb": 512.0
            },
            "conclusion": "Test"
        }

        report_path = tmp_path / "type_report.json"
        with open(report_path, 'w') as f:
            json.dump(invalid_report, f)

        with open(report_path, 'r') as f:
            data = json.load(f)

        assert isinstance(data["model_metrics"]["precision"], str)
        assert not isinstance(data["model_metrics"]["precision"], (int, float))