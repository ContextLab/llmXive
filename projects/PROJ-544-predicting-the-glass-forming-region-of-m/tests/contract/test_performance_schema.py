"""
Contract test for model performance metrics schema (T015).

This test validates that the model performance output file produced by
code/models/evaluate.py conforms to the JSON Schema defined in
contracts/model_performance_record.schema.json.

It ensures:
1. The output file exists and is valid JSON.
2. All required fields defined in the schema are present.
3. Data types match the schema constraints.
4. Traceability fields (data_row_id, code_block_id) are present as per Constitution IV.
"""
import json
import os
import sys
import unittest
from pathlib import Path

# Add project root to path if necessary, though usually tests run from root
# Assuming standard structure: tests/contract/ and code/models/
PROJECT_ROOT = Path(__file__).resolve().parent.parent.parent
CONTRACTS_DIR = PROJECT_ROOT / "contracts"
RESULTS_DIR = PROJECT_ROOT / "results"

SCHEMA_PATH = CONTRACTS_DIR / "model_performance_record.schema.json"
OUTPUT_PATH = RESULTS_DIR / "performance_metrics.json"

class TestPerformanceSchema(unittest.TestCase):
    @classmethod
    def setUpClass(cls):
        """Load the JSON schema once for all tests in this class."""
        if not SCHEMA_PATH.exists():
            raise FileNotFoundError(
                f"Schema file not found at {SCHEMA_PATH}. "
                "Ensure T004 (contracts) is completed."
            )
        with open(SCHEMA_PATH, "r", encoding="utf-8") as f:
            cls.schema = json.load(f)

        # Verify the output file exists (generated by T021)
        if not OUTPUT_PATH.exists():
            # If the file doesn't exist, we cannot test the schema against it.
            # This indicates the training pipeline (T021) hasn't run yet.
            raise FileNotFoundError(
                f"Performance metrics output file not found at {OUTPUT_PATH}. "
                "Run scripts/train_model.py or code/models/evaluate.py first."
            )
        
        with open(OUTPUT_PATH, "r", encoding="utf-8") as f:
            cls.data = json.load(f)

    def test_schema_exists_and_valid(self):
        """Verify the schema itself is a valid JSON object."""
        self.assertIsInstance(self.schema, dict)
        self.assertIn("properties", self.schema)
        self.assertIn("required", self.schema)

    def test_output_is_valid_json(self):
        """Basic check that the output file is valid JSON (already done in setUpClass, but explicit)."""
        self.assertIsInstance(self.data, dict)

    def test_required_fields_present(self):
        """Check that all fields marked as required in the schema are present in the data."""
        required_fields = self.schema.get("required", [])
        missing_fields = [field for field in required_fields if field not in self.data]
        self.assertEqual(
            len(missing_fields), 0,
            f"Missing required fields in performance metrics: {missing_fields}"
        )

    def test_field_types_match_schema(self):
        """Validate that the data types in the output match the schema definitions."""
        properties = self.schema.get("properties", {})
        
        for field_name, field_spec in properties.items():
            if field_name not in self.data:
                continue # Already covered by required check
            
            expected_type = field_spec.get("type")
            actual_value = self.data[field_name]
            actual_type = type(actual_value).__name__

            # Map JSON schema types to Python types
            type_map = {
                "string": str,
                "number": (int, float),
                "integer": int,
                "boolean": bool,
                "array": list,
                "object": dict
            }
            
            expected_python_type = type_map.get(expected_type)
            if expected_python_type:
                self.assertIsInstance(
                    actual_value, expected_python_type,
                    f"Field '{field_name}' expected {expected_type}, got {actual_type}"
                )

    def test_traceability_fields(self):
        """
        Verify Constitution IV compliance: 
        data_row_id and code_block_id must be present and non-empty strings.
        """
        trace_fields = ["data_row_id", "code_block_id"]
        for field in trace_fields:
            self.assertIn(field, self.data, f"Traceability field '{field}' missing.")
            self.assertIsInstance(self.data[field], str, f"Field '{field}' must be a string.")
            self.assertTrue(
                len(self.data[field]) > 0, 
                f"Field '{field}' cannot be an empty string."
            )
            # Basic UUID check for data_row_id if it looks like one
            if field == "data_row_id":
                # Simple check for UUID format (8-4-4-4-12 hex)
                import re
                uuid_pattern = re.compile(r'^[0-9a-f]{8}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{12}$', re.I)
                self.assertTrue(
                    uuid_pattern.match(self.data[field]),
                    f"data_row_id '{self.data[field]}' does not appear to be a valid UUID."
                )

    def test_metrics_structure(self):
        """
        Verify that the metrics object (if present) contains expected sub-fields
        like roc_auc, precision, recall, and std_dev.
        """
        if "metrics" in self.data:
            metrics = self.data["metrics"]
            self.assertIsInstance(metrics, dict)
            expected_metric_keys = ["roc_auc", "precision", "recall", "std_dev"]
            for key in expected_metric_keys:
                self.assertIn(
                    key, metrics, 
                    f"Metric '{key}' missing from 'metrics' object."
                )
                self.assertIsInstance(metrics[key], (int, float), f"Metric '{key}' must be numeric.")

    def test_validation_limitation_note(self):
        """
        Verify the presence of 'validation_limitation_note' as per FR-004 and Constitution VII.
        It should be a non-empty string.
        """
        self.assertIn("validation_limitation_note", self.data)
        self.assertIsInstance(self.data["validation_limitation_note"], str)
        self.assertTrue(
            len(self.data["validation_limitation_note"]) > 0,
            "validation_limitation_note should not be empty."
        )

if __name__ == "__main__":
    unittest.main()