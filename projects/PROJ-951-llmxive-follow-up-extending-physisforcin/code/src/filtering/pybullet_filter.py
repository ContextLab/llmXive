"""
Canonical simulation module for PhysisForcing.

This module implements the canonical simulation step using PyBullet in headless mode.
It generates ground truth trajectories for robotic manipulation tasks based on
scene configurations produced by prompt-to-scene translation (T014).

The module:
1. Loads scene configurations (JSON) containing objects, poses, and robot setup.
2. Initializes a headless PyBullet simulation.
3. Executes the manipulation task (e.g., pick-and-place) using a simple physics-based controller.
4. Records the trajectory (positions, orientations, velocities) of relevant objects and the robot.
5. Saves the ground truth trajectory to `data/curated/ground_truth/<video_id>_gt.json`.
6. Calculates basic physics metrics (contact events, stability).

Dependencies:
- pybullet (CPU-only, headless mode)
- Existing project modules: src.filtering.prompt_to_scene, src.utils.io_utils, src.utils.logging
"""

import os
import json
import time
import logging
from pathlib import Path
from typing import Dict, Any, List, Optional, Tuple, Union
import numpy as np

# PyBullet import (must be installed as per T002/T004)
import pybullet as p
import pybullet_data

# Project imports
from src.filtering.prompt_to_scene import translate_prompt_to_scene, save_scene_config
from src.utils.io_utils import ensure_dirs, get_file_size, calculate_file_checksum
from src.utils.logging import get_logger, log_metric, setup_default_loggers
from src.utils.seeding import set_deterministic_seed
from src.training.config import load_config, get_filter_discard_threshold

# Constants
SIMULATION_DT = 1.0 / 240.0  # 240 Hz physics
SIMULATION_STEPS_PER_FRAME = 1  # Can be increased for stability if needed
TOTAL_SIMULATION_TIME = 5.0  # Seconds to simulate
CONTACT_THRESHOLD = 0.01  # Minimum force to register a contact event

logger = get_logger(__name__)


class CanonicalSimulation:
    """
    Manages the canonical PyBullet simulation for generating ground truth trajectories.
    """

    def __init__(self, scene_config_path: str, video_id: str, output_dir: str):
        """
        Initialize the simulation with a scene configuration.

        Args:
            scene_config_path: Path to the JSON scene config generated by prompt_to_scene.
            video_id: Unique identifier for the video/simulation run.
            output_dir: Directory to save ground truth outputs.
        """
        self.scene_config_path = Path(scene_config_path)
        self.video_id = video_id
        self.output_dir = Path(output_dir)
        self.gt_file_path = self.output_dir / f"{video_id}_gt.json"

        # Ensure output directory exists
        ensure_dirs(self.output_dir)

        # Load scene configuration
        if not self.scene_config_path.exists():
            raise FileNotFoundError(f"Scene config not found: {self.scene_config_path}")

        with open(self.scene_config_path, 'r') as f:
            self.scene_config = json.load(f)

        # Initialize simulation state
        self.physics_client = None
        self.trajectory_data: List[Dict[str, Any]] = []
        self.contact_events: List[Dict[str, Any]] = []
        self.initial_poses: Dict[str, Tuple[List[float], List[float]]] = {}
        self.object_ids: Dict[str, int] = {}
        self.robot_id: Optional[int] = None
        self.robot_controlled_joints: List[int] = []
        self.target_positions: List[float] = []
        self.current_joint_positions: List[float] = []

        logger.info(f"Initialized CanonicalSimulation for video_id: {video_id}")

    def _setup_physics_engine(self):
        """Configure PyBullet for headless CPU simulation."""
        # Connect in direct mode (headless, no GUI)
        self.physics_client = p.connect(p.DIRECT)
        p.setAdditionalSearchPath(pybullet_data.getDataPath())
        p.setGravity(0, 0, -9.81)
        p.setTimeStep(SIMULATION_DT)
        p.setRealTimeSimulation(0)  # Step-based simulation

        # Load ground plane
        plane_id = p.loadURDF("plane.urdf")
        p.changeDynamics(plane_id, -1, lateralFriction=1.0)

        logger.debug("Physics engine setup complete (headless).")

    def _load_scene_objects(self):
        """Load objects defined in the scene configuration."""
        objects = self.scene_config.get("objects", [])
        robot_config = self.scene_config.get("robot", {})

        # Load robot if present
        if robot_config:
            robot_urdf = robot_config.get("urdf_path")
            if robot_urdf and os.path.exists(robot_urdf):
                start_pos = robot_config.get("initial_pose", [0, 0, 0])
                start_orn = robot_config.get("initial_orientation", [0, 0, 0, 1])
                self.robot_id = p.loadURDF(robot_urdf, start_pos, start_orn)
                p.setJointMotorControlArray(
                    bodyIndex=self.robot_id,
                    jointIndices=self._get_active_joints(),
                    controlMode=p.VELOCITY_CONTROL,
                    forces=[0] * len(self._get_active_joints())
                )
                self.robot_controlled_joints = self._get_active_joints()
                logger.info(f"Loaded robot: {robot_urdf}, ID: {self.robot_id}")
            else:
                logger.warning(f"Robot URDF not found or invalid: {robot_urdf}")

        # Load other objects
        for obj in objects:
            obj_name = obj.get("name")
            obj_urdf = obj.get("urdf_path")
            if not obj_urdf or not os.path.exists(obj_urdf):
                logger.warning(f"Skipping object {obj_name}: URDF not found ({obj_urdf})")
                continue

            pos = obj.get("initial_pose", [0, 0, 0])
            orn = obj.get("initial_orientation", [0, 0, 0, 1])

            obj_id = p.loadURDF(obj_urdf, pos, orn)
            self.object_ids[obj_name] = obj_id
            self.initial_poses[obj_name] = (pos, orn)

            # Set some basic dynamics properties if defined
            if "mass" in obj:
                p.changeDynamics(obj_id, -1, mass=obj["mass"])
            if "friction" in obj:
                p.changeDynamics(obj_id, -1, lateralFriction=obj["friction"])

            logger.debug(f"Loaded object: {obj_name}, ID: {obj_id}")

        if not self.object_ids and not self.robot_id:
            raise ValueError("No valid objects or robot loaded in the scene.")

    def _get_active_joints(self) -> List[int]:
        """Get list of joint indices that are actuated/controlled."""
        if self.robot_id is None:
            return []
        num_joints = p.getNumJoints(self.robot_id)
        active_joints = []
        for i in range(num_joints):
            joint_info = p.getJointInfo(self.robot_id, i)
            # Joint types: 0=revolute, 1=prismatic, 2=spherical, 3=planar, 4=point2point
            # We control revolute and prismatic joints
            if joint_info[2] in [0, 1]:
                active_joints.append(i)
        return active_joints

    def _execute_task(self):
        """
        Execute the manipulation task based on scene configuration.
        Currently implements a simple move-to-target logic if target is defined.
        """
        task_config = self.scene_config.get("task", {})
        target_obj = task_config.get("target_object")
        target_pose = task_config.get("target_pose")

        if not target_obj or not target_pose:
            logger.info("No specific task target defined. Running idle simulation.")
            # Just let physics settle
            return

        # If we have a robot, move towards target
        if self.robot_id is not None:
            # Simple PD controller to move end-effector (or base) towards target
            # This is a placeholder for a more sophisticated planner
            # For now, we just apply forces to the robot base or joints towards the target
            # Assuming target_pose is [x, y, z]
            target_pos = np.array(target_pose)

            # Get current base position (assuming base is link 0)
            base_pos, _ = p.getBasePositionAndOrientation(self.robot_id)
            current_pos = np.array(base_pos)

            # Calculate direction
            direction = target_pos - current_pos
            distance = np.linalg.norm(direction)

            if distance > 0.01:
                # Apply a simple velocity command
                velocity = direction / distance * 0.1  # Max speed 0.1 m/s
                # This is a very simplified control; real implementation would use IK
                # For this canonical simulation, we just move the base linearly
                # Note: In a real scenario, we would calculate joint torques via IK
                # Here we directly set base velocity for demonstration of physics
                p.resetBaseVelocity(self.robot_id, linearVelocity=velocity.tolist())

        # Also move the target object if it's one of the loaded objects
        if target_obj in self.object_ids:
            obj_id = self.object_ids[target_obj]
            current_pos, _ = p.getBasePositionAndOrientation(obj_id)
            target_pos = np.array(target_pose)
            direction = target_pos - np.array(current_pos)
            distance = np.linalg.norm(direction)

            if distance > 0.01:
                velocity = direction / distance * 0.05
                p.resetBaseVelocity(obj_id, linearVelocity=velocity.tolist())

    def _record_state(self, step: int):
        """Record the current state of all objects and robot."""
        state_record = {
            "step": step,
            "time": step * SIMULATION_DT,
            "objects": {}
        }

        # Record robot state
        if self.robot_id is not None:
            base_pos, base_orn = p.getBasePositionAndOrientation(self.robot_id)
            base_lin_vel, base_ang_vel = p.getBaseVelocity(self.robot_id)
            joint_states = p.getJointStates(self.robot_id, self.robot_controlled_joints)

            state_record["robot"] = {
                "base_position": list(base_pos),
                "base_orientation": list(base_orn),
                "linear_velocity": list(base_lin_vel),
                "angular_velocity": list(base_ang_vel),
                "joint_positions": [js[0] for js in joint_states],
                "joint_velocities": [js[1] for js in joint_states]
            }

        # Record other objects
        for name, obj_id in self.object_ids.items():
            pos, orn = p.getBasePositionAndOrientation(obj_id)
            lin_vel, ang_vel = p.getBaseVelocity(obj_id)
            state_record["objects"][name] = {
                "position": list(pos),
                "orientation": list(orn),
                "linear_velocity": list(lin_vel),
                "angular_velocity": list(ang_vel)
            }

        self.trajectory_data.append(state_record)

    def _check_contacts(self, step: int):
        """Check for contact events between objects."""
        contacts = p.getContactPoints()
        for contact in contacts:
            # contact tuple: (flags, bodyA, linkA, bodyB, linkB, positionA, positionB, normal, force, lcp)
            body_a = contact[1]
            body_b = contact[3]
            force = contact[8]

            if force > CONTACT_THRESHOLD:
                # Map body IDs to names
                obj_name_a = None
                obj_name_b = None

                if body_a == self.robot_id:
                    obj_name_a = "robot"
                else:
                    for name, oid in self.object_ids.items():
                        if oid == body_a:
                            obj_name_a = name
                            break

                if body_b == self.robot_id:
                    obj_name_b = "robot"
                else:
                    for name, oid in self.object_ids.items():
                        if oid == body_b:
                            obj_name_b = name
                            break

                if obj_name_a and obj_name_b:
                    self.contact_events.append({
                        "step": step,
                        "time": step * SIMULATION_DT,
                        "object_a": obj_name_a,
                        "object_b": obj_name_b,
                        "force": float(force),
                        "position": list(contact[5])
                    })

    def run_simulation(self) -> Dict[str, Any]:
        """
        Execute the full simulation and return results.

        Returns:
            Dict containing trajectory data, contact events, and metadata.
        """
        logger.info(f"Starting canonical simulation for {self.video_id}")

        try:
            self._setup_physics_engine()
            self._load_scene_objects()

            total_steps = int(TOTAL_SIMULATION_TIME / SIMULATION_DT)
            logger.info(f"Running {total_steps} simulation steps ({TOTAL_SIMULATION_TIME}s)")

            for step in range(total_steps):
                # Step physics
                p.stepSimulation()

                # Execute task logic (move towards target)
                self._execute_task()

                # Record state
                self._record_state(step)

                # Check contacts
                self._check_contacts(step)

                # Small delay for CPU load management (optional)
                # time.sleep(0.001)

            logger.info(f"Simulation complete for {self.video_id}")

        except Exception as e:
            logger.error(f"Simulation failed for {self.video_id}: {str(e)}")
            raise
        finally:
            if self.physics_client:
                p.disconnect()

        return {
            "video_id": self.video_id,
            "trajectory": self.trajectory_data,
            "contacts": self.contact_events,
            "initial_poses": self.initial_poses,
            "total_steps": total_steps,
            "simulation_time": TOTAL_SIMULATION_TIME
        }

    def save_ground_truth(self, results: Dict[str, Any]):
        """Save the ground truth trajectory to disk."""
        output_path = self.gt_file_path
        with open(output_path, 'w') as f:
            json.dump(results, f, indent=2)

        # Calculate checksum
        checksum = calculate_file_checksum(output_path)
        logger.info(f"Ground truth saved to {output_path} (checksum: {checksum})")

        # Log metrics
        log_metric("simulation_steps", results["total_steps"], video_id=self.video_id)
        log_metric("contact_events_count", len(results["contacts"]), video_id=self.video_id)


def run_canonical_simulation_for_scene(scene_config_path: str, video_id: str, output_dir: str) -> Dict[str, Any]:
    """
    Convenience function to run simulation for a single scene.

    Args:
        scene_config_path: Path to scene config JSON.
        video_id: Unique ID for the video.
        output_dir: Directory to save outputs.

    Returns:
        Simulation results dictionary.
    """
    sim = CanonicalSimulation(scene_config_path, video_id, output_dir)
    results = sim.run_simulation()
    sim.save_ground_truth(results)
    return results


def main():
    """
    Main entry point for running canonical simulations on a set of scenes.
    Expects scene configs to be in `data/curated/scenes/` and outputs to `data/curated/ground_truth/`.
    """
    setup_default_loggers()
    config = load_config()

    # Set seed for reproducibility
    set_deterministic_seed(config.get("seed", 42))

    scenes_dir = Path("data/curated/scenes")
    gt_dir = Path("data/curated/ground_truth")
    ensure_dirs(gt_dir)

    if not scenes_dir.exists():
        logger.error(f"Scenes directory not found: {scenes_dir}")
        return

    scene_files = list(scenes_dir.glob("*.json"))
    logger.info(f"Found {len(scene_files)} scene configurations to process.")

    successful = 0
    failed = 0

    for scene_file in scene_files:
        video_id = scene_file.stem
        try:
            logger.info(f"Processing {video_id}...")
            run_canonical_simulation_for_scene(str(scene_file), video_id, str(gt_dir))
            successful += 1
        except Exception as e:
            logger.error(f"Failed to process {video_id}: {str(e)}")
            failed += 1

    logger.info(f"Simulation complete: {successful} successful, {failed} failed.")


if __name__ == "__main__":
    main()