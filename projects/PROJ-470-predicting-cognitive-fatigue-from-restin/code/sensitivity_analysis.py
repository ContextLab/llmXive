import os
import sys
import csv
from pathlib import Path
import pandas as pd
import numpy as np
import yaml

def load_config():
    """Load configuration from code/config.yaml."""
    config_path = Path("code/config.yaml")
    if not config_path.exists():
        raise FileNotFoundError(f"Config file not found: {config_path}")
    with open(config_path, 'r') as f:
        return yaml.safe_load(f)

def load_analysis_results():
    """
    Load the correlation analysis results.
    Expects data/analysis/correlation_results.csv generated by analysis.py.
    """
    input_path = Path("data/analysis/correlation_results.csv")
    if not input_path.exists():
        raise FileNotFoundError(
            f"Analysis results not found at {input_path}. "
            "Ensure code/analysis.py has been run successfully."
        )
    return pd.read_csv(input_path)

def run_sensitivity_analysis(results_df, thresholds=None):
    """
    Run sensitivity analysis at specified p-value thresholds.
    
    Args:
        results_df (pd.DataFrame): DataFrame containing correlation results
                                   with columns: 'channel', 'metric', 
                                   'correlation', 'p_value', 'fdr_p_value'
        thresholds (list): List of p-value thresholds to analyze (default [0.05, 0.01])
    
    Returns:
        pd.DataFrame: Summary of significant findings at each threshold
    """
    if thresholds is None:
        thresholds = [0.05, 0.01]
    
    sensitivity_results = []
    
    for threshold in thresholds:
        # Filter for significant results using FDR-corrected p-values
        significant = results_df[results_df['fdr_p_value'] <= threshold]
        
        row = {
            'threshold': threshold,
            'total_tests': len(results_df),
            'significant_count': len(significant),
            'significance_rate': len(significant) / len(results_df) if len(results_df) > 0 else 0.0,
            'channels': ';'.join(significant['channel'].unique()) if len(significant) > 0 else '',
            'metrics': ';'.join(significant['metric'].unique()) if len(significant) > 0 else '',
            'strongest_correlation': significant['correlation'].abs().max() if len(significant) > 0 else 0.0,
            'min_p_value': significant['fdr_p_value'].min() if len(significant) > 0 else 1.0
        }
        sensitivity_results.append(row)
    
    return pd.DataFrame(sensitivity_results)

def generate_sensitivity_table(results_df, output_path):
    """
    Generate and save the sensitivity analysis table.
    
    Args:
        results_df (pd.DataFrame): Correlation analysis results
        output_path (str or Path): Path to save the CSV output
    """
    if not isinstance(output_path, Path):
        output_path = Path(output_path)
    
    # Ensure output directory exists
    output_path.parent.mkdir(parents=True, exist_ok=True)
    
    # Run analysis
    table_df = run_sensitivity_analysis(results_df)
    
    # Save to CSV
    table_df.to_csv(output_path, index=False)
    
    return table_df

def main():
    """Main entry point for sensitivity analysis."""
    try:
        print("Loading configuration...")
        config = load_config()
        
        print("Loading correlation analysis results...")
        results_df = load_analysis_results()
        
        # Validate required columns
        required_cols = ['channel', 'metric', 'correlation', 'p_value', 'fdr_p_value']
        missing_cols = [col for col in required_cols if col not in results_df.columns]
        if missing_cols:
            raise ValueError(f"Missing required columns in analysis results: {missing_cols}")
        
        output_path = Path("data/analysis/sensitivity_table.csv")
        print(f"Running sensitivity analysis and saving to {output_path}...")
        
        table = generate_sensitivity_table(results_df, output_path)
        
        print("\nSensitivity Analysis Results:")
        print(table.to_string(index=False))
        
        print(f"\n✓ Sensitivity table saved to: {output_path}")
        return 0
        
    except FileNotFoundError as e:
        print(f"ERROR: {e}", file=sys.stderr)
        return 1
    except ValueError as e:
        print(f"ERROR: {e}", file=sys.stderr)
        return 1
    except Exception as e:
        print(f"ERROR: Unexpected error during sensitivity analysis: {e}", file=sys.stderr)
        import traceback
        traceback.print_exc()
        return 1

if __name__ == "__main__":
    sys.exit(main())
