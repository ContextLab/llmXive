import os
import sys
import csv
from pathlib import Path
import pandas as pd
import numpy as np
import yaml

# Import shared utilities
from utils.logging import get_logger

def load_config():
    """Load configuration from code/config.yaml."""
    config_path = Path(__file__).parent.parent / 'code' / 'config.yaml'
    if not config_path.exists():
        raise FileNotFoundError(f"Config file not found: {config_path}")
    with open(config_path, 'r') as f:
        return yaml.safe_load(f)

def load_analysis_results():
    """
    Load the correlation analysis results generated by code/analysis.py.
    Expected file: data/analysis/correlation_results.csv
    """
    results_path = Path(__file__).parent.parent / 'data' / 'analysis' / 'correlation_results.csv'
    if not results_path.exists():
        raise FileNotFoundError(
            f"Analysis results not found: {results_path}. "
            "Please run code/analysis.py first to generate correlation data."
        )
    df = pd.read_csv(results_path)
    required_cols = ['metric_name', 'correlation', 'p_value', 'fdr_corrected_p', 'significant']
    missing = [c for c in required_cols if c not in df.columns]
    if missing:
        raise ValueError(f"Missing required columns in results: {missing}")
    return df

def run_sensitivity_analysis(results_df, thresholds=None):
    """
    Run sensitivity analysis by filtering results at different p-value thresholds.
    
    Args:
        results_df: DataFrame with correlation results (p_value, fdr_corrected_p, etc.)
        thresholds: List of p-value thresholds to test (default: [0.05, 0.01])
    
    Returns:
        DataFrame with sensitivity analysis results per threshold.
    """
    if thresholds is None:
        thresholds = [0.05, 0.01]
    
    results = []
    
    for threshold in thresholds:
        # Count significant findings at this threshold
        # Use FDR-corrected p-values for significance
        significant_mask = results_df['fdr_corrected_p'] <= threshold
        significant_count = significant_mask.sum()
        total_tests = len(results_df)
        
        # Extract significant metrics
        sig_metrics = results_df[significant_mask][['metric_name', 'correlation', 'p_value', 'fdr_corrected_p']]
        
        for _, row in sig_metrics.iterrows():
            results.append({
                'threshold': threshold,
                'metric_name': row['metric_name'],
                'correlation': row['correlation'],
                'raw_p_value': row['p_value'],
                'fdr_corrected_p': row['fdr_corrected_p'],
                'is_significant': True
            })
        
        # Add summary row for this threshold
        results.append({
            'threshold': threshold,
            'metric_name': '__SUMMARY__',
            'correlation': None,
            'raw_p_value': None,
            'fdr_corrected_p': None,
            'is_significant': False,
            'total_significant': int(significant_count),
            'total_tests': int(total_tests),
            'proportion_significant': significant_count / total_tests if total_tests > 0 else 0.0
        })
    
    return pd.DataFrame(results)

def generate_sensitivity_table(sensitivity_df, output_path):
    """
    Generate a formatted sensitivity table and save to CSV.
    
    Args:
        sensitivity_df: DataFrame from run_sensitivity_analysis
        output_path: Path to save the CSV file
    """
    # Ensure output directory exists
    output_path = Path(output_path)
    output_path.parent.mkdir(parents=True, exist_ok=True)
    
    # Separate summary rows from detailed rows
    summary_rows = sensitivity_df[sensitivity_df['metric_name'] == '__SUMMARY__']
    detail_rows = sensitivity_df[sensitivity_df['metric_name'] != '__SUMMARY__']
    
    # Create a formatted table for CSV export
    # Group by threshold for readability
    with open(output_path, 'w', newline='') as f:
        writer = csv.writer(f)
        
        # Header
        writer.writerow(['Sensitivity Analysis Results'])
        writer.writerow(['Threshold', 'Total Tests', 'Significant Findings', 'Proportion Significant'])
        writer.writerow(['-' * 10, '-' * 12, '-' * 22, '-' * 23])
        
        for _, row in summary_rows.iterrows():
            writer.writerow([
                f"{row['threshold']:.2f}",
                row['total_tests'],
                row['total_significant'],
                f"{row['proportion_significant']:.4f}"
            ])
        
        writer.writerow([])  # Empty row
        writer.writerow(['Detailed Results by Threshold'])
        writer.writerow(['Threshold', 'Metric Name', 'Correlation', 'Raw P-Value', 'FDR Corrected P'])
        writer.writerow(['-' * 10, '-' * 20, '-' * 12, '-' * 13, '-' * 18])
        
        for _, row in detail_rows.iterrows():
            writer.writerow([
                f"{row['threshold']:.2f}",
                row['metric_name'],
                f"{row['correlation']:.4f}" if pd.notna(row['correlation']) else 'N/A',
                f"{row['raw_p_value']:.6f}" if pd.notna(row['raw_p_value']) else 'N/A',
                f"{row['fdr_corrected_p']:.6f}" if pd.notna(row['fdr_corrected_p']) else 'N/A'
            ])
    
    return output_path

def main():
    """Main entry point for sensitivity analysis."""
    logger = get_logger(__name__)
    logger.info("Starting sensitivity analysis (T021)")
    
    try:
        # Load configuration
        config = load_config()
        logger.info(f"Loaded configuration from code/config.yaml")
        
        # Load analysis results
        results_df = load_analysis_results()
        logger.info(f"Loaded {len(results_df)} correlation results")
        
        # Run sensitivity analysis
        thresholds = config.get('sensitivity_analysis', {}).get('thresholds', [0.05, 0.01])
        sensitivity_df = run_sensitivity_analysis(results_df, thresholds)
        logger.info(f"Completed sensitivity analysis at {len(thresholds)} thresholds")
        
        # Generate output table
        output_path = Path(__file__).parent.parent / 'data' / 'analysis' / 'sensitivity_table.csv'
        generate_sensitivity_table(sensitivity_df, output_path)
        logger.info(f"Sensitivity table written to {output_path}")
        
        # Print summary to console
        summary = sensitivity_df[sensitivity_df['metric_name'] == '__SUMMARY__']
        print("\n=== Sensitivity Analysis Summary ===")
        for _, row in summary.iterrows():
            print(f"Threshold p≤{row['threshold']:.2f}: "
                  f"{row['total_significant']}/{row['total_tests']} significant "
                  f"({row['proportion_significant']:.2%})")
        
        logger.info("Sensitivity analysis completed successfully")
        return 0
        
    except FileNotFoundError as e:
        logger.error(f"File not found: {e}")
        return 1
    except ValueError as e:
        logger.error(f"Validation error: {e}")
        return 1
    except Exception as e:
        logger.error(f"Unexpected error: {e}")
        raise

if __name__ == '__main__':
    sys.exit(main())
