import os
import sys
import csv
from pathlib import Path
import pandas as pd
import numpy as np
import yaml

# Import existing utilities from the project
from utils.logging import get_logger

logger = get_logger(__name__)

def load_config(config_path='code/config.yaml'):
    """Load pipeline configuration from YAML file."""
    with open(config_path, 'r') as f:
        return yaml.safe_load(f)

def load_analysis_results(results_path='data/analysis/correlation_results.csv'):
    """
    Load the correlation results generated by analysis.py.
    Expected columns: channel, correlation, p_value, method, adjusted_p_value
    """
    if not os.path.exists(results_path):
        raise FileNotFoundError(f"Analysis results not found at {results_path}. "
                                "Run code/analysis.py first.")
    return pd.read_csv(results_path)

def run_sensitivity_analysis(results_df, thresholds=[0.05, 0.01]):
    """
    Run sensitivity analysis at specified p-value thresholds.
    
    Args:
        results_df: DataFrame with correlation results including p_values
        thresholds: List of p-value thresholds to check (e.g., [0.05, 0.01])
        
    Returns:
        DataFrame with sensitivity analysis results
    """
    results = []
    
    for threshold in thresholds:
        # Filter results based on the threshold
        # We check both raw and adjusted p-values to be comprehensive
        significant_raw = results_df[results_df['p_value'] <= threshold]
        significant_adj = results_df[results_df['adjusted_p_value'] <= threshold]
        
        # Combine unique significant channels for this threshold
        significant_channels = set(significant_raw['channel'].tolist()) | \
                             set(significant_adj['channel'].tolist())
        
        results.append({
            'threshold': threshold,
            'significant_count_raw': len(significant_raw),
            'significant_count_adjusted': len(significant_adj),
            'unique_significant_channels': len(significant_channels),
            'channels': sorted(list(significant_channels)),
            'proportion_significant': len(significant_channels) / len(results_df) if len(results_df) > 0 else 0
        })
    
    return pd.DataFrame(results)

def generate_sensitivity_table(sensitivity_df, output_path='data/analysis/sensitivity_table.csv'):
    """
    Generate a formatted sensitivity analysis table and save to CSV.
    
    Args:
        sensitivity_df: DataFrame from run_sensitivity_analysis
        output_path: Path to save the output CSV
    """
    # Ensure output directory exists
    output_dir = Path(output_path).parent
    output_dir.mkdir(parents=True, exist_ok=True)
    
    # Create a human-readable table format
    table_data = []
    for _, row in sensitivity_df.iterrows():
        table_data.append({
            'p_threshold': row['threshold'],
            'significant_channels_raw': row['significant_count_raw'],
            'significant_channels_adjusted': row['significant_count_adjusted'],
            'unique_significant_channels': row['unique_significant_channels'],
            'channels': '; '.join(row['channels']) if row['channels'] else 'None',
            'proportion_significant': f"{row['proportion_significant']:.3f}"
        })
    
    table_df = pd.DataFrame(table_data)
    
    # Save to CSV
    table_df.to_csv(output_path, index=False)
    logger.info(f"Sensitivity analysis table saved to {output_path}")
    
    return table_df

def main():
    """Main entry point for sensitivity analysis."""
    logger.info("Starting sensitivity analysis for cognitive fatigue EEG study")
    
    # Load configuration
    config = load_config()
    
    # Load analysis results
    try:
        results_df = load_analysis_results()
        logger.info(f"Loaded {len(results_df)} correlation results")
    except FileNotFoundError as e:
        logger.error(str(e))
        sys.exit(1)
    
    # Define thresholds from config or use defaults
    thresholds = config.get('sensitivity_analysis', {}).get('thresholds', [0.05, 0.01])
    logger.info(f"Running sensitivity analysis at thresholds: {thresholds}")
    
    # Run sensitivity analysis
    sensitivity_df = run_sensitivity_analysis(results_df, thresholds)
    
    # Generate and save the table
    output_path = config.get('paths', {}).get('sensitivity_table', 'data/analysis/sensitivity_table.csv')
    table_df = generate_sensitivity_table(sensitivity_df, output_path)
    
    # Print summary to console
    print("\n=== Sensitivity Analysis Summary ===")
    print(table_df.to_string(index=False))
    print(f"\nDetailed results saved to: {output_path}")
    
    logger.info("Sensitivity analysis completed successfully")
    return 0

if __name__ == "__main__":
    sys.exit(main())
