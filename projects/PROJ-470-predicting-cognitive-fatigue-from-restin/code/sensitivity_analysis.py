def load_analysis_results():
    """
    Load the correlation analysis results generated by T019/T020.
    Expects data/analysis/correlation_results.csv (or similar output from analysis.py).
    If the file doesn't exist, we assume the analysis hasn't been run yet.
    """
    # Determine the likely path based on project structure
    # T019/T020 should have produced this file. We'll look in data/analysis/
    results_path = project_root / "data" / "analysis" / "correlation_results.csv"
    
    if not results_path.exists():
        # Fallback: try to find any CSV in data/analysis that might contain correlations
        analysis_dir = project_root / "data" / "analysis"
        if analysis_dir.exists():
            csv_files = list(analysis_dir.glob("*.csv"))
            if csv_files:
                # Take the first one as a heuristic
                results_path = csv_files[0]
                print(f"Warning: Expected correlation_results.csv not found. Using {results_path.name} instead.")
            else:
                raise FileNotFoundError(
                    f"No correlation results found in {project_root / 'data' / 'analysis'}. "
                    "Please run the analysis pipeline (T019/T020) first."
                )
        else:
            raise FileNotFoundError(
                f"Analysis directory {project_root / 'data' / 'analysis'} does not exist. "
                "Run the analysis pipeline first."
            )
    
    df = pd.read_csv(results_path)
    required_cols = ['channel', 'correlation', 'p_value']
    missing = [c for c in required_cols if c not in df.columns]
    if missing:
        raise ValueError(
            f"Correlation results missing required columns: {missing}. "
            f"Found columns: {list(df.columns)}"
        )
    return df

def run_sensitivity_analysis(results_df, thresholds=(0.05, 0.01)):
    """
    Perform sensitivity analysis at specified p-value thresholds.
    
    Args:
        results_df: DataFrame with columns ['channel', 'correlation', 'p_value']
        thresholds: Tuple of p-value thresholds to test (default: 0.05, 0.01)
        
    Returns:
        DataFrame with sensitivity analysis results
    """
    results = []
    
    for threshold in thresholds:
        significant = results_df[results_df['p_value'] <= threshold]
        non_significant = results_df[results_df['p_value'] > threshold]
        
        row = {
            'threshold': threshold,
            'total_tests': len(results_df),
            'significant_count': len(significant),
            'non_significant_count': len(non_significant),
            'significance_rate': len(significant) / len(results_df) if len(results_df) > 0 else 0.0,
            'mean_correlation_sig': significant['correlation'].mean() if len(significant) > 0 else np.nan,
            'mean_pvalue_sig': significant['p_value'].mean() if len(significant) > 0 else np.nan,
            'min_pvalue': results_df['p_value'].min() if len(results_df) > 0 else np.nan,
            'max_pvalue': results_df['p_value'].max() if len(results_df) > 0 else np.nan
        }
        results.append(row)
    
    return pd.DataFrame(results)

def generate_sensitivity_table(results_df, output_path):
    """
    Generate the sensitivity analysis table and save to CSV.
    
    Args:
        results_df: Correlation results DataFrame
        output_path: Path to save the sensitivity table
    """
    sensitivity_df = run_sensitivity_analysis(results_df)
    
    # Ensure output directory exists
    output_path = Path(output_path)
    output_path.parent.mkdir(parents=True, exist_ok=True)
    
    # Save to CSV
    sensitivity_df.to_csv(output_path, index=False)
    print(f"Sensitivity analysis table saved to: {output_path}")
    
    # Also print a summary to stdout
    print("\nSensitivity Analysis Summary:")
    print(sensitivity_df.to_string(index=False))
    
    return sensitivity_df

