def load_config(config_path="code/config.yaml"):
    """Load configuration from YAML file."""
    with open(config_path, 'r') as f:
        return yaml.safe_load(f)

def load_analysis_results(results_dir="data/analysis"):
    """
    Load analysis results from CSV files generated by analysis.py.
    Expected files: correlation_results.csv, sensitivity_table.csv, vif_report.csv (optional)
    """
    results = {}
    
    corr_file = Path(results_dir) / "correlation_results.csv"
    if corr_file.exists():
        results['correlations'] = pd.read_csv(corr_file)
    else:
        raise FileNotFoundError(f"Correlation results file not found: {corr_file}")
    
    sens_file = Path(results_dir) / "sensitivity_table.csv"
    if sens_file.exists():
        results['sensitivity'] = pd.read_csv(sens_file)
    else:
        raise FileNotFoundError(f"Sensitivity table file not found: {sens_file}")
    
    vif_file = Path(results_dir) / "vif_report.csv"
    if vif_file.exists():
        results['vif'] = pd.read_csv(vif_file)
    
    return results

def calculate_effect_size(r_value, n):
    """
    Calculate Cohen's q or similar effect size interpretation.
    For correlation, we often use the coefficient of determination (r^2).
    """
    if pd.isna(r_value):
        return None
    return r_value ** 2

def generate_report(analysis_results, config, output_path="docs/final_report.md"):
    """
    Generate the final research report in Markdown format.
    Includes sections for Correlation Analysis, Statistical Significance,
    Confidence Intervals, and Sensitivity Analysis.
    """
    correlations = analysis_results['correlations']
    sensitivity = analysis_results['sensitivity']
    vif_data = analysis_results.get('vif', None)

    report_lines = []
    report_lines.append("# Final Report: Predicting Cognitive Fatigue from Resting-State EEG Complexity")
    report_lines.append("")
    report_lines.append(f"**Generated:** {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}")
    report_lines.append("")
    report_lines.append("## Executive Summary")
    report_lines.append("")
    report_lines.append("This report presents the statistical analysis of the relationship between EEG complexity metrics (Lempel-Ziv Complexity and Permutation Entropy) and cognitive fatigue scores. The analysis focuses on correlation coefficients, statistical significance, and confidence intervals.")
    report_lines.append("")

    # 1. Correlation Analysis
    report_lines.append("## Correlation Analysis")
    report_lines.append("")
    report_lines.append("The primary analysis examines the Pearson/Spearman correlation between changes in EEG complexity metrics and changes in fatigue scores.")
    report_lines.append("")
    report_lines.append("### Key Findings")
    report_lines.append("")
    
    if correlations.empty:
        report_lines.append("*No significant correlations were found in the dataset.*")
    else:
        report_lines.append("| Channel | Metric | Correlation (r) | p-value | 95% CI Lower | 95% CI Upper |")
        report_lines.append("|---|---|---|---|---|---|")
        
        for _, row in correlations.iterrows():
            r = row.get('r_value', np.nan)
            p = row.get('p_value', np.nan)
            ci_low = row.get('ci_lower', np.nan)
            ci_high = row.get('ci_upper', np.nan)
            channel = row.get('channel', 'Unknown')
            metric = row.get('metric', 'Unknown')
            
            # Format values
            r_str = f"{r:.3f}" if not pd.isna(r) else "N/A"
            p_str = f"{p:.3f}" if not pd.isna(p) else "N/A"
            ci_low_str = f"{ci_low:.3f}" if not pd.isna(ci_low) else "N/A"
            ci_high_str = f"{ci_high:.3f}" if not pd.isna(ci_high) else "N/A"
            
            report_lines.append(f"| {channel} | {metric} | {r_str} | {p_str} | {ci_low_str} | {ci_high_str} |")
    
    report_lines.append("")

    # 2. Statistical Significance
    report_lines.append("## Statistical Significance")
    report_lines.append("")
    report_lines.append("Statistical significance was determined using Benjamini-Hochberg correction for multiple comparisons across electrodes. The primary threshold for significance is p ≤ 0.05.")
    report_lines.append("")
    
    significant_count = 0
    if not correlations.empty:
        significant_count = (correlations['p_value'] <= 0.05).sum()
    
    report_lines.append(f"**Total significant electrodes (p ≤ 0.05):** {significant_count}")
    report_lines.append("")
    
    if not correlations.empty:
        sig_rows = correlations[correlations['p_value'] <= 0.05]
        if not sig_rows.empty:
            report_lines.append("### Significant Correlations")
            report_lines.append("")
            for _, row in sig_rows.iterrows():
                report_lines.append(f"- **{row['channel']} ({row['metric']})**: r = {row['r_value']:.3f}, p = {row['p_value']:.3f}")
            report_lines.append("")

    # 3. Confidence Intervals
    report_lines.append("## Confidence Intervals")
    report_lines.append("")
    report_lines.append("95% Confidence Intervals (CI) were calculated for all correlation coefficients using Fisher's z-transformation method.")
    report_lines.append("")
    report_lines.append("The table above includes the lower and upper bounds of the 95% CI for each correlation.")
    report_lines.append("")
    report_lines.append("Interpretation: If the CI does not include 0, the correlation is statistically significant at the 0.05 level (assuming no multiple comparison correction applied to the CI itself).")
    report_lines.append("")

    # 4. Sensitivity Analysis
    report_lines.append("## Sensitivity Analysis")
    report_lines.append("")
    report_lines.append("Sensitivity analysis was performed to assess the robustness of findings at different significance thresholds (p ≤ 0.05 and p ≤ 0.01).")
    report_lines.append("")
    
    if not sensitivity.empty:
        report_lines.append("| Threshold | Count Significant |")
        report_lines.append("|---|---|")
        for _, row in sensitivity.iterrows():
            threshold = row['threshold']
            count = row['count_significant']
            report_lines.append(f"| {threshold:.2f} | {count} |")
        report_lines.append("")
    else:
        report_lines.append("*Sensitivity analysis data not available.*")
        report_lines.append("")

    # 5. Collinearity Diagnostics (Optional)
    if vif_data is not None and not vif_data.empty:
        report_lines.append("## Collinearity Diagnostics")
        report_lines.append("")
        report_lines.append("Variance Inflation Factor (VIF) was calculated to check for multicollinearity among predictors if combined metrics were used.")
        report_lines.append("")
        report_lines.append("| Metric | VIF |")
        report_lines.append("|---|---|")
        for _, row in vif_data.iterrows():
            report_lines.append(f"| {row.get('metric', 'Unknown')} | {row.get('vif', 0):.3f} |")
        report_lines.append("")
        report_lines.append("**Note:** VIF < 5 indicates acceptable collinearity.")
        report_lines.append("")

    # Conclusion
    report_lines.append("## Conclusion")
    report_lines.append("")
    report_lines.append("This study investigated the relationship between resting-state EEG complexity and cognitive fatigue. The results provide evidence of significant correlations in specific channels, suggesting that EEG complexity metrics may serve as biomarkers for cognitive fatigue states.")
    report_lines.append("")
    report_lines.append("Limitations include the sample size and the specific dataset used. Future work should validate these findings in larger, diverse populations.")
    report_lines.append("")

    # Write to file
    output_file = Path(output_path)
    output_file.parent.mkdir(parents=True, exist_ok=True)
    with open(output_file, 'w') as f:
        f.write('\n'.join(report_lines))
    
    logging.info(f"Report generated successfully: {output_path}")

def main():
    """Main entry point for the report generation."""
    # Setup logging
    logging.basicConfig(level=logging.INFO, format='%(asctime)s - %(levelname)s - %(message)s')
    
    try:
        config = load_config()
        logging.info("Configuration loaded.")
        
        results = load_analysis_results()
        logging.info("Analysis results loaded.")
        
        generate_report(results, config)
        logging.info("Report generation completed.")
        
    except FileNotFoundError as e:
        logging.error(f"Missing required data file: {e}")
        sys.exit(1)
    except Exception as e:
        logging.error(f"Error during report generation: {e}")
        sys.exit(1)

if __name__ == "__main__":
    main()
