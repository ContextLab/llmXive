"""
Prompts Module (T010)

Combines raw CodeXGLUE prompts (from T005) and handcrafted prompts (from T006)
into a single unified manifest (data/prompts/manifest.json) with source attribution
and checksums.
"""
import hashlib
import json
import os
import logging
from pathlib import Path
from typing import Dict, List, Any, Optional

# Import path constants from config
import config

logging.basicConfig(level=logging.INFO, format='%(asctime)s - %(levelname)s - %(message)s')
logger = logging.getLogger(__name__)

def calculate_file_hash(file_path: Path) -> str:
    """Calculate SHA-256 hash of a file."""
    sha256_hash = hashlib.sha256()
    try:
        with open(file_path, "rb") as f:
            for byte_block in iter(lambda: f.read(4096), b""):
                sha256_hash.update(byte_block)
        return sha256_hash.hexdigest()
    except FileNotFoundError:
        logger.error(f"File not found: {file_path}")
        raise
    except Exception as e:
        logger.error(f"Error calculating hash for {file_path}: {e}")
        raise

def calculate_content_hash(content: str) -> str:
    """Calculate SHA-256 hash of a string content."""
    return hashlib.sha256(content.encode('utf-8')).hexdigest()

def load_json_file(file_path: Path) -> Dict[str, Any]:
    """Load and parse a JSON file."""
    try:
        with open(file_path, 'r', encoding='utf-8') as f:
            return json.load(f)
    except FileNotFoundError:
        logger.error(f"Required file not found: {file_path}")
        raise
    except json.JSONDecodeError as e:
        logger.error(f"Invalid JSON in {file_path}: {e}")
        raise

def load_raw_manifest() -> List[Dict[str, Any]]:
    """
    Load the raw CodeXGLUE manifest generated by T005.
    Expected path: data/prompts/raw_manifest.json
    """
    path = config.DATA_DIR / "prompts" / "raw_manifest.json"
    if not path.exists():
        raise FileNotFoundError(
            f"raw_manifest.json not found at {path}. "
            "Ensure T005 (download.py) has been completed successfully."
        )
    
    data = load_json_file(path)
    # Handle potential wrapper structure
    if isinstance(data, dict) and 'prompts' in data:
        return data['prompts']
    elif isinstance(data, list):
        return data
    else:
        raise ValueError(f"Unexpected format in raw_manifest.json: {type(data)}")

def load_handcrafted_prompts() -> List[Dict[str, Any]]:
    """
    Load the handcrafted prompts generated by T006.
    Expected path: data/prompts/handcrafted.json
    """
    path = config.DATA_DIR / "prompts" / "handcrafted.json"
    if not path.exists():
        raise FileNotFoundError(
            f"handcrafted.json not found at {path}. "
            "Ensure T006 has been completed successfully."
        )
    
    data = load_json_file(path)
    # Handle potential wrapper structure
    if isinstance(data, dict) and 'prompts' in data:
        return data['prompts']
    elif isinstance(data, list):
        return data
    else:
        raise ValueError(f"Unexpected format in handcrafted.json: {type(data)}")

def generate_prompt_id(source: str, index: int) -> str:
    """Generate a unique prompt ID based on source and index."""
    return f"{source}_{index:03d}"

def build_unified_manifest(
    raw_prompts: List[Dict[str, Any]],
    handcrafted_prompts: List[Dict[str, Any]]
) -> Dict[str, Any]:
    """
    Combine raw and handcrafted prompts into a unified manifest.
    
    Returns a dictionary with:
    - metadata: generation timestamp, source counts, total count
    - prompts: list of unified prompt objects with source attribution
    """
    unified_prompts = []
    
    # Process raw CodeXGLUE prompts
    for idx, prompt in enumerate(raw_prompts):
        prompt_id = generate_prompt_id("codexglue", idx)
        content = prompt.get('prompt', prompt.get('text', ''))
        content_hash = calculate_content_hash(content)
        
        unified_entry = {
            "prompt_id": prompt_id,
            "source": "codexglue",
            "original_id": prompt.get('id', f"raw_{idx}"),
            "content": content,
            "content_hash": content_hash,
            "category": prompt.get('category', 'general'),
            "metadata": {
                "relevance_score": prompt.get('relevance_score', 0.0),
                "original_dataset_id": prompt.get('dataset_id', None)
            }
        }
        unified_prompts.append(unified_entry)
    
    # Process handcrafted prompts
    for idx, prompt in enumerate(handcrafted_prompts):
        prompt_id = generate_prompt_id("handcrafted", idx)
        content = prompt.get('prompt', prompt.get('text', ''))
        content_hash = calculate_content_hash(content)
        
        unified_entry = {
            "prompt_id": prompt_id,
            "source": "handcrafted",
            "original_id": prompt.get('id', f"hc_{idx}"),
            "content": content,
            "content_hash": content_hash,
            "category": prompt.get('category', 'security'),
            "metadata": {
                "subcategory": prompt.get('subcategory', 'general'),
                "complexity": prompt.get('complexity', 'medium')
            }
        }
        unified_prompts.append(unified_entry)
    
    # Build manifest structure
    manifest = {
        "metadata": {
            "version": "1.0",
            "generated_at": "T010_combined_manifest",
            "total_prompts": len(unified_prompts),
            "source_breakdown": {
                "codexglue": len(raw_prompts),
                "handcrafted": len(handcrafted_prompts)
            },
            "target_count": 30,  # As per amended spec (N=30)
            "actual_count": len(unified_prompts)
        },
        "prompts": unified_prompts
    }
    
    return manifest

def save_manifest(manifest: Dict[str, Any], output_path: Path) -> str:
    """
    Save the unified manifest to disk and return its hash.
    """
    output_path.parent.mkdir(parents=True, exist_ok=True)
    
    with open(output_path, 'w', encoding='utf-8') as f:
        json.dump(manifest, f, indent=2, ensure_ascii=False)
    
    file_hash = calculate_file_hash(output_path)
    logger.info(f"Manifest saved to {output_path} (hash: {file_hash[:16]}...)")
    return file_hash

def main():
    """
    Main entry point for T010: Combine prompts into unified manifest.
    
    Reads:
      - data/prompts/raw_manifest.json (from T005)
      - data/prompts/handcrafted.json (from T006)
    
    Writes:
      - data/prompts/manifest.json (final combined manifest)
    """
    logger.info("Starting T010: Combining prompts into unified manifest...")
    
    try:
        # Load inputs
        logger.info("Loading raw CodeXGLUE manifest...")
        raw_prompts = load_raw_manifest()
        logger.info(f"Loaded {len(raw_prompts)} raw prompts.")
        
        logger.info("Loading handcrafted prompts...")
        handcrafted_prompts = load_handcrafted_prompts()
        logger.info(f"Loaded {len(handcrafted_prompts)} handcrafted prompts.")
        
        # Validate counts against amended spec (N=30 total)
        expected_total = 30
        actual_total = len(raw_prompts) + len(handcrafted_prompts)
        
        if actual_total != expected_total:
            logger.warning(
                f"Prompt count mismatch: expected {expected_total}, got {actual_total}. "
                f"Raw: {len(raw_prompts)}, Handcrafted: {len(handcrafted_prompts)}."
            )
            # Continue anyway as this might be a partial run or deviation, 
            # but log the warning clearly.
        
        # Build unified manifest
        logger.info("Building unified manifest...")
        manifest = build_unified_manifest(raw_prompts, handcrafted_prompts)
        
        # Save output
        output_path = config.DATA_DIR / "prompts" / "manifest.json"
        manifest_hash = save_manifest(manifest, output_path)
        
        logger.info("T010 completed successfully.")
        return manifest_hash
        
    except FileNotFoundError as e:
        logger.error(f"Missing required input file: {e}")
        raise
    except Exception as e:
        logger.error(f"Error during T010 execution: {e}")
        raise

if __name__ == "__main__":
    main()
