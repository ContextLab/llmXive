"""
Contract tests for model results schema (US2).

These tests verify that the output files generated by the statistical modeling
pipeline conform to the schema defined in `contracts/model_results.schema.yaml`.

The tests load the actual JSON and CSV outputs produced by the pipeline and
assert that:
1. All required fields are present.
2. Data types match the schema expectations.
3. Statistical values (beta, p-value, etc.) are within valid ranges.
"""

import json
import os
import sys
import csv
from pathlib import Path
import pytest
import numpy as np
import pandas as pd

# Add project root to path for imports if running from tests/
PROJECT_ROOT = Path(__file__).resolve().parent.parent.parent
sys.path.insert(0, str(PROJECT_ROOT))

from code.config import get_project_root, ensure_directories
from code.config_env import get_processed_dir


def load_model_results_json() -> dict:
    """Load the model results JSON file."""
    processed_dir = get_processed_dir()
    file_path = processed_dir / "model_results.json"
    if not file_path.exists():
        pytest.fail(f"Model results JSON file not found at {file_path}. "
                    "Run the modeling pipeline (US2) to generate this file.")
    with open(file_path, 'r') as f:
        return json.load(f)


def load_model_results_csv() -> pd.DataFrame:
    """Load the model results summary CSV file."""
    processed_dir = get_processed_dir()
    file_path = processed_dir / "model_results_summary.csv"
    if not file_path.exists():
        pytest.fail(f"Model results summary CSV file not found at {file_path}. "
                    "Run the modeling pipeline (US2) to generate this file.")
    return pd.read_csv(file_path)


class TestModelResultsSchema:
    """Contract tests for the model results output schema."""

    def test_json_file_structure(self):
        """Verify the top-level structure of model_results.json."""
        data = load_model_results_json()

        # Expecting a list of results or a dict containing a list
        assert isinstance(data, list), "Expected model_results.json to be a list of model results."
        assert len(data) > 0, "Model results JSON is empty. No models were fitted."

    def test_required_fields_in_json(self):
        """Verify all required fields are present in each JSON record."""
        data = load_model_results_json()
        required_fields = [
            "subfield",
            "beta_ace",
            "ci_lower_95",
            "ci_upper_95",
            "p_value_uncorrected",
            "p_value_corrected",
            "method",
            "formula"
        ]

        for i, record in enumerate(data):
            missing = [f for f in required_fields if f not in record]
            assert not missing, f"Record {i} is missing required fields: {missing}"

    def test_data_types_and_ranges_json(self):
        """Verify data types and valid ranges for statistical values in JSON."""
        data = load_model_results_json()

        for i, record in enumerate(data):
            # Beta coefficients can be any float
            assert isinstance(record["beta_ace"], (int, float)), \
                f"Record {i}: beta_ace must be numeric"

            # Confidence intervals must be numeric
            assert isinstance(record["ci_lower_95"], (int, float)), \
                f"Record {i}: ci_lower_95 must be numeric"
            assert isinstance(record["ci_upper_95"], (int, float)), \
                f"Record {i}: ci_upper_95 must be numeric"

            # P-values must be between 0 and 1
            p_uncorr = record["p_value_uncorrected"]
            p_corr = record["p_value_corrected"]

            assert 0.0 <= p_uncorr <= 1.0, \
                f"Record {i}: p_value_uncorrected ({p_uncorr}) must be in [0, 1]"
            assert 0.0 <= p_corr <= 1.0, \
                f"Record {i}: p_value_corrected ({p_corr}) must be in [0, 1]"

            # Corrected p-value should typically be >= uncorrected (Bonferroni)
            # Allow small floating point tolerance
            assert p_corr >= p_uncorr - 1e-9, \
                f"Record {i}: Corrected p-value should be >= uncorrected p-value"

            # Check CI logic: lower < upper
            assert record["ci_lower_95"] <= record["ci_upper_95"], \
                f"Record {i}: CI lower bound ({record['ci_lower_95']}) must be <= upper bound ({record['ci_upper_95']})"

    def test_csv_file_structure(self):
        """Verify the structure of model_results_summary.csv."""
        df = load_model_results_csv()

        # Check columns exist
        expected_columns = [
            "subfield",
            "beta_ace",
            "ci_lower_95",
            "ci_upper_95",
            "p_value_uncorrected",
            "p_value_corrected",
            "significant_uncorrected",
            "significant_corrected"
        ]

        missing_cols = [c for c in expected_columns if c not in df.columns]
        assert not missing_cols, f"CSV missing columns: {missing_cols}"

    def test_csv_data_consistency(self):
        """Verify data consistency between CSV and JSON."""
        json_data = load_model_results_json()
        csv_df = load_model_results_csv()

        # Check row counts match (assuming 1:1 mapping for summary)
        # Note: JSON might have nested structures, but summary CSV should be flat
        # We compare the primary keys (subfield)
        json_subfields = {r["subfield"] for r in json_data}
        csv_subfields = set(csv_df["subfield"].unique())

        assert json_subfields == csv_subfields, \
            f"Subfield mismatch: JSON={json_subfields}, CSV={csv_subfields}"

    def test_bonferroni_threshold_application(self):
        """Verify that Bonferroni correction logic is reflected in the data."""
        # The schema expects corrected p-values. We check that the correction
        # was actually applied by ensuring the corrected p-value is not identical
        # to the uncorrected one (unless it's 1.0 or the correction factor is 1).
        # Given we have 3 main subfields + 1 ratio, n_tests = 4.
        # Correction factor = 0.05 / 4 = 0.0125.
        # So corrected = min(uncorrected * 4, 1.0).

        data = load_model_results_json()
        n_tests = 4  # CA3, DG, Subiculum, Ratio

        for record in data:
            p_uncorr = record["p_value_uncorrected"]
            p_corr = record["p_value_corrected"]

            expected_corr = min(p_uncorr * n_tests, 1.0)
            # Allow small floating point error
            assert abs(p_corr - expected_corr) < 1e-9, \
                f"Bonferroni correction mismatch for {record['subfield']}: " \
                f"Got {p_corr}, expected {expected_corr}"

    def test_subfield_names_validity(self):
        """Verify that subfield names match expected values."""
        data = load_model_results_json()
        valid_subfields = {"CA3", "DG", "Subiculum", "CA3:DG_Ratio"}

        for record in data:
            assert record["subfield"] in valid_subfields, \
                f"Invalid subfield name: {record['subfield']}. " \
                f"Must be one of {valid_subfields}"

    def test_formula_presence(self):
        """Verify that the model formula is recorded."""
        data = load_model_results_json()
        for record in data:
            assert "formula" in record and len(record["formula"]) > 0, \
                f"Record for {record['subfield']} is missing the model formula."
            # Basic check that formula contains expected terms
            assert "ACE_score" in record["formula"], \
                f"Formula for {record['subfield']} does not include ACE_score."