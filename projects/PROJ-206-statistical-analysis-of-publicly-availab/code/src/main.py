def check_sufficiency_gate() -> bool:
    """
    System-level blocking gate.
    Checks for data sufficiency flags generated by T013/T014.
    Returns True if data is sufficient to proceed, False otherwise.
    Halts execution with a clear error if checks fail.
    """
    configure_logging()
    
    # Ensure state directory exists
    STATE_DIR.mkdir(parents=True, exist_ok=True)

    LOGGER.info("Starting system-level data sufficiency check...")

    # 1. Check Global Poll Count (T014)
    # We re-run the check logic here to be safe, or read a flag.
    # Since T014 is a script that writes a flag, we verify the flag or re-evaluate.
    # For robustness, we re-evaluate the raw data if available.
    raw_data_path = DATA_DIR / "raw"
    if not raw_data_path.exists():
        LOGGER.error("Data sufficiency check failed: No raw data directory found.")
        return False

    # Re-run the global count check logic (similar to T014)
    # This ensures we don't rely solely on a potentially stale flag file
    # unless the flag file explicitly says "PASS".
    # However, the task says "check for data sufficiency flags (generated by T013/T014)".
    # So we look for the flag first.
    
    global_count_ok = False
    if GLOBAL_COUNT_FLAG_PATH.exists():
        try:
            import yaml
            with open(GLOBAL_COUNT_FLAG_PATH, 'r') as f:
                state = yaml.safe_load(f)
            if state.get("status") == "PASS" and state.get("check") == "global_poll_count":
                global_count_ok = True
                LOGGER.info("Global poll count check (T014) flag found: PASS.")
        except Exception as e:
            LOGGER.warning(f"Could not read global count flag: {e}. Re-evaluating...")
    
    if not global_count_ok:
        # Fallback: Re-run check if flag is missing or invalid
        # This mimics T014 logic
        total_polls = 0
        try:
            # Simple scan of raw data to count rows
            for csv_file in raw_data_path.glob("*.csv"):
                with open(csv_file, 'r') as f:
                    # Skip header
                    next(f, None)
                    total_polls += sum(1 for _ in f)
            
            if total_polls < 500:
                LOGGER.error(f"Global poll count check (T014) FAILED: Found {total_polls} polls. Required >= 500.")
                return False
            else:
                LOGGER.info(f"Global poll count check (T014) PASSED: Found {total_polls} polls.")
                global_count_ok = True
        except Exception as e:
            LOGGER.error(f"Error during global poll count verification: {e}")
            return False

    # 2. Check Data Sufficiency (T013)
    sufficiency_ok = False
    if SUFFICIENCY_FLAG_PATH.exists():
        try:
            import yaml
            with open(SUFFICIENCY_FLAG_PATH, 'r') as f:
                state = yaml.safe_load(f)
            if state.get("status") == "PASS" and state.get("check") == "data_sufficiency":
                sufficiency_ok = True
                LOGGER.info("Data sufficiency check (T013) flag found: PASS.")
        except Exception as e:
            LOGGER.warning(f"Could not read sufficiency flag: {e}. Re-evaluating...")

    if not sufficiency_ok:
        # Fallback: Re-run check logic from harmonize.py
        # We load the processed data (if it exists) or raw data to verify
        # T013 checks: <5 polls in 30 days preceding election OR <3 distinct cycles
        # Since we don't have the specific election date in this generic check,
        # we rely on the flag generated by T013 running on the specific election context.
        # If the flag is missing, we assume the previous step (T013) failed or didn't run.
        
        # Attempt to load processed data to verify sufficiency
        processed_file = PROCESSED_DIR / "poll_data_cleaned.csv"
        if not processed_file.exists():
            LOGGER.error("Data sufficiency check (T013) FAILED: Processed data file not found. T013 may not have run successfully.")
            return False

        try:
            import pandas as pd
            df = pd.read_csv(processed_file)
            
            # Basic heuristic check if specific election date is not known:
            # Ensure we have data. The specific T013 logic requires an election date context.
            # We assume T013 has already written the flag if it passed.
            # If we are here, the flag is missing. We cannot re-evaluate T013 without the election date.
            # Therefore, we treat missing flag as FAIL to be safe (fail loudly).
            LOGGER.error("Data sufficiency check (T013) FAILED: No sufficiency flag found. T013 must run first and generate a flag.")
            return False
            
        except Exception as e:
            LOGGER.error(f"Error verifying data sufficiency: {e}")
            return False

    LOGGER.info("All data sufficiency checks passed. Proceeding with pipeline.")
    return True

