# Constants for state file keys
DATA_SUFFICIENCY_FLAG_KEY = "data_sufficiency_check"
GLOBAL_POLL_COUNT_FLAG_KEY = "global_poll_count_check"
PIPELINE_STATUS_KEY = "pipeline_status"

def check_data_sufficiency_flags() -> bool:
    """
    System-level blocking gate.
    Checks state/project YAML for flags generated by T013 (data sufficiency)
    and T014 (global poll count).
    
    Returns:
        bool: True if all checks pass (data is sufficient), False otherwise.
    
    Raises:
        SystemExit: If checks fail, halting the pipeline.
    """
    state_path = get_state_path()
    project_id = "PROJ-206-statistical-analysis-of-publicly-availab"
    state_file = state_path / f"{project_id}.yaml"
    
    if not state_file.exists():
        logger.error(f"State file not found at {state_file}. Pipeline cannot proceed.")
        logger.error("Ensure T004 (Setup data directory structure) and T008 (State management) are completed.")
        return False

    try:
        state_data = load_state(state_file)
    except Exception as e:
        logger.error(f"Failed to load state file {state_file}: {e}")
        return False

    if not state_data:
        logger.error(f"State file {state_file} is empty or invalid.")
        return False

    # Check Data Sufficiency Flag (T013)
    sufficiency_status = state_data.get(DATA_SUFFICIENCY_FLAG_KEY, {})
    sufficiency_passed = sufficiency_status.get("passed", False)
    sufficiency_reason = sufficiency_status.get("reason", "Unknown reason")
    
    # Check Global Poll Count Flag (T014)
    count_status = state_data.get(GLOBAL_POLL_COUNT_FLAG_KEY, {})
    count_passed = count_status.get("passed", False)
    count_reason = count_status.get("reason", "Unknown reason")

    logger.info("--- Data Sufficiency Gate Check ---")
    
    if not sufficiency_passed:
        logger.error(f"DATA SUFFICIENCY CHECK FAILED: {sufficiency_reason}")
        logger.error("Pipeline halted. Insufficient recent polls or election cycles.")
        return False
    else:
        logger.info("Data Sufficiency Check: PASSED")

    if not count_passed:
        logger.error(f"GLOBAL POLL COUNT CHECK FAILED: {count_reason}")
        logger.error("Pipeline halted. Total poll count below minimum threshold.")
        return False
    else:
        logger.info("Global Poll Count Check: PASSED")

    logger.info("All data gates passed. Pipeline proceeding to downstream tasks.")
    return True

def run_pipeline():
    """
    Main entry point for the statistical analysis pipeline.
    Implements the blocking gate before executing downstream tasks (T016+).
    """
    setup_logging()
    logger.info("Starting Statistical Analysis Pipeline...")
    
    # 1. Execute Blocking Gate (T015)
    if not check_data_sufficiency_flags():
        logger.critical("Pipeline execution aborted due to data insufficiency.")
        # Update state to reflect halted status
        state_path = get_state_path()
        project_id = "PROJ-206-statistical-analysis-of-publicly-availab"
        state_file = state_path / f"{project_id}.yaml"
        
        if state_file.exists():
            state_data = load_state(state_file)
            state_data[PIPELINE_STATUS_KEY] = "HALTED: Data Insufficiency"
            save_state(state_data, state_file)
        
        sys.exit(1)

    # 2. Downstream Tasks (T016+) would go here
