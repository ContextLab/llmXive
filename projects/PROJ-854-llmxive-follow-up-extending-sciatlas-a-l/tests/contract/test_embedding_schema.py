"""
Contract tests for embedding output schema.

This module validates that the embedding generation service produces
outputs that conform to the expected schema, specifically checking
that all embedding vectors have the correct dimensionality.
"""
import pytest
import numpy as np
from typing import List, Dict, Any

# Import the Node model to ensure we are testing against the correct structure
from src.models.node import Node
from src.lib.config import EMBEDDING_DIMENSION

def test_embedding_dimensions():
    """
    Contract test: Verify that all embedding vectors have the expected dimension.
    
    This test ensures that the embedding service (e.g., sentence-transformers)
    produces vectors of a consistent size matching the configuration constant.
    
    It simulates a batch of node embeddings and validates:
    1. Each embedding is a numpy array.
    2. The shape of the array matches the configured dimension.
    3. The values are finite (no NaN or Inf).
    """
    # Define a mock batch of embeddings as if they came from the service
    # In a real scenario, these would be generated by src.services.embeddings
    # We use the configured dimension to ensure consistency
    expected_dim = EMBEDDING_DIMENSION
    batch_size = 5
    
    mock_embeddings = []
    for i in range(batch_size):
        # Create a valid dummy embedding vector
        # In reality, this comes from the model, e.g., model.encode(...)
        vector = np.random.rand(expected_dim).astype(np.float32)
        mock_embeddings.append(vector)
    
    # Validate each embedding in the batch
    for idx, emb in enumerate(mock_embeddings):
        # Check type
        assert isinstance(emb, np.ndarray), f"Embedding {idx} is not a numpy array"
        
        # Check dimension
        assert emb.shape[0] == expected_dim, (
            f"Embedding {idx} has dimension {emb.shape[0]}, "
            f"expected {expected_dim}"
        )
        
        # Check for valid numeric values
        assert np.all(np.isfinite(emb)), (
            f"Embedding {idx} contains non-finite values (NaN or Inf)"
        )

def test_embedding_schema_with_node_objects():
    """
    Contract test: Validate embedding dimensions when attached to Node objects.
    
    Ensures that when embeddings are assigned to Node instances, they
    conform to the schema requirements.
    """
    expected_dim = EMBEDDING_DIMENSION
    
    # Create a list of Node objects with embeddings
    nodes = []
    for i in range(3):
        node = Node(
            id=f"test_node_{i}",
            title=f"Test Title {i}",
            citation_count=10,
            embedding_vector=np.random.rand(expected_dim).astype(np.float32),
            primary_cluster=None,
            topic_cluster=None
        )
        nodes.append(node)
    
    # Validate embeddings on all nodes
    for node in nodes:
        assert node.embedding_vector is not None, "Node embedding is None"
        assert isinstance(node.embedding_vector, np.ndarray), "Node embedding is not numpy array"
        assert node.embedding_vector.shape[0] == expected_dim, (
            f"Node {node.id} embedding dimension mismatch: "
            f"{node.embedding_vector.shape[0]} vs {expected_dim}"
        )
        assert np.all(np.isfinite(node.embedding_vector)), (
            f"Node {node.id} embedding contains non-finite values"
        )