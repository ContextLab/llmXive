"""
Manifest Updater for User Story 1.

This module updates the results/manifest.json with fetch statuses and data checksums
derived from the raw and processed data files generated by T013 and T016.
"""
import os
import json
import hashlib
from pathlib import Path
from typing import Dict, Any, Optional

from src.utils.logging import get_logger
from src.utils.manifest import load_reproducibility_manifest, save_reproducibility_manifest

logger = get_logger(__name__)

# Define expected paths based on T013 and T016 specifications
RAW_DATA_DIR = Path("data/raw")
PROCESSED_DATA_DIR = Path("data/processed")
MANIFEST_PATH = Path("results/manifest.json")

# Expected filenames for US1 (arXiv and PubMed)
# T013 saves raw JSONL here
ARXIV_RAW_FILE = RAW_DATA_DIR / "arxiv_abstracts_raw.jsonl"
PUBMED_RAW_FILE = RAW_DATA_DIR / "pubmed_abstracts_raw.jsonl"

# T016 saves processed CSVs partitioned by window
# We expect files like: arxiv_2000_2004.csv, arxiv_2005_2009.csv, etc.
# We will scan the directory for these patterns.
PROCESSED_ARXIV_PATTERN = "arxiv_"
PROCESSED_PUBMED_PATTERN = "pubmed_"

def compute_file_checksum(filepath: Path) -> Optional[str]:
    """Compute SHA256 checksum of a file."""
    if not filepath.exists():
        return None
    
    sha256_hash = hashlib.sha256()
    try:
        with open(filepath, "rb") as f:
            for byte_block in iter(lambda: f.read(4096), b""):
                sha256_hash.update(byte_block)
        return sha256_hash.hexdigest()
    except Exception as e:
        logger.error(f"Failed to compute checksum for {filepath}: {e}")
        return None

def check_fetch_status(filepath: Path) -> str:
    """Check if a fetch file exists and has content."""
    if not filepath.exists():
        return "missing"
    
    try:
        if filepath.stat().st_size == 0:
            return "empty"
        return "success"
    except Exception as e:
        logger.error(f"Error checking status for {filepath}: {e}")
        return "error"

def gather_processed_checksums(data_dir: Path, prefix: str) -> Dict[str, str]:
    """Gather checksums for all processed files matching a prefix."""
    checksums = {}
    if not data_dir.exists():
        return checksums
    
    for file_path in data_dir.iterdir():
        if file_path.is_file() and file_path.name.startswith(prefix):
            checksum = compute_file_checksum(file_path)
            if checksum:
                checksums[file_path.name] = checksum
            else:
                checksums[file_path.name] = "compute_failed"
    
    return checksums

def update_manifest() -> Dict[str, Any]:
    """
    Updates the results/manifest.json with:
    - arxiv_fetch_status
    - pubmed_fetch_status
    - data_checksums (raw and processed)
    
    Returns the updated manifest content.
    """
    logger.info("Starting manifest update for US1 (T017)...")
    
    # Load existing manifest if it exists, otherwise create a new structure
    # T005 created the manifest module, T016 might have updated it partially.
    # We ensure we preserve existing data while updating US1 specific fields.
    manifest = {}
    if MANIFEST_PATH.exists():
        try:
            manifest = load_reproducibility_manifest(MANIFEST_PATH)
            logger.info(f"Loaded existing manifest from {MANIFEST_PATH}")
        except Exception as e:
            logger.warning(f"Could not load existing manifest, starting fresh: {e}")
            manifest = {"project": "PROJ-287-statistical-analysis-of-publicly-availab", "created_at": None}
    
    # 1. Update Fetch Statuses
    arxiv_status = check_fetch_status(ARXIV_RAW_FILE)
    pubmed_status = check_fetch_status(PUBMED_RAW_FILE)
    
    manifest["arxiv_fetch_status"] = arxiv_status
    manifest["pubmed_fetch_status"] = pubmed_status
    
    logger.info(f"ArXiv fetch status: {arxiv_status}")
    logger.info(f"PubMed fetch status: {pubmed_status}")

    # 2. Compute Raw Data Checksums
    raw_checksums = {}
    if ARXIV_RAW_FILE.exists():
        raw_checksums["arxiv_raw"] = compute_file_checksum(ARXIV_RAW_FILE)
    if PUBMED_RAW_FILE.exists():
        raw_checksums["pubmed_raw"] = compute_file_checksum(PUBMED_RAW_FILE)
    
    # 3. Compute Processed Data Checksums (Partitioned by window)
    processed_checksums = {
        "arxiv": gather_processed_checksums(PROCESSED_DATA_DIR, PROCESSED_ARXIV_PATTERN),
        "pubmed": gather_processed_checksums(PROCESSED_DATA_DIR, PROCESSED_PUBMED_PATTERN)
    }

    manifest["data_checksums"] = {
        "raw": raw_checksums,
        "processed": processed_checksums
    }

    # Save the updated manifest
    try:
        save_reproducibility_manifest(manifest, MANIFEST_PATH)
        logger.info(f"Successfully updated manifest at {MANIFEST_PATH}")
    except Exception as e:
        logger.error(f"Failed to save manifest: {e}")
        raise e

    return manifest

def main():
    """Entry point for the manifest updater."""
    update_manifest()
    logger.info("T017 Manifest update completed.")

if __name__ == "__main__":
    main()