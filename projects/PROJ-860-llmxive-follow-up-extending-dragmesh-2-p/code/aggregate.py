"""
Aggregate success rate data from evaluation logs into a CSV format.

This script collects logs generated by evaluate.py (containing 'object_id',
'policy_type', 'success', and other metrics) and consolidates them into a
single CSV file. It ensures that 'object_id' is preserved to maintain the
pairing structure required for paired statistical tests (e.g., paired t-test).

Expected input: JSONL or JSON log files from evaluation runs.
Output: CSV file with columns including object_id, policy_type, success, etc.
"""

import os
import sys
import json
import csv
import argparse
import logging
from pathlib import Path
from typing import List, Dict, Any, Optional

# Configure logging
logging.basicConfig(
    level=logging.INFO,
    format='%(asctime)s - %(levelname)s - %(message)s'
)
logger = logging.getLogger(__name__)

def find_log_files(data_dir: str) -> List[Path]:
    """
    Recursively find all JSON/JSONL log files in the data directory.
    """
    log_dir = Path(data_dir)
    if not log_dir.exists():
        raise FileNotFoundError(f"Evaluation data directory not found: {data_dir}")
    
    # Look for .json or .jsonl files
    files = list(log_dir.glob("**/*.json")) + list(log_dir.glob("**/*.jsonl"))
    if not files:
        logger.warning(f"No log files found in {data_dir}")
    return files

def parse_log_file(file_path: Path) -> List[Dict[str, Any]]:
    """
    Parse a single log file (JSON or JSONL) and return a list of records.
    """
    records = []
    try:
        with open(file_path, 'r', encoding='utf-8') as f:
            content = f.read().strip()
            
        if not content:
            logger.warning(f"Empty file: {file_path}")
            return records

        # Check if JSONL (one JSON object per line) or single JSON list
        if content.startswith('[') and content.endswith(']'):
            # Single JSON list
            try:
                data = json.loads(content)
                if isinstance(data, list):
                    records.extend(data)
                else:
                    records.append(data)
            except json.JSONDecodeError:
                logger.error(f"Failed to parse JSON list in {file_path}")
        else:
            # JSONL format
            for line_num, line in enumerate(content.splitlines(), 1):
                line = line.strip()
                if not line:
                    continue
                try:
                    records.append(json.loads(line))
                except json.JSONDecodeError:
                    logger.error(f"Failed to parse line {line_num} in {file_path}")
                    continue
    except Exception as e:
        logger.error(f"Error reading {file_path}: {e}")
    
    return records

def aggregate_logs(log_files: List[Path]) -> List[Dict[str, Any]]:
    """
    Aggregate all records from multiple log files.
    """
    all_records = []
    for file_path in log_files:
        logger.info(f"Processing {file_path}...")
        records = parse_log_file(file_path)
        if records:
            all_records.extend(records)
            logger.info(f"  Found {len(records)} records in {file_path.name}")
    
    logger.info(f"Total records aggregated: {len(all_records)}")
    return all_records

def validate_records(records: List[Dict[str, Any]]) -> bool:
    """
    Validate that records contain the required fields for pairing.
    """
    required_fields = ['object_id', 'policy_type', 'success']
    missing_fields = set()
    
    for i, record in enumerate(records):
        for field in required_fields:
            if field not in record:
                missing_fields.add(field)
    
    if missing_fields:
        logger.error(f"Records missing required fields: {missing_fields}")
        return False
    
    return True

def write_csv(records: List[Dict[str, Any]], output_path: Path) -> None:
    """
    Write aggregated records to a CSV file, preserving object_id for pairing.
    """
    if not records:
        logger.warning("No records to write to CSV")
        return

    # Determine all unique keys across all records to form CSV headers
    all_keys = set()
    for record in records:
        all_keys.update(record.keys())
    
    # Ensure required fields are prioritized in the header
    required_fields = ['object_id', 'policy_type', 'success', 'episode_id', 'duration']
    headers = [k for k in required_fields if k in all_keys]
    headers.extend([k for k in sorted(all_keys) if k not in headers])

    with open(output_path, 'w', newline='', encoding='utf-8') as csvfile:
        writer = csv.DictWriter(csvfile, fieldnames=headers)
        writer.writeheader()
        
        for record in records:
            # Ensure object_id is preserved as a string for consistency
            if 'object_id' in record and record['object_id'] is not None:
                record['object_id'] = str(record['object_id'])
            writer.writerow(record)

    logger.info(f"Wrote {len(records)} records to {output_path}")

def main():
    parser = argparse.ArgumentParser(
        description='Aggregate evaluation logs into a single CSV file for analysis.'
    )
    parser.add_argument(
        '--input-dir',
        type=str,
        default='data/evaluation',
        help='Directory containing evaluation log files (JSON/JSONL). Default: data/evaluation'
    )
    parser.add_argument(
        '--output',
        type=str,
        default='data/results/aggregated_results.csv',
        help='Output CSV file path. Default: data/results/aggregated_results.csv'
    )
    
    args = parser.parse_args()

    # Ensure output directory exists
    output_path = Path(args.output)
    output_path.parent.mkdir(parents=True, exist_ok=True)

    try:
        logger.info(f"Scanning for log files in {args.input_dir}...")
        log_files = find_log_files(args.input_dir)
        
        if not log_files:
            logger.error("No log files found to aggregate.")
            sys.exit(1)

        logger.info(f"Found {len(log_files)} log files.")
        
        records = aggregate_logs(log_files)
        
        if not records:
            logger.error("No valid records found in log files.")
            sys.exit(1)

        if not validate_records(records):
            logger.error("Validation failed: missing required fields for pairing.")
            sys.exit(1)

        write_csv(records, output_path)
        
        logger.info("Aggregation complete.")
        
    except Exception as e:
        logger.critical(f"Aggregation failed: {e}")
        sys.exit(1)

if __name__ == '__main__':
    main()