"""
T050: Run quickstart.md validation to ensure reproducibility.

This script validates the project setup by:
1. Checking all required directories exist.
2. Verifying requirements.txt is present and parseable.
3. Running a dry-run of the pipeline logic (import checks) without heavy computation.
4. Verifying that key data files (catalog) exist if generated by previous tasks.
"""
import os
import sys
import json
import argparse
from pathlib import Path

# Add code directory to path for imports
CODE_DIR = Path(__file__).parent
PROJECT_ROOT = CODE_DIR.parent

def check_directory(path_str: str, description: str) -> bool:
    path = PROJECT_ROOT / path_str
    exists = path.exists() and path.is_dir()
    status = "✓" if exists else "✗"
    print(f"  {status} {description}: {path_str}")
    if not exists:
        print(f"     ERROR: Directory missing or not a directory.")
    return exists

def check_file(path_str: str, description: str) -> bool:
    path = PROJECT_ROOT / path_str
    exists = path.exists()
    status = "✓" if exists else "✗"
    print(f"  {status} {description}: {path_str}")
    if not exists:
        print(f"     ERROR: File missing.")
    return exists

def validate_requirements() -> bool:
    req_file = PROJECT_ROOT / "requirements.txt"
    if not req_file.exists():
        print("  ✗ requirements.txt: Missing")
        return False
    
    try:
        with open(req_file, 'r') as f:
            content = f.read()
            # Basic check for key packages mentioned in T002a
            required_pkgs = ["pytest", "pandas", "scipy", "transformers", "datasets"]
            found = 0
            for pkg in required_pkgs:
                if pkg in content:
                    found += 1
            
            print(f"  ✓ requirements.txt: Found ({found}/{len(required_pkgs)} key packages)")
            return True
    except Exception as e:
        print(f"  ✗ requirements.txt: Error reading ({e})")
        return False

def validate_imports() -> bool:
    """Verify that core modules can be imported without syntax errors."""
    print("  Checking module imports...")
    modules_to_check = [
        "config", "utils", "dataset_loader", "test_transformer",
        "llm_generator", "coverage_runner", "main", "analyzer",
        "visualizer", "stratifier", "logger_config"
    ]
    
    all_ok = True
    for mod_name in modules_to_check:
        try:
            # We import from the code directory
            spec = __import__(f"code.{mod_name}")
            # Force reload to ensure no lazy load issues
            import importlib
            importlib.reload(spec)
            print(f"    ✓ code/{mod_name}.py")
        except Exception as e:
            print(f"    ✗ code/{mod_name}.py: {e}")
            all_ok = False
    return all_ok

def validate_data_artifacts() -> bool:
    """Check if expected data artifacts from previous tasks exist."""
    print("  Checking data artifacts...")
    
    # T006c: catalog.json
    catalog_path = PROJECT_ROOT / "data" / "benchmarks" / "processed" / "catalog.json"
    if catalog_path.exists():
        try:
            with open(catalog_path, 'r') as f:
                data = json.load(f)
                print(f"    ✓ catalog.json: Valid JSON with {len(data)} entries")
        except Exception as e:
            print(f"    ✗ catalog.json: Invalid JSON ({e})")
            return False
    else:
        print(f"    ⚠ catalog.json: Not found (Task T006c may not have run yet)")
    
    # T007: Tests directory
    tests_dir = PROJECT_ROOT / "data" / "benchmarks" / "processed" / "tests"
    if tests_dir.exists():
        count = len(list(tests_dir.glob("*.py")))
        print(f"    ✓ tests/ directory: {count} files")
    else:
        print(f"    ⚠ tests/ directory: Not found (Task T007 may not have run yet)")
    
    return True

def main():
    parser = argparse.ArgumentParser(description="Validate quickstart reproducibility (T050)")
    parser.add_argument("--strict", action="store_true", help="Fail on warnings")
    args = parser.parse_args()

    print("=== T050: Quickstart Validation ===")
    print(f"Project Root: {PROJECT_ROOT}")
    print()

    checks = []

    print("1. Directory Structure:")
    checks.append(check_directory("code", "Code directory"))
    checks.append(check_directory("data", "Data directory"))
    checks.append(check_directory("tests", "Tests directory"))
    checks.append(check_directory("data/benchmarks/raw", "Raw benchmarks"))
    checks.append(check_directory("data/benchmarks/processed", "Processed benchmarks"))
    checks.append(check_directory("data/coverage_reports", "Coverage reports"))
    checks.append(check_directory("outputs", "Outputs directory"))
    print()

    print("2. Configuration & Dependencies:")
    checks.append(check_file("requirements.txt", "Requirements file"))
    checks.append(validate_requirements())
    print()

    print("3. Module Integrity:")
    checks.append(validate_imports())
    print()

    print("4. Data Artifacts:")
    checks.append(validate_data_artifacts())
    print()

    all_passed = all(checks)
    
    if all_passed:
        print("=== VALIDATION PASSED ===")
        print("The project structure and core logic are reproducible.")
        return 0
    else:
        print("=== VALIDATION FAILED ===")
        if args.strict:
            print("Strict mode enabled: Failing due to missing components.")
            return 1
        else:
            print("Some checks failed or warnings exist. Review above.")
            return 1

if __name__ == "__main__":
    sys.exit(main())