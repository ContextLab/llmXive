"""
Test Transformer Module

Converts string-based test suites from MBPP and HumanEval datasets
into executable .py files in data/benchmarks/processed/tests/.
Handles missing test suites by logging warnings.
"""

import os
import json
import re
import logging
from typing import Dict, Any, List, Optional

from dataset_loader import validate_and_save_catalog

# Configure logging
logging.basicConfig(
    level=logging.INFO,
    format='%(asctime)s - %(levelname)s - %(message)s'
)
logger = logging.getLogger(__name__)

# Constants
TESTS_OUTPUT_DIR = "data/benchmarks/processed/tests"
CATALOG_PATH = "data/benchmarks/processed/catalog.json"

def ensure_output_dir():
    """Ensure the output directory exists."""
    os.makedirs(TESTS_OUTPUT_DIR, exist_ok=True)

def sanitize_filename(task_id: str) -> str:
    """
    Sanitize task_id to create a valid filename.
    Replaces slashes and other invalid characters with underscores.
    """
    # Replace '/' with '__' to handle HumanEval/0, MBPP/1, etc.
    sanitized = task_id.replace('/', '__')
    # Remove any other characters that aren't alphanumeric, underscore, or dash
    sanitized = re.sub(r'[^a-zA-Z0-9_-]', '_', sanitized)
    return sanitized

def extract_test_code(test_string: str) -> str:
    """
    Extract and clean test code from a string.
    
    Handles various formats:
    - Raw Python code
    - Code wrapped in markdown blocks
    - Code with surrounding text
    
    Returns the cleaned Python code string.
    """
    if not test_string or not isinstance(test_string, str):
        return ""
    
    # Strip whitespace
    test_string = test_string.strip()
    
    # Remove markdown code blocks if present
    if test_string.startswith("```python"):
        test_string = test_string[9:]
    if test_string.startswith("```"):
        test_string = test_string[3:]
    if test_string.endswith("```"):
        test_string = test_string[:-3]
    
    # Clean up any remaining leading/trailing whitespace
    return test_string.strip()

def transform_test_suite(task_id: str, test_string: str, dataset_source: str = "mbpp") -> Optional[str]:
    """
    Transform a test suite string into an executable Python file content.
    
    Args:
        task_id: The unique identifier for the task
        test_string: The string representation of the test suite
        dataset_source: Source dataset ('mbpp' or 'humaneval')
        
    Returns:
        The generated Python code string, or None if transformation fails
    """
    if not test_string or not test_string.strip():
        logger.warning(f"No test suite content for task {task_id} from {dataset_source}")
        return None
    
    # Extract clean code
    clean_code = extract_test_code(test_string)
    
    if not clean_code:
        logger.warning(f"Empty test suite after extraction for task {task_id}")
        return None
    
    # Create a wrapper script that includes the test code
    # This ensures the tests can be run with pytest
    sanitized_id = sanitize_filename(task_id)
    
    # Add a header comment
    header = f"# Auto-generated test file for {dataset_source}/{task_id}\n"
    header += "# Generated by test_transformer.py\n\n"
    
    # Combine header and code
    full_content = header + clean_code
    
    return full_content

def save_test_file(task_id: str, test_code: str, dataset_source: str = "mbpp") -> str:
    """
    Save the transformed test code to a file.
    
    Args:
        task_id: The unique identifier for the task
        test_code: The Python code string to save
        dataset_source: Source dataset ('mbpp' or 'humaneval')
        
    Returns:
        The path to the saved file
    """
    sanitized_id = sanitize_filename(task_id)
    filename = f"{sanitized_id}.py"
    filepath = os.path.join(TESTS_OUTPUT_DIR, filename)
    
    with open(filepath, 'w', encoding='utf-8') as f:
        f.write(test_code)
    
    logger.info(f"Saved test file for {task_id} to {filepath}")
    return filepath

def process_catalog_entry(entry: Dict[str, Any]) -> bool:
    """
    Process a single catalog entry to generate a test file.
    
    Args:
        entry: A dictionary containing task information from the catalog
        
    Returns:
        True if processing succeeded, False otherwise
    """
    task_id = entry.get('task_id')
    test_suite = entry.get('test_suite')
    dataset_source = entry.get('dataset_source', 'mbpp')
    
    if not task_id:
        logger.error("Missing task_id in catalog entry")
        return False
    
    if not test_suite:
        logger.warning(f"Missing test_suite for task {task_id}. Skipping test file generation.")
        return False
    
    try:
        test_code = transform_test_suite(task_id, test_suite, dataset_source)
        if test_code:
            save_test_file(task_id, test_code, dataset_source)
            return True
        else:
            logger.warning(f"Failed to transform test suite for task {task_id}")
            return False
    except Exception as e:
        logger.error(f"Error processing task {task_id}: {str(e)}")
        return False

def run_transformation():
    """
    Main function to transform all test suites in the catalog.
    """
    ensure_output_dir()
    
    if not os.path.exists(CATALOG_PATH):
        logger.error(f"Catalog file not found: {CATALOG_PATH}")
        logger.error("Please run T006c to generate the catalog first.")
        return False
    
    # Load catalog
    with open(CATALOG_PATH, 'r', encoding='utf-8') as f:
        catalog = json.load(f)
    
    entries = catalog.get('tasks', [])
    if not entries:
        logger.warning("No tasks found in catalog.")
        return False
    
    logger.info(f"Processing {len(entries)} tasks from catalog...")
    
    success_count = 0
    failure_count = 0
    skipped_count = 0
    
    for entry in entries:
        task_id = entry.get('task_id', 'unknown')
        
        if not entry.get('test_suite'):
            logger.warning(f"Skipping task {task_id}: no test suite available")
            skipped_count += 1
            continue
        
        success = process_catalog_entry(entry)
        if success:
            success_count += 1
        else:
            failure_count += 1
    
    logger.info(f"Transformation complete.")
    logger.info(f"  Success: {success_count}")
    logger.info(f"  Failed: {failure_count}")
    logger.info(f"  Skipped (missing test suite): {skipped_count}")
    
    return failure_count == 0

if __name__ == "__main__":
    success = run_transformation()
    exit(0 if success else 1)
