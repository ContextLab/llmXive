def test_significance_flag_logic(temp_coverage_dir):
    """Test that is_significant is correctly calculated based on p-value and threshold."""
    with tempfile.TemporaryDirectory() as tmpdir:
        output_path = Path(tmpdir) / "sensitivity_report.csv"

        run_sensitivity_analysis(temp_coverage_dir, output_path)

        with open(output_path, 'r', newline='') as f:
            reader = csv.DictReader(f)
            rows = list(reader)

            # Pick a row where we can verify logic (e.g., the one with p=0.05)
            # Since p-values are generated by the statistical test, we just verify
            # that the boolean logic is consistent: is_significant == (p_value < threshold)
            for row in rows:
                p_val = float(row["p_value"])
                thresh = float(row["threshold"])
                is_sig = row["is_significant"].lower() == "true"

                expected_sig = p_val < thresh
                assert is_sig == expected_sig, \
                    f"Inconsistency at threshold {thresh}: p={p_val}, expected {expected_sig}, got {is_sig}"
