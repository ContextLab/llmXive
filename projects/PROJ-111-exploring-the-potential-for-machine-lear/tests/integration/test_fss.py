    @pytest.fixture(autouse=True)
    def setup_config(self):
        """Ensure config is loaded."""
        self.config = get_config()
        self.data_dir = self.config.data_dir
        self.processed_dir = self.data_dir / "processed"
        self.results_dir = self.data_dir / "results"
        self.results_dir.mkdir(parents=True, exist_ok=True)

    def _load_and_preprocess_sample(self, lattice_size: int = 16, temperature: float = 1.5):
        """
        Helper to load raw data, normalize, and reshape for a specific L and T.
        This simulates the pipeline from T004 -> T005.
        """
        # Construct expected file path based on convention
        # Assuming data_generation.py saves files like: heisenberg_L{L}_T{T}.npy
        filename = f"heisenberg_L{lattice_size}_T{temperature}.npy"
        filepath = self.data_dir / "raw" / filename

        if not filepath.exists():
            pytest.skip(f"Data file not found: {filepath}. Please run data_generation.py first.")

        # Load raw data (Shape: [N, 3, L, L] or [N, L, L] depending on generation)
        # T004 generates raw spins. T005 normalizes and reshapes.
        # For this test, we assume raw data is [N, 3, L, L] (3 spin components)
        try:
            raw_data = np.load(filepath)
        except Exception as e:
            pytest.fail(f"Failed to load data: {e}")

        # Normalize and reshape (mimicking T005)
        normalized = normalize_spins(raw_data)
        reshaped = reshape_to_batch(normalized)

        return reshaped

    def _simulate_latent_representations(self, data: np.ndarray, batch_size: int = 32) -> Dict[str, np.ndarray]:
        """
        Simulate encoding data to latent space to get mu and logvar.
        In a real scenario, this would load a trained VAE (T020/T021).
        Here, we create a dummy VAE to ensure the pipeline works end-to-end.
        """
        # Create a dummy VAE model
        model = create_vae_model(input_channels=3, latent_dim=10, image_size=data.shape[-1])
        model.eval()

        # Create a temporary dataloader
        # We need a dataset wrapper
        from torch.utils.data import TensorDataset, DataLoader
        import torch
        
        tensor_data = torch.tensor(data, dtype=torch.float32)
        dataset = TensorDataset(tensor_data)
        loader = DataLoader(dataset, batch_size=batch_size, shuffle=False)

        all_mus = []
        all_logvars = []

        with torch.no_grad():
            for batch in loader:
                x = batch[0]
                mu, logvar = model.encode(x)
                all_mus.append(mu.numpy())
                all_logvars.append(logvar.numpy())

        mu_all = np.concatenate(all_mus, axis=0)
        logvar_all = np.concatenate(all_logvars, axis=0)

        return {"mu": mu_all, "logvar": logvar_all}

    def test_bootstrap_confidence_intervals(self):
        """
        Test that bootstrap resampling correctly estimates 95% CI for latent variance.
        """
        # 1. Load and preprocess data for a fixed L and T
        L = 16
        T = 1.5
        data = self._load_and_preprocess_sample(L, T)

        # 2. Get latent representations (simulated)
        latent = self._simulate_latent_representations(data)
        mu = latent["mu"]  # Shape: [N, D]

        # 3. Calculate variance per dimension and total variance
        var_per_dim = np.var(mu, axis=0)
        total_variance = np.sum(var_per_dim)

        # 4. Bootstrap resampling
        n_samples = mu.shape[0]
        n_bootstrap = 1000  # Reduced for speed, normally >5000
        bootstrap_variances = []

        rng = np.random.default_rng(42)
        for _ in range(n_bootstrap):
            indices = rng.integers(0, n_samples, size=n_samples)
            sample_mu = mu[indices]
            sample_var = np.sum(np.var(sample_mu, axis=0))
            bootstrap_variances.append(sample_var)

        bootstrap_variances = np.array(bootstrap_variances)

        # 5. Calculate 95% CI
        ci_lower = np.percentile(bootstrap_variances, 2.5)
        ci_upper = np.percentile(bootstrap_variances, 97.5)

        # Assertions
        assert ci_lower <= total_variance <= ci_upper, "True variance should be within 95% CI"
        assert ci_lower > 0, "Lower bound of CI should be positive"
        assert ci_upper > ci_lower, "Upper bound should be greater than lower bound"

        # Save results for inspection
        results = {
            "L": L,
            "T": T,
            "total_variance": total_variance,
            "ci_lower": ci_lower,
            "ci_upper": ci_upper,
            "bootstrap_samples": len(bootstrap_variances)
        }
        output_path = self.results_dir / "bootstrap_test_results.json"
        import json
        with open(output_path, 'w') as f:
            json.dump(results, f, indent=2)

        print(f"Bootstrap CI Test Passed. CI: [{ci_lower:.4f}, {ci_upper:.4f}]")

    def test_fss_extrapolation(self):
        """
        Test FSS extrapolation of T* to L -> infinity.
        Requires data at multiple L sizes (e.g., L=16, L=24).
        """
        lattice_sizes = [16, 24]
        temperatures = [1.0, 1.5, 2.0, 2.5]  # Sample temperatures
        
        # Dictionary to store peak variances per L
        # In a real run, we would find the T where Var is max for each L.
        # Here we simulate the peak detection by calculating variance at these Ts
        # and assuming the max is the pseudo-critical temperature.
        
        peak_data = {} # { L: (T_peak, variance_peak) }
        
        for L in lattice_sizes:
            variances = []
            temps = []
            
            for T in temperatures:
                try:
                    data = self._load_and_preprocess_sample(L, T)
                    latent = self._simulate_latent_representations(data)
                    mu = latent["mu"]
                    total_var = np.sum(np.var(mu, axis=0))
                    variances.append(total_var)
                    temps.append(T)
                except Exception:
                    continue
            
            if not variances:
                continue
                
            # Find pseudo-critical T (max variance)
            idx_max = np.argmax(variances)
            T_peak = temps[idx_max]
            Var_peak = variances[idx_max]
            peak_data[L] = (T_peak, Var_peak)
        
        # Check if we have data for at least two lattice sizes
        if len(peak_data) < 2:
            pytest.skip("Not enough lattice sizes with data to perform FSS.")
        
        # Prepare data for FSS
        # T*(L) = T_c + a * L^(-1/nu)
        # We assume nu = 1 (Ising/XY universality) as per T029 requirements
        nu = 1.0
        Ls = np.array(list(peak_data.keys()))
        T_peaks = np.array([peak_data[L][0] for L in Ls])
        
        # Perform FSS extrapolation
        # We use a simple linear regression on T vs L^(-1/nu)
        # y = T, x = L^(-1/nu), intercept = T_c
        x = Ls ** (-1.0 / nu)
        y = T_peaks
        
        # Fit line: y = mx + c => T_c = c
        # Using numpy polyfit
        coeffs = np.polyfit(x, y, 1)
        T_c_extrapolated = coeffs[1]  # Intercept
        slope = coeffs[0]
        
        # Assertions
        assert T_c_extrapolated is not None, "FSS extrapolation failed to produce T_c"
        assert isinstance(T_c_extrapolated, float), "T_c should be a float"
        
        # Sanity check: T_c should be within the range of observed T_peaks (roughly)
        # Or at least not wildly outside (e.g., < 0 or > 10)
        assert 0.0 < T_c_extrapolated < 10.0, f"Extrapolated T_c {T_c_extrapolated} is outside physical bounds"
        
        # Save results
        results = {
            "lattice_sizes": Ls.tolist(),
            "T_peaks": T_peaks.tolist(),
            "nu_assumed": nu,
            "T_c_extrapolated": float(T_c_extrapolated),
            "slope": float(slope)
        }
        output_path = self.results_dir / "fss_extrapolation_results.json"
        import json
        with open(output_path, 'w') as f:
            json.dump(results, f, indent=2)
        
        print(f"FSS Extrapolation Test Passed. T_c = {T_c_extrapolated:.4f}")

    def test_fss_with_autocorrelation_thinning(self):
        """
        Test that FSS pipeline correctly incorporates autocorrelation time calculation
        and dataset thinning before variance analysis.
        """
        L = 16
        T = 1.5
        
        # Load raw data
        filepath = self.data_dir / "raw" / f"heisenberg_L{L}_T{T}.npy"
        if not filepath.exists():
            pytest.skip(f"Data file not found: {filepath}")
        
        raw_data = np.load(filepath)
        
        # Simulate time series of an observable (e.g., energy or magnetization)
        # For this test, we compute magnetization per configuration as a proxy time series
        # Shape: [N, 3, L, L] -> [N] (magnitude of total magnetization)
        mag = np.linalg.norm(np.sum(raw_data, axis=(1, 2, 3)), axis=1)
        
        # Calculate autocorrelation time
        tau_int = calculate_autocorrelation_time(mag)
        
        # Thinning factor
        thin_factor = int(2 * tau_int) if tau_int > 0 else 1
        if thin_factor < 1: thin_factor = 1
        
        # Thin the dataset
        # We need to thin the raw data array
        indices = np.arange(0, len(raw_data), thin_factor)
        thin_data = raw_data[indices]
        
        # Verify thinning reduced the size appropriately
        assert len(thin_data) <= len(raw_data), "Thinning should reduce or keep size same"
        if thin_factor > 1:
            assert len(thin_data) < len(raw_data), "Thinning should reduce size if tau_int > 0.5"
        
        # Proceed with variance calculation on thinned data
        normalized = normalize_spins(thin_data)
        reshaped = reshape_to_batch(normalized)
        latent = self._simulate_latent_representations(reshaped)
        mu = latent["mu"]
        total_var = np.sum(np.var(mu, axis=0))
        
        # The test passes if we successfully computed variance on thinned data
        assert total_var >= 0, "Variance should be non-negative"
        
        results = {
            "L": L,
            "T": T,
            "tau_int": float(tau_int),
            "thin_factor": thin_factor,
            "original_size": len(raw_data),
            "thinned_size": len(thin_data),
            "variance": float(total_var)
        }
        output_path = self.results_dir / "thinning_test_results.json"
        import json
        with open(output_path, 'w') as f:
            json.dump(results, f, indent=2)

        print(f"Thinning Test Passed. tau_int={tau_int:.2f}, Factor={thin_factor}")