"""
Summary Table Generation for Coverage Rates.

This module implements Task T029: Generate a summary table listing coverage rates
for each (dataset, statistic, epsilon, noise_type) combination.

It reads the aggregated coverage results from artifacts/coverage_results.csv,
groups by the relevant dimensions, and calculates the mean coverage rate and
standard error for each unique combination.
"""

import pandas as pd
import numpy as np
import os
from pathlib import Path
from typing import Optional, List, Dict, Any

# Import existing utilities if needed, though this task is mostly pandas aggregation
from .validation import enforce_float64


def load_coverage_results(input_path: Optional[str] = None) -> pd.DataFrame:
    """
    Load the coverage results CSV generated by T015/T022.

    Args:
        input_path: Path to the coverage results CSV. Defaults to
                    'artifacts/coverage_results.csv' relative to project root.

    Returns:
        DataFrame containing coverage results.

    Raises:
        FileNotFoundError: If the input file does not exist.
    """
    if input_path is None:
        # Assume project root is the parent of 'code'
        base_dir = Path(__file__).parent.parent.parent
        input_path = base_dir / "artifacts" / "coverage_results.csv"
    else:
        input_path = Path(input_path)

    if not input_path.exists():
        raise FileNotFoundError(f"Coverage results file not found at: {input_path}")

    df = pd.read_csv(input_path)
    return df


def generate_summary_table(
    df: pd.DataFrame,
    group_cols: Optional[List[str]] = None,
    value_col: str = "deviation_from_nominal",
    coverage_col: str = "covered"
) -> pd.DataFrame:
    """
    Generate a summary table of coverage rates.

    Aggregates the raw coverage rows to produce a table showing the mean coverage
    rate and standard error for each unique combination of dataset, statistic,
    epsilon, and noise_type.

    Args:
        df: The raw coverage results DataFrame.
        group_cols: Columns to group by. Defaults to ['dataset', 'statistic',
                    'epsilon', 'noise_type'].
        value_col: Not directly used for aggregation if we calculate coverage manually,
                   but kept for API consistency.
        coverage_col: The column name representing the binary coverage outcome (0 or 1).

    Returns:
        DataFrame with aggregated coverage statistics.
    """
    if group_cols is None:
        group_cols = ['dataset', 'statistic', 'epsilon', 'noise_type']

    # Ensure numeric types for aggregation
    df = df.copy()
    df[coverage_col] = enforce_float64(df[coverage_col])
    
    # Group and aggregate
    summary = df.groupby(group_cols, dropna=False).agg(
        coverage_rate=(coverage_col, 'mean'),
        coverage_se=(coverage_col, lambda x: np.std(x, ddof=1) / np.sqrt(len(x))),
        n_samples=(coverage_col, 'count')
    ).reset_index()

    # Sort for readability
    summary = summary.sort_values(by=['dataset', 'statistic', 'noise_type', 'epsilon'])

    return summary


def save_summary_table(
    df: pd.DataFrame,
    output_path: Optional[str] = None
) -> str:
    """
    Save the summary table to a CSV file.

    Args:
        df: The summary DataFrame.
        output_path: Path to save the CSV. Defaults to
                     'artifacts/coverage_summary_table.csv'.

    Returns:
        The absolute path to the saved file.
    """
    if output_path is None:
        base_dir = Path(__file__).parent.parent.parent
        output_path = base_dir / "artifacts" / "coverage_summary_table.csv"
    else:
        output_path = Path(output_path)

    # Ensure output directory exists
    output_path.parent.mkdir(parents=True, exist_ok=True)

    df.to_csv(output_path, index=False)
    return str(output_path)


def main():
    """
    Main entry point to generate the summary table.

    Reads artifacts/coverage_results.csv, aggregates by (dataset, statistic,
    epsilon, noise_type), and writes artifacts/coverage_summary_table.csv.
    """
    print("Loading coverage results...")
    try:
        df = load_coverage_results()
    except FileNotFoundError as e:
        print(f"Error: {e}")
        print("Please ensure that T015/T022 has run and generated artifacts/coverage_results.csv")
        return

    print(f"Loaded {len(df)} rows from coverage results.")

    print("Generating summary table...")
    summary_df = generate_summary_table(df)

    print(f"Generated summary table with {len(summary_df)} unique combinations.")
    print(summary_df.head())

    output_path = save_summary_table(summary_df)
    print(f"Summary table saved to: {output_path}")


if __name__ == "__main__":
    main()