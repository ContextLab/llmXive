"""
Visualization module for T028: Coverage vs Epsilon plots.

Generates line plots of empirical coverage probability against privacy budget (epsilon)
with error bars representing standard error (SE) for both Laplace and Gaussian noise.

Inputs:
    artifacts/coverage_results.csv (generated by T015/T022)
    
Outputs:
    figures/coverage_vs_epsilon.png
"""
import os
import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
import seaborn as sns
from pathlib import Path
from typing import Optional, List, Dict, Any
import warnings

# Import config to get paths
import sys
sys.path.insert(0, str(Path(__file__).resolve().parent.parent))
import config

# Setup style
sns.set_theme(style="whitegrid", context="talk")
plt.rcParams['font.size'] = 12
plt.rcParams['axes.grid'] = True
plt.rcParams['axes.axisbelow'] = True

def load_coverage_data(input_path: Optional[str] = None) -> pd.DataFrame:
    """
    Load the coverage results CSV generated by T015/T022.
    
    Args:
        input_path: Optional path override. Defaults to config.COVERAGE_RESULTS_PATH.
        
    Returns:
        DataFrame with coverage results.
    """
    if input_path is None:
        # Construct path relative to project root
        project_root = Path(__file__).resolve().parent.parent.parent
        input_path = project_root / "artifacts" / "coverage_results.csv"
    
    if not os.path.exists(input_path):
        raise FileNotFoundError(f"Coverage results file not found at {input_path}. "
                              "Run T015/T022 aggregation first.")
    
    df = pd.read_csv(input_path)
    
    # Ensure required columns exist
    required_cols = ['dataset', 'epsilon', 'noise_type', 'covered', 'statistic']
    missing = [c for c in required_cols if c not in df.columns]
    if missing:
        raise ValueError(f"Missing required columns in coverage results: {missing}")
    
    return df

def aggregate_coverage_stats(df: pd.DataFrame) -> pd.DataFrame:
    """
    Aggregate coverage data by dataset, epsilon, noise_type, and statistic.
    Calculates mean coverage and standard error (SE).
    
    Args:
        df: Raw coverage results DataFrame.
        
    Returns:
        DataFrame with aggregated stats: mean_coverage, se_coverage, n_samples.
    """
    # Convert covered to numeric if needed (it should be boolean or 0/1)
    if df['covered'].dtype == bool:
        df['covered_numeric'] = df['covered'].astype(int)
    else:
        df['covered_numeric'] = pd.to_numeric(df['covered'], errors='coerce')
    
    # Group by relevant dimensions
    grouped = df.groupby(['dataset', 'epsilon', 'noise_type', 'statistic']).agg(
        mean_coverage=('covered_numeric', 'mean'),
        se_coverage=('covered_numeric', lambda x: x.std() / np.sqrt(len(x))),
        n_samples=('covered_numeric', 'count')
    ).reset_index()
    
    return grouped

def plot_coverage_vs_epsilon(
    df: pd.DataFrame,
    output_path: Optional[str] = None,
    datasets: Optional[List[str]] = None,
    statistics: Optional[List[str]] = None,
    noise_types: Optional[List[str]] = None,
    figsize: tuple = (12, 8),
    title: str = "Empirical Coverage Probability vs Privacy Budget (ε)"
) -> None:
    """
    Generate line plots of coverage vs epsilon with error bars.
    
    Creates a multi-panel figure if multiple datasets/statistics are present,
    or a single plot with multiple lines if filtering to one dataset/statistic.
    
    Args:
        df: Aggregated coverage statistics DataFrame.
        output_path: Path to save the figure. Defaults to config.FIGURES_PATH/coverage_vs_epsilon.png.
        datasets: Filter to specific datasets. If None, uses all.
        statistics: Filter to specific statistics. If None, uses all.
        noise_types: Filter to specific noise types. If None, uses all.
        figsize: Figure size (width, height) in inches.
        title: Plot title.
    """
    # Filter data
    plot_df = df.copy()
    if datasets:
        plot_df = plot_df[plot_df['dataset'].isin(datasets)]
    if statistics:
        plot_df = plot_df[plot_df['statistic'].isin(statistics)]
    if noise_types:
        plot_df = plot_df[plot_df['noise_type'].isin(noise_types)]
    
    if plot_df.empty:
        raise ValueError("No data to plot after filtering. Check dataset/statistic/noise_type filters.")
    
    # Determine if we need subplots
    n_datasets = plot_df['dataset'].nunique()
    n_stats = plot_df['statistic'].nunique()
    need_subplots = (n_datasets > 1 or n_stats > 1)
    
    # Create figure
    if need_subplots:
        fig, axes = plt.subplots(
            nrows=n_datasets,
            ncols=n_stats,
            figsize=(figsize[0] * n_stats, figsize[1] * n_datasets),
            sharex=True,
            sharey=True
        )
        
        # Handle single row/column cases for indexing
        if n_datasets == 1 and n_stats == 1:
            axes = np.array([[axes]])
        elif n_datasets == 1:
            axes = axes.reshape(1, -1)
        elif n_stats == 1:
            axes = axes.reshape(-1, 1)
        
        axes_flat = axes.flatten()
    else:
        fig, ax = plt.subplots(figsize=figsize)
        axes_flat = [ax]
    
    # Define color palette for noise types
    palette = {
        'Laplace': '#1f77b4',  # Blue
        'Gaussian': '#ff7f0e'  # Orange
    }
    
    # Plot each dataset/statistic combination
    for idx, (dataset, stat) in enumerate(plot_df.groupby(['dataset', 'statistic']).groups.keys()):
        if need_subplots:
            ax = axes_flat[idx]
        else:
            ax = axes_flat[0]
        
        # Filter for this dataset and statistic
        subset = plot_df[(plot_df['dataset'] == dataset) & (plot_df['statistic'] == stat)]
        
        # Sort by epsilon for line plotting
        subset = subset.sort_values('epsilon')
        
        # Plot each noise type
        for noise_type in subset['noise_type'].unique():
            noise_subset = subset[subset['noise_type'] == noise_type]
            noise_subset = noise_subset.sort_values('epsilon')
            
            color = palette.get(noise_type, 'gray')
            label = f"{noise_type} Noise"
            
            ax.errorbar(
                noise_subset['epsilon'],
                noise_subset['mean_coverage'],
                yerr=noise_subset['se_coverage'],
                fmt='-o',
                capsize=5,
                label=label,
                color=color,
                linewidth=2,
                markersize=6,
                alpha=0.8
            )
        
        # Add nominal coverage line (0.95)
        ax.axhline(y=0.95, color='green', linestyle='--', linewidth=1.5, alpha=0.7, label='Nominal (0.95)')
        
        # Add labels and title
        if need_subplots:
            ax.set_title(f"{dataset}\n{stat.capitalize()}", fontsize=14, fontweight='bold')
        else:
            ax.set_title(f"{dataset} - {stat.capitalize()}", fontsize=16, fontweight='bold')
        
        ax.set_xlabel("Privacy Budget (ε)", fontsize=12)
        ax.set_ylabel("Empirical Coverage Probability", fontsize=12)
        ax.set_ylim(0.85, 1.0)  # Focus on the region of interest
        ax.legend(loc='lower right', fontsize=10)
        ax.grid(True, alpha=0.3)
    
    # Overall title if subplots
    if need_subplots:
        fig.suptitle(title, fontsize=16, fontweight='bold', y=1.02)
    else:
        ax.set_title(title, fontsize=16, fontweight='bold')
    
    # Adjust layout
    plt.tight_layout()
    
    # Save figure
    if output_path is None:
        project_root = Path(__file__).resolve().parent.parent.parent
        output_path = project_root / "figures" / "coverage_vs_epsilon.png"
    
    # Ensure output directory exists
    os.makedirs(os.path.dirname(output_path), exist_ok=True)
    
    plt.savefig(output_path, dpi=300, bbox_inches='tight')
    plt.close()
    
    print(f"Plot saved to: {output_path}")

def main():
    """
    Main entry point for T028: Generate coverage vs epsilon visualization.
    
    This function loads the aggregated coverage results, computes statistics,
    and generates the required line plots with error bars.
    """
    print("Starting T028: Visualization of Coverage vs Epsilon...")
    
    try:
        # Load data
        print("Loading coverage results...")
        raw_df = load_coverage_data()
        
        # Aggregate statistics
        print("Aggregating coverage statistics...")
        agg_df = aggregate_coverage_stats(raw_df)
        
        # Generate plot
        print("Generating plots...")
        plot_coverage_vs_epsilon(
            df=agg_df,
            title="Robustness of Confidence Intervals to Differential Privacy Noise"
        )
        
        print("T028 completed successfully.")
        
    except FileNotFoundError as e:
        print(f"ERROR: {e}")
        print("Ensure T015/T022 have been run to generate artifacts/coverage_results.csv")
        raise
    except Exception as e:
        print(f"ERROR during plotting: {e}")
        raise

if __name__ == "__main__":
    main()