"""
detect_llm_code.py
------------------

This script scans the raw PR JSON files produced by the extraction stage
(stored in ``data/raw``) and applies a lightweight heuristic to flag code
fragments that appear to have been generated by a Large Language Model (LLM).

The heuristic looks for common markers inside the diff such as
``generated by``, ``ai‑generated``, ``copilot``, etc.  When a marker is found
the script records a detection entry with a high confidence level; otherwise
the entry is recorded with low confidence.

The results are written to ``data/derived/llm_detections.json`` as a JSON
list where each element follows the ``LLMCodeDetectionResult`` schema defined
in ``src/detection/schema.py``.  The script can be executed directly:

    python code/src/detection/detect_llm_code.py

It will create any missing directories, perform the detection, and emit the
output file.
"""

import json
from pathlib import Path
from typing import List, Dict

# Project utilities
from config.settings import get_paths, ensure_directories

# Schema definitions – imported for type‑checking / future extensibility
from src.detection.schema import LLMCodeDetectionResult, ConfidenceLevel

__all__ = ["detect_llm_generated_code", "main"]


def _heuristic_markers() -> List[str]:
    """
    Common textual markers that suggest a diff chunk was generated by an LLM.

    The list is intentionally short and conservative to avoid false positives.
    """
    return [
        "generated by",
        "ai‑generated",
        "ai generated",
        "llm generated",
        "copilot",
        "codegen",
    ]


def _contains_llm_marker(diff_text: str) -> bool:
    """
    Return ``True`` if any of the heuristic markers appear in ``diff_text``.
    The check is case‑insensitive.
    """
    lowered = diff_text.lower()
    return any(marker in lowered for marker in _heuristic_markers())


def _extract_file_paths_from_diff(diff_text: str) -> List[str]:
    """
    Very naive extraction of file paths from a unified diff.  It looks for
    lines that start with ``--- a/`` or ``+++ b/`` and returns the path
    portion after the prefix.
    """
    paths: List[str] = []
    for line in diff_text.splitlines():
        if line.startswith("--- a/"):
            paths.append(line[6:])
        elif line.startswith("+++ b/"):
            paths.append(line[6:])
    # Deduplicate while preserving order
    seen = set()
    unique = []
    for p in paths:
        if p not in seen:
            seen.add(p)
            unique.append(p)
    return unique


def detect_llm_generated_code(raw_dir: Path, derived_dir: Path) -> List[Dict]:
    """
    Scan all ``*.json`` files in ``raw_dir`` and produce a list of detection
    dictionaries.

    Parameters
    ----------
    raw_dir: Path
        Directory containing the raw PR JSON files created by the extraction
        pipeline (e.g. ``data/raw``).
    derived_dir: Path
        Directory where the resulting ``llm_detections.json`` will be stored
        (e.g. ``data/derived``).

    Returns
    -------
    List[Dict]
        A list of dictionaries ready to be serialised as JSON.  Each dict
        conforms to the ``LLMCodeDetectionResult`` schema.
    """
    detections: List[Dict] = []

    for json_file in raw_dir.glob("*.json"):
        try:
            with json_file.open("r", encoding="utf-8") as f:
                pr_data = json.load(f)
        except json.JSONDecodeError:
            # Skip malformed files but continue processing the rest.
            continue

        # The extraction stage stores the PR identifier under the key ``id``.
        # If the key is missing we fall back to the filename (without suffix).
        pr_id = pr_data.get("id") or json_file.stem

        # The diff may be stored under a top‑level ``diff`` key or, for more
        # complex structures, under ``files`` where each entry has its own
        # ``patch`` field.  We handle both cases.
        diff_texts: List[str] = []

        if "diff" in pr_data and isinstance(pr_data["diff"], str):
            diff_texts.append(pr_data["diff"])

        if "files" in pr_data and isinstance(pr_data["files"], list):
            for file_entry in pr_data["files"]:
                patch = file_entry.get("patch")
                if isinstance(patch, str):
                    diff_texts.append(patch)

        # If no diff information is present we still emit a detection entry
        # with ``llm_code_flag`` set to ``False``.
        if not diff_texts:
            detection = {
                "pr_id": pr_id,
                "file_path": None,
                "llm_code_flag": False,
                "confidence": ConfidenceLevel.LOW.name,
                "description": "No diff information available.",
            }
            detections.append(detection)
            continue

        # Process each diff chunk independently.
        for diff_text in diff_texts:
            flag = _contains_llm_marker(diff_text)
            confidence = (
                ConfidenceLevel.HIGH if flag else ConfidenceLevel.LOW
            )
            file_paths = _extract_file_paths_from_diff(diff_text) or [None]

            for file_path in file_paths:
                detection = {
                    "pr_id": pr_id,
                    "file_path": file_path,
                    "llm_code_flag": flag,
                    "confidence": confidence.name,
                    "description": (
                        "Heuristic marker detected."
                        if flag
                        else "No heuristic marker found."
                    ),
                }
                detections.append(detection)

    # Write the aggregated detections to the derived directory.
    output_path = derived_dir / "llm_detections.json"
    derived_dir.mkdir(parents=True, exist_ok=True)
    with output_path.open("w", encoding="utf-8") as out_f:
        json.dump(detections, out_f, indent=2, ensure_ascii=False)

    return detections


def main() -> None:
    """
    Entry point used by the CI / execution harness.  It resolves the project
    paths via ``config.settings`` and runs the detection routine.
    """
    # Resolve configured directories.
    paths = get_paths()
    raw_dir = Path(paths.get("raw", "data/raw"))
    derived_dir = Path(paths.get("derived", "data/derived"))

    # Ensure the directories exist (no‑op if they already do).
    ensure_directories([raw_dir, derived_dir])

    # Perform detection and report a short summary.
    detections = detect_llm_generated_code(raw_dir, derived_dir)
    print(
        f"LLM code detection complete – {len(detections)} entries written to "
        f"{derived_dir / 'llm_detections.json'}"
    )


if __name__ == "__main__":
    main()