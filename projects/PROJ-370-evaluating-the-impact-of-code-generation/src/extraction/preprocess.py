def estimate_tokens(text: str) -> int:
    """Rough estimate of tokens (1 token ~= 4 chars)."""
    return len(text) // 4

def truncate_diff(diff_text: str, max_tokens: int = 8000) -> str:
    """Truncate diff text if it exceeds token limit."""
    if estimate_tokens(diff_text) <= max_tokens:
        return diff_text
    # Simple truncation strategy: keep start and end
    # In a real scenario, we might want to be smarter about keeping context
    ratio = max_tokens / estimate_tokens(diff_text)
    cut_length = int(len(diff_text) * (1 - ratio) / 2)
    return diff_text[:cut_length] + "\n... [TRUNCATED] ...\n" + diff_text[-cut_length:]

def extract_raw_comments(pr_data: List[Dict[str, Any]]) -> List[Dict[str, Any]]:
    """
    Extract raw review comments from PR data.
    
    Args:
        pr_data: List of PR dictionaries containing 'comments' or 'review_comments'
    
    Returns:
        List of comment dictionaries with metadata (pr_id, author, body, created_at, etc.)
    """
    raw_comments = []
    
    for pr in pr_data:
        pr_id = pr.get("number")
        pr_title = pr.get("title", "No Title")
        
        # Extract standard review comments
        comments = pr.get("review_comments", [])
        if not comments and "comments" in pr:
            comments = pr.get("comments", [])
        
        for comment in comments:
            comment_entry = {
                "pr_id": pr_id,
                "pr_title": pr_title,
                "author": comment.get("user", {}).get("login", "unknown"),
                "body": comment.get("body", ""),
                "created_at": comment.get("created_at", ""),
                "updated_at": comment.get("updated_at", ""),
                "path": comment.get("path", "root"),
                "position": comment.get("position"),
                "original_position": comment.get("original_position"),
                "commit_id": comment.get("commit_id", ""),
                "line": comment.get("line"),
                "side": comment.get("side", "RIGHT"),
                "start_line": comment.get("start_line"),
                "start_side": comment.get("start_side"),
                "in_reply_to_id": comment.get("in_reply_to_id"),
                "reactions": comment.get("reactions", {}),
                "is_raw": True  # Flag indicating this is raw, not ground truth
            }
            raw_comments.append(comment_entry)
        
        # Also extract general PR comments if needed (optional, depending on spec)
        # For now, we focus on review_comments as per "review comments"
    
    return raw_comments

