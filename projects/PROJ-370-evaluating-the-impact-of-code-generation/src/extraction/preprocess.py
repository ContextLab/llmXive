def preprocess_pr_data(pr_data: List[Dict[str, Any]], max_tokens: int = 8000) -> List[Dict[str, Any]]:
    """
    Preprocess PR data: truncate diffs, clean metadata.
    """
    processed = []
    for pr in pr_data:
        new_pr = pr.copy()
        if "diff" in new_pr:
            new_pr["diff"] = truncate_diff(new_pr["diff"], max_tokens)
        processed.append(new_pr)
    return processed

def generate_checksums(file_paths: List[str]) -> Dict[str, str]:
    """Generate SHA-256 checksums for a list of files."""
    checksums = {}
    for fp in file_paths:
        path = Path(fp)
        if path.exists():
            with open(path, "rb") as f:
                checksums[str(path)] = hashlib.sha256(f.read()).hexdigest()
    return checksums

def main():
    """
    Main entry point for T014: Extract raw review comments.
    1. Ensure directories exist.
    2. Load raw PR data from data/raw/ (assuming it was generated by T012).
    3. Extract comments.
    4. Save to data/annotations/raw_comments.json.
    """
    ensure_directories()
    paths = get_paths()
    
    # Determine input file (T012 output)
    # We assume T012 creates a consolidated file or we iterate files
    raw_dir = Path(paths["data_raw"])
    annotation_dir = Path(paths["data_annotations"])
    
    if not raw_dir.exists():
        logger.error(f"Raw data directory {raw_dir} does not exist. Run T012 first.")
        return

    # Collect all JSON files from data/raw/
    pr_files = list(raw_dir.glob("*.json"))
    if not pr_files:
        # Fallback: check if there's a specific file name pattern or just exit
        logger.warning("No JSON files found in data/raw/. Exiting.")
        return

    all_pr_data = []
    for p_file in pr_files:
        with open(p_file, "r", encoding="utf-8") as f:
            try:
                data = json.load(f)
                # Handle if it's a list or a dict with a 'prs' key
                if isinstance(data, list):
                    all_pr_data.extend(data)
                elif isinstance(data, dict):
                    if "prs" in data:
                        all_pr_data.extend(data["prs"])
                    else:
                        all_pr_data.append(data)
            except json.JSONDecodeError as e:
                logger.error(f"Failed to parse {p_file}: {e}")
    
    logger.info(f"Loaded {len(all_pr_data)} PRs from {len(pr_files)} files.")
    
    # Extract raw comments
    raw_comments = extract_raw_comments(all_pr_data)
    logger.info(f"Extracted {len(raw_comments)} raw review comments.")
    
    # Save output
    output_path = annotation_dir / "raw_comments.json"
    with open(output_path, "w", encoding="utf-8") as f:
        json.dump(raw_comments, f, indent=2, ensure_ascii=False)
    
    logger.info(f"Saved raw comments to {output_path}")

if __name__ == "__main__":
    main()