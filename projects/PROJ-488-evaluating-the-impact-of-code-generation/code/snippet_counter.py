"""
T019: Verify snippet count for US1.
Ensures >= 1000 valid Python snippets per group (human-written and LLM-generated).
Aborts with error 104 if the threshold is not met.
"""
import json
import sys
import logging
from pathlib import Path

# Import project utilities
from logging_config import setup_logger, get_logger
from data_model import CodeSnippet, DatasetGroup
from data_ingestion import filter_python_snippets, extract_top_level_functions
from ast_validation import run_ast_validation

# Constants
MIN_SNIPPETS_PER_GROUP = 1000
ERROR_104_MSG = "Error 104: Insufficient valid Python snippets. Required >= {req} per group, found {human} (human) and {llm} (LLM)."
DATA_DIR = Path("data")
PROCESSED_DIR = DATA_DIR / "processed"
VALIDATED_DIR = PROCESSED_DIR / "validated"

def load_processed_snippets(source_name: str) -> list:
    """Load processed snippets from the JSONL file generated by previous steps."""
    file_path = PROCESSED_DIR / f"{source_name}_filtered.jsonl"
    if not file_path.exists():
        raise FileNotFoundError(f"Processed file not found: {file_path}. Run T016-T018 first.")
    
    snippets = []
    with open(file_path, 'r', encoding='utf-8') as f:
        for line in f:
            if line.strip():
                data = json.loads(line)
                # Reconstruct basic object if needed, though dict is sufficient for counting
                snippets.append(data)
    return snippets

def count_valid_snippets(snippets: list, logger: logging.Logger) -> int:
    """
    Count snippets that passed AST validation.
    Assumes the input list comes from the AST validation output or a file
    that was already filtered by the AST validation step (T018).
    If the input is raw, we might need to re-run validation logic, 
    but T019 assumes T018 has run and produced a 'validated' set.
    """
    # Check if we are reading from the validated directory (output of T018)
    # T018 writes to VALIDATED_DIR. If that exists, we count lines there.
    # If not, we assume the 'snippets' passed in are already the valid ones 
    # from the previous step's logic, or we check a specific validation log.
    
    # Strategy: Look for the validated output file first.
    # T018 typically outputs a file like {source}_validated.jsonl
    validated_path = VALIDATED_DIR / f"{source_name}_validated.jsonl"
    if validated_path.exists():
        count = 0
        with open(validated_path, 'r', encoding='utf-8') as f:
            for line in f:
                if line.strip():
                    count += 1
        return count
    
    # Fallback: If we are passed a list that represents the valid set
    # (e.g., if T018 returned a list to T019 directly in memory, though 
    # the pipeline prefers file artifacts).
    return len(snippets)

def main():
    logger = setup_logger("snippet_counter")
    logger.info("Starting T019: Verify snippet count")

    sources = ["codesearchnet", "codegen"]
    counts = {}

    try:
        for source in sources:
            # We rely on the file artifacts produced by T018 (AST validation)
            # T018 should have written {source}_validated.jsonl to data/processed/validated/
            validated_path = VALIDATED_DIR / f"{source}_validated.jsonl"
            
            if not validated_path.exists():
                logger.error(f"Validation output not found for {source}. Ensure T018 completed successfully.")
                # If T018 hasn't run, we can't verify count. 
                # In a strict pipeline, this might be an error, but here we just count what exists.
                count = 0
            else:
                count = 0
                with open(validated_path, 'r', encoding='utf-8') as f:
                    for line in f:
                        if line.strip():
                            count += 1
            
            counts[source] = count
            logger.info(f"Valid snippets found for {source}: {count}")

        # Verification
        human_count = counts.get("codesearchnet", 0)
        llm_count = counts.get("codegen", 0)

        if human_count < MIN_SNIPPETS_PER_GROUP:
            msg = ERROR_104_MSG.format(req=MIN_SNIPPETS_PER_GROUP, human=human_count, llm=llm_count)
            logger.error(msg)
            print(msg, file=sys.stderr)
            sys.exit(104)

        if llm_count < MIN_SNIPPETS_PER_GROUP:
            msg = ERROR_104_MSG.format(req=MIN_SNIPPETS_PER_GROUP, human=human_count, llm=llm_count)
            logger.error(msg)
            print(msg, file=sys.stderr)
            sys.exit(104)

        logger.info(f"T019 Success: Both groups meet the threshold (Human: {human_count}, LLM: {llm_count})")
        
        # Optional: Write a summary report
        report_path = PROCESSED_DIR / "snippet_count_report.json"
        with open(report_path, 'w') as f:
            json.dump({"codesearchnet": human_count, "codegen": llm_count, "status": "passed"}, f, indent=2)
        
        return 0

    except FileNotFoundError as e:
        logger.error(f"File not found: {e}")
        sys.exit(1)
    except Exception as e:
        logger.error(f"Unexpected error: {e}")
        sys.exit(1)

if __name__ == "__main__":
    main()
