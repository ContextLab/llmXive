import os
import sys
import json
import logging
from pathlib import Path
from typing import Dict, List, Any, Optional, Tuple

from logging_config import setup_logger, get_logger
from state_tracker import load_state_file, save_state_file, update_state_with_artifact

# Constants
STATISTICS_DIR = Path("data/metrics")
RESULTS_DIR = Path("results")
GUIDELINES_OUTPUT = RESULTS_DIR / "guidelines.md"
SIGNIFICANCE_THRESHOLD = 0.05
EFFECT_SIZE_THRESHOLD = 0.1

def setup_guideline_logger():
    """Initialize the guideline generator logger."""
    return setup_logger("guideline_generator", "guideline_generator")

def load_statistical_results(logger: logging.Logger) -> Dict[str, Any]:
    """
    Load the statistical results generated by previous tasks.
    Expects a JSON file containing Mann-Whitney U p-values, adjusted p-values,
    and Cliff's delta effect sizes.
    """
    # The BH correction and Cliff's delta results are typically written to a combined stats file
    # or separate files. Based on T030 and T029, we look for the BH results file.
    # Assuming the previous tasks wrote 'statistical_results.json' or similar in data/metrics or results.
    # We check common locations.
    possible_paths = [
        RESULTS_DIR / "statistical_results.json",
        STATISTICS_DIR / "statistical_results.json",
        RESULTS_DIR / "bh_corrected_results.json",
        STATISTICS_DIR / "bh_corrected_results.json"
    ]

    results_path = None
    for p in possible_paths:
        if p.exists():
            results_path = p
            break

    if not results_path:
        # Fallback: try to find any json file with 'stat' or 'bh' in name
        for f in RESULTS_DIR.glob("*.json"):
            if 'stat' in f.name.lower() or 'bh' in f.name.lower():
                results_path = f
                break
        if not results_path:
            for f in STATISTICS_DIR.glob("*.json"):
                if 'stat' in f.name.lower() or 'bh' in f.name.lower():
                    results_path = f
                    break

    if not results_path:
        logger.error("No statistical results file found. T030 (BH correction) or T029 (Cliff's Delta) may not have completed.")
        return {}

    logger.info(f"Loading statistical results from {results_path}")
    try:
        with open(results_path, 'r') as f:
            data = json.load(f)
        return data
    except Exception as e:
        logger.error(f"Failed to load statistical results: {e}")
        return {}

def determine_recommendation(adjusted_p: float, cliff_delta: float, metric_name: str) -> Optional[str]:
    """
    Determine if a guideline recommendation is needed based on thresholds.
    Returns a recommendation string if significant, None otherwise.
    """
    # Thresholds from task description
    p_sig = adjusted_p < SIGNIFICANCE_THRESHOLD
    effect_sig = abs(cliff_delta) >= EFFECT_SIZE_THRESHOLD

    if p_sig and effect_sig:
        direction = "higher" if cliff_delta > 0 else "lower"
        magnitude = get_effect_size_magnitude(cliff_delta)
        
        # Generate specific recommendation text
        recommendation = (
            f"**{metric_name}**: The LLM-generated code shows a statistically "
            f"significant {magnitude} effect size (Cliff's delta = {cliff_delta:.3f}) "
            f"compared to human-written code (adjusted p-value = {adjusted_p:.4f}). "
            f"LLM code tends to be {direction} in this metric. "
            f"Reviewers should pay special attention to {metric_name.lower()} in LLM-generated snippets."
        )
        return recommendation
    return None

def get_effect_size_magnitude(cliff_delta: float) -> str:
    """Map Cliff's delta to magnitude labels."""
    val = abs(cliff_delta)
    if val < 0.147:
        return "negligible"
    elif val < 0.33:
        return "small"
    elif val < 0.474:
        return "medium"
    else:
        return "large"

def generate_guidelines_content(results: Dict[str, Any], logger: logging.Logger) -> str:
    """
    Generate the markdown content for the guidelines file.
    """
    lines = []
    lines.append("# Code Review Guidelines for LLM-Generated Code")
    lines.append("")
    lines.append("## Overview")
    lines.append("This document provides review guidelines based on statistical comparisons between human-written and LLM-generated code snippets.")
    lines.append("Metrics with adjusted p-values < 0.05 and |Cliff's delta| >= 0.1 are flagged for special attention.")
    lines.append("")
    
    recommendations = []
    metrics_found = 0
    
    # Structure of results depends on how T030/T029 wrote them.
    # Assuming a structure like: { "metrics": { "metric_name": { "p_value": ..., "adj_p_value": ..., "cliffs_delta": ... } } }
    # or a list of dicts. We handle common patterns.
    
    data = results.get("metrics", results) # Try 'metrics' key or root
    
    if isinstance(data, list):
        items = data
    elif isinstance(data, dict):
        items = data.items()
    else:
        logger.warning("Unexpected statistical results structure.")
        return "# No recommendations generated due to data format error."

    if isinstance(items, dict):
        items = items.items()

    for key, value in items:
        metric_name = key
        if isinstance(value, dict):
            adj_p = value.get("adj_p_value") or value.get("adjusted_p_value")
            cliff = value.get("cliffs_delta") or value.get("cliff_delta")
            
            if adj_p is not None and cliff is not None:
                metrics_found += 1
                rec = determine_recommendation(adj_p, cliff, metric_name)
                if rec:
                    recommendations.append(rec)
        elif isinstance(value, (list, tuple)) and len(value) >= 3:
            # Possible tuple/list format: [p, adj_p, cliff]
            adj_p = value[1]
            cliff = value[2]
            if adj_p is not None and cliff is not None:
                metrics_found += 1
                rec = determine_recommendation(adj_p, cliff, metric_name)
                if rec:
                    recommendations.append(rec)

    if not recommendations:
        lines.append("## Findings")
        lines.append("No metrics met the criteria for significant difference (adjusted p < 0.05 and |Cliff's delta| >= 0.1).")
        lines.append("Standard review procedures apply.")
    else:
        lines.append("## Recommendations")
        lines.append(f"The following {len(recommendations)} metric(s) showed significant differences:")
        lines.append("")
        for i, rec in enumerate(recommendations, 1):
            lines.append(f"{i}. {rec}")
            lines.append("")
        
        lines.append("## General Guidelines")
        lines.append("- **Complexity**: If LLM code is significantly more complex, consider refactoring or adding comments.")
        lines.append("- **Bug Indicators**: If LLM code has higher bug indicator scores, prioritize unit testing and security review.")
        lines.append("- **Maintainability**: If LLM code is less maintainable, focus on readability and modularity improvements.")

    return "\n".join(lines)

def write_guidelines_to_file(content: str, logger: logging.Logger):
    """Write the generated guidelines to the output file."""
    RESULTS_DIR.mkdir(parents=True, exist_ok=True)
    with open(GUIDELINES_OUTPUT, 'w', encoding='utf-8') as f:
        f.write(content)
    logger.info(f"Guidelines written to {GUIDELINES_OUTPUT}")

def update_state_with_guidelines(logger: logging.Logger):
    """Update the state tracker with the guidelines artifact."""
    state = load_state_file()
    update_state_with_artifact(
        state, 
        "guidelines", 
        str(GUIDELINES_OUTPUT), 
        "guidelines.md"
    )
    save_state_file(state)
    logger.info("State updated with guidelines artifact.")

def run_guideline_generation(logger: logging.Logger):
    """Main execution flow for guideline generation."""
    logger.info("Starting guideline generation...")
    
    # 1. Load results
    stats = load_statistical_results(logger)
    if not stats:
        logger.error("No statistical results found. Aborting guideline generation.")
        return False
    
    # 2. Generate content
    content = generate_guidelines_content(stats, logger)
    
    # 3. Write file
    write_guidelines_to_file(content, logger)
    
    # 4. Update state
    update_state_with_guidelines(logger)
    
    logger.info("Guideline generation completed successfully.")
    return True

def main():
    """Entry point for the guideline generator."""
    logger = setup_guideline_logger()
    try:
        success = run_guideline_generation(logger)
        sys.exit(0 if success else 1)
    except Exception as e:
        logger.exception(f"Fatal error in guideline generation: {e}")
        sys.exit(1)

if __name__ == "__main__":
    sys.exit(main())
