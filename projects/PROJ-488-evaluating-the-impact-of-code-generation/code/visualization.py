def load_metric_data(metric_type: str) -> Optional[pd.DataFrame]:
    """
    Load metric data from CSV files generated by metric_extraction.py.
    
    Args:
        metric_type: The type of metric (e.g., 'cyclomatic_complexity', 'maintainability_index')
    
    Returns:
        DataFrame with columns: snippet_id, group, value
        None if file not found or empty
    """
    file_path = METRICS_DIR / f"{metric_type}.csv"
    
    if not file_path.exists():
        logger.warning(f"Metric file not found: {file_path}")
        return None
    
    try:
        df = pd.read_csv(file_path)
        if df.empty:
            logger.warning(f"Metric file is empty: {file_path}")
            return None
        
        # Ensure required columns exist
        required_cols = ['snippet_id', 'group', 'value']
        if not all(col in df.columns for col in required_cols):
            logger.error(f"Missing required columns in {file_path}. Expected: {required_cols}, Found: {list(df.columns)}")
            return None
        
        # Filter out NaN values
        df = df.dropna(subset=['value'])
        if df.empty:
            logger.warning(f"No valid data after filtering NaNs in {file_path}")
            return None
        
        logger.info(f"Loaded {len(df)} records for metric '{metric_type}' from {file_path}")
        return df
        
    except Exception as e:
        logger.error(f"Error loading metric data from {file_path}: {e}")
        return None

