def generate_all_boxplots() -> List[Dict[str, Any]]:
    """
    Generate boxplots for all available metrics.
    
    Returns:
        List of dictionaries with results: {metric, path, success}
    """
    # List of metric types to process (matching metric_extraction.py output)
    metric_types = [
        'cyclomatic_complexity',
        'maintainability_index',
        'halstead_volume',
        'halstead_difficulty',
        'bug_count',
        'style_issues'
    ]
    
    results = []
    
    for metric in metric_types:
        logger.info(f"Processing metric: {metric}")
        
        # Load data
        df = load_metric_data(metric)
        if df is None:
            results.append({
                'metric': metric,
                'path': None,
                'success': False,
                'reason': 'Data not found or empty'
            })
            continue
        
        # Define output path
        output_filename = f"{metric}_boxplot.{OUTPUT_FORMAT}"
        output_path = FIGURES_DIR / output_filename
        
        # Create boxplot
        success = create_boxplot(df, metric, output_path)
        
        results.append({
            'metric': metric,
            'path': str(output_path) if success else None,
            'success': success,
            'reason': None if success else 'Failed to create plot'
        })
    
    return results

