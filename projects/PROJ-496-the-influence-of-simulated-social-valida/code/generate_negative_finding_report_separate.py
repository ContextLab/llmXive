def load_search_results(csv_path: str) -> List[Dict[str, Any]]:
    """
    Load the dataset search results from the CSV generated by T016a.
    """
    results = []
    path = Path(csv_path)
    if not path.exists():
        raise FileNotFoundError(f"Search results file not found: {csv_path}")
    
    with open(path, 'r', newline='', encoding='utf-8') as f:
        reader = csv.DictReader(f)
        for row in reader:
            results.append(row)
    return results

def filter_separate_datasets(results: List[Dict[str, Any]]) -> Dict[str, List[Dict[str, Any]]]:
    """
    Filter the results into 'Sim-Only' and 'Real-Only' categories.
    Returns a dict with keys 'sim_only' and 'real_only'.
    """
    sim_only = []
    real_only = []
    
    for row in results:
        status = row.get('status', '').lower()
        if status == 'sim-only':
            sim_only.append(row)
        elif status == 'real-only':
            real_only.append(row)
    
    return {
        'sim_only': sim_only,
        'real_only': real_only
    }

def generate_separate_finding_report(
    sim_only_datasets: List[Dict[str, Any]],
    real_only_datasets: List[Dict[str, Any]],
    output_path: str
) -> None:
    """
    Generate a PDF report detailing the 'Separate Datasets Found' scenario.
    This report explicitly lists the identified 'Sim-Only' and 'Real-Only' datasets.
    """
    doc = SimpleDocTemplate(output_path, pagesize=letter)
    styles = getSampleStyleSheet()
    story = []
    
    # Title
    title_style = styles['Heading1']
    story.append(Paragraph("Negative Finding Report: Separate Datasets Found", title_style))
    story.append(Spacer(1, 20))
    
    # Introduction
    intro_text = (
        "This report documents the outcome of the dataset search phase (US1). "
        "No single dataset was found that contains both the required social feedback manipulation "
        "(simulated vs real) AND the necessary social anxiety measures. "
        "However, separate datasets were identified that cover one aspect each. "
        "Per the project plan, this triggers an abort of the main analysis pipeline to prevent "
        "invalid meta-analysis across disparate data sources."
    )
    story.append(Paragraph(intro_text, styles['Normal']))
    story.append(Spacer(1, 10))
    
    # Section: Simulated-Only Datasets
    story.append(Paragraph("Identified 'Simulated-Only' Datasets", styles['Heading2']))
    if sim_only_datasets:
        data = [["Dataset ID", "Title", "URL", "Feedback Type", "Anxiety Measure"]]
        for ds in sim_only_datasets:
            data.append([
                ds.get('dataset_id', 'N/A'),
                ds.get('title', 'N/A'),
                ds.get('url', 'N/A'),
                ds.get('feedback_type', 'N/A'),
                ds.get('anxiety_measure', 'None')
            ])
        
        t = Table(data, colWidths=[100, 250, 150, 100, 100])
        t.setStyle(TableStyle([
            ('BACKGROUND', (0, 0), (-1, 0), colors.grey),
            ('TEXTCOLOR', (0, 0), (-1, 0), colors.whitesmoke),
            ('ALIGN', (0, 0), (-1, -1), 'LEFT'),
            ('FONTNAME', (0, 0), (-1, 0), 'Helvetica-Bold'),
            ('FONTSIZE', (0, 0), (-1, 0), 10),
            ('BOTTOMPADDING', (0, 0), (-1, 0), 12),
            ('BACKGROUND', (0, 1), (-1, -1), colors.beige),
            ('GRID', (0, 0), (-1, -1), 1, colors.black),
            ('VALIGN', (0, 0), (-1, -1), 'MIDDLE'),
        ]))
        story.append(t)
    else:
        story.append(Paragraph("No 'Simulated-Only' datasets were identified.", styles['Normal']))
    
    story.append(Spacer(1, 20))
    
    # Section: Real-Only Datasets
    story.append(Paragraph("Identified 'Real-Only' Datasets", styles['Heading2']))
    if real_only_datasets:
        data = [["Dataset ID", "Title", "URL", "Feedback Type", "Anxiety Measure"]]
        for ds in real_only_datasets:
            data.append([
                ds.get('dataset_id', 'N/A'),
                ds.get('title', 'N/A'),
                ds.get('url', 'N/A'),
                ds.get('feedback_type', 'N/A'),
                ds.get('anxiety_measure', 'None')
            ])
        
        t = Table(data, colWidths=[100, 250, 150, 100, 100])
        t.setStyle(TableStyle([
            ('BACKGROUND', (0, 0), (-1, 0), colors.grey),
            ('TEXTCOLOR', (0, 0), (-1, 0), colors.whitesmoke),
            ('ALIGN', (0, 0), (-1, -1), 'LEFT'),
            ('FONTNAME', (0, 0), (-1, 0), 'Helvetica-Bold'),
            ('FONTSIZE', (0, 0), (-1, 0), 10),
            ('BOTTOMPADDING', (0, 0), (-1, 0), 12),
            ('BACKGROUND', (0, 1), (-1, -1), colors.beige),
            ('GRID', (0, 0), (-1, -1), 1, colors.black),
            ('VALIGN', (0, 0), (-1, -1), 'MIDDLE'),
        ]))
        story.append(t)
    else:
        story.append(Paragraph("No 'Real-Only' datasets were identified.", styles['Normal']))
    
    story.append(Spacer(1, 20))
    
    # Conclusion / Data Gap Statement
    story.append(Paragraph("Data Gap Statement", styles['Heading2']))
    gap_text = (
        "The research pipeline requires a single dataset containing both social feedback manipulation "
        "and social anxiety measures to validly test the interaction hypothesis. "
        "The existence of separate datasets ('Sim-Only' and 'Real-Only') does not satisfy this requirement "
        "as it would necessitate combining heterogeneous data sources, introducing uncontrolled confounds. "
        "Therefore, the analysis pipeline is aborted at this stage."
    )
    story.append(Paragraph(gap_text, styles['Normal']))
    
    # Build PDF
    doc.build(story)

def main():
    logger = get_logger(__name__)
    logger.info("Starting Separate Datasets Negative Finding Report Generation (T016c)")
    
    # Determine paths
    base_dir = Path(get_env_var("PROJECT_ROOT", "."))
    results_dir = base_dir / "data" / "results"
    csv_path = results_dir / "dataset_search_results.csv"
    output_path = results_dir / "negative_finding_report_separate_datasets.pdf"
    
    if not results_dir.exists():
        results_dir.mkdir(parents=True, exist_ok=True)
    
    try:
        # Load search results
        logger.info(f"Loading search results from {csv_path}")
        results = load_search_results(str(csv_path))
        
        # Filter for separate datasets
        separated = filter_separate_datasets(results)
        
        if not separated['sim_only'] and not separated['real_only']:
            logger.warning("No 'Sim-Only' or 'Real-Only' datasets found. This report should not be generated.")
            sys.exit(1)
        
        logger.info(f"Found {len(separated['sim_only'])} Sim-Only and {len(separated['real_only'])} Real-Only datasets.")
        
        # Generate Report
        logger.info(f"Generating report at {output_path}")
        generate_separate_finding_report(
            separated['sim_only'],
            separated['real_only'],
            str(output_path)
        )
        
        logger.info("Report generated successfully.")
        print(f"SUCCESS: Report generated at {output_path}")
        
    except FileNotFoundError as e:
        logger.error(f"Required input file not found: {e}")
        sys.exit(1)
    except Exception as e:
        logger.error(f"Error generating report: {e}")
        sys.exit(1)

if __name__ == "__main__":
    main()