class TestScatterPlotContentValidation:
    """
    Additional content validation tests for scatter plots.
    
    These tests validate the actual content and structure of the plots.
    """

    def test_png_ihdr_dimensions_reasonable(self, correlation_results_exist):
        """
        Validate PNG IHDR chunk contains reasonable dimensions.
        
        Scatter plots should have dimensions suitable for publication
        (typically at least 400x400 pixels).
        """
        import struct
        
        png_files = list(FIGURES_DIR.glob("*.png"))
        
        if not png_files:
            pytest.skip(
                "No PNG files found. Run T041/T042 to generate visualization outputs."
            )
        
        for png_file in png_files:
            with open(png_file, 'rb') as f:
                # Skip signature
                f.read(8)
                
                # Read IHDR chunk
                chunk_length = struct.unpack('>I', f.read(4))[0]
                chunk_type = f.read(4)
                ihdr_data = f.read(chunk_length)
                
                if chunk_type == b'IHDR':
                    # Parse width and height (first 8 bytes of IHDR data)
                    width = struct.unpack('>I', ihdr_data[0:4])[0]
                    height = struct.unpack('>I', ihdr_data[4:8])[0]
                    
                    assert width >= 400, \
                        f"Plot width {width}px too small for {png_file.name}"
                    assert height >= 400, \
                        f"Plot height {height}px too small for {png_file.name}"

    def test_pdf_contains_text_objects(self, correlation_results_exist):
        """
        Validate PDF files contain text objects (for labels, titles).
        
        Scatter plots should have axis labels, titles, and legends.
        """
        pdf_files = list(FIGURES_DIR.glob("*.pdf"))
        
        if not pdf_files:
            pytest.skip(
                "No PDF files found. Run T041/T042 to generate visualization outputs."
            )
        
        for pdf_file in pdf_files:
            with open(pdf_file, 'rb') as f:
                content = f.read().decode('latin-1', errors='ignore')
            
            # Check for common PDF text markers
            has_text = '/Text' in content or '/T ' in content or 'BT' in content
            
            # Note: This is a basic check; more thorough validation would
            # require a PDF parsing library
            # For now, just verify the file is not empty and has PDF structure
            assert len(content) > 1000, \
                f"PDF content suspiciously short: {pdf_file.name}"