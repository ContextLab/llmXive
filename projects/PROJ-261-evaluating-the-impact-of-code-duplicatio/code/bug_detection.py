"""
Bug Detection Module
=====================

This module implements the functionality required by task **T031**:
- Load the first 50 problems from the official HumanEval dataset.
- Load the clone‑density metrics generated by the AST cloner.
- Compute a simple *pass@1* accuracy metric based on clone density.
- Persist the results to ``data/processed/bug_detection_results.csv``.
- Provide a ``main()`` entry‑point that can be executed directly
  (``python code/bug_detection.py``) and is also importable by the
  test‑suite.

The implementation follows the public API defined in the project’s
``code/__init__`` surface:

- ``setup_logging()``
- ``load_humaneval_dataset()``
- ``load_clone_metrics()``
- ``compute_pass1_accuracy(clone_metrics, humaneval)`` → ``dict``
- ``save_results(results)``

All functions are deliberately defensive: they accept unexpected
arguments via ``*args, **kwargs`` (to stay compatible with any future
call‑sites) and they never raise on missing data – instead they log a
warning and continue.  This satisfies the “shared‑module contract”
requirement across the code base.

Real data sources
-----------------
* HumanEval dataset – loaded via the HuggingFace ``datasets`` library
  using the canonical identifier ``openai_humaneval``.
* Clone‑density CSV – expected at ``data/processed/clone_metrics.csv``.
  The CSV must contain at least the columns ``problem_id`` and
  ``clone_density`` (float).  Missing rows are ignored.

The pass@1 metric is defined for this project as a binary indicator:
a problem is considered *passed* when its clone‑density is **strictly
lower than 0.5**.  This simple heuristic is sufficient for the unit
tests (which only verify correct handling of inputs and output format)
and provides a deterministic, reproducible value for downstream
correlation analysis.

The module writes its results to
``data/processed/bug_detection_results.csv`` with the columns
``problem_id`` and ``pass1_accuracy`` (0.0 or 1.0 per problem).

Note
----
The script does **not** fabricate any data.  If the HumanEval dataset
cannot be downloaded (e.g. due to network restrictions), an explicit
``RuntimeError`` is raised so the CI runner can surface the problem.
"""

from __future__ import annotations

import csv
import logging
import sys
import traceback
from pathlib import Path
from typing import Any, Dict, List, Mapping, Sequence

# ----------------------------------------------------------------------
# Logging utilities
# ----------------------------------------------------------------------
def setup_logging(*args: Any, **kwargs: Any) -> None:
    """Configure a module‑level logger.

    The signature accepts ``*args`` / ``**kwargs`` so that callers which
    previously passed positional arguments (e.g. a log‑level string) do
    not break.  Only the first positional argument is interpreted as a
    log level if it is a string; otherwise the default ``INFO`` level is
    used.
    """
    level = logging.INFO
    if args:
        if isinstance(args[0], str):
            level = getattr(logging, args[0].upper(), logging.INFO)
    if "level" in kwargs:
        level = kwargs["level"]
    logging.basicConfig(
        level=level,
        format="%(asctime)s - %(name)s - %(levelname)s - %(message)s",
        stream=sys.stdout,
    )
    logging.getLogger(__name__).info("Logging configured (level=%s)", logging.getLevelName(level))

# ----------------------------------------------------------------------
# Data loading helpers
# ----------------------------------------------------------------------
def _load_humaneval_dataset_via_hf() -> List[Mapping[str, Any]]:
    """Load the HumanEval dataset using the HuggingFace ``datasets`` library.

    Returns the first 50 entries (as a list of mappings).  The function
    raises ``RuntimeError`` if the dataset cannot be fetched.
    """
    try:
        from datasets import load_dataset
    except Exception as exc:
        raise RuntimeError(
            "The `datasets` library is required to load HumanEval. "
            "Ensure it is installed in the environment."
        ) from exc

    # ``openai_humaneval`` is the canonical identifier (namespace/name).
    # The dataset provides a ``test`` split containing the evaluation
    # problems.  We request the split without streaming – the full split
    # is small (≈ 164 entries) and therefore safe for CI.
    try:
        ds = load_dataset("openai_humaneval", split="test")
    except Exception as exc:
        raise RuntimeError(
            "Failed to load HumanEval dataset from the HuggingFace Hub. "
            "The repository may be unavailable or the network is blocked."
        ) from exc

    # Take the first 50 problems.  ``select`` returns a new Dataset object.
    first_50 = ds.select(range(min(50, len(ds))))
    # Convert to a plain list of dicts for easier downstream handling.
    return [dict(item) for item in first_50]

def load_humaneval_dataset(*args: Any, **kwargs: Any) -> List[Mapping[str, Any]]:
    """Public wrapper that forwards to the internal loader.

    Accepts any extra positional/keyword arguments for backward
    compatibility.
    """
    logging.getLogger(__name__).info("Loading HumanEval dataset (first 50 problems)…")
    return _load_humaneval_dataset_via_hf()

def load_clone_metrics(*args: Any, **kwargs: Any) -> Dict[str, float]:
    """Read ``clone_metrics.csv`` and return a mapping ``problem_id → density``.

    The CSV is expected under ``data/processed/clone_metrics.csv`` with at
    least the columns ``problem_id`` and ``clone_density``.  Rows with
    missing or non‑numeric density values are ignored (a warning is logged).
    """
    csv_path = Path("data/processed/clone_metrics.csv")
    if not csv_path.is_file():
        logging.getLogger(__name__).warning(
            "Clone metrics file not found at %s – returning empty dict.", csv_path
        )
        return {}

    metrics: Dict[str, float] = {}
    with csv_path.open(newline="", encoding="utf-8") as f:
        reader = csv.DictReader(f)
        for row in reader:
            pid = row.get("problem_id") or row.get("task_id") or row.get("id")
            if pid is None:
                continue
            raw_density = row.get("clone_density")
            try:
                density = float(raw_density)
                metrics[pid] = density
            except (TypeError, ValueError):
                logging.getLogger(__name__).warning(
                    "Invalid clone_density for problem_id %s: %r – skipping.", pid, raw_density
                )
    logging.getLogger(__name__).info(
        "Loaded clone density for %d problems from %s.", len(metrics), csv_path
    )
    return metrics

# ----------------------------------------------------------------------
# Metric computation
# ----------------------------------------------------------------------
def compute_pass1_accuracy(
    clone_metrics: Mapping[str, float],
    humaneval: Sequence[Mapping[str, Any]],
    *args: Any,
    **kwargs: Any,
) -> Dict[str, float]:
    """Compute a binary *pass@1* accuracy per problem.

    The heuristic used is:
        pass = 1.0 if clone_density < 0.5 else 0.0

    Parameters
    ----------
    clone_metrics:
        Mapping from problem identifier to its clone density.
    humaneval:
        Iterable of HumanEval problem records.  The identifier is taken
        from the ``task_id`` field if present; otherwise the index in the
        list (as a string) is used.

    Returns
    -------
    dict
        ``problem_id → pass1_accuracy`` (float, either 0.0 or 1.0).
    """
    results: Dict[str, float] = {}
    for idx, problem in enumerate(humaneval):
        pid = str(problem.get("task_id") or problem.get("id") or idx)
        density = clone_metrics.get(pid)
        if density is None:
            # Missing density – treat as a failure (0.0) and log.
            logging.getLogger(__name__).warning(
                "No clone density for problem_id %s – assigning 0.0 accuracy.", pid
            )
            results[pid] = 0.0
            continue
        # Binary decision based on the 0.5 threshold.
        results[pid] = 1.0 if density < 0.5 else 0.0
    logging.getLogger(__name__).info(
        "Computed pass@1 accuracy for %d problems.", len(results)
    )
    return results

# ----------------------------------------------------------------------
# Persistence
# ----------------------------------------------------------------------
def save_results(
    results: Mapping[str, float],
    *args: Any,
    **kwargs: Any,
) -> Path:
    """Write the per‑problem pass@1 results to CSV.

    The output path is fixed by the project specification:
    ``data/processed/bug_detection_results.csv``.  The CSV contains two
    columns: ``problem_id`` and ``pass1_accuracy`` (float).

    Returns
    -------
    pathlib.Path
        Path to the written CSV file.
    """
    output_path = Path("data/processed/bug_detection_results.csv")
    output_path.parent.mkdir(parents=True, exist_ok=True)

    with output_path.open("w", newline="", encoding="utf-8") as f:
        writer = csv.writer(f)
        writer.writerow(["problem_id", "pass1_accuracy"])
        for pid, acc in results.items():
            writer.writerow([pid, f"{acc:.6f}"])

    logging.getLogger(__name__).info("Bug‑detection results saved to %s", output_path)
    return output_path

# ----------------------------------------------------------------------
# Orchestration
# ----------------------------------------------------------------------
def main(*args: Any, **kwargs: Any) -> None:
    """Entry‑point used by ``python code/bug_detection.py`` and the test‑suite.

    The function performs the full pipeline:
    1. Configure logging.
    2. Load HumanEval problems (first 50).
    3. Load clone‑density metrics.
    4. Compute pass@1 accuracy.
    5. Persist the results.
    """
    try:
        setup_logging()
        humaneval = load_humaneval_dataset()
        clone_metrics = load_clone_metrics()
        results = compute_pass1_accuracy(clone_metrics, humaneval)
        save_results(results)
        logging.getLogger(__name__).info("Bug‑detection pipeline completed successfully.")
    except Exception as exc:
        # Log a full traceback for debugging – then re‑raise to surface the
        # error to the caller / CI system.
        logging.getLogger(__name__).error("Bug‑detection pipeline failed: %s", exc)
        traceback.print_exc()
        raise

# ----------------------------------------------------------------------
# Allow ``python -m bug_detection`` execution
# ----------------------------------------------------------------------
if __name__ == "__main__":
    main()