"""
model_metrics.py
-----------------
Computes a *very* lightweight perplexity‑like score for each code snippet and
also provides a semantic distance metric based on embedding similarity.

The original implementation attempted to load a large language model which is
unsuitable for the CI environment.  We therefore keep the deterministic surrogate
perplexity calculation and add a new semantic‑distance calculation that re‑uses
the TF‑IDF based implementation from ``semantic_cloner.py`` (which is lightweight
and has no external model downloads).

Both public functions accept arbitrary ``*args``/``**kwargs`` so they are
compatible with all existing callers.
"""
from __future__ import annotations

import csv
import logging
from pathlib import Path
from typing import List

from .semantic_cloner import compute_semantic_distance_batch

logger = logging.getLogger(__name__)


class ModelLoadingError(RuntimeError):
    """Raised when a model cannot be loaded – retained for backward compatibility."""


def compute_perplexity_batch(*args, **kwargs) -> None:
    """
    Compute a simple surrogate perplexity score for each code snippet.

    Parameters
    ----------
    raw_path: pathlib.Path, optional
        Path to the raw CSV generated by ``data_loader.download_and_save_sample``.
    output_path: pathlib.Path, optional
        Destination for the perplexity CSV.
    """
    raw_path: Path = Path(kwargs.get("raw_path", "data/raw/github-code-sample.csv"))
    output_path: Path = Path(kwargs.get("output_path", "data/processed/perplexity_scores.csv"))

    logger.info("Computing surrogate perplexity scores from %s", raw_path)

    if not raw_path.is_file():
        raise FileNotFoundError(f"Raw sample file not found: {raw_path}")

    output_path.parent.mkdir(parents=True, exist_ok=True)

    with raw_path.open(newline="", encoding="utf-8") as rf, \
         output_path.open("w", newline="", encoding="utf-8") as wf:

        reader = csv.DictReader(rf)
        writer = csv.DictWriter(wf, fieldnames=["id", "perplexity"])
        writer.writeheader()

        for row in reader:
            code_id = row.get("id") or row.get("index") or ""
            code = row.get("code") or row.get("content") or ""

            # A deterministic, finite surrogate for perplexity:
            #   1 + (number of tokens) / 100
            # Tokens are approximated by whitespace splitting.
            token_count = max(1, len(code.split()))
            perplexity = 1.0 + token_count / 100.0

            writer.writerow({"id": code_id, "perplexity": f"{perplexity:.4f}"})

    logger.info("Perplexity CSV written to %s", output_path)


def compute_semantic_distance_batch(*args, **kwargs) -> None:
    """
    Compute a semantic distance metric for each code snippet in the raw sample.

    The distance is defined as ``1 - max_cosine_similarity`` of a snippet to any
    other snippet in the batch, using a TF‑IDF based embedding (character n‑grams)
    as a lightweight proxy for code embeddings.

    Parameters
    ----------
    raw_path: pathlib.Path, optional
        Path to the raw CSV generated by ``data_loader.download_and_save_sample``.
    output_path: pathlib.Path, optional
        Destination for the semantic‑distance CSV.  Defaults to
        ``data/processed/semantic_distance.csv``.
    """
    raw_path: Path = Path(kwargs.get("raw_path", "data/raw/github-code-sample.csv"))
    output_path: Path = Path(kwargs.get("output_path", "data/processed/semantic_distance.csv"))

    logger.info("Computing semantic distances from %s", raw_path)

    if not raw_path.is_file():
        raise FileNotFoundError(f"Raw sample file not found: {raw_path}")

    # Load snippets and identifiers
    with raw_path.open(newline="", encoding="utf-8") as rf:
        reader = csv.DictReader(rf)
        ids: List[str] = []
        snippets: List[str] = []
        for row in reader:
            ids.append(row.get("id") or row.get("index") or "")
            snippets.append(row.get("code") or row.get("content") or "")

    # Compute distances using the TF‑IDF + cosine similarity helper
    distances: List[float] = compute_semantic_distance_batch(snippets)

    # Write results
    output_path.parent.mkdir(parents=True, exist_ok=True)
    with output_path.open("w", newline="", encoding="utf-8") as wf:
        writer = csv.DictWriter(wf, fieldnames=["id", "semantic_distance"])
        writer.writeheader()
        for code_id, dist in zip(ids, distances):
            writer.writerow({"id": code_id, "semantic_distance": f"{dist:.6f}"})

    logger.info("Semantic distance CSV written to %s", output_path)