"""
Model metrics module.

Provides functionality to load a language model (with optional 8‑bit quantization
via ``bitsandbytes``) and compute token‑level perplexity scores for a set of
source‑code files. The implementation is deliberately lightweight: it reads the
list of file paths from ``data/processed/clone_metrics.csv`` (generated by the
AST cloner) and writes a CSV of per‑file perplexities to
``data/processed/perplexity_scores.csv``.

The module also defines ``ModelLoadingError`` – a dedicated exception type used
by the test suite to verify proper error handling when model loading fails.
"""
from __future__ import annotations

import csv
import logging
from pathlib import Path
from typing import List

import torch

logger = logging.getLogger(__name__)

class ModelLoadingError(RuntimeError):
    """Raised when the language model cannot be loaded (e.g., missing bitsandbytes)."""

    pass

def load_model() -> torch.nn.Module:
    """
    Load the ``Salesforce/codegen-350M-mono`` model in 8‑bit quantization mode
    using ``bitsandbytes`` if it is available. If any step fails, the exception
    is propagated to the caller, which may wrap it in ``ModelLoadingError``.
    """
    try:
        # Import lazily so that the module can be imported even when bitsandbytes
        # is not installed (the test suite monkey‑patches this function).
        from transformers import AutoModelForCausalLM, AutoTokenizer
        import bitsandbytes as bnb  # type: ignore
    except Exception as exc:
        logger.error("Failed to import model loading dependencies: %s", exc)
        raise

    # Model and tokenizer identifiers.
    model_name = "Salesforce/codegen-350M-mono"

    # Load tokenizer (needed for tokenisation during perplexity calculation).
    tokenizer = AutoTokenizer.from_pretrained(model_name)

    # Load the model with 8‑bit quantization.
    try:
        model = AutoModelForCausalLM.from_pretrained(
            model_name,
            device_map="auto",
            load_in_8bit=True,
            torch_dtype=torch.float16,
        )
    except Exception as exc:
        logger.error("Failed to load model in 8‑bit mode: %s", exc)
        raise

    model.eval()
    # Attach tokenizer for convenience; not required by the core API but useful.
    model.tokenizer = tokenizer  # type: ignore
    return model

def _compute_perplexity_for_text(model: torch.nn.Module, text: str) -> float:
    """
    Compute the perplexity of *text* using *model*.

    This helper tokenises *text* with the model's tokenizer and computes the
    average negative log‑likelihood per token, then exponentiates to obtain
    perplexity. Errors during tokenisation or inference are caught and result
    in ``float('inf')`` so that downstream callers can filter them out.
    """
    try:
        tokenizer = getattr(model, "tokenizer", None)
        if tokenizer is None:
            raise RuntimeError("Model does not have an attached tokenizer")
        inputs = tokenizer(text, return_tensors="pt")
        input_ids = inputs["input_ids"].to(model.device)

        with torch.no_grad():
            outputs = model(input_ids, labels=input_ids)
            # ``loss`` is the mean negative log‑likelihood over the sequence.
            loss = outputs.loss.item()
            perplexity = torch.exp(torch.tensor(loss)).item()
            return float(perplexity)
    except Exception as exc:
        logger.warning("Failed to compute perplexity for a snippet: %s", exc)
        return float("inf")

def compute_perplexity_batch(
    clone_metrics_path: Path | str = Path("data/processed/clone_metrics.csv"),
    output_path: Path | str = Path("data/processed/perplexity_scores.csv"),
) -> None:
    """
    Compute perplexity scores for each file listed in ``clone_metrics_path`` and
    write the results to ``output_path`` as a CSV with columns
    ``file_path,perplexity``.

    The function follows a defensive programming style:
    * It attempts to load the model; any exception is wrapped in
      ``ModelLoadingError``.
    * It reads the clone‑metrics CSV, expecting the first column to contain the
      original file path.
    * For each file, it reads the source code (if the file exists) and computes
      a perplexity value. If the source cannot be read or the model fails, a
      ``float('inf')`` value is recorded.
    """
    # Normalise arguments to ``Path`` objects.
    clone_metrics_path = Path(clone_metrics_path)
    output_path = Path(output_path)

    # -------------------------------------------------------------
    # 1. Load the model – any problem here is escalated as ModelLoadingError.
    # -------------------------------------------------------------
    try:
        model = load_model()
    except Exception as exc:
        raise ModelLoadingError(str(exc)) from exc

    # -------------------------------------------------------------
    # 2. Read the list of file paths from the clone‑metrics CSV.
    # -------------------------------------------------------------
    if not clone_metrics_path.is_file():
        raise FileNotFoundError(f"Clone metrics file not found: {clone_metrics_path}")

    file_paths: List[Path] = []
    with clone_metrics_path.open(newline="", encoding="utf-8") as csvfile:
        reader = csv.reader(csvfile)
        header = next(reader, None)  # Skip header line.
        for row in reader:
            if not row:
                continue
            # The first column of the clone‑metrics CSV is the file path.
            file_paths.append(Path(row[0]))

    # -------------------------------------------------------------
    # 3. Compute perplexity for each file.
    # -------------------------------------------------------------
    results: List[tuple[Path, float]] = []
    for fp in file_paths:
        try:
            code_text = fp.read_text(encoding="utf-8")
        except Exception as exc:
            logger.warning("Unable to read source file %s: %s", fp, exc)
            perplexity = float("inf")
        else:
            perplexity = _compute_perplexity_for_text(model, code_text)
        results.append((fp, perplexity))

    # -------------------------------------------------------------
    # 4. Write the results CSV.
    # -------------------------------------------------------------
    output_path.parent.mkdir(parents=True, exist_ok=True)
    with output_path.open("w", newline="", encoding="utf-8") as csvfile:
        writer = csv.writer(csvfile)
        writer.writerow(["file_path", "perplexity"])
        for fp, perp in results:
            writer.writerow([str(fp), f"{perp:.6f}"])

    logger.info("Perplexity scores written to %s", output_path)