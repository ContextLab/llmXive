"""
Data models for the llmXive Edit-Compass follow-up project.

Defines Pydantic models for:
- EditInstance: Represents a single image editing task from the dataset.
- ScoreRecord: Represents the computed scores and metadata for an instance.
"""
from pathlib import Path
from typing import Optional, List, Dict, Any
from pydantic import BaseModel, Field, field_validator
import json


class EditInstance(BaseModel):
    """
    Represents a single image editing instance from the Edit-Compass dataset.
    
    Attributes:
        source_image_path: Path to the original source image.
        edited_image_path: Path to the edited image.
        instruction: The text instruction used for editing.
        category: The category of the edit (e.g., "World Knowledge Reasoning").
        human_judgment_score: The human evaluation score for the edit quality.
    """
    source_image_path: str = Field(..., description="Path to the source image")
    edited_image_path: str = Field(..., description="Path to the edited image")
    instruction: str = Field(..., description="The editing instruction")
    category: str = Field(..., description="Category of the edit")
    human_judgment_score: float = Field(..., ge=0.0, le=1.0, description="Human judgment score (0-1)")

    @field_validator('source_image_path', 'edited_image_path')
    @classmethod
    def validate_paths(cls, v: str) -> str:
        """Ensure paths are valid strings and exist if they are absolute or relative paths."""
        if not v:
            raise ValueError("Image path cannot be empty")
        # We do not strictly check existence here to allow for lazy loading or remote URLs,
        # but we ensure it's a non-empty string.
        return v

    @field_validator('human_judgment_score')
    @classmethod
    def validate_score_range(cls, v: float) -> float:
        """Ensure the score is within the valid range [0, 1]."""
        if v < 0.0 or v > 1.0:
            raise ValueError(f"Human judgment score must be between 0 and 1, got {v}")
        return v

    def to_dict(self) -> Dict[str, Any]:
        """Convert the model to a dictionary."""
        return self.model_dump()

    def to_json(self, indent: Optional[int] = 2) -> str:
        """Convert the model to a JSON string."""
        return self.model_dump_json(indent=indent)


class ScoreRecord(BaseModel):
    """
    Represents the computed scores and metadata for an EditInstance.
    
    Attributes:
        instance_id: Unique identifier for the instance (usually derived from source/edit paths or a hash).
        logic_score: The logic consistency score (from VLM comparison).
        fidelity_score: The overall fidelity score (combination of SSIM and LPIPS).
        ssim: Structural Similarity Index Measure.
        lpips: Learned Perceptual Image Patch Similarity.
        vllm_description: The description generated by the VLM for the edited image.
        p_value_logic: P-value for the logic score significance.
        p_value_fidelity: P-value for the fidelity score significance.
        beta_logic: Standardized beta coefficient for logic score in regression.
        beta_fidelity: Standardized beta coefficient for fidelity score in regression.
    """
    instance_id: str = Field(..., description="Unique identifier for the instance")
    logic_score: float = Field(..., description="Logic consistency score")
    fidelity_score: float = Field(..., description="Overall fidelity score")
    ssim: float = Field(..., description="SSIM value")
    lpips: float = Field(..., description="LPIPS value")
    vllm_description: str = Field(..., description="VLM generated description")
    p_value_logic: Optional[float] = Field(None, description="P-value for logic score")
    p_value_fidelity: Optional[float] = Field(None, description="P-value for fidelity score")
    beta_logic: Optional[float] = Field(None, description="Standardized beta for logic")
    beta_fidelity: Optional[float] = Field(None, description="Standardized beta for fidelity")

    @field_validator('logic_score', 'fidelity_score', 'ssim', 'lpips')
    @classmethod
    def validate_score_ranges(cls, v: float, info) -> float:
        """Ensure numeric scores are within expected ranges."""
        field_name = info.field_name
        if field_name in ['ssim', 'lpips', 'fidelity_score']:
            if v < 0.0 or v > 1.0:
                raise ValueError(f"{field_name} must be between 0 and 1, got {v}")
        # Logic score can theoretically be cosine similarity [-1, 1], but often normalized to [0, 1] in these contexts.
        # We allow [-1, 1] for logic_score to be safe, but typically expect [0, 1].
        if field_name == 'logic_score':
            if v < -1.0 or v > 1.0:
                raise ValueError(f"Logic score must be between -1 and 1, got {v}")
        return v

    @field_validator('p_value_logic', 'p_value_fidelity')
    @classmethod
    def validate_p_value(cls, v: Optional[float]) -> Optional[float]:
        """Ensure p-values are between 0 and 1 if provided."""
        if v is not None and (v < 0.0 or v > 1.0):
            raise ValueError(f"P-value must be between 0 and 1, got {v}")
        return v

    def to_dict(self) -> Dict[str, Any]:
        """Convert the model to a dictionary."""
        return self.model_dump(exclude_none=True)

    def to_json(self, indent: Optional[int] = 2) -> str:
        """Convert the model to a JSON string."""
        return self.model_dump_json(indent=indent, exclude_none=True)