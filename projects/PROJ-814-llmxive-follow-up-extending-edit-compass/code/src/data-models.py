from pathlib import Path
from typing import Optional, List, Dict, Any
from pydantic import BaseModel, Field, field_validator
import json


class EditInstance(BaseModel):
    """
    Represents a single image editing instance from the dataset.
    Used for loading and validating raw/filtered data entries.
    """
    source_image_path: str = Field(
        ...,
        description="Path to the original source image",
        min_length=1
    )
    edited_image_path: str = Field(
        ...,
        description="Path to the edited output image",
        min_length=1
    )
    instruction: str = Field(
        ...,
        description="The text instruction describing the edit",
        min_length=1
    )
    category: str = Field(
        ...,
        description="Category of the edit (e.g., 'World Knowledge Reasoning')"
    )
    human_judgment_score: float = Field(
        ...,
        description="Human evaluation score for the edit quality",
        ge=0.0,
        le=1.0
    )

    @field_validator('source_image_path', 'edited_image_path')
    @classmethod
    def validate_paths(cls, v: str) -> str:
        if not v:
            raise ValueError("Path cannot be empty")
        return v

    def to_dict(self) -> Dict[str, Any]:
        """Convert instance to dictionary for JSON serialization."""
        return self.model_dump()

    @classmethod
    def from_dict(cls, data: Dict[str, Any]) -> "EditInstance":
        """Create instance from dictionary."""
        return cls(**data)


class ScoreRecord(BaseModel):
    """
    Represents the computed scores for a single edit instance.
    Contains both logic (VLM-based) and fidelity (image-metric) scores.
    """
    instance_id: str = Field(
        ...,
        description="Unique identifier for the edit instance",
        min_length=1
    )
    logic_score: float = Field(
        ...,
        description="Logic consistency score from VLM comparison",
        ge=-1.0,
        le=1.0
    )
    fidelity_score: float = Field(
        ...,
        description="Combined fidelity score (SSIM and LPIPS)",
        ge=0.0,
        le=1.0
    )
    ssim: float = Field(
        ...,
        description="Structural Similarity Index Measure",
        ge=0.0,
        le=1.0
    )
    lpips: float = Field(
        ...,
        description="Learned Perceptual Image Patch Similarity",
        ge=0.0,
        le=1.0
    )
    vllm_description: str = Field(
        ...,
        description="Description generated by the VLM for the edited image",
        min_length=1
    )
    p_value_logic: Optional[float] = Field(
        None,
        description="Statistical p-value for logic score significance"
    )
    p_value_fidelity: Optional[float] = Field(
        None,
        description="Statistical p-value for fidelity score significance"
    )
    beta_logic: Optional[float] = Field(
        None,
        description="Standardized regression coefficient for logic score"
    )
    beta_fidelity: Optional[float] = Field(
        None,
        description="Standardized regression coefficient for fidelity score"
    )

    @field_validator('logic_score', 'fidelity_score', 'ssim', 'lpips')
    @classmethod
    def validate_numeric_range(cls, v: float) -> float:
        if not isinstance(v, (int, float)):
            raise TypeError("Score must be numeric")
        return float(v)

    def to_dict(self) -> Dict[str, Any]:
        """Convert record to dictionary for JSON serialization."""
        return self.model_dump(exclude_none=True)

    @classmethod
    def from_dict(cls, data: Dict[str, Any]) -> "ScoreRecord":
        """Create record from dictionary."""
        return cls(**data)

    @classmethod
    def from_json(cls, json_str: str) -> "ScoreRecord":
        """Create record from JSON string."""
        data = json.loads(json_str)
        return cls(**data)

    def to_json(self, indent: Optional[int] = 2) -> str:
        """Serialize record to JSON string."""
        return json.dumps(self.to_dict(), indent=indent)