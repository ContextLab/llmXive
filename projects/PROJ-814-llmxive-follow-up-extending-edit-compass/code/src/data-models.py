from pathlib import Path
from typing import Optional, List, Dict, Any
from pydantic import BaseModel, Field, field_validator
import json

class EditInstance(BaseModel):
    """
    Represents a single image editing instance from the Edit-Compass dataset.
    
    Attributes:
        source_image_path: Path to the original source image.
        edited_image_path: Path to the edited image.
        instruction: The text instruction used for the edit.
        category: The category of the edit (e.g., 'World Knowledge Reasoning').
        human_judgment_score: The human evaluation score for the edit quality.
    """
    source_image_path: str = Field(..., description="Path to the source image")
    edited_image_path: str = Field(..., description="Path to the edited image")
    instruction: str = Field(..., description="The editing instruction")
    category: str = Field(..., description="Category of the edit")
    human_judgment_score: float = Field(..., description="Human judgment score")

    @field_validator('source_image_path', 'edited_image_path')
    @classmethod
    def validate_paths(cls, v: str) -> str:
        """Ensure paths are valid strings."""
        if not v:
            raise ValueError("Path cannot be empty")
        return v

    @field_validator('human_judgment_score')
    @classmethod
    def validate_score(cls, v: float) -> float:
        """Ensure score is within valid range [0, 1]."""
        if not (0.0 <= v <= 1.0):
            raise ValueError(f"human_judgment_score must be between 0 and 1, got {v}")
        return v


class ScoreRecord(BaseModel):
    """
    Represents the computed scores and metadata for an EditInstance.
    
    Attributes:
        instance_id: Unique identifier for the instance (usually filename or hash).
        logic_score: The VLM-based logic consistency score.
        fidelity_score: The combined image fidelity score.
        ssim: Structural Similarity Index Measure.
        lpips: Learned Perceptual Image Patch Similarity.
        vllm_description: The text description generated by the VLM.
        p_value_logic: P-value for the logic score significance.
        p_value_fidelity: P-value for the fidelity score significance.
        beta_logic: Standardized regression coefficient for logic score.
        beta_fidelity: Standardized regression coefficient for fidelity score.
    """
    instance_id: str = Field(..., description="Unique identifier for the instance")
    logic_score: float = Field(..., description="Logic consistency score")
    fidelity_score: float = Field(..., description="Fidelity score")
    ssim: float = Field(..., description="SSIM metric")
    lpips: float = Field(..., description="LPIPS metric")
    vllm_description: str = Field(..., description="VLM generated description")
    p_value_logic: float = Field(..., description="P-value for logic score")
    p_value_fidelity: float = Field(..., description="P-value for fidelity score")
    beta_logic: float = Field(..., description="Beta coefficient for logic")
    beta_fidelity: float = Field(..., description="Beta coefficient for fidelity")

    @field_validator('logic_score', 'fidelity_score', 'ssim', 'lpips')
    @classmethod
    def validate_range_01(cls, v: float) -> float:
        """Ensure metric scores are within [0, 1] or [-1, 1] where appropriate."""
        # Logic score can be cosine similarity [-1, 1], but often normalized to [0, 1]
        # Fidelity, SSIM, LPIPS are typically [0, 1]
        if not (-1.0 <= v <= 1.0):
            raise ValueError(f"Score {v} is out of expected range [-1, 1]")
        return v

    @field_validator('p_value_logic', 'p_value_fidelity')
    @classmethod
    def validate_p_value(cls, v: float) -> float:
        """Ensure p-values are within [0, 1]."""
        if not (0.0 <= v <= 1.0):
            raise ValueError(f"P-value must be between 0 and 1, got {v}")
        return v

    @field_validator('beta_logic', 'beta_fidelity')
    @classmethod
    def validate_beta(cls, v: float) -> float:
        """Ensure beta coefficients are reasonable (usually [-1, 1] for standardized)."""
        # Standardized betas are typically within [-1, 1] but can exceed slightly
        if abs(v) > 5.0:
            raise ValueError(f"Beta coefficient {v} seems unreasonably large")
        return v

    def to_json(self) -> str:
        """Serialize the record to a JSON string."""
        return self.model_dump_json(indent=2)

    @classmethod
    def from_json(cls, json_str: str) -> 'ScoreRecord':
        """Deserialize a ScoreRecord from a JSON string."""
        return cls.model_validate_json(json_str)