"""
T011a: Gate - Check Data Availability.

Verifies if matched dMRI+EEG data exists after T009 (Download) and T010 (Preprocess).
Reads the routing state established by the download pipeline.

Logic:
1. Check `data/processed/routing_state.json` for `simulation_required` flag.
2. If flag is false (data exists), output `has_real_data: true`.
3. If flag is true (no data), output `has_real_data: false`.

Output: `data/processed/data_availability_status.json`
"""
import os
import json
import logging
from pathlib import Path
from typing import Dict, Any

# Import shared utilities
from config import get_data_root
from utils.logger import get_logger

logger = get_logger(__name__)

def check_availability() -> Dict[str, Any]:
    """
    Checks the routing state to determine if real matched data is available.
    
    Returns:
        Dict containing 'has_real_data' boolean and status details.
    """
    data_root = get_data_root()
    routing_state_path = data_root / "processed" / "routing_state.json"
    output_path = data_root / "processed" / "data_availability_status.json"

    # Ensure output directory exists
    output_path.parent.mkdir(parents=True, exist_ok=True)

    if not routing_state_path.exists():
        logger.error(f"Routing state file not found at {routing_state_path}. "
                     "Did T009 (download) and T010 (preprocess) run successfully? "
                     "This gate cannot proceed without the routing state generated by the download pipeline.")
        raise FileNotFoundError(
            f"Routing state file missing: {routing_state_path}. "
            "The download and preprocessing pipeline (T009/T010) must complete successfully to generate this file. "
            "If T009 failed to find matched data, it must write a 'simulation_required' flag to this file."
        )

    try:
        with open(routing_state_path, 'r') as f:
            routing_state = json.load(f)
    except json.JSONDecodeError as e:
        logger.error(f"Failed to parse routing state JSON: {e}")
        raise

    # Logic: Check simulation_required flag
    # Per T009 spec: "If NO matched subjects exist... flag the dataset as `simulation_required`"
    # If the flag is missing, it implies the download ran but didn't set the flag,
    # which is an anomaly. We treat missing flag as "Unknown/Failed" -> False (requires simulation).
    simulation_required = routing_state.get("simulation_required", True)
    
    # Also check if 'path' indicates simulation path in the routing state
    path_mode = routing_state.get("path", "unknown")
    
    has_real_data = False
    
    if not simulation_required and path_mode == "correlation":
        has_real_data = True
        status = "real_data_available"
        message = "Matched dMRI+EEG data found. Proceeding with real data path."
    elif simulation_required or path_mode == "simulation":
        has_real_data = False
        status = "simulation_required"
        message = "No matched dMRI+EEG data found. Simulation path triggered."
    else:
        # Fallback for ambiguous state
        logger.warning(f"Ambiguous routing state: {routing_state}. Defaulting to simulation_required.")
        has_real_data = False
        status = "simulation_required"
        message = "Routing state ambiguous or missing flag. Defaulting to simulation path."

    result = {
        "has_real_data": has_real_data,
        "status": status,
        "message": message,
        "source_routing_state": str(routing_state_path)
    }

    # Write output
    with open(output_path, 'w') as f:
        json.dump(result, f, indent=2)

    logger.info(f"Data availability check complete. Result: {result}")
    logger.info(f"Output written to: {output_path}")

    return result

def main():
    """Entry point for the script."""
    try:
        check_availability()
        logger.info("T011a (Check Availability) completed successfully.")
    except Exception as e:
        logger.error(f"T011a (Check Availability) failed: {e}", exc_info=True)
        raise

if __name__ == "__main__":
    main()