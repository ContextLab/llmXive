def load_metrics_data() -> pd.DataFrame:
    """
    Loads structural and avalanche metrics from the processed store.
    Expects a CSV file generated by export_metrics.py.
    """
    data_root = get_data_root()
    metrics_path = data_root / "results" / "exported_metrics.csv"
    
    if not metrics_path.exists():
        raise FileNotFoundError(f"Metrics file not found at {metrics_path}. "
                                "Please run the export pipeline first.")
    
    logger.info(f"Loading metrics from {metrics_path}")
    df = pd.read_csv(metrics_path)
    
    # Filter out subjects that failed QC if a QC column exists
    if 'qc_passed' in df.columns:
        df = df[df['qc_passed'] == True]
        logger.info(f"Filtered to {len(df)} subjects with QC passed.")
    elif 'pipeline_complete' in df.columns:
        df = df[df['pipeline_complete'] == True]
        logger.info(f"Filtered to {len(df)} subjects with complete pipeline.")
        
    return df

def compute_spearman_correlations(df: pd.DataFrame, 
                                  x_col: str, 
                                  y_col: str) -> Tuple[float, float]:
    """
    Computes Spearman rank correlation and p-value.
    """
    if x_col not in df.columns or y_col not in df.columns:
        raise ValueError(f"Columns {x_col} or {y_col} not found in dataframe.")
    
    # Drop NaNs
    valid_data = df[[x_col, y_col]].dropna()
    if len(valid_data) < 3:
        logger.warning(f"Insufficient data points ({len(valid_data)}) for correlation.")
        return 0.0, 1.0
        
    corr, p_val = spearmanr(valid_data[x_col], valid_data[y_col])
    return float(corr), float(p_val)

def _permutation_worker(args: Tuple) -> float:
    """
    Worker function for multiprocessing.
    Calculates the correlation for one permutation.
    """
    x, y, seed = args
    np.random.seed(seed)
    # Shuffle y
    y_perm = y.copy()
    np.random.shuffle(y_perm)
    # Calculate correlation (using a simplified rank correlation logic for speed if needed,
    # but here we rely on scipy for accuracy. In a pure numpy implementation,
    # one would rank transform first).
    # For robustness in parallel, we import here to avoid global state issues if any.
    from scipy.stats import spearmanr
    c, _ = spearmanr(x, y_perm)
    return c

def run_permutation_test(df: pd.DataFrame,
                         x_col: str,
                         y_col: str,
                         n_shuffles: int = DEFAULT_N_SHUFFLES,
                         alpha: float = DEFAULT_ALPHA,
                         n_jobs: int = -1) -> Dict[str, Any]:
    """
    Runs a non-parametric permutation test to assess the significance of the
    observed correlation. Optimized with multiprocessing.
    
    SC-006 Optimization: Uses ProcessPoolExecutor to parallelize the shuffling loop.
    """
    valid_data = df[[x_col, y_col]].dropna()
    x = valid_data[x_col].values
    y = valid_data[y_col].values
    n = len(x)
    
    if n < 3:
        raise ValueError("Not enough data points for permutation test.")
    
    # Calculate observed statistic
    from scipy.stats import spearmanr
    obs_corr, obs_p = spearmanr(x, y)
    logger.info(f"Observed correlation: {obs_corr:.4f} (p={obs_p:.4f})")
    
    # Prepare arguments for parallel workers
    # We generate seeds in advance to ensure reproducibility if needed
    seeds = np.random.randint(0, 2**31, size=n_shuffles)
    args_list = [(x, y, seed) for seed in seeds]
    
    logger.info(f"Starting permutation test with {n_shuffles} shuffles using {n_jobs} cores.")
    start_time = time.time()
    
    null_distribution = []
    
    # Use ProcessPoolExecutor for parallelization
    if n_jobs == -1:
        n_jobs = multiprocessing.cpu_count()
        
    with ProcessPoolExecutor(max_workers=n_jobs) as executor:
        futures = [executor.submit(_permutation_worker, arg) for arg in args_list]
        for future in as_completed(futures):
            null_distribution.append(future.result())
            
    elapsed = time.time() - start_time
    logger.info(f"Permutation test completed in {elapsed:.2f} seconds.")
    
    null_distribution = np.array(null_distribution)
    
    # Calculate p-value: proportion of null stats >= observed stat (absolute value for two-tailed)
    # Assuming two-tailed test: |obs| vs |null|
    abs_obs = np.abs(obs_corr)
    abs_null = np.abs(null_distribution)
    p_perm = np.sum(abs_null >= abs_obs) / n_shuffles
    
    # Calculate 95% confidence interval of null distribution
    ci_lower = np.percentile(null_distribution, 2.5)
    ci_upper = np.percentile(null_distribution, 97.5)
    
    return {
        "observed_correlation": float(obs_corr),
        "observed_p_value": float(obs_p),
        "permutation_p_value": float(p_perm),
        "n_shuffles": n_shuffles,
        "null_mean": float(np.mean(null_distribution)),
        "null_std": float(np.std(null_distribution)),
        "ci_95_lower": float(ci_lower),
        "ci_95_upper": float(ci_upper),
        "runtime_seconds": float(elapsed)
    }

def apply_holm_bonferroni_correction(p_values: List[float], 
                                     alpha: float = DEFAULT_ALPHA) -> Dict[str, Any]:
    """
    Applies Holm-Bonferroni correction to a list of p-values.
    """
    if not p_values:
        return {"adjusted": [], "significant": []}
    
    n_tests = len(p_values)
    sorted_indices = np.argsort(p_values)
    sorted_pvals = np.array(p_values)[sorted_indices]
    
    # Holm-Bonferroni thresholds: alpha / (n - i)
    thresholds = alpha / (n_tests - np.arange(n_tests))
    
    # Determine significance
    significant = np.zeros(n_tests, dtype=bool)
    for i, p in enumerate(sorted_pvals):
        if p <= thresholds[i]:
            significant[i] = True
        else:
            # Once we hit a non-significant, all subsequent are non-significant
            break
    
    # Map back to original order
    adjusted_p = np.zeros(n_tests)
    # Calculate adjusted p-values (max of cumulative min)
    # Holm adjusted p-value for the i-th smallest p is max_{j<=i} ( (n-j+1)*p_j )
    # But simpler logic for binary significance is often sufficient. 
    # Here we compute standard Holm adjusted p-values:
    adj_p = np.zeros(n_tests)
    current_max = 0.0
    for i in range(n_tests):
        val = (n_tests - i) * sorted_pvals[i]
        current_max = max(current_max, val)
        adj_p[i] = min(current_max, 1.0)
    
    # Reorder to original
    final_adjusted = np.zeros(n_tests)
    final_adjusted[sorted_indices] = adj_p
    
    final_significant = final_adjusted <= alpha
    
    return {
        "adjusted_p_values": final_adjusted.tolist(),
        "significant": final_significant.tolist(),
        "alpha": alpha
    }

def calculate_vif(df: pd.DataFrame, features: List[str]) -> pd.Series:
    """
    Calculates Variance Inflation Factor for collinearity diagnostics.
    """
    from statsmodels.stats.outliers_influence import variance_inflation_factor
    
    X = df[features].dropna()
    if X.empty:
        return pd.Series([], dtype=float)
        
    # Add constant for intercept
    X_const = sm.add_constant(X)
    vif_data = pd.Series(
        [variance_inflation_factor(X_const.values, i) 
         for i in range(X_const.shape[1])],
        index=X_const.columns
    )
    return vif_data

def run_collinearity_diagnostics(df: pd.DataFrame, 
                                 features: List[str], 
                                 threshold: float = 5.0) -> Dict[str, Any]:
    """
    Runs VIF diagnostics and flags high collinearity.
    """
    try:
        import statsmodels.api as sm
    except ImportError:
        logger.warning("statsmodels not installed. Skipping VIF calculation.")
        return {"vif": {}, "flags": []}
        
    vif_series = calculate_vif(df, features)
    flags = vif_series[vif_series >= threshold].index.tolist()
    
    return {
        "vif_values": vif_series.to_dict(),
        "high_collinearity_features": flags,
        "threshold": threshold
    }

def run_robustness_analysis(df: pd.DataFrame,
                            x_col: str,
                            y_col: str,
                            n_shuffles: int = 1000,
                            alpha: float = 0.05,
                            n_jobs: int = -1) -> Dict[str, Any]:
    """
    Orchestrates the robustness analysis: permutation test and correction.
    """
    perm_result = run_permutation_test(df, x_col, y_col, n_shuffles, alpha, n_jobs)
    
    # If we have multiple correlations to correct (hypothetical future expansion),
    # we would collect p-values here. For now, we return the permutation result directly.
    # If this function is meant to handle multiple metrics, we'd loop.
    
    return perm_result

def run_correlation_analysis(metrics_df: pd.DataFrame,
                             structural_cols: List[str],
                             avalanche_cols: List[str],
                             n_shuffles: int = 1000,
                             alpha: float = 0.05,
                             n_jobs: int = -1) -> pd.DataFrame:
    """
    Runs correlation analysis for all pairs of structural and avalanche metrics.
    Returns a DataFrame of results.
    """
    results = []
    
    logger.info(f"Running correlation analysis for {len(structural_cols)} x {len(avalanche_cols)} pairs.")
    
    for s_col in structural_cols:
        for a_col in avalanche_cols:
            try:
                perm_res = run_robustness_analysis(
                    metrics_df, s_col, a_col, 
                    n_shuffles=n_shuffles, 
                    alpha=alpha,
                    n_jobs=n_jobs
                )
                results.append({
                    "structural_metric": s_col,
                    "avalanche_metric": a_col,
                    "observed_correlation": perm_res["observed_correlation"],
                    "permutation_p_value": perm_res["permutation_p_value"],
                    "ci_95_lower": perm_res["ci_95_lower"],
                    "ci_95_upper": perm_res["ci_95_upper"]
                })
            except Exception as e:
                logger.error(f"Error analyzing {s_col} vs {a_col}: {e}")
    
    return pd.DataFrame(results)

def main():
    """
    Main entry point for stats analysis.
    """
    logger.info("Starting statistical analysis pipeline.")
    
    try:
        df = load_metrics_data()
        logger.info(f"Loaded {len(df)} subjects.")
        
        # Define metrics of interest (adjust based on actual column names in exported_metrics.csv)
        structural_metrics = ['degree_centrality', 'clustering_coefficient', 'rich_club_coefficient']
        avalanche_metrics = ['avalanche_exponent', 'mean_avalanche_size']
        
        # Filter to existing columns
        structural_metrics = [c for c in structural_metrics if c in df.columns]
        avalanche_metrics = [c for c in avalanche_metrics if c in df.columns]
        
        if not structural_metrics or not avalanche_metrics:
            logger.warning("No matching metrics found for correlation analysis.")
            return
        
        # Run analysis with multiprocessing optimization
        results_df = run_correlation_analysis(
            df, 
            structural_metrics, 
            avalanche_metrics,
            n_shuffles=1000,
            n_jobs=-1 # Use all available cores
        )
        
        # Save results
        output_path = get_data_root() / "results" / "correlation_report.csv"
        results_df.to_csv(output_path, index=False)
        logger.info(f"Correlation report saved to {output_path}")
        
        # Apply Holm-Bonferroni if multiple tests were run
        if len(results_df) > 1:
            p_values = results_df['permutation_p_value'].tolist()
            correction = apply_holm_bonferroni_correction(p_values)
            results_df['holm_adjusted_p'] = correction['adjusted_p_values']
            results_df['is_significant'] = correction['significant']
            results_df.to_csv(output_path, index=False)
            logger.info("Holm-Bonferroni correction applied.")
            
    except Exception as e:
        logger.exception("Fatal error in stats pipeline")
        raise

if __name__ == "__main__":
    main()