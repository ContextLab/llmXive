"""
Unit tests for MMD (Maximum Mean Discrepancy) logic.
Tests verification of MMD statistics using synthetic data generated by T008.
"""
import numpy as np
import pandas as pd
import pytest
import os
import sys

# Add parent directory to path to import code modules
sys.path.insert(0, os.path.join(os.path.dirname(__file__), '..'))

from synthetic_data import generate_synthetic_ili_series
from scipy.spatial.distance import pdist, squareform
from scipy.stats import norm

# Constants for test
TEST_SEED = 42
N_SAMPLES = 100
WINDOW_SIZE = 12

def gaussian_kernel(X, Y=None, gamma=None):
    """
    Compute Gaussian (RBF) kernel matrix between X and Y.
    If Y is None, compute kernel between X and itself.
    """
    if Y is None:
        Y = X
    
    # Compute squared Euclidean distances
    if X.shape[0] == Y.shape[0]:
        # Same size, compute pairwise
        dists = squareform(pdist(X, 'sqeuclidean'))
    else:
        # Different sizes
        dists = np.sum(X**2, axis=1, keepdims=True) + np.sum(Y**2, axis=1) - 2 * np.dot(X, Y.T)
    
    if gamma is None:
        # Default gamma: 1 / (2 * median of pairwise distances^2)
        # Use a subset for median calculation if too large
        if X.shape[0] > 1000:
            sample_indices = np.random.choice(X.shape[0], 1000, replace=False)
            sample_dists = squareform(pdist(X[sample_indices], 'sqeuclidean'))
            median_dist = np.median(sample_dists)
        else:
            median_dist = np.median(dists)
        gamma = 1.0 / (2.0 * median_dist) if median_dist > 0 else 1.0
    
    return np.exp(-gamma * dists)

def mmd_statistic(X, Y, gamma=None):
    """
    Compute the unbiased MMD statistic between two samples X and Y.
    
    MMD^2 = E[k(x,x')] + E[k(y,y')] - 2*E[k(x,y)]
    
    Returns the unbiased estimate.
    """
    n = X.shape[0]
    m = Y.shape[0]
    
    if n < 2 or m < 2:
        raise ValueError("Need at least 2 samples in each group for unbiased MMD")
    
    K_xx = gaussian_kernel(X, X, gamma)
    K_yy = gaussian_kernel(Y, Y, gamma)
    K_xy = gaussian_kernel(X, Y, gamma)
    
    # Unbiased estimator
    term1 = (1.0 / (n * (n - 1))) * (np.sum(K_xx) - np.trace(K_xx))
    term2 = (1.0 / (m * (m - 1))) * (np.sum(K_yy) - np.trace(K_yy))
    term3 = (2.0 / (n * m)) * np.sum(K_xy)
    
    return term1 + term2 - term3

def mmd_permutation_test(X, Y, n_permutations=1000, gamma=None, seed=None):
    """
    Perform permutation test to compute p-value for MMD statistic.
    
    Returns:
      mmd_obs: observed MMD statistic
      p_value: p-value from permutation test
      perm_stats: array of permuted MMD statistics (for debugging)
    """
    if seed is not None:
        np.random.seed(seed)
    
    mmd_obs = mmd_statistic(X, Y, gamma)
    
    # Combine samples
    Z = np.vstack([X, Y])
    n = X.shape[0]
    m = Y.shape[0]
    total = n + m
    
    perm_stats = []
    for _ in range(n_permutations):
        # Shuffle labels
        indices = np.random.permutation(total)
        X_perm = Z[indices[:n]]
        Y_perm = Z[indices[n:]]
        
        perm_mmd = mmd_statistic(X_perm, Y_perm, gamma)
        perm_stats.append(perm_mmd)
    
    perm_stats = np.array(perm_stats)
    p_value = np.mean(perm_stats >= mmd_obs)
    
    return mmd_obs, p_value, perm_stats

def test_mmd_stat_correctness():
    """
    Test MMD statistic correctness using synthetic data from T008.
    
    This test verifies:
    1. MMD is approximately zero when samples come from the same distribution
    2. MMD is significantly positive when samples come from different distributions
    3. The permutation test produces reasonable p-values
    """
    # Set seed for reproducibility
    np.random.seed(TEST_SEED)
    
    # Test Case 1: Same distribution (should have low MMD, high p-value)
    # Generate synthetic data with no shift (constant mean)
    data_same = generate_synthetic_ili_series(
        n_weeks=N_SAMPLES + WINDOW_SIZE,
        seed=TEST_SEED,
        include_missing=False,
        include_constant=False,
        include_outliers=False,
        shift_start=None,
        shift_magnitude=0.0
    )
    
    # Extract two windows from the same distribution
    X_same = data_same[:WINDOW_SIZE].values.reshape(-1, 1)
    Y_same = data_same[WINDOW_SIZE:2*WINDOW_SIZE].values.reshape(-1, 1)
    
    mmd_same, p_same, _ = mmd_permutation_test(X_same, Y_same, n_permutations=100, seed=TEST_SEED)
    
    # MMD should be small for same distribution
    assert mmd_same < 0.5, f"MMD for same distribution should be small, got {mmd_same}"
    # P-value should be high (not significant)
    assert p_same > 0.05, f"P-value for same distribution should be high, got {p_same}"
    
    # Test Case 2: Different distributions (should have high MMD, low p-value)
    # Generate synthetic data with a clear shift
    data_shift = generate_synthetic_ili_series(
        n_weeks=N_SAMPLES + WINDOW_SIZE,
        seed=TEST_SEED,
        include_missing=False,
        include_constant=False,
        include_outliers=False,
        shift_start=WINDOW_SIZE,
        shift_magnitude=2.0  # Large shift
    )
    
    # Extract windows before and after shift
    X_shift = data_shift[:WINDOW_SIZE].values.reshape(-1, 1)
    Y_shift = data_shift[WINDOW_SIZE:2*WINDOW_SIZE].values.reshape(-1, 1)
    
    mmd_shift, p_shift, _ = mmd_permutation_test(X_shift, Y_shift, n_permutations=100, seed=TEST_SEED)
    
    # MMD should be larger for different distributions
    assert mmd_shift > mmd_same, f"MMD for shifted distribution should be larger than same, got {mmd_shift} vs {mmd_same}"
    # P-value should be low (significant)
    assert p_shift < 0.1, f"P-value for shifted distribution should be low, got {p_shift}"
    
    # Test Case 3: Verify MMD symmetry (MMD(X,Y) == MMD(Y,X))
    mmd_xy, _, _ = mmd_permutation_test(X_same, Y_same, n_permutations=10, seed=TEST_SEED)
    mmd_yx, _, _ = mmd_permutation_test(Y_same, X_same, n_permutations=10, seed=TEST_SEED)
    
    assert np.isclose(mmd_xy, mmd_yx), f"MMD should be symmetric: {mmd_xy} vs {mmd_yx}"
    
    # Test Case 4: Verify MMD is non-negative
    assert mmd_same >= 0, f"MMD should be non-negative, got {mmd_same}"
    assert mmd_shift >= 0, f"MMD should be non-negative, got {mmd_shift}"
    
    print(f"✓ MMD test passed: Same dist MMD={mmd_same:.4f} (p={p_same:.3f}), Shift dist MMD={mmd_shift:.4f} (p={p_shift:.3f})")

if __name__ == "__main__":
    test_mmd_stat_correctness()
    print("All MMD unit tests passed!")
