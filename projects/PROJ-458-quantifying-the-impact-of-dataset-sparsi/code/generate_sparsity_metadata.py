"""
Task T034: Generate sparsity subset metadata JSON files.

This script reads the sparsity subset definitions generated by T032 (sparsity_generation.py),
computes the SHA-256 checksum of the corresponding CSV files, and writes a metadata
JSON file for each subset to data/metadata/.

Output: data/metadata/sparsity_<level>_<seed>.json
Keys: seed, percentage, criteria, checksum
"""
import os
import sys
import json
import hashlib
import argparse
from pathlib import Path

# Add project root to path for imports
sys.path.insert(0, str(Path(__file__).parent))

from utils.logging import get_logger
from utils.checksum_utils import compute_sha256
from utils.data_models import SparsitySubset

logger = get_logger()

def load_rss_manifest(manifest_path: Path) -> list:
    """
    Load the manifest of generated subsets from sparsity_generation.py.
    Expected format: List of dicts or SparsitySubset-like objects.
    """
    if not manifest_path.exists():
        raise FileNotFoundError(f"Manifest not found: {manifest_path}")
    
    with open(manifest_path, 'r') as f:
        data = json.load(f)
    
    # Ensure it's a list
    if not isinstance(data, list):
        raise ValueError("Manifest must be a list of subset definitions.")
    
    return data

def generate_metadata(subset_def: dict, base_path: Path, output_dir: Path) -> dict:
    """
    Generate metadata for a single sparsity subset.
    
    Args:
        subset_def: Dictionary containing 'level', 'seed', 'percentage', 'filename', etc.
        base_path: Root directory where subset CSVs are stored (e.g., data/processed)
        output_dir: Directory to write the metadata JSON (e.g., data/metadata)
    
    Returns:
        The metadata dictionary that was written.
    """
    level = subset_def.get('level')
    seed = subset_def.get('seed')
    percentage = subset_def.get('percentage')
    filename = subset_def.get('filename')
    
    if not all([level, seed, percentage, filename]):
        raise ValueError(f"Missing required fields in subset definition: {subset_def}")
    
    # Construct path to the actual CSV file
    csv_path = base_path / filename
    if not csv_path.exists():
        raise FileNotFoundError(f"Subset CSV not found: {csv_path}")
    
    # Compute checksum
    checksum = compute_sha256(csv_path)
    
    # Define criteria based on task logic (Stratified by K-Means clusters + Formation Energy)
    criteria = "K-Means Stratified Sampling on Elemental Fingerprints + Formation Energy Bins"
    
    metadata = {
        "seed": seed,
        "percentage": percentage,
        "criteria": criteria,
        "checksum": checksum,
        "level": level,
        "filename": filename,
        "generated_at": None  # Can be populated if needed, but not strictly required by spec
    }
    
    # Ensure output directory exists
    output_dir.mkdir(parents=True, exist_ok=True)
    
    # Write metadata file
    # Format: sparsity_<level>_<seed>.json
    meta_filename = f"sparsity_{level}_{seed}.json"
    meta_path = output_dir / meta_filename
    
    with open(meta_path, 'w') as f:
        json.dump(metadata, f, indent=2)
    
    logger.info(f"Generated metadata: {meta_path}")
    return metadata

def main():
    parser = argparse.ArgumentParser(description="Generate sparsity subset metadata JSONs.")
    parser.add_argument(
        "--manifest",
        type=str,
        default="data/processed/sparsity_manifest.json",
        help="Path to the manifest file generated by sparsity_generation.py"
    )
    parser.add_argument(
        "--base_dir",
        type=str,
        default="data/processed",
        help="Base directory where subset CSVs are located"
    )
    parser.add_argument(
        "--output_dir",
        type=str,
        default="data/metadata",
        help="Directory to write metadata JSON files"
    )
    
    args = parser.parse_args()
    
    manifest_path = Path(args.manifest)
    base_path = Path(args.base_dir)
    output_path = Path(args.output_dir)
    
    logger.info(f"Loading manifest from {manifest_path}")
    try:
        subsets = load_rss_manifest(manifest_path)
    except FileNotFoundError as e:
        logger.error(str(e))
        sys.exit(1)
    
    logger.info(f"Found {len(subsets)} subsets to process.")
    
    processed_count = 0
    for subset_def in subsets:
        try:
            generate_metadata(subset_def, base_path, output_path)
            processed_count += 1
        except Exception as e:
            logger.error(f"Failed to generate metadata for subset {subset_def.get('level', 'unknown')}: {e}")
    
    logger.info(f"Successfully generated metadata for {processed_count}/{len(subsets)} subsets.")

if __name__ == "__main__":
    main()
