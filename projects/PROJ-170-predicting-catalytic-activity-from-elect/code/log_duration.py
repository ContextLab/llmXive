"""
T042b: Log duration to outputs/metrics.json.

Reads the start and end timestamps from outputs/pipeline_run.json (generated by T042a),
calculates the duration, and appends it to outputs/metrics.json.
"""
import json
import os
import sys
import logging
from pathlib import Path
from typing import Dict, Any, Optional

# Add project root to path if needed
project_root = Path(__file__).resolve().parent.parent
if str(project_root) not in sys.path:
    sys.path.insert(0, str(project_root))

from config import get_output_path
from logging_config import setup_logging, get_logger

def load_pipeline_run_info() -> Dict[str, Any]:
    """Load pipeline run timestamps from the run info file."""
    run_info_path = get_output_path("pipeline_run.json")
    
    if not os.path.exists(run_info_path):
        raise FileNotFoundError(
            f"Pipeline run info file not found at {run_info_path}. "
            "Ensure T042a (run full pipeline) has been completed successfully."
        )
    
    with open(run_info_path, 'r') as f:
        return json.load(f)

def load_metrics() -> Dict[str, Any]:
    """Load existing metrics or return empty dict if file doesn't exist."""
    metrics_path = get_output_path("metrics.json")
    
    if not os.path.exists(metrics_path):
        return {}
    
    with open(metrics_path, 'r') as f:
        return json.load(f)

def save_metrics(metrics: Dict[str, Any]) -> None:
    """Save metrics to the metrics file."""
    metrics_path = get_output_path("metrics.json")
    
    with open(metrics_path, 'w') as f:
        json.dump(metrics, f, indent=2)

def log_duration() -> Dict[str, Any]:
    """
    Calculate duration from pipeline run info and append to metrics.
    
    Returns:
        Updated metrics dictionary with duration information.
    """
    logger = get_logger(__name__)
    
    # Load pipeline run info
    run_info = load_pipeline_run_info()
    
    if "start_time" not in run_info or "end_time" not in run_info:
        raise ValueError(
            "Pipeline run info missing 'start_time' or 'end_time'. "
            "Ensure T042a logged these timestamps correctly."
        )
    
    start_time = run_info["start_time"]
    end_time = run_info["end_time"]
    
    # Calculate duration in seconds
    from datetime import datetime
    
    # Parse timestamps (assuming ISO format)
    start_dt = datetime.fromisoformat(start_time.replace('Z', '+00:00'))
    end_dt = datetime.fromisoformat(end_time.replace('Z', '+00:00'))
    
    duration_seconds = (end_dt - start_dt).total_seconds()
    duration_minutes = duration_seconds / 60.0
    duration_hours = duration_minutes / 60.0
    
    logger.info(f"Pipeline duration: {duration_seconds:.2f} seconds ({duration_minutes:.2f} minutes, {duration_hours:.2f} hours)")
    
    # Load existing metrics
    metrics = load_metrics()
    
    # Add duration information
    metrics["duration_seconds"] = duration_seconds
    metrics["duration_minutes"] = duration_minutes
    metrics["duration_hours"] = duration_hours
    metrics["pipeline_start_time"] = start_time
    metrics["pipeline_end_time"] = end_time
    
    # Save updated metrics
    save_metrics(metrics)
    
    logger.info(f"Duration logged to {get_output_path('metrics.json')}")
    
    return metrics

def main() -> None:
    """Main entry point for T042b."""
    setup_logging()
    logger = get_logger(__name__)
    
    try:
        logger.info("Starting T042b: Log duration to outputs/metrics.json")
        metrics = log_duration()
        logger.info("T042b completed successfully")
        print(json.dumps(metrics, indent=2))
    except Exception as e:
        logger.error(f"T042b failed: {str(e)}")
        raise

if __name__ == "__main__":
    main()
