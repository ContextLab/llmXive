def load_aligned_dataset() -> pd.DataFrame:
    """Load the aligned dataset generated by T020."""
    data_path = get_data_path()
    file_path = data_path / "processed" / "aligned_dataset.csv"
    if not file_path.exists():
        raise FileNotFoundError(f"Aligned dataset not found at {file_path}. Run T020 first.")
    logger.info(f"Loading aligned dataset from {file_path}")
    df = pd.read_csv(file_path)
    return df

def get_feature_columns(df: pd.DataFrame) -> List[str]:
    """Identify feature columns (exclude target and metadata)."""
    # Based on T020 schema: composition, surface_facet, energy_change, d_band_center, adsorption_energy
    # Target is 'energy_change'. Metadata: 'composition', 'surface_facet'.
    # Features are the remaining numeric columns.
    exclude_cols = ['energy_change', 'composition', 'surface_facet']
    features = [col for col in df.columns if col not in exclude_cols]
    return features

def stratified_split(df: pd.DataFrame, features: List[str], target: str = 'energy_change') -> Tuple[pd.DataFrame, pd.DataFrame, pd.DataFrame, pd.DataFrame]:
    """Split data into train and test sets."""
    # Since energy_change is continuous, we bin it for stratification if needed,
    # or just use a simple random split if the dataset is large enough and balanced.
    # For this task, we assume the dataset is pre-processed and ready.
    # Using a simple train/test split for the outer loop.
    # To ensure stratification on continuous targets, we can use KFold with shuffling.
    # However, the task asks for "stratified" split. We will use a quantile-based stratification.
    df_shuffled = df.sample(frac=1, random_state=42).reset_index(drop=True)
    
    # Create bins for stratification
    df_shuffled['target_bin'] = pd.qcut(df_shuffled[target], q=10, duplicates='drop')
    
    train_df, test_df = train_test_split(
        df_shuffled, 
        test_size=0.2, 
        stratify=df_shuffled['target_bin'], 
        random_state=42
    )
    return train_df, test_df, df_shuffled[features], df_shuffled[target]

def train_linear_baseline(X_train: pd.DataFrame, y_train: pd.Series, X_test: pd.DataFrame, y_test: pd.Series) -> Tuple[LinearRegression, float, float]:
    """Train a linear baseline model."""
    logger.info("Training Linear Baseline (d_band_center + adsorption_energy)")
    # The task description for T025 says "using only d_band_center and adsorption_energy"
    # But the data might have been scaled or transformed. We use the features available in X_train
    # assuming the baseline uses a subset or all features if not specified otherwise.
    # However, to be precise to T025, we filter features.
    baseline_features = ['d_band_center', 'adsorption_energy']
    available_baseline = [f for f in baseline_features if f in X_train.columns]
    
    if not available_baseline:
        logger.warning("Baseline features not found. Using all features.")
        available_baseline = X_train.columns.tolist()
    
    X_train_base = X_train[available_baseline]
    X_test_base = X_test[available_baseline]
    
    model = LinearRegression()
    model.fit(X_train_base, y_train)
    
    y_pred = model.predict(X_test_base)
    r2 = r2_score(y_test, y_pred)
    mae = mean_absolute_error(y_test, y_pred)
    
    logger.info(f"Linear Baseline - R2: {r2:.4f}, MAE: {mae:.4f}")
    return model, r2, mae

def train_xgboost_nested_cv(X_train: pd.DataFrame, y_train: pd.Series) -> Tuple[xgb.XGBRegressor, Dict[str, Any]]:
    """
    Train XGBoost with nested cross-validation.
    Inner loop: Grid search over max_depth {3,5,7}, lr {0.01, 0.1}, n_est <= 200.
    Outer loop: R2 evaluation.
    """
    logger.info("Starting Nested Cross-Validation for XGBoost")
    
    # Define the parameter grid
    param_grid = {
        'max_depth': [3, 5, 7],
        'learning_rate': [0.01, 0.1],
        'n_estimators': [50, 100, 200]  # <= 200 as per spec
    }
    
    # Base XGBoost model
    base_model = xgb.XGBRegressor(
        objective='reg:squarederror',
        random_state=42,
        n_jobs=-1,
        verbosity=0
    )
    
    # Inner Loop: Grid Search CV
    # We use 3-fold CV for the inner loop to keep it fast
    inner_cv = KFold(n_splits=3, shuffle=True, random_state=42)
    
    grid_search = GridSearchCV(
        estimator=base_model,
        param_grid=param_grid,
        cv=inner_cv,
        scoring='r2',
        n_jobs=-1,
        verbose=1
    )
    
    logger.info("Running Grid Search (Inner Loop)...")
    grid_search.fit(X_train, y_train)
    
    best_params = grid_search.best_params_
    best_score_inner = grid_search.best_score_
    logger.info(f"Best Inner CV Score: {best_score_inner:.4f} with params: {best_params}")
    
    # Outer Loop: Evaluate the best model using Cross-Validation on the training set
    # This simulates the performance on unseen data for the final model selection
    outer_cv = KFold(n_splits=5, shuffle=True, random_state=42)
    
    # Train the best model on the full training set to get the final estimator
    final_model = xgb.XGBRegressor(
        **best_params,
        objective='reg:squarederror',
        random_state=42,
        n_jobs=-1,
        verbosity=0
    )
    
    # Perform outer CV evaluation
    outer_scores = cross_val_score(final_model, X_train, y_train, cv=outer_cv, scoring='r2')
    outer_r2_mean = np.mean(outer_scores)
    outer_r2_std = np.std(outer_scores)
    
    logger.info(f"Outer CV R2 (Mean): {outer_r2_mean:.4f} (+/- {outer_r2_std:.4f})")
    
    # Retrain the final model on the entire training set
    final_model.fit(X_train, y_train)
    
    results = {
        "best_params": best_params,
        "inner_cv_r2": best_score_inner,
        "outer_cv_r2_mean": float(outer_r2_mean),
        "outer_cv_r2_std": float(outer_r2_std)
    }
    
    return final_model, results

def save_split_metadata(train_df: pd.DataFrame, test_df: pd.DataFrame, split_info: Dict[str, Any]):
    """Save metadata about the split."""
    output_path = get_output_path()
    output_path.mkdir(parents=True, exist_ok=True)
    metadata_file = output_path / "split_metadata.json"
    
    split_info['train_size'] = len(train_df)
    split_info['test_size'] = len(test_df)
    split_info['train_indices'] = train_df.index.tolist()
    split_info['test_indices'] = test_df.index.tolist()
    
    with open(metadata_file, 'w') as f:
        json.dump(split_info, f, indent=2)
    logger.info(f"Saved split metadata to {metadata_file}")

def save_model(model: Any, metrics: Dict[str, Any], model_path: str):
    """Save the trained model and its metrics."""
    model_file = Path(model_path)
    model_file.parent.mkdir(parents=True, exist_ok=True)
    
    # XGBoost models can be saved as JSON or binary
    # We save the booster object as JSON for inspection
    if hasattr(model, 'save_model'):
        model.save_model(str(model_file.with_suffix('.json')))
        logger.info(f"Saved XGBoost model to {model_file.with_suffix('.json')}")
    
    # Save metrics and params separately for easy loading
    metrics_file = model_file.with_suffix('.metrics.json')
    with open(metrics_file, 'w') as f:
        json.dump(metrics, f, indent=2)
    logger.info(f"Saved model metrics to {metrics_file}")
    
    # Also save as joblib for sklearn compatibility if needed
    pkl_file = model_file.with_suffix('.pkl')
    joblib.dump(model, str(pkl_file))
    logger.info(f"Saved model pickle to {pkl_file}")

def main():
    """Main execution for T026."""
    # 1. Load Data
    df = load_aligned_dataset()
    features = get_feature_columns(df)
    target = 'energy_change'
    
    logger.info(f"Dataset loaded: {len(df)} rows, {len(features)} features")
    
    # 2. Split Data
    train_df, test_df, X_all, y_all = stratified_split(df, features, target)
    X_train = train_df[features]
    y_train = train_df[target]
    X_test = test_df[features]
    y_test = test_df[target]
    
    logger.info(f"Train size: {len(X_train)}, Test size: {len(X_test)}")
    
    # 3. Train Linear Baseline (T025 dependency)
    # Note: T025 is marked completed, but we implement the logic here to ensure
    # we have the baseline metrics for comparison in the final report.
    # The task T026 specifically asks for XGBoost, but the pipeline needs the baseline.
    linear_model, linear_r2, linear_mae = train_linear_baseline(X_train, y_train, X_test, y_test)
    
    # 4. Train XGBoost with Nested CV (T026 Core)
    xgb_model, cv_results = train_xgboost_nested_cv(X_train, y_train)
    
    # 5. Evaluate XGBoost on Test Set
    y_pred_xgb = xgb_model.predict(X_test)
    xgb_r2 = r2_score(y_test, y_pred_xgb)
    xgb_mae = mean_absolute_error(y_test, y_pred_xgb)
    
    logger.info(f"XGBoost Test - R2: {xgb_r2:.4f}, MAE: {xgb_mae:.4f}")
    
    # 6. Save Model (FR-004)
    # Path: code/models/best_xgboost.json
    project_root = get_project_root()
    model_path = project_root / "code" / "models" / "best_xgboost"
    
    model_metrics = {
        "model_type": "XGBoost",
        "cv_results": cv_results,
        "test_r2": xgb_r2,
        "test_mae": xgb_mae,
        "baseline_r2": linear_r2,
        "baseline_mae": linear_mae
    }
    
    save_model(xgb_model, model_metrics, str(model_path))
    
    # 7. Save Split Metadata
    split_info = {
        "method": "stratified_k_bins",
        "random_state": 42,
        "test_ratio": 0.2
    }
    save_split_metadata(train_df, test_df, split_info)
    
    logger.info("T026 Completed Successfully.")
    print(f"XGBoost Model Saved to: {model_path}.json")
    print(f"Test R2: {xgb_r2:.4f}, Test MAE: {xgb_mae:.4f}")

if __name__ == "__main__":
    main()
