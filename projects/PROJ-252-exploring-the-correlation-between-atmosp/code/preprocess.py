def load_raw_earthquake_data(raw_path: Optional[Path] = None) -> pd.DataFrame:
    """
    Load raw earthquake data from the downloaded JSON or CSV file.
    Expects data generated by code/download.py.
    """
    if raw_path is None:
        raw_path = get_data_path() / "raw" / "usgs_test_subset.json"
    
    if not raw_path.exists():
        logger.error(f"Raw earthquake data not found at {raw_path}")
        raise FileNotFoundError(f"Raw earthquake data not found at {raw_path}")
    
    logger.info(f"Loading raw earthquake data from {raw_path}")
    
    # Handle JSON format typically returned by USGS query
    if raw_path.suffix == '.json':
        with open(raw_path, 'r') as f:
            data = json.load(f)
        
        features = data.get('features', [])
        records = []
        for feat in features:
            props = feat.get('properties', {})
            coords = feat.get('geometry', {}).get('coordinates', [None, None, None])
            records.append({
                'event_id': props.get('id'),
                'magnitude': props.get('mag'),
                'depth': coords[2] if len(coords) > 2 else None,
                'lat': coords[1],
                'lon': coords[0],
                'timestamp': props.get('time'),
                'place': props.get('place')
            })
        df = pd.DataFrame(records)
    else:
        df = pd.read_csv(raw_path)
    
    logger.info(f"Loaded {len(df)} earthquake records")
    return df

