logger = get_logger(__name__)

def load_study_count_from_json(input_path: Path) -> int:
    """
    Load the study count from the meta-analysis result JSON generated by T014.
    
    Args:
        input_path: Path to the meta-analysis result JSON file.
        
    Returns:
        The number of studies (N).
    """
    if not input_path.exists():
        raise FileNotFoundError(f"Input file not found: {input_path}")
        
    with open(input_path, 'r', encoding='utf-8') as f:
        data = json.load(f)
        
    # The study count is typically stored in the root or a specific key depending on T014 output
    # Assuming T014 outputs a structure with 'study_count' at the root or 'meta_analysis' -> 'study_count'
    count = data.get('study_count')
    if count is None:
        # Fallback for nested structures if T014 wraps it
        meta_section = data.get('meta_analysis', {})
        count = meta_section.get('study_count')
        
    if count is None:
        raise ValueError("Could not find 'study_count' in input JSON.")
        
    return int(count)

def load_effect_sizes_and_se(input_path: Path) -> Tuple[List[float], List[float]]:
    """
    Load effect sizes (r) and standard errors (se) from the meta-analysis result JSON.
    
    Args:
        input_path: Path to the meta-analysis result JSON file.
        
    Returns:
        A tuple of (list of effect sizes, list of standard errors).
    """
    if not input_path.exists():
        raise FileNotFoundError(f"Input file not found: {input_path}")
        
    with open(input_path, 'r', encoding='utf-8') as f:
        data = json.load(f)
        
    # Extract studies list. Assuming T014 outputs a 'studies' list or similar.
    studies = data.get('studies', [])
    if not studies:
        # Try nested if T014 wraps it
        studies = data.get('meta_analysis', {}).get('studies', [])
        
    if not studies:
        raise ValueError("No studies found in input JSON.")
        
    effects = []
    ses = []
    
    for study in studies:
        # Ensure we have valid numeric data
        r = study.get('effect_size_r')
        se = study.get('standard_error_se')
        
        if r is not None and se is not None:
            try:
                effects.append(float(r))
                ses.append(float(se))
            except (ValueError, TypeError):
                logger.warning(f"Skipping study with invalid numeric data: {study}")
                
    if len(effects) == 0:
        raise ValueError("No valid effect sizes found in input JSON.")
        
    return effects, ses

def calculate_i_squared(effects: List[float], ses: List[float]) -> float:
    """
    Calculate the I² statistic (Higgins & Thompson, 2002).
    
    I² = max(0, (Q - df) / Q) * 100
    where:
      Q = Cochran's heterogeneity statistic
      df = degrees of freedom = k - 1
      k = number of studies
    
    Args:
        effects: List of effect sizes (r).
        ses: List of standard errors.
        
    Returns:
        I² statistic as a percentage.
    """
    k = len(effects)
    if k < 2:
        # Cannot calculate heterogeneity with less than 2 studies
        return 0.0
        
    df = k - 1
    
    # Calculate weights (inverse variance)
    # w_i = 1 / (se_i^2)
    weights = np.array([1.0 / (se ** 2) if se > 0 else 0.0 for se in ses])
    
    # Handle case where se is 0 (infinite weight) - usually implies no variance, 
    # but in meta-analysis this can cause issues. We'll clamp or skip if all are 0.
    if np.all(weights == 0):
        logger.warning("All standard errors are zero or invalid. Cannot calculate I².")
        return 0.0
        
    # Normalize weights if necessary, though standard calculation uses raw inverse variance
    # Calculate weighted mean effect
    sum_w = np.sum(weights)
    if sum_w == 0:
        logger.warning("Sum of weights is zero. Cannot calculate I².")
        return 0.0
        
    weighted_mean = np.sum(np.array(effects) * weights) / sum_w
    
    # Calculate Q statistic
    # Q = sum(w_i * (y_i - mean)^2)
    Q = np.sum(weights * (np.array(effects) - weighted_mean) ** 2)
    
    # Calculate I²
    # I² = max(0, (Q - df) / Q) * 100
    if Q <= df:
        i_squared = 0.0
    else:
        i_squared = ((Q - df) / Q) * 100.0
        
    return i_squared

def run_heterogeneity_analysis(
    input_json_path: Path,
    output_json_path: Path,
    study_count: Optional[int] = None
) -> Dict[str, Any]:
    """
    Run the full heterogeneity analysis (I² calculation).
    
    This function reads the meta-analysis results, calculates I², and appends the result
    to the output JSON.
    
    Args:
        input_json_path: Path to the input JSON from T014/T021.
        output_json_path: Path to write the updated JSON.
        study_count: Optional pre-loaded study count. If None, it is loaded from the file.
        
    Returns:
        The updated result dictionary.
    """
    # Load data
    if study_count is None:
        study_count = load_study_count_from_json(input_json_path)
        
    # Load effect sizes and SEs
    effects, ses = load_effect_sizes_and_se(input_json_path)
    
    # Calculate I²
    i_squared = calculate_i_squared(effects, ses)
    
    # Format to exactly two decimal places as per SC-002
    i_squared_formatted = round(i_squared, 2)
    
    # Prepare result
    result = {
        "heterogeneity": {
            "i_squared": i_squared_formatted,
            "i_squared_raw": i_squared, # Keep raw for debugging if needed
            "k": len(effects),
            "df": len(effects) - 1 if len(effects) >= 2 else 0,
            "calculation_method": "Higgins-Thompson"
        }
    }
    
    logger.info(f"I² calculated: {i_squared_formatted}%")
    
    return result

def update_output_json(
    input_path: Path,
    output_path: Path,
    heterogeneity_result: Dict[str, Any]
) -> None:
    """
    Load the existing output JSON, update it with heterogeneity results, and save.
    
    This ensures atomic updates to the shared artifact.
    """
    if input_path.exists():
        with open(input_path, 'r', encoding='utf-8') as f:
            data = json.load(f)
    else:
        data = {}
        
    # Merge heterogeneity result
    data.update(heterogeneity_result)
    
    # Ensure output directory exists
    output_path.parent.mkdir(parents=True, exist_ok=True)
    
    with open(output_path, 'w', encoding='utf-8') as f:
        json.dump(data, f, indent=2)
        
    logger.info(f"Heterogeneity results written to {output_path}")

def main() -> int:
    """
    Main entry point for the heterogeneity analysis script.
    
    Usage:
        python code/analysis/heterogeneity.py --input data/derived/meta_analysis_result.json --output data/derived/meta_analysis_result.json
    
    Note: This script updates the same file to ensure atomicity with T021.
    """
    import argparse
    
    parser = argparse.ArgumentParser(description="Calculate I² heterogeneity statistic.")
    parser.add_argument(
        "--input", 
        type=Path, 
        required=True, 
        help="Path to the input meta-analysis JSON (from T014/T021)."
    )
    parser.add_argument(
        "--output", 
        type=Path, 
        required=True, 
        help="Path to write the updated meta-analysis JSON."
    )
    
    args = parser.parse_args()
    
    try:
        # Run analysis
        hetero_result = run_heterogeneity_analysis(
            input_json_path=args.input,
            output_json_path=args.output
        )
        
        # Update the file
        update_output_json(args.input, args.output, hetero_result)
        
        logger.info("Heterogeneity analysis completed successfully.")
        return 0
        
    except FileNotFoundError as e:
        logger.error(f"File not found: {e}")
        return 1
    except ValueError as e:
        logger.error(f"Data error: {e}")
        return 1
    except Exception as e:
        logger.error(f"Unexpected error: {e}")
        import traceback
        traceback.print_exc()
        return 1

if __name__ == "__main__":
    sys.exit(main())