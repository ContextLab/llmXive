"""
Handles the zero-studies edge case for the narrative synthesis.

This module is invoked when the study count is 0. It generates a specific
narrative summary indicating that no studies were found, adhering to the
project's fallback protocols.
"""
import json
import logging
import sys
from datetime import datetime
from pathlib import Path
from typing import Dict, Any, Optional

# Import shared utilities from the project's established API surface
from utils.logger import get_logger
from utils.config import get_project_root, ensure_directory

logger = get_logger(__name__)

def load_study_count(study_count_path: Path) -> int:
    """
    Loads the study count from the JSON file generated by T014a.
    
    Args:
        study_count_path: Path to data/processed/study_count.json
        
    Returns:
        The integer study count N.
        
    Raises:
        FileNotFoundError: If the study count file does not exist.
        json.JSONDecodeError: If the file contains invalid JSON.
    """
    if not study_count_path.exists():
        raise FileNotFoundError(f"Study count file not found: {study_count_path}")
        
    with open(study_count_path, 'r', encoding='utf-8') as f:
        data = json.load(f)
        
    return int(data.get('N', 0))

def generate_zero_studies_summary(output_path: Path) -> None:
    """
    Generates the narrative summary for the case where N=0.
    
    This function creates a Markdown file with a specific header and a JSON
    metadata block as mandated by the task requirements for T015/T018.
    
    Args:
        output_path: Path where data/derived/narrative_summary.md will be written.
    """
    logger.info(f"Generating zero-studies narrative summary at {output_path}")
    
    # Ensure the output directory exists
    ensure_directory(output_path)
    
    metadata = {
        "study_count": 0,
        "synthesis_mode": "narrative",
        "timestamp": datetime.utcnow().isoformat() + "Z"
    }
    
    content_lines = [
        "# No studies found",
        "",
        "```json",
        json.dumps(metadata, indent=2),
        "```",
        "",
        "## Study Overview",
        "",
        "The systematic review process identified zero eligible studies matching the inclusion criteria.",
        "No quantitative meta-analysis or qualitative thematic synthesis could be performed.",
        "",
        "## Limitations",
        "",
        "- **Sample Size**: N = 0. The dataset is insufficient for any statistical inference.",
        "- **Generalizability**: No conclusions can be drawn regarding the correlation between structural brain connectivity and music preferences.",
        "- **Systematic Review Fallback**: As per Constitution Principle VII, this report serves as the systematic review fallback.",
        "",
        "## Data Insufficient Disclaimer",
        "",
        "The analysis could not proceed due to a lack of input data. Please verify the data ingestion pipeline (T013) and the source data availability."
    ]
    
    with open(output_path, 'w', encoding='utf-8') as f:
        f.write('\n'.join(content_lines))
        
    logger.info(f"Successfully wrote zero-studies summary to {output_path}")

def run_zero_case_handler(study_count_path: Path, output_path: Path) -> Dict[str, Any]:
    """
    Orchestrates the zero-studies handling workflow.
    
    1. Verifies the study count is 0.
    2. Generates the summary file.
    3. Returns a status dictionary.
    
    Args:
        study_count_path: Path to the study count JSON.
        output_path: Path for the output narrative summary.
        
    Returns:
        A dictionary with status and metadata.
    """
    try:
        count = load_study_count(study_count_path)
        if count != 0:
            logger.warning(f"Study count is {count}, not 0. Zero-case handler should not be invoked.")
            return {
                "status": "skipped",
                "reason": f"Study count is {count}, expected 0"
            }
        
        generate_zero_studies_summary(output_path)
        
        return {
            "status": "completed",
            "study_count": 0,
            "output_file": str(output_path),
            "synthesis_mode": "narrative"
        }
        
    except FileNotFoundError as e:
        logger.error(f"Failed to load study count: {e}")
        raise
    except Exception as e:
        logger.error(f"Unexpected error in zero-case handler: {e}")
        raise

def main():
    """
    Entry point for the zero-studies edge case handler.
    """
    project_root = get_project_root()
    study_count_path = project_root / "data" / "processed" / "study_count.json"
    output_path = project_root / "data" / "derived" / "narrative_summary.md"
    
    if len(sys.argv) > 1:
        # Allow override for testing
        study_count_path = Path(sys.argv[1])
    if len(sys.argv) > 2:
        output_path = Path(sys.argv[2])
        
    try:
        result = run_zero_case_handler(study_count_path, output_path)
        print(json.dumps(result, indent=2))
    except Exception as e:
        print(json.dumps({"status": "failed", "error": str(e)}), file=sys.stderr)
        sys.exit(1)

if __name__ == "__main__":
    main()