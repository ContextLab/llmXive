logger = logging.getLogger(__name__)

def load_binned_accuracy_data(csv_path: Path) -> dict:
    """
    Load binned accuracy data from a CSV file generated by stratify_accuracy.py.
    Expected columns: bin, accuracy, count, lower_bound, upper_bound
    """
    data = defaultdict(dict)
    if not csv_path.exists():
        logger.error(f"Binned accuracy file not found: {csv_path}")
        return data

    with open(csv_path, 'r', newline='', encoding='utf-8') as f:
        reader = csv.DictReader(f)
        for row in reader:
            bin_name = row['bin']
            data[bin_name] = {
                'accuracy': float(row['accuracy']),
                'count': int(row['count']),
                'lower_bound': float(row['lower_bound']) if row['lower_bound'] else None,
                'upper_bound': float(row['upper_bound']) if row['upper_bound'] else None
            }
    logger.info(f"Loaded binned accuracy data for {len(data)} bins from {csv_path}")
    return data

def load_raw_annotated_data(csv_path: Path) -> list:
    """
    Load raw annotated data from the CSV generated by annotate_graph.py.
    Returns a list of dicts with 'chain_length' and 'correctness'.
    """
    data = []
    if not csv_path.exists():
        logger.error(f"Raw annotated file not found: {csv_path}")
        return data

    with open(csv_path, 'r', newline='', encoding='utf-8') as f:
        reader = csv.DictReader(f)
        for row in reader:
            try:
                chain_length = int(row['chain_length'])
                # Handle potential non-integer or unresolvable values if any
                if row.get('correctness') in ['1', 'True', 'true', 'yes']:
                    correctness = 1.0
                elif row.get('correctness') in ['0', 'False', 'false', 'no', '']:
                    correctness = 0.0
                else:
                    # Skip rows with ambiguous correctness or unresolvable chain lengths
                    continue
                
                data.append({
                    'chain_length': chain_length,
                    'correctness': correctness
                })
            except (ValueError, KeyError):
                # Skip malformed rows
                continue
    
    logger.info(f"Loaded {len(data)} valid records from {csv_path}")
    return data

def plot_binned_accuracy(binned_data: dict, output_path: Path, title: str = "Accuracy by Hop Bin"):
    """
    Generate a bar plot of accuracy vs. hop bin.
    """
    if not binned_data:
        logger.warning("No binned data to plot.")
        return

    bins = sorted(binned_data.keys(), key=lambda x: (len(x), x)) # Sort by length then alpha to handle '3+' correctly
    # Custom sort to ensure order: '1', '2', '3+', etc.
    def sort_key(b):
        if b == '3+': return (3, 0)
        try:
            return (int(b), 0)
        except ValueError:
            return (99, 0)
    
    bins = sorted(bins, key=sort_key)
    
    accuracies = [binned_data[b]['accuracy'] for b in bins]
    counts = [binned_data[b]['count'] for b in bins]

    plt.figure(figsize=(10, 6))
    bars = plt.bar(bins, accuracies, color='skyblue', edgecolor='black', alpha=0.8)
    
    # Add count labels on top of bars
    for bar, count in zip(bars, counts):
        height = bar.get_height()
        plt.text(bar.get_x() + bar.get_width()/2., height + 0.01,
                 f'{count}', ha='center', va='bottom', fontsize=10, fontweight='bold')

    plt.xlabel('Hop Bin', fontsize=12)
    plt.ylabel('Accuracy', fontsize=12)
    plt.title(title, fontsize=14)
    plt.ylim(0, 1.05)
    plt.grid(axis='y', linestyle='--', alpha=0.7)
    
    ensure_dir(output_path.parent)
    plt.savefig(output_path, dpi=300, bbox_inches='tight')
    plt.close()
    logger.info(f"Binned accuracy plot saved to {output_path}")

def plot_continuous_accuracy(raw_data: list, output_path: Path, title: str = "Accuracy vs. Exact Chain Length (LOESS)"):
    """
    Generate a scatter plot with a smoothed trend line (LOESS) of accuracy vs. exact chain_length.
    Uses seaborn's regplot with lowess=True for smoothing.
    """
    if not raw_data:
        logger.warning("No raw data to plot for continuous analysis.")
        return

    chain_lengths = [d['chain_length'] for d in raw_data]
    correct = [d['correctness'] for d in raw_data]

    # Convert to numpy arrays for seaborn
    import numpy as np
    x = np.array(chain_lengths)
    y = np.array(correct)

    plt.figure(figsize=(10, 6))
    
    # Scatter plot of individual points (jittered slightly for visibility if needed, but raw is fine)
    # We use a low alpha to handle overplotting
    plt.scatter(x, y, alpha=0.15, color='gray', s=10, label='Individual Records')

    # Smoothed trend line using LOESS (Lowess) via seaborn regplot
    # lowess=True triggers the smoothing
    sns.regplot(x=x, y=y, scatter=False, lowess=True, color='red', linewidth=2.5, label='LOESS Trend')

    plt.xlabel('Exact Chain Length (Hops)', fontsize=12)
    plt.ylabel('Accuracy', fontsize=12)
    plt.title(title, fontsize=14)
    plt.ylim(-0.05, 1.05)
    plt.grid(axis='both', linestyle='--', alpha=0.7)
    plt.legend()
    
    ensure_dir(output_path.parent)
    plt.savefig(output_path, dpi=300, bbox_inches='tight')
    plt.close()
    logger.info(f"Continuous accuracy plot saved to {output_path}")

def main():
    project_root = get_project_root()
    data_dir = get_path(project_root, 'data/processed')
    figures_dir = get_path(project_root, 'figures')
    
    # Input files
    binned_csv = data_dir / 'binned_accuracy.csv' # Output from T019
    raw_csv = data_dir / 'annotated_videokr.csv'   # Output from T013
    
    # Output files
    binned_plot_path = figures_dir / 'accuracy_by_bin.png'
    continuous_plot_path = figures_dir / 'accuracy_continuous_trend.png'

    # Ensure output directories exist
    ensure_dir(data_dir)
    ensure_dir(figures_dir)

    # Load data
    binned_data = load_binned_accuracy_data(binned_csv)
    raw_data = load_raw_annotated_data(raw_csv)

    if not raw_data:
        logger.error("Cannot proceed with plotting: No raw annotated data found.")
        sys.exit(1)

    # Generate Plots
    plot_binned_accuracy(binned_data, binned_plot_path)
    plot_continuous_accuracy(raw_data, continuous_plot_path)

    logger.info("All plots generated successfully.")

if __name__ == '__main__':
    main()