def load_effect_sizes() -> pd.DataFrame:
    """
    Loads the effect sizes and p-values generated by the models.py script.
    Expects data/processed/effect_sizes.csv to exist.
    """
    path = Path("data/processed/effect_sizes.csv")
    if not path.exists():
        raise FileNotFoundError(f"Required input file not found: {path}")
    df = pd.read_csv(path)
    return df

def load_sensitivity_stats() -> pd.DataFrame:
    """
    Loads the sensitivity analysis results to extract stability metrics.
    Expects data/processed/sensitivity_results.csv or similar intermediate data.
    """
    # T032 generates intermediate data, T033/T034 generate reports.
    # We need to extract the specific stats for the final CSV.
    # Assuming calculate_stability and calculate_flip_rate produce CSVs or we load the raw sensitivity data.
    # Let's try to load the raw sensitivity data first if available, otherwise derive from reports.
    
    # Based on T032/T033/T034 descriptions:
    # T033: save_stability_report -> likely data/processed/significance_stability_report.csv
    # T034: save_flip_rate_report -> likely data/processed/significance_flip_rate_report.csv
    
    stability_path = Path("data/processed/significance_stability_report.csv")
    flip_path = Path("data/processed/significance_flip_rate_report.csv")
    
    stats = {}
    
    if stability_path.exists():
        df_stab = pd.read_csv(stability_path)
        # Expecting a row with the aggregate metric
        if not df_stab.empty:
            stats['significance_stability'] = df_stab.iloc[0].get('stability_rate', df_stab.iloc[0].get('significance_stability', np.nan))
    
    if flip_path.exists():
        df_flip = pd.read_csv(flip_path)
        if not df_flip.empty:
            stats['significance_flip_rate'] = df_flip.iloc[0].get('flip_rate', df_flip.iloc[0].get('significance_flip_rate', np.nan))
    
    return pd.DataFrame([stats])

def merge_metrics(effect_df: pd.DataFrame, sensitivity_df: pd.DataFrame) -> pd.DataFrame:
    """
    Combines effect sizes and sensitivity statistics into a single report.
    """
    # The effect_df likely has rows per comparison (e.g., Immediate vs Delayed).
    # The sensitivity_df is a single row of aggregate stats.
    # We merge them to create a wide format or append columns.
    
    # Ensure sensitivity_df has the right columns
    if sensitivity_df.empty:
        warning("Sensitivity stats could not be loaded. Filling with NaN.")
        sensitivity_df = pd.DataFrame([{'significance_stability': np.nan, 'significance_flip_rate': np.nan}])

    # If effect_df has multiple rows (pairwise comparisons), we might want to repeat the sensitivity stats
    # or create a summary row. The task asks for "results_metrics.csv" with effect sizes AND sensitivity stats.
    # A common pattern is to have one row per comparison, plus the global sensitivity metrics repeated,
    # OR a separate summary. Let's join them so every comparison row has the global stats.
    
    result = effect_df.copy()
    
    # Add stability and flip rate columns to every row
    for col in ['significance_stability', 'significance_flip_rate']:
        if col in sensitivity_df.columns:
            result[col] = sensitivity_df[col].iloc[0]
        else:
            result[col] = np.nan
    
    return result

def save_results_metrics(df: pd.DataFrame, output_path: str):
    """
    Saves the final merged metrics to the specified CSV path.
    """
    Path(output_path).parent.mkdir(parents=True, exist_ok=True)
    df.to_csv(output_path, index=False)
    info(f"Results metrics saved to {output_path}")

def main():
    logger = get_logger(__name__)
    info("Starting T035: Generate results_metrics.csv")
    
    try:
        # 1. Load Effect Sizes (from T030/T031 output)
        # Assuming models.py or posthoc_tukey.py saved this to effect_sizes.csv
        # If not, we might need to re-run extraction, but T030/31 should have done it.
        # Let's assume the path convention: data/processed/effect_sizes.csv
        effect_df = load_effect_sizes()
        info(f"Loaded {len(effect_df)} effect size records.")

        # 2. Load Sensitivity Stats (from T033/T034 output)
        sensitivity_df = load_sensitivity_stats()
        info(f"Loaded sensitivity metrics: {sensitivity_df.to_dict('records')}")

        # 3. Merge
        final_df = merge_metrics(effect_df, sensitivity_df)

        # 4. Save
        output_path = "data/processed/results_metrics.csv"
        save_results_metrics(final_df, output_path)
        
        info("T035 completed successfully.")
        return 0

    except FileNotFoundError as e:
        error(f"Missing required input file: {e}")
        error("Ensure T030, T031, T033, and T034 have been completed successfully.")
        return 1
    except Exception as e:
        error(f"Unexpected error during T035 execution: {e}")
        raise

if __name__ == "__main__":
    sys.exit(main())