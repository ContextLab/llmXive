"""
Task T036: Generate significance_stability_report.csv
Explicitly documents the stability metric and flip rate as required by FR-007 and SC-003.

Inputs:
  - data/processed/results_metrics.csv (generated by T035, contains effect sizes, p-values, stability stats)

Outputs:
  - data/processed/significance_stability_report.csv (explicit stability and flip rate documentation)
"""
import os
import sys
import pandas as pd
import numpy as np
from pathlib import Path
from logging_config import get_logger, info, warning, error
from config import load_config

# Initialize logger
logger = get_logger(__name__)

def load_results_metrics():
    """Load the results metrics file generated by T035."""
    config = load_config()
    base_dir = Path(config.get("project_root", "."))
    input_path = base_dir / "data" / "processed" / "results_metrics.csv"
    
    if not input_path.exists():
        error(f"Input file not found: {input_path}")
        raise FileNotFoundError(f"Input file not found: {input_path}")
    
    df = pd.read_csv(input_path)
    logger.info(f"Loaded results metrics from {input_path} with {len(df)} rows.")
    return df

def generate_stability_report(df_metrics):
    """
    Generate the explicit stability and flip rate report.
    
    Expected columns in df_metrics (from T035):
      - stability_metric: Proportion of shifts where p < 0.05
      - flip_rate: Proportion of shifts where the conclusion changes
      - (Optional) other metrics like effect_size, p_value, etc.
    
    Output columns:
      - metric_name: Name of the metric (e.g., 'significance_stability', 'significance_flip_rate')
      - metric_value: The calculated value
      - description: Human-readable description
      - source_task: Task ID that generated the underlying calculation (T033, T034)
    """
    records = []

    # Extract stability metric
    if 'stability_metric' in df_metrics.columns:
        stability_val = df_metrics['stability_metric'].iloc[0]
        records.append({
            "metric_name": "significance_stability",
            "metric_value": float(stability_val),
            "description": "Proportion of sensitivity boundary shifts where the p-value remained < 0.05.",
            "source_task": "T033"
        })
    else:
        warning("Column 'stability_metric' not found in input file.")

    # Extract flip rate
    if 'flip_rate' in df_metrics.columns:
        flip_val = df_metrics['flip_rate'].iloc[0]
        records.append({
            "metric_name": "significance_flip_rate",
            "metric_value": float(flip_val),
            "description": "Proportion of sensitivity boundary shifts where the statistical conclusion (significant vs. non-significant) changed.",
            "source_task": "T034"
        })
    else:
        warning("Column 'flip_rate' not found in input file.")

    # Create DataFrame
    report_df = pd.DataFrame(records)
    
    # Ensure specific column order
    expected_cols = ["metric_name", "metric_value", "description", "source_task"]
    report_df = report_df[expected_cols]
    
    return report_df

def save_report(df_report):
    """Save the report to data/processed/significance_stability_report.csv."""
    config = load_config()
    base_dir = Path(config.get("project_root", "."))
    output_path = base_dir / "data" / "processed" / "significance_stability_report.csv"
    
    df_report.to_csv(output_path, index=False)
    info(f"Stability report saved to {output_path}")
    return output_path

def main():
    """Main entry point for T036."""
    try:
        df_metrics = load_results_metrics()
        df_report = generate_stability_report(df_metrics)
        save_report(df_report)
        logger.info("Task T036 completed successfully.")
    except Exception as e:
        error(f"Task T036 failed: {e}")
        raise

if __name__ == "__main__":
    main()