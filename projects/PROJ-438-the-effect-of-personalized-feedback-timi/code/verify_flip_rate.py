"""
T037: Verify 'significance flip rate' against SC-003 and log the result.

This script loads the significance flip rate report generated by T034/SC-003,
validates the metric against the specification (SC-003), and logs the result.
It ensures the flip rate is a valid proportion [0.0, 1.0] and logs a pass/fail
status based on the computed value.
"""
import os
import sys
import pandas as pd
import numpy as np
from pathlib import Path
from logging_config import get_logger, info, warning, error, debug

# Project root relative to this script
PROJECT_ROOT = Path(__file__).resolve().parent.parent
DATA_PROCESSED_DIR = PROJECT_ROOT / "data" / "processed"
FLIP_RATE_REPORT_PATH = DATA_PROCESSED_DIR / "significance_stability_report.csv"

# SC-003 Requirement: The flip rate must be calculated and reported.
# This script verifies the integrity and validity of that report.
SC_003_EXPECTED_COLUMNS = ["comparison", "flip_rate", "total_shifts", "flip_count"]

def load_flip_rate_report() -> pd.DataFrame:
    """Load the significance flip rate report generated by previous steps."""
    if not FLIP_RATE_REPORT_PATH.exists():
        raise FileNotFoundError(
            f"Flip rate report not found at {FLIP_RATE_REPORT_PATH}. "
            "Ensure T034 (calculate_flip_rate) has been executed successfully."
        )
    
    df = pd.read_csv(FLIP_RATE_REPORT_PATH)
    return df

def verify_against_sc_003(df: pd.DataFrame) -> bool:
    """
    Verify the report against SC-003 requirements:
    1. Required columns must exist.
    2. flip_rate must be in range [0.0, 1.0].
    3. flip_count must be <= total_shifts.
    """
    # Check columns
    missing_cols = [col for col in SC_003_EXPECTED_COLUMNS if col not in df.columns]
    if missing_cols:
        error(f"SC-003 Verification Failed: Missing columns: {missing_cols}")
        return False

    # Validate data integrity
    invalid_rates = df[~df["flip_rate"].between(0.0, 1.0)]
    if not invalid_rates.empty:
        error(f"SC-003 Verification Failed: Invalid flip_rate values found:\n{invalid_rates}")
        return False

    invalid_counts = df[df["flip_count"] > df["total_shifts"]]
    if not invalid_counts.empty:
        error(f"SC-003 Verification Failed: flip_count exceeds total_shifts:\n{invalid_counts}")
        return False

    return True

def log_result(is_valid: bool, df: pd.DataFrame):
    """Log the verification result and summary statistics."""
    if is_valid:
        avg_flip_rate = df["flip_rate"].mean()
        info("=" * 60)
        info("SC-003 VERIFICATION RESULT: PASSED")
        info("=" * 60)
        info(f"Report loaded: {FLIP_RATE_REPORT_PATH}")
        info(f"Rows verified: {len(df)}")
        info(f"Average Significance Flip Rate: {avg_flip_rate:.4f}")
        info("The calculated flip rate is valid and within expected bounds [0.0, 1.0].")
        info("Conclusion: The stability analysis is robust enough for reporting.")
        info("=" * 60)
    else:
        error("SC-003 VERIFICATION RESULT: FAILED")
        error("The significance flip rate report contains errors or invalid data.")
        error("The pipeline cannot proceed to final reporting until this is resolved.")

def main():
    """Main entry point for T037."""
    logger = get_logger("verify_flip_rate")
    info("Starting T037: Verify Significance Flip Rate against SC-003")
    
    try:
        df = load_flip_rate_report()
        debug(f"Loaded {len(df)} records from flip rate report.")
        
        is_valid = verify_against_sc_003(df)
        log_result(is_valid, df)
        
        if not is_valid:
            sys.exit(1)
            
    except FileNotFoundError as e:
        error(str(e))
        sys.exit(1)
    except Exception as e:
        error(f"Unexpected error during verification: {e}")
        debug(traceback.format_exc())
        sys.exit(1)

if __name__ == "__main__":
    main()