"""
T038: Compare calculated effect sizes against the ≥0.3 target and flag the result.

This script loads the results_metrics.csv generated by previous tasks (T035),
compares the calculated Cohen's d effect sizes against the target threshold of 0.3,
and appends a 'meets_target' flag to the output. It then saves the updated
metrics file and prints a summary to the console.
"""
import os
import sys
import pandas as pd
import numpy as np
from pathlib import Path
from logging_config import get_logger, info, error, warning

# Constants
TARGET_EFFECT_SIZE = 0.3
RESULTS_METRICS_PATH = Path("data/processed/results_metrics.csv")
OUTPUT_PATH = Path("data/processed/results_metrics_flagged.csv")

def load_results_metrics():
    """Load the results metrics CSV."""
    if not RESULTS_METRICS_PATH.exists():
        raise FileNotFoundError(
            f"Required input file not found: {RESULTS_METRICS_PATH}. "
            "Please ensure T035 (generate_results_metrics) has been run."
        )
    df = pd.read_csv(RESULTS_METRICS_PATH)
    return df

def evaluate_against_target(df):
    """
    Compare effect sizes against the target and flag results.
    
    Adds a 'meets_target' boolean column.
    Handles potential missing values in effect size columns.
    """
    # Identify columns containing 'effect_size' or 'cohens_d'
    effect_size_cols = [col for col in df.columns if 'effect_size' in col.lower() or 'cohens_d' in col.lower()]
    
    if not effect_size_cols:
        warning("No effect size columns found in results_metrics.csv. Cannot evaluate target.")
        # Create a placeholder column if no effect sizes are found
        df['meets_target'] = False
        return df

    # Determine if ANY effect size in the row meets the target
    # We assume the target applies to the magnitude of the effect (absolute value)
    # or the specific comparison of interest (often the primary one).
    # For robustness, we check if the maximum absolute effect size meets the threshold.
    
    def check_row_target(row):
        relevant_values = row[effect_size_cols].dropna()
        if relevant_values.empty:
            return False
        # Check if the absolute value of the largest effect meets the target
        max_effect = relevant_values.abs().max()
        return max_effect >= TARGET_EFFECT_SIZE

    df['meets_target'] = df.apply(check_row_target, axis=1)
    
    # Count how many rows meet the target
    meets_count = df['meets_target'].sum()
    total_count = len(df)
    
    info(f"Effect Size Target ({TARGET_EFFECT_SIZE}) Evaluation:")
    info(f"  - Total comparisons: {total_count}")
    info(f"  - Comparisons meeting target (|d| >= {TARGET_EFFECT_SIZE}): {meets_count}")
    
    return df

def save_flagged_metrics(df):
    """Save the updated dataframe with the flag."""
    OUTPUT_PATH.parent.mkdir(parents=True, exist_ok=True)
    df.to_csv(OUTPUT_PATH, index=False)
    info(f"Flagged results saved to: {OUTPUT_PATH}")

def main():
    logger = get_logger(__name__)
    logger.info("Starting T038: Evaluate Effect Sizes against Target")
    
    try:
        # 1. Load data
        df = load_results_metrics()
        info(f"Loaded {len(df)} records from {RESULTS_METRICS_PATH}")
        
        # 2. Evaluate
        df_flagged = evaluate_against_target(df)
        
        # 3. Save
        save_flagged_metrics(df_flagged)
        
        # 4. Print final summary to stdout for easy parsing
        summary = df_flagged['meets_target'].value_counts()
        print("\n--- T038 SUMMARY ---")
        print(f"Target: |Cohen's d| >= {TARGET_EFFECT_SIZE}")
        print(f"Result: {'PASS' if summary.get(True, 0) > 0 else 'NO SIGNIFICANT EFFECT FOUND'}")
        print(f"Details: {summary.get(True, 0)} comparisons met the target out of {len(df_flagged)}.")
        print("--------------------\n")
        
        logger.info("T038 completed successfully.")
        
    except FileNotFoundError as e:
        error(str(e))
        sys.exit(1)
    except Exception as e:
        error(f"Unexpected error during evaluation: {e}")
        sys.exit(1)

if __name__ == "__main__":
    main()