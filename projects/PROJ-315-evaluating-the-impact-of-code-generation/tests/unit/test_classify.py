"""
Unit tests for keyword classification logic in code/labeling/classify.py.
Tests the >=2 keyword threshold rule for classifying commits as LLM-generated.
"""
import pytest
import os
import sys
from pathlib import Path

# Add project root to path to allow imports from code/
project_root = Path(__file__).parent.parent.parent
sys.path.insert(0, str(project_root))

from code.labeling.classify import classify_commit, load_keywords
from code.labeling.keywords import KEYWORDS


class TestLoadKeywords:
    """Tests for loading keywords from YAML configuration."""

    def test_load_keywords_returns_list(self):
        """Verify load_keywords returns a non-empty list."""
        keywords = load_keywords()
        assert isinstance(keywords, list), "Keywords should be a list"
        assert len(keywords) > 0, "Keywords list should not be empty"

    def test_load_keywords_contains_expected_terms(self):
        """Verify the keywords list contains expected LLM terms."""
        keywords = load_keywords()
        keywords_lower = [k.lower() for k in keywords]
        
        # Check for some expected terms
        expected_terms = ["copilot", "generated by llm", "ai code"]
        for term in expected_terms:
            assert any(term in kw for kw in keywords_lower), f"Expected term '{term}' not found in keywords"


class TestClassifyCommit:
    """Tests for the classify_commit function with >=2 threshold."""

    def test_commit_with_zero_keywords_is_not_llm(self):
        """A commit with 0 matching keywords should be classified as not LLM-generated."""
        commit_message = "Fix typo in documentation"
        result = classify_commit(commit_message)
        assert result["is_llm_generated"] is False
        assert result["match_count"] == 0

    def test_commit_with_one_keyword_is_not_llm(self):
        """A commit with 1 matching keyword should NOT be classified as LLM-generated (threshold is 2)."""
        commit_message = "Added code generated by copilot for testing"
        result = classify_commit(commit_message)
        assert result["is_llm_generated"] is False, "Should require >=2 keywords, got 1"
        assert result["match_count"] == 1

    def test_commit_with_two_keywords_is_llm(self):
        """A commit with 2 matching keywords SHOULD be classified as LLM-generated."""
        # Using two distinct keywords from the list
        commit_message = "AI code generated by copilot for new feature"
        result = classify_commit(commit_message)
        assert result["is_llm_generated"] is True, "Should be LLM-generated with 2 keywords"
        assert result["match_count"] >= 2

    def test_commit_with_three_keywords_is_llm(self):
        """A commit with 3+ matching keywords should be classified as LLM-generated."""
        commit_message = "Copilot generated this code using AI code assistance"
        result = classify_commit(commit_message)
        assert result["is_llm_generated"] is True
        assert result["match_count"] >= 3

    def test_case_insensitivity(self):
        """Keyword matching should be case-insensitive."""
        commit_message = "CODE GENERATED BY COPILOT using AI"
        result = classify_commit(commit_message)
        assert result["is_llm_generated"] is True
        assert result["match_count"] >= 2

    def test_partial_word_matching(self):
        """Verify that keywords match as substrings (e.g., 'copilot' matches 'github copilot')."""
        # "copilot" is in the list, so "github copilot" should match
        commit_message = "Implemented with github copilot"
        result = classify_commit(commit_message)
        # Should match at least "copilot"
        assert result["match_count"] >= 1

    def test_empty_commit_message(self):
        """An empty commit message should result in 0 matches."""
        result = classify_commit("")
        assert result["is_llm_generated"] is False
        assert result["match_count"] == 0

    def test_none_commit_message(self):
        """A None commit message should result in 0 matches."""
        result = classify_commit(None)
        assert result["is_llm_generated"] is False
        assert result["match_count"] == 0

    def test_threshold_boundary(self):
        """Explicitly test the boundary condition of the >=2 threshold."""
        # Simulate a message that matches exactly 1 keyword
        # We need to construct a message that triggers exactly one known keyword
        # Since we can't easily control the exact count without knowing all keywords,
        # we test the logic by ensuring 1 match fails and 2 matches pass.
        
        # Test 1 match (should fail)
        msg_1 = "This code uses copilot" 
        res_1 = classify_commit(msg_1)
        
        # Test 2 matches (should pass)
        msg_2 = "This code uses copilot and is ai code"
        res_2 = classify_commit(msg_2)
        
        # Verify the threshold logic holds
        if res_1["match_count"] == 1:
            assert res_1["is_llm_generated"] is False
        if res_2["match_count"] >= 2:
            assert res_2["is_llm_generated"] is True


class TestKeywordListIntegrity:
    """Tests to ensure the keyword list is valid and usable."""

    def test_no_empty_keywords(self):
        """Ensure no empty strings in the keyword list."""
        assert "" not in KEYWORDS
        assert " " not in KEYWORDS

    def test_all_keywords_are_strings(self):
        """Ensure all items in the keyword list are strings."""
        assert all(isinstance(k, str) for k in KEYWORDS)

    def test_keyword_count_reasonable(self):
        """Ensure the keyword list has a reasonable number of entries (e.g., > 5)."""
        assert len(KEYWORDS) > 5, "Keyword list should have enough entries for meaningful classification"