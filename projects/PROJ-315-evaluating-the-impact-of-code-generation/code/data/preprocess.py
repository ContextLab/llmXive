import json
import logging
from pathlib import Path
from typing import Any, Dict, List, Optional, Tuple
import numpy as np
import pandas as pd

from code.utils.config import set_global_seed
from code.utils.logger import get_logger, log_analysis_result

# --- Configuration Constants ---
AUDIT_SAMPLE_DIR = Path("docs/reports")
HUMAN_LABELED_FILE = AUDIT_SAMPLE_DIR / "audit_sample_labeled.csv"
ACCURACY_OUTPUT_FILE = AUDIT_SAMPLE_DIR / "audit_accuracy.json"
KEYWORDS_FILE = Path("code/labeling/keywords.yaml")
MIN_THRESHOLD = 2  # FR-002: Minimum keyword count for classification

logger = get_logger(__name__)

def load_keywords() -> List[str]:
    """Load LLM-associated keywords from the YAML configuration file."""
    if not KEYWORDS_FILE.exists():
        raise FileNotFoundError(f"Keywords file not found at {KEYWORDS_FILE}. Run T015a first.")
    
    # Simple YAML parser since we only expect a list of strings
    # to avoid heavy dependency on PyYAML if not strictly necessary, 
    # though PyYAML is in requirements.
    import yaml
    with open(KEYWORDS_FILE, 'r', encoding='utf-8') as f:
        data = yaml.safe_load(f)
    
    if not isinstance(data, list):
        raise ValueError(f"Expected list in {KEYWORDS_FILE}, got {type(data)}")
    
    return [str(k).lower().strip() for k in data if k]

def classify_pr(commit_message: str, code_snippet: str = "") -> bool:
    """
    Classify a PR as LLM-generated based on keyword matching.
    Returns True if >= MIN_THRESHOLD keywords are found in commit message or snippet.
    """
    keywords = load_keywords()
    text = f"{commit_message or ''} {code_snippet or ''}".lower()
    
    count = sum(1 for kw in keywords if kw in text)
    return count >= MIN_THRESHOLD

def load_human_labels() -> pd.DataFrame:
    """
    Load the human-labeled CSV file generated by T017b.
    Expects columns: ['pr_id', 'commit_message', 'code_snippet', 'human_label']
    """
    if not HUMAN_LABELED_FILE.exists():
        raise FileNotFoundError(
            f"Human labeled file not found at {HUMAN_LABELED_FILE}. "
            "Ensure T017b has been executed successfully."
        )
    
    df = pd.read_csv(HUMAN_LABELED_FILE)
    
    required_cols = ['pr_id', 'commit_message', 'code_snippet', 'human_label']
    missing = [col for col in required_cols if col not in df.columns]
    if missing:
        raise ValueError(f"Human labeled file missing required columns: {missing}")
    
    # Ensure human_label is boolean or integer (0/1)
    # Assuming human_label is 1 for LLM-generated, 0 for human-written
    # or a string 'True'/'False'. We normalize to boolean.
    if df['human_label'].dtype == 'object':
        df['human_label'] = df['human_label'].astype(str).str.lower().map({
            'true': True, '1': True, 'yes': True,
            'false': False, '0': False, 'no': False
        })
    elif df['human_label'].dtype in ['int64', 'float64']:
        df['human_label'] = df['human_label'].astype(bool)
    
    return df

def calculate_audit_accuracy(df: pd.DataFrame) -> Dict[str, Any]:
    """
    Compare heuristic classification against human labels.
    Returns a dictionary with accuracy metrics.
    """
    # Apply heuristic classification to the sample data
    df['heuristic_label'] = df.apply(
        lambda row: classify_pr(row['commit_message'], row['code_snippet']), 
        axis=1
    )
    
    # Calculate accuracy
    matches = df['heuristic_label'] == df['human_label']
    accuracy = matches.mean()
    total_samples = len(df)
    correct_samples = matches.sum()
    
    # Breakdown by class for more insight
    true_positives = ((df['heuristic_label'] == True) & (df['human_label'] == True)).sum()
    false_positives = ((df['heuristic_label'] == True) & (df['human_label'] == False)).sum()
    true_negatives = ((df['heuristic_label'] == False) & (df['human_label'] == False)).sum()
    false_negatives = ((df['heuristic_label'] == False) & (df['human_label'] == True)).sum()
    
    # Precision, Recall, F1
    precision = true_positives / (true_positives + false_positives) if (true_positives + false_positives) > 0 else 0.0
    recall = true_positives / (true_positives + false_negatives) if (true_positives + false_negatives) > 0 else 0.0
    f1 = 2 * (precision * recall) / (precision + recall) if (precision + recall) > 0 else 0.0
    
    return {
        "total_samples": int(total_samples),
        "correct_samples": int(correct_samples),
        "accuracy_rate": float(accuracy),
        "true_positives": int(true_positives),
        "false_positives": int(false_positives),
        "true_negatives": int(true_negatives),
        "false_negatives": int(false_negatives),
        "precision": float(precision),
        "recall": float(recall),
        "f1_score": float(f1),
        "threshold_used": MIN_THRESHOLD
    }

def write_accuracy_report(metrics: Dict[str, Any]) -> Path:
    """Write the accuracy metrics to the JSON report file."""
    AUDIT_SAMPLE_DIR.mkdir(parents=True, exist_ok=True)
    
    with open(ACCURACY_OUTPUT_FILE, 'w', encoding='utf-8') as f:
        json.dump(metrics, f, indent=2, sort_keys=True)
    
    logger.info(f"Audit accuracy report written to {ACCURACY_OUTPUT_FILE}")
    return ACCURACY_OUTPUT_FILE

def main():
    """
    Main entry point for T017c: Ingest human-labeled CSV, compare with heuristics,
    calculate accuracy, and write results to JSON.
    """
    set_global_seed(42)
    logger.info("Starting T017c: Audit Accuracy Calculation")
    
    try:
        # 1. Load human-labeled data
        logger.info(f"Loading human labels from {HUMAN_LABELED_FILE}...")
        df = load_human_labels()
        logger.info(f"Loaded {len(df)} samples.")
        
        if len(df) == 0:
            raise ValueError("Human labeled dataset is empty. Cannot calculate accuracy.")
        
        # 2. Calculate accuracy metrics
        logger.info("Calculating heuristic vs human label accuracy...")
        metrics = calculate_audit_accuracy(df)
        
        # 3. Write report
        logger.info("Writing accuracy report...")
        report_path = write_accuracy_report(metrics)
        
        # 4. Log summary
        log_analysis_result(
            task="T017c",
            result_type="audit_accuracy",
            summary=f"Accuracy: {metrics['accuracy_rate']:.4f} ({metrics['correct_samples']}/{metrics['total_samples']})"
        )
        
        logger.info("T017c completed successfully.")
        return metrics
        
    except FileNotFoundError as e:
        logger.error(f"File error: {e}")
        raise
    except ValueError as e:
        logger.error(f"Validation error: {e}")
        raise
    except Exception as e:
        logger.error(f"Unexpected error during T017c: {e}", exc_info=True)
        raise

if __name__ == "__main__":
    main()