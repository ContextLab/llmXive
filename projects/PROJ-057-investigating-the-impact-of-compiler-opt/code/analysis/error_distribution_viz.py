import os
import json
import logging
import matplotlib.pyplot as plt
import pandas as pd
import numpy as np
from pathlib import Path
from typing import List, Dict, Any, Optional, Tuple

# Configure logging
logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def load_stability_metrics(metrics_path: Optional[str] = None) -> pd.DataFrame:
    """
    Load stability metrics from the CSV file generated by T023.
    
    Args:
        metrics_path: Path to stability_metrics.csv. Defaults to data/results/stability_metrics.csv.
        
    Returns:
        DataFrame containing stability metrics.
        
    Raises:
        FileNotFoundError: If the metrics file does not exist.
        ValueError: If the file is empty or missing required columns.
    """
    if metrics_path is None:
        metrics_path = "data/results/stability_metrics.csv"
        
    path = Path(metrics_path)
    if not path.exists():
        raise FileNotFoundError(f"Stability metrics file not found at {metrics_path}")
        
    df = pd.read_csv(path)
    
    required_columns = ['config_id', 'kernel_type', 'l2_error', 'max_diff', 'status']
    missing_cols = [col for col in required_columns if col not in df.columns]
    if missing_cols:
        raise ValueError(f"Missing required columns: {missing_cols}")
        
    if df.empty:
        raise ValueError("Stability metrics file is empty")
        
    logger.info(f"Loaded {len(df)} stability metrics from {metrics_path}")
    return df

def parse_optimization_level(config_id: str) -> str:
    """
    Extract the optimization level from a config_id string.
    
    Config IDs are expected to follow a pattern like 'matmul_O2_512x512' or 'softmax_O3_768x768'.
    The optimization level is assumed to be the second underscore-separated component.
    
    Args:
        config_id: The configuration identifier string.
        
    Returns:
        The optimization level string (e.g., 'O2', 'O3', 'Os').
    """
    parts = config_id.split('_')
    if len(parts) >= 2:
        # Assume the optimization flag is the second part (e.g., 'matmul_O2_512x512' -> 'O2')
        return parts[1]
    else:
        return "Unknown"

def plot_error_distribution(
    df: pd.DataFrame,
    output_path: str = "data/results/error_distribution_by_opt_level.png",
    error_metric: str = "max_diff",
    stable_only: bool = True
) -> None:
    """
    Plot the distribution of errors (L2 or MaxDiff) grouped by optimization level.
    
    Creates a boxplot showing error distribution per optimization level.
    Stable configurations can be filtered if desired.
    
    Args:
        df: DataFrame with stability metrics.
        output_path: Path to save the generated plot.
        error_metric: Column to use for error values ('l2_error' or 'max_diff').
        stable_only: If True, only plot configurations with status 'stable'.
    """
    if error_metric not in df.columns:
        raise ValueError(f"Error metric '{error_metric}' not found in DataFrame")
        
    plot_df = df.copy()
    
    if stable_only:
        plot_df = plot_df[plot_df['status'] == 'stable']
        logger.info(f"Filtering to stable configurations only. {len(plot_df)} rows remain.")
        
    if plot_df.empty:
        logger.warning("No stable configurations to plot. Plot will be empty.")
        
    # Add optimization level column
    plot_df['opt_level'] = plot_df['config_id'].apply(parse_optimization_level)
    
    # Set up the plot
    plt.figure(figsize=(12, 7))
    
    # Create boxplot for error distribution per optimization level
    # Group by opt_level and error_metric
    unique_levels = sorted(plot_df['opt_level'].unique())
    
    if len(unique_levels) == 0:
        logger.warning("No optimization levels found in data.")
        plt.title("Error Distribution by Optimization Level\n(No data available)")
        plt.savefig(output_path, dpi=150, bbox_inches='tight')
        plt.close()
        return
        
    # Prepare data for boxplot
    data_to_plot = []
    labels = []
    
    for level in unique_levels:
        level_data = plot_df[plot_df['opt_level'] == level][error_metric].values
        data_to_plot.append(level_data)
        labels.append(level)
    
    # Create boxplot
    bp = plt.boxplot(data_to_plot, labels=labels, patch_artist=True,
                    notch=True, showmeans=True)
    
    # Color the boxes
    colors = plt.cm.Set3(np.linspace(0, 1, len(data_to_plot)))
    for patch, color in zip(bp['boxes'], colors):
        patch.set_facecolor(color)
        
    # Customize the plot
    plt.ylabel(f'{error_metric} (log scale)' if error_metric == 'l2_error' else f'{error_metric}')
    plt.xlabel('Optimization Level')
    plt.title(f'Error Distribution by Optimization Level\n({error_metric})')
    
    # Use log scale for y-axis if errors vary widely
    if error_metric == 'l2_error':
        plt.yscale('log')
        
    # Add grid for readability
    plt.grid(True, axis='y', alpha=0.3, linestyle='--')
    
    # Save the plot
    plt.tight_layout()
    plt.savefig(output_path, dpi=150, bbox_inches='tight')
    plt.close()
    
    logger.info(f"Error distribution plot saved to {output_path}")

def generate_error_distribution_summary(
    df: pd.DataFrame,
    output_path: str = "data/results/error_distribution_summary.json",
    stable_only: bool = True
) -> Dict[str, Any]:
    """
    Generate a summary statistics dictionary for error distribution per optimization level.
    
    Args:
        df: DataFrame with stability metrics.
        output_path: Path to save the JSON summary.
        stable_only: If True, only include stable configurations.
        
    Returns:
        Dictionary containing summary statistics.
    """
    plot_df = df.copy()
    
    if stable_only:
        plot_df = plot_df[plot_df['status'] == 'stable']
        
    if plot_df.empty:
        summary = {
            "status": "empty",
            "message": "No stable configurations found to summarize."
        }
        with open(output_path, 'w') as f:
            json.dump(summary, f, indent=2)
        return summary
        
    plot_df['opt_level'] = plot_df['config_id'].apply(parse_optimization_level)
    
    summary = {
        "total_configurations": len(plot_df),
        "stable_only": stable_only,
        "optimization_levels": {}
    }
    
    for level in sorted(plot_df['opt_level'].unique()):
        level_data = plot_df[plot_df['opt_level'] == level]
        
        stats = {
            "count": int(len(level_data)),
            "l2_error": {
                "mean": float(level_data['l2_error'].mean()),
                "std": float(level_data['l2_error'].std()),
                "min": float(level_data['l2_error'].min()),
                "max": float(level_data['l2_error'].max()),
                "median": float(level_data['l2_error'].median())
            },
            "max_diff": {
                "mean": float(level_data['max_diff'].mean()),
                "std": float(level_data['max_diff'].std()),
                "min": float(level_data['max_diff'].min()),
                "max": float(level_data['max_diff'].max()),
                "median": float(level_data['max_diff'].median())
            }
        }
        
        summary["optimization_levels"][level] = stats
        
    # Save summary to JSON
    with open(output_path, 'w') as f:
        json.dump(summary, f, indent=2)
        
    logger.info(f"Error distribution summary saved to {output_path}")
    return summary

def main():
    """
    Main entry point for the error distribution visualization task.
    
    Loads stability metrics, generates a plot of error distribution per optimization level,
    and creates a summary statistics file.
    """
    logger.info("Starting error distribution visualization task (T024)")
    
    # Define paths
    metrics_path = "data/results/stability_metrics.csv"
    plot_output = "data/results/error_distribution_by_opt_level.png"
    summary_output = "data/results/error_distribution_summary.json"
    
    try:
        # Load data
        logger.info(f"Loading stability metrics from {metrics_path}")
        df = load_stability_metrics(metrics_path)
        
        # Generate plot
        logger.info("Generating error distribution plot")
        plot_error_distribution(df, output_path=plot_output, error_metric="max_diff", stable_only=True)
        
        # Also plot L2 error
        logger.info("Generating L2 error distribution plot")
        plot_error_distribution(df, output_path="data/results/l2_error_distribution_by_opt_level.png", 
                              error_metric="l2_error", stable_only=True)
        
        # Generate summary
        logger.info("Generating error distribution summary")
        summary = generate_error_distribution_summary(df, output_path=summary_output, stable_only=True)
        
        logger.info("Error distribution visualization task completed successfully")
        logger.info(f"  - Plot: {plot_output}")
        logger.info(f"  - Summary: {summary_output}")
        
    except FileNotFoundError as e:
        logger.error(f"Data file not found: {e}")
        logger.error("Ensure that T023 has been completed and stability_metrics.csv exists.")
        raise
    except Exception as e:
        logger.error(f"Error during visualization generation: {e}")
        raise

if __name__ == "__main__":
    main()