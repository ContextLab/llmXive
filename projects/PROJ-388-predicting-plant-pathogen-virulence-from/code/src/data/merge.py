def write_merged_dataset(
    df: pd.DataFrame,
    output_path: Path,
    result: MergeResult
) -> None:
    """Write the merged dataset to a Parquet file and log summary."""
    output_path.parent.mkdir(parents=True, exist_ok=True)
    
    # Write Parquet
    df.to_parquet(output_path, index=False)
    logger.info(f"Wrote merged dataset to {output_path} with {len(df)} rows.")
    
    # Log summary
    logger.info(f"Processed count: {result.processed_count}")
    logger.info(f"Missing count: {result.missing_count}")
    logger.info(f"Aggregated: {result.is_aggregated}")

