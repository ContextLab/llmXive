def load_genomic_features(genomic_csv_path: Path) -> List[GenomicFeature]:
    """Load genomic features from a CSV file generated by extract.py."""
    features = []
    if not genomic_csv_path.exists():
        raise FileNotFoundError(f"Genomic features file not found: {genomic_csv_path}")
    
    with open(genomic_csv_path, 'r', newline='', encoding='utf-8') as f:
        reader = csv.DictReader(f)
        for row in reader:
            # Handle potential missing values in numeric columns if necessary
            # Assuming binary presence/absence and counts are stored as strings or ints
            feature = GenomicFeature(
                feature_id=row['feature_id'],
                type=row['type'],
                presence_binary=int(row['presence_binary']),
                pwm_count=int(row['pwm_count']) if row['pwm_count'] else 0,
                source=row['source']
            )
            features.append(feature)
    return features

def load_phenotypic_scores(phenotypic_csv_path: Path) -> Dict[str, float]:
    """Load phenotypic scores from a CSV file generated by download.py.
    Returns a dict mapping isolate_id (or species_id) to score."""
    scores = {}
    if not phenotypic_csv_path.exists():
        raise FileNotFoundError(f"Phenotypic scores file not found: {phenotypic_csv_path}")
    
    with open(phenotypic_csv_path, 'r', newline='', encoding='utf-8') as f:
        reader = csv.DictReader(f)
        for row in reader:
            # Assuming the CSV has columns 'id' and 'score'
            # Adapt based on actual output of T016
            isolate_id = row.get('isolate_id') or row.get('id')
            score_str = row.get('score') or row.get('phenotype_score')
            if isolate_id and score_str:
                try:
                    scores[isolate_id] = float(score_str)
                except ValueError:
                    logger.warning(f"Could not parse score for {isolate_id}: {score_str}")
    return scores

