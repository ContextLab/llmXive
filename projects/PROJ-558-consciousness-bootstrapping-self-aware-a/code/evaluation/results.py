from dataclasses import dataclass, field
from typing import Any, Dict, List, Optional
from datetime import datetime
import json
from pathlib import Path

@dataclass
class EvaluationResult:
    """
    Dataclass to represent the results of a model evaluation.
    
    Attributes:
        model_id: Identifier of the model being evaluated.
        checkpoint_path: Path to the checkpoint used for evaluation.
        benchmark_name: Name of the benchmark (e.g., 'gsm8k', 'mmlu').
        timestamp: ISO format timestamp of when the evaluation was run.
        metrics: Dictionary of calculated metrics (e.g., accuracy, loss).
        raw_predictions: List of raw predictions generated by the model.
        ground_truth: List of ground truth labels (if available).
        self_consistency_score: Score representing self-consistency (if applicable).
        calibration_metrics: Dictionary of calibration metrics (e.g., Brier score, ECE).
        metadata: Arbitrary metadata dictionary.
    """
    model_id: str
    checkpoint_path: str
    benchmark_name: str
    timestamp: str = field(default_factory=lambda: datetime.now().isoformat())
    metrics: dict = field(default_factory=dict)
    raw_predictions: List[Any] = field(default_factory=list)
    ground_truth: List[Any] = field(default_factory=list)
    self_consistency_score: Optional[float] = None
    calibration_metrics: dict = field(default_factory=dict)
    metadata: dict = field(default_factory=dict)

    def to_dict(self) -> dict:
        """Convert the evaluation result to a dictionary for serialization."""
        return {
            "model_id": self.model_id,
            "checkpoint_path": self.checkpoint_path,
            "benchmark_name": self.benchmark_name,
            "timestamp": self.timestamp,
            "metrics": self.metrics,
            "raw_predictions": self.raw_predictions,
            "ground_truth": self.ground_truth,
            "self_consistency_score": self.self_consistency_score,
            "calibration_metrics": self.calibration_metrics,
            "metadata": self.metadata
        }

    def save(self, output_path: str) -> Path:
        """
        Save the evaluation result to a JSON file.
        
        Args:
            output_path: Path to save the JSON file.
            
        Returns:
            Path to the saved file.
        """
        path = Path(output_path)
        path.parent.mkdir(parents=True, exist_ok=True)
        
        with open(path, "w") as f:
            json.dump(self.to_dict(), f, indent=2)
        
        return path

    @classmethod
    def load(cls, path: str) -> "EvaluationResult":
        """
        Load an evaluation result from a JSON file.
        
        Args:
            path: Path to the JSON file.
            
        Returns:
            EvaluationResult instance.
        """
        with open(path, "r") as f:
            data = json.load(f)
        
        return cls(
            model_id=data["model_id"],
            checkpoint_path=data["checkpoint_path"],
            benchmark_name=data["benchmark_name"],
            timestamp=data.get("timestamp", datetime.now().isoformat()),
            metrics=data.get("metrics", {}),
            raw_predictions=data.get("raw_predictions", []),
            ground_truth=data.get("ground_truth", []),
            self_consistency_score=data.get("self_consistency_score"),
            calibration_metrics=data.get("calibration_metrics", {}),
            metadata=data.get("metadata", {})
        )

    def update_metrics(self, new_metrics: dict) -> None:
        """
        Update the metrics dictionary with new values.
        
        Args:
            new_metrics: Dictionary of metrics to update/add.
        """
        self.metrics.update(new_metrics)

    def add_calibration_metric(self, name: str, value: float) -> None:
        """
        Add a single calibration metric.
        
        Args:
            name: Name of the metric.
            value: Value of the metric.
        """
        self.calibration_metrics[name] = value