def load_baseline_metrics(config: dict) -> dict:
    """
    Loads the synthetic baseline comparison metrics.
    Reads from data/results/synthetic_baseline_comparison.json (generated by T053c).
    """
    baseline_path = Path(config['paths']['data_results']) / 'synthetic_baseline_comparison.json'
    if not baseline_path.exists():
        # Fallback to the provided artifact if it exists, or create a default structure
        # Note: In a real run, T053c must have produced this.
        # If missing, we raise an error to prevent silent failure.
        raise FileNotFoundError(f"Baseline comparison file not found at {baseline_path}. Ensure T053c ran successfully.")
    
    with open(baseline_path, 'r') as f:
        return json.load(f)

def load_harmonized_correlations(config: dict) -> dict:
    """
    Loads the correlation results generated from the harmonized dataset.
    Expects data/results/correlation_matrix.json (output of T025 on harmonized data).
    """
    corr_path = Path(config['paths']['data_results']) / 'correlation_matrix.json'
    if not corr_path.exists():
        raise FileNotFoundError(f"Harmonized correlation results not found at {corr_path}. "
                                "Run the analysis pipeline on harmonized data first.")
    
    with open(corr_path, 'r') as f:
        return json.load(f)

def compare_distributions(baseline: dict, harmonized: dict) -> dict:
    """
    Compares statistical properties of the correlation results.
    Returns a detailed comparison dictionary.
    """
    # Extract significant pairs and coefficients
    baseline_sig = baseline.get('significant_pairs', [])
    harmonized_sig = harmonized.get('significant_pairs', [])
    
    # Calculate overlap
    common = set(baseline_sig) & set(harmonized_sig)
    only_baseline = set(baseline_sig) - set(harmonized_sig)
    only_harmonized = set(harmonized_sig) - set(baseline_sig)
    
    # Coefficient stability check (simplified: compare mean absolute correlation)
    # In a full implementation, we would match pairs by name and compare coefficients
    baseline_coeffs = [p['coefficient'] for p in baseline.get('pairs', [])]
    harmonized_coeffs = [p['coefficient'] for p in harmonized.get('pairs', [])]
    
    mean_baseline = np.mean(baseline_coeffs) if baseline_coeffs else 0.0
    mean_harmonized = np.mean(harmonized_coeffs) if harmonized_coeffs else 0.0
    
    stability_score = 1.0 - abs(mean_baseline - mean_harmonized)
    
    return {
        "common_pairs_count": len(common),
        "only_in_baseline_count": len(only_baseline),
        "only_in_harmonized_count": len(only_harmonized),
        "mean_baseline_correlation": mean_baseline,
        "mean_harmonized_correlation": mean_harmonized,
        "stability_score": stability_score,
        "common_pairs": list(common),
        "pairs_only_in_baseline": list(only_baseline),
        "pairs_only_in_harmonized": list(only_harmonized)
    }

def run_validation_pipeline(config: dict) -> dict:
    """
    Orchestrates the validation of harmonized results against synthetic baseline.
    """
    print("Loading baseline metrics...")
    baseline = load_baseline_metrics(config)
    
    print("Loading harmonized correlation results...")
    harmonized = load_harmonized_correlations(config)
    
    print("Comparing distributions...")
    comparison = compare_distributions(baseline, harmonized)
    
    # Determine pass/fail status based on stability
    # Threshold: Stability score > 0.8 and no massive divergence in significant pairs
    is_valid = comparison['stability_score'] > 0.8
    if len(comparison['pairs_only_in_harmonized']) > len(comparison['common_pairs']) * 0.5:
        is_valid = False # Too many new findings suggests artifact or noise
    
    result = {
        "status": "PASS" if is_valid else "FAIL",
        "validation_timestamp": datetime.now().isoformat(),
        "comparison": comparison,
        "details": {
            "total_significant_baseline": len(baseline.get('significant_pairs', [])),
            "total_significant_harmonized": len(harmonized.get('significant_pairs', [])),
            "average_coefficient_stability": comparison['stability_score'],
            "artifacts_detected": not is_valid
        }
    }
    
    return result

def generate_summary(result: dict) -> str:
    """
    Generates a human-readable summary string.
    """
    status = result['status']
    ts = result['validation_timestamp']
    comp = result['comparison']
    
    summary = (
        f"HARMONIZED RESULTS VALIDATION REPORT\n"
        f"Timestamp: {ts}\n"
        f"Status: {status}\n"
        f"Stability Score: {comp['stability_score']:.4f}\n"
        f"Common Significant Pairs: {comp['common_pairs_count']}\n"
        f"Only in Baseline: {comp['only_in_baseline_count']}\n"
        f"Only in Harmonized: {comp['only_in_harmonized_count']}\n"
    )
    
    if status == "FAIL":
        summary += "WARNING: Statistical artifacts may have been introduced during harmonization.\n"
        if comp['pairs_only_in_harmonized']:
            summary += f"New pairs detected: {comp['pairs_only_in_harmonized']}\n"
    
    return summary

def update_paper_draft(config: dict, result: dict):
    """
    Updates the paper draft with the validation results.
    Creates or appends to data/results/paper_validation_notes.md
    """
    paper_path = Path(config['paths']['data_results']) / 'paper_validation_notes.md'
    summary = generate_summary(result)
    
    with open(paper_path, 'a') as f:
        f.write(f"\n---\n## Validation Run: {result['validation_timestamp']}\n\n")
        f.write(summary)
        f.write("\n")
    
    print(f"Paper draft updated at {paper_path}")

def main():
    parser = argparse.ArgumentParser(description="Validate harmonized results against synthetic baseline.")
    parser.add_argument('--config', type=str, default='data/config/config.yaml', help='Path to config file')
    args = parser.parse_args()
    
    config = load_config(args.config)
    
    try:
        result = run_validation_pipeline(config)
        
        # Save the detailed JSON result
        output_path = Path(config['paths']['data_results']) / 'harmonized_vs_synthetic_comparison.json'
        with open(output_path, 'w') as f:
            json.dump(result, f, indent=2)
        
        print(f"Validation result saved to {output_path}")
        print(generate_summary(result))
        
        # Update paper draft
        update_paper_draft(config, result)
        
        if result['status'] == "FAIL":
            sys.exit(1)
            
    except Exception as e:
        print(f"Validation failed with error: {e}")
        sys.exit(1)

if __name__ == "__main__":
    main()
