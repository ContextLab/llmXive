"""
Integration test for method selection logic (Zero-inflated vs Non-normal).

This test verifies that `select_correlation_method` in `code/analysis.py`
correctly chooses the correlation method based on data characteristics:
1. High zero proportion (> 30%) -> ZINB
2. Non-normal distribution (Shapiro-Wilk p < 0.05) -> Spearman
3. Normal distribution -> Pearson

It uses synthetic data generated by `code/data_generator.py` to create
controlled scenarios for testing.
"""

import os
import sys
import tempfile
import pytest
import numpy as np
import pandas as pd

# Add project root to path if running standalone
project_root = os.path.abspath(os.path.join(os.path.dirname(__file__), '..', '..'))
if project_root not in sys.path:
    sys.path.insert(0, project_root)

from data_generator import generate_synthetic_dataset, set_seeds
from analysis import select_correlation_method, check_zero_inflation, check_normality


class TestMethodSelection:
    """Integration tests for correlation method selection logic."""

    def setup_method(self):
        """Set up test fixtures."""
        set_seeds(42)  # Ensure reproducibility
        self.temp_dir = tempfile.mkdtemp()

    def test_select_method_high_zero_inflation(self):
        """
        Test that method selection returns 'zinb' when zero proportion > 30%.

        Scenario: Generate synthetic data with high zero inflation in taxa counts.
        Expected: 'zinb' method selected.
        """
        # Generate synthetic data with specific parameters to induce high zeros
        # We manually construct a dataframe to ensure high zero proportion
        np.random.seed(42)
        n_samples = 100

        # Create taxa with > 40% zeros
        taxa_1 = np.random.choice([0] * 50 + list(range(1, 100)), n_samples)
        taxa_2 = np.random.choice([0] * 45 + list(range(1, 100)), n_samples)

        # Create sleep metrics (normal distribution)
        sleep_metric = np.random.normal(loc=8.0, scale=1.5, size=n_samples)

        df = pd.DataFrame({
            'taxa_1': taxa_1,
            'taxa_2': taxa_2,
            'sleep_duration': sleep_metric
        })

        # Verify zero inflation
        zero_prop_1 = check_zero_inflation(df['taxa_1'])
        zero_prop_2 = check_zero_inflation(df['taxa_2'])

        assert zero_prop_1 > 0.30, f"Expected zero prop > 0.30, got {zero_prop_1}"
        assert zero_prop_2 > 0.30, f"Expected zero prop > 0.30, got {zero_prop_2}"

        # Test method selection
        predictor_col = 'taxa_1'
        outcome_col = 'sleep_duration'

        method = select_correlation_method(
            df, predictor_col, outcome_col,
            compositionality_detected=False
        )

        assert method == 'zinb', f"Expected 'zinb' for high zero inflation, got '{method}'"

    def test_select_method_non_normal_distribution(self):
        """
        Test that method selection returns 'spearman' when data is non-normal
        (Shapiro-Wilk p < 0.05) and zero inflation is low.

        Scenario: Generate synthetic data with normal zero proportion but
        skewed/non-normal distribution.
        Expected: 'spearman' method selected.
        """
        np.random.seed(42)
        n_samples = 100

        # Create taxa with low zero proportion but skewed distribution (exponential)
        taxa_1 = np.random.exponential(scale=2.0, size=n_samples)
        taxa_2 = np.random.exponential(scale=1.5, size=n_samples)

        # Create sleep metrics (skewed)
        sleep_metric = np.random.exponential(scale=2.0, size=n_samples)

        df = pd.DataFrame({
            'taxa_1': taxa_1,
            'taxa_2': taxa_2,
            'sleep_duration': sleep_metric
        })

        # Verify low zero inflation
        zero_prop_1 = check_zero_inflation(df['taxa_1'])
        assert zero_prop_1 < 0.30, f"Expected zero prop < 0.30, got {zero_prop_1}"

        # Verify non-normality
        _, p_value = check_normality(df['sleep_duration'])
        assert p_value < 0.05, f"Expected non-normal distribution (p < 0.05), got p={p_value}"

        # Test method selection
        predictor_col = 'taxa_1'
        outcome_col = 'sleep_duration'

        method = select_correlation_method(
            df, predictor_col, outcome_col,
            compositionality_detected=False
        )

        assert method == 'spearman', f"Expected 'spearman' for non-normal data, got '{method}'"

    def test_select_method_normal_distribution(self):
        """
        Test that method selection returns 'pearson' when data is normally distributed
        and zero inflation is low.

        Scenario: Generate synthetic data with normal zero proportion and normal distribution.
        Expected: 'pearson' method selected.
        """
        np.random.seed(42)
        n_samples = 100

        # Create taxa with normal distribution and no zeros
        taxa_1 = np.random.normal(loc=50.0, scale=10.0, size=n_samples)
        taxa_2 = np.random.normal(loc=30.0, scale=5.0, size=n_samples)

        # Create sleep metrics (normal distribution)
        sleep_metric = np.random.normal(loc=8.0, scale=1.5, size=n_samples)

        df = pd.DataFrame({
            'taxa_1': taxa_1,
            'taxa_2': taxa_2,
            'sleep_duration': sleep_metric
        })

        # Verify low zero inflation
        zero_prop_1 = check_zero_inflation(df['taxa_1'])
        assert zero_prop_1 < 0.30, f"Expected zero prop < 0.30, got {zero_prop_1}"

        # Verify normality
        _, p_value = check_normality(df['sleep_duration'])
        assert p_value >= 0.05, f"Expected normal distribution (p >= 0.05), got p={p_value}"

        # Test method selection
        predictor_col = 'taxa_1'
        outcome_col = 'sleep_duration'

        method = select_correlation_method(
            df, predictor_col, outcome_col,
            compositionality_detected=False
        )

        assert method == 'pearson', f"Expected 'pearson' for normal data, got '{method}'"

    def test_select_method_compositionality_priority(self):
        """
        Test that compositionality detection takes priority over other checks.

        Scenario: Data is normal and low-zero, but compositionality is detected.
        Expected: 'sparcc' method selected (or 'spiecaei' if available).
        """
        np.random.seed(42)
        n_samples = 100

        # Create normal data
        taxa_1 = np.random.normal(loc=50.0, scale=10.0, size=n_samples)
        sleep_metric = np.random.normal(loc=8.0, scale=1.5, size=n_samples)

        df = pd.DataFrame({
            'taxa_1': taxa_1,
            'sleep_duration': sleep_metric
        })

        # Test method selection with compositionality_detected=True
        predictor_col = 'taxa_1'
        outcome_col = 'sleep_duration'

        method = select_correlation_method(
            df, predictor_col, outcome_col,
            compositionality_detected=True
        )

        # Should prefer compositional methods
        assert method in ['sparcc', 'spiecaei'], \
            f"Expected compositional method ('sparcc' or 'spiecaei'), got '{method}'"

    def test_select_method_edge_case_boundary_zeros(self):
        """
        Test method selection at the boundary of zero inflation threshold (30%).

        Scenario: Exactly 30% zeros (or very close).
        Expected: Behavior should be consistent with the threshold logic.
        """
        np.random.seed(42)
        n_samples = 100

        # Create exactly 30 zeros out of 100 samples
        taxa_1 = np.concatenate([np.zeros(30), np.random.normal(loc=50.0, scale=10.0, size=70)])
        sleep_metric = np.random.normal(loc=8.0, scale=1.5, size=n_samples)

        df = pd.DataFrame({
            'taxa_1': taxa_1,
            'sleep_duration': sleep_metric
        })

        zero_prop = check_zero_inflation(df['taxa_1'])
        # 30% is the threshold, so >= 0.30 should trigger ZINB
        assert zero_prop >= 0.30, f"Expected zero prop >= 0.30, got {zero_prop}"

        predictor_col = 'taxa_1'
        outcome_col = 'sleep_duration'

        method = select_correlation_method(
            df, predictor_col, outcome_col,
            compositionality_detected=False
        )

        # Should be ZINB because zeros >= 30%
        assert method == 'zinb', f"Expected 'zinb' for zero prop >= 30%, got '{method}'"

    def test_method_selection_with_realistic_synthetic_data(self):
        """
        Test method selection with data generated by the standard synthetic generator.
        This ensures the logic works with the actual data format the pipeline produces.
        """
        # Generate a full synthetic dataset
        data = generate_synthetic_dataset(n_samples=150, n_taxa=5, seed=123)

        # Select a taxa and sleep metric
        taxa_cols = [col for col in data.columns if col.startswith('taxa_')]
        sleep_cols = [col for col in data.columns if col.startswith('sleep_')]

        if not taxa_cols or not sleep_cols:
            pytest.skip("Generated dataset missing expected columns")

        predictor_col = taxa_cols[0]
        outcome_col = sleep_cols[0]

        # Verify data exists
        assert predictor_col in data.columns
        assert outcome_col in data.columns

        # Test method selection
        method = select_correlation_method(
            data, predictor_col, outcome_col,
            compositionality_detected=False
        )

        # Method should be one of the valid options
        valid_methods = ['pearson', 'spearman', 'zinb', 'sparcc', 'spiecaei']
        assert method in valid_methods, f"Unexpected method '{method}'"

        # Log the characteristics for debugging
        zero_prop = check_zero_inflation(data[predictor_col])
        _, normality_p = check_normality(data[outcome_col])

        # Just assert it returns a valid method - the specific method depends on the data
        assert method is not None

