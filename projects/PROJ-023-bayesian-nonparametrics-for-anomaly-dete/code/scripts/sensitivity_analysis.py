def load_predictions(prediction_path: Path) -> pd.DataFrame:
    """
    Load predictions from a CSV file.
    Expected columns: ['timestamp', 'score', 'prediction'] (or similar).
    We specifically need the continuous 'score' and the binary 'prediction'
    if available, but primarily we will re-threshold the 'score' column.
    """
    if not prediction_path.exists():
        raise FileNotFoundError(f"Prediction file not found: {prediction_path}")
    
    df = pd.read_csv(prediction_path)
    
    # Normalize column names to lowercase for robustness
    df.columns = [col.lower().strip() for col in df.columns]
    
    # Identify score column
    score_col = None
    for candidate in ['score', 'anomaly_score', 'prob', 'probability']:
        if candidate in df.columns:
            score_col = candidate
            break
    
    if score_col is None:
        raise ValueError(f"Could not identify score column in {prediction_path}. Columns: {df.columns.tolist()}")
    
    # Identify ground truth column if present in the file (some pipelines merge GT)
    # If not, we rely on the external ground_truth file passed to the main function
    gt_col = None
    for candidate in ['ground_truth', 'label', 'is_anomaly', 'anomaly']:
        if candidate in df.columns:
            gt_col = candidate
            break
    
    return df, score_col, gt_col


def load_ground_truth(gt_path: Path) -> pd.Series:
    """
    Load ground truth labels.
    Expects a CSV with a boolean or integer column indicating anomalies (1) vs normal (0).
    """
    if not gt_path.exists():
        raise FileNotFoundError(f"Ground truth file not found: {gt_path}")
    
    df = pd.read_csv(gt_path)
    df.columns = [col.lower().strip() for col in df.columns]
    
    gt_col = None
    for candidate in ['ground_truth', 'label', 'is_anomaly', 'anomaly', 'true_label']:
        if candidate in df.columns:
            gt_col = candidate
            break
    
    if gt_col is None:
        raise ValueError(f"Could not identify ground truth column in {gt_path}. Columns: {df.columns.tolist()}")
    
    return df[gt_col]


def sweep_thresholds(
    scores: np.ndarray,
    ground_truth: np.ndarray,
    thresholds: List[float]
) -> List[Dict[str, Any]]:
    """
    Sweep through a list of thresholds and calculate metrics.
    
    Args:
        scores: Array of anomaly scores (higher = more anomalous).
        ground_truth: Array of binary labels (1 = anomaly, 0 = normal).
        thresholds: List of threshold values to test.
        
    Returns:
        List of dictionaries containing metrics for each threshold.
    """
    results = []
    
    # Ensure arrays are numpy for vectorized operations
    scores = np.array(scores)
    ground_truth = np.array(ground_truth)
    
    for thresh in thresholds:
        predictions = (scores >= thresh).astype(int)
        
        # Calculate confusion matrix components
        tp = np.sum((predictions == 1) & (ground_truth == 1))
        fp = np.sum((predictions == 1) & (ground_truth == 0))
        tn = np.sum((predictions == 0) & (ground_truth == 0))
        fn = np.sum((predictions == 0) & (ground_truth == 1))
        
        # Calculate rates
        precision = tp / (tp + fp) if (tp + fp) > 0 else 0.0
        recall = tp / (tp + fn) if (tp + fn) > 0 else 0.0
        f1 = 2 * (precision * recall) / (precision + recall) if (precision + recall) > 0 else 0.0
        
        specificity = tn / (tn + fp) if (tn + fp) > 0 else 1.0
        fpr = fp / (fp + tn) if (fp + tn) > 0 else 0.0
        fnr = fn / (fn + tp) if (fn + tp) > 0 else 0.0
        
        results.append({
            "threshold": float(thresh),
            "precision": float(precision),
            "recall": float(recall),
            "f1_score": float(f1),
            "specificity": float(specificity),
            "fpr": float(fpr),
            "fnr": float(fnr),
            "tp": int(tp),
            "fp": int(fp),
            "tn": int(tn),
            "fn": int(fn)
        })
        
    return results


def find_optimal_threshold(
    scores: np.ndarray,
    ground_truth: np.ndarray,
    target_specificity: Optional[float] = None
) -> Dict[str, Any]:
    """
    Find the optimal threshold based on F1 score or target specificity.
    
    Args:
        scores: Array of anomaly scores.
        ground_truth: Array of binary labels.
        target_specificity: If provided, find the threshold that achieves 
                            this specificity (or closest to it).
                            
    Returns:
        Dictionary with optimal threshold and its metrics.
    """
    # Generate a fine-grained grid of thresholds
    # Use percentiles of the score distribution to avoid unnecessary points
    percentiles = np.percentile(scores, np.linspace(0, 100, 500))
    unique_thresholds = np.unique(percentiles)
    
    all_metrics = sweep_thresholds(scores, ground_truth, unique_thresholds)
    
    if target_specificity is not None:
        # Find threshold closest to target specificity
        # We want specificity >= target_specificity (high specificity = low FPR)
        # But we want the highest F1 among those that meet the constraint? 
        # Usually "High Specificity" means we accept lower recall.
        # Let's find the threshold where specificity is >= target and F1 is maximized.
        
        candidates = [m for m in all_metrics if m['specificity'] >= target_specificity]
        if not candidates:
            # Fallback: closest to target
            candidates = all_metrics
        
        optimal = max(candidates, key=lambda x: x['f1_score'])
        strategy = f"specificity_{target_specificity}"
    else:
        # Maximize F1
        optimal = max(all_metrics, key=lambda x: x['f1_score'])
        strategy = "f1_optimal"
        
    return {
        "strategy": strategy,
        "threshold": optimal['threshold'],
        "metrics": optimal
    }


def run_analysis(
    gt_path: Path,
    prediction_paths: Dict[str, Path],
    output_path: Path
) -> None:
    """
    Run the full sensitivity analysis pipeline.
    """
    logger.info(f"Loading ground truth from {gt_path}")
    ground_truth = load_ground_truth(gt_path).values
    
    analysis_results = {
        "metadata": {
            "ground_truth_path": str(gt_path),
            "total_samples": int(len(ground_truth)),
            "anomaly_count": int(np.sum(ground_truth == 1)),
            "normal_count": int(np.sum(ground_truth == 0))
        },
        "methods": {}
    }
    
    for method_name, pred_path in prediction_paths.items():
        logger.info(f"Processing method: {method_name}")
        
        try:
            df, score_col, gt_col_in_file = load_predictions(pred_path)
            
            # If GT is not in the file, we use the external one. 
            # We must ensure alignment. Assuming the rows are in the same order
            # as the ground truth file if no timestamp alignment is done here.
            # For this task, we assume row-order alignment based on T026 requirements.
            if gt_col_in_file is not None:
                # Verify alignment if both are present
                # (Skipped for brevity, assuming T026 handled alignment)
                scores = df[score_col].values
                # We use the external ground_truth to ensure consistency with the main eval
                current_gt = ground_truth
            else:
                scores = df[score_col].values
                current_gt = ground_truth
            
            if len(scores) != len(current_gt):
                logger.warning(f"Length mismatch for {method_name}: scores={len(scores)}, gt={len(current_gt)}. Truncating to min length.")
                min_len = min(len(scores), len(current_gt))
                scores = scores[:min_len]
                current_gt = current_gt[:min_len]
            
            # Define thresholds to sweep
            # 1. Grid from 0.0 to 1.0 (assuming normalized scores)
            # 2. If scores are not normalized, use percentiles
            if scores.max() <= 1.0 and scores.min() >= 0.0:
                thresholds = np.linspace(0.0, 1.0, 51).tolist()
            else:
                # Use percentiles to cover the range of scores
                thresholds = np.percentile(scores, np.linspace(0, 100, 101)).tolist()
            
            # Run sweep
            sweep_data = sweep_thresholds(scores, current_gt, thresholds)
            
            # Find optimal points
            # Strategy 1: F1 Optimal
            opt_f1 = find_optimal_threshold(scores, current_gt, target_specificity=None)
            
            # Strategy 2: High Specificity (e.g., 95% or 99%)
            # We test 95% and 99% specificity
            opt_spec_95 = find_optimal_threshold(scores, current_gt, target_specificity=0.95)
            opt_spec_99 = find_optimal_threshold(scores, current_gt, target_specificity=0.99)
            
            analysis_results["methods"][method_name] = {
                "threshold_sweep": sweep_data,
                "optimal_f1": opt_f1,
                "high_specificity_95": opt_spec_95,
                "high_specificity_99": opt_spec_99
            }
            
            logger.info(f"  - F1 Optimal Threshold: {opt_f1['threshold']:.4f} (F1={opt_f1['metrics']['f1_score']:.4f})")
            logger.info(f"  - Spec 95% Threshold: {opt_spec_95['threshold']:.4f} (F1={opt_spec_95['metrics']['f1_score']:.4f})")
            
        except Exception as e:
            logger.error(f"Error processing {method_name}: {e}", exc_info=True)
            analysis_results["methods"][method_name] = {"error": str(e)}
    
    # Save results
    output_path.parent.mkdir(parents=True, exist_ok=True)
    with open(output_path, 'w') as f:
        json.dump(analysis_results, f, indent=2)
    
    logger.info(f"Sensitivity analysis saved to {output_path}")


