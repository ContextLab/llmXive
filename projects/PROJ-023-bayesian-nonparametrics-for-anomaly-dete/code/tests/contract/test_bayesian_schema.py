"""
Contract tests for Bayesian Inference Schema (US1).

These tests verify that the output of the Bayesian GP pipeline (T015)
strictly adheres to the schema defined in `contracts/evaluation.schema.yaml`.

Per tasks.md T011: IF plan.md TESTS_ENABLED is true, create this file.
This implementation assumes TESTS_ENABLED is true to ensure reproducibility.
"""
import pytest
import yaml
import pandas as pd
from pathlib import Path
from typing import Any, Dict

# Project root resolution (assuming tests are run from project root or via pytest discovery)
PROJECT_ROOT = Path(__file__).resolve().parent.parent.parent
CONTRACTS_DIR = PROJECT_ROOT / "contracts"
SCHEMA_PATH = CONTRACTS_DIR / "evaluation.schema.yaml"
RESULTS_DIR = PROJECT_ROOT / "data" / "results"
PREDICTIONS_FILE = RESULTS_DIR / "bayesian_predictions.csv"

def load_schema() -> Dict[str, Any]:
    """Load the evaluation schema from the contracts directory."""
    if not SCHEMA_PATH.exists():
        pytest.fail(f"Schema file not found at {SCHEMA_PATH}. "
                    "Ensure T005 (contracts) is completed before running T011.")
    with open(SCHEMA_PATH, "r") as f:
        return yaml.safe_load(f)

def load_predictions() -> pd.DataFrame:
    """Load the predictions file generated by T015."""
    if not PREDICTIONS_FILE.exists():
        pytest.fail(f"Predictions file not found at {PREDICTIONS_FILE}. "
                    "Ensure T015 (bayesian_gp.py) has been executed before running T011.")
    return pd.read_csv(PREDICTIONS_FILE)

class TestBayesianPredictionSchema:
    """
    Contract tests validating the structure and types of 
    data/results/bayesian_predictions.csv against the evaluation schema.
    """

    @pytest.fixture(autouse=True)
    def setup(self):
        """Load schema and data before each test."""
        self.schema = load_schema()
        self.df = load_predictions()

    def test_schema_file_exists(self):
        """Verify the schema definition exists."""
        assert self.schema is not None
        assert "predictions" in self.schema

    def test_required_columns_present(self):
        """Verify all required columns defined in schema exist."""
        required_columns = self.schema["predictions"]["required_columns"]
        actual_columns = set(self.df.columns)
        
        missing = set(required_columns) - actual_columns
        assert not missing, f"Missing required columns: {missing}"

    def test_column_types(self):
        """Verify column data types match schema expectations."""
        type_mapping = self.schema["predictions"]["column_types"]
        
        for col, expected_type in type_mapping.items():
            if col not in self.df.columns:
                continue  # Handled by required_columns test
            
            actual_dtype = self.df[col].dtype
            
            # Map pandas dtypes to schema expectations
            if expected_type == "integer":
                assert actual_dtype in ["int64", "int32", "int16"], \
                    f"Column {col} expected integer, got {actual_dtype}"
            elif expected_type == "float":
                assert actual_dtype in ["float64", "float32"], \
                    f"Column {col} expected float, got {actual_dtype}"
            elif expected_type == "string":
                # Pandas strings are usually object or string
                assert actual_dtype in ["object", "string"], \
                    f"Column {col} expected string, got {actual_dtype}"
            elif expected_type == "datetime":
                # Check if parsed as datetime or object containing dates
                assert actual_dtype == "datetime64[ns]" or actual_dtype == "object", \
                    f"Column {col} expected datetime, got {actual_dtype}"

    def test_no_missing_values(self):
        """Verify no null values in required columns."""
        required_columns = self.schema["predictions"]["required_columns"]
        for col in required_columns:
            if col in self.df.columns:
                null_count = self.df[col].isnull().sum()
                assert null_count == 0, \
                    f"Column {col} contains {null_count} missing values"

    def test_value_ranges(self):
        """Verify numeric columns are within expected bounds."""
        constraints = self.schema["predictions"].get("constraints", {})
        
        for col, constraint in constraints.items():
            if col not in self.df.columns:
                continue
            
            if "min" in constraint:
                min_val = self.df[col].min()
                assert min_val >= constraint["min"], \
                    f"Column {col} has value {min_val} below min {constraint['min']}"
            
            if "max" in constraint:
                max_val = self.df[col].max()
                assert max_val <= constraint["max"], \
                    f"Column {col} has value {max_val} above max {constraint['max']}"

    def test_anomaly_score_distribution(self):
        """Specific check for anomaly_score to ensure it's a probability-like score."""
        if "anomaly_score" in self.df.columns:
            # Scores should typically be between 0 and 1 or non-negative
            min_score = self.df["anomaly_score"].min()
            max_score = self.df["anomaly_score"].max()
            
            # Allow slight floating point errors, but enforce non-negative
            assert min_score >= -1e-9, "Anomaly scores cannot be negative"
            
            # If max is > 1, it might be a logit or raw score, but we flag it
            # Schema should ideally define if it's probability or log-odds
            if max_score > 1.0:
                # Log warning or assert depending on strictness. 
                # Here we assert it's not unreasonably large (e.g. > 1000)
                assert max_score < 1000.0, \
                    f"Anomaly scores seem unnormalized (max={max_score}). Check normalization."

    def test_ground_truth_consistency(self):
        """Verify ground_truth column (if present) is binary."""
        if "ground_truth" in self.df.columns:
            unique_vals = self.df["ground_truth"].unique()
            # Expect 0 and 1 (or True/False)
            valid_vals = {0, 1, False, True}
            invalid = set(unique_vals) - valid_vals
            assert not invalid, f"ground_truth contains invalid values: {invalid}"

    def test_row_count_consistency(self):
        """Verify the number of rows matches expected input size (if metadata exists)."""
        # This is a soft check; we just ensure we have at least one row
        assert len(self.df) > 0, "Predictions file is empty"