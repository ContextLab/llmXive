"""
Contract tests for the evaluation schema (US3).

These tests validate that the evaluation output files generated by
code/scripts/evaluate.py conform to the schema defined in
contracts/evaluation.schema.yaml.

Per Plan.md TESTS_ENABLED condition: These tests are created to ensure
the evaluation pipeline produces correctly structured JSON output.
"""
import pytest
import json
import yaml
import pandas as pd
from pathlib import Path
from typing import Any, Dict, List, Optional
import os
import sys

# Add project root to path for imports
PROJECT_ROOT = Path(__file__).parent.parent.parent
sys.path.insert(0, str(PROJECT_ROOT / "code"))

SCHEMA_PATH = PROJECT_ROOT / "contracts" / "evaluation.schema.yaml"
EVAL_OUTPUT_PATH = PROJECT_ROOT / "data" / "results" / "evaluation.json"


def load_schema(schema_path: Path) -> Dict[str, Any]:
    """Load and return the evaluation schema YAML."""
    if not schema_path.exists():
        pytest.fail(f"Schema file not found: {schema_path}")
    with open(schema_path, "r") as f:
        return yaml.safe_load(f)


def load_predictions(output_path: Path) -> Dict[str, Any]:
    """Load the evaluation JSON output."""
    if not output_path.exists():
        pytest.fail(f"Evaluation output file not found: {output_path}. "
                    "Run code/scripts/evaluate.py first.")
    with open(output_path, "r") as f:
        return json.load(f)


class TestEvaluationSchema:
    """
    Test suite to validate the structure and content of evaluation.json
    against contracts/evaluation.schema.yaml.
    """

    @pytest.fixture
    def schema(self) -> Dict[str, Any]:
        """Load the evaluation schema."""
        return load_schema(SCHEMA_PATH)

    @pytest.fixture
    def evaluation_data(self) -> Dict[str, Any]:
        """Load the evaluation output data."""
        return load_predictions(EVAL_OUTPUT_PATH)

    def test_schema_exists(self):
        """Verify the schema file exists and is valid YAML."""
        assert SCHEMA_PATH.exists(), "contracts/evaluation.schema.yaml must exist"
        schema = load_schema(SCHEMA_PATH)
        assert "properties" in schema, "Schema must define 'properties'"
        assert "required" in schema, "Schema must define 'required' fields"

    def test_required_top_level_fields(self, schema, evaluation_data):
        """Check that all required top-level fields are present."""
        required_fields = schema.get("required", [])
        missing = [f for f in required_fields if f not in evaluation_data]
        assert not missing, f"Missing required fields in evaluation.json: {missing}"

    def test_metrics_structure(self, evaluation_data):
        """Validate the 'metrics' section structure."""
        assert "metrics" in evaluation_data, "Missing 'metrics' key"
        metrics = evaluation_data["metrics"]
        
        # Expected metric keys based on US3 requirements
        expected_metric_keys = ["bayesian", "shewhart", "cusum", "vae"]
        for method in expected_metric_keys:
            assert method in metrics, f"Missing metrics for method: {method}"
            method_metrics = metrics[method]
            assert "precision" in method_metrics, f"Missing 'precision' for {method}"
            assert "recall" in method_metrics, f"Missing 'recall' for {method}"
            assert "f1_score" in method_metrics, f"Missing 'f1_score' for {method}"
            assert "auc_roc" in method_metrics, f"Missing 'auc_roc' for {method}"

    def test_statistical_tests_structure(self, evaluation_data):
        """Validate the 'statistical_tests' section."""
        assert "statistical_tests" in evaluation_data, "Missing 'statistical_tests' key"
        tests = evaluation_data["statistical_tests"]
        
        # Check for Wilcoxon or t-test results
        assert "wilcoxon" in tests or "paired_ttest" in tests, \
            "Must include either 'wilcoxon' or 'paired_ttest' results"
        
        # Verify p-value presence
        if "wilcoxon" in tests:
            assert "p_value" in tests["wilcoxon"], "Missing p-value in wilcoxon test"
        if "paired_ttest" in tests:
            assert "p_value" in tests["paired_ttest"], "Missing p-value in paired_ttest"

    def test_bonferroni_correction(self, evaluation_data):
        """Validate Bonferroni correction results are present."""
        assert "statistical_tests" in evaluation_data
        tests = evaluation_data["statistical_tests"]
        
        # Check for adjusted p-values
        has_adjusted = False
        for test_name, test_results in tests.items():
            if isinstance(test_results, dict) and "adjusted_p_value" in test_results:
                has_adjusted = True
                break
        
        # If Bonferroni is implemented, adjusted_p_value should exist
        # Note: This test passes if the structure is ready for Bonferroni
        # Even if not yet applied, the key should be present per T026c
        # We check for the key presence in the structure
        assert "bonferroni" in tests or any(
            "adjusted_p_value" in (v if isinstance(v, dict) else {})
            for v in tests.values()
        ), "Bonferroni correction or adjusted p-values must be present"

    def test_thresholding_strategy(self, evaluation_data):
        """Validate fixed thresholding strategy parameters are recorded."""
        assert "thresholding" in evaluation_data, \
            "Missing 'thresholding' section for fixed threshold strategy (FR-012)"
        
        thresholding = evaluation_data["thresholding"]
        assert "method" in thresholding, "Missing 'method' in thresholding"
        assert "parameters" in thresholding, "Missing 'parameters' in thresholding"

    def test_data_types(self, evaluation_data):
        """Verify numeric fields are actually numeric."""
        metrics = evaluation_data.get("metrics", {})
        for method, method_metrics in metrics.items():
            for metric_name, value in method_metrics.items():
                assert isinstance(value, (int, float)), \
                    f"Metric {metric_name} for {method} must be numeric, got {type(value)}"

    def test_p_value_range(self, evaluation_data):
        """Ensure p-values are within valid range [0, 1]."""
        tests = evaluation_data.get("statistical_tests", {})
        for test_name, test_results in tests.items():
            if isinstance(test_results, dict):
                p_val = test_results.get("p_value")
                if p_val is not None:
                    assert 0 <= p_val <= 1, \
                        f"P-value for {test_name} must be in [0, 1], got {p_val}"

    def test_schema_compliance(self, schema, evaluation_data):
        """
        Comprehensive check: ensure all properties defined in schema exist
        and match expected types (basic type checking).
        """
        def check_properties(data: Any, props: Dict[str, Any], path: str = ""):
            for prop_name, prop_def in props.items():
                current_path = f"{path}.{prop_name}" if path else prop_name
                assert prop_name in data, f"Missing property: {current_path}"
                
                value = data[prop_name]
                prop_type = prop_def.get("type")
                
                if prop_type == "object" and "properties" in prop_def:
                    check_properties(value, prop_def["properties"], current_path)
                elif prop_type == "array":
                    assert isinstance(value, list), f"{current_path} must be a list"
                elif prop_type == "number":
                    assert isinstance(value, (int, float)), f"{current_path} must be a number"
                elif prop_type == "string":
                    assert isinstance(value, str), f"{current_path} must be a string"
                elif prop_type == "boolean":
                    assert isinstance(value, bool), f"{current_path} must be a boolean"

        schema_props = schema.get("properties", {})
        check_properties(evaluation_data, schema_props)


if __name__ == "__main__":
    pytest.main([__file__, "-v"])
