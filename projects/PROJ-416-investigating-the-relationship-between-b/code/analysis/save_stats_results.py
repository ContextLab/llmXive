def load_stats_results_from_dict(stats_dict: Dict[str, Any]) -> List[Dict[str, Any]]:
    """
    Convert the dictionary output from stats.py into a list of rows for CSV.

    Args:
        stats_dict: Dictionary containing 'ancova_results', 'vif_values', 
                    'power_analysis', and 'sensitivity_analysis'.

    Returns:
        List of dictionaries, where each dict represents a row in the CSV.
    """
    rows = []
    
    # Extract ANCOVA results
    ancova_results = stats_dict.get('ancova_results', {})
    vif_values = stats_dict.get('vif_values', {})
    power_analysis = stats_dict.get('power_analysis', {})
    sensitivity_analysis = stats_dict.get('sensitivity_analysis', {})

    # Flatten power analysis for CSV columns
    power_cols = {f"power_{k}": v for k, v in power_analysis.items()} if power_analysis else {}

    # Flatten sensitivity analysis (if it's a dict of dicts, we might need to aggregate or pick one)
    # For this task, we assume sensitivity analysis is a summary dict or list of scenarios.
    # We will store the primary scenario results or a summary string.
    sens_summary = {}
    if sensitivity_analysis:
        if isinstance(sensitivity_analysis, dict):
            sens_summary = {f"sens_{k}": str(v) for k, v in sensitivity_analysis.items()}
        elif isinstance(sensitivity_analysis, list) and len(sensitivity_analysis) > 0:
            # Take the first scenario as a representative sample or join them
            first = sensitivity_analysis[0]
            sens_summary = {f"sens_{k}": str(v) for k, v in first.items()}

    # Construct the row
    row = {}
    
    # 1. ANCOVA Coefficients and P-values
    if ancova_results:
        for metric_name, results in ancova_results.items():
            # results is likely a dict: {'coef': ..., 'pval': ..., 'std_err': ...}
            row[f"coef_{metric_name}"] = results.get('coef', None)
            row[f"pval_{metric_name}"] = results.get('pval', None)
            row[f"std_err_{metric_name}"] = results.get('std_err', None)
            row[f"t_stat_{metric_name}"] = results.get('t_stat', None)
    
    # 2. VIF Values
    if vif_values:
        for var_name, vif_val in vif_values.items():
            row[f"vif_{var_name}"] = vif_val

    # 3. Power Analysis & Min N
    if power_analysis:
        row["min_N_required"] = power_analysis.get('min_n', None)
        row["power_actual"] = power_analysis.get('power', None)
        row["effect_size_f2"] = power_analysis.get('effect_size_f2', None)
        row["alpha"] = power_analysis.get('alpha', None)
    
    # 4. Sensitivity Analysis Summary
    row.update(sens_summary)

    # 5. Metadata/Run info (optional but good for provenance)
    row["status"] = "completed"
    row["model_type"] = "ANCOVA" if ancova_results else "Fallback"

    rows.append(row)
    return rows


def save_stats_to_csv(stats_dict: Dict[str, Any], output_path: Path) -> None:
    """
    Save statistical results to a CSV file.

    Args:
        stats_dict: The dictionary of results from stats.py.
        output_path: Path to the output CSV file.
    """
    if not output_path.parent.exists():
        logger.info(f"Creating output directory: {output_path.parent}")
        output_path.parent.mkdir(parents=True, exist_ok=True)

    rows = load_stats_results_from_dict(stats_dict)

    if not rows:
        logger.warning("No statistical results found to save.")
        # Create an empty file with headers if we have no data but need a file
        with open(output_path, 'w', newline='') as f:
            pass
        return

    # Determine all unique keys across all rows to ensure consistent columns
    fieldnames = []
    for row in rows:
        for key in row.keys():
            if key not in fieldnames:
                fieldnames.append(key)

    logger.info(f"Saving {len(rows)} row(s) to {output_path} with {len(fieldnames)} columns.")

    with open(output_path, 'w', newline='') as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(rows)

    logger.info(f"Successfully saved statistical results to {output_path}")


def run_save_stats_results(config: Config, stats_results: Dict[str, Any]) -> Path:
    """
    Main entry point for saving stats results.

    Args:
        config: Configuration object.
        stats_results: Dictionary containing the analysis results.

    Returns:
        Path to the saved CSV file.
    """
    output_path = config.OUTPUT_METRICS_DIR / "statistical_results.csv"
    save_stats_to_csv(stats_results, output_path)
    return output_path


