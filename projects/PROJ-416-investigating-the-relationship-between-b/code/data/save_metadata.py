"""
save_metadata.py

Aggregates subject information, exclusion reasons, and motion metrics
into a single JSON file at data/metrics/subject_info.json.

This module is designed to be run after preprocessing (T014-T016) to
consolidate the state of the dataset.
"""

import json
import logging
import sys
from datetime import datetime
from pathlib import Path
from typing import Dict, Any, List, Optional

# Import from project API surface
from code.config import Config
from code.utils.logging import setup_logging, log_provenance

# Import preprocessing logic to re-calculate or verify FD if needed,
# though typically we read from the logs or processed files generated by T015.
# For this task, we assume the preprocessing step has already generated
# the necessary metrics or logs we need to parse.
from code.data.preprocess import calculate_fd, check_motion_threshold

logger = logging.getLogger(__name__)


def load_subject_logs(log_path: Path) -> List[Dict[str, Any]]:
    """
    Parses the preprocessing log to extract exclusion reasons and motion metrics.
    
    This function is a pragmatic way to gather the data generated by T015/T016
    without requiring a shared database. It assumes the log contains structured
    entries or specific patterns for excluded subjects.
    
    In a more robust pipeline, this might read directly from a CSV or DB.
    Here, we simulate reading the 'state' that would exist after T016.
    """
    # Since we are implementing the *code* to save metadata, and the log
    # might not exist yet if run in isolation, we need to handle the case
    # where we are aggregating data that *should* have been produced.
    # 
    # However, the task requires us to produce the JSON. 
    # To make this script runnable and produce real output, we will:
    # 1. Look for a processed metrics CSV (if T015 saved it to a CSV as hinted).
    # 2. If not found, we check if we can reconstruct from the raw download
    #    and re-run the motion check (which is safe and deterministic).
    
    subjects_data = []
    
    # Strategy: Try to find the CSV generated by T015 (data/metrics/network_metrics.csv
    # or similar intermediate). The task description says FD is saved to
    # network_metrics.csv ONLY for passed subjects.
    # We need ALL subjects (passed and excluded) for subject_info.json.
    
    # Let's assume we have a way to get the list of subjects from the raw data
    # or the download step.
    # Since we cannot rely on T015 having run successfully in this specific
    # task execution context (we are implementing T017), we will implement
    # a robust loader that attempts to read existing data and falls back
    # to re-processing if necessary, or simply aggregates known data.
    
    # For the purpose of this implementation, we will assume the existence
    # of a 'subjects.csv' or similar list from the download/validate step,
    # or we will iterate over the raw data directory to find subjects.
    
    config = Config()
    raw_dir = config.data_raw_path
    
    if not raw_dir.exists():
        logger.warning(f"Raw data directory {raw_dir} not found. Cannot generate subject list.")
        return []

    # Identify subjects: Assuming BIDS-like structure or specific OpenNeuro naming
    # We look for directories that look like 'sub-XXXX'
    subject_dirs = [d for d in raw_dir.iterdir() if d.is_dir() and d.name.startswith('sub-')]
    
    # If no BIDS structure, maybe it's just files. 
    # Given T012/T013, we expect a dataset.
    
    if not subject_dirs:
        # Fallback: Check if there's a manifest or CSV from validation
        manifest_path = raw_dir / "dataset_description.json" # Common BIDS
        if manifest_path.exists():
            # If we have a dataset, we still need to find subjects.
            # We'll just return empty if we can't find them dynamically.
            pass
        
    # Since we need to produce a REAL file, and we might not have the 
    # intermediate logs from T016 yet, we will implement the logic to
    # scan the raw data, calculate FD (using the existing API), and
    # determine exclusion status. This makes T017 a standalone aggregator.
    
    for sub_dir in subject_dirs:
        subject_id = sub_dir.name
        fd_value = None
        excluded = False
        exclusion_reason = None
        
        # We need the functional image to calculate FD.
        # Look for a *_bold.nii.gz file
        bold_files = list(sub_dir.rglob("*_bold.nii.gz"))
        
        if bold_files:
            # Calculate FD using the existing API from T015
            try:
                # We need the confounds file usually. 
                # calculate_fd in preprocess.py likely handles this or takes a path.
                # Let's assume we can call it or a helper.
                # Since the API surface says calculate_fd exists, we use it.
                # However, calculate_fd might require the confounds file.
                # We will attempt to find confounds.
                confounds_files = list(sub_dir.rglob("*_confounds.tsv"))
                
                if confounds_files:
                    fd_value = calculate_fd(bold_files[0], confounds_files[0])
                else:
                    # If no confounds, we might not be able to calculate FD accurately.
                    # We'll log a warning and mark as excluded or unknown.
                    logger.warning(f"No confounds found for {subject_id}. Cannot calculate FD.")
                    fd_value = None
            except Exception as e:
                logger.error(f"Error calculating FD for {subject_id}: {e}")
                fd_value = None
        else:
            logger.warning(f"No bold file found for {subject_id}")
        
        if fd_value is not None:
            # Check threshold (3mm as per T015)
            passed, reason = check_motion_threshold(fd_value, threshold=3.0)
            if not passed:
                excluded = True
                exclusion_reason = reason
            else:
                excluded = False
                exclusion_reason = None
        else:
            # If we can't calculate FD, we might exclude or flag.
            # Per strict constraints, if we can't verify, we might exclude.
            excluded = True
            exclusion_reason = "Missing confounds or FD calculation failure"

        subjects_data.append({
            "subject_id": subject_id,
            "excluded": excluded,
            "exclusion_reason": exclusion_reason,
            "motion_metrics": {
                "mean_fd": fd_value
            },
            "data_path": str(sub_dir)
        })

    return subjects_data


def save_subject_info(subjects: List[Dict[str, Any]], output_path: Path) -> None:
    """
    Saves the aggregated subject information to a JSON file.
    """
    output_path.parent.mkdir(parents=True, exist_ok=True)
    
    report = {
        "generated_at": datetime.utcnow().isoformat(),
        "pipeline_version": "1.0.0", # Placeholder, could be from config
        "total_subjects": len(subjects),
        "excluded_count": sum(1 for s in subjects if s["excluded"]),
        "included_count": sum(1 for s in subjects if not s["excluded"]),
        "subjects": subjects
    }
    
    with open(output_path, 'w') as f:
        json.dump(report, f, indent=2)
    
    logger.info(f"Subject metadata saved to {output_path}")


def run_save_metadata(output_path: Optional[Path] = None) -> Path:
    """
    Main entry point for the metadata aggregation task.
    """
    config = Config()
    
    if output_path is None:
        output_path = config.metrics_path / "subject_info.json"
    
    logger.info(f"Starting metadata aggregation for {output_path}")
    
    # 1. Collect data
    subjects = load_subject_logs(config.data_raw_path)
    
    if not subjects:
        logger.warning("No subjects found to process. Creating empty report.")
    
    # 2. Save
    save_subject_info(subjects, output_path)
    
    return output_path


def main():
    """
    CLI entry point.
    """
    logging_config = setup_logging(level=logging.INFO)
    log_provenance("save_metadata", sys.argv)
    
    try:
        output_file = run_save_metadata()
        print(f"Success: {output_file}")
        sys.exit(0)
    except Exception as e:
        logger.critical(f"Failed to save metadata: {e}", exc_info=True)
        sys.exit(1)


if __name__ == "__main__":
    main()