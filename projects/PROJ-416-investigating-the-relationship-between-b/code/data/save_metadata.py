"""
save_metadata.py

Implements T017: Store subject list, exclusion reasons, and motion metrics
in data/metrics/subject_info.json.

Reads logs generated by preprocessing (T016) and metrics calculation (T015)
to compile a comprehensive subject information file.
"""

import json
import logging
import os
import sys
from datetime import datetime
from pathlib import Path
from typing import Dict, List, Any, Optional

# Ensure imports match the API surface provided in the prompt
# The prompt lists: from utils.logging import JsonFormatter, setup_logging, log_provenance, log_exclusion
# We will assume the logging setup is handled by the main pipeline or we set up a basic logger here.
from code.utils.logging import setup_logging, log_provenance, log_exclusion

# Import Config if needed for paths, though we can also derive from standard paths
from code.config import Config

logger = logging.getLogger(__name__)

def load_subject_logs(log_dir: Path) -> List[Dict[str, Any]]:
    """
    Load and parse log entries from the preprocessing logs.
    Expects logs to be in JSON format or structured text lines as produced by T016.
    """
    logs = []
    log_files = list(log_dir.glob("*.log"))
    
    if not log_files:
        logger.warning(f"No log files found in {log_dir}")
        return logs

    for log_file in log_files:
        try:
            with open(log_file, 'r', encoding='utf-8') as f:
                for line in f:
                    line = line.strip()
                    if not line:
                        continue
                    # Attempt to parse as JSON (common for structured logging)
                    if line.startswith('{'):
                        try:
                            entry = json.loads(line)
                            logs.append(entry)
                        except json.JSONDecodeError:
                            # Fallback: treat as raw text if JSON fails
                            logs.append({"raw": line, "source": str(log_file)})
                    else:
                        logs.append({"raw": line, "source": str(log_file)})
        except Exception as e:
            logger.error(f"Error reading log file {log_file}: {e}")
    
    return logs

def parse_log_entry(entry: Dict[str, Any]) -> Optional[Dict[str, Any]]:
    """
    Parse a raw log entry into a structured subject info record.
    Looks for specific keys: subject_id, status, exclusion_reason, fd_value, etc.
    """
    subject_info = {}
    
    # Try to extract standard fields
    if "subject_id" in entry:
        subject_info["subject_id"] = entry["subject_id"]
    elif "subject" in entry:
        subject_info["subject_id"] = entry["subject"]
    
    if "status" in entry:
        subject_info["status"] = entry["status"]
    elif "exclusion_reason" in entry:
        subject_info["status"] = "excluded"
        subject_info["exclusion_reason"] = entry["exclusion_reason"]
    elif "reason" in entry:
        subject_info["status"] = "excluded"
        subject_info["exclusion_reason"] = entry["reason"]
    else:
        # Default to included if no exclusion reason found
        subject_info["status"] = "included"
    
    # Extract motion metrics if present
    if "fd" in entry:
        subject_info["motion_metrics"] = {"fd": entry["fd"]}
    elif "mean_fd" in entry:
        subject_info["motion_metrics"] = {"mean_fd": entry["mean_fd"]}
    elif "metrics" in entry and isinstance(entry["metrics"], dict):
        subject_info["motion_metrics"] = entry["metrics"]
    
    # Extract pre/post scores if present in logs
    if "pre_score" in entry:
        subject_info["clinical_scores"] = subject_info.get("clinical_scores", {})
        subject_info["clinical_scores"]["pre"] = entry["pre_score"]
    if "post_score" in entry:
        subject_info["clinical_scores"] = subject_info.get("clinical_scores", {})
        subject_info["clinical_scores"]["post"] = entry["post_score"]
        
    return subject_info if subject_info.get("subject_id") else None

def load_motion_metrics_from_log(logs: List[Dict[str, Any]]) -> List[Dict[str, Any]]:
    """
    Filter and collect motion metrics from parsed logs.
    Returns a list of dicts containing subject_id and motion metrics.
    """
    metrics = []
    for log in logs:
        parsed = parse_log_entry(log)
        if parsed and parsed.get("status") == "included":
            if "motion_metrics" in parsed:
                metrics.append({
                    "subject_id": parsed["subject_id"],
                    **parsed["motion_metrics"]
                })
    return metrics

def save_subject_info(subject_list: List[Dict[str, Any]], output_path: Path) -> None:
    """
    Save the compiled subject information to a JSON file.
    
    Args:
        subject_list: List of dictionaries containing subject details
        output_path: Path to the output JSON file
    """
    output_path.parent.mkdir(parents=True, exist_ok=True)
    
    output_data = {
        "generated_at": datetime.now().isoformat(),
        "total_subjects": len(subject_list),
        "included_count": sum(1 for s in subject_list if s.get("status") == "included"),
        "excluded_count": sum(1 for s in subject_list if s.get("status") == "excluded"),
        "subjects": subject_list
    }
    
    with open(output_path, 'w', encoding='utf-8') as f:
        json.dump(output_data, f, indent=2)
    
    logger.info(f"Saved subject info to {output_path}")

def run_save_metadata(config: Config) -> Dict[str, Any]:
    """
    Main entry point for T017.
    Reads logs, parses them, and saves to data/metrics/subject_info.json.
    
    Args:
        config: Configuration object from code.config
    
    Returns:
        Dictionary with summary statistics
    """
    # Setup logging
    log_path = config.LOGS_DIR
    output_path = config.METRICS_DIR / "subject_info.json"
    
    # Ensure directories exist
    config.METRICS_DIR.mkdir(parents=True, exist_ok=True)
    
    logger.info(f"Starting subject metadata save. Log dir: {log_path}, Output: {output_path}")
    
    # 1. Load logs
    logs = load_subject_logs(log_path)
    logger.info(f"Loaded {len(logs)} log entries.")
    
    # 2. Parse and compile subject info
    subject_list = []
    for log in logs:
        parsed = parse_log_entry(log)
        if parsed:
            subject_list.append(parsed)
    
    # 3. Deduplicate if necessary (in case multiple logs per subject exist)
    # Simple dedup by subject_id, keeping the last occurrence
    unique_subjects = {}
    for subj in subject_list:
        uid = subj.get("subject_id")
        if uid:
            unique_subjects[uid] = subj
    
    final_list = list(unique_subjects.values())
    
    # 4. Save to JSON
    save_subject_info(final_list, output_path)
    
    return {
        "total": len(final_list),
        "included": sum(1 for s in final_list if s.get("status") == "included"),
        "excluded": sum(1 for s in final_list if s.get("status") == "excluded"),
        "output_file": str(output_path)
    }

def main() -> None:
    """CLI entry point."""
    config = Config()
    setup_logging(config.LOGS_DIR)
    
    try:
        result = run_save_metadata(config)
        logger.info(f"Task T017 completed successfully. Result: {result}")
        print(json.dumps(result, indent=2))
    except Exception as e:
        logger.error(f"Task T017 failed: {e}", exc_info=True)
        sys.exit(1)

if __name__ == "__main__":
    main()
