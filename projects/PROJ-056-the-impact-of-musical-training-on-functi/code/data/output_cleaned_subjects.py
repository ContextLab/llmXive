"""
T019 Implementation: Output cleaned subjects to CSV.

This script takes the preprocessed subject data (generated by the pipeline
in previous steps) and writes it to the required output file:
data/processed/subjects_cleaned.csv

It ensures the CSV contains exactly the columns specified in the task:
subject_id, group, years_of_training, age, sex, motion_score, ses_score.
"""
import os
import sys
import pandas as pd
import logging

# Add project root to path to allow imports
project_root = os.path.dirname(os.path.dirname(os.path.dirname(os.path.abspath(__file__))))
sys.path.insert(0, project_root)

from utils.logging import get_logger
from utils.memory_monitor import check_memory_limit, get_current_memory_mb
from data.preprocess import preprocess_subjects

logger = get_logger(__name__)

def write_cleaned_subjects(output_path: str) -> None:
    """
    Runs the preprocessing pipeline and writes the result to a CSV.
    
    Args:
        output_path: The full path where the CSV should be written.
    """
    logger.info(f"Starting T019: Outputting cleaned subjects to {output_path}")
    
    # Ensure memory is within limits before heavy processing
    check_memory_limit()
    
    # Get the directory for the output file
    output_dir = os.path.dirname(output_path)
    if output_dir and not os.path.exists(output_dir):
        os.makedirs(output_dir)
        logger.info(f"Created output directory: {output_dir}")

    # Run the full preprocessing pipeline
    # This function (from T016/T015) returns a DataFrame with cleaned data
    try:
        df_clean = preprocess_subjects()
    except Exception as e:
        logger.error(f"Preprocessing pipeline failed: {e}")
        raise

    # Validate the DataFrame has the required columns
    required_columns = [
        'subject_id', 'group', 'years_of_training', 
        'age', 'sex', 'motion_score', 'ses_score'
    ]
    
    missing_cols = [col for col in required_columns if col not in df_clean.columns]
    if missing_cols:
        raise ValueError(f"Missing required columns in processed data: {missing_cols}")
    
    # Select only the required columns to ensure exact output format
    df_output = df_clean[required_columns].copy()
    
    # Check memory again before writing
    mem_mb = get_current_memory_mb()
    logger.info(f"Memory usage before write: {mem_mb:.2f} MB")
    check_memory_limit()

    # Write to CSV
    df_output.to_csv(output_path, index=False)
    
    logger.info(f"Successfully wrote {len(df_output)} subjects to {output_path}")
    
    # Log a sample for verification
    logger.debug(f"Sample output:\n{df_output.head()}")

def main():
    """Entry point for the script."""
    output_file = os.path.join(
        project_root, 
        "data", 
        "processed", 
        "subjects_cleaned.csv"
    )
    
    try:
        write_cleaned_subjects(output_file)
        logger.info("T019 completed successfully.")
    except Exception as e:
        logger.critical(f"T019 failed: {e}")
        sys.exit(1)

if __name__ == "__main__":
    main()