"""
Contract test for run_inference.py output schema.

This test verifies that the output file generated by run_inference.py
adheres to the expected schema defined in the project specification.

It checks:
1. The file exists at the expected path.
2. The file is valid JSON.
3. The root is a list of records.
4. Each record contains the required fields:
   - prompt_id (string)
   - model_used (string)
   - energy_kWh (float, > 0)
   - co2_kg (float, > 0)
   - loc_count (int, > 0)
   - generated_code (string, non-empty)
"""
import json
import os
import pytest
from pathlib import Path

# Path to the expected output file (relative to project root)
OUTPUT_PATH = Path("data/processed/llm_inference_results.json")


class TestInferenceOutputSchema:
    """Contract tests for the LLM inference output schema."""

    @pytest.fixture(scope="module")
    def inference_data(self):
        """Load the inference results JSON file."""
        if not OUTPUT_PATH.exists():
            pytest.fail(f"Output file not found: {OUTPUT_PATH}. "
                        "Run code/run_inference.py first.")
        
        with open(OUTPUT_PATH, "r", encoding="utf-8") as f:
            try:
                return json.load(f)
            except json.JSONDecodeError as e:
                pytest.fail(f"Output file is not valid JSON: {e}")

    def test_file_exists(self):
        """Verify the output file exists."""
        assert OUTPUT_PATH.exists(), f"Output file {OUTPUT_PATH} does not exist."

    def test_is_list(self, inference_data):
        """Verify the root element is a list."""
        assert isinstance(inference_data, list), "Root element must be a list of records."

    def test_has_records(self, inference_data):
        """Verify there is at least one record."""
        assert len(inference_data) > 0, "Inference results list must not be empty."

    def test_required_fields_present(self, inference_data):
        """Verify all required fields are present in every record."""
        required_fields = {
            "prompt_id",
            "model_used",
            "energy_kWh",
            "co2_kg",
            "loc_count",
            "generated_code"
        }

        for idx, record in enumerate(inference_data):
            missing = required_fields - set(record.keys())
            assert not missing, f"Record {idx} missing fields: {missing}"

    def test_prompt_id_is_string(self, inference_data):
        """Verify prompt_id is a string."""
        for idx, record in enumerate(inference_data):
            assert isinstance(record["prompt_id"], str), f"Record {idx}: prompt_id must be a string."
            assert len(record["prompt_id"]) > 0, f"Record {idx}: prompt_id cannot be empty."

    def test_model_used_is_string(self, inference_data):
        """Verify model_used is a string."""
        for idx, record in enumerate(inference_data):
            assert isinstance(record["model_used"], str), f"Record {idx}: model_used must be a string."

    def test_energy_kwh_is_positive_float(self, inference_data):
        """Verify energy_kWh is a positive float."""
        for idx, record in enumerate(inference_data):
            val = record["energy_kWh"]
            assert isinstance(val, (int, float)), f"Record {idx}: energy_kWh must be numeric."
            assert val > 0, f"Record {idx}: energy_kWh must be positive, got {val}."

    def test_co2_kg_is_positive_float(self, inference_data):
        """Verify co2_kg is a positive float."""
        for idx, record in enumerate(inference_data):
            val = record["co2_kg"]
            assert isinstance(val, (int, float)), f"Record {idx}: co2_kg must be numeric."
            assert val > 0, f"Record {idx}: co2_kg must be positive, got {val}."

    def test_loc_count_is_positive_int(self, inference_data):
        """Verify loc_count is a positive integer."""
        for idx, record in enumerate(inference_data):
            val = record["loc_count"]
            assert isinstance(val, int), f"Record {idx}: loc_count must be an integer."
            assert val > 0, f"Record {idx}: loc_count must be positive, got {val}."

    def test_generated_code_is_non_empty_string(self, inference_data):
        """Verify generated_code is a non-empty string."""
        for idx, record in enumerate(inference_data):
            val = record["generated_code"]
            assert isinstance(val, str), f"Record {idx}: generated_code must be a string."
            assert len(val) > 0, f"Record {idx}: generated_code cannot be empty."

    def test_no_null_values(self, inference_data):
        """Verify no required field contains null."""
        required_fields = ["prompt_id", "model_used", "energy_kWh", "co2_kg", "loc_count", "generated_code"]
        
        for idx, record in enumerate(inference_data):
            for field in required_fields:
                assert record[field] is not None, f"Record {idx}: {field} cannot be null."