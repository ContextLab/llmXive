def test_full_metric_pipeline(filtered_dataset_path):
    """
    T021: Integration test for the full metric pipeline.
    
    Steps:
    1. Load filtered data.
    2. Compute metrics (shape, spin, concentration).
    3. Validate output ranges (s in [0,1], c > 0, etc.).
    4. Check convergence stats file.
    """
    logger.info("Starting full metric pipeline integration test...")
    
    results_dir = project_root / "results"
    results_dir.mkdir(parents=True, exist_ok=True)
    stats_path = results_dir / "convergence_stats.json"
    
    # Run the pipeline
    # run_compute_metrics_pipeline(input_path, output_path, stats_path)
    # Assuming the function signature based on T026.
    
    # We need to ensure the function exists and takes these args.
    # If the function writes to a default stats path, we check that.
    # For the test, we pass explicit paths.
    
    output_metrics_path = results_dir / "halo_metrics_integration.parquet"
    
    try:
        run_compute_metrics_pipeline(
            input_path=filtered_dataset_path,
            output_path=str(output_metrics_path),
            stats_path=str(stats_path)
        )
    except Exception as e:
        logger.error(f"Pipeline execution failed: {e}", exc_info=True)
        raise

    # Assertions
    assert output_metrics_path.exists(), "Output metrics file not created."
    assert stats_path.exists(), "Convergence stats file not created."

    # Validate metrics file content
    df_metrics = pd.read_parquet(output_metrics_path)
    
    required_columns = ['halo_id', 'shape_s', 'spin_lambda', 'concentration_c']
    for col in required_columns:
        assert col in df_metrics.columns, f"Missing column: {col}"

    # Validate physical ranges
    # Shape s: c/a, should be between 0 and 1 (usually)
    # Note: Depending on definition, s could be > 1 if a < c, but typically s <= 1 for flattened.
    # The task T025 says: if not (0 <= s <= 1): raise ValueError. So we expect valid data.
    if 'shape_s' in df_metrics.columns:
        assert df_metrics['shape_s'].between(0, 1).all(), "Shape values out of expected range [0, 1]."
    
    # Concentration c: must be > 0
    if 'concentration_c' in df_metrics.columns:
        assert (df_metrics['concentration_c'] > 0).all(), "Concentration values must be positive."
    
    # Spin lambda: typically 0 to 1
    if 'spin_lambda' in df_metrics.columns:
        assert df_metrics['spin_lambda'].between(0, 1).all(), "Spin values out of expected range [0, 1]."

    # Validate convergence stats
    with open(stats_path, 'r') as f:
        stats = json.load(f)
    
    assert 'total_halos' in stats, "Missing 'total_halos' in stats."
    assert 'successful_fits' in stats, "Missing 'successful_fits' in stats."
    assert 'failed_fits' in stats, "Missing 'failed_fits' in stats."
    
    total = stats['total_halos']
    success = stats['successful_fits']
    failed = stats['failed_fits']
    
    assert total == success + failed, "Stats sum mismatch."
    assert total > 0, "No halos processed."
    
    logger.info(f"Pipeline test passed. Processed {total} halos. Success rate: {success/total:.2%}")

