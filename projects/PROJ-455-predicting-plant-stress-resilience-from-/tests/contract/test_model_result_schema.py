"""
Contract test for model output schema.

This test verifies that the model output generated by the training pipeline
conforms to the schema defined in `contracts/model_result.schema.yaml`.
It ensures that all required fields are present and have the correct data types.
"""
import os
import json
import yaml
import pytest
from typing import Any, Dict
from pathlib import Path

# Project root is two levels up from tests/contract
PROJECT_ROOT = Path(__file__).resolve().parent.parent.parent
CONTRACTS_DIR = PROJECT_ROOT / "contracts"
SCHEMA_PATH = CONTRACTS_DIR / "model_result.schema.yaml"

# Expected schema structure based on typical model_result requirements
# The schema defines the structure of the ModelResult object
EXPECTED_REQUIRED_FIELDS = {
    "model_type": str,
    "metric_name": str,
    "metric_value": (int, float),
    "top_features": list,
    "training_samples": int,
    "timestamp": str,
    "validation_method": str
}

def load_schema() -> Dict[str, Any]:
    """Load the model result schema from the YAML file."""
    if not SCHEMA_PATH.exists():
        pytest.fail(f"Schema file not found: {SCHEMA_PATH}")
    
    with open(SCHEMA_PATH, "r") as f:
        return yaml.safe_load(f)

def validate_model_result_schema(data: Dict[str, Any], schema: Dict[str, Any]) -> None:
    """
    Validate that the model result data conforms to the schema.
    
    Args:
        data: The model result dictionary to validate.
        schema: The schema definition.
        
    Raises:
        AssertionError: If the data does not conform to the schema.
    """
    # Check required properties
    required_fields = schema.get("required", [])
    properties = schema.get("properties", {})
    
    for field in required_fields:
        assert field in data, f"Missing required field in model result: '{field}'"
        
        if field in properties:
            prop_def = properties[field]
            expected_type = prop_def.get("type")
            
            if expected_type == "string":
                assert isinstance(data[field], str), f"Field '{field}' must be a string"
            elif expected_type == "number":
                assert isinstance(data[field], (int, float)), f"Field '{field}' must be a number"
            elif expected_type == "integer":
                assert isinstance(data[field], int), f"Field '{field}' must be an integer"
            elif expected_type == "array":
                assert isinstance(data[field], list), f"Field '{field}' must be a list"
            elif expected_type == "object":
                assert isinstance(data[field], dict), f"Field '{field}' must be a dictionary"
    
    # Check for unexpected fields if additionalProperties is false
    if schema.get("additionalProperties") is False:
        for key in data.keys():
            assert key in properties, f"Unexpected field in model result: '{key}'"

@pytest.mark.contract
def test_model_result_schema_structure():
    """Test that the schema file exists and has the expected structure."""
    schema = load_schema()
    
    assert "type" in schema, "Schema must define a type"
    assert schema["type"] == "object", "Model result schema type must be 'object'"
    assert "properties" in schema, "Schema must define properties"
    assert "required" in schema, "Schema must define required fields"
    
    # Verify specific expected fields exist in schema
    properties = schema["properties"]
    for field in EXPECTED_REQUIRED_FIELDS.keys():
        assert field in properties, f"Schema missing expected field: '{field}'"

@pytest.mark.contract
def test_model_result_data_conformance():
    """Test a sample model result against the schema."""
    schema = load_schema()
    
    # Create a sample model result that should conform to the schema
    sample_result = {
        "model_type": "RandomForest",
        "metric_name": "R2",
        "metric_value": 0.85,
        "top_features": [
            {"name": "Metabolite_A", "importance": 0.25},
            {"name": "Metabolite_B", "importance": 0.18},
            {"name": "Metabolite_C", "importance": 0.12}
        ],
        "training_samples": 150,
        "timestamp": "2023-10-27T10:30:00Z",
        "validation_method": "LODO"
    }
    
    validate_model_result_schema(sample_result, schema)

@pytest.mark.contract
def test_model_result_invalid_data():
    """Test that invalid data is correctly rejected."""
    schema = load_schema()
    
    # Create an invalid model result (missing required field)
    invalid_result = {
        "model_type": "SVM",
        "metric_name": "Pearson_r",
        # Missing 'metric_value'
        "top_features": [],
        "training_samples": 50,
        "timestamp": "2023-10-27T10:30:00Z",
        "validation_method": "LODO"
    }
    
    with pytest.raises(AssertionError) as exc_info:
        validate_model_result_schema(invalid_result, schema)
    
    assert "metric_value" in str(exc_info.value)

@pytest.mark.contract
def test_model_result_type_mismatch():
    """Test that type mismatches are correctly rejected."""
    schema = load_schema()
    
    # Create a result with wrong type for a field
    invalid_result = {
        "model_type": "RandomForest",
        "metric_name": "R2",
        "metric_value": "not_a_number",  # Should be number
        "top_features": [],
        "training_samples": 150,
        "timestamp": "2023-10-27T10:30:00Z",
        "validation_method": "LODO"
    }
    
    with pytest.raises(AssertionError) as exc_info:
        validate_model_result_schema(invalid_result, schema)
    
    assert "number" in str(exc_info.value).lower() or "metric_value" in str(exc_info.value)