"""
Verification script for the final analysis report (T024).

This script validates that the final report generated by report_generator.py
contains all required metrics and correctly interprets statistical significance
based on the p < 0.05 threshold.

It also provides a programmatic interface for automated testing.
"""
import json
import logging
import sys
from pathlib import Path
from typing import Dict, Any, List, Tuple, Optional

# Add project root to path if running as script
if __name__ == "__main__":
    sys.path.insert(0, str(Path(__file__).parent.parent.parent))

from utils.logging import get_logger

logger = get_logger(__name__)

# Required keys in the report JSON
REQUIRED_KEYS = [
    "spearman_correlation_age_perplexity",
    "spearman_correlation_age_correctness",
    "p_value_age_perplexity",
    "p_value_age_correctness",
    "significant_age_perplexity",
    "significant_age_correctness",
    "interpretation",
    "methodology",
    "data_summary"
]

# Numeric keys that must be floats
NUMERIC_KEYS = [
    "spearman_correlation_age_perplexity",
    "spearman_correlation_age_correctness",
    "p_value_age_perplexity",
    "p_value_age_correctness"
]

# Boolean keys
BOOLEAN_KEYS = [
    "significant_age_perplexity",
    "significant_age_correctness"
]

def load_report(report_path: Path) -> Optional[Dict[str, Any]]:
    """Load the JSON report file."""
    try:
        with open(report_path, 'r', encoding='utf-8') as f:
            return json.load(f)
    except FileNotFoundError:
        logger.error(f"Report file not found: {report_path}")
        return None
    except json.JSONDecodeError as e:
        logger.error(f"Invalid JSON in report file: {e}")
        return None

def validate_required_keys(report: Dict[str, Any]) -> Tuple[bool, List[str]]:
    """Check that all required keys are present."""
    missing = [key for key in REQUIRED_KEYS if key not in report]
    if missing:
        logger.error(f"Missing required keys: {missing}")
        return False, missing
    return True, []

def validate_numeric_values(report: Dict[str, Any]) -> Tuple[bool, List[str]]:
    """Ensure numeric fields are actually numbers."""
    errors = []
    for key in NUMERIC_KEYS:
        value = report.get(key)
        if value is None:
            errors.append(f"{key} is None")
        elif not isinstance(value, (int, float)):
            errors.append(f"{key} is not numeric: {type(value).__name__}")
        elif key.startswith("p_value") and not (0.0 <= value <= 1.0):
            errors.append(f"{key} is not in valid p-value range [0, 1]: {value}")
    
    if errors:
        logger.error("Numeric validation errors: " + "; ".join(errors))
        return False, errors
    return True, []

def validate_boolean_values(report: Dict[str, Any]) -> Tuple[bool, List[str]]:
    """Ensure boolean fields are actually booleans."""
    errors = []
    for key in BOOLEAN_KEYS:
        value = report.get(key)
        if value is None:
            errors.append(f"{key} is None")
        elif not isinstance(value, bool):
            errors.append(f"{key} is not boolean: {type(value).__name__}")
    
    if errors:
        logger.error("Boolean validation errors: " + "; ".join(errors))
        return False, errors
    return True, []

def validate_significance_logic(report: Dict[str, Any]) -> Tuple[bool, List[str]]:
    """
    Verify that significance flags are correctly computed based on p < 0.05.
    
    For each correlation pair:
    - significant_X should be True if p_value_X < 0.05
    - significant_X should be False if p_value_X >= 0.05
    """
    errors = []
    pairs = [
        ("p_value_age_perplexity", "significant_age_perplexity"),
        ("p_value_age_correctness", "significant_age_correctness")
    ]
    
    for p_key, sig_key in pairs:
        p_val = report.get(p_key)
        sig_val = report.get(sig_key)
        
        if p_val is None or sig_val is None:
            continue  # Already caught by previous validators
        
        expected_significance = p_val < 0.05
        if sig_val != expected_significance:
            errors.append(
                f"Inconsistency: {p_key}={p_val} but {sig_key}={sig_val} "
                f"(expected {expected_significance})"
            )
    
    if errors:
        logger.error("Significance logic errors: " + "; ".join(errors))
        return False, errors
    return True, []

def validate_interpretation(report: Dict[str, Any]) -> Tuple[bool, List[str]]:
    """
    Verify that the interpretation field correctly reflects the significance results.
    
    The interpretation should mention "significant" if any p < 0.05,
    and "no significant" if all p >= 0.05.
    """
    interpretation = report.get("interpretation", "")
    if not isinstance(interpretation, str) or len(interpretation) == 0:
        logger.error("Interpretation field is empty or not a string")
        return False, ["Interpretation is missing or empty"]
    
    # Check for consistency with significance flags
    sig_perplexity = report.get("significant_age_perplexity", False)
    sig_correctness = report.get("significant_age_correctness", False)
    
    has_any_significance = sig_perplexity or sig_correctness
    
    if has_any_significance:
        if "no significant" in interpretation.lower():
            logger.warning("Interpretation says 'no significant' but significance flags are True")
            # This is a warning, not a hard failure, as the wording might be nuanced
    else:
        if "significant correlation" in interpretation.lower() and "no" not in interpretation.lower():
            logger.warning("Interpretation implies significance but flags are False")
    
    return True, []

def verify_report(report_path: Path) -> bool:
    """
    Main verification function. Returns True if the report passes all checks.
    
    Args:
        report_path: Path to the JSON report file
        
    Returns:
        bool: True if all validations pass, False otherwise
    """
    logger.info(f"Verifying report: {report_path}")
    
    report = load_report(report_path)
    if report is None:
        return False
    
    all_passed = True
    
    # Run all validators
    passed, missing = validate_required_keys(report)
    if not passed:
        all_passed = False
    
    passed, errors = validate_numeric_values(report)
    if not passed:
        all_passed = False
    
    passed, errors = validate_boolean_values(report)
    if not passed:
        all_passed = False
    
    passed, errors = validate_significance_logic(report)
    if not passed:
        all_passed = False
    
    passed, errors = validate_interpretation(report)
    if not passed:
        all_passed = False
    
    if all_passed:
        logger.info("Report verification PASSED: All required metrics present and significance correctly interpreted.")
    else:
        logger.error("Report verification FAILED: See errors above.")
    
    return all_passed

def main():
    """CLI entry point for report verification."""
    import argparse
    
    parser = argparse.ArgumentParser(
        description="Verify the final analysis report contains all required metrics and correct significance interpretation."
    )
    parser.add_argument(
        "--report-path",
        type=Path,
        default=Path("data/results/final_report.json"),
        help="Path to the JSON report file (default: data/results/final_report.json)"
    )
    
    args = parser.parse_args()
    
    success = verify_report(args.report_path)
    sys.exit(0 if success else 1)

if __name__ == "__main__":
    main()
