#!/usr/bin/env python3
"""
Task T001: Verify HumanEval dataset availability and version from HuggingFace Datasets.

This script:
1. Loads the HumanEval dataset from HuggingFace Datasets
2. Records version information
3. Writes to data/README.md
4. Writes to research.md Section 1

Output:
- data/README.md: Dataset information
- research.md: Section 1 with dataset details
"""

import os
import sys
from datetime import datetime
from pathlib import Path

# Add project root to path for imports
PROJECT_ROOT = Path(__file__).parent.parent
DATA_DIR = PROJECT_ROOT / "data"
RESEARCH_FILE = PROJECT_ROOT / "research.md"

# HumanEval dataset info
DATASET_NAME = "openai_humaneval"
DATASET_SOURCE_URL = "https://huggingface.co/datasets/openai_humaneval"


def verify_humaneval_dataset():
    """
    Verify HumanEval dataset availability and extract version information.

    Returns:
        dict: Dataset information including name, version, fetch_date, source_url
    """
    try:
        from datasets import load_dataset

        # Load the HumanEval dataset
        print(f"Loading dataset: {DATASET_NAME}")
        dataset = load_dataset(DATASET_NAME)

        # Get dataset info
        dataset_info = dataset.info

        # Extract version information if available
        version = getattr(dataset_info, "version", "N/A")
        if version is None:
            version = "N/A"

        # Get the dataset features to verify it loaded correctly
        features = dataset_info.features if dataset_info else {}

        # Get split information
        splits = list(dataset.keys()) if hasattr(dataset, "keys") else []

        # Count total problems
        total_problems = sum(len(dataset[s]) for s in splits) if splits else 0

        return {
            "dataset_name": DATASET_NAME,
            "version": str(version),
            "source_url": DATASET_SOURCE_URL,
            "splits": splits,
            "total_problems": total_problems,
            "features": list(features.keys()) if features else [],
        }
    except Exception as e:
        print(f"Error loading dataset: {e}", file=sys.stderr)
        return None


def write_data_readme(dataset_info, fetch_date):
    """
    Write dataset information to data/README.md.

    Args:
        dataset_info: Dict with dataset details
        fetch_date: ISO8601 formatted fetch date
    """
    DATA_DIR.mkdir(parents=True, exist_ok=True)
    readme_path = DATA_DIR / "README.md"

    content = f"""# Data Directory

## HumanEval Dataset

This directory contains the HumanEval dataset used for evaluating code simplification effectiveness.

### Dataset Information

- **Dataset Name**: {dataset_info["dataset_name"]}
- **Version**: {dataset_info["version"]}
- **Source URL**: {dataset_info["source_url"]}
- **Fetch Date**: {fetch_date}
- **Total Problems**: {dataset_info["total_problems"]}
- **Splits**: {", ".join(dataset_info["splits"])}

### Features

The HumanEval dataset contains the following fields:
{chr(10).join(f'- `{feature}`' for feature in dataset_info["features"])}

### Raw Data

Raw dataset files will be stored in `data/raw/` after download.

### Processed Data

Processed and benchmark results will be stored in `data/processed/`.

### Logs

Pipeline logs will be stored in `data/logs/`.

---
*Generated by verify_dataset.py on {fetch_date}*
"""

    with open(readme_path, "w", encoding="utf-8") as f:
        f.write(content)

    print(f"Written: {readme_path}")


def write_research_section1(dataset_info, fetch_date):
    """
    Write research.md Section 1 with dataset information.

    Args:
        dataset_info: Dict with dataset details
        fetch_date: ISO8601 formatted fetch date
    """
    RESEARCH_FILE.parent.mkdir(parents=True, exist_ok=True)

    # Check if research.md exists and has content
    existing_content = ""
    if RESEARCH_FILE.exists():
        with open(RESEARCH_FILE, "r", encoding="utf-8") as f:
            existing_content = f.read()

    # Build Section 1 content
    section1_content = f"""# Section 1: Dataset Verification

## HumanEval Dataset

| Field | Value |
|-------|-------|
| dataset_name | {dataset_info["dataset_name"]} |
| version_number | {dataset_info["version"]} |
| fetch_date | {fetch_date} |
| source_url | {dataset_info["source_url"]} |

### Dataset Details

- **Total Problems**: {dataset_info["total_problems"]}
- **Splits Available**: {", ".join(dataset_info["splits"])}
- **Features**: {", ".join(dataset_info["features"])}

### Verification Status

- [x] Dataset accessible from HuggingFace Datasets
- [x] Dataset version recorded
- [x] Checksum verification pending (see T001a)

---
"""

    # Check if Section 1 already exists and update it
    if "# Section 1:" in existing_content:
        # Find the position of Section 1 and replace it
        section1_start = existing_content.find("# Section 1:")
        # Find the next section or end of file
        next_section = existing_content.find("# Section 2:", section1_start)
        if next_section == -1:
            next_section = len(existing_content)

        new_content = (
            existing_content[:section1_start] + section1_content + existing_content[next_section:]
        )
    else:
        # Append Section 1 to beginning (after any header)
        if existing_content.strip():
            new_content = section1_content + "\n" + existing_content
        else:
            new_content = section1_content

    with open(RESEARCH_FILE, "w", encoding="utf-8") as f:
        f.write(new_content)

    print(f"Updated: {RESEARCH_FILE} (Section 1)")


def main():
    """Main entry point for T001 verification."""
    print("=" * 60)
    print("Task T001: Verify HumanEval Dataset Availability")
    print("=" * 60)

    # Get current timestamp for fetch date
    fetch_date = datetime.utcnow().strftime("%Y-%m-%dT%H:%M:%SZ")
    print(f"Fetch timestamp: {fetch_date}")

    # Verify dataset
    dataset_info = verify_humaneval_dataset()

    if dataset_info is None:
        print("ERROR: Failed to verify HumanEval dataset", file=sys.stderr)
        sys.exit(1)

    print(f"\nDataset verified successfully:")
    print(f"  Name: {dataset_info['dataset_name']}")
    print(f"  Version: {dataset_info['version']}")
    print(f"  Problems: {dataset_info['total_problems']}")

    # Write output files
    print("\nWriting output files...")
    write_data_readme(dataset_info, fetch_date)
    write_research_section1(dataset_info, fetch_date)

    print("\n" + "=" * 60)
    print("Task T001 completed successfully")
    print("=" * 60)


if __name__ == "__main__":
    main()