"""
Full integration test for the llmXive pipeline (Task T038).

Verifies the end-to-end flow:
1. Data Ingestion (Graph Construction)
2. Topological Metric Extraction
3. Statistical Power Check
4. GNN Training (Proxy Target)
5. Correlation Analysis (LMM & Pearson)
6. Final Results Aggregation

This script runs on real data generated by previous tasks or fetches
fallback samples if raw data is missing. It enforces a 6-hour wall-clock
limit (SC-005) by using small sample sizes and reduced simulation parameters.
"""
import json
import logging
import os
import sys
import time
import traceback
from pathlib import Path
from typing import Dict, Any, List, Optional

# Add project root to path for imports
project_root = Path(__file__).resolve().parent.parent.parent
sys.path.insert(0, str(project_root / "code"))

from config import get_config, get_paths
from ingest.graph_builder import process_directory
from ingest.node_degree_stats_generator import main as generate_degree_stats
from metrics.topology_extractor import extract_topology_metrics
from analysis.power_checker import main as run_power_check
from model.gnn import train_gnn_model
from model.feature_importance import extract_feature_importance
from analysis.lmm_analysis import main as run_lmm_analysis
from analysis.correlation_significance import main as run_correlation_significance
from analysis.final_results_aggregator import main as run_aggregator

# Configure logging
logging.basicConfig(
    level=logging.INFO,
    format='%(asctime)s - %(name)s - %(levelname)s - %(message)s',
    handlers=[
        logging.StreamHandler(sys.stdout),
        logging.FileHandler(project_root / "data" / "integration_test.log")
    ]
)
logger = logging.getLogger("IntegrationTest")

WALL_CLOCK_LIMIT_SECONDS = 6 * 3600  # 6 hours
TIMEOUT_MARGIN_SECONDS = 300  # 5 minutes buffer

def run_stage(stage_name: str, func, *args, **kwargs) -> bool:
    """Run a pipeline stage with timing and error handling."""
    logger.info(f"--- Starting Stage: {stage_name} ---")
    start_time = time.time()
    try:
        result = func(*args, **kwargs)
        elapsed = time.time() - start_time
        logger.info(f"--- Stage {stage_name} completed in {elapsed:.2f}s ---")
        return True, result
    except Exception as e:
        elapsed = time.time() - start_time
        logger.error(f"Stage {stage_name} FAILED after {elapsed:.2f}s: {str(e)}")
        logger.error(traceback.format_exc())
        return False, str(e)

def check_timeout(start_time: float) -> bool:
    """Check if the wall clock limit has been exceeded."""
    elapsed = time.time() - start_time
    if elapsed > (WALL_CLOCK_LIMIT_SECONDS - TIMEOUT_MARGIN_SECONDS):
        logger.warning(f"Approaching wall-clock limit. Elapsed: {elapsed:.2f}s")
        return True
    return False

def main():
    start_time = time.time()
    config = get_config()
    paths = get_paths()
    
    success = True
    errors = []

    # 1. Data Ingestion (Graph Construction)
    # We assume raw data might be missing, so we rely on sample_generator to create fallbacks
    # or fetch real data. The graph_builder processes whatever is in data/raw/
    logger.info("Stage 1: Data Ingestion & Graph Construction")
    if check_timeout(start_time):
        success = False
        errors.append("Timeout before ingestion start")
    else:
        # Ensure raw data directory exists
        raw_dir = paths["data_raw"]
        if not raw_dir.exists():
            raw_dir.mkdir(parents=True, exist_ok=True)
            logger.info(f"Created empty raw data directory: {raw_dir}")
        
        # Run graph builder
        ok, _ = run_stage("Graph Builder", process_directory, str(raw_dir), str(paths["data_processed_graphs"]))
        if not ok:
            success = False
            errors.append("Graph Builder failed")
        else:
            # Generate degree stats
            ok, _ = run_stage("Node Degree Stats", generate_degree_stats)
            if not ok:
                success = False
                errors.append("Node Degree Stats generation failed")

    # 2. Topological Metric Extraction
    logger.info("Stage 2: Topological Metric Extraction")
    if success and not check_timeout(start_time):
        ok, _ = run_stage("Topology Extraction", extract_topology_metrics)
        if not ok:
            success = False
            errors.append("Topology Extraction failed")

    # 3. Statistical Power Check
    logger.info("Stage 3: Statistical Power Check")
    if success and not check_timeout(start_time):
        ok, _ = run_stage("Power Check", run_power_check)
        if not ok:
            success = False
            errors.append("Power Check failed")
        # Note: Power check might log a warning if N < 10, but should not halt

    # 4. GNN Training (Proxy Target)
    logger.info("Stage 4: GNN Training")
    if success and not check_timeout(start_time):
        ok, _ = run_stage("GNN Training", train_gnn_model)
        if not ok:
            success = False
            errors.append("GNN Training failed")

    # 5. Feature Importance Extraction
    logger.info("Stage 5: Feature Importance Extraction")
    if success and not check_timeout(start_time):
        ok, _ = run_stage("Feature Importance", extract_feature_importance)
        if not ok:
            success = False
            errors.append("Feature Importance extraction failed")

    # 6. Correlation Analysis (LMM)
    logger.info("Stage 6: LMM Analysis")
    if success and not check_timeout(start_time):
        ok, _ = run_stage("LMM Analysis", run_lmm_analysis)
        if not ok:
            success = False
            errors.append("LMM Analysis failed")

    # 7. Correlation Significance (Pearson)
    logger.info("Stage 7: Pearson Correlation & Significance")
    if success and not check_timeout(start_time):
        ok, _ = run_stage("Pearson Significance", run_correlation_significance)
        if not ok:
            success = False
            errors.append("Pearson Significance analysis failed")

    # 8. Final Results Aggregation
    logger.info("Stage 8: Final Results Aggregation")
    if success and not check_timeout(start_time):
        ok, _ = run_stage("Results Aggregation", run_aggregator)
        if not ok:
            success = False
            errors.append("Results Aggregation failed")

    # Final Report
    total_time = time.time() - start_time
    report_path = paths["data_processed_model_outputs"] / "integration_test_report.json"
    
    report = {
        "task_id": "T038",
        "status": "SUCCESS" if success else "FAILED",
        "total_time_seconds": total_time,
        "wall_clock_limit_seconds": WALL_CLOCK_LIMIT_SECONDS,
        "within_time_limit": total_time < WALL_CLOCK_LIMIT_SECONDS,
        "errors": errors,
        "artifacts_verified": []
    }

    # Verify key output artifacts exist
    expected_artifacts = [
        paths["data_processed_graphs"] / "node_degree_stats.json",
        paths["data_processed_graphs"] / "excluded_samples.json", # May or may not exist
        paths["data_processed_model_outputs"] / "power_analysis.json",
        paths["data_processed_model_outputs"] / "lmm_results.json",
        paths["data_processed_model_outputs"] / "correlation_pearson_corrected.json",
        paths["data_processed_model_outputs"] / "final_results.json"
    ]

    for artifact in expected_artifacts:
        if artifact.exists():
            report["artifacts_verified"].append(str(artifact.relative_to(project_root)))
        else:
            # Some might be optional or skipped due to low N
            if "excluded_samples" in str(artifact):
                continue 
            report["artifacts_verified"].append(f"MISSING: {artifact.relative_to(project_root)}")

    with open(report_path, "w") as f:
        json.dump(report, f, indent=2)

    logger.info(f"Integration Test Report saved to: {report_path}")
    logger.info(f"Final Status: {'SUCCESS' if success else 'FAILED'}")
    logger.info(f"Total Time: {total_time:.2f}s (Limit: {WALL_CLOCK_LIMIT_SECONDS}s)")

    if not success:
        logger.error(f"Errors encountered: {errors}")
        sys.exit(1)
    else:
        sys.exit(0)

if __name__ == "__main__":
    main()