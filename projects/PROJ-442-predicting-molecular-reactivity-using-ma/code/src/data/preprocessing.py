import pandas as pd
import hashlib
import logging
import os
from pathlib import Path
from typing import Optional, Tuple
import json
import time

from src.modeling.config import load_config
from src.utils.logging import setup_logger, get_logger
from src.utils.state_manager import update_stage_status, register_artifact

# Ensure the logger is configured
logger = get_logger(__name__)

def compute_file_checksum(file_path: str) -> str:
    """
    Compute SHA256 checksum of a file.
    
    Args:
        file_path: Path to the file
        
    Returns:
        Hex digest of the file's SHA256 hash
    """
    sha256_hash = hashlib.sha256()
    try:
        with open(file_path, "rb") as f:
            for byte_block in iter(lambda: f.read(4096), b""):
                sha256_hash.update(byte_block)
        return sha256_hash.hexdigest()
    except FileNotFoundError:
        logger.error(f"File not found for checksum: {file_path}")
        raise

def save_feature_matrix(df: pd.DataFrame, output_path: str) -> str:
    """
    Save feature matrix to parquet format and compute checksum.
    
    Args:
        df: DataFrame containing features
        output_path: Path where the parquet file will be saved
        
    Returns:
        The checksum of the saved file
    """
    # Ensure directory exists
    os.makedirs(os.path.dirname(output_path), exist_ok=True)
    
    # Save to parquet
    df.to_parquet(output_path, index=False, engine='pyarrow')
    
    # Compute checksum
    checksum = compute_file_checksum(output_path)
    logger.info(f"Saved feature matrix to {output_path} with checksum {checksum}")
    
    return checksum

def load_feature_matrix(input_path: str) -> pd.DataFrame:
    """
    Load feature matrix from parquet file.
    
    Args:
        input_path: Path to the parquet file
        
    Returns:
        DataFrame containing features
    """
    if not os.path.exists(input_path):
        raise FileNotFoundError(f"Feature matrix file not found: {input_path}")
    
    df = pd.read_parquet(input_path, engine='pyarrow')
    logger.info(f"Loaded feature matrix from {input_path} with shape {df.shape}")
    return df

def preprocess_and_save_features(
    input_path: str,
    output_path: str,
    config_path: Optional[str] = None
) -> Tuple[str, Dict]:
    """
    Main function to load features (from T021/T022/T023), save to parquet, and register state.
    
    This function assumes that the feature extraction and dimensionality reduction
    (T021, T022, T023) have already been performed and the resulting DataFrame
    is available at `input_path` (or generated in memory if the path is None).
    
    For T024 specifically, we focus on saving the feature matrix to parquet.
    
    Args:
        input_path: Path to the input CSV containing features (from preprocessing steps)
        output_path: Path where the parquet file will be saved
        config_path: Optional path to config file for metadata
        
    Returns:
        Tuple of (checksum, metadata_dict)
    """
    start_time = time.time()
    logger.info(f"Starting feature matrix save process for {input_path} -> {output_path}")
    
    # Load config if provided
    config = None
    if config_path and os.path.exists(config_path):
        config = load_config(config_path)
    
    # Load the feature data
    # Note: In the pipeline, T023 outputs the processed DataFrame.
    # We assume the calling code (e.g., main.py or a wrapper) passes the DataFrame
    # or a path to a CSV generated by T023.
    # For this implementation, we assume input_path points to a CSV of features.
    if not os.path.exists(input_path):
        raise FileNotFoundError(f"Input feature file not found: {input_path}")
    
    df = pd.read_csv(input_path)
    logger.info(f"Loaded input features with shape: {df.shape}")
    
    # Save to parquet
    checksum = save_feature_matrix(df, output_path)
    
    # Prepare metadata
    metadata = {
        "input_file": input_path,
        "output_file": output_path,
        "checksum": checksum,
        "row_count": len(df),
        "column_count": len(df.columns),
        "columns": list(df.columns),
        "timestamp": time.strftime("%Y-%m-%dT%H:%M:%SZ", time.gmtime()),
        "runtime_seconds": time.time() - start_time
    }
    
    if config:
        metadata["config_loaded"] = True
    
    # Register artifact in state manager
    # This assumes the state manager is configured for the project
    try:
        register_artifact(
            artifact_name="feature_matrix",
            file_path=output_path,
            checksum=checksum,
            metadata=metadata
        )
        logger.info("Successfully registered artifact in state manager")
    except Exception as e:
        logger.warning(f"Failed to register artifact in state manager: {e}")
    
    return checksum, metadata

def main():
    """
    Entry point for the preprocessing script.
    Expects command line arguments or defaults to config paths.
    """
    import argparse
    
    parser = argparse.ArgumentParser(description="Save feature matrix to parquet")
    parser.add_argument(
        "--input", 
        type=str, 
        default="data/processed/feature_data.csv",
        help="Path to input CSV with features"
    )
    parser.add_argument(
        "--output", 
        type=str, 
        default="data/processed/feature_matrix.parquet",
        help="Path to output parquet file"
    )
    parser.add_argument(
        "--config", 
        type=str, 
        default="src/modeling/config.yaml",
        help="Path to config file"
    )
    
    args = parser.parse_args()
    
    # Setup logger
    setup_logger(level=logging.INFO)
    
    try:
        checksum, metadata = preprocess_and_save_features(
            input_path=args.input,
            output_path=args.output,
            config_path=args.config
        )
        print(f"Successfully saved feature matrix to {args.output}")
        print(f"Checksum: {checksum}")
        print(f"Metadata: {json.dumps(metadata, indent=2)}")
    except Exception as e:
        logger.error(f"Failed to save feature matrix: {e}")
        raise

if __name__ == "__main__":
    main()