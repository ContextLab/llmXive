def apply_listwise_deletion(
    ratings: List[Dict[str, Any]],
    excluded_participants: Dict[str, Any]
) -> List[Dict[str, Any]]:
    """
    Implements listwise deletion of excluded participants.
    
    Reads exclusion flags from the cleaning log (generated by T016).
    Removes ALL rows associated with any participant marked as excluded.
    
    Args:
        ratings: List of rating records (dicts).
        excluded_participants: Dict mapping participant_id to exclusion reason.
        
    Returns:
        Filtered list of rating records containing only valid participants.
    """
    if not excluded_participants:
        return ratings

    excluded_ids = set(excluded_participants.keys())
    clean_ratings = []
    excluded_count = 0
    total_count = len(ratings)

    for record in ratings:
        p_id = record.get('participant_id')
        if p_id in excluded_ids:
            excluded_count += 1
            # Log the exclusion for audit trail (optional, but good practice)
            # log_exclusion(p_id, "Listwise deletion", excluded_participants[p_id])
        else:
            clean_ratings.append(record)

    logging.info(f"Listwise deletion: Removed {excluded_count} records from {total_count} "
                 f"({excluded_participants[p_id] if p_id in excluded_ids else 'kept'} excluded participants).")
    
    return clean_ratings

