def load_cleaning_log() -> Dict[str, Any]:
    """
    Loads the cleaning log produced by T016 (03_clean_data.py).
    Returns a dictionary mapping participant_id to exclusion status/reason.
    """
    log_path = get_processed_data_dir() / "cleaning_log.csv"
    excluded_participants = {}

    if not log_path.exists():
        logging.warning(f"Cleaning log not found at {log_path}. No exclusions applied.")
        return excluded_participants

    with open(log_path, 'r', encoding='utf-8') as f:
        reader = csv.DictReader(f)
        for row in reader:
            p_id = row.get('participant_id')
            is_excluded = row.get('is_excluded', '').lower() == 'true'
            reason = row.get('reason', 'unknown')

            if is_excluded and p_id:
                excluded_participants[p_id] = reason

    return excluded_participants

