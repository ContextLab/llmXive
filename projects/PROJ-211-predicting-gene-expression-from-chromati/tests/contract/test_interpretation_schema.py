"""
Contract test for interpretation output schema (User Story 3).

This test validates that the output files generated by code/interpret.py
conform to the expected schema definitions. It ensures that:
1. feature_importance.csv contains required columns: gene_id, peak_id, coefficient, abs_coefficient
2. peak_annotations.csv contains required columns: peak_id, chromosome, start, end, strand, distance_to_tss, nearest_gene
3. tss_proximity_stats.json contains required keys: total_features, top_100_count, percentage_within_10kb
4. performance_gap.json contains required keys: housekeeping_r2_mean, cell_type_specific_r2_mean, delta_r2

The test loads the actual output files from data/processed/ and validates
their structure against the schema contracts defined in specs/001-gene-regulation/contracts/
"""

import os
import sys
import json
import csv
import pytest
from typing import Dict, List, Any, Set

# Add the project root to the path to allow imports from code/
project_root = os.path.abspath(os.path.join(os.path.dirname(__file__), '..', '..'))
if project_root not in sys.path:
    sys.path.insert(0, project_root)

# Constants for expected file paths
DATA_PROCESSED_DIR = os.path.join(project_root, "data", "processed")

EXPECTED_FILES = {
    "feature_importance": os.path.join(DATA_PROCESSED_DIR, "feature_importance.csv"),
    "peak_annotations": os.path.join(DATA_PROCESSED_DIR, "peak_annotations.csv"),
    "tss_proximity_stats": os.path.join(DATA_PROCESSED_DIR, "tss_proximity_stats.json"),
    "performance_gap": os.path.join(DATA_PROCESSED_DIR, "performance_gap.json"),
}

# Schema definitions
FEATURE_IMPORTANCE_SCHEMA = {
    "required_columns": {"gene_id", "peak_id", "coefficient", "abs_coefficient"},
    "column_types": {
        "gene_id": str,
        "peak_id": str,
        "coefficient": (int, float),
        "abs_coefficient": (int, float),
    },
}

PEAK_ANNOTATIONS_SCHEMA = {
    "required_columns": {"peak_id", "chromosome", "start", "end", "strand", "distance_to_tss", "nearest_gene"},
    "column_types": {
        "peak_id": str,
        "chromosome": str,
        "start": int,
        "end": int,
        "strand": str,
        "distance_to_tss": (int, float),
        "nearest_gene": str,
    },
}

TSS_PROXIMITY_STATS_SCHEMA = {
    "required_keys": {"total_features", "top_100_count", "percentage_within_10kb"},
    "type_constraints": {
        "total_features": int,
        "top_100_count": int,
        "percentage_within_10kb": (int, float),
    },
}

PERFORMANCE_GAP_SCHEMA = {
    "required_keys": {"housekeeping_r2_mean", "cell_type_specific_r2_mean", "delta_r2"},
    "type_constraints": {
        "housekeeping_r2_mean": (int, float),
        "cell_type_specific_r2_mean": (int, float),
        "delta_r2": (int, float),
    },
}


def load_csv_as_dicts(filepath: str) -> List[Dict[str, Any]]:
    """Load a CSV file and return a list of dictionaries."""
    if not os.path.exists(filepath):
        raise FileNotFoundError(f"File not found: {filepath}")
    with open(filepath, 'r', newline='', encoding='utf-8') as f:
        reader = csv.DictReader(f)
        return list(reader)


def load_json(filepath: str) -> Dict[str, Any]:
    """Load a JSON file and return a dictionary."""
    if not os.path.exists(filepath):
        raise FileNotFoundError(f"File not found: {filepath}")
    with open(filepath, 'r', encoding='utf-8') as f:
        return json.load(f)


class TestFeatureImportanceSchema:
    """Test suite for feature_importance.csv schema validation."""

    def test_file_exists(self):
        """Test that feature_importance.csv exists."""
        assert os.path.exists(EXPECTED_FILES["feature_importance"]), \
            f"Missing file: {EXPECTED_FILES['feature_importance']}"

    def test_required_columns_present(self):
        """Test that all required columns are present."""
        data = load_csv_as_dicts(EXPECTED_FILES["feature_importance"])
        if not data:
            pytest.skip("Empty file, skipping column check")
        headers = set(data[0].keys())
        missing = FEATURE_IMPORTANCE_SCHEMA["required_columns"] - headers
        assert not missing, f"Missing required columns: {missing}"

    def test_column_types(self):
        """Test that values in columns match expected types."""
        data = load_csv_as_dicts(EXPECTED_FILES["feature_importance"])
        if not data:
            pytest.skip("Empty file, skipping type check")

        for row_idx, row in enumerate(data):
            for col, expected_type in FEATURE_IMPORTANCE_SCHEMA["column_types"].items():
                if col not in row:
                    continue  # Handled by column test
                val = row[col]
                # Convert string to appropriate type for checking
                try:
                    if expected_type == str:
                        pass  # Always str
                    elif expected_type == int:
                        int(val)
                    elif expected_type == float:
                        float(val)
                    elif isinstance(expected_type, tuple):
                        # Try int first, then float
                        try:
                            int(val)
                        except ValueError:
                            float(val)
                except ValueError:
                    assert False, f"Row {row_idx}, Col '{col}': '{val}' is not valid {expected_type}"


