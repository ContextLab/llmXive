    def test_synthetic_counts_schema_compliance(self):
        """
        Validate that the synthetic counts file (data/raw/synthetic_counts.csv)
        conforms to the dataset schema requirements (metadata + counts_matrix structure).
        """
        data_path = project_root / "data" / "raw" / "synthetic_counts.csv"
        schema_path = project_root / "specs" / "001-gene-regulation" / "contracts" / "dataset_schema.schema.yaml"
        
        # Ensure data file exists (generated by T011)
        assert data_path.exists(), f"Synthetic counts file not found at {data_path}. Run T011 first."
        
        # Load schema
        with open(schema_path, 'r') as f:
            schema = yaml.safe_load(f)
        
        # Load data
        df = pd.read_csv(data_path)
        
        # Check required columns based on typical schema for counts_matrix
        # Schema usually implies: gene_id, cell_line, and then peak counts or similar structure.
        # Assuming a wide format or long format. Let's validate common expectations:
        # 1. Must have a gene identifier column
        # 2. Must have a cell line identifier column
        # 3. Must have numeric count columns
        
        assert "gene_id" in df.columns, "Dataset must contain 'gene_id' column"
        assert "cell_line" in df.columns, "Dataset must contain 'cell_line' column"
        
        # Check that count columns are numeric
        count_cols = [c for c in df.columns if c not in ['gene_id', 'cell_line']]
        assert len(count_cols) > 0, "Dataset must contain at least one count column"
        
        for col in count_cols:
            assert pd.api.types.is_numeric_dtype(df[col]), f"Column {col} must be numeric"

    def test_synthetic_peaks_schema_compliance(self):
        """
        Validate that the synthetic peaks file (data/raw/synthetic_peaks.bed)
        conforms to the BED format expectations (chrom, start, end, name, score, strand).
        """
        data_path = project_root / "data" / "raw" / "synthetic_peaks.bed"
        
        assert data_path.exists(), f"Synthetic peaks file not found at {data_path}. Run T011 first."
        
        # BED files are tab-separated, usually 3+ columns.
        # We expect: chrom, start, end, name, score, strand (6 cols standard)
        try:
            # Read first few lines to check structure
            with open(data_path, 'r') as f:
                header = f.readline()
                if header.startswith('#'):
                    header = f.readline()
            
            # Re-read with pandas for easier validation
            df = pd.read_csv(data_path, sep='\t', header=None, comment='#', 
                             names=['chrom', 'start', 'end', 'name', 'score', 'strand'],
                             usecols=[0, 1, 2, 3, 4, 5], 
                             on_bad_lines='skip')
            
            assert len(df) > 0, "Peaks file is empty"
            
            # Validate types
            assert df['start'].dtype in [int, 'int64', 'int32'], "Start must be integer"
            assert df['end'].dtype in [int, 'int64', 'int32'], "End must be integer"
            assert (df['end'] > df['start']).all(), "End must be greater than start"
            
        except Exception as e:
            pytest.fail(f"Failed to validate peaks file structure: {e}")