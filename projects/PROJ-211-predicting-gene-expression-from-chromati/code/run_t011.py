import os
import sys
import logging
from utils import checksum_file
from generate_data import set_seed, generate_gene_coordinates, generate_peak_coordinates, generate_counts_matrix, write_counts_csv, write_peaks_bed, main as generate_main

# Configure logging
logging.basicConfig(
    level=logging.INFO,
    format='%(asctime)s - %(levelname)s - %(message)s',
    handlers=[
        logging.FileHandler('logs/run_t011.log'),
        logging.StreamHandler(sys.stdout)
    ]
)
logger = logging.getLogger(__name__)

def main():
    logger.info("Starting T011: Generating synthetic paired RNA-seq and DNase-seq counts")
    
    # Ensure directories exist
    os.makedirs('data/raw', exist_ok=True)
    os.makedirs('logs', exist_ok=True)

    # Parameters from task description
    seed = 42
    cell_lines = ['GM12878', 'K562', 'HMEC', 'IMR90', 'HepG2']
    num_genes = 10000
    num_peaks = 10000

    logger.info(f"Setting seed to {seed}")
    set_seed(seed)

    logger.info(f"Generating coordinates for {num_genes} genes and {num_peaks} peaks")
    gene_coords = generate_gene_coordinates(num_genes)
    peak_coords = generate_peak_coordinates(num_peaks)

    logger.info("Generating counts matrix")
    # Generate counts: shape (num_cells, num_genes) for expression
    # and (num_cells, num_peaks) for accessibility
    counts_data, peaks_data = generate_counts_matrix(
        cell_lines, 
        num_genes, 
        num_peaks, 
        gene_coords, 
        peak_coords
    )

    # Write outputs
    counts_path = 'data/raw/synthetic_counts.csv'
    peaks_path = 'data/raw/synthetic_peaks.bed'

    logger.info(f"Writing counts to {counts_path}")
    write_counts_csv(counts_data, counts_path)

    logger.info(f"Writing peaks to {peaks_path}")
    write_peaks_bed(peak_coords, peaks_path)

    # Verify file existence and calculate checksums
    checksums = {}
    for path in [counts_path, peaks_path]:
        if not os.path.exists(path):
            raise FileNotFoundError(f"Expected output file not created: {path}")
        checksums[path] = checksum_file(path)
        logger.info(f"Checksum for {path}: {checksums[path]}")

    # Write checksums to log file
    checksum_log_path = 'logs/checksums.txt'
    with open(checksum_log_path, 'w') as f:
        f.write(f"# Checksums generated by T011 at seed={seed}\n")
        for path, checksum in checksums.items():
            f.write(f"{checksum}  {os.path.basename(path)}\n")
    
    logger.info(f"Checksums recorded in {checksum_log_path}")
    logger.info("T011 completed successfully")

if __name__ == "__main__":
    main()