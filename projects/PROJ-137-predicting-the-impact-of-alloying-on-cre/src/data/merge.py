"""
Data Merging (T018).

Joins composition data with thermodynamic properties.
"""
import pandas as pd
import logging
from src.utils.validators import parse_composition_string

logger = logging.getLogger(__name__)

def merge_thermodynamic_properties(df: pd.DataFrame, log_exclusions: bool = True) -> pd.DataFrame:
    """
    Merge with thermodynamic descriptors.
    For this implementation, we assume the data already contains these columns
    (as generated by T016) or we calculate them if raw composition is available.
    
    Since T016 generates 'mixing_enthalpy' and 'radius_mismatch' directly,
    this function ensures they are present and valid.
    """
    required_thermo = ["mixing_enthalpy", "radius_mismatch"]
    
    # Check if columns exist
    missing_cols = [c for c in required_thermo if c not in df.columns]
    if missing_cols:
        logger.warning(f"Missing thermodynamic columns: {missing_cols}. Attempting to calculate or skip.")
        # In a real scenario, we would call pymatgen here to calculate these.
        # For this task, we assume they are present from generation or a real DB.
        # If missing, we cannot proceed with the 'Thermodynamic' model.
        # We'll raise an error if they are strictly required and missing.
        raise ValueError(f"Thermodynamic properties missing: {missing_cols}")
    
    # Ensure numeric
    for col in required_thermo:
        df[col] = pd.to_numeric(df[col], errors='coerce')
    
    # Drop rows where thermodynamic data is missing (if any)
    initial_len = len(df)
    df = df.dropna(subset=required_thermo)
    dropped = initial_len - len(df)
    
    if dropped > 0 and log_exclusions:
        logger.warning(f"Dropped {dropped} entries due to missing thermodynamic data.")
    
    return df

