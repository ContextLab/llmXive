"""
Training module for Alloy Creep Resistance models.

Implements:
1. Loading processed data from the pipeline.
2. Ensuring both models train on the exact same intersection of valid rows.
3. Nested Cross-Validation (Outer: Stratified by Temp Range or Repeated k-fold; Inner: GridSearch).
4. Training two Gradient Boosting Regressors:
   - Thermodynamic GBR (features: atomic fractions + mixing enthalpy + radius mismatch)
   - Composition-Only GBR (features: atomic fractions only)
"""

