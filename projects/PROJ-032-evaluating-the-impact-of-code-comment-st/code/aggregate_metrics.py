"""
T027: Aggregate metrics from individual files into a single CSV.

Combines readability, sentiment, density, churn, bug_fix_rate, and complexity
into data/processed/metrics.csv with precision >= 2 decimal places.

Dependencies:
- T021: Extracted comments (data/processed/comments.json)
- T021b: Density calculation
- T022: Readability calculation
- T023: Sentiment calculation
- T024: Churn calculation
- T006b: Complexity calculation
- T007d: Quality rate (bug_fix_rate)
"""
import os
import json
import logging
import csv
from pathlib import Path
from typing import Dict, Any, List, Optional

from utils import configure_logging, MemoryMonitor

# Configure logging
logger = configure_logging(log_path="logs/pipeline.log")

# Constants
PROJECT_ROOT = Path(__file__).parent.parent
DATA_DIR = PROJECT_ROOT / "data"
PROCESSED_DIR = DATA_DIR / "processed"
RAW_DIR = DATA_DIR / "raw"
OUTPUT_FILE = PROCESSED_DIR / "metrics.csv"

# Metric files generated by previous tasks
METRIC_FILES = {
    "readability": PROCESSED_DIR / "readability.json",
    "sentiment": PROCESSED_DIR / "sentiment.json",
    "density": PROCESSED_DIR / "density.json",
    "churn": PROCESSED_DIR / "churn.json",
    "complexity": PROCESSED_DIR / "complexity.json",
    "bug_fix_rate": PROCESSED_DIR / "quality_rate.json",
}

def load_metric_file(file_path: Path) -> Dict[str, float]:
    """Load a metric JSON file and return a dict of repo_id -> value."""
    if not file_path.exists():
        logger.warning(f"Metric file not found: {file_path}")
        return {}
    
    with open(file_path, 'r') as f:
        data = json.load(f)
    
    # Handle different JSON structures
    if isinstance(data, list):
        # Assume list of {"repo_id": ..., "value": ...} dicts
        return {item["repo_id"]: item["value"] for item in data}
    elif isinstance(data, dict):
        # Assume dict of {repo_id: value}
        return data
    else:
        logger.error(f"Unexpected JSON structure in {file_path}")
        return {}

def aggregate_metrics() -> None:
    """
    Aggregate all metrics into a single CSV file.
    
    Reads metric files generated by previous tasks and combines them
    into data/processed/metrics.csv with precision >= 2 decimal places.
    """
    logger.info("Starting metric aggregation...")
    
    # Memory monitoring
    memory_monitor = MemoryMonitor()
    try:
        memory_monitor.check_limit(limit_gb=7)
    except MemoryError as e:
        logger.error(f"Memory limit exceeded: {e}")
        raise
    
    # Load all metric files
    metrics_data = {}
    all_repo_ids = set()
    
    for metric_name, file_path in METRIC_FILES.items():
        logger.info(f"Loading {metric_name} from {file_path}")
        metric_values = load_metric_file(file_path)
        all_repo_ids.update(metric_values.keys())
        
        for repo_id, value in metric_values.items():
            if repo_id not in metrics_data:
                metrics_data[repo_id] = {}
            metrics_data[repo_id][metric_name] = value
    
    if not all_repo_ids:
        logger.warning("No repository data found. Skipping aggregation.")
        return
    
    logger.info(f"Found {len(all_repo_ids)} repositories to aggregate")
    
    # Ensure output directory exists
    OUTPUT_FILE.parent.mkdir(parents=True, exist_ok=True)
    
    # Write aggregated metrics to CSV
    fieldnames = ["repo_id", "readability", "sentiment", "density", 
                 "churn", "bug_fix_rate", "complexity"]
    
    with open(OUTPUT_FILE, 'w', newline='') as csvfile:
        writer = csv.DictWriter(csvfile, fieldnames=fieldnames)
        writer.writeheader()
        
        for repo_id in sorted(all_repo_ids):
            row = {"repo_id": repo_id}
            
            for metric_name in fieldnames[1:]:  # Skip repo_id
                if repo_id in metrics_data and metric_name in metrics_data[repo_id]:
                    value = metrics_data[repo_id][metric_name]
                    # Round to 2 decimal places as required
                    row[metric_name] = round(float(value), 2)
                else:
                    # Use None for missing values (will be written as empty string)
                    row[metric_name] = None
            
            writer.writerow(row)
    
    logger.info(f"Aggregated metrics written to {OUTPUT_FILE}")
    logger.info(f"Total repositories: {len(all_repo_ids)}")
    
    # Log summary statistics
    summary = {
        "total_repos": len(all_repo_ids),
        "metrics_aggregated": list(METRIC_FILES.keys()),
        "output_file": str(OUTPUT_FILE)
    }
    logger.info(f"Aggregation summary: {summary}")

def main():
    """Main entry point for metric aggregation."""
    try:
        aggregate_metrics()
        logger.info("Metric aggregation completed successfully")
    except Exception as e:
        logger.error(f"Metric aggregation failed: {e}", exc_info=True)
        raise

if __name__ == "__main__":
    main()
