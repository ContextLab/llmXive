"""
Main orchestration script for the fMRI Entropy Biomarker pipeline.

This script:
1. Loads the list of valid subjects from data/derived/valid_subjects.csv.
2. Reads the exclusion log (data/raw/exclusions.log) to skip excluded subjects.
3. Iterates over valid subjects, computing Sample Entropy for each parcel.
4. Aggregates results into data/processed/subject_entropy_features.csv.
"""

import os
import csv
import logging
from pathlib import Path
from typing import List, Dict, Any, Set

import numpy as np
import nibabel as nib

# Local imports matching the provided API surface
from config import (
    DATASET_ID,
    ATLAS_N,
    TARGET_LENGTH,
    R_FACTOR,
    M,
    FD_THRESHOLD,
)
from models import Subject, Parcel, EntropyFeature
from preprocessing import process_subject_truncation
from entropy_engine import compute_subject_entropy_features
from utils import setup_logger

# Project root paths
PROJECT_ROOT = Path(__file__).resolve().parent.parent
DATA_RAW_DIR = PROJECT_ROOT / "data" / "raw"
DATA_PROCESSED_DIR = PROJECT_ROOT / "data" / "processed"
DATA_DERIVED_DIR = PROJECT_ROOT / "data" / "derived"
OUTPUT_CSV = DATA_PROCESSED_DIR / "subject_entropy_features.csv"
VALID_SUBJECTS_CSV = DATA_DERIVED_DIR / "valid_subjects.csv"
EXCLUSIONS_LOG = DATA_RAW_DIR / "exclusions.log"

def load_valid_subjects(csv_path: Path) -> List[Dict[str, Any]]:
    """Load valid subjects from the CSV generated by data_loader.py."""
    if not csv_path.exists():
        raise FileNotFoundError(f"Valid subjects file not found: {csv_path}")
    
    subjects = []
    with open(csv_path, "r", newline="", encoding="utf-8") as f:
        reader = csv.DictReader(f)
        for row in reader:
            subjects.append(row)
    return subjects

def load_exclusions(log_path: Path) -> Set[str]:
    """Parse exclusions.log to get a set of excluded subject IDs."""
    excluded_ids = set()
    if not log_path.exists():
        logging.warning(f"Exclusions log not found: {log_path}. Assuming no exclusions.")
        return excluded_ids
    
    with open(log_path, "r", encoding="utf-8") as f:
        lines = f.readlines()
    
    # Skip header if present
    start_idx = 0
    if lines and "subject_id" in lines[0].lower():
        start_idx = 1
    
    for line in lines[start_idx:]:
        parts = line.strip().split(",")
        if len(parts) >= 1:
          excluded_ids.add(parts[0].strip())
    
    return excluded_ids

def main():
    """Orchestrate the full entropy computation pipeline."""
    # Setup logging
    logger = setup_logger("main", level=logging.INFO)
    logger.info("Starting fMRI Entropy Pipeline (T018a)")

    # Ensure output directories exist
    DATA_PROCESSED_DIR.mkdir(parents=True, exist_ok=True)
    DATA_DERIVED_DIR.mkdir(parents=True, exist_ok=True)
    DATA_RAW_DIR.mkdir(parents=True, exist_ok=True)

    # 1. Load valid subjects
    logger.info(f"Loading valid subjects from {VALID_SUBJECTS_CSV}")
    valid_subjects_data = load_valid_subjects(VALID_SUBJECTS_CSV)
    logger.info(f"Found {len(valid_subjects_data)} valid subjects in CSV.")

    # 2. Load exclusions
    excluded_ids = load_exclusions(EXCLUSIONS_LOG)
    logger.info(f"Loaded {len(excluded_ids)} excluded subjects from log.")

    # Filter out excluded subjects
    subjects_to_process = [
        s for s in valid_subjects_data 
        if s.get("subject_id", "").strip() not in excluded_ids
    ]
    
    if not subjects_to_process:
        logger.error("No subjects remaining after applying exclusions. Aborting.")
        return

    logger.info(f"Processing {len(subjects_to_process)} subjects.")

    # 3. Prepare output CSV structure
    # Columns: subject_id, parcel_0, parcel_1, ..., parcel_{N-1}
    parcel_columns = [f"parcel_{i}" for i in range(ATLAS_N)]
    output_header = ["subject_id"] + parcel_columns

    all_results = []

    # 4. Process each subject
    for idx, subject_data in enumerate(subjects_to_process):
        subject_id = subject_data.get("subject_id", "").strip()
        nifti_path_str = subject_data.get("nifti_path", "").strip()
        
        # Validate paths
        if not nifti_path_str or not subject_id:
            logger.warning(f"Skipping invalid entry: {subject_data}")
            continue

        nifti_path = Path(nifti_path_str)
        if not nifti_path.exists():
            logger.error(f"NIfTI file not found for {subject_id}: {nifti_path}")
            continue

        logger.info(f"[{idx+1}/{len(subjects_to_process)}] Processing {subject_id}")

        try:
            # A. Preprocess: Truncate to target length (T014)
            # Note: process_subject_truncation handles scrubbing logic internally or assumes pre-scrubbed input
            # based on the task flow. We call the function that ensures N=120.
            truncated_path = process_subject_truncation(
                input_path=nifti_path,
                subject_id=subject_id,
                target_length=TARGET_LENGTH,
                output_dir=DATA_PROCESSED_DIR
            )

            if not truncated_path or not truncated_path.exists():
                logger.error(f"Truncation failed for {subject_id}. Skipping.")
                continue

            # B. Compute Entropy (T015, T016)
            # This function reads the truncated NIfTI and computes SampEn per parcel
            entropy_values = compute_subject_entropy_features(
                nifti_path=truncated_path,
                m=M,
                r_factor=R_FACTOR,
                atlas_n=ATLAS_N,
                logger=logger
            )

            # C. Aggregate
            row = {"subject_id": subject_id}
            for i, val in enumerate(entropy_values):
                row[f"parcel_{i}"] = val
            all_results.append(row)

        except Exception as e:
            logger.error(f"Critical error processing {subject_id}: {e}", exc_info=True)
            continue

    # 5. Write final CSV
    logger.info(f"Writing {len(all_results)} rows to {OUTPUT_CSV}")
    with open(OUTPUT_CSV, "w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=output_header)
        writer.writeheader()
        writer.writerows(all_results)

    logger.info("Pipeline completed successfully.")
    logger.info(f"Output saved to: {OUTPUT_CSV}")

    # Validation check
    if len(all_results) == 0:
        logger.warning("Output file is empty. No data was generated.")
    else:
        logger.info(f"Generated {len(all_results)} subject rows.")

if __name__ == "__main__":
    main()