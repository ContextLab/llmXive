"""
Sensitivity Report Generator for T033.

Generates the sensitivity analysis report based on the results of T031 and T032.
Reads model artifacts and sensitivity analysis results, then writes a markdown
report to data/validation/sensitivity_report.md.
"""
import os
import json
import logging
from pathlib import Path
from typing import Dict, Any, List, Optional
import sys

# Add project root to path for imports
project_root = Path(__file__).resolve().parent.parent.parent
sys.path.insert(0, str(project_root))

from models.evaluator import load_model_artifacts, run_sensitivity_analysis, save_sensitivity_report
from config import get_data_dir, get_output_dir, get_seed
from utils.logging_config import get_logger

logger = get_logger(__name__)

def load_sensitivity_results() -> Optional[Dict[str, Any]]:
    """
    Load the sensitivity analysis results generated by T031/T032.
    Expects the results to be in data/processed/sensitivity_results.json
    """
    data_dir = get_data_dir()
    results_path = data_dir / "processed" / "sensitivity_results.json"
    
    if not results_path.exists():
        logger.error(f"Sensitivity results not found at {results_path}. "
                     "Please ensure T031 and T032 have been run.")
        return None
    
    with open(results_path, 'r') as f:
        return json.load(f)

def load_model_info() -> Dict[str, Any]:
    """
    Load basic model information from the model run artifact.
    """
    data_dir = get_data_dir()
    model_path = data_dir / "processed" / "model_run.json"
    
    if not model_path.exists():
        logger.warning(f"Model run artifact not found at {model_path}. "
                       "Generating report with placeholder model info.")
        return {
            "model_type": "RandomForest",
            "best_params": {},
            "r2_scores": {},
            "mae_scores": {}
        }
    
    with open(model_path, 'r') as f:
        return json.load(f)

def generate_report_content(
    sensitivity_results: Dict[str, Any], 
    model_info: Dict[str, Any]
) -> str:
    """
    Generate the markdown content for the sensitivity report.
    """
    report_lines = []
    
    # Header
    report_lines.append("# Sensitivity Analysis Report")
    report_lines.append("")
    report_lines.append("## Overview")
    report_lines.append("")
    report_lines.append("This report presents the results of the sensitivity analysis performed on the "
                       "battery electrolyte decomposition prediction model. The analysis evaluates the "
                       "stability of feature importance rankings and model performance metrics across "
                       "different decomposition energy stability cutoff thresholds.")
    report_lines.append("")
    
    # Configuration
    report_lines.append("## Configuration")
    report_lines.append("")
    report_lines.append("- **Thresholds Swept**: 0.45 eV, 0.50 eV, 0.55 eV")
    report_lines.append(f"- **Random Seed**: {get_seed()}")
    report_lines.append("")
    
    # Model Summary
    report_lines.append("## Model Summary")
    report_lines.append("")
    report_lines.append(f"- **Model Type**: {model_info.get('model_type', 'Unknown')}")
    if 'best_params' in model_info and model_info['best_params']:
        report_lines.append("- **Best Hyperparameters**:")
        for param, value in model_info['best_params'].items():
            report_lines.append(f"  - `{param}`: {value}")
    report_lines.append("")
    
    # Sensitivity Results
    report_lines.append("## Sensitivity Analysis Results")
    report_lines.append("")
    
    if sensitivity_results:
        # Performance Metrics Table
        report_lines.append("### Performance Metrics by Threshold")
        report_lines.append("")
        report_lines.append("| Threshold (eV) | R² Score | MAE (eV) | Top 3 Features |")
        report_lines.append("|----------------|----------|----------|----------------|")
        
        thresholds = sensitivity_results.get('thresholds', [])
        metrics = sensitivity_results.get('metrics', [])
        top_features = sensitivity_results.get('top_features', [])
        
        for i, threshold in enumerate(thresholds):
            r2 = metrics[i].get('r2', 'N/A') if i < len(metrics) else 'N/A'
            mae = metrics[i].get('mae', 'N/A') if i < len(metrics) else 'N/A'
            features = ', '.join(top_features[i][:3]) if i < len(top_features) and len(top_features[i]) >= 3 else 'N/A'
            report_lines.append(f"| {threshold} | {r2:.4f}" if isinstance(r2, float) else f"| {threshold} | {r2}")
            report_lines.append(f"| {mae:.4f}" if isinstance(mae, float) else f"| {mae}")
            report_lines.append(f"| {features} |")
            report_lines.append("")
        
        # Rank Stability Check
        report_lines.append("### Rank Stability Analysis")
        report_lines.append("")
        
        rank_stability = sensitivity_results.get('rank_stability', {})
        max_rank_change = rank_stability.get('max_rank_change', 'N/A')
        is_stable = rank_stability.get('is_stable', False)
        
        if isinstance(max_rank_change, int):
            report_lines.append(f"- **Maximum Rank Change**: {max_rank_change} position(s)")
            if is_stable:
                report_lines.append("- **Stability Status**: ✅ **STABLE** (Change ≤ 1 position)")
            else:
                report_lines.append("- **Stability Status**: ❌ **UNSTABLE** (Change > 1 position)")
        else:
            report_lines.append(f"- **Maximum Rank Change**: {max_rank_change}")
            report_lines.append("- **Stability Status**: Unable to determine")
        
        report_lines.append("")
        report_lines.append("The rank stability check verifies that the top 3 descriptors do not change "
                          "by more than 1 position across the swept thresholds, as required by T032.")
        report_lines.append("")
        
        # Detailed Feature Rankings
        report_lines.append("### Detailed Feature Rankings")
        report_lines.append("")
        
        for i, threshold in enumerate(thresholds):
            report_lines.append(f"**Threshold: {threshold} eV**")
            report_lines.append("")
            if i < len(top_features):
                for rank, feature in enumerate(top_features[i], 1):
                    report_lines.append(f"{rank}. {feature}")
            else:
                report_lines.append("No feature rankings available.")
            report_lines.append("")
    else:
        report_lines.append("No sensitivity analysis results available. Please ensure T031 and T032 "
                          "have been completed successfully.")
        report_lines.append("")
    
    # Deviation Notes
    report_lines.append("## Deviation Notes")
    report_lines.append("")
    report_lines.append("> **Warning**: FR-006 and SC-003 (External Validation) could not be fulfilled "
                      "due to unavailability of experimental onset potential datasets. Internal DFT "
                      "validation was used as a fallback.")
    report_lines.append("")
    report_lines.append("This sensitivity analysis is based on internal consistency checks against "
                      "held-out DFT data. The results reflect the model's behavior on thermodynamic "
                      "stability predictions rather than experimental kinetic onset potentials.")
    report_lines.append("")
    
    # Conclusion
    report_lines.append("## Conclusion")
    report_lines.append("")
    if sensitivity_results and sensitivity_results.get('rank_stability', {}).get('is_stable', False):
        report_lines.append("The model demonstrates **stable feature importance rankings** across the "
                          "swept decomposition energy stability thresholds. The top 3 descriptors remain "
                          "consistent with changes of ≤1 position, indicating robust feature selection "
                          "and model reliability within the tested parameter range.")
    else:
        report_lines.append("The model shows **variable feature importance rankings** across the swept "
                          "thresholds. Further investigation is recommended to understand the sources "
                          "of instability and potentially refine the feature set or model architecture.")
    report_lines.append("")
    report_lines.append("---")
    report_lines.append("*Report generated automatically by the llmXive pipeline.*")
    
    return "\n".join(report_lines)

def run_report_generation():
    """
    Main entry point for generating the sensitivity report.
    """
    logger.info("Starting sensitivity report generation (T033)...")
    
    # Load results
    sensitivity_results = load_sensitivity_results()
    model_info = load_model_info()
    
    # Generate content
    report_content = generate_report_content(sensitivity_results, model_info)
    
    # Write report
    output_dir = get_output_dir()
    report_path = output_dir / "validation" / "sensitivity_report.md"
    
    report_path.parent.mkdir(parents=True, exist_ok=True)
    
    with open(report_path, 'w') as f:
        f.write(report_content)
    
    logger.info(f"Sensitivity report generated successfully at: {report_path}")
    return report_path

if __name__ == "__main__":
    run_report_generation()
