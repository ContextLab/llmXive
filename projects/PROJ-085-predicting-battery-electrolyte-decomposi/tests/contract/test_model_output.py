"""
Contract test for model output schema (US2).

This test validates that the model output artifact generated by the training pipeline
strictly adheres to the schema defined in `contracts/model_output.schema.yaml`.

It verifies:
1. The existence of the output file at the expected path.
2. The presence of all required top-level keys.
3. The data types and structure of nested fields (metrics, hyperparameters, importance).
4. Consistency with the project's configuration (e.g., seed).
"""

import json
import os
import pytest
from pathlib import Path
from typing import Any, Dict

# Import the schema loader from the contract test utilities
from tests.contract.schema_loader import (
    load_schema,
    validate_against_schema,
    load_and_validate_model_output_schema
)

# Import config to get expected paths and seed
from config import get_output_dir, get_seed

# Define the expected output path relative to the project root
# Based on T026: Save model artifacts... to `data/processed/model_run.json`
# Note: The schema_loader expects the file to exist at the path provided or derived from config.
# We will assert the path matches the project convention.
MODEL_OUTPUT_FILENAME = "model_run.json"

@pytest.fixture
def model_output_path() -> Path:
    """
    Constructs the full path to the model output JSON file.
    Raises FileNotFoundError if the file does not exist (test fails fast).
    """
    output_dir = get_output_dir()
    # Ensure we are looking in the processed data subdirectory as per T026
    processed_dir = output_dir / "processed"
    path = processed_dir / MODEL_OUTPUT_FILENAME
    
    if not path.exists():
        # Fail fast with a clear message if the artifact is missing
        raise FileNotFoundError(
            f"Model output artifact not found at {path}. "
            "Ensure T022 (trainer) and T026 (save artifacts) have been executed successfully."
        )
    return path

@pytest.fixture
def model_output_data(model_output_path: Path) -> Dict[str, Any]:
    """
    Loads the JSON content of the model output file.
    """
    with open(model_output_path, 'r', encoding='utf-8') as f:
        return json.load(f)

def test_model_output_file_exists(model_output_path: Path):
    """
    Contract Test: Verify the model output file exists at the expected location.
    """
    assert model_output_path.exists(), f"Model output file missing: {model_output_path}"

def test_model_output_schema_structure(model_output_data: Dict[str, Any]):
    """
    Contract Test: Verify the top-level structure matches the expected schema.
    The schema (contracts/model_output.schema.yaml) defines these required keys.
    """
    required_keys = [
        "metadata",
        "hyperparameters",
        "metrics",
        "feature_importance",
        "bin_analysis"
    ]
    
    missing_keys = [key for key in required_keys if key not in model_output_data]
    assert not missing_keys, (
        f"Model output JSON is missing required top-level keys: {missing_keys}. "
        "This violates the model_output.schema.yaml contract."
    )

def test_model_output_schema_validation(model_output_data: Dict[str, Any]):
    """
    Contract Test: Validate the entire JSON structure against the JSON Schema.
    Uses the helper from tests.contract.schema_loader which loads the YAML schema
    and runs jsonschema validation.
    """
    # This function will raise an AssertionError if validation fails
    # It handles loading the schema and the actual validation logic.
    try:
        load_and_validate_model_output_schema(model_output_data)
    except Exception as e:
        pytest.fail(f"Model output JSON failed schema validation: {str(e)}")

def test_model_output_metrics_types(model_output_data: Dict[str, Any]):
    """
    Contract Test: Verify specific metric fields have correct types.
    """
    metrics = model_output_data.get("metrics", {})
    
    # Check for expected metric keys
    expected_metrics = ["r2_score", "mae", "rmse"]
    for metric in expected_metrics:
        assert metric in metrics, f"Metric '{metric}' missing from 'metrics' section."
        assert isinstance(metrics[metric], (int, float)), (
            f"Metric '{metric}' must be numeric, got {type(metrics[metric])}."
        )

def test_model_output_hyperparameters_types(model_output_data: Dict[str, Any]):
    """
    Contract Test: Verify hyperparameters are present and correctly typed.
    """
    hyperparams = model_output_data.get("hyperparameters", {})
    
    # Check for common Random Forest hyperparameters
    assert "n_estimators" in hyperparams, "Hyperparameter 'n_estimators' missing."
    assert isinstance(hyperparams["n_estimators"], int), "n_estimators must be an integer."
    
    assert "max_depth" in hyperparams, "Hyperparameter 'max_depth' missing."
    # max_depth can be int or None
    assert isinstance(hyperparams["max_depth"], (int, type(None))), "max_depth must be int or null."

def test_model_output_feature_importance_format(model_output_data: Dict[str, Any]):
    """
    Contract Test: Verify feature importance is a list of dictionaries with 'feature' and 'importance'.
    """
    importance = model_output_data.get("feature_importance", [])
    
    assert isinstance(importance, list), "feature_importance must be a list."
    assert len(importance) > 0, "feature_importance list cannot be empty."
    
    for item in importance:
        assert isinstance(item, dict), "Each item in feature_importance must be a dict."
        assert "feature" in item, "Missing 'feature' key in importance item."
        assert "importance" in item, "Missing 'importance' key in importance item."
        assert isinstance(item["feature"], str), "'feature' must be a string."
        assert isinstance(item["importance"], (int, float)), "'importance' must be numeric."

