@pytest.fixture
def sample_baseline_csv(tmp_path):
    """Creates a temporary CSV file simulating the CodeParrot sample."""
    # Create a mock CSV with some code that might trigger keywords and some that won't
    data = {
        'body': [
            "print('hello world')",  # Non-disclosing
            "import copilot",        # Disclosing (keyword)
            "def foo(): pass",       # Non-disclosing
            "# Generated by LLM",    # Disclosing (keyword)
            "x = 1 + 2",             # Non-disclosing
            "llm = 'test'",          # Disclosing (keyword)
            "return None",           # Non-disclosing
        ],
        'repo': ['codeparrot/baseline'] * 7,
        'pr_number': list(range(7))
    }
    df = pd.DataFrame(data)
    csv_path = tmp_path / "sample_baseline.csv"
    df.to_csv(csv_path, index=False)
    return csv_path


def test_estimate_fp_rate_calculation(sample_baseline_csv):
    """
    Test that the FP rate is calculated correctly.
    In the sample above:
    - 'import copilot' -> Disclosing
    - '# Generated by LLM' -> Disclosing
    - 'llm = 'test'' -> Disclosing
    Total Disclosing = 3 out of 7.
    Expected FP Rate = 3/7.
    """
    # We need to patch the check_disclosure_keywords to ensure deterministic behavior
    # or rely on the actual implementation if it's stable.
    # For this test, we assume the keywords are: "copilot", "llm", "generated".
    
    # Let's verify the logic manually based on the sample data
    # The sample has 7 rows.
    # 1. "print('hello world')" -> No keywords
    # 2. "import copilot" -> Has "copilot" -> Disclosing
    # 3. "def foo(): pass" -> No keywords
    # 4. "# Generated by LLM" -> Has "generated" and "llm" -> Disclosing
    # 5. "x = 1 + 2" -> No keywords
    # 6. "llm = 'test'" -> Has "llm" -> Disclosing
    # 7. "return None" -> No keywords
    
    # Expected: 3/7
    
    results = estimate_false_positive_rate(sample_baseline_csv)
    
    assert results['total_samples'] == 7
    assert results['disclosing_count'] == 3
    assert abs(results['fp_rate'] - (3/7)) < 1e-6


def test_empty_csv(sample_baseline_csv):
    """Test behavior with an empty dataset (edge case)."""
    # Create empty CSV
    empty_df = pd.DataFrame(columns=['body', 'repo', 'pr_number'])
    empty_path = sample_baseline_csv.parent / "empty.csv"
    empty_df.to_csv(empty_path, index=False)
    
    results = estimate_false_positive_rate(empty_path)
    
    assert results['total_samples'] == 0
    assert results['disclosing_count'] == 0
    assert results['fp_rate'] == 0.0


