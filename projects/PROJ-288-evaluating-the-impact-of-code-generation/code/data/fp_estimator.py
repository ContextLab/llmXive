# We will download the first 1000 rows of the parquet file as a CSV to `data/baseline_corpus/codeparrot_sample.csv`.

DATA_DIR = Path("data/baseline_corpus")
RAW_FILE = DATA_DIR / "codeparrot_sample.parquet"
CSV_FILE = DATA_DIR / "codeparrot_sample.csv"
OUTPUT_FILE = DATA_DIR / "estimated_fp_rate.json"

# We need pandas to read parquet, which is in requirements.txt
import pandas as pd


def download_baseline_corpus() -> Path:
    """
    Downloads a sample of the CodeParrot corpus.
    Since the full dataset is large, we fetch the parquet file and slice it.
    If the download fails, we raise an exception.
    """
    DATA_DIR.mkdir(parents=True, exist_ok=True)

    # URL for the main train split of codeparrot-small
    # Note: This URL might change or require authentication for large files, but usually public datasets are open.
    url = "https://huggingface.co/datasets/codeparrot/codeparrot-small/resolve/main/data/train-00000-of-00001.parquet"
    
    logger.info(f"Attempting to download baseline corpus from: {url}")
    
    try:
        response = requests.get(url, stream=True)
        response.raise_for_status()
        
        # Save to raw parquet file first
        with open(RAW_FILE, 'wb') as f:
            for chunk in response.iter_content(chunk_size=8192):
                f.write(chunk)
        
        logger.info(f"Downloaded raw parquet to {RAW_FILE}")
        
        # Load with pandas and take a sample (e.g., 1000 rows) to keep processing fast
        # This satisfies "real data" without processing the whole corpus.
        df = pd.read_parquet(RAW_FILE)
        
        # The codeparrot dataset usually has a 'content' or 'code' column.
        # Let's inspect columns to be safe, or assume 'code' based on common knowledge of this dataset.
        # Common columns: 'content', 'lang', 'size', 'license', 'path'.
        # We need the actual code text.
        
        code_column = None
        if 'content' in df.columns:
            code_column = 'content'
        elif 'code' in df.columns:
            code_column = 'code'
        else:
            # Fallback: try to find a column with 'code' or 'content' in name
            candidates = [c for c in df.columns if 'content' in c.lower() or 'code' in c.lower()]
            if candidates:
                code_column = candidates[0]
            else:
                raise ValueError(f"Could not find a code content column in the dataset. Columns: {df.columns}")

        logger.info(f"Using column '{code_column}' as code content.")
        
        sample_size = 1000
        if len(df) > sample_size:
            sample_df = df.head(sample_size)
        else:
            sample_df = df
        
        # Ensure we only have the relevant column for classification
        # We will create a synthetic PR structure for classification
        sample_df = sample_df[[code_column]].rename(columns={code_column: 'body'})
        
        # Add a dummy repo and PR number for the classifier interface
        sample_df['repo'] = 'codeparrot/baseline'
        sample_df['pr_number'] = range(len(sample_df))
        
        # Save as CSV
        sample_df.to_csv(CSV_FILE, index=False)
        logger.info(f"Saved sample CSV to {CSV_FILE}")
        
        return CSV_FILE

    except Exception as e:
        logger.error(f"Failed to download or process baseline corpus: {e}")
        raise


def estimate_false_positive_rate(csv_path: Path) -> Dict[str, Any]:
    """
    Reads the baseline corpus CSV and calculates the false positive rate.
    Since CodeParrot is NOT LLM-generated, any 'Disclosing' label is a False Positive.
    """
    if not csv_path.exists():
        raise FileNotFoundError(f"Baseline corpus file not found: {csv_path}")

    logger.info(f"Loading baseline corpus from {csv_path}")
    df = pd.read_csv(csv_path)

    if 'body' not in df.columns:
        raise ValueError("Baseline CSV must contain a 'body' column with code text.")

    # Apply the same classification logic used in T015
    # We need to classify each row as 'Disclosing' or 'Non-Disclosing'
    
    disclosing_count = 0
    total_count = len(df)

    logger.info(f"Classifying {total_count} samples from baseline corpus...")

    for index, row in df.iterrows():
        # The classify function expects a row with 'body'
        # We check if it triggers the disclosure heuristic
        is_disclosing = check_disclosure_keywords(row['body'])
        
        if is_disclosing:
            disclosing_count += 1
        
        if (index + 1) % 100 == 0:
            logger.debug(f"Processed {index + 1}/{total_count}")

    fp_rate = disclosing_count / total_count if total_count > 0 else 0.0

    logger.info(f"False Positive Rate: {fp_rate:.4f} ({disclosing_count}/{total_count})")

    return {
        "fp_rate": fp_rate,
        "disclosing_count": disclosing_count,
        "total_samples": total_count,
        "source": str(csv_path),
        "description": "Fraction of non-LLM code (CodeParrot) incorrectly classified as 'Disclosing'."
    }


def main():
    """
    Main entry point for T018.
    1. Download baseline corpus.
    2. Estimate FP rate.
    3. Save results to JSON.
    """
    try:
        # 1. Download
        csv_path = download_baseline_corpus()
        
        # 2. Estimate
        results = estimate_false_positive_rate(csv_path)
        
        # 3. Save
        with open(OUTPUT_FILE, 'w') as f:
            json.dump(results, f, indent=2)
        
        logger.info(f"Successfully saved false positive rate to {OUTPUT_FILE}")
        print(f"Task T018 Complete. FP Rate: {results['fp_rate']}")
        
    except Exception as e:
        logger.error(f"Task T018 Failed: {e}")
        raise


if __name__ == "__main__":
    main()
