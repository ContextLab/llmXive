"""
Contract test for regression output schema.

This test verifies that the output file generated by analyze_results.py
(specifically the regression summary JSON) adheres to the expected schema.
It ensures that all required fields are present and have the correct data types,
as defined in the project specifications for User Story 3.

The expected schema for `output/regression_summary.json` is:
{
  "interaction_term": {
    "estimate": float,
    "std_err": float,
    "z_value": float,
    "p_value": float
  },
  "model_summary": {
    "log_likelihood": float,
    "aic": float,
    "bic": float
  },
  "parameters": {
    "degrees_of_freedom": int,
    "n_samples": int
  },
  "hypothesis_test": {
    "significant_at_0.05": bool,
    "p_value_interaction": float
  }
}
"""

import json
import os
import pytest
from pathlib import Path

# Define the expected path relative to project root
# Assuming the test runs from the project root or the script adjusts paths
PROJECT_ROOT = Path(__file__).parent.parent.parent
REGRESSION_SUMMARY_PATH = PROJECT_ROOT / "output" / "regression_summary.json"

REQUIRED_TOP_LEVEL_KEYS = {
    "interaction_term",
    "model_summary",
    "parameters",
    "hypothesis_test"
}

REQUIRED_INTERACTION_KEYS = {
    "estimate",
    "std_err",
    "z_value",
    "p_value"
}

REQUIRED_MODEL_SUMMARY_KEYS = {
    "log_likelihood",
    "aic",
    "bic"
}

REQUIRED_PARAMETERS_KEYS = {
    "degrees_of_freedom",
    "n_samples"
}

REQUIRED_HYPOTHESIS_KEYS = {
    "significant_at_0.05",
    "p_value_interaction"
}

TYPE_MAP = {
    "estimate": (int, float),
    "std_err": (int, float),
    "z_value": (int, float),
    "p_value": (int, float),
    "log_likelihood": (int, float),
    "aic": (int, float),
    "bic": (int, float),
    "degrees_of_freedom": int,
    "n_samples": int,
    "significant_at_0.05": bool,
    "p_value_interaction": (int, float)
}


def load_regression_summary():
    """Load the regression summary JSON file."""
    if not REGRESSION_SUMMARY_PATH.exists():
        pytest.fail(f"Regression summary file not found at {REGRESSION_SUMMARY_PATH}. "
                    "Ensure analyze_results.py has been run to generate output/regression_summary.json.")
    
    with open(REGRESSION_SUMMARY_PATH, 'r', encoding='utf-8') as f:
        return json.load(f)


class TestRegressionOutputSchema:
    """Contract tests for the regression output schema."""

    def test_file_exists(self):
        """Verify that the regression summary file exists."""
        assert REGRESSION_SUMMARY_PATH.exists(), "Regression summary file does not exist."

    def test_top_level_keys_present(self):
        """Verify all required top-level keys are present."""
        data = load_regression_summary()
        missing_keys = REQUIRED_TOP_LEVEL_KEYS - set(data.keys())
        assert not missing_keys, f"Missing top-level keys: {missing_keys}"

    def test_interaction_term_structure(self):
        """Verify the interaction_term dictionary structure."""
        data = load_regression_summary()
        interaction = data.get("interaction_term", {})
        
        missing_keys = REQUIRED_INTERACTION_KEYS - set(interaction.keys())
        assert not missing_keys, f"Missing keys in interaction_term: {missing_keys}"

    def test_model_summary_structure(self):
        """Verify the model_summary dictionary structure."""
        data = load_regression_summary()
        model_summary = data.get("model_summary", {})
        
        missing_keys = REQUIRED_MODEL_SUMMARY_KEYS - set(model_summary.keys())
        assert not missing_keys, f"Missing keys in model_summary: {missing_keys}"

    def test_parameters_structure(self):
        """Verify the parameters dictionary structure."""
        data = load_regression_summary()
        parameters = data.get("parameters", {})
        
        missing_keys = REQUIRED_PARAMETERS_KEYS - set(parameters.keys())
        assert not missing_keys, f"Missing keys in parameters: {missing_keys}"

    def test_hypothesis_test_structure(self):
        """Verify the hypothesis_test dictionary structure."""
        data = load_regression_summary()
        hypothesis = data.get("hypothesis_test", {})
        
        missing_keys = REQUIRED_HYPOTHESIS_KEYS - set(hypothesis.keys())
        assert not missing_keys, f"Missing keys in hypothesis_test: {missing_keys}"

    def test_data_types(self):
        """Verify that all values have the expected data types."""
        data = load_regression_summary()
        
        # Check interaction_term
        for key in REQUIRED_INTERACTION_KEYS:
            val = data["interaction_term"][key]
            assert isinstance(val, TYPE_MAP[key]), \
                f"interaction_term.{key} has type {type(val)}, expected {TYPE_MAP[key]}"

        # Check model_summary
        for key in REQUIRED_MODEL_SUMMARY_KEYS:
            val = data["model_summary"][key]
            assert isinstance(val, TYPE_MAP[key]), \
                f"model_summary.{key} has type {type(val)}, expected {TYPE_MAP[key]}"

        # Check parameters
        for key in REQUIRED_PARAMETERS_KEYS:
            val = data["parameters"][key]
            assert isinstance(val, TYPE_MAP[key]), \
                f"parameters.{key} has type {type(val)}, expected {TYPE_MAP[key]}"

        # Check hypothesis_test
        for key in REQUIRED_HYPOTHESIS_KEYS:
            val = data["hypothesis_test"][key]
            assert isinstance(val, TYPE_MAP[key]), \
                f"hypothesis_test.{key} has type {type(val)}, expected {TYPE_MAP[key]}"

    def test_p_value_consistency(self):
        """Verify that p_value in interaction_term matches p_value_interaction in hypothesis_test."""
        data = load_regression_summary()
        
        p_val_interaction = data["interaction_term"]["p_value"]
        p_val_hypothesis = data["hypothesis_test"]["p_value_interaction"]
        
        assert p_val_interaction == p_val_hypothesis, \
            f"p_value mismatch: interaction_term has {p_val_interaction}, hypothesis_test has {p_val_hypothesis}"

    def test_significance_logic(self):
        """Verify that significant_at_0.05 is correctly derived from p_value."""
        data = load_regression_summary()
        
        p_val = data["interaction_term"]["p_value"]
        is_significant = data["hypothesis_test"]["significant_at_0.05"]
        
        expected_significant = p_val < 0.05
        assert is_significant == expected_significant, \
            f"Significance logic error: p_value={p_val}, significant_at_0.05={is_significant}, expected={expected_significant}"

    def test_positive_samples(self):
        """Verify that n_samples is a positive integer."""
        data = load_regression_summary()
        n_samples = data["parameters"]["n_samples"]
        assert n_samples > 0, f"n_samples must be positive, got {n_samples}"

    def test_non_negative_df(self):
        """Verify that degrees_of_freedom is non-negative."""
        data = load_regression_summary()
        df = data["parameters"]["degrees_of_freedom"]
        assert df >= 0, f"degrees_of_freedom must be non-negative, got {df}"