"""
Calibration module for instrument response and wavelength stability.
Implements FR-004: Apply instrument calibration factors and log detector response.
"""
import os
import json
import logging
from datetime import datetime, timezone
from pathlib import Path
from typing import Dict, Any, Optional, List, Tuple
import pandas as pd
import numpy as np

from config import get_processed_data_path, get_raw_data_path, get_chemicals_path, ensure_directories
from utils.logging import log_compliance_check, setup_logging

# Setup module logger
logger = logging.getLogger(__name__)

CALIBRATION_LOG_FILENAME = "calibration_log.json"
CALIBRATED_TRACES_FILENAME = "calibrated_traces.csv"

def load_calibration_factors() -> Dict[str, float]:
    """
    Load calibration factors from a configuration file or return defaults.
    In a real deployment, these would be read from an instrument registry or
    a specific calibration file generated by the hardware team.
    """
    # Default calibration factors (placeholder for real instrument data)
    # Wavelength shift correction (nm)
    # Detector sensitivity scaling factor
    # Dark current offset (arbitrary units)
    default_factors = {
        "wavelength_shift_nm": 0.0,
        "detector_sensitivity": 1.0,
        "dark_current_offset": 0.0,
        "integration_time_ms": 100.0
    }

    # Attempt to load from a potential calibration file if it exists
    cal_file = get_chemicals_path() / "instrument_calibration.json"
    if cal_file.exists():
        try:
            with open(cal_file, "r") as f:
                data = json.load(f)
                # Merge with defaults to ensure all keys exist
                default_factors.update(data)
                logger.info(f"Loaded calibration factors from {cal_file}")
        except (json.JSONDecodeError, IOError) as e:
            logger.warning(f"Could not load calibration file {cal_file}: {e}. Using defaults.")
    else:
        logger.info(f"No calibration file found at {cal_file}. Using default factors.")

    return default_factors

def apply_calibration(
    df: pd.DataFrame,
    factors: Dict[str, float]
) -> pd.DataFrame:
    """
    Apply calibration factors to the raw transient absorption data.
    
    Args:
        df: DataFrame containing raw data with columns ['time', 'wavelength', 'absorbance', 'solvent']
        factors: Dictionary of calibration factors.
    
    Returns:
        Calibrated DataFrame.
    """
    if df.empty:
        logger.warning("Empty DataFrame provided to apply_calibration.")
        return df

    calibrated_df = df.copy()

    # 1. Correct Wavelength Stability
    # Shift the wavelength column if a shift factor is defined
    if factors.get("wavelength_shift_nm", 0.0) != 0.0:
        shift = factors["wavelength_shift_nm"]
        calibrated_df["wavelength"] = calibrated_df["wavelength"] + shift
        logger.debug(f"Applied wavelength shift: {shift} nm")

    # 2. Correct Detector Response
    # Scale absorbance by detector sensitivity
    sensitivity = factors.get("detector_sensitivity", 1.0)
    if sensitivity != 1.0:
        calibrated_df["absorbance"] = calibrated_df["absorbance"] * sensitivity
        logger.debug(f"Applied detector sensitivity scaling: {sensitivity}")

    # 3. Correct Dark Current
    # Subtract dark current offset
    dark_offset = factors.get("dark_current_offset", 0.0)
    if dark_offset != 0.0:
        calibrated_df["absorbance"] = calibrated_df["absorbance"] - dark_offset
        logger.debug(f"Applied dark current offset correction: {dark_offset}")

    # Ensure no negative absorbance values due to noise correction (clamp to 0)
    calibrated_df["absorbance"] = calibrated_df["absorbance"].clip(lower=0.0)

    return calibrated_df

def log_calibration_run(
    factors: Dict[str, float],
    input_file: str,
    output_file: str,
    status: str = "success"
) -> Dict[str, Any]:
    """
    Log the calibration run details to a JSON file.
    """
    log_entry = {
        "timestamp": datetime.now(timezone.utc).isoformat(),
        "input_file": input_file,
        "output_file": output_file,
        "factors_applied": factors,
        "status": status,
        "detector_model": "Generic Transient Absorption Spectrometer", # Fallback per T035 logic
        "wavelength_stability_nm": factors.get("wavelength_shift_nm", 0.0),
        "detector_response_factor": factors.get("detector_sensitivity", 1.0)
    }

    # Load existing log if present
    log_path = get_processed_data_path() / CALIBRATION_LOG_FILENAME
    log_data = []
    if log_path.exists():
        try:
            with open(log_path, "r") as f:
                log_data = json.load(f)
        except json.JSONDecodeError:
            log_data = []

    log_data.append(log_entry)

    ensure_directories()
    with open(log_path, "w") as f:
        json.dump(log_data, f, indent=2)

    logger.info(f"Calibration log updated at {log_path}")
    return log_entry

def run_calibration_pipeline(raw_data_path: Optional[Path] = None) -> str:
    """
    Main pipeline function to calibrate raw transient absorption data.
    
    Args:
        raw_data_path: Optional path to the raw data file. If None, attempts to find
                       the latest synthetic or real trace in data/raw/.
    
    Returns:
        Path to the generated calibrated data file.
    """
    ensure_directories()
    processed_dir = get_processed_data_path()
    
    # Determine input file
    if raw_data_path is None:
        raw_dir = get_raw_data_path()
        # Look for synthetic traces first (as per task T015 logic), then real
        candidates = list(raw_dir.glob("*.csv"))
        if not candidates:
            raise FileNotFoundError("No raw data files found in data/raw/. "
                                    "Please run data generation or ingestion first.")
        # Sort by modification time, newest first
        candidates.sort(key=lambda x: x.stat().st_mtime, reverse=True)
        input_file_path = candidates[0]
        logger.info(f"Using latest raw data file: {input_file_path}")
    else:
        input_file_path = raw_data_path

    if not input_file_path.exists():
        raise FileNotFoundError(f"Raw data file not found: {input_file_path}")

    # Load raw data
    try:
        raw_df = pd.read_csv(input_file_path)
        required_cols = {"time", "wavelength", "absorbance"}
        if not required_cols.issubset(raw_df.columns):
            raise ValueError(f"Raw data missing required columns. Expected {required_cols}, got {raw_df.columns.tolist()}")
    except Exception as e:
        logger.error(f"Failed to load raw data: {e}")
        raise

    # Load factors
    factors = load_calibration_factors()

    # Apply calibration
    calibrated_df = apply_calibration(raw_df, factors)

    # Define output path
    output_filename = CALIBRATED_TRACES_FILENAME
    output_path = processed_dir / output_filename

    # Save calibrated data
    calibrated_df.to_csv(output_path, index=False)
    logger.info(f"Calibrated data saved to {output_path}")

    # Log the run
    log_entry = log_calibration_run(
        factors=factors,
        input_file=str(input_file_path),
        output_file=str(output_path),
        status="success"
    )

    # Log compliance for FR-004
    # Check if calibration factors are within expected ranges (example logic)
    # Assuming sensitivity should be between 0.5 and 2.0 for a valid instrument
    if 0.5 <= factors.get("detector_sensitivity", 1.0) <= 2.0:
        log_compliance_check("FR-004-Detector-Response", "PASS", f"Sensitivity: {factors['detector_sensitivity']}")
    else:
        log_compliance_check("FR-004-Detector-Response", "WARN", f"Sensitivity out of expected range: {factors['detector_sensitivity']}")

    return str(output_path)

def main():
    """CLI entry point for calibration."""
    import argparse
    parser = argparse.ArgumentParser(description="Apply instrument calibration to raw transient absorption data.")
    parser.add_argument(
        "--input",
        type=str,
        default=None,
        help="Path to raw data CSV. If omitted, uses the latest file in data/raw/."
    )
    args = parser.parse_args()

    setup_logging()
    try:
        input_path = Path(args.input) if args.input else None
        output_path = run_calibration_pipeline(input_path)
        print(f"Calibration complete. Output: {output_path}")
    except Exception as e:
        logger.exception("Calibration pipeline failed.")
        print(f"Error: {e}")
        exit(1)

if __name__ == "__main__":
    main()
