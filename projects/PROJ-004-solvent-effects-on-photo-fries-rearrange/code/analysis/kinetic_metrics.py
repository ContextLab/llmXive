"""
Kinetic Metrics Aggregation and Export (Task T026).

This module aggregates the results from `run_global_kinetic_analysis`
to create the final `data/processed/kinetic_metrics.csv`.

It calculates:
- Mean lifetime per solvent
- Standard deviation across replicates
- 95% Confidence Intervals (based on t-distribution for small N)
- Outlier flags (based on previously computed statistics)

It relies on the output of T022 (mean/std) and T023 (outliers)
which are assumed to be present in the processing pipeline or
derived from the raw trace analysis if not explicitly stored yet.
"""
