logger = logging.getLogger(__name__)

# Constants
KINETIC_METRICS_FILE = "kinetic_metrics.csv"
CALIBRATED_TRACES_FILE = "calibrated_traces.csv"
OUTLIER_REPORT_FILE = "outlier_report.json"

def load_kinetic_results() -> pd.DataFrame:
    """
    Loads the kinetic analysis results.
    Expects `data/processed/calibrated_traces.csv` to have been processed
    by `run_global_kinetic_analysis` into a summary format, or attempts
    to re-run the analysis if the summary is missing.
    """
    processed_path = get_processed_data_path()
    # Check for a pre-computed summary if it exists, otherwise trigger analysis
    # For T026, we assume the pipeline has run up to T022/T023.
    # We will re-run the global analysis to ensure we have the fresh data
    # required for the metrics report, as per the "real data" constraint.
    
    logger.info(f"Loading/Generating kinetic results from {processed_path}")
    
    # The global analysis function returns a DataFrame or writes to disk.
    # We call it to ensure we have the data.
    # Note: run_global_kinetic_analysis is expected to handle the file loading.
    results_df = run_global_kinetic_analysis()
    
    if results_df is None or results_df.empty:
        raise RuntimeError("Global kinetic analysis produced no results. "
                           "Check if input data exists in data/processed/calibrated_traces.csv")
    
    return results_df

def load_outlier_flags() -> Dict[str, List[int]]:
    """
    Loads outlier flags generated by T023.
    Expected format: {"solvent_name": [index1, index2, ...]}
    """
    processed_path = get_processed_data_path()
    outlier_file = processed_path / OUTLIER_REPORT_FILE
    
    if not outlier_file.exists():
        logger.warning(f"Outlier report {OUTLIER_REPORT_FILE} not found. "
                       "Proceeding without outlier flags.")
        return {}
    
    try:
        import json
        with open(outlier_file, 'r') as f:
            data = json.load(f)
            return data.get("outliers", {})
    except Exception as e:
        logger.error(f"Failed to parse outlier report: {e}")
        return {}

def aggregate_metrics(df: pd.DataFrame, outlier_flags: Dict[str, List[int]]) -> pd.DataFrame:
    """
    Aggregates replicate data into mean, std, and CI per solvent.
    Also marks outliers.
    """
    if 'solvent' not in df.columns or 'lifetime' not in df.columns:
        raise ValueError("Input DataFrame must contain 'solvent' and 'lifetime' columns.")

    # Ensure outliers are marked
    df['is_outlier'] = False
    for solvent, indices in outlier_flags.items():
        # Filter rows for this solvent and mark indices
        solvent_mask = df['solvent'] == solvent
        solvent_indices = df[solvent_mask].index
        for idx in indices:
            if idx in solvent_indices:
                df.loc[idx, 'is_outlier'] = True

    # Group by solvent
    groups = df.groupby('solvent')
    
    metrics_list = []
    
    for solvent, group in groups:
        lifetimes = group['lifetime'].values
        replicates = len(lifetimes)
        
        if replicates == 0:
            continue
        
        mean_life = float(np.mean(lifetimes))
        std_life = float(np.std(lifetimes, ddof=1)) if replicates > 1 else 0.0
        
        # Calculate 95% CI using t-distribution for small N (T024 context)
        # CI = mean ± t * (std / sqrt(n))
        # For n=3, t_0.975,2 = 4.303
        if replicates > 1:
            from scipy import stats
            t_val = stats.t.ppf(0.975, replicates - 1)
            sem = std_life / np.sqrt(replicates)
            ci_margin = float(t_val * sem)
        else:
            ci_margin = 0.0 # Cannot calculate CI for single replicate
        
        # Count outliers for this solvent
        solvent_outliers = group['is_outlier'].sum()
        
        metrics_list.append({
            'solvent': solvent,
            'mean_lifetime_ns': mean_life,
            'std_lifetime_ns': std_life,
            'n_replicates': replicates,
            'ci_95_margin_ns': ci_margin,
            'ci_95_lower_ns': mean_life - ci_margin,
            'ci_95_upper_ns': mean_life + ci_margin,
            'outlier_count': int(solvent_outliers),
            'timestamp': datetime.now(timezone.utc).isoformat()
        })
    
    return pd.DataFrame(metrics_list)

def write_metrics_csv(metrics_df: pd.DataFrame, output_path: Path) -> None:
    """
    Writes the aggregated metrics to the CSV file.
    """
    ensure_directories()
    metrics_df.to_csv(output_path, index=False)
    logger.info(f"Wrote kinetic metrics to {output_path}")

def main(args: Optional[argparse.Namespace] = None) -> int:
    """
    Main entry point for T026.
    """
    setup_logging()
    logger.info("Starting Kinetic Metrics Aggregation (T026)")
    
    try:
        # 1. Load/Generate Kinetic Results (from T022/T023 logic)
        kinetic_df = load_kinetic_results()
        
        # 2. Load Outlier Flags (from T023)
        outlier_flags = load_outlier_flags()
        
        # 3. Aggregate Metrics
        metrics_df = aggregate_metrics(kinetic_df, outlier_flags)
        
        # 4. Write Output
        output_path = get_processed_data_path() / KINETIC_METRICS_FILE
        write_metrics_csv(metrics_df, output_path)
        
        logger.info("Task T026 completed successfully.")
        return 0
        
    except FileNotFoundError as e:
        logger.error(f"Required data file missing: {e}")
        return 1
    except Exception as e:
        logger.error(f"Error during metric aggregation: {e}", exc_info=True)
        return 1

if __name__ == "__main__":
    parser = argparse.ArgumentParser(description="Aggregate kinetic metrics for T026")
    args = parser.parse_args()
    sys.exit(main(args))
