"""
T035: Write final statistical_comparison.csv to data/processed/

This script consumes the statistical analysis results generated by run_analysis.py
and writes the definitive Single Source of Truth CSV file.

Output: data/processed/statistical_comparison.csv
Columns: thread_count, config, t_stat, p_value, cohens_d, fdr_adjusted_p
"""
import os
import sys
import pandas as pd
from pathlib import Path
from datetime import datetime

# Add project root to path if running as script
project_root = Path(__file__).resolve().parent.parent
if str(project_root) not in sys.path:
    sys.path.insert(0, str(project_root))

from analysis.run_analysis import (
    load_benchmark_data,
    calculate_throughput,
    aggregate_results,
    perform_statistical_tests,
    benjamini_hochberg,
    calculate_cohens_d
)
from contracts.benchmark_contracts import StatisticalComparison

def write_final_results():
    """
    Orchestrates the full analysis pipeline and writes the final CSV.
    """
    # Define paths relative to project root
    raw_data_pattern = str(project_root / "data" / "raw" / "*.csv")
    output_dir = project_root / "data" / "processed"
    output_file = output_dir / "statistical_comparison.csv"

    # Ensure output directory exists
    output_dir.mkdir(parents=True, exist_ok=True)

    print(f"[T035] Loading benchmark data from: {raw_data_pattern}")
    df = load_benchmark_data(raw_data_pattern)
    
    if df.empty:
        raise FileNotFoundError(f"[T035] No benchmark data found matching pattern: {raw_data_pattern}")

    print(f"[T035] Calculating throughput...")
    df = calculate_throughput(df)

    print(f"[T035] Aggregating results...")
    agg_df = aggregate_results(df)

    print(f"[T035] Performing statistical tests (t-test, Cohen's d)...")
    # perform_statistical_tests returns a DataFrame with thread_count, config, t_stat, p_value, cohens_d
    stats_df = perform_statistical_tests(agg_df)

    if stats_df.empty:
        raise ValueError("[T035] Statistical tests produced no results. Check input data structure.")

    print(f"[T035] Applying Benjamini-Hochberg FDR correction...")
    # benjamini_hochberg expects a DataFrame and returns one with 'fdr_adjusted_p'
    final_df = benjamini_hochberg(stats_df)

    # Ensure column order matches spec exactly
    required_cols = ["thread_count", "config", "t_stat", "p_value", "cohens_d", "fdr_adjusted_p"]
    
    # Validate columns exist
    missing_cols = [c for c in required_cols if c not in final_df.columns]
    if missing_cols:
        raise ValueError(f"[T035] Missing expected columns in final result: {missing_cols}")
    
    final_df = final_df[required_cols]

    # Sort for readability (thread_count asc, config asc)
    final_df = final_df.sort_values(by=["thread_count", "config"])

    print(f"[T035] Writing final results to: {output_file}")
    final_df.to_csv(output_file, index=False)

    # Verify the file was written
    if not output_file.exists():
        raise RuntimeError(f"[T035] Failed to write output file: {output_file}")
    
    file_size = output_file.stat().st_size
    print(f"[T035] SUCCESS: Wrote {len(final_df)} rows ({file_size} bytes) to {output_file}")
    
    return final_df

def main():
    try:
        write_final_results()
    except Exception as e:
        print(f"[T035] ERROR: {e}", file=sys.stderr)
        sys.exit(1)

if __name__ == "__main__":
    main()
