# Constants for paths relative to project root
RAW_DATA_DIR = "data/raw"
PROCESSED_DATA_DIR = "data/processed"
INPUT_FILE_NAME = "posts_tags.jsonl"
OUTPUT_FILE_NAME = "tag_monthly_frequencies.csv"

# Configuration
MIN_MONTHS_REQUIRED = 12

def load_raw_data(input_path: Path) -> pd.DataFrame:
    """
    Loads the raw JSONL data generated by T012 (download.py).
    Expects columns: ['tag', 'post_id', 'creation_date'] or similar structure.
    Based on T012 description: "extracting tag names and post creation dates".
    """
    if not input_path.exists():
        raise FileNotFoundError(f"Raw input file not found: {input_path}")

    # Read JSONL
    # Assuming T012 outputs a JSONL file with one record per tag association
    # Expected format: {"tag": "python", "post_id": 123, "creation_date": "2023-01-15T10:00:00Z"}
    records = []
    with open(input_path, 'r', encoding='utf-8') as f:
        for line in f:
            line = line.strip()
            if not line:
                continue
            try:
                data = json.loads(line)
                # Normalize keys just in case
                records.append(data)
            except json.JSONDecodeError:
                continue

    if not records:
        raise ValueError("Input file is empty or contains no valid JSON records.")

    df = pd.DataFrame(records)
    
    # Ensure required columns exist
    required_cols = ['tag', 'creation_date']
    missing = [c for c in required_cols if c not in df.columns]
    if missing:
        raise ValueError(f"Missing required columns in input data: {missing}")

    return df

def normalize_tags(df: pd.DataFrame) -> pd.DataFrame:
    """
    Normalize tag strings: lowercase and trim whitespace.
    FR-003 requirement for normalization.
    """
    df = df.copy()
    # Lowercase and strip
    df['tag'] = df['tag'].astype(str).str.lower().str.strip()
    # Remove empty tags resulting from stripping
    df = df[df['tag'] != '']
    return df

def parse_dates(df: pd.DataFrame) -> pd.DataFrame:
    """
    Parse creation_date to datetime and extract year-month for binning.
    """
    df = df.copy()
    # Try parsing common formats
    # T012 might output ISO format or specific dump format
    df['creation_date'] = pd.to_datetime(df['creation_date'], errors='coerce')
    
    # Drop rows with invalid dates
    initial_count = len(df)
    df = df.dropna(subset=['creation_date'])
    if len(df) < initial_count:
        print(f"Warning: Dropped {initial_count - len(df)} rows with invalid dates.")

    # Create a 'year_month' column for binning (e.g., "2023-01")
    df['year_month'] = df['creation_date'].dt.to_period('M').astype(str)
    
    return df

def aggregate_monthly_frequencies(df: pd.DataFrame) -> pd.DataFrame:
    """
    Aggregate frequencies into monthly bins.
    Output: tag, year_month, count
    """
    # Group by tag and year_month, count occurrences
    agg_df = df.groupby(['tag', 'year_month']).size().reset_index(name='count')
    return agg_df

def filter_min_months(agg_df: pd.DataFrame) -> pd.DataFrame:
    """
    Filter for tags that have data in at least MIN_MONTHS_REQUIRED months.
    FR-003 requirement.
    """
    # Count number of months per tag
    tag_month_counts = agg_df.groupby('tag')['year_month'].nunique().reset_index(name='month_count')
    
    # Filter tags with >= 12 months
    valid_tags = tag_month_counts[tag_month_counts['month_count'] >= MIN_MONTHS_REQUIRED]['tag']
    
    # Filter the main dataframe
    filtered_df = agg_df[agg_df['tag'].isin(valid_tags)].copy()
    
    dropped_count = len(agg_df) - len(filtered_df)
    if dropped_count > 0:
        print(f"Filtered out tags with < {MIN_MONTHS_REQUIRED} months of data.")
    
    return filtered_df

def save_processed_data(df: pd.DataFrame, output_path: Path):
    """
    Save the processed dataframe to CSV.
    """
    output_path.parent.mkdir(parents=True, exist_ok=True)
    df.to_csv(output_path, index=False)
    print(f"Saved processed data to: {output_path}")

def main():
    """
    Main entry point for T013: Preprocess Stack Overflow tag data.
    1. Load raw data from data/raw/posts_tags.jsonl
    2. Normalize tags (lowercase, trim)
    3. Parse dates and bin into monthly frequencies
    4. Filter tags with >= 12 months of data
    5. Save to data/processed/tag_monthly_frequencies.csv
    """
    print("Starting T013: Preprocessing Stack Overflow Tag Data...")
    
    input_path = Path(RAW_DATA_DIR) / INPUT_FILE_NAME
    output_path = Path(PROCESSED_DATA_DIR) / OUTPUT_FILE_NAME

    # 1. Load
    print(f"Loading raw data from {input_path}...")
    df = load_raw_data(input_path)
    print(f"Loaded {len(df)} records.")

    # 2. Normalize
    print("Normalizing tags...")
    df = normalize_tags(df)

    # 3. Parse and Bin
    print("Parsing dates and aggregating into monthly bins...")
    df = parse_dates(df)
    df = aggregate_monthly_frequencies(df)

    # 4. Filter
    print(f"Filtering for tags with >= {MIN_MONTHS_REQUIRED} months of data...")
    df = filter_min_months(df)
    
    if df.empty:
        print("ERROR: No tags met the minimum month requirement. Cannot proceed.")
        return

    # 5. Save
    print(f"Saving results to {output_path}...")
    save_processed_data(df, output_path)

    print("T013 Preprocessing completed successfully.")

if __name__ == "__main__":
    main()
