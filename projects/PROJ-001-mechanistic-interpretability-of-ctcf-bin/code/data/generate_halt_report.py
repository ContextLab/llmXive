"""
T006: Generate Project Halt Report

This script is triggered ONLY if T004 (validate_sources.py) finds zero matched sets
of multi-modal data (ChIP-seq, ATAC-seq, H3K27ac) across >=5 cell types.

It generates a formal project halt report documenting the failure and exits
with a non-zero status code to halt the pipeline.

Output:
    docs/project_halt_report.md
"""

import json
import os
import sys
from datetime import datetime
from pathlib import Path
from typing import List, Dict, Any, Optional

# Add project root to path to allow imports from sibling modules
PROJECT_ROOT = Path(__file__).resolve().parent.parent.parent
sys.path.insert(0, str(PROJECT_ROOT))

from data.validate_sources import load_candidates, generate_gap_report

def load_gap_report_data() -> Optional[Dict[str, Any]]:
    """
    Attempt to load the gap report generated by T004 (validate_sources.py).
    Returns the report data if it exists, otherwise None.
    """
    gap_report_path = PROJECT_ROOT / "data" / "data_gap_report.md"
    manifest_path = PROJECT_ROOT / "data" / "manifest.json"
    candidates_path = PROJECT_ROOT / "data" / "candidate_sources.json"

    # If manifest exists, we shouldn't be running this task (T004 succeeded)
    if manifest_path.exists():
        print("ERROR: manifest.json exists. T004 succeeded. This task should not run.")
        return None

    # If gap report exists, load its content (it's markdown, we'll parse it or just include it)
    if gap_report_path.exists():
        with open(gap_report_path, 'r') as f:
            content = f.read()
        return {
            "source": "data_gap_report.md",
            "content": content,
            "type": "existing_report"
        }

    # Fallback: Try to reconstruct basic info from candidates if the report generation failed
    if candidates_path.exists():
        with open(candidates_path, 'r') as f:
            candidates = json.load(f)
        
        # Count unique cell types
        cell_types = set()
        for item in candidates:
            if 'cell_type' in item:
                cell_types.add(item['cell_type'])
        
        return {
            "source": "candidate_sources.json",
            "candidates_count": len(candidates),
            "unique_cell_types": list(cell_types),
            "type": "reconstructed"
        }

    return None

def generate_halt_report(report_data: Optional[Dict[str, Any]]) -> str:
    """
    Generate the content for the project halt report.
    """
    timestamp = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    
    report_lines = [
        "# Project Halt Report: PROJ-001-Mechanistic-Interpretability-of-CTCF-Bin",
        "",
        f"**Generated:** {timestamp}",
        f"**Status:** HALTED",
        "",
        "## Executive Summary",
        "",
        "The automated data pipeline for this project has been halted due to a critical "
        "failure in Phase 2 (Data Gap Resolution). Specifically, the validation step (T004) "
        "failed to identify a sufficient number of matched multi-modal data sets (ChIP-seq, "
        "ATAC-seq, H3K27ac) across the required minimum of 5 distinct cell types.",
        "",
        "Per the project specification, the pipeline requires >=5 matched sets to proceed. "
        "Zero matched sets were found.",
        "",
        "## Root Cause Analysis",
        ""
    ]

    if report_data:
        if report_data.get("type") == "existing_report":
            report_lines.append("The detailed gap analysis generated by T004 is included below:")
            report_lines.append("")
            report_lines.append(report_data.get("content", "No content available."))
        elif report_data.get("type") == "reconstructed":
            report_lines.append(f"- **Total Candidate Sources Found:** {report_data.get('candidates_count', 0)}")
            report_lines.append(f"- **Unique Cell Types Identified:** {len(report_data.get('unique_cell_types', []))}")
            report_lines.append(f"- **Missing Cell Types:** Unable to match all required modalities (ChIP-seq, ATAC-seq, H3K27ac) for any single cell type.")
            report_lines.append("")
            report_lines.append("The specific `data_gap_report.md` was not generated, likely due to an error in T004 execution. "
                              "However, the candidate search (T003) returned insufficient matched sets.")
    else:
        report_lines.append("- **Error:** Unable to retrieve detailed failure logs from T004 or candidate files.")
        report_lines.append("- **Assumption:** The search for ENCODE and GEO sources returned no valid matched sets.")

    report_lines.extend([
        "",
        "## Impact on Project Timeline",
        "",
        "- **Phase 1 (Setup):** Completed.",
        "- **Phase 2 (Data Gap Resolution):** FAILED.",
        "- **Phase 3+ (Foundational & User Stories):** BLOCKED.",
        "",
        "No further data ingestion, model training, or interpretability analysis can proceed "
        "without a valid dataset. The project is currently in a halted state.",
        "",
        "## Required Actions",
        "",
        "1. **Manual Review:** A human researcher must review the search logs and candidate sources.",
        "2. **Scope Revision:** Determine if the scope should be revised to:",
        "   - Use fewer cell types (if scientifically valid).",
        "   - Use single-modality data (sequence only, excluding chromatin marks).",
        "   - Expand search parameters to include additional data repositories.",
        "3. **Re-run Search:** If scope is revised, update `code/data/search_sources.py` and re-run the pipeline.",
        "4. **Manual Data Acquisition:** If public APIs yield no results, consider manual download of specific datasets.",
        "",
        "## Technical Details",
        "",
        "### Pipeline State",
        "- **Current Task:** T006 (Generate Halt Report)",
        "- **Trigger Condition:** T004 found 0 matched sets.",
        "- **Dependencies:** T001a, T001b, T002, T003, T004",
        "",
        "### Error Code",
        "The pipeline exited with code `1` to enforce the halt condition.",
        "",
        "---",
        "",
        "*This report was automatically generated by the llmXive research-implementer agent.*"
    ])

    return "\n".join(report_lines)

def main():
    """
    Main entry point for T006.
    """
    print("Starting T006: Generate Halt Report...")
    
    # Ensure docs directory exists
    docs_dir = PROJECT_ROOT / "docs"
    docs_dir.mkdir(parents=True, exist_ok=True)

    # Load failure data
    report_data = load_gap_report_data()
    
    if not report_data:
        print("WARNING: Could not load detailed failure data from T004 outputs.")
        print("Generating generic halt report.")

    # Generate report content
    report_content = generate_halt_report(report_data)

    # Write report to disk
    output_path = docs_dir / "project_halt_report.md"
    with open(output_path, 'w') as f:
        f.write(report_content)

    print(f"Halt report generated at: {output_path}")
    print("Pipeline HALTED.")
    
    # Exit with non-zero status to halt the pipeline
    sys.exit(1)

if __name__ == "__main__":
    main()