def extract_signal_from_bigwig(bigwig_path: str, chrom: str, start: int, end: int, resolution: int = 10) -> np.ndarray:
    """Extract normalized signal from BigWig file."""
    try:
        import pyBigWig
        bw = pyBigWig.open(bigwig_path)
        # Extract signal
        signal = bw.values(chrom, start, end, numpy=True)
        bw.close()
        
        if signal is None:
            return np.zeros(end - start)
        
        # Handle NaNs
        signal = np.nan_to_num(signal, nan=0.0)
        
        # Normalize to [0, 1] (simple min-max or z-score per cell type usually done globally)
        # Here we do a simple local normalization for the window to avoid division by zero
        if np.max(signal) > np.min(signal):
            signal = (signal - np.min(signal)) / (np.max(signal) - np.min(signal))
        
        # Downsample to match sequence resolution if needed (1000bp window)
        # BigWig is usually 10-50bp resolution. We need 1000 values for 1000bp.
        # If the signal is longer (e.g. 100bp resolution), we downsample.
        if len(signal) > WINDOW_SIZE:
            step = len(signal) // WINDOW_SIZE
            signal = signal[::step][:WINDOW_SIZE]
        elif len(signal) < WINDOW_SIZE:
            # Pad with zeros
            signal = np.pad(signal, (0, WINDOW_SIZE - len(signal)), mode='constant')
        
        return signal.astype(np.float32)
    except ImportError:
        logger.error("pyBigWig is required for BigWig processing but not installed.")
        return np.zeros(WINDOW_SIZE)
    except Exception as e:
        logger.error(f"Error extracting signal from {bigwig_path}: {e}")
        return np.zeros(WINDOW_SIZE)

def process_window_data(manifest: Dict, peak_coords: List[Tuple[str, int, str]], 
                        cell_type: str, output_dir: Path) -> List[Dict]:
    """Process a list of peak coordinates, extracting sequence and signals."""
    records = []
    
    # Find relevant files in manifest for this cell type
    cell_files = {}
    for entry in manifest.get('entries', []):
        if entry.get('cell_type') == cell_type:
            ftype = entry.get('file_type')
            if ftype in ['bam', 'bigwig'] and ftype not in cell_files:
                cell_files[ftype] = entry.get('local_path')
    
    bam_path = cell_files.get('bam')
    # We might need ATAC and H3K27ac bigwigs. The manifest might have multiple.
    # For simplicity, we assume the manifest groups them or we look for specific types.
    # Let's assume the manifest has 'atac_bigwig' and 'h3k27ac_bigwig' keys or similar.
    # Since the spec says "ATAC-seq, and H3K27ac", we need to find those.
    # We'll scan for files with 'atac' or 'h3k27ac' in the name or type.
    
    atac_bw = None
    h3k27ac_bw = None
    
    for entry in manifest.get('entries', []):
        if entry.get('cell_type') == cell_type:
            ftype = entry.get('file_type', '').lower()
            fname = entry.get('file_name', '').lower()
            if 'atac' in fname or 'atac' in ftype:
                atac_bw = entry.get('local_path')
            elif 'h3k27ac' in fname or 'h3k27ac' in ftype:
                h3k27ac_bw = entry.get('local_path')
    
    for chrom, center, label in peak_coords:
        start = max(0, center - HALF_WINDOW)
        end = center + HALF_WINDOW
        
        # Extract Sequence
        seq = extract_window_from_bam(bam_path, chrom, start, end, WINDOW_SIZE)
        if not seq or not filter_sequence_by_entropy(seq):
            continue # Skip low complexity
        
        # Extract Signals
        atac_signal = extract_signal_from_bigwig(atac_bw, chrom, start, end) if atac_bw else np.zeros(WINDOW_SIZE)
        h3k27ac_signal = extract_signal_from_bigwig(h3k27ac_bw, chrom, start, end) if h3k27ac_bw else np.zeros(WINDOW_SIZE)
        
        # One-hot encode
        one_hot = one_hot_encode(seq)
        
        record = {
            'chrom': chrom,
            'center': center,
            'label': label, # 1 for peak, 0 for non-peak
            'cell_type': cell_type,
            'sequence': seq,
            'one_hot': one_hot.tolist(),
            'atac_signal': atac_signal.tolist(),
            'h3k27ac_signal': h3k27ac_signal.tolist(),
            'entropy': calculate_shannon_entropy(seq)
        }
        records.append(record)
    
    return records

def align_modalities(records: List[Dict]) -> pd.DataFrame:
    """Align all modalities into a single DataFrame."""
    if not records:
        return pd.DataFrame()
    
    # Flatten the nested lists for pandas
    data = []
    for rec in records:
        row = {
            'chrom': rec['chrom'],
            'center': rec['center'],
            'label': rec['label'],
            'cell_type': rec['cell_type'],
            'sequence': rec['sequence'],
            'entropy': rec['entropy']
        }
        # Flatten one-hot and signals
        # We store them as lists in the dataframe, or explode them.
        # For training, we usually want a numpy array.
        # But for the dataset file (parquet), we can store lists.
        row['one_hot'] = rec['one_hot']
        row['atac_signal'] = rec['atac_signal']
        row['h3k27ac_signal'] = rec['h3k27ac_signal']
        data.append(row)
    
    df = pd.DataFrame(data)
    return df

def main():
    """Main entry point for feature extraction."""
    # Paths
    project_root = Path(__file__).resolve().parent.parent.parent
    manifest_path = project_root / "data" / "manifest.json"
    output_dir = project_root / "data" / "processed"
    output_dir.mkdir(parents=True, exist_ok=True)
    
    logger.info("Loading manifest...")
    try:
        manifest = load_manifest(str(manifest_path))
    except FileNotFoundError as e:
        logger.error(str(e))
        sys.exit(1)
    
    # We need to generate peak and non-peak coordinates.
    # The manifest should contain peak coordinates from the ChIP-seq peaks.
    # If not, we need to derive them or assume they are in the manifest.
    # The spec says "centered on CTCF peaks and non-peaks".
    # We assume the manifest has 'peaks' data or we generate non-peaks from the genome.
    # For this implementation, we assume the manifest contains a list of peaks
    # per cell type. If not, we might need to parse the BAM for peaks (too heavy).
    # Let's assume the manifest has a 'peaks' key or we generate synthetic non-peaks
    # from the same chromosomes as peaks.
    
    # Strategy:
    # 1. Iterate cell types in manifest.
    # 2. Get peaks (if in manifest).
    # 3. Generate non-peaks (random locations with same GC content or just random).
    # 4. Process.
    
    all_records = []
    
    # Check if manifest has peaks
    if 'peaks' in manifest:
        peaks_by_cell = manifest['peaks']
    else:
        # Fallback: Generate from BAM if possible? No, too slow.
        # We assume the manifest was built with peaks.
        logger.warning("Manifest does not contain 'peaks' data. Cannot proceed without peaks.")
        sys.exit(1)
    
    for cell_type, peaks in peaks_by_cell.items():
        logger.info(f"Processing cell type: {cell_type} with {len(peaks)} peaks.")
        
        # Generate non-peaks (negative samples)
        # We take the same number of non-peaks as peaks, randomly shifted
        non_peaks = []
        for peak in peaks:
            # Shift by at least 2kb to avoid overlap
            shift = np.random.randint(2000, 10000)
            new_center = peak['center'] + shift
            if new_center > 0:
                non_peaks.append({'chrom': peak['chrom'], 'center': new_center, 'label': 0})
        
        # Combine
        all_coords = [{'chrom': p['chrom'], 'center': p['center'], 'label': 1} for p in peaks] + non_peaks
        
        # Process
        records = process_window_data(manifest, all_coords, cell_type, output_dir)
        all_records.extend(records)
    
    logger.info(f"Total records extracted: {len(all_records)}")
    
    if not all_records:
        logger.warning("No records extracted. Check input data.")
        # Create an empty file to indicate completion
        df = pd.DataFrame()
    else:
        df = align_modalities(all_records)
    
    output_file = output_dir / "unified_ctcf_dataset.parquet"
    df.to_parquet(output_file)
    logger.info(f"Dataset saved to {output_file}")

if __name__ == "__main__":
    main()
