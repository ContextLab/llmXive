# Constants
WINDOW_SIZE = 1000  # ±500 bp
HALF_WINDOW = WINDOW_SIZE // 2
ONE_HOT_MAP = {'A': [1, 0, 0, 0], 'C': [0, 1, 0, 0], 'G': [0, 0, 1, 0], 'T': [0, 0, 0, 1], 'N': [0.25, 0.25, 0.25, 0.25]}

def load_manifest(manifest_path: str) -> Dict[str, Any]:
    """Load the data manifest generated by previous steps."""
    if not os.path.exists(manifest_path):
        raise FileNotFoundError(f"Manifest not found at {manifest_path}. Run T004/T005 first.")
    with open(manifest_path, 'r') as f:
        return json.load(f)

def calculate_shannon_entropy(seq: str) -> float:
    """Calculate Shannon entropy of a DNA sequence."""
    if not seq:
        return 0.0
    counts = np.zeros(4)
    for base in seq.upper():
        if base == 'A': counts[0] += 1
        elif base == 'C': counts[1] += 1
        elif base == 'G': counts[2] += 1
        elif base == 'T': counts[3] += 1
    
    total = np.sum(counts)
    if total == 0:
        return 0.0
    
    probs = counts / total
    # Avoid log(0)
    probs = probs[probs > 0]
    return -np.sum(probs * np.log2(probs))

def filter_sequence_by_entropy(seq: str, threshold: float = 0.8) -> bool:
    """Check if sequence passes low-complexity filter."""
    return calculate_shannon_entropy(seq) > threshold

def one_hot_encode(seq: str) -> np.ndarray:
    """Convert DNA sequence to one-hot encoding (L x 4)."""
    seq = seq.upper()
    encoded = np.zeros((len(seq), 4), dtype=np.float32)
    for i, base in enumerate(seq):
        if base in ONE_HOT_MAP:
            encoded[i] = ONE_HOT_MAP[base]
        else:
            # Handle ambiguous bases (e.g., N) by uniform distribution
            encoded[i] = [0.25, 0.25, 0.25, 0.25]
    return encoded

