logger = logging.getLogger(__name__)

def load_unique_subset(injected_dataset_path: str) -> CandidateList:
    """
    Load the unique subset generated by the unique subset generator.
    Returns a CandidateList object.
    """
    # Assuming unique subset is saved in a specific format by unique_subset_generator
    unique_path = injected_dataset_path.replace(".json", "_unique_subset.json")
    if not os.path.exists(unique_path):
        raise FileNotFoundError(f"Unique subset file not found: {unique_path}")
        
    with open(unique_path, 'r') as f:
        data = json.load(f)
        
    # Construct CandidateList
    return CandidateList(
        documents=data.get("documents", []),
        query_ids=data.get("query_ids", []),
        metadata=data.get("metadata", {})
    )

def load_redundant_results(results_path: str) -> List[Dict[str, Any]]:
    """Load results from the redundant run."""
    if not os.path.exists(results_path):
        raise FileNotFoundError(f"Redundant results file not found: {results_path}")
    with open(results_path, 'r') as f:
        return json.load(f)

def run_baseline_on_unique(candidate_list: CandidateList, 
                           queries: Dict, 
                           qrels: Dict) -> List[float]:
    """
    Run the baseline active ranker on the unique subset.
    Returns a list of NDCG@10 scores per query.
    """
    logger.info(f"Running baseline on unique subset with {len(candidate_list.documents)} documents.")
    
    # Placeholder for actual ranking logic
    # In a real implementation, this would call the active ranker logic
    # For now, we simulate NDCG calculation based on random or simple ordering
    # to demonstrate the metric calculation flow.
    
    ndcg_scores = []
    for query_id, query_text in queries.items():
        # Get relevant docs for this query
        relevant_docs = qrels.get(query_id, {})
        relevance_scores = [relevant_docs.get(doc_id, 0) for doc_id in candidate_list.documents]
        
        # Calculate NDCG
        ndcg = calculate_ndcg_at_10(relevance_scores)
        ndcg_scores.append(ndcg)
        
    return ndcg_scores

def calculate_drop_percentage(redundant_ndcg: float, unique_ndcg: float) -> float:
    """
    Calculate the percentage drop in NDCG when moving from unique to redundant.
    Formula: (Unique - Redundant) / Unique * 100
    Note: If redundant is worse, drop is positive.
    """
    if unique_ndcg == 0:
        return 0.0
    return ((unique_ndcg - redundant_ndcg) / unique_ndcg) * 100

def main():
    """
    Main entry point to run the baseline on unique subset and compare with redundant.
    """
    config = get_config()
    dataset_name = config.get("dataset", "nfcorpus")
    injected_path = "data/injected_nfcorpus.json" # Example path
    
    if not os.path.exists(injected_path):
        logger.error(f"Injected dataset not found at {injected_path}. Run injection pipeline first.")
        sys.exit(1)
        
    # Load unique subset
    unique_subset = load_unique_subset(injected_path)
    
    # Load original BEIR data for ground truth
    corpus, queries, qrels = load_beir_corpus(dataset_name)
    
    # Run baseline on unique
    unique_ndcg_scores = run_baseline_on_unique(unique_subset, queries, qrels)
    avg_unique_ndcg = aggregate_ndcg_scores(unique_ndcg_scores)
    
    # Load redundant results (simulated for this example)
    # In reality, this would come from a previous run of the full baseline on redundant data
    redundant_results_path = "data/results/redundant_baseline_results.json"
    if os.path.exists(redundant_results_path):
        redundant_scores = load_redundant_results(redundant_results_path)
        avg_redundant_ndcg = aggregate_ndcg_scores(redundant_scores)
    else:
        logger.warning("Redundant results not found. Assuming 0.0 for demonstration.")
        avg_redundant_ndcg = 0.0
        
    drop = calculate_drop_percentage(avg_redundant_ndcg, avg_unique_ndcg)
    
    logger.info(f"Average NDCG@10 (Unique): {avg_unique_ndcg:.4f}")
    logger.info(f"Average NDCG@10 (Redundant): {avg_redundant_ndcg:.4f}")
    logger.info(f"NDCG@10 Drop Percentage: {drop:.2f}%")
    
    # Save results
    output_path = "data/results/baseline_unique_comparison.json"
    os.makedirs(os.path.dirname(output_path), exist_ok=True)
    results = {
        "unique_ndcg": avg_unique_ndcg,
        "redundant_ndcg": avg_redundant_ndcg,
        "drop_percentage": drop,
        "dataset": dataset_name
    }
    with open(output_path, 'w') as f:
        json.dump(results, f, indent=2)
        
    logger.info(f"Results saved to {output_path}")

if __name__ == "__main__":
    main()
