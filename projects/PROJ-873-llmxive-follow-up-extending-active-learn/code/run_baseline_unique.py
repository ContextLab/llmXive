"""
Task T015b: Run baseline active ranker on the unique subset to establish reference NDCG@10.

This script:
1. Loads the unique subset generated by T015a (from `data/unique_subset.json`).
2. Runs the baseline active ranker (from `code/ranker.py`) on this unique subset.
3. Calculates NDCG@10 against the BEIR ground truth.
4. Loads the redundant run results (from T015, expected at `data/redundant_run_results.json`) to calculate the drop percentage.
5. Logs the NDCG@10 drop percentage and writes the final results to `data/baseline_unique_results.json`.

Prerequisites:
- T012: Synthetic redundancy injection must have run to create `data/injected_dataset.json`.
- T015a: Unique subset generation must have run to create `data/unique_subset.json`.
- T015: Baseline run on redundant data must have run to create `data/redundant_run_results.json`.
"""
