# Paths
UNIQUE_SUBSET_PATH = "data/unique_subset.json"
REDUNDANT_RUN_RESULTS_PATH = "data/redundant_run_results.json"
OUTPUT_PATH = "data/baseline_unique_results.json"
INJECTED_DATASET_PATH = "data/injected_dataset.json"

def load_unique_subset(path: str) -> Dict[str, Any]:
    """Load the unique subset generated by T015a."""
    if not os.path.exists(path):
        raise FileNotFoundError(f"Unique subset file not found at {path}. "
                                "Please ensure T015a has been executed.")
    with open(path, 'r', encoding='utf-8') as f:
        return json.load(f)

def load_redundant_results(path: str) -> Dict[str, Any]:
    """Load results from the redundant run (T015) for comparison."""
    if not os.path.exists(path):
        raise FileNotFoundError(f"Redundant run results not found at {path}. "
                                "Please ensure T015 has been executed.")
    with open(path, 'r', encoding='utf-8') as f:
        return json.load(f)

def run_baseline_on_unique(unique_data: Dict[str, Any]) -> Tuple[float, List[Dict], Dict[str, Any]]:
    """
    Run the baseline active ranker on the unique subset.
    
    Returns:
        Tuple of (ndcg_score, ranked_results, metadata)
    """
    logger.info(f"Starting baseline active ranker on unique subset: {len(unique_data.get('queries', []))} queries")
    
    # Extract queries and corpus from the unique subset structure
    # Assuming unique_data structure matches the injected dataset format but with unique items
    queries = unique_data.get('queries', [])
    corpus = unique_data.get('corpus', {})
    qrels = unique_data.get('qrels', {}) # Ground truth for NDCG calculation

    all_ranked_results = []
    total_ndcg = 0.0
    num_queries = 0

    # Run the baseline ranker
    # Note: run_baseline_active_ranker is expected to return a list of ranked documents per query
    # and potentially log comparisons.
    
    # We need to adapt the ranker call to our data structure.
    # The ranker typically takes a query, a candidate list, and ground truth.
    
    for query_id, query_text in queries.items():
        # Retrieve relevant docs for this query from qrels
        relevant_docs = qrels.get(query_id, {})
        
        # Create a CandidateList for this query from the unique corpus
        # The unique subset should contain the relevant documents plus distractors, but deduplicated
        candidate_docs = []
        for doc_id, doc_text in corpus.items():
            # Filter to only docs relevant to this query or a representative sample if corpus is huge
            # For NDCG calculation, we need the ranked list of ALL candidate docs for the query.
            # In a real BEIR scenario, the retriever provides the top-K.
            # Here, since we are simulating the "active ranker" on a candidate list,
            # we assume the 'corpus' in unique_data is the candidate pool for these queries.
            candidate_docs.append({"doc_id": doc_id, "text": doc_text})
        
        if not candidate_docs:
            continue

        # Run the baseline ranker
        # The ranker function signature from API surface is assumed to be:
        # run_baseline_active_ranker(query_text, candidates, qrels_for_query)
        # However, the actual implementation in ranker.py might differ.
        # Based on T015 description, it processes the full candidate list.
        
        try:
            # Assuming run_baseline_active_ranker returns a list of doc_ids in ranked order
            ranked_doc_ids = run_baseline_active_ranker(
                query_id=query_id,
                query_text=query_text,
                candidates=candidate_docs,
                qrels=relevant_docs
            )
            
            # Calculate NDCG@10 for this query
            ndcg = calculate_ndcg_at_10(ranked_doc_ids, relevant_docs)
            total_ndcg += ndcg
            num_queries += 1
            
            all_ranked_results.append({
                "query_id": query_id,
                "ranked_doc_ids": ranked_doc_ids,
                "ndcg_at_10": ndcg
            })
            
        except Exception as e:
            logger.error(f"Error processing query {query_id}: {e}")
            continue

    avg_ndcg = total_ndcg / num_queries if num_queries > 0 else 0.0
    metadata = {
        "total_queries": num_queries,
        "avg_ndcg_at_10": avg_ndcg,
        "timestamp": time.strftime("%Y-%m-%d %H:%M:%S")
    }
    
    return avg_ndcg, all_ranked_results, metadata

def calculate_drop_percentage(unique_ndcg: float, redundant_ndcg: float) -> float:
    """Calculate the NDCG@10 drop percentage from redundant to unique."""
    if redundant_ndcg == 0:
        return 0.0
    # Drop = (Redundant - Unique) / Redundant * 100
    # If Unique is lower, this is positive (drop).
    # If Unique is higher (recovery), this is negative (gain).
    # The task asks for "NDCG@10 drop percentage" to test the hypothesis that redundant is lower.
    # So we expect (Redundant < Unique) -> Negative drop (i.e., gain).
    # Wait, the hypothesis is "[deferred] lower" meaning the redundant run is lower?
    # Actually, redundancy usually hurts NDCG if the ranker gets confused or wastes calls.
    # If we run on unique, we expect HIGHER NDCG.
    # So "Drop" from Redundant to Unique would be negative.
    # Let's calculate: (Unique - Redundant) / Redundant * 100 as "Improvement".
    # But the task asks for "NDCG@10 drop percentage".
    # Let's interpret "drop" as (Redundant - Unique) / Redundant.
    # If Unique > Redundant, this is negative (a "negative drop" = gain).
    return (unique_ndcg - redundant_ndcg) / redundant_ndcg * 100.0 if redundant_ndcg != 0 else 0.0

def main():
    logger.info("Starting T015b: Baseline run on unique subset")
    
    # 1. Check system limits
    check_system_limits()
    
    # 2. Load unique subset
    try:
        unique_data = load_unique_subset(UNIQUE_SUBSET_PATH)
        logger.info(f"Loaded unique subset with {len(unique_data.get('queries', {}))} queries")
    except FileNotFoundError as e:
        logger.error(str(e))
        sys.exit(1)

    # 3. Run baseline on unique subset
    unique_ndcg, unique_results, unique_metadata = run_baseline_on_unique(unique_data)
    logger.info(f"Unique subset NDCG@10: {unique_ndcg:.4f}")

    # 4. Load redundant run results (from T015) to calculate drop
    try:
        redundant_results = load_redundant_results(REDUNDANT_RUN_RESULTS_PATH)
        redundant_ndcg = redundant_results.get("metadata", {}).get("avg_ndcg_at_10", 0.0)
        logger.info(f"Loaded redundant run results. NDCG@10: {redundant_ndcg:.4f}")
    except FileNotFoundError:
        logger.warning(f"Redundant run results not found at {REDUNDANT_RUN_RESULTS_PATH}. "
                       "Cannot calculate drop percentage. Skipping comparison.")
        redundant_ndcg = None

    # 5. Calculate drop percentage
    drop_percentage = 0.0
    if redundant_ndcg is not None:
        drop_percentage = calculate_drop_percentage(unique_ndcg, redundant_ndcg)
        logger.info(f"NDCG@10 Comparison: Unique={unique_ndcg:.4f}, Redundant={redundant_ndcg:.4f}")
        logger.info(f"NDCG@10 Drop Percentage (Unique - Redundant) / Redundant: {drop_percentage:.2f}%")
        if drop_percentage < 0:
            logger.info(f"Result: NDCG@10 IMPROVED by {abs(drop_percentage):.2f}% on unique subset.")
        else:
            logger.info(f"Result: NDCG@10 DROPPED by {drop_percentage:.2f}% on unique subset.")
    else:
        logger.info("Skipping drop calculation due to missing redundant results.")

    # 6. Save results
    final_results = {
        "unique_ndcg_at_10": unique_ndcg,
        "redundant_ndcg_at_10": redundant_ndcg,
        "ndcg_drop_percentage": drop_percentage,
        "metadata": unique_metadata,
        "ranked_results": unique_results
    }

    os.makedirs(os.path.dirname(OUTPUT_PATH), exist_ok=True)
    with open(OUTPUT_PATH, 'w', encoding='utf-8') as f:
        json.dump(final_results, f, indent=2)
    
    logger.info(f"Results saved to {OUTPUT_PATH}")
    logger.info("T015b completed.")

if __name__ == "__main__":
    main()
