from config import get_config

# Initialize logger
logger = logging.getLogger(__name__)

def load_cluster_results(cluster_file_path: str) -> List[MinHashCluster]:
    """
    Load clustering results from a JSON file generated by the clustering pipeline.
    
    Args:
        cluster_file_path: Path to the JSON file containing cluster data.
        
    Returns:
        List of MinHashCluster objects.
    """
    if not os.path.exists(cluster_file_path):
        raise FileNotFoundError(f"Cluster results file not found: {cluster_file_path}")
    
    with open(cluster_file_path, 'r') as f:
        data = json.load(f)
    
    clusters = []
    for item in data:
        cluster = MinHashCluster(
            cluster_id=item['cluster_id'],
            representatives=item['representatives'],
            members=item['members'],
            jaccard_threshold=item.get('jaccard_threshold', 0.95)
        )
        clusters.append(cluster)
    
    return clusters

def apply_pre_clustering_filter(
    candidates: CandidateList,
    clusters: List[MinHashCluster],
    min_reduction_ratio: float = 0.30
) -> Tuple[CandidateList, Dict[str, Any]]:
    """
    Apply the pre-clustering filter to reduce the candidate pool.
    
    This function filters the candidate list using the provided MinHash-LSH clusters,
    keeping only the representative documents from each cluster to reduce redundancy.
    It enforces a minimum pool size reduction of `min_reduction_ratio` (default 30%).
    
    Args:
        candidates: The full CandidateList containing redundant passages.
        clusters: List of MinHashCluster objects from the clustering pipeline.
        min_reduction_ratio: Minimum required reduction ratio (0.0 to 1.0).
        
    Returns:
        A tuple containing:
            - Filtered CandidateList with reduced pool size.
            - A stats dictionary with reduction metrics.
            
    Raises:
        ValueError: If the reduction ratio is less than the required minimum.
    """
    original_size = len(candidates.documents)
    
    if original_size == 0:
        logger.warning("Empty candidate list provided to pre-clustering filter.")
        return candidates, {
            'original_size': 0,
            'filtered_size': 0,
            'reduction_ratio': 0.0,
            'aborted': False
        }
    
    # Use the clustering filter logic to select representatives
    # filter_candidates_by_clustering returns a list of unique representative IDs/docs
    filtered_docs = filter_candidates_by_clustering(candidates.documents, clusters)
    
    filtered_size = len(filtered_docs)
    
    if filtered_size == 0:
        raise RuntimeError("Pre-clustering filter resulted in an empty candidate pool.")
    
    reduction_ratio = (original_size - filtered_size) / original_size
    
    stats = {
        'original_size': original_size,
        'filtered_size': filtered_size,
        'reduction_ratio': reduction_ratio,
        'aborted': False
    }
    
    # Enforce constraint: reduction must be >= min_reduction_ratio
    if reduction_ratio < min_reduction_ratio:
        error_msg = (
            f"Constraint Violation: Pre-clustering filter reduction ({reduction_ratio:.2%}) "
            f"is below the required minimum ({min_reduction_ratio:.2%}). "
            f"Original size: {original_size}, Filtered size: {filtered_size}. "
            "Aborting execution as per US-2 requirements."
        )
        logger.error(error_msg)
        log_pairwise_comparison(
            "constraint_violation",
            {
                'metric': 'pool_reduction',
                'expected_min': min_reduction_ratio,
                'actual': reduction_ratio,
                'original_size': original_size,
                'filtered_size': filtered_size
            }
        )
        stats['aborted'] = True
        stats['error'] = error_msg
        raise ValueError(error_msg)
    
    # Construct the new filtered CandidateList
    filtered_candidates = CandidateList(
        documents=filtered_docs,
        query=candidates.query,
        metadata=candidates.metadata.copy() if candidates.metadata else {}
    )
    
    logger.info(
        f"Pre-clustering filter applied: {original_size} -> {filtered_size} docs "
        f"(Reduction: {reduction_ratio:.2%})"
    )
    
    return filtered_candidates, stats

def run_ranker_with_filter(
    candidates: CandidateList,
    clusters: List[MinHashCluster],
    ranking_function: callable,
    min_reduction_ratio: float = 0.30
) -> Tuple[List[Dict[str, Any]], Dict[str, Any]]:
    """
    Run the ranking pipeline with pre-clustering filter applied.
    
    This is the main entry point for the clustering-aided ranking variant.
    It filters candidates first, then runs the provided ranking function.
    
    Args:
        candidates: Full candidate list with redundant passages.
        clusters: Clustering results for redundancy removal.
        ranking_function: A callable that takes a CandidateList and returns ranked results.
        min_reduction_ratio: Minimum required pool size reduction.
        
    Returns:
        A tuple containing:
            - Ranked results list.
            - Execution stats including filtering metrics.
    """
    logger.info("Starting pre-clustering filter for candidate pool reduction.")
    
    try:
        filtered_candidates, filter_stats = apply_pre_clustering_filter(
            candidates, clusters, min_reduction_ratio
        )
    except ValueError as e:
        # Constraint violation - abort as per task requirements
        return [], {
            'success': False,
            'error': str(e),
            'filter_stats': filter_stats
        }
    
    if filter_stats.get('aborted', False):
        return [], {
            'success': False,
            'error': filter_stats.get('error', 'Unknown constraint violation'),
            'filter_stats': filter_stats
        }
    
    logger.info("Running ranking on filtered candidate pool.")
    ranked_results = ranking_function(filtered_candidates)
    
    return ranked_results, {
        'success': True,
        'filter_stats': filter_stats,
        'ranking_count': len(ranked_results)
    }

def main():
    """
    Main entry point for testing the pre-clustering filter logic.
    This function demonstrates the filter by loading sample data and applying the filter.
    """
    init_logging()
    config = get_config()
    
    # Example usage (would be populated with real paths in a full run)
    # In a real scenario, these paths would come from config or command line args
    sample_candidate_file = "data/injected_candidates.json"
    sample_cluster_file = "data/clustering_results.json"
    
    if not os.path.exists(sample_candidate_file) or not os.path.exists(sample_cluster_file):
        logger.info("Sample data files not found. Skipping main execution demo.")
        logger.info("To run this module, ensure data/injected_candidates.json and data/clustering_results.json exist.")
        return
    
    # Load data
    with open(sample_candidate_file, 'r') as f:
        candidate_data = json.load(f)
    
    candidates = CandidateList(
        documents=candidate_data['documents'],
        query=candidate_data['query'],
        metadata=candidate_data.get('metadata', {})
    )
    
    clusters = load_cluster_results(sample_cluster_file)
    
    # Define a dummy ranking function for demonstration
    def dummy_ranker(cands: CandidateList) -> List[Dict[str, Any]]:
        # Return top 10 by document ID order (placeholder logic)
        return [{'doc_id': doc['doc_id'], 'score': i} for i, doc in enumerate(cands.documents[:10])]
    
    # Apply filter and run ranker
    try:
        results, stats = run_ranker_with_filter(
            candidates, clusters, dummy_ranker, min_reduction_ratio=0.30
        )
        
        if stats['success']:
            logger.info(f"Ranking completed successfully. Filtered {stats['filter_stats']['original_size']} to {stats['filter_stats']['filtered_size']} docs.")
        else:
            logger.error(f"Ranking failed: {stats['error']}")
            
    except Exception as e:
        logger.error(f"Unexpected error during ranking: {e}")

if __name__ == "__main__":
    main()