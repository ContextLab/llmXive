"""
Sampling module for T013b: Filter logged comparisons, calculate sample size, and select stratified random sample.

This module implements the logic to:
1. Load pairwise comparison logs generated by T013 (logging_config).
2. Filter for "wasted" calls (similarity > 0.95).
3. Calculate dynamic sample size using T013c logic (min(total_flagged, upper_bound)).
4. Select a stratified random sample based on query IDs.
5. Save the resulting sample to a JSON file for LLM consensus validation.
"""
import json
import os
import random
from typing import List, Dict, Any, Optional, Tuple
from collections import defaultdict
import logging

from metrics import calculate_dynamic_sample_size
from config import get_config

# Configure logging
logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def load_comparison_logs(log_path: Optional[str] = None) -> List[Dict[str, Any]]:
    """
    Load pairwise comparison logs from the file generated by logging_config.

    Args:
        log_path: Path to the comparison log file. If None, uses the default path
                  from logging_config.get_comparison_log_path().

    Returns:
        List of comparison records (dicts).
    """
    if log_path is None:
        # Import here to avoid circular dependency issues if logging_config is heavy
        from logging_config import get_comparison_log_path
        log_path = get_comparison_log_path()

    if not os.path.exists(log_path):
        raise FileNotFoundError(f"Comparison log file not found at {log_path}. "
                                "Ensure T013 (log_pairwise_comparison) has been run.")

    comparisons = []
    with open(log_path, 'r', encoding='utf-8') as f:
        for line_num, line in enumerate(f, 1):
            line = line.strip()
            if not line:
                continue
            try:
                record = json.loads(line)
                comparisons.append(record)
            except json.JSONDecodeError as e:
                logger.warning(f"Skipping malformed JSON on line {line_num}: {e}")

    logger.info(f"Loaded {len(comparisons)} comparison records from {log_path}")
    return comparisons

def filter_wasted_calls(comparisons: List[Dict[str, Any]], threshold: float = 0.95) -> List[Dict[str, Any]]:
    """
    Filter comparisons to retain only those flagged as "wasted" (similarity > threshold).

    Args:
        comparisons: List of all comparison records.
        threshold: Cosine similarity threshold for "wasted" classification.

    Returns:
        List of comparisons where similarity > threshold.
    """
    wasted = []
    for comp in comparisons:
        # The similarity key might be 'similarity', 'cosine_similarity', or 'score' depending on T013 implementation.
        # Based on T013 description: "flag pairs with similarity > 0.95".
        # We assume the log contains a 'similarity' or 'score' field.
        sim_value = comp.get('similarity') or comp.get('cosine_similarity') or comp.get('score')

        if sim_value is None:
            logger.warning(f"Record missing similarity score: {comp}")
            continue

        if float(sim_value) > threshold:
            wasted.append(comp)

    logger.info(f"Filtered {len(wasted)} wasted calls (similarity > {threshold}) out of {len(comparisons)} total.")
    return wasted

def stratify_by_query(comparisons: List[Dict[str, Any]]) -> Dict[str, List[Dict[str, Any]]]:
    """
    Group comparisons by query_id for stratification.

    Args:
        comparisons: List of comparison records.

    Returns:
        Dictionary mapping query_id to list of records for that query.
    """
    stratified = defaultdict(list)
    for comp in comparisons:
        # Assume 'query_id' or 'qid' is present in the log record
        qid = comp.get('query_id') or comp.get('qid') or 'unknown'
        stratified[qid].append(comp)

    return dict(stratified)

def select_stratified_sample(
    wasted_calls: List[Dict[str, Any]],
    target_sample_size: int,
    random_seed: Optional[int] = None
) -> List[Dict[str, Any]]:
    """
    Select a stratified random sample from the wasted calls.

    This ensures representation from every query that generated wasted calls.
    If the total wasted calls is less than target_sample_size, returns all of them.

    Args:
        wasted_calls: List of all wasted call records.
        target_sample_size: Desired number of samples to select.
        random_seed: Random seed for reproducibility.

    Returns:
        List of selected sample records.
    """
    if random_seed is not None:
        random.seed(random_seed)

    if len(wasted_calls) <= target_sample_size:
        logger.info(f"Total wasted calls ({len(wasted_calls)}) <= target sample size ({target_sample_size}). Returning all.")
        return wasted_calls

    # Stratify by query
    stratified = stratify_by_query(wasted_calls)
    query_ids = list(stratified.keys())
    total_wasted = len(wasted_calls)

    # Calculate proportional allocation
    sample_per_query = {}
    remaining_slots = target_sample_size
    remaining_queries = len(query_ids)

    # First pass: assign at least 1 per query if possible, then distribute proportionally
    # Simple proportional allocation:
    for qid in query_ids:
        count = len(stratified[qid])
        proportional = int((count / total_wasted) * target_sample_size)
        if proportional < 1 and target_sample_size > len(query_ids):
            proportional = 1
        sample_per_query[qid] = proportional
        remaining_slots -= proportional
        remaining_queries -= 1

    # Distribute remaining slots to largest groups or randomly
    if remaining_slots > 0:
        # Sort queries by count descending to give extra to larger groups
        sorted_queries = sorted(query_ids, key=lambda q: len(stratified[q]), reverse=True)
        for qid in sorted_queries:
            if remaining_slots <= 0:
                break
            sample_per_query[qid] += 1
            remaining_slots -= 1

    # Select samples
    selected_samples = []
    for qid, count in sample_per_query.items():
        group = stratified[qid]
        # Ensure we don't request more than available
        actual_count = min(count, len(group))
        selected = random.sample(group, actual_count)
        selected_samples.extend(selected)

    logger.info(f"Selected {len(selected_samples)} stratified samples from {len(query_ids)} queries.")
    return selected_samples

def run_sampling_pipeline(
    log_path: Optional[str] = None,
    similarity_threshold: float = 0.95,
    output_path: Optional[str] = None,
    upper_bound: int = 100,
    random_seed: Optional[int] = None
) -> Dict[str, Any]:
    """
    Main entry point for T013b: Run the full sampling pipeline.

    1. Load logs.
    2. Filter wasted calls.
    3. Calculate dynamic sample size (min(total_flagged, upper_bound)).
    4. Select stratified sample.
    5. Save to output file.

    Args:
        log_path: Path to input comparison logs.
        similarity_threshold: Threshold for wasted call classification.
        output_path: Path to save the sample JSON. Defaults to data/sampling/wasted_sample.json.
        upper_bound: Maximum number of samples to validate (from T013c).
        random_seed: Seed for reproducibility.

    Returns:
        Dictionary containing summary statistics and output path.
    """
    # 1. Load
    comparisons = load_comparison_logs(log_path)

    # 2. Filter
    wasted_calls = filter_wasted_calls(comparisons, similarity_threshold)

    if not wasted_calls:
        logger.warning("No wasted calls found. Returning empty sample.")
        return {
            "total_comparisons": len(comparisons),
            "wasted_calls": 0,
            "sample_size": 0,
            "output_path": None,
            "message": "No wasted calls found to sample."
        }

    # 3. Calculate Sample Size (T013c logic)
    # Formula: min([deferred] of total flagged calls, upper_bound)
    # Here [deferred] is the total flagged calls count.
    total_flagged = len(wasted_calls)
    sample_size = calculate_dynamic_sample_size(total_flagged, upper_bound=upper_bound)
    logger.info(f"Calculated sample size: {sample_size} (Total flagged: {total_flagged}, Upper bound: {upper_bound})")

    # 4. Select Sample
    sample = select_stratified_sample(wasted_calls, sample_size, random_seed)

    # 5. Save
    if output_path is None:
        output_dir = "data/sampling"
        os.makedirs(output_dir, exist_ok=True)
        output_path = os.path.join(output_dir, "wasted_sample.json")

    with open(output_path, 'w', encoding='utf-8') as f:
        json.dump(sample, f, indent=2, ensure_ascii=False)

    logger.info(f"Saved {len(sample)} samples to {output_path}")

    return {
        "total_comparisons": len(comparisons),
        "wasted_calls": total_flagged,
        "sample_size": len(sample),
        "upper_bound": upper_bound,
        "output_path": output_path
    }

def main():
    """CLI entry point for T013b."""
    import argparse

    parser = argparse.ArgumentParser(description="T013b: Filter and sample wasted calls for validation.")
    parser.add_argument("--log-path", type=str, help="Path to comparison log file.")
    parser.add_argument("--threshold", type=float, default=0.95, help="Similarity threshold for wasted calls.")
    parser.add_argument("--output", type=str, help="Output path for sample JSON.")
    parser.add_argument("--upper-bound", type=int, default=100, help="Maximum sample size (T013c).")
    parser.add_argument("--seed", type=int, default=42, help="Random seed.")

    args = parser.parse_args()

    result = run_sampling_pipeline(
        log_path=args.log_path,
        similarity_threshold=args.threshold,
        output_path=args.output,
        upper_bound=args.upper_bound,
        random_seed=args.seed
    )

    print(json.dumps(result, indent=2))

if __name__ == "__main__":
    main()
