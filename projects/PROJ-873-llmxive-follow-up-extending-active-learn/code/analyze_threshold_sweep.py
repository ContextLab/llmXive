"""
Task T025a: Analyze threshold sweep results and output optimal threshold data.

This script reads the individual threshold sweep result files generated by T025,
compares them against the baseline, identifies the optimal threshold based on
the best trade-off between NDCG recovery and reduction ratio, and outputs the
complete sensitivity analysis to data/results/threshold_sweep.json.
"""
import os
import json
import sys
from typing import List, Dict, Any, Optional
from dataclasses import dataclass, asdict
import logging

# Project imports
from config import get_config
from metrics import calculate_ndcg_at_10

# Configure logging
logging.basicConfig(
    level=logging.INFO,
    format='%(asctime)s - %(levelname)s - %(message)s'
)
logger = logging.getLogger(__name__)

@dataclass
class SweepResult:
    """Data class representing a single threshold sweep result."""
    threshold: float
    original_count: int
    filtered_count: int
    reduction_ratio: float
    ndcg_score: float
    wasted_call_ratio: float
    result_file: str

@dataclass
class ThresholdSweepSummary:
    """Data class for the complete sweep summary."""
    dataset: str
    thresholds_tested: List[float]
    results: List[SweepResult]
    optimal_threshold: float
    baseline_ndcg: Optional[float] = None
    baseline_reduction_ratio: Optional[float] = None
    baseline_wasted_call_ratio: Optional[float] = None

def load_sweep_result(result_file: str) -> Optional[Dict[str, Any]]:
    """Load a single threshold sweep result file."""
    if not os.path.exists(result_file):
        logger.warning(f"Result file not found: {result_file}")
        return None
    
    try:
        with open(result_file, 'r') as f:
            return json.load(f)
    except (json.JSONDecodeError, IOError) as e:
        logger.error(f"Failed to load {result_file}: {e}")
        return None

def find_optimal_threshold(
    results: List[SweepResult],
    baseline_ndcg: Optional[float] = None,
    min_reduction_ratio: float = 0.30
) -> float:
    """
    Determine the optimal threshold based on NDCG recovery and reduction ratio.
    
    Selection criteria:
    1. Must achieve at least min_reduction_ratio (30% as per FR-002/US-2)
    2. Among qualifying thresholds, select the one with highest NDCG score
    3. If tie in NDCG, prefer the one with higher reduction ratio
    """
    qualifying = [
        r for r in results 
        if r.reduction_ratio >= min_reduction_ratio
    ]
    
    if not qualifying:
        logger.warning(
            f"No thresholds meet minimum reduction ratio of {min_reduction_ratio}. "
            "Selecting threshold with best NDCG anyway."
        )
        qualifying = results if results else []
    
    if not qualifying:
        raise ValueError("No valid results to analyze")
    
    # Sort by NDCG (descending), then by reduction_ratio (descending)
    qualifying.sort(key=lambda x: (x.ndcg_score, x.reduction_ratio), reverse=True)
    
    optimal = qualifying[0]
    logger.info(
        f"Selected optimal threshold: {optimal.threshold} "
        f"(NDCG: {optimal.ndcg_score:.4f}, Reduction: {optimal.reduction_ratio:.2%})"
    )
    
    return optimal.threshold

def calculate_baseline_metrics(
    redundant_results_file: str = "data/results/baseline_redundant.json"
) -> Dict[str, float]:
    """Calculate baseline metrics from the redundant dataset run."""
    baseline = {
        'ndcg': None,
        'wasted_call_ratio': None,
        'reduction_ratio': 0.0
    }
    
    if os.path.exists(redundant_results_file):
        try:
            with open(redundant_results_file, 'r') as f:
                data = json.load(f)
                baseline['ndcg'] = data.get('ndcg_score')
                baseline['wasted_call_ratio'] = data.get('wasted_call_ratio')
        except (json.JSONDecodeError, IOError) as e:
            logger.warning(f"Could not load baseline results: {e}")
    
    return baseline

def run_analysis(
    sweep_dir: str = "data/results",
    output_file: str = "data/results/threshold_sweep.json",
    dataset_name: str = "nfcorpus"
) -> Dict[str, Any]:
    """
    Main analysis function for threshold sweep results.
    
    Args:
        sweep_dir: Directory containing individual threshold result files
        output_file: Path to write the final summary JSON
        dataset_name: Name of the dataset being analyzed
    
    Returns:
        Dictionary containing the complete analysis results
    """
    config = get_config()
    
    # Define thresholds tested (matching T025 implementation)
    thresholds = [0.85, 0.90, 0.92, 0.95, 0.97]
    
    results = []
    
    # Load each threshold result
    for threshold in thresholds:
        result_file = os.path.join(sweep_dir, f"sweep_threshold_{threshold:.2f}.json")
        data = load_sweep_result(result_file)
        
        if data:
            result = SweepResult(
                threshold=data.get('threshold', threshold),
                original_count=data.get('original_count', 0),
                filtered_count=data.get('filtered_count', 0),
                reduction_ratio=data.get('reduction_ratio', 0.0),
                ndcg_score=data.get('ndcg_score', 0.0),
                wasted_call_ratio=data.get('wasted_call_ratio', 0.0),
                result_file=data.get('result_file', result_file)
            )
            results.append(result)
            logger.info(
                f"Loaded result for threshold {threshold}: "
                f"NDCG={result.ndcg_score:.4f}, Reduction={result.reduction_ratio:.2%}"
            )
    
    if not results:
        raise ValueError("No threshold sweep results found to analyze")
    
    # Calculate baseline metrics
    baseline_metrics = calculate_baseline_metrics()
    
    # Determine optimal threshold
    optimal_threshold = find_optimal_threshold(
        results,
        baseline_ndcg=baseline_metrics.get('ndcg'),
        min_reduction_ratio=0.30
    )
    
    # Build summary
    summary = ThresholdSweepSummary(
        dataset=dataset_name,
        thresholds_tested=thresholds,
        results=results,
        optimal_threshold=optimal_threshold,
        baseline_ndcg=baseline_metrics.get('ndcg'),
        baseline_reduction_ratio=baseline_metrics.get('reduction_ratio'),
        baseline_wasted_call_ratio=baseline_metrics.get('wasted_call_ratio')
    )
    
    # Convert to dictionary for JSON serialization
    summary_dict = {
        'dataset': summary.dataset,
        'thresholds_tested': summary.thresholds_tested,
        'results': [asdict(r) for r in summary.results],
        'optimal_threshold': summary.optimal_threshold,
        'baseline_metrics': {
            'ndcg_score': summary.baseline_ndcg,
            'reduction_ratio': summary.baseline_reduction_ratio,
            'wasted_call_ratio': summary.baseline_wasted_call_ratio
        } if any([summary.baseline_ndcg, summary.baseline_reduction_ratio, summary.baseline_wasted_call_ratio]) else None
    }
    
    # Ensure output directory exists
    os.makedirs(os.path.dirname(output_file), exist_ok=True)
    
    # Write output
    with open(output_file, 'w') as f:
        json.dump(summary_dict, f, indent=2)
    
    logger.info(f"Threshold sweep analysis complete. Output written to {output_file}")
    logger.info(f"Optimal threshold: {optimal_threshold}")
    
    return summary_dict

def main():
    """Entry point for the threshold sweep analysis."""
    try:
        config = get_config()
        logger.info(f"Starting threshold sweep analysis with config: {config}")
        
        result = run_analysis(
            sweep_dir="data/results",
            output_file="data/results/threshold_sweep.json",
            dataset_name="nfcorpus"
        )
        
        # Print summary to stdout
        print("\n" + "="*60)
        print("THRESHOLD SWEEP ANALYSIS SUMMARY")
        print("="*60)
        print(f"Dataset: {result['dataset']}")
        print(f"Thresholds tested: {result['thresholds_tested']}")
        print(f"Optimal threshold: {result['optimal_threshold']}")
        
        if result.get('baseline_metrics'):
            print(f"\nBaseline Metrics:")
            print(f"  NDCG@10: {result['baseline_metrics'].get('ndcg_score')}")
            print(f"  Wasted Call Ratio: {result['baseline_metrics'].get('wasted_call_ratio')}")
        
        print(f"\nResults by threshold:")
        for r in result['results']:
            print(f"  Threshold {r['threshold']:.2f}: "
                  f"NDCG={r['ndcg_score']:.4f}, "
                  f"Reduction={r['reduction_ratio']:.2%}, "
                  f"Wasted={r['wasted_call_ratio']:.3f}")
        
        print("="*60 + "\n")
        
        sys.exit(0)
        
    except Exception as e:
        logger.error(f"Analysis failed: {e}", exc_info=True)
        sys.exit(1)

if __name__ == "__main__":
    main()
