import os
import sys
import logging
from pathlib import Path
from typing import Optional, Dict, Any

# Configure logging
logging.basicConfig(
    level=logging.INFO,
    format='%(asctime)s - %(name)s - %(levelname)s - %(message)s',
    handlers=[
        logging.StreamHandler(sys.stdout)
    ]
)
logger = logging.getLogger(__name__)

# Constants based on project specs
PRINCIPLE_VII_MODEL = "StarCoder-15B"
SPEC_FR001_MODEL = "CodeLlama-7B"
CONSTITUTION_AMENDMENT_FLAG = "CONST_AMEND_VII_TO_FR001"
AMENDMENT_PATH = "data/governance/constitution_amendment_vii_to_fr001.flag"

def check_constitution_amendment() -> bool:
    """
    Verifies that the Constitution has been amended to align Principle VII
    (StarCoder-15B) with Spec FR-001 (CodeLlama-7B).

    Returns:
        bool: True if the amendment is verified, False otherwise.

    Raises:
        RuntimeError: If the amendment is not found, halts execution logic.
    """
    logger.info(f"Checking Constitution Amendment for Principle VII -> {SPEC_FR001_MODEL}")
    
    # Strategy 1: Check for explicit environment variable override (for CI/CD)
    env_flag = os.getenv(CONSTITUTION_AMENDMENT_FLAG, "").lower()
    if env_flag == "true" or env_flag == "1":
        logger.info(f"Amendment verified via environment variable {CONSTITUTION_AMENDMENT_FLAG}.")
        return True

    # Strategy 2: Check for a physical flag file generated by governance process
    # This file should be created by a human or separate governance task when the amendment is ratified.
    root_dir = Path(__file__).resolve().parent.parent
    flag_file_path = root_dir / AMENDMENT_PATH

    if flag_file_path.exists():
        try:
            with open(flag_file_path, 'r') as f:
                content = f.read().strip()
            if "PRINCIPLE_VII" in content and "FR-001" in content:
                logger.info(f"Amendment verified via flag file: {flag_file_path}")
                return True
            else:
                logger.warning(f"Flag file exists but content is invalid: {content}")
        except Exception as e:
            logger.error(f"Error reading flag file: {e}")
    
    # If neither check passes, the amendment is NOT verified.
    # Per task requirement: "If not amended, halt execution and log an error."
    error_msg = (
        f"CONSTITUTION AMENDMENT REQUIRED: "
        f"Principle VII currently specifies {PRINCIPLE_VII_MODEL}, "
        f"but Spec FR-001 requires {SPEC_FR001_MODEL}. "
        f"Please ratify the amendment and create the flag file at: {flag_file_path} "
        f"or set env var {CONSTITUTION_AMENDMENT_FLAG}=1."
    )
    
    logger.error(error_msg)
    # We do not raise here to allow the main() function to handle the exit code cleanly,
    # but we return False to indicate failure to the caller.
    return False

def main():
    """
    Entry point for the governance checker script.
    Exits with code 0 if amendment is verified, 1 if not.
    """
    logger.info("Starting Governance Verification for Project PROJ-188")
    
    is_amended = check_constitution_amendment()
    
    if not is_amended:
        logger.error("Governance check FAILED. Execution halted.")
        sys.exit(1)
    
    logger.info("Governance check PASSED. Proceeding with task execution.")
    sys.exit(0)

if __name__ == "__main__":
    main()
