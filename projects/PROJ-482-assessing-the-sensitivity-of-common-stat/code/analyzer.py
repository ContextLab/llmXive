def load_simulation_results(filepath: str = "data/processed/raw_pvalues.csv") -> pd.DataFrame:
    """
    Load the raw p-values generated by the simulation engine.
    
    Args:
        filepath: Path to the raw p-values CSV file.
        
    Returns:
        DataFrame containing the simulation results.
        
    Raises:
        FileNotFoundError: If the file does not exist.
        ValueError: If the file is empty or missing required columns.
    """
    if not os.path.exists(filepath):
        raise FileNotFoundError(f"Raw p-values file not found at {filepath}. "
                                "Ensure T021b has completed successfully.")
    
    df = pd.read_csv(filepath)
    required_cols = ['sample_size', 'distribution_type', 'test_type', 'p_value', 'hypothesis_type']
    missing_cols = [c for c in required_cols if c not in df.columns]
    if missing_cols:
        raise ValueError(f"Missing required columns in {filepath}: {missing_cols}")
    
    if df.empty:
        raise ValueError(f"Raw p-values file {filepath} is empty.")
        
    logger.info(f"Loaded {len(df)} raw p-values from {filepath}")
    return df

def aggregate_results(df: pd.DataFrame) -> pd.DataFrame:
    """
    Aggregate results by sample size, distribution, and test type.
    Calculates error rates and counts.
    """
    alpha = config.SimulationConfig().alpha
    
    # Determine if null is true or false based on hypothesis_type
    # Assuming 'null_true' means the null hypothesis is true (Type I error check)
    # and 'null_false' means the alternative is true (Power check)
    df['is_reject'] = df['p_value'] < alpha
    
    if 'null_true' in df['hypothesis_type'].values:
        df['is_type_i_error'] = (df['hypothesis_type'] == 'null_true') & df['is_reject']
        agg_func = {'is_type_i_error': 'mean', 'is_reject': 'count'}
        group_cols = ['sample_size', 'distribution_type', 'test_type']
    else:
        # For power analysis (null_false), we track rejection rate (Power)
        df['is_reject_null_false'] = (df['hypothesis_type'] == 'null_false') & df['is_reject']
        agg_func = {'is_reject_null_false': 'mean', 'is_reject': 'count'}
        group_cols = ['sample_size', 'distribution_type', 'test_type']

    # Generic aggregation logic that adapts to the data present
    if 'is_type_i_error' in df.columns:
        grouped = df.groupby(group_cols).agg(
            type_i_error_rate=('is_type_i_error', 'mean'),
            count=('is_reject', 'count')
        ).reset_index()
    else:
        grouped = df.groupby(group_cols).agg(
            power=('is_reject_null_false', 'mean'),
            count=('is_reject', 'count')
        ).reset_index()
        
    return grouped

def compute_bootstrap_ci(data: List[float], n_resamples: int = 1000, confidence: float = 0.95) -> Tuple[float, float]:
    """
    Compute bootstrap confidence interval for a given metric (e.g., error rate).
    
    Args:
        data: List of binary outcomes (0 or 1) or rates.
        n_resamples: Number of bootstrap resamples.
        confidence: Confidence level (default 0.95).
        
    Returns:
        Tuple of (lower_bound, upper_bound).
    """
    data_arr = np.array(data)
    if len(data_arr) == 0:
        return (np.nan, np.nan)
        
    bootstrap_means = []
    for _ in range(n_resamples):
        resample = np.random.choice(data_arr, size=len(data_arr), replace=True)
        bootstrap_means.append(np.mean(resample))
        
    lower = np.percentile(bootstrap_means, (1 - confidence) / 2 * 100)
    upper = np.percentile(bootstrap_means, (1 + confidence) / 2 * 100)
    return (lower, upper)

