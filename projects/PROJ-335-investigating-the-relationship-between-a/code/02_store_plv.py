"""
Task T025: Store PLV metrics per participant in data/metrics/plv.csv with electrode pair identifiers.

This script loads precomputed PLV metrics (generated by code/02_extract_metrics.py),
validates the data structure, and saves them to a CSV file in the data/metrics directory.
It ensures that electrode pair identifiers are included as specified.
"""
import os
import sys
import json
import logging
import glob
from pathlib import Path
import pandas as pd
import numpy as np

# Add project root to path for imports
project_root = Path(__file__).parent.parent
sys.path.insert(0, str(project_root))

from utils.logging_config import setup_logging, get_logger
from utils.validation import exit_on_validation_failure, validate_dataframe_not_empty
from models.plv_metric import PLVMetric, PLVCollection
from code.config import load_config

def setup_logger(name: str) -> logging.Logger:
    """Setup a logger for this module."""
    return setup_logging(name, log_file="data/results/plv_store.log", console=True)

def load_plv_metrics_from_json(input_path: Path) -> PLVCollection:
    """
    Load PLV metrics from a JSON file generated by the extraction script.
    Returns a PLVCollection object.
    """
    if not input_path.exists():
        raise FileNotFoundError(f"Input PLV metrics file not found: {input_path}")

    with open(input_path, 'r') as f:
        data = json.load(f)

    # The extraction script likely outputs a list of metrics per subject
    # We need to map this to our PLVCollection model
    collection = PLVCollection(metrics=[])
    
    if "metrics" in data:
        raw_metrics = data["metrics"]
    elif isinstance(data, list):
        raw_metrics = data
    else:
        # Try to handle if it's a single object wrapped
        raw_metrics = [data]

    for item in raw_metrics:
        # Ensure we have the required fields
        subject_id = item.get("subject_id") or item.get("subject")
        if not subject_id:
            logging.warning("Skipping item without subject_id")
            continue
        
        electrode_pair = item.get("electrode_pair")
        if not electrode_pair:
            # Try to construct from individual electrodes if available
            e1 = item.get("electrode1")
            e2 = item.get("electrode2")
            if e1 and e2:
                electrode_pair = f"{e1}-{e2}"
            else:
                logging.warning(f"Skipping item for {subject_id} without electrode pair info")
                continue

        plv_value = item.get("plv_value") or item.get("plv")
        if plv_value is None:
            logging.warning(f"Skipping item for {subject_id} without PLV value")
            continue

        # Extract other potential fields
        delay_start = item.get("delay_start", 0.0)
        delay_end = item.get("delay_end", 1.0)
        condition = item.get("condition", "all")

        metric = PLVMetric(
            subject_id=subject_id,
            electrode_pair=electrode_pair,
            plv_value=float(plv_value),
            delay_start=delay_start,
            delay_end=delay_end,
            condition=condition
        )
        collection.metrics.append(metric)

    return collection

def save_plv_to_csv(collection: PLVCollection, output_path: Path) -> None:
    """
    Save the PLVCollection to a CSV file with electrode pair identifiers.
    """
    if not collection.metrics:
        raise ValueError("No PLV metrics to save. The collection is empty.")

    # Validate the collection
    validate_dataframe_not_empty(collection.to_dataframe(), "PLV metrics")

    df = collection.to_dataframe()
    
    # Ensure output directory exists
    output_path.parent.mkdir(parents=True, exist_ok=True)
    
    df.to_csv(output_path, index=False)
    logging.info(f"Successfully saved PLV metrics to {output_path}")
    logging.info(f"Saved {len(df)} records")
    
    # Log a sample of the electrode pairs
    pairs = df['electrode_pair'].unique()
    logging.info(f"Unique electrode pairs: {list(pairs)}")

def main():
    """Main entry point for T025."""
    logger = setup_logger("T025_store_plv")
    logger.info("Starting T025: Store PLV metrics per participant")

    try:
        # Load configuration
        config = load_config()
        
        # Define paths
        # We assume the extraction script (T021-T022) outputs to a specific location or format
        # Based on typical pipeline, we look for the JSON output from 02_extract_metrics
        # If 02_extract_metrics outputs directly to CSV, we might just need to move/rename.
        # However, the task says "Store ... in data/metrics/plv.csv", implying we are
        # generating that specific file.
        
        # Let's assume the extraction script produces a JSON intermediate or we need to
        # run the calculation again. Since T024 (alpha power) is done, T021/T022 (PLV extraction)
        # should have run or produced data.
        
        # Strategy: Try to find the JSON output from the extraction script.
        # Common location: data/processed/plv_metrics.json or similar.
        # If not found, we might need to re-run the extraction logic here, but that violates separation.
        # Let's assume the extraction script T021/T022 writes to `data/processed/plv_raw.json`.
        
        input_candidates = [
            project_root / "data" / "processed" / "plv_metrics.json",
            project_root / "data" / "processed" / "plv_raw.json",
            project_root / "data" / "metrics" / "plv_raw.json"
        ]
        
        input_file = None
        for candidate in input_candidates:
            if candidate.exists():
                input_file = candidate
                break
        
        if not input_file:
            # If no file found, we must assume the extraction step hasn't run or failed.
            # However, T025 depends on the data existing.
            # We will raise an error to "fail loudly" as per constraints.
            raise FileNotFoundError(
                "No precomputed PLV metrics found. "
                "Ensure T021/T022 (02_extract_metrics.py) has run successfully and output a JSON file. "
                "Searched in: " + str(input_candidates)
            )

        logger.info(f"Loading PLV metrics from {input_file}")
        collection = load_plv_metrics_from_json(input_file)
        
        if not collection.metrics:
            logger.error("No valid PLV metrics found in the input file.")
            sys.exit(1)

        output_path = project_root / "data" / "metrics" / "plv.csv"
        logger.info(f"Saving PLV metrics to {output_path}")
        
        save_plv_to_csv(collection, output_path)
        
        logger.info("T025 completed successfully.")
        
    except Exception as e:
        logger.error(f"Error during T025 execution: {e}", exc_info=True)
        sys.exit(1)

if __name__ == "__main__":
    main()
