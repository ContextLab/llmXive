"""
T018: Save preprocessed epochs to data/processed/ in HDF5/npz format.

This script loads the preprocessed EEG data generated by 01_download_preprocess.py
and saves it to the data/processed directory in a compressed format suitable
for downstream analysis.
"""
import os
import sys
import json
import logging
from pathlib import Path
import numpy as np
import h5py

# Add project root to path for imports
project_root = Path(__file__).parent.parent
sys.path.insert(0, str(project_root))

from code.utils.logging_config import get_logger, setup_logging
from code.utils.validation import validate_file_exists, exit_on_validation_failure

# Setup logging
setup_logging()
logger = get_logger(__name__)

# Configuration paths
PROCESSED_DIR = project_root / "data" / "processed"
RAW_DIR = project_root / "data" / "raw"
CONFIG_FILE = project_root / "code" / "config.yaml"

def load_config():
    """Load configuration from YAML file."""
    import yaml
    with open(CONFIG_FILE, 'r') as f:
        return yaml.safe_load(f)

def ensure_processed_directory():
    """Ensure the processed data directory exists."""
    PROCESSED_DIR.mkdir(parents=True, exist_ok=True)
    logger.info(f"Ensured processed directory exists: {PROCESSED_DIR}")

def find_preprocessed_epochs():
    """
    Locate the preprocessed epochs file generated by 01_download_preprocess.py.
    Returns the path to the file or None if not found.
    """
    # Look for the standard output file from preprocessing
    # Based on T015 implementation, epochs are saved to data/raw/processed_epochs.npz
    # or similar structure
    possible_paths = [
        RAW_DIR / "processed_epochs.npz",
        RAW_DIR / "ds000248" / "processed" / "epochs.npz",
        RAW_DIR / "ds000248" / "epochs.npz",
    ]
    
    for path in possible_paths:
        if path.exists():
            logger.info(f"Found preprocessed epochs at: {path}")
            return path
    
    # Also check for HDF5 format
    possible_h5_paths = [
        RAW_DIR / "processed_epochs.h5",
        RAW_DIR / "ds000248" / "processed" / "epochs.h5",
    ]
    
    for path in possible_h5_paths:
        if path.exists():
            logger.info(f"Found preprocessed epochs at: {path}")
            return path
    
    logger.error("Could not find preprocessed epochs file in expected locations.")
    return None

def load_epochs_data(epochs_path):
    """
    Load epochs data from the preprocessing output file.
    Returns a dictionary with the necessary data for saving.
    """
    if epochs_path.suffix == '.npz':
        data = np.load(epochs_path, allow_pickle=True)
        logger.info(f"Loaded epochs from NPZ file: {epochs_path}")
        return data
    elif epochs_path.suffix in ['.h5', '.hdf5']:
        with h5py.File(epochs_path, 'r') as f:
            data = {key: f[key][()] for key in f.keys()}
        logger.info(f"Loaded epochs from HDF5 file: {epochs_path}")
        return data
    else:
        raise ValueError(f"Unsupported file format: {epochs_path.suffix}")

def save_epochs_to_hdf5(data, output_path):
    """
    Save epochs data to HDF5 format with proper compression.
    
    Args:
        data: Dictionary containing epochs data
        output_path: Path to save the HDF5 file
    """
    with h5py.File(output_path, 'w') as f:
        for key, value in data.items():
            if isinstance(value, np.ndarray):
                # Create dataset with compression
                f.create_dataset(key, data=value, compression="gzip", compression_opts=9)
            elif isinstance(value, (int, float, str, bool)):
                f.attrs[key] = value
            else:
                # Try to convert to string representation for other types
                f.attrs[key] = str(value)
    
    logger.info(f"Saved epochs to HDF5: {output_path}")

def save_epochs_to_npz(data, output_path):
    """
    Save epochs data to NPZ format with compression.
    
    Args:
        data: Dictionary containing epochs data
        output_path: Path to save the NPZ file
    """
    # Filter out non-array items for np.savez_compressed
    array_data = {k: v for k, v in data.items() if isinstance(v, np.ndarray)}
    np.savez_compressed(output_path, **array_data)
    
    # Save metadata separately
    metadata_path = output_path.with_suffix('.json')
    metadata = {k: v for k, v in data.items() if not isinstance(v, np.ndarray)}
    with open(metadata_path, 'w') as f:
        json.dump(metadata, f, indent=2, default=str)
    
    logger.info(f"Saved epochs to NPZ: {output_path}")
    logger.info(f"Saved metadata to: {metadata_path}")

def main():
    """Main function to save preprocessed epochs."""
    logger.info("Starting T018: Save preprocessed epochs")
    
    # Load configuration
    try:
        config = load_config()
        logger.info(f"Loaded configuration from {CONFIG_FILE}")
    except Exception as e:
        logger.error(f"Failed to load configuration: {e}")
        sys.exit(1)
    
    # Ensure output directory exists
    ensure_processed_directory()
    
    # Find preprocessed epochs
    epochs_path = find_preprocessed_epochs()
    if epochs_path is None:
        logger.error("No preprocessed epochs file found. Please run 01_download_preprocess.py first.")
        sys.exit(1)
    
    # Validate that the file exists
    if not validate_file_exists(epochs_path):
        logger.error(f"Validation failed for epochs file: {epochs_path}")
        sys.exit(1)
    
    # Load epochs data
    try:
        epochs_data = load_epochs_data(epochs_path)
    except Exception as e:
        logger.error(f"Failed to load epochs data: {e}")
        sys.exit(1)
    
    # Define output filenames
    base_name = "preprocessed_epochs"
    hdf5_output = PROCESSED_DIR / f"{base_name}.h5"
    npz_output = PROCESSED_DIR / f"{base_name}.npz"
    
    # Save in both formats for compatibility
    try:
        save_epochs_to_hdf5(epochs_data, hdf5_output)
        logger.info(f"Successfully saved epochs to HDF5: {hdf5_output}")
    except Exception as e:
        logger.error(f"Failed to save epochs to HDF5: {e}")
        sys.exit(1)
    
    try:
        save_epochs_to_npz(epochs_data, npz_output)
        logger.info(f"Successfully saved epochs to NPZ: {npz_output}")
    except Exception as e:
        logger.error(f"Failed to save epochs to NPZ: {e}")
        sys.exit(1)
    
    # Log summary
    logger.info("T018 completed successfully")
    logger.info(f"Output files: {hdf5_output}, {npz_output}")
    
    # Verify output files exist
    if not hdf5_output.exists() or not npz_output.exists():
        logger.error("Output files were not created successfully")
        sys.exit(1)
    
    return 0

if __name__ == "__main__":
    sys.exit(main())