# Constants
PROJECT_ROOT = Path(__file__).resolve().parent.parent.parent
DATA_RAW_DIR = PROJECT_ROOT / "data" / "raw"
DATA_PROCESSED_DIR = PROJECT_ROOT / "data" / "processed"
OUTPUT_FILE = DATA_PROCESSED_DIR / "predictions.csv"

logger = get_logger(__name__)

def ensure_directories():
    """Ensure output directories exist."""
    DATA_PROCESSED_DIR.mkdir(parents=True, exist_ok=True)

def load_snippets_from_cache() -> List[CodeSnippet]:
    """
    Loads preprocessed snippets from the cache file generated by T012.
    If the cache doesn't exist, it triggers preprocessing.
    """
    cache_file = DATA_PROCESSED_DIR / "snippets_cache.json"
    
    if cache_file.exists():
        logger.info(f"Loading snippets from cache: {cache_file}")
        with open(cache_file, 'r', encoding='utf-8') as f:
            data = json.load(f)
        # Reconstruct CodeSnippet objects
        snippets = []
        for item in data:
            # Ensure all required fields are present, use defaults if missing
            snippet = CodeSnippet(
                id=item.get('id'),
                language=item.get('language', 'unknown'),
                source_code=item.get('source_code', ''),
                ground_truth_label=item.get('ground_truth_label'),
                ground_truth_category=item.get('ground_truth_category')
            )
            snippets.append(snippet)
        return snippets
    else:
        # If cache missing, assume preprocessing hasn't run or failed.
        # In a real pipeline, we might trigger T012 here, but T015 depends on T012.
        # We assume T012 has run and produced the cache.
        raise FileNotFoundError(
            f"Preprocessed snippets cache not found at {cache_file}. "
            "Ensure T012 (preprocess) has completed successfully."
        )

def run_inference_pipeline(
    batch_size: int = 4,
    max_batches: Optional[int] = None,
    model_name: Optional[str] = None
) -> List[PredictionResult]:
    """
    Orchestrates the inference process:
    1. Loads snippets.
    2. Checks memory constraints.
    3. Loads model.
    4. Processes snippets in batches with dynamic batch size adjustment.
    5. Returns list of PredictionResult objects.
    """
    log_stage_start("Ingest Pipeline - Inference")
    
    # 1. Load Snippets
    try:
        snippets = load_snippets_from_cache()
        logger.info(f"Loaded {len(snippets)} snippets from cache.")
    except FileNotFoundError as e:
        log_stage_failure("Ingest Pipeline - Inference", str(e))
        raise

    if not snippets:
        logger.warning("No snippets found to process.")
        return []

    # 2. Memory Check & Model Loading
    current_ram = get_available_ram_gb()
    logger.info(f"Available RAM: {current_ram:.2f} GB")
    
    if not check_memory_constraint(required_gb=2.0): # Minimum requirement
        raise MemoryError(
            f"Insufficient RAM for inference. Available: {current_ram:.2f} GB. "
            "Required minimum: 2.0 GB."
        )

    # Load model (T013 logic)
    # If model_name is not provided, the model selection logic in T004/T013 should handle it
    # or we use a default safe selection. Here we assume the config handles selection.
    config = InferenceConfig(
        model_name=model_name,
        device="cpu",
        quantization="4bit",
        batch_size=batch_size,
        max_context_length=2048
    )
    
    try:
        model, tokenizer = load_model_4bit_cpu(config)
        logger.info("Model loaded successfully.")
    except Exception as e:
        log_stage_failure("Ingest Pipeline - Model Loading", str(e))
        raise

    # 3. Process in Batches with Dynamic Adjustment
    results: List[PredictionResult] = []
    total_snippets = len(snippets)
    processed_count = 0
    start_time = time.time()
    
    current_batch_size = batch_size

    for i in range(0, total_snippets, current_batch_size):
        # Check cumulative time
        elapsed = time.time() - start_time
        if elapsed > 21600: # 6 hours limit (FR-007)
            logger.error("TIMEOUT: Cumulative runtime exceeded 6 hours. Aborting.")
            break

        # Check memory before batch
        ram_gb = get_available_ram_gb()
        if not check_memory_constraint(required_gb=1.5):
            logger.warning(f"RAM low ({ram_gb:.2f} GB). Reducing batch size.")
            current_batch_size = max(1, current_batch_size // 2)
            gc.collect()
            if current_batch_size < 1:
                raise MemoryError("Cannot reduce batch size further. Out of memory.")
            continue

        batch_snippets = snippets[i : i + current_batch_size]
        batch_end = i + current_batch_size
        
        logger.info(f"Processing batch {i//current_batch_size + 1}: snippets {i}-{min(batch_end, total_snippets)}")

        try:
            batch_results = run_inference_batch(
                model=model,
                tokenizer=tokenizer,
                snippets=batch_snippets,
                config=config
            )
            results.extend(batch_results)
            processed_count += len(batch_results)
            
            # Optional: Increase batch size if stable
            if current_batch_size < batch_size and len(batch_results) == len(batch_snippets):
                current_batch_size = min(batch_size, current_batch_size + 1)
                
        except Exception as e:
            logger.error(f"Error processing batch {i}: {e}")
            # Log error but continue with next batch if possible
            # In a strict pipeline, we might abort here.
            continue

        # Explicit garbage collection between batches
        gc.collect()

        if max_batches and (i // current_batch_size + 1) >= max_batches:
            logger.info(f"Reached max_batches limit: {max_batches}")
            break

    total_time = time.time() - start_time
    log_stage_complete("Ingest Pipeline - Inference", f"Processed {processed_count}/{total_snippets} snippets in {total_time:.2f}s")
    
    return results

def save_predictions_to_csv(results: List[PredictionResult], output_path: Path):
    """
    Saves the list of PredictionResult objects to a CSV file.
    Strictly conforms to the PredictionResult schema.
    """
    if not results:
        logger.warning("No results to save.")
        # Create empty file with headers
        with open(output_path, 'w', newline='', encoding='utf-8') as f:
            writer = csv.writer(f)
            writer.writerow([
                'snippet_id', 'predicted_label', 'predicted_category', 
                'is_correct', 'inference_time_ms'
            ])
        return

    with open(output_path, 'w', newline='', encoding='utf-8') as f:
        writer = csv.writer(f)
        # Write header based on PredictionResult dataclass fields
        writer.writerow([
            'snippet_id', 'predicted_label', 'predicted_category', 
            'is_correct', 'inference_time_ms'
        ])
        
        for res in results:
            writer.writerow([
                res.snippet_id,
                res.predicted_label,
                res.predicted_category,
                res.is_correct,
                res.inference_time_ms
            ])
    
    logger.info(f"Predictions saved to {output_path}")

def main():
    """
    Main entry point for the ingest pipeline.
    Executes the full flow: Download -> Preprocess -> Inference -> Save.
    """
    log_stage_start("Ingest Pipeline - Main")
    
    try:
        # 1. Ensure directories
        ensure_directories()

        # 2. Download Datasets (T011)
        # Note: T011 is a dependency. We call it to ensure data is present.
        # If data is already present, this should be idempotent or fast.
        logger.info("Checking/Downloading datasets...")
        # We assume download_all_datasets handles the logic of checking existence vs downloading
        # Based on T011 description: "fetch ... to data/raw/ with checksum verification"
        try:
            download_all_datasets(DATA_RAW_DIR)
        except Exception as e:
            # If download fails, we cannot proceed.
            logger.error(f"Dataset download failed: {e}")
            # Depending on strictness, we might raise. Here we log and attempt to proceed if cache exists?
            # No, T015 depends on T011. If T011 fails, T015 fails.
            raise RuntimeError("Dataset download failed. Cannot proceed with ingestion.") from e

        # 3. Preprocess Data (T012)
        # T012 produces the cache or the snippets.
        # We assume T012 logic is invoked here or the cache is expected.
        # Since T012 is a separate task, we call the parsing logic directly to ensure we have snippets.
        # However, the task description says T015 "Calls T013 functions" and depends on T011, T012.
        # To be safe and self-contained in this script, we invoke the parsing logic if cache is missing.
        cache_file = DATA_PROCESSED_DIR / "snippets_cache.json"
        if not cache_file.exists():
            logger.info("Preprocessing raw data (T012 step)...")
            # Parse raw directory
            raw_dirs = [d for d in DATA_RAW_DIR.iterdir() if d.is_dir()]
            all_snippets = []
            for raw_dir in raw_dirs:
                logger.info(f"Parsing directory: {raw_dir}")
                snippets = parse_raw_directory(raw_dir)
                all_snippets.extend(snippets)
            
            # Save to cache for future use
            snippet_dicts = [
                {
                    'id': s.id,
                    'language': s.language,
                    'source_code': s.source_code,
                    'ground_truth_label': s.ground_truth_label,
                    'ground_truth_category': s.ground_truth_category
                }
                for s in all_snippets
            ]
            with open(cache_file, 'w', encoding='utf-8') as f:
                json.dump(snippet_dicts, f, indent=2)
            logger.info(f"Preprocessed {len(all_snippets)} snippets and saved to cache.")
        else:
            logger.info("Using existing preprocessed cache.")

        # 4. Run Inference (T013)
        results = run_inference_pipeline(batch_size=4)

        # 5. Save Results
        save_predictions_to_csv(results, OUTPUT_FILE)

        logger.info("Ingest Pipeline completed successfully.")
        return 0

    except Exception as e:
        log_stage_failure("Ingest Pipeline - Main", str(e))
        return 1

if __name__ == "__main__":
    sys.exit(main())