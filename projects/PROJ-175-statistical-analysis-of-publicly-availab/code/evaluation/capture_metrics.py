"""
Metric Extraction and Final Validation Report Generation.

This module extracts metrics from the execution logs generated by the pipeline,
model fitting, and evaluation stages, and consolidates them into a single
final validation report.
"""
import os
import sys
import json
import logging
from pathlib import Path
from typing import Any, Dict, Optional

# Configure logging
logging.basicConfig(
    level=logging.INFO,
    format='%(asctime)s - %(levelname)s - %(message)s'
)
logger = logging.getLogger(__name__)

# Define base paths relative to project root
BASE_DIR = Path(__file__).resolve().parent.parent.parent
DATA_DIR = BASE_DIR / "data"
FINAL_REPORT_PATH = DATA_DIR / "final_validation_report.json"

# Input file paths
PIPELINE_LOG_PATH = DATA_DIR / "pipeline_execution_log.json"
MODEL_FITTING_LOG_PATH = DATA_DIR / "model_fitting_log.json"
EVALUATION_LOG_PATH = DATA_DIR / "evaluation_log.json"
VIF_INITIAL_PATH = DATA_DIR / "vif_scores_initial.json"
LRT_VIF_CORRECTED_PATH = DATA_DIR / "lrt_result_vif_corrected.json"
BAYESIAN_CONVERGENCE_PATH = DATA_DIR / "bayesian_convergence_log.json"
AUC_DELTA_PATH = DATA_DIR / "auc_delta_metrics.json"
CALIBRATION_PATH = DATA_DIR / "calibration_test_results.json"
VIF_TEST_SET_PATH = DATA_DIR / "vif_test_set.json"


def load_json_safe(file_path: Path) -> Optional[Dict[str, Any]]:
    """
    Safely load a JSON file. Returns None if the file does not exist or is invalid.
    
    Args:
        file_path: Path to the JSON file.
        
    Returns:
        Parsed JSON data as a dictionary, or None if loading fails.
    """
    if not file_path.exists():
        logger.warning(f"File not found: {file_path}")
        return None
    
    try:
        with open(file_path, 'r', encoding='utf-8') as f:
            return json.load(f)
    except json.JSONDecodeError as e:
        logger.error(f"Invalid JSON in {file_path}: {e}")
        return None
    except Exception as e:
        logger.error(f"Error reading {file_path}: {e}")
        return None


def extract_pipeline_metrics(log_data: Optional[Dict[str, Any]]) -> Dict[str, Any]:
    """
    Extract relevant metrics from the pipeline execution log.
    
    Args:
        log_data: The parsed JSON content of pipeline_execution_log.json.
        
    Returns:
        Dictionary containing extracted pipeline metrics.
    """
    if not log_data:
        return {
            "status": "MISSING",
            "runtime_seconds": None,
            "peak_ram_mb": None,
            "artifacts_created": []
        }
    
    return {
        "status": log_data.get("status", "UNKNOWN"),
        "runtime_seconds": log_data.get("runtime_seconds"),
        "peak_ram_mb": log_data.get("peak_ram_mb"),
        "artifacts_created": log_data.get("artifacts_created", [])
    }


def extract_logistic_metrics(log_data: Optional[Dict[str, Any]]) -> Dict[str, Any]:
    """
    Extract logistic regression specific metrics from model fitting log.
    Note: This assumes the model fitting log contains a nested structure
    for logistic results, or we rely on the specific logistic results file
    if available. For now, we extract general status and specific keys if present.
    """
    if not log_data:
        return {
            "status": "MISSING",
            "convergence": None,
            "metrics": {}
        }
    
    # Try to find specific keys that might be in the log
    status = log_data.get("logistic_status", log_data.get("status", "UNKNOWN"))
    convergence = log_data.get("logistic_convergence", log_data.get("convergence", None))
    
    # Extract specific metrics if available in the log structure
    metrics = {}
    if "logistic_results" in log_data:
        metrics = log_data["logistic_results"]
    elif "results" in log_data and "logistic" in log_data["results"]:
        metrics = log_data["results"]["logistic"]
        
    return {
        "status": status,
        "convergence": convergence,
        "metrics": metrics
    }


def extract_bayesian_metrics(log_data: Optional[Dict[str, Any]]) -> Dict[str, Any]:
    """
    Extract Bayesian model specific metrics from model fitting log.
    """
    if not log_data:
        return {
            "status": "MISSING",
            "convergence": None,
            "r_hat": None,
            "ess": None
        }
    
    status = log_data.get("bayesian_status", log_data.get("status", "UNKNOWN"))
    convergence = log_data.get("bayesian_convergence", log_data.get("convergence", None))
    
    # Extract specific convergence metrics
    r_hat = None
    ess = None
    if "bayesian_convergence_log" in log_data:
        conv_log = log_data["bayesian_convergence_log"]
        r_hat = conv_log.get("R_hat")
        ess = conv_log.get("ESS")
    elif "metrics" in log_data:
        r_hat = log_data["metrics"].get("R_hat")
        ess = log_data["metrics"].get("ESS")
        
    return {
        "status": status,
        "convergence": convergence,
        "r_hat": r_hat,
        "ess": ess
    }


def extract_vif_metrics(vif_data: Optional[Dict[str, Any]]) -> Dict[str, Any]:
    """
    Extract VIF scores from the initial VIF calculation file.
    """
    if not vif_data:
        return {
            "max_vif": None,
            "predictors": {},
            "dropped": []
        }
    
    return {
        "max_vif": vif_data.get("max_vif"),
        "predictors": vif_data.get("predictors", {}),
        "dropped": vif_data.get("dropped", [])
    }


def extract_auc_delta_metrics(auc_data: Optional[Dict[str, Any]]) -> Dict[str, Any]:
    """
    Extract AUC delta metrics including p-value and confidence intervals.
    """
    if not auc_data:
        return {
            "delta": None,
            "p_value": None,
            "ci_95": None,
            "test_method": None
        }
    
    return {
        "delta": auc_data.get("delta"),
        "p_value": auc_data.get("p_value"),
        "ci_95": auc_data.get("ci_95"),
        "test_method": auc_data.get("test_method"),
        "auc_full": auc_data.get("auc_full"),
        "auc_baseline": auc_data.get("auc_baseline")
    }


def extract_lrt_vif_corrected(lrt_data: Optional[Dict[str, Any]]) -> Dict[str, Any]:
    """
    Extract Likelihood Ratio Test results and multicollinearity flags.
    """
    if not lrt_data:
        return {
            "p_value": None,
            "statistic": None,
            "multicollinearity_flag": None,
            "final_predictors": []
        }
    
    return {
        "p_value": lrt_data.get("p_value"),
        "statistic": lrt_data.get("statistic"),
        "multicollinearity_flag": lrt_data.get("multicollinearity_flag"),
        "final_predictors": lrt_data.get("final_predictors", [])
    }


def extract_bayesian_convergence(conv_data: Optional[Dict[str, Any]]) -> Dict[str, Any]:
    """
    Extract Bayesian convergence status and details.
    """
    if not conv_data:
        return {
            "status": "MISSING",
            "R_hat": None,
            "ESS": None
        }
    
    return {
        "status": conv_data.get("status", "UNKNOWN"),
        "R_hat": conv_data.get("R_hat"),
        "ESS": conv_data.get("ESS")
    }


def extract_vif_test_set(vif_test_data: Optional[Dict[str, Any]]) -> Dict[str, Any]:
    """
    Extract VIF scores calculated on the test set for stability check.
    """
    if not vif_test_data:
        return {
            "max_vif_test": None,
            "is_stable": None
        }
    
    max_vif = vif_test_data.get("max_vif")
    is_stable = max_vif is not None and max_vif <= 5.0
    
    return {
        "max_vif_test": max_vif,
        "is_stable": is_stable,
        "predictors": vif_test_data.get("predictors", {})
    }


def extract_calibration_results(calib_data: Optional[Dict[str, Any]]) -> Dict[str, Any]:
    """
    Extract calibration test results.
    """
    if not calib_data:
        return {
            "passed": False,
            "max_deviation": None,
            "bins": None
        }
    
    return {
        "passed": calib_data.get("passed", False),
        "max_deviation": calib_data.get("max_deviation"),
        "bins": calib_data.get("bins")
    }


def generate_final_validation_report() -> Dict[str, Any]:
    """
    Aggregate all metrics from the various log files into a single report.
    
    Returns:
        Dictionary containing the complete final validation report.
    """
    logger.info("Starting metric extraction for final validation report...")
    
    # Load all source logs
    pipeline_log = load_json_safe(PIPELINE_LOG_PATH)
    model_fitting_log = load_json_safe(MODEL_FITTING_LOG_PATH)
    evaluation_log = load_json_safe(EVALUATION_LOG_PATH)
    
    # Load specific result files
    vif_initial = load_json_safe(VIF_INITIAL_PATH)
    lrt_vif_corrected = load_json_safe(LRT_VIF_CORRECTED_PATH)
    bayesian_conv = load_json_safe(BAYESIAN_CONVERGENCE_PATH)
    auc_delta = load_json_safe(AUC_DELTA_PATH)
    calibration = load_json_safe(CALIBRATION_PATH)
    vif_test = load_json_safe(VIF_TEST_SET_PATH)
    
    # Extract metrics
    pipeline_metrics = extract_pipeline_metrics(pipeline_log)
    logistic_metrics = extract_logistic_metrics(model_fitting_log)
    bayesian_metrics = extract_bayesian_metrics(model_fitting_log)
    vif_metrics = extract_vif_metrics(vif_initial)
    auc_delta_metrics = extract_auc_delta_metrics(auc_delta)
    lrt_metrics = extract_lrt_vif_corrected(lrt_vif_corrected)
    bayesian_conv_metrics = extract_bayesian_convergence(bayesian_conv)
    vif_test_metrics = extract_vif_test_set(vif_test)
    calibration_metrics = extract_calibration_results(calibration)
    
    # Determine overall success status
    # Success requires: Pipeline success, Logistic success, Bayesian success (or flagged), 
    # Evaluation success, and passing critical gates (Calibration, VIF stability)
    overall_success = (
        pipeline_metrics["status"] == "SUCCESS" and
        logistic_metrics["status"] == "SUCCESS" and
        bayesian_metrics["status"] == "SUCCESS" and
        evaluation_log is not None and evaluation_log.get("status") == "SUCCESS" and
        calibration_metrics["passed"] and
        vif_test_metrics["is_stable"]
    )
    
    # Construct final report
    report = {
        "timestamp": json.dumps({"status": "generated"}) and __import__('datetime').datetime.now().isoformat(),
        "overall_status": "SUCCESS" if overall_success else "FAILED",
        "pipeline_execution": pipeline_metrics,
        "model_fitting": {
            "logistic": logistic_metrics,
            "bayesian": bayesian_metrics
        },
        "evaluation": {
            "auc_delta": auc_delta_metrics,
            "lrt": lrt_metrics,
            "calibration": calibration_metrics
        },
        "diagnostics": {
            "vif_initial": vif_metrics,
            "vif_test_set": vif_test_metrics,
            "bayesian_convergence": bayesian_conv_metrics
        },
        "validation_checks": {
            "pipeline_ran": pipeline_metrics["status"] == "SUCCESS",
            "models_fitted": logistic_metrics["status"] == "SUCCESS" and bayesian_metrics["status"] == "SUCCESS",
            "evaluation_completed": evaluation_log is not None and evaluation_log.get("status") == "SUCCESS",
            "calibration_passed": calibration_metrics["passed"],
            "vif_stable": vif_test_metrics["is_stable"]
        }
    }
    
    return report


def main():
    """
    Main entry point for the metric capture task.
    Generates the final_validation_report.json file.
    """
    try:
        report = generate_final_validation_report()
        
        # Ensure data directory exists
        DATA_DIR.mkdir(parents=True, exist_ok=True)
        
        # Write the report
        with open(FINAL_REPORT_PATH, 'w', encoding='utf-8') as f:
            json.dump(report, f, indent=2)
        
        logger.info(f"Final validation report generated successfully at: {FINAL_REPORT_PATH}")
        logger.info(f"Overall Status: {report['overall_status']}")
        
        # Print summary to stdout for CI visibility
        print(json.dumps(report, indent=2))
        
    except Exception as e:
        logger.error(f"Failed to generate final validation report: {e}")
        raise


if __name__ == "__main__":
    main()