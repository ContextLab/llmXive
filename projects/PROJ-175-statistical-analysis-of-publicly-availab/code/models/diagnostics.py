def calculate_vif(df: pd.DataFrame, features: List[str]) -> pd.DataFrame:
    """
    Calculate Variance Inflation Factors (VIF) for a set of features.
    
    Args:
        df: DataFrame containing the features.
        features: List of column names to calculate VIF for.
        
    Returns:
        DataFrame with feature names and their VIF values.
    """
    vif_data = []
    for feature in features:
        if feature not in df.columns:
            continue
        y = df[feature]
        X = df[features]
        X = sm.add_constant(X)
        # Avoid singular matrix warnings
        with warnings.catch_warnings():
            warnings.simplefilter("ignore")
            try:
                vif = variance_inflation_factor(X.values, features.index(feature))
                vif_data.append({"feature": feature, "VIF": vif})
            except Exception:
                vif_data.append({"feature": feature, "VIF": np.inf})
    
    return pd.DataFrame(vif_data)

def drop_high_vif_predictors(df: pd.DataFrame, features: List[str], threshold: float = 5.0) -> List[str]:
    """
    Iteratively drop predictors with VIF > threshold.
    
    Returns the list of remaining features.
    """
    remaining = list(features)
    while True:
        vif_df = calculate_vif(df, remaining)
        if vif_df.empty:
            break
        max_vif_row = vif_df.loc[vif_df["VIF"].idxmax()]
        if max_vif_row["VIF"] <= threshold:
            break
        remaining.remove(max_vif_row["feature"])
    return remaining

def power_analysis_logistic_regression(effect_size: float = 0.1, alpha: float = 0.05, power: float = 0.80) -> int:
    """
    Calculate required sample size for logistic regression.
    Using approximation via GofChisquarePower for likelihood ratio tests.
    """
    # For logistic regression, we often approximate using the non-centrality parameter
    # This is a simplified approximation for the task T008a
    # In practice, we use statsmodels' GofChisquarePower for chi-square tests
    # which approximates the LRT in logistic regression.
    # Effect size w = sqrt(chi2 / N). We want N such that power is achieved.
    # w = effect_size (Cohen's w for chi-square)
    analysis = GofChisquarePower()
    # We assume a 1 degree of freedom test for the specific effect
    try:
        n_obs = analysis.solve_power(effect_size=effect_size, alpha=alpha, power=power, n_params=1)
        return int(np.ceil(n_obs))
    except Exception:
        # Fallback to a heuristic if solver fails
        return int(np.ceil(10000 * (effect_size ** -2)))

