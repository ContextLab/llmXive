def test_derive_orthogonalized_functional_role():
    # Create synthetic data for testing the regression logic
    np.random.seed(42)
    log_freq = np.random.rand(100) * 10
    raw_rank = log_freq * 2 + np.random.rand(100) * 0.5
    
    residuals = derive_orthogonalized_functional_role(log_freq, raw_rank)
    
    # Residuals should have mean close to 0
    assert np.abs(np.mean(residuals)) < 0.1
    # Residuals should not be perfectly correlated with log_freq
    corr = np.corrcoef(log_freq, residuals)[0, 1]
    assert abs(corr) < 0.1

