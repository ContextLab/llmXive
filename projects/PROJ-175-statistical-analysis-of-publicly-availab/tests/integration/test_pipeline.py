"""
Integration test for the full pre-processing pipeline (User Story 1).

This test verifies the end-to-end flow of data acquisition, normalization,
co-occurrence matrix construction, flavor similarity calculation, and
functional role derivation.

It expects the following artifacts to exist or be generated by the pipeline:
- data/raw/ (verified by T012/T013)
- data/processed/intermediate_*.csv (generated by T014-T018)
- data/final/cooccurrence_features.csv (final output)

The test asserts:
1. All intermediate and final files are created.
2. The final CSV contains the required columns (log_cooccurrence, cosine_similarity, functional_role).
3. Data types are correct (numeric features are float, role is string).
4. No NaN values exist in critical numeric columns.
"""
import os
import sys
import json
import pytest
import pandas as pd
import numpy as np
from pathlib import Path

# Add project root to path to import code modules
PROJECT_ROOT = Path(__file__).parent.parent.parent
sys.path.insert(0, str(PROJECT_ROOT / "code"))

from data.verify import verify_data_sources
from data.download import download_datasets
from data.preprocess import run_full_preprocessing
from data.split import create_train_test_split

# Constants
DATA_DIR = PROJECT_ROOT / "data"
RAW_DIR = DATA_DIR / "raw"
PROCESSED_DIR = DATA_DIR / "processed"
FINAL_DIR = DATA_DIR / "final"

REQUIRED_COLUMNS = [
    "ingredient_pair_id",
    "ingredient_a",
    "ingredient_b",
    "log_cooccurrence",
    "cosine_similarity",
    "functional_role",
    "role_missing_flag"
]

