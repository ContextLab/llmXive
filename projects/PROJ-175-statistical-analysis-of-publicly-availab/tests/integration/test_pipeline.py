@pytest.mark.integration
def test_pipeline_preprocessing_step(setup_environment):
    """
    Test T014-T018: Full preprocessing pipeline.
    This is the core integration test for User Story 1.
    It runs the entire preprocessing chain and validates the output.
    """
    output_path = FINAL_DIR / "cooccurrence_features.csv"
    
    # Ensure output does not exist before run (clean state)
    if output_path.exists():
        output_path.unlink()

    try:
        # Run the full preprocessing pipeline
        # This function should orchestrate T014 through T018
        success = run_full_preprocessing()
        
        assert success is True, "Preprocessing pipeline returned failure status"
        assert output_path.exists(), f"Final output file {output_path} was not created"
        
        # Load and validate the resulting dataframe
        df = pd.read_csv(output_path)
        
        # 1. Check required columns
        missing_cols = set(REQUIRED_COLUMNS) - set(df.columns)
        assert not missing_cols, f"Missing required columns: {missing_cols}"
        
        # 2. Check data types
        assert df['log_cooccurrence'].dtype in ['float64', 'int64', 'float32'], \
            "log_cooccurrence must be numeric"
        assert df['cosine_similarity'].dtype in ['float64', 'float32'], \
            "cosine_similarity must be numeric"
        assert df['functional_role'].dtype == 'object', \
            "functional_role must be categorical/string"
        
        # 3. Check for NaN in critical numeric columns
        assert not df['log_cooccurrence'].isna().any(), \
            "log_cooccurrence contains NaN values"
        assert not df['cosine_similarity'].isna().any(), \
            "cosine_similarity contains NaN values"
        
        # 4. Validate functional role values
        valid_roles = ['Primary', 'Secondary', 'Garnish', 'Unknown']
        actual_roles = df['functional_role'].unique()
        invalid_roles = set(actual_roles) - set(valid_roles)
        assert not invalid_roles, f"Invalid functional roles found: {invalid_roles}"
        
        # 5. Check row count (should be > 0)
        assert len(df) > 0, "Preprocessing produced an empty dataframe"
        
        print(f"Pipeline integration successful. Output shape: {df.shape}")
        
    except Exception as e:
        pytest.fail(f"Preprocessing pipeline execution failed: {e}")

@pytest.mark.integration
def test_pipeline_split_step(setup_environment):
    """
    Test T019: Train/Test split.
    Ensures the split script runs and creates stratified splits.
    """
    input_path = FINAL_DIR / "cooccurrence_features.csv"
    
    if not input_path.exists():
        pytest.skip("Input file for splitting not found. Run preprocessing first.")
    
    try:
        # Run the split function
        split_result = create_train_test_split(input_path)
        
        assert split_result is True, "Split operation returned failure"
        
        # Check for output files
        train_path = PROCESSED_DIR / "train_split.csv"
        test_path = PROCESSED_DIR / "test_split.csv"
        
        assert train_path.exists(), "Train split file not created"
        assert test_path.exists(), "Test split file not created"
        
        # Validate split sizes
        train_df = pd.read_csv(train_path)
        test_df = pd.read_csv(test_path)
        
        total_rows = len(train_df) + len(test_df)
        input_rows = len(pd.read_csv(input_path))
        
        assert total_rows == input_rows, "Split lost or duplicated rows"
        assert len(train_df) > 0 and len(test_df) > 0, "Split resulted in empty sets"
        
    except Exception as e:
        pytest.fail(f"Train/Test split failed: {e}")

@pytest.mark.integration
def test_full_pipeline_end_to_end():
    """
    High-level integration test: Run the entire pipeline from scratch.
    This combines verification, download, preprocessing, and splitting.
    """
    # This test reuses the logic of the previous tests but ensures
    # they run in the correct order without external fixture dependency
