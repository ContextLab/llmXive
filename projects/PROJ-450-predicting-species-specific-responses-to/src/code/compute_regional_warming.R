# Compute Regional Warming (Delta T)
# Task: T022
# Description: Calculate mean annual temperature change (Delta T) between
# WorldClim v2 periods (1970-2000 vs 1991-2020) over species occurrence envelopes.
# Uses an independent regional climate grid to avoid circularity (FR-006).

library(raster)
library(sf)
library(dplyr)
library(here)
library(utils)

# Source utility functions for logging and directory checks
source(here("src", "code", "utils.R"))

# --- Configuration ---
# Paths relative to project root
RAW_DATA_DIR <- here("data", "raw")
PROCESSED_DIR <- here("data", "processed")
OUTPUT_FILE <- here("data", "processed", "regional_warming.csv")
WORLDCLIM_DIR <- here("data", "raw", "worldclim_v2")

# WorldClim variable indices (Bio1 = Mean Annual Temperature)
# WorldClim v2.1 variable codes: bio1 is the first layer
TEMP_BIOCODE <- 1

# Periods
PERIOD_OLD <- "1970-2000"
PERIOD_NEW <- "1991-2020"

# --- Helper Functions ---

get_worldclim_paths <- function(period) {
  # Construct path to the directory containing the specific period rasters
  # Expected structure: data/raw/worldclim_v2/1970-2000/bio1.tif, etc.
  # Or if all in one folder with suffixes, adjust logic.
  # Based on T007, we expect rasters in data/raw/worldclim_v2/*.tif
  # We assume the directory structure:
  # data/raw/worldclim_v2/1970-2000/bio1.tif
  # data/raw/worldclim_v2/1991-2020/bio1.tif
  
  period_dir <- file.path(WORLDCLIM_DIR, period)
  if (!dir.exists(period_dir)) {
    stop(sprintf("WorldClim directory for period '%s' not found at %s. Please run T007 to download data.", period, period_dir))
  }
  
  # Find bio1 (Mean Annual Temperature)
  # WorldClim files are typically named like: wc2.1_30s_bio_1.tif
  # We look for the file containing 'bio_1' or 'bio1'
  files <- list.files(period_dir, pattern = sprintf("bio.*%s", TEMP_BIOCODE), full.names = TRUE, ignore.case = TRUE)
  
  if (length(files) == 0) {
    stop(sprintf("Temperature raster (bio1) not found in %s", period_dir))
  }
  
  # Return the first match (assuming one per period)
  return(files[1])
}

calculate_zonal_mean <- function(raster_path, species_bounds) {
  # Load the raster
  r <- raster(raster_path)
  
  # species_bounds is a data frame with columns: species, min_lat, max_lat, min_lon, max_lon
  # We process each species individually to avoid memory issues with large rasters
  
  results <- vector("list", nrow(species_bounds))
  
  for (i in seq_len(nrow(species_bounds))) {
    row <- species_bounds[i, ]
    
    # Create a bounding box extent
    ext <- extent(row$min_lon, row$max_lon, row$min_lat, row$max_lat)
    
    # Extract values within the extent
    # Using cell numbers to avoid re-projection if CRS matches
    # Assuming rasters are in WGS84 (EPSG:4326)
    cells <- cellFromWindow(r, row$min_lon, row$min_lat, row$max_lon, row$max_lat)
    
    if (length(cells) == 0) {
      warning(sprintf("No cells found for species %s in raster %s", row$species, row$species))
      results[[i]] <- list(species = row$species, mean_temp = NA_real_)
      next
    }
    
    vals <- values(r, cells = cells, na.rm = TRUE)
    
    if (length(vals) == 0 || all(is.na(vals))) {
      results[[i]] <- list(species = row$species, mean_temp = NA_real_)
    } else {
      results[[i]] <- list(species = row$species, mean_temp = mean(vals, na.rm = TRUE))
    }
  }
  
  # Convert list to data frame
  df <- do.call(rbind, lapply(results, as.data.frame))
  return(df)
}

# --- Main Execution ---

main <- function() {
  log_info("Starting T022: Compute Regional Warming")
  
  # 1. Load species occurrence envelope data
  # This data should have been generated by T021 (compute_shifts.R) or T015
  # We expect a file with min/max lat/lon per species.
  # If points_with_climate.csv exists, we aggregate it to find bounds.
  
  points_file <- here("data", "processed", "points_with_climate.csv")
  
  if (!file.exists(points_file)) {
    stop("Input file 'data/processed/points_with_climate.csv' not found. Please ensure T015b is completed.")
  }
  
  log_info(sprintf("Loading occurrence data from %s", points_file))
  occ_data <- read.csv(points_file, stringsAsFactors = FALSE)
  
  # Validate required columns
  required_cols <- c("species", "latitude", "longitude")
  if (!all(required_cols %in% names(occ_data))) {
    stop(sprintf("Input file missing required columns: %s", paste(required_cols, collapse = ", ")))
  }
  
  # Filter out NA coordinates
  occ_data <- occ_data %>%
    filter(!is.na(latitude), !is.na(longitude))
  
  if (nrow(occ_data) == 0) {
    stop("No valid occurrence records found in input file.")
  }
  
  # Aggregate to find bounding box per species
  species_bounds <- occ_data %>%
    group_by(species) %>%
    summarise(
      min_lat = min(latitude),
      max_lat = max(latitude),
      min_lon = min(longitude),
      max_lon = max(longitude),
      .groups = 'drop'
    )
  
  log_info(sprintf("Computed envelopes for %d species", nrow(species_bounds)))
  
  # 2. Load WorldClim Rasters
  log_info("Loading WorldClim rasters...")
  path_old <- get_worldclim_paths(PERIOD_OLD)
  path_new <- get_worldclim_paths(PERIOD_NEW)
  
  log_info(sprintf("Old period raster: %s", path_old))
  log_info(sprintf("New period raster: %s", path_new))
  
  # 3. Calculate Zonal Means
  log_info("Calculating zonal means for 1970-2000...")
  temps_old <- calculate_zonal_mean(path_old, species_bounds)
  colnames(temps_old)[2] <- "temp_old"
  
  log_info("Calculating zonal means for 1991-2020...")
  temps_new <- calculate_zonal_mean(path_new, species_bounds)
  colnames(temps_new)[2] <- "temp_new"
  
  # 4. Merge and Calculate Delta T
  warming_data <- merge(temps_old, temps_new, by = "species")
  
  # Calculate Delta T (New - Old)
  # Positive value indicates warming
  warming_data$delta_t <- warming_data$temp_new - warming_data$temp_old
  
  # Round to 2 decimal places
  warming_data$temp_old <- round(warming_data$temp_old, 2)
  warming_data$temp_new <- round(warming_data$temp_new, 2)
  warming_data$delta_t <- round(warming_data$delta_t, 2)
  
  # 5. Save Output
  log_info(sprintf("Saving results to %s", OUTPUT_FILE))
  
  # Ensure output directory exists
  if (!dir.exists(dirname(OUTPUT_FILE))) {
    dir.create(dirname(OUTPUT_FILE), recursive = TRUE)
  }
  
  write.csv(warming_data, OUTPUT_FILE, row.names = FALSE)
  
  log_info("T022 completed successfully.")
  log_info(sprintf("Output summary: %d species processed, %d with valid Delta T", 
                   nrow(warming_data), sum(!is.na(warming_data$delta_t))))
  
  return(invisible(TRUE))
}

# Run main
if (!interactive()) {
  main()
}
