"""
Sensitivity Analysis for P-Value Validity (Task T029)

Implements a sweep over discrete correlation thresholds (rho) to quantify
the variation in KS statistics under high-dimensional null conditions.

This script reads pre-computed p-value trajectories (generated by US1/US2),
computes the KS statistic against the uniform distribution for each rho level,
and outputs a summary report.

FR-007: Report KS variations across rho.
"""
import json
import logging
import os
import sys
from pathlib import Path
from typing import Dict, List, Any, Optional

import numpy as np
from scipy import stats

# Add project root to path for imports
PROJECT_ROOT = Path(__file__).resolve().parent.parent
sys.path.insert(0, str(PROJECT_ROOT))

from utils.simulation import SimulationConfig
from utils.exceptions import AnalysisError

# Configure logging
logging.basicConfig(
    level=logging.INFO,
    format='%(asctime)s - %(levelname)s - %(message)s',
    handlers=[
        logging.StreamHandler(sys.stdout),
        logging.FileHandler(PROJECT_ROOT / "logs" / "sensitivity_analysis.log")
    ]
)
logger = logging.getLogger(__name__)

# Constants
RHO_VALUES = [0.0, 0.1, 0.3, 0.5, 0.7, 0.9]
DATA_DIR = PROJECT_ROOT / "data" / "synthetic"
TRAJECTORIES_DIR = DATA_DIR / "trajectories"
OUTPUT_FILE = PROJECT_ROOT / "data" / "results" / "sensitivity_analysis.json"

def load_trajectories_for_rho(rho: float, n: Optional[int] = None, p: Optional[int] = None) -> List[np.ndarray]:
    """
    Load all p-value trajectories for a specific rho value (and optionally n, p).

    Args:
        rho: Correlation threshold to filter by.
        n: Optional sample size filter.
        p: Optional dimension filter.

    Returns:
        List of 1D numpy arrays, each containing p-values for one iteration.
    """
    if not TRAJECTORIES_DIR.exists():
        raise FileNotFoundError(f"Trajectories directory not found: {TRAJECTORIES_DIR}")

    trajectories = []
    found_any = False

    for file_path in TRAJECTORIES_DIR.glob("*.json"):
        try:
            with open(file_path, 'r') as f:
                data = json.load(f)

            # Filter by metadata
            file_rho = data.get('rho')
            file_n = data.get('n')
            file_p = data.get('p')

            if file_rho is None:
                continue

            # Compare floats with tolerance
            if abs(float(file_rho) - rho) > 1e-6:
                continue

            if n is not None and file_n != n:
                continue
            if p is not None and file_p != p:
                continue

            # Extract p-values
            if 'p_values' in data:
                p_vals = np.array(data['p_values'])
                trajectories.append(p_vals)
                found_any = True
            else:
                logger.warning(f"File {file_path} has no 'p_values' key, skipping.")

        except (json.JSONDecodeError, KeyError, ValueError) as e:
            logger.error(f"Error reading {file_path}: {e}")
            continue

    if not found_any:
        raise AnalysisError(f"No trajectory files found for rho={rho} (n={n}, p={p})")

    logger.info(f"Loaded {len(trajectories)} trajectories for rho={rho}")
    return trajectories

def calculate_ks_statistic_for_rho(rho: float, n: Optional[int] = None, p: Optional[int] = None) -> Dict[str, Any]:
    """
    Calculate KS statistics for all iterations at a given rho level.

    Args:
        rho: Correlation threshold.
        n: Optional sample size.
        p: Optional dimension.

    Returns:
        Dictionary with KS statistics and metadata.
    """
    try:
        trajectories = load_trajectories_for_rho(rho, n, p)
    except FileNotFoundError as e:
        logger.error(f"Missing data for rho={rho}: {e}")
        return {
            "rho": rho,
            "n": n,
            "p": p,
            "error": str(e),
            "ks_statistics": [],
            "mean_ks": None,
            "std_ks": None
        }

    ks_stats = []
    for i, p_vals in enumerate(trajectories):
        # KS test against uniform distribution [0, 1]
        ks_stat, _ = stats.kstest(p_vals, 'uniform')
        ks_stats.append(float(ks_stat))

    if not ks_stats:
        raise AnalysisError(f"No valid KS statistics computed for rho={rho}")

    return {
        "rho": rho,
        "n": n,
        "p": p,
        "ks_statistics": ks_stats,
        "mean_ks": float(np.mean(ks_stats)),
        "std_ks": float(np.std(ks_stats)),
        "min_ks": float(np.min(ks_stats)),
        "max_ks": float(np.max(ks_stats)),
        "iteration_count": len(ks_stats)
    }

def run_sensitivity_analysis(n: Optional[int] = None, p: Optional[int] = None) -> Dict[str, Any]:
    """
    Run the full sensitivity analysis sweep over rho values.

    Args:
        n: Optional fixed sample size.
        p: Optional fixed dimension.

    Returns:
        Summary dictionary with results for all rho levels.
    """
    logger.info(f"Starting sensitivity analysis sweep (n={n}, p={p})")
    results = []
    errors = []

    for rho in RHO_VALUES:
        logger.info(f"Processing rho={rho}...")
        try:
            result = calculate_ks_statistic_for_rho(rho, n, p)
            results.append(result)
            if result.get("error"):
                errors.append({"rho": rho, "error": result["error"]})
        except Exception as e:
            logger.error(f"Failed for rho={rho}: {e}")
            errors.append({"rho": rho, "error": str(e)})
            results.append({
                "rho": rho,
                "n": n,
                "p": p,
                "error": str(e),
                "ks_statistics": [],
                "mean_ks": None,
                "std_ks": None
            })

    summary = {
        "parameters": {"n": n, "p": p},
        "rho_values_tested": RHO_VALUES,
        "results": results,
        "errors": errors,
        "total_iterations_processed": sum(r.get("iteration_count", 0) for r in results if r.get("iteration_count"))
    }

    # Calculate global trend
    valid_means = [r["mean_ks"] for r in results if r.get("mean_ks") is not None]
    if valid_means:
        summary["trend_mean_ks"] = float(np.mean(valid_means))
        summary["trend_std_ks"] = float(np.std(valid_means))
    else:
        summary["trend_mean_ks"] = None
        summary["trend_std_ks"] = None

    return summary

def main():
    """Main entry point for sensitivity analysis."""
    # Ensure output directory exists
    OUTPUT_FILE.parent.mkdir(parents=True, exist_ok=True)
    (PROJECT_ROOT / "logs").mkdir(parents=True, exist_ok=True)

    # Run analysis
    # Note: If n/p are not provided, it aggregates across all available n/p for each rho
    summary = run_sensitivity_analysis()

    # Write results
    with open(OUTPUT_FILE, 'w') as f:
        json.dump(summary, f, indent=2)

    logger.info(f"Sensitivity analysis complete. Results written to {OUTPUT_FILE}")

    # Print summary to stdout
    print(f"\nSensitivity Analysis Summary:")
    print(f"  Total iterations processed: {summary['total_iterations_processed']}")
    print(f"  Rho values: {summary['rho_values_tested']}")
    print(f"  Errors encountered: {len(summary['errors'])}")
    print(f"  Global Mean KS: {summary.get('trend_mean_ks', 'N/A')}")

    if summary['results']:
        print(f"\nKS Statistics by Rho:")
        for r in summary['results']:
            if r.get('mean_ks') is not None:
                print(f"  rho={r['rho']}: Mean KS = {r['mean_ks']:.4f} (SD={r['std_ks']:.4f}, n={r['iteration_count']})")
            else:
                print(f"  rho={r['rho']}: ERROR - {r.get('error', 'Unknown')}")

    return 0

if __name__ == "__main__":
    sys.exit(main())
