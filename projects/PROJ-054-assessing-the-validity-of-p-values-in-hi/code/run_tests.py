"""
Hypothesis test execution module for high-dimensional p-value validity assessment.

This module implements the core logic for running t-tests and F-tests on synthetic
datasets generated by the simulation framework, collecting p-values to analyze
their distribution under the null hypothesis with violated assumptions.
"""
import logging
from typing import Dict, List, Tuple, Any, Optional

import numpy as np
from scipy import stats

from utils.exceptions import HypothesisTestError
from utils.regularization import regularize_covariance

logger = logging.getLogger(__name__)


def run_hypothesis_tests(
    data: np.ndarray,
    group_indices: Optional[np.ndarray] = None,
    regularization: bool = False,
    regularize_kwargs: Optional[Dict[str, Any]] = None
) -> Dict[str, np.ndarray]:
    """
    Execute t-tests and F-tests on high-dimensional data to assess p-value validity.

    This function performs hypothesis testing on each feature (column) of the input
    data matrix. For t-tests, it compares two groups (either provided via
    `group_indices` or inferred as odd/even rows). For F-tests, it compares
    variances between the same groups.

    The function is designed to handle high-dimensional data where p (features)
    may be larger than n (samples), which can lead to singular covariance matrices
    in standard implementations.

    Args:
        data: np.ndarray of shape (n_samples, n_features). The data matrix to test.
        group_indices: Optional np.ndarray of shape (n_samples,) with binary labels
            (0 or 1) indicating group membership. If None, groups are inferred
            as odd/even row indices.
        regularization: If True, apply covariance regularization for F-tests
            when the pooled covariance is singular.
        regularize_kwargs: Optional dict of kwargs to pass to regularize_covariance.

    Returns:
        Dict with keys:
            - 't_pvalues': np.ndarray of shape (n_features,) containing t-test p-values.
            - 'f_pvalues': np.ndarray of shape (n_features,) containing F-test p-values.
            - 't_statistics': np.ndarray of shape (n_features,) containing t-statistics.
            - 'f_statistics': np.ndarray of shape (n_features,) containing F-statistics.

    Raises:
        HypothesisTestError: If data dimensions are invalid, groups are unbalanced,
            or if regularization fails to produce a valid covariance matrix.
    """
    if data.ndim != 2:
        raise HypothesisTestError(
            f"Expected 2D data matrix, got {data.ndim}D array with shape {data.shape}"
        )

    n_samples, n_features = data.shape

    if n_samples < 2:
        raise HypothesisTestError(
            f"Insufficient samples for hypothesis testing: n_samples={n_samples}"
        )

    if n_features == 0:
        raise HypothesisTestError(
            "No features to test: n_features=0"
        )

    # Determine group indices if not provided
    if group_indices is None:
        group_indices = np.arange(n_samples) % 2
        logger.debug("Inferred group indices from row parity (odd/even)")
    else:
        if group_indices.shape != (n_samples,):
            raise HypothesisTestError(
                f"group_indices must be 1D array of length {n_samples}, "
                f"got shape {group_indices.shape}"
            )
        if not set(np.unique(group_indices)).issubset({0, 1}):
            raise HypothesisTestError(
                f"group_indices must contain only 0 and 1, got {np.unique(group_indices)}"
            )

    group0_mask = group_indices == 0
    group1_mask = group_indices == 1

    n0 = np.sum(group0_mask)
    n1 = np.sum(group1_mask)

    if n0 < 2 or n1 < 2:
        raise HypothesisTestError(
            f"Insufficient samples per group for hypothesis testing: "
            f"n0={n0}, n1={n1}. Each group needs at least 2 samples."
        )

    logger.info(
        f"Running hypothesis tests on {n_samples} samples ({n0} in group 0, "
        f"{n1} in group 1) across {n_features} features"
    )

    # Extract group data
    data0 = data[group0_mask, :]
    data1 = data[group1_mask, :]

    # Initialize result arrays
    t_statistics = np.zeros(n_features)
    f_statistics = np.zeros(n_features)
    t_pvalues = np.zeros(n_features)
    f_pvalues = np.zeros(n_features)

    # Perform tests for each feature
    for i in range(n_features):
        x0 = data0[:, i]
        x1 = data1[:, i]

        # T-test (Welch's t-test by default for unequal variances)
        try:
            t_stat, t_pval = stats.ttest_ind(x0, x1, equal_var=False)
            t_statistics[i] = t_stat
            t_pvalues[i] = t_pval
        except Exception as e:
            raise HypothesisTestError(
                f"t-test failed for feature {i}: {e}"
            ) from e

        # F-test for equality of variances
        # F = var1 / var0 (always positive)
        var0 = np.var(x0, ddof=1)
        var1 = np.var(x1, ddof=1)

        # Handle zero variance case
        if var0 == 0 or var1 == 0:
            # If one variance is zero, F-test is degenerate
            # Set F-statistic to infinity or 0 depending on which variance is zero
            if var0 == 0 and var1 == 0:
                f_stat = 1.0
                f_pval = 1.0
            elif var0 == 0:
                f_stat = np.inf
                f_pval = 0.0
            else:
                f_stat = 0.0
                f_pval = 0.0
        else:
            # F-statistic: ratio of larger variance to smaller
            # For two-sided test, we use the standard F-test
            f_stat = var1 / var0
            dfn = n1 - 1
            dfd = n0 - 1

            try:
                # Two-tailed p-value for F-test
                # P(F > f_stat) * 2, capped at 1.0
                p_upper = stats.f.sf(f_stat, dfn, dfd)
                f_pval = min(2.0 * p_upper, 1.0)
            except Exception as e:
                raise HypothesisTestError(
                    f"F-test p-value calculation failed for feature {i}: {e}"
                ) from e

        f_statistics[i] = f_stat
        f_pvalues[i] = f_pval

    logger.info(
        f"Completed hypothesis tests: {n_features} t-tests and {n_features} F-tests"
    )

    return {
        't_pvalues': t_pvalues,
        'f_pvalues': f_pvalues,
        't_statistics': t_statistics,
        'f_statistics': f_statistics
    }


def run_hypothesis_tests_batch(
    data_batches: List[np.ndarray],
    group_indices: Optional[np.ndarray] = None,
    regularization: bool = False,
    regularize_kwargs: Optional[Dict[str, Any]] = None
) -> List[Dict[str, np.ndarray]]:
    """
    Run hypothesis tests on a list of data batches (e.g., multiple simulation iterations).

    Args:
        data_batches: List of np.ndarray, each of shape (n_samples, n_features).
        group_indices: Optional np.ndarray for group assignment (same as run_hypothesis_tests).
        regularization: If True, apply covariance regularization.
        regularize_kwargs: Optional kwargs for regularization.

    Returns:
        List of result dictionaries, one per batch.
    """
    results = []
    for i, batch in enumerate(data_batches):
        logger.debug(f"Processing batch {i+1}/{len(data_batches)}")
        result = run_hypothesis_tests(
            batch,
            group_indices=group_indices,
            regularization=regularization,
            regularize_kwargs=regularize_kwargs
        )
        results.append(result)

    return results


def main():
    """
    Entry point for standalone execution.

    This function demonstrates the usage of run_hypothesis_tests by:
    1. Generating a small synthetic dataset
    2. Running t-tests and F-tests on it
    3. Printing the results

    Note: For actual research, this should be integrated with the simulation
    orchestrator (SimulationOrchestrator) to run tests on generated datasets.
    """
    logging.basicConfig(
        level=logging.INFO,
        format='%(asctime)s - %(name)s - %(levelname)s - %(message)s'
    )

    # Generate a small synthetic dataset for demonstration
    np.random.seed(42)
    n_samples = 100
    n_features = 50

    # Create data with known structure (null hypothesis true)
    # Both groups come from the same distribution
    group_indices = np.random.randint(0, 2, n_samples)
    data = np.random.normal(loc=0, scale=1, size=(n_samples, n_features))

    logger.info(f"Generated synthetic data: {data.shape}")

    # Run hypothesis tests
    results = run_hypothesis_tests(data, group_indices=group_indices)

    # Print summary statistics
    logger.info("T-test results:")
    logger.info(f"  Mean t-statistic: {np.mean(results['t_statistics']):.4f}")
    logger.info(f"  Std t-statistic: {np.std(results['t_statistics']):.4f}")
    logger.info(f"  Mean p-value: {np.mean(results['t_pvalues']):.4f}")
    logger.info(f"  Proportion p < 0.05: {np.mean(results['t_pvalues'] < 0.05):.4f}")

    logger.info("F-test results:")
    logger.info(f"  Mean F-statistic: {np.mean(results['f_statistics']):.4f}")
    logger.info(f"  Std F-statistic: {np.std(results['f_statistics']):.4f}")
    logger.info(f"  Mean p-value: {np.mean(results['f_pvalues']):.4f}")
    logger.info(f"  Proportion p < 0.05: {np.mean(results['f_pvalues'] < 0.05):.4f}")

    # Verify uniformity of p-values under null (should be roughly uniform)
    # For a true null, ~5% of p-values should be < 0.05
    expected_alpha = 0.05
    observed_t_alpha = np.mean(results['t_pvalues'] < expected_alpha)
    observed_f_alpha = np.mean(results['f_pvalues'] < expected_alpha)

    logger.info(f"Expected proportion p < {expected_alpha}: {expected_alpha:.4f}")
    logger.info(f"Observed proportion (t-test) p < {expected_alpha}: {observed_t_alpha:.4f}")
    logger.info(f"Observed proportion (F-test) p < {expected_alpha}: {observed_f_alpha:.4f}")

    return results


if __name__ == '__main__':
    main()
