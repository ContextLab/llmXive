class TestHypothesisTests:
    """Tests for hypothesis test execution on null data."""

    def test_ttest_execution_on_null_data(self):
        """Test that independent t-test executes correctly on null data.
        
        Under the null hypothesis (no mean difference), the t-test should
        execute without errors and return a valid p-value.
        """
        # Generate correlated null data (no mean difference between groups)
        n, p = 100, 50
        rho = 0.3
        
        # Generate two groups with identical distribution (null hypothesis true)
        group1 = generate_correlated_data(n, p, rho)
        group2 = generate_correlated_data(n, p, rho)
        
        # Perform t-test for each feature (column)
        p_values = []
        for i in range(p):
            t_stat, p_val = stats.ttest_ind(group1[:, i], group2[:, i])
            p_values.append(p_val)
        
        # Verify all p-values are valid floats in [0, 1]
        assert len(p_values) == p
        assert all(0.0 <= pv <= 1.0 for pv in p_values)
        assert all(not np.isnan(pv) for pv in p_values)
        assert all(not np.isinf(pv) for pv in p_values)
    
    def test_ftest_execution_on_null_data(self):
        """Test that F-test (variance ratio) executes correctly on null data.
        
        Under the null hypothesis (equal variances), the F-test should
        execute without errors and return a valid p-value.
        """
        # Generate correlated null data
        n, p = 100, 50
        rho = 0.3
        
        # Generate two groups with identical distribution (null hypothesis true)
        group1 = generate_correlated_data(n, p, rho)
        group2 = generate_correlated_data(n, p, rho)
        
        # Perform F-test for each feature (column)
        p_values = []
        for i in range(p):
            var1 = np.var(group1[:, i], ddof=1)
            var2 = np.var(group2[:, i], ddof=1)
            
            # F-statistic: ratio of variances
            if var2 > 0:
                f_stat = var1 / var2
                # Two-tailed F-test p-value
                df1 = n - 1
                df2 = n - 1
                p_val = 2 * min(
                    stats.f.cdf(f_stat, df1, df2),
                    1 - stats.f.cdf(f_stat, df1, df2)
                )
                p_values.append(p_val)
            else:
                # Handle edge case of zero variance
                p_values.append(1.0)
        
        # Verify all p-values are valid floats in [0, 1]
        assert len(p_values) == p
        assert all(0.0 <= pv <= 1.0 for pv in p_values)
        assert all(not np.isnan(pv) for pv in p_values)
        assert all(not np.isinf(pv) for pv in p_values)
    
