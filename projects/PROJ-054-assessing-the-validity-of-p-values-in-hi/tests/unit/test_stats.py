"""
Unit tests for t-test and F-test execution on null data.

This module verifies that hypothesis tests (t-test and F-test) execute correctly
on synthetic null data generated by the project's data generation pipeline.
The tests ensure no runtime errors occur and that p-values are properly computed.
"""

import numpy as np
