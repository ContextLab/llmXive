    def test_pvalue_distribution_under_null(self):
        """Test that p-values are approximately uniformly distributed under true null.
        
        This is a statistical validation: under the true null hypothesis,
        p-values should follow a Uniform(0,1) distribution. We verify this
        using the Kolmogorov-Smirnov test.
        """
        n, p = 200, 100  # Larger sample for better statistical power
        rho = 0.0  # No correlation for cleaner null
        
        # Generate many independent datasets to collect p-values
        num_iterations = 50
        all_p_values = []
        
        for _ in range(num_iterations):
            group1 = generate_correlated_data(n, p, rho)
            group2 = generate_correlated_data(n, p, rho)
            
            # Collect p-value for first feature
            _, p_val = stats.ttest_ind(group1[:, 0], group2[:, 0])
            all_p_values.append(p_val)
        
        # Test uniformity using KS test
        ks_stat, ks_pval = stats.kstest(all_p_values, 'uniform')
        
        # Under true null, KS p-value should be > 0.05 (fail to reject uniformity)
        # Note: This is a statistical test, so we allow some variability
        # but it should generally pass most of the time
        assert ks_pval > 0.01, f"P-values not uniformly distributed: KS p-value={ks_pval}"
    
