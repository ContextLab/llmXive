"""
Script to execute covariate collection for ANCOVA adjustment.
This script serves as the entry point for T021c.

It reads the list of candidate repositories from data/raw/repo_selection_rubric.json
(generated by T021b), extracts the valid paths, and generates
data/raw/repo_covariates.json.
"""

import json
import os
import sys

# Ensure we can import from the code directory
script_dir = os.path.dirname(os.path.abspath(__file__))
if script_dir not in sys.path:
    sys.path.insert(0, script_dir)

from validation import generate_covariates_json

def main():
    rubric_path = "data/raw/repo_selection_rubric.json"
    output_path = "data/raw/repo_covariates.json"
    
    if not os.path.exists(rubric_path):
        print(f"Error: Rubric file not found at {rubric_path}")
        print("Please ensure T021b has been completed successfully.")
        sys.exit(1)
    
    # Load rubric results to get candidate paths
    try:
        with open(rubric_path, 'r', encoding='utf-8') as f:
            rubric_data = json.load(f)
    except (json.JSONDecodeError, IOError) as e:
        print(f"Error reading rubric file: {e}")
        sys.exit(1)
    
    # Extract paths from rubric results
    # The structure depends on T021b output, assuming a list of dicts with 'path' or 'repository'
    repo_paths = []
    
    if isinstance(rubric_data, list):
        for item in rubric_data:
            if isinstance(item, dict):
                path = item.get("path") or item.get("repository")
                if path and os.path.isdir(path):
                    repo_paths.append(path)
    elif isinstance(rubric_data, dict) and "results" in rubric_data:
        for item in rubric_data["results"]:
            path = item.get("path") or item.get("repository")
            if path and os.path.isdir(path):
                repo_paths.append(path)
    
    if not repo_paths:
        print("No valid repository paths found in rubric file.")
        print("Ensure the rubric file contains 'path' or 'repository' keys with valid directories.")
        sys.exit(1)
    
    print(f"Found {len(repo_paths)} valid repositories.")
    print(f"Collecting covariates for ANCOVA adjustment...")
    
    generate_covariates_json(repo_paths, output_path)
    print(f"Successfully wrote covariates to {output_path}")

if __name__ == "__main__":
    main()