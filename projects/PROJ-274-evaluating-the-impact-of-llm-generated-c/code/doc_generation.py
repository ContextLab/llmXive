def ensure_dirs():
    """Ensure required directories exist."""
    dirs = [
        'data/raw/llm_docs',
        'data/processed',
        'data/reports',
        'logs'
    ]
    for d in dirs:
        os.makedirs(d, exist_ok=True)
        logger.debug(f"Ensured directory: {d}")

def calculate_checksum(content: str) -> str:
    """Calculate SHA-256 checksum of content."""
    if isinstance(content, str):
        content_bytes = content.encode('utf-8')
    else:
        content_bytes = content
    return hashlib.sha256(content_bytes).hexdigest()

def update_checksum_file(checksum: str, artifact_name: str, checksum_file: str = 'data/checksums.txt'):
    """Append a checksum record to the checksum file."""
    os.makedirs(os.path.dirname(checksum_file), exist_ok=True)
    timestamp = datetime.utcnow().isoformat()
    record = f"{timestamp} | {artifact_name} | {checksum}\n"
    with open(checksum_file, 'a', encoding='utf-8') as f:
        f.write(record)
    logger.info(f"Appended checksum for {artifact_name} to {checksum_file}")

def load_llama_model(model_path: str = None):
    """
    Load a quantized Llama model using llama-cpp-python.
    Falls back to environment variable HF_COMMIT_HASH if model_path is not provided.
    """
    try:
        from llama_cpp import Llama
    except ImportError:
        logger.error("llama-cpp-python not installed. Please install it via requirements.txt.")
        raise

    if model_path is None:
        model_path = os.getenv('LLAMA_MODEL_PATH', 'models/llama-2-7b-chat.Q4_K_M.gguf')
    
    if not os.path.exists(model_path):
        raise FileNotFoundError(f"Model file not found at {model_path}. Please download the quantized model.")

    logger.info(f"Loading model from {model_path}...")
    model = Llama(
        model_path=model_path,
        n_ctx=4096,
        n_threads=4,
        verbose=False
    )
    logger.info("Model loaded successfully.")
    return model

def generate_with_llm(model, prompt: str, max_tokens: int = 2048, temperature: float = 0.7) -> str:
    """Generate documentation using the loaded LLM."""
    logger.info("Starting LLM generation...")
    start_time = time.time()
    
    try:
        response = model(
            prompt,
            max_tokens=max_tokens,
            temperature=temperature,
            stop=["###", "```"],
            echo=False
        )
        generated_text = response['choices'][0]['text'].strip()
        elapsed = time.time() - start_time
        logger.info(f"Generation completed in {elapsed:.2f}s. Tokens generated: {len(generated_text)}")
        return generated_text
    except Exception as e:
        logger.error(f"LLM generation failed: {e}")
        raise

def generate_documentation_fallback(code_context: str, repo_name: str, output_dir: str) -> str:
    """
    Fallback generation if primary LLM fails or is unavailable.
    Uses a simple heuristic-based approach to generate basic Markdown structure.
    """
    logger.warning("Using fallback documentation generation.")
    
    # Basic heuristic: Extract function definitions and create stub docs
    import re
    functions = re.findall(r'def\s+(\w+)\s*\((.*?)\):', code_context)
    
    md_content = f"# Documentation for {repo_name}\n\n"
    md_content += "## Auto-generated Fallback\n\n"
    md_content += "This documentation was generated by a fallback heuristic due to LLM unavailability.\n\n"
    
    md_content += "### Detected Functions\n\n"
    for func_name, args in functions[:20]: # Limit to first 20 to avoid huge output
        md_content += f"- `{func_name}({args})`\n"
    
    md_content += "\n## Architecture Overview\n\n"
    md_content += "No architecture analysis performed in fallback mode.\n"
    
    md_content += "\n## Setup Instructions\n\n"
    md_content += "No setup instructions available in fallback mode.\n"
    
    return md_content

def log_config_and_checksum(model_name: str, temperature: float, prompt_hash: str, doc_content: str, artifact_name: str):
    """Log generation config and save checksum."""
    config = {
        "model": model_name,
        "temperature": temperature,
        "prompt_hash": prompt_hash,
        "timestamp": datetime.utcnow().isoformat()
    }
    
    config_path = 'data/llm_config.yaml'
    # Simple YAML-like logging since pyyaml might not be strictly needed for just appending
    # But we will try to use yaml if available, otherwise manual formatting
    try:
        import yaml
        with open(config_path, 'a', encoding='utf-8') as f:
            yaml.dump(config, f, default_flow_style=False)
            f.write("---\n")
    except ImportError:
        with open(config_path, 'a', encoding='utf-8') as f:
            f.write(f"model: {model_name}\n")
            f.write(f"temperature: {temperature}\n")
            f.write(f"prompt_hash: {prompt_hash}\n")
            f.write(f"timestamp: {config['timestamp']}\n")
            f.write("---\n")
    
    checksum = calculate_checksum(doc_content)
    update_checksum_file(checksum, artifact_name)
    logger.info(f"Logged config and checksum for {artifact_name}")
    return checksum

def save_generated_docs(doc_content: str, repo_name: str, version: str, output_dir: str = 'data/raw/llm_docs'):
    """
    Save generated Markdown documentation to file with checksum.
    
    Args:
        doc_content: The generated Markdown content
        repo_name: Name of the repository (sanitized for filename)
        version: Version string or commit hash
        output_dir: Directory to save the file
    """
    ensure_dirs()
    
    # Sanitize repo name for filename
    safe_name = "".join(c for c in repo_name if c.isalnum() or c in ('-', '_')).rstrip()
    filename = f"{safe_name}_{version}.md"
    filepath = os.path.join(output_dir, filename)
    
    with open(filepath, 'w', encoding='utf-8') as f:
        f.write(doc_content)
    
    logger.info(f"Saved documentation to {filepath}")
    return filepath

def main():
    """
    Main entry point for the documentation generation task.
    This script expects environment variables or a config file to define:
    - REPO_NAME: The name of the repository
    - REPO_VERSION: The commit hash or version tag
    - CODE_CONTEXT: The code content to document (or a path to it)
    - OUTPUT_DIR: Where to save the docs
    
    For this specific task (T031), we assume the generation logic (T027/T028) 
    has already produced the content in memory or a temporary buffer. 
    This function focuses on saving that content to `data/raw/llm_docs/` 
    and generating the checksum.
    
    To test:
    python code/doc_generation.py --repo_name "test-repo" --version "abc123" --input "path/to/content.md"
    """
    import argparse

    parser = argparse.ArgumentParser(description="Save generated LLM docs and checksums")
    parser.add_argument('--repo_name', type=str, required=True, help="Repository name")
    parser.add_argument('--version', type=str, required=True, help="Version or commit hash")
    parser.add_argument('--input', type=str, required=True, help="Path to generated Markdown file or content string")
    parser.add_argument('--output_dir', type=str, default='data/raw/llm_docs', help="Output directory")
    parser.add_argument('--model_name', type=str, default='llama-fallback', help="Model name for logging")
    parser.add_argument('--temperature', type=float, default=0.7, help="Temperature used")
    
    args = parser.parse_args()

    ensure_dirs()

    # Read content
    doc_content = ""
    if os.path.exists(args.input):
        with open(args.input, 'r', encoding='utf-8') as f:
            doc_content = f.read()
    else:
        # Assume input is the content string itself if file not found
        doc_content = args.input

    if not doc_content.strip():
        logger.error("No content provided to save.")
        sys.exit(1)

    # Save to file
    filepath = save_generated_docs(doc_content, args.repo_name, args.version, args.output_dir)
    
    # Calculate and log checksum
    artifact_name = f"llm_docs_{args.repo_name}_{args.version}"
    checksum = calculate_checksum(doc_content)
    update_checksum_file(checksum, artifact_name)
    
    # Log config
    prompt_hash = calculate_checksum(doc_content[:1000]) # Hash first 1k chars as representative
    log_config_and_checksum(args.model_name, args.temperature, prompt_hash, doc_content, artifact_name)

    print(f"Successfully saved documentation to {filepath}")
    print(f"Checksum: {checksum}")

if __name__ == "__main__":
    main()