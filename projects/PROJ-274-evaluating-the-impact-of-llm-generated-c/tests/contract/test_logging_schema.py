"""
Contract test for the data logging schema used in User Story 1.

This test verifies that the participant logs generated by the data collection
pipeline conform to the expected schema defined in the project specifications.

It validates:
1. Top-level structure (list of participant records).
2. Required fields per record (participant_id, condition, timestamps, metrics).
3. Data types for specific fields (e.g., timestamps as ISO strings, times as numbers).
4. Valid enumeration values for 'condition'.
5. Presence of derived metrics (cognitive_load_proxy) if applicable.
"""

import json
import os
import pytest
from datetime import datetime

# Path constants relative to project root
PROJECT_ROOT = os.path.dirname(os.path.dirname(os.path.dirname(os.path.abspath(__file__))))
DATA_DIR = os.path.join(PROJECT_ROOT, "data", "raw")
LOG_FILE = os.path.join(DATA_DIR, "participant_logs.json")

# Expected schema definition
REQUIRED_TOP_LEVEL_FIELDS = [
    "participant_id",
    "condition",
    "session_start",
    "session_end",
    "total_time_seconds",
    "clarification_questions",
    "cognitive_load_proxy",
    "helpfulness_score",
    "intervention_flag",
    "status"
]

VALID_CONDITIONS = ["llm_generated", "human_written", "none"]

def load_sample_data():
    """
    Loads the real participant logs from disk.
    If the file does not exist or is empty, this test is skipped (not failed)
    because the data generation phase (T020) has not run yet.
    """
    if not os.path.exists(LOG_FILE):
        pytest.skip(f"Data file {LOG_FILE} not found. Run data collection first.")
    
    with open(LOG_FILE, 'r', encoding='utf-8') as f:
        try:
            data = json.load(f)
            if not isinstance(data, list) or len(data) == 0:
                pytest.skip(f"Data file {LOG_FILE} is empty or not a list.")
            return data
        except json.JSONDecodeError:
            pytest.fail(f"Data file {LOG_FILE} is not valid JSON.")

def test_schema_structure(data):
    """Verify that the data is a list of dictionaries."""
    assert isinstance(data, list), "Top-level data must be a list."
    assert len(data) > 0, "Data list must not be empty."
    
    for i, record in enumerate(data):
        assert isinstance(record, dict), f"Record at index {i} must be a dictionary."

def test_required_fields_present(data):
    """Verify all required fields exist in every record."""
    for i, record in enumerate(data):
        missing = [field for field in REQUIRED_TOP_LEVEL_FIELDS if field not in record]
        assert not missing, f"Record at index {i} is missing fields: {missing}"

def test_condition_values(data):
    """Verify that 'condition' is one of the valid enums."""
    for i, record in enumerate(data):
        condition = record.get("condition")
        assert condition in VALID_CONDITIONS, (
            f"Record at index {i} has invalid condition '{condition}'. "
            f"Must be one of {VALID_CONDITIONS}."
        )

def test_timestamp_format(data):
    """Verify that start/end timestamps are valid ISO 8601 strings."""
    for i, record in enumerate(data):
        start = record.get("session_start")
        end = record.get("session_end")
        
        # Basic ISO format check (YYYY-MM-DDTHH:MM:SS)
        try:
            if start:
                datetime.fromisoformat(start.replace('Z', '+00:00'))
            if end:
                datetime.fromisoformat(end.replace('Z', '+00:00'))
        except ValueError:
            pytest.fail(f"Record at index {i} has invalid timestamp format.")

def test_numeric_fields(data):
    """Verify that time and score fields are numeric."""
    for i, record in enumerate(data):
        # Total time should be a number
        total_time = record.get("total_time_seconds")
        assert isinstance(total_time, (int, float)), (
            f"Record at index {i}: 'total_time_seconds' must be numeric, got {type(total_time)}"
        )
        
        # Cognitive load proxy should be numeric
        cl_proxy = record.get("cognitive_load_proxy")
        assert isinstance(cl_proxy, (int, float)), (
            f"Record at index {i}: 'cognitive_load_proxy' must be numeric, got {type(cl_proxy)}"
        )
        
        # Helpfulness score should be numeric (if present)
        if "helpfulness_score" in record:
            assert isinstance(record["helpfulness_score"], (int, float)), (
                f"Record at index {i}: 'helpfulness_score' must be numeric"
            )

def test_status_values(data):
    """Verify that 'status' is valid (completed, failed, abandoned)."""
    valid_statuses = ["completed", "failed", "abandoned"]
    for i, record in enumerate(data):
        status = record.get("status")
        assert status in valid_statuses, (
            f"Record at index {i} has invalid status '{status}'. "
            f"Must be one of {valid_statuses}."
        )

def test_intervention_flag_type(data):
    """Verify that intervention_flag is a boolean."""
    for i, record in enumerate(data):
        flag = record.get("intervention_flag")
        # Flag might be missing if no intervention occurred, but if present must be bool
        if flag is not None:
            assert isinstance(flag, bool), (
                f"Record at index {i}: 'intervention_flag' must be boolean, got {type(flag)}"
            )

# Pytest fixture to load data once for all tests
@pytest.fixture(scope="module")
def data():
    return load_sample_data()