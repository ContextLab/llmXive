"""
Contract test for analysis output schema (Task T034).

This test verifies that the statistical analysis pipeline produces outputs
that strictly adhere to the pre-defined JSON schemas defined in
contracts/analysis_output.schema.yaml.

It ensures that the keys, data types, and required fields for:
- Welch's ANOVA results
- Games-Howell post-hoc tests
- ANCOVA results
- Sensitivity Analysis
- Final Aggregated Report
are present and correctly typed before the results are consumed by the reporting engine.
"""

import json
import os
import pytest
import yaml
from typing import Any, Dict, List, Union

# Path constants relative to project root
PROJECT_ROOT = os.path.abspath(os.path.join(os.path.dirname(__file__), '..', '..'))
CONTRACTS_DIR = os.path.join(PROJECT_ROOT, 'contracts')
SCHEMA_PATH = os.path.join(CONTRACTS_DIR, 'analysis_output.schema.yaml')

# Expected output paths (these would be generated by the analysis runner)
EXPECTED_OUTPUTS = {
    'welch': os.path.join(PROJECT_ROOT, 'data', 'reports', 'welch_results.json'),
    'posthoc': os.path.join(PROJECT_ROOT, 'data', 'reports', 'welch_posthoc.json'),
    'ancova': os.path.join(PROJECT_ROOT, 'data', 'reports', 'ancova_results.json'),
    'sensitivity': os.path.join(PROJECT_ROOT, 'data', 'reports', 'sensitivity_analysis.json'),
    'final': os.path.join(PROJECT_ROOT, 'data', 'reports', 'analysis_results.json'),
}

def load_schema() -> Dict[str, Any]:
    """Load the analysis output schema from the YAML contract file."""
    if not os.path.exists(SCHEMA_PATH):
        # If schema doesn't exist yet, we cannot validate, but the test 
        # should ideally fail if the schema is missing during CI.
        # For this contract test, we assume the schema file exists.
        pytest.fail(f"Schema file not found at {SCHEMA_PATH}")
    
    with open(SCHEMA_PATH, 'r') as f:
        return yaml.safe_load(f)

def validate_type(value: Any, expected_type: str) -> bool:
    """Check if a value matches the expected type string (e.g., 'number', 'string', 'boolean', 'array')."""
    type_map = {
        'number': (int, float),
        'string': str,
        'boolean': bool,
        'array': list,
        'object': dict,
        'null': type(None)
    }
    
    expected_python_type = type_map.get(expected_type)
    if expected_python_type is None:
        # Handle complex types or allow any if not specified
        return True
    
    return isinstance(value, expected_python_type)

def validate_structure(data: Dict[str, Any], schema: Dict[str, Any], path: str = "") -> List[str]:
    """
    Recursively validate data against the schema.
    Returns a list of validation error messages.
    """
    errors = []
    
    # Check required fields
    required_fields = schema.get('required', [])
    for field in required_fields:
        if field not in data:
            errors.append(f"Missing required field: {path}.{field}")
    
    # Validate properties
    properties = schema.get('properties', {})
    for key, value in data.items():
        current_path = f"{path}.{key}" if path else key
        
        if key in properties:
            prop_schema = properties[key]
            
            # Type check
            if 'type' in prop_schema:
                if not validate_type(value, prop_schema['type']):
                    errors.append(f"Type mismatch at {current_path}: expected {prop_schema['type']}, got {type(value).__name__}")
            
            # Nested object check
            if prop_schema.get('type') == 'object' and isinstance(value, dict):
                nested_errors = validate_structure(value, prop_schema, current_path)
                errors.extend(nested_errors)
            
            # Array check
            if prop_schema.get('type') == 'array' and isinstance(value, list):
                items_schema = prop_schema.get('items', {})
                if items_schema:
                    for idx, item in enumerate(value):
                        if isinstance(item, dict):
                            item_errors = validate_structure(item, items_schema, f"{current_path}[{idx}]")
                            errors.extend(item_errors)
        else:
            # Optional warning for unexpected fields? 
            # Strict contract tests might fail on extra fields, but usually we allow extensions.
            pass
    
    return errors

def load_json_file(filepath: str) -> Dict[str, Any]:
    """Load a JSON file from disk."""
    if not os.path.exists(filepath):
        pytest.fail(f"Output file not found: {filepath}")
    
    with open(filepath, 'r') as f:
        return json.load(f)

class TestAnalysisOutputSchema:
    """
    Contract tests ensuring analysis outputs match the specification.
    """

    @pytest.fixture(autouse=True)
    def setup(self):
        """Ensure the schema exists before tests run."""
        self.schema = load_schema()

    def test_welch_anova_schema(self):
        """Verify welch_results.json matches the Welch's ANOVA schema."""
        if not os.path.exists(EXPECTED_OUTPUTS['welch']):
            pytest.skip(f"File {EXPECTED_OUTPUTS['welch']} not generated yet.")
        
        data = load_json_file(EXPECTED_OUTPUTS['welch'])
        schema = self.schema.get('welch_anova', {})
        errors = validate_structure(data, schema, 'welch_anova')
        
        assert not errors, f"Welch's ANOVA schema validation failed:\n" + "\n".join(errors)

