"""
Report generator for the Network Topology Energy Transfer study.

This module generates a comprehensive text report framing findings as associational
(per ROC-001) and documenting p-values and effect sizes derived from the analysis pipeline.
"""

import json
import logging
from datetime import datetime
from pathlib import Path
from typing import Any, Dict, List, Optional

from code.src.utils.reproducibility import ensure_data_directory
from code.src.analysis.regression import RegressionResult
from code.src.analysis.anova import run_anova_on_diffusion_by_topology

logger = logging.getLogger(__name__)

REPORT_OUTPUT_PATH = "data/analysis/final_report.txt"

def _load_regression_results(path: str) -> List[Dict[str, Any]]:
    """Load regression results from the JSON file generated by run_analysis.py."""
    p = Path(path)
    if not p.exists():
        logger.warning(f"Regression results file not found at {path}. Skipping regression section.")
        return []
    with open(p, 'r', encoding='utf-8') as f:
        data = json.load(f)
    # Expecting a list or a dict with a 'regression_results' key
    if isinstance(data, list):
        return data
    return data.get('regression_results', [])

def _load_anova_results(path: str) -> Dict[str, Any]:
    """Load ANOVA results if available."""
    # The run_analysis.py typically aggregates ANOVA into final_results.json
    # We attempt to load it from there, or a specific ANOVA file if split
    final_path = Path("data/analysis/final_results.json")
    if final_path.exists():
        with open(final_path, 'r', encoding='utf-8') as f:
            data = json.load(f)
            return data.get('anova_results', {})
    return {}

def _load_simulation_summary(path: str) -> Dict[str, Any]:
    """Load simulation summary statistics."""
    p = Path(path)
    if not p.exists():
        return {}
    with open(p, 'r', encoding='utf-8') as f:
        return json.load(f)

def _format_regression_section(results: List[Dict[str, Any]]) -> str:
    """Format the regression analysis section of the report."""
    lines = []
    lines.append("=" * 60)
    lines.append("REGRESSION ANALYSIS RESULTS")
    lines.append("=" * 60)
    lines.append("")
    
    if not results:
        lines.append("No regression results available.")
        lines.append("")
        return "\n".join(lines)

    for i, res in enumerate(results):
        lines.append(f"Model {i+1}: {res.get('model_type', 'Unknown')}")
        lines.append("-" * 40)
        
        # Coefficients
        coeffs = res.get('coefficients', {})
        if coeffs:
            lines.append("Coefficients:")
            for var, val in coeffs.items():
                lines.append(f"  {var}: {val:.4f}")
        
        # Statistics
        r_squared = res.get('r_squared')
        p_value = res.get('p_value')
        f_stat = res.get('f_statistic')
        
        if r_squared is not None:
            lines.append(f"R-squared: {r_squared:.4f}")
        if f_stat is not None:
            lines.append(f"F-statistic: {f_stat:.4f}")
        if p_value is not None:
            lines.append(f"P-value: {p_value:.4e}")
            if p_value < 0.05:
                lines.append("  -> Statistically significant (p < 0.05)")
            else:
                lines.append("  -> Not statistically significant (p >= 0.05)")
        
        # Effect Size (Cohen's d or similar if available)
        effect_size = res.get('effect_size')
        if effect_size is not None:
            lines.append(f"Effect Size (e.g., Cohen's d): {effect_size:.4f}")
            if 0.2 <= effect_size < 0.5:
                lines.append("  -> Small effect")
            elif 0.5 <= effect_size < 0.8:
                lines.append("  -> Medium effect")
            elif effect_size >= 0.8:
                lines.append("  -> Large effect")
        
        lines.append("")

    lines.append("NOTE: These results indicate statistical associations between network")
    lines.append("topology metrics (e.g., clustering coefficient) and diffusion rates.")
    lines.append("They do not establish causal mechanisms.")
    lines.append("")
    return "\n".join(lines)

def _format_anova_section(results: Dict[str, Any]) -> str:
    """Format the ANOVA analysis section of the report."""
    lines = []
    lines.append("=" * 60)
    lines.append("ANOVA ANALYSIS RESULTS (Topology Class Comparison)")
    lines.append("=" * 60)
    lines.append("")

    if not results:
        lines.append("No ANOVA results available.")
        lines.append("")
        return "\n".join(lines)

    f_stat = results.get('f_statistic')
    p_value = results.get('p_value')
    groups = results.get('group_means', {})
    correction_method = results.get('correction_method', 'None')
    corrected_p = results.get('corrected_p_value')

    lines.append("One-Way ANOVA on Diffusion Rates by Topology Class")
    lines.append("-" * 40)
    
    if f_stat is not None:
        lines.append(f"F-statistic: {f_stat:.4f}")
    if p_value is not None:
        lines.append(f"Raw P-value: {p_value:.4e}")
    
    if corrected_p is not None:
        lines.append(f"Corrected P-value ({correction_method}): {corrected_p:.4e}")
        if corrected_p < 0.05:
            lines.append("  -> Significant difference between groups (corrected)")
        else:
            lines.append("  -> No significant difference between groups (corrected)")
    elif p_value is not None and p_value < 0.05:
        lines.append("  -> Significant difference between groups (uncorrected)")

    if groups:
        lines.append("\nGroup Means (Diffusion Rate):")
        for group, mean in groups.items():
            lines.append(f"  {group}: {mean:.4f}")

    lines.append("")
    lines.append("NOTE: This analysis tests for associational differences in diffusion")
    lines.append("rates across different network topology classes.")
    lines.append("")
    return "\n".join(lines)

def _format_sensitivity_section(sensitivity_path: str) -> str:
    """Format the sensitivity analysis section."""
    lines = []
    lines.append("=" * 60)
    lines.append("SENSITIVITY ANALYSIS RESULTS")
    lines.append("=" * 60)
    lines.append("")

    data = _load_simulation_summary(sensitivity_path)
    if not data or 'sweep_results' not in data:
        lines.append("No sensitivity sweep results available.")
        lines.append("")
        return "\n".join(lines)

    sweep = data['sweep_results']
    lines.append("Clustering Coefficient Threshold Sensitivity")
    lines.append("-" * 40)
    
    thresholds = sweep.get('thresholds', [])
    metrics = sweep.get('metrics', [])
    
    if thresholds and metrics:
        lines.append(f"{'Threshold':<15} {'Metric Value':<15} {'Significance':<15}")
        lines.append("-" * 45)
        for t, m in zip(thresholds, metrics):
            sig = "Significant" if m.get('significant', False) else "Not Significant"
            lines.append(f"{t:<15.3f} {m.get('value', 0):<15.4f} {sig:<15}")
    
    lines.append("")
    lines.append("NOTE: This analysis explores the robustness of findings across")
    lines.append("varying clustering coefficient thresholds.")
    lines.append("")
    return "\n".join(lines)

def generate_report(
    regression_path: str = "data/analysis/regression_results.json",
    anova_path: str = "data/analysis/final_results.json",
    sensitivity_path: str = "data/analysis/sensitivity_sweep.json",
    output_path: str = REPORT_OUTPUT_PATH
) -> str:
    """
    Generate the final research report.
    
    This report frames all findings as associational (ROC-001) and documents
    p-values and effect sizes.
    
    Args:
        regression_path: Path to regression results JSON.
        anova_path: Path to ANOVA results JSON.
        sensitivity_path: Path to sensitivity sweep JSON.
        output_path: Path to write the final text report.
        
    Returns:
        The generated report text.
    """
    logger.info(f"Generating report at {output_path}")
    
    ensure_data_directory(output_path)
    
    timestamp = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    
    header = [
        "=" * 70,
        "NETWORK TOPOLOGY ENERGY TRANSFER: RESEARCH REPORT",
        "=" * 70,
        "",
        f"Generated: {timestamp}",
        "",
        "DISCLAIMER (ROC-001):",
        "This report presents associational findings derived from statistical",
        "analysis of simulated data. The results indicate correlations and",
        "differences between variables but do not establish causal relationships.",
        "All conclusions should be interpreted as hypotheses for further",
        "investigation rather than definitive causal mechanisms.",
        "",
        "-" * 70,
        "",
    ]
    
    sections = []
    sections.append(_format_regression_section(_load_regression_results(regression_path)))
    sections.append(_format_anova_section(_load_anova_results(anova_path)))
    sections.append(_format_sensitivity_section(sensitivity_path))
    
    footer = [
        "-" * 70,
        "END OF REPORT",
        "=" * 70,
    ]
    
    full_report = "\n".join(header) + "\n".join(sections) + "\n".join(footer)
    
    with open(output_path, 'w', encoding='utf-8') as f:
        f.write(full_report)
        
    logger.info(f"Report successfully written to {output_path}")
    return full_report

def main():
    """Entry point for the report generator script."""
    logging.basicConfig(
        level=logging.INFO,
        format='%(asctime)s - %(name)s - %(levelname)s - %(message)s'
    )
    
    try:
        report_text = generate_report()
        print(report_text)
    except Exception as e:
        logger.error(f"Failed to generate report: {e}")
        raise

if __name__ == "__main__":
    main()