def calculate_correlation(df: pd.DataFrame) -> dict:
    """
    Calculates Spearman correlation between burden and age.
    Returns dict with coefficient and p-value.
    """
    # Drop rows with missing values in critical columns
    clean_df = df.dropna(subset=['burden', 'age'])
    
    if len(clean_df) < 10:
        logger.warning("Not enough samples for correlation after dropping NaNs.")
        return {'rho': np.nan, 'p_value': np.nan}
    
    rho, p_val = stats.spearmanr(clean_df['burden'], clean_df['age'])
    
    return {
        'rho': rho,
        'p_value': p_val,
        'n_samples': len(clean_df)
    }

