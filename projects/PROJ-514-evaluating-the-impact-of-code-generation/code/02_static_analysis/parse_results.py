"""
Parse PMD XML/JSON output into structured analysis results.

This module reads the raw output generated by run_pmd.py (stored in
data/intermediate/pmd_raw_results.json) and transforms it into a
normalized JSON structure (data/intermediate/analysis_results.json)
that maps smells to SmellMetric entities.

It handles:
- Parsing PMD XML reports (converted to JSON by run_pmd.py)
- Mapping raw PMD violation types to the 4 target smell categories
- Aggregating counts per sample
- Writing the final structured output
"""
import os
import sys
import json
import logging
from pathlib import Path
from typing import Dict, List, Any, Optional

# Import project utilities
from utils.logger import get_logger
from utils.data_models import SmellMetric

# Configure logger
logger = get_logger(__name__)

# Mapping of PMD rule names to our target smell categories
# Based on standard PMD rulesets for Java/Python
SMELL_CATEGORY_MAPPING = {
    # Long Method
    "LongMethod": "Long Method",
    "long_method": "Long Method",
    "LongMethodStatement": "Long Method",
    
    # Duplicated Code
    "DuplicateProperty": "Duplicated Code",
    "DuplicateCode": "Duplicated Code",
    "CloneMethod": "Duplicated Code",
    "DataflowAnomaly": "Duplicated Code",
    
    # Feature Envy
    "FeatureEnvy": "Feature Envy",
    "CouplingBetweenObjects": "Feature Envy",
    "ExcessiveImports": "Feature Envy",
    
    # Long Parameter List
    "LongParameterList": "Long Parameter List",
    "TooManyParameters": "Long Parameter List",
    "LongParameter": "Long Parameter List"
}

# Target smell categories we care about
TARGET_SMELL_CATEGORIES = [
    "Long Method",
    "Duplicated Code",
    "Feature Envy",
    "Long Parameter List"
]

def load_pmd_raw_results(raw_results_path: Path) -> Dict[str, Any]:
    """
    Load the raw PMD results from the JSON file generated by run_pmd.py.
    
    Args:
        raw_results_path: Path to data/intermediate/pmd_raw_results.json
        
    Returns:
        Dictionary containing the raw PMD results
        
    Raises:
        FileNotFoundError: If the raw results file doesn't exist
        json.JSONDecodeError: If the file contains invalid JSON
    """
    if not raw_results_path.exists():
        raise FileNotFoundError(
            f"Raw PMD results file not found: {raw_results_path}. "
            "Ensure run_pmd.py has been executed first."
        )
    
    with open(raw_results_path, 'r', encoding='utf-8') as f:
        return json.load(f)

def map_smell_type(pmd_rule_name: str) -> Optional[str]:
    """
    Map a PMD rule name to one of our target smell categories.
    
    Args:
        pmd_rule_name: The raw PMD rule name (e.g., "LongMethod")
        
    Returns:
        The mapped smell category or None if not a target category
    """
    # Normalize the rule name for matching
    normalized = pmd_rule_name.strip()
    
    # Direct lookup
    if normalized in SMELL_CATEGORY_MAPPING:
        return SMELL_CATEGORY_MAPPING[normalized]
    
    # Case-insensitive lookup
    for key, value in SMELL_CATEGORY_MAPPING.items():
        if key.lower() == normalized.lower():
            return value
    
    return None

def parse_violations(raw_results: Dict[str, Any]) -> List[SmellMetric]:
    """
    Parse violations from raw PMD results into SmellMetric objects.
    
    Args:
        raw_results: Dictionary containing raw PMD output
        
    Returns:
        List of SmellMetric objects for all valid violations
    """
    metrics = []
    
    # Handle different possible structures of raw results
    # Expected structure from run_pmd.py:
    # {
    #   "samples": [
    #     {
    #       "sample_id": "...",
    #       "file_path": "...",
    #       "violations": [
    #         {"rule": "...", "line": ..., "description": "...", ...},
    #         ...
    #       ]
    #     },
    #     ...
    #   ]
    # }
    
    samples = raw_results.get("samples", [])
    
    for sample in samples:
        sample_id = sample.get("sample_id")
        file_path = sample.get("file_path")
        violations = sample.get("violations", [])
        
        if not sample_id or not file_path:
            logger.warning(f"Skipping sample with missing ID or path: {sample}")
            continue
        
        for violation in violations:
            rule_name = violation.get("rule", "")
            smell_category = map_smell_type(rule_name)
            
            if smell_category is None:
                # Not a target smell category, skip
                continue
            
            # Extract violation details
            line_number = violation.get("line", 0)
            description = violation.get("description", "")
            priority = violation.get("priority", 3)
            
            # Create SmellMetric object
            # For PMD, the count is 1 per violation, continuous_metric_value is the line number
            metric = SmellMetric(
                sample_id=sample_id,
                smell_type=smell_category,
                count=1,
                threshold_used=priority,
                continuous_metric_value=line_number
            )
            metrics.append(metric)
    
    return metrics

def aggregate_metrics_by_sample(metrics: List[SmellMetric]) -> Dict[str, Dict[str, int]]:
    """
    Aggregate violation counts by sample_id and smell_type.
    
    Args:
        metrics: List of SmellMetric objects
        
    Returns:
        Dictionary mapping sample_id -> {smell_type: count}
    """
    aggregated = {}
    
    for metric in metrics:
        if metric.sample_id not in aggregated:
            aggregated[metric.sample_id] = {}
        
        if metric.smell_type not in aggregated[metric.sample_id]:
            aggregated[metric.sample_id][metric.smell_type] = 0
        
        aggregated[metric.sample_id][metric.smell_type] += metric.count
    
    return aggregated

def generate_analysis_results(
    raw_results_path: Path,
    output_path: Path
) -> Dict[str, Any]:
    """
    Generate the structured analysis results JSON file.
    
    Args:
        raw_results_path: Path to raw PMD results
        output_path: Path to write the structured results
        
    Returns:
        The generated analysis results dictionary
    """
    logger.info(f"Loading raw PMD results from {raw_results_path}")
    raw_results = load_pmd_raw_results(raw_results_path)
    
    logger.info("Parsing violations into SmellMetric objects")
    metrics = parse_violations(raw_results)
    logger.info(f"Found {len(metrics)} violations for target smell categories")
    
    logger.info("Aggregating metrics by sample")
    aggregated = aggregate_metrics_by_sample(metrics)
    
    # Build the final results structure
    analysis_results = {
        "metadata": {
            "source_file": str(raw_results_path),
            "generated_at": str(Path(__file__).resolve()),
            "total_samples": len(raw_results.get("samples", [])),
            "samples_with_violations": len(aggregated),
            "target_smell_categories": TARGET_SMELL_CATEGORIES
        },
        "results": []
    }
    
    # Add aggregated results for each sample
    for sample_id, smell_counts in aggregated.items():
        sample_result = {
            "sample_id": sample_id,
            "smell_metrics": []
        }
        
        for smell_type, count in smell_counts.items():
            sample_result["smell_metrics"].append({
                "smell_type": smell_type,
                "count": count,
                "threshold_used": 3,  # Default threshold
                "continuous_metric_value": 0  # Aggregated, no single line
            })
        
        analysis_results["results"].append(sample_result)
    
    # Ensure output directory exists
    output_path.parent.mkdir(parents=True, exist_ok=True)
    
    # Write results
    logger.info(f"Writing analysis results to {output_path}")
    with open(output_path, 'w', encoding='utf-8') as f:
        json.dump(analysis_results, f, indent=2)
    
    logger.info(f"Successfully generated {len(analysis_results['results'])} sample results")
    return analysis_results

def main():
    """Main entry point for parsing PMD results."""
    # Define paths relative to project root
    project_root = Path(__file__).resolve().parent.parent.parent
    raw_results_path = project_root / "data" / "intermediate" / "pmd_raw_results.json"
    output_path = project_root / "data" / "intermediate" / "analysis_results.json"
    
    logger.info("Starting PMD results parsing")
    logger.info(f"Raw results path: {raw_results_path}")
    logger.info(f"Output path: {output_path}")
    
    try:
        results = generate_analysis_results(raw_results_path, output_path)
        
        # Log summary
        total_violations = sum(
            sum(smell["count"] for smell in sample["smell_metrics"])
            for sample in results["results"]
        )
        
        logger.info(f"Analysis complete:")
        logger.info(f"  - Total samples processed: {results['metadata']['total_samples']}")
        logger.info(f"  - Samples with violations: {results['metadata']['samples_with_violations']}")
        logger.info(f"  - Total violations found: {total_violations}")
        logger.info(f"  - Output written to: {output_path}")
        
    except FileNotFoundError as e:
        logger.error(f"Input file not found: {e}")
        sys.exit(1)
    except json.JSONDecodeError as e:
        logger.error(f"Invalid JSON in input file: {e}")
        sys.exit(1)
    except Exception as e:
        logger.error(f"Unexpected error during parsing: {e}")
        raise

if __name__ == "__main__":
    main()
