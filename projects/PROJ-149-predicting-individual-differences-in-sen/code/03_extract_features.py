"""
Task T012: Compute Welch's PSD on continuous 5-minute epochs.

Computes Power Spectral Density (PSD) using Welch's method on 4-second windows
with 2-second overlap. Aggregates power into standard frequency bands.

Input: Preprocessed EEG data from data/interim/preprocessed_eeg/
Output: data/processed/features_raw.csv (one row per participant)
"""
