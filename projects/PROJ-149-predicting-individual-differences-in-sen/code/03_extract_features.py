def load_preprocessed_eeg(subject_id: str) -> mne.io.BaseRaw:
    """
    Load preprocessed EEG data for a specific subject from the interim directory.
    Expects .fif files generated by code/02_preprocess_eeg.py.
    """
    raw_path = get_path("interim_preprocessed_eeg", f"{subject_id}_preprocessed.fif")
    if not os.path.exists(raw_path):
        # Fallback check for generic naming if specific ID fails
        pattern = get_path("interim_preprocessed_eeg", f"*{subject_id}*")
        matches = glob.glob(pattern)
        if matches:
            raw_path = matches[0]
        else:
            raise FileNotFoundError(f"Preprocessed EEG file not found for subject {subject_id}")
    
    return mne.io.read_raw_fif(raw_path, preload=True)

def compute_welch_psd(raw: mne.io.BaseRaw, window_sec: float = 4.0, overlap_sec: float = 2.0):
    """
    Compute Welch's PSD for all channels.
    
    Parameters
    ----------
    raw : mne.io.BaseRaw
        Preprocessed EEG data.
    window_sec : float
        Window length in seconds (default 4.0).
    overlap_sec : float
        Overlap in seconds (default 2.0).
        
    Returns
    -------
    freqs : np.ndarray
        Frequency array.
    psd : np.ndarray
        Power spectral density (channels x frequencies).
    """
    data = raw.get_data()  # Shape: (n_channels, n_times)
    sfreq = raw.info['sfreq']
    
    nperseg = int(window_sec * sfreq)
    noverlap = int(overlap_sec * sfreq)
    
    # Ensure overlap is less than window
    if noverlap >= nperseg:
        noverlap = nperseg // 2
        
    # Compute PSD using scipy.signal.welch
    # f, Pxx_den = signal.welch(x, fs, nperseg, noverlap, axis=-1)
    # We need to compute for each channel
    
    freqs, psd = signal.welch(
        data, 
        fs=sfreq, 
        nperseg=nperseg, 
        noverlap=noverlap, 
        axis=1, 
        scaling='density'
    )
    
    return freqs, psd

def aggregate_band_power(freqs: np.ndarray, psd: np.ndarray, band_freqs: dict):
    """
    Aggregate PSD into frequency bands.
    
    Parameters
    ----------
    freqs : np.ndarray
        Frequency array from Welch's method.
    psd : np.ndarray
        PSD array (n_channels x n_freqs).
    band_freqs : dict
        Dictionary mapping band names to (low, high) frequency tuples.
        
    Returns
    -------
    dict
        Dictionary mapping band names to mean power per channel.
    """
    band_powers = {}
    
    for band_name, (low, high) in band_freqs.items():
        # Find indices for the frequency range
        mask = (freqs >= low) & (freqs <= high)
        if np.sum(mask) == 0:
            # If no frequencies in range, fill with 0 or NaN
            band_powers[band_name] = np.zeros(psd.shape[0])
            continue
        
        # Average power across the frequency band for each channel
        band_power = np.mean(psd[:, mask], axis=1)
        band_powers[band_name] = band_power
        
    return band_powers

def extract_features_for_subject(subject_id: str, raw: mne.io.BaseRaw) -> dict:
    """
    Extract all features for a single subject.
    
    Returns a dictionary with:
    - subject_id
    - Band powers (absolute) for each band per channel
    - Mean band power across all channels
    """
    band_freqs = get_band_freqs()
    band_names = get_all_band_names()
    
    # Compute PSD
    freqs, psd = compute_welch_psd(raw)
    
    # Aggregate into bands
    band_powers = aggregate_band_power(freqs, psd, band_freqs)
    
    # Create result dictionary
    features = {
        'subject_id': subject_id
    }
    
    # Add absolute power for each band (mean across channels)
    for band_name in band_names:
        mean_power = float(np.mean(band_powers[band_name]))
        features[f'{band_name}_power'] = mean_power
        
        # Optionally add per-channel power if needed (flattened)
        # For now, we stick to mean power as per typical feature extraction
        # unless the spec requires per-channel. The task says "aggregate power",
        # which usually implies mean across channels for a participant-level feature.
    
    return features

def main():
    """
    Main entry point for feature extraction.
    """
    set_global_seed()
    ensure_dirs()
    
    data_dir = get_path("interim_preprocessed_eeg")
    output_path = get_path("processed_features_raw")
    
    print(f"Reading preprocessed EEG from: {data_dir}")
    print(f"Output will be written to: {output_path}")
    
    # Find all preprocessed files
    # Expecting files named like: sub-XXX_preprocessed.fif
    fif_files = glob.glob(os.path.join(data_dir, "*_preprocessed.fif"))
    
    if not fif_files:
        print("ERROR: No preprocessed EEG files found. Ensure T010b has completed.")
        sys.exit(1)
    
    print(f"Found {len(fif_files)} preprocessed files.")
    
    all_features = []
    
    for fpath in fif_files:
        filename = os.path.basename(fpath)
        # Extract subject ID: sub-XXX_preprocessed.fif -> XXX
        # Or generic: 001_preprocessed.fif -> 001
        parts = filename.replace("_preprocessed.fif", "").split("-")
        if len(parts) > 1:
            subject_id = parts[1]
        else:
            subject_id = parts[0]
        
        print(f"Processing subject: {subject_id}")
        
        try:
            raw = load_preprocessed_eeg(subject_id)
            
            # Check for minimum data duration (5 minutes = 300 seconds)
            duration = raw.info['n_times'] / raw.info['sfreq']
            if duration < 300:
                print(f"  Warning: Subject {subject_id} has only {duration:.1f}s of data. "
                      f"Using available data (min 5min recommended).")
            
            features = extract_features_for_subject(subject_id, raw)
            all_features.append(features)
            print(f"  Completed. Mean Alpha: {features.get('alpha_power', 'N/A'):.4f}")
            
        except Exception as e:
            print(f"  ERROR processing {subject_id}: {str(e)}")
            # Decide whether to skip or fail. Spec says "exclude if >30% channels rejected"
            # which is handled in preprocessing. If file missing/corrupt, we log and skip.
            continue
    
    if not all_features:
        print("ERROR: No features extracted from any subject.")
        sys.exit(1)
    
    # Create DataFrame
    df = pd.DataFrame(all_features)
    
    # Sort by subject_id
    df = df.sort_values('subject_id')
    
    # Save to CSV
    df.to_csv(output_path, index=False)
    print(f"\nSaved {len(df)} subjects to {output_path}")
    print("Features extracted successfully.")

if __name__ == "__main__":
    main()
