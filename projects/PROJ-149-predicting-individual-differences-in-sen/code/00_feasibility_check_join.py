def load_physionet_metadata(raw_dir: Path) -> pd.DataFrame:
    """
    Loads the metadata generated by T007 (01_download_data.py).
    Expects a file like 'metadata.json' or 'subjects.csv' created during download.
    If T007 created a manifest, we parse it.
    
    The T007 task (download) should have created a manifest of downloaded subjects.
    We assume the download script created 'data/raw/eeg/metadata.json' with subject info.
    """
    # T007 typically creates a manifest. Let's look for it.
    # If T007 is incomplete (as per rejection notes), we might need to scan the directory.
    # But the task says "Must run after T007". We assume T007 creates a metadata file.
    
    metadata_path = raw_dir / 'metadata.json'
    
    if not metadata_path.exists():
        # Fallback: Scan directory if metadata.json is missing (robustness)
        # PhysioNet structure: sub-XX/sub-XX_...
        subjects = []
        for item in raw_dir.iterdir():
            if item.is_dir() and item.name.startswith('sub-'):
                # Extract ID
                subj_id = int(item.name.split('-')[1])
                # Check for EEG files
                eeg_files = list(item.glob('*.edf')) or list(item.glob('*.bdf'))
                if eeg_files:
                    subjects.append({'participant_id': subj_id, 'source': 'physionet', 'files': len(eeg_files)})
        
        if not subjects:
            raise FileNotFoundError(f"No EEG data found in {raw_dir} and no metadata.json")
        
        df = pd.DataFrame(subjects)
        # Save it for consistency
        df.to_csv(metadata_path.with_suffix('.csv'), index=False)
        return df
    
    # Load existing metadata
    with open(metadata_path, 'r') as f:
        data = json.load(f)
    
    # Normalize to DataFrame
    if isinstance(data, list):
        df = pd.DataFrame(data)
    elif isinstance(data, dict):
        # If it's a dict of lists or similar
        df = pd.DataFrame([data])
    else:
        raise ValueError("Unexpected metadata format")
    
    if 'participant_id' not in df.columns:
        # Try to infer ID from filename if present
        if 'filename' in df.columns:
            df['participant_id'] = df['filename'].apply(lambda x: int(x.split('-')[1].split('.')[0]) if '-' in str(x) else None)
        else:
            raise ValueError("Metadata missing 'participant_id' column")
    
    return df[['participant_id']]

def load_behavioral_metadata(raw_dir: Path) -> pd.DataFrame:
    """
    Loads behavioral metadata (RT data) created by T007.
    Expects a file like 'behavioral_metadata.json' or 'rt_data.csv'.
    """
    # Assume T007 downloaded behavioral data into a specific structure or created a manifest.
    # We look for a common manifest name or scan.
    metadata_path = raw_dir / 'behavioral_metadata.json'
    csv_path = raw_dir / 'rt_data.csv'
    
    df = None
    
    if metadata_path.exists():
        with open(metadata_path, 'r') as f:
            data = json.load(f)
        if isinstance(data, list):
            df = pd.DataFrame(data)
        elif isinstance(data, dict):
            df = pd.DataFrame([data])
    elif csv_path.exists():
        df = pd.read_csv(csv_path)
    
    if df is None:
        # Fallback: Scan for behavioral files if no manifest
        # Assume structure: data/raw/behavioral/subject_XX/rt.csv
        subjects = []
        if raw_dir.exists():
            for item in raw_dir.iterdir():
                if item.is_dir() and ('subject' in item.name or 'sub' in item.name):
                    # Try to find RT file inside
                    rt_files = list(item.glob('*.csv'))
                    if rt_files:
                        # Extract ID
                        name = item.name
                        # Try to extract number
                        parts = name.replace('subject', '').replace('sub', '').replace('_', '').split('-')
                        if parts and parts[0].isdigit():
                            subj_id = int(parts[0])
                            subjects.append({'participant_id': subj_id, 'source': 'behavioral'})
        
        if not subjects:
            raise FileNotFoundError(f"No behavioral data found in {raw_dir}")
        
        df = pd.DataFrame(subjects)
        # Save for consistency
        df.to_csv(csv_path, index=False)
    
    if 'participant_id' not in df.columns:
        raise ValueError("Behavioral metadata missing 'participant_id' column")
    
    return df[['participant_id']]

def generate_report(join_df: pd.DataFrame, eeg_df: pd.DataFrame, behavioral_df: pd.DataFrame, output_path: Path) -> dict:
    """
    Generates the feasibility report.
    Returns a dict with summary stats.
    """
    total_eeg = len(eeg_df)
    total_behavioral = len(behavioral_df)
    total_joined = len(join_df)
    
    # Demographic validation (placeholder for real demographic checks if data exists)
    # If the datasets had age/sex columns, we would check for nulls here.
    # Since we only have IDs, we check for ID consistency.
    
    report = {
        "status": "success" if total_joined > 0 else "failure",
        "total_eeg_subjects": total_eeg,
        "total_behavioral_subjects": total_behavioral,
        "matched_subjects": total_joined,
        "drop_rate": 1.0 - (total_joined / max(total_eeg, total_behavioral, 1)),
        "matched_ids": join_df['participant_id'].tolist(),
        "message": f"Successfully joined {total_joined} subjects." if total_joined > 0 else "No matching subjects found."
    }
    
    # Write markdown report
    output_path.parent.mkdir(parents=True, exist_ok=True)
    with open(output_path, 'w') as f:
        f.write("# Feasibility Check Report\n\n")
        f.write(f"### Summary\n")
        f.write(f"- **Status**: {report['status']}\n")
        f.write(f"- **EEG Subjects**: {total_eeg}\n")
        f.write(f"- **Behavioral Subjects**: {total_behavioral}\n")
        f.write(f"- **Matched Subjects**: {total_joined}\n")
        f.write(f"- **Drop Rate**: {report['drop_rate']:.2%}\n\n")
        
        f.write(f"### Matched Participant IDs\n")
        f.write(f"```json\n{json.dumps(report['matched_ids'], indent=2)}\n```\n")
        
        if total_joined == 0:
            f.write("\n### Critical Issue\n")
            f.write("No matching participants found between EEG and Behavioral datasets. \n")
            f.write("The pipeline cannot proceed without matched data.\n")
        
    return report

def main():
    """
    Main entry point for T008a.
    1. Load EEG metadata.
    2. Load Behavioral metadata.
    3. Join on participant_id.
    4. Validate.
    5. Generate report.
    6. Exit with code 1 if join fails or no matches.
    """
    print("Starting T008a: Feasibility Check Join...")
    
    # Define paths
    raw_eeg_dir = get_path('raw_eeg')
    raw_behavioral_dir = get_path('raw_behavioral')
    processed_dir = get_path('processed')
    report_path = processed_dir / 'feasibility_report.md'
    
    try:
        # Load datasets
        print(f"Loading EEG metadata from {raw_eeg_dir}...")
        eeg_df = load_physionet_metadata(raw_eeg_dir)
        print(f"Loaded {len(eeg_df)} EEG subjects.")
        
        print(f"Loading Behavioral metadata from {raw_behavioral_dir}...")
        behavioral_df = load_behavioral_metadata(raw_behavioral_dir)
        print(f"Loaded {len(behavioral_df)} behavioral subjects.")
        
        # Join
        print("Joining datasets on 'participant_id'...")
        join_df = pd.merge(
            eeg_df, 
            behavioral_df, 
            on='participant_id', 
            how='inner'
        )
        
        # Validate
        if len(join_df) == 0:
            print("ERROR: No matching participants found.")
            generate_report(join_df, eeg_df, behavioral_df, report_path)
            sys.exit(1)
        
        print(f"Join successful. {len(join_df)} matched participants.")
        
        # Generate report
        report = generate_report(join_df, eeg_df, behavioral_df, report_path)
        print(f"Report generated at {report_path}")
        
        if report['status'] == 'failure':
            sys.exit(1)
            
        sys.exit(0)
        
    except FileNotFoundError as e:
        print(f"ERROR: Data not found - {e}")
        # Generate failure report
        if 'report_path' in locals():
            generate_report(pd.DataFrame(), pd.DataFrame(), pd.DataFrame(), report_path)
        sys.exit(1)
    except Exception as e:
        print(f"ERROR: Unexpected error - {e}")
        import traceback
        traceback.print_exc()
        sys.exit(1)

if __name__ == "__main__":
    main()
