"""
Task T021: Apply Bonferroni correction for multiple comparisons.

Reads correlation results from data/processed/correlations.csv,
applies Bonferroni correction (alpha = 0.05 / 6 bands),
flags significant results, and writes the corrected results to
data/processed/correlations_corrected.csv.

Dependencies:
- data/processed/correlations.csv (output of T020)
- code/config.py (for alpha threshold constants if needed)
- code/utils/stats_helpers.py (for bonferroni_correct utility)
"""

import os
import sys
import argparse
import pandas as pd
from pathlib import Path

# Add project root to path to allow imports from code/
project_root = Path(__file__).resolve().parent.parent
sys.path.insert(0, str(project_root / "code"))

from config import get_path
from utils.stats_helpers import bonferroni_correct


def load_correlations(input_path: str) -> pd.DataFrame:
    """Load the correlation results generated by T020."""
    if not os.path.exists(input_path):
        raise FileNotFoundError(f"Correlation results file not found: {input_path}")
    
    df = pd.read_csv(input_path)
    
    # Validate expected columns
    required_cols = ['band', 'correlation', 'p_value', 'n']
    missing_cols = [col for col in required_cols if col not in df.columns]
    if missing_cols:
        raise ValueError(f"Missing required columns in {input_path}: {missing_cols}")
    
    return df


def apply_bonferroni_correction(df: pd.DataFrame, alpha: float = 0.05) -> pd.DataFrame:
    """
    Apply Bonferroni correction to p-values.
    
    The correction divides alpha by the number of tests (6 bands).
    We calculate the corrected p-value (p * n_tests) and flag
    significance based on the corrected threshold.
    
    Args:
        df: DataFrame with 'p_value' column
        alpha: Original significance threshold (default 0.05)
        
    Returns:
        DataFrame with added columns:
        - 'n_tests': Number of tests performed (6)
        - 'bonferroni_threshold': Corrected alpha threshold
        - 'corrected_p_value': Adjusted p-value (min(p * n_tests, 1.0))
        - 'is_significant': Boolean flag for significance after correction
    """
    n_bands = 6
    bonferroni_threshold = alpha / n_bands
    
    result = df.copy()
    result['n_tests'] = n_bands
    result['bonferroni_threshold'] = bonferroni_threshold
    
    # Calculate corrected p-value (capped at 1.0)
    result['corrected_p_value'] = result['p_value'] * n_bands
    result['corrected_p_value'] = result['corrected_p_value'].clip(upper=1.0)
    
    # Flag significant results
    result['is_significant'] = result['corrected_p_value'] < bonferroni_threshold
    
    return result


def save_corrected_results(df: pd.DataFrame, output_path: str) -> None:
    """Save the corrected correlation results to CSV."""
    output_dir = Path(output_path).parent
    output_dir.mkdir(parents=True, exist_ok=True)
    df.to_csv(output_path, index=False)


def main():
    parser = argparse.ArgumentParser(
        description="Apply Bonferroni correction to correlation results (T021)"
    )
    parser.add_argument(
        "--input",
        type=str,
        default=None,
        help="Path to input correlations CSV (default: from config)"
    )
    parser.add_argument(
        "--output",
        type=str,
        default=None,
        help="Path to output corrected results CSV (default: from config)"
    )
    parser.add_argument(
        "--alpha",
        type=float,
        default=0.05,
        help="Significance threshold for correction (default: 0.05)"
    )
    
    args = parser.parse_args()
    
    # Determine input/output paths
    if args.input:
        input_path = args.input
    else:
        input_path = get_path("correlations_csv")
    
    if args.output:
        output_path = args.output
    else:
        output_path = get_path("correlations_corrected_csv")
    
    print(f"Loading correlations from: {input_path}")
    df = load_correlations(input_path)
    print(f"Loaded {len(df)} correlation results")
    
    print(f"Applying Bonferroni correction (alpha={args.alpha}, n_tests=6)")
    corrected_df = apply_bonferroni_correction(df, alpha=args.alpha)
    
    print(f"Saving corrected results to: {output_path}")
    save_corrected_results(corrected_df, output_path)
    
    # Print summary
    n_sig = corrected_df['is_significant'].sum()
    print(f"\nSummary:")
    print(f"  Total tests: {len(df)}")
    print(f"  Bonferroni threshold: {corrected_df['bonferroni_threshold'].iloc[0]:.4f}")
    print(f"  Significant results: {n_sig} / {len(df)}")
    
    if n_sig > 0:
        print(f"\nSignificant bands:")
        sig_bands = corrected_df[corrected_df['is_significant']]
        for _, row in sig_bands.iterrows():
            print(f"  - {row['band']}: r={row['correlation']:.3f}, "
                  f"p={row['p_value']:.4f}, "
                  f"p_corrected={row['corrected_p_value']:.4f}")
    else:
        print("\nNo bands remained significant after Bonferroni correction.")
    
    print("\nTask T021 completed successfully.")


if __name__ == "__main__":
    main()