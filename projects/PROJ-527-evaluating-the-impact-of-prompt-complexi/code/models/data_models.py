"""
Base Data Models for the pipeline.
"""
from datetime import datetime
from typing import Any, Dict, List, Optional
from pydantic import BaseModel, Field, field_validator
from enum import Enum

class ComplexityLabel(str, Enum):
    SIMPLE = "simple"
    MODERATE = "moderate"
    COMPLEX = "complex"
    VERY_COMPLEX = "very_complex"
    DEGENERATE = "degenerate"

class ExecutionStatus(str, Enum):
    PASS = "pass"
    FAIL = "fail"
    TIMEOUT = "timeout"
    SYNTAX_ERROR = "syntax_error"
    RUNTIME_ERROR = "runtime_error"

class HumanEvalProblem(BaseModel):
    """Represents a single HumanEval problem."""
    problem_id: str
    prompt: str
    canonical_solution: str
    test: str
    entry_point: str

class PromptVariant(BaseModel):
    """Represents a generated prompt variant for a problem."""
    problem_id: str
    complexity_label: ComplexityLabel
    prompt_text: str
    token_count: int
    structural_element_count: int
    generated_at: datetime = Field(default_factory=datetime.utcnow)

class GeneratedCode(BaseModel):
    """Represents code generated by an LLM for a prompt variant."""
    variant_id: str
    problem_id: str
    complexity_label: ComplexityLabel
    code: str
    token_count: int
    generation_time_ms: float
    llm_model: str

class ExecutionResult(BaseModel):
    """Result of executing generated code against unit tests."""
    code_id: str
    problem_id: str
    status: ExecutionStatus
    duration_ms: Optional[float] = None
    error_message: Optional[str] = None
    test_results: Optional[List[Dict[str, Any]]] = None

class AnalysisResult(BaseModel):
    """Aggregated analysis result for a complexity level."""
    complexity_label: ComplexityLabel
    total_samples: int
    pass_count: int
    fail_count: int
    pass_rate: float
    avg_token_count: float
    avg_structural_count: float
    lmm_effect_size: Optional[float] = None
    lmm_p_value: Optional[float] = None
    analysis_date: datetime = Field(default_factory=datetime.utcnow)
