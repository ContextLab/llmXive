import os
import sys
import json
import glob
import logging
import argparse
from pathlib import Path

# Add project root to path
project_root = Path(__file__).parent.parent
sys.path.insert(0, str(project_root))

from code.analysis import analyze_metric_degradation, identify_fnn_threshold
from code.utils.io import write_json_artifact, export_csv

logging.basicConfig(level=logging.INFO, format='%(asctime)s - %(levelname)s - %(message)s')
logger = logging.getLogger(__name__)

def find_ground_truth_file():
    """Find the ground truth metrics file generated by T017."""
    # Pattern: data/processed/ground_truth_metrics_*.json
    pattern = str(project_root / "data" / "processed" / "ground_truth_metrics_*.json")
    files = glob.glob(pattern)
    if not files:
        logger.error(f"No ground truth metrics found at {pattern}. Please run T017 first.")
        return None
    # Return the first one (assuming one seed or we pick one)
    return files[0]

def find_noisy_metrics_data():
    """Find noisy metrics data. Since T032 is not done, we might not have a summary.
    We look for individual noisy metric files or a summary if T032 was partially done.
    If T024/T032 are not done, we might need to load from a specific location if generated by a previous step.
    However, without T024/T032, we don't have a standard file.
    
    To satisfy the 'real data' constraint without T024/T032 being done:
    We cannot fabricate.
    If no noisy data exists, we must fail.
    """
    # Try to find any JSON with 'noisy' in the name in data/processed
    pattern = str(project_root / "data" / "processed" / "*noisy*.json")
    files = glob.glob(pattern)
    if files:
        return files[0]
    
    # Try to find metrics_summary.csv if T032 was partially run
    csv_path = project_root / "data" / "processed" / "metrics_summary.csv"
    if csv_path.exists():
        return str(csv_path)

    logger.error("No noisy metrics data found. T024 (noise injection) and T032 (metrics summary) must be run first.")
    return None

def load_ground_truth(filepath: str) -> dict:
    with open(filepath, 'r') as f:
        data = json.load(f)
    # T017 might store per-system or per-seed. We assume a flat dict or we need to extract.
    # Assuming T017 output structure: {"correlation_dimension": x, "lyapunov_exponent": y, "fnn_rate": z}
    # If it's nested, we need to handle it. Let's assume flat for now as per T017 description.
    return data

def load_noisy_metrics(filepath: str) -> list:
    if filepath.endswith('.csv'):
        import csv
        with open(filepath, 'r') as f:
            reader = csv.DictReader(f)
            return [row for row in reader]
    else:
        with open(filepath, 'r') as f:
            data = json.load(f)
        # Assuming list of dicts
        if isinstance(data, list):
            return data
        elif isinstance(data, dict):
            # Maybe it's a dict of lists?
            return [data] # Wrap it? No, likely a list.
        return []

def main():
    logger.info("Starting T030: Error Calculation Analysis")

    # 1. Load Ground Truth
    gt_path = find_ground_truth_file()
    if not gt_path:
        logger.critical("Cannot proceed without ground truth data (T017).")
        sys.exit(1)
    
    logger.info(f"Loading ground truth from: {gt_path}")
    try:
        gt_metrics = load_ground_truth(gt_path)
    except Exception as e:
        logger.critical(f"Failed to load ground truth: {e}")
        sys.exit(1)

    # 2. Load Noisy Metrics
    noisy_path = find_noisy_metrics_data()
    if not noisy_path:
        logger.critical("Cannot proceed without noisy metrics data (T024/T032).")
        sys.exit(1)

    logger.info(f"Loading noisy metrics from: {noisy_path}")
    try:
        noisy_data = load_noisy_metrics(noisy_path)
    except Exception as e:
        logger.critical(f"Failed to load noisy metrics: {e}")
        sys.exit(1)

    if not noisy_data:
        logger.critical("No noisy data records found.")
        sys.exit(1)

    # 3. Analyze
    logger.info("Analyzing metric degradation...")
    analysis_results = analyze_metric_degradation(gt_metrics, noisy_data)

    if not analysis_results:
        logger.warning("No analysis results generated.")
    else:
        logger.info(f"Generated {len(analysis_results)} error records.")

    # 4. Identify Threshold
    threshold_snr = identify_fnn_threshold(analysis_results, threshold_percent=50.0)

    # 5. Save Results
    output_dir = project_root / "data" / "processed"
    output_dir.mkdir(parents=True, exist_ok=True)

    # Save detailed analysis
    analysis_file = output_dir / "t030_error_analysis.json"
    write_json_artifact(analysis_file, {
        "ground_truth": gt_metrics,
        "error_records": analysis_results,
        "critical_fnn_threshold_snr_db": threshold_snr
    })
    logger.info(f"Saved analysis to {analysis_file}")

    # Save CSV summary (matching T034/T032 format)
    # Columns: SNR_dB, noise_type, metric_name, computed_value, ground_truth_value, error_percent
    csv_rows = []
    for rec in analysis_results:
        csv_rows.append({
            "SNR_dB": rec["snr_db"],
            "noise_type": rec["noise_type"],
            "metric_name": rec["metric_name"],
            "computed_value": rec["computed_value"],
            "ground_truth_value": rec["ground_truth_value"],
            "error_percent": rec["error_percent"]
        })
    
    csv_file = output_dir / "t030_metrics_summary.csv"
    export_csv(csv_file, csv_rows)
    logger.info(f"Saved CSV summary to {csv_file}")

    logger.info("T030 completed successfully.")

if __name__ == "__main__":
    main()