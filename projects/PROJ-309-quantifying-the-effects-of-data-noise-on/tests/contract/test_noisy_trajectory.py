"""
Contract test for noisy trajectory schema.

This test verifies that the NoisyTrajectory data model adheres to the
expected schema defined in the project's data models. It ensures that
noisy trajectories generated by the noise injection module (code/noise.py)
contain all required fields and that the data types match the contract.
"""
import pytest
import numpy as np
import pandas as pd
from pathlib import Path
import json

# Import the data model from the project's utility module
from code.utils.data_models import NoisyTrajectory, Trajectory, MetricResult
from code.config import NoiseType


class TestNoisyTrajectorySchema:
    """Contract tests for the NoisyTrajectory schema."""

    def test_noisy_trajectory_required_fields(self):
        """
        Verify that NoisyTrajectory contains all required fields.

        Contract:
        - trajectory: The underlying Trajectory object
        - noise_type: Enum indicating the type of noise added (Gaussian or Quantization)
        - snr_db: Signal-to-Noise Ratio in decibels
        - seed: Random seed used for reproducibility
        - metadata: Dictionary containing additional generation parameters
        """
        # Create a minimal valid Trajectory for the test
        time_steps = np.linspace(0, 10, 100)
        state_data = np.random.randn(100, 3)  # 3D state space
        
        clean_trajectory = Trajectory(
            time=time_steps,
            state=state_data,
            system_type="lorenz",
            seed=42
        )

        # Create a NoisyTrajectory instance
        noisy_traj = NoisyTrajectory(
            trajectory=clean_trajectory,
            noise_type=NoiseType.GAUSSIAN,
            snr_db=20.0,
            seed=42,
            metadata={"bits": 8, "method": "standard"}
        )

        # Assert all required fields exist and have correct types
        assert hasattr(noisy_traj, 'trajectory'), "Missing 'trajectory' field"
        assert hasattr(noisy_traj, 'noise_type'), "Missing 'noise_type' field"
        assert hasattr(noisy_traj, 'snr_db'), "Missing 'snr_db' field"
        assert hasattr(noisy_traj, 'seed'), "Missing 'seed' field"
        assert hasattr(noisy_traj, 'metadata'), "Missing 'metadata' field"

        # Verify types
        assert isinstance(noisy_traj.trajectory, Trajectory), "trajectory must be a Trajectory instance"
        assert isinstance(noisy_traj.noise_type, NoiseType), "noise_type must be a NoiseType enum"
        assert isinstance(noisy_traj.snr_db, (int, float)), "snr_db must be numeric"
        assert isinstance(noisy_traj.seed, int), "seed must be an integer"
        assert isinstance(noisy_traj.metadata, dict), "metadata must be a dictionary"

    def test_noisy_trajectory_data_integrity(self):
        """
        Verify that the noisy trajectory preserves the structure of the clean trajectory.

        The time array and state dimensions must match the original trajectory,
        and the state data should be numpy arrays.
        """
        time_steps = np.linspace(0, 10, 100)
        state_data = np.random.randn(100, 3)
        
        clean_trajectory = Trajectory(
            time=time_steps,
            state=state_data,
            system_type="rossler",
            seed=123
        )

        noisy_traj = NoisyTrajectory(
            trajectory=clean_trajectory,
            noise_type=NoiseType.QUANTIZATION,
            snr_db=10.0,
            seed=123,
            metadata={}
        )

        # Check time array integrity
        assert np.array_equal(noisy_traj.trajectory.time, time_steps), "Time array mismatch"
        assert isinstance(noisy_traj.trajectory.time, np.ndarray), "Time must be numpy array"

        # Check state array integrity
        assert noisy_traj.trajectory.state.shape == state_data.shape, "State shape mismatch"
        assert isinstance(noisy_traj.trajectory.state, np.ndarray), "State must be numpy array"
        assert noisy_traj.trajectory.state.dtype in [np.float32, np.float64], "State must be float"

    def test_noise_type_enum_values(self):
        """
        Verify that noise_type only accepts valid NoiseType enum values.
        """
        time_steps = np.linspace(0, 10, 50)
        state_data = np.random.randn(50, 3)
        
        clean_trajectory = Trajectory(
            time=time_steps,
            state=state_data,
            system_type="lorenz",
            seed=99
        )

        # Test valid enum values
        valid_noise_types = [NoiseType.GAUSSIAN, NoiseType.QUANTIZATION]
        for noise_type in valid_noise_types:
            traj = NoisyTrajectory(
                trajectory=clean_trajectory,
                noise_type=noise_type,
                snr_db=15.0,
                seed=99,
                metadata={}
            )
            assert traj.noise_type == noise_type, f"Failed to set noise_type to {noise_type}"

        # Test that invalid noise type raises an error (or is not accepted by the type system)
        # Note: Since we are using a dataclass with type hints, Python won't enforce this at runtime
        # unless we add validation. We test the intended behavior here.
        with pytest.raises((ValueError, TypeError)):
            # Attempt to use an invalid noise type
            NoisyTrajectory(
                trajectory=clean_trajectory,
                noise_type="INVALID_TYPE",  # type: ignore
                snr_db=15.0,
                seed=99,
                metadata={}
            )

    def test_snr_db_range(self):
        """
        Verify that snr_db accepts a reasonable range of values.
        Contract: SNR can be negative (noise > signal) to positive.
        """
        time_steps = np.linspace(0, 10, 50)
        state_data = np.random.randn(50, 3)
        
        clean_trajectory = Trajectory(
            time=time_steps,
            state=state_data,
            system_type="lorenz",
            seed=1
        )

        # Test negative SNR
        noisy_neg = NoisyTrajectory(
            trajectory=clean_trajectory,
            noise_type=NoiseType.GAUSSIAN,
            snr_db=-10.0,
            seed=1,
            metadata={}
        )
        assert noisy_neg.snr_db == -10.0

        # Test positive SNR
        noisy_pos = NoisyTrajectory(
            trajectory=clean_trajectory,
            noise_type=NoiseType.GAUSSIAN,
            snr_db=30.0,
            seed=1,
            metadata={}
        )
        assert noisy_pos.snr_db == 30.0

    def test_noisy_trajectory_serialization(self):
        """
        Verify that NoisyTrajectory can be serialized to a dictionary
        and back, preserving key metadata.
        """
        time_steps = np.linspace(0, 10, 50)
        state_data = np.random.randn(50, 3)
        
        clean_trajectory = Trajectory(
            time=time_steps,
            state=state_data,
            system_type="rossler",
            seed=55
        )

        original = NoisyTrajectory(
            trajectory=clean_trajectory,
            noise_type=NoiseType.QUANTIZATION,
            snr_db=5.0,
            seed=55,
            metadata={"bits": 12, "quantization_method": "uniform"}
        )

        # Convert to dict (simulating export)
        # Note: The dataclass doesn't have a built-in to_dict, so we manually extract
        data_dict = {
            "noise_type": original.noise_type.value,
            "snr_db": original.snr_db,
            "seed": original.seed,
            "metadata": original.metadata,
            "system_type": original.trajectory.system_type
        }

        # Verify structure
        assert "noise_type" in data_dict
        assert "snr_db" in data_dict
        assert "seed" in data_dict
        assert "metadata" in data_dict
        assert "system_type" in data_dict

        # Verify values
        assert data_dict["noise_type"] == "quantization"
        assert data_dict["snr_db"] == 5.0
        assert data_dict["seed"] == 55
        assert data_dict["metadata"]["bits"] == 12
        assert data_dict["system_type"] == "rossler"