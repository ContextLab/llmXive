"""
Contract test for trajectory schema (User Story 1).

This test verifies that the trajectory data generated by the system adheres
to the defined schema in `code/utils/data_models.py`. It ensures that:
1. The data structure matches the expected `Trajectory` dataclass fields.
2. Required fields (system_type, seed, time, states) are present.
3. Data types are correct (e.g., numpy arrays for time and states).
4. The trajectory contains no NaN or infinite values.
5. The trajectory meets the minimum length requirement.
"""

import pytest
import numpy as np
from pathlib import Path
import sys

# Add the project root to the path to allow imports from code/
project_root = Path(__file__).parent.parent.parent
sys.path.insert(0, str(project_root))

from code.utils.data_models import Trajectory
from code.generators import generate_lorenz_trajectory
from code.config import LORENZ_PARAMS, DT, T_MAX


def test_trajectory_schema_structure():
    """
    Contract Test: Verify the generated trajectory object matches the Trajectory schema.
    """
    # Generate a small trajectory for testing
    seed = 42
    trajectory = generate_lorenz_trajectory(seed=seed)

    # 1. Check instance type
    assert isinstance(trajectory, Trajectory), f"Expected Trajectory instance, got {type(trajectory)}"

    # 2. Check required fields exist and have correct types
    assert hasattr(trajectory, 'system_type'), "Missing 'system_type' field"
    assert hasattr(trajectory, 'seed'), "Missing 'seed' field"
    assert hasattr(trajectory, 'time'), "Missing 'time' field"
    assert hasattr(trajectory, 'states'), "Missing 'states' field"
    assert hasattr(trajectory, 'params'), "Missing 'params' field"

    assert isinstance(trajectory.system_type, str), "system_type must be a string"
    assert isinstance(trajectory.seed, int), "seed must be an integer"
    assert isinstance(trajectory.time, np.ndarray), "time must be a numpy array"
    assert isinstance(trajectory.states, np.ndarray), "states must be a numpy array"
    assert isinstance(trajectory.params, dict), "params must be a dictionary"

    # 3. Verify specific values
    assert trajectory.system_type == "lorenz", f"Expected system_type 'lorenz', got '{trajectory.system_type}'"
    assert trajectory.seed == seed, f"Expected seed {seed}, got {trajectory.seed}"

    # 4. Verify data integrity (no NaNs or Infs)
    assert not np.any(np.isnan(trajectory.time)), "Time array contains NaN values"
    assert not np.any(np.isinf(trajectory.time)), "Time array contains Inf values"
    assert not np.any(np.isnan(trajectory.states)), "States array contains NaN values"
    assert not np.any(np.isinf(trajectory.states)), "States array contains Inf values"

    # 5. Verify dimensions
    # States should be (N, 3) for Lorenz (x, y, z)
    assert trajectory.states.ndim == 2, f"States must be 2D, got {trajectory.states.ndim}D"
    assert trajectory.states.shape[1] == 3, f"States must have 3 columns (x, y, z), got {trajectory.states.shape[1]}"

    # Time and states length must match
    assert len(trajectory.time) == trajectory.states.shape[0], "Time and states length mismatch"

    # 6. Verify minimum length constraint (from T014 requirement)
    # The integration runs from 0 to T_MAX with step DT
    expected_length = int(T_MAX / DT) + 1
    assert len(trajectory.time) >= 100, f"Trajectory too short: {len(trajectory.time)} steps. Minimum expected ~{expected_length}"


def test_trajectory_reproducibility_contract():
    """
    Contract Test: Verify that the same seed produces the same trajectory schema and data.
    This ensures the generator is deterministic as per the contract.
    """
    seed = 12345
    traj1 = generate_lorenz_trajectory(seed=seed)
    traj2 = generate_lorenz_trajectory(seed=seed)

    # Check structural equality
    assert traj1.system_type == traj2.system_type
    assert traj1.seed == traj2.seed
    assert np.array_equal(traj1.time, traj2.time), "Time arrays differ for same seed"
    assert np.array_equal(traj1.states, traj2.states), "State arrays differ for same seed"


def test_trajectory_schema_with_different_systems():
    """
    Contract Test: Verify schema compliance for different system types (Lorenz vs Rössler).
    """
    from code.generators import generate_rossler_trajectory

    # Test Lorenz
    lorenz_traj = generate_lorenz_trajectory(seed=99)
    assert lorenz_traj.system_type == "lorenz"
    assert lorenz_traj.states.shape[1] == 3

    # Test Rössler
    rossler_traj = generate_rossler_trajectory(seed=99)
    assert rossler_traj.system_type == "rossler"
    assert rossler_traj.states.shape[1] == 3

    # Both should pass the generic schema checks
    for traj in [lorenz_traj, rossler_traj]:
        assert isinstance(traj.time, np.ndarray)
        assert isinstance(traj.states, np.ndarray)
        assert not np.any(np.isnan(traj.states))