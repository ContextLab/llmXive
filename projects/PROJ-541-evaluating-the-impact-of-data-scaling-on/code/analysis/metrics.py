def generate_comparison_report(
    results_df: pd.DataFrame,
    output_path: Path,
    nominal_alpha: float = 0.05
) -> Dict:
    """
    Implement comparison report generation (p-values, effect sizes before/after scaling).
    
    This function analyzes the `results_df` (typically generated by the real-world pipeline)
    to compare statistical test outcomes (p-values) and effect sizes (Cohen's d) 
    across different scaling methods (Standardization, Min-Max, Robust) versus the 
    unscaled baseline.
    
    It produces:
    1. A detailed CSV report of p-values and effect sizes per dataset/method.
    2. A JSON summary of aggregate statistics (mean p-value, mean effect size, deviation from baseline).
    3. A text report with key findings.
    
    Args:
        results_df: DataFrame containing test results with columns:
            - 'dataset_id': Identifier for the dataset
            - 'scaling_method': Method used (e.g., 'unscaled', 'standardize', 'min_max', 'robust')
            - 'p_value': Resulting p-value from the test
            - 'effect_size': Calculated effect size (e.g., Cohen's d)
            - 'test_type': Type of test performed
        output_path: Path to save the generated report files.
        nominal_alpha: The significance level threshold (default 0.05).
    
    Returns:
        Dict containing paths to generated artifacts and summary statistics.
    """
    logger.info(f"Generating comparison report for {len(results_df)} results.")
    
    # Ensure output directory exists
    output_path.parent.mkdir(parents=True, exist_ok=True)
    
    # 1. Validate and Prepare Data
    required_cols = ['dataset_id', 'scaling_method', 'p_value', 'effect_size']
    if not all(col in results_df.columns for col in required_cols):
        missing = [c for c in required_cols if c not in results_df.columns]
        raise ValueError(f"Results DataFrame missing required columns: {missing}")
    
    # Filter out invalid results (NaNs)
    clean_df = results_df.dropna(subset=required_cols + ['dataset_id', 'scaling_method'])
    
    # Calculate Baseline (Unscaled) Metrics per dataset
    # We assume 'unscaled' is the baseline method
    if 'unscaled' not in clean_df['scaling_method'].unique():
        logger.warning("No 'unscaled' baseline found in data. Calculations will be relative to global mean.")
        baseline_df = None
    else:
        baseline_df = clean_df[clean_df['scaling_method'] == 'unscaled'][['dataset_id', 'p_value', 'effect_size']].copy()
        baseline_df.columns = ['dataset_id', 'baseline_p', 'baseline_effect']
    
    # 2. Aggregate Statistics per Scaling Method
    # Group by scaling method and calculate mean/median/std of p-values and effect sizes
    method_stats = clean_df.groupby('scaling_method').agg(
        mean_p_value=('p_value', 'mean'),
        median_p_value=('p_value', 'median'),
        std_p_value=('p_value', 'std'),
        mean_effect_size=('effect_size', 'mean'),
        median_effect_size=('effect_size', 'median'),
        std_effect_size=('effect_size', 'std'),
        count=('p_value', 'count')
    ).reset_index()
    
    # 3. Calculate Deviations from Baseline (if baseline exists)
    if baseline_df is not None:
        # Merge baseline onto the main data
        analysis_df = clean_df.merge(baseline_df, on='dataset_id', how='left')
        
        # Calculate deltas
        analysis_df['p_value_delta'] = analysis_df['p_value'] - analysis_df['baseline_p']
        analysis_df['effect_size_delta'] = analysis_df['effect_size'] - analysis_df['baseline_effect']
        
        # Aggregate deltas per method
        delta_stats = analysis_df.groupby('scaling_method').agg(
            mean_p_delta=('p_value_delta', 'mean'),
            mean_effect_delta=('effect_size_delta', 'mean'),
            count=('p_value_delta', 'count')
        ).reset_index()
        
        # Merge stats and deltas
        summary_stats = method_stats.merge(delta_stats[['scaling_method', 'mean_p_delta', 'mean_effect_delta']], 
                                           on='scaling_method', how='left')
    else:
        summary_stats = method_stats
        summary_stats['mean_p_delta'] = np.nan
        summary_stats['mean_effect_delta'] = np.nan
    
    # 4. Generate Detailed CSV Report
    # Include raw values and deltas for every dataset/method combination
    report_df = clean_df.copy()
    if baseline_df is not None:
        report_df = report_df.merge(baseline_df, on='dataset_id', how='left')
        report_df['p_value_delta'] = report_df['p_value'] - report_df['baseline_p']
        report_df['effect_size_delta'] = report_df['effect_size'] - report_df['baseline_effect']
    
    csv_path = output_path / "comparison_report_detailed.csv"
    report_df.to_csv(csv_path, index=False)
    logger.info(f"Saved detailed report to {csv_path}")
    
    # 5. Generate JSON Summary
    summary_dict = {
        "nominal_alpha": nominal_alpha,
        "total_records": len(clean_df),
        "methods_analyzed": list(summary_stats['scaling_method']),
        "statistics": summary_stats.to_dict(orient='records'),
        "timestamp": pd.Timestamp.now().isoformat()
    }
    
    json_path = output_path / "comparison_report_summary.json"
    with open(json_path, 'w') as f:
        json.dump(summary_dict, f, indent=2, default=str)
    logger.info(f"Saved summary JSON to {json_path}")
    
    # 6. Generate Human-Readable Text Report
    txt_path = output_path / "comparison_report.txt"
    with open(txt_path, 'w') as f:
        f.write("Comparison Report: Scaling Methods vs Baseline\n")
        f.write("=" * 50 + "\n\n")
        f.write(f"Total Records Analyzed: {len(clean_df)}\n")
        f.write(f"Nominal Alpha: {nominal_alpha}\n\n")
        
        f.write("Aggregate Statistics by Scaling Method:\n")
        f.write("-" * 40 + "\n")
        f.write(f"{'Method':<15} | {'Mean P':<10} | {'Mean Eff':<10} | {'P Delta':<10} | {'Eff Delta':<10}\n")
        f.write("-" * 40 + "\n")
        
        for _, row in summary_stats.iterrows():
            p_delta = f"{row['mean_p_delta']:.4f}" if not pd.isna(row['mean_p_delta']) else "N/A"
            eff_delta = f"{row['mean_effect_delta']:.4f}" if not pd.isna(row['mean_effect_delta']) else "N/A"
            f.write(f"{row['scaling_method']:<15} | {row['mean_p_value']:.4f}   | {row['mean_effect_size']:.4f}   | {p_delta:<10} | {eff_delta:<10}\n")
        
        f.write("\n")
        f.write("Key Findings:\n")
        f.write("-" * 40 + "\n")
        
        # Simple heuristic: Check if any method significantly shifts p-values
        if 'unscaled' in summary_stats['scaling_method'].values:
            unscaled_row = summary_stats[summary_stats['scaling_method'] == 'unscaled'].iloc[0]
            for _, row in summary_stats.iterrows():
                if row['scaling_method'] == 'unscaled':
                    continue
                if not pd.isna(row['mean_p_delta']):
                    if row['mean_p_delta'] > 0.01:
                        f.write(f"- {row['scaling_method']} tends to INCREASE p-values (less significant) by {row['mean_p_delta']:.4f} on average.\n")
                    elif row['mean_p_delta'] < -0.01:
                        f.write(f"- {row['scaling_method']} tends to DECREASE p-values (more significant) by {abs(row['mean_p_delta']):.4f} on average.\n")
                    else:
                        f.write(f"- {row['scaling_method']} shows minimal p-value shift ({row['mean_p_delta']:.4f}) compared to baseline.\n")
        else:
            f.write("- No baseline ('unscaled') found for comparison.\n")
    
    logger.info(f"Saved text report to {txt_path}")
    
    return {
        "detailed_csv": str(csv_path),
        "summary_json": str(json_path),
        "text_report": str(txt_path),
        "summary_stats": summary_stats
    }

