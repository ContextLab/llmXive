"""
Verification script for GroupKFold split integrity.

This script loads the split indices generated by code/data/split.py,
validates that there is zero overlap of specific_alloy_designation_id
between any train/test pairs across all folds, and writes the
validation report to data/logs/split_validation.json.
"""
import os
import json
import logging
from pathlib import Path
from typing import Dict, Any, List, Set, Tuple, Optional
import pandas as pd

# Import project utilities
from utils.config import (
    get_processed_dataset_path,
    get_split_indices_path,
    get_split_validation_path,
    get_log_path,
    get_config
)
from utils.logging import setup_logger, get_logger
from utils.exceptions import DataInsufficientError

# Setup logger
logger = get_logger(__name__)
log_path = get_log_path()
logger = setup_logger(logger, log_file=log_path / "pipeline.log")


def load_split_indices() -> Dict[str, Any]:
    """Load the split indices from the JSON file generated by split.py."""
    split_path = get_split_indices_path()
    if not split_path.exists():
        raise FileNotFoundError(f"Split indices file not found: {split_path}. "
                                "Run code/data/split.py first.")
    
    with open(split_path, 'r') as f:
        return json.load(f)


def load_processed_data() -> pd.DataFrame:
    """Load the processed corrosion dataset."""
    data_path = get_processed_dataset_path()
    if not data_path.exists():
        raise FileNotFoundError(f"Processed dataset not found: {data_path}. "
                                "Run code/data/preprocess.py first.")
    
    # Try parquet first, then csv
    if data_path.suffix == '.parquet':
        return pd.read_parquet(data_path)
    elif data_path.suffix == '.csv':
        return pd.read_csv(data_path)
    else:
        # Fallback: try reading as parquet
        return pd.read_parquet(data_path.with_suffix('.parquet'))


def validate_split_integrity(split_data: Dict[str, Any], 
                             dataset: pd.DataFrame) -> Tuple[bool, Dict[str, Any]]:
    """
    Validate that the GroupKFold split has zero overlap of alloy designations
    between train and test sets for each fold.
    
    Returns:
        Tuple of (is_valid, validation_report)
    """
    report = {
        "total_folds": 0,
        "fold_statistics": [],
        "is_valid": True,
        "errors": [],
        "warnings": []
    }
    
    folds = split_data.get("folds", [])
    if not folds:
        report["errors"].append("No folds found in split indices file")
        report["is_valid"] = False
        return False, report
    
    report["total_folds"] = len(folds)
    alloy_column = "specific_alloy_designation_id"
    
    if alloy_column not in dataset.columns:
        # Try to find a similar column
        possible_cols = [c for c in dataset.columns if 'alloy' in c.lower()]
        if possible_cols:
            alloy_column = possible_cols[0]
            report["warnings"].append(f"Using '{alloy_column}' instead of 'specific_alloy_designation_id'")
        else:
            report["errors"].append(f"Alloy designation column not found. Available: {list(dataset.columns)}")
            report["is_valid"] = False
            return False, report
    
    for fold_idx, fold_data in enumerate(folds):
        fold_report = {
            "fold_index": fold_idx,
            "train_size": 0,
            "test_size": 0,
            "train_alloys": 0,
            "test_alloys": 0,
            "overlap_count": 0,
            "overlap_ids": [],
            "is_valid": True
        }
        
        train_indices = fold_data.get("train_indices", [])
        test_indices = fold_data.get("test_indices", [])
        
        if not train_indices or not test_indices:
            fold_report["is_valid"] = False
            report["errors"].append(f"Fold {fold_idx} has empty train or test set")
            report["is_valid"] = False
            report["fold_statistics"].append(fold_report)
            continue
        
        # Get alloy IDs for train and test sets
        train_alloys = set(dataset.iloc[train_indices][alloy_column].unique())
        test_alloys = set(dataset.iloc[test_indices][alloy_column].unique())
        
        # Check for overlap
        overlap = train_alloys.intersection(test_alloys)
        
        fold_report["train_size"] = len(train_indices)
        fold_report["test_size"] = len(test_indices)
        fold_report["train_alloys"] = len(train_alloys)
        fold_report["test_alloys"] = len(test_alloys)
        fold_report["overlap_count"] = len(overlap)
        fold_report["overlap_ids"] = sorted(list(overlap)) if overlap else []
        
        if overlap:
            fold_report["is_valid"] = False
            report["errors"].append(
                f"Fold {fold_idx}: Found {len(overlap)} overlapping alloy IDs: {sorted(list(overlap))}"
            )
            report["is_valid"] = False
        
        report["fold_statistics"].append(fold_report)
    
    return report["is_valid"], report


def main():
    """Main entry point for split validation."""
    logger.info("Starting split integrity validation (T017)")
    
    try:
        # Load data
        logger.info("Loading split indices...")
        split_data = load_split_indices()
        
        logger.info("Loading processed dataset...")
        dataset = load_processed_data()
        logger.info(f"Dataset loaded: {len(dataset)} records")
        
        # Validate
        logger.info("Validating split integrity...")
        is_valid, report = validate_split_integrity(split_data, dataset)
        
        # Add summary
        report["validation_timestamp"] = pd.Timestamp.now().isoformat()
        report["dataset_records"] = len(dataset)
        report["validation_status"] = "PASSED" if is_valid else "FAILED"
        
        # Write report
        validation_path = get_split_validation_path()
        validation_path.parent.mkdir(parents=True, exist_ok=True)
        
        with open(validation_path, 'w') as f:
            json.dump(report, f, indent=2, default=str)
        
        logger.info(f"Validation report written to: {validation_path}")
        logger.info(f"Validation status: {report['validation_status']}")
        
        if not is_valid:
            logger.error(f"Split validation FAILED: {len(report['errors'])} errors found")
            for error in report["errors"]:
                logger.error(f"  - {error}")
            raise DataInsufficientError("Split integrity validation failed. Train/test sets overlap on alloy designations.")
        else:
            logger.info("Split validation PASSED: Zero overlap confirmed across all folds")
            for fold_stat in report["fold_statistics"]:
                logger.info(
                    f"  Fold {fold_stat['fold_index']}: "
                    f"Train={fold_stat['train_size']} ({fold_stat['train_alloys']} alloys), "
                    f"Test={fold_stat['test_size']} ({fold_stat['test_alloys']} alloys), "
                    f"Overlap={fold_stat['overlap_count']}"
                )
        
        return report
        
    except Exception as e:
        logger.error(f"Split validation failed with error: {str(e)}")
        raise


if __name__ == "__main__":
    main()