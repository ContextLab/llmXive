def load_model_results() -> Dict[str, Any]:
    """Load the aggregated model results from the previous training/evaluation step."""
    config = get_config()
    results_path = get_path(config, "model_results_path")
    if not os.path.exists(results_path):
        raise FileNotFoundError(f"Model results file not found at {results_path}. Run training first.")
    
    with open(results_path, 'r') as f:
        return json.load(f)

def load_predictions_for_permutation() -> Tuple[np.ndarray, np.ndarray, np.ndarray]:
    """
    Load the aggregated predictions and true targets from the cross-validation loop.
    
    We expect the training script to have saved a 'fold_predictions' or similar
    structure. If not explicitly saved as a separate file, we assume the
    'model_results.json' contains the necessary aggregated arrays or paths to them.
    
    For this implementation, we assume the 'evaluate.py' step saved a specific
    file: `data/processed/cv_predictions.json` containing:
    - 'y_true': all true values
    - 'y_pred_best': predictions from the best model
    - 'y_pred_null': predictions from the null model (mean) - optional, can be computed
    
    If the file doesn't exist, we attempt to reconstruct from the results or raise.
    """
    config = get_config()
    # Expected path for CV predictions (generated by evaluate.py or train.py)
    predictions_path = get_path(config, "cv_predictions_path")
    
    if not os.path.exists(predictions_path):
        # Fallback: try to reconstruct if only model_results.json exists and contains arrays
        # This is a fallback for robustness, but ideally evaluate.py saves this explicitly.
        logger.warning(f"CV predictions file not found at {predictions_path}. Attempting reconstruction or error.")
        # In a strict pipeline, we might raise here. Let's try to load from model_results if structure allows.
        results = load_model_results()
        if 'aggregated_predictions' in results:
            pred_data = results['aggregated_predictions']
            y_true = np.array(pred_data['y_true'])
            y_pred = np.array(pred_data['y_pred_best'])
            return y_true, y_pred, None
        else:
            raise FileNotFoundError(
                f"Cannot perform permutation test. Missing CV predictions file: {predictions_path}. "
                "Ensure `code/models/evaluate.py` saves aggregated predictions to `data/processed/cv_predictions.json`."
            )
    
    with open(predictions_path, 'r') as f:
        data = json.load(f)
    
    y_true = np.array(data['y_true'])
    y_pred_best = np.array(data['y_pred_best'])
    # y_pred_null is not needed here as we compute it dynamically as mean(y_true)
    
    return y_true, y_pred_best, None

def calculate_null_baseline_metrics(y_true: np.ndarray, y_pred_best: np.ndarray) -> Dict[str, float]:
    """
    Calculate metrics for the null baseline (mean prediction) and compare with the best model.
    
    Returns:
        Dict with 'r2_null', 'r2_best', 'rmse_null', 'rmse_best', 'improvement'
    """
    # Null baseline prediction: mean of training targets
    # Note: In a proper CV setup, the null mean should be calculated on the TRAIN fold only for each fold.
    # However, if we are using aggregated predictions from a CV run, we must be careful.
    # If y_pred_best comes from CV (out-of-fold), then the corresponding null prediction
    # for each sample should be the mean of the TRAIN fold that sample came from.
    # If the 'evaluate.py' didn't save fold-specific null predictions, we approximate
    # by using the global mean. This is a slight approximation but standard for quick baselines.
    # Ideally, we would have y_pred_null in the saved file.
    
    y_mean = np.mean(y_true)
    y_pred_null = np.full_like(y_true, y_mean, dtype=float)
    
    r2_null = r2_score(y_true, y_pred_null)
    r2_best = r2_score(y_true, y_pred_best)
    
    # RMSE calculation
    rmse_null = np.sqrt(np.mean((y_true - y_pred_null) ** 2))
    rmse_best = np.sqrt(np.mean((y_true - y_pred_best) ** 2))
    
    return {
        "r2_null": float(r2_null),
        "r2_best": float(r2_best),
        "rmse_null": float(rmse_null),
        "rmse_best": float(rmse_best),
        "improvement": float(r2_best - r2_null)
    }

def permutation_test_on_aggregated_predictions(
    y_true: np.ndarray, 
    y_pred_best: np.ndarray, 
    n_permutations: int = 1000, 
    random_state: int = 42
) -> float:
    """
    Perform a permutation test to determine if the model's performance is significantly better than random.
    
    Null Hypothesis (H0): The model's predictions are no better than random noise (labels permuted).
    Alternative Hypothesis (H1): The model's performance is significantly better than random.
    
    We calculate the R² of the model on the true labels, then permute the labels n_permutations times,
    calculate R² for each permutation, and find the proportion of permuted R² scores >= observed R².
    
    Args:
        y_true: True target values.
        y_pred_best: Model predictions (fixed).
        n_permutations: Number of permutations.
        random_state: Random seed for reproducibility.
    
    Returns:
        p_value: The probability of observing a score as good or better than the model by chance.
    """
    rng = np.random.default_rng(random_state)
    observed_score = r2_score(y_true, y_pred_best)
    
    # If the observed score is already very low (e.g. negative), it's likely not learnable,
    # but we still run the test to get a p-value relative to the null distribution.
    
    perm_scores = []
    for _ in range(n_permutations):
        y_perm = rng.permutation(y_true)
        score = r2_score(y_perm, y_pred_best)
        perm_scores.append(score)
    
    perm_scores = np.array(perm_scores)
    
    # One-sided test: probability that permuted score >= observed score
    # Note: If observed_score is very high, p-value will be small.
    p_value = np.sum(perm_scores >= observed_score) / n_permutations
    
    logger.info(f"Permutation Test Results: Observed R² = {observed_score:.4f}, p-value = {p_value:.4f}")
    return float(p_value)

def classify_learnability(r2_best: float, p_value: float, r2_threshold: float = 0.0, p_threshold: float = 0.05) -> Dict[str, Any]:
    """
    Determine if the problem is "learnable" based on R² and p-value.
    
    Criteria:
    - R² > 0.0 (better than mean baseline)
    - p-value < 0.05 (statistically significant improvement over random)
    
    Returns:
        Dict with 'is_learnable', 'reasoning'
    """
    is_learnable = (r2_best > r2_threshold) and (p_value < p_threshold)
    
    reasoning = []
    if r2_best <= r2_threshold:
        reasoning.append(f"R² ({r2_best:.4f}) is not greater than threshold ({r2_threshold}).")
    if p_value >= p_threshold:
        reasoning.append(f"p-value ({p_value:.4f}) is not less than threshold ({p_threshold}).")
    
    if is_learnable:
        reasoning_str = "Model significantly outperforms null baseline and random chance."
    else:
        reasoning_str = "Model does not meet criteria for learnability: " + " ".join(reasoning)
    
    return {
        "is_learnable": is_learnable,
        "reasoning": reasoning_str,
        "r2_threshold": r2_threshold,
        "p_threshold": p_threshold
    }

def run_null_baseline_analysis() -> Dict[str, Any]:
    """
    Main entry point for null baseline analysis.
    
    1. Load predictions.
    2. Calculate null baseline metrics.
    3. Run permutation test.
    4. Classify learnability.
    5. Save results.
    """
    logger.info("Starting null baseline analysis and learnability classification.")
    
    # 1. Load data
    y_true, y_pred_best, _ = load_predictions_for_permutation()
    
    if len(y_true) == 0:
        raise DataInsufficientError("No data found for analysis. Ensure pipeline produced predictions.")
    
    logger.info(f"Loaded {len(y_true)} samples for analysis.")
    
    # 2. Calculate metrics
    metrics = calculate_null_baseline_metrics(y_true, y_pred_best)
    
    # 3. Permutation test
    p_value = permutation_test_on_aggregated_predictions(y_true, y_pred_best)
    
    # 4. Classify
    classification = classify_learnability(metrics['r2_best'], p_value)
    
    # 5. Compile results
    results = {
        "null_baseline_metrics": metrics,
        "permutation_test": {
            "p_value": p_value,
            "n_permutations": 1000,
            "random_state": 42
        },
        "learnability_classification": classification,
        "status": "completed"
    }
    
    # 6. Save results
    config = get_config()
    results_path = get_path(config, "model_results_path")
    
    # Load existing results to merge if necessary, or overwrite
    # We assume model_results.json is the single source of truth for final results
    # We will update the existing file with the new analysis
    if os.path.exists(results_path):
        with open(results_path, 'r') as f:
            existing_results = json.load(f)
        existing_results['null_baseline_analysis'] = results
        with open(results_path, 'w') as f:
            json.dump(existing_results, f, indent=2)
    else:
        with open(results_path, 'w') as f:
            json.dump(results, f, indent=2)
    
    logger.info(f"Null baseline analysis complete. Results saved to {results_path}")
    logger.info(f"Is Learnable: {classification['is_learnable']} - {classification['reasoning']}")
    
    return results

def main():
    """CLI entry point."""
    try:
        results = run_null_baseline_analysis()
        print(json.dumps(results, indent=2))
    except Exception as e:
        logger.error(f"Analysis failed: {e}")
        raise

if __name__ == "__main__":
    main()