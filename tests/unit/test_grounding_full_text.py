# tests/unit/test_grounding_full_text.py
from llmxive.grounding.full_text import RetrievedDoc, html_to_text


def test_retrieved_doc_readable_flag():
    assert RetrievedDoc(kind="doi", value="10.x/y", tier=None, full_text=None,
                        abstract=None, title=None, final_url="", error="e").readable is False
    assert RetrievedDoc(kind="doi", value="10.x/y", tier="unpaywall",
                        full_text="body", abstract=None, title=None,
                        final_url="u", error=None).readable is True
    assert RetrievedDoc(kind="doi", value="10.x/y", tier="s2", full_text=None,
                        abstract="abs only", title=None, final_url="u",
                        error=None).readable is True


def test_html_to_text_strips_tags_and_scripts():
    html = ("<html><head><title>T</title><style>.x{}</style>"
            "<script>var a=1;</script></head><body><h1>Result</h1>"
            "<p>The correlation was r=0.42 across knots.</p></body></html>")
    text = html_to_text(html, max_chars=10_000)
    assert "r=0.42 across knots" in text
    assert "var a=1" not in text and ".x{}" not in text


def test_extract_pdf_text_caps_length():
    # extract_pdf_text post-processes already-extracted page strings; feed raw text
    from llmxive.grounding.full_text import _normalize_extracted
    big = "word " * 100_000
    out = _normalize_extracted(big, max_chars=1000)
    assert len(out) <= 1000
    assert "  " not in out  # whitespace collapsed


def test_extract_pdf_text_real_pypdf():
    """Run pypdf on a real, hand-authored minimal PDF and assert roundtrip.

    The PDF byte string is a valid (if minimal) PDF 1.4 document containing
    a single page with the text stream:
        BT /F1 24 Tf 72 720 Td (Grounding test value 12345) Tj ET
    pypdf rebuilds a broken xref automatically, so the minor offset
    inaccuracy in the xref table does not prevent parsing.
    Generated by hand (no external tool) to keep the test self-contained.
    """
    from llmxive.grounding.full_text import extract_pdf_text

    # Minimal valid PDF 1.4 with one page and one text operator.
    # Content-stream length must match the literal byte count of the stream body.
    # "BT /F1 24 Tf 72 720 Td (Grounding test value 12345) Tj ET\n" = 60 bytes
    pdf_bytes = (
        b"%PDF-1.4\n"
        b"1 0 obj << /Type /Catalog /Pages 2 0 R >> endobj\n"
        b"2 0 obj << /Type /Pages /Kids [3 0 R] /Count 1 >> endobj\n"
        b"3 0 obj << /Type /Page /Parent 2 0 R /MediaBox [0 0 612 792]\n"
        b"  /Contents 4 0 R /Resources << /Font << /F1 5 0 R >> >> >> endobj\n"
        b"4 0 obj << /Length 60 >>\n"
        b"stream\n"
        b"BT /F1 24 Tf 72 720 Td (Grounding test value 12345) Tj ET\n"
        b"endstream\n"
        b"endobj\n"
        b"5 0 obj << /Type /Font /Subtype /Type1 /BaseFont /Helvetica >> endobj\n"
        b"xref\n"
        b"0 6\n"
        b"0000000000 65535 f \n"
        b"0000000009 00000 n \n"
        b"0000000058 00000 n \n"
        b"0000000115 00000 n \n"
        b"0000000270 00000 n \n"
        b"0000000383 00000 n \n"
        b"trailer << /Size 6 /Root 1 0 R >>\n"
        b"startxref\n"
        b"460\n"
        b"%%EOF\n"
    )

    result = extract_pdf_text(pdf_bytes)
    assert "Grounding" in result, f"Expected 'Grounding' in extracted text, got: {result!r}"
    assert "12345" in result, f"Expected '12345' in extracted text, got: {result!r}"
